const scoreSession = ({ plannedMin, actualMin, overtimeMin, pauseCount, distractionCount }) => {
  if (!actualMin || actualMin < 1) return 0;
  let score = 100;
  // Completion penalty — proportional. 0 min = -100, 50% = -50, 90%+ = 0
  const completionRatio = Math.min(1, actualMin / Math.max(1, plannedMin));
  if (completionRatio < 0.9) score -= Math.round((1 - completionRatio) * 100);
  // Overtime penalty (gentle)
  if (overtimeMin > 0) score -= Math.min(20, overtimeMin);
  // Pause penalty
  score -= pauseCount * 5;
  // Distraction penalty
  score -= distractionCount * 3;
  return Math.max(0, Math.min(100, Math.round(score)));
};

