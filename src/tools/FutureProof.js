import React, { useState } from 'react';
import { useTheme } from '../hooks/useTheme';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { CopyBtn, ActionBar } from '../components/ActionButtons';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const SUBJECT_TYPES = [
  { id: 'skill', label: 'Skill', icon: '🧠' },
  { id: 'career', label: 'Career', icon: '💼' },
  { id: 'investment', label: 'Investment', icon: '📈' },
  { id: 'technology', label: 'Technology', icon: '💻' },
  { id: 'commitment', label: 'Commitment', icon: '🤝' },
  { id: 'habit', label: 'Habit', icon: '🔁' },
];

const EXAMPLES = [
  { s: 'Learning SQL', t: 'skill', ctx: 'Work in finance, thinking about moving to data analysis' },
  { s: 'Real estate agent', t: 'career', ctx: 'Currently in sales, considering the switch' },
  { s: 'Moving to a LCOL city to save money', t: 'commitment', ctx: 'Remote worker, 30s, considering Austin or Raleigh' },
  { s: 'Becoming a UX designer', t: 'career', ctx: 'Graphic design background, interested in tech products' },
  { s: 'Learning a second language (Mandarin)', t: 'skill', ctx: 'Business context, interested in China market opportunities' },
];

const TRAJECTORY_CONFIG = {
  'growing':           { label: 'GROWING',           icon: '↑', color: 'emerald', light: 'bg-[#e8f0e8] border-[#a8c4a8] text-[#3d6e3f]', dark: 'bg-[#5a8a5c]/15 border-[#5a8a5c]/40 text-[#8aba8c]' },
  'stable':            { label: 'STABLE',             icon: '→', color: 'blue',    light: 'bg-[#e8eef5] border-[#b0c4d8] text-[#1e3a58]', dark: 'bg-[#2c4a6e]/15 border-[#2c4a6e]/40 text-[#a8b9ce]' },
  'transforming':      { label: 'TRANSFORMING',       icon: '⟳', color: 'amber',   light: 'bg-[#fdf3e4] border-[#e0c49a] text-[#93541f]', dark: 'bg-[#c8872e]/15 border-[#c8872e]/40 text-[#d9a04e]' },
  'declining':         { label: 'DECLINING',          icon: '↓', color: 'red',     light: 'bg-[#fceae8] border-[#d4908a] text-[#8a3028]', dark: 'bg-[#b54a3f]/15 border-[#b54a3f]/40 text-[#e8a9a3]' },
  'volatile':          { label: 'VOLATILE',           icon: '⚡', color: 'purple',  light: 'bg-[#f3efe8] border-[#d5cab8] text-[#5e5042]', dark: 'bg-[#3d3630]/60 border-[#3d3630] text-[#c8c3b9]' },
  'context_dependent': { label: 'CONTEXT-DEPENDENT',  icon: '◇', color: 'neutral', light: 'bg-[#f3efe8] border-[#d5cab8] text-[#5e5042]', dark: 'bg-[#3d3630]/60 border-[#3d3630] text-[#c8c3b9]' },
};

const FutureProof = () => {
  const { theme } = useTheme();
  const isDark = theme === 'dark';
  const { callToolEndpoint, loading } = useClaudeAPI();

  const c = {
    pageBg:       isDark ? 'bg-[#1a1816]'    : 'bg-[#faf8f5]',
    card:         isDark ? 'bg-[#2a2623]'     : 'bg-white',
    cardAlt:      isDark ? 'bg-[#332e2a]'     : 'bg-[#faf8f5]',
    cardBorder:   isDark ? 'border-[#3d3630]' : 'border-[#e8e1d5]',
    inputBg:      isDark ? 'bg-[#1a1816]'     : 'bg-[#faf8f5]',
    inputBorder:  isDark ? 'border-[#3d3630]' : 'border-[#d5cab8]',
    inputFocus:   'focus:outline-none focus:ring-2 focus:ring-[#2c4a6e]/20 focus:border-[#4a6a8a]',
    text:          isDark ? 'text-[#f0eeea]'  : 'text-[#3d3935]',
    textSecondary: isDark ? 'text-[#c8c3b9]'  : 'text-[#5a544a]',
    textMuted:     isDark ? 'text-[#8a8275]'  : 'text-[#8a8275]',
    heading:       isDark ? 'text-[#f3efe8]'  : 'text-[#1e2a3a]',
    btnPrimary:    isDark ? 'bg-[#2c4a6e] hover:bg-[#4a6a8a] text-white' : 'bg-[#2c4a6e] hover:bg-[#1e3a58] text-white',
    btnSecondary:  isDark ? 'bg-[#332e2a] hover:bg-[#3d3630] text-[#c8c3b9] border border-[#3d3630]' : 'bg-[#f3efe8] hover:bg-[#e8e1d5] text-[#5e5042] border border-[#d5cab8]',
    divider:       isDark ? 'border-[#3d3630]' : 'border-[#e8e1d5]',
    error:         isDark ? 'bg-[#b54a3f]/15 border-[#b54a3f]/50 text-[#e8a9a3]' : 'bg-[#fceae8] border-[#d4908a] text-[#8a3028]',
    input:         isDark
      ? 'bg-[#1a1816] border-[#3d3630] text-[#f0eeea] placeholder:text-[#8a8275]'
      : 'bg-[#faf8f5] border-[#d5cab8] text-[#3d3935] placeholder:text-[#8a8275]',
  };

  const [subject, setSubject] = useState('');
  const [subjectType, setSubjectType] = useState('');
  const [context, setContext] = useState('');
  const [results, setResults] = useState(null);
  const [error, setError] = useState('');
  const [expandedSections, setExpandedSections] = useState({ scenarios: false, automation: false });

  const toggle = (key) => setExpandedSections(p => ({ ...p, [key]: !p[key] }));

  const handleSubmit = async () => {
    if (!subject.trim()) return;
    setError(''); setResults(null);
    try {
      const data = await callToolEndpoint('future-proof', {
        subject: subject.trim(),
        subjectType: subjectType || undefined,
        context: context.trim() || undefined,
      });
      setResults(data);
    } catch (e) { setError(e.message || 'Failed to analyze trajectory.'); }
  };

  const handleExample = () => {
    const ex = EXAMPLES[Math.floor(Math.random() * EXAMPLES.length)];
    setSubject(ex.s); setSubjectType(ex.t); setContext(ex.ctx); setResults(null);
  };

  const buildText = () => {
    if (!results) return '';
    const traj = TRAJECTORY_CONFIG[results.trajectory];
    let t = `🔮 FUTUREPROOF\n\n${results.subject_as_understood}\n\nTrajectory: ${traj?.label || results.trajectory_label} (${results.confidence} confidence)\n\n`;
    t += `The Pattern:\n${results.the_pattern}\n\n`;
    if (results.tailwinds?.length) { t += `Tailwinds:\n${results.tailwinds.map(w => `• ${w.force}: ${w.explanation}`).join('\n')}\n\n`; }
    if (results.headwinds?.length) { t += `Headwinds:\n${results.headwinds.map(w => `• ${w.force}: ${w.explanation}`).join('\n')}\n\n`; }
    if (results.the_pivot?.the_version_worth_pursuing) { t += `The Version Worth Pursuing:\n${results.the_pivot.the_version_worth_pursuing}\n\n`; }
    if (results.the_honest_take) { t += `The Honest Take:\n${results.the_honest_take}\n\n`; }
    if (results.one_action) { t += `One Action:\n${results.one_action}\n`; }
    return t + BRAND;
  };

  const traj = results ? (TRAJECTORY_CONFIG[results.trajectory] || TRAJECTORY_CONFIG['stable']) : null;
  const trajStyle = traj ? (isDark ? traj.dark : traj.light) : '';

  return (
    <div className={`min-h-screen py-8 px-4 ${c.pageBg}`}>
      <div className="max-w-2xl mx-auto">

        {/* Header */}
        <div className="text-center mb-8">
          <div className="text-5xl mb-3">🔮</div>
          <h1 className={`text-3xl font-black tracking-tight mb-2 ${c.heading}`}>FutureProof</h1>
          <p className={`text-sm ${c.textMuted}`}>5-year trajectory analysis. Not predictions — pattern recognition.</p>
        </div>

        {/* Input */}
        {!results && (
          <div className={`rounded-2xl border p-6 shadow-sm space-y-4 ${c.card} ${c.cardBorder}`}>
            <div>
              <label className={`block text-sm font-semibold mb-1.5 ${c.text}`}>What do you want to assess? <span className="text-red-400">*</span></label>
              <input type="text" value={subject} onChange={e => setSubject(e.target.value)}
                placeholder="e.g. Learning Python, real estate agent career, buying rental property…"
                className={`w-full px-4 py-3 rounded-xl border text-sm ${c.input} ${c.inputFocus}`} />
            </div>

            <div>
              <label className={`block text-sm font-semibold mb-2 ${c.text}`}>What is it? <span className={`font-normal ${c.textMuted}`}>(optional — helps calibrate the analysis)</span></label>
              <div className="flex flex-wrap gap-2">
                {SUBJECT_TYPES.map(t => (
                  <button key={t.id} onClick={() => setSubjectType(subjectType === t.id ? '' : t.id)}
                    className={`px-3 py-1.5 rounded-lg text-xs font-semibold border transition-all ${
                      subjectType === t.id
                        ? 'bg-[#2c4a6e] text-white border-[#2c4a6e]'
                        : `${c.btnSecondary}`
                    }`}>
                    {t.icon} {t.label}
                  </button>
                ))}
              </div>
            </div>

            <div>
              <label className={`block text-sm font-semibold mb-1.5 ${c.text}`}>Your context <span className={`font-normal ${c.textMuted}`}>(makes it much more specific)</span></label>
              <textarea value={context} onChange={e => setContext(e.target.value)}
                placeholder="Your background, why you're considering this, your situation…"
                rows={2} maxLength={300}
                className={`w-full px-4 py-3 rounded-xl border text-sm resize-none ${c.input} ${c.inputFocus}`} />
            </div>

            {error && <div className={`p-3 rounded-xl border text-sm ${c.error}`}><span className="mr-1">⚠️</span>{error}</div>}

            <div className="flex gap-2">
              <button onClick={handleSubmit} disabled={loading || !subject.trim()}
                className={`flex-1 py-3 rounded-xl font-bold disabled:opacity-40 ${c.btnPrimary}`}>
                {loading ? <><span className="animate-spin inline-block mr-2">⏳</span>Analyzing…</> : '🔮 Analyze Trajectory'}
              </button>
              <button onClick={handleExample} disabled={loading}
                className={`px-4 py-3 rounded-xl text-sm font-medium ${c.btnSecondary}`}>
                Example
              </button>
            </div>
          </div>
        )}

        {/* Results */}
        {results && (
          <div className="space-y-4">

            {/* Trajectory header */}
            <div className={`rounded-2xl border p-6 text-center ${trajStyle}`}>
              <p className={`text-xs font-black uppercase tracking-widest mb-2 opacity-70`}>5-Year Trajectory</p>
              <div className="text-5xl font-black mb-2">{traj?.icon || '→'}</div>
              <h2 className="text-2xl font-black tracking-tight">{results.trajectory_label || traj?.label}</h2>
              <p className="text-xs mt-2 opacity-70 uppercase tracking-wide">{results.trajectory_strength} signal · {results.confidence} confidence</p>
              <p className="text-sm mt-3 font-medium opacity-90">{results.subject_as_understood}</p>
            </div>

            {/* The Pattern */}
            <div className={`rounded-2xl border p-5 ${c.card} ${c.cardBorder}`}>
              <p className={`text-xs font-bold uppercase tracking-wider mb-2 ${c.textMuted}`}>📊 The Pattern</p>
              <p className={`text-sm leading-relaxed ${c.textSecondary}`}>{results.the_pattern}</p>
            </div>

            {/* Tailwinds + Headwinds */}
            <div className="grid grid-cols-1 sm:grid-cols-2 gap-4">
              {results.tailwinds?.length > 0 && (
                <div className={`rounded-2xl border p-4 ${c.card} ${c.cardBorder}`}>
                  <p className={`text-xs font-bold uppercase tracking-wider mb-3 ${isDark ? 'text-[#8aba8c]' : 'text-[#3d6e3f]'}`}>↑ Tailwinds</p>
                  <div className="space-y-2">
                    {results.tailwinds.map((w, i) => (
                      <div key={i} className="text-sm">
                        <span className={`font-semibold ${c.text}`}>{w.force}</span>
                        <span className={` · ${c.textMuted} text-xs`}>{w.strength}</span>
                        <p className={`text-xs mt-0.5 ${c.textMuted}`}>{w.explanation}</p>
                      </div>
                    ))}
                  </div>
                </div>
              )}
              {results.headwinds?.length > 0 && (
                <div className={`rounded-2xl border p-4 ${c.card} ${c.cardBorder}`}>
                  <p className={`text-xs font-bold uppercase tracking-wider mb-3 ${isDark ? 'text-[#e8a9a3]' : 'text-[#8a3028]'}`}>↓ Headwinds</p>
                  <div className="space-y-2">
                    {results.headwinds.map((w, i) => (
                      <div key={i} className="text-sm">
                        <span className={`font-semibold ${c.text}`}>{w.force}</span>
                        {w.timeline && <span className={` · text-xs ${c.textMuted}`}>{w.timeline}</span>}
                        <p className={`text-xs mt-0.5 ${c.textMuted}`}>{w.explanation}</p>
                      </div>
                    ))}
                  </div>
                </div>
              )}
            </div>

            {/* Automation */}
            {results.the_automation_question && (
              <div className={`rounded-2xl border overflow-hidden ${c.card} ${c.cardBorder}`}>
                <button onClick={() => toggle('automation')} className="w-full text-left px-5 py-4 flex items-center justify-between">
                  <p className={`text-xs font-bold uppercase tracking-wider ${c.textMuted}`}>🤖 The Automation Question</p>
                  <span className={`text-sm ${c.textMuted}`}>{expandedSections.automation ? '▲' : '▼'}</span>
                </button>
                {expandedSections.automation && (
                  <div className={`px-5 pb-5 space-y-2 border-t ${c.divider}`}>
                    <div className="pt-3 flex items-center gap-2">
                      <span className={`text-xs font-bold px-2 py-0.5 rounded-full ${
                        results.the_automation_question.risk_level === 'high' ? (isDark ? 'bg-[#b54a3f]/20 text-[#e8a9a3]' : 'bg-[#fceae8] text-[#8a3028]')
                        : results.the_automation_question.risk_level === 'medium' ? (isDark ? 'bg-[#c8872e]/20 text-[#d9a04e]' : 'bg-[#fdf3e4] text-[#93541f]')
                        : (isDark ? 'bg-[#5a8a5c]/20 text-[#8aba8c]' : 'bg-[#e8f0e8] text-[#3d6e3f]')
                      }`}>
                        {results.the_automation_question.risk_level} risk
                      </span>
                    </div>
                    {results.the_automation_question.what_gets_automated && (
                      <p className={`text-sm ${c.textSecondary}`}><span className={`font-semibold ${c.text}`}>Gets automated:</span> {results.the_automation_question.what_gets_automated}</p>
                    )}
                    {results.the_automation_question.what_doesnt && (
                      <p className={`text-sm ${c.textSecondary}`}><span className={`font-semibold ${c.text}`}>Hard to automate:</span> {results.the_automation_question.what_doesnt}</p>
                    )}
                    <p className={`text-sm ${c.textSecondary}`}>{results.the_automation_question.net_effect}</p>
                  </div>
                )}
              </div>
            )}

            {/* The Pivot */}
            {results.the_pivot && (
              <div className={`rounded-2xl border p-5 ${isDark ? 'bg-[#c8872e]/10 border-[#c8872e]/30' : 'bg-[#fdf3e4] border-[#e0c49a]'}`}>
                <p className={`text-xs font-bold uppercase tracking-wider mb-3 ${isDark ? 'text-[#d9a04e]' : 'text-[#93541f]'}`}>🎯 The Version Worth Pursuing</p>
                {results.the_pivot.the_version_worth_pursuing && (
                  <p className={`text-sm leading-relaxed mb-3 ${c.textSecondary}`}>{results.the_pivot.the_version_worth_pursuing}</p>
                )}
                {results.the_pivot.adjacent_moves?.length > 0 && (
                  <div className="space-y-2">
                    {results.the_pivot.adjacent_moves.map((m, i) => (
                      <div key={i} className={`p-3 rounded-xl ${isDark ? 'bg-[#1a1816]/50' : 'bg-white/70'}`}>
                        <p className={`text-sm font-semibold ${c.text}`}>{m.move}</p>
                        <p className={`text-xs mt-0.5 ${c.textMuted}`}>{m.why_it_changes_the_picture}</p>
                        <span className={`text-xs font-medium ${c.textMuted}`}>Effort: {m.effort_required}</span>
                      </div>
                    ))}
                  </div>
                )}
              </div>
            )}

            {/* Scenarios */}
            {results.scenarios && (
              <div className={`rounded-2xl border overflow-hidden ${c.card} ${c.cardBorder}`}>
                <button onClick={() => toggle('scenarios')} className="w-full text-left px-5 py-4 flex items-center justify-between">
                  <p className={`text-xs font-bold uppercase tracking-wider ${c.textMuted}`}>🎲 Best / Base / Worst Case</p>
                  <span className={`text-sm ${c.textMuted}`}>{expandedSections.scenarios ? '▲' : '▼'}</span>
                </button>
                {expandedSections.scenarios && (
                  <div className={`px-5 pb-5 grid grid-cols-1 gap-3 border-t ${c.divider} pt-4`}>
                    {[
                      { key: 'bull_case', label: '🟢 Best case', bg: isDark ? 'bg-[#5a8a5c]/10 border-[#5a8a5c]/30' : 'bg-[#e8f0e8] border-[#a8c4a8]' },
                      { key: 'base_case', label: '🔵 Most likely', bg: isDark ? 'bg-[#2c4a6e]/10 border-[#2c4a6e]/30' : 'bg-[#e8eef5] border-[#b0c4d8]' },
                      { key: 'bear_case', label: '🔴 Worst case', bg: isDark ? 'bg-[#b54a3f]/10 border-[#b54a3f]/30' : 'bg-[#fceae8] border-[#d4908a]' },
                    ].map(s => results.scenarios[s.key] && (
                      <div key={s.key} className={`p-3 rounded-xl border ${s.bg}`}>
                        <p className={`text-xs font-bold mb-1 ${c.textMuted}`}>{s.label}</p>
                        <p className={`text-sm ${c.textSecondary}`}>{results.scenarios[s.key]}</p>
                      </div>
                    ))}
                  </div>
                )}
              </div>
            )}

            {/* The Honest Take */}
            {results.the_honest_take && (
              <div className={`rounded-2xl border p-5 ${c.card} ${c.cardBorder}`}>
                <p className={`text-xs font-bold uppercase tracking-wider mb-2 ${c.textMuted}`}>💬 The Honest Take</p>
                <p className={`text-sm leading-relaxed ${c.textSecondary}`}>{results.the_honest_take}</p>
              </div>
            )}

            {/* One Action */}
            {results.one_action && (
              <div className={`rounded-2xl border-2 p-5 ${isDark ? 'border-[#2c4a6e] bg-[#2c4a6e]/10' : 'border-[#2c4a6e] bg-[#e8eef5]'}`}>
                <p className={`text-xs font-bold uppercase tracking-wider mb-2 ${isDark ? 'text-[#a8b9ce]' : 'text-[#2c4a6e]'}`}>⚡ One Action — Next 90 Days</p>
                <p className={`text-sm font-medium ${c.text}`}>{results.one_action}</p>
              </div>
            )}

            {/* Actions */}
            <div className="flex items-center gap-3 flex-wrap">
              <ActionBar content={buildText()} title="FutureProof Analysis" />
              <button onClick={() => { setResults(null); setSubject(''); setSubjectType(''); setContext(''); }}
                className={`px-4 py-2 rounded-lg text-sm font-medium ${c.btnSecondary}`}>
                🔄 New Analysis
              </button>
            </div>

            {/* Cross-references */}
            <div className={`rounded-xl border p-4 ${c.cardAlt} ${c.cardBorder}`}>
              <p className={`text-xs font-semibold uppercase tracking-wide mb-3 ${c.textMuted}`}>Related tools</p>
              <div className="flex flex-wrap gap-2">
                {[
                  { id: 'PreMortem', icon: '💀', label: 'Write your plan\'s obituary' },
                  { id: 'SignalVsNoise', icon: '📡', label: 'Cut through conflicting advice' },
                  { id: 'EgoKiller', icon: '🪦', label: 'Challenge your assumptions' },
                ].map(r => (
                  <a key={r.id} href={`/tool/${r.id}`} target="_blank" rel="noopener noreferrer"
                    className={`px-3 py-1.5 rounded-lg text-xs font-medium transition-colors ${c.btnSecondary}`}>
                    {r.icon} {r.label}
                  </a>
                ))}
              </div>
            </div>
          </div>
        )}
      </div>
    </div>
  );
};

FutureProof.displayName = 'FutureProof';
export default FutureProof;
