import React, { useState, useEffect, useRef, useCallback } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { usePersistentState } from '../hooks/usePersistentState';
import { useRegisterActions } from '../components/ActionBarContext';
import { useTranslation } from '../i18n/useTranslation';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';


// ─── Constants ───
// Subject types: value is sent to the backend; labelKey is localized at render.
const SUBJECT_TYPES = [
  { value: 'skill',      labelKey: 'fp_type_skill',      icon: '🛠️' },
  { value: 'career',     labelKey: 'fp_type_career',     icon: '💼' },
  { value: 'technology', labelKey: 'fp_type_technology', icon: '⚙️' },
  { value: 'investment', labelKey: 'fp_type_investment', icon: '📈' },
  { value: 'habit',      labelKey: 'fp_type_habit',      icon: '🔁' },
  { value: 'commitment', labelKey: 'fp_type_commitment', icon: '🤝' },
];

const TIMEFRAMES = [
  { value: '1 year',   labelKey: 'fp_tf_1y' },
  { value: '3 years',  labelKey: 'fp_tf_3y' },
  { value: '5 years',  labelKey: 'fp_tf_5y' },
  { value: '10 years', labelKey: 'fp_tf_10y' },
];

const STRENGTH_ICONS = { strong: '●●●', moderate: '●●○', weak: '●○○' };
const EFFORT_KEYS   = { low: 'fp_effort_low', medium: 'fp_effort_medium', high: 'fp_effort_high' };

// ════════════════════════════════════════════════════════════
// COMPONENT
// ════════════════════════════════════════════════════════════
const FutureProof = ({ tool }) => {
  const { callToolEndpoint, loading, userLocale, userCurrency, userRegion } = useClaudeAPI();
  const { isDark } = useTheme();
  const { t } = useTranslation();

  const c = {
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:         isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20'
                          : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-500' : 'text-gray-400',
    labelText:     isDark ? 'text-zinc-200' : 'text-gray-700',
    accentTxt:     isDark ? 'text-cyan-400' : 'text-cyan-600',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white'
                          : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200'
                          : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200'
                          : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200'
                          : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200'
                          : 'bg-red-50 border-red-200 text-red-800',
    infoBox:       isDark ? 'bg-sky-900/20 border-sky-700 text-sky-200'
                          : 'bg-sky-50 border-sky-200 text-sky-800',
    pillActive:    isDark ? 'border-cyan-500 bg-cyan-900/30 text-cyan-200'
                          : 'border-cyan-600 bg-cyan-100 text-cyan-900',
    pillInactive:  isDark ? 'border-zinc-600 text-zinc-400 hover:border-zinc-500'
                          : 'border-gray-300 text-gray-500 hover:border-gray-400',
    required:      isDark ? 'text-amber-400' : 'text-amber-500',
  };
  c.textMuteded = c.textMuted;
  c.label = c.labelText;

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';

  const trajStyle = (traj) => {
    const map = {
      growing:           { bg: isDark ? 'bg-emerald-900/30 border-emerald-600' : 'bg-emerald-50 border-emerald-300', txt: isDark ? 'text-emerald-300' : 'text-emerald-800', dot: '🟢', label: t('fp_traj_growing') },
      stable:            { bg: isDark ? 'bg-cyan-900/30 border-cyan-600'       : 'bg-cyan-50 border-cyan-300',       txt: isDark ? 'text-cyan-300'    : 'text-cyan-800',    dot: '🔵', label: t('fp_traj_stable') },
      transforming:      { bg: isDark ? 'bg-amber-900/30 border-amber-600'     : 'bg-amber-50 border-amber-300',     txt: isDark ? 'text-amber-300'   : 'text-amber-800',   dot: '🟡', label: t('fp_traj_transforming') },
      declining:         { bg: isDark ? 'bg-red-900/30 border-red-600'         : 'bg-red-50 border-red-300',         txt: isDark ? 'text-red-300'     : 'text-red-800',     dot: '🔴', label: t('fp_traj_declining') },
      volatile:          { bg: isDark ? 'bg-orange-900/30 border-orange-600'   : 'bg-orange-50 border-orange-300',   txt: isDark ? 'text-orange-300'  : 'text-orange-800',  dot: '🟠', label: t('fp_traj_volatile') },
      context_dependent: { bg: isDark ? 'bg-cyan-900/30 border-cyan-600'   : 'bg-cyan-50 border-cyan-300',   txt: isDark ? 'text-cyan-300'  : 'text-cyan-800',  dot: '🟣', label: t('fp_traj_context') },
    };
    return map[traj] || map.context_dependent;
  };

  const autoStyle = (level) => {
    const map = {
      high:       { bg: isDark ? 'bg-red-900/20 border-red-700'         : 'bg-red-50 border-red-300',         txt: isDark ? 'text-red-300'     : 'text-red-800'     },
      medium:     { bg: isDark ? 'bg-amber-900/20 border-amber-700'     : 'bg-amber-50 border-amber-300',     txt: isDark ? 'text-amber-300'   : 'text-amber-800'   },
      low:        { bg: isDark ? 'bg-cyan-900/20 border-cyan-700'       : 'bg-cyan-50 border-cyan-300',       txt: isDark ? 'text-cyan-300'    : 'text-cyan-800'    },
      negligible: { bg: isDark ? 'bg-emerald-900/20 border-emerald-700' : 'bg-emerald-50 border-emerald-300', txt: isDark ? 'text-emerald-300' : 'text-emerald-800' },
    };
    return map[level] || map.medium;
  };

  // ─── State ───
  const [subject, setSubject] = useState('');
  const [subjectType, setSubjectType] = useState('');
  const [context, setContext] = useState('');
  const [timeframe, setTimeframe] = useState('5 years');
  const [results, setResults] = usePersistentState('futureproof-result', null);
  const [error, setError] = useState('');
  const [activeScenario, setActiveScenario] = useState('base_case');
  const [sessionHistory, setSessionHistory] = usePersistentState('futureproof-history', []);

  // ─── Refs ───
  const resultsRef = useRef(null);
  const handleSubmitRef = useRef(null);
  const canSubmitRef = useRef(false);

  canSubmitRef.current = !!subject.trim();

  // ─── Handlers ───
  const handleSubmit = async () => {
    if (!subject.trim()) return;
    setError('');
    setResults(null);
    setActiveScenario('base_case');
    try {
      const data = await callToolEndpoint('future-proof', {
        subject: subject.trim(),
        subjectType: subjectType || undefined,
        context: context.trim() || undefined,
        timeframe,
        userLocale,
        userCurrency,
        userRegion,
      });
      setResults(data);
      setSessionHistory(prev => [{
        id: Date.now(),
        date: new Date().toISOString(),
        preview: subject.trim().slice(0, 40),
        subject: subject.trim(),
        trajectory: data.trajectory,
        timestamp: new Date().toISOString(),
        result: data,
      }, ...(prev || [])].slice(0, 6));
    } catch (err) {
      setError(err.message || t('fp_error'));
    }
  };
  handleSubmitRef.current = handleSubmit;

  const loadExample = useCallback(() => {
    setSubject(t('fp_ex_subject'));
    setSubjectType('career');
    setContext(t('fp_ex_context'));
    setTimeframe('5 years');
    setResults(null);
  }, [setSubject, setSubjectType, setContext, setTimeframe, setResults, t]);

  const handleReset = () => {
    setSubject('');
    setSubjectType('');
    setContext('');
    setTimeframe('5 years');
    setResults(null);
    setError('');
    setActiveScenario('base_case');
  };

  // ─── Build full text ───
  const buildFullText = useCallback(() => {
    if (!results) return '';
    const r = results;
    const ts = trajStyle(r.trajectory);
    const lines = [
      `🔮 ${t('fp_copy_header')} — ${r.subject_as_understood || subject}`,
      `${t('fp_copy_trajectory')} ${r.trajectory_label || ts.label} (${r.trajectory_strength || ''}, ${r.confidence || ''} ${t('fp_copy_confidence')})`,
      '',
      `${t('fp_copy_pattern')}\n${r.the_pattern || ''}`,
    ];
    if (r.tailwinds?.length) {
      lines.push('', t('fp_copy_tailwinds'));
      r.tailwinds.forEach(tw => lines.push(`+ ${tw.force}: ${tw.explanation}`));
    }
    if (r.headwinds?.length) {
      lines.push('', t('fp_copy_headwinds'));
      r.headwinds.forEach(h => lines.push(`- ${h.force}: ${h.explanation} (${h.timeline || ''})`));
    }
    if (r.the_automation_question) {
      const aq = r.the_automation_question;
      lines.push('', `${t('fp_copy_automation')} ${(aq.risk_level || '').toUpperCase()}`, aq.net_effect || '');
    }
    if (r.the_pivot?.the_version_worth_pursuing) {
      lines.push('', t('fp_copy_version'), r.the_pivot.the_version_worth_pursuing);
    }
    if (r.scenarios) {
      lines.push('', t('fp_copy_scenarios'));
      if (r.scenarios.bull_case) lines.push(`${t('fp_copy_bull')} ${r.scenarios.bull_case}`);
      if (r.scenarios.base_case) lines.push(`${t('fp_copy_base')} ${r.scenarios.base_case}`);
      if (r.scenarios.bear_case) lines.push(`${t('fp_copy_bear')} ${r.scenarios.bear_case}`);
    }
    if (r.the_honest_take) lines.push('', t('fp_copy_honest'), r.the_honest_take);
    if (r.one_action)      lines.push('', `${t('fp_copy_action')} ${r.one_action}`);
    lines.push(BRAND);
    return lines.join('\n');
  }, [results, subject, t]);

  useRegisterActions(buildFullText(), tool?.title || 'Future Proof');

  // ─── Scroll useEffect ───
  useEffect(() => {
    if (!results || !resultsRef.current) return;
    const timer = setTimeout(() => resultsRef.current?.scrollIntoView({ behavior: 'smooth', block: 'start' }), 200);
    return () => clearTimeout(timer);
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [results]);

  // ─── Keyboard useEffect ───
  useEffect(() => {
    const handler = (e) => {
      const tag = document.activeElement?.tagName;
      if (tag === 'SELECT') return;
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && !loading && canSubmitRef.current)
        handleSubmitRef.current?.();
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [loading]);

  // ════════════════════════════════════════════════════════════
  // RENDER
  // ════════════════════════════════════════════════════════════
  return (
    <div className={`space-y-4 ${c.text}`}>

      {/* Input card */}
      <div className={`${c.card} border ${c.border} rounded-xl shadow-sm p-5`}>
        <div className="pb-3 border-b border-zinc-500">
          <div className="flex items-start justify-between gap-3">
            <div>
              <h2 className={`text-xl font-bold ${c.text} flex items-center gap-2`}>
                <span className="mr-2">{tool?.icon ?? '🔮'}</span>{tool?.title ?? t('fp_title')}
              </h2>
              <p className={`text-sm ${c.textSecondary}`}>{tool?.tagline ?? t('fp_tagline')}</p>
              <button onClick={loadExample} disabled={loading} style={{ backgroundColor: (tool?.headerColor ?? '#888888') + '80' }} className={`mt-1.5 px-2.5 py-0.5 rounded-full text-xs font-medium border disabled:opacity-40 ${isDark ? 'text-white border-white/40' : 'text-gray-800 border-transparent'}`}>{t('try_example')}</button>
            </div>
            {(results || subject.trim()) && (
              <button onClick={handleReset} className={`${c.btnSecondary} px-3 py-1.5 rounded-lg text-xs font-bold flex-shrink-0`}>
                ↺ {t('start_over')}
              </button>
            )}
          </div>
        </div>

        <div className="mt-4 space-y-4">
          <div>
            <label className={`block text-sm font-medium ${c.labelText} mb-1`}>
              {t('fp_q_label')} <span className={c.required}>*</span>
            </label>
            <input
              type="text"
              value={subject}
              onChange={e => setSubject(e.target.value)}
              placeholder={t('fp_ph_subject')}
              maxLength={200}
              className={`w-full px-3 py-2.5 border rounded-lg text-sm ${c.input} outline-none focus:ring-2`}
            />
          </div>

          <div className="grid grid-cols-1 sm:grid-cols-2 gap-3">
            <div>
              <label className={`block text-sm font-medium ${c.labelText} mb-1.5`}>{t('fp_type_label')} <span className={`font-normal ${c.textMuted}`}>{t('fp_optional')}</span></label>
              <div className="flex flex-wrap gap-1.5">
                {SUBJECT_TYPES.map(st => (
                  <button key={st.value} onClick={() => setSubjectType(subjectType === st.value ? '' : st.value)}
                    className={`px-2.5 py-1.5 rounded-lg text-xs font-medium border transition-colors flex items-center gap-1 ${subjectType === st.value ? c.pillActive : c.pillInactive}`}>
                    <span>{st.icon}</span> {t(st.labelKey)}
                  </button>
                ))}
              </div>
            </div>
            <div>
              <label className={`block text-sm font-medium ${c.labelText} mb-1.5`}>{t('fp_horizon_label')}</label>
              <div className="flex flex-wrap gap-1.5">
                {TIMEFRAMES.map(tf => (
                  <button key={tf.value} onClick={() => setTimeframe(tf.value)}
                    className={`px-2.5 py-1.5 rounded-lg text-xs font-medium border transition-colors ${timeframe === tf.value ? c.pillActive : c.pillInactive}`}>
                    {t(tf.labelKey)}
                  </button>
                ))}
              </div>
            </div>
          </div>

          <div>
            <label className={`block text-sm font-medium ${c.labelText} mb-1`}>
              {t('fp_context_label')} <span className={`font-normal ${c.textMuted}`}>{t('fp_optional')}</span>
            </label>
            <textarea
              value={context}
              onChange={e => setContext(e.target.value)}
              placeholder={t('fp_ph_context')}
              rows={2}
              maxLength={400}
              className={`w-full px-3 py-2.5 border rounded-lg text-sm ${c.input} outline-none focus:ring-2 resize-none`}
            />
          </div>

          <button onClick={handleSubmit} disabled={!subject.trim() || loading}
          className={`w-full ${c.btnPrimary} disabled:opacity-40 font-bold py-3 rounded-lg flex items-center justify-center gap-2 min-h-[48px]`}>
          {loading
            ? <><span className="inline-block animate-spin">{tool?.icon ?? '🔮'}</span> {t('fp_analyzing')}</>
            : <><span>{tool?.icon ?? '🔮'}</span> {t('fp_run')}</>}
          </button>

          {/* Pre-result cross-ref */}
          <p className={`text-xs text-center ${c.textMuted}`}>
            {t('fp_xref_pre')}{' '}
            <a href="/LeverageLogic" className={linkStyle}>🎯 {t('fp_leverage_logic')}</a>{' '}
            {t('fp_xref_leverage_suffix')}
          </p>
        </div>
      </div>

      {error && (
        <div className={`${c.danger} border rounded-lg p-4 flex items-start gap-3`}>
          <span className="flex-shrink-0">⚠️</span>
          <p className="text-sm">{error}</p>
        </div>
      )}

      {results && (
        <div ref={resultsRef} className="space-y-4">

          {/* Trajectory banner */}
          {results?.trajectory && (() => {
            const ts = trajStyle(results?.trajectory);
            return (
              <div className={`${ts.bg} border-2 rounded-xl p-5`}>
                <div className="flex items-center justify-between flex-wrap gap-3 mb-3">
                  <div>
                    <p className={`text-[10px] font-bold uppercase ${c.textMuted} mb-0.5`}>
                      {results?.subject_as_understood || subject} · {timeframe}
                    </p>
                    <p className={`text-xl font-bold ${ts.txt}`}>
                      {ts.dot} {results?.trajectory_label || ts.label}
                    </p>
                  </div>
                  <div className="flex items-center gap-3">
                    {results?.trajectory_strength && (
                      <div className="text-center">
                        <p className={`text-[9px] ${c.textMuted} uppercase`}>{t('fp_signal')}</p>
                        <p className={`text-xs font-mono font-bold ${ts.txt}`}>{STRENGTH_ICONS[results?.trajectory_strength] || results?.trajectory_strength}</p>
                      </div>
                    )}
                    {results?.confidence && (
                      <div className="text-center">
                        <p className={`text-[9px] ${c.textMuted} uppercase`}>{t('fp_confidence')}</p>
                        <p className={`text-xs font-bold capitalize ${ts.txt}`}>{results?.confidence}</p>
                      </div>
                    )}
                  </div>
                </div>
                {results?.the_pattern && (
                  <p className={`text-sm ${ts.txt} leading-relaxed`}>{results?.the_pattern}</p>
                )}
              </div>
            );
          })()}

          {/* Tailwinds + Headwinds */}
          {(results?.tailwinds?.length > 0 || results?.headwinds?.length > 0) && (
            <div className="grid grid-cols-1 sm:grid-cols-2 gap-3">
              {results?.tailwinds?.length > 0 && (
                <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
                  <p className={`text-[10px] font-bold uppercase ${c.textMuted} mb-3`}>🌬️ {t('fp_tailwinds')}</p>
                  <div className="space-y-3">
                    {results?.tailwinds?.map((tw, i) => (
                      <div key={i}>
                        <div className="flex items-center justify-between gap-2 mb-0.5">
                          <p className={`text-xs font-semibold ${c.text}`}>{tw.force}</p>
                          {tw.strength && <span className={`text-[9px] font-mono ${c.textMuted}`}>{STRENGTH_ICONS[tw.strength] || tw.strength}</span>}
                        </div>
                        <p className={`text-xs ${c.textSecondary}`}>{tw.explanation}</p>
                      </div>
                    ))}
                  </div>
                </div>
              )}
              {results?.headwinds?.length > 0 && (
                <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
                  <p className={`text-[10px] font-bold uppercase ${c.textMuted} mb-3`}>⛈️ {t('fp_headwinds')}</p>
                  <div className="space-y-3">
                    {results?.headwinds?.map((h, i) => (
                      <div key={i}>
                        <div className="flex items-center justify-between gap-2 mb-0.5">
                          <p className={`text-xs font-semibold ${c.text}`}>{h.force}</p>
                          {h.timeline && <span className={`text-[9px] ${c.textMuted}`}>{h.timeline}</span>}
                        </div>
                        <p className={`text-xs ${c.textSecondary}`}>{h.explanation}</p>
                      </div>
                    ))}
                  </div>
                </div>
              )}
            </div>
          )}

          {/* Automation question */}
          {results?.the_automation_question && (() => {
            const aq = results?.the_automation_question;
            const as_ = autoStyle(aq.risk_level);
            return (
              <div className={`${as_.bg} border rounded-xl p-4`}>
                <div className="flex items-center gap-2 mb-3">
                  <p className={`text-[10px] font-bold uppercase ${c.textMuted}`}>🤖 {t('fp_automation_q')}</p>
                  {aq.risk_level && (
                    <span className={`text-[10px] font-bold uppercase px-2 py-0.5 rounded-full border ${as_.bg} ${as_.txt}`}>
                      {aq.risk_level} {t('fp_risk_suffix')}
                    </span>
                  )}
                </div>
                <div className="space-y-2">
                  {aq.what_gets_automated && (
                    <div>
                      <p className={`text-[10px] font-semibold ${c.textMuted} mb-0.5`}>{t('fp_what_automated')}</p>
                      <p className={`text-xs ${c.textSecondary}`}>{aq.what_gets_automated}</p>
                    </div>
                  )}
                  {aq.what_doesnt && (
                    <div>
                      <p className={`text-[10px] font-semibold ${c.textMuted} mb-0.5`}>{t('fp_what_doesnt')}</p>
                      <p className={`text-xs ${c.textSecondary}`}>{aq.what_doesnt}</p>
                    </div>
                  )}
                  {aq.net_effect && (
                    <div className={`pt-2 border-t ${c.border}`}>
                      <p className={`text-xs font-medium ${as_.txt}`}>{aq.net_effect}</p>
                    </div>
                  )}
                </div>
              </div>
            );
          })()}

          {/* The pivot */}
          {results?.the_pivot && (
            <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
              <p className={`text-[10px] font-bold uppercase ${c.textMuted} mb-3`}>🔀 {t('fp_adjacent_moves')}</p>
              {results?.the_pivot?.adjacent_moves?.length > 0 && (
                <div className="space-y-3 mb-4">
                  {results?.the_pivot?.adjacent_moves.map((move, i) => (
                    <div key={i} className={`${c.cardAlt} rounded-lg p-3`}>
                      <div className="flex items-start justify-between gap-2 mb-1">
                        <p className={`text-sm font-semibold ${c.text}`}>{move.move}</p>
                        {move.effort_required && (
                          <span className={`text-[9px] flex-shrink-0 ${c.textMuted}`}>{EFFORT_KEYS[move.effort_required] ? t(EFFORT_KEYS[move.effort_required]) : move.effort_required}</span>
                        )}
                      </div>
                      <p className={`text-xs ${c.textSecondary}`}>{move.why_it_changes_the_picture}</p>
                    </div>
                  ))}
                </div>
              )}
              {results?.the_pivot?.the_version_worth_pursuing && (
                <div className={`${c.infoBox} border rounded-lg p-3`}>
                  <p className={`text-[10px] font-bold uppercase mb-1`}>💡 {t('fp_version_worth')}</p>
                  <p className="text-xs leading-relaxed">{results?.the_pivot?.the_version_worth_pursuing}</p>
                </div>
              )}
            </div>
          )}

          {/* Scenarios */}
          {results?.scenarios && (
            <div className={`${c.card} border ${c.border} rounded-xl overflow-hidden`}>
              <div className={`flex border-b ${c.border}`}>
                {[
                  { key: 'bull_case', label: t('fp_scenario_bull'), icon: '🐂' },
                  { key: 'base_case', label: t('fp_scenario_base'), icon: '📊' },
                  { key: 'bear_case', label: t('fp_scenario_bear'), icon: '🐻' },
                ].map(s => (
                  <button key={s.key} onClick={() => setActiveScenario(s.key)}
                    className={`flex-1 flex items-center justify-center gap-1.5 px-4 py-3 text-sm font-semibold transition-all ${
                      activeScenario === s.key
                        ? (isDark ? 'bg-zinc-900 text-cyan-300 border-b-2 border-cyan-500' : 'bg-white text-cyan-700 border-b-2 border-cyan-600')
                        : (isDark ? 'text-zinc-400 hover:bg-zinc-700/50' : 'text-gray-500 hover:bg-gray-50')
                    }`}>
                    <span>{s.icon}</span>
                    <span className="hidden sm:inline">{s.label} {t('fp_scenario_case')}</span>
                    <span className="sm:hidden">{s.label}</span>
                  </button>
                ))}
              </div>
              <div className="p-5">
                <p className={`text-sm ${c.textSecondary} leading-relaxed`}>{results?.scenarios?.[activeScenario] || '—'}</p>
              </div>
            </div>
          )}

          {/* Honest take */}
          {results?.the_honest_take && (
            <div className={`${c.cardAlt} border ${c.border} rounded-xl p-5`}>
              <p className={`text-[10px] font-bold uppercase ${c.textMuted} mb-2`}>📣 {t('fp_honest_take')}</p>
              <p className={`text-sm ${c.text} leading-relaxed`}>{results?.the_honest_take}</p>
            </div>
          )}

          {/* One action */}
          {results?.one_action && (
            <div className={`${c.success} border-2 rounded-xl p-5`}>
              <p className={`text-[10px] font-bold uppercase mb-2`}>⚡ {t('fp_one_action')}</p>
              <p className={`text-sm font-semibold leading-relaxed`}>{results?.one_action}</p>
            </div>
          )}

          {/* Post-result cross-refs */}
          <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
            <p className={`text-[10px] font-bold ${c.textMuted} uppercase mb-2`}>🔗 {t('fp_related')}</p>
            <div className="flex flex-wrap gap-3">
              <a href="/DecisionCoach" className={`text-xs ${linkStyle}`}>🧭 {t('fp_decision_coach')}</a>
              <a href="/LeverageLogic" className={`text-xs ${linkStyle}`}>🎯 {t('fp_leverage_logic')}</a>
            </div>
          </div>
        </div>
      )}

      {/* History */}
      {sessionHistory?.length > 0 && !results && (
        <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
          <h3 className={`text-sm font-bold ${c.text} mb-3`}>🕐 {t('fp_recent')}</h3>
          <div className="space-y-1.5">
            {sessionHistory.map(entry => {
              const ts = trajStyle(entry.trajectory);
              return (
                <button key={entry.id} onClick={() => setResults(entry.result)}
                  className={`w-full text-left px-3 py-2 rounded-lg ${c.btnSecondary} text-xs flex items-center gap-2`}>
                  <span>{ts.dot}</span>
                  <span className={`${c.text} flex-1 truncate`}>{entry.subject}</span>
                  <span className={c.textMuted}>
                    {new Date(entry.timestamp).toLocaleDateString(undefined, { month: 'short', day: 'numeric' })}
                  </span>
                </button>
              );
            })}
          </div>
        </div>
      )}
    </div>
  );
};

FutureProof.displayName = 'FutureProof';
export default FutureProof;
