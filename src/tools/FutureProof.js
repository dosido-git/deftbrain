import React, { useState, useEffect } from 'react';
import { useTheme } from '../hooks/useTheme';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { usePersistentState } from '../hooks/usePersistentState';
import { CopyBtn, ActionBar } from '../components/ActionButtons';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const SUBJECT_TYPES = [
  { id: 'skill', label: 'Skill', icon: '🧠' },
  { id: 'career', label: 'Career', icon: '💼' },
  { id: 'investment', label: 'Investment', icon: '📈' },
  { id: 'technology', label: 'Technology', icon: '💻' },
  { id: 'commitment', label: 'Commitment', icon: '🤝' },
  { id: 'habit', label: 'Habit', icon: '🔁' },
];

const EXAMPLES = [
  { s: 'Learning SQL', t: 'skill', ctx: 'Work in finance, thinking about moving to data analysis' },
  { s: 'Real estate agent', t: 'career', ctx: 'Currently in sales, considering the switch' },
  { s: 'Moving to a LCOL city to save money', t: 'commitment', ctx: 'Remote worker, 30s, considering Austin or Raleigh' },
  { s: 'Becoming a UX designer', t: 'career', ctx: 'Graphic design background, interested in tech products' },
  { s: 'Learning a second language (Mandarin)', t: 'skill', ctx: 'Business context, interested in China market opportunities' },
];

const TRAJECTORY_CONFIG = {
  'growing':           { label: 'GROWING',           icon: '↑', color: 'emerald', light: 'bg-emerald-50 border-emerald-200 text-emerald-800', dark: 'bg-emerald-900/20 border-emerald-700/40 text-emerald-300' },
  'stable':            { label: 'STABLE',             icon: '→', color: 'blue',    light: 'bg-emerald-50 border-emerald-200 text-emerald-800', dark: 'bg-cyan-900/20 border-cyan-700/40 text-cyan-300' },
  'transforming':      { label: 'TRANSFORMING',       icon: '⟳', color: 'amber',   light: 'bg-emerald-50 border-emerald-200 text-emerald-800', dark: 'bg-amber-900/20 border-amber-700/40 text-amber-300' },
  'declining':         { label: 'DECLINING',          icon: '↓', color: 'red',     light: 'bg-emerald-50 border-emerald-200 text-emerald-800', dark: 'bg-red-900/20 border-red-700/40 text-red-300' },
  'volatile':          { label: 'VOLATILE',           icon: '⚡', color: 'purple',  light: 'bg-amber-50 border-amber-200 text-amber-800', dark: 'bg-amber-900/20 border-amber-700 text-amber-300' },
  'context_dependent': { label: 'CONTEXT-DEPENDENT',  icon: '◇', color: 'neutral', light: 'bg-gray-50 border-gray-200 text-gray-700',   dark: 'bg-zinc-700/50 border-zinc-600 text-zinc-300' },
};

const FutureProof = ({ tool }) => {
  const { isDark } = useTheme();

  useEffect(() => {
    const handler = (e) => {
      const tag = document.activeElement?.tagName;
      if (tag === 'INPUT' || tag === 'TEXTAREA' || tag === 'SELECT') return;
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && !loading) handleSubmit();
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
  }, [loading]);
  const { callToolEndpoint, loading } = useClaudeAPI();


  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';

  const c = {
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:         isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20' : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-500' : 'text-gray-400',
    labelText:     isDark ? 'text-zinc-200' : 'text-gray-700',
    accentTxt:     isDark ? 'text-cyan-400' : 'text-cyan-600',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white' : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200' : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200' : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200' : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200' : 'bg-red-50 border-red-200 text-red-800',
    infoBox:       isDark ? 'bg-sky-900/20 border-sky-700 text-sky-200' : 'bg-sky-50 border-sky-200 text-sky-800',
    successBox:    isDark ? 'bg-emerald-900/20 border-emerald-700' : 'bg-emerald-50 border-emerald-300',
    successTxt:    isDark ? 'text-emerald-300' : 'text-emerald-800',
    warningBox:    isDark ? 'bg-amber-900/20 border-amber-700' : 'bg-amber-50 border-amber-300',
    warningTxt:    isDark ? 'text-amber-300' : 'text-amber-800',
    pillActive:    isDark ? 'border-cyan-500 bg-cyan-900/30 text-cyan-200' : 'border-cyan-600 bg-cyan-100 text-cyan-900',
    pillInactive:  isDark ? 'border-zinc-600 text-zinc-400 hover:border-zinc-500' : 'border-gray-300 text-gray-500 hover:border-gray-400',
  };

  const [subject, setSubject] = usePersistentState('futureproof-subject', '');
  const [history, setHistory] = usePersistentState('futureproof-history', []);
  const [subjectType, setSubjectType] = useState('');
  const [context, setContext] = useState('');
  const [results, setResults] = usePersistentState('futureproof-results', null);
  const [error, setError] = useState('');
  const resultsRef = React.useRef(null);
  const [expandedSections, setExpandedSections] = useState({ scenarios: false, automation: false });

  const toggle = (key) => setExpandedSections(p => ({ ...p, [key]: !p[key] }));

  const handleSubmit = async () => {
    if (!subject.trim()) return;
    setError(''); setResults(null);
    try {
      const data = await callToolEndpoint('future-proof', {
        subject: subject.trim(),
        subjectType: subjectType || undefined,
        context: context.trim() || undefined,
      });
      setResults(data);
      setHistory(prev => [{ id: Date.now(), date: new Date().toISOString(), preview: '' }, ...prev].slice(0, 6));
      setTimeout(() => resultsRef.current?.scrollIntoView({ behavior: 'smooth' }), 100);
    } catch (e) { setError(e.message || 'Failed to analyze trajectory.'); }
  };

  const handleExample = () => {
    const ex = EXAMPLES[Math.floor(Math.random() * EXAMPLES.length)];
    setSubject(ex.s); setSubjectType(ex.t); setContext(ex.ctx); setResults(null);
  };

  const buildText = () => {
    if (!results) return '';
    const traj = TRAJECTORY_CONFIG[results.trajectory];
    let t = `🔮 FUTUREPROOF\n\n${results.subject_as_understood}\n\nTrajectory: ${traj?.label || results.trajectory_label} (${results.confidence} confidence)\n\n`;
    t += `The Pattern:\n${results.the_pattern}\n\n`;
    if (results.tailwinds?.length) { t += `Tailwinds:\n${results.tailwinds.map(w => `• ${w.force}: ${w.explanation}`).join('\n')}\n\n`; }
    if (results.headwinds?.length) { t += `Headwinds:\n${results.headwinds.map(w => `• ${w.force}: ${w.explanation}`).join('\n')}\n\n`; }
    if (results.the_pivot?.the_version_worth_pursuing) { t += `The Version Worth Pursuing:\n${results.the_pivot.the_version_worth_pursuing}\n\n`; }
    if (results.the_honest_take) { t += `The Honest Take:\n${results.the_honest_take}\n\n`; }
    if (results.one_action) { t += `One Action:\n${results.one_action}\n`; }
    return t + BRAND;
  };

  const traj = results ? (TRAJECTORY_CONFIG[results.trajectory] || TRAJECTORY_CONFIG['stable']) : null;
  const trajStyle = traj ? (isDark ? traj.dark : traj.light) : '';

  return (
    <div className="py-8 px-4">
      <div className="max-w-2xl mx-auto">

        {/* Header */}
        <div className="text-center mb-8">
          <div className="text-5xl mb-3">🔮</div>
          <h1 className={`text-3xl font-black tracking-tight mb-2 ${c.text}`}>FutureProof</h1>
          <p className={`text-sm ${c.textMuteded}`}>5-year trajectory analysis. Not predictions — pattern recognition.</p>
        </div>

        {/* Input */}
        {!results && (
          <div className={`rounded-2xl border p-6 shadow-sm space-y-4 ${c.card} ${c.border}`}>
            <div>
              <label className={`block text-sm font-semibold mb-1.5 ${c.text}`}>What do you want to assess? <span className="text-red-400">*</span></label>
              <input type="text" value={subject} onChange={e => setSubject(e.target.value)} onKeyDown={e => { if (e.key === 'Enter' && subject.trim()) handleSubmit(); }}
                placeholder="e.g. Learning Python, real estate agent career, buying rental property…"
                className={`w-full px-4 py-3 rounded-xl border text-sm ${c.input}`} />
            </div>

            <div>
              <label className={`block text-sm font-semibold mb-2 ${c.text}`}>What is it? <span className={`font-normal ${c.textMuteded}`}>(optional — helps calibrate the analysis)</span></label>
              <div className="flex flex-wrap gap-2">
                {SUBJECT_TYPES.map(t => (
                  <button key={t.id} onClick={() => setSubjectType(subjectType === t.id ? '' : t.id)}
                    className={`px-3 py-1.5 rounded-lg text-xs font-semibold border transition-all ${
                      subjectType === t.id
                        ? 'bg-cyan-600 text-white border-cyan-600'
                        : `${c.btnSecondaryondary}`
                    }`}>
                    {t.icon} {t.label}
                  </button>
                ))}
              </div>
            </div>

            <div>
              <label className={`block text-sm font-semibold mb-1.5 ${c.text}`}>Your context <span className={`font-normal ${c.textMuteded}`}>(makes it much more specific)</span></label>
              <textarea value={context} onChange={e => setContext(e.target.value)} onKeyDown={e => { if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && subject.trim()) handleSubmit(); }}
                placeholder="Your background, why you're considering this, your situation…"
                rows={2} maxLength={300}
                className={`w-full px-4 py-3 rounded-xl border text-sm resize-none ${c.input}`} />
            </div>

            {error && <div className={`p-3 rounded-xl border text-sm ${c.danger}`}><span className="mr-1">⚠️</span>{error}</div>}

            <div className="flex gap-2">
              <button onClick={handleSubmit} disabled={loading || !subject.trim()}
                className={`flex-1 py-3 rounded-xl font-bold disabled:opacity-40 ${c.btnPrimary}`}>
                {loading ? <><span className="animate-spin inline-block mr-2">{tool?.icon ?? '🔮'}</span>Analyzing…</> : '🔮 Analyze Trajectory'}
              </button>
              <button onClick={handleExample} disabled={loading}
                className={`px-4 py-3 rounded-xl text-sm font-medium ${c.btnSecondaryondary}`}>
                Example
              </button>
            </div>
          </div>
        )}

        {/* Results */}
        {results && (
          <div className="space-y-4">

            {/* Trajectory header */}
            <div className={`rounded-2xl border p-6 text-center ${trajStyle}`}>
              <p className={`text-xs font-black uppercase tracking-widest mb-2 opacity-70`}>5-Year Trajectory</p>
              <div className="text-5xl font-black mb-2">{traj?.icon || '→'}</div>
              <h2 className="text-2xl font-black tracking-tight">{results.trajectory_label || traj?.label}</h2>
              <p className="text-xs mt-2 opacity-70 uppercase tracking-wide">{results.trajectory_strength} signal · {results.confidence} confidence</p>
              <p className="text-sm mt-3 font-medium opacity-90">{results.subject_as_understood}</p>
            </div>

            {/* The Pattern */}
            <div className={`rounded-2xl border p-5 ${c.card} ${c.border}`}>
              <p className={`text-xs font-bold uppercase tracking-wider mb-2 ${c.textMuteded}`}>📊 The Pattern</p>
              <p className={`text-sm leading-relaxed ${c.textSecondaryondary}`}>{results.the_pattern}</p>
            </div>

            {/* Tailwinds + Headwinds */}
            <div className="grid grid-cols-1 sm:grid-cols-2 gap-4">
              {results.tailwinds?.length > 0 && (
                <div className={`rounded-2xl border p-4 ${c.card} ${c.border}`}>
                  <p className={`text-xs font-bold uppercase tracking-wider mb-3 ${isDark ? 'text-emerald-300' : 'text-emerald-700'}`}>↑ Tailwinds</p>
                  <div className="space-y-2">
                    {results.tailwinds.map((w, i) => (
                      <div key={i} className="text-sm">
                        <span className={`font-semibold ${c.text}`}>{w.force}</span>
                        <span className={` · ${c.textMuteded} text-xs`}>{w.strength}</span>
                        <p className={`text-xs mt-0.5 ${c.textMuteded}`}>{w.explanation}</p>
                      </div>
                    ))}
                  </div>
                </div>
              )}
              {results.headwinds?.length > 0 && (
                <div className={`rounded-2xl border p-4 ${c.card} ${c.border}`}>
                  <p className={`text-xs font-bold uppercase tracking-wider mb-3 ${isDark ? 'text-red-300' : 'text-red-800'}`}>↓ Headwinds</p>
                  <div className="space-y-2">
                    {results.headwinds.map((w, i) => (
                      <div key={i} className="text-sm">
                        <span className={`font-semibold ${c.text}`}>{w.force}</span>
                        {w.timeline && <span className={` · text-xs ${c.textMuteded}`}>{w.timeline}</span>}
                        <p className={`text-xs mt-0.5 ${c.textMuteded}`}>{w.explanation}</p>
                      </div>
                    ))}
                  </div>
                </div>
              )}
            </div>

            {/* Automation */}
            {results.the_automation_question && (
              <div className={`rounded-2xl border overflow-hidden ${c.card} ${c.border}`}>
                <button onClick={() => toggle('automation')} className="w-full text-left px-5 py-4 flex items-center justify-between">
                  <p className={`text-xs font-bold uppercase tracking-wider ${c.textMuteded}`}>🤖 The Automation Question</p>
                  <span className={`text-sm ${c.textMuteded}`}>{expandedSections.automation ? '▲' : '▼'}</span>
                </button>
                {expandedSections.automation && (
                  <div className={`px-5 pb-5 space-y-2 border-t ${c.border}`}>
                    <div className="pt-3 flex items-center gap-2">
                      <span className={`text-xs font-bold px-2 py-0.5 rounded-full ${
                        results.the_automation_question.risk_level === 'high' ? (isDark ? 'bg-red-900/20 text-red-300' : 'bg-red-50 text-red-800')
                        : results.the_automation_question.risk_level === 'medium' ? (isDark ? 'bg-amber-900/20 text-amber-300' : 'bg-amber-50 text-amber-800')
                        : (isDark ? 'bg-emerald-900/20 text-emerald-300' : 'bg-emerald-50 text-emerald-800')
                      }`}>
                        {results.the_automation_question.risk_level} risk
                      </span>
                    </div>
                    {results.the_automation_question.what_gets_automated && (
                      <p className={`text-sm ${c.textSecondaryondary}`}><span className={`font-semibold ${c.text}`}>Gets automated:</span> {results.the_automation_question.what_gets_automated}</p>
                    )}
                    {results.the_automation_question.what_doesnt && (
                      <p className={`text-sm ${c.textSecondaryondary}`}><span className={`font-semibold ${c.text}`}>Hard to automate:</span> {results.the_automation_question.what_doesnt}</p>
                    )}
                    <p className={`text-sm ${c.textSecondaryondary}`}>{results.the_automation_question.net_effect}</p>
                  </div>
                )}
              </div>
            )}

            {/* The Pivot */}
            {results.the_pivot && (
              <div className={`rounded-2xl border p-5 ${isDark ? 'bg-amber-900/10 border-amber-700/30' : 'bg-amber-50 border-amber-200'}`}>
                <p className={`text-xs font-bold uppercase tracking-wider mb-3 ${isDark ? 'text-amber-300' : 'text-amber-800'}`}>🎯 The Version Worth Pursuing</p>
                {results.the_pivot.the_version_worth_pursuing && (
                  <p className={`text-sm leading-relaxed mb-3 ${c.textSecondaryondary}`}>{results.the_pivot.the_version_worth_pursuing}</p>
                )}
                {results.the_pivot.adjacent_moves?.length > 0 && (
                  <div className="space-y-2">
                    {results.the_pivot.adjacent_moves.map((m, i) => (
                      <div key={i} className={`p-3 rounded-xl ${isDark ? 'bg-zinc-900/50' : 'bg-white/70'}`}>
                        <p className={`text-sm font-semibold ${c.text}`}>{m.move}</p>
                        <p className={`text-xs mt-0.5 ${c.textMuteded}`}>{m.why_it_changes_the_picture}</p>
                        <span className={`text-xs font-medium ${c.textMuteded}`}>Effort: {m.effort_required}</span>
                      </div>
                    ))}
                  </div>
                )}
              </div>
            )}

            {/* Scenarios */}
            {results.scenarios && (
              <div className={`rounded-2xl border overflow-hidden ${c.card} ${c.border}`}>
                <button onClick={() => toggle('scenarios')} className="w-full text-left px-5 py-4 flex items-center justify-between">
                  <p className={`text-xs font-bold uppercase tracking-wider ${c.textMuteded}`}>🎲 Best / Base / Worst Case</p>
                  <span className={`text-sm ${c.textMuteded}`}>{expandedSections.scenarios ? '▲' : '▼'}</span>
                </button>
                {expandedSections.scenarios && (
                  <div className={`px-5 pb-5 grid grid-cols-1 gap-3 border-t ${c.border} pt-4`}>
                    {[
                      { key: 'bull_case', label: '🟢 Best case', bg: isDark ? 'bg-emerald-900/10 border-emerald-700/30' : 'bg-emerald-50 border-emerald-200' },
                      { key: 'base_case', label: '🔵 Most likely', bg: isDark ? 'bg-cyan-900/10 border-cyan-700/30' : 'bg-cyan-50 border-cyan-200' },
                      { key: 'bear_case', label: '🔴 Worst case', bg: isDark ? 'bg-red-900/10 border-red-700/30' : 'bg-red-50 border-red-200' },
                    ].map(s => results.scenarios[s.key] && (
                      <div key={s.key} className={`p-3 rounded-xl border ${s.bg}`}>
                        <p className={`text-xs font-bold mb-1 ${c.textMuteded}`}>{s.label}</p>
                        <p className={`text-sm ${c.textSecondaryondary}`}>{results.scenarios[s.key]}</p>
                      </div>
                    ))}
                  </div>
                )}
              </div>
            )}

            {/* The Honest Take */}
            {results.the_honest_take && (
              <div className={`rounded-2xl border p-5 ${c.card} ${c.border}`}>
                <p className={`text-xs font-bold uppercase tracking-wider mb-2 ${c.textMuteded}`}>💬 The Honest Take</p>
                <p className={`text-sm leading-relaxed ${c.textSecondaryondary}`}>{results.the_honest_take}</p>
              </div>
            )}

            {/* One Action */}
            {results.one_action && (
              <div className={`rounded-2xl border-2 p-5 ${isDark ? 'border-cyan-700 bg-cyan-900/10' : 'border-cyan-400 bg-cyan-50'}`}>
                <p className={`text-xs font-bold uppercase tracking-wider mb-2 ${isDark ? 'text-cyan-300' : 'text-cyan-700'}`}>⚡ One Action — Next 90 Days</p>
                <p className={`text-sm font-medium ${c.text}`}>{results.one_action}</p>
              </div>
            )}

            {/* Actions */}
            <div className="flex items-center gap-3 flex-wrap">
              <ActionBar content={buildText()} title="FutureProof Analysis" />
              <button onClick={() => { setResults(null); setSubject(''); setSubjectType(''); setContext(''); }}
                className={`px-4 py-2 rounded-lg text-sm font-medium ${c.btnSecondaryondary}`}>
                🔄 New Analysis
              </button>
            </div>

            {/* Cross-references */}
            <div className={`rounded-xl border p-4 ${c.cardAlt} ${c.border}`}>
              <p className={`text-xs font-semibold uppercase tracking-wide mb-3 ${c.textMuteded}`}>Related tools</p>
              <div className="flex flex-wrap gap-2">
                {[
                  { id: 'PreMortem', icon: '💀', label: 'Write your plan\'s obituary' },
                  { id: 'SignalVsNoise', icon: '📡', label: 'Cut through conflicting advice' },
                  { id: 'EgoKiller', icon: '🪦', label: 'Challenge your assumptions' },
                ].map(r => (
                  <a key={r.id} href={`${r.id}`} target="_blank" rel="noopener noreferrer"
                    className={`px-3 py-1.5 rounded-lg text-xs font-medium transition-colors ${c.btnSecondaryondary}`}>
                    {r.icon} {r.label}
                  </a>
                ))}
              </div>
            </div>
          </div>
        )}
      </div>
    </div>
  );
};

FutureProof.displayName = 'FutureProof';
export default FutureProof;
