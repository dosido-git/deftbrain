import React, { useState, useCallback, useEffect, useRef } from 'react';
import { useRegisterActions } from '../components/ActionBarContext';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { usePersistentState } from '../hooks/usePersistentState';
import { useTheme } from '../hooks/useTheme';
import { useTranslation } from '../i18n/useTranslation';
import { currencySymbol } from '../utils/formatLocale';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const WhatIf = ({ tool }) => {
  const { callToolEndpoint, loading, userLocale, userCurrency, userRegion } = useClaudeAPI();
  const { isDark } = useTheme();
  const { t } = useTranslation();
  const sym = currencySymbol(userLocale, userCurrency);

  const TIMEFRAMES = [
    { value: 'one_month', label: t('wi_tf_one_month'), emoji: '📅' },
    { value: 'six_months', label: t('wi_tf_six_months'), emoji: '🗓️' },
    { value: 'one_year', label: t('wi_tf_one_year'), emoji: '🔮' },
    { value: 'five_years', label: t('wi_tf_five_years'), emoji: '🌍' },
  ];

  const EXAMPLES = [
    {
      decision: t('wi_ex1_decision'),
      optionNotChosen: t('wi_ex1_option'),
      context: t('wi_ex1_context', { sym }),
      timeframe: 'one_year',
    },
    {
      decision: t('wi_ex2_decision'),
      optionNotChosen: t('wi_ex2_option'),
      context: t('wi_ex2_context', { sym }),
      timeframe: 'five_years',
    },
  ];

  const c = {
    card:              isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:           isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:             isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20' : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:              isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary:     isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:         isDark ? 'text-zinc-500' : 'text-gray-400',
    required:      isDark ? 'text-amber-400' : 'text-amber-500',
    btnPrimary:        isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white' : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:      isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200' : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:            isDark ? 'border-zinc-700' : 'border-gray-200',
    success:           isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200' : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:           isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200' : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:            isDark ? 'bg-red-900/20 border-red-700 text-red-200' : 'bg-red-50 border-red-200 text-red-800',
    pillActive:        isDark ? 'border-cyan-500 bg-cyan-900/30 text-cyan-200' : 'border-cyan-600 bg-cyan-100 text-cyan-900',
    pillInactive:      isDark ? 'border-zinc-600 text-zinc-400 hover:border-zinc-500' : 'border-gray-300 text-gray-500 hover:border-gray-400',
    badge:             isDark ? 'bg-cyan-900/30 text-cyan-300' : 'bg-cyan-100 text-cyan-800',
    labelText:         isDark ? 'text-zinc-200' : 'text-gray-700',
    // Category panel tints — Teal family (What If?)
    // Gold AI insight — all tools
    // Tool-specific
    infoBox:           isDark ? 'bg-[#2a5248]/30 border-[#3a6e60]' : 'bg-[#e4f2f0] border-[#8cc8c0]',
    sceneBg:           isDark ? 'bg-zinc-700/40' : 'bg-[#f3efe8]',
    timelineBar:       isDark ? 'bg-zinc-600' : 'bg-zinc-300',
  };
  c.textMuteded = c.textMuted;
  c.label = c.labelText;

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';

  // ─── State (useState before useRef before usePersistentState — PF-11/PF-14) ───
  const [optionNotChosen, setOptionNotChosen] = useState('');
  const [context, setContext] = useState('');
  const [timeframe, setTimeframe] = useState('one_year');
  const [error, setError] = useState('');

  // ─── Refs ───
  const resultsRef = useRef(null);

  // ─── Persistent state ───
  const [decision, setDecision] = usePersistentState('whatif-decision', '');
  const [results, setResults] = usePersistentState('whatif-result', null);
  const [pastDecisions, setPastDecisions] = usePersistentState('whatif-history', []);

  const loadExample = () => {
    const ex = EXAMPLES[Math.floor(Math.random() * EXAMPLES.length)];
    setDecision(ex.decision);
    setOptionNotChosen(ex.optionNotChosen);
    setContext(ex.context);
    setTimeframe(ex.timeframe);
  };

  // ─── Actions ───
  const generate = useCallback(async () => {
    if (!decision.trim()) return;
    setError('');
    setResults(null);

    try {
      const data = await callToolEndpoint('what-if', {
        decision: decision.trim(),
        optionNotChosen: optionNotChosen.trim() || null,
        context: context.trim() || null,
        timeframe,
        userLocale, userCurrency, userRegion,
      });
      setResults(data);
      setPastDecisions(prev => [{
        id: Date.now(), date: new Date().toISOString(),
        preview: decision.trim().slice(0, 40),
      }, ...prev].slice(0, 6));
    } catch (err) {
      setError(err.message || t('wi_error'));
    }
  }, [decision, optionNotChosen, context, timeframe, callToolEndpoint, setResults, setPastDecisions, userLocale, userCurrency, userRegion, t]);

  const handleReset = useCallback(() => {
    setDecision(''); setOptionNotChosen(''); setContext('');
    setTimeframe('one_year'); setResults(null); setError('');
  }, [setResults]);

  // ─── Build text ───
  const buildFullText = useCallback(() => {
    if (!results) return '';
    const r = results;
    let text = `🎲 ${t('wi_copy_header')}\n`;
    if (r.decision_read) text += `\n${r.decision_read}\n`;
    if (r.the_path_not_taken) text += `\n${t('wi_copy_path')} ${r.the_path_not_taken}\n`;
    if (r.scenarios?.length) {
      r.scenarios.forEach(s => {
        text += `\n--- ${s.timepoint} ---\n${s.scene}`;
        if (s.the_good) text += `\n✅ ${s.the_good}`;
        if (s.the_cost) text += `\n💔 ${s.the_cost}`;
      });
    }
    if (r.the_surprise) text += `\n\n⚡ ${t('wi_copy_surprise')} ${r.the_surprise}`;
    if (r.what_you_keep) text += `\n🔒 ${t('wi_copy_keep')} ${r.what_you_keep}`;
    if (r.what_you_lose) text += `\n💔 ${t('wi_copy_lose')} ${r.what_you_lose}`;
    if (r.clarity_question) text += `\n\n❓ ${r.clarity_question}`;
    if (r.honest_take) text += `\n\n${r.honest_take}`;
    return text + BRAND;
  }, [results, t]);

  // ─── Register actions ───
  useRegisterActions(buildFullText(), tool?.title);

  // ─── Scroll to results ───
  useEffect(() => {
    if (!results) return;
    const timer = setTimeout(() => resultsRef.current?.scrollIntoView({ behavior: 'smooth', block: 'start' }), 100);
    return () => clearTimeout(timer);
  }, [results]);

  // ─── Global Cmd/Ctrl+Enter — SELECT-only guard, ref pattern ───
  const generateRef = useRef(null);
  const canSubmitRef = useRef(false);
  generateRef.current = generate;
  canSubmitRef.current = !!decision.trim() && !loading;

  useEffect(() => {
    const handler = (e) => {
      const tag = document.activeElement?.tagName;
      if (tag === 'SELECT') return;
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && !loading && canSubmitRef.current) {
        e.preventDefault();
        generateRef.current?.();
      }
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
  }, [loading]);

  // ════════════════════════════════════════════════════════════
  // RENDER
  // ════════════════════════════════════════════════════════════
  return (
    <div className={`space-y-4 ${c.text}`}>

      {/* ── INPUT CARD ── */}
      <div className={`${c.card} border ${c.border} rounded-xl shadow-sm p-6`}>
        <div className={`mb-5 pb-4 border-b border-zinc-500`}>
          <div className="flex items-start justify-between gap-3">
            <div>
              <h2 className={`text-2xl font-bold ${c.text}`}><span className="mr-2">{tool?.icon}</span>{tool?.title}</h2>
              <p className={`text-sm ${c.textSecondary} mt-1`}>{tool?.tagline}</p>
              <button onClick={loadExample} disabled={loading} style={{ backgroundColor: (tool?.headerColor ?? '#888888') + '80' }} className={`mt-1.5 px-2.5 py-0.5 rounded-full text-xs font-medium border disabled:opacity-40 ${isDark ? 'text-white border-white/40' : 'text-gray-800 border-transparent'}`}>{t('try_example')}</button>
            </div>
            {(results || decision.trim()) && (
              <button onClick={handleReset} className={`${c.btnSecondary} px-3 py-1.5 rounded-lg text-xs font-bold flex-shrink-0`}>
                ↺ {t('wi_start_over')}
              </button>
            )}
          </div>
        </div>

        {/* Decision */}
        <div className="mb-4">
          <label className={`text-sm font-bold ${c.text} block mb-1.5`}>{t('wi_q_decision')} <span className={c.required}>*</span></label>
          <textarea
            value={decision}
            onChange={e => setDecision(e.target.value)}
            onKeyDown={e => { if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && !loading && decision.trim()) { e.preventDefault(); generate(); } }}
            placeholder={t('wi_ph_decision')}
            rows={2}
            className={`w-full px-4 py-3 border rounded-xl text-sm ${c.input} ${c.border} ${c.text} outline-none focus:ring-2 resize-none`} />
        </div>

        {/* Option not chosen */}
        <div className="mb-4">
          <label className={`text-sm font-bold ${c.text} block mb-1.5`}>
            {t('wi_q_option')} <span className={`font-normal ${c.textMuted}`}>{t('wi_q_option_hint')}</span>
          </label>
          <input
            type="text"
            value={optionNotChosen}
            onChange={e => setOptionNotChosen(e.target.value)}
            placeholder={t('wi_ph_option')}
            className={`w-full px-4 py-3 border rounded-xl text-sm ${c.input} ${c.border} ${c.text} outline-none focus:ring-2`} />
        </div>

        {/* Context */}
        <div className="mb-4">
          <label className={`text-sm font-bold ${c.text} block mb-1.5`}>
            {t('wi_q_context')} <span className={`font-normal ${c.textMuted}`}>{t('wi_q_context_hint')}</span>
          </label>
          <input
            type="text"
            value={context}
            onChange={e => setContext(e.target.value)}
            placeholder={t('wi_ph_context')}
            className={`w-full px-4 py-3 border rounded-xl text-sm ${c.input} ${c.border} ${c.text} outline-none focus:ring-2`} />
        </div>

        {/* Timeframe */}
        <div className="mb-5">
          <label className={`text-sm font-bold ${c.text} block mb-2`}>{t('wi_q_timeframe')}</label>
          <div className="flex gap-2">
            {TIMEFRAMES.map(tf => (
              <button
                key={tf.value}
                onClick={() => setTimeframe(tf.value)}
                className={`flex-1 py-2.5 rounded-xl text-[11px] font-bold border transition-colors min-h-[40px] flex flex-col items-center gap-0.5 ${
                  timeframe === tf.value ? c.pillActive : c.pillInactive
                }`}>
                <span className="text-base">{tf.emoji}</span>
                {tf.label}
              </button>
            ))}
          </div>
        </div>

        {/* Actions */}
        <div className="flex gap-3">
          <button
            onClick={generate}
            disabled={loading || !decision.trim()}
            className={`flex-1 ${c.btnPrimary} disabled:opacity-40 disabled:cursor-not-allowed font-bold py-3 px-6 rounded-xl flex items-center justify-center gap-2 min-h-[48px] shadow-lg`}>
            {loading
              ? (<><span className="inline-block animate-spin">{tool?.icon ?? '🎲'}</span> {t('wi_simulating')}</>)
              : (<><span>{tool?.icon ?? '🎲'}</span> {t('wi_show_future')}</>)
            }
          </button>
        </div>

        {/* Pre-result cross-ref */}
        <p className={`text-xs ${c.textMuted} mt-3`}>
          {t('wi_pre_xref')} <a href="/WrongAnswersOnly" className={linkStyle}>🙃 {t('wi_pre_xref_link')}</a> {t('wi_pre_xref_tail')}
        </p>
      </div>

      {/* Error */}
      {error && (
        <div className={`${c.danger} border rounded-xl p-4 flex items-start gap-3`}>
          <span className="flex-shrink-0 mt-0.5">⚠️</span>
          <p className="text-sm">{error}</p>
        </div>
      )}

      {/* ══════════════════════════════════════════════════════════ */}
      {/* RESULTS                                                    */}
      {/* ══════════════════════════════════════════════════════════ */}
      {results && (
        <div className="space-y-4">

          <div ref={resultsRef} data-results-anchor />

          {/* ── DECISION READ ── */}
          {results?.decision_read && (
            <div className={`${c.card} border ${c.border} rounded-xl p-5`}>
              <p className={`text-sm ${c.textSecondary} leading-relaxed`}>{results?.decision_read}</p>
            </div>
          )}

          {/* ── PATH LABEL ── */}
          {results?.the_path_not_taken && (
            <div className={`${c.infoBox} border rounded-xl p-4 text-center`}>
              <p className="text-xs font-bold mb-0.5">{t('wi_simulating_path')}</p>
              <p className="text-sm font-bold">{results?.the_path_not_taken}</p>
            </div>
          )}

          {/* ── SCENARIOS / TIMELINE ── */}
          {results?.scenarios?.length > 0 && (
            <div className="space-y-0">
              {results?.scenarios?.map((scene, idx) => (
                <div key={idx} className="relative">
                  {/* Timeline connector */}
                  {idx < results?.scenarios?.length - 1 && (
                    <div className={`absolute left-5 top-full w-0.5 h-4 ${c.timelineBar}`} />
                  )}
                  <div className={`${c.card} border ${c.border} rounded-xl overflow-hidden ${idx > 0 ? 'mt-4' : ''}`}>
                    {/* Timepoint header */}
                    <div className={`px-5 py-3 border-b ${c.border} flex items-center gap-2`}>
                      <span className={`text-xs font-bold px-2.5 py-1 rounded-full ${c.badge}`}>
                        {scene.timepoint}
                      </span>
                    </div>

                    {/* The scene */}
                    <div className="p-5">
                      <div className={`${c.sceneBg} border ${c.border} rounded-xl p-5 mb-4`}>
                        <p className={`text-sm ${c.text} leading-[1.9] italic`}>{scene.scene}</p>
                      </div>

                      <div className="grid grid-cols-1 sm:grid-cols-2 gap-3">
                        {scene.the_good && (
                          <div className={`${c.success} border rounded-lg p-3`}>
                            <p className="text-[10px] font-bold mb-1">✅ {t('wi_scene_good')}</p>
                            <p className="text-xs leading-relaxed">{scene.the_good}</p>
                          </div>
                        )}
                        {scene.the_cost && (
                          <div className={`${c.warning} border rounded-lg p-3`}>
                            <p className="text-[10px] font-bold mb-1">💔 {t('wi_scene_cost')}</p>
                            <p className="text-xs leading-relaxed">{scene.the_cost}</p>
                          </div>
                        )}
                      </div>
                    </div>
                  </div>
                </div>
              ))}
            </div>
          )}

          {/* ── THE SURPRISE ── */}
          {results?.the_surprise && (
            <div className={`${c.warning} border rounded-xl p-4 flex items-start gap-3`}>
              <span className="flex-shrink-0 mt-0.5">⚡</span>
              <div>
                <p className="text-xs font-bold mb-1">{t('wi_surprise_title')}</p>
                <p className="text-sm leading-relaxed">{results?.the_surprise}</p>
              </div>
            </div>
          )}

          {/* ── KEEP / LOSE ── */}
          <div className="grid grid-cols-1 sm:grid-cols-2 gap-3">
            {results?.what_you_keep && (
              <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
                <p className={`text-[10px] font-bold ${c.textMuted} mb-1`}>🔒 {t('wi_keep_title')}</p>
                <p className={`text-xs ${c.textSecondary} leading-relaxed`}>{results?.what_you_keep}</p>
              </div>
            )}
            {results?.what_you_lose && (
              <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
                <p className={`text-[10px] font-bold ${c.textMuted} mb-1`}>💔 {t('wi_lose_title')}</p>
                <p className={`text-xs ${c.textSecondary} leading-relaxed`}>{results?.what_you_lose}</p>
              </div>
            )}
          </div>

          {/* ── CLARITY QUESTION ── */}
          {results?.clarity_question && (
            <div className={`${c.card} border ${c.border} rounded-xl p-5 text-center`}>
              <p className={`text-xs font-bold ${c.textMuted} mb-2`}>❓ {t('wi_clarity_title')}</p>
              <p className={`text-base font-bold ${c.text} leading-relaxed`}>{results?.clarity_question}</p>
            </div>
          )}

          {/* ── HONEST TAKE ── */}
          {results?.honest_take && (
            <div className={`${c.success} border rounded-xl p-4 flex items-start gap-3`}>
              <span className="flex-shrink-0 mt-0.5">🎯</span>
              <div>
                <p className="text-xs font-bold mb-1">{t('wi_honest_title')}</p>
                <p className="text-sm leading-relaxed">{results?.honest_take}</p>
              </div>
            </div>
          )}

          <p className={`text-[10px] ${c.textMuted} text-center px-4`}>
            {t('wi_disclaimer')}
          </p>

          {/* Post-result cross-refs */}
          <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
            <p className={`text-xs font-bold ${c.textMuted} mb-2`}>🔗 {t('wi_whats_next')}</p>
            <div className="flex flex-wrap gap-3">
              <a href="/PlotTwist" className={`text-xs ${linkStyle}`}>🌀 {t('wi_xref_plottwist')}</a>
              <a href="/TimeWarp" className={`text-xs ${linkStyle}`}>⏰ {t('wi_xref_timewarp')}</a>
            </div>
          </div>

        </div>
      )}

      {/* History */}
      {/* eslint-disable-next-line no-restricted-globals */}
      {pastDecisions.length > 0 && (
        <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4 mt-4`}>
          <p className={`text-xs font-bold ${c.textMuted} mb-2`}>📋 {t('wi_recent_sessions')}</p>
          <div className="space-y-1">
            {pastDecisions.map(s => (
              <div key={s.id} className="flex items-center justify-between">
                <span className={`text-xs ${c.textSecondary} truncate`}>{s.preview || t('wi_session')}</span>
                <span className={`text-xs ${c.textMuted} ml-2`}>{new Date(s.date).toLocaleDateString()}</span>
              </div>
            ))}
          </div>
        </div>
      )}

    </div>
  );
};

WhatIf.displayName = 'WhatIf';
export default WhatIf;
