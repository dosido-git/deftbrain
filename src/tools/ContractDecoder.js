import React, { useState, useEffect, useRef, useCallback } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { usePersistentState } from '../hooks/usePersistentState';
import { useRegisterActions } from '../components/ActionBarContext';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const CONTRACT_TYPES = [
  { id: 'employment',   label: 'Employment',      icon: '💼' },
  { id: 'freelance',    label: 'Freelance / NDA',  icon: '🤝' },
  { id: 'lease',        label: 'Lease / Rental',   icon: '🏠' },
  { id: 'saas',         label: 'SaaS / Terms',     icon: '💻' },
  { id: 'service',      label: 'Service Agreement', icon: '🔧' },
  { id: 'purchase',     label: 'Purchase / Sale',  icon: '🛒' },
  { id: 'partnership',  label: 'Partnership',      icon: '🤜' },
  { id: 'other',        label: 'Other',            icon: '📄' },
];

const FOCUS_AREAS = [
  { id: 'exit',        label: 'Exit / termination terms' },
  { id: 'liability',   label: 'Liability & indemnification' },
  { id: 'ip',          label: 'IP & ownership rights' },
  { id: 'autorenewal', label: 'Auto-renewal & cancellation' },
  { id: 'payment',     label: 'Payment & refund terms' },
  { id: 'noncompete',  label: 'Non-compete / non-solicitation' },
  { id: 'privacy',     label: 'Data & privacy' },
  { id: 'dispute',     label: 'Dispute resolution' },
];

const EXAMPLE = {
  contractType: 'freelance',
  focusAreas: ['ip', 'exit', 'payment'],
  context: 'I\'m a freelance designer being asked to sign this before starting a project for a startup.',
  contractText: `FREELANCE SERVICES AGREEMENT

This Agreement is entered into as of the date of signing between ACME Corp ("Client") and the undersigned designer ("Contractor").

1. SERVICES
Contractor agrees to provide graphic design services as directed by Client.

2. COMPENSATION
Client shall pay Contractor $75/hour. Payment is due within 60 days of invoice. Client may dispute any invoice within 90 days of receipt. Contractor waives all right to payment if not claimed within 6 months of completion.

3. INTELLECTUAL PROPERTY
All work product, including preliminary designs, sketches, and concepts, shall be the sole and exclusive property of Client upon creation, regardless of payment status. Contractor hereby assigns all rights, title, and interest in any work product to Client. Contractor waives all moral rights. Client may use Contractor's name and portfolio samples in perpetuity for marketing purposes.

4. TERMINATION
Client may terminate this agreement at any time with or without cause, with zero notice. Upon termination, Contractor shall deliver all work in progress immediately. No compensation shall be due for work not yet invoiced at time of termination.

5. NON-COMPETE
Contractor agrees not to perform design services for any company in the technology sector for a period of 24 months following termination of this agreement.

6. CONFIDENTIALITY
Contractor shall keep all Client information confidential in perpetuity, including after termination, and shall not discuss the existence of this agreement with any third party.

7. DISPUTE RESOLUTION
Any disputes shall be resolved by binding arbitration in the Client's jurisdiction. Contractor waives all right to jury trial. Client may seek injunctive relief in any court without posting bond.

8. GOVERNING LAW
This agreement shall be governed by the laws of Delaware, regardless of Contractor's location.

9. ENTIRE AGREEMENT
This agreement supersedes all prior agreements. Client may modify this agreement at any time by posting updates to its website.`,
};

function ContractDecoder({ tool }) {
  const { callToolEndpoint, loading } = useClaudeAPI();
  const { isDark } = useTheme();

  const c = {
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:         isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20'
                          : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-500' : 'text-gray-400',
    labelText:     isDark ? 'text-zinc-200' : 'text-gray-700',
    accentTxt:     isDark ? 'text-cyan-400' : 'text-cyan-600',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white'
                          : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200'
                          : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200'
                          : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200'
                          : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200'
                          : 'bg-red-50 border-red-200 text-red-800',
    infoBox:       isDark ? 'bg-sky-900/20 border-sky-700 text-sky-200'
                          : 'bg-sky-50 border-sky-200 text-sky-800',
    pillActive:    isDark ? 'border-cyan-500 bg-cyan-900/30 text-cyan-200'
                          : 'border-cyan-600 bg-cyan-100 text-cyan-900',
    pillInactive:  isDark ? 'border-zinc-600 text-zinc-400 hover:border-zinc-500'
                          : 'border-gray-300 text-gray-500 hover:border-gray-400',
    required:      isDark ? 'text-amber-400' : 'text-amber-500',
    warningTxt:    isDark ? 'text-amber-300' : 'text-amber-800',
  };
  c.textMuteded = c.textMuted;
  c.label = c.labelText;

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';

  const [contractType, setContractType] = useState('other');
  const [focusAreas, setFocusAreas]     = useState([]);
  const [contractText, setContractText] = useState('');
  const [context, setContext]           = useState('');
  const [results, setResults]           = usePersistentState('contractdecoder-results', null);
  const [sessionHistory, setSessionHistory]           = usePersistentState('contractdecoder-history', []);
  const [error, setError]               = useState('');

  const resultsRef       = useRef(null);
  const handleAnalyzeRef = useRef(null);
  const canSubmitRef     = useRef(false);

  const canSubmit = contractText.trim().length >= 100;

  const handleReset = useCallback(() => {
    setResults(null);
    setError('');
    setContractText('');
    setContext('');
    setFocusAreas([]);
    setContractType('other');
  }, [setResults]);

  const loadExample = useCallback(() => {
    setContractType(EXAMPLE.contractType);
    setFocusAreas(EXAMPLE.focusAreas);
    setContext(EXAMPLE.context);
    setContractText(EXAMPLE.contractText);
    setResults(null);
    setError('');
  }, [setResults]);

  const toggleFocus = useCallback((id) => {
    setFocusAreas(prev => prev.includes(id) ? prev.filter(f => f !== id) : [...prev, id]);
  }, []);

  const handleAnalyze = useCallback(async () => {
    if (!canSubmit || loading) return;
    setError('');
    setResults(null);

    try {
      const parsed = await callToolEndpoint('contract-decoder/stream', {
        contractText,
        contractType,
        focusAreas,
        context,
      });
      setResults({ ...parsed, contractType, context });
      setSessionHistory(prev => [{
        preview:      contractText.slice(0, 80) + (contractText.length > 80 ? '…' : ''),
        overall_risk: parsed.overall_risk,
        red_flags:    parsed.red_flags_count ?? 0,
        ts:           Date.now(),
      }, ...prev].slice(0, 8));
    } catch (err) {
      setError(err.message || 'Something went wrong. Please try again.');
    }
  }, [canSubmit, loading, contractText, contractType, focusAreas, context, callToolEndpoint, setResults, setSessionHistory]);

  handleAnalyzeRef.current = handleAnalyze;
  canSubmitRef.current     = canSubmit;

  const buildFullText = useCallback(() => {
    if (!results) return '';
    const lines = ['CONTRACT DECODER ANALYSIS', ''];
    lines.push(`Overall risk: ${results.overall_risk?.toUpperCase()}`);
    if (results.red_flags_count) lines.push(`High-risk clauses: ${results.red_flags_count}`);
    if (results.overall_summary) lines.push(`\n${results.overall_summary}`);
    if (results.clauses?.length) {
      lines.push('\nCLAUSES FLAGGED');
      results.clauses.forEach(cl => {
        lines.push(`\n[${cl.risk_level?.toUpperCase()}] ${cl.clause_name}`);
        lines.push(cl.plain_english);
        if (cl.what_to_do) lines.push(`→ ${cl.what_to_do}`);
        if (cl.negotiate) lines.push(`💬 Negotiate: ${cl.negotiate}`);
      });
    }
    if (results.missing_protections?.length) {
      lines.push('\nMISSING PROTECTIONS');
      results.missing_protections.forEach(m => lines.push(`• ${m}`));
    }
    if (results.before_you_sign?.length) {
      lines.push('\nBEFORE YOU SIGN');
      results.before_you_sign.forEach((a, i) => lines.push(`${i + 1}. ${a}`));
    }
    lines.push(BRAND);
    return lines.join('\n');
  }, [results]);

  useRegisterActions(buildFullText(), tool?.title || 'ContractDecoder');

  useEffect(() => {
    if (!results || !resultsRef.current) return;
    const t = setTimeout(() => resultsRef.current?.scrollIntoView({ behavior: 'smooth', block: 'start' }), 200);
    return () => clearTimeout(t);
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [results]);

  useEffect(() => {
    const handler = (e) => {
      const tag = document.activeElement?.tagName;
      if (tag === 'SELECT') return;
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && !loading && canSubmitRef.current)
        handleAnalyzeRef.current?.();
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [loading]);

  const riskSummaryConfig = results ? {
    high:   { bg: isDark ? 'bg-red-900/40 border-red-600'         : 'bg-red-50 border-red-400',         txt: isDark ? 'text-red-200'     : 'text-red-800',     icon: '🚨', label: 'HIGH RISK' },
    medium: { bg: isDark ? 'bg-amber-900/40 border-amber-600'     : 'bg-amber-50 border-amber-400',     txt: isDark ? 'text-amber-200'   : 'text-amber-800',   icon: '⚠️', label: 'REVIEW CAREFULLY' },
    low:    { bg: isDark ? 'bg-emerald-900/40 border-emerald-600' : 'bg-emerald-50 border-emerald-400', txt: isDark ? 'text-emerald-200' : 'text-emerald-800', icon: '✅', label: 'LOOKS REASONABLE' },
  }[results.overall_risk] ?? { bg: c.warning, txt: c.warningTxt, icon: '📋', label: 'REVIEWED' } : null;

  const renderInput = () => (
    <div className={`${c.card} border ${c.border} rounded-xl shadow-sm p-5 space-y-4`}>
      <div className="pb-3 border-b border-zinc-500">
        <h2 className={`text-xl font-bold ${c.text}`}>
          <span className="mr-2">{tool?.icon ?? '📋'}</span>{tool?.title ?? 'Contract Decoder'}
        </h2>
        <p className={`text-sm ${c.textSecondary}`}>{tool?.tagline ?? 'Paste any contract — know exactly what you\'re signing.'}</p>
        <button onClick={loadExample} disabled={loading} style={{ backgroundColor: (tool?.headerColor ?? '#888888') + '80' }} className={`mt-1.5 px-2.5 py-0.5 rounded-full text-xs font-medium border disabled:opacity-40 ${isDark ? 'text-white border-white/40' : 'text-gray-800 border-transparent'}`}>Try example</button>
      </div>

      {/* Contract type */}
      <div>
        <label className={`block text-sm font-medium ${c.labelText} mb-2`}>Contract type</label>
        <div className="flex flex-wrap gap-2">
          {CONTRACT_TYPES.map(ct => (
            <button key={ct.id} onClick={() => setContractType(ct.id)}
              className={`px-3 py-1.5 rounded-lg border text-xs font-medium transition-colors ${contractType === ct.id ? c.pillActive : c.pillInactive}`}>
              <span className="mr-1">{ct.icon}</span>{ct.label}
            </button>
          ))}
        </div>
      </div>

      {/* Focus areas */}
      <div>
        <label className={`block text-sm font-medium ${c.labelText} mb-2`}>
          Focus areas <span className={`text-xs font-normal ${c.textMuted}`}>(optional — select what matters most to you)</span>
        </label>
        <div className="flex flex-wrap gap-2">
          {FOCUS_AREAS.map(fa => (
            <button key={fa.id} onClick={() => toggleFocus(fa.id)}
              className={`px-3 py-1.5 rounded-lg border text-xs font-medium transition-colors ${focusAreas.includes(fa.id) ? c.pillActive : c.pillInactive}`}>
              {fa.label}
            </button>
          ))}
        </div>
      </div>

      {/* Contract text */}
      <div>
        <label className={`block text-sm font-medium ${c.labelText} mb-1`}>
          Contract text <span className={c.required}>*</span>
        </label>
        <textarea
          value={contractText}
          onChange={e => setContractText(e.target.value)}
          placeholder="Paste the full contract text here — employment agreement, NDA, lease, terms of service, service agreement…"
          rows={10}
          className={`w-full px-3 py-2.5 border rounded-xl text-sm resize-y focus:outline-none focus:ring-2 ${c.input}`}
        />
        {contractText.length > 0 && contractText.length < 100 && (
          <p className={`text-xs ${c.textMuted} mt-1`}>Paste more of the contract for a useful analysis.</p>
        )}
      </div>

      {/* Context */}
      <div>
        <label className={`block text-sm font-medium ${c.labelText} mb-1`}>
          Your situation <span className={`text-xs font-normal ${c.textMuted}`}>(optional)</span>
        </label>
        <input type="text" value={context} onChange={e => setContext(e.target.value)}
          placeholder="e.g., 'I'm a freelancer, this is for a 3-month project' or 'First job offer, no leverage to negotiate'"
          className={`w-full px-3 py-2 border rounded-xl text-sm focus:outline-none focus:ring-2 ${c.input}`}
        />
      </div>

      {error && <p className={`text-sm ${c.danger} border rounded-lg px-3 py-2`}>{error}</p>}

      <p className={`text-xs ${c.textMuted}`}>
        Signing a lease? <a href="/LeaseTrapDetector" className={linkStyle}>🏠 Lease Trap Detector</a> goes deeper on rental agreements.
      </p>

      <button onClick={handleAnalyze} disabled={!canSubmit || loading}
        className={`w-full ${c.btnPrimary} disabled:opacity-40 font-bold py-3 rounded-lg flex items-center justify-center gap-2 min-h-[48px]`}>
        {loading
          ? <><span className="inline-block animate-spin">{tool?.icon ?? '📋'}</span> Analyzing…</>
          : <><span className="mr-1">{tool?.icon ?? '📋'}</span> Decode This Contract</>}
      </button>

      {sessionHistory.length > 0 && (
        <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
          <div className="flex items-center justify-between mb-3">
            <p className={`text-xs font-bold ${c.textMuted} uppercase tracking-wide`}>🕓 Recent analyses</p>
            <button onClick={() => setSessionHistory([])} className={`text-xs ${c.textMuted}`}>Clear</button>
          </div>
          <ul className="space-y-2">
            {sessionHistory.map((h, i) => {
              const rIcon = h.overall_risk === 'high' ? '🚨' : h.overall_risk === 'medium' ? '⚠️' : '✅';
              const rColor = h.overall_risk === 'high'
                ? (isDark ? 'text-red-400' : 'text-red-600')
                : h.overall_risk === 'medium'
                  ? (isDark ? 'text-amber-400' : 'text-amber-600')
                  : (isDark ? 'text-emerald-400' : 'text-emerald-600');
              return (
                <li key={i} className={`text-xs ${c.textSecondary} flex items-start gap-2`}>
                  <span className="flex-shrink-0">{rIcon}</span>
                  <span>
                    <span className={`font-bold ${rColor}`}>{h.overall_risk?.toUpperCase()}</span>
                    {h.red_flags > 0 && <span className={c.textMuted}> · {h.red_flags} high-risk clause{h.red_flags !== 1 ? 's' : ''}</span>}
                    <span className={`block ${c.textMuted} mt-0.5`}>{h.preview}</span>
                  </span>
                </li>
              );
            })}
          </ul>
        </div>
      )}
    </div>
  );

  const renderResults = () => {
    if (!results || !riskSummaryConfig) return null;
    const high   = results?.clauses?.filter(cl => cl.risk_level === 'high')   ?? [];
    const medium = results?.clauses?.filter(cl => cl.risk_level === 'medium') ?? [];
    const low    = results?.clauses?.filter(cl => cl.risk_level === 'low')    ?? [];

    return (
      <div className="space-y-4" ref={resultsRef}>

        {/* Overall verdict */}
        <div className={`border-2 rounded-xl p-5 ${riskSummaryConfig.bg}`}>
          <div className="flex items-start gap-3">
            <span className="text-3xl">{riskSummaryConfig.icon}</span>
            <div>
              <p className={`text-xl font-black ${riskSummaryConfig.txt}`}>{riskSummaryConfig.label}</p>
              {results.red_flags_count > 0 && (
                <p className={`text-sm font-medium ${riskSummaryConfig.txt} opacity-80`}>
                  {results.red_flags_count} high-risk clause{results.red_flags_count !== 1 ? 's' : ''} found
                </p>
              )}
              {results.overall_summary && (
                <p className={`text-sm mt-2 ${riskSummaryConfig.txt}`}>{results.overall_summary}</p>
              )}
            </div>
          </div>
        </div>

        {/* Clauses — high risk first */}
        {high.length > 0 && (
          <div className={`${c.card} border ${c.border} rounded-xl p-4 space-y-3`}>
            <p className={`text-xs font-bold ${c.textMuted} uppercase tracking-wide`}>🚨 High-risk clauses</p>
        {high.map((cl, i) => <InlineClause key={i} clause={cl} isDark={isDark} c={c} />)}
          </div>
        )}

        {medium.length > 0 && (
          <div className={`${c.card} border ${c.border} rounded-xl p-4 space-y-3`}>
            <p className={`text-xs font-bold ${c.textMuted} uppercase tracking-wide`}>⚠️ Watch these clauses</p>
            {medium.map((cl, i) => <InlineClause key={i} clause={cl} isDark={isDark} c={c} />)}
          </div>
        )}

        {low.length > 0 && (
          <div className={`${c.card} border ${c.border} rounded-xl p-4 space-y-3`}>
            <p className={`text-xs font-bold ${c.textMuted} uppercase tracking-wide`}>✅ Standard clauses</p>
            {low.map((cl, i) => <InlineClause key={i} clause={cl} isDark={isDark} c={c} />)}
          </div>
        )}

        {/* Missing protections */}
        {results.missing_protections?.length > 0 && (
          <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
            <p className={`text-xs font-bold ${c.textMuted} uppercase tracking-wide mb-3`}>🛡️ Missing protections</p>
            <ul className="space-y-2">
              {results.missing_protections.map((m, i) => (
                <li key={i} className={`text-sm ${c.warning} border rounded-lg px-3 py-2`}>• {m}</li>
              ))}
            </ul>
          </div>
        )}

        {/* Before you sign */}
        {results.before_you_sign?.length > 0 && (
          <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
            <p className={`text-xs font-bold ${c.textMuted} uppercase tracking-wide mb-3`}>✍️ Before you sign</p>
            <ul className="space-y-2">
              {results.before_you_sign.map((a, i) => (
                <li key={i} className={`text-sm ${c.textSecondary}`}>
                  <span className={`font-bold ${c.accentTxt}`}>{i + 1}.</span> {a}
                </li>
              ))}
            </ul>
          </div>
        )}

        {/* Post-result cross-refs */}
        <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
          <p className={`text-[10px] font-bold ${c.textMuted} uppercase mb-2`}>🔗 Related tools</p>
          <div className="flex flex-wrap gap-3">
            <a href="/LeaseTrapDetector" className={`text-xs ${linkStyle}`}>🏠 Lease Trap Detector</a>
            <a href="/ComplaintEscalationWriter" className={`text-xs ${linkStyle}`}>📨 Complaint Escalation Writer</a>
            <a href="/LeverageLogic" className={`text-xs ${linkStyle}`}>⚖️ Leverage Logic</a>
          </div>
        </div>
      </div>
    );
  };

  return (
    <div className={`space-y-4 ${c.text}`}>
      {!results && <p className={`text-xs ${c.textMuted} px-1`}>Signing a lease? <a href="/LeaseTrapDetector" className={linkStyle}>🏠 Lease Trap Detector</a> goes deeper on rental agreements.</p>}
      {!results && renderInput()}
      {results ? (
        <div className={`${c.card} border ${c.border} rounded-xl shadow-sm p-5`}>
          <div className="pb-3 border-b border-zinc-500">
            <div className="flex items-start justify-between">
              <div>
                <h2 className={`text-xl font-bold ${c.text}`}>
                  <span className="mr-2">{tool?.icon ?? '📋'}</span>{tool?.title ?? 'Contract Decoder'}
                </h2>
                <p className={`text-sm ${c.textSecondary}`}>{tool?.tagline ?? 'Paste any contract — know exactly what you\'re signing.'}</p>
              </div>
              <button onClick={handleReset} className={`${c.btnSecondary} px-3 py-1.5 rounded-lg text-xs font-bold`}>
                ↺ New contract
              </button>
            </div>
          </div>
        </div>
      ) : null}
      {results && renderResults()}
      {results && (
        <div className={`${c.cardAlt} border ${c.border} rounded-xl p-3`}>
          <p className={`text-[10px] font-bold ${c.textMuted} uppercase mb-2`}>🔗 Related tools</p>
          <div className="flex flex-wrap gap-3">
            <a href="/LeaseTrapDetector" className={`text-xs ${linkStyle}`}>🏠 Lease Trap Detector</a>
            <a href="/LeverageLogic" className={`text-xs ${linkStyle}`}>⚖️ Leverage Logic</a>
          </div>
        </div>
      )}
    </div>
  );
}

ContractDecoder.displayName = 'ContractDecoder';
export default ContractDecoder;

function InlineClause({ clause, isDark, c }) {
  const riskCfg = {
    high:   { border: 'border-l-red-500',     badge: isDark ? 'bg-red-900/40 border-red-700 text-red-300'         : 'bg-red-50 border-red-300 text-red-700',         icon: '🚨' },
    medium: { border: 'border-l-amber-500',   badge: isDark ? 'bg-amber-900/40 border-amber-700 text-amber-300'   : 'bg-amber-50 border-amber-300 text-amber-700',   icon: '⚠️' },
    low:    { border: 'border-l-emerald-500', badge: isDark ? 'bg-emerald-900/40 border-emerald-700 text-emerald-300' : 'bg-emerald-50 border-emerald-300 text-emerald-700', icon: '✅' },
  }[clause?.risk_level] ?? { border: 'border-l-zinc-500', badge: '', icon: '📋' };

  return <div className={`${c.cardAlt} border border-l-4 ${riskCfg.border} ${c.border} rounded-xl p-4 space-y-2`}>
      <div className="flex items-center gap-2 flex-wrap">
        <span className="text-base">{riskCfg.icon}</span>
        <span className={`font-bold text-sm ${c.text}`}>{clause?.clause_name}</span>
        <span className={`text-[10px] font-bold border px-2 py-0.5 rounded-full ${riskCfg.badge}`}>
          {clause?.risk_level?.toUpperCase()}
        </span>
      </div>
      {clause?.plain_english && <p className={`text-sm ${c.textSecondary}`}>{clause.plain_english}</p>}
      {clause?.quote && (
        <div className={`${c.card} border ${c.border} rounded-lg p-3`}>
          <p className={`text-[10px] font-bold ${c.textMuted} uppercase mb-1`}>Contract language</p>
          <p className={`text-xs ${c.textSecondary} italic`}>"{clause.quote}"</p>
        </div>
      )}
      {clause?.why_it_matters && (
        <p className={`text-xs ${c.textMuted}`}><span className="font-semibold">Why it matters:</span> {clause.why_it_matters}</p>
      )}
      {clause?.what_to_do && (
        <p className={`text-xs font-medium ${isDark ? 'text-cyan-300' : 'text-cyan-700'}`}>→ {clause.what_to_do}</p>
      )}
      {clause?.negotiate && (
        <div className={`${c.infoBox} border rounded-lg px-3 py-2`}>
          <p className={`text-[10px] font-bold uppercase mb-0.5`}>💬 Ask for:</p>
          <p className="text-xs">{clause.negotiate}</p>
        </div>
      )}
    </div>;
}
