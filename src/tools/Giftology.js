import React, { useState, useEffect, useRef, useCallback } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { usePersistentState } from '../hooks/usePersistentState';
import { useRegisterActions } from '../components/ActionBarContext';
import { useTranslation } from '../i18n/useTranslation';
import { currencySymbol } from '../utils/formatLocale';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

// ════════════════════════════════════════════════════════════
// COMPONENT
// ════════════════════════════════════════════════════════════
const Giftology = ({ tool }) => {
  const { callToolEndpoint, loading, userLocale, userCurrency, userRegion } = useClaudeAPI();
  const { isDark } = useTheme();
  const { t } = useTranslation();
  const sym = currencySymbol(userLocale, userCurrency);

  // ─── Localized constants (depend on t / sym) ───
  const OCCASIONS = [
    { id: 'birthday',     label: t('gft_occ_birthday'),     icon: '🎂' },
    { id: 'holiday',      label: t('gft_occ_holiday'),      icon: '🎄' },
    { id: 'thankyou',     label: t('gft_occ_thankyou'),     icon: '🙏' },
    { id: 'wedding',      label: t('gft_occ_wedding'),      icon: '💍' },
    { id: 'baby',         label: t('gft_occ_baby'),         icon: '👶' },
    { id: 'graduation',   label: t('gft_occ_graduation'),   icon: '🎓' },
    { id: 'housewarming', label: t('gft_occ_housewarming'), icon: '🏠' },
    { id: 'justbecause',  label: t('gft_occ_justbecause'),  icon: '💛' },
    { id: 'apology',      label: t('gft_occ_apology'),      icon: '🕊️' },
    { id: 'other',        label: t('gft_occ_other'),        icon: '🎁' },
  ];

  const DEADLINES = [
    { id: 'today',     label: t('gft_dl_today') },
    { id: 'tomorrow',  label: t('gft_dl_tomorrow') },
    { id: 'this-week', label: t('gft_dl_thisweek') },
    { id: 'no-rush',   label: t('gft_dl_norush') },
  ];

  const EXAMPLES = [t('gft_ex1'), t('gft_ex2'), t('gft_ex3'), t('gft_ex4'), t('gft_ex5'), t('gft_ex6')];

  // ─── c block (PF-2 verbatim + tool-specific keys) ───
  const c = {
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:         isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20'
                          : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-400' : 'text-gray-500',
    labelText:     isDark ? 'text-zinc-200' : 'text-gray-700',
    accentTxt:     isDark ? 'text-cyan-400' : 'text-cyan-600',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white'
                          : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200'
                          : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200'
                          : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200'
                          : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200'
                          : 'bg-red-50 border-red-200 text-red-800',
    pillActive:    isDark ? 'border-cyan-500 bg-cyan-900/30 text-cyan-200'
                          : 'border-cyan-600 bg-cyan-100 text-cyan-900',
    pillInactive:  isDark ? 'border-zinc-600 text-zinc-400 hover:border-zinc-500'
                          : 'border-gray-300 text-gray-500 hover:border-gray-400',
    required:      isDark ? 'text-amber-400' : 'text-amber-700',
    // Tool-specific semantic keys
    giftCard:      isDark ? 'bg-zinc-800' : 'bg-white',
    warmBg:        isDark ? 'bg-amber-900/20' : 'bg-amber-50',
    warmText:      isDark ? 'text-amber-300' : 'text-amber-700',
    wildcardBg:    isDark ? 'bg-cyan-900/20' : 'bg-cyan-50',
    wildcardText:  isDark ? 'text-cyan-300' : 'text-cyan-700',
  };
  c.textMuteded = c.textMuted;
  c.label = c.labelText;

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-700 hover:text-cyan-800 underline underline-offset-2';

  // ─── State ───
  const [occasion, setOccasion] = useState('');
  const [budget, setBudget] = useState('');
  const [deadline, setDeadline] = useState('');
  const [alreadyGiven, setAlreadyGiven] = useState('');
  const [avoid, setAvoid] = useState('');
  const [error, setError] = useState('');
  const [expandedPick, setExpandedPick] = useState(0);
  const [showWildcard, setShowWildcard] = useState(false);
  const [recipient, setRecipient] = usePersistentState('giftology-recipient', '');
  const [results, setResults] = usePersistentState('giftology-last', null);
  const [sessionHistory, setSessionHistory] = usePersistentState('giftology-history', []);

  // ─── Refs ───
  const resultsRef = useRef(null);
  const handleSubmitRef = useRef(null);
  const canSubmitRef = useRef(false);

  // Keep refs fresh every render
  canSubmitRef.current = !!recipient.trim();

  // ─── Handlers ───
  const handleSubmit = async () => {
    if (!recipient.trim()) return;
    setError('');
    setResults(null);
    setExpandedPick(0);
    setShowWildcard(false);

    try {
      const occasionLabel = OCCASIONS.find(o => o.id === occasion)?.label || occasion || undefined;
      const deadlineLabel = DEADLINES.find(d => d.id === deadline)?.label || deadline || undefined;

      const data = await callToolEndpoint('giftology', {
        recipient: recipient.trim(),
        occasion: occasionLabel,
        budget: budget.trim() || undefined,
        deadline: deadlineLabel,
        alreadyGiven: alreadyGiven.trim() || undefined,
        avoid: avoid.trim() || undefined,
        userLocale, userCurrency, userRegion,
      });
      setResults(data);
      const newEntry = {
        id: Date.now(),
        timestamp: new Date().toISOString(),
        preview: recipient.trim().slice(0, 40),
        result: data,
      };
      setSessionHistory(prev => [newEntry, ...(prev || [])].slice(0, 6));
    } catch (err) {
      setError(err.message || t('gft_error'));
    }
  };
  handleSubmitRef.current = handleSubmit;

  const handleReset = () => {
    setRecipient('');
    setOccasion('');
    setBudget('');
    setDeadline('');
    setAlreadyGiven('');
    setAvoid('');
    setResults(null);
    setError('');
    setShowWildcard(false);
  };

  const loadExample = () => {
    const ex = EXAMPLES[Math.floor(Math.random() * EXAMPLES.length)];
    setRecipient(ex);
  };

  // ─── Build full text ───
  const buildFullText = useCallback(() => {
    if (!results) return '';
    const r = results;
    let out = `🎁 ${t('gft_copy_header')}\n\n`;
    if (r.situation_read) out += `${r.situation_read}\n\n`;

    if (r.perfect_picks?.length) {
      out += `━━ ${t('gft_copy_ideas')} ━━\n\n`;
      r.perfect_picks.forEach((p, i) => {
        out += `${i + 1}. ${p.gift} (${p.price_range})\n`;
        out += `   ${t('gft_copy_why')} ${p.why_its_perfect}\n`;
        out += `   ${t('gft_copy_where')} ${p.where_to_get}\n`;
        if (p.presentation_tip) out += `   ${t('gft_copy_tip')} ${p.presentation_tip}\n`;
        out += `   ${t('gft_copy_card')} "${p.card_message}"\n\n`;
      });
    }

    if (r.the_wildcard) {
      out += `━━ ${t('gft_copy_wildcard')} ━━\n`;
      out += `${r.the_wildcard.gift} (${r.the_wildcard.price_range})\n`;
      out += `${r.the_wildcard.why_its_perfect}\n`;
      out += `${t('gft_copy_card')} "${r.the_wildcard.card_message}"\n\n`;
    }

    if (r.if_deadline_is_now) {
      out += `━━ ${t('gft_copy_lastminute')} ━━\n`;
      out += `${r.if_deadline_is_now.instant_option}\n`;
      out += `${r.if_deadline_is_now.how}\n\n`;
    }

    if (r.never_do_this) out += `⚠️ ${t('gft_copy_dont')} ${r.never_do_this}\n\n`;

    out += BRAND;
    return out;
  }, [results, t]);

  useRegisterActions(buildFullText(), tool?.title || 'Giftology');

  // ─── Scroll useEffect ───
  useEffect(() => {
    if (!results || !resultsRef.current) return;
    const timer = setTimeout(() => resultsRef.current?.scrollIntoView({ behavior: 'smooth', block: 'start' }), 200);
    return () => clearTimeout(timer);
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [results]);

  // ─── Keyboard useEffect ───
  useEffect(() => {
    const handler = (e) => {
      const tag = document.activeElement?.tagName;
      if (tag === 'SELECT') return;
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && !loading && canSubmitRef.current)
        handleSubmitRef.current?.();
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [loading]);

  // ════════════════════════════════════════════════════════════
  // RENDER
  // ════════════════════════════════════════════════════════════
  return (
    <div className={`space-y-4 ${c.text}`}>

      {/* ── Input card ── */}
      <div className={`${c.card} border ${c.border} rounded-xl shadow-sm p-5`}>
        <div className="pb-3 border-b border-zinc-500">
          <div className="flex items-start justify-between gap-3">
            <div>
              <h2 className={`text-xl font-bold ${c.text} flex items-center gap-2`}>
                <span className="me-2">{tool?.icon ?? '🎁'}</span>{tool?.title ?? t('gft_title')}
              </h2>
              <p className={`text-sm ${c.textSecondary}`}>{tool?.tagline ?? t('gft_tagline')}</p>
              <button onClick={loadExample} disabled={loading} style={{ backgroundColor: (tool?.headerColor ?? '#888888') + '80' }} className={`mt-1.5 px-2.5 py-0.5 rounded-full text-xs font-medium border disabled:opacity-40 ${isDark ? 'text-white border-white/40' : 'text-gray-800 border-transparent'}`}>{t('try_example')}</button>
            </div>
            {(results || recipient.trim()) && (
              <button onClick={handleReset} className={`${c.btnSecondary} px-3 py-1.5 rounded-lg text-xs font-bold flex-shrink-0`}>
                ↺ {t('start_over')}
              </button>
            )}
          </div>
        </div>

        {/* Recipient */}
        <div className="mt-4 mb-4">
          <label className={`block text-sm font-medium ${c.labelText} mb-1`}>
            {t('gft_recipient_label')} <span className={c.required}>*</span>
          </label>
          <textarea
            value={recipient}
            onChange={e => setRecipient(e.target.value)}
            onKeyDown={e => (e.key === 'Enter' && (e.ctrlKey || e.metaKey)) && recipient.trim() && handleSubmit()}
            placeholder={t('gft_recipient_ph')}
            rows={4}
            maxLength={800}
            className={`w-full px-3 py-2.5 border rounded-lg text-sm ${c.input} outline-none focus:ring-2 resize-none`}
          />
          <div className="flex justify-between mt-1">
            <span className={`text-[10px] ${c.textMuted}`}>{t('gft_submit_hint')}</span>

          </div>
        </div>

        {/* Occasion */}
        <div className="mb-4">
          <label className={`text-[10px] font-bold ${c.textSecondary} uppercase block mb-2`}>{t('gft_occasion')}</label>
          <div className="flex flex-wrap gap-1.5">
            {OCCASIONS.map(o => (
              <button
                key={o.id}
                onClick={() => setOccasion(occasion === o.id ? '' : o.id)}
                className={`px-2.5 py-1.5 rounded-lg text-xs font-medium border transition-colors min-h-[32px] flex items-center gap-1 ${
                  occasion === o.id ? c.pillActive : c.pillInactive
                }`}
              >
                <span>{o.icon}</span> {o.label}
              </button>
            ))}
          </div>
        </div>

        {/* Budget + Deadline row */}
        <div className="grid grid-cols-1 sm:grid-cols-2 gap-3 mb-4">
          <div>
            <label className={`text-[10px] font-bold ${c.textSecondary} uppercase block mb-1.5`}>{t('gft_budget')}</label>
            <input
              type="text"
              value={budget}
              onChange={e => setBudget(e.target.value)}
              onKeyDown={e => e.key === 'Enter' && recipient.trim() && handleSubmit()}
              placeholder={t('gft_budget_ph', { sym })}
              className={`w-full px-3 py-2 border rounded-lg text-sm ${c.input} outline-none focus:ring-2`}
            />
          </div>
          <div>
            <label className={`text-[10px] font-bold ${c.textSecondary} uppercase block mb-1.5`}>{t('gft_deadline')}</label>
            <div className="flex flex-wrap gap-1.5">
              {DEADLINES.map(d => (
                <button
                  key={d.id}
                  onClick={() => setDeadline(deadline === d.id ? '' : d.id)}
                  className={`px-2 py-1.5 rounded-lg text-[11px] font-medium border transition-colors ${
                    deadline === d.id ? c.pillActive : c.pillInactive
                  }`}
                >
                  {d.label}
                </button>
              ))}
            </div>
          </div>
        </div>

        {/* Optional extras */}
        <details className="mb-5">
          <summary className={`text-xs font-bold cursor-pointer ${c.textSecondary} mb-2`}>{t('gft_extras_summary')}</summary>
          <div className="space-y-3 mt-3">
            <div>
              <label className={`text-xs ${c.textSecondary} block mb-1`}>{t('gft_already_label')}</label>
              <input
                type="text"
                value={alreadyGiven}
                onChange={e => setAlreadyGiven(e.target.value)}
                placeholder={t('gft_already_ph')}
                className={`w-full px-3 py-2 border rounded-lg text-sm ${c.input} outline-none focus:ring-2`}
              />
            </div>
            <div>
              <label className={`text-xs ${c.textSecondary} block mb-1`}>{t('gft_avoid_label')}</label>
              <input
                type="text"
                value={avoid}
                onChange={e => setAvoid(e.target.value)}
                placeholder={t('gft_avoid_ph')}
                className={`w-full px-3 py-2 border rounded-lg text-sm ${c.input} outline-none focus:ring-2`}
              />
            </div>
          </div>
        </details>

        {/* Submit */}
        <button
          onClick={handleSubmit}
          disabled={!recipient.trim() || loading}
          className={`w-full ${c.btnPrimary} disabled:opacity-40 font-bold py-3 rounded-lg flex items-center justify-center gap-2 min-h-[48px]`}
        >
          {loading
            ? <><span className="animate-spin inline-block">{tool?.icon ?? '🎁'}</span> {t('gft_finding')}</>
            : <><span>{tool?.icon ?? '🎁'}</span> {t('gft_find_ideas')}</>}
        </button>

        {/* Pre-result cross-ref */}
        <p className={`text-xs text-center ${c.textMuted} mt-3`}>
          {t('gft_xref_pre_q')}{' '}
          <a href="/BuyWise" className={linkStyle}>💰 {t('gft_buywise')}</a>{' '}
          {t('gft_xref_pre_after')}
        </p>
      </div>

      {error && (
        <div className={`${c.danger} border rounded-lg p-4 flex items-start gap-3`}>
          <span className="flex-shrink-0">⚠️</span>
          <p className="text-sm">{error}</p>
        </div>
      )}

      {results && (
        <div ref={resultsRef} className="space-y-4">

          {/* Situation read */}
          {results?.situation_read && (
            <div className={`${c.warmBg} border ${c.border} rounded-xl p-4`}>
              <p className={`text-sm ${c.text} leading-relaxed`}>{results?.situation_read}</p>
            </div>
          )}

          {results?.perfect_picks?.length > 0 && (
            <div className="space-y-3">
              <p className={`text-xs font-bold uppercase tracking-wider ${c.textSecondary}`}>🎁 {t('gft_gift_ideas')}</p>
              {results?.perfect_picks?.map((pick, i) => {
                const isExpanded = expandedPick === i;
                return (
                  <div key={i} className={`${c.giftCard} border ${c.border} rounded-xl overflow-hidden transition-all duration-200`}>
                    <button
                      onClick={() => setExpandedPick(isExpanded ? -1 : i)}
                      className="w-full text-start px-5 py-4 flex items-start justify-between gap-3"
                    >
                      <div className="flex-1 min-w-0">
                        <div className="flex items-center gap-2 mb-1">
                          <span className={`text-xs font-bold ${c.textMuted}`}>#{i + 1}</span>
                          <h3 className={`text-sm font-bold ${c.text} truncate`}>{pick.gift}</h3>
                        </div>
                        <p className={`text-xs ${c.textMuted}`}>{pick.price_range}</p>
                      </div>
                      <span className={`text-xs ${c.textMuted} flex-shrink-0 mt-1`}>
                        {isExpanded ? '▲' : '▼'}
                      </span>
                    </button>

                    {isExpanded && (
                      <div className={`px-5 pb-5 space-y-3 border-t ${c.border} pt-3`}>
                        <div>
                          <p className={`text-[10px] font-bold uppercase ${c.textSecondary} mb-1`}>{t('gft_why_perfect')}</p>
                          <p className={`text-sm ${c.textSecondary} leading-relaxed`}>{pick.why_its_perfect}</p>
                        </div>
                        <div>
                          <p className={`text-[10px] font-bold uppercase ${c.textSecondary} mb-1`}>{t('gft_where_get')}</p>
                          <p className={`text-sm ${c.text}`}>{pick.where_to_get}</p>
                        </div>
                        {pick.presentation_tip && (
                          <div className={`${c.cardAlt} rounded-lg p-3`}>
                            <p className={`text-[10px] font-bold uppercase ${c.textSecondary} mb-1`}>✨ {t('gft_presentation_tip')}</p>
                            <p className={`text-xs ${c.textSecondary}`}>{pick.presentation_tip}</p>
                          </div>
                        )}
                        {pick.card_message && (
                          <div className={`${c.warmBg} border ${c.border} rounded-lg p-4`}>
                            <p className={`text-[10px] font-bold uppercase ${c.warmText} mb-2`}>✉️ {t('gft_card_message')}</p>
                            <p className={`text-sm italic ${c.text} leading-relaxed`} style={{ fontFamily: 'Georgia, serif' }}>
                              "{pick.card_message}"
                            </p>
                          </div>
                        )}
                      </div>
                    )}
                  </div>
                );
              })}
            </div>
          )}

          {results?.the_wildcard && (
            <div>
              {!showWildcard ? (
                <button
                  onClick={() => setShowWildcard(true)}
                  className={`w-full ${c.btnSecondary} py-3 rounded-xl text-sm font-medium flex items-center justify-center gap-2`}
                >
                  <span>🃏</span> {t('gft_show_wildcard')}
                </button>
              ) : (
                <div className={`${c.wildcardBg} border ${c.border} rounded-xl p-5`}>
                  <p className={`text-xs font-bold uppercase tracking-wider ${c.wildcardText} mb-2 flex items-center gap-1.5`}>
                    <span>🃏</span> {t('gft_wildcard')}
                  </p>
                  <h3 className={`text-base font-bold ${c.text} mb-1`}>{results?.the_wildcard?.gift}</h3>
                  <p className={`text-xs ${c.textMuted} mb-2`}>{results?.the_wildcard?.price_range}</p>
                  <p className={`text-sm ${c.textSecondary} mb-3`}>{results?.the_wildcard?.why_its_perfect}</p>
                  <p className={`text-xs ${c.text} mb-3`}>
                    <span className={`font-bold ${c.textSecondary}`}>{t('gft_where')} </span>{results?.the_wildcard?.where_to_get}
                  </p>
                  {results?.the_wildcard?.card_message && (
                    <div className={`${c.warmBg} border ${c.border} rounded-lg p-3`}>
                      <p className={`text-[10px] font-bold uppercase ${c.warmText} mb-1`}>✉️ {t('gft_card_short')}</p>
                      <p className={`text-sm italic ${c.text}`} style={{ fontFamily: 'Georgia, serif' }}>"{results?.the_wildcard?.card_message}"</p>
                    </div>
                  )}
                </div>
              )}
            </div>
          )}

          {results?.if_deadline_is_now && (
            <div className={`${c.success} border rounded-xl p-4`}>
              <p className={`text-xs font-bold uppercase tracking-wider mb-2 flex items-center gap-1.5`}>
                <span>⏰</span> {t('gft_lastminute')}
              </p>
              <p className={`text-sm font-semibold ${c.text} mb-1`}>{results?.if_deadline_is_now?.instant_option}</p>
              <p className={`text-xs mb-2`}>{results?.if_deadline_is_now?.how}</p>
              {results?.if_deadline_is_now?.card_message && (
                <div className="mt-2">
                  <p className={`text-xs italic`}>✉️ "{results?.if_deadline_is_now?.card_message}"</p>
                </div>
              )}
            </div>
          )}

          {results?.never_do_this && (
            <div className={`${c.danger} border rounded-xl p-4 flex items-start gap-3`}>
              <span className="flex-shrink-0">⚠️</span>
              <div>
                <p className={`text-[10px] font-bold uppercase mb-1`}>{t('gft_dont_title')}</p>
                <p className="text-sm">{results?.never_do_this}</p>
              </div>
            </div>
          )}

          {/* Post-result cross-refs */}
          <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
            <p className={`text-[10px] font-bold ${c.textMuted} uppercase mb-2`}>🔗 {t('gft_related')}</p>
            <div className="flex flex-wrap gap-3">
              <a href="/VelvetHammer" className={`text-xs ${linkStyle}`}>🔨 {t('gft_velvethammer')}</a>
              <a href="/BuyWise" className={`text-xs ${linkStyle}`}>💰 {t('gft_buywise')}</a>
            </div>
          </div>
        </div>
      )}

      {sessionHistory?.length > 0 && (
        <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
          <h3 className={`text-sm font-bold ${c.text} mb-3`}>🕐 {t('gft_recent_searches')}</h3>
          <div className="space-y-1.5">
            {sessionHistory.map(entry => (
              <button key={entry.id}
                onClick={() => setResults(entry.result)}
                className={`w-full text-start px-3 py-2 rounded-lg ${c.btnSecondary} text-xs flex items-center gap-2`}>
                <span className={c.textMuted}>
                  {new Date(entry.timestamp).toLocaleDateString(undefined, { month: 'short', day: 'numeric', hour: 'numeric', minute: '2-digit' })}
                </span>
                <span className={c.text}>{entry.preview}{entry.preview.length >= 40 ? '…' : ''}</span>
              </button>
            ))}
          </div>
        </div>
      )}
    </div>
  );
};

