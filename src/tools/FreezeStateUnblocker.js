import React, { useState, useEffect, useRef, useMemo, useCallback } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { usePersistentState } from '../hooks/usePersistentState';
import { CopyBtn, ShareBtn } from '../components/ActionButtons';

// ════════════════════════════════════════════════════════════
// CONSTANTS
// ════════════════════════════════════════════════════════════

const BRANDING = '\n\n— Generated by DeftBrain · deftbrain.com';

const CATEGORIES = [
  { id: 'bed', label: "Can't get out of bed", icon: '🛏️' },
  { id: 'work', label: "Can't start working", icon: '💻' },
  { id: 'house', label: "Can't leave the house", icon: '🚪' },
  { id: 'chore', label: "Can't start a chore", icon: '🧹' },
  { id: 'email', label: "Can't write the message", icon: '📨' },
  { id: 'overwhelm', label: 'Overwhelmed by everything', icon: '🌊' },
  { id: 'other', label: 'Something else', icon: '❓' },
];

const BREATHING_PHASES = [
  { label: 'Breathe in', duration: 4 },
  { label: 'Hold', duration: 4 },
  { label: 'Breathe out', duration: 6 },
  { label: 'Rest', duration: 2 },
];
const BREATHING_TOTAL_MS = BREATHING_PHASES.reduce((s, p) => s + p.duration, 0) * 1000;
const BREATHING_CYCLES = 3;

const BODY_SCAN_STEPS = [
  { sense: '👀 See', instruction: 'Look around. Name 5 things you can see right now.', count: 5, icon: '👀' },
  { sense: '👂 Hear', instruction: 'Be still. Name 4 things you can hear.', count: 4, icon: '👂' },
  { sense: '✋ Touch', instruction: 'Notice 3 things you can physically feel right now.', count: 3, icon: '✋' },
  { sense: '👃 Smell', instruction: 'Notice 2 things you can smell. (Sniff your sleeve if nothing comes to mind.)', count: 2, icon: '👃' },
  { sense: '👅 Taste', instruction: 'Notice 1 thing you can taste. (Run your tongue over your teeth.)', count: 1, icon: '👅' },
];

const STEP_TIMER_SECS = 45;

// ════════════════════════════════════════════════════════════
// COMPONENT
// ════════════════════════════════════════════════════════════

const FreezeStateUnblocker = () => {
  const { theme } = useTheme();
  const isDark = theme === 'dark';
  const { callToolEndpoint, loading } = useClaudeAPI();

  // ── Form state ──
  const [view, setView] = useState('start'); // start | breathing | bodyscan | actions | complete
  const [category, setCategory] = useState(null);
  const [taskDescription, setTaskDescription] = useState('');
  const [showTaskInput, setShowTaskInput] = useState(false);

  // ── Results ──
  const [results, setResults] = useState(null);
  const [currentStep, setCurrentStep] = useState(0);
  const [completedSteps, setCompletedSteps] = useState([]);
  const [error, setError] = useState('');

  // ── Check-in ──
  const [checkInResponse, setCheckInResponse] = useState(null);

  // ── Breathing ──
  const [breathingPhaseIdx, setBreathingPhaseIdx] = useState(0);
  const [breathingCycle, setBreathingCycle] = useState(0);
  const [breathingProgress, setBreathingProgress] = useState(0);
  const breathingRef = useRef(null);
  const breathStartRef = useRef(null);

  // ── Body scan (5-4-3-2-1) ──
  const [bodyScanStep, setBodyScanStep] = useState(0);
  const [bodyScanItems, setBodyScanItems] = useState([]);

  // ── Gentle timer ──
  const [timerEnabled, setTimerEnabled] = usePersistentState('fsu-timer-on', false);
  const [autoAdvance, setAutoAdvance] = usePersistentState('fsu-auto-advance', false);
  const [stepTimerSec, setStepTimerSec] = useState(0);
  const stepTimerRef = useRef(null);
  const [timerPulse, setTimerPulse] = useState(false);

  // ── Pattern detection ──
  const [patterns, setPatterns] = useState(null);
  const [patternLoading, setPatternLoading] = useState(false);
  const [showPatterns, setShowPatterns] = useState(false);

  // ── Emergency contact ──
  const [emergencyContact, setEmergencyContact] = usePersistentState('fsu-emergency', null);
  const [showContactSetup, setShowContactSetup] = useState(false);
  const [contactName, setContactName] = useState('');
  const [contactPhone, setContactPhone] = useState('');
  const [showSendOffer, setShowSendOffer] = useState(false);
  const [messageSent, setMessageSent] = useState(false);

  // ── Session history (persistent) ──
  const [history, setHistory] = usePersistentState('fsu-history', []);
  const [showHistory, setShowHistory] = useState(false);

  // ── Theme ──
  const c = {
    bg: isDark ? 'bg-zinc-900' : 'bg-slate-50',
    card: isDark ? 'bg-zinc-800 border-zinc-700' : 'bg-white border-stone-200',
    cardAlt: isDark ? 'bg-zinc-700/40 border-zinc-600' : 'bg-gray-50 border-stone-200',
    text: isDark ? 'text-zinc-50' : 'text-gray-900',
    textSec: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted: isDark ? 'text-zinc-500' : 'text-gray-400',
    input: isDark
      ? 'bg-zinc-700 border-zinc-600 text-zinc-100 placeholder:text-zinc-500 focus:border-blue-500 focus:ring-blue-500/20'
      : 'bg-white border-stone-300 text-gray-900 placeholder:text-stone-400 focus:border-blue-500 focus:ring-blue-500/20',
    btnPrimary: isDark ? 'bg-blue-600 hover:bg-blue-500 text-white' : 'bg-blue-600 hover:bg-blue-700 text-white',
    btnSecondary: isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200' : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    btnSoft: isDark ? 'bg-zinc-700/50 hover:bg-zinc-600/50 text-zinc-300' : 'bg-gray-50 hover:bg-gray-100 text-gray-500',
    accent: isDark ? 'text-blue-400' : 'text-blue-600',
    success: isDark ? 'text-emerald-400' : 'text-emerald-600',
    border: isDark ? 'border-zinc-700' : 'border-stone-200',
    warmthEmpty: isDark ? 'bg-zinc-800' : 'bg-gray-100',
    warmth1: isDark ? 'bg-blue-900/60' : 'bg-blue-100',
    warmth2: isDark ? 'bg-blue-700/60' : 'bg-blue-300',
    warmth3: isDark ? 'bg-blue-500/70' : 'bg-blue-500',
  };

  // ════════════════════════════════════════
  // GENTLE STEP TIMER
  // ════════════════════════════════════════

  const stopStepTimer = useCallback(() => {
    if (stepTimerRef.current) clearInterval(stepTimerRef.current);
    stepTimerRef.current = null;
    setStepTimerSec(0);
    setTimerPulse(false);
  }, []);

  const startStepTimer = useCallback(() => {
    if (!timerEnabled) return;
    setStepTimerSec(0);
    setTimerPulse(false);
    if (stepTimerRef.current) clearInterval(stepTimerRef.current);
    stepTimerRef.current = setInterval(() => {
      setStepTimerSec(prev => {
        const next = prev + 1;
        if (next >= STEP_TIMER_SECS) setTimerPulse(true);
        return next;
      });
    }, 1000);
  }, [timerEnabled]);

  // Start timer when step changes
  useEffect(() => {
    if (view === 'actions' && timerEnabled) startStepTimer();
    return () => stopStepTimer();
  }, [currentStep, view, timerEnabled, startStepTimer, stopStepTimer]);

  // Cleanup all intervals/rafs
  useEffect(() => {
    return () => {
      if (stepTimerRef.current) clearInterval(stepTimerRef.current);
      if (breathingRef.current) cancelAnimationFrame(breathingRef.current);
    };
  }, []);

  // ════════════════════════════════════════
  // BREATHING EXERCISE
  // ════════════════════════════════════════

  const startBreathing = () => {
    setView('breathing');
    setBreathingCycle(0);
    setBreathingPhaseIdx(0);
    setBreathingProgress(0);
    breathStartRef.current = Date.now();
    runBreathingLoop();
  };

  const runBreathingLoop = () => {
    const totalDuration = BREATHING_TOTAL_MS * BREATHING_CYCLES;
    const tick = () => {
      const elapsed = Date.now() - breathStartRef.current;
      if (elapsed >= totalDuration) {
        cancelAnimationFrame(breathingRef.current);
        getUnstuck();
        return;
      }
      const cycleElapsed = elapsed % BREATHING_TOTAL_MS;
      let acc = 0;
      let phaseIdx = 0;
      for (let i = 0; i < BREATHING_PHASES.length; i++) {
        const phaseMs = BREATHING_PHASES[i].duration * 1000;
        if (cycleElapsed < acc + phaseMs) { phaseIdx = i; break; }
        acc += phaseMs;
      }
      const phaseMs = BREATHING_PHASES[phaseIdx].duration * 1000;
      const phaseElapsed = cycleElapsed - acc;
      setBreathingPhaseIdx(phaseIdx);
      setBreathingCycle(Math.floor(elapsed / BREATHING_TOTAL_MS));
      setBreathingProgress(phaseElapsed / phaseMs);
      breathingRef.current = requestAnimationFrame(tick);
    };
    breathingRef.current = requestAnimationFrame(tick);
  };

  const skipBreathing = () => {
    if (breathingRef.current) cancelAnimationFrame(breathingRef.current);
    getUnstuck();
  };

  // ════════════════════════════════════════
  // BODY SCAN (5-4-3-2-1 GROUNDING)
  // ════════════════════════════════════════

  const startBodyScan = () => {
    setView('bodyscan');
    setBodyScanStep(0);
    setBodyScanItems([]);
  };

  const bodyScanTapItem = () => {
    const current = BODY_SCAN_STEPS[bodyScanStep];
    const newItems = [...bodyScanItems, true];
    setBodyScanItems(newItems);
    if (newItems.length >= current.count) {
      if (bodyScanStep >= BODY_SCAN_STEPS.length - 1) {
        getUnstuck();
      } else {
        setBodyScanStep(bodyScanStep + 1);
        setBodyScanItems([]);
      }
    }
  };

  // ════════════════════════════════════════
  // API CALLS
  // ════════════════════════════════════════

  const getUnstuck = async () => {
    setError('');
    setResults(null);
    try {
      const data = await callToolEndpoint('freeze-state-unblocker', {
        taskDescription: taskDescription || null,
        category: category ? CATEGORIES.find(ct => ct.id === category)?.label : null,
        stillStuck: false,
      });
      setResults(data);
      setView('actions');
      setCurrentStep(0);
      setCompletedSteps([]);
      setCheckInResponse(null);
      setShowSendOffer(false);
      setMessageSent(false);
    } catch (err) {
      setError(err.message || 'Failed to generate actions');
      setView('start');
    }
  };

  const getSmallerSteps = async () => {
    setError('');
    stopStepTimer();
    try {
      const data = await callToolEndpoint('freeze-state-unblocker', {
        taskDescription: taskDescription || null,
        category: category ? CATEGORIES.find(ct => ct.id === category)?.label : null,
        stillStuck: true,
        currentAction: results?.sequential_micro_actions?.[currentStep]?.action,
      });
      setResults(data);
      setCurrentStep(0);
      setCompletedSteps([]);
    } catch (err) {
      setError(err.message || 'Failed to generate smaller steps');
    }
  };

  const analyzePatterns = async () => {
    setPatternLoading(true);
    try {
      const data = await callToolEndpoint('freeze-state-unblocker/patterns', { sessions: history });
      setPatterns(data);
      setShowPatterns(true);
    } catch (err) {
      setError(err.message || 'Failed to analyze patterns');
    } finally {
      setPatternLoading(false);
    }
  };

  // ════════════════════════════════════════
  // STEP COMPLETION
  // ════════════════════════════════════════

  const completeStep = useCallback(() => {
    stopStepTimer();
    setCompletedSteps(prev => {
      const newCompleted = [...prev, currentStep];
      const total = results?.sequential_micro_actions?.length || 1;
      if (currentStep >= total - 1) {
        setHistory(h => [{
          id: Date.now(),
          date: new Date().toISOString(),
          category: category || 'general',
          task: taskDescription || null,
          stepsCompleted: newCompleted.length,
          totalSteps: total,
        }, ...h].slice(0, 100));
        setView('complete');
      } else {
        setCurrentStep(s => s + 1);
      }
      return newCompleted;
    });
  }, [currentStep, results, category, taskDescription, stopStepTimer, setHistory]);

  // Auto-advance when timer reaches threshold + 10s grace
  useEffect(() => {
    if (autoAdvance && timerEnabled && stepTimerSec >= STEP_TIMER_SECS + 10 && view === 'actions' && results) {
      completeStep();
    }
  }, [stepTimerSec, autoAdvance, timerEnabled, view, results, completeStep]);

  // ════════════════════════════════════════
  // CHECK-IN
  // ════════════════════════════════════════

  const handleCheckIn = (response) => {
    setCheckInResponse(response);
    setHistory(prev => {
      if (prev.length === 0) return prev;
      const updated = [...prev];
      updated[0] = { ...updated[0], checkIn: response };
      return updated;
    });
    if (response === 'worse' && emergencyContact) setShowSendOffer(true);
  };

  // ════════════════════════════════════════
  // EMERGENCY CONTACT
  // ════════════════════════════════════════

  const saveContact = () => {
    if (!contactName.trim()) return;
    setEmergencyContact({ name: contactName.trim(), phone: contactPhone.trim() || null });
    setShowContactSetup(false);
    setContactName('');
    setContactPhone('');
  };

  const sendEmergencyText = () => {
    const msg = encodeURIComponent(
      `Hey ${emergencyContact.name}, I'm having a rough time. You don't need to do anything, just wanted someone to know. 💙`
    );
    if (emergencyContact.phone) {
      window.open(`sms:${emergencyContact.phone}?body=${msg}`, '_blank');
    } else {
      navigator.clipboard?.writeText(decodeURIComponent(msg));
    }
    setMessageSent(true);
  };

  // ════════════════════════════════════════
  // RESET
  // ════════════════════════════════════════

  const reset = () => {
    stopStepTimer();
    setView('start');
    setCategory(null);
    setTaskDescription('');
    setShowTaskInput(false);
    setResults(null);
    setCurrentStep(0);
    setCompletedSteps([]);
    setError('');
    setCheckInResponse(null);
    setShowSendOffer(false);
    setMessageSent(false);
  };

  // ════════════════════════════════════════
  // TEXT HELPERS
  // ════════════════════════════════════════

  const buildFullText = () => {
    if (!results) return '';
    const lines = ['❄️ Freeze State Unblocker\n'];
    if (results.acknowledgment) lines.push(results.acknowledgment + '\n');
    (results.sequential_micro_actions || []).forEach((a, i) => {
      lines.push(`Step ${i + 1}: ${a.action}`);
      lines.push(`  ✓ Done when: ${a.completion_signal}`);
    });
    if (results.after_sequence?.permission_to_stop) lines.push('\n' + results.after_sequence.permission_to_stop);
    return lines.join('\n') + BRANDING;
  };

  // ════════════════════════════════════════
  // STATS + WARMTH MAP DATA
  // ════════════════════════════════════════

  const stats = useMemo(() => {
    if (history.length === 0) return null;
    const totalSteps = history.reduce((s, h) => s + (h.stepsCompleted || 0), 0);
    const checkIns = history.filter(h => h.checkIn);
    const betterCount = checkIns.filter(h => h.checkIn === 'better').length;
    return { totalSessions: history.length, totalSteps, betterCount, checkIns: checkIns.length };
  }, [history]);

  const warmthData = useMemo(() => {
    const days = [];
    const today = new Date();
    today.setHours(0, 0, 0, 0);
    for (let i = 34; i >= 0; i--) {
      const d = new Date(today);
      d.setDate(d.getDate() - i);
      const dateStr = d.toISOString().slice(0, 10);
      const sessionsOnDay = history.filter(h => h.date && h.date.slice(0, 10) === dateStr);
      days.push({ date: d, dateStr, count: sessionsOnDay.length, isToday: i === 0 });
    }
    return days;
  }, [history]);

  const currentAction = results?.sequential_micro_actions?.[currentStep];
  const timerProgress = Math.min(stepTimerSec / STEP_TIMER_SECS, 1);

  // ═══════════════════════════════════════════════════════════
  // RENDER
  // ═══════════════════════════════════════════════════════════

  return (
    <div className={`min-h-screen ${c.bg} p-4 sm:p-6 flex items-center justify-center`}>
      <div className="max-w-xl w-full">

        {/* ════════ START VIEW ════════ */}
        {view === 'start' && (
          <div className="space-y-6">

            {/* Header */}
            <div className="text-center">
              <span className="text-5xl block mb-3">❄️</span>
              <h1 className={`text-3xl sm:text-4xl font-black tracking-tight ${c.text}`}>
                Freeze State Unblocker
              </h1>
              <p className={`text-lg mt-2 ${c.textSec}`}>
                Completely stuck? Let's just do one tiny thing.
              </p>
            </div>

            {/* Main card */}
            <div className={`${c.card} border rounded-3xl shadow-xl p-6 sm:p-10 space-y-6`}>

              {/* Category selector */}
              <div>
                <p className={`text-xs font-bold uppercase tracking-wider mb-3 ${c.textMuted}`}>
                  What kind of stuck? (optional)
                </p>
                <div className="grid grid-cols-2 gap-2">
                  {CATEGORIES.map(cat => (
                    <button
                      key={cat.id}
                      onClick={() => {
                        setCategory(category === cat.id ? null : cat.id);
                        if (cat.id === 'other') setShowTaskInput(true);
                      }}
                      className={`p-3 rounded-xl border text-left text-sm font-semibold transition-all ${
                        category === cat.id
                          ? isDark ? 'border-blue-500 bg-blue-900/30 text-blue-300' : 'border-blue-500 bg-blue-50 text-blue-700'
                          : isDark ? 'border-zinc-600 hover:border-zinc-500 text-zinc-300' : 'border-stone-200 hover:border-stone-300 text-gray-700'
                      }`}
                    >
                      <span className="mr-2">{cat.icon}</span>{cat.label}
                    </button>
                  ))}
                </div>
              </div>

              {/* Optional task description */}
              {(showTaskInput || category === 'other') && (
                <div>
                  <input type="text" value={taskDescription} onChange={(e) => setTaskDescription(e.target.value)}
                    placeholder="A few words about what you're stuck on (optional)"
                    className={`w-full px-4 py-3 rounded-xl border-2 text-sm transition-all focus:outline-none focus:ring-2 ${c.input}`} />
                  <button onClick={() => { setShowTaskInput(false); setTaskDescription(''); }}
                    className={`text-xs mt-1.5 ${c.textMuted} hover:underline`}>✕ Clear</button>
                </div>
              )}
              {!showTaskInput && category !== 'other' && (
                <button onClick={() => setShowTaskInput(true)} className={`text-xs ${c.textMuted} hover:underline`}>
                  Or describe what you're stuck on...
                </button>
              )}

              {/* THE BUTTON */}
              <button onClick={getUnstuck} disabled={loading}
                className={`w-full py-6 sm:py-8 rounded-2xl font-black text-xl sm:text-2xl shadow-lg disabled:opacity-50 transition-all hover:scale-[1.02] active:scale-[0.98] ${c.btnPrimary}`}>
                {loading
                  ? <><span className="animate-spin inline-block mr-3">⏳</span> Getting first step...</>
                  : <><span className="mr-3">⚡</span> I'm Stuck</>}
              </button>

              {/* Grounding options */}
              <div className="grid grid-cols-2 gap-2">
                <button onClick={startBreathing} disabled={loading}
                  className={`py-3 rounded-xl text-sm font-semibold transition-all ${c.btnSoft}`}>
                  <span className="mr-1.5">🫁</span> Breathing first
                </button>
                <button onClick={startBodyScan} disabled={loading}
                  className={`py-3 rounded-xl text-sm font-semibold transition-all ${c.btnSoft}`}>
                  <span className="mr-1.5">🧘</span> Body scan first
                </button>
              </div>

              {/* Settings row */}
              <div className={`flex flex-wrap gap-3 pt-2 border-t ${c.border}`}>
                <label className={`flex items-center gap-2 text-xs ${c.textMuted} cursor-pointer`}>
                  <input type="checkbox" checked={timerEnabled} onChange={e => setTimerEnabled(e.target.checked)}
                    className="rounded border-gray-400" />
                  ⏱️ Gentle timer
                </label>
                {timerEnabled && (
                  <label className={`flex items-center gap-2 text-xs ${c.textMuted} cursor-pointer`}>
                    <input type="checkbox" checked={autoAdvance} onChange={e => setAutoAdvance(e.target.checked)}
                      className="rounded border-gray-400" />
                    Auto-advance
                  </label>
                )}
                <button onClick={() => setShowContactSetup(!showContactSetup)}
                  className={`text-xs ${c.textMuted} hover:underline ml-auto`}>
                  {emergencyContact ? `🛟 ${emergencyContact.name}` : '🛟 Set up safe person'}
                </button>
              </div>

              {/* Emergency contact setup */}
              {showContactSetup && (
                <div className={`p-4 rounded-xl border ${c.cardAlt} space-y-3`}>
                  <p className={`text-xs font-bold ${c.text}`}>🛟 Safe Person</p>
                  <p className={`text-xs ${c.textMuted}`}>
                    If you're having a hard time, we can offer to send them a pre-written text. Nothing automatic — always your choice.
                  </p>
                  <input type="text" value={contactName} onChange={e => setContactName(e.target.value)}
                    placeholder="Their name" className={`w-full px-3 py-2 rounded-lg border-2 text-sm ${c.input}`} />
                  <input type="tel" value={contactPhone} onChange={e => setContactPhone(e.target.value)}
                    placeholder="Phone number (optional)" className={`w-full px-3 py-2 rounded-lg border-2 text-sm ${c.input}`} />
                  <div className="flex gap-2">
                    <button onClick={saveContact} disabled={!contactName.trim()}
                      className={`px-4 py-2 rounded-lg text-xs font-bold disabled:opacity-40 ${c.btnPrimary}`}>Save</button>
                    {emergencyContact && (
                      <button onClick={() => { setEmergencyContact(null); setShowContactSetup(false); }}
                        className={`px-4 py-2 rounded-lg text-xs font-bold ${c.btnSecondary}`}>Remove</button>
                    )}
                    <button onClick={() => setShowContactSetup(false)}
                      className={`px-4 py-2 rounded-lg text-xs ${c.textMuted}`}>Cancel</button>
                  </div>
                </div>
              )}

              {error && (
                <div className={`p-4 rounded-xl border ${isDark ? 'bg-red-900/20 border-red-800/50 text-red-300' : 'bg-red-50 border-red-200 text-red-700'}`}>
                  <span className="mr-1">⚠️</span> {error}
                </div>
              )}
            </div>

            {/* Reassurance */}
            <div className={`text-center text-sm space-y-1 ${c.textMuted}`}>
              <p>You don't have to explain why you're stuck.</p>
              <p>You don't have to do the whole thing.</p>
              <p className="font-semibold">Just click the button.</p>
            </div>

            {/* ── Warmth Map ── */}
            {history.length > 0 && (
              <div className={`${c.card} border rounded-2xl p-5`}>
                <p className={`text-xs font-bold uppercase tracking-wider mb-3 ${c.textMuted}`}>
                  Days you moved (last 5 weeks)
                </p>
                <div className="grid grid-cols-7 gap-1.5">
                  {['S','M','T','W','T','F','S'].map((d, i) => (
                    <div key={i} className={`text-[10px] text-center font-bold ${c.textMuted}`}>{d}</div>
                  ))}
                  {Array.from({ length: warmthData[0]?.date.getDay() || 0 }).map((_, i) => (
                    <div key={`pad-${i}`} />
                  ))}
                  {warmthData.map((d) => (
                    <div
                      key={d.dateStr}
                      title={`${d.dateStr}: ${d.count} session${d.count !== 1 ? 's' : ''}`}
                      className={`aspect-square rounded-md transition-all ${
                        d.isToday ? 'ring-2 ring-blue-500 ring-offset-1' : ''
                      } ${
                        d.count === 0 ? c.warmthEmpty
                        : d.count === 1 ? c.warmth1
                        : d.count === 2 ? c.warmth2
                        : c.warmth3
                      }`}
                    />
                  ))}
                </div>
                <div className="flex items-center justify-between mt-3">
                  <div className="flex items-center gap-1.5">
                    <span className={`text-[10px] ${c.textMuted}`}>Less</span>
                    <div className={`w-3 h-3 rounded-sm ${c.warmthEmpty}`} />
                    <div className={`w-3 h-3 rounded-sm ${c.warmth1}`} />
                    <div className={`w-3 h-3 rounded-sm ${c.warmth2}`} />
                    <div className={`w-3 h-3 rounded-sm ${c.warmth3}`} />
                    <span className={`text-[10px] ${c.textMuted}`}>More</span>
                  </div>
                  <p className={`text-[10px] ${c.textMuted}`}>Not a streak. Gaps are normal.</p>
                </div>
              </div>
            )}

            {/* ── Stats + Pattern Analysis ── */}
            {stats && (
              <div className="space-y-3">
                <div className={`${c.card} border rounded-2xl p-5`}>
                  <div className="flex items-center justify-between mb-3">
                    <p className={`text-xs font-bold uppercase tracking-wider ${c.textMuted}`}>📊 Your stats</p>
                    <button onClick={() => setShowHistory(!showHistory)}
                      className={`text-xs ${c.textMuted} hover:underline`}>
                      {showHistory ? 'Hide history' : `History (${history.length})`}
                    </button>
                  </div>
                  <div className="flex gap-6">
                    <div>
                      <p className={`text-2xl font-black ${c.text}`}>{stats.totalSessions}</p>
                      <p className={`text-xs ${c.textMuted}`}>Freezes broken</p>
                    </div>
                    <div>
                      <p className={`text-2xl font-black ${c.text}`}>{stats.totalSteps}</p>
                      <p className={`text-xs ${c.textMuted}`}>Steps taken</p>
                    </div>
                    {stats.checkIns > 0 && (
                      <div>
                        <p className={`text-2xl font-black ${c.success}`}>{Math.round((stats.betterCount / stats.checkIns) * 100)}%</p>
                        <p className={`text-xs ${c.textMuted}`}>Felt better</p>
                      </div>
                    )}
                  </div>
                  {history.length >= 5 && (
                    <button onClick={analyzePatterns} disabled={patternLoading}
                      className={`mt-4 w-full py-2.5 rounded-xl text-xs font-bold transition-all ${c.btnSecondary}`}>
                      {patternLoading
                        ? <><span className="animate-spin inline-block mr-1">⏳</span> Analyzing...</>
                        : <><span className="mr-1">🔍</span> Analyze my freeze patterns</>}
                    </button>
                  )}
                </div>

                {/* Pattern results */}
                {showPatterns && patterns && (
                  <div className={`${c.card} border rounded-2xl p-5 space-y-4`}>
                    <p className={`text-xs font-bold uppercase tracking-wider ${c.accent}`}>🔍 Pattern Insights</p>
                    {patterns.patterns_found?.map((p, i) => (
                      <div key={i} className={`p-3 rounded-xl ${c.cardAlt} border`}>
                        <p className={`text-sm font-bold ${c.text}`}>{p.pattern}</p>
                        <p className={`text-xs mt-1 ${c.textSec}`}>{p.evidence}</p>
                        <p className={`text-xs mt-1 italic ${c.textMuted}`}>{p.insight}</p>
                      </div>
                    ))}
                    {patterns.what_helps_you_most && (
                      <div className={`p-3 rounded-xl border-2 ${isDark ? 'border-emerald-700/50 bg-emerald-900/10' : 'border-emerald-200 bg-emerald-50'}`}>
                        <p className={`text-xs font-bold uppercase tracking-wider mb-1 ${c.success}`}>What works for you</p>
                        <p className={`text-sm ${c.text}`}>{patterns.what_helps_you_most}</p>
                      </div>
                    )}
                    {patterns.gentle_suggestion && (
                      <p className={`text-sm ${c.textSec}`}>💡 {patterns.gentle_suggestion}</p>
                    )}
                    {patterns.affirmation && (
                      <p className={`text-sm italic ${c.textMuted}`}>{patterns.affirmation}</p>
                    )}
                    <button onClick={() => setShowPatterns(false)}
                      className={`text-xs ${c.textMuted} hover:underline`}>Close</button>
                  </div>
                )}

                {/* History list */}
                {showHistory && (
                  <div className={`${c.card} border rounded-2xl p-4 space-y-2 max-h-60 overflow-y-auto`}>
                    {history.slice(0, 20).map((h) => (
                      <div key={h.id} className={`flex items-center justify-between p-3 rounded-xl ${c.cardAlt}`}>
                        <div className="flex-1 min-w-0">
                          <p className={`text-sm font-semibold truncate ${c.text}`}>
                            {CATEGORIES.find(ct => ct.id === h.category)?.icon || '❄️'}{' '}
                            {h.task || CATEGORIES.find(ct => ct.id === h.category)?.label || 'General'}
                          </p>
                          <p className={`text-xs ${c.textMuted}`}>
                            {new Date(h.date).toLocaleDateString()} · {h.stepsCompleted}/{h.totalSteps} steps
                            {h.checkIn && ` · ${h.checkIn === 'better' ? '😊' : h.checkIn === 'same' ? '😐' : '😔'}`}
                          </p>
                        </div>
                      </div>
                    ))}
                    <button onClick={() => { setHistory([]); setShowHistory(false); }}
                      className={`text-xs ${c.textMuted} hover:underline`}>🗑️ Clear history</button>
                  </div>
                )}
              </div>
            )}

            {/* Cross-references */}
            <div className={`text-center text-xs ${c.textMuted} space-y-1`}>
              <p>Stuck on a decision? Try <a href="/tool/DecisionCoach" target="_blank" rel="noopener noreferrer" className={`underline ${c.accent}`}>Decision Coach</a></p>
              <p>Feeling burned out? Try <a href="/tool/BurnoutBreadcrumbTracker" target="_blank" rel="noopener noreferrer" className={`underline ${c.accent}`}>Burnout Breadcrumb Tracker</a></p>
            </div>
          </div>
        )}

        {/* ════════ BREATHING VIEW ════════ */}
        {view === 'breathing' && (
          <div className="text-center space-y-8">
            <p className={`text-sm ${c.textMuted}`}>Cycle {breathingCycle + 1} of {BREATHING_CYCLES}</p>
            <div className="flex items-center justify-center">
              <div className="rounded-full flex items-center justify-center transition-all duration-700 ease-in-out"
                style={{
                  width: BREATHING_PHASES[breathingPhaseIdx].label === 'Breathe in'
                    ? `${160 + breathingProgress * 80}px`
                    : BREATHING_PHASES[breathingPhaseIdx].label === 'Breathe out'
                    ? `${240 - breathingProgress * 80}px` : '200px',
                  height: BREATHING_PHASES[breathingPhaseIdx].label === 'Breathe in'
                    ? `${160 + breathingProgress * 80}px`
                    : BREATHING_PHASES[breathingPhaseIdx].label === 'Breathe out'
                    ? `${240 - breathingProgress * 80}px` : '200px',
                  background: isDark
                    ? 'radial-gradient(circle, rgba(59,130,246,0.3), rgba(59,130,246,0.05))'
                    : 'radial-gradient(circle, rgba(59,130,246,0.15), rgba(59,130,246,0.03))',
                  border: `2px solid ${isDark ? 'rgba(59,130,246,0.4)' : 'rgba(59,130,246,0.25)'}`,
                }}>
                <span className={`text-2xl font-bold ${c.accent}`}>{BREATHING_PHASES[breathingPhaseIdx].label}</span>
              </div>
            </div>
            <p className={`text-lg ${c.textSec}`}>{BREATHING_PHASES[breathingPhaseIdx].duration} seconds</p>
            <button onClick={skipBreathing} className={`text-sm px-6 py-3 rounded-xl transition-all ${c.btnSecondary}`}>
              {loading ? <><span className="animate-spin inline-block mr-2">⏳</span> Getting steps...</> : "I'm ready — get my first step"}
            </button>
          </div>
        )}

        {/* ════════ BODY SCAN VIEW ════════ */}
        {view === 'bodyscan' && (
          <div className="space-y-6">
            <div className="text-center">
              <p className={`text-xs uppercase tracking-wider font-bold mb-2 ${c.textMuted}`}>5-4-3-2-1 Grounding</p>
              <div className="flex justify-center gap-2 mb-4">
                {BODY_SCAN_STEPS.map((s, i) => (
                  <div key={i} className={`w-8 h-8 rounded-full flex items-center justify-center text-sm font-bold ${
                    i < bodyScanStep ? isDark ? 'bg-emerald-600 text-white' : 'bg-emerald-500 text-white'
                    : i === bodyScanStep ? isDark ? 'bg-blue-600 text-white' : 'bg-blue-500 text-white'
                    : isDark ? 'bg-zinc-700 text-zinc-400' : 'bg-gray-200 text-gray-400'
                  }`}>
                    {i < bodyScanStep ? '✓' : s.count}
                  </div>
                ))}
              </div>
            </div>

            <div className={`${c.card} border rounded-3xl shadow-xl p-8 sm:p-10 text-center space-y-6`}>
              <span className="text-5xl block">{BODY_SCAN_STEPS[bodyScanStep].icon}</span>
              <p className={`text-2xl font-black ${c.text}`}>{BODY_SCAN_STEPS[bodyScanStep].sense}</p>
              <p className={`text-base ${c.textSec}`}>{BODY_SCAN_STEPS[bodyScanStep].instruction}</p>

              <div className="flex justify-center gap-3">
                {Array.from({ length: BODY_SCAN_STEPS[bodyScanStep].count }).map((_, i) => (
                  <button key={i} onClick={bodyScanTapItem} disabled={i < bodyScanItems.length}
                    className={`w-12 h-12 rounded-full text-lg font-bold transition-all ${
                      i < bodyScanItems.length
                        ? isDark ? 'bg-emerald-600 text-white scale-95' : 'bg-emerald-500 text-white scale-95'
                        : i === bodyScanItems.length
                        ? `${c.btnPrimary} animate-pulse`
                        : isDark ? 'bg-zinc-700 text-zinc-500' : 'bg-gray-200 text-gray-400'
                    }`}>
                    {i < bodyScanItems.length ? '✓' : i + 1}
                  </button>
                ))}
              </div>
              <p className={`text-xs ${c.textMuted}`}>Tap each circle as you notice something. No rush.</p>
            </div>

            <div className="flex gap-2 justify-center">
              <button onClick={() => getUnstuck()} className={`text-sm px-6 py-3 rounded-xl ${c.btnSecondary}`}>
                {loading ? <><span className="animate-spin inline-block mr-2">⏳</span> Loading...</> : 'Skip to steps'}
              </button>
              <button onClick={reset} className={`text-sm px-4 py-3 rounded-xl ${c.btnSoft}`}>Back</button>
            </div>
          </div>
        )}

        {/* ════════ ACTIONS VIEW ════════ */}
        {view === 'actions' && results && currentAction && (
          <div className="space-y-5">

            {/* Progress */}
            <div className="text-center">
              <p className={`text-sm ${c.textMuted} mb-2`}>Step {currentStep + 1} of {results.sequential_micro_actions.length}</p>
              <div className="flex gap-1.5 justify-center">
                {results.sequential_micro_actions.map((_, idx) => (
                  <div key={idx}
                    className={`h-2 flex-1 max-w-10 rounded-full transition-all ${
                      completedSteps.includes(idx) ? 'bg-emerald-500'
                      : idx === currentStep ? 'bg-blue-500'
                      : isDark ? 'bg-zinc-700' : 'bg-gray-200'
                    }`} />
                ))}
              </div>
            </div>

            {/* Acknowledgment (first step only) */}
            {currentStep === 0 && results.acknowledgment && (
              <div className={`${c.card} border rounded-2xl p-5 border-l-4 border-l-blue-500`}>
                <p className={`text-base ${c.text} text-center`}>{results.acknowledgment}</p>
              </div>
            )}

            {/* Previous step encouragement */}
            {currentStep > 0 && completedSteps.length > 0 && (
              <div className="text-center py-2">
                <p className={`text-sm font-semibold ${c.success}`}>
                  ✅ {results.sequential_micro_actions[currentStep - 1]?.encouragement || 'That counts.'}
                </p>
              </div>
            )}

            {/* Current action card */}
            <div className={`${c.card} border rounded-3xl shadow-xl p-8 sm:p-10 text-center space-y-6 relative overflow-hidden`}>

              {/* Gentle timer bar */}
              {timerEnabled && (
                <div className="absolute top-0 left-0 right-0 h-1">
                  <div
                    className={`h-full transition-all duration-1000 ease-linear rounded-full ${
                      timerPulse ? 'bg-amber-500 animate-pulse' : isDark ? 'bg-blue-600/60' : 'bg-blue-300'
                    }`}
                    style={{ width: `${timerProgress * 100}%` }}
                  />
                </div>
              )}

              {/* Step badge + timer */}
              <div className={`inline-block px-4 py-1.5 rounded-full text-sm font-bold ${
                isDark ? 'bg-blue-900/40 text-blue-300' : 'bg-blue-100 text-blue-700'
              }`}>
                Step {currentStep + 1}
                {timerEnabled && stepTimerSec > 0 && (
                  <span className={`ml-2 ${c.textMuted}`}>
                    {stepTimerSec < 60 ? `${stepTimerSec}s` : `${Math.floor(stepTimerSec / 60)}m${stepTimerSec % 60}s`}
                  </span>
                )}
              </div>

              {/* Main action — LARGE */}
              <div>
                <span className="text-4xl block mb-4">👣</span>
                <p className={`text-2xl sm:text-3xl font-black leading-tight ${c.text}`}>{currentAction.action}</p>
              </div>

              {/* Completion signal */}
              <div className={`p-4 rounded-xl ${isDark ? 'bg-zinc-700/50' : 'bg-gray-50'}`}>
                <p className={`text-xs uppercase tracking-wider font-bold mb-1 ${c.textMuted}`}>Done when:</p>
                <p className={`text-sm font-semibold ${c.textSec}`}>{currentAction.completion_signal}</p>
              </div>

              {/* Timer pulse encouragement */}
              {timerPulse && (
                <p className={`text-xs ${c.textMuted} italic animate-pulse`}>Take your time. Tap when you're ready. 💙</p>
              )}

              {/* Just this one thing (first step) */}
              {currentStep === 0 && results.first_micro_action?.just_this_one_thing && (
                <div className={`p-4 rounded-xl border-2 ${isDark ? 'border-blue-700/50 bg-blue-900/10' : 'border-blue-200 bg-blue-50'}`}>
                  <p className={`text-sm ${c.text}`}>
                    <strong>Just this one thing.</strong> {results.first_micro_action.just_this_one_thing}
                  </p>
                </div>
              )}

              {/* Action buttons */}
              <div className="space-y-3">
                <button onClick={completeStep}
                  className={`w-full py-5 sm:py-6 rounded-xl font-black text-lg sm:text-xl shadow-lg transition-all hover:scale-[1.02] active:scale-[0.98] ${c.btnPrimary}`}>
                  <span className="mr-2">✅</span> I Did It
                </button>
                <button onClick={getSmallerSteps} disabled={loading}
                  className={`w-full py-4 rounded-xl font-semibold text-sm transition-all ${c.btnSecondary}`}>
                  {loading
                    ? <><span className="animate-spin inline-block mr-2">⏳</span> Getting smaller steps...</>
                    : "This step is too big — give me something smaller"}
                </button>
                <button onClick={reset} className={`w-full py-3 rounded-xl text-sm transition-all ${c.btnSoft}`}>
                  I need to stop (that's okay)
                </button>
              </div>

              {results.what_we_are_NOT_doing && (
                <p className={`text-xs ${c.textMuted} pt-2 border-t ${c.border}`}>
                  {results.what_we_are_NOT_doing.not_forcing}
                </p>
              )}
            </div>

            {currentStep === 0 && results.first_micro_action?.why_this_helps && (
              <p className={`text-center text-xs ${c.textMuted} max-w-md mx-auto`}>
                💡 {results.first_micro_action.why_this_helps}
              </p>
            )}

            {error && (
              <div className={`p-4 rounded-xl border ${isDark ? 'bg-red-900/20 border-red-800/50 text-red-300' : 'bg-red-50 border-red-200 text-red-700'}`}>
                <span className="mr-1">⚠️</span> {error}
              </div>
            )}
          </div>
        )}

        {/* ════════ COMPLETE VIEW ════════ */}
        {view === 'complete' && results && (
          <div className="space-y-5">
            <div className={`${c.card} border rounded-3xl shadow-xl p-8 sm:p-10 text-center space-y-6`}>
              <span className="text-6xl block">🎉</span>
              <div>
                <h2 className={`text-3xl font-black ${c.text} mb-2`}>You Moved</h2>
                <p className={`text-lg ${c.textSec}`}>
                  You completed {completedSteps.length + 1} step{completedSteps.length > 0 ? 's' : ''}
                </p>
              </div>

              {/* Check-in */}
              {results.after_sequence && !checkInResponse && (
                <div className={`p-6 rounded-2xl ${isDark ? 'bg-zinc-700/50' : 'bg-blue-50'}`}>
                  <p className={`text-lg ${c.text} mb-4 font-semibold`}>{results.after_sequence.check_in}</p>
                  <div className="flex gap-3 justify-center">
                    {[
                      { id: 'better', label: 'Better', icon: '😊', cls: 'bg-emerald-600 hover:bg-emerald-700' },
                      { id: 'same', label: 'Same', icon: '😐', cls: 'bg-amber-600 hover:bg-amber-700' },
                      { id: 'worse', label: 'Worse', icon: '😔', cls: 'bg-rose-600 hover:bg-rose-700' },
                    ].map(opt => (
                      <button key={opt.id} onClick={() => handleCheckIn(opt.id)}
                        className={`px-5 py-3 rounded-xl text-white font-semibold transition-all hover:scale-105 ${opt.cls}`}>
                        <span className="mr-1.5">{opt.icon}</span> {opt.label}
                      </button>
                    ))}
                  </div>
                </div>
              )}

              {/* Check-in response */}
              {checkInResponse && results.after_sequence && (
                <div className={`p-5 rounded-2xl ${isDark ? 'bg-zinc-700/50' : 'bg-blue-50'}`}>
                  <p className={`text-base ${c.text} font-semibold`}>
                    {checkInResponse === 'better' && (results.after_sequence.if_better || "Good. You moved — that's what matters.")}
                    {checkInResponse === 'same' && (results.after_sequence.if_still_stuck || "That's okay. You moved. That matters.")}
                    {checkInResponse === 'worse' && "That happens. You still did something, and that's not nothing. Be gentle with yourself."}
                  </p>
                </div>
              )}

              {/* Emergency contact offer — only on "worse" */}
              {showSendOffer && emergencyContact && !messageSent && (
                <div className={`p-5 rounded-2xl border-2 ${isDark ? 'border-amber-700/50 bg-amber-900/10' : 'border-amber-200 bg-amber-50'}`}>
                  <p className={`text-sm ${c.text} mb-3`}>
                    Would you like to let <strong>{emergencyContact.name}</strong> know you're having a hard time?
                  </p>
                  <p className={`text-xs ${c.textMuted} mb-3 italic`}>
                    Pre-written message: "Hey {emergencyContact.name}, I'm having a rough time. You don't need to do anything, just wanted someone to know. 💙"
                  </p>
                  <div className="flex gap-2 justify-center">
                    <button onClick={sendEmergencyText}
                      className={`px-5 py-2.5 rounded-xl text-sm font-bold ${c.btnPrimary}`}>
                      <span className="mr-1">💬</span> Send text
                    </button>
                    <button onClick={() => setShowSendOffer(false)}
                      className={`px-5 py-2.5 rounded-xl text-sm ${c.btnSecondary}`}>Not right now</button>
                  </div>
                </div>
              )}

              {messageSent && (
                <div className={`p-4 rounded-xl ${isDark ? 'bg-emerald-900/20' : 'bg-emerald-50'}`}>
                  <p className={`text-sm ${c.success} font-semibold`}>
                    ✅ Message ready to send to {emergencyContact?.name}
                  </p>
                </div>
              )}

              {/* Task-specific next step */}
              {results.if_freeze_is_about_specific_task && (
                <div className={`p-5 rounded-2xl border-2 ${isDark ? 'border-blue-700/50 bg-blue-900/10' : 'border-blue-200 bg-blue-50'} text-left`}>
                  <p className={`text-xs uppercase tracking-wider font-bold mb-2 ${c.textMuted}`}>
                    If you want to keep going with: {results.if_freeze_is_about_specific_task.task_identified}
                  </p>
                  <p className={`text-base font-bold mb-1 ${c.text}`}>Smallest next thing:</p>
                  <p className={`text-sm mb-2 ${c.textSec}`}>{results.if_freeze_is_about_specific_task.smallest_possible_step}</p>
                  <p className={`text-xs italic ${c.textMuted}`}>{results.if_freeze_is_about_specific_task.permission}</p>
                </div>
              )}

              {/* Permissions */}
              {results.after_sequence && (
                <div className="space-y-2">
                  <p className={`text-sm ${c.text}`}>{results.after_sequence.permission_to_stop}</p>
                  {results.what_we_are_NOT_doing && (
                    <p className={`text-xs ${c.textMuted}`}>{results.what_we_are_NOT_doing.not_judging}</p>
                  )}
                </div>
              )}

              {/* Actions */}
              <div className="flex gap-2 pt-2 justify-center">
                <CopyBtn content={buildFullText()} label="Copy Steps" />
                <ShareBtn content={buildFullText()} title="Freeze State Unblocker" />
              </div>

              <div className="space-y-3 pt-2">
                <button onClick={reset}
                  className={`w-full py-4 rounded-xl font-bold text-base transition-all ${c.btnPrimary}`}>
                  <span className="mr-2">🔄</span> Start Over (if stuck again)
                </button>
                <button onClick={reset}
                  className={`w-full py-3 rounded-xl text-sm transition-all ${c.btnSoft}`}>I'm done for now</button>
              </div>
            </div>

            {/* Session summary */}
            <div className={`${c.card} border rounded-2xl p-5 text-center`}>
              <p className={`text-xs uppercase tracking-wider font-bold mb-3 ${c.textMuted}`}>What you did</p>
              <div className="flex justify-center gap-8">
                <div>
                  <p className={`text-3xl font-black ${c.text}`}>{completedSteps.length + 1}</p>
                  <p className={`text-xs ${c.textMuted}`}>Steps completed</p>
                </div>
                <div>
                  <p className={`text-3xl font-black ${c.success}`}>✓</p>
                  <p className={`text-xs ${c.textMuted}`}>Freeze broken</p>
                </div>
                {stats && (
                  <div>
                    <p className={`text-3xl font-black ${c.accent}`}>{stats.totalSessions}</p>
                    <p className={`text-xs ${c.textMuted}`}>All-time</p>
                  </div>
                )}
              </div>
            </div>

            <p className={`text-center text-xs ${c.textMuted}`}>
              Need help planning what's next? Try <a href="/tool/DopamineMenuBuilder" target="_blank" rel="noopener noreferrer" className={`underline ${c.accent}`}>Dopamine Menu Builder</a>
            </p>
          </div>
        )}

      </div>
    </div>
  );
};

export default FreezeStateUnblocker;
