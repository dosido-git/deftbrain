import React, { useState, useCallback } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { CopyBtn, ActionBar } from '../components/ActionButtons';

// ════════════════════════════════════════════════════════════
// CONSTANTS
// ════════════════════════════════════════════════════════════
const MEDIA_TYPES = [
  { value: 'movie', label: 'Movie', icon: '🎬' },
  { value: 'show', label: 'TV Show', icon: '📺' },
  { value: 'book', label: 'Book', icon: '📖' },
  { value: 'game', label: 'Game', icon: '🎮' },
];

const SEVERITY_STYLES = {
  'NITPICK': { icon: '🤓', color: 'textMuted' },
  'MINOR': { icon: '🟡', color: 'warningText' },
  'MAJOR': { icon: '🟠', color: 'accent' },
  'UNIVERSE-BREAKING': { icon: '💀', color: 'dangerText' },
};

// ════════════════════════════════════════════════════════════
// COMPONENT
// ════════════════════════════════════════════════════════════
const PlotHole = () => {
  const { callToolEndpoint, loading } = useClaudeAPI();
  const { theme } = useTheme();
  const isDark = theme === 'dark';

  const c = {
    card: isDark ? 'bg-[#1a1a2e] border-[#2a2a4a]' : 'bg-white border-[#d4d4d4]',
    input: isDark
      ? 'bg-[#0f0f1e] border-[#2a2a4a] text-[#e8e8e8] placeholder:text-[#555] focus:border-[#c8a951] focus:ring-[#c8a951]/20'
      : 'bg-white border-[#ccc] text-[#1e3a5f] placeholder:text-[#999] focus:border-[#1e3a5f] focus:ring-[#1e3a5f]/20',
    text: isDark ? 'text-[#e8e8e8]' : 'text-[#1e3a5f]',
    textSec: isDark ? 'text-[#a0a0b8]' : 'text-[#4a6a8a]',
    textMuted: isDark ? 'text-[#666680]' : 'text-[#8a8a8a]',
    label: isDark ? 'text-[#c0c0d0]' : 'text-[#2a4a6a]',
    accent: isDark ? 'text-[#c8a951]' : 'text-[#1e3a5f]',
    accentBg: isDark ? 'bg-[#c8a951]/10 border-[#c8a951]/30' : 'bg-[#1e3a5f]/5 border-[#1e3a5f]/20',
    btnPrimary: isDark ? 'bg-[#c8a951] hover:bg-[#d4b85c] text-[#1a1a2e]' : 'bg-[#1e3a5f] hover:bg-[#2a4a6f] text-white',
    btnSec: isDark ? 'bg-[#2a2a4a] hover:bg-[#3a3a5a] text-[#e8e8e8]' : 'bg-[#f0f0f0] hover:bg-[#e0e0e0] text-[#1e3a5f]',
    danger: isDark ? 'bg-[#3a1a1a] border-[#5a2a2a] text-[#f0a0a0]' : 'bg-[#fef2f2] border-[#e8c0c0] text-[#7a2a2a]',
    dangerText: isDark ? 'text-[#f0a0a0]' : 'text-[#a03030]',
    warningText: isDark ? 'text-[#e0c080]' : 'text-[#8a6a20]',
    success: isDark ? 'bg-[#1a2e1a] border-[#2a4a2a] text-[#a0e0a0]' : 'bg-[#f0faf0] border-[#c0dcc0] text-[#2a5a2a]',
    pillActive: isDark ? 'bg-[#c8a951] border-[#c8a951] text-[#1a1a2e]' : 'bg-[#1e3a5f] border-[#1e3a5f] text-white',
    pillInactive: isDark ? 'bg-[#2a2a4a] border-[#3a3a5a] text-[#c0c0d0] hover:border-[#4a4a6a]' : 'bg-white border-[#d4d4d4] text-[#4a6a8a] hover:border-[#aaa]',
    quoteBg: isDark ? 'bg-[#0f0f1e]/60' : 'bg-[#f8f8f8]',
    warning: isDark ? 'bg-[#2e2a1a] border-[#4a3a2a] text-[#e0c080]' : 'bg-[#fffbf0] border-[#dcc8a0] text-[#6a4a1a]',
  };

  // ── State ──
  const [view, setView] = useState('find'); // find | defend
  const [title, setTitle] = useState('');
  const [description, setDescription] = useState('');
  const [mediaType, setMediaType] = useState('movie');
  const [results, setResults] = useState(null);
  const [defendHole, setDefendHole] = useState('');
  const [defendResults, setDefendResults] = useState(null);
  const [error, setError] = useState('');

  // ── API: Find holes ──
  const runAnalysis = useCallback(async () => {
    if (!title.trim()) return;
    setError(''); setResults(null);
    try {
      const data = await callToolEndpoint('plot-hole', { title: title.trim(), description: description.trim(), mediaType });
      setResults(data);
    } catch (err) { setError(err.message || 'Analysis failed'); }
  }, [title, description, mediaType, callToolEndpoint]);

  // ── API: Defend hole ──
  const runDefend = useCallback(async () => {
    if (!defendHole.trim()) return;
    setError(''); setDefendResults(null);
    try {
      const data = await callToolEndpoint('plot-hole/defend', { title: title.trim(), plotHole: defendHole.trim() });
      setDefendResults(data);
    } catch (err) { setError(err.message || 'Defense failed'); }
  }, [title, defendHole, callToolEndpoint]);

  // ── Copy builder ──
  const buildFullText = useCallback(() => {
    if (!results) return '';
    const lines = [`🕳️ PLOT HOLES: ${results.title_analyzed || title}`, ''];
    if (results.overall_verdict) lines.push(`Verdict: ${results.overall_verdict}`);
    if (results.swiss_cheese_rating) lines.push(`Swiss Cheese Rating: ${results.swiss_cheese_rating}/10`, '');
    results.holes?.forEach((h, i) => {
      lines.push(`${i + 1}. [${h.severity}] ${h.name}`);
      lines.push(`   ${h.description}`);
      if (h.reddit_would_say) lines.push(`   💬 "${h.reddit_would_say}"`);
      lines.push('');
    });
    if (results.biggest_hole) lines.push(`💀 Biggest: ${results.biggest_hole}`);
    lines.push('\n— Generated by DeftBrain · deftbrain.com');
    return lines.join('\n');
  }, [results, title]);

  const severityStyle = (sev) => SEVERITY_STYLES[sev] || SEVERITY_STYLES['MINOR'];

  // ════════════════════════════════════════════════════════════
  // RENDER
  // ════════════════════════════════════════════════════════════
  return (
    <div className={`space-y-4 ${c.text}`}>
      {/* Nav */}
      <div className="flex gap-2">
        {[
          { key: 'find', label: '🕳️ Find Holes' },
          { key: 'defend', label: '⚖️ Defend a Hole' },
        ].map(t => (
          <button key={t.key} onClick={() => setView(t.key)}
            className={`px-4 py-2 rounded-lg text-xs font-bold border transition-colors min-h-[36px] ${
              view === t.key ? c.pillActive : c.pillInactive}`}>
            {t.label}
          </button>
        ))}
      </div>

      {/* Find Holes view */}
      {view === 'find' && (
        <>
          <div className={`${c.card} border rounded-xl p-5`}>
            <div className={`mb-4 pb-3 border-b ${isDark ? 'border-[#2a2a4a]' : 'border-[#e0e0e0]'}`}>
              <h2 className={`text-xl font-bold`}><span className="mr-2">🕳️</span>Plot Hole Finder</h2>
              <p className={`text-sm ${c.textSec}`}>Name a movie, show, book, or game — I'll find where the logic breaks</p>
            </div>

            <div className="mb-3">
              <label className={`text-xs font-bold ${c.label} uppercase block mb-2`}>Type</label>
              <div className="flex gap-2">
                {MEDIA_TYPES.map(m => (
                  <button key={m.value} onClick={() => setMediaType(m.value)}
                    className={`px-3 py-1.5 rounded-lg text-xs font-bold border min-h-[32px] ${
                      mediaType === m.value ? c.pillActive : c.pillInactive}`}>
                    <span className="mr-1">{m.icon}</span>{m.label}
                  </button>
                ))}
              </div>
            </div>

            <div className="mb-3">
              <label className={`text-xs font-bold ${c.label} block mb-1.5`}>Title</label>
              <input type="text" value={title} onChange={e => setTitle(e.target.value)}
                placeholder="e.g., The Dark Knight, Breaking Bad, Harry Potter..."
                className={`w-full px-3 py-2.5 border rounded-lg text-sm ${c.input} outline-none focus:ring-2`}
                onKeyDown={e => e.key === 'Enter' && title.trim() && runAnalysis()} />
            </div>

            <div className="mb-4">
              <label className={`text-xs font-bold ${c.label} block mb-1.5`}>
                Extra context <span className={`font-normal ${c.textMuted}`}>(optional — specific scene or season)</span>
              </label>
              <textarea value={description} onChange={e => setDescription(e.target.value)}
                placeholder="e.g., 'Specifically Season 4' or 'The time travel rules'"
                rows={2} className={`w-full px-3 py-2 border rounded-lg text-sm ${c.input} outline-none focus:ring-2 resize-y`} />
            </div>

            <button onClick={runAnalysis} disabled={!title.trim() || loading}
              className={`w-full ${c.btnPrimary} disabled:opacity-40 font-bold py-3 rounded-lg flex items-center justify-center gap-2 min-h-[48px]`}>
              {loading ? <><span className="animate-spin inline-block">⏳</span> Analyzing...</> : <><span>🕳️</span> Find Plot Holes</>}
            </button>
          </div>

          {/* Results */}
          {results && (
            <div className="space-y-4">
              <div className="flex justify-end"><ActionBar content={buildFullText()} title="Plot Holes" /></div>

              {/* Verdict + swiss cheese rating */}
              <div className={`${c.accentBg} border-2 rounded-xl p-5 text-center`}>
                <span className="text-3xl block mb-2">🧀</span>
                <p className={`text-lg font-black ${c.text} mb-1`}>{results.title_analyzed}</p>
                <p className={`text-sm ${c.textSec} mb-3`}>{results.overall_verdict}</p>
                {results.swiss_cheese_rating && (
                  <div className="inline-block">
                    <span className={`text-3xl font-black ${c.accent}`}>{results.swiss_cheese_rating}</span>
                    <span className={`text-sm ${c.textMuted}`}>/10 Swiss Cheese Rating</span>
                  </div>
                )}
              </div>

              {/* Holes */}
              {results.holes?.length > 0 && (
                <div className={`${c.card} border rounded-xl p-4`}>
                  <h3 className={`text-sm font-bold ${c.text} mb-3`}>🕳️ The Holes ({results.holes.length})</h3>
                  <div className="space-y-3">
                    {results.holes.map((h, i) => {
                      const sev = severityStyle(h.severity);
                      return (
                        <div key={i} className={`${c.quoteBg} rounded-lg p-4`}>
                          <div className="flex items-start justify-between gap-2 mb-2">
                            <p className={`text-sm font-bold ${c.text}`}>{sev.icon} {h.name}</p>
                            <span className={`text-[9px] font-black px-2 py-0.5 rounded ${c[sev.color]} whitespace-nowrap`}>{h.severity}</span>
                          </div>
                          <p className={`text-xs ${c.textSec} mb-2`}>{h.description}</p>
                          {h.why_it_matters && <p className={`text-[11px] ${c.textMuted} mb-1`}>💥 {h.why_it_matters}</p>}
                          {h.best_defense && <p className={`text-[11px] ${isDark ? 'text-[#a0e0a0]' : 'text-[#2a7a2a]'} mb-1`}>🛡️ {h.best_defense}</p>}
                          {h.reddit_would_say && (
                            <p className={`text-[11px] ${c.textMuted} italic mt-2`}>💬 r/plotholes: "{h.reddit_would_say}"</p>
                          )}
                          <button onClick={() => { setDefendHole(h.description || h.name); setView('defend'); setDefendResults(null); }}
                            className={`text-[10px] font-bold ${c.accent} mt-2 min-h-[24px]`}>
                            ⚖️ Defend this hole
                          </button>
                        </div>
                      );
                    })}
                  </div>
                </div>
              )}

              {/* Biggest + Actually Clever + Why Nobody Cares */}
              {results.biggest_hole && (
                <div className={`${c.danger} border rounded-xl p-4`}>
                  <p className={`text-[10px] font-bold uppercase mb-1`}>💀 Biggest Hole</p>
                  <p className="text-xs">{results.biggest_hole}</p>
                </div>
              )}
              <div className="grid grid-cols-1 sm:grid-cols-2 gap-3">
                {results.actually_clever && (
                  <div className={`${c.success} border rounded-xl p-4`}>
                    <p className={`text-[10px] font-bold uppercase mb-1`}>✨ Actually Clever</p>
                    <p className="text-xs">{results.actually_clever}</p>
                  </div>
                )}
                {results.why_nobody_cares && (
                  <div className={`${c.warning} border rounded-xl p-4`}>
                    <p className={`text-[10px] font-bold uppercase mb-1`}>❤️ Why Nobody Cares</p>
                    <p className="text-xs">{results.why_nobody_cares}</p>
                  </div>
                )}
              </div>
            </div>
          )}
        </>
      )}

      {/* Defend view */}
      {view === 'defend' && (
        <>
          <div className={`${c.card} border rounded-xl p-5`}>
            <h2 className={`text-lg font-bold mb-1`}><span className="mr-2">⚖️</span>Defend a Plot Hole</h2>
            <p className={`text-sm ${c.textSec} mb-4`}>Describe the hole — I'll build the strongest possible defense</p>

            <div className="mb-3">
              <label className={`text-xs font-bold ${c.label} block mb-1.5`}>Story title</label>
              <input type="text" value={title} onChange={e => setTitle(e.target.value)}
                placeholder="What movie/show/book?" className={`w-full px-3 py-2.5 border rounded-lg text-sm ${c.input} outline-none focus:ring-2`} />
            </div>
            <div className="mb-4">
              <label className={`text-xs font-bold ${c.label} block mb-1.5`}>The plot hole</label>
              <textarea value={defendHole} onChange={e => setDefendHole(e.target.value)}
                placeholder="Describe the plot hole... e.g., 'Why didn't the eagles just fly them to Mordor?'"
                rows={3} className={`w-full px-3 py-2.5 border rounded-lg text-sm ${c.input} outline-none focus:ring-2 resize-y`} />
            </div>

            <button onClick={runDefend} disabled={!defendHole.trim() || loading}
              className={`w-full ${c.btnPrimary} disabled:opacity-40 font-bold py-3 rounded-lg flex items-center justify-center gap-2 min-h-[48px]`}>
              {loading ? <><span className="animate-spin inline-block">⏳</span> Building defense...</> : <><span>⚖️</span> Defend It</>}
            </button>
          </div>

          {defendResults && (
            <div className="space-y-4">
              <div className={`${c.accentBg} border-2 rounded-xl p-5 text-center`}>
                <span className="text-3xl block mb-2">⚖️</span>
                <p className={`text-xs ${c.textMuted} mb-1`}>{defendResults.hole_summary}</p>
                <p className={`text-xl font-black ${c.text}`}>{defendResults.defense_verdict}</p>
              </div>

              {defendResults.defense_arguments?.length > 0 && (
                <div className={`${c.card} border rounded-xl p-4`}>
                  <h3 className={`text-sm font-bold ${c.text} mb-3`}>🗣️ Defense Arguments</h3>
                  <div className="space-y-2">
                    {defendResults.defense_arguments.map((a, i) => (
                      <div key={i} className={`${c.quoteBg} rounded-lg p-3`}>
                        <div className="flex items-start justify-between gap-2 mb-1">
                          <p className={`text-xs font-bold ${c.text}`}>{a.argument}</p>
                          <span className={`text-[8px] font-black ${c.textMuted} whitespace-nowrap`}>{a.strength}</span>
                        </div>
                        <p className={`text-[10px] ${c.textMuted} mb-1`}>Type: {a.type}</p>
                        {a.counterpoint && <p className={`text-[10px] ${c.dangerText}`}>↩️ {a.counterpoint}</p>}
                      </div>
                    ))}
                  </div>
                </div>
              )}

              {defendResults.closing_statement && (
                <div className={`${c.accentBg} border rounded-xl p-4`}>
                  <p className={`text-[10px] font-bold ${c.accent} uppercase mb-1`}>🎤 Closing Statement</p>
                  <p className={`text-sm ${c.text} italic`}>{defendResults.closing_statement}</p>
                </div>
              )}

              {defendResults.honest_take && (
                <div className={`${c.quoteBg} border ${isDark ? 'border-[#2a2a4a]' : 'border-[#d4d4d4]'} rounded-xl p-4`}>
                  <p className={`text-[10px] font-bold ${c.textMuted} uppercase mb-1`}>🤫 Off the Record</p>
                  <p className={`text-xs ${c.textSec}`}>{defendResults.honest_take}</p>
                </div>
              )}

              <CopyBtn content={`⚖️ Plot Hole Defense: ${defendResults.hole_summary}\n\nVerdict: ${defendResults.defense_verdict}\n\n${defendResults.best_defense || ''}\n\n${defendResults.closing_statement || ''}\n\n— Generated by DeftBrain · deftbrain.com`} label="Copy Defense" />
            </div>
          )}
        </>
      )}

      {/* Error */}
      {error && (
        <div className={`${c.danger} border rounded-lg p-4 flex items-start gap-3`}>
          <span>⚠️</span><p className="text-sm">{error}</p>
        </div>
      )}
    </div>
  );
};

PlotHole.displayName = 'PlotHole';
export default PlotHole;
