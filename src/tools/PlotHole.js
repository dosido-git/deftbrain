import React, { useState, useRef, useCallback, useEffect } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { usePersistentState } from '../hooks/usePersistentState';
import { useRegisterActions } from '../components/ActionBarContext';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const MEDIA_TYPES = [
  { value: 'movie', label: 'Movie',   icon: '🎬' },
  { value: 'show',  label: 'TV Show', icon: '📺' },
  { value: 'book',  label: 'Book',    icon: '📖' },
  { value: 'game',  label: 'Game',    icon: '🎮' },
];

const PlotHole = ({ tool }) => {
  const { callToolEndpoint, loading } = useClaudeAPI();
  const { isDark } = useTheme();

  // ── Theme — verbatim from CONVENTIONS.md PF-2 ──
  const c = {
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:         isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20'
                          : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-500' : 'text-gray-400',
    required:      isDark ? 'text-amber-400' : 'text-amber-500',
    labelText:     isDark ? 'text-zinc-200' : 'text-gray-700',
    accentTxt:     isDark ? 'text-cyan-400' : 'text-cyan-600',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white'
                          : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200'
                          : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200'
                          : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200'
                          : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200'
                          : 'bg-red-50 border-red-200 text-red-800',
    warningBox:    isDark ? 'bg-amber-900/20 border-amber-700' : 'bg-amber-50 border-amber-300',
    warningTxt:    isDark ? 'text-amber-300' : 'text-amber-800',
    pillActive:    isDark ? 'border-cyan-500 bg-cyan-900/30 text-cyan-200'
                          : 'border-cyan-600 bg-cyan-100 text-cyan-900',
    pillInactive:  isDark ? 'border-zinc-600 text-zinc-400 hover:border-zinc-500'
                          : 'border-gray-300 text-gray-500 hover:border-gray-400',
  };
  c.textMuteded = c.textMuted;
  c.label       = c.labelText;
  // PlotHole-specific extensions
  c.quoteBg     = isDark ? 'bg-zinc-700/60' : 'bg-slate-100';

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';

  // ── State ──
  const [view, setView]             = useState('find');
  const [title, setTitle]           = useState('');
  const [description, setDescription] = useState('');
  const [mediaType, setMediaType]   = useState('movie');
  const [defendHole, setDefendHole] = useState('');
  const [defendResults, setDefendResults] = useState(null);
  const [error, setError]           = useState('');

  // ── Refs ──
  const resultsRef   = useRef(null);
  const submitRef    = useRef(null);
  const canSubmitRef = useRef(false);

  // ── Persistent state ──
  const [results, setResults]   = usePersistentState('plothole-result', null);
  const [history, setHistory]   = usePersistentState('plothole-history', []);

  // ── Handlers ──
  const runAnalysis = useCallback(async () => {
    if (!title.trim()) return;
    setError(''); setResults(null);
    try {
      const data = await callToolEndpoint('plot-hole', {
        title: title.trim(), description: description.trim(), mediaType,
      });
      setResults(data);
      setHistory(prev => [{
        id: Date.now(), date: new Date().toISOString(),
        preview: (title || '').slice(0, 40),
      }, ...prev].slice(0, 6));
    } catch (err) { setError(err.message || 'Analysis failed'); }
  }, [title, description, mediaType, callToolEndpoint, setResults, setHistory]);

  const runDefend = useCallback(async () => {
    if (!defendHole.trim()) return;
    setError(''); setDefendResults(null);
    try {
      const data = await callToolEndpoint('plot-hole/defend', {
        title: title.trim(), plotHole: defendHole.trim(),
      });
      setDefendResults(data);
    } catch (err) { setError(err.message || 'Defense failed'); }
  }, [title, defendHole, callToolEndpoint]);

  const handleReset = () => {
    setResults(null); setDefendResults(null);
    setTitle(''); setDescription(''); setDefendHole('');
    setError('');
  };

  // ── buildFullText ──
  const buildFullText = useCallback(() => {
    if (!results) return '';
    const lines = [`🕳️ PLOT HOLES: ${results.title_analyzed || title}`, ''];
    if (results.overall_verdict) lines.push(`Verdict: ${results.overall_verdict}`);
    if (results.swiss_cheese_rating) lines.push(`Swiss Cheese Rating: ${results.swiss_cheese_rating}/10`, '');
    results.holes?.forEach((h, i) => {
      lines.push(`${i + 1}. [${h.severity}] ${h.name}`);
      lines.push(`   ${h.description}`);
      if (h.reddit_would_say) lines.push(`   💬 "${h.reddit_would_say}"`);
      lines.push('');
    });
    if (results.biggest_hole) lines.push(`💀 Biggest: ${results.biggest_hole}`);
    return lines.join('\n') + BRAND;
  }, [results, title]);

  const buildDefendText = useCallback(() => {
    if (!defendResults) return '';
    return `⚖️ Plot Hole Defense: ${defendResults.hole_summary}\n\nVerdict: ${defendResults.defense_verdict}\n\n${defendResults.best_defense || ''}\n\n${defendResults.closing_statement || ''}` + BRAND;
  }, [defendResults]);

  // Which text to register depends on the active view
  const activeText = view === 'find' ? buildFullText() : buildDefendText();

  // Live ref assignments
  submitRef.current    = view === 'find' ? runAnalysis : runDefend;
  canSubmitRef.current = view === 'find' ? !!title.trim() : !!defendHole.trim();

  useRegisterActions(activeText, tool?.title || 'Plot Hole Finder');

  // ── Scroll to results ──
  useEffect(() => {
    if (!results && !defendResults) return;
    const t = setTimeout(() => resultsRef.current?.scrollIntoView({ behavior: 'smooth', block: 'start' }), 100);
    return () => clearTimeout(t);
  }, [results, defendResults]);

  // ── Keyboard shortcut ──
  useEffect(() => {
    const handler = (e) => {
      const tag = document.activeElement?.tagName;
      if (tag === 'SELECT') return;
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && !loading && canSubmitRef.current)
        submitRef.current?.();
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [loading]);

  const getSevStyle = (sev) => {
    switch (sev) {
      case 'UNIVERSE-BREAKING': return { icon: '💀', cls: c.danger };
      case 'MAJOR':             return { icon: '🟠', cls: c.warning };
      case 'MINOR':             return { icon: '🟡', cls: c.warningBox };
      default:                  return { icon: '🤓', cls: c.cardAlt };
    }
  };

  return (
    <div className={`space-y-4 ${c.text}`}>

      {/* ── Persistent header + tab nav ── */}
      <div className={`${c.card} border ${c.border} rounded-xl shadow-sm overflow-hidden`}>
        <div className="px-5 pt-5">
          <div className="pb-3 border-b border-zinc-500">
            <div className="flex items-start justify-between gap-3">
              <div>
                <h2 className={`text-xl font-bold ${c.text}`}>
                  <span className="mr-2">{tool?.icon ?? '🕳️'}</span>{tool?.title ?? 'Plot Hole Finder'}
                </h2>
                <p className={`text-sm ${c.textSecondary}`}>{tool?.tagline ?? "Name a movie, show, book, or game — I'll find where the logic breaks"}</p>
              </div>
              {(results || defendResults || title.trim() || defendHole.trim()) && (
                <button onClick={handleReset} className={`${c.btnSecondary} px-3 py-1.5 rounded-lg text-xs font-bold flex-shrink-0`}>
                  ↺ Start Over
                </button>
              )}
            </div>
          </div>
        </div>
        <div className="px-5 py-3 flex gap-2">
          {[
            { key: 'find',   label: '🕳️ Find Holes' },
            { key: 'defend', label: '⚖️ Defend a Hole' },
          ].map(t => (
            <button key={t.key} onClick={() => { setView(t.key); setError(''); }}
              className={`px-4 py-2 rounded-lg text-xs font-bold border transition-colors min-h-[36px] ${
                view === t.key ? c.pillActive : c.pillInactive}`}>
              {t.label}
            </button>
          ))}
        </div>
      </div>

      {/* ── Find Holes ── */}
      {view === 'find' && (
        <>
          <div className={`${c.card} border ${c.border} rounded-xl p-5 space-y-4`}>
            <div>
              <label className={`text-xs font-bold ${c.label} uppercase block mb-2`}>Type</label>
              <div className="flex gap-2">
                {MEDIA_TYPES.map(m => (
                  <button key={m.value} onClick={() => setMediaType(m.value)}
                    className={`px-3 py-1.5 rounded-lg text-xs font-bold border min-h-[32px] ${
                      mediaType === m.value ? c.pillActive : c.pillInactive}`}>
                    <span className="mr-1">{m.icon}</span>{m.label}
                  </button>
                ))}
              </div>
            </div>

            <div>
              <label className={`text-xs font-bold ${c.label} block mb-1.5`}>Title <span className={c.required}>*</span></label>
              <input type="text" value={title} onChange={e => setTitle(e.target.value)}
                placeholder="e.g., The Dark Knight, Breaking Bad, Harry Potter..."
                className={`w-full px-3 py-2.5 border rounded-lg text-sm ${c.input} outline-none focus:ring-2`}
                onKeyDown={e => e.key === 'Enter' && title.trim() && runAnalysis()} />
            </div>

            <div>
              <label className={`text-xs font-bold ${c.label} block mb-1.5`}>
                Extra context <span className={`font-normal ${c.textMuted}`}>(optional — specific scene or season)</span>
              </label>
              <textarea value={description} onChange={e => setDescription(e.target.value)}
                placeholder="e.g., 'Specifically Season 4' or 'The time travel rules'"
                rows={2} className={`w-full px-3 py-2 border rounded-lg text-sm ${c.input} outline-none focus:ring-2 resize-y`} />
            </div>

            <button onClick={runAnalysis} disabled={!title.trim() || loading}
              className={`w-full ${c.btnPrimary} disabled:opacity-40 font-bold py-3 rounded-lg flex items-center justify-center gap-2 min-h-[48px]`}>
              {loading
                ? <><span className="inline-block animate-spin">{tool?.icon ?? '⚙️'}</span> Analyzing…</>
                : <><span>{tool?.icon ?? '🕳️'}</span> Find Plot Holes</>}
            </button>

            <p className={`text-xs text-center ${c.textMuted}`}>
              Found a hole you want to debate? Switch to <a href="/PlotTwist" className={linkStyle}>🔀 Plot Twist</a> to weigh both sides.
            </p>
          </div>

          {/* Results */}
          {results && (
            <div className="space-y-4" ref={resultsRef}>
              {/* Verdict + rating */}
              <div className={`${c.warningBox} border-2 rounded-xl p-5 text-center`}>
                <span className="text-3xl block mb-2">🧀</span>
                <p className={`text-lg font-black ${c.text} mb-1`}>{results.title_analyzed}</p>
                <p className={`text-sm ${c.textSecondary} mb-3`}>{results.overall_verdict}</p>
                {results.swiss_cheese_rating && (
                  <div className="inline-block">
                    <span className={`text-3xl font-black ${c.accentTxt}`}>{results.swiss_cheese_rating}</span>
                    <span className={`text-sm ${c.textMuted}`}>/10 Swiss Cheese Rating</span>
                  </div>
                )}
              </div>

              {/* Holes */}
              {results.holes?.length > 0 && (
                <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
                  <h3 className={`text-sm font-bold ${c.text} mb-3`}>🕳️ The Holes ({results.holes.length})</h3>
                  <div className="space-y-3">
                    {results.holes.map((h, i) => {
                      const sev = getSevStyle(h.severity);
                      return (
                        <div key={i} className={`${c.quoteBg} rounded-lg p-4`}>
                          <div className="flex items-start justify-between gap-2 mb-2">
                            <p className={`text-sm font-bold ${c.text}`}>{sev.icon} {h.name}</p>
                            <span className={`text-[9px] font-black px-2 py-0.5 rounded border whitespace-nowrap ${sev.cls}`}>{h.severity}</span>
                          </div>
                          <p className={`text-xs ${c.textSecondary} mb-2`}>{h.description}</p>
                          {h.why_it_matters && <p className={`text-[11px] ${c.textMuted} mb-1`}>💥 {h.why_it_matters}</p>}
                          {h.best_defense    && <p className={`text-[11px] ${c.textMuted} mb-1`}>🛡️ {h.best_defense}</p>}
                          {h.reddit_would_say && (
                            <p className={`text-[11px] ${c.textMuted} italic mt-2`}>💬 r/plotholes: "{h.reddit_would_say}"</p>
                          )}
                          <button onClick={() => { setDefendHole(h.description || h.name); setView('defend'); setDefendResults(null); }}
                            className={`text-[10px] font-bold ${c.accentTxt} mt-2 min-h-[24px]`}>
                            ⚖️ Defend this hole
                          </button>
                        </div>
                      );
                    })}
                  </div>
                </div>
              )}

              {/* Biggest + Actually Clever + Why Nobody Cares */}
              {results.biggest_hole && (
                <div className={`${c.danger} border rounded-xl p-4`}>
                  <p className="text-[10px] font-bold uppercase mb-1">💀 Biggest Hole</p>
                  <p className="text-xs">{results.biggest_hole}</p>
                </div>
              )}
              <div className="grid grid-cols-1 sm:grid-cols-2 gap-3">
                {results.actually_clever && (
                  <div className={`${c.success} border rounded-xl p-4`}>
                    <p className="text-[10px] font-bold uppercase mb-1">✨ Actually Clever</p>
                    <p className="text-xs">{results.actually_clever}</p>
                  </div>
                )}
                {results.why_nobody_cares && (
                  <div className={`${c.warning} border rounded-xl p-4`}>
                    <p className="text-[10px] font-bold uppercase mb-1">❤️ Why Nobody Cares</p>
                    <p className="text-xs">{results.why_nobody_cares}</p>
                  </div>
                )}
              </div>

              {/* Post-result cross-refs */}
              <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
                <p className={`text-xs font-bold ${c.textMuted} uppercase tracking-wide mb-3`}>🔗 Related tools</p>
                <div className="flex flex-wrap gap-3">
                  <a href="/FanTheory" className={`text-xs ${linkStyle}`}>🔭 Fan Theory</a>
                </div>
              </div>
            </div>
          )}
        </>
      )}

      {/* ── Defend ── */}
      {view === 'defend' && (
        <>
          <div className={`${c.card} border ${c.border} rounded-xl p-5 space-y-4`}>
            <p className={`text-sm font-bold ${c.textSecondary}`}>Describe the hole — I'll build the strongest possible defense</p>

            <div>
              <label className={`text-xs font-bold ${c.label} block mb-1.5`}>Story title</label>
              <input type="text" value={title} onChange={e => setTitle(e.target.value)}
                placeholder="What movie/show/book?"
                className={`w-full px-3 py-2.5 border rounded-lg text-sm ${c.input} outline-none focus:ring-2`} />
            </div>

            <div>
              <label className={`text-xs font-bold ${c.label} block mb-1.5`}>The plot hole <span className={c.required}>*</span></label>
              <textarea value={defendHole} onChange={e => setDefendHole(e.target.value)}
                placeholder="Describe the plot hole... e.g., 'Why didn't the eagles just fly them to Mordor?'"
                rows={3} className={`w-full px-3 py-2.5 border rounded-lg text-sm ${c.input} outline-none focus:ring-2 resize-y`} />
            </div>

            <button onClick={runDefend} disabled={!defendHole.trim() || loading}
              className={`w-full ${c.btnPrimary} disabled:opacity-40 font-bold py-3 rounded-lg flex items-center justify-center gap-2 min-h-[48px]`}>
              {loading
                ? <><span className="inline-block animate-spin">{tool?.icon ?? '⚙️'}</span> Building defense…</>
                : <><span>{tool?.icon ?? '⚖️'}</span> Defend It</>}
            </button>
          </div>

          {defendResults && (
            <div className="space-y-4" ref={resultsRef}>
              <div className={`${c.warningBox} border-2 rounded-xl p-5 text-center`}>
                <span className="text-3xl block mb-2">⚖️</span>
                <p className={`text-xs ${c.textMuted} mb-1`}>{defendResults.hole_summary}</p>
                <p className={`text-xl font-black ${c.text}`}>{defendResults.defense_verdict}</p>
              </div>

              {defendResults.defense_arguments?.length > 0 && (
                <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
                  <h3 className={`text-sm font-bold ${c.text} mb-3`}>🗣️ Defense Arguments</h3>
                  <div className="space-y-2">
                    {defendResults.defense_arguments.map((a, i) => (
                      <div key={i} className={`${c.quoteBg} rounded-lg p-3`}>
                        <div className="flex items-start justify-between gap-2 mb-1">
                          <p className={`text-xs font-bold ${c.text}`}>{a.argument}</p>
                          <span className={`text-[8px] font-black ${c.textMuted} whitespace-nowrap`}>{a.strength}</span>
                        </div>
                        <p className={`text-[10px] ${c.textMuted} mb-1`}>Type: {a.type}</p>
                        {a.counterpoint && <p className={`text-[10px] ${c.warningTxt}`}>↩️ {a.counterpoint}</p>}
                      </div>
                    ))}
                  </div>
                </div>
              )}

              {defendResults.closing_statement && (
                <div className={`${c.warningBox} border rounded-xl p-4`}>
                  <p className={`text-[10px] font-bold ${c.accentTxt} uppercase mb-1`}>🎤 Closing Statement</p>
                  <p className={`text-sm ${c.text} italic`}>{defendResults.closing_statement}</p>
                </div>
              )}

              {defendResults.honest_take && (
                <div className={`${c.quoteBg} border ${c.border} rounded-xl p-4`}>
                  <p className={`text-[10px] font-bold ${c.textMuted} uppercase mb-1`}>🤫 Off the Record</p>
                  <p className={`text-xs ${c.textSecondary}`}>{defendResults.honest_take}</p>
                </div>
              )}

              {/* Post-result cross-refs */}
              <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
                <p className={`text-xs font-bold ${c.textMuted} uppercase tracking-wide mb-3`}>🔗 Related tools</p>
                <div className="flex flex-wrap gap-3">
                  <a href="/PlotTwist" className={`text-xs ${linkStyle}`}>🔀 Plot Twist</a>
                </div>
              </div>
            </div>
          )}
        </>
      )}

      {/* ── Error ── */}
      {error && (
        <div className={`${c.danger} border rounded-lg p-4 flex items-start gap-3`}>
          <span>⚠️</span><p className="text-sm">{error}</p>
        </div>
      )}

      {/* ── History ── */}
      {history.length > 0 && (
        <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
          <p className={`text-xs font-bold ${c.textMuted} mb-2`}>📋 Recent</p>
          <div className="space-y-1">
            {history.map(s => (
              <div key={s.id} className="flex items-center justify-between">
                <span className={`text-xs ${c.textSecondary} truncate`}>{s.preview || 'Session'}</span>
                <span className={`text-xs ${c.textMuted} ml-2`}>{new Date(s.date).toLocaleDateString()}</span>
              </div>
            ))}
          </div>
        </div>
      )}
    </div>
  );
};

PlotHole.displayName = 'PlotHole';
export default PlotHole;
