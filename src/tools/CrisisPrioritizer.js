function deadlineCountdown(deadlineStr) {
  if (!deadlineStr || deadlineStr === 'no hard deadline') return null;
  const now = new Date();
  const kw = deadlineStr.toLowerCase();
  let target = null;
  if (kw.includes('today') || kw.includes('tonight') || kw.includes('end of day') || kw.includes('eod')) {
    target = new Date(now); target.setHours(23, 59, 59);
  } else if (kw.includes('tomorrow')) {
    target = new Date(now); target.setDate(target.getDate() + 1); target.setHours(17, 0, 0);
  } else if (kw.includes('monday')) { target = nextDay(1); }
  else if (kw.includes('tuesday')) { target = nextDay(2); }
  else if (kw.includes('wednesday')) { target = nextDay(3); }
  else if (kw.includes('thursday')) { target = nextDay(4); }
  else if (kw.includes('friday')) { target = nextDay(5); }
  else if (kw.includes('saturday')) { target = nextDay(6); }
  else if (kw.includes('sunday')) { target = nextDay(0); }
  else {
    const parsed = Date.parse(deadlineStr);
    if (!isNaN(parsed)) target = new Date(parsed);
  }
  if (!target) return null;
  const diff = target - now;
  if (diff < 0) return { label: 'Overdue', urgency: 'critical', hours: 0 };
  const hrs = Math.floor(diff / 3600000);
  const mins = Math.floor((diff % 3600000) / 60000);
  if (hrs < 2) return { label: `${hrs}h ${mins}m left`, urgency: 'critical', hours: hrs };
  if (hrs < 6) return { label: `${hrs}h left`, urgency: 'critical', hours: hrs };
  if (hrs < 24) return { label: `${hrs}h left`, urgency: 'important', hours: hrs };
  const days = Math.floor(hrs / 24);
  if (days < 3) return { label: `${days}d ${hrs % 24}h left`, urgency: 'important', hours: hrs };
  return { label: `${days} days left`, urgency: 'medium', hours: hrs };
}

function nextDay(dayNum) {
  const d = new Date(); d.setHours(17, 0, 0, 0);
  while (d.getDay() !== dayNum || d <= new Date()) d.setDate(d.getDate() + 1);
  return d;
}

function fmtDate(iso) {
  try { const d = Math.floor((Date.now() - new Date(iso)) / 86400000); return d === 0 ? 'Today' : d === 1 ? 'Yesterday' : d < 7 ? `${d}d ago` : new Date(iso).toLocaleDateString('en', { month: 'short', day: 'numeric' }); } catch { return ''; }
}

