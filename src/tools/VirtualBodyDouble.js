                <button onClick={() => setSoundEnabled(!soundEnabled)} className={`text-xs px-2.5 py-1.5 rounded-lg ${soundEnabled ? c.tagActive : c.tag}`} title={soundEnabled ? 'Sound on' : 'Sound off'}>{soundEnabled ? '🔔' : '🔕'}</button>
