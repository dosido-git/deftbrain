import React, { useState, useRef, useCallback, useEffect } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { usePersistentState } from '../hooks/usePersistentState';
import { useRegisterActions } from '../components/ActionBarContext';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const DOC_TYPES = [
  { value: 'insurance_eob', label: '🏥 Insurance EOB' },
  { value: 'school_newsletter', label: '🏫 School Newsletter' },
  { value: 'hoa_notice', label: '🏠 HOA / Condo Notice' },
  { value: 'lease', label: '📄 Lease / Amendment' },
  { value: 'policy_update', label: '🏢 Company Policy Update' },
  { value: 'benefits', label: '💼 Benefits Enrollment' },
  { value: 'government', label: '🏛️ Government Notice' },
  { value: 'medical', label: '⚕️ Medical / Billing' },
  { value: 'legal', label: '⚖️ Legal Document' },
  { value: 'other', label: '📎 Other' },
];

const NoiseCanceler = ({ tool }) => {
  const { callToolEndpoint, loading } = useClaudeAPI();
  const { isDark } = useTheme();
  const resultsRef = useRef(null);
  const filterRef = useRef(null);
  const canSubmitRef = useRef(false);

  const c = {
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:         isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20'
                          : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-500' : 'text-gray-400',
    labelText:     isDark ? 'text-zinc-200' : 'text-gray-700',
    accentTxt:     isDark ? 'text-cyan-400' : 'text-cyan-600',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white'
                          : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200'
                          : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200'
                          : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200'
                          : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200'
                          : 'bg-red-50 border-red-200 text-red-800',
    warningBox:    isDark ? 'bg-amber-900/20 border-amber-700' : 'bg-amber-50 border-amber-300',
    warningTxt:    isDark ? 'text-amber-300' : 'text-amber-800',
    pillActive:    isDark ? 'border-cyan-500 bg-cyan-900/30 text-cyan-200'
                          : 'border-cyan-600 bg-cyan-100 text-cyan-900',
    pillInactive:  isDark ? 'border-zinc-600 text-zinc-400 hover:border-zinc-500'
                          : 'border-gray-300 text-gray-500 hover:border-gray-400',
    required:      isDark ? 'text-amber-400' : 'text-amber-500',
    // Tool-specific keys
    actionBg:      isDark ? 'bg-amber-900/20 border-amber-600' : 'bg-amber-50 border-amber-400',
    actionText:    isDark ? 'text-amber-300' : 'text-amber-700',
    tipBg:         isDark ? 'bg-sky-900/20 border-sky-700' : 'bg-sky-50 border-sky-200',
    tipText:       isDark ? 'text-sky-300' : 'text-sky-700',
    costBg:        isDark ? 'bg-red-900/20 border-red-700' : 'bg-red-50 border-red-200',
    costText:      isDark ? 'text-red-300' : 'text-red-700',
    saveBg:        isDark ? 'bg-emerald-900/20 border-emerald-700' : 'bg-emerald-50 border-emerald-300',
    saveText:      isDark ? 'text-emerald-300' : 'text-emerald-700',
    badge:         isDark ? 'bg-zinc-700 text-zinc-300' : 'bg-gray-100 text-gray-500',
    inset:         isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    infoBg:        isDark ? 'bg-sky-900/20 border-sky-700' : 'bg-sky-50 border-sky-200',
    infoText:      isDark ? 'text-sky-300' : 'text-sky-700',
    histBg:        isDark ? 'bg-zinc-800 border-zinc-700' : 'bg-white border-gray-200',
    histCard:      isDark ? 'bg-zinc-700/50 border-zinc-600' : 'bg-slate-50 border-gray-200',
  };
  c.textMuteded = c.textMuted;
  c.label = c.labelText;

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';

  // ─── State ───
  const [docText, setDocText] = useState('');
  const [documentType, setDocumentType] = useState('other');
  const [mySituation, setMySituation] = useState('');
  const [concerns, setConcerns] = useState('');
  const [error, setError] = useState('');
  const [showAffects, setShowAffects] = useState(true);
  const [showBuried, setShowBuried] = useState(true);
  const [showQuestions, setShowQuestions] = useState(false);
  const [showHistory, setShowHistory] = useState(false);

  // ─── Persistent ───
  const [history, setHistory] = usePersistentState('noisecanceler-history', []);
  const [results, setResults] = usePersistentState('noisecanceler-result', null);

  // ═══ HANDLERS ═══
  const filter = useCallback(async () => {
    if (!docText.trim()) { setError('Paste the document you received.'); return; }
    if (!mySituation.trim()) { setError('Tell us your situation so we can filter what\'s relevant.'); return; }
    setError(''); setResults(null);
    try {
      const data = await callToolEndpoint('noise-canceler', {
        document: docText.trim(), documentType, mySituation: mySituation.trim(),
        concerns: concerns.trim() || null,
      });
      setResults(data);
      setHistory(prev => [{
        id: 'nc_' + Date.now(), date: new Date().toISOString(),
        type: data?.document_type || documentType,
        tldr: data?.tldr?.substring(0, 60) || '...',
        preview: docText.slice(0, 40),
        results: data,
      }, ...prev].slice(0, 6));
    } catch (err) { setError(err.message || 'Failed to filter document.'); }
  }, [docText, documentType, mySituation, concerns, callToolEndpoint, setResults, setHistory]);

  const handleReset = useCallback(() => {
    setDocText(''); setMySituation(''); setConcerns(''); setResults(null); setError('');
  }, [setResults]);

  const buildCopy = useCallback(() => {
    if (!results) return '';
    const lines = ['🔇 Noise Canceler — What Matters to You', '', 'TL;DR: ' + (results.tldr || ''), ''];
    if (results.action_required?.length) {
      lines.push('⚡ ACTION REQUIRED:');
      results.action_required.forEach(a => lines.push('  • ' + a.what + (a.deadline ? ' (by ' + a.deadline + ')' : '')));
      lines.push('');
    }
    if (results.costs_you_money?.length) {
      lines.push('💸 COSTS YOU MONEY:');
      results.costs_you_money.forEach(item => lines.push('  • ' + item.what + (item.amount ? ': ' + item.amount : '')));
      lines.push('');
    }
    if (results.saves_you_money?.length) {
      lines.push('💚 SAVES YOU MONEY:');
      results.saves_you_money.forEach(item => lines.push('  • ' + item.what + (item.amount ? ': ' + item.amount : '')));
      lines.push('');
    }
    return lines.join('\n') + BRAND;
  }, [results]);

  filterRef.current = filter;
  canSubmitRef.current = !!docText.trim() && !!mySituation.trim();

  useRegisterActions(buildCopy(), tool?.title || 'Noise Canceler');

  // ─── Scroll to results ───
  useEffect(() => {
    if (!results || !resultsRef.current) return;
    const t = setTimeout(() => resultsRef.current?.scrollIntoView({ behavior: 'smooth', block: 'start' }), 200);
    return () => clearTimeout(t);
  }, [results]);

  // ─── Keyboard submit ───
  useEffect(() => {
    const handler = (e) => {
      const tag = document.activeElement?.tagName;
      if (tag === 'SELECT') return;
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && !loading && canSubmitRef.current)
        filterRef.current?.();
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [loading]);

  // ═══ RENDER HELPERS ═══
  const Section = ({ title, emoji, open, onToggle, badge, children }) => (
    <div className={`${c.card} border ${c.border} rounded-xl overflow-hidden`}>
      <button onClick={onToggle} className="w-full flex items-center justify-between p-5 text-left hover:opacity-80">
        <div className="flex items-center gap-3">
          <span className="text-lg">{emoji}</span>
          <span className={`text-base font-semibold ${c.text}`}>{title}</span>
          {badge && <span className={`text-xs px-2 py-0.5 rounded-full ${c.badge}`}>{badge}</span>}
        </div>
        <span className={`text-xs ${c.textMuted}`}>{open ? '▲' : '▼'}</span>
      </button>
      {open && <div className={`px-5 pb-5 border-t ${c.border}`}>{children}</div>}
    </div>
  );

  const Pill = ({ active, onClick, children }) => (
    <button onClick={onClick} className={`px-3 py-1.5 rounded-lg border text-xs font-semibold transition-all ${active ? c.pillActive : c.pillInactive}`}>
      {active && <span className="mr-1">✓</span>}{children}
    </button>
  );

  const effortBadge = (e) => {
    if (e === 'quick') return '⚡ Quick';
    if (e === 'moderate') return '🕐 ~30 min';
    return '📋 Involved';
  };

  const renderResults = () => {
    if (!results) return null;
    const hasActions = results.action_required?.length > 0;
    const hasCosts = results.costs_you_money?.length > 0;
    const hasSaves = results.saves_you_money?.length > 0;

    return (
      <div ref={resultsRef} className="space-y-4">
        {/* TL;DR */}
        <div className={`p-5 rounded-2xl border-2 ${hasActions ? c.actionBg : c.tipBg}`}>
          <div className="flex items-center gap-2 mb-2">
            <span className="text-lg">{hasActions ? '⚡' : '✅'}</span>
            <span className={`text-sm font-bold ${hasActions ? c.actionText : c.tipText}`}>
              {results.document_type || 'Document'}
            </span>
            {results.confidence && results.confidence !== 'high' && (
              <span className={`text-xs px-2 py-0.5 rounded-full ${c.badge}`}>⚠️ {results.confidence} confidence</span>
            )}
          </div>
          <p className={`text-sm font-bold ${c.text}`}>{results.tldr}</p>
          {results.confidence_note && <p className={`text-xs ${c.textMuted} mt-2 italic`}>{results.confidence_note}</p>}
        </div>

        {/* Action Required */}
        {hasActions && (
          <div className="space-y-2">
            <p className={`text-xs font-bold ${c.actionText} uppercase`}>⚡ Action Required</p>
            {results.action_required.map((a, idx) => (
              <div key={idx} className={`p-4 rounded-xl border-2 ${c.actionBg}`}>
                <div className="flex items-center justify-between mb-1">
                  <span className={`text-sm font-bold ${c.text}`}>{a.what}</span>
                  {a.effort && <span className={`text-[10px] px-2 py-0.5 rounded-full ${c.badge}`}>{effortBadge(a.effort)}</span>}
                </div>
                {a.deadline && <p className={`text-xs font-bold ${c.actionText} mb-1`}>📅 Deadline: {a.deadline}</p>}
                {a.consequence && <p className={`text-xs ${c.textSecondary} mb-1`}>⚠️ If you don't: {a.consequence}</p>}
                {a.how && <p className={`text-xs ${c.textMuted}`}>📝 How: {a.how}</p>}
              </div>
            ))}
          </div>
        )}

        {/* Costs you money */}
        {hasCosts && (
          <div className="space-y-2">
            <p className={`text-xs font-bold ${c.costText} uppercase`}>💸 Costs You Money</p>
            {results.costs_you_money.map((item, idx) => (
              <div key={idx} className={`p-4 rounded-xl border ${c.costBg}`}>
                <div className="flex items-center justify-between mb-1">
                  <span className={`text-sm font-bold ${c.text}`}>{item.what}</span>
                  {item.amount && <span className={`text-sm font-bold ${c.costText}`}>{item.amount}</span>}
                </div>
                {item.when && <p className={`text-xs ${c.textSecondary}`}>Effective: {item.when}</p>}
                {item.avoidable && <p className={`text-xs ${c.tipText} mt-1`}>💡 {item.avoidable}</p>}
              </div>
            ))}
          </div>
        )}

        {/* Saves you money */}
        {hasSaves && (
          <div className="space-y-2">
            <p className={`text-xs font-bold ${c.saveText} uppercase`}>💚 Saves You Money</p>
            {results.saves_you_money.map((item, idx) => (
              <div key={idx} className={`p-4 rounded-xl border ${c.saveBg}`}>
                <div className="flex items-center justify-between mb-1">
                  <span className={`text-sm font-bold ${c.text}`}>{item.what}</span>
                  {item.amount && <span className={`text-sm font-bold ${c.saveText}`}>{item.amount}</span>}
                </div>
                {item.how_to_claim && <p className={`text-xs ${c.textSecondary}`}>How: {item.how_to_claim}</p>}
                {item.deadline && <p className={`text-xs ${c.saveText} mt-1`}>📅 By: {item.deadline}</p>}
              </div>
            ))}
          </div>
        )}

        {/* Affects you */}
        {results.affects_you?.length > 0 && (
          <Section title="Also Relevant" emoji="📌" open={showAffects} onToggle={() => setShowAffects(p => !p)} badge={String(results.affects_you.length)}>
            <div className="space-y-2 mt-4">
              {results.affects_you.map((item, idx) => (
                <div key={idx} className={`p-3 rounded-lg border ${c.cardAlt} ${c.border}`}>
                  <div className="flex items-center gap-2 mb-1">
                    <span className={`text-xs font-bold px-2 py-0.5 rounded-full ${item.priority === 'high' ? c.badge : c.badge}`}>{item.priority}</span>
                    <span className={`text-sm font-semibold ${c.text}`}>{item.what}</span>
                  </div>
                  <p className={`text-xs ${c.textSecondary}`}>{item.why_it_matters}</p>
                </div>
              ))}
            </div>
          </Section>
        )}

        {/* Doesn't affect you */}
        {results.does_not_affect_you && (
          <div className={`p-4 rounded-xl ${c.inset} border ${c.border}`}>
            <p className={`text-xs font-bold ${c.textMuted} mb-1`}>🔇 Safely ignore</p>
            <p className={`text-sm ${c.text}`}>{results.does_not_affect_you}</p>
          </div>
        )}

        {/* Buried but important */}
        {results.buried_important?.length > 0 && (
          <Section title="Buried but Important" emoji="🔍" open={showBuried} onToggle={() => setShowBuried(p => !p)} badge={String(results.buried_important.length)}>
            <div className="space-y-2 mt-4">
              {results.buried_important.map((item, idx) => (
                <div key={idx} className={`p-3 rounded-lg border ${c.tipBg}`}>
                  <p className={`text-xs font-bold ${c.tipText} mb-1`}>{item.what}</p>
                  <p className={`text-[10px] ${c.textMuted}`}>Found: {item.where}</p>
                  <p className={`text-xs ${c.tipText} mt-1`}>Why it's easy to miss: {item.why_buried}</p>
                </div>
              ))}
            </div>
          </Section>
        )}

        {/* Questions to ask */}
        {results.questions_to_ask?.length > 0 && (
          <Section title="Questions to Ask" emoji="❓" open={showQuestions} onToggle={() => setShowQuestions(p => !p)}>
            <div className="space-y-1 mt-4">
              {results.questions_to_ask.map((q, idx) => (
                <div key={idx} className={`p-2 rounded-lg ${c.inset}`}>
                  <p className={`text-xs ${c.text}`}>"{q}"</p>
                </div>
              ))}
            </div>
          </Section>
        )}

        {/* Consider consulting a professional */}
        {results.consult_professional?.length > 0 && (
          <div className={`p-4 rounded-xl border ${c.infoBg}`}>
            <p className={`text-xs font-bold ${c.infoText} mb-2`}>👨‍⚖️ Consider consulting a professional</p>
            {results.consult_professional.map((item, idx) => (
              <div key={idx} className="mb-2 last:mb-0">
                <p className={`text-xs font-semibold ${c.text}`}>{item.topic}</p>
                <p className={`text-[10px] ${c.textSecondary}`}>{item.why} → Talk to a {item.who}</p>
              </div>
            ))}
          </div>
        )}

      </div>
    );
  };

  const renderHistory = () => {
    if (history.length === 0) return null;
    const formatDate = (iso) => {
      try {
        const d = new Date(iso);
        const diff = Math.floor((new Date() - d) / 86400000);
        return diff === 0 ? 'Today' : diff === 1 ? 'Yesterday' : diff < 7 ? diff + 'd ago' : d.toLocaleDateString('en-US', { month: 'short', day: 'numeric' });
      } catch { return ''; }
    };
    return (
      <div className={`p-4 rounded-xl border ${c.histBg}`}>
        <button onClick={() => setShowHistory(p => !p)} className="w-full flex items-center gap-2 text-left">
          <span>📋</span>
          <span className={`text-sm font-bold ${c.text} flex-1`}>Past Filters</span>
          <span className={`text-xs ${c.textMuted}`}>{history.length}</span>
          <span className={`text-xs ${c.textMuted}`}>{showHistory ? '▲' : '▼'}</span>
        </button>
        {showHistory && (
          <div className="mt-3 space-y-2">
            {history.map(entry => (
              <div key={entry.id} className={`rounded-xl border ${c.histCard} p-3 flex items-center gap-3`}>
                <div className="flex-1 min-w-0">
                  <div className={`text-sm font-semibold ${c.text} truncate`}>{entry.tldr}</div>
                  <div className={`text-xs ${c.textMuted} mt-0.5`}>{formatDate(entry.date)} · {entry.type}</div>
                </div>
                <button onClick={() => { setResults(entry.results); setShowHistory(false); }}
                  className={`px-3 py-1.5 rounded-lg text-xs font-bold ${c.btnSecondary}`}>View</button>
              </div>
            ))}
          </div>
        )}
      </div>
    );
  };

  // ═══ RENDER ═══
  return (
    <div className={`space-y-4 ${c.text}`}>

      {/* ─── Persistent input card ─── */}
      <div className={`${c.card} border ${c.border} rounded-xl shadow-sm`}>
        <div className="px-5 pt-5">
          <div className="pb-3 border-b border-zinc-500">
            <div className="flex items-start justify-between gap-3">
              <div>
                <h2 className={`text-xl font-bold ${c.text}`}>
                  <span className="mr-2">{tool?.icon ?? '🔇'}</span>{tool?.title ?? 'Noise Canceler'}
                </h2>
                <p className={`text-sm ${c.textSecondary}`}>{tool?.tagline ?? 'Paste a long document, tell us your situation — we\'ll extract only what affects you'}</p>
              </div>
              {(results || docText.trim()) && (
                <button onClick={handleReset} className={`${c.btnSecondary} px-3 py-1.5 rounded-lg text-xs font-bold flex-shrink-0`}>
                  ↺ Start Over
                </button>
              )}
            </div>
          </div>
        </div>

        {/* ── Input fields (hidden when results are showing) ── */}
        {!results && (
          <div className="px-5 pb-5 pt-4 space-y-4">

            {/* Document paste */}
            <div>
              <label className={`block text-sm font-medium ${c.labelText} mb-1`}>
                Paste the document <span className={c.required}>*</span>
              </label>
              <p className={`text-xs ${c.textMuted} mb-2`}>Insurance EOB, HOA notice, policy update, lease amendment — whatever wall of text you received.</p>
              <textarea
                value={docText}
                onChange={e => setDocText(e.target.value)}
                placeholder="Paste the full text here..."
                rows={8}
                className={`w-full p-4 border rounded-xl text-sm font-mono resize-none ${c.input}`}
              />
              {docText.length > 0 && <p className={`text-xs ${c.textMuted} mt-1`}>{docText.length.toLocaleString()} characters</p>}
            </div>

            {/* Document type */}
            <div>
              <label className={`block text-xs font-bold ${c.labelText} uppercase tracking-wide mb-2`}>📎 Document type</label>
              <div className="flex flex-wrap gap-1.5">
                {DOC_TYPES.map(dt => (
                  <Pill key={dt.value} active={documentType === dt.value} onClick={() => setDocumentType(dt.value)}>
                    {dt.label}
                  </Pill>
                ))}
              </div>
            </div>

            {/* Your situation */}
            <div>
              <label className={`block text-sm font-medium ${c.labelText} mb-1`}>
                Your situation <span className={c.required}>*</span>
              </label>
              <p className={`text-xs ${c.textMuted} mb-2`}>The more specific, the better we can filter.</p>
              <textarea
                value={mySituation}
                onChange={e => setMySituation(e.target.value)}
                placeholder="e.g. 'Renter, no kids, have a dog, work from home' or 'Single, 28, on the basic health plan, no dependents'"
                rows={2}
                className={`w-full p-3 border rounded-xl text-sm resize-none ${c.input}`}
              />
            </div>

            {/* Specific concerns */}
            <div>
              <label className={`block text-sm font-medium ${c.labelText} mb-1`}>Anything specific you're worried about? <span className={`text-xs font-normal ${c.textMuted}`}>(optional)</span></label>
              <input
                type="text"
                value={concerns}
                onChange={e => setConcerns(e.target.value)}
                placeholder="e.g. 'Did they raise the rent?', 'Am I covered for this procedure?'"
                className={`w-full px-4 py-2.5 rounded-xl border text-sm ${c.input}`}
              />
            </div>

            {/* Submit */}
            <button
              onClick={filter}
              disabled={loading || !docText.trim() || !mySituation.trim()}
              className={`w-full ${c.btnPrimary} disabled:opacity-40 font-bold py-3 rounded-lg flex items-center justify-center gap-2 min-h-[48px]`}
            >
              {loading
                ? <><span className="inline-block animate-spin">{tool?.icon ?? '⚙️'}</span> Filtering for you...</>
                : <><span className="mr-1">{tool?.icon ?? '🔇'}</span> Filter for What Matters</>}
            </button>

            {/* Pre-result cross-ref */}
            <p className={`text-xs ${c.textMuted}`}>Dealing with a lease? <a href="/LeaseTrapDetector" className={linkStyle}>📄 Lease Trap Detector</a> goes even deeper on predatory clauses.</p>

          </div>
        )}
      </div>

      {/* ─── Results ─── */}
      {results && renderResults()}

      {/* ─── Error ─── */}
      {error && (
        <div className={`${c.warningBox} border rounded-xl p-4 flex items-start gap-3`}>
          <span className={c.warningTxt}>⚠️</span>
          <p className={`text-sm ${c.warningTxt}`}>{error}</p>
        </div>
      )}

      {/* ─── Post-result cross-refs ─── */}
      {results && (
        <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
          <p className={`text-[10px] font-bold ${c.textMuted} uppercase mb-2`}>🔗 Related tools</p>
          <div className="flex flex-wrap gap-3">
            <a href="/JargonAssassin" className={`text-xs ${linkStyle}`}>🗡️ Jargon Assassin</a>
            <a href="/LeaseTrapDetector" className={`text-xs ${linkStyle}`}>📄 Lease Trap Detector</a>
          </div>
        </div>
      )}

      {/* ─── History ─── */}
      {renderHistory()}

    </div>
  );
};

