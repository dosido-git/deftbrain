const getSoloFeedback = (correct, total, streak, t) => {
  if (streak >= 5) return { emoji: '🔥', text: t('tfw_solo_unstoppable') };
  if (streak >= 3) return { emoji: '🔥', text: t('tfw_solo_on_fire') };
  if (total < 3) return { emoji: '🎯', text: t('tfw_solo_lets_go') };
  const pct = Math.round((correct / total) * 100);
  if (pct >= 90) return { emoji: '🏆', text: t('tfw_solo_genius') };
  if (pct >= 70) return { emoji: '💪', text: t('tfw_solo_strong') };
  if (pct >= 50) return { emoji: '👍', text: t('tfw_solo_solid') };
  if (pct >= 30) return { emoji: '🤔', text: t('tfw_solo_hang_in') };
  return { emoji: '😅', text: t('tfw_solo_rough') };
};

function getTimeAgo(timestamp, t) {
  const diff = Date.now() - new Date(timestamp).getTime();
  const mins = Math.floor(diff / 60000);
  if (mins < 1) return t('tfw_time_just_now');
  if (mins < 60) return t('tfw_time_m_ago', { mins });
  const hrs = Math.floor(mins / 60);
  if (hrs < 24) return t('tfw_time_h_ago', { hrs });
  const days = Math.floor(hrs / 24);
  if (days < 7) return t('tfw_time_d_ago', { days });
  return new Date(timestamp).toLocaleDateString();
}

