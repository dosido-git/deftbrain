import React, { useState, useEffect, useRef, useCallback } from 'react';
import { useTheme } from '../hooks/useTheme';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { usePersistentState } from '../hooks/usePersistentState';
import { CopyBtn } from '../components/ActionButtons';
import { useRegisterActions } from '../components/ActionBarContext';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const EXAMPLE_QUESTIONS = [
  { q: 'Is an MBA worth it at 35?', ctx: 'Mid-career in marketing, considering switching to strategy consulting' },
  { q: 'Should I move cities for a relationship?', ctx: 'Together 2 years, long-distance for 6 months, they have roots there' },
  { q: 'Should I go freelance or stay employed?', ctx: '7 years at the same company, have 3 clients who\'d hire me' },
  { q: 'Should I have a second child?', ctx: 'First is 3, partner is on the fence, finances are stable but time is not' },
  { q: 'Should I confront my friend about this?', ctx: 'They said something that really hurt, they don\'t seem to know it' },
];

// Maps archetype emoji → c object key (approved color families only)
// Five distinct voice colors are a core function of this tool — documented exception
const ARCHETYPE_KEY = {
  '🔧': 'archetypePragmatist',
  '🎲': 'archetypeRisk',
  '🪞': 'archetypeRegret',
  '🕰️': 'archetypeWait',
  '🔄': 'archetypeContrarian',
};

const CrowdWisdom = ({ tool }) => {
  const { isDark } = useTheme();
  const { callToolEndpoint, loading } = useClaudeAPI();


  const c = {
    card:                isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:             isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:               isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20'
                                : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:                isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary:       isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:           isDark ? 'text-zinc-500' : 'text-gray-400',
    labelText:           isDark ? 'text-zinc-200' : 'text-gray-700',
    accentTxt:           isDark ? 'text-cyan-400' : 'text-cyan-600',
    btnPrimary:          isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white'
                                : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:        isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200'
                                : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:              isDark ? 'border-zinc-700' : 'border-gray-200',
    success:             isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200'
                                : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:             isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200'
                                : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:              isDark ? 'bg-red-900/20 border-red-700 text-red-200'
                                : 'bg-red-50 border-red-200 text-red-800',
    infoBox:             isDark ? 'bg-sky-900/20 border-sky-700 text-sky-200'
                                : 'bg-sky-50 border-sky-200 text-sky-800',
    successBox:          isDark ? 'bg-emerald-900/20 border-emerald-700' : 'bg-emerald-50 border-emerald-300',
    successTxt:          isDark ? 'text-emerald-300' : 'text-emerald-800',
    warningBox:          isDark ? 'bg-amber-900/20 border-amber-700' : 'bg-amber-50 border-amber-300',
    warningTxt:          isDark ? 'text-amber-300' : 'text-amber-800',
    pillActive:          isDark ? 'border-cyan-500 bg-cyan-900/30 text-cyan-200'
                                : 'border-cyan-600 bg-cyan-100 text-cyan-900',
    pillInactive:        isDark ? 'border-zinc-600 text-zinc-400 hover:border-zinc-500'
                                : 'border-gray-300 text-gray-500 hover:border-gray-400',
    required:            isDark ? 'text-amber-400' : 'text-amber-500',
    // Bespoke — five distinct archetype voice colors (documented exception to color family limit)
    archetypePragmatist: isDark ? 'bg-zinc-800 border-zinc-600' : 'bg-slate-50 border-slate-300',
    archetypeRisk:       isDark ? 'bg-amber-900/20 border-amber-700' : 'bg-amber-50 border-amber-300',
    archetypeRegret:     isDark ? 'bg-rose-900/20 border-rose-700' : 'bg-rose-50 border-rose-200',
    archetypeWait:       isDark ? 'bg-sky-900/20 border-sky-700' : 'bg-sky-50 border-sky-200',
    archetypeContrarian: isDark ? 'bg-emerald-900/20 border-emerald-700' : 'bg-emerald-50 border-emerald-200',
    quoteInner:          isDark ? 'bg-zinc-700/60' : 'bg-slate-100',
    tensionBg:           isDark ? 'bg-amber-900/20 border-amber-700' : 'bg-amber-50 border-amber-300',
    tensionLabel:        isDark ? 'text-amber-400' : 'text-amber-700',
  };
  c.textMuteded = c.textMuted;
  c.label = c.labelText;

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';


  // ─── State ───
  const [error, setError] = useState('');
  const [expanded, setExpanded] = useState({});

  const [question, setQuestion] = usePersistentState('crowd-wisdom-question', '');
  const [context, setContext] = usePersistentState('crowd-wisdom-context', '');
  const [results, setResults] = usePersistentState('crowd-wisdom-results', null);
  const [history, setHistory] = usePersistentState('crowd-wisdom-history', []);

  // ─── Refs ───
  const resultsRef = useRef(null);
  const handleRef = useRef(null);
  const canSubmitRef = useRef(false);

  // Scroll to results when they load
  useEffect(() => {
    if (results && resultsRef.current) {
      resultsRef.current.scrollIntoView({ behavior: 'smooth', block: 'start' });
    }
  }, [results]);

  // Keyboard: Cmd/Ctrl+Enter to submit
  useEffect(() => {
    const handler = (e) => {
      if ((e.metaKey || e.ctrlKey) && e.key === 'Enter' && canSubmitRef.current)
        handleRef.current?.();
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
  }, []);

  const toggle = (key) => setExpanded(p => ({ ...p, [key]: !p[key] }));

  const handleSubmit = async () => {
    if (!question.trim()) return;
    setError(''); setResults(null);
    try {
      const data = await callToolEndpoint('crowd-wisdom', {
        question: question.trim(),
        context: context.trim() || undefined,
      });
      setResults(data);
      setHistory(p => [{
        preview: question.trim().slice(0, 40),
        question: question.trim(),
        context: context.trim(),
        result: data,
        date: new Date().toISOString(),
      }, ...p].slice(0, 6));
    } catch (e) {
      setError(e.message || 'Failed to gather perspectives.');
    }
  };

  const handleExample = () => {
    const ex = EXAMPLE_QUESTIONS[Math.floor(Math.random() * EXAMPLE_QUESTIONS.length)];
    setQuestion(ex.q); setContext(ex.ctx); setResults(null); setError('');
  };

  const handleReset = () => {
    setResults(null); setQuestion(''); setContext(''); setExpanded({}); setError('');
  };

  const buildText = () => {
    if (!results) return '';
    let t = `👥 CROWD WISDOM\n\n"${question}"\n\n`;
    if (results.question_reframed) t += `The deeper question: ${results.question_reframed}\n\n`;
    results.voices?.forEach(v => {
      t += `${v.emoji} ${v.archetype}\n${v.what_they_say}\n\nWhat they uniquely see: ${v.the_truth_only_they_see}\n\n`;
    });
    if (results.the_tension) t += `━━ THE TENSION ━━\n${results.the_tension}\n\n`;
    if (results.the_question_nobody_asked) t += `The question nobody asked: ${results.the_question_nobody_asked}\n`;
    return t + BRAND;
  };

  const buildFullText = useCallback(() => {
    if (!results) return '';
    return buildText();
  }, [results, question]);

  useRegisterActions(buildFullText(), tool?.title || 'Crowd Wisdom');

  handleRef.current = handleSubmit;
  canSubmitRef.current = !loading && !!question.trim();

  return (
    <div className={`space-y-4 ${c.text}`}>

      {/* Header card */}
      <div className={`${c.card} border ${c.border} rounded-xl shadow-sm`}>
        <div className="px-5 pt-5">
          <div className="pb-3 border-b border-zinc-500">
            <div className="flex items-start justify-between gap-3">
              <div>
                <h2 className={`text-xl font-bold ${c.text}`}>
                  <span className="mr-2">{tool?.icon ?? '👥'}</span>{tool?.title ?? 'Crowd Wisdom'}
                </h2>
                <p className={`text-sm ${c.textSecondary}`}>{tool?.tagline ?? 'Five real perspectives on the choice you can\'t stop thinking about'}</p>
              </div>
              {(results || question.trim()) && (
                <button onClick={handleReset} className={`${c.btnSecondary} px-3 py-1.5 rounded-lg text-xs font-bold flex-shrink-0`}>
                  ↺ Start Over
                </button>
              )}
            </div>
          </div>
        </div>
      </div>

      {/* Pre-result cross-ref */}
      {!results && (
        <p className={`text-xs ${c.textMuted}`}>
          Want to stress-test the belief driving this first?{' '}
          <a href="/BeliefStressTest" className={linkStyle}>🧪 Belief Stress Test</a>
        </p>
      )}

      {/* Input card */}
      <div className={`${c.card} border ${c.border} rounded-xl shadow-sm p-5 space-y-4`}>
        <div className="pb-3 border-b border-zinc-500">
          <h3 className={`font-semibold text-sm ${c.text}`}>{tool?.icon ?? '👥'} Your question</h3>
          <p className={`text-xs ${c.textSecondary}`}>Five people who've lived it. Five different answers.</p>
        </div>

        <div>
          <label className={`block text-sm font-semibold mb-1.5 ${c.labelText}`}>
            Question <span className={c.required}>*</span>
          </label>
          <textarea
            value={question}
            onChange={e => setQuestion(e.target.value)}
            placeholder="Should I quit my job? Move cities? Say something? Start over?"
            rows={3}
            maxLength={500}
            className={`w-full px-4 py-3 rounded-xl border text-sm resize-none ${c.input}`}
          />
        </div>

        <div>
          <label className={`block text-sm font-semibold mb-1.5 ${c.labelText}`}>
            Context <span className={`font-normal ${c.textMuted}`}>(optional — makes the voices sharper)</span>
          </label>
          <textarea
            value={context}
            onChange={e => setContext(e.target.value)}
            placeholder="A sentence or two about your specific situation…"
            rows={2}
            maxLength={300}
            className={`w-full px-4 py-3 rounded-xl border text-sm resize-none ${c.input}`}
          />
        </div>

        {error && (
          <div className={`p-3 rounded-xl border text-sm ${c.danger}`}>
            <span className="mr-1">⚠️</span>{error}
          </div>
        )}

        <div className="flex gap-2">
          <button
            onClick={handleSubmit}
            disabled={loading || !question.trim()}
            className={`w-full py-3 rounded-xl font-bold disabled:opacity-40 ${c.btnPrimary}`}
          >
            {loading
              ? <><span className="animate-spin inline-block mr-2">{tool?.icon ?? '👥'}</span>Gathering voices…</>
              : <><span className="mr-1">{tool?.icon ?? '👥'}</span>Ask the Crowd</>}
          </button>
          <button
            onClick={handleExample}
            disabled={loading}
            className={`px-4 py-3 rounded-xl text-sm font-medium flex-shrink-0 ${c.btnSecondary}`}
          >
            Example
          </button>
        </div>
        <p className={`text-xs text-center ${c.textMuted}`}>Cmd+Enter to submit</p>
      </div>

      {/* History panel */}
      {history.length > 0 && !results && (
        <div className={`rounded-xl border p-4 space-y-2 ${c.card} ${c.border}`}>
          <p className={`text-xs font-bold uppercase tracking-wide ${c.textMuted}`}>Recent questions</p>
          {history.map((h, i) => (
            <button
              key={i}
              onClick={() => { setQuestion(h.question); setContext(h.context); setResults(h.result); setExpanded({}); setError(''); }}
              className={`w-full text-left px-3 py-2 rounded-lg text-xs ${c.cardAlt} ${c.textSecondary} hover:opacity-80`}
            >
              {h.preview}{h.preview.length >= 40 ? '…' : ''}
            </button>
          ))}
        </div>
      )}

      {/* Results */}
      {results && (
        <div ref={resultsRef} className="space-y-4">

          {/* Question header */}
          <div className={`rounded-xl border p-5 ${c.card} ${c.border}`}>
            <p className={`text-xs font-bold uppercase tracking-wider mb-1 ${c.textMuted}`}>The question</p>
            <p className={`text-base font-semibold ${c.text}`}>"{question}"</p>
            {results.question_reframed && (
              <p className={`text-sm mt-2 italic ${c.textSecondary}`}>
                The deeper question: {results.question_reframed}
              </p>
            )}
          </div>

          {/* Five voices */}
          {results.voices?.map((voice, i) => {
            const isOpen = expanded[i];
            const cardKey = ARCHETYPE_KEY[voice.emoji] || 'archetypeContrarian';
            return (
              <div key={i} className={`rounded-xl overflow-hidden border ${c[cardKey]}`}>
                <button onClick={() => toggle(i)} className="w-full text-left p-5">
                  <div className="flex items-start justify-between gap-3">
                    <div className="flex items-center gap-3">
                      <span className="text-2xl">{voice.emoji}</span>
                      <div>
                        <p className={`font-bold ${c.text}`}>{voice.archetype}</p>
                        <p className={`text-xs mt-0.5 ${c.textMuted}`}>{voice.profile}</p>
                      </div>
                    </div>
                    <span className={`flex-shrink-0 text-sm mt-1 ${c.textMuted}`}>{isOpen ? '▲' : '▼'}</span>
                  </div>
                  {!isOpen && (
                    <p className={`text-sm mt-3 leading-relaxed line-clamp-2 ${c.textSecondary}`}>
                      {voice.what_they_say}
                    </p>
                  )}
                </button>
                {isOpen && (
                  <div className={`px-5 pb-5 space-y-3 border-t ${c.border}`}>
                    <div className="pt-3">
                      <p className={`text-xs font-bold uppercase tracking-wider mb-1 ${c.textMuted}`}>What they say</p>
                      <p className={`text-sm leading-relaxed ${c.text}`}>{voice.what_they_say}</p>
                    </div>
                    <div className={`p-3 rounded-xl ${c.quoteInner}`}>
                      <p className={`text-xs font-bold uppercase tracking-wider mb-1 ${c.textMuted}`}>🔦 What only they can see</p>
                      <p className={`text-sm ${c.textSecondary}`}>{voice.the_truth_only_they_see}</p>
                    </div>
                    <div className={`text-xs ${c.textMuted} italic`}>
                      <span className="font-semibold not-italic">Blind spot:</span> {voice.the_thing_they_might_miss}
                    </div>
                    <CopyBtn
                      content={`${voice.emoji} ${voice.archetype}\n\n${voice.what_they_say}\n\n"${voice.the_truth_only_they_see}"${BRAND}`}
                      label="Copy this voice"
                    />
                  </div>
                )}
              </div>
            );
          })}

          {/* Tension + closing question */}
          <div className="grid grid-cols-1 sm:grid-cols-2 gap-4">
            {results.the_tension && (
              <div className={`rounded-xl border p-4 ${c.tensionBg}`}>
                <p className={`text-xs font-bold uppercase tracking-wider mb-2 ${c.tensionLabel}`}>⚡ The Real Tension</p>
                <p className={`text-sm ${c.textSecondary}`}>{results.the_tension}</p>
              </div>
            )}
            {results.the_question_nobody_asked && (
              <div className={`rounded-xl border p-4 ${c.card} ${c.border}`}>
                <p className={`text-xs font-bold uppercase tracking-wider mb-2 ${c.textMuted}`}>❓ The question nobody asked</p>
                <p className={`text-sm italic ${c.textSecondary}`}>{results.the_question_nobody_asked}</p>
              </div>
            )}
          </div>

          {/* AI disclaimer */}
          <p className={`text-xs text-center italic ${c.textMuted}`}>
            AI-generated perspectives. Voices are simulated — not real people. Use as a thinking prompt, not as advice.
          </p>

          {/* Post-result cross-refs */}
          <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
            <p className={`text-[10px] font-bold ${c.textMuted} uppercase mb-2`}>🔗 Related tools</p>
            <div className="flex flex-wrap gap-3">
              <a href="/DecisionCoach" className={`text-xs ${linkStyle}`}>🎯 Decision Coach</a>
              <a href="/BeliefStressTest" className={`text-xs ${linkStyle}`}>🧪 Belief Stress Test</a>
            </div>
          </div>
        </div>
      )}
    </div>
  );
};

CrowdWisdom.displayName = 'CrowdWisdom';
export default CrowdWisdom;
