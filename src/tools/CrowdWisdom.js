import React, { useState } from 'react';
import { useTheme } from '../hooks/useTheme';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { CopyBtn, ActionBar } from '../components/ActionButtons';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const EXAMPLE_QUESTIONS = [
  { q: 'Is an MBA worth it at 35?', ctx: 'Mid-career in marketing, considering switching to strategy consulting' },
  { q: 'Should I move cities for a relationship?', ctx: 'Together 2 years, long-distance for 6 months, they have roots there' },
  { q: 'Should I go freelance or stay employed?', ctx: '7 years at the same company, have 3 clients who\'d hire me' },
  { q: 'Should I have a second child?', ctx: 'First is 3, partner is on the fence, finances are stable but time is not' },
  { q: 'Should I confront my friend about this?', ctx: 'They said something that really hurt, they don\'t seem to know it' },
];

const ARCHETYPE_BG = {
  '🔧': { light: 'bg-[#e8eef5] border-[#b0c4d8]', dark: 'bg-[#2c4a6e]/15 border-[#2c4a6e]/30', label: 'light:text-[#1e3a58] dark:text-[#a8b9ce]' },
  '🎲': { light: 'bg-[#fdf3e4] border-[#e0c49a]', dark: 'bg-[#c8872e]/15 border-[#c8872e]/30', label: 'light:text-[#93541f] dark:text-[#d9a04e]' },
  '🪞': { light: 'bg-[#fceae8] border-[#d4908a]', dark: 'bg-[#b54a3f]/15 border-[#b54a3f]/30', label: 'light:text-[#8a3028] dark:text-[#e8a9a3]' },
  '⏳': { light: 'bg-[#e8f0e8] border-[#a8c4a8]', dark: 'bg-[#5a8a5c]/15 border-[#5a8a5c]/30', label: 'light:text-[#3d6e3f] dark:text-[#8aba8c]' },
  '🔄': { light: 'bg-[#f3efe8] border-[#d5cab8]', dark: 'bg-[#3d3630]/50 border-[#3d3630]', label: 'light:text-[#5e5042] dark:text-[#c8c3b9]' },
};

const CrowdWisdom = () => {
  const { theme } = useTheme();
  const isDark = theme === 'dark';
  const { callToolEndpoint, loading } = useClaudeAPI();

  const c = {
    pageBg:       isDark ? 'bg-[#1a1816]'    : 'bg-[#faf8f5]',
    card:         isDark ? 'bg-[#2a2623]'     : 'bg-white',
    cardAlt:      isDark ? 'bg-[#332e2a]'     : 'bg-[#faf8f5]',
    cardBorder:   isDark ? 'border-[#3d3630]' : 'border-[#e8e1d5]',
    inputBg:      isDark ? 'bg-[#1a1816]'     : 'bg-[#faf8f5]',
    inputBorder:  isDark ? 'border-[#3d3630]' : 'border-[#d5cab8]',
    inputFocus:   'focus:outline-none focus:ring-2 focus:ring-[#2c4a6e]/20 focus:border-[#4a6a8a]',
    text:          isDark ? 'text-[#f0eeea]'  : 'text-[#3d3935]',
    textSecondary: isDark ? 'text-[#c8c3b9]'  : 'text-[#5a544a]',
    textMuted:     isDark ? 'text-[#8a8275]'  : 'text-[#8a8275]',
    heading:       isDark ? 'text-[#f3efe8]'  : 'text-[#1e2a3a]',
    btnPrimary:    isDark ? 'bg-[#2c4a6e] hover:bg-[#4a6a8a] text-white' : 'bg-[#2c4a6e] hover:bg-[#1e3a58] text-white',
    btnSecondary:  isDark ? 'bg-[#332e2a] hover:bg-[#3d3630] text-[#c8c3b9] border border-[#3d3630]' : 'bg-[#f3efe8] hover:bg-[#e8e1d5] text-[#5e5042] border border-[#d5cab8]',
    divider:       isDark ? 'border-[#3d3630]' : 'border-[#e8e1d5]',
    error:         isDark ? 'bg-[#b54a3f]/15 border-[#b54a3f]/50 text-[#e8a9a3]' : 'bg-[#fceae8] border-[#d4908a] text-[#8a3028]',
    tensionBg:     isDark ? 'bg-[#2c4a6e]/15 border-[#2c4a6e]/30' : 'bg-[#e8eef5] border-[#b0c4d8]',
  };

  const [question, setQuestion] = useState('');
  const [context, setContext] = useState('');
  const [results, setResults] = useState(null);
  const [error, setError] = useState('');
  const [expanded, setExpanded] = useState({});

  const toggle = (key) => setExpanded(p => ({ ...p, [key]: !p[key] }));

  const handleSubmit = async () => {
    if (!question.trim()) return;
    setError(''); setResults(null);
    try {
      const data = await callToolEndpoint('crowd-wisdom', { question: question.trim(), context: context.trim() || undefined });
      setResults(data);
    } catch (e) { setError(e.message || 'Failed to gather perspectives.'); }
  };

  const handleExample = () => {
    const ex = EXAMPLE_QUESTIONS[Math.floor(Math.random() * EXAMPLE_QUESTIONS.length)];
    setQuestion(ex.q); setContext(ex.ctx); setResults(null);
  };

  const buildText = () => {
    if (!results) return '';
    let t = `👥 CROWD WISDOM\n\n"${question}"\n\n`;
    if (results.question_reframed) t += `The deeper question: ${results.question_reframed}\n\n`;
    results.voices?.forEach(v => {
      t += `${v.emoji} ${v.archetype}\n${v.what_they_say}\n\nWhat they uniquely see: ${v.the_truth_only_they_see}\n\n`;
    });
    if (results.the_tension) t += `━━ THE TENSION ━━\n${results.the_tension}\n\n`;
    if (results.the_question_nobody_asked) t += `The question nobody asked: ${results.the_question_nobody_asked}\n`;
    return t + BRAND;
  };

  return (
    <div className={`min-h-screen py-8 px-4 ${c.pageBg}`}>
      <div className="max-w-2xl mx-auto">

        {/* Header */}
        <div className="text-center mb-8">
          <div className="text-5xl mb-3">👥</div>
          <h1 className={`text-3xl font-black tracking-tight mb-2 ${c.heading}`}>Crowd Wisdom</h1>
          <p className={`text-sm ${c.textMuted}`}>Five people who've lived your question. Five different answers.</p>
        </div>

        {/* Input */}
        {!results && (
          <div className={`rounded-2xl border p-6 shadow-sm space-y-4 ${c.card} ${c.cardBorder}`}>
            <div>
              <label className={`block text-sm font-semibold mb-1.5 ${c.text}`}>Your question <span className="text-red-400">*</span></label>
              <textarea value={question} onChange={e => setQuestion(e.target.value)}
                placeholder="Ask a question that needs experience, not just facts…"
                rows={2} maxLength={300}
                className={`w-full px-4 py-3 rounded-xl border text-sm resize-none ${c.inputBg} ${c.inputBorder} ${c.inputFocus} ${c.text}`} />
            </div>
            <div>
              <label className={`block text-sm font-semibold mb-1.5 ${c.text}`}>Your context <span className={`font-normal ${c.textMuted}`}>(optional — makes the voices sharper)</span></label>
              <textarea value={context} onChange={e => setContext(e.target.value)}
                placeholder="A sentence or two about your specific situation…"
                rows={2} maxLength={300}
                className={`w-full px-4 py-3 rounded-xl border text-sm resize-none ${c.inputBg} ${c.inputBorder} ${c.inputFocus} ${c.text}`} />
            </div>
            {error && <div className={`p-3 rounded-xl border text-sm ${c.error}`}><span className="mr-1">⚠️</span>{error}</div>}
            <div className="flex gap-2">
              <button onClick={handleSubmit} disabled={loading || !question.trim()}
                className={`flex-1 py-3 rounded-xl font-bold disabled:opacity-40 ${c.btnPrimary}`}>
                {loading ? <><span className="animate-spin inline-block mr-2">⏳</span>Gathering voices…</> : '👥 Ask the Crowd'}
              </button>
              <button onClick={handleExample} disabled={loading}
                className={`px-4 py-3 rounded-xl text-sm font-medium ${c.btnSecondary}`}>
                Example
              </button>
            </div>
          </div>
        )}

        {/* Results */}
        {results && (
          <div className="space-y-4">
            {/* Question header */}
            <div className={`rounded-2xl border p-5 ${c.card} ${c.cardBorder}`}>
              <p className={`text-xs font-bold uppercase tracking-wider mb-1 ${c.textMuted}`}>The question</p>
              <p className={`text-base font-semibold ${c.text}`}>"{question}"</p>
              {results.question_reframed && (
                <p className={`text-sm mt-2 italic ${c.textSecondary}`}>The deeper question: {results.question_reframed}</p>
              )}
            </div>

            {/* Five voices */}
            {results.voices?.map((voice, i) => {
              const isOpen = expanded[i];
              const bg = ARCHETYPE_BG[voice.emoji] || ARCHETYPE_BG['🔄'];
              const cardClass = isDark
                ? `${bg.dark} border`
                : `${bg.light} border`;

              return (
                <div key={i} className={`rounded-2xl overflow-hidden ${cardClass}`}>
                  <button onClick={() => toggle(i)} className="w-full text-left p-5">
                    <div className="flex items-start justify-between gap-3">
                      <div className="flex items-center gap-3">
                        <span className="text-2xl">{voice.emoji}</span>
                        <div>
                          <p className={`font-bold ${c.text}`}>{voice.archetype}</p>
                          <p className={`text-xs mt-0.5 ${c.textMuted}`}>{voice.profile}</p>
                        </div>
                      </div>
                      <span className={`flex-shrink-0 text-sm mt-1 ${c.textMuted}`}>{isOpen ? '▲' : '▼'}</span>
                    </div>
                    {!isOpen && (
                      <p className={`text-sm mt-3 leading-relaxed line-clamp-2 ${c.textSecondary}`}>{voice.what_they_say}</p>
                    )}
                  </button>

                  {isOpen && (
                    <div className={`px-5 pb-5 space-y-3 border-t ${c.divider}`}>
                      <div className="pt-3">
                        <p className={`text-xs font-bold uppercase tracking-wider mb-1 ${c.textMuted}`}>What they say</p>
                        <p className={`text-sm leading-relaxed ${c.text}`}>{voice.what_they_say}</p>
                      </div>
                      <div className={`p-3 rounded-xl ${isDark ? 'bg-[#1a1816]/60' : 'bg-white/70'}`}>
                        <p className={`text-xs font-bold uppercase tracking-wider mb-1 ${c.textMuted}`}>🔦 What only they can see</p>
                        <p className={`text-sm ${c.textSecondary}`}>{voice.the_truth_only_they_see}</p>
                      </div>
                      <div className={`text-xs ${c.textMuted} italic`}>
                        <span className="font-semibold not-italic">Blind spot:</span> {voice.the_thing_they_might_miss}
                      </div>
                      <CopyBtn content={`${voice.emoji} ${voice.archetype}\n\n${voice.what_they_say}\n\n"${voice.the_truth_only_they_see}"${BRAND}`} label="Copy this voice" />
                    </div>
                  )}
                </div>
              );
            })}

            {/* Tension + closing question */}
            <div className="grid grid-cols-1 sm:grid-cols-2 gap-4">
              {results.the_tension && (
                <div className={`rounded-2xl border p-4 ${c.tensionBg}`}>
                  <p className={`text-xs font-bold uppercase tracking-wider mb-2 ${isDark ? 'text-[#a8b9ce]' : 'text-[#2c4a6e]'}`}>⚡ The Real Tension</p>
                  <p className={`text-sm ${c.textSecondary}`}>{results.the_tension}</p>
                </div>
              )}
              {results.the_question_nobody_asked && (
                <div className={`rounded-2xl border p-4 ${c.card} ${c.cardBorder}`}>
                  <p className={`text-xs font-bold uppercase tracking-wider mb-2 ${c.textMuted}`}>❓ The question nobody asked</p>
                  <p className={`text-sm italic ${c.textSecondary}`}>{results.the_question_nobody_asked}</p>
                </div>
              )}
            </div>

            {/* Actions */}
            <div className="flex items-center gap-3 flex-wrap">
              <ActionBar content={buildText()} title="Crowd Wisdom" />
              <button onClick={() => { setResults(null); setQuestion(''); setContext(''); setExpanded({}); }}
                className={`px-4 py-2 rounded-lg text-sm font-medium ${c.btnSecondary}`}>
                🔄 New Question
              </button>
            </div>

            {/* Cross-references */}
            <div className={`rounded-xl border p-4 ${c.cardAlt} ${c.cardBorder}`}>
              <p className={`text-xs font-semibold uppercase tracking-wide mb-3 ${c.textMuted}`}>Related tools</p>
              <div className="flex flex-wrap gap-2">
                {[
                  { id: 'EgoKiller', icon: '🪦', label: 'Challenge what you believe' },
                  { id: 'PreMortem', icon: '💀', label: 'Write your plan\'s obituary' },
                  { id: 'BeliefStressTest', icon: '🧪', label: 'Test the belief driving this' },
                ].map(r => (
                  <a key={r.id} href={`/tool/${r.id}`} target="_blank" rel="noopener noreferrer"
                    className={`px-3 py-1.5 rounded-lg text-xs font-medium transition-colors ${c.btnSecondary}`}>
                    {r.icon} {r.label}
                  </a>
                ))}
              </div>
            </div>
          </div>
        )}
      </div>
    </div>
  );
};

CrowdWisdom.displayName = 'CrowdWisdom';
export default CrowdWisdom;
