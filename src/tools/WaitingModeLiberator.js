// ─── Parse time input ───
// Named time aliases
const NAMED_TIMES = {
  'midnight':   [0,  0],
  'noon':       [12, 0],
  'midday':     [12, 0],
  'lunch':      [12, 0],
  'morning':    [9,  0],
  'eod':        [17, 0],
  'end of day': [17, 0],
  'evening':    [18, 0],
  'tonight':    [18, 0],
  'night':      [20, 0],
};

// dayOffset: 0 = today, 1 = tomorrow, 2 = day after, etc.
const parseTimeInput = (input, dayOffset = 0) => {
  if (!input?.trim()) return null;
  const clean = input.trim().toLowerCase().replace(/\./g, '');

  // Named time lookup
  let hours, minutes;
  if (NAMED_TIMES[clean]) {
    [hours, minutes] = NAMED_TIMES[clean];
  } else {
    const match = clean.match(/^(\d{1,2})(?::(\d{2}))?\s*(am|pm)?$/);
    if (!match) return null;
    hours = parseInt(match[1]);
    minutes = parseInt(match[2] || '0');
    const ampm = match[3];
    if (ampm === 'pm' && hours < 12) hours += 12;
    if (ampm === 'am' && hours === 12) hours = 0;
    if (!ampm && hours < 7) hours += 12;
  } const d = new Date();
  d.setDate(d.getDate() + (dayOffset || 0));
  d.setHours(hours, minutes, 0, 0);
  return d;
};

const formatTimeShort = (date) => {
  if (!date) return '';
  return date.toLocaleTimeString([], { hour: 'numeric', minute: '2-digit' });
};

