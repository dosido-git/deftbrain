    setEvents([]); setEnergy(3); setUserTasks(''); setAnxietyBefore(5); setError('');
    setDraftName(''); setDraftTime(''); setDraftType(''); setDraftCustomType(''); setDraftDayOffset(0); setExpandedEventId(null); setContextOpen(false);
