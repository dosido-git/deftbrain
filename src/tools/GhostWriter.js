import React, { useState, useRef, useCallback, useEffect } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { usePersistentState } from '../hooks/usePersistentState';
import { useRegisterActions } from '../components/ActionBarContext';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

// ════════════════════════════════════════════════════════════
// CONSTANTS
// ════════════════════════════════════════════════════════════
const LETTER_TYPES = [
  { value: 'job',        label: '💼 Job recommendation' },
  { value: 'grad-school',label: '🎓 Graduate school' },
  { value: 'linkedin',   label: '💬 LinkedIn recommendation' },
  { value: 'scholarship',label: '🏆 Scholarship' },
  { value: 'rental',     label: '🏠 Rental/housing reference' },
  { value: 'character',  label: '🤝 Character reference' },
  { value: 'other',      label: '📝 Other' },
];

const FORMALITY_OPTIONS = [
  { value: 'casual',       label: 'Casual (LinkedIn, brief reference)' },
  { value: 'professional', label: 'Professional (job rec, standard)' },
  { value: 'formal',       label: 'Formal (grad school, legal, scholarship)' },
];

const QUALITY_OPTIONS = [
  'Leadership', 'Problem-solving', 'Communication', 'Work ethic',
  'Creativity', 'Teamwork', 'Technical skills', 'Reliability',
  'Initiative', 'Empathy', 'Attention to detail', 'Adaptability',
  'Mentorship', 'Strategic thinking', 'Resilience',
];

const VERSION_ICONS = { narrative: '📖', structured: '📋', concise: '⚡' };

// ════════════════════════════════════════════════════════════
// MAIN COMPONENT
// ════════════════════════════════════════════════════════════
const GhostWriter = ({ tool }) => {
  const { callToolEndpoint, loading } = useClaudeAPI();
  const { isDark } = useTheme();

  // ─── c block (PF-2 verbatim + tool-specific keys) ───
  const c = {
    card:            isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:         isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:           isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20'
                            : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:            isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary:   isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:       isDark ? 'text-zinc-500' : 'text-gray-400',
    labelText:       isDark ? 'text-zinc-200' : 'text-gray-700',
    accentTxt:       isDark ? 'text-cyan-400' : 'text-cyan-600',
    btnPrimary:      isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white'
                            : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:    isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200'
                            : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:          isDark ? 'border-zinc-700' : 'border-gray-200',
    success:         isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200'
                            : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:         isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200'
                            : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:          isDark ? 'bg-red-900/20 border-red-700 text-red-200'
                            : 'bg-red-50 border-red-200 text-red-800',
    pillActive:      isDark ? 'border-cyan-500 bg-cyan-900/30 text-cyan-200'
                            : 'border-cyan-600 bg-cyan-100 text-cyan-900',
    pillInactive:    isDark ? 'border-zinc-600 text-zinc-400 hover:border-zinc-500'
                            : 'border-gray-300 text-gray-500 hover:border-gray-400',
    required:        isDark ? 'text-amber-400' : 'text-amber-500',
    // Tool-specific keys
    btnDisabled:     isDark ? 'bg-zinc-700 text-zinc-500 cursor-not-allowed opacity-60'
                            : 'bg-gray-200 text-gray-400 cursor-not-allowed opacity-60',
    btnGhost:        isDark ? 'text-zinc-500 hover:bg-zinc-700/50 rounded-lg'
                            : 'text-gray-400 hover:bg-gray-100 rounded-lg',
    badge:           isDark ? 'bg-zinc-700 text-zinc-300' : 'bg-gray-100 text-gray-600',
    letterBg:        isDark ? 'bg-zinc-900/50 border-zinc-700' : 'bg-slate-50 border-gray-200',
    tipText:         isDark ? 'text-emerald-400' : 'text-emerald-600',
    placeholderBg:   isDark ? 'bg-amber-900/20 border-amber-700' : 'bg-amber-50 border-amber-200',
    placeholderText: isDark ? 'text-amber-300' : 'text-amber-700',
    powerBg:         isDark ? 'bg-zinc-700/50 border-zinc-600' : 'bg-slate-50 border-gray-200',
    powerText:       isDark ? 'text-cyan-300' : 'text-cyan-700',
    tabActive:       isDark ? 'bg-zinc-900 text-cyan-300 border-b-2 border-cyan-500'
                            : 'bg-white text-cyan-700 border-b-2 border-cyan-600',
    tabInactive:     isDark ? 'text-zinc-400 hover:bg-zinc-700/50'
                            : 'text-gray-500 hover:bg-gray-50',
    histAccent:      isDark ? 'text-cyan-400' : 'text-cyan-600',
  };
  c.textMuteded = c.textMuted;
  c.label = c.labelText;

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';

  // ─── State (all useState before usePersistentState — PF-11/PF-14) ───
  const [showHistory, setShowHistory] = useState(false);
  const [recipientName, setRecipientName] = useState('');
  const [yourRelationship, setYourRelationship] = useState('');
  const [whatFor, setWhatFor] = useState('');
  const [letterType, setLetterType] = useState('job');
  const [formalityLevel, setFormalityLevel] = useState('professional');
  const [qualities, setQualities] = useState([]);
  const [anecdotes, setAnecdotes] = useState(['']);
  const [duration, setDuration] = useState('');
  const [additionalContext, setAdditionalContext] = useState('');
  const [previousResults, setPreviousResults] = useState(null);
  const [error, setError] = useState('');
  const [activeVersion, setActiveVersion] = useState('narrative');
  const [refiningVersion, setRefiningVersion] = useState(null);
  const [refinementText, setRefinementText] = useState('');
  const [refinedVersions, setRefinedVersions] = useState({});
  const [refineLoading, setRefineLoading] = useState(false);
  const [showPlaceholders, setShowPlaceholders] = useState(true);
  const [showPowerPhrases, setShowPowerPhrases] = useState(false);
  const [showWritingTips, setShowWritingTips] = useState(false);

  // ─── Persistent state ───
  const [history, setHistory] = usePersistentState('ghost-writer-history', []);
  const [results, setResults] = usePersistentState('ghostwriter-result', null);

  // ─── Refs ───
  const resultsRef = useRef(null);
  const handleSubmitRef = useRef(null);
  const canSubmitRef = useRef(false);

  // Keep refs fresh every render
  canSubmitRef.current = !!recipientName.trim() && !!yourRelationship.trim();

  // ══════════════════════════════════════════
  // HANDLERS
  // ══════════════════════════════════════════
  const toggleQuality = useCallback((val) => {
    setQualities(prev => prev.includes(val) ? prev.filter(q => q !== val) : [...prev, val]);
  }, []);

  const updateAnecdote = useCallback((idx, val) => {
    setAnecdotes(prev => prev.map((a, i) => i === idx ? val : a));
  }, []);

  const addAnecdote = useCallback(() => {
    if (anecdotes.length < 5) setAnecdotes(prev => [...prev, '']);
  }, [anecdotes.length]);

  const removeAnecdote = useCallback((idx) => {
    if (anecdotes.length > 1) setAnecdotes(prev => prev.filter((_, i) => i !== idx));
  }, [anecdotes.length]);

  const generate = useCallback(async () => {
    if (!recipientName.trim() || !yourRelationship.trim()) {
      setError('We need the person\'s name and your relationship to them'); return;
    }
    setError(''); setResults(null); setRefinedVersions({});
    setRefiningVersion(null); setActiveVersion('narrative'); setPreviousResults(null);
    try {
      const data = await callToolEndpoint('ghost-writer', {
        recipientName: recipientName.trim(),
        yourRelationship: yourRelationship.trim(),
        whatTheyreApplyingFor: whatFor.trim() || null,
        letterType: LETTER_TYPES.find(t => t.value === letterType)?.label || letterType,
        qualities: qualities.length > 0 ? qualities : null,
        anecdotes: anecdotes.filter(a => a.trim()).length > 0 ? anecdotes.filter(a => a.trim()) : null,
        duration: duration.trim() || null,
        formalityLevel,
        additionalContext: additionalContext.trim() || null,
      });
      setResults(data);
      saveToHistory(data);
    } catch (err) { setError(err.message || 'Failed to generate letter.'); }
  }, [recipientName, yourRelationship, whatFor, letterType, qualities, anecdotes, duration, formalityLevel, additionalContext, callToolEndpoint]);
  handleSubmitRef.current = generate;

  const handleRefine = useCallback(async (version) => {
    if (!refinementText.trim()) return;
    setRefineLoading(true);
    try {
      const data = await callToolEndpoint('ghost-writer/refine', {
        letterText: version.letter,
        refinementRequest: refinementText.trim(),
        letterType, formalityLevel,
      });
      if (data.refined_letter) {
        setRefinedVersions(prev => ({ ...prev, [version.style]: data.refined_letter }));
      }
      setRefiningVersion(null); setRefinementText('');
    } catch (err) { setError(err.message || 'Failed to refine.'); }
    finally { setRefineLoading(false); }
  }, [refinementText, letterType, formalityLevel, callToolEndpoint]);

  const revertRefinement = useCallback((style) => {
    setRefinedVersions(prev => { const n = { ...prev }; delete n[style]; return n; });
  }, []);

  const handleReset = useCallback(() => {
    setRecipientName(''); setYourRelationship(''); setWhatFor('');
    setQualities([]); setAnecdotes(['']); setDuration('');
    setAdditionalContext(''); setResults(null); setError('');
    setRefinedVersions({}); setRefiningVersion(null); setPreviousResults(null);
  }, []);

  // ══════════════════════════════════════════
  // HISTORY
  // ══════════════════════════════════════════
  const saveToHistory = useCallback((data) => {
    const entry = {
      id: `gw_${Date.now()}`, date: new Date().toISOString(),
      preview: `Letter for ${recipientName}`,
      type: letterType, results: data,
    };
    setHistory(prev => [entry, ...prev].slice(0, 6));
  }, [recipientName, letterType, setHistory]);

  // ══════════════════════════════════════════
  // COPY / BUILD
  // ══════════════════════════════════════════
  const buildLetterCopy = useCallback((version) => {
    const text = refinedVersions[version.style] || version.letter;
    return `${text}${BRAND}`;
  }, [refinedVersions]);

  const buildAllCopy = useCallback(() => {
    if (!results?.versions) return '';
    const lines = ['✍️ Ghost Writer — Recommendation Letters', ''];
    results.versions.forEach(v => {
      const text = refinedVersions[v.style] || v.letter;
      lines.push(`══ ${v.label || v.style} ══`, text, '');
    });
    lines.push(BRAND);
    return lines.join('\n');
  }, [results, refinedVersions]);

  useRegisterActions(buildAllCopy(), tool?.title || 'Ghost Writer');

  // ─── Scroll useEffect ───
  useEffect(() => {
    if (!results || !resultsRef.current) return;
    const t = setTimeout(() => resultsRef.current?.scrollIntoView({ behavior: 'smooth', block: 'start' }), 200);
    return () => clearTimeout(t);
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [results]);

  // ─── Keyboard useEffect ───
  useEffect(() => {
    const handler = (e) => {
      const tag = document.activeElement?.tagName;
      if (tag === 'SELECT') return;
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && !loading && canSubmitRef.current)
        handleSubmitRef.current?.();
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [loading]);

  // ══════════════════════════════════════════
  // RENDER HELPERS
  // ══════════════════════════════════════════
  const Pill = ({ active, onClick, children }) => (
    <button onClick={onClick}
      className={`px-3 py-1.5 rounded-lg border text-xs font-semibold transition-all ${active ? c.pillActive : c.pillInactive}`}>
      {active && <span className="mr-1">✓</span>}{children}
    </button>
  );

  const Section = ({ title, emoji, open, onToggle, badge, children }) => (
    <div className={`${c.card} ${c.border} border rounded-xl overflow-hidden`}>
      <button onClick={onToggle} className="w-full flex items-center justify-between p-5 text-left hover:opacity-80">
        <div className="flex items-center gap-3">
          <span className="text-lg">{emoji}</span>
          <span className={`text-base font-semibold ${c.text}`}>{title}</span>
          {badge && <span className={`text-xs px-2 py-0.5 rounded-full ${c.badge}`}>{badge}</span>}
        </div>
        <span className={`text-xs ${c.textMuted}`}>{open ? '▲' : '▼'}</span>
      </button>
      {open && <div className={`px-5 pb-5 border-t ${c.border}`}>{children}</div>}
    </div>
  );

  // ══════════════════════════════════════════
  // RENDER: Additional input sections
  // ══════════════════════════════════════════
  const renderInputSections = () => (
    <>
      {/* Letter type + formality */}
      <div className={`${c.card} ${c.border} border rounded-xl p-5`}>
        <label className={`text-xs font-bold ${c.textSecondary} uppercase tracking-wide mb-2 block`}>📄 Letter type</label>
        <div className="flex flex-wrap gap-1.5">
          {LETTER_TYPES.map(opt => (
            <Pill key={opt.value} active={letterType === opt.value} onClick={() => setLetterType(opt.value)}>{opt.label}</Pill>
          ))}
        </div>
        <label className={`text-xs font-bold ${c.textSecondary} uppercase tracking-wide mt-4 mb-2 block`}>📏 Formality</label>
        <div className="flex flex-wrap gap-1.5">
          {FORMALITY_OPTIONS.map(opt => (
            <Pill key={opt.value} active={formalityLevel === opt.value} onClick={() => setFormalityLevel(opt.value)}>{opt.label}</Pill>
          ))}
        </div>
      </div>

      {/* Qualities */}
      <div className={`${c.card} ${c.border} border rounded-xl p-5`}>
        <label className={`text-xs font-bold ${c.textSecondary} uppercase tracking-wide mb-1 block`}>⭐ Qualities to highlight</label>
        <p className={`text-xs ${c.textMuted} mb-2`}>Select the traits that stand out about this person</p>
        <div className="flex flex-wrap gap-1.5">
          {QUALITY_OPTIONS.map(q => (
            <Pill key={q} active={qualities.includes(q)} onClick={() => toggleQuality(q)}>{q}</Pill>
          ))}
        </div>
      </div>

      {/* Anecdotes */}
      <div className={`${c.card} ${c.border} border rounded-xl p-5`}>
        <label className={`text-xs font-bold ${c.textSecondary} uppercase tracking-wide mb-1 block`}>📝 Specific examples or stories</label>
        <p className={`text-xs ${c.textMuted} mb-3`}>Even rough bullet points work — we'll turn them into compelling mini-stories</p>
        <div className="space-y-2">
          {anecdotes.map((a, idx) => (
            <div key={idx} className="flex gap-2">
              <input type="text" value={a} onChange={e => updateAnecdote(idx, e.target.value)}
                placeholder={`e.g., "${idx === 0 ? 'Led the Q3 product launch that exceeded targets by 20%' : 'Mentored two junior team members who both got promoted'}"`}
                className={`flex-1 px-4 py-2.5 rounded-xl border text-sm ${c.input} outline-none`} />
              {anecdotes.length > 1 && (
                <button onClick={() => removeAnecdote(idx)} className={`px-3 ${c.btnGhost}`}>✕</button>
              )}
            </div>
          ))}
        </div>
        {anecdotes.length < 5 && (
          <button onClick={addAnecdote} className={`mt-2 text-xs font-semibold ${linkStyle}`}>➕ Add another</button>
        )}
      </div>

      {/* Additional context */}
      <div className={`${c.card} ${c.border} border rounded-xl p-5`}>
        <label className={`text-xs font-bold ${c.textSecondary} uppercase tracking-wide mb-1 block`}>💬 Anything else?</label>
        <input type="text" value={additionalContext} onChange={e => setAdditionalContext(e.target.value)}
          placeholder="e.g., 'They're career-switching from marketing to product management'"
          className={`w-full px-4 py-2.5 rounded-xl border text-sm ${c.input} outline-none`} />
      </div>

      {/* Submit */}
      <button onClick={generate}
        disabled={loading || !recipientName.trim() || !yourRelationship.trim()}
        className={`w-full py-4 rounded-2xl text-sm font-bold flex items-center justify-center gap-2 transition-all disabled:opacity-40 ${
          loading || !recipientName.trim() || !yourRelationship.trim() ? c.btnDisabled : c.btnPrimary
        }`}>
        {loading
          ? <><span className="animate-spin inline-block">{tool?.icon ?? '✍️'}</span> Writing your letter...</>
          : <><span>{tool?.icon ?? '✍️'}</span> Generate Letter</>}
      </button>

      {/* Try Example */}
      {!recipientName.trim() && !yourRelationship.trim() && !loading && (
        <div className="flex justify-center">
          <button
            onClick={() => {
              setRecipientName('Maya Chen');
              setYourRelationship('I managed Maya for 3 years on the platform team — she reported directly to me.');
              setWhatFor('Senior engineering role at a Series B startup');
              setLetterType('job');
              setFormalityLevel('professional');
              setQualities(['Leadership', 'Technical skills', 'Initiative', 'Communication']);
              setAnecdotes([
                'Led the migration off our legacy auth system — designed the rollback plan that ran zero incidents over a 6-week cutover.',
                'Mentored two junior engineers who both got promoted; one now runs her own pod.',
              ]);
              setDuration('3 years');
              setAdditionalContext('She is moving for family reasons; this is not a performance issue.');
            }}
            className={`text-xs font-medium ${c.accentTxt} underline underline-offset-2 min-h-[32px]`}
          >
            ✨ Try an example
          </button>
        </div>
      )}
    </>
  );

  // ══════════════════════════════════════════
  // RENDER: Results
  // ══════════════════════════════════════════
  const renderResults = () => {
    if (!results?.versions?.length) return null;

    return (
      <div ref={resultsRef} className="space-y-4 mt-4">
        {/* Version tabs */}
        <div className={`${c.card} ${c.border} border rounded-xl overflow-hidden`}>
          <div className={`flex border-b ${c.border}`}>
            {results.versions.map(v => (
              <button key={v.style} onClick={() => setActiveVersion(v.style)}
                className={`flex-1 flex items-center justify-center gap-2 px-4 py-4 text-sm font-semibold transition-all ${
                  activeVersion === v.style ? c.tabActive : c.tabInactive
                }`}>
                <span>{VERSION_ICONS[v.style] || '📝'}</span>
                <span className="hidden sm:inline">{v.label?.split('—')[0]?.trim() || v.style}</span>
                <span className="sm:hidden capitalize">{v.style}</span>
              </button>
            ))}
          </div>

          {results.versions.map(v => {
            if (v.style !== activeVersion) return null;
            const displayText = refinedVersions[v.style] || v.letter;
            const isRefined = !!refinedVersions[v.style];

            return (
              <div key={v.style} className="p-6 space-y-4">
                <div className="flex items-center justify-between flex-wrap gap-2">
                  {v.best_for && <span className={`text-sm ${c.textMuted}`}>💡 {v.best_for}</span>}
                  {v.word_count && <span className={`text-xs px-2 py-0.5 rounded-full ${c.badge}`}>{v.word_count} words</span>}
                </div>

                {v.strengths?.length > 0 && (
                  <div className="flex flex-wrap gap-1.5">
                    {v.strengths.map((s, i) => (
                      <span key={i} className={`text-xs px-2.5 py-1 rounded-full ${c.badge}`}>{s}</span>
                    ))}
                  </div>
                )}

                <div className={`p-5 rounded-xl border ${c.letterBg}`}>
                  {isRefined && (
                    <div className={`text-xs font-medium mb-2 flex items-center gap-1 ${c.tipText}`}>
                      <span>✏️</span> Refined version
                      <button onClick={() => revertRefinement(v.style)} className="ml-2 underline hover:no-underline">Revert</button>
                    </div>
                  )}
                  <p className={`text-sm leading-relaxed whitespace-pre-wrap ${c.text}`}>{displayText}</p>
                </div>

                {v.customize_prompts?.length > 0 && (
                  <div className={`p-4 rounded-xl border ${c.placeholderBg}`}>
                    <p className={`text-xs font-bold ${c.placeholderText} mb-2`}>✏️ Personalize these parts</p>
                    {v.customize_prompts.map((prompt, i) => (
                      <p key={i} className={`text-xs ${c.placeholderText} mb-1`}>• {prompt}</p>
                    ))}
                  </div>
                )}

                {/* Actions */}
                <div className="flex flex-wrap gap-3">
                  <button onClick={() => { setRefiningVersion(refiningVersion === v.style ? null : v.style); setRefinementText(''); }}
                    className={`${c.btnSecondary} px-4 py-2.5 rounded-lg text-sm font-medium flex items-center gap-2`}>
                    <span>✏️</span> Refine
                  </button>
                </div>

                {refiningVersion === v.style && (
                  <div className={`${c.cardAlt} ${c.border} border rounded-xl p-4 space-y-3`}>
                    <label className={`text-sm font-semibold ${c.textSecondary}`}>How should I adjust this? <span className={c.required}>*</span></label>
                    <textarea value={refinementText} onChange={e => setRefinementText(e.target.value)}
                      placeholder='e.g., "Make it shorter", "Add the client presentation story", "Sound warmer"'
                      className={`w-full p-3 border rounded-lg ${c.input} outline-none focus:ring-2 resize-none text-sm`} rows={2} />
                    <div className="flex gap-2">
                      <button onClick={() => handleRefine(v)}
                        disabled={refineLoading || !refinementText.trim()}
                        className={`${refineLoading || !refinementText.trim() ? c.btnDisabled : c.btnPrimary} px-4 py-2 rounded-lg text-sm font-semibold flex items-center gap-2 disabled:opacity-40`}>
                        {refineLoading ? <span className="animate-spin inline-block">{tool?.icon ?? '✍️'}</span> : <span>✨</span>} Refine
                      </button>
                      <button onClick={() => { setRefiningVersion(null); setRefinementText(''); }}
                        className={`${c.btnSecondary} px-4 py-2 rounded-lg text-sm`}>Cancel</button>
                    </div>
                  </div>
                )}
              </div>
            );
          })}
        </div>

        {results.placeholders_to_fill?.length > 0 && (
          <Section title="Fill In These Details" emoji="📝" open={showPlaceholders}
            onToggle={() => setShowPlaceholders(!showPlaceholders)} badge={`${results.placeholders_to_fill.length} items`}>
            <div className="space-y-2 mt-4">
              {results.placeholders_to_fill.map((p, idx) => (
                <div key={idx} className={`p-3 rounded-lg ${c.cardAlt} ${c.border} flex items-start gap-3`}>
                  <code className={`text-xs font-mono px-1.5 py-0.5 rounded ${c.badge} flex-shrink-0`}>{p.placeholder}</code>
                  <span className={`text-xs ${c.textSecondary}`}>→ {p.suggestion}</span>
                </div>
              ))}
            </div>
          </Section>
        )}

        {results.power_phrases?.length > 0 && (
          <Section title="Power Phrases" emoji="💪" open={showPowerPhrases}
            onToggle={() => setShowPowerPhrases(!showPowerPhrases)}>
            <div className="space-y-2 mt-4">
              {results.power_phrases.map((phrase, idx) => (
                <div key={idx} className={`p-3 rounded-lg border ${c.powerBg}`}>
                  <p className={`text-sm ${c.powerText} italic`}>"{phrase}"</p>
                </div>
              ))}
            </div>
          </Section>
        )}

        {results.writing_tips?.length > 0 && (
          <Section title="Writing Tips" emoji="💡" open={showWritingTips}
            onToggle={() => setShowWritingTips(!showWritingTips)}>
            <div className="space-y-2 mt-4">
              {results.writing_tips.map((tip, idx) => (
                <p key={idx} className={`text-sm ${c.textSecondary}`}>• {tip}</p>
              ))}
            </div>
          </Section>
        )}

        {/* Post-result cross-refs */}
        <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
          <p className={`text-[10px] font-bold ${c.textMuted} uppercase mb-2`}>🔗 Related tools</p>
          <div className="flex flex-wrap gap-3">
            <a href="/VelvetHammer" className={`text-xs ${linkStyle}`}>🔨 Velvet Hammer</a>
            <a href="/BragSheetBuilder" className={`text-xs ${linkStyle}`}>🏆 Brag Sheet Builder</a>
          </div>
        </div>
      </div>
    );
  };

  // ══════════════════════════════════════════
  // RENDER: History
  // ══════════════════════════════════════════
  const renderHistory = () => {
    if (history.length === 0) return null;
    const formatDate = (iso) => {
      try {
        const d = new Date(iso);
        const diff = Math.floor((new Date() - d) / 86400000);
        return diff === 0 ? 'Today' : diff === 1 ? 'Yesterday' : diff < 7 ? `${diff}d ago` : d.toLocaleDateString('en-US', { month: 'short', day: 'numeric' });
      } catch { return ''; }
    };
    return (
      <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
        <button onClick={() => setShowHistory(!showHistory)} className="w-full flex items-center gap-2 text-left">
          <span className={`text-base ${c.histAccent}`}>✍️</span>
          <span className={`text-sm font-bold ${c.text} flex-1`}>Past Letters</span>
          <span className={`text-xs ${c.textMuted}`}>{history.length}</span>
          <span className={`text-xs ${c.textMuted}`}>{showHistory ? '▲' : '▼'}</span>
        </button>
        {showHistory && (
          <div className="mt-3 space-y-2">
            {history.map(entry => (
              <div key={entry.id} className={`rounded-xl border ${c.card} ${c.border} p-3 flex items-center gap-3`}>
                <div className="flex-1 min-w-0">
                  <div className={`text-sm font-semibold ${c.text} truncate`}>{entry.preview}</div>
                  <div className={`text-xs ${c.textMuted} mt-0.5`}>{formatDate(entry.date)} · {entry.type}</div>
                </div>
                <button onClick={() => { setPreviousResults(results); setResults(entry.results); setShowHistory(false); setRefinedVersions({}); setActiveVersion('narrative'); }}
                  className={`px-3 py-1.5 rounded-lg text-xs font-bold ${c.btnSecondary}`}>View</button>
                <button onClick={() => setHistory(prev => prev.filter(h => h.id !== entry.id))}
                  className={`px-2 py-1.5 ${c.btnGhost} hover:text-zinc-400`}>🗑️</button>
              </div>
            ))}
          </div>
        )}
      </div>
    );
  };

  // ════════════════════════════════════════════════════════════
  // RETURN
  // ════════════════════════════════════════════════════════════
  return (
    <div className={`space-y-4 ${c.text}`}>

      {/* ── Persistent header card ── */}
      <div className={`${c.card} border ${c.border} rounded-xl shadow-sm p-5`}>
        <div className="pb-3 border-b border-zinc-500">
          <h2 className={`text-xl font-bold ${c.text} flex items-center gap-2`}>
            <span className="mr-2">{tool?.icon ?? '✍️'}</span>{tool?.title ?? 'Ghost Writer'}
          </h2>
          <p className={`text-sm ${c.textSecondary}`}>{tool?.tagline ?? 'Turn rough notes into polished recommendation letters in seconds'}</p>
        </div>

        {!results ? (
          <div className="mt-4 space-y-4">
            {/* Required fields */}
            <div className="grid grid-cols-1 sm:grid-cols-2 gap-3">
              <div>
                <label className={`block text-sm font-medium ${c.labelText} mb-1`}>
                  Who is this for? <span className={c.required}>*</span>
                </label>
                <input type="text" value={recipientName} onChange={e => setRecipientName(e.target.value)}
                  placeholder="Their name"
                  className={`w-full px-4 py-2.5 rounded-xl border text-sm ${c.input} outline-none`} />
              </div>
              <div>
                <label className={`block text-sm font-medium ${c.labelText} mb-1`}>
                  Your relationship <span className={c.required}>*</span>
                </label>
                <input type="text" value={yourRelationship} onChange={e => setYourRelationship(e.target.value)}
                  placeholder="e.g., 'their manager for 2 years'"
                  className={`w-full px-4 py-2.5 rounded-xl border text-sm ${c.input} outline-none`} />
              </div>
            </div>
            <div className="grid grid-cols-1 sm:grid-cols-2 gap-3">
              <div>
                <label className={`block text-sm font-medium ${c.labelText} mb-1`}>What they're applying for</label>
                <input type="text" value={whatFor} onChange={e => setWhatFor(e.target.value)}
                  placeholder="e.g., 'Senior Product Manager at Google'"
                  className={`w-full px-4 py-2.5 rounded-xl border text-sm ${c.input} outline-none`} />
              </div>
              <div>
                <label className={`block text-sm font-medium ${c.labelText} mb-1`}>How long you've known them</label>
                <input type="text" value={duration} onChange={e => setDuration(e.target.value)}
                  placeholder="e.g., '3 years'"
                  className={`w-full px-4 py-2.5 rounded-xl border text-sm ${c.input} outline-none`} />
              </div>
            </div>

            {/* Pre-result cross-ref */}
            <p className={`text-xs text-center ${c.textMuted}`}>
              Need to document their wins first?{' '}
              <a href="/BragSheetBuilder" className={linkStyle}>🏆 Brag Sheet Builder</a>{' '}
              organizes the achievements worth highlighting.
            </p>
          </div>
        ) : (
          <div className="mt-4 flex items-center justify-between gap-3">
            <p className={`text-sm ${c.textSecondary} truncate`}>
              {previousResults ? '📜 Viewing past letter' : `Letter for ${recipientName}`}
            </p>
            <div className="flex items-center gap-2 flex-shrink-0">
              {previousResults && (
                <button
                  onClick={() => { setResults(previousResults); setPreviousResults(null); setRefinedVersions({}); setActiveVersion('narrative'); }}
                  className={`${c.btnSecondary} px-3 py-2 rounded-lg text-xs font-bold flex items-center gap-1.5`}>
                  ← Back
                </button>
              )}
              <button onClick={handleReset}
                className={`${c.btnSecondary} px-4 py-2 rounded-lg text-sm font-bold flex items-center gap-1.5`}>
                ↩ New Letter
              </button>
            </div>
          </div>
        )}
      </div>

      {/* ── Additional input sections (input mode only) ── */}
      {!results && renderInputSections()}

      {/* ── Results ── */}
      {results && renderResults()}

      {error && (
        <div className={`p-4 ${c.danger} border rounded-xl flex items-start gap-3`}>
          <span className="flex-shrink-0">⚠️</span>
          <p className="text-sm">{error}</p>
        </div>
      )}

      {renderHistory()}
    </div>
  );
};

GhostWriter.displayName = 'GhostWriter';
export default GhostWriter;
