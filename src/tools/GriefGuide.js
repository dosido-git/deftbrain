import React, { useState, useEffect, useRef, useCallback } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { usePersistentState } from '../hooks/usePersistentState';
import { useRegisterActions } from '../components/ActionBarContext';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const GRIEF_MODES = [
  { id: 'myself',  label: 'I\'m grieving',            icon: '💙', desc: 'Support and guidance for your own loss' },
  { id: 'helping', label: 'Helping someone grieve',   icon: '🤝', desc: 'How to support someone you care about' },
  { id: 'both',    label: 'Both',                     icon: '💛', desc: 'You\'re grieving and supporting others' },
];

const LOSS_TYPES = [
  { id: 'death_person',  label: 'Death of a person' },
  { id: 'death_pet',     label: 'Death of a pet' },
  { id: 'relationship',  label: 'End of a relationship' },
  { id: 'job',           label: 'Job / career loss' },
  { id: 'health',        label: 'Health / diagnosis' },
  { id: 'pregnancy',     label: 'Pregnancy / fertility loss' },
  { id: 'identity',      label: 'Identity / life chapter' },
  { id: 'friendship',    label: 'Friendship / community' },
  { id: 'home',          label: 'Home / place' },
  { id: 'other',         label: 'Other loss' },
];

const TIMELINE = [
  { id: 'just',     label: 'Just happened' },
  { id: 'days',     label: 'Days ago' },
  { id: 'weeks',    label: 'Weeks ago' },
  { id: 'months',   label: 'Months ago' },
  { id: 'years',    label: 'A year or more' },
];

const EXAMPLE = {
  mode: 'myself',
  lossType: 'death_person',
  timeline: 'weeks',
  freeform: "My dad died three weeks ago. It was sudden — a heart attack. I keep thinking I need to call him, then remembering he's gone. I'm functional at work but I cry when I get home. I don't know if what I'm feeling is normal or if there's something wrong with me. My family keeps saying I need to be strong for my mom.",
  country: '',
};

function GriefGuide({ tool }) {
  const { callToolEndpoint, loading } = useClaudeAPI();
  const { isDark } = useTheme();

  const c = {
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:         isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20'
                          : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-500' : 'text-gray-400',
    labelText:     isDark ? 'text-zinc-200' : 'text-gray-700',
    accentTxt:     isDark ? 'text-cyan-400' : 'text-cyan-600',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white'
                          : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200'
                          : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    pillActive:    isDark ? 'border-cyan-500 bg-cyan-900/30 text-cyan-200'
                          : 'border-cyan-600 bg-cyan-100 text-cyan-900',
    pillInactive:  isDark ? 'border-zinc-600 text-zinc-400 hover:border-zinc-500'
                          : 'border-gray-300 text-gray-500 hover:border-gray-400',
    required:      isDark ? 'text-amber-400' : 'text-amber-500',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200' : 'bg-amber-50 border-amber-300 text-amber-800',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200' : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    infoBox:       isDark ? 'bg-sky-900/20 border-sky-700 text-sky-200'   : 'bg-sky-50 border-sky-200 text-sky-800',
    softBox:       isDark ? 'bg-zinc-700/40 border-zinc-600 text-zinc-200' : 'bg-slate-100 border-slate-200 text-slate-700',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200'   : 'bg-red-50 border-red-200 text-red-800',
  };
  c.textMuteded = c.textMuted;
  c.label = c.labelText;

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';

  const [mode, setMode]         = useState('myself');
  const [lossType, setLossType] = useState('');
  const [timeline, setTimeline] = useState('');
  const [freeform, setFreeform] = useState('');
  const [country, setCountry]   = useState('');
  const [results, setResults]   = usePersistentState('griefguide-results', null);
  const [history, setHistory]   = usePersistentState('griefguide-history', []);
  const [error, setError]       = useState('');

  const resultsRef   = useRef(null);
  const handleRef    = useRef(null);
  const canSubmitRef = useRef(false);

  const canSubmit = freeform.trim().length >= 20 || (lossType && timeline);

  const handleReset = useCallback(() => {
    setResults(null);
    setError('');
    setMode('myself');
    setLossType('');
    setTimeline('');
    setFreeform('');
    setCountry('');
  }, [setResults]);

  const loadExample = useCallback(() => {
    setMode(EXAMPLE.mode);
    setLossType(EXAMPLE.lossType);
    setTimeline(EXAMPLE.timeline);
    setFreeform(EXAMPLE.freeform);
    setCountry(EXAMPLE.country);
    setResults(null);
    setError('');
  }, [setResults]);

  const handleGuide = useCallback(async () => {
    if (!canSubmit || loading) return;
    setError('');
    setResults(null);

    try {
      const parsed = await callToolEndpoint('grief-guide/stream', {
        mode,
        lossType: lossType || null,
        timeline: timeline || null,
        freeform: freeform.trim() || null,
        country: country.trim() || null,
      });
      setResults(parsed);
      const lossLabel = LOSS_TYPES.find(l => l.id === lossType)?.label ?? lossType ?? 'loss';
      const previewText = `${lossLabel} · ${TIMELINE.find(t => t.id === timeline)?.label ?? ''}`;
      setHistory(prev => [{
        preview: previewText.slice(0, 50),
        ts: Date.now(),
      }, ...prev].slice(0, 5));
    } catch (err) {
      setError(err.message || 'Something went wrong. Please try again.');
    }
  }, [canSubmit, loading, mode, lossType, timeline, freeform, country, callToolEndpoint, setResults, setHistory]);

  handleRef.current    = handleGuide;
  canSubmitRef.current = canSubmit;

  const buildFullText = useCallback(() => {
    if (!results) return '';
    const lines = ['GRIEF GUIDE', ''];
    if (results?.opening) lines.push(results?.opening, '');
    if (results?.what_is_normal?.length) {
      lines.push('WHAT YOU\'RE EXPERIENCING IS NORMAL');
      results?.what_is_normal.forEach(n => lines.push(`• ${n}`));
    }
    if (results?.guidance?.length) {
      lines.push('\nGUIDANCE');
      results?.guidance.forEach(g => {
        lines.push(`\n${g.title}`);
        lines.push(g.body);
      });
    }
    if (results?.what_to_say?.length) {
      lines.push('\nWHAT TO SAY');
      results?.what_to_say.forEach(s => lines.push(`• ${s}`));
    }
    if (results?.what_not_to_say?.length) {
      lines.push('\nAVOID SAYING');
      results?.what_not_to_say.forEach(s => lines.push(`• ${s}`));
    }
    if (results?.when_to_seek_help) lines.push(`\nWHEN TO SEEK MORE SUPPORT\n${results?.when_to_seek_help}`);
    lines.push(BRAND);
    return lines.join('\n');
  }, [results]);

  useRegisterActions(buildFullText(), tool?.title || 'GriefGuide');

  useEffect(() => {
    if (!results || !resultsRef.current) return;
    const t = setTimeout(() => resultsRef.current?.scrollIntoView({ behavior: 'smooth', block: 'start' }), 200);
    return () => clearTimeout(t);
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [results]);

  useEffect(() => {
    const handler = (e) => {
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && !loading && canSubmitRef.current)
        handleRef.current?.();
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [loading]);

  const renderInput = () => (
    <div className={`${c.card} border ${c.border} rounded-xl shadow-sm p-5 space-y-5`}>

      {/* Gentle intro */}
      <div className={`${c.softBox} border rounded-xl px-4 py-3`}>
        <p className="text-sm">Whatever you're going through, you don't have to figure it out alone. This tool offers guidance, not answers — grief is personal and there's no right way to do it.</p>
      </div>

      {/* Mode */}
      <div>
        <label className={`block text-sm font-medium ${c.labelText} mb-2`}>Who is this for?</label>
        <div className="space-y-2">
          {GRIEF_MODES.map(m => (
            <button key={m.id} onClick={() => setMode(m.id)}
              className={`w-full text-left px-4 py-3 rounded-xl border transition-colors ${mode === m.id ? c.pillActive : c.pillInactive}`}>
              <span className="mr-2">{m.icon}</span>
              <span className="font-medium text-sm">{m.label}</span>
              <span className={`ml-2 text-xs ${c.textMuted}`}>{m.desc}</span>
            </button>
          ))}
        </div>
      </div>

      {/* Loss type */}
      <div>
        <label className={`block text-sm font-medium ${c.labelText} mb-2`}>Type of loss <span className={`text-xs font-normal ${c.textMuted}`}>(optional)</span></label>
        <div className="flex flex-wrap gap-2">
          {LOSS_TYPES.map(l => (
            <button key={l.id} onClick={() => setLossType(lossType === l.id ? '' : l.id)}
              className={`px-3 py-1.5 rounded-lg border text-xs font-medium transition-colors ${lossType === l.id ? c.pillActive : c.pillInactive}`}>
              {l.label}
            </button>
          ))}
        </div>
      </div>

      {/* Timeline */}
      <div>
        <label className={`block text-sm font-medium ${c.labelText} mb-2`}>When did it happen? <span className={`text-xs font-normal ${c.textMuted}`}>(optional)</span></label>
        <div className="flex flex-wrap gap-2">
          {TIMELINE.map(t => (
            <button key={t.id} onClick={() => setTimeline(timeline === t.id ? '' : t.id)}
              className={`px-3 py-1.5 rounded-lg border text-xs font-medium transition-colors ${timeline === t.id ? c.pillActive : c.pillInactive}`}>
              {t.label}
            </button>
          ))}
        </div>
      </div>

      {/* Freeform */}
      <div>
        <label className={`block text-sm font-medium ${c.labelText} mb-1`}>
          Share what you'd like <span className={`text-xs font-normal ${c.textMuted}`}>(optional — share only what feels comfortable)</span>
        </label>
        <textarea value={freeform} onChange={e => setFreeform(e.target.value)}
          placeholder="What's been happening? What are you finding hardest? What are you feeling? You can share as much or as little as you like."
          rows={5}
          className={`w-full px-3 py-2.5 border rounded-xl text-sm resize-y focus:outline-none focus:ring-2 ${c.input}`} />
      </div>

      {/* Country */}
      <div>
        <label className={`block text-sm font-medium ${c.labelText} mb-1`}>
          Country <span className={`text-xs font-normal ${c.textMuted}`}>(optional — for local resources)</span>
        </label>
        <input type="text" value={country} onChange={e => setCountry(e.target.value)}
          placeholder="e.g. United States, UK, Australia…"
          className={`w-full px-3 py-2 border rounded-xl text-sm focus:outline-none focus:ring-2 ${c.input}`} />
      </div>

      {error && <p className={`text-sm ${c.danger} border rounded-lg px-3 py-2`}>{error}</p>}

      <p className={`text-xs ${c.textMuted}`}>
        Looking for a therapist or grief counselor? <a href="/DifficultTalkCoach" className={linkStyle}>🗣️ Difficult Talk Coach</a> can help you find the right support.
      </p>

      <div className="flex gap-2">
        <button onClick={handleGuide} disabled={!canSubmit || loading}
          className={`flex-1 ${c.btnPrimary} disabled:opacity-40 font-bold py-3 rounded-lg flex items-center justify-center gap-2 min-h-[48px]`}>
          {loading
            ? <><span className="inline-block animate-spin">{tool?.icon ?? '💙'}</span> Finding guidance…</>
            : <><span className="mr-1">{tool?.icon ?? '💙'}</span> Get Guidance</>}
        </button>
        <button onClick={loadExample} className={`${c.btnSecondary} px-4 py-3 rounded-lg text-sm font-bold min-h-[48px]`}>
          Try example
        </button>
      </div>

      {history.length > 0 && (
        <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
          <div className="flex items-center justify-between mb-2">
            <p className={`text-xs font-bold ${c.textMuted} uppercase tracking-wide`}>🕓 Previous sessions</p>
            <button onClick={() => setHistory([])} className={`text-xs ${c.textMuted}`}>Clear</button>
          </div>
          <ul className="space-y-1">
            {history.map((h, i) => (
              <li key={i} className={`text-xs ${c.textMuted}`}>{h.preview}</li>
            ))}
          </ul>
        </div>
      )}
    </div>
  );

  const renderResults = () => {
    if (!results) return null;
    return (
      <div className="space-y-4" ref={resultsRef}>

        {/* Opening */}
        {results?.opening && (
          <div className={`${c.softBox} border rounded-xl px-5 py-4`}>
            <p className={`text-sm leading-relaxed ${c.textSecondary}`}>{results?.opening}</p>
          </div>
        )}

        {/* What is normal */}
        {results?.what_is_normal?.length > 0 && (
          <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
            <p className={`text-xs font-bold ${c.textMuted} uppercase tracking-wide mb-3`}>💙 What you're experiencing is normal</p>
            <ul className="space-y-2">
              {results?.what_is_normal.map((n, i) => (
                <li key={i} className={`text-sm ${c.success} border rounded-lg px-3 py-2 flex gap-2`}>
                  <span className="flex-shrink-0">✓</span><span>{n}</span>
                </li>
              ))}
            </ul>
          </div>
        )}

        {/* Guidance sections */}
        {results?.guidance?.length > 0 && (
          <div className="space-y-3">
            {results?.guidance.map((g, i) => (
              <div key={i} className={`${c.card} border ${c.border} rounded-xl p-4`}>
                <p className={`font-bold text-sm ${c.text} mb-2`}>{g.title}</p>
                <p className={`text-sm ${c.textSecondary} leading-relaxed`}>{g.body}</p>
                {g.practical?.length > 0 && (
                  <ul className="mt-3 space-y-1.5">
                    {g.practical.map((p, j) => (
                      <li key={j} className={`text-xs ${c.textSecondary} flex gap-2`}>
                        <span className={`flex-shrink-0 ${c.accentTxt}`}>→</span><span>{p}</span>
                      </li>
                    ))}
                  </ul>
                )}
              </div>
            ))}
          </div>
        )}

        {/* What to say / not say (for helping mode) */}
        {(results?.what_to_say?.length > 0 || results?.what_not_to_say?.length > 0) && (
          <div className="grid grid-cols-1 sm:grid-cols-2 gap-3">
            {results?.what_to_say?.length > 0 && (
              <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
                <p className={`text-xs font-bold ${c.textMuted} uppercase tracking-wide mb-3`}>✅ Say this</p>
                <ul className="space-y-2">
                  {results?.what_to_say.map((s, i) => (
                    <li key={i} className={`text-sm ${c.success} border rounded-lg px-3 py-2`}>"{s}"</li>
                  ))}
                </ul>
              </div>
            )}
            {results?.what_not_to_say?.length > 0 && (
              <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
                <p className={`text-xs font-bold ${c.textMuted} uppercase tracking-wide mb-3`}>🚫 Avoid saying</p>
                <ul className="space-y-2">
                  {results?.what_not_to_say.map((s, i) => (
                    <li key={i} className={`text-sm ${c.warning} border rounded-lg px-3 py-2`}>"{s}"</li>
                  ))}
                </ul>
              </div>
            )}
          </div>
        )}

        {/* When to seek professional help */}
        {results?.when_to_seek_help && (
          <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
            <p className={`text-xs font-bold ${c.textMuted} uppercase tracking-wide mb-2`}>🧭 When to seek more support</p>
            <p className={`text-sm ${c.textSecondary}`}>{results?.when_to_seek_help}</p>
            {results?.support_resources?.length > 0 && (
              <ul className="mt-3 space-y-1.5">
                {results?.support_resources.map((r, i) => (
                  <li key={i} className={`text-xs ${c.infoBox} border rounded-lg px-3 py-2`}>{r}</li>
                ))}
              </ul>
            )}
          </div>
        )}

        {/* Post-result cross-refs */}
        <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
          <p className={`text-[10px] font-bold ${c.textMuted} uppercase mb-2`}>🔗 Related tools</p>
          <div className="flex flex-wrap gap-3">
            <a href="/DifficultTalkCoach" className={`text-xs ${linkStyle}`}>🗣️ Difficult Talk Coach</a>
            <a href="/DifficultTalkCoach" className={`text-xs ${linkStyle}`}>🗣️ Difficult Talk Coach</a>
            <a href="/SpiralStopper" className={`text-xs ${linkStyle}`}>🌀 Spiral Stopper</a>
          </div>
        </div>
      </div>
    );
  };

  return (
    <div className={`space-y-4 ${c.text}`}>
      <div className={`${c.card} border ${c.border} rounded-xl shadow-sm p-5`}>
        <div className="flex items-start justify-between">
          <div>
            <h2 className={`text-xl font-bold ${c.text}`}>
              <span className="mr-2">{tool?.icon ?? '💙'}</span>{tool?.title ?? 'GriefGuide'}
            </h2>
            <p className={`text-sm ${c.textSecondary}`}>{tool?.tagline ?? 'Compassionate guidance for navigating loss — yours or someone else\'s.'}</p>
          </div>
          <button
            onClick={handleReset}
            style={{ visibility: (results || lossType || freeform.trim()) ? 'visible' : 'hidden' }}
            className={`${c.btnSecondary} px-3 py-1.5 rounded-lg text-xs font-bold`}
          >
            ↺ Start over
          </button>
        </div>
      </div>
      {!results && <p className={`text-xs ${c.textMuted} px-1`}>Looking for a grief counselor? <a href="/DifficultTalkCoach" className={linkStyle}>🗣️ Difficult Talk Coach</a> helps you find the right support.</p>}
      {!results && renderInput()}
      {results && renderResults()}
    </div>
  );
}

GriefGuide.displayName = 'GriefGuide';
export default GriefGuide;
