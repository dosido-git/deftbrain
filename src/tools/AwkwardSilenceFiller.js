import React, { useState, useCallback } from 'react';
import { CopyBtn, ActionBar } from '../components/ActionButtons';
import { usePersistentState } from '../hooks/usePersistentState';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

// ════════════════════════════════════════════════════════════
// CONSTANTS
// ════════════════════════════════════════════════════════════
const SCENARIOS = [
  { value: 'elevator_boss', label: 'Elevator with boss', emoji: '🏢' },
  { value: 'first_date', label: 'First date', emoji: '💘' },
  { value: 'uber', label: 'Uber / taxi', emoji: '🚗' },
  { value: 'hairdresser', label: 'Hairdresser', emoji: '💇' },
  { value: 'friend_of_friend', label: "Friend's friend at dinner", emoji: '🍽️' },
  { value: 'networking', label: 'Networking event', emoji: '🤝' },
  { value: 'family_gathering', label: 'Family gathering', emoji: '👨‍👩‍👧‍👦' },
  { value: 'waiting_room', label: 'Waiting room', emoji: '🪑' },
  { value: 'neighbors', label: 'Running into neighbor', emoji: '🏡' },
  { value: 'work_lunch', label: 'Work lunch', emoji: '🥗' },
];

const RELATIONSHIPS = [
  { value: 'stranger', label: 'Stranger', emoji: '👤' },
  { value: 'acquaintance', label: 'Acquaintance', emoji: '🙂' },
  { value: 'coworker', label: 'Coworker', emoji: '💼' },
  { value: 'boss', label: 'Boss / authority', emoji: '👔' },
  { value: 'date', label: 'Date / crush', emoji: '💕' },
  { value: 'in_laws', label: 'In-laws / partner family', emoji: '👪' },
  { value: 'old_friend', label: 'Old friend', emoji: '🫂' },
  { value: 'new_friend', label: 'New friend', emoji: '✨' },
];

const COMFORT_LEVELS = [
  { value: 'panicking', label: 'Panicking', emoji: '😰' },
  { value: 'nervous', label: 'Nervous', emoji: '😬' },
  { value: 'slightly_awkward', label: 'Slightly awkward', emoji: '😅' },
  { value: 'fine', label: 'Mostly fine', emoji: '🙂' },
];

// ════════════════════════════════════════════════════════════
// COMPONENT
// ════════════════════════════════════════════════════════════
const AwkwardSilenceFiller = ({ tool }) => {
  const { callToolEndpoint, loading } = useClaudeAPI();
  const { isDark } = useTheme();

  // ─── Theme-aware colors ───
  const c = {
    card:          isDark ? 'bg-zinc-800'      : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50'   : 'bg-slate-50',
    text:          isDark ? 'text-zinc-50'     : 'text-slate-900',
    textSecondary: isDark ? 'text-zinc-400'    : 'text-slate-600',
    textMuted:     isDark ? 'text-zinc-500'    : 'text-slate-500',
    label:         isDark ? 'text-zinc-300'    : 'text-slate-700',
    input:         isDark
      ? 'bg-zinc-900 border-zinc-600 text-zinc-50 placeholder:text-zinc-500 focus:border-cyan-500 focus:ring-cyan-500/20'
      : 'bg-white border-slate-300 text-slate-900 placeholder:text-slate-400 focus:border-cyan-500 focus:ring-cyan-500/20',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white' : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnPanic:      isDark ? 'bg-red-600 hover:bg-red-500 text-white'      : 'bg-red-600 hover:bg-red-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-100' : 'bg-slate-100 hover:bg-slate-200 text-slate-800',
    border:        isDark ? 'border-zinc-700'  : 'border-slate-200',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700/50 text-emerald-200' : 'bg-emerald-50 border-emerald-200 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700/50 text-amber-200'   : 'bg-amber-50 border-amber-200 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700/50 text-red-200'         : 'bg-red-50 border-red-200 text-red-800',
    pillActive:    isDark ? 'bg-cyan-600 border-cyan-500 text-white'       : 'bg-cyan-600 border-cyan-600 text-white',
    pillInactive:  isDark ? 'bg-zinc-700 border-zinc-600 text-zinc-300 hover:border-zinc-500' : 'bg-white border-slate-200 text-slate-700 hover:border-slate-300',
    quoteBg:       isDark ? 'bg-zinc-900/60'   : 'bg-slate-50',
    chainBg:       isDark ? 'bg-zinc-900/40 border-zinc-700' : 'bg-emerald-50/50 border-emerald-100',
  };

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';

  // ─── State ───
  const [scenario, setScenario] = usePersistentState('awkwardsilencefiller-scenario', '');
  const [customContext, setCustomContext] = usePersistentState('awkwardsilencefiller-context', '');
  const [relationship, setRelationship] = useState('');
  const [comfort, setComfort] = useState('nervous');
  const [landmines, setLandmines] = useState('');

  const [results, setResults] = usePersistentState('awkwardsilencefiller-last', null);
  const [panicResult, setPanicResult] = usePersistentState('awkwardsilencefiller-panic', null);
  const [history, setHistory] = usePersistentState('awkwardsilencefiller-history', []);
  const [error, setError] = useState('');
  const [expandedSections, setExpandedSections] = useState({});
  const [panicLoading, setPanicLoading] = useState(false);

  // ─── Helpers ───
  const toggleSection = useCallback((key) => {
    setExpandedSections(p => ({ ...p, [key]: !p[key] }));
  }, []);

  const isRunning = loading || panicLoading;

  // ─── Context helper (shared between panic + full) ───
  const getContext = () =>
    customContext.trim() ||
    (scenario ? SCENARIOS.find(s => s.value === scenario)?.label : '') || '';

  // ─── Actions ───
  const generate = useCallback(async () => {
    setError('');
    setResults(null);
    setPanicResult(null);

    const context = getContext();

    try {
      const data = await callToolEndpoint('awkward-silence-filler', {
        action: 'full',
        context: context || 'general social situation',
        scenario,
        relationship: relationship || 'acquaintance',
        comfort: comfort || 'nervous',
        landmines: landmines.trim() || null,
      });
      setResults(data);
      setExpandedSections({});

      const preview = (customContext.trim() || SCENARIOS.find(s => s.value === scenario)?.label || 'general situation').slice(0, 6);
      setHistory(prev => [{
        id: Date.now(),
        timestamp: new Date().toISOString(),
        preview: preview.slice(0, 40),
        result: data,
      }, ...(prev || [])].slice(0, 6));
    } catch (err) {
      setError(err.message || 'Failed to generate. Try again.');
    }
  }, [customContext, scenario, relationship, comfort, landmines, callToolEndpoint]);

  const panicMode = useCallback(async () => {
    setPanicResult(null);
    setError('');
    setPanicLoading(true);

    const context = getContext();

    try {
      const data = await callToolEndpoint('awkward-silence-filler', {
        action: 'panic',
        context: context || 'general social situation',
        relationship: relationship || 'acquaintance',
      });
      setPanicResult(data);
    } catch (err) {
      setError(err.message || 'Panic mode failed. Deep breath.');
    }
    setPanicLoading(false);
  }, [customContext, scenario, relationship, callToolEndpoint]);

  const refresh = useCallback(async () => {
    if (results) await generate();
  }, [results, generate]);

  const handleReset = useCallback(() => {
    setScenario(''); setCustomContext(''); setRelationship('');
    setComfort('nervous'); setLandmines('');
    setResults(null); setPanicResult(null); setError('');
  }, []);

  // ─── Build copyable text ───
  const buildPanicText = useCallback(() => {
    if (!panicResult) return '';
    let text = `Say this: "${panicResult.line}"`;
    if (panicResult.they_say) text += `\nThey'll probably say: "${panicResult.they_say}"`;
    if (panicResult.follow_up) text += `\nThen you say: "${panicResult.follow_up}"`;
    if (panicResult.silence_ok) text += `\n\n${panicResult.silence_ok}`;
    return text + BRAND;
  }, [panicResult]);

  const buildChainText = useCallback((chain) => {
    let text = `[${chain.category}] You say: "${chain.opener}"`;
    if (chain.likely_response) text += `\nThey say: "${chain.likely_response}"`;
    if (chain.your_follow_up) text += `\nYou follow up: "${chain.your_follow_up}"`;
    if (chain.where_it_leads) text += `\n→ ${chain.where_it_leads}`;
    return text + BRAND;
  }, []);

  const buildFullText = useCallback(() => {
    if (!results) return '';
    let text = '💬 Awkward Silence Filler Results\n';
    if (results.silence_reframe) text += `\n${results.silence_reframe}\n`;
    if (results.read_the_room) text += `\nRead the Room: ${results.read_the_room}\n`;
    if (results.conversation_chains?.length) {
      text += '\n— Conversation Starters —\n';
      results.conversation_chains.forEach((ch, i) => {
        text += `\n${i + 1}. [${ch.category}] "${ch.opener}"`;
        if (ch.likely_response) text += `\n   They say: "${ch.likely_response}"`;
        if (ch.your_follow_up) text += `\n   You follow up: "${ch.your_follow_up}"`;
      });
    }
    if (results.exit_strategies?.length) {
      text += '\n\n— Graceful Exits —\n';
      results.exit_strategies.forEach(e => {
        text += `\n• ${e.scenario}: "${e.script}"`;
      });
    }
    if (results.encouragement) text += `\n\n💚 ${results.encouragement}`;
    return text + BRAND;
  }, [results]);

  const r = results;

  // ════════════════════════════════════════════════════════════
  // RENDER
  // ════════════════════════════════════════════════════════════
  return (
    <div className={`space-y-6 ${c.text}`}>

      {/* ── INPUT CARD ── */}
      <div className={`${c.card} border ${c.border} rounded-xl p-6`}>

        {/* Standard header */}
        <div className={`mb-5 pb-4 border-b ${c.border}`}>
          <h2 className={`text-xl font-bold ${c.text} flex items-center gap-2`}>
            <span>{tool.icon}</span> Awkward Silence Filler
          </h2>
          <p className={`text-sm ${c.textSecondary}`}>Context-smart things to say when conversation stalls</p>
        </div>

        {/* ── PANIC MODE BUTTON ── */}
        <div className="mb-5">
          <button
            onClick={panicMode}
            disabled={isRunning}
            className={`w-full ${c.btnPrimaryPanic} disabled:opacity-40 font-black py-3.5 px-6 rounded-xl flex items-center justify-center gap-2 text-base min-h-[52px] shadow-lg`}
          >
            {panicLoading ? (
              <><span className="animate-spin inline-block">{tool?.icon ?? '⚙️'}</span> Hold on...</>
            ) : (
              <>🚨 I'M IN AN AWKWARD SILENCE RIGHT NOW</>
            )}
          </button>
          <p className={`text-[10px] ${c.textMuteded} text-center mt-1`}>One tap, one line, no form required</p>
        </div>

        {/* ── PANIC RESULT ── */}
        {panicResult && (
          <div className={`mb-5 p-5 rounded-xl border-2 ${isDark ? 'bg-emerald-900/30 border-emerald-600' : 'bg-emerald-50 border-emerald-300'}`}>
            <p className={`text-[10px] font-bold uppercase ${c.success} mb-2`}>Say this right now:</p>
            <p className={`text-lg font-bold ${c.text} mb-3`}>"{panicResult.line}"</p>
            {panicResult.they_say && (
              <div className={`${isDark ? 'bg-zinc-700/50' : 'bg-white/70'} rounded-lg p-3 mb-2`}>
                <p className={`text-[10px] font-bold ${c.textMuteded} mb-1`}>They'll probably say:</p>
                <p className={`text-xs ${c.textSecondary} italic`}>"{panicResult.they_say}"</p>
              </div>
            )}
            {panicResult.follow_up && (
              <div className={`${c.chainBg} border rounded-lg p-3 mb-2`}>
                <p className={`text-[10px] font-bold ${c.textSecondary} mb-1`}>Then you say:</p>
                <p className={`text-sm font-semibold ${c.text}`}>"{panicResult.follow_up}"</p>
              </div>
            )}
            {panicResult.silence_ok && (
              <p className={`text-xs ${isDark ? 'text-emerald-300' : 'text-emerald-700'} italic mt-2`}>💚 {panicResult.silence_ok}</p>
            )}
            <div className="mt-3">
              <CopyBtn content={buildPanicText()} label="Copy opener" />
            </div>
          </div>
        )}

        <div className={`text-center ${c.textMuteded} text-xs mb-4`}>— or plan ahead —</div>

        {/* ── SCENARIO QUICK PICKS ── */}
        <div className="mb-4">
          <label className={`text-sm font-bold ${c.label} block mb-2`}>Quick scenario</label>
          <div className="flex flex-wrap gap-1.5">
            {SCENARIOS.map(s => (
              <button
                key={s.value}
                onClick={() => { setScenario(s.value === scenario ? '' : s.value); setCustomContext(''); }}
                className={`px-3 py-1.5 rounded-lg text-xs font-bold border transition-colors min-h-[32px] ${
                  scenario === s.value ? c.pillActive : c.pillInactive
                }`}
              >
                {s.emoji} {s.label}
              </button>
            ))}
          </div>
        </div>

        {/* ── CUSTOM CONTEXT ── */}
        <div className="mb-4">
          <label className={`text-sm font-bold ${c.label} block mb-1.5`}>
            Or describe the situation <span className={`font-normal ${c.textMuteded}`}>(optional if you picked a scenario)</span>
          </label>
          <input
            type="text"
            value={customContext}
            onChange={e => { setCustomContext(e.target.value); if (e.target.value.trim()) setScenario(''); }}
            onKeyDown={e => { if (e.key === 'Enter') generate(); }}
            placeholder="e.g., Stuck at coworker's BBQ, don't know anyone else"
            className={`w-full px-3 py-2.5 border rounded-lg text-sm ${c.input} outline-none focus:ring-2`}
          />
        </div>

        {/* ── RELATIONSHIP ── */}
        <div className="mb-4">
          <label className={`text-sm font-bold ${c.label} block mb-2`}>Who are you talking to?</label>
          <div className="flex flex-wrap gap-1.5">
            {RELATIONSHIPS.map(rel => (
              <button
                key={rel.value}
                onClick={() => setRelationship(rel.value === relationship ? '' : rel.value)}
                className={`px-3 py-1.5 rounded-lg text-xs font-bold border transition-colors min-h-[32px] ${
                  relationship === rel.value ? c.pillActive : c.pillInactive
                }`}
              >
                {rel.emoji} {rel.label}
              </button>
            ))}
          </div>
        </div>

        {/* ── COMFORT LEVEL ── */}
        <div className="mb-4">
          <label className={`text-sm font-bold ${c.label} block mb-2`}>How are you feeling?</label>
          <div className="flex gap-2">
            {COMFORT_LEVELS.map(cl => (
              <button
                key={cl.value}
                onClick={() => setComfort(cl.value)}
                className={`flex-1 py-2.5 rounded-lg text-[11px] font-bold border transition-colors min-h-[40px] flex flex-col items-center gap-0.5 ${
                  comfort === cl.value ? c.pillActive : c.pillInactive
                }`}
              >
                <span className="text-base">{cl.emoji}</span>
                {cl.label}
              </button>
            ))}
          </div>
        </div>

        {/* ── TOPIC LANDMINES ── */}
        <div className="mb-5">
          <label className={`text-sm font-bold ${c.label} block mb-1.5`}>
            Topic landmines <span className={`font-normal ${c.textMuteded}`}>(optional — things to avoid)</span>
          </label>
          <input
            type="text"
            value={landmines}
            onChange={e => setLandmines(e.target.value)}
            onKeyDown={e => { if (e.key === 'Enter') generate(); }}
            placeholder='e.g., "politics, their divorce, my job search, kids"'
            className={`w-full px-3 py-2.5 border rounded-lg text-sm ${c.input} outline-none focus:ring-2`}
          />
        </div>

        {/* Pre-result cross-ref */}
        <p className={`text-xs text-center ${c.textMuteded} mb-4`}>
          Planning a date?{' '}
          <a href="/DateNight" className={linkStyle}>Date Night Planner</a>{' '}
          finds the perfect spot first.
        </p>

        {/* ── SUBMIT / RESET ── */}
        <div className="flex gap-3">
          <button
            onClick={generate}
            disabled={loading || isRunning}
            className={`flex-1 ${c.btnPrimaryPrimary} disabled:opacity-40 disabled:cursor-not-allowed font-bold py-3 px-6 rounded-lg flex items-center justify-center gap-2 min-h-[48px]`}
          >
            {loading ? (
              <><span className="animate-spin inline-block">{tool?.icon ?? '⚙️'}</span> Generating...</>
            ) : (
              <><span>💬</span> Get Conversation Lines</>
            )}
          </button>
          {results && (
            <>
              <button
                onClick={refresh}
                disabled={isRunning}
                className={`px-4 py-3 ${c.btnPrimarySecondaryondary} rounded-lg min-h-[48px]`}
                title="Different suggestions"
              >
                <span className={isRunning ? 'animate-spin inline-block' : ''}>{tool?.icon ?? '⚙️'}</span>
              </button>
              <button
                onClick={handleReset}
                className={`px-4 py-3 ${c.btnPrimarySecondaryondary} rounded-lg font-bold min-h-[48px]`}
              >
                ↩ Reset
              </button>
            </>
          )}
        </div>
      </div>

      {/* ── ERROR ── */}
      {error && (
        <div className={`${c.danger} border rounded-lg p-4 flex items-start gap-3`}>
          <span className="flex-shrink-0 mt-0.5">⚠️</span>
          <p className="text-sm">{error}</p>
        </div>
      )}

      {/* ════════════════════════════════════════════════════════ */}
      {/* RESULTS                                                  */}
      {/* ════════════════════════════════════════════════════════ */}
      {r && (
        <div className="space-y-4">

          {/* ActionBar — top of results */}
          <div className="flex justify-end">
            <ActionBar content={buildFullText()} title="Awkward Silence Filler" />
          </div>

          {/* ── SILENCE REFRAME (prominent) ── */}
          {r.silence_reframe && (
            <div className={`${c.cardAlt} border rounded-xl p-5 flex items-start gap-3`}>
              <span className="flex-shrink-0 mt-0.5 text-lg">🔇</span>
              <div>
                <h3 className={`text-sm font-bold ${c.text} mb-1`}>First: Is This Silence Actually a Problem?</h3>
                <p className={`text-sm ${c.textSecondary}`}>{r.silence_reframe}</p>
              </div>
            </div>
          )}

          {/* ── READ THE ROOM ── */}
          {r.read_the_room && (
            <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
              <div className="flex items-start gap-2.5">
                <span className="flex-shrink-0 mt-0.5">👥</span>
                <div>
                  <p className={`text-xs font-bold ${c.label} mb-1`}>Read the Room</p>
                  <p className={`text-sm ${c.textSecondary}`}>{r.read_the_room}</p>
                </div>
              </div>
            </div>
          )}

          {/* ── CONVERSATION CHAINS ── */}
          {r.conversation_chains && r.conversation_chains.length > 0 && (
            <div className="space-y-3">
              <h3 className={`text-sm font-bold ${c.text} flex items-center gap-2`}>
                <span>💬</span> Conversation Starters
              </h3>
              {r.conversation_chains.map((chain, idx) => {
                const expanded = expandedSections[`chain-${idx}`];
                return (
                  <div key={idx} className={`${c.card} border ${c.border} rounded-xl overflow-hidden`}>
                    {/* Header */}
                    <div className="p-4">
                      <div className="flex items-start justify-between mb-1">
                        <span className={`text-[10px] font-bold uppercase ${c.textMuteded}`}>{chain.category}</span>
                        {chain.risk_level && (
                          <span className={`text-[9px] font-bold px-1.5 py-0.5 rounded ${
                            chain.risk_level === 'low' ? c.success
                              : chain.risk_level === 'high' ? c.danger
                              : c.warning
                          }`}>
                            {chain.risk_level === 'low' ? '🟢 Safe' : chain.risk_level === 'high' ? '🔴 Bold' : '🟡 Medium'}
                          </span>
                        )}
                      </div>

                      {/* YOUR OPENER */}
                      <div className="flex items-start gap-2 mt-2">
                        <div className={`flex-1 ${c.quoteBg} rounded-lg p-3`}>
                          <p className={`text-[10px] font-bold ${c.textSecondary} mb-1`}>You say:</p>
                          <p className={`text-sm font-semibold ${c.text}`}>"{chain.opener}"</p>
                        </div>
                        <CopyBtn content={`${chain.opener}${BRAND}`} label="Copy" />
                      </div>

                      {/* CONVERSATION CHAIN (collapsible) */}
                      {(chain.likely_response || chain.your_follow_up) && (
                        <button
                          onClick={() => toggleSection(`chain-${idx}`)}
                          className={`mt-2 text-[10px] font-bold ${c.textSecondary} flex items-center gap-1`}
                        >
                          <span>{expanded ? '▲' : '▼'}</span>
                          {expanded ? 'Hide conversation flow' : 'See how it plays out →'}
                        </button>
                      )}
                    </div>

                    {/* EXPANDED CHAIN */}
                    {expanded && (chain.likely_response || chain.your_follow_up) && (
                      <div className={`px-4 pb-4 border-t ${c.border} pt-3 space-y-2`}>
                        {chain.likely_response && (
                          <div className={`${isDark ? 'bg-zinc-700/50' : 'bg-gray-100'} rounded-lg p-3`}>
                            <p className={`text-[10px] font-bold ${c.textMuteded} mb-1`}>They'll probably say something like:</p>
                            <p className={`text-xs ${c.textSecondary} italic`}>"{chain.likely_response}"</p>
                          </div>
                        )}
                        <div className={`flex justify-center ${c.textMuteded}`}>
                          <span>↓</span>
                        </div>
                        {chain.your_follow_up && (
                          <div className="flex items-start gap-2">
                            <div className={`flex-1 ${c.chainBg} border rounded-lg p-3`}>
                              <p className={`text-[10px] font-bold ${c.textSecondary} mb-1`}>Then you say:</p>
                              <p className={`text-xs font-semibold ${c.text}`}>"{chain.your_follow_up}"</p>
                            </div>
                            <CopyBtn content={`${chain.your_follow_up}${BRAND}`} label="Copy" />
                          </div>
                        )}
                        {chain.where_it_leads && (
                          <p className={`text-[10px] ${c.textMuteded}`}>→ {chain.where_it_leads}</p>
                        )}
                        <div className="pt-1">
                          <CopyBtn content={buildChainText(chain)} label="Copy full chain" />
                        </div>
                      </div>
                    )}
                  </div>
                );
              })}
            </div>
          )}

          {/* ── BODY LANGUAGE ── */}
          {r.body_language && r.body_language.length > 0 && (
            <div className={`${c.card} border ${c.border} rounded-xl overflow-hidden`}>
              <button
                onClick={() => toggleSection('body')}
                className="w-full p-4 flex items-center justify-between text-left min-h-[44px]"
              >
                <h3 className={`text-sm font-bold ${c.text} flex items-center gap-2`}>
                  <span>🛡️</span> Body Language Tips
                </h3>
                <span className={c.textMuteded}>{expandedSections.body ? '▲' : '▼'}</span>
              </button>
              {expandedSections.body && (
                <div className={`px-4 pb-4 border-t ${c.border} pt-3 space-y-2`}>
                  {r.body_language.map((tip, i) => (
                    <p key={i} className={`text-xs ${c.textSecondary}`}>• {tip}</p>
                  ))}
                </div>
              )}
            </div>
          )}

          {/* ── EXIT STRATEGIES ── */}
          {r.exit_strategies && r.exit_strategies.length > 0 && (
            <div className={`${c.card} border ${c.border} rounded-xl overflow-hidden`}>
              <button
                onClick={() => toggleSection('exit')}
                className="w-full p-4 flex items-center justify-between text-left min-h-[44px]"
              >
                <h3 className={`text-sm font-bold ${c.text} flex items-center gap-2`}>
                  <span>🚪</span> Graceful Exits
                </h3>
                <span className={c.textMuteded}>{expandedSections.exit ? '▲' : '▼'}</span>
              </button>
              {expandedSections.exit && (
                <div className={`px-4 pb-4 border-t ${c.border} pt-3 space-y-3`}>
                  {r.exit_strategies.map((exit, i) => (
                    <div key={i} className="flex items-start gap-2">
                      <div className={`flex-1 ${c.quoteBg} rounded-lg p-3`}>
                        <p className={`text-[10px] font-bold ${c.textMuteded} mb-1`}>{exit.scenario}</p>
                        <p className={`text-xs ${c.text}`}>"{exit.script}"</p>
                      </div>
                      <CopyBtn content={`${exit.script}${BRAND}`} label="Copy" />
                    </div>
                  ))}
                </div>
              )}
            </div>
          )}

          {/* ── WHAT NOT TO SAY ── */}
          {r.what_not_to_say && r.what_not_to_say.length > 0 && (
            <div className={`${c.card} border ${c.border} rounded-xl overflow-hidden`}>
              <button
                onClick={() => toggleSection('avoid')}
                className="w-full p-4 flex items-center justify-between text-left min-h-[44px]"
              >
                <h3 className={`text-sm font-bold ${c.text} flex items-center gap-2`}>
                  <span>🚫</span> What NOT to Say
                </h3>
                <span className={c.textMuteded}>{expandedSections.avoid ? '▲' : '▼'}</span>
              </button>
              {expandedSections.avoid && (
                <div className={`px-4 pb-4 border-t ${c.border} pt-3 space-y-2`}>
                  {r.what_not_to_say.map((item, i) => (
                    <p key={i} className={`text-xs ${c.textSecondary}`}>⚠️ {item}</p>
                  ))}
                </div>
              )}
            </div>
          )}

          {/* ── ENCOURAGEMENT ── */}
          {r.encouragement && (
            <div className={`${c.success} border rounded-xl p-4 flex items-start gap-3`}>
              <span className={`flex-shrink-0 mt-0.5 ${c.success}`}>💚</span>
              <p className={`text-sm ${isDark ? 'text-emerald-300' : 'text-emerald-700'}`}>{r.encouragement}</p>
            </div>
          )}

          {/* Conditional cross-ref: panicking comfort → Argument Simulator for practice */}
          {comfort === 'panicking' && (
            <p className={`text-xs text-center ${c.textMuteded}`}>
              Want to practice staying calm under pressure?{' '}
              <a href="/ArgumentSimulator" className={linkStyle}>Argument Simulator</a>{' '}
              lets you rehearse any conversation scenario.
            </p>
          )}

          <p className={`text-[10px] ${c.textMuteded} text-center px-4`}>
            Every conversation is different. Use these as starting points, not scripts. It's okay to be quiet sometimes.
          </p>

          {/* Post-result cross-refs */}
          <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4 space-y-2`}>
            <p className={`text-xs font-semibold ${c.textMuteded} uppercase tracking-wider`}>You might also like</p>
            <div className="flex flex-wrap gap-x-4 gap-y-1">
              <a href="/DateNight" className={`text-xs ${linkStyle}`}>💘 Date Night Planner</a>
              <a href="/SayItRight" className={`text-xs ${linkStyle}`}>🗣️ Say It Right</a>
              <a href="/RoomReader" className={`text-xs ${linkStyle}`}>👀 Room Reader</a>
            </div>
          </div>
        </div>
      )}

      {/* ── HISTORY ── */}
      {history?.length > 0 && (
        <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
          <h3 className={`text-sm font-bold ${c.text} mb-3`}>🕐 Recent Sessions</h3>
          <div className="space-y-1.5">
            {history.map(entry => (
              <button key={entry.id}
                onClick={() => setResults(entry.result)}
                className={`w-full text-left px-3 py-2 rounded-lg ${c.btnPrimarySecondaryondary} text-xs flex items-center gap-2`}
              >
                <span className={c.textMuteded}>
                  {new Date(entry.timestamp).toLocaleDateString(undefined, { month: 'short', day: 'numeric', hour: 'numeric', minute: '2-digit' })}
                </span>
                <span className={`${c.text} truncate`}>{entry.preview}{entry.preview?.length >= 40 ? '…' : ''}</span>
              </button>
            ))}
          </div>
        </div>
      )}

        <div className={`mt-6 pt-4 border-t text-sm ${c.border} ${c.textMuted}`}>
          <p className="mb-2 font-medium">You might also like:</p>
          <div className="flex flex-wrap gap-2">
            {[{slug:'comeback-cooker',label:'🥊 Comeback Cooker'},{slug:'conflict-coach',label:'🤝 Conflict Coach'},{slug:'say-it-right',label:'🗣️ Say It Right'}].map(({slug,label})=>(
              <a key={slug} href={`${slug}`} className={linkStyle}>{label}</a>
            ))}
          </div>
        </div>
    </div>
  );
};

AwkwardSilenceFiller.displayName = 'AwkwardSilenceFiller';
export default AwkwardSilenceFiller;
