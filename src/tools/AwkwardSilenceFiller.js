import React, { useState, useEffect, useRef, useCallback } from 'react';
import { useRegisterActions } from '../components/ActionBarContext';
import { usePersistentState } from '../hooks/usePersistentState';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { useTranslation } from '../i18n/useTranslation';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

// ════════════════════════════════════════════════════════════
// CONSTANTS (labels resolved at render time via t())
// ════════════════════════════════════════════════════════════
const SCENARIOS = [
  { value: 'elevator_boss', labelKey: 'asf_sc_elevator_boss', emoji: '🏢' },
  { value: 'first_date', labelKey: 'asf_sc_first_date', emoji: '💘' },
  { value: 'uber', labelKey: 'asf_sc_uber', emoji: '🚗' },
  { value: 'hairdresser', labelKey: 'asf_sc_hairdresser', emoji: '💇' },
  { value: 'friend_of_friend', labelKey: 'asf_sc_friend_of_friend', emoji: '🍽️' },
  { value: 'networking', labelKey: 'asf_sc_networking', emoji: '🤝' },
  { value: 'family_gathering', labelKey: 'asf_sc_family_gathering', emoji: '👨‍👩‍👧‍👦' },
  { value: 'waiting_room', labelKey: 'asf_sc_waiting_room', emoji: '🪑' },
  { value: 'neighbors', labelKey: 'asf_sc_neighbors', emoji: '🏡' },
  { value: 'work_lunch', labelKey: 'asf_sc_work_lunch', emoji: '🥗' },
];

const RELATIONSHIPS = [
  { value: 'stranger', labelKey: 'asf_rel_stranger', emoji: '👤' },
  { value: 'acquaintance', labelKey: 'asf_rel_acquaintance', emoji: '🙂' },
  { value: 'coworker', labelKey: 'asf_rel_coworker', emoji: '💼' },
  { value: 'boss', labelKey: 'asf_rel_boss', emoji: '👔' },
  { value: 'date', labelKey: 'asf_rel_date', emoji: '💕' },
  { value: 'in_laws', labelKey: 'asf_rel_in_laws', emoji: '👪' },
  { value: 'old_friend', labelKey: 'asf_rel_old_friend', emoji: '🫂' },
  { value: 'new_friend', labelKey: 'asf_rel_new_friend', emoji: '✨' },
];

const COMFORT_LEVELS = [
  { value: 'panicking', labelKey: 'asf_cf_panicking', emoji: '😰' },
  { value: 'nervous', labelKey: 'asf_cf_nervous', emoji: '😬' },
  { value: 'slightly_awkward', labelKey: 'asf_cf_slightly_awkward', emoji: '😅' },
  { value: 'fine', labelKey: 'asf_cf_fine', emoji: '🙂' },
];

// ════════════════════════════════════════════════════════════
// COMPONENT
// ════════════════════════════════════════════════════════════
const AwkwardSilenceFiller = ({ tool }) => {
  const { callToolEndpoint, loading, userLocale, userCurrency, userRegion } = useClaudeAPI();
  const { isDark } = useTheme();
  const { t } = useTranslation();

  // Example seeds — scenario text localized via t().
  const EXAMPLES = [
    {
      scenario: t('asf_ex1_scenario'),
      customContext: '',
      relationship: 'date',
      comfort: 'nervous',
    },
    {
      scenario: t('asf_ex2_scenario'),
      customContext: t('asf_ex2_context'),
      relationship: 'boss',
      comfort: 'panicking',
    },
  ];

  // ─── Theme-aware colors ───
  const c = {
    card:          isDark ? 'bg-zinc-800'      : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50'   : 'bg-slate-50',
    text:          isDark ? 'text-zinc-50'     : 'text-slate-900',
    textSecondary: isDark ? 'text-zinc-400'    : 'text-slate-600',
    textMuted:     isDark ? 'text-zinc-500'    : 'text-slate-500',
    label:         isDark ? 'text-zinc-300'    : 'text-slate-700',
    input:         isDark
      ? 'bg-zinc-900 border-zinc-600 text-zinc-50 placeholder:text-zinc-500 focus:border-cyan-500 focus:ring-cyan-500/20'
      : 'bg-white border-slate-300 text-slate-900 placeholder:text-slate-400 focus:border-cyan-500 focus:ring-cyan-500/20',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white' : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnPanic:      isDark ? 'bg-red-600 hover:bg-red-500 text-white'      : 'bg-red-600 hover:bg-red-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-100' : 'bg-slate-100 hover:bg-slate-200 text-slate-800',
    border:        isDark ? 'border-zinc-700'  : 'border-slate-200',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700/50 text-emerald-200' : 'bg-emerald-50 border-emerald-200 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700/50 text-amber-200'   : 'bg-amber-50 border-amber-200 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700/50 text-red-200'         : 'bg-red-50 border-red-200 text-red-800',
    pillActive:    isDark ? 'bg-cyan-600 border-cyan-500 text-white'       : 'bg-cyan-600 border-cyan-600 text-white',
    pillInactive:  isDark ? 'bg-zinc-700 border-zinc-600 text-zinc-300 hover:border-zinc-500' : 'bg-white border-slate-200 text-slate-700 hover:border-slate-300',
    quoteBg:       isDark ? 'bg-zinc-900/60'   : 'bg-slate-50',
    chainBg:       isDark ? 'bg-zinc-900/40 border-zinc-700' : 'bg-emerald-50/50 border-emerald-100',
    required:      isDark ? 'text-amber-400'   : 'text-amber-500',
  };
  // Alias for common typo
  c.textMuteded = c.textMuted;

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';

  // ─── State (useState before usePersistentState — PF-11/PF-14) ───
  const [relationship, setRelationship] = useState('');
  const [comfort, setComfort] = useState('nervous');
  const [landmines, setLandmines] = useState('');
  const [error, setError] = useState('');
  const [expandedSections, setExpandedSections] = useState({});
  const [panicLoading, setPanicLoading] = useState(false);

  // ─── Persistent state ───
  const [scenario, setScenario] = usePersistentState('awkwardsilencefiller-scenario', '');
  const [customContext, setCustomContext] = usePersistentState('awkwardsilencefiller-context', '');
  const [results, setResults] = usePersistentState('awkwardsilencefiller-last', null);
  const [panicResult, setPanicResult] = usePersistentState('awkwardsilencefiller-panic', null);
  const [sessionHistory, setSessionHistory] = usePersistentState('awkwardsilencefiller-history', []);

  // ─── Refs for keyboard handler ───
  const loadExample = () => {
    const ex = EXAMPLES[Math.floor(Math.random() * EXAMPLES.length)];
    setScenario(ex.scenario);
    setCustomContext(ex.customContext);
    setRelationship(ex.relationship);
    setComfort(ex.comfort);
  };

  const generateRef = useRef(null);
  const canGenerateRef = useRef(false);

  // ─── Helpers ───
  const toggleSection = useCallback((key) => {
    setExpandedSections(p => ({ ...p, [key]: !p[key] }));
  }, []);

  const isRunning = loading || panicLoading;

  // ─── Context helper (shared between panic + full) ───
  const getContext = () => {
    const sc = SCENARIOS.find(s => s.value === scenario);
    return customContext.trim() || (sc ? t(sc.labelKey) : '') || '';
  };

  // ─── Actions ───
  const generate = useCallback(async () => {
    setError('');
    setResults(null);
    setPanicResult(null);

    const context = getContext();

    try {
      const data = await callToolEndpoint('awkward-silence-filler', {
        action: 'full',
        context: context || 'general social situation',
        scenario,
        relationship: relationship || 'acquaintance',
        comfort: comfort || 'nervous',
        landmines: landmines.trim() || null,
        userLocale, userCurrency, userRegion,
      });
      setResults(data);
      setExpandedSections({});

      const sc = SCENARIOS.find(s => s.value === scenario);
      const preview = (customContext.trim() || (sc ? t(sc.labelKey) : '') || context).slice(0, 6);
      setSessionHistory(prev => [{
        id: Date.now(),
        timestamp: new Date().toISOString(),
        // PF-25 exception: 40-char preview-text truncation; session history is capped at 6.
        preview: preview.slice(0, 40),
        result: data,
      }, ...(prev || [])].slice(0, 6));
    } catch (err) {
      setError(err.message || t('asf_err_generate'));
    }
  }, [customContext, scenario, relationship, comfort, landmines, callToolEndpoint, setResults, setSessionHistory, userLocale, userCurrency, userRegion, t]);

  const panicMode = useCallback(async () => {
    setPanicResult(null);
    setError('');
    setPanicLoading(true);

    const context = getContext();

    try {
      const data = await callToolEndpoint('awkward-silence-filler', {
        action: 'panic',
        context: context || 'general social situation',
        relationship: relationship || 'acquaintance',
        userLocale, userCurrency, userRegion,
      });
      setPanicResult(data);
    } catch (err) {
      setError(err.message || t('asf_err_panic'));
    }
    setPanicLoading(false);
  }, [customContext, scenario, relationship, callToolEndpoint, setPanicResult, userLocale, userCurrency, userRegion, t]);

  const refresh = useCallback(async () => {
    if (results) await generate();
  }, [results, generate]);

  const handleReset = useCallback(() => {
    setScenario(''); setCustomContext(''); setRelationship('');
    setComfort('nervous'); setLandmines('');
    setResults(null); setPanicResult(null); setError('');
  }, []);

  // ─── Build copyable text ───
  const buildPanicText = useCallback(() => {
    if (!panicResult) return '';
    let text = `${t('asf_copy_say_this')} "${panicResult.line}"`;
    if (panicResult.they_say) text += `\n${t('asf_panic_they_say')} "${panicResult.they_say}"`;
    if (panicResult.follow_up) text += `\n${t('asf_panic_then_you')} "${panicResult.follow_up}"`;
    if (panicResult.silence_ok) text += `\n\n${panicResult.silence_ok}`;
    return text + BRAND;
  }, [panicResult, t]);

  const buildFullText = useCallback(() => {
    if (!results) return '';
    let text = `💬 ${t('asf_copy_results')}\n`;
    if (results?.silence_reframe) text += `\n${results?.silence_reframe}\n`;
    if (results?.read_the_room) text += `\n${t('asf_copy_read_room')} ${results?.read_the_room}\n`;
    if (results?.conversation_chains?.length) {
      text += `\n${t('asf_copy_starters')}\n`;
      results?.conversation_chains?.forEach((ch, i) => {
        text += `\n${i + 1}. [${ch.category}] "${ch.opener}"`;
        if (ch.likely_response) text += `\n   ${t('asf_copy_they_say')} "${ch.likely_response}"`;
        if (ch.your_follow_up) text += `\n   ${t('asf_copy_you_follow')} "${ch.your_follow_up}"`;
      });
    }
    if (results?.exit_strategies?.length) {
      text += `\n\n${t('asf_copy_exits')}\n`;
      results?.exit_strategies?.forEach(e => {
        text += `\n• ${e.scenario}: "${e.script}"`;
      });
    }
    if (results?.encouragement) text += `\n\n💚 ${results?.encouragement}`;
    return text + BRAND;
  }, [results, t]);

  // ─── Register actions for the global ActionBar (header) ───
  useRegisterActions(panicResult ? buildPanicText() : buildFullText(), tool?.title || 'Awkward Silence Filler');

  // ─── Ref assignments every render (for keyboard handler routing) ───
  generateRef.current = generate;
  canGenerateRef.current = Boolean(getContext().trim()) && !isRunning;

  // ─── Global ⌘/Ctrl+Enter keyboard handler ───
  useEffect(() => {
    const handler = (e) => {
      const tag = document.activeElement?.tagName;
      if (tag === 'SELECT') return;
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && canGenerateRef.current) {
        e.preventDefault();
        generateRef.current?.();
      }
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
  }, []);

  const r = results;

  // ════════════════════════════════════════════════════════════
  // RENDER
  // ════════════════════════════════════════════════════════════
  return (
    <div className={`space-y-4 ${c.text}`}>

      {/* ── INPUT CARD ── */}
      <div className={`${c.card} border ${c.border} rounded-xl p-6`}>

        {/* Standard header */}
        <div className="flex items-start justify-between gap-3 mb-5 pb-4 border-b border-zinc-500">
          <div className="flex-1 min-w-0">
            <h2 className={`text-xl font-bold ${c.text} flex items-center gap-2`}>
              <span className="mr-2">{tool?.icon ?? '💬'}</span>{tool?.title ?? t('asf_title')}
            </h2>
            <p className={`text-sm ${c.textSecondary} mt-1`}>{tool?.tagline ?? t('asf_tagline')}</p>
            <button onClick={loadExample} disabled={loading} style={{ backgroundColor: (tool?.headerColor ?? '#888888') + '80' }} className={`mt-1.5 px-2.5 py-0.5 rounded-full text-xs font-medium border disabled:opacity-40 ${isDark ? 'text-white border-white/40' : 'text-gray-800 border-transparent'}`}>{t('try_example')}</button>
          </div>
          {(results || panicResult || customContext.trim() || scenario || landmines.trim()) ? (
            <button
              onClick={handleReset}
              className={`shrink-0 px-3 py-2 rounded-lg text-sm font-bold min-h-[40px] ${c.btnSecondary}`}
            >↺ {t('start_over')}</button>
          ) : null}
        </div>

        {/* ── PANIC MODE BUTTON ── */}
        <div className="mb-5">
          <button
            onClick={panicMode}
            disabled={isRunning}
            className={`w-full ${c.btnPanic} disabled:opacity-40 font-black py-3.5 px-6 rounded-xl flex items-center justify-center gap-2 text-base min-h-[52px] shadow-lg`}
          >
            {panicLoading ? (
              <><span className="animate-spin inline-block">{tool?.icon ?? '💬'}</span> {t('asf_panic_hold')}</>
            ) : (
              <>{t('asf_panic_btn')}</>
            )}
          </button>
          <p className={`text-[10px] ${c.textMuteded} text-center mt-1`}>{t('asf_panic_hint')}</p>
        </div>

        {/* ── PANIC RESULT ── */}
        {panicResult && (
          <div className={`mb-5 p-5 rounded-xl border-2 ${isDark ? 'bg-emerald-900/30 border-emerald-600' : 'bg-emerald-50 border-emerald-300'}`}>
            <p className={`text-[10px] font-bold uppercase ${c.success} mb-2`}>{t('asf_panic_say_now')}</p>
            <p className={`text-lg font-bold ${c.text} mb-3`}>"{panicResult.line}"</p>
            {panicResult.they_say && (
              <div className={`${isDark ? 'bg-zinc-700/50' : 'bg-white/70'} rounded-lg p-3 mb-2`}>
                <p className={`text-[10px] font-bold ${c.textMuteded} mb-1`}>{t('asf_panic_they_say')}</p>
                <p className={`text-xs ${c.textSecondary} italic`}>"{panicResult.they_say}"</p>
              </div>
            )}
            {panicResult.follow_up && (
              <div className={`${c.chainBg} border rounded-lg p-3 mb-2`}>
                <p className={`text-[10px] font-bold ${c.textSecondary} mb-1`}>{t('asf_panic_then_you')}</p>
                <p className={`text-sm font-semibold ${c.text}`}>"{panicResult.follow_up}"</p>
              </div>
            )}
            {panicResult.silence_ok && (
              <p className={`text-xs ${isDark ? 'text-emerald-300' : 'text-emerald-700'} italic mt-2`}>💚 {panicResult.silence_ok}</p>
            )}
          </div>
        )}

        <div className={`text-center ${c.textMuteded} text-xs mb-4`}>{t('asf_plan_ahead')}</div>

        {/* ── SCENARIO QUICK PICKS ── */}
        <div className="mb-4">
          <label className={`text-sm font-bold ${c.label} block mb-2`}>{t('asf_quick_scenario')}</label>
          <div className="flex flex-wrap gap-1.5">
            {SCENARIOS.map(s => (
              <button
                key={s.value}
                onClick={() => { setScenario(s.value === scenario ? '' : s.value); setCustomContext(''); }}
                className={`px-3 py-1.5 rounded-lg text-xs font-bold border transition-colors min-h-[32px] ${
                  scenario === s.value ? c.pillActive : c.pillInactive
                }`}
              >
                {s.emoji} {t(s.labelKey)}
              </button>
            ))}
          </div>
        </div>

        {/* ── CUSTOM CONTEXT ── */}
        <div className="mb-4">
          <label className={`text-sm font-bold ${c.label} block mb-1.5`}>
            {t('asf_describe_label')} <span className={`font-normal ${c.textMuteded}`}>{t('asf_describe_optional')}</span>
          </label>
          <input
            type="text"
            value={customContext}
            onChange={e => { setCustomContext(e.target.value); if (e.target.value.trim()) setScenario(''); }}
            onKeyDown={e => { if (e.key === 'Enter') generate(); }}
            placeholder={t('asf_describe_ph')}
            className={`w-full px-3 py-2.5 border rounded-lg text-sm ${c.input} outline-none focus:ring-2`}
          />
        </div>

        {/* ── RELATIONSHIP ── */}
        <div className="mb-4">
          <label className={`text-sm font-bold ${c.label} block mb-2`}>{t('asf_who_label')}</label>
          <div className="flex flex-wrap gap-1.5">
            {RELATIONSHIPS.map(rel => (
              <button
                key={rel.value}
                onClick={() => setRelationship(rel.value === relationship ? '' : rel.value)}
                className={`px-3 py-1.5 rounded-lg text-xs font-bold border transition-colors min-h-[32px] ${
                  relationship === rel.value ? c.pillActive : c.pillInactive
                }`}
              >
                {rel.emoji} {t(rel.labelKey)}
              </button>
            ))}
          </div>
        </div>

        {/* ── COMFORT LEVEL ── */}
        <div className="mb-4">
          <label className={`text-sm font-bold ${c.label} block mb-2`}>{t('asf_feeling_label')}</label>
          <div className="flex gap-2">
            {COMFORT_LEVELS.map(cl => (
              <button
                key={cl.value}
                onClick={() => setComfort(cl.value)}
                className={`flex-1 py-2.5 rounded-lg text-[11px] font-bold border transition-colors min-h-[40px] flex flex-col items-center gap-0.5 ${
                  comfort === cl.value ? c.pillActive : c.pillInactive
                }`}
              >
                <span className="text-base">{cl.emoji}</span>
                {t(cl.labelKey)}
              </button>
            ))}
          </div>
        </div>

        {/* ── TOPIC LANDMINES ── */}
        <div className="mb-5">
          <label className={`text-sm font-bold ${c.label} block mb-1.5`}>
            {t('asf_landmines_label')} <span className={`font-normal ${c.textMuteded}`}>{t('asf_landmines_optional')}</span>
          </label>
          <input
            type="text"
            value={landmines}
            onChange={e => setLandmines(e.target.value)}
            onKeyDown={e => { if (e.key === 'Enter') generate(); }}
            placeholder={t('asf_landmines_ph')}
            className={`w-full px-3 py-2.5 border rounded-lg text-sm ${c.input} outline-none focus:ring-2`}
          />
        </div>

        {/* Pre-result cross-ref */}
        <p className={`text-xs text-center ${c.textMuteded} mb-4`}>
          {t('asf_pre_xref_q')}{' '}
          <a href="/DateNight" className={linkStyle}>{t('asf_xref_datenight')}</a>{' '}
          {t('asf_pre_xref_tail')}
        </p>

        {/* ── SUBMIT / REFRESH ── */}
        <div className="flex gap-3">
          <button
            onClick={generate}
            disabled={loading || isRunning}
            className={`flex-1 ${c.btnPrimary} disabled:opacity-40 disabled:cursor-not-allowed font-bold py-3 px-6 rounded-lg flex items-center justify-center gap-2 min-h-[48px]`}
          >
            {loading ? (
              <><span className="animate-spin inline-block">{tool?.icon ?? '💬'}</span> {t('asf_generating')}</>
            ) : (
              <><span>{tool?.icon ?? '💬'}</span> {t('asf_generate_btn')}</>
            )}
          </button>
          {results && (
            <button
              onClick={refresh}
              disabled={isRunning}
              className={`px-4 py-3 ${c.btnSecondary} rounded-lg min-h-[48px]`}
              title={t('asf_refresh_title')}
            >
              <span className={isRunning ? 'animate-spin inline-block' : ''}>{tool?.icon ?? '💬'}</span>
            </button>
          )}
        </div>

      </div>

      {/* ── ERROR ── */}
      {error && (
        <div className={`${c.danger} border rounded-lg p-4 flex items-start gap-3`}>
          <span className="flex-shrink-0 mt-0.5">⚠️</span>
          <p className="text-sm">{error}</p>
        </div>
      )}

      {/* ════════════════════════════════════════════════════════ */}
      {/* RESULTS                                                  */}
      {/* ════════════════════════════════════════════════════════ */}
      {r && (
        <div className="space-y-4">

          {/* ── SILENCE REFRAME (prominent) ── */}
          {r.silence_reframe && (
            <div className={`${c.cardAlt} border rounded-xl p-5 flex items-start gap-3`}>
              <span className="flex-shrink-0 mt-0.5 text-lg">🔇</span>
              <div>
                <h3 className={`text-sm font-bold ${c.text} mb-1`}>{t('asf_reframe_title')}</h3>
                <p className={`text-sm ${c.textSecondary}`}>{r.silence_reframe}</p>
              </div>
            </div>
          )}

          {/* ── READ THE ROOM ── */}
          {r.read_the_room && (
            <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
              <div className="flex items-start gap-2.5">
                <span className="flex-shrink-0 mt-0.5">👥</span>
                <div>
                  <p className={`text-xs font-bold ${c.label} mb-1`}>{t('asf_read_room')}</p>
                  <p className={`text-sm ${c.textSecondary}`}>{r.read_the_room}</p>
                </div>
              </div>
            </div>
          )}

          {/* ── CONVERSATION CHAINS ── */}
          {r.conversation_chains && r.conversation_chains.length > 0 && (
            <div className="space-y-3">
              <h3 className={`text-sm font-bold ${c.text} flex items-center gap-2`}>
                <span>💬</span> {t('asf_starters_title')}
              </h3>
              {r.conversation_chains.map((chain, idx) => {
                const expanded = expandedSections[`chain-${idx}`];
                return (
                  <div key={idx} className={`${c.card} border ${c.border} rounded-xl overflow-hidden`}>
                    {/* Header */}
                    <div className="p-4">
                      <div className="flex items-start justify-between mb-1">
                        <span className={`text-[10px] font-bold uppercase ${c.textMuteded}`}>{chain.category}</span>
                        {chain.risk_level && (
                          <span className={`text-[9px] font-bold px-1.5 py-0.5 rounded ${
                            chain.risk_level === 'low' ? c.success
                              : chain.risk_level === 'high' ? c.danger
                              : c.warning
                          }`}>
                            {chain.risk_level === 'low' ? t('asf_risk_safe') : chain.risk_level === 'high' ? t('asf_risk_bold') : t('asf_risk_medium')}
                          </span>
                        )}
                      </div>

                      {/* YOUR OPENER */}
                      <div className="flex items-start gap-2 mt-2">
                        <div className={`flex-1 ${c.quoteBg} rounded-lg p-3`}>
                          <p className={`text-[10px] font-bold ${c.textSecondary} mb-1`}>{t('asf_you_say')}</p>
                          <p className={`text-sm font-semibold ${c.text}`}>"{chain.opener}"</p>
                        </div>
                      </div>

                      {/* CONVERSATION CHAIN (collapsible) */}
                      {(chain.likely_response || chain.your_follow_up) && (
                        <button
                          onClick={() => toggleSection(`chain-${idx}`)}
                          className={`mt-2 text-[10px] font-bold ${c.textSecondary} flex items-center gap-1`}
                        >
                          <span>{expanded ? '▲' : '▼'}</span>
                          {expanded ? t('asf_hide_flow') : t('asf_see_flow')}
                        </button>
                      )}
                    </div>

                    {/* EXPANDED CHAIN */}
                    {expanded && (chain.likely_response || chain.your_follow_up) && (
                      <div className={`px-4 pb-4 border-t ${c.border} pt-3 space-y-2`}>
                        {chain.likely_response && (
                          <div className={`${isDark ? 'bg-zinc-700/50' : 'bg-gray-100'} rounded-lg p-3`}>
                            <p className={`text-[10px] font-bold ${c.textMuteded} mb-1`}>{t('asf_they_say_like')}</p>
                            <p className={`text-xs ${c.textSecondary} italic`}>"{chain.likely_response}"</p>
                          </div>
                        )}
                        <div className={`flex justify-center ${c.textMuteded}`}>
                          <span>↓</span>
                        </div>
                        {chain.your_follow_up && (
                          <div className="flex items-start gap-2">
                            <div className={`flex-1 ${c.chainBg} border rounded-lg p-3`}>
                              <p className={`text-[10px] font-bold ${c.textSecondary} mb-1`}>{t('asf_then_you_say')}</p>
                              <p className={`text-xs font-semibold ${c.text}`}>"{chain.your_follow_up}"</p>
                            </div>
                          </div>
                        )}
                        {chain.where_it_leads && (
                          <p className={`text-[10px] ${c.textMuteded}`}>→ {chain.where_it_leads}</p>
                        )}
                      </div>
                    )}
                  </div>
                );
              })}
            </div>
          )}

          {/* ── BODY LANGUAGE ── */}
          {r.body_language && r.body_language.length > 0 && (
            <div className={`${c.card} border ${c.border} rounded-xl overflow-hidden`}>
              <button
                onClick={() => toggleSection('body')}
                className="w-full p-4 flex items-center justify-between text-left min-h-[44px]"
              >
                <h3 className={`text-sm font-bold ${c.text} flex items-center gap-2`}>
                  <span>🛡️</span> {t('asf_body_title')}
                </h3>
                <span className={c.textMuteded}>{expandedSections.body ? '▲' : '▼'}</span>
              </button>
              {expandedSections.body && (
                <div className={`px-4 pb-4 border-t ${c.border} pt-3 space-y-2`}>
                  {r.body_language.map((tip, i) => (
                    <p key={i} className={`text-xs ${c.textSecondary}`}>• {tip}</p>
                  ))}
                </div>
              )}
            </div>
          )}

          {/* ── EXIT STRATEGIES ── */}
          {r.exit_strategies && r.exit_strategies.length > 0 && (
            <div className={`${c.card} border ${c.border} rounded-xl overflow-hidden`}>
              <button
                onClick={() => toggleSection('exit')}
                className="w-full p-4 flex items-center justify-between text-left min-h-[44px]"
              >
                <h3 className={`text-sm font-bold ${c.text} flex items-center gap-2`}>
                  <span>🚪</span> {t('asf_exits_title')}
                </h3>
                <span className={c.textMuteded}>{expandedSections.exit ? '▲' : '▼'}</span>
              </button>
              {expandedSections.exit && (
                <div className={`px-4 pb-4 border-t ${c.border} pt-3 space-y-3`}>
                  {r.exit_strategies.map((exit, i) => (
                    <div key={i} className="flex items-start gap-2">
                      <div className={`flex-1 ${c.quoteBg} rounded-lg p-3`}>
                        <p className={`text-[10px] font-bold ${c.textMuteded} mb-1`}>{exit.scenario}</p>
                        <p className={`text-xs ${c.text}`}>"{exit.script}"</p>
                      </div>
                    </div>
                  ))}
                </div>
              )}
            </div>
          )}

          {/* ── WHAT NOT TO SAY ── */}
          {r.what_not_to_say && r.what_not_to_say.length > 0 && (
            <div className={`${c.card} border ${c.border} rounded-xl overflow-hidden`}>
              <button
                onClick={() => toggleSection('avoid')}
                className="w-full p-4 flex items-center justify-between text-left min-h-[44px]"
              >
                <h3 className={`text-sm font-bold ${c.text} flex items-center gap-2`}>
                  <span>🚫</span> {t('asf_not_to_say_title')}
                </h3>
                <span className={c.textMuteded}>{expandedSections.avoid ? '▲' : '▼'}</span>
              </button>
              {expandedSections.avoid && (
                <div className={`px-4 pb-4 border-t ${c.border} pt-3 space-y-2`}>
                  {r.what_not_to_say.map((item, i) => (
                    <p key={i} className={`text-xs ${c.textSecondary}`}>⚠️ {item}</p>
                  ))}
                </div>
              )}
            </div>
          )}

          {/* ── ENCOURAGEMENT ── */}
          {r.encouragement && (
            <div className={`${c.success} border rounded-xl p-4 flex items-start gap-3`}>
              <span className={`flex-shrink-0 mt-0.5 ${c.success}`}>💚</span>
              <p className={`text-sm ${isDark ? 'text-emerald-300' : 'text-emerald-700'}`}>{r.encouragement}</p>
            </div>
          )}

          {/* Conditional cross-ref: panicking comfort → Argument Simulator for practice */}
          {comfort === 'panicking' && (
            <p className={`text-xs text-center ${c.textMuteded}`}>
              {t('asf_panic_xref_q')}{' '}
              <a href="/ArgueBetter" className={linkStyle}>{t('asf_xref_argsim')}</a>{' '}
              {t('asf_panic_xref_tail')}
            </p>
          )}

          <p className={`text-[10px] ${c.textMuteded} text-center px-4`}>
            {t('asf_footer_note')}
          </p>

          {/* Post-result cross-refs */}
          <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4 space-y-2`}>
            <p className={`text-xs font-semibold ${c.textMuteded} uppercase tracking-wider`}>{t('asf_also_like')}</p>
            <div className="flex flex-wrap gap-x-4 gap-y-1">
              <a href="/DateNight" className={`text-xs ${linkStyle}`}>💘 {t('asf_xref_datenight')}</a>
              <a href="/PronounceItRight" className={`text-xs ${linkStyle}`}>🗣️ {t('asf_xref_pronounce')}</a>
              <a href="/RoomReader" className={`text-xs ${linkStyle}`}>👀 {t('asf_xref_roomreader')}</a>
            </div>
          </div>
        </div>
      )}

      {/* ── HISTORY ── */}
      {sessionHistory?.length > 0 && (
        <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
          <h3 className={`text-sm font-bold ${c.text} mb-3`}>🕐 {t('asf_recent_sessions')}</h3>
          <div className="space-y-1.5">
            {sessionHistory.map(entry => (
              <button key={entry.id}
                onClick={() => setResults(entry.result)}
                className={`w-full text-left px-3 py-2 rounded-lg ${c.btnSecondary} text-xs flex items-center gap-2`}
              >
                <span className={c.textMuteded}>
                  {new Date(entry.timestamp).toLocaleDateString(undefined, { month: 'short', day: 'numeric', hour: 'numeric', minute: '2-digit' })}
                </span>
                <span className={`${c.text} truncate`}>{entry.preview}{entry.preview?.length >= 40 ? '…' : ''}</span>
              </button>
            ))}
          </div>
        </div>
      )}
    </div>
  );
};

AwkwardSilenceFiller.displayName = 'AwkwardSilenceFiller';
export default AwkwardSilenceFiller;
