import React, { useState, useEffect, useRef, useCallback } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { usePersistentState } from '../hooks/usePersistentState';
import { useRegisterActions } from '../components/ActionBarContext';
import { useTranslation } from '../i18n/useTranslation';
import { currencySymbol } from '../utils/formatLocale';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';
const PREVIEW_CHARS = 40; // chars of the product kept for the history-row label

const MarkupDetective = ({ tool }) => {
  const { callToolEndpoint, loading, userLocale, userCurrency, userRegion } = useClaudeAPI();
  const { isDark } = useTheme();
  const { t } = useTranslation();
  const sym = currencySymbol(userLocale, userCurrency);

  const c = {
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:         isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20'
                          : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-500' : 'text-gray-400',
    labelText:     isDark ? 'text-zinc-200' : 'text-gray-700',
    accentTxt:     isDark ? 'text-cyan-400' : 'text-cyan-600',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white'
                          : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200'
                          : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200'
                          : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200'
                          : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200'
                          : 'bg-red-50 border-red-200 text-red-800',
    successTxt:    isDark ? 'text-emerald-300' : 'text-emerald-800',
    warningTxt:    isDark ? 'text-amber-300' : 'text-amber-800',
    pillActive:    isDark ? 'border-cyan-500 bg-cyan-900/30 text-cyan-200'
                          : 'border-cyan-600 bg-cyan-100 text-cyan-900',
    pillInactive:  isDark ? 'border-zinc-600 text-zinc-400 hover:border-zinc-500'
                          : 'border-gray-300 text-gray-500 hover:border-gray-400',
    required:      isDark ? 'text-amber-400' : 'text-amber-500',
    // ─── Chart legend colors ───
    chartMaterials: isDark ? 'bg-emerald-500' : 'bg-emerald-400',
    chartLabor:     isDark ? 'bg-sky-500'     : 'bg-sky-400',
    chartBrand:     isDark ? 'bg-amber-500'   : 'bg-amber-400',
    chartProfit:    isDark ? 'bg-red-500'     : 'bg-red-400',
  };
  c.textMuteded = c.textMuted;
  c.label = c.labelText;

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';

  // ── State ──
  const [product, setProduct] = useState('');
  const [error, setError] = useState('');
  const [showHistory, setShowHistory] = useState(false);

  // ── Persistent state ──
  const [results, setResults] = usePersistentState('markup-detective-results', null);
  const [sessionHistory, setSessionHistory] = usePersistentState('markup-detective-history', []);

  // Example pills — labels + seed text, localized and currency-aware.
  const EXAMPLES = [
    { label: t('md_ex1_label', { sym }), text: t('md_ex1_text', { sym }) },
    { label: t('md_ex2_label', { sym }), text: t('md_ex2_text', { sym }) },
    { label: t('md_ex3_label', { sym }), text: t('md_ex3_text', { sym }) },
    { label: t('md_ex4_label', { sym }), text: t('md_ex4_text', { sym }) },
    { label: t('md_ex5_label', { sym }), text: t('md_ex5_text', { sym }) },
    { label: t('md_ex6_label', { sym }), text: t('md_ex6_text', { sym }) },
  ];

  // ── Refs ──
  const resultsRef = useRef(null);
  const loadExample = () => {
    const ex = EXAMPLES[Math.floor(Math.random() * EXAMPLES.length)];
    setProduct(ex.text);
  };

  const handleAnalyzeRef = useRef(null);
  const canSubmitRef = useRef(false);

  // ── Handlers ──
  const handleAnalyze = useCallback(async () => {
    if (!product.trim()) return;
    setError('');
    setResults(null);
    try {
      const data = await callToolEndpoint('markup-detective', { product: product.trim(), userLocale, userCurrency, userRegion });
      setResults(data);
      setSessionHistory(prev => [{
        id: Date.now(),
        date: new Date().toISOString(),
        preview: product.trim().slice(0, PREVIEW_CHARS),
        markup: data.markup_multiplier,
      }, ...prev].slice(0, 6));
    } catch (err) {
      setError(err.message || t('md_error'));
    }
  }, [product, callToolEndpoint, setResults, setSessionHistory, userLocale, userCurrency, userRegion, t]);

  const handleReset = () => { setResults(null); setProduct(''); setError(''); };

  // ── Render-time ref assignments ──
  handleAnalyzeRef.current = handleAnalyze;
  canSubmitRef.current = !!product.trim();

  // ── Build copy text ──
  const buildFullText = useCallback(() => {
    if (!results) return '';
    const lines = [`🏷️ ${t('md_copy_header')} — ${product}`, ''];
    lines.push(`${t('md_copy_markup')} ${results?.markup_multiplier}x`);
    lines.push(`${t('md_copy_youpay')} ${results?.price_paid || '?'}`);
    lines.push(`${t('md_copy_fair')} ${results?.fair_price}`);
    lines.push('');
    lines.push(t('md_copy_breakdown'));
    (results?.cost_breakdown || []).forEach(item => {
      lines.push(`  ${item.label}: ${item.amount} (${item.percent}%)`);
    });
    if (results?.psychological_tactics?.length) {
      lines.push('');
      lines.push(t('md_copy_tactics'));
      results?.psychological_tactics?.forEach(tac => lines.push(`  • ${tac}`));
    }
    if (results?.industry_secrets?.length) {
      lines.push('');
      lines.push(t('md_copy_secrets'));
      results?.industry_secrets?.forEach(s => lines.push(`  • ${s}`));
    }
    if (results?.how_to_pay_less?.length) {
      lines.push('');
      lines.push(t('md_copy_payless'));
      results?.how_to_pay_less?.forEach(tip => lines.push(`  • ${tip}`));
    }
    lines.push(BRAND);
    return lines.join('\n');
  }, [results, product, t]);

  useRegisterActions(buildFullText(), tool?.title || 'MarkupDetective');

  // ── PF-7: Scroll to results ──
  useEffect(() => {
    if (!results || !resultsRef.current) return;
    const t = setTimeout(() => resultsRef.current?.scrollIntoView({ behavior: 'smooth', block: 'start' }), 200);
    return () => clearTimeout(t);
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [results]);

  // ── PF-6: Keyboard handler ──
  useEffect(() => {
    const handler = (e) => {
      const tag = document.activeElement?.tagName;
      if (tag === 'SELECT') return;
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && !loading && canSubmitRef.current)
        handleAnalyzeRef.current?.();
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [loading]);

  // ── Render helpers ──
  const markupColor = (multiplier) => {
    const m = parseFloat(multiplier) || 0;
    if (m >= 10) return isDark ? 'text-red-400' : 'text-red-600';
    if (m >= 5)  return isDark ? 'text-amber-400' : 'text-amber-600';
    if (m >= 2)  return isDark ? 'text-sky-400' : 'text-sky-600';
    return isDark ? 'text-emerald-400' : 'text-emerald-600';
  };

  const barColor = (label) => {
    const l = (label || '').toLowerCase();
    if (l.includes('profit') || l.includes('margin')) return isDark ? 'bg-red-500' : 'bg-red-400';
    if (l.includes('brand') || l.includes('premium') || l.includes('marketing')) return isDark ? 'bg-amber-500' : 'bg-amber-400';
    if (l.includes('labor') || l.includes('overhead')) return isDark ? 'bg-sky-500' : 'bg-sky-400';
    return isDark ? 'bg-emerald-500' : 'bg-emerald-400';
  };

  return (
    <div className={`space-y-4 ${c.text}`}>

      {/* ═══ INPUT CARD — h2 first in-flow element ═══ */}
      <div className={`${c.card} border ${c.border} rounded-xl shadow-sm`}>
        <div className="px-5 pt-5">
          <div className="pb-3 border-b border-zinc-500 flex items-center justify-between">
            <div>
              <h2 className={`text-xl font-bold ${c.text}`}>
                <span className="mr-2">{tool?.icon ?? '🏷️'}</span>{tool?.title ?? t('md_title')}
              </h2>
              <p className={`text-sm ${c.textSecondary}`}>{tool?.tagline ?? t('md_tagline')}</p>
              <button onClick={loadExample} disabled={loading} style={{ backgroundColor: (tool?.headerColor ?? '#888888') + '80' }} className={`mt-1.5 px-2.5 py-0.5 rounded-full text-xs font-medium border disabled:opacity-40 ${isDark ? 'text-white border-white/40' : 'text-gray-800 border-transparent'}`}>{t('try_example')}</button>
            </div>
            <div className="flex items-center gap-2 flex-shrink-0">
              {sessionHistory.length > 0 && (
                <button onClick={() => setShowHistory(!showHistory)}
                  className={`text-xs font-bold px-3 py-1.5 rounded-lg ${c.btnSecondary}`}>
                  📋 {sessionHistory.length}
                </button>
              )}
              {(results || product.trim()) && (
                <button onClick={handleReset} className={`${c.btnSecondary} px-3 py-1.5 rounded-lg text-xs font-bold`}>
                  ↺ {t('start_over')}
                </button>
              )}
            </div>
          </div>
        </div>

        <div className="px-5 pb-5 pt-4 space-y-4">

          {/* History panel */}
          {showHistory && sessionHistory.length > 0 && (
            <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4 space-y-2`}>
              <p className={`text-xs font-bold ${c.textMuted} mb-1`}>{t('md_recent_lookups')}</p>
              {sessionHistory.map(h => (
                <button key={h.id} onClick={() => { setProduct(h.preview); setShowHistory(false); }}
                  className={`w-full text-left flex items-center justify-between px-3 py-2 rounded-lg ${c.card} border ${c.border} hover:opacity-80 transition-opacity`}>
                  <span className={`text-sm ${c.textSecondary} truncate`}>{h.preview}</span>
                  {h.markup && <span className={`text-xs font-bold ml-2 shrink-0 ${markupColor(h.markup)}`}>{h.markup}x</span>}
                </button>
              ))}
            </div>
          )}

          {/* Textarea — required field */}
          <div>
            <label className={`text-sm font-medium ${c.label} block mb-2`}>
              {t('md_q_label')} <span className={c.required}>*</span>
            </label>
            <textarea
              value={product}
              onChange={e => setProduct(e.target.value)}
              placeholder={t('md_ph', { sym })}
              rows={3}
              className={`w-full p-3 border rounded-lg text-sm resize-none ${c.input}`}
            />
          </div>

          {/* Quick examples */}
          <div>
            <p className={`text-xs font-semibold ${c.textMuted} mb-2`}>{t('md_popular')}</p>
            <div className="flex flex-wrap gap-1.5">
              {EXAMPLES.map(ex => (
                <button key={ex.label} onClick={() => setProduct(ex.text)}
                  className={`px-3 py-1.5 rounded-lg border text-xs font-medium transition-all ${product === ex.text ? c.pillActive : c.pillInactive}`}>
                  {ex.label}
                </button>
              ))}
            </div>
          </div>

          {/* Pre-result cross-ref */}
          <p className={`text-xs text-center ${c.textMuted}`}>
            {t('md_xref_q')} <a href="/BuyWise" className={`text-xs ${linkStyle}`}>🛒 {t('md_buywise')}</a>.
          </p>

          {/* Submit */}
          <button
            onClick={handleAnalyze}
            disabled={loading || !canSubmitRef.current}
            className={`w-full ${c.btnPrimary} disabled:opacity-40 font-bold py-3 rounded-lg flex items-center justify-center gap-2 min-h-[48px]`}>
            {loading
              ? <><span className="inline-block animate-spin">{tool?.icon ?? '🏷️'}</span> {t('md_investigating')}</>
              : <><span className="mr-1">{tool?.icon ?? '🏷️'}</span> {t('md_investigate')}</>}
          </button>

          <p className={`text-xs text-center ${c.textMuted}`}>{t('md_disclaimer')}</p>

          {error && (
            <div className={`p-3 rounded-lg border text-sm ${c.danger}`}>⚠️ {error}</div>
          )}
        </div>
      </div>

      {/* ═══ RESULTS ═══ */}
      {results && (
        <div ref={resultsRef} className="space-y-4">

          {/* Verdict banner */}
          <div className={`${c.card} border ${c.border} rounded-xl p-5`}>
            {results?.product_identified && (
              <p className={`text-xs font-medium ${c.textMuted} mb-3`}>{t('md_analyzing')} {results.product_identified}</p>
            )}
            <div className="flex items-center justify-between mb-3 flex-wrap gap-2">
              <div>
                <p className={`text-xs font-bold uppercase tracking-wide ${c.textMuted} mb-1`}>{t('md_markup_mult')}</p>
                <p className={`text-4xl font-black ${markupColor(results?.markup_multiplier)}`}>
                  {results?.markup_multiplier}x
                </p>
              </div>
              <div className="text-right">
                {results?.price_paid && (
                  <div className="mb-1">
                    <p className={`text-xs ${c.textMuted}`}>{t('md_you_pay')}</p>
                    <p className={`text-xl font-bold ${c.text}`}>{results?.price_paid}</p>
                  </div>
                )}
                {results?.true_cost && (
                  <div className="mb-1">
                    <p className={`text-xs ${c.textMuted}`}>{t('md_true_cost')}</p>
                    <p className={`text-xl font-bold ${isDark ? 'text-amber-400' : 'text-amber-600'}`}>{results?.true_cost}</p>
                  </div>
                )}
                <div>
                  <p className={`text-xs ${c.textMuted}`}>{t('md_fair_price')}</p>
                  <p className={`text-xl font-bold ${isDark ? 'text-emerald-400' : 'text-emerald-600'}`}>{results?.fair_price}</p>
                </div>
              </div>
            </div>
            {results?.one_line_verdict && (
              <p className={`text-sm ${c.textSecondary} border-t ${c.border} pt-3`}>{results?.one_line_verdict}</p>
            )}
          </div>

          {/* Cost breakdown */}
          {results?.cost_breakdown?.length > 0 && (
            <div className={`${c.card} border ${c.border} rounded-xl p-5`}>
              <h3 className={`font-bold text-sm ${c.text} mb-2`}>💰 {t('md_where_money')}</h3>
              <div className="flex flex-wrap gap-3 mb-4">
                {[
                  { colorKey: 'chartMaterials', label: t('md_legend_materials') },
                  { colorKey: 'chartLabor',     label: t('md_legend_labor') },
                  { colorKey: 'chartBrand',     label: t('md_legend_brand') },
                  { colorKey: 'chartProfit',    label: t('md_legend_profit') },
                ].map(({ colorKey, label }) => (
                  <div key={label} className="flex items-center gap-1.5">
                    <span className={`inline-block w-2.5 h-2.5 rounded-full ${c[colorKey]}`} />
                    <span className={`text-[10px] ${c.textMuted}`}>{label}</span>
                  </div>
                ))}
              </div>
              <div className="space-y-3">
                {results?.cost_breakdown?.map((item, i) => (
                  <div key={i}>
                    <div className="flex items-center justify-between mb-1">
                      <span className={`text-sm ${c.textSecondary}`}>{item.label}</span>
                      <span className={`text-sm font-semibold ${c.text}`}>{item.amount} <span className={`text-xs ${c.textMuted}`}>({item.percent}%)</span></span>
                    </div>
                    <div className={`h-2 rounded-full ${isDark ? 'bg-zinc-700' : 'bg-gray-100'} overflow-hidden`}>
                      <div
                        className={`h-full rounded-full transition-all duration-700 ${barColor(item.label)}`}
                        style={{ width: `${Math.min(100, parseFloat(item.percent) || 0)}%` }}
                      />
                    </div>
                  </div>
                ))}
              </div>
            </div>
          )}

          {/* Psychological tactics */}
          {results?.psychological_tactics?.length > 0 && (
            <div className={`${c.warning} border rounded-xl p-5`}>
              <h3 className={`font-bold text-sm mb-3 ${c.warningTxt}`}>🧠 {t('md_tactics_title')}</h3>
              <ul className="space-y-1.5">
                {results?.psychological_tactics?.map((tac, i) => (
                  <li key={i} className="text-sm flex items-start gap-2">
                    <span className="mt-0.5 shrink-0">•</span>{tac}
                  </li>
                ))}
              </ul>
            </div>
          )}

          {/* Industry secrets */}
          {results?.industry_secrets?.length > 0 && (
            <div className={`${c.card} border ${c.border} rounded-xl p-5`}>
              <h3 className={`font-bold text-sm ${c.text} mb-3`}>🔍 {t('md_secrets_title')}</h3>
              <ul className="space-y-1.5">
                {results?.industry_secrets?.map((s, i) => (
                  <li key={i} className={`text-sm ${c.textSecondary} flex items-start gap-2`}>
                    <span className="mt-0.5 shrink-0">•</span>{s}
                  </li>
                ))}
              </ul>
            </div>
          )}

          {/* How to pay less */}
          {results?.how_to_pay_less?.length > 0 && (
            <div className={`${c.success} border rounded-xl p-5`}>
              <h3 className={`font-bold text-sm mb-3 ${c.successTxt}`}>💡 {t('md_pay_less_title')}</h3>
              <ul className="space-y-1.5">
                {results?.how_to_pay_less?.map((tip, i) => (
                  <li key={i} className="text-sm flex items-start gap-2">
                    <span className="mt-0.5 shrink-0">✓</span>{tip}
                  </li>
                ))}
              </ul>
            </div>
          )}

          {/* Post-result cross-refs */}
          <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
            <p className={`text-[10px] font-bold ${c.textMuted} uppercase mb-2`}>🔗 {t('md_related')}</p>
            <div className="flex flex-wrap gap-3">
              <a href="/BuyWise" className={`text-xs ${linkStyle}`}>🛒 {t('md_buywise')}</a>
              <a href="/FakeReviewDetective" className={`text-xs ${linkStyle}`}>🕵️ {t('md_fakereview')}</a>
            </div>
          </div>

        </div>
      )}
    </div>
  );
};

MarkupDetective.displayName = 'MarkupDetective';
export default MarkupDetective;
