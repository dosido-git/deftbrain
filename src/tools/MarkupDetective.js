import React, { useState, useEffect } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { usePersistentState } from '../hooks/usePersistentState';
import { CopyBtn, ActionBar } from '../components/ActionButtons';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const EXAMPLES = [
  { label: '$6 coffee', text: '$6 latte at a trendy coffee shop' },
  { label: '$25 hospital aspirin', text: 'Hospital aspirin charged at $25/tablet on a bill' },
  { label: '$80 concert T-shirt', text: '$80 band t-shirt sold at a concert venue' },
  { label: '$400 hotel minibar water', text: '$8 bottle of water from a hotel minibar' },
  { label: '$15 movie popcorn', text: '$15 large popcorn at a movie theater' },
  { label: '$200 Nike sneakers', text: '$200 Nike Air Max sneakers at retail' },
];

const MarkupDetective = ({ tool }) => {
  const { callToolEndpoint, loading } = useClaudeAPI();
  const { isDark } = useTheme();

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';

  const c = {
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:         isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20' : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-500' : 'text-gray-400',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white' : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-100' : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200' : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200' : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200' : 'bg-red-50 border-red-200 text-red-800',
    pillActive:    isDark ? 'border-cyan-500 bg-cyan-900/30 text-cyan-200' : 'border-cyan-600 bg-cyan-100 text-cyan-900',
    pillInactive:  isDark ? 'border-zinc-600 text-zinc-400 hover:border-zinc-500' : 'border-gray-300 text-gray-500 hover:border-gray-400',
  };

  const [product, setProduct] = useState('');
  const [results, setResults] = usePersistentState('markup-detective-results', null);
  const [error, setError] = useState('');
  const [history, setHistory] = usePersistentState('markup-detective-history', []);
  const [showHistory, setShowHistory] = useState(false);

  useEffect(() => {
    const handler = (e) => {
      const tag = document.activeElement?.tagName;
      if (tag === 'INPUT' || tag === 'TEXTAREA' || tag === 'SELECT') return;
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && !loading) handleAnalyze();
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [loading]);

  const handleAnalyze = async () => {
    if (!product.trim()) return;
    setError('');
    setResults(null);
    try {
      const data = await callToolEndpoint('markup-detective', { product: product.trim() });
      setResults(data);
      setHistory(prev => [{
        id: Date.now(),
        date: new Date().toISOString(),
        preview: product.trim().slice(0, 40),
        markup: data.markup_multiplier,
      }, ...prev].slice(0, 6));
    } catch (err) {
      setError(err.message || 'Failed to analyze pricing. Try again.');
    }
  };

  const buildCopyText = () => {
    if (!results) return '';
    const lines = [`🏷️ MARKUP DETECTIVE — ${product}`, ''];
    lines.push(`Markup multiplier: ${results.markup_multiplier}x`);
    lines.push(`You pay: ${results.price_paid || '?'}`);
    lines.push(`Fair price: ${results.fair_price}`);
    lines.push('');
    lines.push('COST BREAKDOWN:');
    (results.cost_breakdown || []).forEach(item => {
      lines.push(`  ${item.label}: ${item.amount} (${item.percent}%)`);
    });
    if (results.psychological_tactics?.length) {
      lines.push('');
      lines.push('PRICING TACTICS USED ON YOU:');
      results.psychological_tactics.forEach(t => lines.push(`  • ${t}`));
    }
    if (results.industry_secrets?.length) {
      lines.push('');
      lines.push('INDUSTRY SECRETS:');
      results.industry_secrets.forEach(s => lines.push(`  • ${s}`));
    }
    if (results.how_to_pay_less?.length) {
      lines.push('');
      lines.push('HOW TO PAY LESS:');
      results.how_to_pay_less.forEach(tip => lines.push(`  • ${tip}`));
    }
    lines.push(BRAND);
    return lines.join('\n');
  };

  const markupColor = (multiplier) => {
    const m = parseFloat(multiplier) || 0;
    if (m >= 10) return isDark ? 'text-red-400' : 'text-red-600';
    if (m >= 5)  return isDark ? 'text-amber-400' : 'text-amber-600';
    if (m >= 2)  return isDark ? 'text-sky-400' : 'text-sky-600';
    return isDark ? 'text-emerald-400' : 'text-emerald-600';
  };

  const barColor = (label) => {
    const l = (label || '').toLowerCase();
    if (l.includes('profit') || l.includes('margin')) return isDark ? 'bg-red-500' : 'bg-red-400';
    if (l.includes('brand') || l.includes('premium') || l.includes('marketing')) return isDark ? 'bg-amber-500' : 'bg-amber-400';
    if (l.includes('labor') || l.includes('overhead')) return isDark ? 'bg-sky-500' : 'bg-sky-400';
    return isDark ? 'bg-emerald-500' : 'bg-emerald-400';
  };

  return (
    <div className="max-w-xl mx-auto space-y-5">

      {/* Header */}
      <div className="flex items-center justify-between">
        <div>
          <h2 className={`text-2xl font-bold ${c.text}`}>
            <span className="mr-2">{tool?.icon ?? '🏷️'}</span>{tool?.title ?? 'Markup Detective'}
          </h2>
          <p className={`text-sm ${c.textMuted}`}>{tool?.tagline ?? 'Why does this cost that? Follow the money.'}</p>
        </div>
        {history.length > 0 && (
          <button onClick={() => setShowHistory(!showHistory)}
            className={`text-xs font-bold px-3 py-1.5 rounded-lg ${c.btnSecondary}`}>
            📋 {history.length}
          </button>
        )}
      </div>

      {/* History panel */}
      {showHistory && history.length > 0 && (
        <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4 space-y-2`}>
          <p className={`text-xs font-bold ${c.textMuted} mb-1`}>Recent lookups</p>
          {history.map(h => (
            <button key={h.id} onClick={() => { setProduct(h.preview); setShowHistory(false); }}
              className={`w-full text-left flex items-center justify-between px-3 py-2 rounded-lg ${c.card} border ${c.border} hover:opacity-80 transition-opacity`}>
              <span className={`text-sm ${c.textSecondary} truncate`}>{h.preview}</span>
              {h.markup && <span className={`text-xs font-bold ml-2 shrink-0 ${markupColor(h.markup)}`}>{h.markup}x</span>}
            </button>
          ))}
        </div>
      )}

      {/* Input card */}
      <div className={`${c.card} border ${c.border} rounded-xl shadow-sm p-5 space-y-4`}>
        <div>
          <label className={`text-sm font-semibold ${c.text} block mb-2`}>
            What are you curious about?
          </label>
          <textarea
            value={product}
            onChange={e => setProduct(e.target.value)}
            placeholder="e.g. '$6 latte at a coffee shop' or 'hotel minibar beer' or '$200 Nike sneakers'"
            rows={3}
            className={`w-full p-3 border rounded-lg text-sm resize-none outline-none focus:ring-2 transition-colors ${c.input}`}
          />
        </div>

        {/* Quick examples */}
        <div>
          <p className={`text-xs font-semibold ${c.textMuted} mb-2`}>Popular mysteries:</p>
          <div className="flex flex-wrap gap-1.5">
            {EXAMPLES.map(ex => (
              <button key={ex.label} onClick={() => setProduct(ex.text)}
                className={`px-3 py-1.5 rounded-lg border text-xs font-medium transition-all ${product === ex.text ? c.pillActive : c.pillInactive}`}>
                {ex.label}
              </button>
            ))}
          </div>
        </div>

        <button
          onClick={handleAnalyze}
          disabled={loading || !product.trim()}
          className={`w-full py-3 rounded-xl font-semibold text-sm flex items-center justify-center gap-2 transition-all disabled:opacity-40 ${c.btnPrimary}`}>
          {loading
            ? <><span className="inline-block animate-spin">{tool?.icon ?? '🏷️'}</span> Investigating...</>
            : <><span>{tool?.icon ?? '🏷️'}</span> Investigate the Markup</>}
        </button>
        <p className={`text-xs text-center ${c.textMuted}`}>AI-generated estimates — actual margins vary by brand and market.</p>

        {error && (
          <div className={`p-3 rounded-lg border text-sm ${c.danger}`}>⚠️ {error}</div>
        )}
      </div>

      {/* Results */}
      {results && (
        <div className="space-y-4">

          {/* Verdict banner */}
          <div className={`${c.card} border ${c.border} rounded-xl p-5`}>
            <div className="flex items-center justify-between mb-3 flex-wrap gap-2">
              <div>
                <p className={`text-xs font-bold uppercase tracking-wide ${c.textMuted} mb-1`}>Markup multiplier</p>
                <p className={`text-4xl font-black ${markupColor(results.markup_multiplier)}`}>
                  {results.markup_multiplier}x
                </p>
              </div>
              <div className="text-right">
                {results.price_paid && (
                  <div className="mb-1">
                    <p className={`text-xs ${c.textMuted}`}>You pay</p>
                    <p className={`text-xl font-bold ${c.text}`}>{results.price_paid}</p>
                  </div>
                )}
                <div>
                  <p className={`text-xs ${c.textMuted}`}>Fair price</p>
                  <p className={`text-xl font-bold ${isDark ? 'text-emerald-400' : 'text-emerald-600'}`}>{results.fair_price}</p>
                </div>
              </div>
            </div>
            {results.one_line_verdict && (
              <p className={`text-sm ${c.textSecondary} border-t ${c.border} pt-3`}>{results.one_line_verdict}</p>
            )}
          </div>

          {/* Cost breakdown */}
          {results.cost_breakdown?.length > 0 && (
            <div className={`${c.card} border ${c.border} rounded-xl p-5`}>
              <h3 className={`font-bold text-sm ${c.text} mb-4`}>💰 Where your money actually goes</h3>
              <div className="space-y-3">
                {results.cost_breakdown.map((item, i) => (
                  <div key={i}>
                    <div className="flex items-center justify-between mb-1">
                      <span className={`text-sm ${c.textSecondary}`}>{item.label}</span>
                      <span className={`text-sm font-semibold ${c.text}`}>{item.amount} <span className={`text-xs ${c.textMuted}`}>({item.percent}%)</span></span>
                    </div>
                    <div className={`h-2 rounded-full ${isDark ? 'bg-zinc-700' : 'bg-gray-100'} overflow-hidden`}>
                      <div
                        className={`h-full rounded-full transition-all duration-700 ${barColor(item.label)}`}
                        style={{ width: `${Math.min(100, parseFloat(item.percent) || 0)}%` }}
                      />
                    </div>
                  </div>
                ))}
              </div>
            </div>
          )}

          {/* Psychological tactics */}
          {results.psychological_tactics?.length > 0 && (
            <div className={`${c.warning} border rounded-xl p-5`}>
              <h3 className="font-bold text-sm mb-3">🧠 Psychological pricing tactics being used on you</h3>
              <ul className="space-y-1.5">
                {results.psychological_tactics.map((t, i) => (
                  <li key={i} className="text-sm flex items-start gap-2">
                    <span className="mt-0.5 shrink-0">•</span>{t}
                  </li>
                ))}
              </ul>
            </div>
          )}

          {/* Industry secrets */}
          {results.industry_secrets?.length > 0 && (
            <div className={`${c.card} border ${c.border} rounded-xl p-5`}>
              <h3 className={`font-bold text-sm ${c.text} mb-3`}>🔍 Industry secrets they don't want you to know</h3>
              <ul className="space-y-1.5">
                {results.industry_secrets.map((s, i) => (
                  <li key={i} className={`text-sm ${c.textSecondary} flex items-start gap-2`}>
                    <span className="mt-0.5 shrink-0">•</span>{s}
                  </li>
                ))}
              </ul>
            </div>
          )}

          {/* How to pay less */}
          {results.how_to_pay_less?.length > 0 && (
            <div className={`${c.success} border rounded-xl p-5`}>
              <h3 className="font-bold text-sm mb-3">💡 How to pay less</h3>
              <ul className="space-y-1.5">
                {results.how_to_pay_less.map((tip, i) => (
                  <li key={i} className="text-sm flex items-start gap-2">
                    <span className="mt-0.5 shrink-0">✓</span>{tip}
                  </li>
                ))}
              </ul>
            </div>
          )}

          {/* Actions */}
          <div className="flex gap-3">
            <div className="flex-1">
              <ActionBar content={buildCopyText()} title="Markup Analysis" />
            </div>
            <button onClick={() => { setResults(null); setProduct(''); setError(''); }}
              className={`px-4 py-2 rounded-xl text-sm font-semibold ${c.btnSecondary}`}>
              🔍 New lookup
            </button>
          </div>

          {/* Cross-tool links */}
          <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
            <p className={`text-xs font-bold ${c.textMuted} mb-2`}>🔗 Related tools</p>
            <div className="flex flex-wrap gap-3">
              <a href="/tool/buy-wise" className={`text-xs ${linkStyle}`}>🛒 Should you buy it? Try BuyWise</a>
              <a href="/tool/fake-review-detective" className={`text-xs ${linkStyle}`}>🕵️ Spot fake reviews first</a>
              <a href="/tool/sub-sweep" className={`text-xs ${linkStyle}`}>💳 Check subscription costs</a>
            </div>
          </div>

        </div>
      )}
    </div>
  );
};

MarkupDetective.displayName = 'MarkupDetective';
export default MarkupDetective;
