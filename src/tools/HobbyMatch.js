import React, { useState, useCallback, useEffect, useRef } from 'react';
import { CopyBtn } from '../components/ActionButtons';
import { useRegisterActions } from '../components/ActionBarContext';
import { usePersistentState } from '../hooks/usePersistentState';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const BUDGET_LEVELS = [
  { value: 'free', label: 'Free only', emoji: '🆓' },
  { value: 'under_50', label: 'Under $50', emoji: '💵' },
  { value: 'under_200', label: 'Under $200', emoji: '💰' },
  { value: 'flexible', label: 'Flexible', emoji: '💎' },
];

const LOOKING_FOR = [
  { value: 'relax', label: 'Something relaxing', emoji: '🧘' },
  { value: 'social', label: 'Meet people', emoji: '👥' },
  { value: 'creative', label: 'Creative outlet', emoji: '🎨' },
  { value: 'physical', label: 'Get moving', emoji: '🏃' },
  { value: 'learn', label: 'Learn something', emoji: '📚' },
  { value: 'outdoors', label: 'Get outside', emoji: '🌲' },
  { value: 'solo', label: 'Solo escape', emoji: '🎧' },
  { value: 'weird', label: 'Something weird', emoji: '🦑' },
];

const HobbyMatch = ({ tool }) => {
  const { callToolEndpoint, loading } = useClaudeAPI();
  const { isDark } = useTheme();



  const c = {
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:         isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20' : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-500' : 'text-gray-400',
    labelText:     isDark ? 'text-zinc-200' : 'text-gray-700',
    accentTxt:     isDark ? 'text-cyan-400' : 'text-cyan-600',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white' : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200' : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200' : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200' : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200' : 'bg-red-50 border-red-200 text-red-800',
    pillActive:    isDark ? 'border-cyan-500 bg-cyan-900/30 text-cyan-200' : 'border-cyan-600 bg-cyan-100 text-cyan-900',
    pillInactive:  isDark ? 'border-zinc-600 text-zinc-400 hover:border-zinc-500' : 'border-gray-300 text-gray-500 hover:border-gray-400',
    badgeNeutral:  isDark ? 'bg-zinc-700 text-zinc-300' : 'bg-gray-100 text-gray-600',
    labelText:     isDark ? 'text-zinc-200' : 'text-gray-700',
    required:      isDark ? 'text-amber-400' : 'text-amber-500',
  };
  c.textMuteded = c.textMuted;
  c.label = c.labelText;

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';

  // ─── State ───
  const [personality, setPersonality] = usePersistentState('hm-personality', '');
  const [history, setHistory] = usePersistentState('hobbymatch-history', []);
  const [schedule, setSchedule] = useState('');
  const [budget, setBudget] = useState('');
  const [physical, setPhysical] = useState('');
  const [triedBefore, setTriedBefore] = useState('');
  const [selectedGoals, setSelectedGoals] = useState([]);
  const [results, setResults] = usePersistentState('hobbymatch-result', null);
  const [error, setError] = useState('');
  const resultsRef = React.useRef(null);
  const [expandedHobby, setExpandedHobby] = useState(null);

  const toggleGoal = (value) => {
    setSelectedGoals(prev => prev.includes(value) ? prev.filter(g => g !== value) : [...prev, value]);
  };

  // ─── Actions ───
  const generate = useCallback(async () => {
    if (!personality.trim() && selectedGoals.length === 0) return;
    setError('');
    setResults(null);
    setExpandedHobby(null);

    const lookingFor = selectedGoals.map(g => LOOKING_FOR.find(l => l.value === g)?.label).join(', ');

    try {
      const data = await callToolEndpoint('hobby-match', {
        personality: personality.trim() || null,
        schedule: schedule.trim() || null,
        budget: budget || 'flexible',
        physical: physical.trim() || null,
        triedBefore: triedBefore.trim() || null,
        lookingFor: lookingFor || null,
      });
      setResults(data);
      setHistory(prev => [{ id: Date.now(), date: new Date().toISOString(), preview: '' }, ...prev].slice(0, 6));
    setTimeout(() => resultsRef.current?.scrollIntoView({ behavior: 'smooth' }), 100);
    } catch (err) {
      setError(err.message || 'Failed to find hobbies.');
    }
  }, [personality, schedule, budget, physical, triedBefore, selectedGoals, callToolEndpoint]);

  const handleReset = useCallback(() => {
    setPersonality(''); setSchedule(''); setBudget('');
    setPhysical(''); setTriedBefore(''); setSelectedGoals([]);
    setResults(null); setError(''); setExpandedHobby(null);
  }, []);

  // ─── Build text ───
  const buildFullText = useCallback(() => {
    if (!results) return '';
    let text = '🧬 HobbyMatch Results\n';
    if (results.profile_read) text += `\n${results.profile_read}\n`;
    if (results.hobbies?.length) {
      results.hobbies.forEach((h, i) => {
        text += `\n${i + 1}. ${h.icon || '🎯'} ${h.name}\n${h.one_liner}\n${h.what_it_is}`;
        text += `\n⏱ ${h.time_required} | 💰 ${h.startup_cost}`;
        text += `\n🚀 First step: ${h.first_step}`;
        text += `\n👥 Community: ${h.find_your_people}\n`;
      });
    }
    if (results.wildcard) text += `\n🃏 Wildcard: ${results.wildcard.name} — ${results.wildcard.why}`;
    if (results.pattern_noticed) text += `\n\n💡 ${results.pattern_noticed}`;
    return text + BRAND;
  }, [results]);

  useRegisterActions(buildFullText(), tool?.title || 'HobbyMatch');

  const generateRef = useRef(null);
  const canSubmitRef = useRef(false);
  generateRef.current = generate;
  canSubmitRef.current = !!personality.trim() || selectedGoals.length > 0;

  useEffect(() => {
    const handler = (e) => {
      const tag = document.activeElement?.tagName;
      if (tag === 'SELECT') return;
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && !loading && canSubmitRef.current)
        generateRef.current?.();
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [loading]);

  const r = results;

  // ════════════════════════════════════════════════════════════
  // RENDER
  // ════════════════════════════════════════════════════════════
  return (
    <div className={`space-y-4 ${c.text}`}>

      {/* ── HEADER ── */}
      <div className={`${c.card} border ${c.border} rounded-xl p-6`}>
        <div className="mb-5 pb-4 border-b border-zinc-500">
          <div className="flex items-start justify-between gap-3">
            <div>
              <h2 className={`text-2xl font-bold ${c.text}`}><span className="mr-2">{tool?.icon ?? '🎯'}</span>{tool?.title || 'HobbyMatch'}</h2>
              <p className={`text-sm ${c.textSecondary} mt-1`}>Discover hobbies you didn't know existed</p>
            </div>
            {(results || personality.trim()) && (
              <button onClick={handleReset} className={`${c.btnSecondary} px-3 py-1.5 rounded-lg text-xs font-bold flex-shrink-0`}>
                ↺ Start Over
              </button>
            )}
          </div>
        </div>

        {/* Personality */}
        <div className="mb-4">
          <label className={`text-sm font-bold ${c.text} block mb-1.5`}>Tell me about yourself <span className={c.required}>*</span></label>
          <textarea
            value={personality}
            onChange={e => setPersonality(e.target.value)}
            placeholder="e.g., introverted but want to meet people, love building things, easily bored, competitive, curious about science, night owl"
            rows={2}
            className={`w-full px-4 py-3 border rounded-xl text-sm ${c.input} ${c.text} outline-none focus:ring-2 resize-none`}
          />
        </div>

        {/* Looking for */}
        <div className="mb-4">
          <label className={`text-sm font-bold ${c.text} block mb-2`}>What are you looking for? <span className={`font-normal ${c.textMuteded}`}>(pick any)</span></label>
          <div className="flex flex-wrap gap-1.5">
            {LOOKING_FOR.map(g => (
              <button
                key={g.value}
                onClick={() => toggleGoal(g.value)}
                className={`px-3 py-1.5 rounded-lg text-xs font-bold border transition-colors min-h-[32px] ${
                  selectedGoals.includes(g.value) ? c.pillActive : c.pillInactive
                }`}
              >
                {g.emoji} {g.label}
              </button>
            ))}
          </div>
        </div>

        {/* Schedule */}
        <div className="mb-4">
          <label className={`text-sm font-bold ${c.text} block mb-1.5`}>
            Your free time <span className={`font-normal ${c.textMuteded}`}>(optional)</span>
          </label>
          <input
            type="text"
            value={schedule}
            onChange={e => setSchedule(e.target.value)}
            placeholder="e.g., weekday evenings, 2 hours on weekends, random 30-min gaps"
            className={`w-full px-4 py-3 border rounded-xl text-sm ${c.input} ${c.text} outline-none focus:ring-2`}
          />
        </div>

        {/* Budget */}
        <div className="mb-4">
          <label className={`text-sm font-bold ${c.text} block mb-2`}>Budget for startup</label>
          <div className="flex gap-2">
            {BUDGET_LEVELS.map(b => (
              <button
                key={b.value}
                onClick={() => setBudget(budget === b.value ? '' : b.value)}
                className={`flex-1 py-2 rounded-xl text-[11px] font-bold border transition-colors min-h-[36px] ${
                  budget === b.value ? c.pillActive : c.pillInactive
                }`}
              >
                {b.emoji} {b.label}
              </button>
            ))}
          </div>
        </div>

        {/* Physical */}
        <div className="mb-4">
          <label className={`text-sm font-bold ${c.text} block mb-1.5`}>
            Physical limitations <span className={`font-normal ${c.textMuteded}`}>(optional)</span>
          </label>
          <input
            type="text"
            value={physical}
            onChange={e => setPhysical(e.target.value)}
            placeholder="e.g., bad knees, wheelchair user, recovering from surgery, none"
            className={`w-full px-4 py-3 border rounded-xl text-sm ${c.input} ${c.text} outline-none focus:ring-2`}
          />
        </div>

        {/* Tried before */}
        <div className="mb-5">
          <label className={`text-sm font-bold ${c.text} block mb-1.5`}>
            Things you've already tried <span className={`font-normal ${c.textMuteded}`}>(so I don't suggest them)</span>
          </label>
          <input
            type="text"
            value={triedBefore}
            onChange={e => setTriedBefore(e.target.value)}
            placeholder="e.g., yoga, painting, guitar, running, chess"
            className={`w-full px-4 py-3 border rounded-xl text-sm ${c.input} ${c.text} outline-none focus:ring-2`}
          />
        </div>

        {/* Actions */}
        <div className="flex gap-3">
          <button
            onClick={generate}
            disabled={loading || (!personality.trim() && selectedGoals.length === 0)}
            className={`flex-1 ${c.btnPrimary} disabled:opacity-40 disabled:cursor-not-allowed font-bold py-3 px-6 rounded-xl flex items-center justify-center gap-2 min-h-[48px] shadow-lg`}
          >
            {loading ? (
              <><span className="animate-spin inline-block">{tool?.icon ?? '🎯'}</span> Matching hobbies...</>
            ) : (
              <><span className="mr-1">{tool?.icon ?? '🎯'}</span> Find My Hobbies</>
            )}
          </button>
        </div>
      </div>

      {/* Error */}
      {error && (
        <div ref={resultsRef} className={`${c.danger} border rounded-xl p-4 flex items-start gap-3`}>
          <span className="flex-shrink-0 mt-0.5">⚠️</span>
          <p className="text-sm">{error}</p>
        </div>
      )}

      {/* ══════════════════════════════════════════════════════════ */}
      {/* RESULTS                                                  */}
      {/* ══════════════════════════════════════════════════════════ */}
      {r && (
        <div ref={resultsRef} className="space-y-4">

          {/* ── PROFILE READ ── */}
          {r.profile_read && (
            <div className={`${c.card} border ${c.border} rounded-xl p-5`}>
              <p className={`text-sm ${c.textSecondary} leading-relaxed`}>{r.profile_read}</p>
            </div>
          )}

          {/* ── HOBBIES ── */}
          {r.hobbies?.length > 0 && (
            <div className="space-y-3">
              {r.hobbies.map((hobby, idx) => {
                const isExpanded = expandedHobby === idx;
                return (
                  <div key={idx} className={`${c.card} border ${c.border} rounded-xl overflow-hidden`}>
                    {/* Header — always visible */}
                    <button
                      onClick={() => setExpandedHobby(isExpanded ? null : idx)}
                      className="w-full p-5 text-left"
                    >
                      <div className="flex items-start gap-3">
                        <span className="text-2xl flex-shrink-0">{hobby.icon || '🎯'}</span>
                        <div className="flex-1 min-w-0">
                          <h4 className={`text-sm font-bold ${c.text}`}>{hobby.name}</h4>
                          <p className={`text-xs ${c.textSecondary} mt-0.5`}>{hobby.one_liner}</p>
                          <div className="flex flex-wrap gap-1.5 mt-2">
                            <span className={`text-[9px] font-bold px-2 py-0.5 rounded-full ${c.badgeNeutral}`}>
                              {hobby.energy_type === 'solo' ? '🎧 Solo' : hobby.energy_type === 'social' ? '👥 Social' : '🔄 Both'}
                            </span>
                            <span className={`text-[9px] font-bold px-2 py-0.5 rounded-full ${c.badgeNeutral}`}>
                              💰 {hobby.startup_cost}
                            </span>
                            <span className={`text-[9px] font-bold px-2 py-0.5 rounded-full ${c.badgeNeutral}`}>
                              ⏱ {hobby.time_required}
                            </span>
                          </div>
                        </div>
                        <span className={`${c.textMuteded} flex-shrink-0`}>{isExpanded ? '▲' : '▼'}</span>
                      </div>
                    </button>

                    {/* Expanded details */}
                    {isExpanded && (
                      <div className={`px-5 pb-5 border-t ${c.border} pt-4 space-y-3`}>
                        <div>
                          <p className={`text-[10px] font-bold ${c.textMuteded} mb-1`}>What it is</p>
                          <p className={`text-xs ${c.textSecondary} leading-relaxed`}>{hobby.what_it_is}</p>
                        </div>
                        {hobby.the_hook && (
                          <div className={`${c.warning} border rounded-lg p-3`}>
                            <p className="text-[10px] font-bold mb-0.5">🪝 The hook</p>
                            <p className="text-xs leading-relaxed">{hobby.the_hook}</p>
                          </div>
                        )}
                        {hobby.why_you && (
                          <div>
                            <p className={`text-[10px] font-bold ${c.textMuteded} mb-1`}>Why this fits you</p>
                            <p className={`text-xs ${c.textSecondary} leading-relaxed`}>{hobby.why_you}</p>
                          </div>
                        )}
                        <div className={`${c.success} border rounded-lg p-3`}>
                          <p className="text-[10px] font-bold mb-0.5">🚀 Start today</p>
                          <p className="text-xs leading-relaxed">{hobby.first_step}</p>
                        </div>
                        {hobby.find_your_people && (
                          <div>
                            <p className={`text-[10px] font-bold ${c.textMuteded} mb-1`}>👥 Find your people</p>
                            <p className={`text-xs ${c.textSecondary} leading-relaxed`}>{hobby.find_your_people}</p>
                          </div>
                        )}
                      </div>
                    )}
                  </div>
                );
              })}
            </div>
          )}

          {/* ── WILDCARD ── */}
          {r.wildcard && (
            <div className={`${c.warning} border rounded-xl p-4 flex items-start gap-3`}>
              <span className="flex-shrink-0 mt-0.5 text-lg">🃏</span>
              <div>
                <p className="text-xs font-bold mb-1">Wildcard pick</p>
                <p className="text-sm font-bold leading-relaxed">{r.wildcard.name}</p>
                <p className="text-xs leading-relaxed mt-1">{r.wildcard.why}</p>
              </div>
            </div>
          )}

          {/* ── PATTERN ── */}
          {r.pattern_noticed && (
            <div className={`${c.success} border rounded-xl p-4 flex items-start gap-3`}>
              <span className="flex-shrink-0 mt-0.5">💡</span>
              <div>
                <p className="text-xs font-bold mb-1">Pattern I noticed about you</p>
                <p className="text-sm leading-relaxed">{r.pattern_noticed}</p>
              </div>
            </div>
          )}

          {/* ── CROSS-REFERENCES ── */}
          <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
            <p className={`text-[10px] font-bold ${c.textMuted} uppercase mb-2`}>🔗 Related tools</p>
            <div className="flex flex-wrap gap-3">
              <a href="/MicroAdventureMapper" className={`text-xs ${linkStyle}`}>🗺️ Micro Adventure Mapper</a>
              <a href="/DopamineMenuBuilder" className={`text-xs ${linkStyle}`}>🍽️ Dopamine Menu Builder</a>
              <a href="/SixDegreesOfMe" className={`text-xs ${linkStyle}`}>🕸️ Six Degrees of Me</a>
            </div>
          </div>
        </div>
      )}
    </div>
  );
};

HobbyMatch.displayName = 'HobbyMatch';
export default HobbyMatch;
