import React, { useState, useCallback } from 'react';
import { CopyBtn } from '../components/ActionButtons';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const CHANNELS = [
  { value: 'email', label: 'Email', emoji: '📧' },
  { value: 'linkedin', label: 'LinkedIn', emoji: '💼' },
  { value: 'twitter_dm', label: 'Twitter/X DM', emoji: '🐦' },
  { value: 'instagram_dm', label: 'Instagram DM', emoji: '📸' },
  { value: 'text', label: 'Text / iMessage', emoji: '💬' },
  { value: 'in_person', label: 'In person', emoji: '🤝' },
];

const TONES = [
  { value: 'safe', label: 'Safe', emoji: '🛡️', desc: 'Polite, conventional' },
  { value: 'medium', label: 'Balanced', emoji: '⚖️', desc: 'Personal, warm' },
  { value: 'bold', label: 'Bold', emoji: '🔥', desc: 'Pattern-breaking' },
];

const ColdOpenCraft = () => {
  const { callToolEndpoint, loading } = useClaudeAPI();
  const { theme } = useTheme();
  const isDark = theme === 'dark';

  const c = {
    pageBg:       isDark ? 'bg-[#1a1816]'    : 'bg-[#faf8f5]',
    card:         isDark ? 'bg-[#2a2623]'     : 'bg-white',
    cardAlt:      isDark ? 'bg-[#332e2a]'     : 'bg-[#faf8f5]',
    cardBorder:   isDark ? 'border-[#3d3630]' : 'border-[#e8e1d5]',
    inputBg:      isDark ? 'bg-[#1a1816]'     : 'bg-[#faf8f5]',
    inputBorder:  isDark ? 'border-[#3d3630]' : 'border-[#d5cab8]',
    inputFocus:   isDark ? 'focus:border-[#4a6a8a] focus:ring-[#2c4a6e]/20'
                         : 'focus:border-[#4a6a8a] focus:ring-[#2c4a6e]/12',
    text:          isDark ? 'text-[#f0eeea]'  : 'text-[#3d3935]',
    textSecondary: isDark ? 'text-[#c8c3b9]'  : 'text-[#5a544a]',
    textMuted:     isDark ? 'text-[#8a8275]'  : 'text-[#8a8275]',
    heading:       isDark ? 'text-[#f3efe8]'  : 'text-[#1e2a3a]',
    btnPrimary:    isDark ? 'bg-[#2c4a6e] hover:bg-[#4a6a8a] text-white' : 'bg-[#2c4a6e] hover:bg-[#1e3a58] text-white',
    btnSecondary:  isDark ? 'bg-[#332e2a] hover:bg-[#3d3630] text-[#c8c3b9] border border-[#3d3630]' : 'bg-[#f3efe8] hover:bg-[#e8e1d5] text-[#5e5042] border border-[#d5cab8]',
    btnGold:       isDark ? 'bg-[#b06d22] hover:bg-[#c8872e] text-white' : 'bg-[#c8872e] hover:bg-[#b06d22] text-white',
    badgeNeutral:  isDark ? 'bg-[#3d3630] text-[#c8c3b9]' : 'bg-[#f3efe8] text-[#73614e]',
    badgePrimary:  isDark ? 'bg-[#2c4a6e]/30 text-[#a8b9ce]' : 'bg-[#d4dde8] text-[#1e3a58]',
    badgeGold:     isDark ? 'bg-[#b06d22]/20 text-[#d9a04e]' : 'bg-[#f9edd8] text-[#93541f]',
    divider:       isDark ? 'border-[#3d3630]' : 'border-[#e8e1d5]',
    link:          isDark ? 'text-[#6e8aaa] hover:text-[#a8b9ce]' : 'text-[#2c4a6e] hover:text-[#1e3a58]',
    success:       isDark ? 'bg-[#5a8a5c]/15 border-[#5a8a5c]/30 text-[#8abf8c]' : 'bg-[#e8f0e8] border-[#5a8a5c]/20 text-[#3d6b3f]',
    warning:       isDark ? 'bg-[#c8872e]/15 border-[#c8872e]/30 text-[#d9a04e]' : 'bg-[#fdf3e4] border-[#c8872e]/20 text-[#93541f]',
    error:         isDark ? 'bg-[#b54a3f]/15 border-[#b54a3f]/30 text-[#e08a82]' : 'bg-[#fceae8] border-[#b54a3f]/20 text-[#8a3530]',
    pillActive:    isDark ? 'bg-[#2c4a6e] border-[#4a6a8a] text-white' : 'bg-[#2c4a6e] border-[#2c4a6e] text-white',
    pillInactive:  isDark ? 'bg-[#2a2623] border-[#3d3630] text-[#c8c3b9] hover:border-[#5a544a]' : 'bg-white border-[#e8e1d5] text-[#5a544a] hover:border-[#d5cab8]',
  };

  // ─── State ───
  const [who, setWho] = useState('');
  const [why, setWhy] = useState('');
  const [channel, setChannel] = useState('email');
  const [whatYouKnow, setWhatYouKnow] = useState('');
  const [yourBackground, setYourBackground] = useState('');
  const [tone, setTone] = useState('medium');
  const [results, setResults] = useState(null);
  const [error, setError] = useState('');

  // ─── Actions ───
  const generate = useCallback(async () => {
    if (!who.trim() || !why.trim()) return;
    setError('');
    setResults(null);

    try {
      const data = await callToolEndpoint('cold-open-craft', {
        who: who.trim(),
        why: why.trim(),
        channel,
        whatYouKnow: whatYouKnow.trim() || null,
        yourBackground: yourBackground.trim() || null,
        tone,
      });
      setResults(data);
    } catch (err) {
      setError(err.message || 'Failed to generate openers.');
    }
  }, [who, why, channel, whatYouKnow, yourBackground, tone, callToolEndpoint]);

  const handleReset = useCallback(() => {
    setWho(''); setWhy(''); setChannel('email'); setWhatYouKnow('');
    setYourBackground(''); setTone('medium'); setResults(null); setError('');
  }, []);

  // ─── Build text ───
  const buildFullText = useCallback(() => {
    if (!results) return '';
    const r = results;
    let text = `📬 ColdOpenCraft: Reaching out to ${who}\n`;
    if (r.situation_read) text += `\n${r.situation_read}\n`;
    if (r.subject_line) text += `\nSubject: ${r.subject_line}\n`;
    if (r.openers?.length) {
      r.openers.forEach((o, i) => {
        text += `\n--- ${o.label} [${o.boldness}] ---\n${o.message}\n`;
        if (o.why_it_works) text += `Why: ${o.why_it_works}\n`;
      });
    }
    if (r.follow_up_plan) {
      text += `\n--- Follow-up ---\nWait: ${r.follow_up_plan.when}\n${r.follow_up_plan.message}\nStop when: ${r.follow_up_plan.when_to_stop}`;
    }
    return text + BRAND;
  }, [results, who]);

  const boldnessEmoji = (b) => b === 'safe' ? '🛡️' : b === 'bold' ? '🔥' : '⚖️';
  const boldnessColor = (b) => b === 'safe' ? c.success : b === 'bold' ? c.warning : c.badgePrimary;
  const responseColor = (r) => r === 'high' ? c.success : r === 'low' ? c.error : c.warning;

  const r = results;

  // ════════════════════════════════════════════════════════════
  // RENDER
  // ════════════════════════════════════════════════════════════
  return (
    <div className={`space-y-6 ${c.text}`}>

      {/* ── HEADER ── */}
      <div className={`${c.card} border ${c.cardBorder} rounded-xl p-6`}>
        <div className={`mb-5 pb-4 border-b ${c.divider}`}>
          <h2 className={`text-2xl font-bold ${c.heading}`}>ColdOpenCraft 📬</h2>
          <p className={`text-sm ${c.textSecondary} mt-1`}>First messages that actually get responses</p>
        </div>

        {/* Who */}
        <div className="mb-4">
          <label className={`text-sm font-bold ${c.text} block mb-1.5`}>Who are you reaching out to?</label>
          <input
            type="text"
            value={who}
            onChange={e => setWho(e.target.value)}
            placeholder="e.g., a hiring manager at Google, a podcast host, a potential mentor"
            className={`w-full px-4 py-3 border rounded-xl text-sm ${c.inputBg} ${c.inputBorder} ${c.text} ${c.inputFocus} outline-none focus:ring-2`}
          />
        </div>

        {/* Why */}
        <div className="mb-4">
          <label className={`text-sm font-bold ${c.text} block mb-1.5`}>Why are you reaching out?</label>
          <textarea
            value={why}
            onChange={e => setWhy(e.target.value)}
            placeholder="e.g., I want to pitch my startup for their podcast, I want an informational interview about switching to product management"
            rows={2}
            className={`w-full px-4 py-3 border rounded-xl text-sm ${c.inputBg} ${c.inputBorder} ${c.text} ${c.inputFocus} outline-none focus:ring-2 resize-none`}
          />
        </div>

        {/* Channel */}
        <div className="mb-4">
          <label className={`text-sm font-bold ${c.text} block mb-2`}>Channel</label>
          <div className="flex flex-wrap gap-1.5">
            {CHANNELS.map(ch => (
              <button
                key={ch.value}
                onClick={() => setChannel(ch.value)}
                className={`px-3 py-1.5 rounded-lg text-xs font-bold border transition-colors min-h-[32px] ${
                  channel === ch.value ? c.pillActive : c.pillInactive
                }`}
              >
                {ch.emoji} {ch.label}
              </button>
            ))}
          </div>
        </div>

        {/* What you know */}
        <div className="mb-4">
          <label className={`text-sm font-bold ${c.text} block mb-1.5`}>
            What do you know about them? <span className={`font-normal ${c.textMuted}`}>(optional — massively improves results)</span>
          </label>
          <input
            type="text"
            value={whatYouKnow}
            onChange={e => setWhatYouKnow(e.target.value)}
            placeholder="e.g., they recently spoke at SXSW, they wrote a book on leadership, they post about running"
            className={`w-full px-4 py-3 border rounded-xl text-sm ${c.inputBg} ${c.inputBorder} ${c.text} ${c.inputFocus} outline-none focus:ring-2`}
          />
        </div>

        {/* Your background */}
        <div className="mb-4">
          <label className={`text-sm font-bold ${c.text} block mb-1.5`}>
            Your background <span className={`font-normal ${c.textMuted}`}>(optional)</span>
          </label>
          <input
            type="text"
            value={yourBackground}
            onChange={e => setYourBackground(e.target.value)}
            placeholder="e.g., 5 years in marketing, CS student at MIT, freelance designer"
            className={`w-full px-4 py-3 border rounded-xl text-sm ${c.inputBg} ${c.inputBorder} ${c.text} ${c.inputFocus} outline-none focus:ring-2`}
          />
        </div>

        {/* Tone */}
        <div className="mb-5">
          <label className={`text-sm font-bold ${c.text} block mb-2`}>Boldness level</label>
          <div className="flex gap-2">
            {TONES.map(t => (
              <button
                key={t.value}
                onClick={() => setTone(t.value)}
                className={`flex-1 py-2.5 rounded-xl text-[11px] font-bold border transition-colors min-h-[44px] flex flex-col items-center gap-0.5 ${
                  tone === t.value ? c.pillActive : c.pillInactive
                }`}
              >
                <span className="text-base">{t.emoji}</span>
                <span>{t.label}</span>
                <span className={`font-normal text-[9px] ${tone === t.value ? 'opacity-80' : c.textMuted}`}>{t.desc}</span>
              </button>
            ))}
          </div>
        </div>

        {/* Actions */}
        <div className="flex gap-3">
          <button
            onClick={generate}
            disabled={loading || !who.trim() || !why.trim()}
            className={`flex-1 ${c.btnGold} disabled:opacity-40 disabled:cursor-not-allowed font-bold py-3 px-6 rounded-xl flex items-center justify-center gap-2 min-h-[48px] shadow-lg`}
          >
            {loading ? (
              <><span className="animate-spin inline-block">⏳</span> Crafting openers...</>
            ) : (
              <><span>📬</span> Craft My Opener</>
            )}
          </button>
          {results && (
            <button onClick={handleReset} className={`px-5 py-3 ${c.btnSecondary} rounded-xl font-medium min-h-[48px]`}>
              New
            </button>
          )}
        </div>

        {/* Pre-result cross-ref */}
        {!results && (
          <div className={`mt-5 pt-4 border-t ${c.divider}`}>
            <p className={`text-xs ${c.textMuted}`}>
              🎭 Need to explain your story first? <a href="/TheAlibi" target="_blank" rel="noopener noreferrer" className={`underline ${c.link}`}>The Alibi</a> frames your narrative.
            </p>
          </div>
        )}
      </div>

      {/* Error */}
      {error && (
        <div className={`${c.error} border rounded-xl p-4 flex items-start gap-3`}>
          <span className="flex-shrink-0 mt-0.5">⚠️</span>
          <p className="text-sm">{error}</p>
        </div>
      )}

      {/* ══════════════════════════════════════════════════════════ */}
      {/* RESULTS                                                  */}
      {/* ══════════════════════════════════════════════════════════ */}
      {r && (
        <div className="space-y-4">

          <div className="flex justify-end">
            <CopyBtn content={buildFullText()} label="Copy All" />
          </div>

          {/* ── SITUATION READ ── */}
          {r.situation_read && (
            <div className={`${c.card} border ${c.cardBorder} rounded-xl p-5`}>
              <p className={`text-sm ${c.textSecondary} leading-relaxed`}>{r.situation_read}</p>
            </div>
          )}

          {/* ── SUBJECT LINE ── */}
          {r.subject_line && (
            <div className={`${c.cardAlt} border ${c.cardBorder} rounded-xl p-4 flex items-center justify-between`}>
              <div>
                <p className={`text-[10px] font-bold ${c.textMuted} mb-0.5`}>Subject line</p>
                <p className={`text-sm font-bold ${c.heading}`}>{r.subject_line}</p>
              </div>
              <CopyBtn content={r.subject_line} label="Copy" />
            </div>
          )}

          {/* ── OPENERS ── */}
          {r.openers?.length > 0 && (
            <div className="space-y-3">
              {r.openers.map((opener, idx) => (
                <div key={idx} className={`${c.card} border ${c.cardBorder} rounded-xl overflow-hidden`}>
                  <div className="p-5">
                    {/* Header */}
                    <div className="flex items-start justify-between mb-3">
                      <div>
                        <h4 className={`text-sm font-bold ${c.heading}`}>{boldnessEmoji(opener.boldness)} {opener.label}</h4>
                        <div className="flex gap-1.5 mt-1">
                          <span className={`text-[9px] font-bold px-2 py-0.5 rounded-full border ${boldnessColor(opener.boldness)}`}>
                            {opener.boldness}
                          </span>
                          {opener.response_rate && (
                            <span className={`text-[9px] font-bold px-2 py-0.5 rounded-full border ${responseColor(opener.response_rate)}`}>
                              {opener.response_rate} response rate
                            </span>
                          )}
                        </div>
                      </div>
                    </div>

                    {/* The message */}
                    <div className={`${c.cardAlt} rounded-lg p-4 mb-3`}>
                      <p className={`text-sm ${c.text} leading-relaxed whitespace-pre-line`}>{opener.message}</p>
                    </div>

                    <div className="flex items-center justify-between">
                      <div className="flex-1">
                        {opener.why_it_works && (
                          <p className={`text-xs ${c.textSecondary} mb-1`}>
                            <span className="font-bold">Why it works:</span> {opener.why_it_works}
                          </p>
                        )}
                        {opener.best_if && (
                          <p className={`text-xs ${c.textMuted} italic`}>Best if: {opener.best_if}</p>
                        )}
                      </div>
                      <CopyBtn content={`${opener.message}${BRAND}`} label="Copy" />
                    </div>
                  </div>
                </div>
              ))}
            </div>
          )}

          {/* ── WHAT NOT TO SAY ── */}
          {r.what_not_to_say?.length > 0 && (
            <div className={`${c.card} border ${c.cardBorder} rounded-xl p-4`}>
              <p className={`text-xs font-bold ${c.heading} mb-2`}>🚫 What NOT to say</p>
              <div className="space-y-1.5">
                {r.what_not_to_say.map((item, i) => (
                  <p key={i} className={`text-xs ${c.textSecondary} leading-relaxed`}>• {item}</p>
                ))}
              </div>
            </div>
          )}

          {/* ── FOLLOW-UP PLAN ── */}
          {r.follow_up_plan && (
            <div className={`${c.card} border ${c.cardBorder} rounded-xl p-4`}>
              <p className={`text-xs font-bold ${c.heading} mb-3`}>📅 Follow-up plan</p>
              <div className="space-y-3">
                <div>
                  <p className={`text-[10px] font-bold ${c.textMuted} mb-0.5`}>When to follow up</p>
                  <p className={`text-xs ${c.textSecondary}`}>{r.follow_up_plan.when}</p>
                </div>
                {r.follow_up_plan.message && (
                  <div className={`${c.cardAlt} rounded-lg p-3 flex items-start justify-between gap-2`}>
                    <div className="flex-1">
                      <p className={`text-[10px] font-bold ${c.textMuted} mb-0.5`}>Follow-up message</p>
                      <p className={`text-xs ${c.text}`}>{r.follow_up_plan.message}</p>
                    </div>
                    <CopyBtn content={`${r.follow_up_plan.message}${BRAND}`} label="Copy" />
                  </div>
                )}
                <div>
                  <p className={`text-[10px] font-bold ${c.textMuted} mb-0.5`}>When to stop</p>
                  <p className={`text-xs ${c.textSecondary}`}>{r.follow_up_plan.when_to_stop}</p>
                </div>
              </div>
            </div>
          )}

          {/* ── POWER MOVE ── */}
          {r.power_move && (
            <div className={`${c.warning} border rounded-xl p-4 flex items-start gap-3`}>
              <span className="flex-shrink-0 mt-0.5">⚡</span>
              <div>
                <p className="text-xs font-bold mb-1">Power move (high-risk, high-reward)</p>
                <p className="text-sm leading-relaxed">{r.power_move}</p>
              </div>
            </div>
          )}

          {/* ── CROSS-REFERENCES ── */}
          <div className={`${c.card} border ${c.cardBorder} rounded-xl p-4`}>
            <p className={`text-xs font-bold ${c.text} mb-2`}>Related tools</p>
            <div className="flex flex-wrap gap-2">
              <a href="/TheAlibi" target="_blank" rel="noopener noreferrer" className={`text-xs px-3 py-1.5 rounded-lg border ${c.pillInactive} no-underline`}>
                🎭 The Alibi
              </a>
              <a href="/VelvetHammer" target="_blank" rel="noopener noreferrer" className={`text-xs px-3 py-1.5 rounded-lg border ${c.pillInactive} no-underline`}>
                🔨 Velvet Hammer
              </a>
              <a href="/BragSheetBuilder" target="_blank" rel="noopener noreferrer" className={`text-xs px-3 py-1.5 rounded-lg border ${c.pillInactive} no-underline`}>
                🏆 Brag Sheet Builder
              </a>
            </div>
          </div>
        </div>
      )}
    </div>
  );
};

ColdOpenCraft.displayName = 'ColdOpenCraft';
export default ColdOpenCraft;
