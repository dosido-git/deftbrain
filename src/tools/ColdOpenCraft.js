import React, { useState, useCallback, useRef, useEffect } from 'react';
import { ActionBar } from '../components/ActionButtons';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { usePersistentState } from '../hooks/usePersistentState';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const CHANNELS = [
  { value: 'email',        label: 'Email',           emoji: '📧' },
  { value: 'linkedin',     label: 'LinkedIn',         emoji: '💼' },
  { value: 'twitter_dm',   label: 'Twitter/X DM',     emoji: '🐦' },
  { value: 'instagram_dm', label: 'Instagram DM',     emoji: '📸' },
  { value: 'text',         label: 'Text / iMessage',  emoji: '💬' },
  { value: 'in_person',    label: 'In person',        emoji: '🤝' },
];

const TONES = [
  { value: 'safe',   label: 'Safe',     emoji: '🛡️', desc: 'Polite, conventional' },
  { value: 'medium', label: 'Balanced', emoji: '⚖️', desc: 'Personal, warm' },
  { value: 'bold',   label: 'Bold',     emoji: '🔥', desc: 'Pattern-breaking' },
];

// ════════════════════════════════════════════════════════════
// MAIN COMPONENT
// ════════════════════════════════════════════════════════════
const ColdOpenCraft = ({ tool }) => {
  const { callToolEndpoint, loading } = useClaudeAPI();
  const { isDark } = useTheme();

  const c = {
    card:          isDark ? 'bg-zinc-800'        : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50'     : 'bg-slate-50',
    text:          isDark ? 'text-zinc-50'       : 'text-slate-900',
    textSecondary: isDark ? 'text-zinc-300'      : 'text-slate-600',
    textMuted:     isDark ? 'text-zinc-500'      : 'text-slate-400',
    input:         isDark
      ? 'bg-zinc-900 border-zinc-700 text-zinc-50 placeholder:text-zinc-500 focus:border-cyan-500'
      : 'bg-slate-50 border-slate-200 text-slate-900 placeholder:text-slate-400 focus:border-cyan-600',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white' : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200' : 'bg-slate-100 hover:bg-slate-200 text-slate-700',
    border:        isDark ? 'border-zinc-700'    : 'border-slate-200',
    success:       isDark ? 'bg-emerald-900/30 border-emerald-700 text-emerald-300' : 'bg-emerald-50 border-emerald-200 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-300'       : 'bg-amber-50 border-amber-200 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-300'             : 'bg-red-50 border-red-200 text-red-800',
    // Tool-specific: boldness / channel pill states
    pillActive:    isDark ? 'bg-cyan-600 border-cyan-500 text-white'                         : 'bg-cyan-600 border-cyan-700 text-white',
    pillInactive:  isDark ? 'bg-zinc-700 border-zinc-600 text-zinc-300 hover:border-zinc-500' : 'bg-white border-slate-200 text-slate-600 hover:border-slate-400',
  };

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';

  // ── Persisted state ──
  const [who,             setWho]             = usePersistentState('cold-open-craft-who', '');
  const [why,             setWhy]             = usePersistentState('cold-open-craft-why', '');
  const [channel,         setChannel]         = usePersistentState('cold-open-craft-channel', 'email');
  const [whatYouKnow,     setWhatYouKnow]     = usePersistentState('cold-open-craft-know', '');
  const [yourBackground,  setYourBackground]  = usePersistentState('cold-open-craft-bg', '');
  const [tone,            setTone]            = usePersistentState('cold-open-craft-tone', 'medium');
  const [results,         setResults]         = usePersistentState('cold-open-craft-results', null);
  const [history,         setHistory]         = usePersistentState('cold-open-craft-history', []);

  const [error, setError] = useState('');
  const resultsRef = useRef(null);

  // ── Document-level Ctrl+Enter submit ──
  useEffect(() => {
    const handler = (e) => {
      if ((e.ctrlKey || e.metaKey) && e.key === 'Enter') {
        e.preventDefault();
        if (!loading && who.trim() && why.trim()) generate();
      }
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
  }); // eslint-disable-line react-hooks/exhaustive-deps

  // ── Actions ──
  const generate = useCallback(async () => {
    if (!who.trim() || !why.trim()) return;
    setError('');
    setResults(null);
    try {
      const data = await callToolEndpoint('cold-open-craft', {
        who: who.trim(),
        why: why.trim(),
        channel,
        whatYouKnow: whatYouKnow.trim() || null,
        yourBackground: yourBackground.trim() || null,
        tone,
      });
      setResults(data);
      const entry = {
        id: Date.now(),
        timestamp: new Date().toISOString(),
        preview: who.trim().slice(0, 40),
        result: data,
      };
      setHistory(prev => [entry, ...prev].slice(0, 6));
    } catch (err) {
      setError(err.message || 'Failed to generate openers.');
    }
  }, [who, why, channel, whatYouKnow, yourBackground, tone, callToolEndpoint]); // eslint-disable-line react-hooks/exhaustive-deps

  const handleReset = useCallback(() => {
    setWho(''); setWhy(''); setChannel('email'); setWhatYouKnow('');
    setYourBackground(''); setTone('medium'); setResults(null); setError('');
  }, []); // eslint-disable-line react-hooks/exhaustive-deps

  // ── Build copy text ──
  const buildFullText = useCallback(() => {
    if (!results) return '';
    const r = results;
    let text = `📬 COLD OPEN CRAFT\nReaching out to: ${who}\n`;
    if (r.situation_read) text += `\n${r.situation_read}\n`;
    if (r.subject_line) text += `\nSubject: ${r.subject_line}\n`;
    if (r.openers?.length) {
      r.openers.forEach(o => {
        text += `\n--- ${o.label} [${o.boldness}] ---\n${o.message}\n`;
        if (o.why_it_works) text += `Why it works: ${o.why_it_works}\n`;
      });
    }
    if (r.what_not_to_say?.length) {
      text += `\nDO NOT SAY:\n${r.what_not_to_say.map(x => `• ${x}`).join('\n')}\n`;
    }
    if (r.follow_up_plan) {
      text += `\nFOLLOW-UP PLAN:\nWait: ${r.follow_up_plan.when}\n${r.follow_up_plan.message}\nStop when: ${r.follow_up_plan.when_to_stop}`;
    }
    if (r.power_move) text += `\n\nPOWER MOVE:\n${r.power_move}`;
    return text + BRAND;
  }, [results, who]);

  const boldnessEmoji  = (b) => b === 'safe' ? '🛡️' : b === 'bold' ? '🔥' : '⚖️';
  const boldnessColor  = (b) => b === 'safe' ? c.success : b === 'bold' ? c.warning : c.warning;
  const responseColor  = (rv) => rv === 'high' ? c.success : rv === 'low' ? c.danger : c.warning;

  const r = results;

  // ════════════════════════════════════════════════════════════
  // RENDER
  // ════════════════════════════════════════════════════════════
  return (
    <div className={`space-y-4 ${c.text}`}>

      {/* ── INPUT CARD ── */}
      <div className={`${c.card} border ${c.border} rounded-xl shadow-lg p-5`}>
        {/* Standard header */}
        <div className={`mb-4 pb-3 border-b ${c.border}`}>
          <h2 className={`text-xl font-bold ${c.text} flex items-center gap-2`}>
            <span>{tool?.icon ?? '📬'}</span>{tool?.title ?? 'Cold Open Craft'}
          </h2>
          <p className={`text-sm ${c.textSecondary}`}>{tool?.tagline ?? 'First messages that actually get responses.'}</p>
        </div>

        {/* Pre-result cross-ref */}
        <p className={`text-xs text-center ${c.textMuted} mb-4`}>
          Need to frame your story first?{' '}
          <a href="/TheAlibi" className={linkStyle}>The Alibi</a> sharpens your narrative before you reach out.
        </p>

        <div className="space-y-4">
          {/* Who */}
          <div>
            <label className={`text-sm font-bold ${c.text} block mb-1.5`}>
              Who are you reaching out to? <span className={c.textMuted}>*</span>
            </label>
            <input
              type="text"
              value={who}
              onChange={e => setWho(e.target.value)}
              placeholder="e.g., a hiring manager at Google, a podcast host, a potential mentor"
              className={`w-full px-4 py-3 border rounded-xl text-sm focus:outline-none transition-colors ${c.input}`}
            />
          </div>

          {/* Why */}
          <div>
            <label className={`text-sm font-bold ${c.text} block mb-1.5`}>
              Why are you reaching out? <span className={c.textMuted}>*</span>
            </label>
            <textarea
              value={why}
              onChange={e => setWhy(e.target.value)}
              placeholder="e.g., I want to pitch my startup for their podcast, I want an informational interview about switching to product management"
              rows={2}
              className={`w-full px-4 py-3 border rounded-xl text-sm focus:outline-none transition-colors resize-none ${c.input}`}
            />
            <p className={`text-xs ${c.textMuted} mt-1`}>Ctrl+Enter to generate</p>
          </div>

          {/* Channel */}
          <div>
            <label className={`text-sm font-bold ${c.text} block mb-2`}>Channel</label>
            <div className="flex flex-wrap gap-1.5">
              {CHANNELS.map(ch => (
                <button
                  key={ch.value}
                  onClick={() => setChannel(ch.value)}
                  className={`px-3 py-1.5 rounded-lg text-xs font-bold border transition-colors min-h-[32px] ${
                    channel === ch.value ? c.pillActive : c.pillInactive
                  }`}
                >
                  {ch.emoji} {ch.label}
                </button>
              ))}
            </div>
          </div>

          {/* What you know */}
          <div>
            <label className={`text-sm font-bold ${c.text} block mb-1.5`}>
              What do you know about them?{' '}
              <span className={`font-normal ${c.textMuted}`}>(optional — massively improves results)</span>
            </label>
            <input
              type="text"
              value={whatYouKnow}
              onChange={e => setWhatYouKnow(e.target.value)}
              placeholder="e.g., they recently spoke at SXSW, they wrote a book on leadership, they post about running"
              className={`w-full px-4 py-3 border rounded-xl text-sm focus:outline-none transition-colors ${c.input}`}
            />
          </div>

          {/* Your background */}
          <div>
            <label className={`text-sm font-bold ${c.text} block mb-1.5`}>
              Your background <span className={`font-normal ${c.textMuted}`}>(optional)</span>
            </label>
            <input
              type="text"
              value={yourBackground}
              onChange={e => setYourBackground(e.target.value)}
              placeholder="e.g., 5 years in marketing, CS student at MIT, freelance designer"
              className={`w-full px-4 py-3 border rounded-xl text-sm focus:outline-none transition-colors ${c.input}`}
            />
          </div>

          {/* Tone */}
          <div>
            <label className={`text-sm font-bold ${c.text} block mb-2`}>Boldness level</label>
            <div className="flex gap-2">
              {TONES.map(t => (
                <button
                  key={t.value}
                  onClick={() => setTone(t.value)}
                  className={`flex-1 py-2.5 rounded-xl text-[11px] font-bold border transition-colors min-h-[44px] flex flex-col items-center gap-0.5 ${
                    tone === t.value ? c.pillActive : c.pillInactive
                  }`}
                >
                  <span className="text-base">{t.emoji}</span>
                  <span>{t.label}</span>
                  <span className={`font-normal text-[9px] ${tone === t.value ? 'opacity-80' : c.textMuted}`}>{t.desc}</span>
                </button>
              ))}
            </div>
          </div>

          {error && (
            <div className={`${c.danger} border rounded-xl p-4 flex items-start gap-3`}>
              <span className="flex-shrink-0">⚠️</span>
              <p className="text-sm">{error}</p>
            </div>
          )}

          {/* Submit */}
          <button
            onClick={generate}
            disabled={loading || !who.trim() || !why.trim()}
            className={`w-full ${c.btnPrimary} disabled:opacity-40 disabled:cursor-not-allowed font-bold py-3 px-6 rounded-xl flex items-center justify-center gap-2 min-h-[48px] transition-colors`}
          >
            {loading
              ? <><span className="animate-spin inline-block">{tool?.icon ?? '📬'}</span> Crafting openers...</>
              : <><span>{tool?.icon ?? '📬'}</span> Craft My Opener</>}
          </button>
        </div>
      </div>

      {/* ── HISTORY ── */}
      {history.length > 0 && !results && (
        <div className={`${c.card} rounded-xl border ${c.border} p-4`}>
          <h3 className={`text-sm font-bold ${c.text} mb-3`}>🕐 Recent Outreach</h3>
          <div className="space-y-1.5">
            {history.map(entry => (
              <button
                key={entry.id}
                onClick={() => setResults(entry.result)}
                className={`w-full text-left px-3 py-2 rounded-lg ${c.btnSecondary} text-xs`}
              >
                <span className={c.textMuted}>
                  {new Date(entry.timestamp).toLocaleDateString(undefined, { month: 'short', day: 'numeric', hour: 'numeric', minute: '2-digit' })}
                </span>
                <span className={`ml-2 ${c.text}`}>{entry.preview}…</span>
              </button>
            ))}
          </div>
        </div>
      )}

      {/* ════════════════════════════════════════════════════════
          RESULTS
          ════════════════════════════════════════════════════════ */}
      {r && (
        <div ref={resultsRef} className="space-y-4">

          {/* ActionBar + Reset — top of results */}
          <div className="flex items-center justify-between gap-3 flex-wrap">
            <ActionBar content={buildFullText()} title={`Cold Open Craft: ${who}`} />
            <button
              onClick={handleReset}
              className={`px-4 py-2 rounded-lg text-sm font-medium transition-colors ${c.btnSecondary}`}
            >
              ↺ Start Over
            </button>
          </div>

          {/* Situation read */}
          {r.situation_read && (
            <div className={`${c.card} border ${c.border} rounded-xl p-5`}>
              <p className={`text-xs font-bold uppercase tracking-wide ${c.textMuted} mb-2`}>🎯 Situation Read</p>
              <p className={`text-sm ${c.textSecondary} leading-relaxed`}>{r.situation_read}</p>
            </div>
          )}

          {/* Subject line */}
          {r.subject_line && (
            <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
              <p className={`text-[10px] font-bold ${c.textMuted} mb-0.5`}>Subject line</p>
              <p className={`text-sm font-bold ${c.text}`}>{r.subject_line}</p>
            </div>
          )}

          {/* Openers */}
          {r.openers?.length > 0 && (
            <div className="space-y-3">
              {r.openers.map((opener, idx) => (
                <div key={idx} className={`${c.card} border ${c.border} rounded-xl overflow-hidden`}>
                  <div className="p-5">
                    <div className="flex items-start justify-between mb-3">
                      <div>
                        <h4 className={`text-sm font-bold ${c.text}`}>{boldnessEmoji(opener.boldness)} {opener.label}</h4>
                        <div className="flex gap-1.5 mt-1">
                          <span className={`text-[9px] font-bold px-2 py-0.5 rounded-full border ${boldnessColor(opener.boldness)}`}>
                            {opener.boldness}
                          </span>
                          {opener.response_rate && (
                            <span className={`text-[9px] font-bold px-2 py-0.5 rounded-full border ${responseColor(opener.response_rate)}`}>
                              {opener.response_rate} response rate
                            </span>
                          )}
                        </div>
                      </div>
                    </div>

                    {/* The message */}
                    <div className={`${c.cardAlt} rounded-lg p-4 mb-3`}>
                      <p className={`text-sm ${c.text} leading-relaxed whitespace-pre-line`}>{opener.message}</p>
                    </div>

                    {opener.why_it_works && (
                      <p className={`text-xs ${c.textSecondary} mb-1`}>
                        <span className="font-bold">Why it works:</span> {opener.why_it_works}
                      </p>
                    )}
                    {opener.best_if && (
                      <p className={`text-xs ${c.textMuted} italic`}>Best if: {opener.best_if}</p>
                    )}
                  </div>
                </div>
              ))}
            </div>
          )}

          {/* What not to say */}
          {r.what_not_to_say?.length > 0 && (
            <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
              <p className={`text-xs font-bold ${c.text} mb-2`}>🚫 What NOT to say</p>
              <div className="space-y-1.5">
                {r.what_not_to_say.map((item, i) => (
                  <p key={i} className={`text-xs ${c.textSecondary} leading-relaxed`}>• {item}</p>
                ))}
              </div>
            </div>
          )}

          {/* Follow-up plan */}
          {r.follow_up_plan && (
            <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
              <p className={`text-xs font-bold ${c.text} mb-3`}>📅 Follow-up plan</p>
              <div className="space-y-3">
                <div>
                  <p className={`text-[10px] font-bold ${c.textMuted} mb-0.5`}>When to follow up</p>
                  <p className={`text-xs ${c.textSecondary}`}>{r.follow_up_plan.when}</p>
                </div>
                {r.follow_up_plan.message && (
                  <div className={`${c.cardAlt} rounded-lg p-3`}>
                    <p className={`text-[10px] font-bold ${c.textMuted} mb-0.5`}>Follow-up message</p>
                    <p className={`text-xs ${c.text}`}>{r.follow_up_plan.message}</p>
                  </div>
                )}
                <div>
                  <p className={`text-[10px] font-bold ${c.textMuted} mb-0.5`}>When to stop</p>
                  <p className={`text-xs ${c.textSecondary}`}>{r.follow_up_plan.when_to_stop}</p>
                </div>
              </div>
            </div>
          )}

          {/* Power move */}
          {r.power_move && (
            <div className={`${c.warning} border rounded-xl p-4 flex items-start gap-3`}>
              <span className="flex-shrink-0 mt-0.5">⚡</span>
              <div>
                <p className="text-xs font-bold mb-1">Power move (high-risk, high-reward)</p>
                <p className="text-sm leading-relaxed">{r.power_move}</p>
              </div>
            </div>
          )}

          {/* Cross-refs — post-result */}
          <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4 space-y-2`}>
            <p className={`text-xs ${c.textMuted} text-center`}>
              Need help with follow-up tone?{' '}
              <a href="/VelvetHammer" className={linkStyle}>Velvet Hammer</a>{' '}
              gets the message right when stakes are high.
            </p>
            {(r.openers?.some(o => o.boldness === 'bold') || yourBackground) && (
              <p className={`text-xs ${c.textMuted} text-center`}>
                Turn your wins into a credibility hook:{' '}
                <a href="/BragSheetBuilder" className={linkStyle}>Brag Sheet Builder</a>{' '}
                gives you polished, copy-ready highlights.
              </p>
            )}
          </div>

          {/* AI disclaimer */}
          <p className={`text-xs text-center ${c.textMuted}`}>
            AI-generated — review all messages before sending.
          </p>

        </div>
      )}
    </div>
  );
};

ColdOpenCraft.displayName = 'ColdOpenCraft';
export default ColdOpenCraft;
