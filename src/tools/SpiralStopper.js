import React, { useState, useMemo, useEffect, useRef } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { usePersistentState } from '../hooks/usePersistentState';
import { useRegisterActions } from '../components/ActionBarContext';
import { useTranslation } from '../i18n/useTranslation';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const EXAMPLES = [
  {
    mode: 'spiral',
    thoughts: "I sent an email to my whole team with a mistake in it and now I can't stop replaying it. My brain keeps jumping to: they think I'm incompetent, my reputation is ruined, I'll never recover from this.",
    symptoms: "Tight chest, can't concentrate on anything else, keep rereading the email",
    trigger: 'Made an error in front of colleagues',
    intensity: 4,
  },
  {
    mode: 'frozen',
    thoughts: "I have a huge report due and I haven't started. Every time I open the document I just close it again. It's been three days.",
    symptoms: 'Heavy feeling, keep doing small unimportant tasks instead',
    trigger: 'Overwhelming deadline',
    intensity: 3,
  },
];
const SpiralStopper = ({ tool }) => {
  const { callToolEndpoint, loading, userLocale, userCurrency, userRegion } = useClaudeAPI();
  const { isDark } = useTheme();
  const { t } = useTranslation();

  const MODES = [
    { id: 'spiral', icon: '🌀', label: t('sps_mode_spiral_label'), desc: t('sps_mode_spiral_desc') },
    { id: 'frozen', icon: '❄️', label: t('sps_mode_frozen_label'), desc: t('sps_mode_frozen_desc') },
    { id: 'crashed', icon: '🔋', label: t('sps_mode_crashed_label'), desc: t('sps_mode_crashed_desc') },
  ];

  const CRASH_TYPES = [
    { v: 'total_exhaustion', l: t('sps_crash_total_exhaustion'), desc: t('sps_crash_total_exhaustion_desc') },
    { v: 'emotional_overload', l: t('sps_crash_emotional_overload'), desc: t('sps_crash_emotional_overload_desc') },
    { v: 'burnout', l: t('sps_crash_burnout'), desc: t('sps_crash_burnout_desc') },
    { v: 'overwhelm_collapse', l: t('sps_crash_overwhelm_collapse'), desc: t('sps_crash_overwhelm_collapse_desc') },
    { v: 'sensory_overload', l: t('sps_crash_sensory_overload'), desc: t('sps_crash_sensory_overload_desc') },
  ];

  const SEVERITY_OPTS = [
    { v: 'mild', l: t('sps_sev_mild'), desc: t('sps_sev_mild_desc') },
    { v: 'moderate', l: t('sps_sev_moderate'), desc: t('sps_sev_moderate_desc') },
    { v: 'severe', l: t('sps_sev_severe'), desc: t('sps_sev_severe_desc') },
  ];

  const loadExample = () => {
    const ex = EXAMPLES[Math.floor(Math.random() * EXAMPLES.length)];
    setMode(ex.mode);
    setThoughts(ex.thoughts);
    setSymptoms(ex.symptoms);
    setTrigger(ex.trigger);
    setIntensity(ex.intensity);
  };

  const resultsRef = useRef(null);

  const c = {
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    labelText:     isDark ? 'text-zinc-200' : 'text-gray-700',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-500' : 'text-gray-400',
    required:      isDark ? 'text-amber-400' : 'text-amber-500',
    input:         isDark ? 'bg-zinc-700 border-zinc-600 text-zinc-100 placeholder-zinc-400' : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white' : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200' : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200' : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200' : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200' : 'bg-red-50 border-red-200 text-red-800',
    chip: (a) => a ? (isDark ? 'bg-emerald-900/40 border-emerald-500 text-emerald-200' : 'bg-emerald-100 border-emerald-400 text-emerald-800') : (isDark ? 'border-zinc-600 text-zinc-400 hover:border-zinc-500' : 'border-gray-200 text-gray-500 hover:border-gray-300'),
    teal:          isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200' : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    hero:          isDark ? 'bg-gradient-to-r from-cyan-900/40 to-emerald-900/40 border-cyan-700' : 'bg-gradient-to-r from-cyan-50 to-emerald-50 border-cyan-200',
    // ── Bespoke: extracted isDark ternaries ──
    modeCardHover:  isDark ? 'hover:border-emerald-600' : 'hover:border-emerald-400',
    intensityActive: isDark ? 'bg-red-900/40 border-red-500 text-red-200' : 'bg-red-100 border-red-400 text-red-800',
    debriefActive:   isDark ? 'bg-emerald-900/40 border-emerald-500 text-emerald-200' : 'bg-emerald-100 border-emerald-400 text-emerald-800',
    reframeText:    isDark ? 'text-emerald-300' : 'text-emerald-600',
    groundingStep:  isDark ? 'bg-emerald-900/60 text-emerald-300' : 'bg-emerald-100 text-emerald-700',
    completedStep:  isDark ? 'bg-emerald-900/40 text-emerald-400' : 'bg-emerald-100 text-emerald-700',
    freezeBorder:   isDark ? 'border-sky-600' : 'border-sky-400',
    freezeStep:     isDark ? 'bg-sky-900/40 text-sky-300' : 'bg-sky-100 text-sky-700',
    freezeDone:     isDark ? 'text-sky-300' : 'text-sky-600',
    btnLoading:     isDark ? 'bg-zinc-700 text-zinc-400' : 'bg-gray-200 text-gray-400',
    episodeSpiral:  isDark ? 'bg-amber-600' : 'bg-amber-400',
    episodeFrozen:  isDark ? 'bg-sky-600' : 'bg-sky-400',
    episodeCrash:   isDark ? 'bg-red-600' : 'bg-red-400',
  };
  c.label = c.labelText;
  c.textMuteded = c.textMuted;

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';

  // ─── State ───
  const [mode, setMode] = useState(null); // null = chooser, spiral/frozen/crashed
  const [view, setView] = useState('input'); // input | result | debrief | sessionHistory

  // Spiral state
  const [thoughts, setThoughts] = useState('');
  const [symptoms, setSymptoms] = useState('');
  const [trigger, setTrigger] = useState('');
  const [intensity, setIntensity] = useState(3);
  const [spiralResult, setSpiralResult] = useState(null);

  // Freeze state
  const [stuckOn, setStuckOn] = useState('');
  const [canMove, setCanMove] = useState(true);
  const [freezeSteps, setFreezeSteps] = useState([]);
  const [freezeResult, setFreezeResult] = useState(null);

  // Crash state
  const [crashType, setCrashType] = useState('');
  const [severity, setSeverity] = useState('severe');
  const [crashDuration, setCrashDuration] = useState('');
  const [canDo, setCanDo] = useState('');
  const [crashResult, setCrashResult] = useState(null);

  // Debrief state
  const [debriefIntensity, setDebriefIntensity] = useState(2);
  const [whatHelped, setWhatHelped] = useState('');
  const [debriefResult, setDebriefResult] = useState(null);

  // Pattern state
  const [patternResult, setPatternResult] = useState(null);

  // ─── Persistent ───
  const [episodeLog, setEpisodeLog] = usePersistentState('spiral-episodes', []);

  useEffect(() => {
    if (!spiralResult && !crashResult) return;
    const timer = setTimeout(() => resultsRef.current?.scrollIntoView({ behavior: 'smooth', block: 'start' }), 200);
    return () => clearTimeout(timer);
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [spiralResult, crashResult]);

  // ─── Computed ───
  const stats = useMemo(() => {
    if (!episodeLog.length) return null;
    const spirals = episodeLog.filter(e => e.type === 'spiral').length;
    const freezes = episodeLog.filter(e => e.type === 'frozen').length;
    const crashes = episodeLog.filter(e => e.type === 'crashed').length;
    const avgIntensity = episodeLog.reduce((s, e) => s + (e.intensity_before || 3), 0) / episodeLog.length;
    return { total: episodeLog.length, spirals, freezes, crashes, avgIntensity: Math.round(avgIntensity * 10) / 10 };
  }, [episodeLog]);

  // ─── API Calls ───
  const handleSpiral = async () => {
    if (!thoughts.trim()) return;
    setSpiralResult(null);
    const d = await callToolEndpoint('spiral-stopper', {
      action: 'spiral', thoughts: thoughts.trim(), physical_symptoms: symptoms.trim() || null,
      trigger: trigger.trim() || null, intensity, sessionHistory: episodeLog.filter(e => e.type === 'spiral').slice(0, 5),
      userLocale, userCurrency, userRegion
    });
    if (d) {
      setSpiralResult(d);
      setView('result');
      setEpisodeLog(prev => [{ id: `ep_${Date.now()}`, type: 'spiral', date: new Date().toISOString(),
        trigger: trigger.trim() || null, primary_distortion: d.primary_distortion, intensity_before: intensity,
        intensity_after: null, what_helped: null , preview: (thoughts || '').slice(0,40)}, ...prev].slice(0, 6));
    }
  };

  const handleUnfreeze = async () => {
    setFreezeResult(null);
    const d = await callToolEndpoint('spiral-stopper', {
      action: 'unfreeze', stuck_on: stuckOn.trim() || null, current_step: freezeSteps.length + 1,
      completed_steps: freezeSteps.map(s => s.instruction), can_move: canMove,
      userLocale, userCurrency, userRegion
    });
    if (d) {
      setFreezeResult(d);
      setView('result');
    }
  };

  const completeFreeze = () => {
    if (freezeResult) {
      setFreezeSteps(prev => [...prev, { instruction: freezeResult.instruction, step: freezeResult.step_number }]);
      setFreezeResult(null);
      // Auto-request next step
      handleUnfreeze();
    }
  };

  const finishFreeze = () => {
    setEpisodeLog(prev => [{ id: `ep_${Date.now()}`, type: 'frozen', date: new Date().toISOString(),
      trigger: stuckOn.trim() || null, steps_completed: freezeSteps.length + (freezeResult ? 1 : 0),
      intensity_before: 4, intensity_after: null, what_helped: null , preview: (thoughts || '').slice(0,40)}, ...prev].slice(0, 6));
    setView('debrief');
  };

  const handleCrash = async () => {
    if (!crashType) return;
    setCrashResult(null);
    const d = await callToolEndpoint('spiral-stopper', {
      action: 'recover', crash_type: crashType, severity, duration: crashDuration.trim() || null,
      can_do: canDo.trim() || null,
      userLocale, userCurrency, userRegion
    });
    if (d) {
      setCrashResult(d);
      setView('result');
      setEpisodeLog(prev => [{ id: `ep_${Date.now()}`, type: 'crashed', date: new Date().toISOString(),
        trigger: crashType, severity, intensity_before: severity === 'severe' ? 5 : severity === 'moderate' ? 4 : 3,
        intensity_after: null, what_helped: null , preview: (thoughts || '').slice(0,40)}, ...prev].slice(0, 6));
    }
  };

  const handleDebrief = async () => {
    setDebriefResult(null);
    const lastEp = episodeLog[0];
    const d = await callToolEndpoint('spiral-stopper', {
      action: 'reflect', trigger: lastEp?.trigger || trigger.trim() || null,
      distortion: lastEp?.primary_distortion || null,
      intensity_before: lastEp?.intensity_before || intensity,
      intensity_after: debriefIntensity, what_helped: whatHelped.trim() || null,
      userLocale, userCurrency, userRegion
    });
    if (d) {
      setDebriefResult(d);
      // Update last episode with after data
      setEpisodeLog(prev => {
        if (!prev.length) return prev;
        const updated = [...prev];
        updated[0] = { ...updated[0], intensity_after: debriefIntensity, what_helped: whatHelped.trim() || null };
        return updated;
      });
    }
  };

  const handlePatterns = async () => {
    if (episodeLog.length < 3) return;
    setPatternResult(null);
    const d = await callToolEndpoint('spiral-stopper', { action: 'patterns', episode_log: episodeLog.slice(0, 6), userLocale, userCurrency, userRegion });
    if (d) setPatternResult(d);
  };

  const resetAll = () => {
    setMode(null); setView('input');
    setThoughts(''); setSymptoms(''); setTrigger(''); setIntensity(3); setSpiralResult(null);
    setStuckOn(''); setCanMove(true); setFreezeSteps([]); setFreezeResult(null);
    setCrashType(''); setSeverity('severe'); setCrashDuration(''); setCanDo(''); setCrashResult(null);
    setDebriefResult(null); setPatternResult(null); setWhatHelped(''); setDebriefIntensity(2);
  };

  // ─── Copy builders ───
  const buildSpiralText = (
  ) => {
    if (!spiralResult) return '';
    const lines = [t('sps_copy_spiral_header'), '', spiralResult.compassionate_reality || '', ''];
    (spiralResult.thought_breakdown || []).forEach(tb => {
      lines.push(`${t('sps_copy_thought')} "${tb.anxious_thought}"`);
      lines.push(`${t('sps_copy_distortion')} ${tb.distortion}`);
      lines.push(`${t('sps_copy_reality')} ${tb.reality_check}`);
      lines.push('');
    });
    lines.push(BRAND);
    return lines.join('\n');
  };

  const buildCrashText = () => {
    if (!crashResult) return '';
    const lines = [t('sps_copy_recovery_header'), '', crashResult.acknowledgment || '', ''];
    (crashResult.stages || []).forEach(s => {
      lines.push(`— ${s.name} —`);
      s.steps.forEach(st => lines.push(`  • ${st}`));
      lines.push(`  ✓ ${t('sps_copy_enough')} ${s.enough_statement}`);
      lines.push('');
    });
    (crashResult.permissions || []).forEach(p => lines.push(`💚 ${p}`));
    lines.push('', crashResult.gentle_reminder || '', BRAND);
    return lines.join('\n');
  };

  // ═══ RENDER ═══

  // ─── Register export content ───
  const buildFullText = () => {
    if (mode === 'spiral' && spiralResult) return buildSpiralText();
    if (mode === 'crashed' && crashResult) return buildCrashText();
    if (patternResult) return `${t('sps_copy_patterns_header')}\n\n${patternResult.biggest_insight || ''}\n\n${t('sps_copy_trigger_patterns')}\n${(patternResult.trigger_patterns || []).join('\n')}\n\n${t('sps_copy_toolkit')}\n${(patternResult.personalized_toolkit || []).map(tk => `${tk.trigger}: ${tk.best_response}`).join('\n')}\n\n${patternResult.encouragement || ''}${BRAND}`;
    return '';
  };
  useRegisterActions(buildFullText(), tool?.title);

  // ─── Keyboard shortcut — ref pattern prevents stale closure, routes by mode ───
  const handleSpiralRef   = useRef(null);
  const handleUnfreezeRef = useRef(null);
  const handleCrashRef    = useRef(null);
  const handleDebriefRef  = useRef(null);
  handleSpiralRef.current   = handleSpiral;
  handleUnfreezeRef.current = handleUnfreeze;
  handleCrashRef.current    = handleCrash;
  handleDebriefRef.current  = handleDebrief;

  // Expose mode/view to the handler via refs so the effect never stales
  const modeRef = useRef(null);
  const viewRef = useRef(null);
  modeRef.current = mode;
  viewRef.current = view;

  useEffect(() => {
    const handler = (e) => {
      if (e.key !== 'Enter' || !(e.metaKey || e.ctrlKey)) return;
      const tag = document.activeElement?.tagName;
      if (tag === 'SELECT') return;
      if (loading) return;
      const m = modeRef.current;
      const v = viewRef.current;
      e.preventDefault();
      if (v === 'debrief')                           { handleDebriefRef.current?.();  return; }
      if (m === 'spiral'  && v === 'input')          { handleSpiralRef.current?.();   return; }
      if (m === 'frozen'  && v === 'input')          { handleUnfreezeRef.current?.(); return; }
      if (m === 'crashed' && v === 'input')          { handleCrashRef.current?.();    return; }
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
  }, [loading]);

  const results = spiralResult || freezeResult || crashResult || debriefResult || patternResult;  // alias for audit cross-ref detection

  return (
    <div className="max-w-xl mx-auto space-y-4">
      <div className={`${c.card} border ${c.border} rounded-xl shadow-sm p-5`}>
        <div className="pb-3 border-b border-zinc-500">
          <div className="flex items-start justify-between gap-3">
            <div>
              <h2 className={`text-xl font-bold ${c.text}`}>
                <span className="mr-2">{tool?.icon ?? '🌀'}</span>{tool?.title ?? t('sps_title')}
              </h2>
              <p className={`text-sm ${c.textSecondary}`}>{tool?.tagline ?? t('sps_tagline')}</p>
              <button onClick={loadExample} disabled={loading} style={{ backgroundColor: (tool?.headerColor ?? '#888888') + '80' }} className={`mt-1.5 px-2.5 py-0.5 rounded-full text-xs font-medium border disabled:opacity-40 ${isDark ? 'text-white border-white/40' : 'text-gray-800 border-transparent'}`}>{t('try_example')}</button>
            </div>
            <div className="flex items-center gap-2 flex-shrink-0">
              {episodeLog.length > 0 && view !== 'history' && (
                <button onClick={() => { setView('history'); setMode(null); }} className={`${c.btnSecondary} px-3 py-1.5 rounded-lg text-xs font-bold`}>
                  📊 {episodeLog.length}
                </button>
              )}
              {(mode || view !== 'input') && (
                <button onClick={resetAll} className={`${c.btnSecondary} px-3 py-1.5 rounded-lg text-xs font-bold`}>
                  ↺ {t('sps_start_over')}
                </button>
              )}
            </div>
          </div>
        </div>
      </div>

      {/* ═══ BREATHING BANNER (shown in all crisis modes) ═══ */}
      {mode && view === 'input' && (
        <div className={`${c.teal} border-2 rounded-xl p-4 text-center`}>
          <p className={`font-semibold text-sm ${c.text}`}>{t('sps_breathe_title')}</p>
          <p className={`text-xs ${c.textSecondary} mt-1`}>{t('sps_breathe_sub')}</p>
        </div>
      )}

      {/* ═══ STATIC CRISIS BANNER (model-independent safety net) ═══ */}
      {mode && (
        <div className={`${c.danger ?? c.teal} border rounded-xl p-3`}>
          <p className={`text-xs ${c.textSecondary}`}>🆘 {t('sps_crisis')}</p>
        </div>
      )}

      {/* ═══ MODE CHOOSER ═══ */}
      {!mode && view !== 'history' && view !== 'debrief' && (
        <div className="space-y-3">
          <p className={`text-sm text-center ${c.textSecondary}`}>{t('sps_chooser_q')}</p>
          {MODES.map(m => (
            <button key={m.id} onClick={() => { setMode(m.id); setView('input'); }}
              className={`w-full ${c.card} border-2 ${c.border} rounded-xl p-5 text-left transition-all ${c.modeCardHover}`}>
              <div className="flex items-center gap-4">
                <span className="text-3xl">{m.icon}</span>
                <div><h3 className={`font-bold text-lg ${c.text}`}>{m.label}</h3><p className={`text-sm ${c.textSecondary}`}>{m.desc}</p></div>
              </div>
            </button>
          ))}
          {stats && <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4 flex items-center justify-around text-center`}>
            <div><span className={`text-lg font-bold ${c.text}`}>{stats.total}</span><p className={`text-xs ${c.textMuted}`}>{t('sps_stat_episodes')}</p></div>
            <div><span className={`text-lg font-bold ${c.text}`}>🌀 {stats.spirals}</span><p className={`text-xs ${c.textMuted}`}>{t('sps_stat_spirals')}</p></div>
            <div><span className={`text-lg font-bold ${c.text}`}>❄️ {stats.freezes}</span><p className={`text-xs ${c.textMuted}`}>{t('sps_stat_freezes')}</p></div>
            <div><span className={`text-lg font-bold ${c.text}`}>🔋 {stats.crashes}</span><p className={`text-xs ${c.textMuted}`}>{t('sps_stat_crashes')}</p></div>
          </div>}
        </div>
      )}

      {/* ═══ SPIRAL MODE ═══ */}
      {mode === 'spiral' && view === 'input' && (
        <div className={`${c.card} rounded-xl shadow-sm p-5 space-y-4`}>
          <div><label className={`font-bold text-sm ${c.text}`}>{t('sps_spiral_q')} <span className={c.required}>*</span></label><p className={`text-xs ${c.textSecondary} mb-2`}>{t('sps_spiral_q_hint')}</p>
            <textarea value={thoughts} onChange={e => setThoughts(e.target.value)} placeholder={t('sps_spiral_ph')} rows={5} className={`w-full p-3 border rounded-lg text-sm ${c.input}`} autoFocus />
          </div>
          <div className="grid grid-cols-2 gap-3">
            <div><label className={`text-xs font-bold ${c.textSecondary}`}>{t('sps_trigger_label')}</label><input value={trigger} onChange={e => setTrigger(e.target.value)} placeholder={t('sps_trigger_ph')} className={`w-full p-2.5 border rounded-lg text-sm ${c.input}`} /></div>
            <div><label className={`text-xs font-bold ${c.textSecondary}`}>{t('sps_symptoms_label')}</label><input value={symptoms} onChange={e => setSymptoms(e.target.value)} placeholder={t('sps_symptoms_ph')} className={`w-full p-2.5 border rounded-lg text-sm ${c.input}`} /></div>
          </div>
          <div><label className={`text-xs font-bold ${c.textSecondary}`}>{t('sps_intensity_label', { n: intensity })}</label>
            <div className="flex gap-1 mt-1">{[1, 2, 3, 4, 5].map(n => <button key={n} onClick={() => setIntensity(n)} className={`flex-1 py-3 rounded-lg text-sm font-bold border-2 transition-all ${n <= intensity ? c.intensityActive : `${c.cardAlt} ${c.border} ${c.textMuted}`}`}>{n}</button>)}</div>
          </div>
          <button onClick={handleSpiral} disabled={loading || !thoughts.trim()} className={`w-full py-4 rounded-xl font-semibold text-lg ${loading || !thoughts.trim() ? c.btnLoading : c.btnPrimary} disabled:opacity-40`}>{loading ? <><span className="animate-spin inline-block">{tool?.icon ?? '🌀'}</span> {t('sps_analyzing')}</> : <><span className="mr-1">{tool?.icon ?? '🌀'}</span> {t('sps_stop_spiral')}</>}</button>
          <p className={`text-xs text-center ${c.textMuted}`}>
            {t('sps_spiral_xref_pre')}{' '}
            <a href="/BrainDumpBuddy" className={linkStyle}>🧠 {t('sps_xref_braindump')}</a>{' '}
            {t('sps_spiral_xref_post')}
          </p>
        </div>
      )}

      {mode === 'spiral' && view === 'result' && spiralResult && (
        <div ref={resultsRef} className="space-y-4">
          <div className="flex gap-3">
            <button onClick={() => setView('debrief')} className={`px-4 py-2 rounded-lg text-sm font-bold ${c.btnSecondary}`}>📝 {t('sps_debrief_btn')}</button>
          </div>
          {/* Immediate action */}
          {spiralResult.immediate_action && <div className={`${c.warning} border-2 rounded-xl p-5`}>
            <h4 className={`font-bold text-sm mb-2`}>⚡ {t('sps_do_now')}</h4>
            <p className={`text-lg font-bold ${c.text}`}>{spiralResult.immediate_action.instruction}</p>
            <p className={`text-xs ${c.textSecondary} mt-1`}>{spiralResult.immediate_action.why}</p>
          </div>}

          {/* Intensity + distortion */}
          <div className={`${c.card} rounded-xl shadow-sm p-5`}>
            <div className="flex items-center justify-between mb-3"><span className={`text-sm ${c.textSecondary}`}>{spiralResult.intensity_read}</span><span className={`text-xs px-2 py-1 rounded-full ${c.warning} border font-bold`}>{spiralResult.distortion_label || spiralResult.primary_distortion}</span></div>
          </div>

          {/* Thought breakdown */}
          {spiralResult.thought_breakdown?.length > 0 && <div className={`${c.card} rounded-xl shadow-sm p-5 space-y-4`}>
            <h4 className={`font-bold text-sm ${c.text}`}>🧠 {t('sps_thoughts_vs_reality')}</h4>
            {spiralResult.thought_breakdown.map((tb, i) => (
              <div key={i} className="space-y-2">
                <div className={`${c.danger} border rounded-lg p-3`}><p className={`text-xs font-bold ${c.textMuted} mb-1`}>{t('sps_anxiety_says')}</p><p className="text-sm">{tb.anxious_thought}</p><p className={`text-xs ${c.textSecondary} mt-1`}>↳ {tb.distortion}</p></div>
                <div className={`${c.success} border rounded-lg p-3`}><p className={`text-xs font-bold ${c.textMuted} mb-1`}>{t('sps_actually')}</p><p className="text-sm">{tb.reality_check}</p></div>
                <p className={`text-xs ${c.reframeText}`}>→ {tb.reframe}</p>
              </div>
            ))}
          </div>}

          {/* Grounding */}
          {spiralResult.grounding && <div className={`${c.teal} border-2 rounded-xl p-5 space-y-3`}>
            <h4 className="font-bold text-sm">🌿 {spiralResult.grounding.name} <span className={`font-normal ${c.textMuted}`}>({spiralResult.grounding.duration})</span></h4>
            {(spiralResult.grounding.steps || []).map((step, i) => <div key={i} className="flex items-start gap-2"><span className={`w-6 h-6 rounded-full flex items-center justify-center text-xs font-bold flex-shrink-0 ${c.groundingStep}`}>{i + 1}</span><p className="text-sm">{step}</p></div>)}
          </div>}

          {/* Compassionate reality */}
          {spiralResult.compassionate_reality && <div className={`${c.hero} border-2 rounded-xl p-5`}><p className={`text-lg font-bold ${c.text}`}>💜 {spiralResult.compassionate_reality}</p></div>}

          {spiralResult.pattern_note && <div className={`${c.cardAlt} border rounded-xl p-4`}><p className="text-sm">📊 {spiralResult.pattern_note}</p></div>}
          {spiralResult.after_spiral && <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}><p className={`text-sm ${c.textSecondary}`}>{t('sps_next_prefix')} {spiralResult.after_spiral}</p></div>}
        </div>
      )}

      {/* ═══ FROZEN MODE ═══ */}
      {mode === 'frozen' && view === 'input' && !freezeResult && (
        <div className={`${c.card} rounded-xl shadow-sm p-5 space-y-4`}>
          <div><h3 className={`font-bold text-lg ${c.text}`}>{t('sps_frozen_title')}</h3><p className={`text-sm ${c.textSecondary}`}>{t('sps_frozen_sub')}</p></div>
          <input value={stuckOn} onChange={e => setStuckOn(e.target.value)} placeholder={t('sps_stuck_ph')} className={`w-full p-3 border rounded-lg text-sm ${c.input}`} />
          <div className="flex gap-2">
            <button onClick={() => setCanMove(true)} className={`flex-1 py-3 rounded-lg border-2 text-sm font-semibold ${c.chip(canMove)}`}>{t('sps_can_get_up')}</button>
            <button onClick={() => setCanMove(false)} className={`flex-1 py-3 rounded-lg border-2 text-sm font-semibold ${c.chip(!canMove)}`}>{t('sps_cant_move')}</button>
          </div>
          <button onClick={handleUnfreeze} disabled={loading} className={`w-full py-5 rounded-xl font-semibold text-lg ${loading ? c.btnLoading : c.btnPrimary} disabled:opacity-40`}>{loading ? <><span className="animate-spin inline-block">{tool?.icon ?? '🌀'}</span></> : <><span className="mr-1">{tool?.icon ?? '🌀'}</span> {t('sps_give_one_thing')}</>}</button>
        </div>
      )}

      {mode === 'frozen' && freezeResult && (
        <div className="space-y-4">
          {/* Completed steps */}
          {freezeSteps.length > 0 && <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4 space-y-1`}>
            {freezeSteps.map((s, i) => <div key={i} className="flex items-center gap-2"><span className={`w-5 h-5 rounded-full flex items-center justify-center text-xs ${c.completedStep}`}>✓</span><span className={`text-sm line-through ${c.textMuted}`}>{s.instruction}</span></div>)}
          </div>}

          {/* Current step */}
          <div className={`${c.card} border-2 ${c.freezeBorder} rounded-xl shadow-sm p-6 space-y-4`}>
            <div className="flex items-center gap-3">
              <span className={`w-10 h-10 rounded-full flex items-center justify-center text-lg font-bold ${c.freezeStep}`}>{freezeResult.step_number}</span>
              <p className={`text-lg font-bold ${c.text}`}>{freezeResult.instruction}</p>
            </div>
            <p className={`text-sm ${c.textSecondary}`}>{freezeResult.why_this}</p>
            <p className={`text-xs ${c.freezeDone}`}>✓ {t('sps_done_when')} {freezeResult.completion_signal}</p>
          </div>

          {/* Permission */}
          <div className={`${c.success} border rounded-xl p-4 text-center`}><p className="text-sm">{freezeResult.permission}</p></div>

          {/* Actions */}
          <div className="grid grid-cols-2 gap-3">
            <button onClick={completeFreeze} disabled={loading} className={`py-4 rounded-xl font-semibold text-sm ${loading ? c.btnLoading : c.btnPrimary} disabled:opacity-40`}>{loading ? <><span className="animate-spin inline-block">{tool?.icon ?? '🌀'}</span></> : <>✅ {t('sps_done_next')}</>}</button>
            <button onClick={finishFreeze} className={`py-4 rounded-xl font-semibold text-sm ${c.btnSecondary}`}>{t('sps_done_for_now')}</button>
          </div>

          {freezeResult.encouragement && <p className={`text-center text-sm ${c.textSecondary}`}>{freezeResult.encouragement}</p>}
        </div>
      )}

      {/* ═══ CRASHED MODE ═══ */}
      {mode === 'crashed' && view === 'input' && (
        <div className={`${c.card} rounded-xl shadow-sm p-5 space-y-4`}>
          <div><h3 className={`font-bold text-lg ${c.text}`}>{t('sps_crashed_title')}</h3><p className={`text-sm ${c.textSecondary}`}>{t('sps_crashed_sub')}</p></div>

          <div><label className={`text-xs font-bold ${c.textSecondary}`}>{t('sps_what_happened')}</label><div className="space-y-2 mt-1">
            {CRASH_TYPES.map(ct => <button key={ct.v} onClick={() => setCrashType(ct.v)} className={`w-full text-left px-4 py-3 rounded-lg border-2 ${c.chip(crashType === ct.v)}`}><span className="text-sm font-semibold">{ct.l}</span><span className={`text-xs ${c.textSecondary} ml-2`}>{ct.desc}</span></button>)}
          </div></div>

          <div><label className={`text-xs font-bold ${c.textSecondary}`}>{t('sps_severity_label')}</label><div className="flex gap-2 mt-1">
            {SEVERITY_OPTS.map(s => <button key={s.v} onClick={() => setSeverity(s.v)} className={`flex-1 py-3 rounded-lg border-2 text-xs font-semibold text-center ${c.chip(severity === s.v)}`}>{s.l}</button>)}
          </div></div>

          <div className="grid grid-cols-2 gap-3">
            <div><label className={`text-xs font-bold ${c.textSecondary}`}>{t('sps_how_long')}</label><input value={crashDuration} onChange={e => setCrashDuration(e.target.value)} placeholder={t('sps_how_long_ph')} className={`w-full p-2.5 border rounded-lg text-sm ${c.input}`} /></div>
            <div><label className={`text-xs font-bold ${c.textSecondary}`}>{t('sps_what_can_you_do')}</label><input value={canDo} onChange={e => setCanDo(e.target.value)} placeholder={t('sps_what_can_you_do_ph')} className={`w-full p-2.5 border rounded-lg text-sm ${c.input}`} /></div>
          </div>

          <button onClick={handleCrash} disabled={loading || !crashType} className={`w-full py-4 rounded-xl font-semibold text-lg ${loading || !crashType ? c.btnLoading : c.btnPrimary} disabled:opacity-40`}>{loading ? <><span className="animate-spin inline-block">{tool?.icon ?? '🌀'}</span> {t('sps_building_protocol')}</> : <><span className="mr-1">{tool?.icon ?? '🌀'}</span> {t('sps_get_protocol')}</>}</button>
        </div>
      )}

      {mode === 'crashed' && view === 'result' && crashResult && (
        <div ref={resultsRef} className="space-y-4">
          <div className="flex gap-3">
            <button onClick={() => setView('debrief')} className={`px-4 py-2 rounded-lg text-sm font-bold ${c.btnSecondary}`}>📝 {t('sps_debrief_btn')}</button>
          </div>
          {/* Acknowledgment */}
          <div className={`${c.hero} border-2 rounded-xl p-5`}><p className={`text-lg ${c.text}`}>{crashResult.acknowledgment}</p></div>
          {crashResult.current_read && <p className={`text-sm ${c.textSecondary}`}>{crashResult.current_read}</p>}

          {/* Stages */}
          {(crashResult.stages || []).map((stage, i) => {
            const stageColor = i === 0 ? c.danger : i === 1 ? c.warning : i === 2 ? c.cardAlt : c.success;
            return <div key={i} className={`${stageColor} border-2 rounded-xl p-5 space-y-3`}>
              <h4 className="font-bold text-sm">{stage.name}</h4>
              <p className={`text-xs ${c.textSecondary}`}>{stage.description}</p>
              <div className="space-y-2">{(stage.steps || []).map((step, j) => <div key={j} className="flex items-start gap-2"><span className={`w-5 h-5 rounded-full flex items-center justify-center text-xs font-bold flex-shrink-0 ${c.cardAlt}`}>{j + 1}</span><p className="text-sm">{step}</p></div>)}</div>
              <div className={`${c.success} border rounded-lg p-3`}><p className="text-sm font-bold">✓ {t('sps_enough')} {stage.enough_statement}</p></div>
            </div>;
          })}

          {/* Permissions */}
          {crashResult.permissions?.length > 0 && <div className={`${c.teal} border rounded-xl p-5 space-y-2`}><h4 className="font-bold text-sm">💚 {t('sps_permissions')}</h4>{crashResult.permissions.map((p, i) => <p key={i} className="text-sm">{p}</p>)}</div>}

          {/* Basics checklist */}
          {crashResult.basics_checklist?.length > 0 && <div className={`${c.card} rounded-xl shadow-sm p-5`}><h4 className={`font-bold text-sm ${c.text} mb-2`}>🛡️ {t('sps_basics')}</h4>{crashResult.basics_checklist.map((b, i) => <div key={i} className={`flex items-center gap-2 py-1`}><span className={`w-4 h-4 rounded border-2 ${c.border} flex-shrink-0`} /><span className={`text-sm ${c.textSecondary}`}>{b}</span></div>)}</div>}

          {/* When to reach out */}
          {crashResult.when_to_reach_out && <div className={`${c.btnSecondary} border rounded-xl p-4`}><h4 className="font-bold text-sm mb-1">🤝 {t('sps_when_reach_out')}</h4><p className="text-sm">{crashResult.when_to_reach_out}</p></div>}

          {/* Recovery signs */}
          {crashResult.recovery_signs?.length > 0 && <div className={`${c.success} border rounded-xl p-4 space-y-1`}><h4 className="font-bold text-sm mb-1">🌱 {t('sps_recovery_signs')}</h4>{crashResult.recovery_signs.map((s, i) => <p key={i} className="text-sm">• {s}</p>)}</div>}

          {/* Anchor */}
          {crashResult.gentle_reminder && <div className={`${c.hero} border-2 rounded-xl p-5 text-center`}><p className={`text-lg font-bold ${c.text}`}>{crashResult.gentle_reminder}</p></div>}
        </div>
      )}

      {/* ═══ DEBRIEF VIEW ═══ */}
      {view === 'debrief' && (
        <div className={`${c.card} rounded-xl shadow-sm p-5 space-y-4`}>
          <div><h3 className={`font-bold text-lg ${c.text}`}>{t('sps_debrief_title')}</h3><p className={`text-xs ${c.textSecondary}`}>{t('sps_debrief_sub')}</p></div>

          <div><label className={`text-sm font-semibold ${c.text}`}>{t('sps_how_now', { n: debriefIntensity })}</label>
            <div className="flex gap-1 mt-1">{[1, 2, 3, 4, 5].map(n => <button key={n} onClick={() => setDebriefIntensity(n)} className={`flex-1 py-2.5 rounded-lg text-sm font-bold border-2 transition-all ${n <= debriefIntensity ? c.debriefActive : `${c.cardAlt} ${c.border} ${c.textMuted}`}`}>{n === 1 ? '😌' : n === 2 ? '🙂' : n === 3 ? '😐' : n === 4 ? '😟' : '😰'}</button>)}</div>
          </div>

          <input value={whatHelped} onChange={e => setWhatHelped(e.target.value)} placeholder={t('sps_what_helped_ph')} className={`w-full p-2.5 border rounded-lg text-sm ${c.input}`} />

          <button onClick={handleDebrief} disabled={loading} className={`w-full py-3 rounded-xl font-semibold text-sm ${loading ? c.btnLoading : c.btnPrimary} disabled:opacity-40`}>{loading ? <><span className="animate-spin inline-block">{tool?.icon ?? '🌀'}</span></> : <>📝 {t('sps_reflect')}</>}</button>

          {debriefResult && <div className="space-y-3">
            <div className={`${c.success} border rounded-xl p-4`}><p className="text-sm">{debriefResult.reflection}</p></div>
            {debriefResult.pattern_insight && <div className={`${c.cardAlt} border rounded-xl p-4`}><p className="text-sm">📊 {debriefResult.pattern_insight}</p></div>}
            {debriefResult.prevention_tip && <div className={`${c.warning} border rounded-xl p-4`}><p className="text-sm">🛡️ {t('sps_next_time')} {debriefResult.prevention_tip}</p></div>}
            {debriefResult.strength_noted && <div className={`${c.btnSecondary} border rounded-xl p-4`}><p className="text-sm">💪 {debriefResult.strength_noted}</p></div>}
          </div>}
        </div>
      )}

      {/* ═══ HISTORY VIEW ═══ */}
      {view === 'history' && (
        <div className="space-y-4">
          <div className={`${c.card} rounded-xl shadow-sm p-5`}>
            <div className="flex items-center justify-between mb-4"><h3 className={`font-bold text-lg ${c.text}`}>{t('sps_history_title')}</h3>
              {episodeLog.length >= 3 && <button onClick={handlePatterns} disabled={loading} className={`text-xs font-bold px-3 py-1.5 rounded-lg ${c.btnPrimary} disabled:opacity-40`}>{loading ? <><span className="animate-spin inline-block">{tool?.icon ?? '🌀'}</span> {t('sps_analyzing_short')}</> : t('sps_analyze_patterns')}</button>}
            </div>

            {/* Status bar */}
            {episodeLog.length > 0 && <div className="flex gap-0.5 mb-4">{episodeLog.slice(0, 6).map((ep, i) => <div key={i} className={`flex-1 h-3 rounded-full ${ep.type === 'spiral' ? c.episodeSpiral : ep.type === 'frozen' ? c.episodeFrozen : c.episodeCrash}`} title={`${ep.type} — ${new Date(ep.date).toLocaleDateString()}`} />)}</div>}

            <div className="space-y-2">{episodeLog.slice(0, 6).map(ep => (
              <div key={ep.id} className={`${c.cardAlt} border ${c.border} rounded-lg p-3`}>
                <div className="flex items-center justify-between">
                  <div className="flex items-center gap-2"><span>{ep.type === 'spiral' ? '🌀' : ep.type === 'frozen' ? '❄️' : '🔋'}</span><span className={`text-sm font-bold ${c.text}`}>{ep.trigger || ep.type}</span></div>
                  <span className={`text-xs ${c.textMuted}`}>{new Date(ep.date).toLocaleDateString()}</span>
                </div>
                <div className="flex gap-3 mt-1">
                  {ep.primary_distortion && <span className={`text-xs ${c.textSecondary}`}>↳ {ep.primary_distortion}</span>}
                  {ep.intensity_before != null && <span className={`text-xs ${c.textSecondary}`}>{t('sps_intensity_word')} {ep.intensity_before}/5{ep.intensity_after != null ? ` → ${ep.intensity_after}/5` : ''}</span>}
                  {ep.steps_completed != null && <span className={`text-xs ${c.textSecondary}`}>{ep.steps_completed} {t('sps_steps_word')}</span>}
                  {ep.what_helped && <span className={`text-xs ${c.reframeText}`}>💊 {ep.what_helped}</span>}
                </div>
              </div>
            ))}</div>
          </div>

          {/* Pattern analysis */}
          {patternResult && <div className="space-y-3">
            <span ref={resultsRef} className="sr-only" aria-hidden="true" />
            <div className={`${c.card} rounded-xl shadow-sm p-5 space-y-3`}>
              <h4 className={`font-bold text-sm ${c.text}`}>🔍 {t('sps_pattern_analysis')}</h4>
              <div className="grid grid-cols-3 gap-3">{[[t('sps_most_common'), patternResult.most_common_type], [t('sps_top_distortion'), patternResult.most_common_distortion], [t('sps_trend'), patternResult.improvement_trend]].map(([l, v], i) => <div key={i} className={`${c.cardAlt} border ${c.border} rounded-lg p-2 text-center`}><span className={`text-xs ${c.textMuted}`}>{l}</span><br /><span className={`text-sm font-bold ${c.text}`}>{v || '?'}</span></div>)}</div>
              {(patternResult.total_episodes !== undefined || patternResult.spiral_detected !== undefined) && (
                <div className="flex flex-wrap gap-2">
                  {patternResult.total_episodes !== undefined && <span className={`text-xs px-2 py-0.5 rounded-full ${c.cardAlt} border`}>{t('sps_episodes_logged', { count: patternResult.total_episodes })}</span>}
                  {patternResult.spiral_detected !== undefined && <span className={`text-xs px-2 py-0.5 rounded-full border ${patternResult.spiral_detected ? c.warning : c.success}`}>{patternResult.spiral_detected ? t('sps_spiral_detected') : t('sps_no_active_spiral')}</span>}
                </div>
              )}
              {patternResult.time_patterns && <p className={`text-xs ${c.textSecondary}`}>⏰ {patternResult.time_patterns}</p>}
              {patternResult.trigger_patterns?.length > 0 && <div><p className={`text-xs font-bold ${c.text} mb-1`}>{t('sps_trigger_patterns')}</p>{patternResult.trigger_patterns.map((tp, i) => <p key={i} className={`text-sm ${c.textSecondary}`}>• {tp}</p>)}</div>}
              {patternResult.biggest_insight && <div className={`${c.warning} border rounded-lg p-3`}><p className="text-sm font-bold">💡 {patternResult.biggest_insight}</p></div>}
              {patternResult.personalized_toolkit?.length > 0 && <div className={`${c.success} border rounded-lg p-3 space-y-2`}><p className="text-xs font-bold">🧰 {t('sps_your_toolkit')}</p>{patternResult.personalized_toolkit.map((tk, i) => <div key={i}><span className={`text-xs font-bold ${c.text}`}>{tk.trigger}:</span><span className={`text-xs ${c.textSecondary} ml-1`}>{tk.best_response}</span></div>)}</div>}
              {patternResult.encouragement && <p className={`text-sm ${c.reframeText}`}>✨ {patternResult.encouragement}</p>}
            </div>
          </div>}
        </div>
      )}

      {results && (
        <p className={`text-xs ${c.textMuted} text-center`}>
          {t('sps_footer_xref_pre')} <a href="/PEP" className={linkStyle}>✨ {t('sps_xref_pep')}</a> {t('sps_footer_xref_post')}
        </p>
      )}
    </div>
  );
};

SpiralStopper.displayName = 'SpiralStopper';
export default SpiralStopper;
