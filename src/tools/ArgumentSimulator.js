import React, { useState, useEffect, useRef, useCallback } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { usePersistentState } from '../hooks/usePersistentState';
import { CopyBtn } from '../components/ActionButtons';
import { useRegisterActions } from '../components/ActionBarContext';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const INTENSITY_OPTIONS = [
  { id: 'civil',    label: 'Civil',    icon: '🎓', desc: 'Oxford Union energy — sharp, respectful' },
  { id: 'heated',   label: 'Heated',   icon: '🔥', desc: 'Smart friends at dinner — passionate, pointed' },
  { id: 'unhinged', label: 'Unhinged', icon: '🌋', desc: 'Full rhetorical firepower — mic drops galore' },
];

const EXAMPLE_TOPICS = [
  'Pineapple belongs on pizza',
  'Remote work is better than office work',
  'Hot dogs are sandwiches',
  'The book is always better than the movie',
  'Breakfast for dinner is superior',
  'Cats are better pets than dogs',
  'Cash tips are better than no-tip pricing',
  'AI art is real art',
  'College degrees are overrated',
  'Social media does more harm than good',
];

const ArgumentSimulator = ({ tool }) => {
  const { callToolEndpoint, loading } = useClaudeAPI();
  const { isDark } = useTheme();

  // ─── Colors ───
  const c = {
    card:          isDark ? 'bg-zinc-800'      : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50'   : 'bg-slate-50',
    input:         isDark
      ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20'
      : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-zinc-50'     : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300'    : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-500'    : 'text-gray-400',
    labelText:     isDark ? 'text-zinc-200'    : 'text-gray-700',
    accentTxt:     isDark ? 'text-cyan-400'    : 'text-cyan-600',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white' : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200' : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700'  : 'border-gray-200',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200' : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200' : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200' : 'bg-red-50 border-red-200 text-red-800',
    infoBox:       isDark ? 'bg-sky-900/20 border-sky-700 text-sky-200' : 'bg-sky-50 border-sky-200 text-sky-800',
    successBox:    isDark ? 'bg-emerald-900/20 border-emerald-700' : 'bg-emerald-50 border-emerald-300',
    successTxt:    isDark ? 'text-emerald-300' : 'text-emerald-800',
    warningBox:    isDark ? 'bg-amber-900/20 border-amber-700' : 'bg-amber-50 border-amber-300',
    warningTxt:    isDark ? 'text-amber-300'   : 'text-amber-800',
    pillActive:    isDark ? 'bg-cyan-600 border-cyan-600 text-white' : 'bg-cyan-600 border-cyan-600 text-white',
    pillInactive:  isDark ? 'bg-zinc-700 border-zinc-600 text-zinc-300 hover:border-zinc-500' : 'bg-white border-gray-200 text-gray-600 hover:border-gray-300',
    required:      isDark ? 'text-amber-400'   : 'text-amber-500',
    // ── Custom side-specific color tokens for the VS debate visual ──
    sideABg:       isDark ? 'bg-sky-900/20'    : 'bg-sky-50',
    sideABorder:   isDark ? 'border-sky-700'   : 'border-sky-200',
    sideAAccent:   isDark ? 'text-sky-300'     : 'text-sky-700',
    sideBBg:       isDark ? 'bg-amber-900/20'  : 'bg-amber-50',
    sideBBorder:   isDark ? 'border-amber-700' : 'border-amber-200',
    sideBAccent:   isDark ? 'text-amber-300'   : 'text-amber-700',
    verdictBg:     isDark ? 'bg-zinc-700/50'   : 'bg-slate-50',
  };
  // Always include these two alias lines immediately after the closing brace:
  c.textMuteded = c.textMuted;
  c.label = c.labelText;

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';

  // ─── State ───
  const [hotTake, setHotTake] = usePersistentState('argumentsimulator-hottake', '');
  const [intensity, setIntensity] = useState('heated');
  const [error, setError] = useState('');
  const [expandedSections, setExpandedSections] = useState({});
  const [showVerdict, setShowVerdict] = useState(false);

  const [results, setResults] = usePersistentState('argumentsimulator-last', null);
  const [history, setHistory] = usePersistentState('argumentsimulator-history', []);

  // ─── Refs ───
  const resultsRef = useRef(null);
  const handleSubmitRef = useRef(null);
  const canSubmitRef = useRef(false);

  // ─── Handlers ───
  const handleRandomTopic = () => {
    const filtered = EXAMPLE_TOPICS.filter(t => t !== hotTake);
    setHotTake(filtered[Math.floor(Math.random() * filtered.length)]);
  };

  const handleSubmit = async () => {
    if (!hotTake.trim()) return;
    setError('');
    setResults(null);
    setShowVerdict(false);
    setExpandedSections({});

    try {
      const res = await callToolEndpoint('argument-simulator', {
        hotTake: hotTake.trim(),
        intensity,
      });
      setResults(res);
      setHistory(prev => [{
        id: Date.now(),
        timestamp: new Date().toISOString(),
        preview: hotTake.trim().slice(0, 40),
        result: res,
      }, ...(prev || [])].slice(0, 6));
    } catch (err) {
      setError(err.message || 'Debate failed. Try again.');
    }
  };

  const handleReset = () => {
    setHotTake('');
    setResults(null);
    setError('');
    setShowVerdict(false);
    setExpandedSections({});
  };

  const toggleSection = (key) => {
    setExpandedSections(prev => ({ ...prev, [key]: !prev[key] }));
  };

  // ─── Build copyable text ───
  const buildFullText = useCallback(() => {
    if (!results) return '';
    let text = `⚔️ ARGUMENT SIMULATOR\n`;
    text += `"${results.topic_framed || hotTake}"\n`;
    text += `Intensity: ${INTENSITY_OPTIONS.find(o => o.id === intensity)?.label || intensity}\n\n`;

    if (results.side_a) {
      text += `━━ ${results.side_a.position} ━━\n`;
      text += `${results.side_a.argument}\n\n`;
      text += `💥 Killer point: ${results.side_a.killer_point}\n`;
      text += `📊 Evidence: ${results.side_a.evidence}\n`;
      text += `😬 Uncomfortable truth: ${results.side_a.uncomfortable_truth}\n\n`;
    }

    if (results.side_b) {
      text += `━━ ${results.side_b.position} ━━\n`;
      text += `${results.side_b.argument}\n\n`;
      text += `💥 Killer point: ${results.side_b.killer_point}\n`;
      text += `📊 Evidence: ${results.side_b.evidence}\n`;
      text += `😬 Uncomfortable truth: ${results.side_b.uncomfortable_truth}\n\n`;
    }

    if (results.where_they_actually_disagree) {
      text += `━━ THE REAL DISAGREEMENT ━━\n`;
      text += `${results.where_they_actually_disagree}\n\n`;
    }

    if (results.judge_verdict) {
      text += `━━ JUDGE'S VERDICT ━━\n`;
      text += `${results.judge_verdict}\n\n`;
    }

    if (results.dinner_party_take) {
      text += `━━ DINNER PARTY TAKE ━━\n`;
      text += `${results.dinner_party_take}\n\n`;
    }

    return text + BRAND;
  }, [results, hotTake, intensity]);

  // ─── Register actions (copy/share/print via global ActionBar) ───
  useRegisterActions(buildFullText(), tool?.title || 'Argument Simulator');

  // ─── Ref assignments every render (for keyboard handler) ───
  handleSubmitRef.current = handleSubmit;
  canSubmitRef.current = Boolean(hotTake.trim()) && !loading;

  // ─── Scroll to results ───
  useEffect(() => {
    if (!results || !resultsRef.current) return;
    const t = setTimeout(() => resultsRef.current?.scrollIntoView({ behavior: 'smooth', block: 'start' }), 250);
    return () => clearTimeout(t);
  }, [results]);

  // ─── Global ⌘/Ctrl+Enter keyboard handler ───
  useEffect(() => {
    const handler = (e) => {
      const tag = document.activeElement?.tagName;
      if (tag === 'SELECT') return;
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && canSubmitRef.current) {
        e.preventDefault();
        handleSubmitRef.current?.();
      }
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
  }, []);

  // ─── Side Card sub-component (steelmanned position + killer point/evidence/truth) ───
  const SideCard = ({ side, sideKey, emoji }) => {
    if (!side) return null;
    const isA = sideKey === 'a';
    const bg = isA ? c.sideABg : c.sideBBg;
    const border = isA ? c.sideABorder : c.sideBBorder;
    const accent = isA ? c.sideAAccent : c.sideBAccent;
    const detailsKey = `${sideKey}-details`;
    const isExpanded = expandedSections[detailsKey] !== false; // default open

    return (
      <div className={`${bg} ${border} border rounded-2xl overflow-hidden transition-all duration-200`}>
        {/* Position header */}
        <div className="p-5 pb-0">
          <div className="flex items-start gap-3">
            <span className="text-2xl">{emoji}</span>
            <div className="flex-1 min-w-0">
              <p className={`font-bold ${accent} text-base leading-snug`}>
                {side.position}
              </p>
            </div>
          </div>
        </div>

        {/* Main argument */}
        <div className="px-5 py-4">
          <p className={`text-sm ${c.text} leading-relaxed`}>
            {side.argument}
          </p>
        </div>

        {/* Expandable details */}
        <div className={`border-t ${isA ? (isDark ? 'border-sky-700/30' : 'border-sky-200/60') : (isDark ? 'border-amber-700/20' : 'border-amber-200/50')}`}>
          <button
            onClick={() => toggleSection(detailsKey)}
            className="w-full px-5 py-3 flex items-center justify-between text-left transition-colors"
          >
            <span className={`text-xs font-semibold uppercase tracking-wider ${c.textMuteded}`}>
              Killer point · Evidence · Uncomfortable truth
            </span>
            <span className={`text-sm ${c.textMuteded}`}>{isExpanded ? '▲' : '▼'}</span>
          </button>

          {isExpanded && (
            <div className="px-5 pb-5 space-y-4">
              <div className="flex items-start gap-2.5">
                <span className="text-base flex-shrink-0 mt-0.5">💥</span>
                <div>
                  <p className={`text-xs font-semibold uppercase ${accent} mb-1`}>Killer Point</p>
                  <p className={`text-sm ${c.textSecondary} leading-relaxed`}>{side.killer_point}</p>
                </div>
              </div>

              <div className="flex items-start gap-2.5">
                <span className="text-base flex-shrink-0 mt-0.5">📊</span>
                <div>
                  <p className={`text-xs font-semibold uppercase ${accent} mb-1`}>Evidence</p>
                  <p className={`text-sm ${c.textSecondary} leading-relaxed`}>{side.evidence}</p>
                </div>
              </div>

              <div className="flex items-start gap-2.5">
                <span className="text-base flex-shrink-0 mt-0.5">😬</span>
                <div>
                  <p className={`text-xs font-semibold uppercase ${accent} mb-1`}>Uncomfortable Truth</p>
                  <p className={`text-sm ${c.textSecondary} leading-relaxed italic`}>{side.uncomfortable_truth}</p>
                </div>
              </div>
            </div>
          )}
        </div>
      </div>
    );
  };

  // ─── Render ───
  const hasAnyInput = hotTake.trim().length > 0;

  return (
    <div className={`space-y-4 ${c.text}`}>

      {/* ── Input Card ── */}
      <div className={`${c.card} border ${c.border} rounded-2xl p-5 shadow-sm space-y-5`}>

        {/* Standard header */}
        <div className="flex items-start justify-between gap-3 pb-4 border-b border-zinc-500">
          <div className="flex-1 min-w-0">
            <h2 className={`text-xl font-bold ${c.text} flex items-center gap-2`}>
              <span className="mr-2">{tool?.icon ?? '⚔️'}</span>{tool?.title ?? 'Argument Simulator'}
            </h2>
            <p className={`text-sm ${c.textSecondary} mt-1`}>{tool?.tagline ?? 'Drop a hot take. AI argues both sides.'}</p>
          </div>
          {(results || hasAnyInput) ? (
            <button
              onClick={handleReset}
              className={`shrink-0 px-3 py-2 rounded-lg text-sm font-bold min-h-[40px] ${c.btnSecondary}`}
            >↺ Start Over</button>
          ) : null}
        </div>

        {/* Hot take input */}
        <div className="space-y-2">
          <label className={`text-sm font-semibold ${c.text}`}>
            <span className="mr-1.5">💬</span> Your hot take <span className={c.required}>*</span>
          </label>
          <textarea
            value={hotTake}
            onChange={(e) => setHotTake(e.target.value)}
            placeholder="e.g. Pineapple belongs on pizza..."
            rows={2}
            maxLength={300}
            className={`w-full px-4 py-3 rounded-xl text-sm ${c.input} outline-none focus:ring-2 resize-none`}
          />
          <div className="flex items-center justify-between">
            <button
              onClick={handleRandomTopic}
              className={`text-xs ${c.btnSecondary} px-3 py-1.5 rounded-lg transition-colors flex items-center gap-1.5`}
            >
              <span>🎲</span> Random topic
            </button>
            <span className={`text-xs ${c.textMuteded}`}>{hotTake.length}/300</span>
          </div>
        </div>

        {/* Intensity selector */}
        <div className="space-y-2">
          <label className={`text-sm font-semibold ${c.text}`}>
            <span className="mr-1.5">🎚️</span> Intensity
          </label>
          <div className="grid grid-cols-3 gap-2">
            {INTENSITY_OPTIONS.map(opt => (
              <button
                key={opt.id}
                onClick={() => setIntensity(opt.id)}
                className={`${intensity === opt.id ? c.pillActive : c.pillInactive} border rounded-xl px-3 py-3 text-center transition-all duration-150`}
              >
                <span className="text-xl block mb-1">{opt.icon}</span>
                <p className="text-sm font-medium leading-tight">{opt.label}</p>
                <p className={`text-xs ${c.textMuteded} leading-tight mt-0.5 hidden sm:block`}>{opt.desc}</p>
              </button>
            ))}
          </div>
        </div>

        {/* Pre-result cross-ref */}
        <p className={`text-xs text-center ${c.textMuteded}`}>
          Want to test your own belief first?{' '}
          <a href="/BeliefStressTest" className={linkStyle}>🧪 Belief Stress Test</a>{' '}
          finds the weakest assumptions before you argue them.
        </p>

        {/* Submit */}
        <button
          onClick={handleSubmit}
          disabled={loading || !hotTake.trim()}
          className={`w-full ${c.btnPrimary} disabled:opacity-40 font-bold py-3 rounded-lg flex items-center justify-center gap-2 min-h-[48px]`}
        >
          {loading ? (
            <>
              <span className="animate-spin inline-block">{tool?.icon ?? '⚔️'}</span>
              Both sides loading their weapons...
            </>
          ) : (
            <>
              <span>{tool?.icon ?? '⚔️'}</span> Start the Argument
            </>
          )}
        </button>
      </div>

      {/* ── Error ── */}
      {error && (
        <div className={`${c.danger} border rounded-xl p-4 text-sm flex items-center gap-2`}>
          <span>⚠️</span> {error}
        </div>
      )}

      {/* ── Results ── */}
      {results && (
        <div ref={resultsRef} className="space-y-5">

          {/* Topic banner */}
          {results.topic_framed && (
            <div className={`${c.cardAlt} border ${c.border} rounded-2xl p-4 text-center`}>
              <p className={`text-xs font-semibold uppercase tracking-wider ${c.textMuteded} mb-1`}>
                Tonight's debate
              </p>
              <p className={`text-lg font-bold ${c.text} italic`}>
                "{results.topic_framed}"
              </p>
            </div>
          )}

          {/* Side A — FOR */}
          <SideCard side={results.side_a} sideKey="a" emoji="👍" />

          {/* VS divider */}
          <div className="flex items-center gap-4 py-1">
            <div className={`flex-1 border-t ${c.border}`} />
            <span className={`text-2xl font-black ${c.textMuteded}`}>VS</span>
            <div className={`flex-1 border-t ${c.border}`} />
          </div>

          {/* Side B — AGAINST */}
          <SideCard side={results.side_b} sideKey="b" emoji="👎" />

          {/* Where they actually disagree */}
          {results.where_they_actually_disagree && (
            <div className={`${c.card} border ${c.border} rounded-2xl p-5`}>
              <div className="flex items-center gap-2 mb-3">
                <span className="text-lg">🔍</span>
                <h3 className={`font-bold ${c.text}`}>Where They Actually Disagree</h3>
              </div>
              <p className={`text-sm ${c.textSecondary} leading-relaxed`}>
                {results.where_they_actually_disagree}
              </p>
            </div>
          )}

          {/* Verdict — hidden by default, reveal on click */}
          {results.judge_verdict && (
            <div className="space-y-3">
              {!showVerdict ? (
                <button
                  onClick={() => setShowVerdict(true)}
                  className={`w-full ${c.btnPrimary} disabled:opacity-40 font-bold py-3 rounded-lg flex items-center justify-center gap-2 min-h-[48px]`}
                >
                  <span>🏛️</span> Reveal the Judge's Verdict
                </button>
              ) : (
                <div className={`${c.verdictBg} border ${c.border} rounded-2xl p-5 space-y-4`}>
                  <div className="flex items-center gap-2">
                    <span className="text-2xl">🏛️</span>
                    <h3 className={`text-lg font-bold ${c.text}`}>Judge's Verdict</h3>
                  </div>
                  <p className={`text-sm ${c.text} leading-relaxed`}>
                    {results.judge_verdict}
                  </p>
                </div>
              )}
            </div>
          )}

          {/* Dinner party take */}
          {results.dinner_party_take && (
            <div className={`${c.cardAlt} border ${c.border} rounded-2xl p-5`}>
              <div className="flex items-center gap-2 mb-3">
                <span className="text-lg">🍷</span>
                <h3 className={`font-bold ${c.text}`}>The Dinner Party Take</h3>
              </div>
              <p className={`text-sm italic ${c.textSecondary} leading-relaxed`}>
                "{results.dinner_party_take}"
              </p>
              <p className={`text-xs ${c.textMuteded} mt-2`}>
                — The nuanced take that makes you sound smart without alienating anyone
              </p>
            </div>
          )}

          {/* Conditional cross-ref: unhinged intensity → Roast Me */}
          {intensity === 'unhinged' && (
            <p className={`text-xs text-center ${c.textMuteded}`}>
              Enjoyed the chaos?{' '}
              <a href="/RoastMe" className={linkStyle}>🔥 Roast Me</a>{' '}
              turns that energy on you — if you can handle it.
            </p>
          )}

          {/* Post-result cross-refs */}
          <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4 space-y-2`}>
            <p className={`text-xs font-semibold ${c.textMuteded} uppercase tracking-wider`}>
              Keep the fun going
            </p>
            <div className="flex flex-wrap gap-x-4 gap-y-1">
              <a href="/PlotTwist" className={`text-xs ${linkStyle}`}>🔄 Plot Twist</a>
              <a href="/WrongAnswersOnly" className={`text-xs ${linkStyle}`}>🙃 Wrong Answers Only</a>
            </div>
          </div>
        </div>
      )}

      {/* ── History ── */}
      {history?.length > 0 && (
        <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
          <h3 className={`text-sm font-bold ${c.text} mb-3`}>🕐 Recent Debates</h3>
          <div className="space-y-1.5">
            {history.map(entry => (
              <button key={entry.id}
                onClick={() => setResults(entry.result)}
                className={`w-full text-left px-3 py-2 rounded-lg ${c.btnSecondary} text-xs flex items-center gap-2`}>
                <span className={c.textMuteded}>
                  {new Date(entry.timestamp).toLocaleDateString(undefined, { month: 'short', day: 'numeric', hour: 'numeric', minute: '2-digit' })}
                </span>
                <span className={`${c.text} truncate`}>{entry.preview}{entry.preview?.length >= 40 ? '…' : ''}</span>
              </button>
            ))}
          </div>
        </div>
      )}
    </div>
  );
};

ArgumentSimulator.displayName = 'ArgumentSimulator';
export default ArgumentSimulator;
