import React, { useState, useRef, useEffect, useCallback } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { usePersistentState } from '../hooks/usePersistentState';
import { ActionBar } from '../components/ActionButtons';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const TIMEFRAME_OPTIONS = [
  { id: '1 year',   label: '1 year' },
  { id: '2 years',  label: '2 years' },
  { id: '5 years',  label: '5 years' },
  { id: '10 years', label: '10 years' },
];

const EXAMPLE_DECISIONS = [
  { a: 'Stay in my current city',      b: 'Move across the country for the new job' },
  { a: 'Stay at my stable job',        b: 'Quit and start my own business' },
  { a: 'Stay in this relationship',    b: 'Leave and start over' },
  { a: 'Go back to school full-time',  b: 'Keep working and study part-time' },
  { a: 'Keep renting in the city',     b: 'Buy a house in the suburbs' },
  { a: 'Accept the promotion',         b: 'Turn it down and protect my free time' },
];


// ════════════════════════════════════════════════════════════
// MAIN COMPONENT
// ════════════════════════════════════════════════════════════
const ContrastReport = ({ tool }) => {
  const { callToolEndpoint, loading } = useClaudeAPI();
  const { isDark } = useTheme();
  const resultsRef = useRef(null);

  // ─── Standard c object ───
  const c = {
    card:          isDark ? 'bg-zinc-800'     : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50'  : 'bg-slate-50',
    text:          isDark ? 'text-zinc-50'    : 'text-slate-900',
    textSecondary: isDark ? 'text-zinc-300'   : 'text-slate-600',
    textMuted:     isDark ? 'text-zinc-500'   : 'text-slate-400',
    input:         isDark
      ? 'bg-zinc-700 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20'
      : 'bg-white border-slate-300 text-slate-900 placeholder-slate-400 focus:border-cyan-600 focus:ring-cyan-300/20',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white' : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200' : 'bg-slate-100 hover:bg-slate-200 text-slate-700',
    border:        isDark ? 'border-zinc-700' : 'border-slate-200',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-300' : 'bg-emerald-50 border-emerald-200 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-300' : 'bg-amber-50 border-amber-200 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-300' : 'bg-red-50 border-red-200 text-red-800',
    // Tool-specific: Path A (sky family) — genuinely distinct semantic color for two-path comparison
    pathABg:       isDark ? 'bg-sky-900/10'      : 'bg-sky-50',
    pathABorder:   isDark ? 'border-sky-800/40'  : 'border-sky-200',
    pathAAccent:   isDark ? 'text-sky-400'        : 'text-sky-700',
    pathABar:      isDark ? 'bg-sky-500'          : 'bg-sky-600',
    pathATabBorder: isDark ? 'border-sky-400'     : 'border-sky-600',
    // Tool-specific: Path B (amber family) — genuinely distinct semantic color
    pathBBg:       isDark ? 'bg-amber-900/10'    : 'bg-amber-50',
    pathBBorder:   isDark ? 'border-amber-800/40': 'border-amber-200',
    pathBAccent:   isDark ? 'text-amber-400'      : 'text-amber-700',
    pathBBar:      isDark ? 'bg-amber-500'        : 'bg-amber-600',
    pathBTabBorder: isDark ? 'border-amber-400'   : 'border-amber-600',
    // Streaming
    chipActive:    isDark ? 'border-cyan-500 bg-cyan-900/30 text-cyan-300' : 'border-cyan-600 bg-cyan-50 text-cyan-800',
    chipInactive:  isDark ? 'border-zinc-600 text-zinc-400 hover:border-zinc-500' : 'border-slate-300 text-slate-500 hover:border-slate-400',
  };

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';

  // ─── Persisted state ───
  const [pathA,    setPathA]    = usePersistentState('cr-pathA', '');
  const [pathB,    setPathB]    = usePersistentState('cr-pathB', '');
  const [aboutYou, setAboutYou] = usePersistentState('cr-about', '');
  const [timeframe,setTimeframe]= usePersistentState('cr-timeframe', '2 years');
  const [results,  setResults]  = usePersistentState('cr-results', null);
  const [history,  setHistory]  = usePersistentState('cr-history', []);

  // ─── Session state ───
  const [error,          setError]          = useState('');
  const [activeNarrative,setActiveNarrative] = useState('a');
  const [showHistory,    setShowHistory]    = useState(false);

  useEffect(() => {
    if (results && resultsRef.current) {
      setTimeout(() => resultsRef.current.scrollIntoView({ behavior: 'smooth', block: 'start' }), 250);
    }
  }, [results]);

  // ─── Handlers ───
  const handleExample = useCallback(() => {
    const filtered = EXAMPLE_DECISIONS.filter(e => e.a !== pathA);
    const ex = filtered[Math.floor(Math.random() * filtered.length)];
    setPathA(ex.a);
    setPathB(ex.b);
  }, [pathA, setPathA, setPathB]);

  const handleSubmit = useCallback(async () => {
    if (!pathA.trim() || !pathB.trim()) return;
    setError('');
    setResults(null);
    setActiveNarrative('a');

    const parsed = await callToolEndpoint('contrast-report', {
      pathA: pathA.trim(),
      pathB: pathB.trim(),
      aboutYou: aboutYou.trim() || undefined,
      timeframe,
    });

    if (parsed) {
      setResults(parsed);
      const entry = {
        id: 'cr_' + Date.now(),
        date: new Date().toISOString(),
        pathA: pathA.trim().substring(0, 50),
        pathB: pathB.trim().substring(0, 50),
        framed: parsed?.decision_framed?.substring(0, 80) || '',
        results: parsed,
      };
      setHistory(prev => [entry, ...prev].slice(0, 6));
    } else {
      setError('Failed to generate contrast report. Please try again.');
    }
  }, [pathA, pathB, aboutYou, timeframe, callToolEndpoint, setResults, setHistory]);

  // ─── Document-level Ctrl+Enter submit ───
  useEffect(() => {
    const handler = (e) => {
      if ((e.ctrlKey || e.metaKey) && e.key === 'Enter' && !loading) {
        e.preventDefault();
        handleSubmit();
      }
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
  }); // eslint-disable-line react-hooks/exhaustive-deps

  const handleReset = useCallback(() => {
    setPathA('');
    setPathB('');
    setAboutYou('');
    setResults(null);
    setError('');
    setActiveNarrative('a');
  }, [setPathA, setPathB, setAboutYou, setResults]);

  // ─── Build copyable text ───
  const buildFullText = useCallback(() => {
    if (!results) return '';
    let t = `🔮 THE CONTRAST REPORT\n`;
    t += `"${results.decision_framed || `${pathA} vs. ${pathB}`}"\n`;
    t += `Timeframe: ${timeframe}\n\n`;
    if (results.path_a) {
      t += `━━ PATH A: ${results.path_a.label} ━━\n\n`;
      t += `${results.path_a.narrative?.trimEnd()}…\n\n`;
      t += `✦ Best moment: ${results.path_a.the_good_moment}\n`;
      t += `✧ Honest cost: ${results.path_a.the_honest_cost}\n\n`;
    }
    if (results.path_b) {
      t += `━━ PATH B: ${results.path_b.label} ━━\n\n`;
      t += `${results.path_b.narrative?.trimEnd()}…\n\n`;
      t += `✦ Best moment: ${results.path_b.the_good_moment}\n`;
      t += `✧ Honest cost: ${results.path_b.the_honest_cost}\n\n`;
    }
    if (results.what_i_noticed) {
      t += `━━ WHAT I NOTICED ━━\n\n`;
      t += `The pull: ${results.what_i_noticed.the_pull}\n\n`;
      t += `What you're trading: ${results.what_i_noticed.what_youre_trading}\n\n`;
      t += `The question underneath: ${results.what_i_noticed.the_question_underneath}\n\n`;
    }
    return t + BRAND;
  }, [results, pathA, pathB, timeframe]);

  const canSubmit = !loading && pathA.trim() && pathB.trim();

  // ─── Loading state ───
  const LoadingCard = () => (
    <div className={`${c.card} border ${c.border} rounded-2xl p-8 text-center space-y-3`}>
      <span className="animate-spin inline-block text-3xl">{tool?.icon ?? '🔮'}</span>
      <p className={`text-sm font-semibold ${c.text}`}>Imagining both futures…</p>
      <p className={`text-xs ${c.textMuted}`}>This takes about 20 seconds</p>
    </div>
  );

  // ─── Narrative Section ───
  const NarrativeSection = ({ path, side }) => {
    if (!path) return null;
    const isA = side === 'a';
    return (
      <div className="space-y-5">
        <div className="flex items-center gap-3">
          <div className={`w-1 h-8 rounded-full ${isA ? c.pathABar : c.pathBBar}`} />
          <div>
            <p className={`text-xs font-semibold uppercase tracking-wider ${c.textMuted}`}>
              Path {side.toUpperCase()}
            </p>
            <h3 className={`text-lg font-bold ${c.text}`}>{path.label}</h3>
          </div>
        </div>
        <div className={`${isA ? c.pathABg : c.pathBBg} ${isA ? c.pathABorder : c.pathBBorder} border rounded-2xl p-6 sm:p-8`}>
          <p
            className={`text-sm sm:text-base ${c.text} leading-[1.85] whitespace-pre-wrap`}
            style={{ fontFamily: 'Georgia, "Times New Roman", serif' }}>
            {path.narrative?.trimEnd()}…
          </p>
        </div>
        <div className="grid grid-cols-1 sm:grid-cols-2 gap-3">
          {path.the_good_moment && (
            <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
              <p className={`text-xs font-semibold uppercase tracking-wider ${isA ? c.pathAAccent : c.pathBAccent} mb-2 flex items-center gap-1.5`}>
                <span>✦</span> The moment that makes it worth it
              </p>
              <p className={`text-sm ${c.textSecondary} leading-relaxed italic`}>{path.the_good_moment}</p>
            </div>
          )}
          {path.the_honest_cost && (
            <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
              <p className={`text-xs font-semibold uppercase tracking-wider ${c.textMuted} mb-2 flex items-center gap-1.5`}>
                <span>✧</span> The cost nobody warns you about
              </p>
              <p className={`text-sm ${c.textSecondary} leading-relaxed italic`}>{path.the_honest_cost}</p>
            </div>
          )}
        </div>
      </div>
    );
  };

  // ════════════════════════════════════════════════════════════
  // RENDER
  // ════════════════════════════════════════════════════════════
  return (
    <div className="space-y-4">

      {/* ─── Standard header card ─── */}
      <div className={`${c.card} border ${c.border} rounded-xl shadow-sm p-5`}>
        <div className={`pb-3 mb-4 border-b ${c.border}`}>
          <h2 className={`text-xl font-bold ${c.text} flex items-center gap-2`}>
            <span>{tool?.icon ?? '🔮'}</span>{tool?.title ?? 'The Contrast Report'}
          </h2>
          <p className={`text-sm ${c.textSecondary} mt-0.5`}>
            {tool?.tagline ?? 'Feel both futures before you choose.'}
          </p>
        </div>

        {/* Pre-result cross-ref */}
        <p className={`text-xs text-center ${c.textMuted} mb-4`}>
          Already know what you want but can't commit?{' '}
          <a href="/DecisionCoach" rel="noopener noreferrer" className={linkStyle}>Decision Coach</a>{' '}
          helps you make it stick.
        </p>

        {/* ─── Input form — always visible ─── */}
        <div className="space-y-5">

          {/* Path A */}
          <div className="space-y-2">
            <div className="flex items-center gap-2">
              <div className={`w-1 h-5 rounded-full ${c.pathABar}`} />
              <label className={`text-sm font-semibold ${c.text}`}>
                Path A <span className={c.textMuted}>*</span>
              </label>
            </div>
            <textarea
              value={pathA}
              onChange={e => setPathA(e.target.value)}
              placeholder="e.g. Stay in my current city, keep my job, stay close to family…"
              rows={2}
              maxLength={400}
              className={`w-full px-4 py-3 rounded-xl text-sm border ${c.input} resize-none outline-none`}
            />
          </div>

          {/* Path B */}
          <div className="space-y-2">
            <div className="flex items-center gap-2">
              <div className={`w-1 h-5 rounded-full ${c.pathBBar}`} />
              <label className={`text-sm font-semibold ${c.text}`}>
                Path B <span className={c.textMuted}>*</span>
              </label>
            </div>
            <textarea
              value={pathB}
              onChange={e => setPathB(e.target.value)}
              placeholder="e.g. Move across the country for the new job, start over in a new city…"
              rows={2}
              maxLength={400}
              className={`w-full px-4 py-3 rounded-xl text-sm border ${c.input} resize-none outline-none`}
            />
          </div>

          {/* About you */}
          <div className="space-y-2">
            <label className={`text-sm font-semibold ${c.text}`}>
              About you{' '}
              <span className={`font-normal ${c.textMuted}`}>(optional — makes narratives more personal)</span>
            </label>
            <textarea
              value={aboutYou}
              onChange={e => setAboutYou(e.target.value)}
              placeholder="e.g. I'm 28, in a relationship, love cooking, work in marketing, introverted…"
              rows={2}
              maxLength={400}
              className={`w-full px-4 py-3 rounded-xl text-sm border ${c.input} resize-none outline-none`}
            />
          </div>

          {/* Timeframe */}
          <div className="space-y-2">
            <label className={`text-sm font-semibold ${c.text}`}>
              <span className="mr-1.5">⏳</span> How far out?
            </label>
            <div className="flex gap-2">
              {TIMEFRAME_OPTIONS.map(t => (
                <button
                  key={t.id}
                  onClick={() => setTimeframe(t.id)}
                  className={`flex-1 border rounded-xl py-2 text-sm font-medium text-center transition-all duration-150 ${timeframe === t.id ? c.chipActive : c.chipInactive}`}>
                  {t.label}
                </button>
              ))}
            </div>
          </div>

          {/* Example + Submit */}
          <div className="flex items-center gap-2 pt-1">
            <button
              onClick={handleExample}
              className={`${c.btnSecondary} px-3 py-1.5 rounded-lg text-xs transition-colors flex items-center gap-1.5`}>
              <span>🎲</span> Example
            </button>
            {results && (
              <button
                onClick={handleReset}
                className={`${c.btnSecondary} px-3 py-1.5 rounded-lg text-xs transition-colors flex items-center gap-1.5`}>
                <span>🔄</span> Reset
              </button>
            )}
          </div>

          <button
            onClick={handleSubmit}
            disabled={!canSubmit}
            className={`w-full ${c.btnPrimary} py-3 rounded-xl font-semibold text-sm shadow-sm
              disabled:opacity-40 disabled:cursor-not-allowed transition-all duration-200 flex items-center justify-center gap-2 min-h-[48px]`}>
            {loading
              ? <><span className="animate-spin inline-block">{tool?.icon ?? '🔮'}</span> Writing your futures…</>
              : <><span>{tool?.icon ?? '🔮'}</span> Show me both futures</>}
          </button>

          {error && (
            <div className={`${c.danger} border rounded-xl p-4 text-sm flex items-center gap-2`}>
              <span>⚠️</span> {error}
            </div>
          )}
        </div>
      </div>

      {/* Streaming Progress */}
      {loading && <LoadingCard />}

      {/* ═══════════════════════════════════════════════════════
          RESULTS
          ═══════════════════════════════════════════════════════ */}
      {results && (
        <div ref={resultsRef} className="space-y-6">

          {/* ActionBar */}
          <div className={`${c.card} border ${c.border} rounded-xl p-4 flex items-center justify-between flex-wrap gap-2`}>
            <span className={`text-sm font-semibold ${c.text}`}>Your Contrast Report</span>
            <ActionBar content={buildFullText()} title="The Contrast Report" />
          </div>

          {/* Decision framed */}
          {results.decision_framed && (
            <div className="text-center py-2">
              <p className={`text-xs font-semibold uppercase tracking-wider ${c.textMuted} mb-2`}>The decision</p>
              <p className={`text-base font-medium italic ${c.text} max-w-md mx-auto`}>
                "{results.decision_framed}"
              </p>
              <p className={`text-xs ${c.textMuted} mt-1`}>{timeframe} from now</p>
            </div>
          )}

          {/* Narrative tabs */}
          <div className={`flex border-b ${c.border}`}>
            <button
              onClick={() => setActiveNarrative('a')}
              className={`flex-1 py-3 text-sm font-medium text-center transition-all duration-150 flex items-center justify-center gap-2
                ${activeNarrative === 'a'
                  ? `${c.pathAAccent} border-b-2 ${c.pathATabBorder}`
                  : c.textMuted}`}>
              <div className={`w-2 h-2 rounded-full ${c.pathABar}`} />
              {results.path_a?.label || 'Path A'}
            </button>
            <button
              onClick={() => setActiveNarrative('b')}
              className={`flex-1 py-3 text-sm font-medium text-center transition-all duration-150 flex items-center justify-center gap-2
                ${activeNarrative === 'b'
                  ? `${c.pathBAccent} border-b-2 ${c.pathBTabBorder}`
                  : c.textMuted}`}>
              <div className={`w-2 h-2 rounded-full ${c.pathBBar}`} />
              {results.path_b?.label || 'Path B'}
            </button>
          </div>

          {/* Active narrative */}
          <div>
            {activeNarrative === 'a' && <NarrativeSection path={results.path_a} side="a" />}
            {activeNarrative === 'b' && <NarrativeSection path={results.path_b} side="b" />}
          </div>

          {/* What I noticed */}
          {results.what_i_noticed && (
            <div className="space-y-4">
              <div className={`flex items-center gap-4 py-2`}>
                <div className={`flex-1 border-t ${c.border}`} />
                <span className={`text-xs font-semibold uppercase tracking-wider ${c.textMuted}`}>What I noticed</span>
                <div className={`flex-1 border-t ${c.border}`} />
              </div>

              {results.what_i_noticed.the_pull && (
                <div className={`${c.cardAlt} rounded-2xl p-5`}>
                  <p className={`text-xs font-semibold uppercase tracking-wider ${c.textMuted} mb-2 flex items-center gap-1.5`}>
                    <span>🧲</span> The pull
                  </p>
                  <p className={`text-sm ${c.text} leading-relaxed`}>{results.what_i_noticed.the_pull}</p>
                </div>
              )}

              {results.what_i_noticed.what_youre_trading && (
                <div className={`${c.card} border ${c.border} rounded-2xl p-5`}>
                  <p className={`text-xs font-semibold uppercase tracking-wider ${c.textMuted} mb-2 flex items-center gap-1.5`}>
                    <span>⚖️</span> What you're trading
                  </p>
                  <p className={`text-sm ${c.text} leading-relaxed`}>{results.what_i_noticed.what_youre_trading}</p>
                </div>
              )}

              {results.what_i_noticed.the_question_underneath && (
                <div className={`${c.cardAlt} border ${c.border} rounded-2xl p-6 text-center`}>
                  <p className={`text-xs font-semibold uppercase tracking-wider ${c.textMuted} mb-3`}>
                    The question underneath
                  </p>
                  <p className={`text-base italic ${c.text} leading-relaxed max-w-md mx-auto`}>
                    "{results.what_i_noticed.the_question_underneath}"
                  </p>
                </div>
              )}
            </div>
          )}

          {/* Cross-references */}
          <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
            <p className={`text-xs font-semibold ${c.textMuted} uppercase tracking-wider mb-2`}>Related tools</p>
            <div className={`space-y-1 text-xs ${c.textSecondary}`}>
              <p>Challenge the assumptions behind this choice → <a href="/BeliefStressTest" rel="noopener noreferrer" className={linkStyle}>Belief Stress Test</a></p>
              <p>Explore alternative framings → <a href="/AlternatePath" rel="noopener noreferrer" className={linkStyle}>Alternate Path</a></p>
              <p>Compare the financial trade-offs → <a href="/BuyWise" rel="noopener noreferrer" className={linkStyle}>BuyWise</a></p>
            </div>
          </div>

          {/* AI disclaimer */}
          <div className={`p-4 rounded-xl text-center ${c.cardAlt}`}>
            <p className={`text-xs ${c.textMuted}`}>🔮 AI-generated narratives. These are simulations to help you feel possibilities — not predictions or advice. Your gut reaction is the data.</p>
          </div>
        </div>
      )}

      {/* ─── History ─── */}
      {history.length > 0 && (
        <div className={`p-4 rounded-2xl border ${c.border} ${c.cardAlt}`}>
          <button onClick={() => setShowHistory(!showHistory)} className="w-full flex items-center gap-2 text-left">
            <span>📋</span>
            <span className={`text-sm font-bold ${c.text} flex-1`}>Past Decisions</span>
            <span className={`text-xs ${c.textMuted}`}>{history.length}</span>
            <span className={`text-xs ${c.textMuted}`}>{showHistory ? '▲' : '▼'}</span>
          </button>
          {showHistory && (
            <div className="mt-3 space-y-2">
              {history.map(entry => (
                <div key={entry.id} className={`rounded-xl border ${c.border} ${c.card} p-3 flex items-center gap-3`}>
                  <span className="text-lg">🔮</span>
                  <div className="flex-1 min-w-0">
                    <div className={`text-xs ${c.text} truncate`}>
                      <span className={c.pathAAccent}>A:</span> {entry.pathA}
                    </div>
                    <div className={`text-xs ${c.text} truncate`}>
                      <span className={c.pathBAccent}>B:</span> {entry.pathB}
                    </div>
                    <div className={`text-xs ${c.textMuted} mt-0.5`}>
                      {(() => {
                        try {
                          const d = new Date(entry.date);
                          const diff = Math.floor((new Date() - d) / 86400000);
                          return diff === 0 ? 'Today' : diff === 1 ? 'Yesterday' : diff < 7 ? diff + 'd ago' : d.toLocaleDateString('en-US', { month: 'short', day: 'numeric' });
                        } catch { return ''; }
                      })()}
                    </div>
                  </div>
                  <button
                    onClick={() => {
                      setResults(entry.results);
                      setPathA(entry.pathA);
                      setPathB(entry.pathB);
                      setShowHistory(false);
                    }}
                    className={`px-3 py-1.5 rounded-lg text-xs font-bold ${c.btnSecondary}`}>
                    View
                  </button>
                </div>
              ))}
            </div>
          )}
        </div>
      )}
    </div>
  );
};

ContrastReport.displayName = 'ContrastReport';
export default ContrastReport;
