import React, { useState, useRef, useCallback, useEffect, useMemo } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { usePersistentState } from '../hooks/usePersistentState';
import { useRegisterActions } from '../components/ActionBarContext';
import { useTranslation } from '../i18n/useTranslation';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

// ════════════════════════════════════════════════════════════
// CONSTANTS — values are stable keys; labels are localized at render time
// ════════════════════════════════════════════════════════════
const MODE_VALUES = ['distill', 'study_guide', 'test_prep', 'connect'];
const MODE_EMOJI = { distill: '🎯', study_guide: '📝', test_prep: '🧪', connect: '🔗' };

// ════════════════════════════════════════════════════════════
// MAIN COMPONENT
// ════════════════════════════════════════════════════════════
const Recall = ({ tool }) => {
  const { callToolEndpoint, loading, userLocale, userCurrency, userRegion } = useClaudeAPI();
  const { isDark } = useTheme();
  const { t } = useTranslation();

  // Localized label maps (depend on t())
  const MODE_KEY = { distill: 'distill', study_guide: 'study', test_prep: 'test', connect: 'connect' };
  const MODES = MODE_VALUES.map(value => ({
    value, emoji: MODE_EMOJI[value],
    label: t(`rec_mode_${MODE_KEY[value]}_label`),
    desc: t(`rec_mode_${MODE_KEY[value]}_desc`),
  }));
  const PRIORITIES = [
    { value: 'balanced',   label: t('rec_prio_balanced') },
    { value: 'conceptual', label: t('rec_prio_conceptual') },
    { value: 'factual',    label: t('rec_prio_factual') },
    { value: 'applied',    label: t('rec_prio_applied') },
  ];
  const EXAM_FORMATS = [
    { value: 'mixed',           label: t('rec_fmt_mixed') },
    { value: 'multiple_choice', label: t('rec_fmt_mc') },
    { value: 'short_answer',    label: t('rec_fmt_short') },
    { value: 'essay',           label: t('rec_fmt_essay') },
    { value: 'problem_solving', label: t('rec_fmt_problem') },
  ];
  const Q_TYPES = [
    { value: 'multiple_choice', label: t('rec_qt_mc') },
    { value: 'short_answer',    label: t('rec_qt_short') },
    { value: 'essay',           label: t('rec_qt_essay') },
    { value: 'true_false',      label: t('rec_qt_tf') },
    { value: 'fill_blank',      label: t('rec_qt_fill') },
  ];
  const TYPE_LABELS = {
    definition: t('rec_type_definition'), process: t('rec_type_process'), cause_effect: t('rec_type_cause_effect'),
    comparison: t('rec_type_comparison'), application: t('rec_type_application'), framework: t('rec_type_framework'),
    fact: t('rec_type_fact'), formula: t('rec_type_formula'),
  };
  const DIFF_LABELS = { easy: t('rec_diff_easy'), medium: t('rec_diff_medium'), hard: t('rec_diff_hard') };

  // ── Theme — verbatim from CONVENTIONS.md PF-2 ──
  const c = {
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:         isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20'
                          : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-500' : 'text-gray-400',
    required:      isDark ? 'text-amber-400' : 'text-amber-500',
    labelText:     isDark ? 'text-zinc-200' : 'text-gray-700',
    accentTxt:     isDark ? 'text-cyan-400' : 'text-cyan-600',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white'
                          : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200'
                          : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200'
                          : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200'
                          : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200'
                          : 'bg-red-50 border-red-200 text-red-800',
    successTxt:    isDark ? 'text-emerald-300' : 'text-emerald-800',
    pillActive:    isDark ? 'border-cyan-500 bg-cyan-900/30 text-cyan-200'
                          : 'border-cyan-600 bg-cyan-100 text-cyan-900',
    pillInactive:  isDark ? 'border-zinc-600 text-zinc-400 hover:border-zinc-500'
                          : 'border-gray-300 text-gray-500 hover:border-gray-400',
    tipBg:         isDark ? 'bg-cyan-900/20 border-cyan-700' : 'bg-cyan-50 border-cyan-200',
    tipText:       isDark ? 'text-cyan-300' : 'text-cyan-700',
    inset:         isDark ? 'bg-zinc-700/60' : 'bg-slate-100',
    badge:         isDark ? 'bg-zinc-600 text-zinc-200' : 'bg-zinc-100 text-zinc-600',
    defBg:         isDark ? 'bg-fuchsia-900/30 text-fuchsia-300' : 'bg-fuchsia-50 text-fuchsia-700',
    procBg:        isDark ? 'bg-sky-900/30 text-sky-300'         : 'bg-sky-50 text-sky-700',
    factBg:        isDark ? 'bg-amber-900/30 text-amber-300'     : 'bg-amber-50 text-amber-700',
    histBg:        isDark ? 'bg-zinc-700/30 border-zinc-600'     : 'bg-slate-50 border-slate-200',
    histCard:      isDark ? 'bg-zinc-700/50 border-zinc-600'     : 'bg-white border-slate-200',
    cardAltBg:     isDark ? 'bg-zinc-700/40 border-zinc-600'     : 'bg-slate-50 border-slate-200',
    cardAltText:   isDark ? 'text-zinc-300' : 'text-gray-600',
    warnText:      isDark ? 'text-amber-300' : 'text-amber-700',
  };
  c.textMuteded = c.textMuted;
  c.label       = c.labelText;

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';

  // ── State ──
  const [mode, setMode]             = useState('distill');
  const [transcript, setTranscript] = useState('');
  const [subject, setSubject]       = useState('');
  const [lectureTitle, setLectureTitle] = useState('');
  const [bulletCount, setBulletCount]   = useState(10);
  const [priority, setPriority]         = useState('balanced');
  const [examFormat, setExamFormat]     = useState('mixed');
  const [questionTypes, setQuestionTypes] = useState(['multiple_choice', 'short_answer']);
  const [difficulty, setDifficulty]     = useState('mixed');
  const [questionCount, setQuestionCount] = useState(10);
  const [lectures, setLectures]         = useState([{ title: '', transcript: '' }, { title: '', transcript: '' }]);
  const [error, setError]               = useState('');
  const [showAnswers, setShowAnswers]   = useState({});
  const [expandedSections, setExpandedSections] = useState({});
  const [showHistory, setShowHistory]   = useState(false);

  // ── Refs ──
  const resultsRef   = useRef(null);
  const submitRef    = useRef(null);
  const canSubmitRef = useRef(false);

  const lecturesInputRefs = useRef([]);
  const shouldFocusNewLecturesRef = useRef(false);
  // ── Persistent state ──
  const [results, setResults] = usePersistentState('recall-results', null);
  const [sessionHistory, setSessionHistory] = usePersistentState('recall-history', []);

  // ── Helpers ──
  const addLecture    = useCallback(() => { if (lectures.length < 5) { shouldFocusNewLecturesRef.current = true; setLectures(p => [...p, { title: '', transcript: '' }]); }; }, [lectures.length]);
  const removeLecture = useCallback((i) => { if (lectures.length > 2) setLectures(p => p.filter((_, idx) => idx !== i)); }, [lectures.length]);
  const updateLecture = useCallback((i, field, val) => setLectures(p => p.map((l, idx) => idx === i ? { ...l, [field]: val } : l)), []);
  const toggleQType   = useCallback((val) => setQuestionTypes(prev => prev.includes(val) ? prev.filter(v => v !== val) : [...prev, val]), []);
  const toggleSection = useCallback((key) => setExpandedSections(prev => ({ ...prev, [key]: !prev[key] })), []);

  const charCount = useMemo(() => {
    if (mode === 'connect') return lectures.reduce((sum, l) => sum + (l.transcript?.length || 0), 0);
    return transcript.length;
  }, [mode, transcript, lectures]);

  const canSubmit = mode === 'connect'
    ? lectures.filter(l => l.transcript?.trim()).length >= 2
    : transcript.trim().length > 0;

  // ── API ──
  const submit = useCallback(async () => {
    setError(''); setResults(null); setShowAnswers({});
    try {
      let data;
      if (mode === 'distill') {
        if (!transcript.trim()) { setError(t('rec_err_paste')); return; }
        data = await callToolEndpoint('recall', {
          transcript: transcript.trim(), subject: subject.trim() || null,
          lectureTitle: lectureTitle.trim() || null, bulletCount, priority,
          userLocale, userCurrency, userRegion,
        });
      } else if (mode === 'study_guide') {
        if (!transcript.trim()) { setError(t('rec_err_paste')); return; }
        data = await callToolEndpoint('recall/study-guide', {
          transcript: transcript.trim(), subject: subject.trim() || null,
          lectureTitle: lectureTitle.trim() || null, examFormat,
          userLocale, userCurrency, userRegion,
        });
      } else if (mode === 'test_prep') {
        if (!transcript.trim()) { setError(t('rec_err_paste')); return; }
        data = await callToolEndpoint('recall/test-prep', {
          transcript: transcript.trim(), subject: subject.trim() || null,
          lectureTitle: lectureTitle.trim() || null, questionTypes, difficulty, questionCount,
          userLocale, userCurrency, userRegion,
        });
      } else if (mode === 'connect') {
        const valid = lectures.filter(l => l.transcript?.trim());
        if (valid.length < 2) { setError(t('rec_err_two_lectures')); return; }
        data = await callToolEndpoint('recall/connect', {
          lectures: valid.map(l => ({ title: l.title.trim() || null, transcript: l.transcript.trim() })),
          subject: subject.trim() || null,
          userLocale, userCurrency, userRegion,
        });
      }
      setResults(data);
      setSessionHistory(prev => [{
        id: 'rc_' + Date.now(), date: new Date().toISOString(), mode,
        title: lectureTitle.trim() || subject.trim() || data?.lecture_summary?.substring(0, 40) || mode,
        subject: data?.subject_detected || subject.trim() || '',
        preview: (lectureTitle.trim() || subject.trim() || mode).slice(0, 40),
      }, ...prev].slice(0, 6));
    } catch (err) { setError(err.message || t('rec_err_failed')); }
  }, [mode, transcript, subject, lectureTitle, bulletCount, priority, examFormat,
      questionTypes, difficulty, questionCount, lectures, callToolEndpoint, setResults, setSessionHistory,
      userLocale, userCurrency, userRegion, t]);

  const handleReset = useCallback(() => {
    setTranscript(''); setSubject(''); setLectureTitle('');
    setResults(null); setError(''); setShowAnswers({});
    setLectures([{ title: '', transcript: '' }, { title: '', transcript: '' }]);
  }, [setResults]);

  const loadExample = useCallback(() => {
    setMode('distill');
    setSubject(t('rec_ex_subject'));
    setLectureTitle(t('rec_ex_title'));
    setTranscript(t('rec_ex_transcript'));
    setResults(null); setError(''); setShowAnswers({});
  }, [setResults, t]);

  // ── buildFullText ──
  const buildFullText = useCallback(() => {
    if (!results) return '';
    const lines = [];
    if (mode === 'distill' && results.bullets?.length) {
      lines.push(`🎯 ${t('rec_copy_distill')} — ${results.lecture_summary || t('rec_copy_notes_fallback')}`, '');
      results.bullets.forEach(b => lines.push(`${b.rank}. ${b.point}`));
      if (results.vocabulary?.length) {
        lines.push('', t('rec_copy_key_terms'));
        results.vocabulary.forEach(v => lines.push(`• ${v.term}: ${v.definition}`));
      }
    } else if (mode === 'study_guide' && results) {
      lines.push(`📝 ${t('rec_copy_study')} — ${results.title || t('rec_lecture_fallback')}`, '', results.overview || '', '');
      results.concepts_to_know?.forEach(item => lines.push(`■ ${item.concept}: ${item.explanation}`));
      if (results.key_definitions?.length) {
        lines.push('', t('rec_copy_definitions'));
        results.key_definitions.forEach(d => lines.push(`• ${d.term}: ${d.definition}`));
      }
    } else if (mode === 'test_prep' && results.questions?.length) {
      lines.push(t('rec_copy_practice'), '');
      results.questions.forEach(q => {
        lines.push(`Q${q.number}. ${q.question}`);
        if (q.options) q.options.forEach(o => lines.push(`  ${o}`));
        lines.push(`  ${t('rec_copy_answer')} ${q.answer}`, '');
      });
    } else if (mode === 'connect' && results) {
      if (results.course_narrative) lines.push(`🔗 ${results.course_narrative}`, '');
      results.recurring_themes?.forEach(theme => lines.push(`■ ${theme.theme}: ${theme.why_recurring}`));
    }
    const body = lines.join('\n').trim();
    return body ? body + BRAND : '';
  }, [results, mode, t]);

  // Live ref assignments
  submitRef.current    = submit;
  canSubmitRef.current = canSubmit;

  useRegisterActions(buildFullText(), tool?.title || 'Recall');

  // Scroll to results
  useEffect(() => {
    if (!results) return;
    const timer = setTimeout(() => resultsRef.current?.scrollIntoView({ behavior: 'smooth', block: 'start' }), 200);
    return () => clearTimeout(timer);
  }, [results]);

  // Keyboard shortcut
  useEffect(() => {
    const handler = (e) => {
      const tag = document.activeElement?.tagName;
      if (tag === 'SELECT') return;
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && !loading && canSubmitRef.current)
        submitRef.current?.();
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [loading]);

  useEffect(() => {
    if (shouldFocusNewLecturesRef.current) {
      const last = lecturesInputRefs.current[lectures.length - 1];
      if (last) last.focus();
      shouldFocusNewLecturesRef.current = false;
    }
  }, [lectures.length]);

  // ── Shared sub-components ──
  const Pill = ({ active, onClick, children }) => (
    <button onClick={onClick}
      className={`px-3 py-1.5 rounded-lg border text-xs font-semibold transition-all ${active ? c.pillActive : c.pillInactive}`}>
      {active && <span className="mr-1">✓</span>}{children}
    </button>
  );

  const Section = ({ title, emoji, sKey, badge, defaultOpen = false, children }) => {
    const open = expandedSections[sKey] ?? defaultOpen;
    return (
      <div className={`${c.card} border ${c.border} rounded-xl overflow-hidden`}>
        <button onClick={() => toggleSection(sKey)} className="w-full flex items-center justify-between p-4 text-left hover:opacity-80">
          <div className="flex items-center gap-2">
            <span className="text-base">{emoji}</span>
            <span className={`text-sm font-semibold ${c.text}`}>{title}</span>
            {badge && <span className={`text-xs px-2 py-0.5 rounded-full ${c.badge}`}>{badge}</span>}
          </div>
          <span className={`text-xs ${c.textMuted}`}>{open ? '▲' : '▼'}</span>
        </button>
        {open && <div className={`px-4 pb-4 border-t ${c.border}`}>{children}</div>}
      </div>
    );
  };

  // ════════════════════════════════════════════════════════════
  // INPUT
  // ════════════════════════════════════════════════════════════
  const renderInput = () => (
    <div className="space-y-4">
      {/* Mode tabs */}
      <div className="flex gap-1.5 overflow-x-auto pb-1">
        {MODES.map(m => (
          <button key={m.value} onClick={() => { setMode(m.value); setResults(null); setError(''); }}
            className={`flex items-center gap-2 px-4 py-3 rounded-xl border-2 transition-all flex-shrink-0 ${
              mode === m.value ? `${c.pillActive} border-2` : c.pillInactive}`}>
            <span className="text-lg">{m.emoji}</span>
            <div className="text-left">
              <p className={`text-xs font-bold ${mode === m.value ? '' : c.textMuted}`}>{m.label}</p>
              <p className={`text-[10px] ${c.textMuted}`}>{m.desc}</p>
            </div>
          </button>
        ))}
      </div>

      {/* Transcript — all modes except Connect */}
      {mode !== 'connect' ? (
        <div className={`${c.card} border ${c.border} rounded-xl p-5`}>
          <label className={`text-base font-bold ${c.text} mb-1 block`}>{t('rec_input_label')} <span className={c.required}>*</span></label>
          <p className={`text-xs ${c.textMuted} mb-3`}>{t('rec_input_help')}</p>
          <textarea value={transcript} onChange={e => setTranscript(e.target.value)}
            placeholder={t('rec_input_ph')}
            className={`w-full h-40 p-4 border-2 rounded-xl ${c.input} outline-none focus:ring-2 resize-none text-sm font-mono`} />
          {charCount > 0 && (
            <p className={`text-xs ${c.textMuted} mt-1`}>
              {t('rec_chars_minutes', { chars: charCount.toLocaleString(), min: Math.round(charCount / 750) })}
            </p>
          )}
        </div>
      ) : (
        <div className="space-y-3">
          {lectures.map((lec, idx) => (
            <div key={idx} className={`${c.card} border ${c.border} rounded-xl p-4`}>
              <div className="flex items-center gap-2 mb-2">
                <span className={`text-xs font-bold ${c.badge} px-2 py-0.5 rounded-full`}>{t('rec_lecture_n', { n: idx + 1 })}</span>
                <input ref={el => { lecturesInputRefs.current[idx] = el; }} type="text" value={lec.title} onChange={e => updateLecture(idx, 'title', e.target.value)}
                  placeholder={t('rec_lecture_title_ph')}
                  className={`flex-1 px-3 py-1.5 rounded-lg border text-xs ${c.input} outline-none`} />
                {lectures.length > 2 && (
                  <button onClick={() => removeLecture(idx)} className={`text-xs px-2 py-1 rounded ${c.warnText}`}>✕</button>
                )}
              </div>
              <textarea value={lec.transcript} onChange={e => updateLecture(idx, 'transcript', e.target.value)}
                placeholder={t('rec_lecture_transcript_ph')}
                className={`w-full h-24 p-3 border-2 rounded-xl ${c.input} outline-none focus:ring-2 resize-none text-xs font-mono`} />
            </div>
          ))}
          {lectures.length < 5 && (
            <button onClick={addLecture} className={`w-full py-3 rounded-xl border-2 border-dashed text-xs font-bold ${c.pillInactive}`}>
              {t('rec_add_lecture')}
            </button>
          )}
          {charCount > 0 && (
            <p className={`text-xs ${c.textMuted}`}>
              {t('rec_chars_across', { chars: charCount.toLocaleString(), count: lectures.filter(l => l.transcript?.trim()).length })}
            </p>
          )}
        </div>
      )}

      {/* Context + mode options */}
      <div className={`${c.card} border ${c.border} rounded-xl p-5 space-y-3`}>
        <div className="grid grid-cols-2 gap-3">
          <div>
            <label className={`text-xs font-bold ${c.textSecondary} uppercase tracking-wide mb-1 block`}>{t('rec_subject_label')}</label>
            <input type="text" value={subject} onChange={e => setSubject(e.target.value)}
              placeholder={t('rec_subject_ph')}
              className={`w-full px-3 py-2 rounded-xl border text-sm ${c.input} outline-none`} />
          </div>
          {mode !== 'connect' && (
            <div>
              <label className={`text-xs font-bold ${c.textSecondary} uppercase tracking-wide mb-1 block`}>{t('rec_topic_label')}</label>
              <input type="text" value={lectureTitle} onChange={e => setLectureTitle(e.target.value)}
                placeholder={t('rec_topic_ph')}
                className={`w-full px-3 py-2 rounded-xl border text-sm ${c.input} outline-none`} />
            </div>
          )}
        </div>

        {mode === 'distill' && (
          <>
            <div>
              <label className={`text-xs font-bold ${c.textSecondary} uppercase tracking-wide mb-2 block`}>{t('rec_bullets_label', { count: bulletCount })}</label>
              <input type="range" min={5} max={20} value={bulletCount} onChange={e => setBulletCount(Number(e.target.value))}
                className="w-full accent-cyan-600" />
            </div>
            <div>
              <label className={`text-xs font-bold ${c.textSecondary} uppercase tracking-wide mb-2 block`}>{t('rec_priority_label')}</label>
              <div className="flex flex-wrap gap-1.5">
                {PRIORITIES.map(p => <Pill key={p.value} active={priority === p.value} onClick={() => setPriority(p.value)}>{p.label}</Pill>)}
              </div>
            </div>
          </>
        )}

        {mode === 'study_guide' && (
          <div>
            <label className={`text-xs font-bold ${c.textSecondary} uppercase tracking-wide mb-2 block`}>{t('rec_exam_format_label')}</label>
            <div className="flex flex-wrap gap-1.5">
              {EXAM_FORMATS.map(f => <Pill key={f.value} active={examFormat === f.value} onClick={() => setExamFormat(f.value)}>{f.label}</Pill>)}
            </div>
          </div>
        )}

        {mode === 'test_prep' && (
          <>
            <div>
              <label className={`text-xs font-bold ${c.textSecondary} uppercase tracking-wide mb-2 block`}>{t('rec_qtypes_label')}</label>
              <div className="flex flex-wrap gap-1.5">
                {Q_TYPES.map(qt => <Pill key={qt.value} active={questionTypes.includes(qt.value)} onClick={() => toggleQType(qt.value)}>{qt.label}</Pill>)}
              </div>
            </div>
            <div className="flex items-center gap-4">
              <div className="flex-1">
                <label className={`text-xs font-bold ${c.textSecondary} uppercase tracking-wide mb-1 block`}>{t('rec_questions_label', { count: questionCount })}</label>
                <input type="range" min={5} max={20} value={questionCount} onChange={e => setQuestionCount(Number(e.target.value))}
                  className="w-full accent-cyan-600" />
              </div>
              <div>
                <label className={`text-xs font-bold ${c.textSecondary} uppercase tracking-wide mb-1 block`}>{t('rec_difficulty_label')}</label>
                <select value={difficulty} onChange={e => setDifficulty(e.target.value)}
                  className={`px-3 py-2 rounded-lg border text-xs ${c.input} outline-none`}>
                  <option value="mixed">{t('rec_diff_mixed')}</option>
                  <option value="easy">{t('rec_diff_easy')}</option>
                  <option value="hard">{t('rec_diff_hard')}</option>
                </select>
              </div>
            </div>
          </>
        )}
      </div>

      <div className="flex gap-2">
        <button onClick={submit} disabled={loading || !canSubmit}
          className={`flex-1 ${c.btnPrimary} disabled:opacity-40 py-4 rounded-2xl text-sm font-bold flex items-center justify-center gap-2 transition-all min-h-[48px]`}>
          {loading
            ? <><span className="animate-spin inline-block">{tool?.icon ?? '🧠'}</span> {t('rec_processing')}</>
            : mode === 'distill'     ? <><span>{tool?.icon ?? '🧠'}</span> {t('rec_submit_distill')}</>
            : mode === 'study_guide' ? <><span>{tool?.icon ?? '🧠'}</span> {t('rec_submit_study')}</>
            : mode === 'test_prep'   ? <><span>{tool?.icon ?? '🧠'}</span> {t('rec_submit_test')}</>
            : <><span>{tool?.icon ?? '🧠'}</span> {t('rec_submit_connect')}</>}
        </button>
      </div>
      <p className={`text-xs text-center ${c.textMuted}`}>
        {t('rec_pre_xref')}{' '}
        <a href="/BrainDumpBuddy" className={linkStyle}>🧠 {t('rec_braindump')}</a>
        {' '}{t('rec_pre_xref_after')}
      </p>
    </div>
  );

  // ════════════════════════════════════════════════════════════
  // RESULTS — DISTILL
  // ════════════════════════════════════════════════════════════
  const renderDistill = () => {
    if (!results?.bullets?.length) return null;
    return (
      <div className="space-y-4">
        <div className={`p-5 rounded-2xl border-2 ${c.tipBg}`}>
          <p className={`text-xs font-bold ${c.tipText} uppercase mb-1`}>{results.subject_detected || subject || t('rec_lecture_fallback')}</p>
          <p className={`text-sm font-bold ${c.text}`}>{results.lecture_summary}</p>
        </div>
        <div className="space-y-2">
          {results.bullets.map((b, idx) => (
            <div key={idx} className={`${c.card} border ${c.border} rounded-xl p-4`}>
              <div className="flex items-start gap-3">
                <span className={`text-lg font-black ${idx < 3 ? c.tipText : c.textMuted}`}>{b.rank}</span>
                <div className="flex-1 min-w-0">
                  <p className={`text-sm ${c.text} mb-1.5`}>{b.point}</p>
                  <div className="flex flex-wrap gap-1.5">
                    {b.type && TYPE_LABELS[b.type] && (
                      <span className={`text-[9px] font-bold px-2 py-0.5 rounded-full ${
                        b.type === 'definition' ? c.defBg : b.type === 'process' || b.type === 'formula' ? c.procBg : c.factBg}`}>
                        {TYPE_LABELS[b.type]}
                      </span>
                    )}
                    {b.testable && <span className={`text-[9px] font-bold px-2 py-0.5 rounded-full ${c.badge}`}>{t('rec_testable')}</span>}
                  </div>
                  {b.test_hint && <p className={`text-[10px] ${c.textMuted} mt-1 italic`}>📝 {b.test_hint}</p>}
                </div>
              </div>
            </div>
          ))}
        </div>

        {results.vocabulary?.length > 0 && (
          <Section title={t('rec_key_terms')} emoji="📖" sKey="vocab" badge={String(results.vocabulary.length)} defaultOpen>
            <div className="space-y-2 mt-3">
              {results.vocabulary.map((v, i) => (
                <div key={i} className={`p-3 rounded-lg ${c.inset}`}>
                  <span className={`text-xs font-bold ${c.text}`}>{v.term}</span>
                  <span className={`text-xs ${c.textSecondary}`}> — {v.definition}</span>
                </div>
              ))}
            </div>
          </Section>
        )}

        {results.professor_signals?.length > 0 && (
          <div className={`p-4 rounded-xl border ${c.warning}`}>
            <p className={`text-xs font-bold ${c.warnText} mb-2`}>{t('rec_prof_flagged')}</p>
            {results.professor_signals.map((s, i) => <p key={i} className={`text-xs ${c.warnText} mb-1`}>• {s}</p>)}
          </div>
        )}

        {results.connections?.length > 0 && (
          <Section title={t('rec_connections_to_course')} emoji="🔗" sKey="conn">
            <div className="space-y-1 mt-3">
              {results.connections.map((cn, i) => <p key={i} className={`text-xs ${c.textSecondary}`}>→ {cn}</p>)}
            </div>
          </Section>
        )}
        {results.gaps?.length > 0 && (
          <Section title={t('rec_gaps_coming_next')} emoji="🔮" sKey="gaps">
            <div className="space-y-1 mt-3">
              {results.gaps.map((g, i) => <p key={i} className={`text-xs ${c.textSecondary}`}>• {g}</p>)}
            </div>
          </Section>
        )}
      </div>
    );
  };

  // ════════════════════════════════════════════════════════════
  // RESULTS — STUDY GUIDE
  // ════════════════════════════════════════════════════════════
  const renderStudyGuide = () => {
    if (!results) return null;
    return (
      <div className="space-y-4">
        <div className={`p-5 rounded-2xl border-2 ${c.tipBg}`}>
          <p className={`text-base font-bold ${c.text} mb-1`}>📝 {results.title}</p>
          <p className={`text-sm ${c.textSecondary}`}>{results.overview}</p>
        </div>

        {results.concepts_to_know?.length > 0 && (
          <Section title={t('rec_concepts_to_know')} emoji="🧠" sKey="concepts" badge={String(results.concepts_to_know.length)} defaultOpen>
            <div className="space-y-3 mt-3">
              {results.concepts_to_know.map((item, i) => (
                <div key={i} className={`p-3 rounded-lg border ${c.cardAlt}`}>
                  <div className="flex items-center gap-2 mb-1">
                    <span className={`text-sm font-bold ${c.text}`}>{item.concept}</span>
                    <span className={`text-[9px] font-bold px-2 py-0.5 rounded-full ${c.badge}`}>
                      {item.memorize_vs_understand === 'memorize' ? t('rec_memorize') : item.memorize_vs_understand === 'understand' ? t('rec_understand') : t('rec_both')}
                    </span>
                  </div>
                  <p className={`text-xs ${c.textSecondary} mb-1`}>{item.explanation}</p>
                  {item.mnemonic && <p className={`text-[10px] ${c.tipText} italic`}>💡 {item.mnemonic}</p>}
                </div>
              ))}
            </div>
          </Section>
        )}

        {results.key_definitions?.length > 0 && (
          <Section title={t('rec_key_definitions')} emoji="📖" sKey="defs" badge={String(results.key_definitions.length)}>
            <div className="space-y-2 mt-3">
              {results.key_definitions.map((d, i) => (
                <div key={i} className={`p-3 rounded-lg ${c.inset}`}>
                  <p className={`text-xs font-bold ${c.text}`}>{d.term}</p>
                  <p className={`text-xs ${c.textSecondary}`}>{d.definition}</p>
                  {d.distinguish_from && <p className={`text-[10px] ${c.tipText} mt-0.5`}>{t('rec_dont_confuse', { val: d.distinguish_from })}</p>}
                </div>
              ))}
            </div>
          </Section>
        )}

        {results.processes_and_formulas?.length > 0 && (
          <Section title={t('rec_processes_formulas')} emoji="⚙️" sKey="procs">
            <div className="space-y-3 mt-3">
              {results.processes_and_formulas.map((p, i) => (
                <div key={i} className={`p-3 rounded-lg border ${c.cardAlt}`}>
                  <p className={`text-xs font-bold ${c.text} mb-1`}>{p.name}</p>
                  <p className={`text-xs ${c.textSecondary} mb-1`}>{p.steps_or_formula}</p>
                  {p.when_to_use && <p className={`text-[10px] ${c.cardAltText}`}>{t('rec_when_to_use', { val: p.when_to_use })}</p>}
                  {p.common_mistake && <p className={`text-[10px] ${c.warnText} mt-0.5`}>{t('rec_common_mistake', { val: p.common_mistake })}</p>}
                </div>
              ))}
            </div>
          </Section>
        )}

        {results.relationships?.length > 0 && (
          <Section title={t('rec_relationships')} emoji="🔗" sKey="rels">
            <div className="space-y-2 mt-3">
              {results.relationships.map((r, i) => (
                <div key={i} className={`p-2 rounded-lg ${c.inset} flex items-center gap-2`}>
                  <span className={`text-[9px] font-bold px-2 py-0.5 rounded-full ${c.badge}`}>{r.type?.replace('_', ' ')}</span>
                  <p className={`text-xs ${c.text}`}>{r.relationship}</p>
                </div>
              ))}
            </div>
          </Section>
        )}

        {results.exam_strategy && (
          <div className={`p-4 rounded-xl border ${c.success}`}>
            <p className={`text-xs font-bold ${c.successTxt} mb-2`}>{t('rec_exam_strategy')}</p>
            {results.exam_strategy.likely_questions && <p className={`text-xs ${c.text} mb-1`}>📝 {results.exam_strategy.likely_questions}</p>}
            {results.exam_strategy.trap_warnings && <p className={`text-xs ${c.warnText} mb-1`}>⚠️ {results.exam_strategy.trap_warnings}</p>}
            {results.exam_strategy.time_allocation && <p className={`text-xs ${c.textMuted}`}>⏱️ {results.exam_strategy.time_allocation}</p>}
          </div>
        )}
      </div>
    );
  };

  // ════════════════════════════════════════════════════════════
  // RESULTS — TEST PREP
  // ════════════════════════════════════════════════════════════
  const renderTestPrep = () => {
    if (!results?.questions?.length) return null;
    return (
      <div className="space-y-4">
        <div className={`p-4 rounded-xl ${c.inset} flex items-center justify-between`}>
          <p className={`text-sm font-bold ${c.text}`}>🧪 {t('rec_practice_questions', { count: results.questions.length })}</p>
          <button
            onClick={() => setShowAnswers(results.questions.reduce((acc, q) => ({ ...acc, [q.number]: !Object.values(showAnswers).some(v => v) }), {}))}
            className={`text-xs font-bold ${c.accentTxt}`}>
            {Object.values(showAnswers).some(v => v) ? t('rec_hide_all') : t('rec_show_all')}
          </button>
        </div>

        {results.questions.map((q) => (
          <div key={q.number} className={`${c.card} border ${c.border} rounded-xl p-4`}>
            <div className="flex items-center gap-2 mb-2">
              <span className={`text-xs font-bold px-2 py-0.5 rounded-full ${c.badge}`}>Q{q.number}</span>
              <span className={`text-[9px] font-bold px-2 py-0.5 rounded-full ${c.defBg}`}>{q.type?.replace('_', ' ')}</span>
              {q.difficulty && <span className={`text-[9px] ${c.textMuted}`}>{DIFF_LABELS[q.difficulty] || q.difficulty}</span>}
            </div>
            <p className={`text-sm font-semibold ${c.text} mb-2`}>{q.question}</p>
            {q.options?.length > 0 && (
              <div className="space-y-1 mb-2">
                {q.options.map((opt, oi) => (
                  <p key={oi} className={`text-xs px-3 py-1.5 rounded-lg ${c.inset} ${c.text}`}>{opt}</p>
                ))}
              </div>
            )}
            <button onClick={() => setShowAnswers(prev => ({ ...prev, [q.number]: !prev[q.number] }))}
              className={`text-xs font-bold ${c.accentTxt} mt-1`}>
              {showAnswers[q.number] ? t('rec_hide_answer') : t('rec_show_answer')}
            </button>
            {showAnswers[q.number] && (
              <div className={`mt-2 p-3 rounded-lg border ${c.success}`}>
                <p className={`text-xs font-bold ${c.successTxt} mb-1`}>{t('rec_answer')}</p>
                <p className={`text-xs ${c.text} mb-1`}>{q.answer}</p>
                {q.why_wrong && Object.entries(q.why_wrong).map(([key, val]) => (
                  val ? <p key={key} className={`text-[10px] ${c.warnText}`}>❌ {key}: {val}</p> : null
                ))}
                {q.points_hint && <p className={`text-[10px] ${c.textMuted} mt-1 italic`}>{t('rec_grader_looks', { val: q.points_hint })}</p>}
              </div>
            )}
          </div>
        ))}

        {results.study_tips?.length > 0 && (
          <div className={`p-4 rounded-xl border ${c.cardAltBg}`}>
            <p className={`text-xs font-bold ${c.cardAltText} mb-2`}>{t('rec_study_tips')}</p>
            {results.study_tips.map((tip, i) => <p key={i} className={`text-xs ${c.cardAltText} mb-1`}>• {tip}</p>)}
          </div>
        )}
      </div>
    );
  };

  // ════════════════════════════════════════════════════════════
  // RESULTS — CONNECT
  // ════════════════════════════════════════════════════════════
  const renderConnect = () => {
    if (!results) return null;
    return (
      <div className="space-y-4">
        {results.course_narrative && (
          <div className={`p-5 rounded-2xl border-2 ${c.tipBg}`}>
            <p className={`text-xs font-bold ${c.tipText} uppercase mb-1`}>{t('rec_course_narrative')}</p>
            <p className={`text-sm font-bold ${c.text}`}>{results.course_narrative}</p>
          </div>
        )}

        {results.recurring_themes?.length > 0 && (
          <Section title={t('rec_recurring_themes')} emoji="🔄" sKey="themes" badge={String(results.recurring_themes.length)} defaultOpen>
            <div className="space-y-3 mt-3">
              {results.recurring_themes.map((theme, i) => (
                <div key={i} className={`p-3 rounded-lg border ${c.cardAlt}`}>
                  <p className={`text-xs font-bold ${c.text} mb-1`}>{theme.theme}</p>
                  {theme.appearances?.map((a, ai) => <p key={ai} className={`text-[10px] ${c.textSecondary} mb-0.5`}>• {a}</p>)}
                  <p className={`text-[10px] ${c.tipText} mt-1 italic`}>📋 {theme.why_recurring}</p>
                </div>
              ))}
            </div>
          </Section>
        )}

        {results.concept_chain?.length > 0 && (
          <Section title={t('rec_concept_chain')} emoji="⛓️" sKey="chain">
            <div className="space-y-2 mt-3">
              {results.concept_chain.map((ch, i) => (
                <div key={i} className={`p-3 rounded-lg ${c.inset}`}>
                  <p className={`text-xs font-bold ${c.text}`}>{ch.concept}</p>
                  <p className={`text-xs ${c.textSecondary} mt-0.5`}>{ch.progression}</p>
                </div>
              ))}
            </div>
          </Section>
        )}

        {results.cumulative_exam_focus?.length > 0 && (
          <div className={`p-4 rounded-xl border ${c.warning}`}>
            <p className={`text-xs font-bold ${c.warnText} mb-2`}>{t('rec_cumulative_exam')}</p>
            {results.cumulative_exam_focus.map((f, i) => <p key={i} className={`text-xs ${c.warnText} mb-1`}>• {f}</p>)}
          </div>
        )}

        {results.contradictions_or_nuance?.length > 0 && (
          <Section title={t('rec_evolving_ideas')} emoji="💭" sKey="nuance">
            <div className="space-y-2 mt-3">
              {results.contradictions_or_nuance.map((cn, i) => (
                <div key={i} className={`p-3 rounded-lg ${c.inset}`}>
                  <p className={`text-xs font-bold ${c.text}`}>{cn.topic}</p>
                  <p className={`text-xs ${c.textSecondary} mt-0.5`}>{cn.evolution}</p>
                </div>
              ))}
            </div>
          </Section>
        )}

        {results.gaps_between_lectures?.length > 0 && (
          <Section title={t('rec_gaps_between')} emoji="🔮" sKey="lgaps">
            <div className="space-y-1 mt-3">
              {results.gaps_between_lectures.map((g, i) => <p key={i} className={`text-xs ${c.textSecondary}`}>• {g}</p>)}
            </div>
          </Section>
        )}
      </div>
    );
  };

  // ════════════════════════════════════════════════════════════
  // HISTORY
  // ════════════════════════════════════════════════════════════
  const renderHistory = () => {
    if (sessionHistory.length === 0) return null;
    const modeEmoji = (m) => MODE_EMOJI[m] || '🧠';
    const formatDate = (iso) => {
      try {
        const d = new Date(iso); const diff = Math.floor((new Date() - d) / 86400000);
        return diff === 0 ? t('rec_today') : diff === 1 ? t('rec_yesterday') : diff < 7 ? t('rec_days_ago', { n: diff }) : d.toLocaleDateString(userLocale || 'en-US', { month: 'short', day: 'numeric' });
      } catch { return ''; }
    };
    return (
      <div className={`p-4 rounded-2xl border ${c.histBg}`}>
        <button onClick={() => setShowHistory(!showHistory)} className="w-full flex items-center gap-2 text-left">
          <span>🧠</span>
          <span className={`text-sm font-bold ${c.text} flex-1`}>{t('rec_past_sessions')}</span>
          <span className={`text-xs ${c.textMuted}`}>{sessionHistory.length}</span>
          <span className={`text-xs ${c.textMuted}`}>{showHistory ? '▲' : '▼'}</span>
        </button>
        {showHistory && (
          <div className="mt-3 space-y-2">
            {sessionHistory.map(entry => (
              <div key={entry.id} className={`rounded-xl border ${c.histCard} p-3 flex items-center gap-3`}>
                <span className="text-lg">{modeEmoji(entry.mode)}</span>
                <div className="flex-1 min-w-0">
                  <div className={`text-sm font-semibold ${c.text} truncate`}>{entry.title}</div>
                  <div className={`text-xs ${c.textMuted} mt-0.5`}>{formatDate(entry.date)}{entry.subject ? ` · ${entry.subject}` : ''}</div>
                </div>
              </div>
            ))}
          </div>
        )}
      </div>
    );
  };

  // ════════════════════════════════════════════════════════════
  // MAIN RENDER
  // ════════════════════════════════════════════════════════════
  return (
    <div className={`space-y-4 ${c.text}`}>

      {/* ── Input phase: unified card (header + input) ── */}
      {!results && (
        <div className={`${c.card} border ${c.border} rounded-xl shadow-sm p-5 space-y-4`}>
          <div className="pb-3 border-b border-zinc-500">
            <h2 className={`text-xl font-bold ${c.text}`}>
              <span className="mr-2">{tool?.icon ?? '🧠'}</span>{tool?.title ?? t('rec_title')}
            </h2>
            <p className={`text-sm ${c.textSecondary}`}>{tool?.tagline ?? t('rec_tagline')}</p>
            <button onClick={loadExample} disabled={loading} style={{ backgroundColor: (tool?.headerColor ?? '#888888') + '80' }} className={`mt-1.5 px-2.5 py-0.5 rounded-full text-xs font-medium border disabled:opacity-40 ${isDark ? 'text-white border-white/40' : 'text-gray-800 border-transparent'}`}>{t('try_example')}</button>
          </div>
          {renderInput()}
        </div>
      )}

      {/* ── Results phase: persistent header card with reset (ternary, not && — see PF-3 replace-mode note) ── */}
      {results ? (
        <div className={`${c.card} border ${c.border} rounded-xl shadow-sm p-5`}>
          <div className="pb-3 border-b border-zinc-500">
            <div className="flex items-start justify-between">
              <div>
                <h2 className={`text-xl font-bold ${c.text}`}>
                  <span className="mr-2">{tool?.icon ?? '🧠'}</span>{tool?.title ?? t('rec_title')}
                </h2>
                <p className={`text-sm ${c.textSecondary}`}>{tool?.tagline ?? t('rec_tagline')}</p>
              </div>
              <button onClick={handleReset} className={`${c.btnSecondary} px-3 py-1.5 rounded-lg text-xs font-bold flex-shrink-0`}>
                ↺ {t('rec_start_over')}
              </button>
            </div>
          </div>
        </div>
      ) : null}

      {/* ── Results body ── */}
      {results && (
        <div ref={resultsRef} className="space-y-4">
          {mode === 'distill'     && renderDistill()}
          {mode === 'study_guide' && renderStudyGuide()}
          {mode === 'test_prep'   && renderTestPrep()}
          {mode === 'connect'     && renderConnect()}

          {/* Edit Input — partial reset, returns to input form keeping content */}
          <button onClick={() => { setResults(null); setError(''); }}
            className={`w-full py-3 rounded-xl text-xs font-bold flex items-center justify-center gap-1.5 ${c.btnSecondary}`}>
            <span>✏️</span> {t('rec_edit_input')}
          </button>

          {/* Post-result cross-refs */}
          <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
            <p className={`text-xs font-semibold uppercase tracking-wide mb-3 ${c.textMuted}`}>🔗 {t('rec_related')}</p>
            <div className="flex flex-wrap gap-3">
              <a href="/BrainDumpBuddy" className={`text-xs ${linkStyle}`}>🧠 {t('rec_braindump')}</a>
              <a href="/TheGap"          className={`text-xs ${linkStyle}`}>🔍 {t('rec_thegap')}</a>
            </div>
          </div>
        </div>
      )}

      {/* ── Error ── */}
      {error && (
        <div className={`p-4 ${c.danger} border rounded-xl flex items-start gap-3`}>
          <span>⚠️</span>
          <p className="text-sm">{error}</p>
        </div>
      )}

      {renderHistory()}
    </div>
  );
};

Recall.displayName = 'Recall';
export default Recall;
