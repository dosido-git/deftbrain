function Section({ icon, title, badge, badgeClass, children, defaultOpen = false, c }) {
  const [open, toggleOpen] = useReducer(o => !o, defaultOpen);
  return (
    <div className={`${c.card} border ${c.border} rounded-xl overflow-hidden`}>
      <button
        onClick={toggleOpen}
        className="w-full p-4 flex items-center justify-between text-left min-h-[44px]"
      >
        <div className="flex items-center gap-2.5">
          {icon && <span className="text-sm">{icon}</span>}
          <h3 className={`text-sm font-bold ${c.text}`}>{title}</h3>
          {badge && (
            <span className={`text-[9px] font-black px-2 py-0.5 rounded ${badgeClass || c.highlightBg}`}>
              {badge}
            </span>
          )}
        </div>
        <span className={`text-xs ${c.textMuteded} print:hidden`}>{open ? '▲' : '▼'}</span>
      </button>
      {/* Body stays mounted and reveals on print (`hidden print:block`) so a
          printed/exported page shows every section expanded, even ones the
          user left collapsed on screen. Same pattern as RentersDepositSaver. */}
      <div className={`px-4 pb-4 border-t ${c.border} pt-3 space-y-3 ${open ? '' : 'hidden print:block'}`}>
        {children}
      </div>
    </div>
  );
}

