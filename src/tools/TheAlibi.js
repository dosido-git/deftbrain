import React, { useState, useRef, useCallback, useEffect } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { usePersistentState } from '../hooks/usePersistentState';
import { useRegisterActions } from '../components/ActionBarContext';
import { useTranslation } from '../i18n/useTranslation';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

// ════════════════════════════════════════════════════════════
// MAIN COMPONENT
// ════════════════════════════════════════════════════════════
const TheAlibi = ({ tool }) => {
  const { callToolEndpoint, loading, userLocale, userCurrency, userRegion } = useClaudeAPI();
  const { isDark } = useTheme();
  const { t } = useTranslation();
  const resultsRef = useRef(null);

  // ── Localized config (depends on t) ──
  const AUDIENCES = [
    { value: 'interviewer', label: '💼 ' + t('ta_aud_interviewer') },
    { value: 'landlord', label: '🏠 ' + t('ta_aud_landlord') },
    { value: 'date', label: '💕 ' + t('ta_aud_date') },
    { value: 'in-laws', label: '👨‍👩‍👧 ' + t('ta_aud_inlaws') },
    { value: 'lender', label: '🏦 ' + t('ta_aud_lender') },
    { value: 'coworker', label: '🤝 ' + t('ta_aud_coworker') },
    { value: 'friend', label: '👋 ' + t('ta_aud_friend') },
    { value: 'networking', label: '📇 ' + t('ta_aud_networking') },
    { value: 'custom', label: '✏️ ' + t('ta_aud_custom') },
  ];

  const TONES = [
    { value: 'professional', label: '💼 ' + t('ta_tone_professional') },
    { value: 'casual', label: '😊 ' + t('ta_tone_casual') },
    { value: 'warm', label: '🤗 ' + t('ta_tone_warm') },
    { value: 'confident', label: '💪 ' + t('ta_tone_confident') },
  ];

  const EXAMPLES = [
    { label: t('ta_ex1_label'), situation: t('ta_ex1_situation'), audience: 'interviewer' },
    { label: t('ta_ex2_label'), situation: t('ta_ex2_situation'), audience: 'interviewer' },
    { label: t('ta_ex3_label'), situation: t('ta_ex3_situation'), audience: 'interviewer' },
    { label: t('ta_ex4_label'), situation: t('ta_ex4_situation'), audience: 'landlord' },
  ];

  const c = {
    card:         isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:      isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:        isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20' : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:         isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:    isDark ? 'text-zinc-400' : 'text-gray-500',
    required:      isDark ? 'text-amber-400' : 'text-amber-700',
    btnPrimary:   isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white' : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary: isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-100' : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    btnDis:       isDark ? 'bg-zinc-700 text-zinc-500 cursor-not-allowed' : 'bg-gray-200 text-gray-400 cursor-not-allowed',
    border:       isDark ? 'border-zinc-700' : 'border-gray-200',
    success:      isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200' : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:      isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200' : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:       isDark ? 'bg-red-900/20 border-red-700 text-red-200' : 'bg-red-50 border-red-200 text-red-800',
    pillActive:   isDark ? 'border-cyan-500 bg-cyan-900/30 text-cyan-200' : 'border-cyan-600 bg-cyan-100 text-cyan-900',
    pillInactive: isDark ? 'border-zinc-600 text-zinc-400 hover:border-zinc-500' : 'border-gray-300 text-gray-500 hover:border-gray-400',
    badge:        isDark ? 'bg-cyan-900/30 text-cyan-300' : 'bg-cyan-100 text-cyan-800',
    tipBg:        isDark ? 'bg-amber-900/20 border-amber-700' : 'bg-amber-50 border-amber-300',
    tipText:      isDark ? 'text-amber-300' : 'text-amber-800',
    histBg:       isDark ? 'bg-sky-900/20 border-sky-700/30' : 'bg-sky-50 border-sky-200',
    inset:        isDark ? 'bg-zinc-700/40' : 'bg-slate-100',
    safeBg:       isDark ? 'bg-emerald-900/20 border-emerald-700' : 'bg-emerald-50 border-emerald-300',
    boldBg:       isDark ? 'bg-amber-900/20 border-amber-700' : 'bg-amber-50 border-amber-300',
    midBg:        isDark ? 'bg-sky-900/20 border-sky-700/30' : 'bg-sky-50 border-sky-200',
    labelText:    isDark ? 'text-zinc-200' : 'text-gray-700',
  };
  c.label = c.labelText;
  c.textMuteded = c.textMuted;

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-700 hover:text-cyan-800 underline underline-offset-2';

  const [showHistory, setShowHistory] = useState(false);

  // Input
  const [situation, setSituation] = useState('');
  const [audience, setAudience] = useState('interviewer');
  const [customAudience, setCustomAudience] = useState('');
  const [tone, setTone] = useState('professional');
  const [concerns, setConcerns] = useState('');
  const [context, setContext] = useState('');
  const [error, setError] = useState('');
  const [expandedVersion, setExpandedVersion] = useState(0);
  const [showFollowups, setShowFollowups] = useState(true);
  const [showMistakes, setShowMistakes] = useState(false);
  const [sessionHistory, setSessionHistory] = usePersistentState('the-alibi-history', []);

  // Results
  const [results, setResults] = usePersistentState('the-alibi-results', null);

  useEffect(() => {
    if (!results) return;
    const t = setTimeout(() => resultsRef.current?.scrollIntoView({ behavior: 'smooth', block: 'start' }), 200);
    return () => clearTimeout(t);
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [results]);

  const frame = useCallback(async () => {
    if (!situation.trim()) { setError(t('ta_err_empty')); return; }
    setError(''); setResults(null);
    try {
      const data = await callToolEndpoint('the-alibi', {
        situation: situation.trim(), audience,
        customAudience: audience === 'custom' ? customAudience.trim() : null,
        tone, concerns: concerns.trim() || null, context: context.trim() || null,
        userLocale, userCurrency, userRegion,
      });
      setResults(data);
      const label = AUDIENCES.find(a => a.value === audience)?.label || audience;
      const entry = { id: 'ali_' + Date.now(), date: new Date().toISOString(), audience: label, situation: situation.trim().substring(0, 60) + '...', preview: situation.trim().slice(0, 40), results: data };
      setSessionHistory(prev => [entry, ...prev].slice(0, 6));
    } catch (err) { setError(err.message || t('ta_err_failed')); }
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [situation, audience, customAudience, tone, concerns, context, callToolEndpoint, setSessionHistory, userLocale, userCurrency, userRegion, t]);

  const handleReset = useCallback(() => {
    setSituation(''); setConcerns(''); setContext(''); setCustomAudience('');
    setResults(null); setError('');
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, []);

  const loadExample = useCallback((ex) => {
    setSituation(ex.situation); setAudience(ex.audience); setResults(null); setError('');
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, []);

  const buildCopy = useCallback(() => {
    if (!results) return '';
    const lines = ['🎭 ' + t('ta_copy_header'), ''];
    if (results?.reframe) lines.push(t('ta_copy_insight') + ' ' + results?.reframe, '');
    results?.versions?.forEach((v, i) => {
      lines.push(t('ta_copy_version') + ' ' + (i + 1) + ': ' + v.label);
      lines.push(v.script); lines.push('');
    });
    if (results?.follow_up_prep?.length) {
      lines.push(t('ta_copy_followup'));
      results?.follow_up_prep?.forEach(f => lines.push('Q: ' + f.question, 'A: ' + f.answer, ''));
    }
    lines.push(BRAND);
    return lines.join('\n');
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [results, t]);

  // ─── Register export content ───
  useRegisterActions(results ? buildCopy() : '', tool?.title);

  // ─── Keyboard shortcut (after frame declaration — TDZ-safe) ───
  useEffect(() => {
    const handler = (e) => {
      const tag = document.activeElement?.tagName;
      if (tag === 'SELECT') return;
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && !loading) frame();
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [loading, frame]);

  const versionBg = (idx) => idx === 0 ? c.safeBg : idx === 1 ? c.midBg : c.boldBg;

  // ══════════════════════════════════════════
  // RENDER HELPERS
  // ══════════════════════════════════════════
  const Section = ({ title, emoji, open, onToggle, badge, children }) => (
    <div className={c.card + ' border rounded-xl overflow-hidden'}>
      <button onClick={onToggle} className="w-full flex items-center justify-between p-5 text-left hover:opacity-80">
        <div className="flex items-center gap-3">
          <span className="text-lg">{emoji}</span>
          <span className={'text-base font-semibold ' + c.text}>{title}</span>
          {badge && <span className={'text-xs px-2 py-0.5 rounded-full ' + c.badge}>{badge}</span>}
        </div>
        <span className={'text-xs ' + c.textMuted}>{open ? '▲' : '▼'}</span>
      </button>
      {open && <div className={'px-5 pb-5 border-t ' + c.border}>{children}</div>}
    </div>
  );

  const Pill = ({ active, onClick, children }) => (
    <button onClick={onClick}
      className={'px-3 py-1.5 rounded-lg border text-xs font-semibold transition-all ' + (active ? c.pillActive : c.pillInactive)}>
      {active && <span className="mr-1">✓</span>}{children}
    </button>
  );

  // ══════════════════════════════════════════
  // INPUT
  // ══════════════════════════════════════════
  const renderInput = () => (
    <div className="space-y-4">
      <div className={c.card + ' border rounded-xl p-5'}>
        <label className={'text-base font-bold ' + c.text + ' mb-1 block'}>🎭 {t('ta_story_label')} <span className={c.required}>*</span></label>
        <p className={'text-sm ' + c.textMuted + ' mb-4'}>{t('ta_story_help')}</p>
        <textarea value={situation} onChange={e => setSituation(e.target.value)}
          placeholder={t('ta_story_ph')}
          className={'w-full h-32 p-4 border-2 rounded-xl ' + c.input + ' outline-none focus:ring-2 resize-none text-sm'} />
      </div>

      <div className={c.card + ' border rounded-xl p-5 space-y-4'}>
        <div>
          <label className={'text-xs font-bold ' + c.textSecondary + ' uppercase tracking-wide mb-2 block'}>👥 {t('ta_audience_label')}</label>
          <div className="flex flex-wrap gap-1.5">
            {AUDIENCES.map(a => <Pill key={a.value} active={audience === a.value} onClick={() => setAudience(a.value)}>{a.label}</Pill>)}
          </div>
          {audience === 'custom' && (
            <input type="text" value={customAudience} onChange={e => setCustomAudience(e.target.value)}
              placeholder={t('ta_audience_ph')} className={'w-full mt-2 px-4 py-2.5 rounded-xl border text-sm ' + c.input + ' outline-none'} />
          )}
        </div>
        <div>
          <label className={'text-xs font-bold ' + c.textSecondary + ' uppercase tracking-wide mb-2 block'}>🎯 {t('ta_tone_label')}</label>
          <div className="flex flex-wrap gap-1.5">
            {TONES.map(toneOpt => <Pill key={toneOpt.value} active={tone === toneOpt.value} onClick={() => setTone(toneOpt.value)}>{toneOpt.label}</Pill>)}
          </div>
        </div>
        <div>
          <label className={'text-xs font-bold ' + c.textSecondary + ' uppercase tracking-wide mb-1 block'}>😰 {t('ta_concerns_label')}</label>
          <input type="text" value={concerns} onChange={e => setConcerns(e.target.value)}
            placeholder={t('ta_concerns_ph')}
            className={'w-full px-4 py-2.5 rounded-xl border text-sm ' + c.input + ' outline-none'} />
        </div>
        <div>
          <label className={'text-xs font-bold ' + c.textSecondary + ' uppercase tracking-wide mb-1 block'}>📝 {t('ta_context_label')}</label>
          <input type="text" value={context} onChange={e => setContext(e.target.value)}
            placeholder={t('ta_context_ph')}
            className={'w-full px-4 py-2.5 rounded-xl border text-sm ' + c.input + ' outline-none'} />
        </div>
      </div>

      {/* Examples */}
      <div className={'p-4 rounded-xl ' + c.inset}>
        <p className={'text-xs font-bold ' + c.textMuted + ' mb-2'}>💡 {t('ta_try_example')}</p>
        <div className="flex flex-wrap gap-1.5">
          {EXAMPLES.map((ex, i) => (
            <button key={i} onClick={() => loadExample(ex)} className={'px-3 py-1.5 rounded-lg border text-xs font-semibold ' + c.pillInactive}>{ex.label}</button>
          ))}
        </div>
      </div>

      <button onClick={frame} disabled={loading || !situation.trim()}
        className={'w-full py-4 rounded-2xl text-sm font-bold flex items-center justify-center gap-2 transition-all disabled:opacity-40 ' + (loading || !situation.trim() ? c.btnDis : c.btnPrimary)}>
        {loading ? <><span className="inline-block animate-spin">{tool?.icon ?? '🎭'}</span> {t('ta_framing')}</> : <><span className="mr-1">{tool?.icon ?? '🎭'}</span> {t('ta_frame')}</>}
      </button>
    </div>
  );

  // ══════════════════════════════════════════
  // RESULTS
  // ══════════════════════════════════════════
  const renderResults = () => {
    if (!results) return null;
    return (
      <div ref={resultsRef} className="space-y-4 mt-4">
        {/* Situation read */}
        {results?.situation_read && (
          <div className={'p-5 rounded-2xl border-2 ' + c.tipBg}>
            <p className={'text-sm ' + c.tipText}>{results?.situation_read}</p>
          </div>
        )}

        {/* Reframe */}
        {results?.reframe && (
          <div className={c.card + ' border rounded-xl p-5'}>
            <p className={'text-xs font-bold ' + c.textMuted + ' uppercase mb-2'}>💡 {t('ta_reframe_label')}</p>
            <p className={'text-base font-bold ' + c.text}>{results?.reframe}</p>
          </div>
        )}

        {/* Versions */}
        {results?.versions?.map((v, idx) => (
          <div key={idx} className={'p-5 rounded-xl border-2 ' + versionBg(idx)}>
            <button onClick={() => setExpandedVersion(expandedVersion === idx ? -1 : idx)} className="w-full text-left">
              <div className="flex items-center justify-between mb-2">
                <div className="flex items-center gap-2">
                  <span className={'text-xs font-bold px-2 py-0.5 rounded-full ' + c.badge}>{t('ta_version')} {idx + 1}</span>
                  <span className={'text-sm font-bold ' + c.text}>{v.label}</span>
                </div>
                <span className={'text-xs ' + c.textMuted}>{expandedVersion === idx ? '▲' : '▼'}</span>
              </div>
              <p className={'text-xs ' + c.textSecondary + ' italic'}>{v.strategy}</p>
            </button>

            {expandedVersion === idx && (
              <div className="mt-4 space-y-3">
                <div className={'p-4 rounded-xl ' + c.inset}>
                  <p className={'text-xs font-bold ' + c.textMuted + ' uppercase mb-2'}>📝 {t('ta_what_to_say')}</p>
                  <p className={'text-sm leading-relaxed ' + c.text + ' italic'}>"{v.script}"</p>
                </div>
                <div className="grid grid-cols-2 gap-2">
                  <div className={'p-3 rounded-lg border ' + c.success}>
                    <p className={'text-[10px] font-bold uppercase mb-1'}>{t('ta_best_for')}</p>
                    <p className='text-xs'>{v.when_to_use}</p>
                  </div>
                  <div className={'p-3 rounded-lg border ' + c.danger}>
                    <p className={'text-[10px] font-bold uppercase mb-1'}>{t('ta_risk')}</p>
                    <p className='text-xs'>{v.risk}</p>
                  </div>
                </div>
              </div>
            )}
          </div>
        ))}

        {/* Follow-up prep */}
        {results?.follow_up_prep?.length > 0 && (
          <Section title={t('ta_followups_title')} emoji="❓" open={showFollowups} onToggle={() => setShowFollowups(!showFollowups)} badge={results?.follow_up_prep?.length + ''}>
            <div className="space-y-3 mt-4">
              {results?.follow_up_prep?.map((f, idx) => (
                <div key={idx} className={'p-4 rounded-xl border ' + c.cardAlt}>
                  <p className={'text-xs font-bold ' + c.textMuted + ' mb-1'}>Q: "{f.question}"</p>
                  <p className={'text-sm ' + c.text + ' mb-2 italic'}>"{f.answer}"</p>
                  {f.trap_to_avoid && (
                    <div className={'p-2 rounded-lg border ' + c.danger}>
                      <p className='text-[10px] font-bold'>🚩 {t('ta_trap_to_avoid')} {f.trap_to_avoid}</p>
                    </div>
                  )}
                </div>
              ))}
            </div>
          </Section>
        )}

        {/* Body language */}
        {results?.body_language && (
          <div className={c.card + ' border rounded-xl p-5'}>
            <p className={'text-xs font-bold ' + c.textMuted + ' uppercase mb-2'}>🎭 {t('ta_delivery_title')}</p>
            <p className={'text-sm ' + c.text}>{results?.body_language}</p>
          </div>
        )}

        {/* Common mistakes */}
        {results?.common_mistakes?.length > 0 && (
          <Section title={t('ta_mistakes_title')} emoji="🚫" open={showMistakes} onToggle={() => setShowMistakes(!showMistakes)}>
            <div className="space-y-2 mt-4">
              {results?.common_mistakes?.map((m, idx) => (
                <div key={idx} className={'p-3 rounded-lg border ' + c.danger}>
                  <p className='text-xs'>{m}</p>
                </div>
              ))}
            </div>
          </Section>
        )}

        {/* Confidence note */}
        {results?.confidence_note && (
          <div className={'p-4 rounded-xl border ' + c.success}>
            <p className='text-xs font-bold mb-1'>💚 {t('ta_perspective_title')}</p>
            <p className='text-sm'>{results?.confidence_note}</p>
          </div>
        )}

        {/* Nuclear option */}
        {results?.nuclear_option && (
          <div className={'p-4 rounded-xl ' + c.inset}>
            <p className={'text-xs font-bold ' + c.textMuted + ' mb-1'}>🚪 {t('ta_nuclear_title')}</p>
            <p className={'text-sm ' + c.text + ' italic'}>"{results?.nuclear_option}"</p>
          </div>
        )}

        <p className={"text-xs text-center " + c.textMuted}>{t('ta_disclaimer')}</p>

        {/* Cross-refs */}
        <div className={'p-4 rounded-2xl border ' + c.card}>
          <p className={'text-xs font-bold ' + c.textMuted + ' uppercase tracking-wide mb-2'}>🔗 {t('ta_related')}</p>
          <div className={'space-y-1.5 text-xs ' + c.textSecondary}>
            <p>{t('ta_xref_velvet_intro')} <a href="/VelvetHammer" className={linkStyle}>{t('ta_xref_velvet')}</a> {t('ta_xref_velvet_after')}</p>
            <p>{t('ta_xref_ghost_intro')} <a href="/GhostWriter" className={linkStyle}>{t('ta_xref_ghost')}</a> {t('ta_xref_ghost_after')}</p>
          </div>
        </div>
      </div>
    );
  };

  // ══════════════════════════════════════════
  // HISTORY
  // ══════════════════════════════════════════
  const renderHistory = () => {
    if (sessionHistory.length === 0) return null;
    const formatDate = (iso) => { try { const d = new Date(iso); const diff = Math.floor((new Date() - d) / 86400000); return diff === 0 ? t('ta_today') : diff === 1 ? t('ta_yesterday') : diff < 7 ? t('ta_days_ago', { count: diff }) : d.toLocaleDateString(userLocale || 'en-US', { month: 'short', day: 'numeric' }); } catch { return ''; } };
    return (
      <div className={'mt-6 p-4 rounded-2xl border ' + c.histBg}>
        <button onClick={() => setShowHistory(!showHistory)} className="w-full flex items-center gap-2 text-left">
          <span>🎭</span>
          <span className={'text-sm font-bold ' + c.text + ' flex-1'}>{t('ta_history_title')}</span>
          <span className={'text-xs ' + c.textMuted}>{sessionHistory.length}</span>
          <span className={'text-xs ' + c.textMuted}>{showHistory ? '▲' : '▼'}</span>
        </button>
        {showHistory && (
          <div className="mt-3 space-y-2">
            {sessionHistory.map(entry => (
              <div key={entry.id} className={'rounded-xl border ' + c.card + ' p-3 flex items-center gap-3'}>
                <div className="flex-1 min-w-0">
                  <div className={'text-sm font-semibold ' + c.text + ' truncate'}>{entry.situation}</div>
                  <div className={'text-xs ' + c.textMuted + ' mt-0.5'}>{formatDate(entry.date)} · {entry.audience}</div>
                </div>
                <button onClick={() => { setResults(entry.results); setShowHistory(false); }}
                  className={'px-3 py-1.5 rounded-lg text-xs font-bold ' + c.btnSecondary}>{t('ta_view')}</button>
              </div>
            ))}
          </div>
        )}
      </div>
    );
  };

  return (
    <div>
      <div className="flex items-start justify-between gap-3 mb-5">
        <div>
          <h2 className={'text-2xl font-bold ' + c.text}>
            <span className="mr-2">{tool?.icon ?? '🎭'}</span>{tool?.title ?? t('ta_title')}
          </h2>
          <p className={'text-sm ' + c.textMuted}>{tool?.tagline ?? t('ta_tagline')}</p>
        </div>
        {(results || situation.trim()) && (
          <button onClick={handleReset} className={'flex-shrink-0 ' + c.btnSecondary + ' px-3 py-1.5 rounded-lg text-xs font-medium'}>
            ↺ {t('start_over')}
          </button>
        )}
      </div>
      {!results && (
        <p className={'text-xs ' + c.textMuted + ' mb-3'}>
          {t('ta_xref_intro')} <a href="/RoomReader" className={linkStyle}>🎭 {t('ta_xref_roomreader')}</a> {t('ta_xref_roomreader_after')}
        </p>
      )}
      {!results && renderInput()}
      {renderResults()}
      {error && (
        <div className={'mt-4 p-4 border rounded-xl flex items-start gap-3 ' + c.danger}>
          <span className='text-base'>⚠️</span>
          <p className='text-sm'>{error}</p>
        </div>
      )}
      {renderHistory()}
    </div>
  );
};

