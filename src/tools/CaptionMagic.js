// ════════════════════════════════════════════════════════════
// MAIN COMPONENT
// ════════════════════════════════════════════════════════════
const CaptionMagic = ({ tool }) => {
  const { callToolEndpoint, loading } = useClaudeAPI();
  const { isDark } = useTheme();

  const c = {
    card:           isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:        isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:          isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20' : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:           isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary:  isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:      isDark ? 'text-zinc-500' : 'text-gray-400',
    labelText:      isDark ? 'text-zinc-200' : 'text-gray-700',
    required:       isDark ? 'text-amber-400' : 'text-amber-500',
    badgeNeutral:   isDark ? 'bg-zinc-700 text-zinc-300' : 'bg-gray-100 text-gray-600',
    accentTxt:      isDark ? 'text-cyan-400' : 'text-cyan-600',
    btnPrimary:     isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white' : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:   isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200' : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:         isDark ? 'border-zinc-700' : 'border-gray-200',
    success:        isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200' : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:        isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200' : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:         isDark ? 'bg-red-900/20 border-red-700 text-red-200' : 'bg-red-50 border-red-200 text-red-800',
    infoBox:        isDark ? 'bg-sky-900/20 border-sky-700 text-sky-200' : 'bg-sky-50 border-sky-200 text-sky-800',
    successBox:     isDark ? 'bg-emerald-900/20 border-emerald-700' : 'bg-emerald-50 border-emerald-300',
    successTxt:     isDark ? 'text-emerald-300' : 'text-emerald-800',
    warningBox:     isDark ? 'bg-amber-900/20 border-amber-700' : 'bg-amber-50 border-amber-300',
    warningTxt:     isDark ? 'text-amber-300' : 'text-amber-800',
    pillActive:     isDark ? 'border-cyan-500 bg-cyan-900/30 text-cyan-200' : 'border-cyan-600 bg-cyan-100 text-cyan-900',
    pillInactive:   isDark ? 'border-zinc-600 text-zinc-400 hover:border-zinc-500' : 'border-gray-300 text-gray-500 hover:border-gray-400',
    // Semantic colour tokens
    textDanger:     isDark ? 'text-red-300' : 'text-red-700',
    textOk:         isDark ? 'text-emerald-400' : 'text-emerald-700',
    textCaution:    isDark ? 'text-amber-300' : 'text-amber-700',
    textCyan:       isDark ? 'text-cyan-400' : 'text-cyan-600',
    textHighlight:  isDark ? 'text-zinc-100' : 'text-gray-800',
    textGhostDel:   isDark ? 'text-red-400 hover:text-red-300' : 'text-red-500 hover:text-red-600',
    // Upload / drag
    dropzone:       isDark ? 'border-zinc-600 bg-zinc-900/30' : 'border-gray-300 bg-slate-50',
    dropActive:     isDark ? 'border-cyan-500 bg-cyan-900/20' : 'border-cyan-400 bg-cyan-50',
    // Buttons
    btnDelete:      isDark ? 'bg-red-900/40 text-red-300 hover:bg-red-900/60' : 'bg-red-100 text-red-600 hover:bg-red-200',
    stateDisabled:  isDark ? 'bg-zinc-700 text-zinc-500 cursor-not-allowed' : 'bg-gray-100 text-gray-400 cursor-not-allowed',
    // Content surfaces
    captionBg:      isDark ? 'bg-zinc-900/60 border-zinc-700' : 'bg-slate-50 border-gray-200',
    altSection:     isDark ? 'bg-sky-900/20 border-sky-700' : 'bg-sky-50 border-sky-200',
    tonePill:       isDark ? 'bg-cyan-900/30 text-cyan-300' : 'bg-cyan-100 text-cyan-800',
    okBg:           isDark ? 'bg-emerald-900/20 border-emerald-700' : 'bg-emerald-50 border-emerald-300',
    // Heat / schedule badges
    heatHigh:       isDark ? 'bg-emerald-900/40' : 'bg-emerald-100',
    heatMed:        isDark ? 'bg-amber-900/30' : 'bg-amber-100',
    // Hashtag category pills
    htTrending:     isDark ? 'bg-red-900/30 border-red-700 text-red-300' : 'bg-red-50 border-red-200 text-red-700',
    htNiche:        isDark ? 'bg-cyan-900/30 border-cyan-700 text-cyan-300' : 'bg-cyan-50 border-cyan-200 text-cyan-700',
    htBranded:      isDark ? 'bg-fuchsia-900/30 border-fuchsia-700 text-fuchsia-300' : 'bg-fuchsia-50 border-fuchsia-200 text-fuchsia-700',
    // History
    histBg:         isDark ? 'bg-zinc-700/30 border-zinc-600' : 'bg-slate-50 border-gray-200',
    histCard:       isDark ? 'border-zinc-600' : 'border-gray-200',
  };
  c.textMuteded = c.textMuted;
  c.label = c.labelText;

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';
  // ── Persistent ──
  const [history, setHistory] = usePersistentState('caption-magic-history', []);
  const [brandProfile, setBrandProfile] = usePersistentState('caption-magic-brand', { generations: 0, toneFreq: {}, lengthFreq: {}, platformFreq: {} });
  const [abResults, setAbResults] = usePersistentState('caption-magic-ab', []);
  const [platform, setPlatform] = usePersistentState('caption-magic-platform', 'instagram');
  const [selectedTones, setSelectedTones] = usePersistentState('caption-magic-tones', ['casual']);
  const [captionLength, setCaptionLength] = usePersistentState('caption-magic-length', 'medium');

  // ── Transient state ──
  const [showHistory, setShowHistory] = useState(false);
  const [imagePreview, setImagePreview] = useState(null);
  const [imageBase64, setImageBase64] = useState(null);
  const [imageDescription, setImageDescription] = useState('');
  const [context, setContext] = useState('');
  const [useBrandVoice, setUseBrandVoice] = useState(false);
  const [compressing, setCompressing] = useState(false);
  const [isDragging, setIsDragging] = useState(false);
  const [results, setResults] = useState(null);
  const [error, setError] = useState('');
  const [revisingIndex, setRevisingIndex] = useState(null);
  const [adaptResults, setAdaptResults] = useState({});
  const [adapting, setAdapting] = useState(null);
  const [remixSelections, setRemixSelections] = useState([]);
  const [remixInstructions, setRemixInstructions] = useState('');
  const [remixResult, setRemixResult] = useState(null);
  const [remixing, setRemixing] = useState(false);
  const [showRemix, setShowRemix] = useState(false);
  const [showAbPanel, setShowAbPanel] = useState(false);
  const [winnerIndex, setWinnerIndex] = useState(null);

  // ── Refs ──
  const fileInputRef    = useRef(null);
  const resultsRef      = useRef(null);
  const handleSubmitRef = useRef(null);
  const canSubmitRef    = useRef(false);

  // ══════════════════════════════════════════
  // BRAND VOICE MEMORY (#1)
  // ══════════════════════════════════════════
  const brandReady = brandProfile.generations >= 3;

  const brandVoiceSummary = useMemo(() => {
    if (!brandReady) return '';
    const topTones = Object.entries(brandProfile.toneFreq || {})
      .sort(([,a], [,b]) => b - a).slice(0, 3).map(([t]) => t);
    const topLength = Object.entries(brandProfile.lengthFreq || {})
      .sort(([,a], [,b]) => b - a)[0]?.[0] || 'medium';
    return `Preferred tones: ${topTones.join(', ')}. Preferred length: ${topLength}.`;
  }, [brandProfile, brandReady]);

  const updateBrandProfile = useCallback((tones, length, plat) => {
    setBrandProfile(prev => {
      const next = { ...prev, generations: (prev.generations || 0) + 1 };
      const tf = { ...(prev.toneFreq || {}) };
      tones.forEach(t => { tf[t] = (tf[t] || 0) + 1; });
      next.toneFreq = tf;
      const lf = { ...(prev.lengthFreq || {}) };
      lf[length] = (lf[length] || 0) + 1;
      next.lengthFreq = lf;
      const pf = { ...(prev.platformFreq || {}) };
      pf[plat] = (pf[plat] || 0) + 1;
      next.platformFreq = pf;
      return next;
    });
  }, [setBrandProfile]);

  // ══════════════════════════════════════════
  // A/B TESTING INSIGHTS (#3)
  // ══════════════════════════════════════════
  const abInsights = useMemo(() => {
    if (abResults.length < 3) return null;
    const toneWins = {};
    const lengthWins = {};
    abResults.forEach(r => {
      if (r.winnerTone) toneWins[r.winnerTone] = (toneWins[r.winnerTone] || 0) + 1;
      if (r.winnerLength) lengthWins[r.winnerLength] = (lengthWins[r.winnerLength] || 0) + 1;
    });
    const topTone = Object.entries(toneWins).sort(([,a],[,b]) => b - a)[0];
    const topLength = Object.entries(lengthWins).sort(([,a],[,b]) => b - a)[0];
    return {
      topTone: topTone ? topTone[0] : null,
      topLength: topLength ? topLength[0] : null,
      totalTests: abResults.length,
    };
  }, [abResults]);

  const markAbWinner = useCallback((captionIndex) => {
    if (!results?.captions?.[captionIndex]) return;
    const cap = results.captions[captionIndex];
    setAbResults(prev => [...prev, {
      date: new Date().toISOString(),
      winnerTone: cap.tone,
      winnerLength: captionLength,
      platform,
    }].slice(-50));
    setWinnerIndex(captionIndex);
    setTimeout(() => setWinnerIndex(null), 2000);
  }, [results, captionLength, platform, setAbResults]);

  // ══════════════════════════════════════════
  // IMAGE HANDLING
  // ══════════════════════════════════════════
  const processFile = useCallback(async (file) => {
    if (!file || !file.type.startsWith('image/')) {
      setError('Please upload an image file');
      return;
    }
    setError(''); setCompressing(true);
    try {
      const compressed = await compressImage(file, CompressionPresets.API_UPLOAD);
      setImagePreview(compressed);
      setImageBase64(compressed);
    } catch (err) {
      setError(err.message || 'Failed to process image.');
    } finally {
      setCompressing(false);
    }
  }, []);

  const handleImageUpload = useCallback((e) => {
    const file = e.target.files[0];
    if (file) processFile(file);
  }, [processFile]);

  const handlePaste = useCallback(async () => {
    try {
      const items = await navigator.clipboard.read();
      for (const item of items) {
        for (const type of item.types) {
          if (type.startsWith('image/')) {
            const blob = await item.getType(type);
            await processFile(new File([blob], 'pasted.png', { type: blob.type }));
            return;
          }
        }
      }
      setError('No image found in clipboard');
    } catch { setError('Failed to paste. Try uploading instead.'); }
  }, [processFile]);

  const handleDragOver = useCallback((e) => { e.preventDefault(); setIsDragging(true); }, []);
  const handleDragLeave = useCallback(() => setIsDragging(false), []);
  const handleDrop = useCallback((e) => {
    e.preventDefault(); setIsDragging(false);
    const file = e.dataTransfer.files[0];
    if (file) processFile(file);
  }, [processFile]);

  const clearImage = useCallback(() => {
    setImagePreview(null); setImageBase64(null);
    if (fileInputRef.current) fileInputRef.current.value = '';
  }, []);

  // ══════════════════════════════════════════
  // TOGGLES
  // ══════════════════════════════════════════
  const toggleTone = useCallback((val) => {
    setSelectedTones(prev => {
      if (prev.includes(val)) return prev.filter(t => t !== val);
      if (prev.length >= 3) return prev;
      return [...prev, val];
    });
  }, []);

  // ══════════════════════════════════════════
  // API: Generate
  // ══════════════════════════════════════════
  const generate = useCallback(async () => {
    if (!imageBase64 && !imageDescription.trim()) { setError('Upload an image or describe it'); return; }
    if (selectedTones.length === 0) { setError('Select at least one tone'); return; }
    setError(''); setResults(null); setAdaptResults({}); setRemixResult(null); setShowRemix(false);
    try {
      const data = await callToolEndpoint('caption-magic', {
        imageBase64,
        imageDescription: imageDescription.trim() || null,
        platform,
        tones: selectedTones,
        captionLength,
        context: context.trim() || null,
        brandVoice: useBrandVoice && brandReady ? brandVoiceSummary : null,
      });
      setResults(data);
      updateBrandProfile(selectedTones, captionLength, platform);
      saveToHistory(data);
    } catch (err) {
      setError(err.message || 'Failed to generate captions.');
    }
  }, [imageBase64, imageDescription, platform, selectedTones, captionLength, context, useBrandVoice, brandReady, brandVoiceSummary, callToolEndpoint, updateBrandProfile]);

  // API: Revise
  const reviseCaption = useCallback(async (text, index, direction) => {
    setRevisingIndex(index);
    try {
      const data = await callToolEndpoint('caption-magic/revise', { captionText: text, direction, platform });
      if (data.revised_text) {
        setResults(prev => ({
          ...prev,
          captions: prev.captions.map((cap, i) =>
            i === index ? { ...cap, text: data.revised_text, char_count: data.char_count || data.revised_text.length, revision_note: data.what_changed } : cap
          ),
        }));
      }
    } catch (err) { console.error('Revise failed:', err); }
    finally { setRevisingIndex(null); }
  }, [callToolEndpoint, platform]);

  // API: Multi-platform adapt (#4)
  const adaptAllPlatforms = useCallback(async (captionText, hashtags, index) => {
    setAdapting(index);
    setAdaptResults(prev => ({ ...prev, [index]: null }));
    try {
      const data = await callToolEndpoint('caption-magic/adapt', {
        captionText,
        hashtags,
        sourcePlatform: platform,
      });
      setAdaptResults(prev => ({ ...prev, [index]: data }));
    } catch (err) { setError(err.message || 'Failed to adapt.'); }
    finally { setAdapting(null); }
  }, [callToolEndpoint, platform]);

  // API: Remix (#6)
  const submitRemix = useCallback(async () => {
    if (remixSelections.length < 2) return;
    setRemixing(true); setRemixResult(null);
    try {
      const selectedCaptions = remixSelections.map(i => results.captions[i]);
      const data = await callToolEndpoint('caption-magic/remix', {
        captions: selectedCaptions,
        remixInstructions: remixInstructions.trim() || null,
        platform,
      });
      setRemixResult(data.remixed_caption || data);
    } catch (err) { setError(err.message || 'Failed to remix.'); }
    finally { setRemixing(false); }
  }, [remixSelections, remixInstructions, results, platform, callToolEndpoint]);

  const handleReset = useCallback(() => {
    clearImage(); setImageDescription(''); setContext('');
    setResults(null); setError(''); setSelectedTones(['casual']);
    setCaptionLength('medium'); setAdaptResults({});
    setRemixResult(null); setShowRemix(false); setRemixSelections([]);
  }, [clearImage]);

  // ── Assign refs every render ──
  handleSubmitRef.current = generate;
  canSubmitRef.current = !loading && !!(imageBase64 || imageDescription.trim());

  // ── Build full copy text ──
  const buildFullText = useCallback(() => {
    if (!results?.captions) return '';
    const lines = ['✨ Caption Magic Results', ''];
    results.captions.forEach((cap, i) => {
      lines.push(`── Option ${i + 1}: ${cap.tone} ──`);
      lines.push(cap.text);
      if (cap.hashtags?.length > 0) lines.push(cap.hashtags.map(h => '#' + (typeof h === 'object' ? h.tag : h)).join(' '));
      lines.push('');
    });
    if (results.alt_text) lines.push(`Alt text: ${results.alt_text}`, '');
    lines.push(BRAND);
    return lines.join('\n');
  }, [results]);

  // ── Register global actions ──
  useRegisterActions(buildFullText(), tool?.title || 'Caption Magic');

  // ── Scroll to results ──
  useEffect(() => {
    if (!results) return;
    const t = setTimeout(() => {
      resultsRef.current?.scrollIntoView({ behavior: 'smooth', block: 'start' });
    }, 100);
    return () => clearTimeout(t);
  }, [results]);

  // ── Keyboard shortcut (Ctrl/Cmd+Enter) ──
  useEffect(() => {
    const handler = (e) => {
      if (e.target.tagName === 'SELECT') return;
      if ((e.ctrlKey || e.metaKey) && e.key === 'Enter') {
        e.preventDefault();
        if (canSubmitRef.current) handleSubmitRef.current();
      }
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
  }, []);

  // ══════════════════════════════════════════
  // HISTORY
  // ══════════════════════════════════════════
  const saveToHistory = useCallback((data) => {
    const entry = {
      id: `cm_${Date.now()}`, date: new Date().toISOString(), platform,
      captionCount: (data.captions || []).length,
      preview: (data.captions?.[0]?.text || '').slice(0, 40),
      results: data,
    };
    // Cap at 10 (not 5–6): each entry stores full nested results (captions + hashtags + schedule)
    setHistory(prev => [entry, ...prev].slice(0, 6));
  }, [platform, setHistory]);

  const loadFromHistory = useCallback((entry) => {
    setResults(entry.results); setShowHistory(false);
    setAdaptResults({}); setRemixResult(null);
  }, []);

  const removeFromHistory = useCallback((id) => {
    setHistory(prev => prev.filter(h => h.id !== id));
  }, [setHistory]);

  // ══════════════════════════════════════════
  // COPY HELPERS
  // ══════════════════════════════════════════
  const getTagText = (h) => typeof h === 'object' ? h.tag : h;

  const buildCaptionCopy = (caption) => {
    const parts = [caption.text];
    if (caption.hashtags?.length > 0) parts.push('', caption.hashtags.map(h => '#' + getTagText(h)).join(' '));
    parts.push(BRAND);
    return parts.join('\n');
  };

  // ══════════════════════════════════════════
  // RENDER: Reusable Pills
  // ══════════════════════════════════════════
  const renderPills = (options, value, setter, multi = false) => (
    <div className="flex flex-wrap gap-1.5">
      {options.map(opt => {
        const active = multi ? value.includes(opt.value) : value === opt.value;
        return (
          <button key={opt.value} onClick={() => setter(opt.value)}
            className={`px-3 py-1.5 rounded-lg text-xs font-semibold border transition-all ${active ? c.pillActive : c.pillInactive}`}>
            {active && <span className="mr-1">✓</span>}
            {opt.label}
          </button>
        );
      })}
    </div>
  );

  // ══════════════════════════════════════════
  // RENDER: Header
  // ══════════════════════════════════════════
  const renderHeader = () => (
    <div className={`${c.card} rounded-xl shadow-lg p-5 mb-4`}>
      <div className="mb-4 pb-3 border-b border-zinc-500">
        <div className="flex items-center justify-between">
          <div>
            <h2 className={`text-xl font-bold ${c.text} flex items-center gap-2`}>
              <span className="mr-2">{tool?.icon ?? '📸'}</span>{tool?.title ?? 'Caption Magic'}
            </h2>
            <p className={`text-sm ${c.textSecondary}`}>{tool?.tagline ?? 'Turn any photo into engaging social media captions'}</p>
          </div>
          {(!!results || !!imageBase64 || !!imageDescription.trim()) && (
            <button onClick={handleReset} className={`${c.btnSecondary} px-3 py-1.5 rounded-lg text-xs`}>
              ↺ Start Over
            </button>
          )}
        </div>
      </div>
    </div>
  );

  // ══════════════════════════════════════════
  // RENDER: Image Upload
  // ══════════════════════════════════════════
  const renderImageUpload = () => (
    <div className={`p-5 rounded-2xl border ${c.border} ${c.card}`}>
      <label className={`text-xs font-bold ${c.textSecondary} uppercase tracking-wide mb-3 block`}>📷 Your photo or description <span className={c.required}>*</span></label>
      {compressing ? (
        <div className={`border-2 border-dashed rounded-xl p-8 text-center ${c.dropActive}`}>
          <span className="animate-spin inline-block text-2xl mb-3">{tool?.icon ?? '📸'}</span>
          <p className={`text-sm font-semibold ${c.text}`}>Compressing image...</p>
        </div>
      ) : !imagePreview ? (
        <div className={`border-2 border-dashed rounded-xl p-6 text-center transition-colors ${isDragging ? c.dropActive : c.dropzone}`}
          onDragOver={handleDragOver} onDragLeave={handleDragLeave} onDrop={handleDrop}>
          <input ref={fileInputRef} type="file" accept="image/*" onChange={handleImageUpload} className="hidden" id="cm-image-upload" />
          <span className="text-3xl block mb-3">📷</span>
          <div className="flex items-center justify-center gap-2 mb-3">
            <label htmlFor="cm-image-upload" className={`px-4 py-2 rounded-lg text-xs font-bold cursor-pointer ${c.btnPrimary}`}>Upload Image</label>
            <button onClick={handlePaste} className={`px-4 py-2 rounded-lg text-xs font-bold ${c.btnSecondary}`}>📋 Paste</button>
          </div>
          <p className={`text-xs ${c.textMuted}`}>{isDragging ? 'Drop your image here!' : 'Drag & drop, paste, or upload — or describe it below'}</p>
        </div>
      ) : (
        <div className="relative">
          <img src={imagePreview} alt="Preview" className={`w-full max-h-72 object-contain rounded-xl border ${c.border}`} />
          <button onClick={clearImage} className={`absolute top-2 right-2 ${c.btnDelete} px-2 py-1 rounded-lg text-xs font-bold`}>✕</button>
        </div>
      )}
      <div className="mt-3">
        <label className={`text-xs font-semibold ${c.textMuted} block mb-1`}>
          Or describe what's in the photo <span className={c.required}>*</span>
        </label>
        <input type="text" value={imageDescription} onChange={e => setImageDescription(e.target.value)}
          placeholder="e.g., 'coffee on my desk with laptop'"
          className={`w-full px-4 py-2.5 rounded-xl border text-sm ${c.input} outline-none`} />
      </div>
    </div>
  );

  // ══════════════════════════════════════════
  // RENDER: Input Form
  // ══════════════════════════════════════════
  const renderInputForm = () => (
    <div className="space-y-4">
      {renderImageUpload()}

      <div className={`p-5 rounded-2xl border ${c.border} ${c.card}`}>
        <label className={`text-xs font-bold ${c.textSecondary} uppercase tracking-wide mb-2 block`}>📱 Platform</label>
        {renderPills(PLATFORMS, platform, setPlatform)}
        <p className={`text-xs ${c.textMuted} mt-2`}>Character limit: {PLATFORMS.find(p => p.value === platform)?.limit?.toLocaleString()}</p>
      </div>

      <div className={`p-5 rounded-2xl border ${c.border} ${c.card}`}>
        <label className={`text-xs font-bold ${c.textSecondary} uppercase tracking-wide mb-1 block`}>🎭 Tone</label>
        <p className={`text-xs ${c.textMuted} mb-2`}>Pick up to 3</p>
        {renderPills(TONES, selectedTones, toggleTone, true)}
      </div>

      <div className={`p-5 rounded-2xl border ${c.border} ${c.card}`}>
        <label className={`text-xs font-bold ${c.textSecondary} uppercase tracking-wide mb-2 block`}>📏 Caption Length</label>
        {renderPills(LENGTH_OPTIONS, captionLength, setCaptionLength)}
      </div>

      <div className={`p-5 rounded-2xl border ${c.border} ${c.card}`}>
        <label className={`text-xs font-bold ${c.textSecondary} uppercase tracking-wide mb-1 block`}>💬 Context</label>
        <p className={`text-xs ${c.textMuted} mb-2`}>Optional — backstory helps craft better captions</p>
        <input type="text" value={context} onChange={e => setContext(e.target.value)}
          placeholder="e.g., 'Team retreat in Colorado' or 'First time making sourdough'"
          className={`w-full px-4 py-2.5 rounded-xl border text-sm ${c.input} outline-none`} />
      </div>

      {/* Brand Voice Toggle (#1) */}
      {brandReady && (
        <div className={`p-4 rounded-2xl border ${c.okBg}`}>
          <div className="flex items-center justify-between">
            <div className="flex items-center gap-2">
              <span className="text-sm">🎙️</span>
              <div>
                <span className={`text-xs font-bold ${c.textOk}`}>Your Voice</span>
                <p className={`text-xs ${c.textOk} opacity-80`}>{brandVoiceSummary}</p>
              </div>
            </div>
            <button onClick={() => setUseBrandVoice(!useBrandVoice)}
              className={`px-3 py-1.5 rounded-lg text-xs font-bold ${useBrandVoice ? c.btnPrimary : c.btnSecondary}`}>
              {useBrandVoice ? '✓ On' : 'Use'}
            </button>
          </div>
        </div>
      )}

      {/* A/B Insights (#3) */}
      {abInsights && (
        <div className={`p-4 rounded-2xl border ${c.warning}`}>
          <div className="flex items-center gap-2 mb-1">
            <span className="text-sm">📊</span>
            <span className={`text-xs font-bold ${c.textCaution}`}>Your Audience Insights ({abInsights.totalTests} tests)</span>
          </div>
          <p className={`text-xs ${c.textCaution}`}>
            {abInsights.topTone && `Best-performing tone: ${abInsights.topTone}`}
            {abInsights.topTone && abInsights.topLength && ' · '}
            {abInsights.topLength && `Preferred length: ${abInsights.topLength}`}
          </p>
        </div>
      )}

      <button onClick={generate}
        disabled={loading || (!imageBase64 && !imageDescription.trim())}
        className={`w-full py-4 rounded-2xl text-sm font-bold flex items-center justify-center gap-2 transition-all
          ${loading || (!imageBase64 && !imageDescription.trim()) ? c.stateDisabled : c.btnPrimary}`}>
        {loading ? <><span className="animate-spin inline-block">{tool?.icon ?? '📸'}</span> Crafting captions...</>
          : <><span>{tool?.icon ?? '📸'}</span> Generate Captions</>}
      </button>

    </div>
  );

  // ══════════════════════════════════════════
  // RENDER: Hashtag with intelligence (#5)
  // ══════════════════════════════════════════
  const renderHashtags = (hashtags) => {
    if (!hashtags?.length) return null;
    // Group by category
    const grouped = { trending: [], niche: [], branded: [], uncategorized: [] };
    hashtags.forEach(h => {
      if (typeof h === 'object' && h.category) {
        (grouped[h.category] || grouped.uncategorized).push(h.tag);
      } else {
        grouped.uncategorized.push(typeof h === 'object' ? h.tag : h);
      }
    });

    const hasCats = grouped.trending.length || grouped.niche.length || grouped.branded.length;

    return (
      <div className="mb-3">
        <div className={`text-xs font-semibold ${c.textSecondary} mb-1.5`}>Hashtags</div>
        {hasCats ? (
          <div className="space-y-1.5">
            {Object.entries(grouped).filter(([,tags]) => tags.length > 0).map(([cat, tags]) => {
              const meta = HASHTAG_CATEGORY_STYLES[cat];
              const pillClass = cat === 'trending' ? c.htTrending : cat === 'niche' ? c.htNiche : cat === 'branded' ? c.htBranded : c.pillInactive;
              return (
                <div key={cat} className="flex flex-wrap gap-1.5 items-center">
                  {meta && <span className={`text-[10px] font-bold ${c.textMuted} mr-1`}>{meta.emoji} {meta.label}</span>}
                  {tags.map((tag, i) => (
                    <span key={i} className={`px-2 py-0.5 rounded text-xs font-medium border ${pillClass}`}>#{tag}</span>
                  ))}
                </div>
              );
            })}
          </div>
        ) : (
          <div className="flex flex-wrap gap-1.5">
            {hashtags.map((h, i) => (
              <span key={i} className={`px-2 py-0.5 rounded text-xs font-medium ${c.pillInactive}`}>#{getTagText(h)}</span>
            ))}
          </div>
        )}
      </div>
    );
  };

  // ══════════════════════════════════════════
  // RENDER: Character count
  // ══════════════════════════════════════════
  const renderCharCount = (count) => {
    const limit = PLATFORMS.find(p => p.value === platform)?.limit || 2200;
    const isOver = count > limit;
    const isNearLimit = count / limit >= 0.75;
    if (!isOver && !isNearLimit) return null;
    return (
      <span className={`text-xs font-semibold ${isOver ? c.textDanger : c.textCaution}`}>
        {count?.toLocaleString()} / {limit.toLocaleString()} {isOver ? '⚠️' : ''}
      </span>
    );
  };

  // ══════════════════════════════════════════
  // RENDER: Posting Schedule (#2)
  // ══════════════════════════════════════════
  const renderPostingSchedule = () => {
    const sched = results?.posting_schedule;
    if (!sched) return null;

    return (
      <div className={`p-4 rounded-2xl border ${c.border} ${c.card}`}>
        <div className="flex items-center gap-2 mb-3">
          <span className="text-base">📅</span>
          <h3 className={`text-sm font-bold ${c.text}`}>Best Posting Times</h3>
        </div>
        <div className="flex gap-2 flex-wrap mb-2">
          {(sched.best_days || []).map((day, i) => (
            <span key={i} className={`px-2.5 py-1 rounded-lg text-xs font-bold ${c.heatHigh} ${c.text}`}>{day}</span>
          ))}
          {(sched.best_hours || []).map((hour, i) => (
            <span key={i} className={`px-2.5 py-1 rounded-lg text-xs font-semibold ${c.heatMed} ${c.text}`}>🕐 {hour}</span>
          ))}
        </div>
        {sched.why && <p className={`text-xs ${c.textMuted}`}>{sched.why}</p>}
      </div>
    );
  };

  // ══════════════════════════════════════════
  // RENDER: Caption Remix (#6)
  // ══════════════════════════════════════════
  const renderRemixPanel = () => {
    if (!results?.captions || results.captions.length < 2) return null;

    const toggleRemixSelection = (idx) => {
      setRemixSelections(prev =>
        prev.includes(idx) ? prev.filter(i => i !== idx) : [...prev, idx]
      );
    };

    return (
      <div className={`p-4 rounded-2xl border ${c.border} ${c.card}`}>
        <button onClick={() => setShowRemix(!showRemix)}
          className={`flex items-center gap-2 text-xs font-bold ${c.textSecondary} w-full text-left`}>
          <span>{showRemix ? '▲' : '▼'}</span>
          <span>🎰 Remix Captions</span>
        </button>
        {showRemix && (
          <div className="mt-3 space-y-3">
            <p className={`text-xs ${c.textMuted}`}>Select 2+ captions to blend, then tell us what to combine</p>
            <div className="flex gap-2">
              {results.captions.map((cap, idx) => (
                <button key={idx} onClick={() => toggleRemixSelection(idx)}
                  className={`flex-1 py-2 rounded-lg text-xs font-bold border transition-all ${
                    remixSelections.includes(idx) ? c.pillActive : c.pillInactive
                  }`}>
                  {remixSelections.includes(idx) && <span className="mr-1">✓</span>}
                  Option {idx + 1}
                </button>
              ))}
            </div>
            <input type="text" value={remixInstructions} onChange={e => setRemixInstructions(e.target.value)}
              placeholder="e.g., 'Opening from #1, tone from #3, hashtags from #2'"
              className={`w-full px-4 py-2.5 rounded-xl border text-sm ${c.input} outline-none`} />
            <button onClick={submitRemix}
              disabled={remixSelections.length < 2 || remixing}
              className={`w-full py-3 rounded-xl text-xs font-bold flex items-center justify-center gap-2 ${
                remixSelections.length < 2 || remixing ? c.stateDisabled : c.btnPrimary
              }`}>
              {remixing ? <><span className="animate-spin inline-block">{tool?.icon ?? '📸'}</span> Remixing...</>
                : <><span>🎰</span> Remix Selected</>}
            </button>

            {/* Remix result */}
            {remixResult && (
              <div className={`p-4 rounded-xl border ${c.okBg}`}>
                <div className="flex items-center gap-2 mb-2">
                  <span className={`px-2.5 py-0.5 rounded-full text-xs font-semibold ${c.tonePill}`}>{remixResult.tone || 'Remixed'}</span>
                  {remixResult.char_count != null && renderCharCount(remixResult.char_count)}
                </div>
                <p className={`text-sm whitespace-pre-wrap mb-2 ${c.text}`}>{remixResult.text}</p>
                {remixResult.remix_explanation && (
                  <p className={`text-xs ${c.textOk} mb-2 italic`}>🎰 {remixResult.remix_explanation}</p>
                )}
                {renderHashtags(remixResult.hashtags)}
              </div>
            )}
          </div>
        )}
      </div>
    );
  };

  // ══════════════════════════════════════════
  // RENDER: Results
  // ══════════════════════════════════════════
  const renderResults = () => {
    if (!results) return null;
    const captions = results.captions || [];

    return (
      <div ref={resultsRef} className="space-y-4 mt-4">
        {/* Results header */}
        <div className="flex items-center justify-between flex-wrap gap-2">
          <h3 className={`text-sm font-bold ${c.text}`}>Your Captions</h3>
          <div className="flex gap-2 flex-wrap items-center">
            <button
              onClick={generate}
              disabled={loading || (!imageBase64 && !imageDescription.trim())}
              className={`px-3 py-1.5 rounded-lg text-xs font-bold disabled:opacity-40 transition-colors ${c.btnPrimary}`}
            >
              {loading ? <span className="animate-spin inline-block">{tool?.icon ?? '📸'}</span> : '🔄 Regenerate'}
            </button>
          </div>
        </div>

        {/* What I see */}
        {(imagePreview || results.image_read) && (
          <div className={`rounded-xl ${c.cardAlt} overflow-hidden`}>
            {imagePreview && (
              <img
                src={imagePreview}
                alt="Your photo"
                className="w-full object-contain max-h-96"
              />
            )}
            {results.image_read && (
              <p className={`text-xs ${c.textMuted} p-3 leading-relaxed`}>
                <strong>What I see:</strong> {results.image_read}
              </p>
            )}
          </div>
        )}

        {/* Caption cards with A/B framing (#3) */}
        {captions.map((caption, index) => (
          <div key={index} className={`p-5 rounded-2xl border ${c.border} ${c.card}`}>
            <div className="flex items-center gap-2 mb-2 flex-wrap">
              <span className={`px-2.5 py-0.5 rounded-full text-xs font-semibold ${c.tonePill}`}>
                {caption.tone}
              </span>
              {caption.char_count != null && renderCharCount(caption.char_count)}
              {caption.best_for && <span className={`text-xs ${c.textMuted}`}>· {caption.best_for}</span>}
            </div>

            {/* Caption text */}
            <div className={`p-4 rounded-xl border ${c.captionBg} mb-3`}>
              <p className={`text-sm leading-relaxed whitespace-pre-wrap ${c.text}`}>{caption.text}</p>
            </div>

            {caption.why_it_works && <p className={`text-xs ${c.textSecondary} mb-2 italic`}>{caption.why_it_works}</p>}
            {caption.revision_note && <p className={`text-xs ${c.textCaution} mb-2`}>🎛️ {caption.revision_note}</p>}

            {/* Hashtags with intelligence (#5) */}
            {renderHashtags(caption.hashtags)}

            {/* Actions row */}
            <div className="flex gap-2 flex-wrap">
              {REVISE_OPTIONS.map(opt => (
                <button key={opt.value}
                  onClick={() => reviseCaption(caption.text, index, opt.value)}
                  disabled={revisingIndex === index}
                  className={`px-3 py-2 rounded-lg text-xs font-semibold ${c.btnSecondary} ${revisingIndex === index ? 'opacity-50' : ''}`}>
                  {revisingIndex === index ? <span className="animate-spin inline-block">{tool?.icon ?? '📸'}</span> : opt.label}
                </button>
              ))}
            </div>

            {/* A/B winner + adapt row */}
            <div className="flex gap-2 mt-2">
              <button onClick={() => markAbWinner(index)}
                className={`px-3 py-2 rounded-lg text-xs font-semibold transition-colors ${winnerIndex === index ? c.success : c.btnSecondary}`}>
                {winnerIndex === index ? '✓ Noted!' : '📊 This one won'}
              </button>
              <button onClick={() => adaptAllPlatforms(caption.text, caption.hashtags, index)}
                disabled={adapting === index}
                className={`px-3 py-2 rounded-lg text-xs font-semibold ${adapting === index ? c.stateDisabled : c.btnPrimary}`}>
                {adapting === index ? <span className="animate-spin inline-block">{tool?.icon ?? '📸'}</span> : <><span>🌐</span> Adapt to all platforms</>}
              </button>
            </div>
            <p className={`text-xs ${winnerIndex === index ? c.successTxt : c.textMuted} mt-1.5 transition-colors`}>
              {winnerIndex === index
                ? 'Got it — we\'ll factor this into your audience insights.'
                : 'Mark whichever caption performed best — we\'ll remember your audience\'s preferences over time.'}
            </p>

            {/* Per-caption adapt results */}
            {adaptResults?.[index]?.adaptations && (
              <div className={`mt-4 p-4 rounded-xl border ${c.border} space-y-3`}>
                <h4 className={`text-xs font-bold ${c.text}`}>🌐 Adapted for All Platforms</h4>
                {adaptResults[index].adaptations.map((adapt, idx) => (
                  <div key={idx} className={`p-3 rounded-xl border ${c.captionBg}`}>
                    <div className="flex items-center justify-between mb-1.5">
                      <span className={`text-xs font-bold ${c.text}`}>{adapt.platform_name || adapt.platform}</span>
                      {adapt.char_count != null && renderCharCount(adapt.char_count)}
                    </div>
                    <p className={`text-sm whitespace-pre-wrap mb-1.5 ${c.text}`}>{adapt.text}</p>
                    {adapt.hashtags?.length > 0 && (
                      <p className={`text-xs ${c.textMuted}`}>{adapt.hashtags.map(h => '#' + h).join(' ')}</p>
                    )}
                    {adapt.adaptation_note && (
                      <p className={`text-[10px] ${c.textMuted} italic mt-1`}>{adapt.adaptation_note}</p>
                    )}
                  </div>
                ))}
              </div>
            )}
          </div>
        ))}

        {/* Posting Schedule (#2) */}
        {renderPostingSchedule()}

        {/* Engagement Tips */}
        {(results.engagement_tips?.length > 0 || results.avoid?.length > 0) && (
          <div className={`p-5 rounded-2xl border ${c.warning}`}>
            <h3 className={`text-sm font-bold mb-3 ${c.textCaution}`}>💡 Engagement Tips</h3>
            {results.engagement_tips?.map((tip, i) => (
              <p key={i} className={`text-xs ${c.textCaution} mb-1`}>✓ {tip}</p>
            ))}
            {results.avoid?.length > 0 && (
              <>
                <p className={`text-xs font-bold ${c.textCaution} mt-2`}>Avoid:</p>
                {results.avoid.map((a, i) => <p key={i} className={`text-xs ${c.textCaution} opacity-80`}>✕ {a}</p>)}
              </>
            )}
          </div>
        )}

        {/* Caption Remix (#6) */}
        {renderRemixPanel()}

        <p className={`text-[10px] ${c.textMuted} text-center px-4`}>
          Captions are AI-generated suggestions. Review and personalize before posting.
        </p>
      </div>
    );
  };

  const renderError = () => error ? (
    <div className={`mt-4 p-4 ${c.danger} border rounded-xl flex items-start gap-3`}>
      <span className={`text-base ${c.textDanger} flex-shrink-0`}>⚠️</span>
      <p className={`text-sm ${c.textDanger}`}>{error}</p>
    </div>
  ) : null;

  // ══════════════════════════════════════════
  // RENDER: History
  // ══════════════════════════════════════════
  const renderHistory = () => {
    if (history.length === 0) return null;
    const formatDate = (iso) => {
      try {
        const d = new Date(iso); const now = new Date();
        const diff = Math.floor((now - d) / (1000 * 60 * 60 * 24));
        if (diff === 0) return 'Today';
        if (diff === 1) return 'Yesterday';
        if (diff < 7) return `${diff} days ago`;
        return d.toLocaleDateString('en-US', { month: 'short', day: 'numeric' });
      } catch { return ''; }
    };

    return (
      <div className={`mt-6 p-4 rounded-2xl border ${c.histBg}`}>
        <button onClick={() => setShowHistory(!showHistory)} className="w-full flex items-center gap-2 text-left">
          <span className={`text-base ${c.textCyan}`}>✨</span>
          <span className={`text-sm font-bold ${c.text} flex-1`}>Past Captions</span>
          <span className={`text-xs ${c.textMuted}`}>{history.length}</span>
          <span className={`text-xs ${c.textMuted}`}>{showHistory ? '▲' : '▼'}</span>
        </button>
        {showHistory && (
          <div className="mt-3 space-y-2">
            {history.map(entry => (
              <div key={entry.id} className={`rounded-xl border ${c.histCard} p-3 flex items-center gap-3`}>
                <div className="flex-1 min-w-0">
                  <div className={`text-sm font-semibold ${c.text} truncate`}>{entry.preview}...</div>
                  <div className={`text-xs ${c.textMuted} mt-0.5`}>{formatDate(entry.date)} · {entry.platform} · {entry.captionCount} captions</div>
                </div>
                <button onClick={() => loadFromHistory(entry)} className={`px-3 py-1.5 rounded-lg text-xs font-bold ${c.btnSecondary}`}>View</button>
                <button onClick={() => removeFromHistory(entry.id)} className={`px-2 py-1.5 rounded-lg text-xs ${c.textGhostDel}`}>🗑️</button>
              </div>
            ))}
            {history.length > 1 && (
              <button onClick={() => setHistory([])}
                className={`w-full mt-1 text-center text-xs font-semibold ${c.textGhostDel} py-1.5`}>Clear all history</button>
            )}
          </div>
        )}
      </div>
    );
  };

  // ══════════════════════════════════════════
  // MAIN RENDER
  // ══════════════════════════════════════════
  return (
    <div className={`space-y-4 ${c.text}`}>
      {renderHeader()}
      {!results && renderInputForm()}
      {!results && (
        <p className={`text-xs text-center ${c.textMuted}`}>
          Want help crafting what you write next?{' '}
          <a href="/VelvetHammer" className={linkStyle}>✉️ Velvet Hammer</a> nails any tricky reply or comment.
        </p>
      )}
      {results && renderResults()}
      {results && (
        <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
          <p className={`text-xs ${c.textMuted} text-center`}>
            Turning professional wins into posts?{' '}
            <a href="/BragSheetBuilder" className={linkStyle}>🏆 Brag Sheet Builder</a> turns your achievements into LinkedIn-ready copy.
          </p>
        </div>
      )}
      {renderError()}
      {renderHistory()}

    </div>
  );
};

CaptionMagic.displayName = 'CaptionMagic';
export default CaptionMagic;
