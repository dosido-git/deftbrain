function DiagramBtn({ description, diagramType, isDark, c }) {
  const { callToolEndpoint, userLocale, userCurrency, userRegion } = useClaudeAPI();
  const { t } = useTranslation();
  const [state, setState] = useState('idle'); // idle | loading | done | error
  const [output, setOutput] = useState(null); // { html, type }
  const [err, setErr] = useState('');

  const generate = async () => {
    setState('loading');
    setErr('');
    try {
      const data = await callToolEndpoint('generate-diagram', { description, diagramType, userLocale, userCurrency, userRegion });
      if (!data.html) throw new Error(t('dvt_err_no_diagram'));
      setOutput(data);
      setState('done');
    } catch (e) {
      console.error('DiagramBtn error:', e);
      setErr(e.message || t('dvt_err_diagram'));
      setState('error');
    }
  };

  if (state === 'done' && output) {
    return (
      <div className="mt-3">
        <div className={`rounded-lg overflow-hidden border ${isDark ? 'border-zinc-600 bg-zinc-900' : 'border-gray-200 bg-white'} p-2 relative`}>
          <button
            onClick={() => { setOutput(null); setState('idle'); }}
            title={t('dvt_close')}
            className={`absolute top-1.5 right-1.5 w-5 h-5 flex items-center justify-center rounded text-xs leading-none
              ${isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-300' : 'bg-gray-100 hover:bg-gray-200 text-gray-500'}`}>
            ✕
          </button>
          <div dangerouslySetInnerHTML={{ __html: output.html }} />
        </div>
        <button onClick={generate} className={`text-[10px] ${c.textMuted} mt-1 hover:underline`}>{t('dvt_regenerate')}</button>
      </div>
    );
  }

  return (
    <div className="mt-2">
      {state === 'error' && <p className="text-[10px] text-red-500 mb-1">{err}</p>}
      <button
        onClick={generate}
        disabled={state === 'loading'}
        className={`flex items-center gap-1.5 px-3 py-1.5 rounded-lg text-xs font-medium border transition-all disabled:opacity-40
          ${isDark ? 'border-zinc-600 bg-zinc-700 hover:bg-zinc-600 text-zinc-200' : 'border-gray-300 bg-gray-50 hover:bg-gray-100 text-gray-700'}`}>
        {state === 'loading'
          ? <><span>🖼️</span> {t('dvt_generating')}</>
          : <><span>🖼️</span> {t('dvt_generate_visual')}</>}
      </button>
    </div>
  );
}


