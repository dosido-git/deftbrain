  const readPdfAsBase64 = (file) => new Promise((resolve, reject) => {
    if (!file || file.type !== 'application/pdf') { reject(new Error(t('dvt_err_pdf_type'))); return; }
    if (file.size > 10 * 1024 * 1024) { reject(new Error(t('dvt_err_pdf_size'))); return; }
    const reader = new FileReader();
    reader.onload = () => resolve(reader.result.split(',')[1]);
    reader.onerror = () => reject(new Error(t('dvt_err_read_file')));
    reader.readAsDataURL(file);
  });

