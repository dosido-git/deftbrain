import React, { useState } from 'react';
import { useTheme } from '../hooks/useTheme';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { CopyBtn, ActionBar } from '../components/ActionButtons';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const TARGET_TYPES = [
  { id: 'mentor', label: '🧭 Mentor', icon: '🧭' },
  { id: 'investor', label: '💰 Investor', icon: '💰' },
  { id: 'collaborator', label: '🤝 Collaborator', icon: '🤝' },
  { id: 'employer', label: '💼 Employer / Hiring Manager', icon: '💼' },
  { id: 'friend', label: '👥 Friend', icon: '👥' },
  { id: 'partner', label: '❤️ Partner', icon: '❤️' },
  { id: 'client', label: '📦 Client', icon: '📦' },
  { id: 'connector', label: '🌐 Super-connector', icon: '🌐' },
];

const PHASE_COLORS = [
  { border: '#c8872e', bg: (d) => d ? 'bg-[#c8872e]/10' : 'bg-[#fdf3e4]', text: (d) => d ? 'text-[#d9a04e]' : 'text-[#93541f]' },
  { border: '#2c4a6e', bg: (d) => d ? 'bg-[#2c4a6e]/10' : 'bg-[#e8eef5]', text: (d) => d ? 'text-[#a8b9ce]' : 'text-[#1e3a58]' },
  { border: '#5a8a5c', bg: (d) => d ? 'bg-[#5a8a5c]/10' : 'bg-[#e8f0e8]', text: (d) => d ? 'text-[#8aba8c]' : 'text-[#3d6e3f]' },
];

const GravityWell = () => {
  const { theme } = useTheme();
  const isDark = theme === 'dark';
  const { callToolEndpoint, loading } = useClaudeAPI();

  const c = {
    pageBg:       isDark ? 'bg-[#1a1816]'    : 'bg-[#faf8f5]',
    card:         isDark ? 'bg-[#2a2623]'     : 'bg-white',
    cardAlt:      isDark ? 'bg-[#332e2a]'     : 'bg-[#faf8f5]',
    cardBorder:   isDark ? 'border-[#3d3630]' : 'border-[#e8e1d5]',
    input:        isDark ? 'bg-[#1a1816] border-[#3d3630] text-[#f0eeea] placeholder:text-[#8a8275]'
                         : 'bg-[#faf8f5] border-[#d5cab8] text-[#3d3935] placeholder:text-[#8a8275]',
    inputFocus:   'focus:outline-none focus:ring-2 focus:ring-[#2c4a6e]/20 focus:border-[#4a6a8a]',
    text:          isDark ? 'text-[#f0eeea]'  : 'text-[#3d3935]',
    textSecondary: isDark ? 'text-[#c8c3b9]'  : 'text-[#5a544a]',
    textMuted:     isDark ? 'text-[#8a8275]'  : 'text-[#8a8275]',
    heading:       isDark ? 'text-[#f3efe8]'  : 'text-[#1e2a3a]',
    btnPrimary:    isDark ? 'bg-[#2c4a6e] hover:bg-[#4a6a8a] text-white' : 'bg-[#2c4a6e] hover:bg-[#1e3a58] text-white',
    btnSecondary:  isDark ? 'bg-[#332e2a] hover:bg-[#3d3630] text-[#c8c3b9] border border-[#3d3630]' : 'bg-[#f3efe8] hover:bg-[#e8e1d5] text-[#5e5042] border border-[#d5cab8]',
    divider:       isDark ? 'border-[#3d3630]' : 'border-[#e8e1d5]',
    error:         isDark ? 'bg-[#b54a3f]/15 border-[#b54a3f]/50 text-[#e8a9a3]' : 'bg-[#fceae8] border-[#d4908a] text-[#8a3028]',
  };

  const [targetDescription, setTargetDescription] = useState('');
  const [targetType, setTargetType] = useState('');
  const [whyThemContext, setWhyThemContext] = useState('');
  const [yourBackground, setYourBackground] = useState('');
  const [results, setResults] = useState(null);
  const [error, setError] = useState('');
  const [expanded, setExpanded] = useState({ profile: false, value: false });
  const toggle = (k) => setExpanded(p => ({ ...p, [k]: !p[k] }));

  const handleSubmit = async () => {
    if (!targetDescription.trim()) return;
    setError(''); setResults(null);
    try {
      const data = await callToolEndpoint('gravity-well', {
        targetDescription: targetDescription.trim(),
        targetType: targetType || undefined,
        whyThemContext: whyThemContext.trim() || undefined,
        yourBackground: yourBackground.trim() || undefined,
      });
      setResults(data);
    } catch (e) { setError(e.message || 'Failed to design gravity strategy.'); }
  };

  const buildText = () => {
    if (!results) return '';
    let t = `🌀 GRAVITY WELL — 90-DAY ORBIT STRATEGY\n\nTarget: ${targetDescription}\nCurrent gravity: ${results.your_gravity_score?.current}\n\n`;
    ['phase_1', 'phase_2', 'phase_3'].forEach(ph => {
      const p = results.the_90_day_plan?.[ph];
      if (!p) return;
      t += `${p.name}\n${p.objective}\n`;
      p.actions?.forEach(a => { t += `• ${a.action} (${a.platform_or_venue})\n`; });
      t += '\n';
    });
    if (results.the_first_contact?.what_to_say) t += `FIRST CONTACT:\n${results.the_first_contact.what_to_say}\n\n`;
    if (results.the_one_thing_today) t += `DO TODAY: ${results.the_one_thing_today}\n`;
    return t + BRAND;
  };

  const plan = results?.the_90_day_plan;
  const phases = plan ? [
    { key: 'phase_1', data: plan.phase_1, cfg: PHASE_COLORS[0] },
    { key: 'phase_2', data: plan.phase_2, cfg: PHASE_COLORS[1] },
    { key: 'phase_3', data: plan.phase_3, cfg: PHASE_COLORS[2] },
  ] : [];

  return (
    <div className={`min-h-screen py-8 px-4 ${c.pageBg}`}>
      <div className="max-w-2xl mx-auto">

        <div className="text-center mb-8">
          <div className="text-5xl mb-3">🌀</div>
          <h1 className={`text-3xl font-black tracking-tight mb-2 ${c.heading}`}>Gravity Well</h1>
          <p className={`text-sm ${c.textMuted}`}>A 90-day plan to naturally enter someone's orbit — before you ever reach out.</p>
        </div>

        {!results && (
          <div className={`rounded-2xl border p-6 shadow-sm space-y-4 ${c.card} ${c.cardBorder}`}>
            <div>
              <label className={`block text-sm font-semibold mb-1.5 ${c.text}`}>
                Describe the person you want in your life <span className="text-red-400">*</span>
              </label>
              <textarea value={targetDescription} onChange={e => setTargetDescription(e.target.value)}
                placeholder="Their role, what they're known for, what world they operate in — specific enough to design a real strategy…"
                rows={3} maxLength={500}
                className={`w-full px-4 py-3 rounded-xl border text-sm resize-none ${c.input} ${c.inputFocus}`} />
            </div>

            <div>
              <label className={`block text-sm font-semibold mb-2 ${c.text}`}>Type of relationship <span className={`font-normal ${c.textMuted}`}>(optional)</span></label>
              <div className="flex flex-wrap gap-2">
                {TARGET_TYPES.map(t => (
                  <button key={t.id} onClick={() => setTargetType(targetType === t.id ? '' : t.id)}
                    className={`px-3 py-1.5 rounded-lg text-xs font-semibold border transition-all ${
                      targetType === t.id ? 'bg-[#2c4a6e] text-white border-[#2c4a6e]' : c.btnSecondary
                    }`}>
                    {t.label}
                  </button>
                ))}
              </div>
            </div>

            <div className="grid grid-cols-1 sm:grid-cols-2 gap-3">
              <div>
                <label className={`block text-sm font-semibold mb-1.5 ${c.text}`}>Why this person? <span className={`font-normal ${c.textMuted}`}>(optional)</span></label>
                <textarea value={whyThemContext} onChange={e => setWhyThemContext(e.target.value)}
                  placeholder="What you'd learn from them, why they specifically…"
                  rows={2} maxLength={300}
                  className={`w-full px-4 py-3 rounded-xl border text-sm resize-none ${c.input} ${c.inputFocus}`} />
              </div>
              <div>
                <label className={`block text-sm font-semibold mb-1.5 ${c.text}`}>Your background / what you bring <span className={`font-normal ${c.textMuted}`}>(optional)</span></label>
                <textarea value={yourBackground} onChange={e => setYourBackground(e.target.value)}
                  placeholder="Skills, work, perspective — what makes you worth knowing…"
                  rows={2} maxLength={300}
                  className={`w-full px-4 py-3 rounded-xl border text-sm resize-none ${c.input} ${c.inputFocus}`} />
              </div>
            </div>

            {error && <div className={`p-3 rounded-xl border text-sm ${c.error}`}><span className="mr-1">⚠️</span>{error}</div>}

            <button onClick={handleSubmit} disabled={loading || !targetDescription.trim()}
              className={`w-full py-3 rounded-xl font-bold disabled:opacity-40 ${c.btnPrimary}`}>
              {loading ? <><span className="animate-spin inline-block mr-2">⏳</span>Designing your orbit strategy…</> : '🌀 Build My Gravity Strategy'}
            </button>

            <p className={`text-xs text-center ${c.textMuted}`}>Not cold outreach. Gravitational pull — so when you finally connect, they already know who you are.</p>
          </div>
        )}

        {results && (
          <div className="space-y-4">

            {/* Gravity score */}
            {results.your_gravity_score && (
              <div className={`rounded-2xl border p-5 ${c.card} ${c.cardBorder}`}>
                <div className="flex items-center justify-between mb-3">
                  <p className={`text-xs font-black uppercase tracking-widest ${c.textMuted}`}>Current Gravity Score</p>
                  <div className="text-right">
                    <span className={`text-3xl font-black ${isDark ? 'text-[#e8a9a3]' : 'text-[#8a3028]'}`}>{results.your_gravity_score.current}</span>
                    {results.gravity_score_target && (
                      <span className={`text-lg font-bold ml-2 ${isDark ? 'text-[#8aba8c]' : 'text-[#3d6e3f]'}`}>→ {results.gravity_score_target}</span>
                    )}
                  </div>
                </div>
                <div className={`h-2.5 rounded-full mb-3 ${isDark ? 'bg-[#3d3630]' : 'bg-[#e8e1d5]'}`}>
                  <div className="h-full rounded-full" style={{ width: results.your_gravity_score.current, background: 'linear-gradient(90deg, #b54a3f, #e8a9a3)' }} />
                </div>
                <p className={`text-sm mb-2 ${c.textSecondary}`}>{results.your_gravity_score.the_gap}</p>
                {results.your_gravity_score.your_natural_advantages && (
                  <p className={`text-xs ${c.textMuted}`}><span className="font-semibold">Your advantage:</span> {results.your_gravity_score.your_natural_advantages}</p>
                )}
              </div>
            )}

            {/* Target profile */}
            {results.target_profile && (
              <div className={`rounded-2xl border overflow-hidden ${c.card} ${c.cardBorder}`}>
                <button onClick={() => toggle('profile')} className="w-full text-left px-5 py-4 flex items-center justify-between">
                  <p className={`text-xs font-bold uppercase tracking-wider ${c.textMuted}`}>🎯 Understanding Their World</p>
                  <span className={`text-sm ${c.textMuted}`}>{expanded.profile ? '▲' : '▼'}</span>
                </button>
                {expanded.profile && (
                  <div className={`px-5 pb-5 space-y-2 border-t ${c.divider} pt-4`}>
                    {[
                      { key: 'what_they_care_about', label: 'What they care about' },
                      { key: 'what_gets_their_attention', label: 'What breaks through their noise' },
                      { key: 'what_they_avoid', label: 'What marks you as not worth their time' },
                      { key: 'their_world', label: 'Where they already exist' },
                    ].map(row => results.target_profile[row.key] && (
                      <p key={row.key} className={`text-sm ${c.textSecondary}`}>
                        <span className={`font-semibold ${c.text}`}>{row.label}:</span> {results.target_profile[row.key]}
                      </p>
                    ))}
                  </div>
                )}
              </div>
            )}

            {/* 90-day phases */}
            <p className={`text-xs font-black uppercase tracking-widest px-1 ${c.textMuted}`}>📅 The 90-Day Plan</p>
            {phases.map(({ key, data, cfg }) => data && (
              <div key={key} className="rounded-2xl overflow-hidden border-2" style={{ borderColor: cfg.border }}>
                <div className={`px-5 py-4 ${cfg.bg(isDark)}`}>
                  <p className={`text-xs font-black uppercase tracking-widest mb-0.5 ${cfg.text(isDark)}`}>{data.name}</p>
                  <p className={`text-sm font-semibold ${c.text}`}>{data.objective}</p>
                </div>
                <div className={`px-5 py-4 space-y-3 ${c.card}`}>
                  {data.actions?.map((action, j) => (
                    <div key={j} className={`p-3 rounded-xl border ${c.cardAlt} ${c.cardBorder}`}>
                      <p className={`text-sm font-semibold mb-0.5 ${c.text}`}>{action.action}</p>
                      {action.platform_or_venue && (
                        <p className={`text-xs ${c.textMuted}`}><span className="font-semibold">Where:</span> {action.platform_or_venue}</p>
                      )}
                      {action.the_signal_it_sends && (
                        <p className={`text-xs italic mt-0.5 ${c.textMuted}`}>Signal: {action.the_signal_it_sends}</p>
                      )}
                    </div>
                  ))}
                </div>
              </div>
            ))}

            {/* First contact */}
            {results.the_first_contact && (
              <div className={`rounded-2xl border p-5 ${c.card} ${c.cardBorder}`}>
                <p className={`text-xs font-bold uppercase tracking-wider mb-3 ${c.textMuted}`}>✉️ The First Contact</p>
                {results.the_first_contact.when_to_reach_out && (
                  <p className={`text-sm mb-2 ${c.textSecondary}`}><span className={`font-semibold ${c.text}`}>When to reach out:</span> {results.the_first_contact.when_to_reach_out}</p>
                )}
                {results.the_first_contact.the_frame && (
                  <p className={`text-sm mb-3 ${c.textSecondary}`}><span className={`font-semibold ${c.text}`}>The frame:</span> {results.the_first_contact.the_frame}</p>
                )}
                {results.the_first_contact.what_to_say && (
                  <div className={`p-3 rounded-xl border font-mono text-sm mb-2 ${isDark ? 'bg-[#1a1816] border-[#3d3630]' : 'bg-[#faf8f5] border-[#d5cab8]'} ${c.textSecondary}`}>
                    {results.the_first_contact.what_to_say}
                  </div>
                )}
                {results.the_first_contact.what_not_to_say && (
                  <p className={`text-xs ${isDark ? 'text-[#e8a9a3]' : 'text-[#8a3028]'}`}><span className="font-semibold">Don't say:</span> {results.the_first_contact.what_not_to_say}</p>
                )}
                {results.the_first_contact.what_to_say && (
                  <div className="mt-3">
                    <CopyBtn content={results.the_first_contact.what_to_say + BRAND} label="Copy message" />
                  </div>
                )}
              </div>
            )}

            {/* Value offer */}
            {results.the_value_offer && (
              <div onClick={() => toggle('value')} className={`rounded-2xl border overflow-hidden ${c.card} ${c.cardBorder}`}>
                <button className="w-full text-left px-5 py-4 flex items-center justify-between">
                  <p className={`text-xs font-bold uppercase tracking-wider ${c.textMuted}`}>🎁 The Value Offer</p>
                  <span className={`text-sm ${c.textMuted}`}>{expanded.value ? '▲' : '▼'}</span>
                </button>
                {expanded.value && (
                  <div className={`px-5 pb-5 space-y-2 border-t ${c.divider} pt-4`}>
                    {[
                      { key: 'what_you_can_give', label: 'What you can give' },
                      { key: 'why_its_genuine', label: 'Why it\'s genuine' },
                      { key: 'the_asymmetry', label: 'The asymmetry' },
                    ].map(row => results.the_value_offer[row.key] && (
                      <p key={row.key} className={`text-sm ${c.textSecondary}`}>
                        <span className={`font-semibold ${c.text}`}>{row.label}:</span> {results.the_value_offer[row.key]}
                      </p>
                    ))}
                  </div>
                )}
              </div>
            )}

            {/* Do today */}
            {results.the_one_thing_today && (
              <div className={`rounded-2xl border-2 p-5 ${isDark ? 'border-[#2c4a6e] bg-[#2c4a6e]/10' : 'border-[#2c4a6e] bg-[#e8eef5]'}`}>
                <p className={`text-xs font-black uppercase tracking-widest mb-2 ${isDark ? 'text-[#a8b9ce]' : 'text-[#2c4a6e]'}`}>⚡ Do Today — First Step</p>
                <p className={`text-sm font-medium ${c.text}`}>{results.the_one_thing_today}</p>
              </div>
            )}

            <div className="flex items-center gap-3 flex-wrap">
              <ActionBar content={buildText()} title="Gravity Well" />
              <button onClick={() => { setResults(null); setTargetDescription(''); setTargetType(''); setWhyThemContext(''); setYourBackground(''); }}
                className={`px-4 py-2 rounded-lg text-sm font-medium ${c.btnSecondary}`}>
                🔄 New Target
              </button>
            </div>

            <div className={`rounded-xl border p-4 ${c.cardAlt} ${c.cardBorder}`}>
              <p className={`text-xs font-semibold uppercase tracking-wide mb-3 ${c.textMuted}`}>Related tools</p>
              <div className="flex flex-wrap gap-2">
                {[
                  { id: 'LuckSurface', icon: '🧲', label: 'Expand your luck surface area' },
                  { id: 'MagicMouth', icon: '🎤', label: 'Script a difficult conversation' },
                  { id: 'ChaosPilot', icon: '🎰', label: 'Break out of your patterns' },
                ].map(r => (
                  <a key={r.id} href={`/tool/${r.id}`} target="_blank" rel="noopener noreferrer"
                    className={`px-3 py-1.5 rounded-lg text-xs font-medium transition-colors ${c.btnSecondary}`}>
                    {r.icon} {r.label}
                  </a>
                ))}
              </div>
            </div>
          </div>
        )}
      </div>
    </div>
  );
};

GravityWell.displayName = 'GravityWell';
export default GravityWell;
