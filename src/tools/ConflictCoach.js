import React, { useState, useEffect, useRef, useCallback } from 'react';
import { CopyBtn } from '../components/ActionButtons';
import { useRegisterActions } from '../components/ActionBarContext';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { usePersistentState } from '../hooks/usePersistentState';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const ConflictCoach = ({ tool }) => {
  const { callToolEndpoint, loading } = useClaudeAPI();
  const { isDark } = useTheme();

  // ─── Standard c object ───
  const c = {
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:         isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20' : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-500' : 'text-gray-400',
    labelText:     isDark ? 'text-zinc-200' : 'text-gray-700',
    accentTxt:     isDark ? 'text-cyan-400' : 'text-cyan-600',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white' : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200' : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200' : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200' : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200' : 'bg-red-50 border-red-200 text-red-800',
    required:      'text-red-500',
    // ─── Tool-specific semantic colors ───
    toast:             isDark ? 'bg-zinc-700 text-zinc-100 border border-zinc-600' : 'bg-white text-gray-900 border border-gray-200',
    manipulation:      isDark ? 'bg-red-950/30 border-red-800' : 'bg-red-50 border-red-300',
    manipulationInner: isDark ? 'bg-red-900/20' : 'bg-red-100/50',
    highlight:         isDark ? 'bg-cyan-900/20 border-cyan-700' : 'bg-cyan-50 border-cyan-300',
    historyPanel:      isDark ? 'bg-zinc-800/60 border-zinc-700' : 'bg-white border-gray-200',
    historyItem:       isDark ? 'bg-zinc-700/50' : 'bg-gray-50',
    draftBox:          isDark ? 'border-red-800 bg-red-900/10' : 'border-red-300 bg-red-50',
    draftFlag:         isDark ? 'bg-amber-900/20' : 'bg-amber-50',
    strategySelected:  isDark ? 'border-cyan-500 bg-cyan-900/20' : 'border-cyan-500 bg-cyan-50',
    strategyHover:     isDark ? 'border-zinc-600 hover:border-zinc-500' : 'border-gray-200 hover:border-gray-300',
    toneBadge:         isDark ? 'bg-zinc-700 text-zinc-300' : 'bg-gray-100 text-gray-600',
    lockIcon:          isDark ? 'text-red-400' : 'text-red-600',
    unlockIcon:        isDark ? 'text-emerald-400' : 'text-emerald-600',
    strategyText:      isDark ? 'bg-zinc-700/50' : 'bg-gray-50',
    toneSlider:        isDark ? 'bg-zinc-700/50' : 'bg-gray-50',
    toneAdjusted:      isDark ? 'bg-zinc-700/30' : 'bg-gray-50',
    apologyBox:        isDark ? 'bg-cyan-900/20' : 'bg-cyan-50',
    landminePhrase:    isDark ? 'bg-red-900/20' : 'bg-red-50',
    escalationScript:  isDark ? 'bg-amber-900/10' : 'bg-amber-50',
    followupMe:        isDark ? 'bg-zinc-700/50' : 'bg-gray-100',
    confirmModal:      isDark ? 'bg-zinc-700/50' : 'bg-gray-100',
    goalActive:        isDark ? 'border-cyan-500 bg-cyan-900/30 text-cyan-200' : 'border-cyan-600 bg-cyan-100 text-cyan-900',
    goalInactive:      isDark ? 'border-zinc-600 text-zinc-400 hover:border-zinc-500' : 'border-gray-300 text-gray-500 hover:border-gray-400',
    threadContainer:   isDark ? 'bg-zinc-800/50 border-zinc-700' : 'bg-gray-50 border-gray-200',
    threadMsgMe:       isDark ? 'bg-cyan-900/30 border-cyan-700' : 'bg-cyan-50 border-cyan-200',
    threadMsgThem:     isDark ? 'bg-zinc-700/50 border-zinc-600' : 'bg-white border-gray-200',
  };
  c.textMuteded = c.textMuted;
  c.label = c.labelText;

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';

  // Emotion-specific active state colors (tool-specific: each emotion has a semantically distinct color)
  const emotionActive = {
    angry:      isDark ? 'border-red-500 bg-red-900/30'      : 'border-red-500 bg-red-100',
    hurt:       isDark ? 'border-sky-500 bg-sky-900/30'      : 'border-sky-500 bg-sky-100',
    defensive:  isDark ? 'border-amber-500 bg-amber-900/30'  : 'border-amber-500 bg-amber-100',
    frustrated: isDark ? 'border-amber-500 bg-amber-900/30': 'border-amber-500 bg-amber-100',
    calm:       isDark ? 'border-green-500 bg-green-900/30'  : 'border-green-500 bg-green-100',
    confused:   isDark ? 'border-cyan-500 bg-cyan-900/30': 'border-cyan-500 bg-cyan-100',
  };
  const emotionInactive = isDark ? 'border-zinc-700' : 'border-slate-200';

  // Temperature color map
  const tempColor = (t) => {
    switch (t?.toLowerCase()) {
      case 'high':   return isDark ? 'bg-red-900/30 border-red-700 text-red-300'         : 'bg-red-100 border-red-400 text-red-800';
      case 'medium': return isDark ? 'bg-amber-900/30 border-amber-700 text-amber-300'   : 'bg-amber-100 border-amber-400 text-amber-800';
      case 'low':    return isDark ? 'bg-emerald-900/30 border-emerald-700 text-emerald-300' : 'bg-emerald-100 border-emerald-400 text-emerald-800';
      default: return c.highlight;
    }
  };
  const tempEmoji = (t) => ({ high: '🔥', medium: '⚠️', low: '✅' })[t?.toLowerCase()] ?? '🌡️';

  // History temperature badge (dark-mode aware)
  const histTempBadge = (t) => {
    if (t === 'high')   return isDark ? 'bg-red-900/40 text-red-300'     : 'bg-red-100 text-red-700';
    if (t === 'medium') return isDark ? 'bg-amber-900/40 text-amber-300' : 'bg-amber-100 text-amber-700';
    return isDark ? 'bg-emerald-900/40 text-emerald-300' : 'bg-emerald-100 text-emerald-700';
  };

  // ─── Persisted state ───
  const [receivedMessage, setReceivedMessage] = usePersistentState('cc-message', '');
  const [relationship,    setRelationship]    = usePersistentState('cc-relationship', 'Friend');
  const [personLabel,     setPersonLabel]     = usePersistentState('cc-person', '');
  const [actualGoal,      setActualGoal]      = usePersistentState('cc-goal', '');
  const [results,         setResults]         = usePersistentState('cc-results', null);
  const [conflictHistory, setConflictHistory] = usePersistentState('cc-history', []);

  // ─── Session state ───
  const [emotionalState, setEmotionalState] = useState({ angry: false, hurt: false, defensive: false, frustrated: false, calm: false, confused: false });
  const [goals,          setGoals]          = useState({ resolve: false, boundary: false, disengage: false, validate: false, schedule_talk: false });
  const [userDraft,      setUserDraft]      = useState('');
  const [error,          setError]          = useState('');
  const [toast,          setToast]          = useState(null);
  const toastTimerRef = useRef(null);
  const mountedRef   = useRef(true);
  const showToast = (msg, dur = 4000) => {
    if (!mountedRef.current) return;
    if (toastTimerRef.current) clearTimeout(toastTimerRef.current);
    setToast(msg);
    toastTimerRef.current = setTimeout(() => { if (mountedRef.current) setToast(null); }, dur);
  };

  const [mandatoryDelay,        setMandatoryDelay]        = useState(false);
  const [delayEndTime,          setDelayEndTime]          = useState(null);
  const [now,                   setNow]                   = useState(Date.now);
  const delayActive  = delayEndTime !== null && now < delayEndTime;
  const delaySeconds = delayEndTime !== null ? Math.max(0, Math.round((delayEndTime - now) / 1000)) : 0;
  const [selectedResponse,      setSelectedResponse]      = useState(null);
  const [showConfirmSend,       setShowConfirmSend]       = useState(false);
  const [threadMessages,        setThreadMessages]        = useState([]);
  const [threadInput,           setThreadInput]           = useState('');
  const [threadSender,          setThreadSender]          = useState('them');
  const [useThread,             setUseThread]             = useState(false);
  const [followupQuestion,      setFollowupQuestion]      = useState('');
  const [followupHistory,       setFollowupHistory]       = useState([]);
  const [followupLoading,       setFollowupLoading]       = useState(false);
  const [selectedStrategyIdx,   setSelectedStrategyIdx]   = useState(null);
  const [toneLevel,             setToneLevel]             = useState(50);
  const [adjustedResponse,      setAdjustedResponse]      = useState(null);
  const [toneLoading,           setToneLoading]           = useState(false);
  const [showHistory,           setShowHistory]           = useState(false);

  const followupRef    = useRef(null);
  const resultsRef     = useRef(null);
  const submitRef      = useRef(null);
  const canSubmitRef   = useRef(false);

  const relationshipOptions = ['Partner', 'Family', 'Friend', 'Coworker', 'Ex', 'Customer', 'Other'];

  useEffect(() => {
    if (!delayEndTime) return;
    const tick = () => {
      if (!mountedRef.current) return;
      const remaining = delayEndTime - Date.now();
      if (remaining <= 0) {
        setDelayEndTime(null);
        setNow(Date.now());
        showToast('⏰ Cooling period complete.', 6000);
      } else {
        setNow(Date.now());
      }
    };
    const iv = setInterval(tick, 1000);
    return () => clearInterval(iv);
  }, [delayEndTime]); // eslint-disable-line react-hooks/exhaustive-deps

  // ─── Cleanup all timers on unmount ───
  useEffect(() => {
    mountedRef.current = true;
    return () => {
      mountedRef.current = false;
      if (toastTimerRef.current) clearTimeout(toastTimerRef.current);
    };
  }, []);

  const formatTime = (s) => `${Math.floor(s / 60)}:${(s % 60).toString().padStart(2, '0')}`;

  // ─── Core actions ───
  const handleAnalyze = async () => {
    const msg = useThread
      ? threadMessages.map(m => `[${m.sender}]: ${m.text}`).join('\n')
      : receivedMessage.trim();
    if (!msg || msg.length < 10) { setError('Paste the message (10+ chars) or add thread messages'); return; }

    setError(''); setResults(null); setFollowupHistory([]); setSelectedStrategyIdx(null); setAdjustedResponse(null);
    try {
      const data = await callToolEndpoint('conflict-coach', {
        receivedMessage: msg, relationship,
        emotionalState: Object.keys(emotionalState).filter(k => emotionalState[k]),
        goals: Object.keys(goals).filter(k => goals[k]),
        userDraft: userDraft.trim(), actualGoal: actualGoal.trim(),
        isThread: useThread, personLabel: personLabel.trim() || null,
      });
      setResults(data);
      if (data.message_analysis?.emotional_temperature === 'high' || emotionalState.angry || emotionalState.defensive) {
        setMandatoryDelay(true);
        if (!delayActive) { setDelayEndTime(Date.now() + 600 * 1000); }
      }
      handleSaveConflict(data);
    } catch (err) { setError(err.message || 'Analysis failed.'); }
  };

  const handleEmotionToggle = (e) => { setEmotionalState(p => ({ ...p, [e]: !p[e] })); };
  const handleGoalToggle    = (g) => setGoals(p => ({ ...p, [g]: !p[g] }));

  const handleCopyResponse = (text) => {
    if (mandatoryDelay && delayActive) { showToast('⏸️ Wait for cooling period to end.'); return; }
    setSelectedResponse(text); setShowConfirmSend(true);
  };
  const handleConfirmCopy = async () => {
    if (!selectedResponse) return;
    try {
      await navigator.clipboard.writeText(`${selectedResponse}${BRAND}`);
      setShowConfirmSend(false); showToast('✅ Copied. Breathe before you send.'); setSelectedResponse(null);
    } catch { showToast('Copy failed.'); }
  };

  const handleReset = () => {
    setReceivedMessage(''); setRelationship('Friend');
    setEmotionalState({ angry: false, hurt: false, defensive: false, frustrated: false, calm: false, confused: false });
    setGoals({ resolve: false, boundary: false, disengage: false, validate: false, schedule_talk: false });
    setUserDraft(''); setResults(null); setError('');
    setMandatoryDelay(false); setDelayEndTime(null);
    setSelectedResponse(null); setActualGoal('');
    setShowConfirmSend(false);
    setThreadMessages([]); setFollowupHistory([]); setSelectedStrategyIdx(null); setAdjustedResponse(null);
  };

  // ─── Thread builder ───
  const handleAddThreadMessage = () => {
    if (!threadInput.trim()) return;
    setThreadMessages(p => [...p, { sender: threadSender, text: threadInput.trim(), id: Date.now() }]);
    setThreadInput(''); setThreadSender(threadSender === 'them' ? 'me' : 'them');
  };
  const handleRemoveThreadMessage = (id) => setThreadMessages(p => p.filter(m => m.id !== id));

  // ─── Follow-up coaching ───
  const handleFollowup = async () => {
    if (!followupQuestion.trim() || !results) return;
    setFollowupLoading(true); const q = followupQuestion.trim(); setFollowupQuestion('');
    try {
      const data = await callToolEndpoint('conflict-coach/followup', {
        question: q, originalAnalysis: results, relationship,
        receivedMessage: receivedMessage.trim(), actualGoal: actualGoal.trim(),
        personLabel: personLabel.trim() || null,
      });
      setFollowupHistory(p => [...p, { question: q, answer: data.answer }]);
      setTimeout(() => followupRef.current?.scrollIntoView({ behavior: 'smooth' }), 100);
    } catch { showToast('❌ Follow-up failed.'); }
    setFollowupLoading(false);
  };

  // ─── Tone adjustment ───
  const handleAdjustTone = async () => {
    if (selectedStrategyIdx === null || !results?.response_strategies?.[selectedStrategyIdx]) return;
    setToneLoading(true);
    const strategy = results.response_strategies[selectedStrategyIdx];
    try {
      const data = await callToolEndpoint('conflict-coach/adjust-tone', {
        originalResponse: strategy.response_text, originalStrategy: strategy.strategy,
        toneLevel, relationship, receivedMessage: receivedMessage.trim(), actualGoal: actualGoal.trim(),
      });
      setAdjustedResponse(data);
    } catch { showToast('❌ Tone adjustment failed.'); }
    setToneLoading(false);
  };

  // ─── Conflict history ───
  const handleSaveConflict = (data) => {
    const entry = {
      id: Date.now().toString(), date: new Date().toISOString(),
      person: personLabel.trim() || relationship, relationship,
      topic: receivedMessage.trim().slice(0, 80),
      preview: data?.message_analysis?.primary_emotion_detected || '',
      temperature: data?.message_analysis?.emotional_temperature || 'unknown',
      tactics: data?.manipulation_tactics?.map(t => t.tactic) || [],
    };
    setConflictHistory(p => [entry, ...p].slice(0, 6));
  };

  const detectPatterns = () => {
    if (conflictHistory.length < 2) return null;
    const person = personLabel.trim() || relationship;
    const withPerson = conflictHistory.filter(e => e.person === person);
    if (withPerson.length < 2) return null;
    const thirtyDays = new Date(); thirtyDays.setDate(thirtyDays.getDate() - 30);
    const recent = withPerson.filter(e => new Date(e.date) > thirtyDays);
    if (recent.length < 2) return null;
    const allTactics = recent.flatMap(e => e.tactics || []);
    const tacticCounts = {}; allTactics.forEach(t => { tacticCounts[t] = (tacticCounts[t] || 0) + 1; });
    const repeatedTactics = Object.entries(tacticCounts).filter(([, ct]) => ct >= 2).map(([t]) => t);
    return { count: recent.length, person, repeatedTactics, timeframe: '30 days' };
  };

  // ─── Build full text ───
  const buildFullText = useCallback(() => {
    if (!results) return '';
    const l = ['💬 CONFLICT COACH', '═'.repeat(40)];
    if (results.message_analysis) {
      l.push('', `🌡️ TEMPERATURE: ${results.message_analysis.emotional_temperature?.toUpperCase()}`, `Emotion: ${results.message_analysis.primary_emotion_detected}`);
      if (results.message_analysis.triggers_identified?.length) l.push(`Triggers: ${results.message_analysis.triggers_identified.join('; ')}`);
      if (results.message_analysis.underlying_need) l.push(`Need: ${results.message_analysis.underlying_need}`);
    }
    if (results.manipulation_tactics?.length) { l.push('', '🎭 MANIPULATION DETECTED:'); results.manipulation_tactics.forEach(t => l.push(`  ⚠️ ${t.tactic}: ${t.description}`)); }
    if (results.goal_reality_check) { l.push('', '🎯 GOAL CHECK', results.goal_reality_check.assessment); if (results.goal_reality_check.alternative_approach) l.push(`Alternative: ${results.goal_reality_check.alternative_approach}`); }
    if (results.draft_analysis) { l.push('', '🚨 DRAFT ANALYSIS', results.draft_analysis.overall_assessment); if (results.draft_analysis.escalation_risk) l.push(`Escalation: ${results.draft_analysis.escalation_risk.level?.toUpperCase()} — ${results.draft_analysis.escalation_risk.why}`); }
    if (results.response_strategies?.length) { l.push('', '💡 STRATEGIES'); results.response_strategies.forEach((s, i) => { l.push(`\n  ${i + 1}. ${s.strategy} (${s.tone})`, `  "${s.response_text}"`, s.risks ? `  Risk: ${s.risks}` : ''); }); }
    if (results.what_NOT_to_say?.length) { l.push('', '❌ LANDMINES'); results.what_NOT_to_say.forEach(i => l.push(`  ❌ "${i.phrase}" — ${i.why_avoid}`)); }
    if (results.timing_landmines?.length) { l.push('', '⏰ TIMING'); results.timing_landmines.forEach(t => l.push(`  ⏰ ${t}`)); }
    if (followupHistory.length) { l.push('', 'FOLLOW-UP:'); followupHistory.forEach(f => l.push(`  Q: ${f.question}`, `  A: ${f.answer}`, '')); }
    return l.join('\n') + BRAND;
  }, [results, followupHistory]); // eslint-disable-line react-hooks/exhaustive-deps

  useRegisterActions(buildFullText, tool?.title ?? 'Conflict Coach');

  // ─── Scroll to results ───
  useEffect(() => {
    if (!results) return;
    const t = setTimeout(() => resultsRef.current?.scrollIntoView({ behavior: 'smooth', block: 'start' }), 100);
    return () => clearTimeout(t);
  }, [results]);

  // ─── Keyboard refs (assigned on every render so handler sees fresh state) ───
  submitRef.current    = handleAnalyze;
  canSubmitRef.current = !loading &&
    (useThread ? threadMessages.length > 0 : receivedMessage.trim().length >= 10);

  // ─── Document-level Ctrl+Enter submit ───
  useEffect(() => {
    const handler = (e) => {
      const tag = e.target?.tagName;
      if (tag === 'SELECT') return;
      if ((e.ctrlKey || e.metaKey) && e.key === 'Enter') {
        if (!loading && canSubmitRef.current) {
          e.preventDefault();
          submitRef.current?.();
        }
      }
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
  }, [loading]);

  const exampleMessage = "I can't believe you did that again. You never think about how your actions affect me. This is exactly why we have problems. I'm so sick of this.";
  const hasContent = !!(results || receivedMessage.trim());

  // ═══════════════════════════════════════════════════════════════
  // RENDER
  // ═══════════════════════════════════════════════════════════════
  return (
    <div className={`space-y-4 ${c.text}`}>

      {/* Toast */}
      {toast && (
        <div className={`fixed top-4 left-1/2 -translate-x-1/2 z-50 px-6 py-3 rounded-lg shadow-lg text-sm font-medium max-w-md text-center ${c.toast}`}>
          {toast}
        </div>
      )}

      {/* ═══ INPUT CARD ═══ */}
      <div className={`${c.card} border ${c.border} rounded-xl shadow-lg p-5`}>
        {/* Standard header */}
        <div className="mb-4 pb-3 border-b border-zinc-500">
          <div className="flex items-center justify-between">
            <div>
              <h2 className={`text-xl font-bold ${c.text} flex items-center gap-2`}>
                <span className="mr-2">{tool?.icon ?? '💬'}</span>{tool?.title ?? 'Conflict Coach'}
              </h2>
              <p className={`text-sm ${c.textSecondary}`}>{tool?.tagline ?? 'Stop, breathe, and craft the right response'}</p>
            </div>
            <div className="flex items-center gap-2">
              {hasContent && (
                <button onClick={handleReset} className={`${c.btnSecondary} px-3 py-1.5 rounded-lg text-xs`}>
                  ↺ Start Over
                </button>
              )}
              <button onClick={() => setShowHistory(!showHistory)} className={`${c.btnSecondary} px-3 py-1.5 rounded-lg text-xs`}>
                📋 History
              </button>
            </div>
          </div>
        </div>

        {/* Warning notice */}
        <div className={`${c.warning} border-l-4 rounded-r-lg p-4 mb-4`}>
          <p className={`text-sm font-semibold ${c.text}`}>⚠️ Before you send that message — STOP.</p>
          <p className={`text-xs ${c.textSecondary} mt-1`}>Includes cooling periods, manipulation detection, draft analysis, and goal clarification. Not a replacement for therapy. For abusive situations, seek professional help.</p>
        </div>

        {/* History panel */}
        {showHistory && (
          <div className={`mb-5 p-4 rounded-lg border max-h-64 overflow-y-auto ${c.historyPanel}`}>
            <h4 className={`font-bold mb-2 ${c.text}`}>📋 Past Conflicts</h4>
            {conflictHistory.length > 0 ? (
              <div className="space-y-2">
                {conflictHistory.map(e => (
                  <div key={e.id} className={`flex items-center justify-between p-2 rounded ${c.historyItem}`}>
                    <div className="min-w-0 flex-1">
                      <div className="flex items-center gap-2">
                        <span className={`text-xs px-1.5 py-0.5 rounded font-semibold ${histTempBadge(e.temperature)}`}>{e.temperature}</span>
                        <span className={`text-xs ${c.textMuteded}`}>{e.person} · {new Date(e.date).toLocaleDateString()}</span>
                      </div>
                      <p className={`text-sm truncate ${c.text}`}>{e.topic}</p>
                      {e.tactics?.length > 0 && <p className={`text-xs ${c.textMuteded}`}>Tactics: {e.tactics.join(', ')}</p>}
                    </div>
                  </div>
                ))}
              </div>
            ) : <p className={`text-sm ${c.textMuteded}`}>No past conflicts saved.</p>}
          </div>
        )}

        <div className="space-y-5">

          {/* Mandatory delay — below header so tool name always renders first */}
          {delayActive && (
            <div className={`${c.danger} border-4 rounded-xl p-6 text-center`}>
              <span className="text-6xl block mb-4">🔒</span>
              <h3 className={`text-2xl font-bold mb-2 ${c.text}`}>Mandatory Cooling Period</h3>
              <div className={`text-5xl font-mono font-bold mb-3 ${c.text}`}>{formatTime(delaySeconds)}</div>
              <p className={`text-sm ${c.text}`}>High emotions detected. Responses locked. Breathe.</p>
            </div>
          )}

          {/* Person + Relationship */}
          <div className="grid grid-cols-1 md:grid-cols-2 gap-4">
            <div>
              <label className={`block text-sm font-medium ${c.text} mb-1`}>Who is this person? (optional)</label>
              <input type="text" value={personLabel} onChange={e => setPersonLabel(e.target.value)}
                placeholder="Name or nickname for tracking"
                className={`w-full p-3 border rounded-lg ${c.input}`} />
            </div>
            <div>
              <label className={`block text-sm font-medium ${c.text} mb-1`}>Relationship</label>
              <select value={relationship} onChange={e => setRelationship(e.target.value)}
                className={`w-full p-3 border rounded-lg ${c.input}`}>
                {relationshipOptions.map(o => <option key={o} value={o}>{o}</option>)}
              </select>
            </div>
          </div>

          {/* Thread toggle */}
          <label className={`flex items-center gap-2 cursor-pointer text-sm ${c.text}`}>
            <input type="checkbox" checked={useThread} onChange={e => setUseThread(e.target.checked)} className="w-4 h-4" />
            📜 Build conversation thread (multiple back-and-forth messages)
          </label>

          {/* Thread builder OR single message */}
          {useThread ? (
            <div>
              <label className={`block text-sm font-medium ${c.text} mb-2`}>Build the conversation</label>
              {threadMessages.length > 0 && (
                <div className={`mb-3 p-3 rounded-lg border ${c.threadContainer} max-h-48 overflow-y-auto space-y-2`}>
                  {threadMessages.map(m => (
                    <div key={m.id} className={`flex ${m.sender === 'me' ? 'justify-end' : 'justify-start'}`}>
                      <div className={`max-w-[75%] p-2 rounded-lg text-sm border ${m.sender === 'me' ? c.threadMsgMe : c.threadMsgThem}`}>
                        <p className={`text-xs font-bold mb-0.5 ${c.textMuteded}`}>{m.sender === 'me' ? 'You' : 'Them'}</p>
                        <p className={c.text}>{m.text}</p>
                        <button onClick={() => handleRemoveThreadMessage(m.id)} className={`text-xs ${c.textMuteded} mt-1 hover:underline`}>✕ remove</button>
                      </div>
                    </div>
                  ))}
                </div>
              )}
              <div className="flex gap-2">
                <select value={threadSender} onChange={e => setThreadSender(e.target.value)} className={`p-2 border rounded text-sm ${c.input} w-24`}>
                  <option value="them">Them</option><option value="me">Me</option>
                </select>
                <input type="text" value={threadInput} onChange={e => setThreadInput(e.target.value)}
                  placeholder="Type message…"
                  className={`flex-1 p-2 border rounded text-sm ${c.input}`}
                  onKeyDown={e => { if (e.key === 'Enter') handleAddThreadMessage(); }} />
                <button onClick={handleAddThreadMessage} className={`${c.btnPrimary} px-3 py-2 rounded text-sm`}>Add</button>
              </div>
              <p className={`text-xs ${c.textMuteded} mt-1`}>Add messages in order. The AI analyzes the full dynamic.</p>
            </div>
          ) : (
            <div>
              <div className="flex justify-between mb-1">
                <label className={`text-sm font-medium ${c.text}`}>The message you received <span className={c.required}>*</span></label>
                <button onClick={() => setReceivedMessage(exampleMessage)} className={`text-xs ${linkStyle}`}>Example</button>
              </div>
              <textarea value={receivedMessage} onChange={e => setReceivedMessage(e.target.value)}
                placeholder="Paste the tense/upsetting message… (Ctrl+Enter to analyze)"
                className={`w-full p-4 border rounded-lg ${c.input}`} rows={4} />
            </div>
          )}

          {/* Draft */}
          <div className={`border-2 rounded-lg p-4 ${c.draftBox}`}>
            <div className="flex items-center gap-2 mb-1">
              <span>🛑</span>
              <label className={`text-sm font-bold ${c.text}`}>What you're tempted to say (CRITICAL)</label>
            </div>
            <textarea value={userDraft} onChange={e => setUserDraft(e.target.value)}
              placeholder="Your reactive response… be honest"
              className={`w-full p-3 border rounded-lg ${c.input}`} rows={3} />
            <p className={`text-xs ${c.textMuteded} mt-1`}>We catch escalation BEFORE it happens. Honesty helps.</p>
          </div>

          {/* Emotions */}
          <div className="border-2 rounded-lg p-4">
            <label className={`block text-sm font-bold ${c.text} mb-2`}>How are you feeling RIGHT NOW?</label>
            <div className="grid grid-cols-2 md:grid-cols-3 gap-2">
              {[
                ['angry',      '😠 Angry'],
                ['hurt',       '💔 Hurt'],
                ['defensive',  '🛡️ Defensive'],
                ['frustrated', '😤 Frustrated'],
                ['calm',       '😌 Calm'],
                ['confused',   '😕 Confused'],
              ].map(([key, label]) => (
                <label key={key} className={`p-2.5 rounded-lg border-2 cursor-pointer ${emotionalState[key] ? emotionActive[key] : emotionInactive}`}>
                  <input type="checkbox" checked={emotionalState[key]} onChange={() => handleEmotionToggle(key)} className="sr-only" />
                  <span className={`text-sm ${emotionalState[key] ? 'font-bold' : ''} ${c.text}`}>{label}</span>
                </label>
              ))}
            </div>

          </div>

          {/* Goals */}
          <div>
            <label className={`block text-sm font-medium ${c.text} mb-2`}>Goal</label>
            <div className="grid grid-cols-1 md:grid-cols-2 gap-2">
              {[
                ['resolve',       'Resolve the issue'],
                ['boundary',      'Set a boundary'],
                ['disengage',     'Disengage gracefully'],
                ['validate',      'Validate without conceding'],
                ['schedule_talk', 'Schedule face-to-face'],
              ].map(([key, label]) => (
                <label key={key} className={`p-2.5 rounded-lg border-2 cursor-pointer flex items-center gap-2 ${goals[key] ? c.goalActive : c.goalInactive}`}>
                  <input type="checkbox" checked={goals[key]} onChange={() => handleGoalToggle(key)} className="w-4 h-4" />
                  <span className={`text-sm ${c.text}`}>{label}</span>
                </label>
              ))}
            </div>
          </div>

          {/* Pre-result cross-ref */}
          <p className={`text-xs text-center ${c.textMuteded}`}>
            Need to rehearse? <a href="/DifficultTalkCoach" className={linkStyle}>🎭 Difficult Talk Rehearser</a> lets you practice.
          </p>

          <div className="flex gap-3">
            <button onClick={handleAnalyze} disabled={loading}
              className={`flex-1 ${c.btnPrimary} disabled:opacity-40 font-medium py-3 rounded-lg flex items-center justify-center gap-2 min-h-[48px]`}>
              {loading
                ? <><span className="animate-spin inline-block">{tool?.icon ?? '📱'}</span> Analyzing...</>
                : <><span>{tool?.icon ?? '📱'}</span> Analyze</>}
            </button>
          </div>
          {error && (
            <div className={`p-4 ${c.danger} border rounded-lg flex items-start gap-3`}>
              <span>⚠️</span><p className="text-sm">{error}</p>
            </div>
          )}
        </div>
      </div>

      {/* ═══════════════════════════════════════════════════════
          RESULTS
          ═══════════════════════════════════════════════════════ */}
      {results && (
        <div ref={resultsRef} className="space-y-4">

          {/* Pattern Alert — shown inside results so tool title always appears first */}
          {(() => {
            const p = detectPatterns();
            return p ? (
              <div className={`${c.manipulation} border-2 rounded-xl p-5`}>
                <h3 className={`font-bold mb-2 ${c.text}`}>🔗 Pattern Detected</h3>
                <p className={`text-sm ${c.textSecondary}`}>You've logged <strong>{p.count} conflicts</strong> with <strong>{p.person}</strong> in the past {p.timeframe}.</p>
                {p.repeatedTactics.length > 0 && <p className={`text-sm mt-1 ${c.textSecondary}`}>Recurring tactics: <strong>{p.repeatedTactics.join(', ')}</strong>. This is a pattern, not an incident.</p>}
                <p className={`text-xs mt-2 ${c.textMuted}`}>Consider whether this relationship dynamic needs professional support.</p>
              </div>
            ) : null;
          })()}

          {/* Temperature */}
          {results.message_analysis && (
            <div className={`${c.card} border ${c.border} rounded-xl shadow-lg p-6 text-center`}>
              <div className="text-6xl mb-3">{tempEmoji(results.message_analysis.emotional_temperature)}</div>
              <div className={`text-2xl font-black ${c.text} mb-1`}>{results.message_analysis.emotional_temperature?.toUpperCase()} TEMPERATURE</div>
              <p className={`text-sm ${c.textSecondary}`}>Primary: <strong>{results.message_analysis.primary_emotion_detected}</strong></p>
              {results.message_analysis.underlying_need && <p className={`text-xs ${c.textMuteded} mt-1`}>Need: {results.message_analysis.underlying_need}</p>}
            </div>
          )}

          {/* Manipulation Detector */}
          {results.manipulation_tactics?.length > 0 && (
            <div className={`${c.manipulation} border-2 rounded-xl p-6`}>
              <h3 className={`text-lg font-bold mb-4 flex items-center gap-2 ${c.text}`}><span>🎭</span> Manipulation Tactics Detected</h3>
              <p className={`text-sm mb-4 ${c.textSecondary}`}>These patterns in their message may be attempts to control the conversation:</p>
              <div className="space-y-3">
                {results.manipulation_tactics.map((t, i) => (
                  <div key={i} className={`p-4 rounded-lg ${c.manipulationInner}`}>
                    <div className="flex items-center gap-2 mb-1"><span className="text-lg">{t.icon || '🚩'}</span><h4 className={`font-bold ${c.text}`}>{t.tactic}</h4></div>
                    <p className={`text-sm mb-2 ${c.textSecondary}`}>{t.description}</p>
                    {t.example_phrase && <p className={`text-xs italic ${c.textMuteded}`}>From their message: "{t.example_phrase}"</p>}
                    {t.healthy_response && <p className={`text-sm mt-2 font-semibold ${c.text}`}>💡 Healthy response: {t.healthy_response}</p>}
                  </div>
                ))}
              </div>
            </div>
          )}

          {/* Goal Reality Check */}
          {results.goal_reality_check && (
            <div className={`${c.highlight} border-l-4 rounded-r-lg p-5`}>
              <h3 className={`font-bold mb-2 ${c.text}`}>🎯 Reality Check</h3>
              {actualGoal && <p className={`text-sm mb-2 ${c.text}`}><strong>You want:</strong> "{actualGoal}"</p>}
              <p className={`text-sm ${c.text}`}>{results.goal_reality_check.assessment}</p>
              {results.goal_reality_check.will_this_message_achieve_it !== undefined && (
                <div className={`p-3 rounded mt-2 border ${results.goal_reality_check.will_this_message_achieve_it ? c.success : c.danger}`}>
                  <p className="text-sm font-bold">{results.goal_reality_check.will_this_message_achieve_it ? '✅ Responses CAN help achieve this' : '❌ Messaging now UNLIKELY to achieve this'}</p>
                  {results.goal_reality_check.alternative_approach && <p className="text-sm mt-1"><strong>Better:</strong> {results.goal_reality_check.alternative_approach}</p>}
                </div>
              )}
            </div>
          )}

          {/* Triggers */}
          {results.message_analysis?.triggers_identified?.length > 0 && (
            <div className={`${tempColor(results.message_analysis.emotional_temperature)} border-l-4 rounded-r-lg p-5`}>
              <h3 className={`font-bold mb-3 ${c.text}`}>{tempEmoji(results.message_analysis.emotional_temperature)} Triggers</h3>
              <ul className={`text-sm space-y-1 ${c.text}`}>{results.message_analysis.triggers_identified.map((t, i) => <li key={i}>• "{t}"</li>)}</ul>
              {results.message_analysis.communication_style && <p className={`text-sm mt-2 ${c.textMuteded}`}>Style: {results.message_analysis.communication_style}</p>}
            </div>
          )}

          {/* Draft Analysis */}
          {results.draft_analysis && userDraft && (
            <div className={`${c.card} border ${c.border} rounded-xl p-6`}>
              <h3 className={`text-lg font-bold mb-3 ${c.text}`}>🚨 Your Draft — Honest Analysis</h3>
              <p className={`text-sm mb-3 ${c.textSecondary}`}>{results.draft_analysis.overall_assessment}</p>
              {results.draft_analysis.tone_flags?.length > 0 && (
                <div className="mb-3">
                  <p className={`text-sm font-bold mb-2 ${c.text}`}>Tone Flags:</p>
                  {results.draft_analysis.tone_flags.map((f, i) => (
                    <div key={i} className={`p-2 rounded mb-1 ${c.draftFlag}`}><p className="text-sm"><strong>{f.flag}</strong>: {f.why_problematic}</p></div>
                  ))}
                </div>
              )}
              {results.draft_analysis.escalation_risk && (
                <div className={`p-3 rounded border ${results.draft_analysis.escalation_risk.level === 'high' || results.draft_analysis.escalation_risk.level === 'extreme' ? c.danger : c.warning}`}>
                  <p className="text-sm font-bold">⚠️ Escalation: {results.draft_analysis.escalation_risk.level?.toUpperCase()}</p>
                  <p className="text-sm">{results.draft_analysis.escalation_risk.why}</p>
                </div>
              )}
            </div>
          )}

          {/* Response Strategies + Tone Slider */}
          {results.response_strategies?.length > 0 && (
            <div className={`${c.card} border ${c.border} rounded-xl p-6`}>
              <h3 className={`text-lg font-bold ${c.text} mb-4 flex items-center gap-2`}>
                {delayActive ? <span className={c.lockIcon}>🔒</span> : <span className={c.unlockIcon}>🔓</span>} Strategies
                {delayActive && <span className={`text-sm font-normal ${c.textMuteded}`}>(Locked)</span>}
              </h3>
              <div className={`space-y-4 ${c.text}`}>
                {results.response_strategies.map((s, idx) => (
                  <div key={idx} className={`border-2 rounded-lg p-5 transition-colors ${delayActive ? 'opacity-50' : selectedStrategyIdx === idx ? c.strategySelected : c.strategyHover}`}>
                    <div className="flex items-start justify-between mb-2">
                      <div>
                        <h4 className={`font-bold ${c.text}`}>{s.strategy}</h4>
                        {s.tone && <span className={`text-xs px-2 py-0.5 rounded ${c.toneBadge}`}>Tone: {s.tone}</span>}
                      </div>
                      <button onClick={() => handleCopyResponse(s.response_text)} disabled={delayActive}
                        className={`${c.btnSecondary} px-3 py-1.5 rounded text-sm disabled:opacity-40`}>
                        {delayActive ? '🔒' : '📋'} Copy
                      </button>
                    </div>
                    <div className={`p-3 rounded ${c.strategyText} mb-2`}>
                      <p className={`${c.text} font-medium`}>"{s.response_text}"</p>
                    </div>
                    {s.what_this_does && <p className={`text-sm ${c.textSecondary} mb-1`}><strong>Effect:</strong> {s.what_this_does}</p>}
                    {s.risks && <p className={`text-sm ${c.textMuteded}`}><strong>⚠️ Risk:</strong> {s.risks}</p>}

                    {!delayActive && (
                      <div className="mt-3">
                        <button
                          onClick={() => { setSelectedStrategyIdx(selectedStrategyIdx === idx ? null : idx); setAdjustedResponse(null); setToneLevel(50); }}
                          className={`text-xs ${linkStyle}`}>
                          {selectedStrategyIdx === idx ? '▲ Hide tone slider' : '🎚️ Adjust tone'}
                        </button>
                        {selectedStrategyIdx === idx && (
                          <div className={`mt-2 p-3 rounded ${c.toneSlider}`}>
                            <div className="flex items-center gap-3 mb-2">
                              <span className="text-xs">🕊️ Gentle</span>
                              <input type="range" min="0" max="100" value={toneLevel}
                                onChange={e => setToneLevel(parseInt(e.target.value))} className="flex-1" />
                              <span className="text-xs">💪 Firm</span>
                            </div>
                            <button onClick={handleAdjustTone} disabled={toneLoading}
                              className={`${c.btnPrimary} px-4 py-2 rounded text-sm w-full disabled:opacity-40`}>
                              {toneLoading ? <span className="animate-spin inline-block">{tool?.icon ?? '📱'}</span> : `Regenerate at ${toneLevel}% firmness`}
                            </button>
                            {adjustedResponse && (
                              <div className="mt-2">
                                <div className={`p-3 rounded border ${c.border} ${c.toneAdjusted}`}>
                                  <p className={`text-sm ${c.text}`}>"{adjustedResponse.adjusted_text}"</p>
                                </div>
                                {adjustedResponse.tone_note && <p className={`text-xs mt-1 ${c.textMuteded}`}>{adjustedResponse.tone_note}</p>}
                                <button onClick={() => handleCopyResponse(adjustedResponse.adjusted_text)}
                                  className={`${c.btnSecondary} px-3 py-1 rounded text-xs mt-2`}>📋 Copy adjusted</button>
                              </div>
                            )}
                          </div>
                        )}
                      </div>
                    )}
                  </div>
                ))}
              </div>
            </div>
          )}

          {/* Apology Assessment */}
          {results.apology_assessment && (
            <div className={`${c.highlight} border-l-4 rounded-r-lg p-5`}>
              <h3 className={`font-bold mb-2 ${c.text}`}>🙏 Should You Apologize?</h3>
              <p className={`text-sm ${c.text}`}><strong>{results.apology_assessment.is_apology_appropriate ? 'Yes' : 'No'}</strong> — {results.apology_assessment.reasoning}</p>
              {results.apology_assessment.is_apology_appropriate && results.apology_assessment.suggested_apology && (
                <div className={`p-3 rounded mt-2 ${c.apologyBox}`}>
                  <p className={`text-sm ${c.text}`}>"{results.apology_assessment.suggested_apology}"</p>
                </div>
              )}
            </div>
          )}

          {/* Landmines */}
          {(results.what_NOT_to_say?.length > 0 || results.timing_landmines?.length > 0 || results.channel_landmines?.length > 0) && (
            <div className={`${c.danger} border-2 rounded-xl p-6`}>
              <h3 className={`text-lg font-bold mb-4 flex items-center gap-2 ${c.text}`}>💣 Landmines — Avoid These</h3>
              {results.what_NOT_to_say?.length > 0 && (
                <div className="mb-4">
                  <p className={`text-sm font-bold mb-2 ${c.text}`}>❌ Phrases:</p>
                  <div className="space-y-2">
                    {results.what_NOT_to_say.map((it, i) => (
                      <div key={i} className={`p-3 rounded ${c.landminePhrase}`}>
                        <p className={`font-semibold text-sm ${c.text}`}>"{it.phrase}"</p>
                        <p className={`text-sm ${c.textSecondary}`}>{it.why_avoid}</p>
                      </div>
                    ))}
                  </div>
                </div>
              )}
              {results.timing_landmines?.length > 0 && (
                <div className="mb-4">
                  <p className={`text-sm font-bold mb-2 ${c.text}`}>⏰ Timing:</p>
                  <ul className="space-y-1">{results.timing_landmines.map((t, i) => <li key={i} className={`text-sm ${c.textSecondary}`}>⏰ {t}</li>)}</ul>
                </div>
              )}
              {results.channel_landmines?.length > 0 && (
                <div>
                  <p className={`text-sm font-bold mb-2 ${c.text}`}>📱 Channel:</p>
                  <ul className="space-y-1">{results.channel_landmines.map((ch, i) => <li key={i} className={`text-sm ${c.textSecondary}`}>📱 {ch}</li>)}</ul>
                </div>
              )}
            </div>
          )}

          {/* If They Escalate */}
          {results.if_they_continue_escalating && (
            <div className={`${c.warning} border-l-4 rounded-r-lg p-5`}>
              <h3 className={`font-bold mb-2 ${c.text}`}>⚠️ If They Escalate</h3>
              <div className={`p-3 rounded border-2 ${c.border} mb-2 ${c.escalationScript}`}>
                <p className={`${c.text} font-medium`}>"{results.if_they_continue_escalating.script}"</p>
              </div>
              <p className={`text-sm font-bold ${c.text}`}>{results.if_they_continue_escalating.then_what}</p>
            </div>
          )}

          {/* Repair Strategy */}
          {results.repair_strategy_later && (
            <div className={`${c.success} border-l-4 rounded-r-lg p-5`}>
              <h3 className={`font-bold mb-1 ${c.text}`}>💚 Repair (Later)</h3>
              <p className={`text-sm ${c.text}`}>{results.repair_strategy_later}</p>
            </div>
          )}

          {/* Follow-up Q&A */}
          <div className={`${c.card} border ${c.border} rounded-xl p-5`}>
            <h3 className={`font-bold mb-3 ${c.text}`}>💬 Follow-up Coaching</h3>
            <p className={`text-xs mb-3 ${c.textMuteded}`}>"They responded with X, now what?" or "What if they bring up Y?"</p>
            {followupHistory.length > 0 && (
              <div className="space-y-3 mb-4">
                {followupHistory.map((f, i) => (
                  <div key={i} className="space-y-1">
                    <div className={`p-3 rounded ${c.followupMe}`}>
                      <p className={`text-xs font-semibold ${c.textMuteded}`}>You:</p>
                      <p className={`text-sm ${c.text}`}>{f.question}</p>
                    </div>
                    <div className={`p-3 rounded ${c.cardAlt}`}>
                      <p className={`text-sm ${c.textSecondary}`}>{f.answer}</p>
                    </div>
                  </div>
                ))}
              </div>
            )}
            <div className="flex gap-2" ref={followupRef}>
              <input type="text" value={followupQuestion} onChange={e => setFollowupQuestion(e.target.value)}
                placeholder="They said this back…"
                className={`flex-1 p-3 border rounded-lg ${c.input}`}
                onKeyDown={e => { if (e.key === 'Enter') handleFollowup(); }} />
              <button onClick={handleFollowup} disabled={followupLoading || !followupQuestion.trim()}
                className={`${c.btnPrimary} px-4 py-2 rounded-lg disabled:opacity-40`}>
                {followupLoading ? <span className="animate-spin inline-block">{tool?.icon ?? '📱'}</span> : '➤'}
              </button>
            </div>
          </div>

          {/* Cross-refs (post-result) */}
          <div className="text-center space-y-1">
            <p className={`text-xs ${c.textMuted}`}>Need a firm response? <a href="/VelvetHammer" className={linkStyle}>🔨 Velvet Hammer</a></p>
            {results.message_analysis?.emotional_temperature === 'high' && (
              <p className={`text-xs ${c.textMuted}`}>Still spiraling? → <a href="/SpiralStopper" className={linkStyle}>🌀 Spiral Stopper</a></p>
            )}
          </div>

          {/* AI disclaimer */}
          <div className={`p-4 rounded-xl text-center ${c.cardAlt}`}>
            <p className={`text-xs ${c.textMuteded}`}>💬 AI-generated guidance. Conflict dynamics are complex — trust your judgment. Not a substitute for professional support.</p>
          </div>
        </div>
      )}

      {/* Confirm Send Modal */}
      {showConfirmSend && selectedResponse && (
        <div className="fixed inset-0 bg-black/50 flex items-center justify-center p-4 z-50" onClick={() => setShowConfirmSend(false)}>
          <div className={`${c.card} border-2 border-cyan-500 rounded-xl p-6 max-w-md w-full`} onClick={e => e.stopPropagation()}>
            <h3 className={`text-lg font-bold mb-4 ${c.text}`}>⚠️ Are You Sure?</h3>
            <ul className={`text-sm space-y-1 mb-4 ${c.text}`}>
              <li>• Have I cooled down?</li>
              <li>• Will this help or hurt?</li>
              <li>• Am I responding or reacting?</li>
              <li>• Would I say this face-to-face?</li>
            </ul>
            <div className={`p-3 rounded ${c.confirmModal} mb-4`}>
              <p className={`text-sm ${c.text}`}>"{selectedResponse}"</p>
            </div>
            <div className="flex gap-3">
              <button onClick={handleConfirmCopy} className={`flex-1 ${c.btnPrimary} py-3 rounded-lg font-semibold`}>Yes, Copy</button>
              <button onClick={() => { setShowConfirmSend(false); setSelectedResponse(null); }} className={`flex-1 ${c.btnSecondary} py-3 rounded-lg`}>Wait, Think More</button>
            </div>
          </div>
        </div>
      )}
    </div>
  );
};

ConflictCoach.displayName = 'ConflictCoach';
export default ConflictCoach;
