// FORCE-DIRECTED LAYOUT (simple spring model)
// ════════════════════════════════════════════════════════════
const computeLayout = (nodes, edges, width, height) => {
  if (!nodes.length) return [];
  const pos = nodes.map((_, i) => ({
    x: width / 2 + (Math.cos(i * 2.399) * width * 0.35),
    y: height / 2 + (Math.sin(i * 2.399) * height * 0.35),
    vx: 0, vy: 0,
  }));
  const nodeIdx = {};
  nodes.forEach((n, i) => { nodeIdx[n.id] = i; });

  for (let iter = 0; iter < 80; iter++) {
    // Repulsion
    for (let i = 0; i < nodes.length; i++) {
      for (let j = i + 1; j < nodes.length; j++) {
        let dx = pos[i].x - pos[j].x, dy = pos[i].y - pos[j].y;
        let dist = Math.sqrt(dx * dx + dy * dy) || 1;
        let force = 4000 / (dist * dist);
        pos[i].vx += (dx / dist) * force;
        pos[i].vy += (dy / dist) * force;
        pos[j].vx -= (dx / dist) * force;
        pos[j].vy -= (dy / dist) * force;
      }
    }
    // Attraction along edges
    edges.forEach(e => {
      const si = nodeIdx[e.from], ti = nodeIdx[e.to];
      if (si === undefined || ti === undefined) return;
      let dx = pos[ti].x - pos[si].x, dy = pos[ti].y - pos[si].y;
      let dist = Math.sqrt(dx * dx + dy * dy) || 1;
      let force = (dist - 100) * 0.01;
      pos[si].vx += (dx / dist) * force;
      pos[si].vy += (dy / dist) * force;
      pos[ti].vx -= (dx / dist) * force;
      pos[ti].vy -= (dy / dist) * force;
    });
    // Apply velocity with damping
    const damping = 0.85;
    pos.forEach(p => {
      p.x += p.vx * damping;
      p.y += p.vy * damping;
      p.vx *= 0.8; p.vy *= 0.8;
      // Keep in bounds
      p.x = Math.max(50, Math.min(width - 50, p.x));
      p.y = Math.max(30, Math.min(height - 30, p.y));
    });
  }
  return pos;
};

