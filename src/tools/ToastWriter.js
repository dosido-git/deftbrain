import React, { useState, useCallback } from 'react';
import { CopyBtn } from '../components/ActionButtons';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const OCCASIONS = [
  { value: 'wedding', label: 'Wedding', emoji: '💒' },
  { value: 'birthday', label: 'Birthday', emoji: '🎂' },
  { value: 'retirement', label: 'Retirement', emoji: '🏖️' },
  { value: 'roast', label: 'Roast', emoji: '🔥' },
  { value: 'memorial', label: 'Memorial', emoji: '🕯️' },
  { value: 'graduation', label: 'Graduation', emoji: '🎓' },
  { value: 'farewell', label: 'Farewell / going-away', emoji: '✈️' },
  { value: 'award', label: 'Award / recognition', emoji: '🏆' },
  { value: 'baby_shower', label: 'Baby shower', emoji: '🍼' },
  { value: 'other', label: 'Other', emoji: '🎤' },
];

const TONES = [
  { value: 'warm_and_funny', label: 'Warm & funny', emoji: '😊' },
  { value: 'heartfelt', label: 'Heartfelt', emoji: '❤️' },
  { value: 'roast_style', label: 'Roast-y', emoji: '🔥' },
  { value: 'elegant', label: 'Elegant', emoji: '✨' },
];

const DURATIONS = [
  { value: '1_minute', label: '~1 min', emoji: '⚡' },
  { value: '2_minutes', label: '~2 min', emoji: '📖' },
  { value: '3_minutes', label: '~3 min', emoji: '🎤' },
  { value: '5_minutes', label: '~5 min', emoji: '📝' },
];

const ToastWriter = () => {
  const { callToolEndpoint, loading } = useClaudeAPI();
  const { theme } = useTheme();
  const isDark = theme === 'dark';

  const c = {
    pageBg:       isDark ? 'bg-[#1a1816]'    : 'bg-[#faf8f5]',
    card:         isDark ? 'bg-[#2a2623]'     : 'bg-white',
    cardAlt:      isDark ? 'bg-[#332e2a]'     : 'bg-[#faf8f5]',
    cardBorder:   isDark ? 'border-[#3d3630]' : 'border-[#e8e1d5]',
    inputBg:      isDark ? 'bg-[#1a1816]'     : 'bg-[#faf8f5]',
    inputBorder:  isDark ? 'border-[#3d3630]' : 'border-[#d5cab8]',
    inputFocus:   isDark ? 'focus:border-[#4a6a8a] focus:ring-[#2c4a6e]/20'
                         : 'focus:border-[#4a6a8a] focus:ring-[#2c4a6e]/12',
    text:          isDark ? 'text-[#f0eeea]'  : 'text-[#3d3935]',
    textSecondary: isDark ? 'text-[#c8c3b9]'  : 'text-[#5a544a]',
    textMuted:     isDark ? 'text-[#8a8275]'  : 'text-[#8a8275]',
    heading:       isDark ? 'text-[#f3efe8]'  : 'text-[#1e2a3a]',
    btnPrimary:    isDark ? 'bg-[#2c4a6e] hover:bg-[#4a6a8a] text-white' : 'bg-[#2c4a6e] hover:bg-[#1e3a58] text-white',
    btnSecondary:  isDark ? 'bg-[#332e2a] hover:bg-[#3d3630] text-[#c8c3b9] border border-[#3d3630]' : 'bg-[#f3efe8] hover:bg-[#e8e1d5] text-[#5e5042] border border-[#d5cab8]',
    btnGold:       isDark ? 'bg-[#b06d22] hover:bg-[#c8872e] text-white' : 'bg-[#c8872e] hover:bg-[#b06d22] text-white',
    badgeNeutral:  isDark ? 'bg-[#3d3630] text-[#c8c3b9]' : 'bg-[#f3efe8] text-[#73614e]',
    badgeGold:     isDark ? 'bg-[#b06d22]/20 text-[#d9a04e]' : 'bg-[#f9edd8] text-[#93541f]',
    divider:       isDark ? 'border-[#3d3630]' : 'border-[#e8e1d5]',
    link:          isDark ? 'text-[#6e8aaa] hover:text-[#a8b9ce]' : 'text-[#2c4a6e] hover:text-[#1e3a58]',
    success:       isDark ? 'bg-[#5a8a5c]/15 border-[#5a8a5c]/30 text-[#8abf8c]' : 'bg-[#e8f0e8] border-[#5a8a5c]/20 text-[#3d6b3f]',
    warning:       isDark ? 'bg-[#c8872e]/15 border-[#c8872e]/30 text-[#d9a04e]' : 'bg-[#fdf3e4] border-[#c8872e]/20 text-[#93541f]',
    error:         isDark ? 'bg-[#b54a3f]/15 border-[#b54a3f]/30 text-[#e08a82]' : 'bg-[#fceae8] border-[#b54a3f]/20 text-[#8a3530]',
    pillActive:    isDark ? 'bg-[#2c4a6e] border-[#4a6a8a] text-white' : 'bg-[#2c4a6e] border-[#2c4a6e] text-white',
    pillInactive:  isDark ? 'bg-[#2a2623] border-[#3d3630] text-[#c8c3b9] hover:border-[#5a544a]' : 'bg-white border-[#e8e1d5] text-[#5a544a] hover:border-[#d5cab8]',
  };

  // ─── State ───
  const [person, setPerson] = useState('');
  const [occasion, setOccasion] = useState('');
  const [relationship, setRelationship] = useState('');
  const [stories, setStories] = useState('');
  const [tone, setTone] = useState('warm_and_funny');
  const [duration, setDuration] = useState('2_minutes');
  const [avoid, setAvoid] = useState('');
  const [results, setResults] = useState(null);
  const [error, setError] = useState('');
  const [activeVersion, setActiveVersion] = useState(0);

  // ─── Actions ───
  const generate = useCallback(async () => {
    if (!person.trim() || !occasion) return;
    setError('');
    setResults(null);
    setActiveVersion(0);

    try {
      const data = await callToolEndpoint('toast-writer', {
        person: person.trim(),
        occasion: occasion === 'other' ? 'special occasion' : OCCASIONS.find(o => o.value === occasion)?.label || occasion,
        relationship: relationship.trim() || null,
        stories: stories.trim() || null,
        tone,
        duration,
        avoid: avoid.trim() || null,
      });
      setResults(data);
    } catch (err) {
      setError(err.message || 'Failed to write toast.');
    }
  }, [person, occasion, relationship, stories, tone, duration, avoid, callToolEndpoint]);

  const handleReset = useCallback(() => {
    setPerson(''); setOccasion(''); setRelationship('');
    setStories(''); setTone('warm_and_funny'); setDuration('2_minutes');
    setAvoid(''); setResults(null); setError(''); setActiveVersion(0);
  }, []);

  // ─── Build text ───
  const buildSpeechText = useCallback((v) => {
    return `${v.label} — ${v.style}\n\n${v.speech.replace(/\[PAUSE\]/g, '...').replace(/\[LOOK AT THEM\]/g, '').replace(/\[WAIT FOR LAUGH\]/g, '')}` + BRAND;
  }, []);

  const buildFullText = useCallback(() => {
    if (!results?.versions?.length) return '';
    let text = `🎙️ ToastWriter: ${occasion} toast for ${person}\n`;
    results.versions.forEach((v, i) => {
      text += `\n\n=== VERSION ${i + 1}: ${v.label} (${v.style}) ===\n\n${v.speech}`;
    });
    if (results.delivery_tips?.length) {
      text += '\n\n— Delivery Tips —\n';
      results.delivery_tips.forEach(t => { text += `\n• ${t}`; });
    }
    if (results.emergency_closer) text += `\n\n🆘 Emergency closer: ${results.emergency_closer}`;
    return text + BRAND;
  }, [results, occasion, person]);

  const r = results;
  const activeV = r?.versions?.[activeVersion];

  // ════════════════════════════════════════════════════════════
  // RENDER
  // ════════════════════════════════════════════════════════════
  return (
    <div className={`space-y-6 ${c.text}`}>

      {/* ── HEADER ── */}
      <div className={`${c.card} border ${c.cardBorder} rounded-xl p-6`}>
        <div className={`mb-5 pb-4 border-b ${c.divider}`}>
          <h2 className={`text-2xl font-bold ${c.heading}`}>ToastWriter 🎙️</h2>
          <p className={`text-sm ${c.textSecondary} mt-1`}>Toasts, speeches, and tributes that land</p>
        </div>

        {/* Person */}
        <div className="mb-4">
          <label className={`text-sm font-bold ${c.text} block mb-1.5`}>Who is this for?</label>
          <input
            type="text"
            value={person}
            onChange={e => setPerson(e.target.value)}
            placeholder="e.g., my best friend Sarah, my dad, my coworker Mike"
            className={`w-full px-4 py-3 border rounded-xl text-sm ${c.inputBg} ${c.inputBorder} ${c.text} ${c.inputFocus} outline-none focus:ring-2`}
          />
        </div>

        {/* Occasion */}
        <div className="mb-4">
          <label className={`text-sm font-bold ${c.text} block mb-2`}>The occasion</label>
          <div className="flex flex-wrap gap-1.5">
            {OCCASIONS.map(o => (
              <button
                key={o.value}
                onClick={() => setOccasion(occasion === o.value ? '' : o.value)}
                className={`px-3 py-1.5 rounded-lg text-xs font-bold border transition-colors min-h-[32px] ${
                  occasion === o.value ? c.pillActive : c.pillInactive
                }`}
              >
                {o.emoji} {o.label}
              </button>
            ))}
          </div>
        </div>

        {/* Relationship */}
        <div className="mb-4">
          <label className={`text-sm font-bold ${c.text} block mb-1.5`}>
            Your relationship <span className={`font-normal ${c.textMuted}`}>(optional)</span>
          </label>
          <input
            type="text"
            value={relationship}
            onChange={e => setRelationship(e.target.value)}
            placeholder="e.g., best man, daughter, team lead, college roommate"
            className={`w-full px-4 py-3 border rounded-xl text-sm ${c.inputBg} ${c.inputBorder} ${c.text} ${c.inputFocus} outline-none focus:ring-2`}
          />
        </div>

        {/* Stories */}
        <div className="mb-4">
          <label className={`text-sm font-bold ${c.text} block mb-1.5`}>
            Stories or details to include <span className={`font-normal ${c.textMuted}`}>(the more detail, the better the toast)</span>
          </label>
          <textarea
            value={stories}
            onChange={e => setStories(e.target.value)}
            placeholder="e.g., the time we got lost in Tokyo, how she always brings snacks to meetings, the fact that he still can't parallel park after 30 years"
            rows={3}
            className={`w-full px-4 py-3 border rounded-xl text-sm ${c.inputBg} ${c.inputBorder} ${c.text} ${c.inputFocus} outline-none focus:ring-2 resize-none`}
          />
        </div>

        {/* Tone */}
        <div className="mb-4">
          <label className={`text-sm font-bold ${c.text} block mb-2`}>Tone</label>
          <div className="flex gap-2">
            {TONES.map(t => (
              <button
                key={t.value}
                onClick={() => setTone(t.value)}
                className={`flex-1 py-2.5 rounded-xl text-[11px] font-bold border transition-colors min-h-[40px] flex flex-col items-center gap-0.5 ${
                  tone === t.value ? c.pillActive : c.pillInactive
                }`}
              >
                <span className="text-base">{t.emoji}</span>
                {t.label}
              </button>
            ))}
          </div>
        </div>

        {/* Duration */}
        <div className="mb-4">
          <label className={`text-sm font-bold ${c.text} block mb-2`}>Length</label>
          <div className="flex gap-2">
            {DURATIONS.map(d => (
              <button
                key={d.value}
                onClick={() => setDuration(d.value)}
                className={`flex-1 py-2 rounded-xl text-[11px] font-bold border transition-colors min-h-[36px] ${
                  duration === d.value ? c.pillActive : c.pillInactive
                }`}
              >
                {d.emoji} {d.label}
              </button>
            ))}
          </div>
        </div>

        {/* Avoid */}
        <div className="mb-5">
          <label className={`text-sm font-bold ${c.text} block mb-1.5`}>
            Topics to avoid <span className={`font-normal ${c.textMuted}`}>(optional)</span>
          </label>
          <input
            type="text"
            value={avoid}
            onChange={e => setAvoid(e.target.value)}
            placeholder='e.g., "the divorce", "that time in Vegas", "anything about weight"'
            className={`w-full px-4 py-3 border rounded-xl text-sm ${c.inputBg} ${c.inputBorder} ${c.text} ${c.inputFocus} outline-none focus:ring-2`}
          />
        </div>

        {/* Actions */}
        <div className="flex gap-3">
          <button
            onClick={generate}
            disabled={loading || !person.trim() || !occasion}
            className={`flex-1 ${c.btnGold} disabled:opacity-40 disabled:cursor-not-allowed font-bold py-3 px-6 rounded-xl flex items-center justify-center gap-2 min-h-[48px] shadow-lg`}
          >
            {loading ? (
              <><span className="animate-spin inline-block">⏳</span> Writing your toast...</>
            ) : (
              <><span>🎙️</span> Write My Toast</>
            )}
          </button>
          {results && (
            <button onClick={handleReset} className={`px-5 py-3 ${c.btnSecondary} rounded-xl font-medium min-h-[48px]`}>
              Start Over
            </button>
          )}
        </div>
      </div>

      {/* Error */}
      {error && (
        <div className={`${c.error} border rounded-xl p-4 flex items-start gap-3`}>
          <span className="flex-shrink-0 mt-0.5">⚠️</span>
          <p className="text-sm">{error}</p>
        </div>
      )}

      {/* ══════════════════════════════════════════════════════════ */}
      {/* RESULTS                                                  */}
      {/* ══════════════════════════════════════════════════════════ */}
      {r && (
        <div className="space-y-4">

          <div className="flex justify-end">
            <CopyBtn content={buildFullText()} label="Copy All Versions" />
          </div>

          {/* ── OCCASION READ ── */}
          {r.occasion_read && (
            <div className={`${c.card} border ${c.cardBorder} rounded-xl p-5`}>
              <p className={`text-sm ${c.textSecondary} leading-relaxed`}>{r.occasion_read}</p>
            </div>
          )}

          {/* ── VERSION TABS ── */}
          {r.versions?.length > 1 && (
            <div className="flex gap-2">
              {r.versions.map((v, idx) => (
                <button
                  key={idx}
                  onClick={() => setActiveVersion(idx)}
                  className={`flex-1 py-2.5 px-3 rounded-xl text-xs font-bold border transition-colors min-h-[40px] ${
                    activeVersion === idx ? c.pillActive : c.pillInactive
                  }`}
                >
                  {v.label}
                  <span className={`block text-[9px] font-normal mt-0.5 ${activeVersion === idx ? 'opacity-80' : c.textMuted}`}>
                    {v.style}
                  </span>
                </button>
              ))}
            </div>
          )}

          {/* ── ACTIVE SPEECH ── */}
          {activeV && (
            <div className={`${c.card} border ${c.cardBorder} rounded-xl overflow-hidden`}>
              <div className="p-5">
                {/* Header */}
                <div className="flex items-start justify-between mb-4">
                  <div>
                    <h3 className={`text-base font-bold ${c.heading}`}>{activeV.label}</h3>
                    <div className="flex gap-2 mt-1">
                      <span className={`text-[9px] font-bold px-2 py-0.5 rounded-full ${c.badgeNeutral}`}>{activeV.style}</span>
                      {activeV.estimated_time && (
                        <span className={`text-[9px] font-bold px-2 py-0.5 rounded-full ${c.badgeGold}`}>⏱️ {activeV.estimated_time}</span>
                      )}
                    </div>
                  </div>
                  <CopyBtn content={buildSpeechText(activeV)} label="Copy Speech" />
                </div>

                {/* Opening line highlight */}
                {activeV.opening_line && (
                  <div className={`${c.warning} border rounded-lg p-3 mb-4`}>
                    <p className={`text-[10px] font-bold mb-1`}>🎯 Your opening line (the hook):</p>
                    <p className={`text-sm font-bold leading-relaxed`}>"{activeV.opening_line}"</p>
                  </div>
                )}

                {/* The speech */}
                <div className={`${c.cardAlt} rounded-xl p-5 mb-4`}>
                  <p className={`text-sm ${c.text} leading-[1.8] whitespace-pre-line`}>{activeV.speech}</p>
                </div>

                {/* Closing line highlight */}
                {activeV.closing_line && (
                  <div className={`${c.success} border rounded-lg p-3`}>
                    <p className={`text-[10px] font-bold mb-1`}>🎬 Your closing line (the landing):</p>
                    <p className={`text-sm font-bold leading-relaxed`}>"{activeV.closing_line}"</p>
                  </div>
                )}
              </div>
            </div>
          )}

          {/* ── DELIVERY TIPS ── */}
          {r.delivery_tips?.length > 0 && (
            <div className={`${c.card} border ${c.cardBorder} rounded-xl p-4`}>
              <p className={`text-xs font-bold ${c.heading} mb-2`}>🎯 Delivery tips</p>
              <div className="space-y-2">
                {r.delivery_tips.map((tip, i) => (
                  <p key={i} className={`text-xs ${c.textSecondary} leading-relaxed`}>• {tip}</p>
                ))}
              </div>
            </div>
          )}

          {/* ── COMMON MISTAKES ── */}
          {r.common_mistakes?.length > 0 && (
            <div className={`${c.card} border ${c.cardBorder} rounded-xl p-4`}>
              <p className={`text-xs font-bold ${c.heading} mb-2`}>🚫 Mistakes to avoid</p>
              <div className="space-y-2">
                {r.common_mistakes.map((m, i) => (
                  <p key={i} className={`text-xs ${c.textSecondary} leading-relaxed`}>• {m}</p>
                ))}
              </div>
            </div>
          )}

          {/* ── EMERGENCY CLOSER ── */}
          {r.emergency_closer && (
            <div className={`${c.warning} border rounded-xl p-4 flex items-start gap-3`}>
              <span className="flex-shrink-0 mt-0.5">🆘</span>
              <div>
                <p className="text-xs font-bold mb-1">If you freeze, say this:</p>
                <p className="text-sm font-medium leading-relaxed">"{r.emergency_closer}"</p>
              </div>
            </div>
          )}

          {/* ── CROSS-REFERENCES ── */}
          <div className={`${c.card} border ${c.cardBorder} rounded-xl p-4`}>
            <p className={`text-xs font-bold ${c.text} mb-2`}>Related tools</p>
            <div className="flex flex-wrap gap-2">
              <a href="/GhostWriter" target="_blank" rel="noopener noreferrer" className={`text-xs px-3 py-1.5 rounded-lg border ${c.pillInactive} no-underline`}>
                👻 GhostWriter
              </a>
              <a href="/AwkwardSilenceFiller" target="_blank" rel="noopener noreferrer" className={`text-xs px-3 py-1.5 rounded-lg border ${c.pillInactive} no-underline`}>
                💬 Awkward Silence Filler
              </a>
              <a href="/CaptionMagic" target="_blank" rel="noopener noreferrer" className={`text-xs px-3 py-1.5 rounded-lg border ${c.pillInactive} no-underline`}>
                ✨ Caption Magic
              </a>
            </div>
          </div>
        </div>
      )}
    </div>
  );
};

ToastWriter.displayName = 'ToastWriter';
export default ToastWriter;
