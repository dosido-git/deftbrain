import React, { useState, useCallback } from 'react';
import { CopyBtn } from '../components/ActionButtons';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

// ════════════════════════════════════════════════════════════
// QUICK-PICK SCENARIOS
// ════════════════════════════════════════════════════════════
const QUICK_PICKS = [
  { label: 'I need to write something difficult', emoji: '✍️' },
  { label: 'I have a money question', emoji: '💰' },
  { label: "I'm stuck on a decision", emoji: '🤔' },
  { label: 'I need to prepare for a conversation', emoji: '🗣️' },
  { label: "Something broke and I don't know what to do", emoji: '🔧' },
  { label: 'I want to learn or understand something', emoji: '📚' },
  { label: "I'm planning an event or trip", emoji: '🗺️' },
  { label: 'I need help with food or cooking', emoji: '🍳' },
];

// ════════════════════════════════════════════════════════════
// COMPONENT
// ════════════════════════════════════════════════════════════
const ToolFinder = () => {
  const { callToolEndpoint, loading } = useClaudeAPI();
  const { theme } = useTheme();
  const isDark = theme === 'dark';

  // ─── Navy & Gold palette ───
  const c = {
    pageBg:       isDark ? 'bg-[#1a1816]'    : 'bg-[#faf8f5]',
    card:         isDark ? 'bg-[#2a2623]'     : 'bg-white',
    cardAlt:      isDark ? 'bg-[#332e2a]'     : 'bg-[#faf8f5]',
    cardBorder:   isDark ? 'border-[#3d3630]' : 'border-[#e8e1d5]',
    inputBg:      isDark ? 'bg-[#1a1816]'     : 'bg-[#faf8f5]',
    inputBorder:  isDark ? 'border-[#3d3630]' : 'border-[#d5cab8]',
    inputFocus:   isDark ? 'focus:border-[#4a6a8a] focus:ring-[#2c4a6e]/20'
                         : 'focus:border-[#4a6a8a] focus:ring-[#2c4a6e]/12',
    text:          isDark ? 'text-[#f0eeea]'  : 'text-[#3d3935]',
    textSecondary: isDark ? 'text-[#c8c3b9]'  : 'text-[#5a544a]',
    textMuted:     isDark ? 'text-[#8a8275]'  : 'text-[#8a8275]',
    heading:       isDark ? 'text-[#f3efe8]'  : 'text-[#1e2a3a]',
    btnPrimary:    isDark
      ? 'bg-[#2c4a6e] hover:bg-[#4a6a8a] text-white'
      : 'bg-[#2c4a6e] hover:bg-[#1e3a58] text-white',
    btnSecondary:  isDark
      ? 'bg-[#332e2a] hover:bg-[#3d3630] text-[#c8c3b9] border border-[#3d3630]'
      : 'bg-[#f3efe8] hover:bg-[#e8e1d5] text-[#5e5042] border border-[#d5cab8]',
    btnGold:       isDark
      ? 'bg-[#b06d22] hover:bg-[#c8872e] text-white'
      : 'bg-[#c8872e] hover:bg-[#b06d22] text-white',
    badgePrimary:  isDark ? 'bg-[#2c4a6e]/30 text-[#a8b9ce]' : 'bg-[#d4dde8] text-[#1e3a58]',
    badgeGold:     isDark ? 'bg-[#b06d22]/20 text-[#d9a04e]' : 'bg-[#f9edd8] text-[#93541f]',
    badgeNeutral:  isDark ? 'bg-[#3d3630] text-[#c8c3b9]'    : 'bg-[#f3efe8] text-[#73614e]',
    divider:       isDark ? 'border-[#3d3630]' : 'border-[#e8e1d5]',
    link:          isDark ? 'text-[#6e8aaa] hover:text-[#a8b9ce]'
                          : 'text-[#2c4a6e] hover:text-[#1e3a58]',
    success:       isDark ? 'bg-[#5a8a5c]/15 border-[#5a8a5c]/30 text-[#8abf8c]'
                          : 'bg-[#e8f0e8] border-[#5a8a5c]/20 text-[#3d6b3f]',
    error:         isDark ? 'bg-[#b54a3f]/15 border-[#b54a3f]/30 text-[#e08a82]'
                          : 'bg-[#fceae8] border-[#b54a3f]/20 text-[#8a3530]',
    pillActive:    isDark
      ? 'bg-[#2c4a6e] border-[#4a6a8a] text-white'
      : 'bg-[#2c4a6e] border-[#2c4a6e] text-white',
    pillInactive:  isDark
      ? 'bg-[#2a2623] border-[#3d3630] text-[#c8c3b9] hover:border-[#5a544a]'
      : 'bg-white border-[#e8e1d5] text-[#5a544a] hover:border-[#d5cab8]',
  };

  // ─── State ───
  const [problem, setProblem] = useState('');
  const [results, setResults] = useState(null);
  const [error, setError] = useState('');

  // ─── Actions ───
  const findTools = useCallback(async (text) => {
    const query = text || problem;
    if (!query.trim()) return;

    setError('');
    setResults(null);

    try {
      const data = await callToolEndpoint('tool-finder', {
        problem: query.trim(),
      });
      setResults(data);
    } catch (err) {
      setError(err.message || 'Something went wrong. Try again.');
    }
  }, [problem, callToolEndpoint]);

  const handleQuickPick = useCallback((label) => {
    setProblem(label);
    findTools(label);
  }, [findTools]);

  const handleReset = useCallback(() => {
    setProblem('');
    setResults(null);
    setError('');
  }, []);

  // ─── Build copyable text ───
  const buildFullText = useCallback(() => {
    if (!results) return '';
    let text = '🧰 ToolFinder Results\n';
    if (results.understanding) text += `\n${results.understanding}\n`;
    if (results.recommendations?.length) {
      text += '\nRecommended tools:\n';
      results.recommendations.forEach((rec, i) => {
        text += `\n${i + 1}. ${rec.icon || '🔧'} ${rec.title} — ${rec.why}`;
        if (rec.what_to_do) text += `\n   → ${rec.what_to_do}`;
      });
    }
    if (results.workflow) text += `\n\nWorkflow: ${results.workflow}`;
    return text + BRAND;
  }, [results]);

  const r = results;

  // ════════════════════════════════════════════════════════════
  // RENDER
  // ════════════════════════════════════════════════════════════
  return (
    <div className={`space-y-6 ${c.text}`}>

      {/* ── HEADER ── */}
      <div className={`${c.card} border ${c.cardBorder} rounded-xl p-6`}>
        <div className={`mb-5 pb-4 border-b ${c.divider}`}>
          <h2 className={`text-2xl font-bold ${c.heading}`}>ToolFinder 🧰</h2>
          <p className={`text-sm ${c.textSecondary} mt-1`}>
            Describe your problem — I'll find the right tools for you
          </p>
        </div>

        {/* ── PROBLEM INPUT ── */}
        <div className="mb-4">
          <label className={`text-sm font-bold ${c.text} block mb-1.5`}>
            What do you need help with?
          </label>
          <textarea
            value={problem}
            onChange={e => setProblem(e.target.value)}
            placeholder="e.g., My landlord is trying to keep my security deposit and I think it's unfair..."
            rows={3}
            className={`w-full px-4 py-3 border rounded-xl text-sm ${c.inputBg} ${c.inputBorder} ${c.text} ${c.inputFocus} outline-none focus:ring-2 resize-none placeholder:${c.textMuted}`}
          />
        </div>

        {/* ── QUICK PICKS ── */}
        {!results && (
          <div className="mb-5">
            <p className={`text-xs font-bold ${c.textMuted} mb-2`}>Or jump in:</p>
            <div className="flex flex-wrap gap-1.5">
              {QUICK_PICKS.map(qp => (
                <button
                  key={qp.label}
                  onClick={() => handleQuickPick(qp.label)}
                  disabled={loading}
                  className={`px-3 py-1.5 rounded-lg text-xs font-medium border transition-colors min-h-[32px] ${c.pillInactive} disabled:opacity-40`}
                >
                  {qp.emoji} {qp.label}
                </button>
              ))}
            </div>
          </div>
        )}

        {/* ── SUBMIT ── */}
        <div className="flex gap-3">
          <button
            onClick={() => findTools()}
            disabled={loading || !problem.trim()}
            className={`flex-1 ${c.btnGold} disabled:opacity-40 disabled:cursor-not-allowed font-bold py-3 px-6 rounded-xl flex items-center justify-center gap-2 min-h-[48px] shadow-lg transition-all`}
          >
            {loading ? (
              <><span className="animate-spin inline-block">⏳</span> Searching...</>
            ) : (
              <><span>🔍</span> Find My Tools</>
            )}
          </button>
          {results && (
            <button
              onClick={handleReset}
              className={`px-5 py-3 ${c.btnSecondary} rounded-xl font-medium min-h-[48px]`}
            >
              New Search
            </button>
          )}
        </div>
      </div>

      {/* Error */}
      {error && (
        <div className={`${c.error} border rounded-xl p-4 flex items-start gap-3`}>
          <span className="flex-shrink-0 mt-0.5">⚠️</span>
          <p className="text-sm">{error}</p>
        </div>
      )}

      {/* ══════════════════════════════════════════════════════════ */}
      {/* RESULTS                                                  */}
      {/* ══════════════════════════════════════════════════════════ */}
      {r && (
        <div className="space-y-4">

          {/* ── UNDERSTANDING ── */}
          {r.understanding && (
            <div className={`${c.card} border ${c.cardBorder} rounded-xl p-5`}>
              <p className={`text-sm ${c.textSecondary} leading-relaxed`}>
                <span className={`font-bold ${c.text}`}>I hear you: </span>
                {r.understanding}
              </p>
            </div>
          )}

          {/* ── RECOMMENDATIONS ── */}
          {r.recommendations && r.recommendations.length > 0 && (
            <div className="space-y-3">
              <div className="flex items-center justify-between">
                <h3 className={`text-sm font-bold ${c.heading}`}>
                  {r.recommendations.length === 1 ? 'Your best tool:' : `Your top ${r.recommendations.length} tools:`}
                </h3>
                <CopyBtn content={buildFullText()} label="Copy All" />
              </div>

              {r.recommendations.map((rec, idx) => (
                <a
                  key={rec.id}
                  href={`/${rec.id}`}
                  target="_blank"
                  rel="noopener noreferrer"
                  className="block no-underline group"
                >
                  <div className={`${c.card} border ${c.cardBorder} rounded-xl p-5 transition-all group-hover:shadow-md ${
                    isDark ? 'group-hover:border-[#4a6a8a]' : 'group-hover:border-[#2c4a6e]/40'
                  }`}>
                    {/* Tool header */}
                    <div className="flex items-start gap-3 mb-3">
                      <span className="text-2xl flex-shrink-0">{rec.icon || '🔧'}</span>
                      <div className="flex-1 min-w-0">
                        <div className="flex items-center gap-2 flex-wrap">
                          <h4 className={`text-base font-bold ${c.heading} group-hover:underline`}>
                            {rec.title}
                          </h4>
                          {idx === 0 && r.recommendations.length > 1 && (
                            <span className={`text-[9px] font-bold px-2 py-0.5 rounded-full ${c.badgeGold}`}>
                              BEST MATCH
                            </span>
                          )}
                          <span className={`text-[9px] font-bold px-2 py-0.5 rounded-full ${c.badgeNeutral}`}>
                            {rec.category}
                          </span>
                        </div>
                      </div>
                      <span className={`text-sm ${c.textMuted} group-hover:translate-x-1 transition-transform flex-shrink-0`}>→</span>
                    </div>

                    {/* Why this tool */}
                    <p className={`text-sm ${c.textSecondary} leading-relaxed mb-2`}>
                      {rec.why}
                    </p>

                    {/* What to do */}
                    {rec.what_to_do && (
                      <div className={`${c.cardAlt} rounded-lg px-3 py-2 mt-2`}>
                        <p className={`text-xs ${c.textMuted}`}>
                          <span className="font-bold">Quick start:</span> {rec.what_to_do}
                        </p>
                      </div>
                    )}
                  </div>
                </a>
              ))}
            </div>
          )}

          {/* ── WORKFLOW ── */}
          {r.workflow && (
            <div className={`${c.success} border rounded-xl p-4 flex items-start gap-3`}>
              <span className="flex-shrink-0 mt-0.5">🔗</span>
              <div>
                <p className={`text-xs font-bold mb-1`}>Recommended workflow:</p>
                <p className={`text-sm leading-relaxed`}>{r.workflow}</p>
              </div>
            </div>
          )}

          {/* ── NO PERFECT FIT ── */}
          {r.no_perfect_fit && (
            <div className={`${c.card} border ${c.cardBorder} rounded-xl p-4`}>
              <p className={`text-xs font-bold ${c.textMuted} mb-1`}>Closest match note:</p>
              <p className={`text-sm ${c.textSecondary}`}>{r.no_perfect_fit}</p>
            </div>
          )}

          {/* ── CLARIFICATION ── */}
          {r.clarification && (
            <div className={`${c.cardAlt} border ${c.cardBorder} rounded-xl p-4`}>
              <p className={`text-xs font-bold ${c.textMuted} mb-1`}>💡 Want better results?</p>
              <p className={`text-sm ${c.textSecondary}`}>{r.clarification}</p>
            </div>
          )}

          {/* ── BROWSE ALL ── */}
          <div className={`text-center pt-2`}>
            <p className={`text-xs ${c.textMuted} mb-2`}>Didn't find what you need?</p>
            <a
              href="/"
              className={`text-sm font-bold underline ${c.link}`}
            >
              Browse all tools →
            </a>
          </div>
        </div>
      )}
    </div>
  );
};

ToolFinder.displayName = 'ToolFinder';
export default ToolFinder;
