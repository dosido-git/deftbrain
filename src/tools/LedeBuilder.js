import React, { useState, useEffect, useRef, useCallback } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { usePersistentState } from '../hooks/usePersistentState';
import { CopyBtn } from '../components/ActionButtons';
import { useRegisterActions } from '../components/ActionBarContext';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const SYSTEM_PROMPT = `You are a writer for DeftBrain, a practical tools website. Your job is to write the opening paragraph(s) of a landing page — called a "lede" — for a specific search query.

The lede must:
- Open in second person, present tense — put the reader inside their exact moment
- Match the emotional context provided — nervous, frustrated, embarrassed, etc.
- Use the example scenario as texture, not as the whole story
- Never mention AI, tools, DeftBrain, or anything about technology
- Never use the word "conversation" — people don't search that word
- Never start with "In today's..." or any generic opener
- Be 2 short paragraphs maximum — tight, human, felt
- End naturally so "Here's how to do it" follows without a seam
- Sound like it was written by a sharp human who has been in this situation
- No bullet points, no headers, no em-dashes used for decoration

Return ONLY the lede text. No preamble, no explanation, no label. Just the paragraphs.`;

export default function LedeBuilder({ tool }) {
  const { callClaude, loading } = useClaudeAPI();
  const { isDark } = useTheme();

  const c = {
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:         isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20'
                          : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-500' : 'text-gray-400',
    labelText:     isDark ? 'text-zinc-200' : 'text-gray-700',
    accentTxt:     isDark ? 'text-cyan-400' : 'text-cyan-600',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white'
                          : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200'
                          : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200'
                          : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200'
                          : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200'
                          : 'bg-red-50 border-red-200 text-red-800',
    infoBox:       isDark ? 'bg-sky-900/20 border-sky-700 text-sky-200'
                          : 'bg-sky-50 border-sky-200 text-sky-800',
    required:      isDark ? 'text-amber-400' : 'text-amber-500',
  };
  c.textMuteded = c.textMuted;
  c.label = c.labelText;

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';

  const [searchPhrase,     setSearchPhrase]     = useState('');
  const [emotionalContext, setEmotionalContext] = useState('');
  const [exampleScenario,  setExampleScenario]  = useState('');
  const [toolTitle,        setToolTitle]        = useState('');
  const [results,          setResults]          = usePersistentState('LedeBuilder_results', null);
  const [history,          setHistory]          = usePersistentState('LedeBuilder_history', []);

  const resultsRef = useRef(null);

  const canSubmit = searchPhrase.trim() && emotionalContext.trim() && exampleScenario.trim();

  const handleSubmit = useCallback(async () => {
    if (!canSubmit || loading) return;
    setResults(null);

    const userMessage = `Search phrase: "${searchPhrase.trim()}"
Emotional context: ${emotionalContext.trim()}
Example scenario: ${exampleScenario.trim()}${toolTitle.trim() ? `\nTool this page promotes: ${toolTitle.trim()}` : ''}

Write the lede.`;

    const response = await callClaude(userMessage, { systemPrompt: SYSTEM_PROMPT });
    if (!response) return;

    const entry = {
      lede:            response,
      searchPhrase:    searchPhrase.trim(),
      emotionalContext: emotionalContext.trim(),
      exampleScenario: exampleScenario.trim(),
      toolTitle:       toolTitle.trim(),
      preview:         searchPhrase.trim().slice(0, 60),
      timestamp:       Date.now(),
    };

    setResults(entry);
    setHistory(prev => [entry, ...prev].slice(0, 6));
  }, [canSubmit, loading, searchPhrase, emotionalContext, exampleScenario, toolTitle, callClaude, setResults, setHistory]);

  const handleReset = useCallback(() => {
    setResults(null);
    setSearchPhrase('');
    setEmotionalContext('');
    setExampleScenario('');
    setToolTitle('');
  }, [setResults]);

  const buildFullText = useCallback(() => {
    if (!results) return '';
    const lines = [
      'LedeBuilder — SEO Lede Draft',
      '',
      `Search phrase: ${results.searchPhrase}`,
      '',
      results.lede,
      BRAND,
    ];
    return lines.join('\n');
  }, [results]);

  useRegisterActions(buildFullText(), tool?.title || 'LedeBuilder');

  useEffect(() => {
    if (results && resultsRef.current) {
      resultsRef.current.scrollIntoView({ behavior: 'smooth', block: 'start' });
    }
  }, [results]);

  useEffect(() => {
    const handler = e => {
      const tag = document.activeElement?.tagName;
      if (tag === 'TEXTAREA' && !e.metaKey && !e.ctrlKey) return;
      if ((e.metaKey || e.ctrlKey) && e.key === 'Enter') {
        e.preventDefault();
        handleSubmit();
      }
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
  }, [handleSubmit]);

  return (
    <div className={`space-y-4 ${c.text}`}>

      {/* Input card */}
      <div className={`${c.card} border ${c.border} rounded-xl shadow-sm p-5 space-y-4`}>
        <div className="pb-3 border-b border-zinc-500">
          <div className="flex items-start justify-between gap-3">
            <div>
              <h2 className={`text-xl font-bold ${c.text}`}>
                <span className="mr-2">{tool?.icon ?? '✍️'}</span>{tool?.title ?? 'LedeBuilder'}
              </h2>
              <p className={`text-sm ${c.textSecondary}`}>{tool?.tagline ?? 'Draft the human-feel opening for any SEO guide page'}</p>
            </div>
            {(results || searchPhrase.trim() || emotionalContext.trim() || exampleScenario.trim()) && (
              <button onClick={handleReset} className={`${c.btnSecondary} px-3 py-1.5 rounded-lg text-xs font-bold flex-shrink-0`}>
                ↺ Start Over
              </button>
            )}
          </div>
        </div>

        {/* Search phrase */}
        <div>
          <label className={`block text-sm font-medium ${c.labelText} mb-1`}>
            Search phrase <span className={c.required}>*</span>
          </label>
          <input
            type="text"
            value={searchPhrase}
            onChange={e => setSearchPhrase(e.target.value)}
            placeholder="how to tell your boss they're wrong"
            className={`w-full border rounded-lg px-3 py-2 text-sm ${c.input}`}
          />
          <p className={`text-xs ${c.textMuted} mt-1`}>Paste the exact phrase from the JSON record's <code>search_phrase</code> field.</p>
        </div>

        {/* Emotional context */}
        <div>
          <label className={`block text-sm font-medium ${c.labelText} mb-1`}>
            Emotional context <span className={c.required}>*</span>
          </label>
          <input
            type="text"
            value={emotionalContext}
            onChange={e => setEmotionalContext(e.target.value)}
            placeholder="nervous, second-guessing yourself, slightly resentful"
            className={`w-full border rounded-lg px-3 py-2 text-sm ${c.input}`}
          />
          <p className={`text-xs ${c.textMuted} mt-1`}>How the reader is feeling right now. The more specific, the more human the lede.</p>
        </div>

        {/* Example scenario */}
        <div>
          <label className={`block text-sm font-medium ${c.labelText} mb-1`}>
            Example scenario <span className={c.required}>*</span>
          </label>
          <textarea
            value={exampleScenario}
            onChange={e => setExampleScenario(e.target.value)}
            placeholder="your manager greenlights a plan you know will fail and is now asking for your buy-in"
            rows={2}
            className={`w-full border rounded-lg px-3 py-2 text-sm resize-none ${c.input}`}
          />
          <p className={`text-xs ${c.textMuted} mt-1`}>A concrete situation — gives the lede something real to open with.</p>
        </div>

        {/* Tool title (optional) */}
        <div>
          <label className={`block text-sm font-medium ${c.labelText} mb-1`}>
            Tool this page promotes <span className={`${c.textMuted} font-normal`}>(optional)</span>
          </label>
          <input
            type="text"
            value={toolTitle}
            onChange={e => setToolTitle(e.target.value)}
            placeholder="Difficult Talk Coach"
            className={`w-full border rounded-lg px-3 py-2 text-sm ${c.input}`}
          />
        </div>

        {/* Submit */}
        <button
          onClick={handleSubmit}
          disabled={!canSubmit || loading}
          className={`w-full ${c.btnPrimary} disabled:opacity-40 font-bold py-3 rounded-lg flex items-center justify-center gap-2 min-h-[48px]`}
        >
          {loading
            ? <><span className="inline-block animate-spin">{tool?.icon ?? '✍️'}</span> Drafting...</>
            : <><span className="mr-1">{tool?.icon ?? '✍️'}</span> Draft lede</>}
        </button>

        {/* Pre-result cross-ref */}
        <p className={`text-xs ${c.textMuted} text-center`}>
          Crafting an opener for a message instead?{' '}
          <a href="/ColdOpenCraft" className={`text-xs ${linkStyle}`}>🎯 Cold Open Craft</a> handles that.
        </p>
      </div>

      {/* Results */}
      {results && (
        <div ref={resultsRef} className="space-y-4">

          <div className={`${c.card} border ${c.border} rounded-xl shadow-sm p-5 space-y-4`}>
            <div className="flex items-start justify-between gap-3">
              <div>
                <p className={`text-xs font-bold uppercase ${c.textMuted} mb-1`}>Lede draft</p>
                <p className={`text-xs ${c.textMuted}`}>{results.searchPhrase}</p>
              </div>
              <CopyBtn content={buildFullText()} label="Copy lede" />
            </div>

            {/* Lede text */}
            <div className={`${c.cardAlt} border ${c.border} rounded-lg p-4`}>
              {results.lede.split('\n\n').filter(Boolean).map((para, i) => (
                <p key={i} className={`text-sm leading-relaxed ${c.text} ${i > 0 ? 'mt-3' : ''}`}>
                  {para.trim()}
                </p>
              ))}
            </div>

            {/* Context echo */}
            <div className={`border ${c.border} rounded-lg p-3 space-y-1`}>
              <p className={`text-xs font-bold uppercase ${c.textMuted}`}>From your record</p>
              <p className={`text-xs ${c.textSecondary}`}>
                <span className={`font-medium ${c.labelText}`}>Emotional context: </span>
                {results.emotionalContext}
              </p>
              <p className={`text-xs ${c.textSecondary}`}>
                <span className={`font-medium ${c.labelText}`}>Scenario: </span>
                {results.exampleScenario}
              </p>
            </div>

            {/* Usage note */}
            <div className={`${c.infoBox} border rounded-lg p-3 text-xs leading-relaxed`}>
              Paste the lede text into the <code>lede</code> field of your JSON record, replacing double newlines with <code>\n\n</code>. Run <code>npm run build:seo</code> to regenerate the page.
            </div>

            {/* Actions */}
            <button
              onClick={handleSubmit}
              disabled={loading}
              className={`w-full ${c.btnSecondary} disabled:opacity-40 font-medium py-2 rounded-lg text-sm`}
            >
              Redraft
            </button>

            {/* Post-result cross-ref */}
            <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
              <p className={`text-[10px] font-bold ${c.textMuted} uppercase mb-2`}>🔗 Related tools</p>
              <div className="flex flex-wrap gap-3">
                <a href="/VelvetHammer" className={`text-xs ${linkStyle}`}>🔨 Velvet Hammer</a>
              </div>
            </div>
          </div>
        </div>
      )}

      {/* History */}
      {history.length > 0 && (
        <div className={`${c.card} border ${c.border} rounded-xl shadow-sm p-5`}>
          <p className={`text-xs font-bold uppercase ${c.textMuted} mb-3`}>Recent drafts</p>
          <div className="space-y-2">
            {history.map((entry, i) => (
              <button
                key={i}
                onClick={() => setResults(entry)}
                className={`w-full text-left ${c.cardAlt} border ${c.border} rounded-lg px-3 py-2 hover:border-cyan-500 transition-colors`}
              >
                <p className={`text-xs font-medium ${c.text} truncate`}>{entry.preview}</p>
                <p className={`text-xs ${c.textMuted} mt-0.5`}>
                  {new Date(entry.timestamp).toLocaleDateString(undefined, { month: 'short', day: 'numeric', hour: '2-digit', minute: '2-digit' })}
                </p>
              </button>
            ))}
          </div>
        </div>
      )}
    </div>
  );
}

LedeBuilder.displayName = 'LedeBuilder';
