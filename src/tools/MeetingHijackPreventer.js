// FACILITATOR MODE (with F1: Speaking Tracker + F3: Cost)
// ════════════════════════════════════════════════════════════
// Internal state combined under a single useReducer (rather than 7
// separate useState calls) — the slices are tightly coupled (timer,
// speaking tracker, current-item index all advance together) and
// keeping them in one reducer keeps the state machine explicit.
const FACILITATOR_INITIAL = {
  currentIdx: 0,
  elapsed: 0,
  totalElapsed: 0,
  paused: false,
  extendedTime: 0,
  speakingLog: {},
  activeSpeaker: null,
};
function facilitatorReducer(state, action) {
  switch (action.type) {
    case 'init':
      return { ...state, speakingLog: action.speakingLog };
    case 'tick':
      return { ...state, elapsed: state.elapsed + 1, totalElapsed: state.totalElapsed + 1 };
    case 'next':
      return { ...state, currentIdx: state.currentIdx + 1, elapsed: 0, extendedTime: 0 };
    case 'prev':
      return { ...state, currentIdx: state.currentIdx - 1, elapsed: 0, extendedTime: 0 };
    case 'extend':
      return { ...state, extendedTime: state.extendedTime + action.secs };
    case 'togglePaused':
      return { ...state, paused: !state.paused };
    case 'startSpeaker':
      return {
        ...state,
        activeSpeaker: action.name,
        speakingLog: {
          ...state.speakingLog,
          [action.name]: {
            ...state.speakingLog[action.name],
            turns: (state.speakingLog[action.name]?.turns || 0) + 1,
          },
        },
      };
    case 'stopSpeaker':
      return { ...state, activeSpeaker: null };
    case 'tickSpeaker':
      if (!state.activeSpeaker) return state;
      return {
        ...state,
        speakingLog: {
          ...state.speakingLog,
          [state.activeSpeaker]: {
            ...state.speakingLog[state.activeSpeaker],
            totalSec: (state.speakingLog[state.activeSpeaker]?.totalSec || 0) + 1,
          },
        },
      };
    default:
      return state;
  }
}

function FacilitatorMode({ results, participants, c, isDark, onEnd, hourlyRate, t, sym }) {
  const [fState, dispatch] = useReducer(facilitatorReducer, FACILITATOR_INITIAL);
  const { currentIdx, elapsed, totalElapsed, paused, extendedTime, speakingLog, activeSpeaker } = fState;
  const timerRef = useRef(null);

  // Initialize speakingLog once participants are known
  useEffect(() => {
    const log = {};
    participants.forEach(p => { log[p.name] = { turns: 0, lastStart: null, totalSec: 0 }; });
    dispatch({ type: 'init', speakingLog: log });
  }, [participants]);

  const items = results?.meeting_structure?.agenda_items || [];
  const current = items[currentIdx];
  const allocated = (current?.time_allocated || 0) * 60 + extendedTime;
  const remaining = Math.max(allocated - elapsed, 0);
  const progress = allocated > 0 ? Math.min(elapsed / allocated, 1) : 0;
  const overTime = elapsed > allocated;
  // F3: Live cost
  const costPerSecond = hourlyRate ? (hourlyRate * participants.length / 3600) : 0;
  const liveCost = (costPerSecond * totalElapsed).toFixed(2);

  useEffect(() => {
    if (!paused) {
      timerRef.current = setInterval(() => { dispatch({ type: 'tick' }); }, 1000);
    }
    return () => clearInterval(timerRef.current);
  }, [paused]);

  // F1: Track active speaker duration
  useEffect(() => {
    if (!activeSpeaker) return;
    const iv = setInterval(() => { dispatch({ type: 'tickSpeaker' }); }, 1000);
    return () => clearInterval(iv);
  }, [activeSpeaker]);

  const next = () => { if (currentIdx < items.length - 1) { dispatch({ type: 'next' }); } else endMeeting(); };
  const prev = () => { if (currentIdx > 0) { dispatch({ type: 'prev' }); } };
  const extend = (secs) => dispatch({ type: 'extend', secs });
  const fmt = (s) => `${Math.floor(s / 60)}:${String(s % 60).padStart(2, '0')}`;

  const toggleSpeaker = (name) => {
    if (activeSpeaker === name) { dispatch({ type: 'stopSpeaker' }); return; }
    dispatch({ type: 'startSpeaker', name });
  };

  const endMeeting = () => {
    dispatch({ type: 'stopSpeaker' });
    onEnd({ speakingLog, totalElapsed, totalCost: liveCost });
  };

  const scripts = results?.facilitator_scripts || {};
  const currentTopic = (current?.topic || '').toLowerCase();
  const relevantScript = currentTopic.includes('open') ? scripts.opening : currentTopic.includes('clos') || currentTopic.includes('wrap') ? scripts.closing : null;

  return (
    <div className="space-y-4">
      {/* Progress bar */}
      <div className={`${c.card} border rounded-xl p-4`}>
        <div className="flex items-center gap-2 mb-2">{items.map((_, i) => (
          <div key={i} className={`flex-1 h-2 rounded-full ${i < currentIdx ? 'bg-emerald-500' : i === currentIdx ? (overTime ? 'bg-red-500' : 'bg-sky-500') : (isDark ? 'bg-zinc-700' : 'bg-sky-100')}`} />
        ))}</div>
        <div className="flex items-center justify-between">
          <p className={`text-xs ${c.textMuted}`}>{t('mhp_item_of', { n: currentIdx + 1, total: items.length })}</p>
          {/* F3: Live cost */}
          {costPerSecond > 0 && <p className={`text-xs font-mono font-bold ${totalElapsed > (results?.meeting_structure?.total_duration || 60) * 60 ? 'text-red-500' : c.accentTxt}`}>💰 {sym}{liveCost}</p>}
        </div>
      </div>

      {/* Timer */}
      <div className={`${c.timerBg} rounded-xl p-6 text-center`}>
        <p className={`text-sm ${c.timerText} opacity-70 mb-1`}>{current?.topic}</p>
        <p className={`text-6xl font-black ${overTime ? 'text-red-400' : c.timerText} font-mono`}>{overTime ? '-' : ''}{fmt(overTime ? elapsed - allocated : remaining)}</p>
        <p className={`text-xs ${c.timerText} opacity-50 mt-1`}>{t('mhp_elapsed_allocated', { elapsed: fmt(elapsed), allocated: fmt(allocated) })}</p>
        <div className="w-full h-2 rounded-full bg-white/10 mt-4 overflow-hidden">
          <div className={`h-full rounded-full transition-all ${overTime ? 'bg-red-500' : progress > 0.8 ? 'bg-amber-400' : 'bg-emerald-400'}`} style={{ width: `${Math.min(progress * 100, 100)}%` }} />
        </div>
        <div className="flex items-center justify-center gap-3 mt-4">
          <button onClick={prev} disabled={currentIdx === 0} className="text-white/60 hover:text-white disabled:opacity-40 text-lg">⏮️</button>
          <button onClick={() => dispatch({ type: 'togglePaused' })} className="text-white text-2xl">{paused ? '▶️' : '⏸️'}</button>
          <button onClick={next} className="text-white text-lg">{currentIdx < items.length - 1 ? '⏭️' : '🏁'}</button>
        </div>
        <div className="flex items-center justify-center gap-2 mt-3">
          {[60, 120, 300].map(s => <button key={s} onClick={() => extend(s)} className="text-xs text-white/60 hover:text-white border border-white/20 px-2 py-1 rounded">+{s / 60}m</button>)}
        </div>
      </div>

      {/* Current item details */}
      <div className={`${c.card} border rounded-xl p-5 space-y-3`}>
        <div><p className={`text-[10px] font-bold ${c.textMuted} uppercase`}>{t('mhp_objective')}</p><p className={`text-sm ${c.textSecondary}`}>{current?.objective}</p></div>
        <div><p className={`text-[10px] font-bold ${c.textMuted} uppercase`}>{t('mhp_your_role')}</p><p className={`text-sm ${c.textSecondary}`}>{current?.facilitator_role}</p></div>
        {current?.speaker_order?.length > 0 && <div><p className={`text-[10px] font-bold ${c.textMuted} uppercase`}>{t('mhp_speaking_order')}</p><div className="flex flex-wrap gap-1 mt-1">{current.speaker_order.map((s, i) => <span key={i} className={`${c.pillSky} border text-xs px-2 py-0.5 rounded`}>{i + 1}. {s}</span>)}</div></div>}
        {current?.time_warning && <div className={`${c.warning} border rounded-lg p-2`}><p className="text-xs">⏰ {current.time_warning}</p></div>}
        {relevantScript && <div className={`${c.infoBox} border rounded-lg p-3`}><p className="text-[10px] font-bold mb-1">💬 {t('mhp_script')}</p><p className="text-xs italic">"{relevantScript}"</p></div>}
      </div>

      {/* F1: SPEAKING TRACKER */}
      {participants.length > 0 && (
        <div className={`${c.card} border rounded-xl p-5`}>
          <p className={`text-xs font-bold ${c.labelText} mb-3`}>🗣️ {t('mhp_tap_speaking')}</p>
          <div className="grid grid-cols-2 gap-2">{participants.map(p => {
            const log = speakingLog[p.name] || { turns: 0, totalSec: 0 };
            const isActive = activeSpeaker === p.name;
            return (
              <button key={p.name} onClick={() => toggleSpeaker(p.name)}
                className={`p-3 border-2 rounded-lg text-left transition-all ${isActive ? 'border-emerald-500 bg-emerald-900/20 ring-2 ring-emerald-500/30' : (isDark ? 'border-zinc-600 hover:border-zinc-500' : 'border-sky-200 hover:border-sky-300')}`}>
                <div className="flex items-center justify-between">
                  <span className={`text-sm font-semibold ${c.text}`}>{p.name}</span>
                  {isActive && <span className="text-xs animate-pulse">🔴</span>}
                </div>
                <div className="flex items-center gap-2 mt-1">
                  <span className={`text-[10px] ${c.textMuted}`}>{t('mhp_turns', { count: log.turns })}</span>
                  <span className={`text-[10px] ${c.textMuted}`}>{fmt(log.totalSec)}</span>
                </div>
              </button>
            );
          })}</div>
          {activeSpeaker && <p className={`text-xs text-center mt-2 text-emerald-500 font-semibold`}>🎙️ {t('mhp_is_speaking', { name: activeSpeaker })}</p>}
        </div>
      )}

      {/* Quick scripts */}
      <div className={`${c.card} border rounded-xl p-5`}>
        <p className={`text-xs font-bold ${c.labelText} mb-3`}>🚨 {t('mhp_quick_scripts')}</p>
        <div className="grid grid-cols-2 gap-2">{[
          { labelKey: 'mhp_qs_redirect', key: 'redirecting_tangent', icon: '🐇' },
          { labelKey: 'mhp_qs_dominance', key: 'managing_dominance', icon: '🗣️' },
          { labelKey: 'mhp_qs_quiet', key: 'encouraging_quiet_voices', icon: '🤫' },
          { labelKey: 'mhp_qs_park', key: 'parking_lot_response', icon: '🅿️' },
        ].map(s => scripts[s.key] && (
          <div key={s.key} className={`${c.cardAlt} border rounded-lg p-2`}>
            <p className="text-[10px] font-bold">{s.icon} {t(s.labelKey)}</p>
            <p className={`text-[10px] ${c.textSecondary} italic mt-0.5 line-clamp-3`}>"{scripts[s.key]}"</p>
          </div>
        ))}</div>
      </div>

      <button onClick={endMeeting} className={`w-full ${c.btnDanger} border py-2 rounded-lg text-sm font-semibold`}>🏁 {t('mhp_end_meeting')}</button>
    </div>
  );
}

