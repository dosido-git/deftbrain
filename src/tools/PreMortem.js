import React, { useState, useEffect, useRef, useCallback } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { usePersistentState } from '../hooks/usePersistentState';
import { useRegisterActions } from '../components/ActionBarContext';
import { useTranslation } from '../i18n/useTranslation';
import { currencySymbol } from '../utils/formatLocale';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';


const PreMortem = ({ tool }) => {
  const { callToolEndpoint, loading, userLocale, userCurrency, userRegion } = useClaudeAPI();
  const { isDark } = useTheme();
  const { t } = useTranslation();
  const sym = currencySymbol(userLocale, userCurrency);

  const EXAMPLE = {
    plan: t('pm_ex_plan', { sym }),
    planType: 'side_project',
    stakes: t('pm_ex_stakes', { sym }),
    assumptions: t('pm_ex_assumptions'),
  };
  const PLAN_TYPES = [
    { id: 'startup',      label: t('pm_type_startup'),      icon: '🚀' },
    { id: 'career',       label: t('pm_type_career'),       icon: '💼' },
    { id: 'product',      label: t('pm_type_product'),      icon: '📦' },
    { id: 'personal',     label: t('pm_type_personal'),     icon: '🧍' },
    { id: 'financial',    label: t('pm_type_financial'),    icon: '💰' },
    { id: 'project',      label: t('pm_type_project'),      icon: '🏗️' },
    { id: 'relationship', label: t('pm_type_relationship'), icon: '❤️' },
    { id: 'creative',     label: t('pm_type_creative'),     icon: '🎨' },
  ];

  const PROB_CONFIG = {
    high:   { label: t('pm_prob_high'),   cls: 'red' },
    medium: { label: t('pm_prob_medium'), cls: 'amber' },
    low:    { label: t('pm_prob_low'),    cls: 'emerald' },
  };

  const c = {
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:         isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20'
                          : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-500' : 'text-gray-400',
    required:      isDark ? 'text-amber-400' : 'text-amber-500',
    labelText:     isDark ? 'text-zinc-200' : 'text-gray-700',
    accentTxt:     isDark ? 'text-cyan-400' : 'text-cyan-600',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white'
                          : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200'
                          : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200'
                          : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200'
                          : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200'
                          : 'bg-red-50 border-red-200 text-red-800',
    pillActive:    isDark ? 'border-cyan-500 bg-cyan-900/30 text-cyan-200'
                          : 'border-cyan-600 bg-cyan-100 text-cyan-900',
    pillInactive:  isDark ? 'border-zinc-600 text-zinc-400 hover:border-zinc-500'
                          : 'border-gray-300 text-gray-500 hover:border-gray-400',
  };
  c.textMuteded = c.textMuted;
  const probClsMap = {
    red:     isDark ? 'bg-red-600/20 text-red-300 border-red-600/40'       : 'bg-red-50 text-red-700 border-red-300',
    amber:   isDark ? 'bg-amber-900/20 text-amber-300 border-amber-700'    : 'bg-amber-50 text-amber-700 border-amber-300',
    emerald: isDark ? 'bg-emerald-900/20 text-emerald-300 border-emerald-700' : 'bg-emerald-50 text-emerald-700 border-emerald-300',
  };
  c.label       = c.labelText;

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';

  // ── State ──
  const [plan, setPlan]             = useState('');
  const [planType, setPlanType]     = useState('');
  const [stakes, setStakes]         = useState('');
  const [assumptions, setAssumptions] = useState('');
  const [error, setError]           = useState('');
  const [expanded, setExpanded]     = useState({ warnings: true, modes: true, assumptions: false });

  // ── Refs ──
  const resultsRef   = useRef(null);
  const submitRef    = useRef(null);
  const canSubmitRef = useRef(false);

  // ── Persistent state ──
  const [results, setResults] = usePersistentState('premortem-result', null);
  const [sessionHistory, setSessionHistory] = usePersistentState('premortem-history', []);

  // ── Helpers ──
  const toggle = useCallback((k) => setExpanded(p => ({ ...p, [k]: !p[k] })), []);

  // ── Handler ──
  const handleSubmit = useCallback(async () => {
    if (!plan.trim()) return;
    setError(''); setResults(null);
    try {
      const data = await callToolEndpoint('pre-mortem', {
        plan:        plan.trim(),
        planType:    planType || undefined,
        stakes:      stakes.trim() || undefined,
        assumptions: assumptions.trim() || undefined,
        userLocale, userCurrency, userRegion,
      });
      setResults(data);
      // PF-25 exception: 40-char preview-text truncation; session history is capped at 6.
      setSessionHistory(prev => [{ id: Date.now(), date: new Date().toISOString(), preview: plan.slice(0, 40) }, ...prev].slice(0, 6));
    } catch (e) { setError(e.message || t('pm_error')); }
  }, [plan, planType, stakes, assumptions, callToolEndpoint, setResults, setSessionHistory, userLocale, userCurrency, userRegion, t]);

  // ── buildFullText ──
  const loadExample = useCallback(() => {
    setPlan(EXAMPLE.plan);
    setPlanType(EXAMPLE.planType);
    setStakes(EXAMPLE.stakes);
    setAssumptions(EXAMPLE.assumptions);
    setResults(null);
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [setResults, sym, t]);

  const buildFullText = useCallback(() => {
    if (!results) return '';
    const pm = results.the_postmortem;
    let txt = `💀 ${t('pm_copy_title')}\n\n${pm?.memo_header || t('pm_copy_report_default')}\n\n`;
    txt += `${pm?.executive_summary}\n\n`;
    txt += `${t('pm_copy_narrative')}\n${pm?.narrative}\n\n`;
    if (pm?.the_fatal_assumption) txt += `${t('pm_copy_fatal')} ${pm.the_fatal_assumption}\n`;
    if (pm?.point_of_no_return)   txt += `${t('pm_copy_pnr')} ${pm.point_of_no_return}\n\n`;
    if (results.the_most_likely) {
      txt += `${t('pm_copy_most_likely')} ${results.the_most_likely.failure_mode}\n${t('pm_copy_prevention')} ${results.the_most_likely.the_prevention}\n\n`;
    }
    if (results.the_one_thing) txt += `${t('pm_copy_one_thing')}\n${results.the_one_thing}\n`;
    return txt + BRAND;
  }, [results, t]);

  // Live ref assignments
  submitRef.current    = handleSubmit;
  canSubmitRef.current = !!plan.trim() && !loading;

  useRegisterActions(buildFullText(), tool?.title || t('pm_title'));

  // ── Scroll to results ──
  useEffect(() => {
    if (!results || !resultsRef.current) return;
    const tm = setTimeout(() => resultsRef.current?.scrollIntoView({ behavior: 'smooth', block: 'start' }), 200);
    return () => clearTimeout(tm);
  }, [results]);

  // ── Keyboard shortcut ──
  useEffect(() => {
    const handler = (e) => {
      const tag = document.activeElement?.tagName;
      if (tag === 'SELECT') return;
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && canSubmitRef.current)
        submitRef.current?.();
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [loading]);

  const pm = results?.the_postmortem;

  return (
    <div className={`space-y-4 ${c.text}`}>

      {/* ── Persistent header card ── */}
      <div className={`${c.card} border ${c.border} rounded-xl shadow-sm overflow-hidden`}>
        <div className="px-5 pt-5">
          <div className="pb-3 border-b border-zinc-500">
            <h2 className={`text-xl font-bold ${c.text}`}>
              <span className="mr-2">{tool?.icon ?? '💀'}</span>{tool?.title ?? t('pm_title')}
            </h2>
            <p className={`text-sm ${c.textSecondary}`}>{tool?.tagline ?? t('pm_tagline')}</p>
            <button onClick={loadExample} disabled={loading} style={{ backgroundColor: (tool?.headerColor ?? '#888888') + '80' }} className={`mt-1.5 px-2.5 py-0.5 rounded-full text-xs font-medium border disabled:opacity-40 ${isDark ? 'text-white border-white/40' : 'text-gray-800 border-transparent'}`}>{t('try_example')}</button>
          </div>
        </div>

        {/* ── Input section ── */}
        {!results && (
          <div className="p-5 space-y-4">

            <p className={`text-xs ${c.textMuted}`}>
              {t('pm_xref_pre')} <a href="/DecisionCoach" className={linkStyle}>🎯 {t('pm_decisioncoach')}</a> {t('pm_xref_post')}
            </p>

            <div>
              <label className={`block text-sm font-semibold mb-1.5 ${c.text}`}>
                {t('pm_plan_label')} <span className={c.required}>*</span>
              </label>
              <textarea value={plan} onChange={e => setPlan(e.target.value)}
                placeholder={t('pm_plan_ph')}
                rows={4} maxLength={800}
                className={`w-full px-4 py-3 rounded-xl border text-sm resize-none ${c.input}`} />
            </div>

            <div>
              <label className={`block text-sm font-semibold mb-2 ${c.text}`}>
                {t('pm_type_label')} <span className={`font-normal ${c.textMuted}`}>({t('optional')})</span>
              </label>
              <div className="flex flex-wrap gap-2">
                {PLAN_TYPES.map(pt => (
                  <button key={pt.id} onClick={() => setPlanType(planType === pt.id ? '' : pt.id)}
                    className={`px-3 py-1.5 rounded-lg text-xs font-semibold border transition-all ${
                      planType === pt.id ? c.pillActive : c.pillInactive
                    }`}>
                    {pt.label}
                  </button>
                ))}
              </div>
            </div>

            <div className="grid grid-cols-1 sm:grid-cols-2 gap-3">
              <div>
                <label className={`block text-sm font-semibold mb-1.5 ${c.text}`}>
                  {t('pm_stakes_label')} <span className={`font-normal ${c.textMuted}`}>({t('optional')})</span>
                </label>
                <input type="text" value={stakes} onChange={e => setStakes(e.target.value)}
                  placeholder={t('pm_stakes_ph')}
                  className={`w-full px-3 py-2.5 rounded-xl border text-sm ${c.input}`} />
              </div>
              <div>
                <label className={`block text-sm font-semibold mb-1.5 ${c.text}`}>
                  {t('pm_assumptions_label')} <span className={`font-normal ${c.textMuted}`}>({t('optional')})</span>
                </label>
                <input type="text" value={assumptions} onChange={e => setAssumptions(e.target.value)}
                  placeholder={t('pm_assumptions_ph')}
                  className={`w-full px-3 py-2.5 rounded-xl border text-sm ${c.input}`} />
              </div>
            </div>

            {error && (
              <div className={`p-3 rounded-xl border text-sm ${c.danger}`}>
                <span className="mr-1">⚠️</span>{error}
              </div>
            )}

          <button onClick={handleSubmit} disabled={loading || !plan.trim()}
            className={`w-full ${c.btnPrimary} disabled:opacity-40 font-bold py-3 rounded-xl flex items-center justify-center gap-2 min-h-[48px]`}>
            {loading
              ? <><span className="inline-block animate-spin">{tool?.icon ?? '💀'}</span> {t('pm_writing')}</>
              : <><span>{tool?.icon ?? '💀'}</span> {t('pm_submit')}</>}
            </button>

            <p className={`text-xs text-center ${c.textMuted}`}>
              {t('pm_submit_hint')}
            </p>
          </div>
        )}
      </div>

      {/* ── Results ── */}
      {results && (
        <div className="space-y-4" ref={resultsRef}>

          {/* Memo header */}
          <div className={`${c.card} border-2 rounded-2xl overflow-hidden ${isDark ? 'border-red-600/50' : 'border-red-200'}`}>
            <div className={`px-6 py-4 ${isDark ? 'bg-red-600/10' : 'bg-red-50'}`}>
              <p className={`text-xs font-black uppercase tracking-widest mb-1 ${isDark ? 'text-red-400' : 'text-red-600'}`}>
                💀 {t('pm_report_label')}
              </p>
              <p className={`text-sm font-mono font-semibold ${isDark ? 'text-zinc-300' : 'text-zinc-700'}`}>{pm?.memo_header}</p>
            </div>
            <div className="px-6 py-4">
              <p className={`text-sm font-semibold leading-relaxed ${c.textSecondary}`}>{pm?.executive_summary}</p>
            </div>
          </div>

          {/* Narrative */}
          {pm?.narrative && (
            <div className={`${c.card} border ${c.border} rounded-2xl p-5`}>
              <p className={`text-xs font-bold uppercase tracking-wider mb-3 ${c.textMuted}`}>📖 {t('pm_how_unfolded')}</p>
              <div className={`text-sm leading-relaxed whitespace-pre-line ${c.textSecondary}`} style={{ fontFamily: 'Georgia, serif', fontStyle: 'italic' }}>
                {pm.narrative}
              </div>
            </div>
          )}

          {/* Warning signs */}
          {pm?.warning_signs_ignored?.length > 0 && (
            <div className={`${c.card} border ${c.border} rounded-2xl overflow-hidden`}>
              <button onClick={() => toggle('warnings')} className="w-full text-left px-5 py-4 flex items-center justify-between">
                <p className={`text-xs font-bold uppercase tracking-wider ${c.textMuted}`}>
                  ⚠️ {t('pm_warning_signs')}
                </p>
                <span className={`text-sm ${c.textMuted}`}>{expanded.warnings ? '▲' : '▼'}</span>
              </button>
              {expanded.warnings && (
                <div className={`border-t ${c.border}`}>
                  {pm.warning_signs_ignored.map((w, i) => (
                    <div key={i} className={`px-5 py-3 ${i > 0 ? `border-t ${c.border}` : ''}`}>
                      <div className="flex items-start gap-3">
                        <span className={`flex-shrink-0 text-xs font-bold px-2 py-0.5 rounded-full mt-0.5 ${c.warning}`}>{w.when}</span>
                        <div>
                          <p className={`text-sm font-semibold ${c.text}`}>{w.sign}</p>
                          <p className={`text-xs mt-0.5 italic ${c.textMuted}`}>{t('pm_dismissed_because')} {w.why_it_was_dismissed}</p>
                        </div>
                      </div>
                    </div>
                  ))}
                </div>
              )}
            </div>
          )}

          {/* Fatal assumption + point of no return */}
          <div className="grid grid-cols-1 sm:grid-cols-2 gap-4">
            {pm?.the_fatal_assumption && (
              <div className={`rounded-2xl border p-4 ${isDark ? 'bg-red-600/10 border-red-600/40' : 'bg-red-50 border-red-200'}`}>
                <p className={`text-xs font-bold uppercase tracking-wider mb-2 ${isDark ? 'text-red-400' : 'text-red-600'}`}>💣 {t('pm_fatal_assumption')}</p>
                <p className={`text-sm ${c.textSecondary}`}>{pm.the_fatal_assumption}</p>
              </div>
            )}
            {pm?.point_of_no_return && (
              <div className={`rounded-2xl border p-4 ${c.cardAlt} ${c.border}`}>
                <p className={`text-xs font-bold uppercase tracking-wider mb-2 ${c.textMuted}`}>🔒 {t('pm_point_no_return')}</p>
                <p className={`text-sm ${c.textSecondary}`}>{pm.point_of_no_return}</p>
              </div>
            )}
          </div>

          {/* Failure modes */}
          {results.failure_modes?.length > 0 && (
            <div className={`${c.card} border ${c.border} rounded-2xl overflow-hidden`}>
              <button onClick={() => toggle('modes')} className="w-full text-left px-5 py-4 flex items-center justify-between">
                <p className={`text-xs font-bold uppercase tracking-wider ${c.textMuted}`}>🎲 {t('pm_failure_modes')}</p>
                <span className={`text-sm ${c.textMuted}`}>{expanded.modes ? '▲' : '▼'}</span>
              </button>
              {expanded.modes && (
                <div className={`border-t ${c.border}`}>
                  {results.failure_modes.map((fm, i) => {
                    const pcfg = PROB_CONFIG[fm.probability] || PROB_CONFIG.medium;
                    return (
                      <div key={i} className={`px-5 py-4 ${i > 0 ? `border-t ${c.border}` : ''}`}>
                        <div className="flex items-start gap-2 mb-2 flex-wrap">
                          <span className={`text-xs font-bold px-2 py-0.5 rounded-full border ${probClsMap[pcfg.cls] || probClsMap.amber}`}>{pcfg.label}</span>
                          <span className={`text-sm font-bold ${c.text}`}>{fm.mode}</span>
                        </div>
                        <p className={`text-sm mb-1 ${c.textSecondary}`}>{fm.description}</p>
                        {fm.trigger       && <p className={`text-xs ${c.textMuted}`}><span className="font-semibold">{t('pm_trigger')}</span> {fm.trigger}</p>}
                        {fm.early_warning && <p className={`text-xs mt-0.5 ${c.textMuted}`}><span className="font-semibold">{t('pm_early_warning')}</span> {fm.early_warning}</p>}
                      </div>
                    );
                  })}
                </div>
              )}
            </div>
          )}

          {/* Most likely failure → prevention */}
          {results.the_most_likely && (
            <div className={`rounded-2xl border-2 p-5 ${isDark ? 'border-cyan-800 bg-cyan-800/10' : 'border-cyan-200 bg-cyan-50'}`}>
              <p className={`text-xs font-black uppercase tracking-widest mb-3 ${isDark ? 'text-cyan-400' : 'text-cyan-800'}`}>🎯 {t('pm_most_likely')}</p>
              <p className={`text-sm font-semibold mb-1 ${c.text}`}>{results.the_most_likely.failure_mode}</p>
              <div className={`mt-3 p-3 rounded-xl ${isDark ? 'bg-zinc-900/60' : 'bg-white/80'}`}>
                <p className={`text-xs font-bold uppercase tracking-wide mb-1 ${c.textMuted}`}>✅ {t('pm_the_prevention')}</p>
                <p className={`text-sm ${c.textSecondary}`}>{results.the_most_likely.the_prevention}</p>
              </div>
            </div>
          )}

          {/* Assumptions autopsy */}
          {results.assumptions_autopsy?.length > 0 && (
            <div className={`${c.card} border ${c.border} rounded-2xl overflow-hidden`}>
              <button onClick={() => toggle('assumptions')} className="w-full text-left px-5 py-4 flex items-center justify-between">
                <p className={`text-xs font-bold uppercase tracking-wider ${c.textMuted}`}>🔬 {t('pm_assumptions_autopsy')}</p>
                <span className={`text-sm ${c.textMuted}`}>{expanded.assumptions ? '▲' : '▼'}</span>
              </button>
              {expanded.assumptions && (
                <div className={`border-t ${c.border}`}>
                  {results.assumptions_autopsy.map((a, i) => (
                    <div key={i} className={`px-5 py-4 ${i > 0 ? `border-t ${c.border}` : ''}`}>
                      <p className={`text-sm font-semibold mb-1 ${c.text}`}>"{a.assumption}"</p>
                      <p className={`text-xs mb-0.5 ${c.textMuted}`}><span className="font-semibold">{t('pm_how_to_verify')}</span> {a.how_to_verify}</p>
                      <p className={`text-xs ${c.textMuted}`}><span className="font-semibold">{t('pm_risk_if_wrong')}</span> {a.risk_if_wrong}</p>
                    </div>
                  ))}
                </div>
              )}
            </div>
          )}

          {/* The One Thing */}
          {results.the_one_thing && (
            <div className={`${c.cardAlt} border ${c.border} rounded-2xl p-5`}>
              <p className={`text-xs font-black uppercase tracking-widest mb-2 ${c.accentTxt}`}>⚡ {t('pm_one_thing')}</p>
              <p className={`text-sm font-medium ${c.text}`}>{results.the_one_thing}</p>
            </div>
          )}

          {/* Actions */}
          <div className="flex items-center gap-3 flex-wrap">
            <button onClick={() => { setResults(null); setPlan(''); setPlanType(''); setStakes(''); setAssumptions(''); }}
              className={`px-4 py-2.5 rounded-xl text-sm font-semibold ${c.btnSecondary}`}>
              {t('pm_new_plan')}
            </button>
          </div>

          {/* Cross-references */}
          <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
            <p className={`text-xs font-bold ${c.textMuted} uppercase tracking-wide mb-3`}>🔗 {t('pm_related')}</p>
            <div className="flex flex-wrap gap-3">
              <a href="/DecisionCoach"  className={`text-xs ${linkStyle}`}>🧭 {t('pm_decisioncoach')}</a>
              <a href="/EgoKiller"      className={`text-xs ${linkStyle}`}>🪦 {t('pm_egokiller')}</a>
              <a href="/FutureProof"    className={`text-xs ${linkStyle}`}>🔮 {t('pm_futureproof')}</a>
            </div>
          </div>
        </div>
      )}

      {/* ── History ── */}
      {sessionHistory.length > 0 && (
        <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
          <p className={`text-xs font-bold ${c.textMuted} mb-2`}>📋 {t('pm_recent')}</p>
          <div className="space-y-1">
            {sessionHistory.map(s => (
              <div key={s.id} className="flex items-center justify-between">
                <span className={`text-xs ${c.textSecondary} truncate`}>{s.preview || t('pm_session')}</span>
                <span className={`text-xs ${c.textMuted} ml-2`}>{new Date(s.date).toLocaleDateString()}</span>
              </div>
            ))}
          </div>
        </div>
      )}
    </div>
  );
};

PreMortem.displayName = 'PreMortem';
export default PreMortem;
