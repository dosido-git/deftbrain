import React, { useState, useEffect } from 'react';
import { useTheme } from '../hooks/useTheme';
import { usePersistentState } from '../hooks/usePersistentState';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { CopyBtn, ActionBar } from '../components/ActionButtons';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const PLAN_TYPES = [
  { id: 'startup', label: '🚀 Startup / Business', icon: '🚀' },
  { id: 'career', label: '💼 Career Move', icon: '💼' },
  { id: 'product', label: '📦 Product Launch', icon: '📦' },
  { id: 'personal', label: '🧍 Personal Commitment', icon: '🧍' },
  { id: 'financial', label: '💰 Financial Decision', icon: '💰' },
  { id: 'project', label: '🏗️ Project', icon: '🏗️' },
  { id: 'relationship', label: '❤️ Relationship', icon: '❤️' },
  { id: 'creative', label: '🎨 Creative Venture', icon: '🎨' },
];

const PROB_CONFIG = {
  high:   { label: 'HIGH RISK',   color: (d) => d ? 'bg-red-600/20 text-zinc-500 border-red-600/40' : 'bg-zinc-500 text-zinc-500 border-zinc-500' },
  medium: { label: 'MEDIUM RISK', color: (d) => d ? 'bg-zinc-500 text-zinc-500 border-zinc-500' : 'bg-zinc-500 text-zinc-500 border-zinc-500' },
  low:    { label: 'LOW RISK',    color: (d) => d ? 'bg-zinc-500 text-zinc-500 border-zinc-500' : 'bg-zinc-500 text-zinc-500 border-zinc-500' },
};

const PreMortem = ({ tool }) => {
  const { isDark } = useTheme();

  useEffect(() => {
    const handler = (e) => {
      const tag = document.activeElement?.tagName;
      if (tag === 'INPUT' || tag === 'TEXTAREA' || tag === 'SELECT') return;
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && !loading) handleSubmit();
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
  }, [loading]);
  const { callToolEndpoint, loading } = useClaudeAPI();


  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';

  const c = {
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:         isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20' : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-500' : 'text-gray-400',
    labelText:     isDark ? 'text-zinc-200' : 'text-gray-700',
    accentTxt:     isDark ? 'text-cyan-400' : 'text-cyan-600',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white' : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200' : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200' : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200' : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200' : 'bg-red-50 border-red-200 text-red-800',
    infoBox:       isDark ? 'bg-sky-900/20 border-sky-700 text-sky-200' : 'bg-sky-50 border-sky-200 text-sky-800',
    successBox:    isDark ? 'bg-emerald-900/20 border-emerald-700' : 'bg-emerald-50 border-emerald-300',
    successTxt:    isDark ? 'text-emerald-300' : 'text-emerald-800',
    warningBox:    isDark ? 'bg-amber-900/20 border-amber-700' : 'bg-amber-50 border-amber-300',
    warningTxt:    isDark ? 'text-amber-300' : 'text-amber-800',
    pillActive:    isDark ? 'border-cyan-500 bg-cyan-900/30 text-cyan-200' : 'border-cyan-600 bg-cyan-100 text-cyan-900',
    pillInactive:  isDark ? 'border-zinc-600 text-zinc-400 hover:border-zinc-500' : 'border-gray-300 text-gray-500 hover:border-gray-400',
  };;

  const [plan, setPlan] = useState('');
  const [planType, setPlanType] = useState('');
  const [stakes, setStakes] = useState('');
  const [assumptions, setAssumptions] = useState('');
  const [results, setResults] = usePersistentState('premortem-result', null);
  const [history, setHistory] = usePersistentState('premortem-history', []);
  const [error, setError] = useState('');
  const [expanded, setExpanded] = useState({ warnings: true, modes: true, assumptions: false });

  const toggle = (k) => setExpanded(p => ({ ...p, [k]: !p[k] }));

  const handleSubmit = async () => {
    if (!plan.trim()) return;
    setError(''); setResults(null);
    try {
      const data = await callToolEndpoint('pre-mortem', {
        plan: plan.trim(),
        planType: planType || undefined,
        stakes: stakes.trim() || undefined,
        assumptions: assumptions.trim() || undefined,
      });
      setResults(data);
      setHistory(prev => [{ id: Date.now(), date: new Date().toISOString(), preview: (plan || '').slice(0, 40) }, ...prev].slice(0, 6));
    } catch (e) { setError(e.message || 'Failed to write pre-mortem.'); }
  };

  const buildText = () => {
    if (!results) return '';
    const pm = results.the_postmortem;
    let t = `💀 PRE-MORTEM\n\n${pm?.memo_header || 'POST-MORTEM REPORT'}\n\n`;
    t += `${pm?.executive_summary}\n\n`;
    t += `NARRATIVE:\n${pm?.narrative}\n\n`;
    if (pm?.the_fatal_assumption) t += `Fatal assumption: ${pm.the_fatal_assumption}\n`;
    if (pm?.point_of_no_return) t += `Point of no return: ${pm.point_of_no_return}\n\n`;
    if (results.the_most_likely) {
      t += `MOST LIKELY FAILURE: ${results.the_most_likely.failure_mode}\nPrevention: ${results.the_most_likely.the_prevention}\n\n`;
    }
    if (results.the_one_thing) t += `THE ONE THING:\n${results.the_one_thing}\n`;
    return t + BRAND;
  };

  const pm = results?.the_postmortem;

  return (
    <div className={`min-h-screen py-8 px-4 ${c.cardAlt}`}>
      <div className="max-w-2xl mx-auto">

        {/* Header */}
        <div className="text-center mb-8">
          <div className="text-5xl mb-3">💀</div>
          <h1 className={`text-3xl font-black tracking-tight mb-2 ${c.text}`}>Pre-Mortem</h1>
          <p className={`text-sm ${c.textMuteded}`}>The obituary for your plan — written before you execute it.</p>
        </div>

        {/* Input */}
        {!results && (
          <div className={`rounded-2xl border p-6 shadow-sm space-y-4 ${c.card} ${c.border}`}>
            <div>
              <label className={`block text-sm font-semibold mb-1.5 ${c.text}`}>What are you about to launch, start, or commit to? <span className="text-red-400">*</span></label>
              <textarea value={plan} onChange={e => setPlan(e.target.value)}
                placeholder="Describe your plan in enough detail to make the failure realistic — what it is, how it works, what you're betting on…"
                rows={4} maxLength={800}
                className={`w-full px-4 py-3 rounded-xl border text-sm resize-none ${c.input}`} />
            </div>

            <div>
              <label className={`block text-sm font-semibold mb-2 ${c.text}`}>Type <span className={`font-normal ${c.textMuteded}`}>(optional)</span></label>
              <div className="flex flex-wrap gap-2">
                {PLAN_TYPES.map(t => (
                  <button key={t.id} onClick={() => setPlanType(planType === t.id ? '' : t.id)}
                    className={`px-3 py-1.5 rounded-lg text-xs font-semibold border transition-all ${
                      planType === t.id
                        ? 'bg-cyan-800 text-white border-cyan-800'
                        : `${c.btnSecondaryondary}`
                    }`}>
                    {t.label}
                  </button>
                ))}
              </div>
            </div>

            <div className="grid grid-cols-1 sm:grid-cols-2 gap-3">
              <div>
                <label className={`block text-sm font-semibold mb-1.5 ${c.text}`}>What's at stake <span className={`font-normal ${c.textMuteded}`}>(optional)</span></label>
                <input type="text" value={stakes} onChange={e => setStakes(e.target.value)}
                  placeholder="Money, time, reputation, relationships…"
                  className={`w-full px-3 py-2.5 rounded-xl border text-sm ${c.input}`} />
              </div>
              <div>
                <label className={`block text-sm font-semibold mb-1.5 ${c.text}`}>Key assumptions you're making <span className={`font-normal ${c.textMuteded}`}>(optional)</span></label>
                <input type="text" value={assumptions} onChange={e => setAssumptions(e.target.value)}
                  placeholder="What has to be true for this to work…"
                  className={`w-full px-3 py-2.5 rounded-xl border text-sm ${c.input}`} />
              </div>
            </div>

            {error && <div className={`p-3 rounded-xl border text-sm ${c.danger}`}><span className="mr-1">⚠️</span>{error}</div>}

            <button onClick={handleSubmit} disabled={loading || !plan.trim()}
              className={`w-full py-3 rounded-xl font-bold disabled:opacity-40 ${c.btnPrimary}`}>
              {loading ? <><span className="inline-block animate-spin">{tool?.icon ?? '⚙️'}</span>Writing the failure…</> : '💀 Write the Pre-Mortem'}
            </button>

            <p className={`text-xs text-center ${c.textMuteded}`}>This doesn't mean your plan will fail. It means you'll know where to look.</p>
          </div>
        )}

        {/* Results */}
        {results && (
          <div className="space-y-4">

            {/* Memo header */}
            <div className={`rounded-2xl border-2 overflow-hidden ${isDark ? 'border-red-600/50 bg-zinc-800' : 'border-zinc-500 bg-white'}`}>
              <div className={`px-6 py-4 ${isDark ? 'bg-red-600/10' : 'bg-zinc-500'}`}>
                <p className={`text-xs font-black uppercase tracking-widest mb-1 ${isDark ? 'text-zinc-500' : 'text-zinc-500'}`}>
                  💀 Post-Mortem Report
                </p>
                <p className={`text-sm font-mono font-semibold ${isDark ? 'text-zinc-500' : 'text-zinc-500'}`}>{pm?.memo_header}</p>
              </div>
              <div className="px-6 py-4">
                <p className={`text-sm font-semibold leading-relaxed ${c.textSecondaryondary}`}>{pm?.executive_summary}</p>
              </div>
            </div>

            {/* Narrative */}
            {pm?.narrative && (
              <div className={`rounded-2xl border p-5 ${c.card} ${c.border}`}>
                <p className={`text-xs font-bold uppercase tracking-wider mb-3 ${c.textMuteded}`}>📖 How It Unfolded</p>
                <div className={`text-sm leading-relaxed whitespace-pre-line ${c.textSecondaryondary} font-mono`} style={{ fontFamily: 'Georgia, serif', fontStyle: 'italic' }}>
                  {pm.narrative}
                </div>
              </div>
            )}

            {/* Warning signs */}
            {pm?.warning_signs_ignored?.length > 0 && (
              <div className={`rounded-2xl border overflow-hidden ${c.card} ${c.border}`}>
                <button onClick={() => toggle('warnings')} className="w-full text-left px-5 py-4 flex items-center justify-between">
                  <p className={`text-xs font-bold uppercase tracking-wider ${isDark ? 'text-zinc-500' : 'text-zinc-500'}`}>
                    ⚠️ Warning Signs That Were Ignored
                  </p>
                  <span className={`text-sm ${c.textMuteded}`}>{expanded.warnings ? '▲' : '▼'}</span>
                </button>
                {expanded.warnings && (
                  <div className={`border-t ${c.border}`}>
                    {pm.warning_signs_ignored.map((w, i) => (
                      <div key={i} className={`px-5 py-3 ${i > 0 ? `border-t ${c.border}` : ''}`}>
                        <div className="flex items-start gap-3">
                          <span className={`flex-shrink-0 text-xs font-bold px-2 py-0.5 rounded-full mt-0.5 ${isDark ? 'bg-zinc-500 text-zinc-500' : 'bg-zinc-500 text-zinc-500'}`}>{w.when}</span>
                          <div>
                            <p className={`text-sm font-semibold ${c.text}`}>{w.sign}</p>
                            <p className={`text-xs mt-0.5 italic ${c.textMuteded}`}>Dismissed because: {w.why_it_was_dismissed}</p>
                          </div>
                        </div>
                      </div>
                    ))}
                  </div>
                )}
              </div>
            )}

            {/* Fatal assumption + point of no return */}
            <div className="grid grid-cols-1 sm:grid-cols-2 gap-4">
              {pm?.the_fatal_assumption && (
                <div className={`rounded-2xl border p-4 ${isDark ? 'bg-red-600/10 border-red-600/40' : 'bg-zinc-500 border-zinc-500'}`}>
                  <p className={`text-xs font-bold uppercase tracking-wider mb-2 ${isDark ? 'text-zinc-500' : 'text-zinc-500'}`}>💣 Fatal Assumption</p>
                  <p className={`text-sm ${c.textSecondaryondary}`}>{pm.the_fatal_assumption}</p>
                </div>
              )}
              {pm?.point_of_no_return && (
                <div className={`rounded-2xl border p-4 ${isDark ? 'bg-zinc-500 border-zinc-500' : 'bg-zinc-500 border-zinc-500'}`}>
                  <p className={`text-xs font-bold uppercase tracking-wider mb-2 ${c.textMuteded}`}>🔒 Point of No Return</p>
                  <p className={`text-sm ${c.textSecondaryondary}`}>{pm.point_of_no_return}</p>
                </div>
              )}
            </div>

            {/* Failure modes */}
            {results.failure_modes?.length > 0 && (
              <div className={`rounded-2xl border overflow-hidden ${c.card} ${c.border}`}>
                <button onClick={() => toggle('modes')} className="w-full text-left px-5 py-4 flex items-center justify-between">
                  <p className={`text-xs font-bold uppercase tracking-wider ${c.textMuteded}`}>🎲 Failure Modes</p>
                  <span className={`text-sm ${c.textMuteded}`}>{expanded.modes ? '▲' : '▼'}</span>
                </button>
                {expanded.modes && (
                  <div className={`border-t ${c.border}`}>
                    {results.failure_modes.map((fm, i) => {
                      const pcfg = PROB_CONFIG[fm.probability] || PROB_CONFIG.medium;
                      return (
                        <div key={i} className={`px-5 py-4 ${i > 0 ? `border-t ${c.border}` : ''}`}>
                          <div className="flex items-start gap-2 mb-2 flex-wrap">
                            <span className={`text-xs font-bold px-2 py-0.5 rounded-full border ${pcfg.color(isDark)}`}>{pcfg.label}</span>
                            <span className={`text-sm font-bold ${c.text}`}>{fm.mode}</span>
                          </div>
                          <p className={`text-sm mb-1 ${c.textSecondaryondary}`}>{fm.description}</p>
                          {fm.trigger && <p className={`text-xs ${c.textMuteded}`}><span className="font-semibold">Trigger:</span> {fm.trigger}</p>}
                          {fm.early_warning && <p className={`text-xs mt-0.5 ${c.textMuteded}`}><span className="font-semibold">Early warning:</span> {fm.early_warning}</p>}
                        </div>
                      );
                    })}
                  </div>
                )}
              </div>
            )}

            {/* Most likely failure → prevention */}
            {results.the_most_likely && (
              <div className={`rounded-2xl border-2 p-5 ${isDark ? 'border-cyan-800 bg-cyan-800/10' : 'border-cyan-800 bg-zinc-500'}`}>
                <p className={`text-xs font-black uppercase tracking-widest mb-3 ${isDark ? 'text-zinc-500' : 'text-cyan-800'}`}>🎯 Most Likely · One Prevention</p>
                <p className={`text-sm font-semibold mb-1 ${c.text}`}>{results.the_most_likely.failure_mode}</p>
                <div className={`mt-3 p-3 rounded-xl ${isDark ? 'bg-zinc-900/60' : 'bg-white/80'}`}>
                  <p className={`text-xs font-bold uppercase tracking-wide mb-1 ${isDark ? 'text-zinc-500' : 'text-zinc-500'}`}>✅ The Prevention</p>
                  <p className={`text-sm ${c.textSecondaryondary}`}>{results.the_most_likely.the_prevention}</p>
                </div>
              </div>
            )}

            {/* Assumptions autopsy */}
            {results.assumptions_autopsy?.length > 0 && (
              <div className={`rounded-2xl border overflow-hidden ${c.card} ${c.border}`}>
                <button onClick={() => toggle('assumptions')} className="w-full text-left px-5 py-4 flex items-center justify-between">
                  <p className={`text-xs font-bold uppercase tracking-wider ${c.textMuteded}`}>🔬 Assumptions Autopsy</p>
                  <span className={`text-sm ${c.textMuteded}`}>{expanded.assumptions ? '▲' : '▼'}</span>
                </button>
                {expanded.assumptions && (
                  <div className={`border-t ${c.border}`}>
                    {results.assumptions_autopsy.map((a, i) => (
                      <div key={i} className={`px-5 py-4 ${i > 0 ? `border-t ${c.border}` : ''}`}>
                        <p className={`text-sm font-semibold mb-1 ${c.text}`}>"{a.assumption}"</p>
                        <p className={`text-xs mb-0.5 ${c.textMuteded}`}><span className="font-semibold">How to verify:</span> {a.how_to_verify}</p>
                        <p className={`text-xs ${c.textMuteded}`}><span className="font-semibold">Risk if wrong:</span> {a.risk_if_wrong}</p>
                      </div>
                    ))}
                  </div>
                )}
              </div>
            )}

            {/* The One Thing */}
            {results.the_one_thing && (
              <div className={`rounded-2xl border p-5 ${isDark ? 'bg-zinc-500 border-zinc-500' : 'bg-zinc-500 border-zinc-500'}`}>
                <p className={`text-xs font-black uppercase tracking-widest mb-2 ${isDark ? 'text-zinc-500' : 'text-zinc-500'}`}>⚡ The One Thing — Before You Launch</p>
                <p className={`text-sm font-medium ${c.text}`}>{results.the_one_thing}</p>
              </div>
            )}

            {/* Actions */}
            <div className="flex items-center gap-3 flex-wrap">
              <ActionBar content={buildText()} title="Pre-Mortem" />
              <button onClick={() => { setResults(null); setPlan(''); setPlanType(''); setStakes(''); setAssumptions(''); }}
                className={`px-4 py-2 rounded-lg text-sm font-medium ${c.btnSecondaryondary}`}>
                🔄 New Plan
              </button>
            </div>

            {/* Cross-references */}
            <div className={`rounded-xl border p-4 ${c.cardAlt} ${c.border}`}>
              <p className={`text-xs font-semibold uppercase tracking-wide mb-3 ${c.textMuteded}`}>Related tools</p>
              <div className="flex flex-wrap gap-2">
                {[
                  { id: 'EgoKiller', icon: '🪦', label: 'Challenge your assumptions' },
                  { id: 'FutureProof', icon: '🔮', label: '5-year trajectory' },
                  { id: 'CrowdWisdom', icon: '👥', label: 'Get five perspectives' },
                ].map(r => (
                  <a key={r.id} href={`/tool/${r.id}`} target="_blank" rel="noopener noreferrer"
                    className={`px-3 py-1.5 rounded-lg text-xs font-medium transition-colors ${c.btnSecondaryondary}`}>
                    {r.icon} {r.label}
                  </a>
                ))}
              </div>
            </div>
          </div>
        )}
      </div>

      {/* eslint-disable-next-line no-restricted-globals */}
      {history.length > 0 && (<div className={`${c.cardAlt} border ${c.border} rounded-xl p-4 mt-4`}><p className={`text-xs font-bold ${c.textMuted} mb-2`}>📋 Recent</p><div className="space-y-1">{history.map(s => (<div key={s.id} className="flex items-center justify-between"><span className={`text-xs ${c.textSecondary} truncate`}>{s.preview||'Session'}</span><span className={`text-xs ${c.textMuted} ml-2`}>{new Date(s.date).toLocaleDateString()}</span></div>))}</div></div>)}
    </div>
  );
};

PreMortem.displayName = 'PreMortem';
export default PreMortem;
