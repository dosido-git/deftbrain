import React, { useState } from 'react';
import { useTheme } from '../hooks/useTheme';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { CopyBtn, ActionBar } from '../components/ActionButtons';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const PLAN_TYPES = [
  { id: 'startup', label: '🚀 Startup / Business', icon: '🚀' },
  { id: 'career', label: '💼 Career Move', icon: '💼' },
  { id: 'product', label: '📦 Product Launch', icon: '📦' },
  { id: 'personal', label: '🧍 Personal Commitment', icon: '🧍' },
  { id: 'financial', label: '💰 Financial Decision', icon: '💰' },
  { id: 'project', label: '🏗️ Project', icon: '🏗️' },
  { id: 'relationship', label: '❤️ Relationship', icon: '❤️' },
  { id: 'creative', label: '🎨 Creative Venture', icon: '🎨' },
];

const PROB_CONFIG = {
  high:   { label: 'HIGH RISK',   color: (d) => d ? 'bg-[#b54a3f]/20 text-[#e8a9a3] border-[#b54a3f]/40' : 'bg-[#fceae8] text-[#8a3028] border-[#d4908a]' },
  medium: { label: 'MEDIUM RISK', color: (d) => d ? 'bg-[#c8872e]/20 text-[#d9a04e] border-[#c8872e]/40' : 'bg-[#fdf3e4] text-[#93541f] border-[#e0c49a]' },
  low:    { label: 'LOW RISK',    color: (d) => d ? 'bg-[#5a8a5c]/20 text-[#8aba8c] border-[#5a8a5c]/40' : 'bg-[#e8f0e8] text-[#3d6e3f] border-[#a8c4a8]' },
};

const PreMortem = () => {
  const { theme } = useTheme();
  const isDark = theme === 'dark';
  const { callToolEndpoint, loading } = useClaudeAPI();

  const c = {
    pageBg:       isDark ? 'bg-[#1a1816]'    : 'bg-[#faf8f5]',
    card:         isDark ? 'bg-[#2a2623]'     : 'bg-white',
    cardAlt:      isDark ? 'bg-[#332e2a]'     : 'bg-[#faf8f5]',
    cardBorder:   isDark ? 'border-[#3d3630]' : 'border-[#e8e1d5]',
    input:        isDark ? 'bg-[#1a1816] border-[#3d3630] text-[#f0eeea] placeholder:text-[#8a8275]'
                         : 'bg-[#faf8f5] border-[#d5cab8] text-[#3d3935] placeholder:text-[#8a8275]',
    inputFocus:   'focus:outline-none focus:ring-2 focus:ring-[#2c4a6e]/20 focus:border-[#4a6a8a]',
    text:          isDark ? 'text-[#f0eeea]'  : 'text-[#3d3935]',
    textSecondary: isDark ? 'text-[#c8c3b9]'  : 'text-[#5a544a]',
    textMuted:     isDark ? 'text-[#8a8275]'  : 'text-[#8a8275]',
    heading:       isDark ? 'text-[#f3efe8]'  : 'text-[#1e2a3a]',
    btnPrimary:    isDark ? 'bg-[#2c4a6e] hover:bg-[#4a6a8a] text-white' : 'bg-[#2c4a6e] hover:bg-[#1e3a58] text-white',
    btnSecondary:  isDark ? 'bg-[#332e2a] hover:bg-[#3d3630] text-[#c8c3b9] border border-[#3d3630]' : 'bg-[#f3efe8] hover:bg-[#e8e1d5] text-[#5e5042] border border-[#d5cab8]',
    divider:       isDark ? 'border-[#3d3630]' : 'border-[#e8e1d5]',
    error:         isDark ? 'bg-[#b54a3f]/15 border-[#b54a3f]/50 text-[#e8a9a3]' : 'bg-[#fceae8] border-[#d4908a] text-[#8a3028]',
  };

  const [plan, setPlan] = useState('');
  const [planType, setPlanType] = useState('');
  const [stakes, setStakes] = useState('');
  const [assumptions, setAssumptions] = useState('');
  const [results, setResults] = useState(null);
  const [error, setError] = useState('');
  const [expanded, setExpanded] = useState({ warnings: true, modes: true, assumptions: false });

  const toggle = (k) => setExpanded(p => ({ ...p, [k]: !p[k] }));

  const handleSubmit = async () => {
    if (!plan.trim()) return;
    setError(''); setResults(null);
    try {
      const data = await callToolEndpoint('pre-mortem', {
        plan: plan.trim(),
        planType: planType || undefined,
        stakes: stakes.trim() || undefined,
        assumptions: assumptions.trim() || undefined,
      });
      setResults(data);
    } catch (e) { setError(e.message || 'Failed to write pre-mortem.'); }
  };

  const buildText = () => {
    if (!results) return '';
    const pm = results.the_postmortem;
    let t = `💀 PRE-MORTEM\n\n${pm?.memo_header || 'POST-MORTEM REPORT'}\n\n`;
    t += `${pm?.executive_summary}\n\n`;
    t += `NARRATIVE:\n${pm?.narrative}\n\n`;
    if (pm?.the_fatal_assumption) t += `Fatal assumption: ${pm.the_fatal_assumption}\n`;
    if (pm?.point_of_no_return) t += `Point of no return: ${pm.point_of_no_return}\n\n`;
    if (results.the_most_likely) {
      t += `MOST LIKELY FAILURE: ${results.the_most_likely.failure_mode}\nPrevention: ${results.the_most_likely.the_prevention}\n\n`;
    }
    if (results.the_one_thing) t += `THE ONE THING:\n${results.the_one_thing}\n`;
    return t + BRAND;
  };

  const pm = results?.the_postmortem;

  return (
    <div className={`min-h-screen py-8 px-4 ${c.pageBg}`}>
      <div className="max-w-2xl mx-auto">

        {/* Header */}
        <div className="text-center mb-8">
          <div className="text-5xl mb-3">💀</div>
          <h1 className={`text-3xl font-black tracking-tight mb-2 ${c.heading}`}>Pre-Mortem</h1>
          <p className={`text-sm ${c.textMuted}`}>The obituary for your plan — written before you execute it.</p>
        </div>

        {/* Input */}
        {!results && (
          <div className={`rounded-2xl border p-6 shadow-sm space-y-4 ${c.card} ${c.cardBorder}`}>
            <div>
              <label className={`block text-sm font-semibold mb-1.5 ${c.text}`}>What are you about to launch, start, or commit to? <span className="text-red-400">*</span></label>
              <textarea value={plan} onChange={e => setPlan(e.target.value)}
                placeholder="Describe your plan in enough detail to make the failure realistic — what it is, how it works, what you're betting on…"
                rows={4} maxLength={800}
                className={`w-full px-4 py-3 rounded-xl border text-sm resize-none ${c.input} ${c.inputFocus}`} />
            </div>

            <div>
              <label className={`block text-sm font-semibold mb-2 ${c.text}`}>Type <span className={`font-normal ${c.textMuted}`}>(optional)</span></label>
              <div className="flex flex-wrap gap-2">
                {PLAN_TYPES.map(t => (
                  <button key={t.id} onClick={() => setPlanType(planType === t.id ? '' : t.id)}
                    className={`px-3 py-1.5 rounded-lg text-xs font-semibold border transition-all ${
                      planType === t.id
                        ? 'bg-[#2c4a6e] text-white border-[#2c4a6e]'
                        : `${c.btnSecondary}`
                    }`}>
                    {t.label}
                  </button>
                ))}
              </div>
            </div>

            <div className="grid grid-cols-1 sm:grid-cols-2 gap-3">
              <div>
                <label className={`block text-sm font-semibold mb-1.5 ${c.text}`}>What's at stake <span className={`font-normal ${c.textMuted}`}>(optional)</span></label>
                <input type="text" value={stakes} onChange={e => setStakes(e.target.value)}
                  placeholder="Money, time, reputation, relationships…"
                  className={`w-full px-3 py-2.5 rounded-xl border text-sm ${c.input} ${c.inputFocus}`} />
              </div>
              <div>
                <label className={`block text-sm font-semibold mb-1.5 ${c.text}`}>Key assumptions you're making <span className={`font-normal ${c.textMuted}`}>(optional)</span></label>
                <input type="text" value={assumptions} onChange={e => setAssumptions(e.target.value)}
                  placeholder="What has to be true for this to work…"
                  className={`w-full px-3 py-2.5 rounded-xl border text-sm ${c.input} ${c.inputFocus}`} />
              </div>
            </div>

            {error && <div className={`p-3 rounded-xl border text-sm ${c.error}`}><span className="mr-1">⚠️</span>{error}</div>}

            <button onClick={handleSubmit} disabled={loading || !plan.trim()}
              className={`w-full py-3 rounded-xl font-bold disabled:opacity-40 ${c.btnPrimary}`}>
              {loading ? <><span className="animate-spin inline-block mr-2">⏳</span>Writing the failure…</> : '💀 Write the Pre-Mortem'}
            </button>

            <p className={`text-xs text-center ${c.textMuted}`}>This doesn't mean your plan will fail. It means you'll know where to look.</p>
          </div>
        )}

        {/* Results */}
        {results && (
          <div className="space-y-4">

            {/* Memo header */}
            <div className={`rounded-2xl border-2 overflow-hidden ${isDark ? 'border-[#b54a3f]/50 bg-[#2a2623]' : 'border-[#d4908a] bg-white'}`}>
              <div className={`px-6 py-4 ${isDark ? 'bg-[#b54a3f]/10' : 'bg-[#fceae8]'}`}>
                <p className={`text-xs font-black uppercase tracking-widest mb-1 ${isDark ? 'text-[#e8a9a3]' : 'text-[#8a3028]'}`}>
                  💀 Post-Mortem Report
                </p>
                <p className={`text-sm font-mono font-semibold ${isDark ? 'text-[#e8a9a3]' : 'text-[#8a3028]'}`}>{pm?.memo_header}</p>
              </div>
              <div className="px-6 py-4">
                <p className={`text-sm font-semibold leading-relaxed ${c.textSecondary}`}>{pm?.executive_summary}</p>
              </div>
            </div>

            {/* Narrative */}
            {pm?.narrative && (
              <div className={`rounded-2xl border p-5 ${c.card} ${c.cardBorder}`}>
                <p className={`text-xs font-bold uppercase tracking-wider mb-3 ${c.textMuted}`}>📖 How It Unfolded</p>
                <div className={`text-sm leading-relaxed whitespace-pre-line ${c.textSecondary} font-mono`} style={{ fontFamily: 'Georgia, serif', fontStyle: 'italic' }}>
                  {pm.narrative}
                </div>
              </div>
            )}

            {/* Warning signs */}
            {pm?.warning_signs_ignored?.length > 0 && (
              <div className={`rounded-2xl border overflow-hidden ${c.card} ${c.cardBorder}`}>
                <button onClick={() => toggle('warnings')} className="w-full text-left px-5 py-4 flex items-center justify-between">
                  <p className={`text-xs font-bold uppercase tracking-wider ${isDark ? 'text-[#d9a04e]' : 'text-[#93541f]'}`}>
                    ⚠️ Warning Signs That Were Ignored
                  </p>
                  <span className={`text-sm ${c.textMuted}`}>{expanded.warnings ? '▲' : '▼'}</span>
                </button>
                {expanded.warnings && (
                  <div className={`border-t ${c.divider}`}>
                    {pm.warning_signs_ignored.map((w, i) => (
                      <div key={i} className={`px-5 py-3 ${i > 0 ? `border-t ${c.divider}` : ''}`}>
                        <div className="flex items-start gap-3">
                          <span className={`flex-shrink-0 text-xs font-bold px-2 py-0.5 rounded-full mt-0.5 ${isDark ? 'bg-[#c8872e]/20 text-[#d9a04e]' : 'bg-[#fdf3e4] text-[#93541f]'}`}>{w.when}</span>
                          <div>
                            <p className={`text-sm font-semibold ${c.text}`}>{w.sign}</p>
                            <p className={`text-xs mt-0.5 italic ${c.textMuted}`}>Dismissed because: {w.why_it_was_dismissed}</p>
                          </div>
                        </div>
                      </div>
                    ))}
                  </div>
                )}
              </div>
            )}

            {/* Fatal assumption + point of no return */}
            <div className="grid grid-cols-1 sm:grid-cols-2 gap-4">
              {pm?.the_fatal_assumption && (
                <div className={`rounded-2xl border p-4 ${isDark ? 'bg-[#b54a3f]/10 border-[#b54a3f]/40' : 'bg-[#fceae8] border-[#d4908a]'}`}>
                  <p className={`text-xs font-bold uppercase tracking-wider mb-2 ${isDark ? 'text-[#e8a9a3]' : 'text-[#8a3028]'}`}>💣 Fatal Assumption</p>
                  <p className={`text-sm ${c.textSecondary}`}>{pm.the_fatal_assumption}</p>
                </div>
              )}
              {pm?.point_of_no_return && (
                <div className={`rounded-2xl border p-4 ${isDark ? 'bg-[#3d3630]/60 border-[#3d3630]' : 'bg-[#f3efe8] border-[#d5cab8]'}`}>
                  <p className={`text-xs font-bold uppercase tracking-wider mb-2 ${c.textMuted}`}>🔒 Point of No Return</p>
                  <p className={`text-sm ${c.textSecondary}`}>{pm.point_of_no_return}</p>
                </div>
              )}
            </div>

            {/* Failure modes */}
            {results.failure_modes?.length > 0 && (
              <div className={`rounded-2xl border overflow-hidden ${c.card} ${c.cardBorder}`}>
                <button onClick={() => toggle('modes')} className="w-full text-left px-5 py-4 flex items-center justify-between">
                  <p className={`text-xs font-bold uppercase tracking-wider ${c.textMuted}`}>🎲 Failure Modes</p>
                  <span className={`text-sm ${c.textMuted}`}>{expanded.modes ? '▲' : '▼'}</span>
                </button>
                {expanded.modes && (
                  <div className={`border-t ${c.divider}`}>
                    {results.failure_modes.map((fm, i) => {
                      const pcfg = PROB_CONFIG[fm.probability] || PROB_CONFIG.medium;
                      return (
                        <div key={i} className={`px-5 py-4 ${i > 0 ? `border-t ${c.divider}` : ''}`}>
                          <div className="flex items-start gap-2 mb-2 flex-wrap">
                            <span className={`text-xs font-bold px-2 py-0.5 rounded-full border ${pcfg.color(isDark)}`}>{pcfg.label}</span>
                            <span className={`text-sm font-bold ${c.text}`}>{fm.mode}</span>
                          </div>
                          <p className={`text-sm mb-1 ${c.textSecondary}`}>{fm.description}</p>
                          {fm.trigger && <p className={`text-xs ${c.textMuted}`}><span className="font-semibold">Trigger:</span> {fm.trigger}</p>}
                          {fm.early_warning && <p className={`text-xs mt-0.5 ${c.textMuted}`}><span className="font-semibold">Early warning:</span> {fm.early_warning}</p>}
                        </div>
                      );
                    })}
                  </div>
                )}
              </div>
            )}

            {/* Most likely failure → prevention */}
            {results.the_most_likely && (
              <div className={`rounded-2xl border-2 p-5 ${isDark ? 'border-[#2c4a6e] bg-[#2c4a6e]/10' : 'border-[#2c4a6e] bg-[#e8eef5]'}`}>
                <p className={`text-xs font-black uppercase tracking-widest mb-3 ${isDark ? 'text-[#a8b9ce]' : 'text-[#2c4a6e]'}`}>🎯 Most Likely · One Prevention</p>
                <p className={`text-sm font-semibold mb-1 ${c.text}`}>{results.the_most_likely.failure_mode}</p>
                <div className={`mt-3 p-3 rounded-xl ${isDark ? 'bg-[#1a1816]/60' : 'bg-white/80'}`}>
                  <p className={`text-xs font-bold uppercase tracking-wide mb-1 ${isDark ? 'text-[#8aba8c]' : 'text-[#3d6e3f]'}`}>✅ The Prevention</p>
                  <p className={`text-sm ${c.textSecondary}`}>{results.the_most_likely.the_prevention}</p>
                </div>
              </div>
            )}

            {/* Assumptions autopsy */}
            {results.assumptions_autopsy?.length > 0 && (
              <div className={`rounded-2xl border overflow-hidden ${c.card} ${c.cardBorder}`}>
                <button onClick={() => toggle('assumptions')} className="w-full text-left px-5 py-4 flex items-center justify-between">
                  <p className={`text-xs font-bold uppercase tracking-wider ${c.textMuted}`}>🔬 Assumptions Autopsy</p>
                  <span className={`text-sm ${c.textMuted}`}>{expanded.assumptions ? '▲' : '▼'}</span>
                </button>
                {expanded.assumptions && (
                  <div className={`border-t ${c.divider}`}>
                    {results.assumptions_autopsy.map((a, i) => (
                      <div key={i} className={`px-5 py-4 ${i > 0 ? `border-t ${c.divider}` : ''}`}>
                        <p className={`text-sm font-semibold mb-1 ${c.text}`}>"{a.assumption}"</p>
                        <p className={`text-xs mb-0.5 ${c.textMuted}`}><span className="font-semibold">How to verify:</span> {a.how_to_verify}</p>
                        <p className={`text-xs ${c.textMuted}`}><span className="font-semibold">Risk if wrong:</span> {a.risk_if_wrong}</p>
                      </div>
                    ))}
                  </div>
                )}
              </div>
            )}

            {/* The One Thing */}
            {results.the_one_thing && (
              <div className={`rounded-2xl border p-5 ${isDark ? 'bg-[#c8872e]/10 border-[#c8872e]/30' : 'bg-[#fdf3e4] border-[#e0c49a]'}`}>
                <p className={`text-xs font-black uppercase tracking-widest mb-2 ${isDark ? 'text-[#d9a04e]' : 'text-[#93541f]'}`}>⚡ The One Thing — Before You Launch</p>
                <p className={`text-sm font-medium ${c.text}`}>{results.the_one_thing}</p>
              </div>
            )}

            {/* Actions */}
            <div className="flex items-center gap-3 flex-wrap">
              <ActionBar content={buildText()} title="Pre-Mortem" />
              <button onClick={() => { setResults(null); setPlan(''); setPlanType(''); setStakes(''); setAssumptions(''); }}
                className={`px-4 py-2 rounded-lg text-sm font-medium ${c.btnSecondary}`}>
                🔄 New Plan
              </button>
            </div>

            {/* Cross-references */}
            <div className={`rounded-xl border p-4 ${c.cardAlt} ${c.cardBorder}`}>
              <p className={`text-xs font-semibold uppercase tracking-wide mb-3 ${c.textMuted}`}>Related tools</p>
              <div className="flex flex-wrap gap-2">
                {[
                  { id: 'EgoKiller', icon: '🪦', label: 'Challenge your assumptions' },
                  { id: 'FutureProof', icon: '🔮', label: '5-year trajectory' },
                  { id: 'CrowdWisdom', icon: '👥', label: 'Get five perspectives' },
                ].map(r => (
                  <a key={r.id} href={`/tool/${r.id}`} target="_blank" rel="noopener noreferrer"
                    className={`px-3 py-1.5 rounded-lg text-xs font-medium transition-colors ${c.btnSecondary}`}>
                    {r.icon} {r.label}
                  </a>
                ))}
              </div>
            </div>
          </div>
        )}
      </div>
    </div>
  );
};

PreMortem.displayName = 'PreMortem';
export default PreMortem;
