import React, { useState, useRef, useEffect, useCallback } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { CopyBtn } from '../components/ActionButtons';
import { useRegisterActions } from '../components/ActionBarContext';
import { usePersistentState } from '../hooks/usePersistentState';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const MODES = [
  { id: 'cut',        label: 'Cut',        icon: '✂️', desc: 'Trim to fit your time' },
  { id: 'anticipate', label: 'Anticipate', icon: '🎯', desc: 'Predict tough Q&A' },
  { id: 'hook',       label: 'Hook',       icon: '🪝', desc: 'Rewrite open & close' },
];

const AUDIENCE_OPTIONS = [
  { id: 'executives',    label: 'Executives',     icon: '👔' },
  { id: 'investors',     label: 'Investors / VCs', icon: '💰' },
  { id: 'team',          label: 'Internal Team',   icon: '🤝' },
  { id: 'clients',       label: 'Clients',         icon: '💼' },
  { id: 'academic',      label: 'Academic',        icon: '🎓' },
  { id: 'general',       label: 'General',         icon: '👥' },
];

const TONE_OPTIONS = [
  { id: 'authoritative',  label: 'Authoritative',  icon: '🎤', desc: 'Commanding keynote' },
  { id: 'conversational', label: 'Conversational', icon: '☕', desc: 'Warm fireside chat' },
  { id: 'provocative',    label: 'Provocative',    icon: '⚡', desc: 'Bold and challenging' },
  { id: 'inspirational',  label: 'Inspirational',  icon: '✨', desc: 'Uplifting vision' },
];

const DIFFICULTY_COLORS = {
  hard:      { badge: 'bg-amber-900/20 text-amber-400', label: 'Hard' },
  very_hard: { badge: 'bg-red-900/20 text-red-400', label: 'Very Hard' },
  killer:    { badge: 'bg-red-900/30 text-red-300', label: 'Killer' },
};

const TheRunthrough = ({ tool }) => {
  const { callToolEndpoint, loading } = useClaudeAPI();
  const { isDark } = useTheme();
  const resultsRef = useRef(null);

  // ─── Colors ───
  const c = {
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:         isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20' : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-500' : 'text-gray-400',
    labelText:     isDark ? 'text-zinc-200' : 'text-gray-700',
    required:      isDark ? 'text-amber-400' : 'text-amber-500',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white' : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200' : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200' : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200' : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200' : 'bg-red-50 border-red-200 text-red-800',
    tabActive:     isDark ? 'text-cyan-300 border-b-2 border-cyan-400' : 'text-cyan-700 border-b-2 border-cyan-600',
    tabInactive:   isDark ? 'text-zinc-500 hover:text-zinc-300' : 'text-gray-400 hover:text-gray-600',
    chipActive:    isDark ? 'bg-cyan-900/40 border-cyan-500 text-cyan-200' : 'bg-cyan-100 border-cyan-600 text-cyan-900',
    chipInactive:  isDark ? 'bg-zinc-800 border-zinc-600 text-zinc-400 hover:border-zinc-500' : 'bg-white border-gray-200 text-gray-500 hover:border-gray-300',
    accentBox:     isDark ? 'bg-sky-900/20 border-sky-700' : 'bg-sky-50 border-sky-200',
    accentTxt:     isDark ? 'text-sky-300' : 'text-sky-700',
    goldBox:       isDark ? 'bg-amber-900/20 border-amber-700' : 'bg-amber-50 border-amber-300',
    goldTxt:       isDark ? 'text-amber-300' : 'text-amber-700',
  };
  c.textMuteded = c.textMuted;
  c.label       = c.labelText;

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';


  // ─── State ───
  const [mode, setMode] = useState('cut');
  const [content, setContent] = useState('');
  const [timeMinutes, setTimeMinutes] = useState(5);
  const [context, setContext] = useState('');
  const [audience, setAudience] = useState('general');
  const [stakes, setStakes] = useState('');
  const [tone, setTone] = useState('conversational');
  const [goal, setGoal] = useState('');
  const [history, setHistory] = usePersistentState('the-runthrough-history', []);
  const [results, setResults] = usePersistentState('the-runthrough-results', null);
  const [error, setError] = useState('');
  const [expandedSections, setExpandedSections] = useState({});

  useEffect(() => {
    if (!results) return;
    const timer = setTimeout(() => {
      resultsRef.current?.scrollIntoView({ behavior: 'smooth', block: 'start' });
    }, 250);
    return () => clearTimeout(timer);
  }, [results]);

  const toggleSection = (key) => {
    setExpandedSections(prev => ({ ...prev, [key]: !prev[key] }));
  };

  // ─── Handlers ───
  const handleSubmit = useCallback(async () => {
    if (!content.trim()) return;
    setError('');
    setResults(null);
    setExpandedSections({});

    try {
      let endpoint, payload;

      if (mode === 'cut') {
        endpoint = 'the-runthrough-cut';
        payload = { content: content.trim(), timeMinutes, context };
      } else if (mode === 'anticipate') {
        endpoint = 'the-runthrough-anticipate';
        payload = { content: content.trim(), audience, stakes };
      } else {
        endpoint = 'the-runthrough-hook';
        payload = { content: content.trim(), tone, goal };
      }

      const res = await callToolEndpoint(endpoint, payload);
      setResults({ mode, data: res });
      setHistory(prev => [{
        id: 'tr_' + Date.now(),
        date: new Date().toISOString(),
        preview: content.trim().slice(0, 40),
        mode,
      }, ...prev].slice(0, 6));
    } catch (err) {
      setError(err.message || 'Something went wrong. Try again.');
    }
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [content, mode, timeMinutes, context, audience, stakes, tone, goal, loading]);

  // ─── Keyboard: SELECT-only guard, ref pattern to avoid stale closures ───
  const handleSubmitRef = useRef(null);
  const canSubmitRef = useRef(false);
  handleSubmitRef.current = handleSubmit;
  canSubmitRef.current = !!content.trim() && !loading;

  useEffect(() => {
    const handler = (e) => {
      const tag = document.activeElement?.tagName;
      if (tag === 'SELECT') return;
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && !loading && canSubmitRef.current) {
        e.preventDefault();
        handleSubmitRef.current?.();
      }
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
  }, [loading]);

  const handleReset = () => {
    setResults(null);
    setError('');
    setExpandedSections({});
  };

  const handleModeSwitch = (newMode) => {
    setMode(newMode);
    setResults(null);
    setError('');
    setExpandedSections({});
  };

  // ─── Build text helpers ───
  const buildCutText = (d) => {
    let t = `✂️ THE RUNTHROUGH — CUT MODE\n`;
    t += `Original: ~${d.original_word_count} words (~${d.original_est_minutes} min)\n`;
    t += `Trimmed: ~${d.trimmed_word_count} words (~${d.trimmed_est_minutes} min) → Target: ${d.target_minutes} min\n\n`;
    t += `━━ TRIMMED CONTENT ━━\n${d.trimmed_content}\n\n`;
    if (d.what_was_cut?.length) {
      t += `━━ WHAT WAS CUT ━━\n`;
      d.what_was_cut.forEach(cut => { t += `• ${cut.section}: ${cut.reason}\n`; });
      t += '\n';
    }
    if (d.what_was_kept) t += `━━ CORE THREAD ━━\n${d.what_was_kept}\n\n`;
    if (d.pacing_notes) t += `━━ PACING ━━\n${d.pacing_notes}\n\n`;
    return t + BRAND.trimStart();
  };

  const buildAnticipateText = (d) => {
    let t = `🎯 THE RUNTHROUGH — ANTICIPATE MODE\n`;
    t += `${d.presentation_summary}\n\n`;
    if (d.vulnerability_scan) {
      t += `━━ VULNERABILITY SCAN ━━\n`;
      t += `Weakest claim: ${d.vulnerability_scan.weakest_claim}\n`;
      t += `Missing data: ${d.vulnerability_scan.missing_data}\n`;
      t += `Assumption risk: ${d.vulnerability_scan.assumption_risk}\n\n`;
    }
    t += `━━ TOUGH QUESTIONS ━━\n`;
    (d.tough_questions || []).forEach((q, i) => {
      t += `${i + 1}. [${q.difficulty}] ${q.question}\n`;
      t += `   Answer: ${q.draft_answer}\n`;
      t += `   Trap: ${q.trap_to_avoid}\n\n`;
    });
    if (d.curveball) {
      t += `━━ CURVEBALL ━━\n${d.curveball.question}\nAnswer: ${d.curveball.draft_answer}\n\n`;
    }
    if (d.overall_readiness) t += `━━ READINESS ━━\n${d.overall_readiness}\n\n`;
    return t + BRAND.trimStart();
  };

  const buildHookText = (d) => {
    let t = `🪝 THE RUNTHROUGH — HOOK MODE\n\n`;
    if (d.diagnosis) {
      t += `━━ DIAGNOSIS ━━\n`;
      t += `Opening: ${d.diagnosis.current_opening} — ${d.diagnosis.opening_problem}\n`;
      t += `Closing: ${d.diagnosis.current_closing} — ${d.diagnosis.closing_problem}\n\n`;
    }
    if (d.new_opening) {
      t += `━━ NEW OPENING (${d.new_opening.technique}) ━━\n${d.new_opening.text}\n\n`;
    }
    if (d.new_closing) {
      t += `━━ NEW CLOSING (${d.new_closing.technique}) ━━\n${d.new_closing.text}\n\n`;
    }
    if (d.transitions?.length) {
      t += `━━ TRANSITIONS ━━\n`;
      d.transitions.forEach(tr => { t += `${tr.between}: "${tr.rewritten}"\n`; });
      t += '\n';
    }
    if (d.energy_arc) t += `━━ ENERGY ARC ━━\n${d.energy_arc}\n\n`;
    return t + BRAND.trimStart();
  };

  const buildFullText = () => {
    if (!results) return '';
    const d = results.data;
    if (results.mode === 'cut') return buildCutText(d);
    if (results.mode === 'anticipate') return buildAnticipateText(d);
    return buildHookText(d);
  };

  // Register export content with the wrapper's persistent ActionBar
  useRegisterActions(buildFullText(), tool?.title || 'The Runthrough');

  // ─── Loading messages ───
  const loadingMessages = {
    cut: 'Trimming the fat...',
    anticipate: 'Thinking like your toughest audience member...',
    hook: 'Rewriting your opening to land harder...',
  };

  // ─── Submit button labels ───
  const submitLabels = {
    cut: <><span className="mr-1">{tool?.icon ?? '🎙️'}</span> Cut It Down</>,
    anticipate: <><span className="mr-1">{tool?.icon ?? '🎙️'}</span> Predict the Q&amp;A</>,
    hook: <><span className="mr-1">{tool?.icon ?? '🎙️'}</span> Rewrite My Hooks</>,
  };

  // ─── Render: Cut Results ───
  const CutResults = ({ data: d }) => (
    <div className="space-y-5">
      {/* Stats bar */}
      <div className={`grid grid-cols-3 gap-3`}>
        {[
          { label: 'Original', value: `~${d.original_est_minutes} min`, sub: `${d.original_word_count} words` },
          { label: 'Trimmed', value: `~${d.trimmed_est_minutes} min`, sub: `${d.trimmed_word_count} words` },
          { label: 'Target', value: `${d.target_minutes} min`, sub: d.trimmed_est_minutes <= d.target_minutes ? '✅ Fits!' : '⚠️ Close' },
        ].map((s, i) => (
          <div key={i} className={`${c.cardAlt} ${c.border} border rounded-xl p-3 text-center`}>
            <p className={`text-xs ${c.textMuted} uppercase font-semibold`}>{s.label}</p>
            <p className={`text-lg font-bold ${c.text}`}>{s.value}</p>
            <p className={`text-xs ${c.textMuted}`}>{s.sub}</p>
          </div>
        ))}
      </div>

      {/* Trimmed content */}
      <div className={`${c.card} ${c.border} border rounded-2xl p-5`}>
        <div className="flex items-center justify-between mb-3">
          <h3 className={`font-bold ${c.text} flex items-center gap-2`}>
            <span>📄</span> Trimmed Presentation
          </h3>
          <CopyBtn content={`${d.trimmed_content}${BRAND}`} label="Copy" />
        </div>
        <div className={`${c.input} ${c.border} border rounded-xl p-4`}>
          <p className={`text-sm ${c.text} leading-relaxed whitespace-pre-wrap`}>
            {d.trimmed_content}
          </p>
        </div>
      </div>

      {/* What was cut */}
      {d.what_was_cut?.length > 0 && (
        <div className={`${c.card} ${c.border} border rounded-2xl p-5`}>
          <button onClick={() => toggleSection('cuts')} className="w-full flex items-center justify-between">
            <h3 className={`font-bold ${c.text} flex items-center gap-2`}>
              <span>🗑️</span> What Was Cut ({d.what_was_cut.length})
            </h3>
            <span className={`text-sm ${c.textMuted}`}>{expandedSections['cuts'] !== false ? '▲' : '▼'}</span>
          </button>
          {expandedSections['cuts'] !== false && (
            <div className="mt-3 space-y-2">
              {d.what_was_cut.map((cut, i) => (
                <div key={i} className={`${c.danger} rounded-xl p-3 flex items-start gap-2`}>
                  <span className="text-sm flex-shrink-0">✕</span>
                  <div>
                    <p className={`text-sm font-medium ${c.text}`}>{cut.section}</p>
                    <p className={`text-xs ${c.textMuted} mt-0.5`}>{cut.reason}</p>
                  </div>
                </div>
              ))}
            </div>
          )}
        </div>
      )}

      {/* Core thread + pacing */}
      <div className="grid grid-cols-1 sm:grid-cols-2 gap-4">
        {d.what_was_kept && (
          <div className={`${c.success} rounded-2xl p-4`}>
            <p className={`text-xs font-semibold uppercase ${isDark ? 'text-emerald-400' : 'text-emerald-700'} mb-2 flex items-center gap-1.5`}>
              <span>💎</span> Core Thread
            </p>
            <p className={`text-sm ${c.text} leading-relaxed`}>{d.what_was_kept}</p>
          </div>
        )}
        {d.pacing_notes && (
          <div className={`${c.accentBox} rounded-2xl p-4`}>
            <p className={`text-xs font-semibold uppercase ${c.accentTxt} mb-2 flex items-center gap-1.5`}>
              <span>🎵</span> Pacing Notes
            </p>
            <p className={`text-sm ${c.text} leading-relaxed`}>{d.pacing_notes}</p>
          </div>
        )}
      </div>
    </div>
  );

  // ─── Render: Anticipate Results ───
  const AnticipateResults = ({ data: d }) => (
    <div className="space-y-5">
      {/* Summary */}
      {d.presentation_summary && (
        <div className={`${c.cardAlt} ${c.border} border rounded-2xl p-4 text-center`}>
          <p className={`text-xs font-semibold uppercase tracking-wider ${c.textMuted} mb-1`}>Your presentation argues</p>
          <p className={`text-sm font-medium italic ${c.text}`}>"{d.presentation_summary}"</p>
        </div>
      )}

      {/* Vulnerability scan */}
      {d.vulnerability_scan && (
        <div className={`${c.card} ${c.border} border rounded-2xl p-5`}>
          <h3 className={`font-bold ${c.text} flex items-center gap-2 mb-4`}>
            <span>🛡️</span> Vulnerability Scan
          </h3>
          <div className="space-y-3">
            {[
              { icon: '⚡', label: 'Weakest Claim', value: d.vulnerability_scan.weakest_claim, bg: c.danger },
              { icon: '📊', label: 'Missing Data', value: d.vulnerability_scan.missing_data, bg: c.warning },
              { icon: '🧊', label: 'Assumption Risk', value: d.vulnerability_scan.assumption_risk, bg: c.accentBox },
            ].map((v, i) => (
              <div key={i} className={`${v.bg} rounded-xl p-3 flex items-start gap-2.5`}>
                <span className="text-base flex-shrink-0">{v.icon}</span>
                <div>
                  <p className={`text-xs font-semibold uppercase ${c.textMuted} mb-0.5`}>{v.label}</p>
                  <p className={`text-sm ${c.text} leading-relaxed`}>{v.value}</p>
                </div>
              </div>
            ))}
          </div>
        </div>
      )}

      {/* Tough questions */}
      {d.tough_questions?.length > 0 && (
        <div className="space-y-3">
          <h3 className={`font-bold ${c.text} flex items-center gap-2 px-1`}>
            <span>🎯</span> Tough Questions ({d.tough_questions.length})
          </h3>
          {d.tough_questions.map((q, i) => {
            const isOpen = expandedSections[`q-${i}`] !== false;
            const diff = DIFFICULTY_COLORS[q.difficulty] || DIFFICULTY_COLORS.hard;
            return (
              <div key={i} className={`${c.card} ${c.border} border rounded-2xl overflow-hidden transition-all`}>
                <button onClick={() => toggleSection(`q-${i}`)} className="w-full p-4 flex items-start gap-3 text-left">
                  <span className={`text-sm font-bold ${c.accentTxt} mt-0.5 flex-shrink-0`}>{i + 1}.</span>
                  <div className="flex-1 min-w-0">
                    <p className={`text-sm font-semibold ${c.text} leading-snug`}>{q.question}</p>
                    <p className={`text-xs ${c.textMuted} mt-1`}>{q.why_they_ask}</p>
                  </div>
                  <div className="flex items-center gap-2 flex-shrink-0">
                    <span className={`text-xs font-semibold px-2 py-0.5 rounded-full ${diff.badge}`}>{diff.label}</span>
                    <span className={`text-sm ${c.textMuted}`}>{isOpen ? '▲' : '▼'}</span>
                  </div>
                </button>
                {isOpen && (
                  <div className={`px-4 pb-4 pt-0 ml-7 space-y-3 border-t ${c.border}`}>
                    <div className={`${c.success} rounded-xl p-3 mt-3`}>
                      <p className={`text-xs font-semibold uppercase ${isDark ? 'text-emerald-400' : 'text-emerald-700'} mb-1`}>Draft Answer</p>
                      <p className={`text-sm ${c.text} leading-relaxed`}>{q.draft_answer}</p>
                    </div>
                    <div className={`${c.warning} rounded-xl p-3`}>
                      <p className={`text-xs font-semibold uppercase ${c.goldTxt} mb-1`}>⚠️ Trap to Avoid</p>
                      <p className={`text-sm ${c.text} leading-relaxed`}>{q.trap_to_avoid}</p>
                    </div>
                  </div>
                )}
              </div>
            );
          })}
        </div>
      )}

      {/* Curveball */}
      {d.curveball && (
        <div className={`${c.goldBox} ${c.border} border rounded-2xl p-5`}>
          <h3 className={`font-bold ${c.goldTxt} flex items-center gap-2 mb-3`}>
            <span>🃏</span> Curveball
          </h3>
          <p className={`text-sm font-semibold ${c.text} mb-2`}>{d.curveball.question}</p>
          <p className={`text-sm ${c.textSecondary} leading-relaxed`}>{d.curveball.draft_answer}</p>
        </div>
      )}

      {/* Readiness */}
      {d.overall_readiness && (
        <div className={`${c.accentBox} ${c.border} border rounded-2xl p-4`}>
          <p className={`text-xs font-semibold uppercase ${c.accentTxt} mb-1 flex items-center gap-1.5`}>
            <span>📊</span> Overall Readiness
          </p>
          <p className={`text-sm ${c.text} leading-relaxed`}>{d.overall_readiness}</p>
        </div>
      )}
    </div>
  );

  // ─── Render: Hook Results ───
  const HookResults = ({ data: d }) => (
    <div className="space-y-5">
      {/* Diagnosis */}
      {d.diagnosis && (
        <div className={`${c.card} ${c.border} border rounded-2xl p-5`}>
          <h3 className={`font-bold ${c.text} flex items-center gap-2 mb-4`}>
            <span>🔍</span> Diagnosis
          </h3>
          <div className="grid grid-cols-1 sm:grid-cols-2 gap-3">
            <div className={`${c.danger} rounded-xl p-3`}>
              <p className={`text-xs font-semibold uppercase ${isDark ? 'text-red-300' : 'text-red-800'} mb-1`}>Current Opening</p>
              <p className={`text-sm ${c.text} mb-1`}>{d.diagnosis.current_opening}</p>
              <p className={`text-xs ${c.textMuted} italic`}>{d.diagnosis.opening_problem}</p>
            </div>
            <div className={`${c.danger} rounded-xl p-3`}>
              <p className={`text-xs font-semibold uppercase ${isDark ? 'text-red-300' : 'text-red-800'} mb-1`}>Current Closing</p>
              <p className={`text-sm ${c.text} mb-1`}>{d.diagnosis.current_closing}</p>
              <p className={`text-xs ${c.textMuted} italic`}>{d.diagnosis.closing_problem}</p>
            </div>
          </div>
        </div>
      )}

      {/* New opening */}
      {d.new_opening && (
        <div className={`${c.success} rounded-2xl p-5`}>
          <div className="flex items-center justify-between mb-3">
            <h3 className={`font-bold ${isDark ? 'text-emerald-400' : 'text-emerald-700'} flex items-center gap-2`}>
              <span>🎬</span> New Opening
            </h3>
            <CopyBtn content={`${d.new_opening.text}${BRAND}`} label="Copy" />
          </div>
          <span className={`inline-block text-xs font-semibold px-2 py-0.5 rounded-full mb-3 ${isDark ? 'bg-emerald-900/30 text-emerald-400' : 'bg-emerald-100 text-emerald-800'}`}>
            {d.new_opening.technique}
          </span>
          <p className={`text-sm ${c.text} leading-relaxed whitespace-pre-wrap`}>{d.new_opening.text}</p>
          <p className={`text-xs ${c.textMuted} mt-2 italic`}>{d.new_opening.why_it_works}</p>
        </div>
      )}

      {/* New closing */}
      {d.new_closing && (
        <div className={`${c.accentBox} rounded-2xl p-5`}>
          <div className="flex items-center justify-between mb-3">
            <h3 className={`font-bold ${c.accentTxt} flex items-center gap-2`}>
              <span>🎬</span> New Closing
            </h3>
            <CopyBtn content={`${d.new_closing.text}${BRAND}`} label="Copy" />
          </div>
          <span className={`inline-block text-xs font-semibold px-2 py-0.5 rounded-full mb-3 ${isDark ? 'bg-sky-900/30 text-sky-300' : 'bg-sky-100 text-sky-800'}`}>
            {d.new_closing.technique}
          </span>
          <p className={`text-sm ${c.text} leading-relaxed whitespace-pre-wrap`}>{d.new_closing.text}</p>
          <p className={`text-xs ${c.textMuted} mt-2 italic`}>{d.new_closing.why_it_works}</p>
        </div>
      )}

      {/* Transitions */}
      {d.transitions?.length > 0 && (
        <div className={`${c.card} ${c.border} border rounded-2xl p-5`}>
          <h3 className={`font-bold ${c.text} flex items-center gap-2 mb-4`}>
            <span>🔗</span> Key Transitions ({d.transitions.length})
          </h3>
          <div className="space-y-3">
            {d.transitions.map((tr, i) => (
              <div key={i} className={`${c.cardAlt} ${c.border} border rounded-xl p-4`}>
                <p className={`text-xs font-semibold ${c.accentTxt} mb-2`}>{tr.between}</p>
                {tr.original && (
                  <p className={`text-sm ${c.textMuted} line-through mb-1`}>{tr.original}</p>
                )}
                <p className={`text-sm font-medium ${c.text} leading-relaxed`}>"{tr.rewritten}"</p>
                <p className={`text-xs ${c.textMuted} mt-1 italic`}>{tr.why}</p>
              </div>
            ))}
          </div>
        </div>
      )}

      {/* Energy arc */}
      {d.energy_arc && (
        <div className={`${c.goldBox} ${c.border} border rounded-2xl p-4`}>
          <p className={`text-xs font-semibold uppercase ${c.goldTxt} mb-2 flex items-center gap-1.5`}>
            <span>📈</span> Energy Arc
          </p>
          <p className={`text-sm ${c.text} leading-relaxed`}>{d.energy_arc}</p>
        </div>
      )}
    </div>
  );

  // ─── Main Render ───
  return (
    <div className={`space-y-4 ${c.text}`}>
      <div className="space-y-6">

        {/* Header */}
        <div className={`${c.card} border ${c.border} rounded-xl shadow-sm p-5`}>
          <div className="pb-3 border-b border-zinc-500">
            <div className="flex items-start justify-between gap-3">
              <div>
                <h2 className={`text-xl font-bold ${c.text}`}>
                  <span className="mr-2">{tool?.icon ?? '🎙️'}</span>{tool?.title ?? 'The Runthrough'}
                </h2>
                <p className={`text-sm ${c.textSecondary}`}>
                  {tool?.tagline ?? 'Presentation coach in your pocket. Cut, prepare, and polish.'}
                </p>
              </div>
              {(results || content.trim()) && (
                <button onClick={handleReset} className={`${c.btnSecondary} px-3 py-1.5 rounded-lg text-xs font-bold flex-shrink-0`}>
                  ↺ Start Over
                </button>
              )}
            </div>
          </div>
        </div>

        {/* Mode tabs */}
        <div className={`flex border-b ${c.border}`}>
          {MODES.map(m => (
            <button
              key={m.id}
              onClick={() => handleModeSwitch(m.id)}
              className={`flex-1 py-3 text-center text-sm font-medium transition-all duration-150
                ${mode === m.id ? c.tabActive : c.tabInactive}`}
            >
              <span className="mr-1.5">{m.icon}</span>
              <span className="hidden sm:inline">{m.label}</span>
              <span className="sm:hidden">{m.label}</span>
            </button>
          ))}
        </div>

        {/* Mode description */}
        <p className={`text-xs ${c.textMuted} italic`}>
          {MODES.find(m => m.id === mode)?.desc}
        </p>

        {/* Pre-result cross-ref */}
        <p className={`text-xs ${c.textMuted}`}>
          Need to prep for tough questions specifically? <a href="/HecklerPrep" className={linkStyle}>🎤 HecklerPrep</a> drills you on the hardest objections your audience will raise.
        </p>

        {/* Input card */}
        <div className={`${c.card} ${c.border} border rounded-2xl p-5 shadow-sm space-y-4`}>

          {/* Content textarea (all modes) */}
          <div className="space-y-2">
            <label className={`text-sm font-semibold ${c.text}`}>
              <span className="mr-1.5">📝</span> Presentation content <span className={c.required}>*</span>
            </label>
            <textarea
              value={content}
              onChange={(e) => setContent(e.target.value)}
              placeholder="Paste your presentation text, speaker notes, or outline here..."
              rows={6}
              className={`w-full px-4 py-3 rounded-xl text-sm ${c.input} ${c.border} border ${c.text} placeholder:${c.textMuted} resize-y outline-none transition-colors`}
            />
            <p className={`text-xs ${c.textMuted} text-right`}>{content.length > 0 ? `~${Math.round(content.split(/\s+/).filter(Boolean).length / 130)} min at speaking pace` : ''}</p>
          </div>

          {/* Mode-specific inputs */}
          {mode === 'cut' && (
            <div className="space-y-4">
              <div className="space-y-2">
                <label className={`text-sm font-semibold ${c.text}`}>
                  <span className="mr-1.5">⏱️</span> Time limit (minutes)
                </label>
                <div className="flex items-center gap-3">
                  <input
                    type="range"
                    min={1}
                    max={60}
                    value={timeMinutes}
                    onChange={(e) => setTimeMinutes(Number(e.target.value))}
                    className="flex-1"
                  />
                  <span className={`text-sm font-bold ${c.text} w-12 text-center`}>{timeMinutes}m</span>
                </div>
              </div>
              <div className="space-y-2">
                <label className={`text-sm font-semibold ${c.text}`}>
                  <span className="mr-1.5">💡</span> Context <span className={`font-normal ${c.textMuted}`}>(optional)</span>
                </label>
                <input
                  type="text"
                  value={context}
                  onChange={(e) => setContext(e.target.value)}
                  placeholder="e.g. Conference keynote, team standup, investor pitch..."
                  className={`w-full px-4 py-2.5 rounded-xl text-sm ${c.input} ${c.border} border ${c.text} placeholder:${c.textMuted} outline-none transition-colors`}
                />
              </div>
            </div>
          )}

          {mode === 'anticipate' && (
            <div className="space-y-4">
              <div className="space-y-2">
                <label className={`text-sm font-semibold ${c.text}`}>
                  <span className="mr-1.5">👥</span> Audience
                </label>
                <div className="grid grid-cols-3 gap-2">
                  {AUDIENCE_OPTIONS.map(a => (
                    <button
                      key={a.id}
                      onClick={() => setAudience(a.id)}
                      className={`${audience === a.id ? c.chipActive : c.chipInactive} border rounded-xl px-2 py-2.5 text-center transition-all duration-150`}
                    >
                      <span className="text-lg block mb-0.5">{a.icon}</span>
                      <p className="text-xs font-medium leading-tight">{a.label}</p>
                    </button>
                  ))}
                </div>
              </div>
              <div className="space-y-2">
                <label className={`text-sm font-semibold ${c.text}`}>
                  <span className="mr-1.5">🔥</span> Stakes <span className={`font-normal ${c.textMuted}`}>(optional)</span>
                </label>
                <input
                  type="text"
                  value={stakes}
                  onChange={(e) => setStakes(e.target.value)}
                  placeholder="e.g. Asking for $2M funding, proposing a reorg..."
                  className={`w-full px-4 py-2.5 rounded-xl text-sm ${c.input} ${c.border} border ${c.text} placeholder:${c.textMuted} outline-none transition-colors`}
                />
              </div>
            </div>
          )}

          {mode === 'hook' && (
            <div className="space-y-4">
              <div className="space-y-2">
                <label className={`text-sm font-semibold ${c.text}`}>
                  <span className="mr-1.5">🎭</span> Tone
                </label>
                <div className="grid grid-cols-2 gap-2">
                  {TONE_OPTIONS.map(t => (
                    <button
                      key={t.id}
                      onClick={() => setTone(t.id)}
                      className={`${tone === t.id ? c.chipActive : c.chipInactive} border rounded-xl px-3 py-2.5 text-left transition-all duration-150`}
                    >
                      <div className="flex items-center gap-2">
                        <span className="text-lg">{t.icon}</span>
                        <div>
                          <p className="text-sm font-medium leading-tight">{t.label}</p>
                          <p className={`text-xs ${c.textMuted} leading-tight mt-0.5`}>{t.desc}</p>
                        </div>
                      </div>
                    </button>
                  ))}
                </div>
              </div>
              <div className="space-y-2">
                <label className={`text-sm font-semibold ${c.text}`}>
                  <span className="mr-1.5">🎯</span> Goal <span className={`font-normal ${c.textMuted}`}>(optional)</span>
                </label>
                <input
                  type="text"
                  value={goal}
                  onChange={(e) => setGoal(e.target.value)}
                  placeholder="e.g. Get them excited about our roadmap, convince the board..."
                  className={`w-full px-4 py-2.5 rounded-xl text-sm ${c.input} ${c.border} border ${c.text} placeholder:${c.textMuted} outline-none transition-colors`}
                />
              </div>
            </div>
          )}

          {/* Submit */}
          <button
            onClick={handleSubmit}
            disabled={loading || !content.trim()}
            className={`w-full ${c.btnPrimary} py-3 rounded-xl font-semibold text-sm shadow-md
              disabled:opacity-40 disabled:cursor-not-allowed transition-all duration-200 flex items-center justify-center gap-2`}
          >
            {loading ? (
              <>
                <span className="inline-block animate-spin">{tool?.icon ?? '🎙️'}</span>
                {loadingMessages[mode]}
              </>
            ) : (
              submitLabels[mode]
            )}
          </button>
          <p className={`text-xs text-center ${c.textMuted}`}>AI-generated — review before delivering</p>

          {/* Try Example */}
          {!content.trim() && !loading && (
            <div className="flex justify-center">
              <button
                onClick={() => {
                  setContent("Today I want to walk you through Q3 results and where we're heading. Revenue is up 32% year-over-year, driven mainly by enterprise expansion in EMEA. Three of our top-five accounts grew by more than 50%. Margin expanded 4 points to 71% as we leaned into the higher-tier SKU. We added 47 net new logos, up from 31 in Q2, and pipeline coverage for Q4 sits at 4.2x. The big strategic question for next quarter is whether to invest deeper in EMEA or open up the APAC region — we're proposing EMEA depth based on conversion economics. I'll walk through the numbers in detail, then we'll open it up for questions.");
                  setMode('cut');
                  setTimeMinutes(5);
                  setAudience('executives');
                  setTone('authoritative');
                  setStakes('Quarterly board update — three new directors in the room, first time hearing from me');
                  setGoal('Get sign-off on the EMEA-deepening strategy and budget approval for two new EMEA AEs');
                }}
                className={`text-xs font-medium ${c.accentTxt} underline underline-offset-2 min-h-[32px]`}
              >
                ✨ Try an example
              </button>
            </div>
          )}
        </div>

        {/* Error */}
        {error && (
          <div className={`${c.danger} border rounded-xl p-4 text-sm flex items-center gap-2`}>
            <span>⚠️</span> {error}
          </div>
        )}

        {/* Results */}
        {results && (
          <div ref={resultsRef} className="space-y-5">
            {results.mode === 'cut' && <CutResults data={results.data} />}
            {results.mode === 'anticipate' && <AnticipateResults data={results.data} />}
            {results.mode === 'hook' && <HookResults data={results.data} />}

            {/* Cross-references */}
            <div className={`${c.cardAlt} ${c.border} border rounded-xl p-4 space-y-2`}>
              <p className={`text-xs font-semibold ${c.textMuted} uppercase tracking-wider`}>
                Related tools
              </p>
              <div className="space-y-1.5 text-xs">
                <p className={c.textSecondary}>Go deeper on any objection with <a href="/HecklerPrep" className={linkStyle}>🎤 HecklerPrep</a> — coaches you through the toughest questions one by one.</p>
                <p className={c.textSecondary}>Writing a speech instead of a presentation? <a href="/ToastWriter" className={linkStyle}>🥂 ToastWriter</a> builds it from your details.</p>
              </div>
            </div>
          </div>
        )}
      </div>

      {/* Session history */}
      {/* eslint-disable-next-line no-restricted-globals */}
      {history.length > 0 && (
        <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
          <p className={`text-xs font-bold ${c.textMuted} mb-2`}>📋 Recent sessions</p>
          <div className="space-y-1">
            {/* eslint-disable-next-line no-restricted-globals */}

            {history.map(s => (
              <div key={s.id} className="flex items-center justify-between">
                <span className={`text-xs ${c.textSecondary} truncate`}>{s.preview}</span>
                <span className={`text-xs ${c.textMuted} ml-2 shrink-0`}>{s.mode}</span>
              </div>
            ))}
          </div>
        </div>
      )}
    </div>
  );
};

TheRunthrough.displayName = 'TheRunthrough';
export default TheRunthrough;
