import React, { useState, useRef, useEffect, useCallback } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { useRegisterActions } from '../components/ActionBarContext';
import { usePersistentState } from '../hooks/usePersistentState';
import { useTranslation } from '../i18n/useTranslation';
import { currencySymbol } from '../utils/formatLocale';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const MODES = [
  { id: 'cut',        icon: '✂️', labelKey: 'trt_mode_cut_label',        descKey: 'trt_mode_cut_desc' },
  { id: 'anticipate', icon: '🎯', labelKey: 'trt_mode_anticipate_label', descKey: 'trt_mode_anticipate_desc' },
  { id: 'hook',       icon: '🪝', labelKey: 'trt_mode_hook_label',       descKey: 'trt_mode_hook_desc' },
];

const AUDIENCE_OPTIONS = [
  { id: 'executives',    labelKey: 'trt_aud_executives', icon: '👔' },
  { id: 'investors',     labelKey: 'trt_aud_investors',  icon: '💰' },
  { id: 'team',          labelKey: 'trt_aud_team',       icon: '🤝' },
  { id: 'clients',       labelKey: 'trt_aud_clients',    icon: '💼' },
  { id: 'academic',      labelKey: 'trt_aud_academic',   icon: '🎓' },
  { id: 'general',       labelKey: 'trt_aud_general',    icon: '👥' },
];

const TONE_OPTIONS = [
  { id: 'authoritative',  labelKey: 'trt_tone_authoritative',  descKey: 'trt_tone_authoritative_desc',  icon: '🎤' },
  { id: 'conversational', labelKey: 'trt_tone_conversational', descKey: 'trt_tone_conversational_desc', icon: '☕' },
  { id: 'provocative',    labelKey: 'trt_tone_provocative',    descKey: 'trt_tone_provocative_desc',    icon: '⚡' },
  { id: 'inspirational',  labelKey: 'trt_tone_inspirational',  descKey: 'trt_tone_inspirational_desc',  icon: '✨' },
];

const TheRunthrough = ({ tool }) => {
  const { callToolEndpoint, loading, userLocale, userCurrency, userRegion } = useClaudeAPI();
  const { isDark } = useTheme();
  const { t } = useTranslation();
  const sym = currencySymbol(userLocale, userCurrency);

  const loadExample = () => {
    setContent(t('trt_ex_content', { sym }));
    setMode('anticipate');
    setTimeMinutes(10);
    setContext(t('trt_ex_context'));
    setAudience('general');
    setStakes(t('trt_ex_stakes'));
  };

  const resultsRef = useRef(null);

  // ─── Colors ───
  const c = {
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:         isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20' : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-500' : 'text-gray-400',
    labelText:     isDark ? 'text-zinc-200' : 'text-gray-700',
    required:      isDark ? 'text-amber-400' : 'text-amber-500',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white' : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200' : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200' : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200' : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200' : 'bg-red-50 border-red-200 text-red-800',
    tabActive:     isDark ? 'text-cyan-300 border-b-2 border-cyan-400' : 'text-cyan-700 border-b-2 border-cyan-600',
    tabInactive:   isDark ? 'text-zinc-500 hover:text-zinc-300' : 'text-gray-400 hover:text-gray-600',
    chipActive:    isDark ? 'bg-cyan-900/40 border-cyan-500 text-cyan-200' : 'bg-cyan-100 border-cyan-600 text-cyan-900',
    chipInactive:  isDark ? 'bg-zinc-800 border-zinc-600 text-zinc-400 hover:border-zinc-500' : 'bg-white border-gray-200 text-gray-500 hover:border-gray-300',
    accentBox:     isDark ? 'bg-sky-900/20 border-sky-700' : 'bg-sky-50 border-sky-200',
    accentTxt:     isDark ? 'text-sky-300' : 'text-sky-700',
    goldBox:       isDark ? 'bg-amber-900/20 border-amber-700' : 'bg-amber-50 border-amber-300',
    goldTxt:       isDark ? 'text-amber-300' : 'text-amber-700',
  };
  c.textMuteded = c.textMuted;
  c.label = c.labelText;

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';

  const DIFFICULTY_COLORS = {
    hard:      { badge: isDark ? 'bg-amber-900/20 text-amber-400' : 'bg-amber-100 text-amber-800', labelKey: 'trt_diff_hard' },
    very_hard: { badge: isDark ? 'bg-red-900/20 text-red-400' : 'bg-red-100 text-red-800', labelKey: 'trt_diff_very_hard' },
    killer:    { badge: isDark ? 'bg-red-900/30 text-red-300' : 'bg-red-200 text-red-900', labelKey: 'trt_diff_killer' },
  };


  // ─── State ───
  const [mode, setMode] = useState('cut');
  const [content, setContent] = useState('');
  const [timeMinutes, setTimeMinutes] = useState(5);
  const [context, setContext] = useState('');
  const [audience, setAudience] = useState('general');
  const [stakes, setStakes] = useState('');
  const [tone, setTone] = useState('conversational');
  const [goal, setGoal] = useState('');
  const [sessionHistory, setSessionHistory] = usePersistentState('the-runthrough-history', []);
  const [results, setResults] = usePersistentState('the-runthrough-results', null);
  const [error, setError] = useState('');
  const [expandedSections, setExpandedSections] = useState({});

  useEffect(() => {
    if (!results) return;
    const timer = setTimeout(() => {
      resultsRef.current?.scrollIntoView({ behavior: 'smooth', block: 'start' });
    }, 250);
    return () => clearTimeout(timer);
  }, [results]);

  const toggleSection = (key) => {
    setExpandedSections(prev => ({ ...prev, [key]: !prev[key] }));
  };

  // ─── Handlers ───
  const handleSubmit = useCallback(async () => {
    if (!content.trim()) return;
    setError('');
    setResults(null);
    setExpandedSections({});

    try {
      let endpoint, payload;

      if (mode === 'cut') {
        endpoint = 'the-runthrough-cut';
        payload = { content: content.trim(), timeMinutes, context, userLocale, userCurrency, userRegion };
      } else if (mode === 'anticipate') {
        endpoint = 'the-runthrough-anticipate';
        payload = { content: content.trim(), audience, stakes, userLocale, userCurrency, userRegion };
      } else {
        endpoint = 'the-runthrough-hook';
        payload = { content: content.trim(), tone, goal, userLocale, userCurrency, userRegion };
      }

      const res = await callToolEndpoint(endpoint, payload);
      setResults({ mode, data: res });
      setSessionHistory(prev => [{
        id: 'tr_' + Date.now(),
        date: new Date().toISOString(),
        // PF-25 exception: 40-char preview-text truncation; session history is capped at 6.
        preview: content.trim().slice(0, 40),
        mode,
      }, ...prev].slice(0, 6));
    } catch (err) {
      setError(err.message || t('trt_error_generic'));
    }
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [content, mode, timeMinutes, context, audience, stakes, tone, goal, loading, userLocale, userCurrency, userRegion, t]);

  // ─── Keyboard: SELECT-only guard, ref pattern to avoid stale closures ───
  const handleSubmitRef = useRef(null);
  const canSubmitRef = useRef(false);
  handleSubmitRef.current = handleSubmit;
  canSubmitRef.current = !!content.trim() && !loading;

  useEffect(() => {
    const handler = (e) => {
      const tag = document.activeElement?.tagName;
      if (tag === 'SELECT') return;
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && !loading && canSubmitRef.current) {
        e.preventDefault();
        handleSubmitRef.current?.();
      }
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
  }, [loading]);

  const handleReset = () => {
    setResults(null);
    setError('');
    setExpandedSections({});
    setContent('');
    setContext('');
    setStakes('');
    setGoal('');
    setTimeMinutes(5);
    setAudience('general');
    setTone('conversational');
  };

  const handleModeSwitch = (newMode) => {
    setMode(newMode);
    setResults(null);
    setError('');
    setExpandedSections({});
  };

  // ─── Build text helpers ───
  const buildCutText = (d) => {
    let out = `✂️ ${t('trt_copy_cut_header')}\n`;
    out += `${t('trt_copy_original')} ~${d.original_word_count} ${t('trt_copy_words_short')} (~${d.original_est_minutes} ${t('trt_copy_min_short')})\n`;
    out += `${t('trt_copy_trimmed')} ~${d.trimmed_word_count} ${t('trt_copy_words_short')} (~${d.trimmed_est_minutes} ${t('trt_copy_min_short')}) → ${t('trt_copy_target')} ${d.target_minutes} ${t('trt_copy_min_short')}\n\n`;
    out += `━━ ${t('trt_copy_trimmed_content')} ━━\n${d.trimmed_content}\n\n`;
    if (d.what_was_cut?.length) {
      out += `━━ ${t('trt_copy_what_cut')} ━━\n`;
      d.what_was_cut.forEach(cut => { out += `• ${cut.section}: ${cut.reason}\n`; });
      out += '\n';
    }
    if (d.what_was_kept) out += `━━ ${t('trt_copy_core_thread')} ━━\n${d.what_was_kept}\n\n`;
    if (d.pacing_notes) out += `━━ ${t('trt_copy_pacing')} ━━\n${d.pacing_notes}\n\n`;
    return out + BRAND.trimStart();
  };

  const buildAnticipateText = (d) => {
    let out = `🎯 ${t('trt_copy_anticipate_header')}\n`;
    out += `${d.presentation_summary}\n\n`;
    if (d.vulnerability_scan) {
      out += `━━ ${t('trt_copy_vuln_scan')} ━━\n`;
      out += `${t('trt_copy_weakest')} ${d.vulnerability_scan.weakest_claim}\n`;
      out += `${t('trt_copy_missing')} ${d.vulnerability_scan.missing_data}\n`;
      out += `${t('trt_copy_assumption')} ${d.vulnerability_scan.assumption_risk}\n\n`;
    }
    out += `━━ ${t('trt_copy_tough_q')} ━━\n`;
    (d.tough_questions || []).forEach((q, i) => {
      out += `${i + 1}. [${t((DIFFICULTY_COLORS[q.difficulty] || DIFFICULTY_COLORS.hard).labelKey)}] ${q.question}\n`;
      out += `   ${t('trt_copy_answer')} ${q.draft_answer}\n`;
      out += `   ${t('trt_copy_trap')} ${q.trap_to_avoid}\n\n`;
    });
    if (d.curveball) {
      out += `━━ ${t('trt_copy_curveball')} ━━\n${d.curveball.question}\n${t('trt_copy_answer')} ${d.curveball.draft_answer}\n\n`;
    }
    if (d.overall_readiness) out += `━━ ${t('trt_copy_readiness')} ━━\n${d.overall_readiness}\n\n`;
    return out + BRAND.trimStart();
  };

  const buildHookText = (d) => {
    let out = `🪝 ${t('trt_copy_hook_header')}\n\n`;
    if (d.diagnosis) {
      out += `━━ ${t('trt_copy_diagnosis')} ━━\n`;
      out += `${t('trt_copy_opening')} ${d.diagnosis.current_opening} — ${d.diagnosis.opening_problem}\n`;
      out += `${t('trt_copy_closing')} ${d.diagnosis.current_closing} — ${d.diagnosis.closing_problem}\n\n`;
    }
    if (d.new_opening) {
      out += `━━ ${t('trt_copy_new_opening')} (${d.new_opening.technique}) ━━\n${d.new_opening.text}\n\n`;
    }
    if (d.new_closing) {
      out += `━━ ${t('trt_copy_new_closing')} (${d.new_closing.technique}) ━━\n${d.new_closing.text}\n\n`;
    }
    if (d.transitions?.length) {
      out += `━━ ${t('trt_copy_transitions')} ━━\n`;
      d.transitions.forEach(tr => { out += `${tr.between}: "${tr.rewritten}"\n`; });
      out += '\n';
    }
    if (d.energy_arc) out += `━━ ${t('trt_copy_energy_arc')} ━━\n${d.energy_arc}\n\n`;
    return out + BRAND.trimStart();
  };

  const buildFullText = () => {
    if (!results) return '';
    const d = results.data;
    if (results.mode === 'cut') return buildCutText(d);
    if (results.mode === 'anticipate') return buildAnticipateText(d);
    return buildHookText(d);
  };

  // Register export content with the wrapper's persistent ActionBar
  useRegisterActions(buildFullText(), tool?.title || 'The Runthrough');

  // ─── Loading messages ───
  const loadingMessages = {
    cut: t('trt_loading_cut'),
    anticipate: t('trt_loading_anticipate'),
    hook: t('trt_loading_hook'),
  };

  // ─── Submit button labels ───
  const submitLabels = {
    cut: <><span className="mr-1">{tool?.icon ?? '🎙️'}</span> {t('trt_submit_cut')}</>,
    anticipate: <><span className="mr-1">{tool?.icon ?? '🎙️'}</span> {t('trt_submit_anticipate')}</>,
    hook: <><span className="mr-1">{tool?.icon ?? '🎙️'}</span> {t('trt_submit_hook')}</>,
  };

  // ─── Render: Cut Results ───
  const CutResults = ({ data: d }) => (
    <div className="space-y-5">
      {/* Stats bar */}
      <div className={`grid grid-cols-3 gap-3`}>
        {[
          { label: t('trt_stat_original'), value: t('trt_min_value', { count: d.original_est_minutes }), sub: t('trt_words', { count: d.original_word_count }) },
          { label: t('trt_stat_trimmed'), value: t('trt_min_value', { count: d.trimmed_est_minutes }), sub: t('trt_words', { count: d.trimmed_word_count }) },
          { label: t('trt_stat_target'), value: t('trt_target_value', { count: d.target_minutes }), sub: d.trimmed_est_minutes <= d.target_minutes ? t('trt_fits') : t('trt_close') },
        ].map((s, i) => (
          <div key={i} className={`${c.cardAlt} ${c.border} border rounded-xl p-3 text-center`}>
            <p className={`text-xs ${c.textMuted} uppercase font-semibold`}>{s.label}</p>
            <p className={`text-lg font-bold ${c.text}`}>{s.value}</p>
            <p className={`text-xs ${c.textMuted}`}>{s.sub}</p>
          </div>
        ))}
      </div>

      {/* Trimmed content */}
      <div className={`${c.card} ${c.border} border rounded-2xl p-5`}>
        <div className="flex items-center justify-between mb-3">
          <h3 className={`font-bold ${c.text} flex items-center gap-2`}>
            <span>📄</span> {t('trt_trimmed_presentation')}
          </h3>
        </div>
        <div className={`${c.input} ${c.border} border rounded-xl p-4`}>
          <p className={`text-sm ${c.text} leading-relaxed whitespace-pre-wrap`}>
            {d.trimmed_content}
          </p>
        </div>
      </div>

      {/* What was cut */}
      {d.what_was_cut?.length > 0 && (
        <div className={`${c.card} ${c.border} border rounded-2xl p-5`}>
          <button onClick={() => toggleSection('cuts')} className="w-full flex items-center justify-between">
            <h3 className={`font-bold ${c.text} flex items-center gap-2`}>
              <span>🗑️</span> {t('trt_what_was_cut')} ({d.what_was_cut.length})
            </h3>
            <span className={`text-sm ${c.textMuted}`}>{expandedSections['cuts'] !== false ? '▲' : '▼'}</span>
          </button>
          {expandedSections['cuts'] !== false && (
            <div className="mt-3 space-y-2">
              {d.what_was_cut.map((cut, i) => (
                <div key={i} className={`${c.danger} rounded-xl p-3 flex items-start gap-2`}>
                  <span className="text-sm flex-shrink-0">✕</span>
                  <div>
                    <p className={`text-sm font-medium ${c.text}`}>{cut.section}</p>
                    <p className={`text-xs ${c.textMuted} mt-0.5`}>{cut.reason}</p>
                  </div>
                </div>
              ))}
            </div>
          )}
        </div>
      )}

      {/* Core thread + pacing */}
      <div className="grid grid-cols-1 sm:grid-cols-2 gap-4">
        {d.what_was_kept && (
          <div className={`${c.success} rounded-2xl p-4`}>
            <p className={`text-xs font-semibold uppercase ${isDark ? 'text-emerald-400' : 'text-emerald-700'} mb-2 flex items-center gap-1.5`}>
              <span>💎</span> {t('trt_core_thread')}
            </p>
            <p className={`text-sm ${c.text} leading-relaxed`}>{d.what_was_kept}</p>
          </div>
        )}
        {d.pacing_notes && (
          <div className={`${c.accentBox} rounded-2xl p-4`}>
            <p className={`text-xs font-semibold uppercase ${c.accentTxt} mb-2 flex items-center gap-1.5`}>
              <span>🎵</span> {t('trt_pacing_notes')}
            </p>
            <p className={`text-sm ${c.text} leading-relaxed`}>{d.pacing_notes}</p>
          </div>
        )}
      </div>
    </div>
  );

  // ─── Render: Anticipate Results ───
  const AnticipateResults = ({ data: d }) => (
    <div className="space-y-5">
      {/* Summary */}
      {d.presentation_summary && (
        <div className={`${c.cardAlt} ${c.border} border rounded-2xl p-4 text-center`}>
          <p className={`text-xs font-semibold uppercase tracking-wider ${c.textMuted} mb-1`}>{t('trt_arg_summary')}</p>
          <p className={`text-sm font-medium italic ${c.text}`}>"{d.presentation_summary}"</p>
        </div>
      )}

      {/* Vulnerability scan */}
      {d.vulnerability_scan && (
        <div className={`${c.card} ${c.border} border rounded-2xl p-5`}>
          <h3 className={`font-bold ${c.text} flex items-center gap-2 mb-4`}>
            <span>🛡️</span> {t('trt_vuln_scan')}
          </h3>
          <div className="space-y-3">
            {[
              { icon: '⚡', label: t('trt_weakest_claim'), value: d.vulnerability_scan.weakest_claim, bg: c.danger },
              { icon: '📊', label: t('trt_missing_data'), value: d.vulnerability_scan.missing_data, bg: c.warning },
              { icon: '🧊', label: t('trt_assumption_risk'), value: d.vulnerability_scan.assumption_risk, bg: c.accentBox },
            ].map((v, i) => (
              <div key={i} className={`${v.bg} rounded-xl p-3 flex items-start gap-2.5`}>
                <span className="text-base flex-shrink-0">{v.icon}</span>
                <div>
                  <p className={`text-xs font-semibold uppercase ${c.textMuted} mb-0.5`}>{v.label}</p>
                  <p className={`text-sm ${c.text} leading-relaxed`}>{v.value}</p>
                </div>
              </div>
            ))}
          </div>
        </div>
      )}

      {/* Tough questions */}
      {d.tough_questions?.length > 0 && (
        <div className="space-y-3">
          <h3 className={`font-bold ${c.text} flex items-center gap-2 px-1`}>
            <span>🎯</span> {t('trt_tough_questions')} ({d.tough_questions.length})
          </h3>
          {d.tough_questions.map((q, i) => {
            const isOpen = expandedSections[`q-${i}`] !== false;
            const diff = DIFFICULTY_COLORS[q.difficulty] || DIFFICULTY_COLORS.hard;
            return (
              <div key={i} className={`${c.card} ${c.border} border rounded-2xl overflow-hidden transition-all`}>
                <button onClick={() => toggleSection(`q-${i}`)} className="w-full p-4 flex items-start gap-3 text-left">
                  <span className={`text-sm font-bold ${c.accentTxt} mt-0.5 flex-shrink-0`}>{i + 1}.</span>
                  <div className="flex-1 min-w-0">
                    <p className={`text-sm font-semibold ${c.text} leading-snug`}>{q.question}</p>
                    <p className={`text-xs ${c.textMuted} mt-1`}>{q.why_they_ask}</p>
                  </div>
                  <div className="flex items-center gap-2 flex-shrink-0">
                    <span className={`text-xs font-semibold px-2 py-0.5 rounded-full ${diff.badge}`}>{t(diff.labelKey)}</span>
                    <span className={`text-sm ${c.textMuted}`}>{isOpen ? '▲' : '▼'}</span>
                  </div>
                </button>
                {isOpen && (
                  <div className={`px-4 pb-4 pt-0 ml-7 space-y-3 border-t ${c.border}`}>
                    <div className={`${c.success} rounded-xl p-3 mt-3`}>
                      <p className={`text-xs font-semibold uppercase ${isDark ? 'text-emerald-400' : 'text-emerald-700'} mb-1`}>{t('trt_draft_answer')}</p>
                      <p className={`text-sm ${c.text} leading-relaxed`}>{q.draft_answer}</p>
                    </div>
                    <div className={`${c.warning} rounded-xl p-3`}>
                      <p className={`text-xs font-semibold uppercase ${c.goldTxt} mb-1`}>⚠️ {t('trt_trap_to_avoid')}</p>
                      <p className={`text-sm ${c.text} leading-relaxed`}>{q.trap_to_avoid}</p>
                    </div>
                  </div>
                )}
              </div>
            );
          })}
        </div>
      )}

      {/* Curveball */}
      {d.curveball && (
        <div className={`${c.goldBox} ${c.border} border rounded-2xl p-5`}>
          <h3 className={`font-bold ${c.goldTxt} flex items-center gap-2 mb-3`}>
            <span>🃏</span> {t('trt_curveball')}
          </h3>
          <p className={`text-sm font-semibold ${c.text} mb-2`}>{d.curveball.question}</p>
          <p className={`text-sm ${c.textSecondary} leading-relaxed`}>{d.curveball.draft_answer}</p>
        </div>
      )}

      {/* Readiness */}
      {d.overall_readiness && (
        <div className={`${c.accentBox} ${c.border} border rounded-2xl p-4`}>
          <p className={`text-xs font-semibold uppercase ${c.accentTxt} mb-1 flex items-center gap-1.5`}>
            <span>📊</span> {t('trt_overall_readiness')}
          </p>
          <p className={`text-sm ${c.text} leading-relaxed`}>{d.overall_readiness}</p>
        </div>
      )}
    </div>
  );

  // ─── Render: Hook Results ───
  const HookResults = ({ data: d }) => (
    <div className="space-y-5">
      {/* Diagnosis */}
      {d.diagnosis && (
        <div className={`${c.card} ${c.border} border rounded-2xl p-5`}>
          <h3 className={`font-bold ${c.text} flex items-center gap-2 mb-4`}>
            <span>🔍</span> {t('trt_diagnosis')}
          </h3>
          <div className="grid grid-cols-1 sm:grid-cols-2 gap-3">
            <div className={`${c.danger} rounded-xl p-3`}>
              <p className={`text-xs font-semibold uppercase ${isDark ? 'text-red-300' : 'text-red-800'} mb-1`}>{t('trt_current_opening')}</p>
              <p className={`text-sm ${c.text} mb-1`}>{d.diagnosis.current_opening}</p>
              <p className={`text-xs ${c.textMuted} italic`}>{d.diagnosis.opening_problem}</p>
            </div>
            <div className={`${c.danger} rounded-xl p-3`}>
              <p className={`text-xs font-semibold uppercase ${isDark ? 'text-red-300' : 'text-red-800'} mb-1`}>{t('trt_current_closing')}</p>
              <p className={`text-sm ${c.text} mb-1`}>{d.diagnosis.current_closing}</p>
              <p className={`text-xs ${c.textMuted} italic`}>{d.diagnosis.closing_problem}</p>
            </div>
          </div>
        </div>
      )}

      {/* New opening */}
      {d.new_opening && (
        <div className={`${c.success} rounded-2xl p-5`}>
          <div className="flex items-center justify-between mb-3">
            <h3 className={`font-bold ${isDark ? 'text-emerald-400' : 'text-emerald-700'} flex items-center gap-2`}>
              <span>🎬</span> {t('trt_new_opening')}
            </h3>
          </div>
          <span className={`inline-block text-xs font-semibold px-2 py-0.5 rounded-full mb-3 ${isDark ? 'bg-emerald-900/30 text-emerald-400' : 'bg-emerald-100 text-emerald-800'}`}>
            {d.new_opening.technique}
          </span>
          <p className={`text-sm ${c.text} leading-relaxed whitespace-pre-wrap`}>{d.new_opening.text}</p>
          <p className={`text-xs ${c.textMuted} mt-2 italic`}>{d.new_opening.why_it_works}</p>
        </div>
      )}

      {/* New closing */}
      {d.new_closing && (
        <div className={`${c.accentBox} rounded-2xl p-5`}>
          <div className="flex items-center justify-between mb-3">
            <h3 className={`font-bold ${c.accentTxt} flex items-center gap-2`}>
              <span>🎬</span> {t('trt_new_closing')}
            </h3>
          </div>
          <span className={`inline-block text-xs font-semibold px-2 py-0.5 rounded-full mb-3 ${isDark ? 'bg-sky-900/30 text-sky-300' : 'bg-sky-100 text-sky-800'}`}>
            {d.new_closing.technique}
          </span>
          <p className={`text-sm ${c.text} leading-relaxed whitespace-pre-wrap`}>{d.new_closing.text}</p>
          <p className={`text-xs ${c.textMuted} mt-2 italic`}>{d.new_closing.why_it_works}</p>
        </div>
      )}

      {/* Transitions */}
      {d.transitions?.length > 0 && (
        <div className={`${c.card} ${c.border} border rounded-2xl p-5`}>
          <h3 className={`font-bold ${c.text} flex items-center gap-2 mb-4`}>
            <span>🔗</span> {t('trt_key_transitions')} ({d.transitions.length})
          </h3>
          <div className="space-y-3">
            {d.transitions.map((tr, i) => (
              <div key={i} className={`${c.cardAlt} ${c.border} border rounded-xl p-4`}>
                <p className={`text-xs font-semibold ${c.accentTxt} mb-2`}>{tr.between}</p>
                {tr.original && (
                  <p className={`text-sm ${c.textMuted} line-through mb-1`}>{tr.original}</p>
                )}
                <p className={`text-sm font-medium ${c.text} leading-relaxed`}>"{tr.rewritten}"</p>
                <p className={`text-xs ${c.textMuted} mt-1 italic`}>{tr.why}</p>
              </div>
            ))}
          </div>
        </div>
      )}

      {/* Energy arc */}
      {d.energy_arc && (
        <div className={`${c.goldBox} ${c.border} border rounded-2xl p-4`}>
          <p className={`text-xs font-semibold uppercase ${c.goldTxt} mb-2 flex items-center gap-1.5`}>
            <span>📈</span> {t('trt_energy_arc')}
          </p>
          <p className={`text-sm ${c.text} leading-relaxed`}>{d.energy_arc}</p>
        </div>
      )}
    </div>
  );

  // ─── Main Render ───
  return (
    <div className={`space-y-4 ${c.text}`}>
      <div className="space-y-6">

        {/* Header */}
        <div className={`${c.card} border ${c.border} rounded-xl shadow-sm p-5`}>
          <div className="pb-3 border-b border-zinc-500">
            <div className="flex items-start justify-between gap-3">
              <div>
                <h2 className={`text-xl font-bold ${c.text}`}>
                  <span className="mr-2">{tool?.icon ?? '🎙️'}</span>{tool?.title ?? t('trt_title')}
                </h2>
                <p className={`text-sm ${c.textSecondary}`}>
                  {tool?.tagline ?? t('trt_tagline')}
                  <button onClick={loadExample} disabled={loading} style={{ backgroundColor: (tool?.headerColor ?? '#888888') + '80' }} className={`mt-1.5 px-2.5 py-0.5 rounded-full text-xs font-medium border disabled:opacity-40 ${isDark ? 'text-white border-white/40' : 'text-gray-800 border-transparent'}`}>{t('try_example')}</button>
                </p>
              </div>
              {(results || content.trim()) && (
                <button onClick={handleReset} className={`${c.btnSecondary} px-3 py-1.5 rounded-lg text-xs font-bold flex-shrink-0`}>
                  ↺ {t('start_over')}
                </button>
              )}
            </div>
          </div>
        </div>

        {/* Mode tabs */}
        <div className={`flex border-b ${c.border}`}>
          {MODES.map(m => (
            <button
              key={m.id}
              onClick={() => handleModeSwitch(m.id)}
              className={`flex-1 py-3 text-center text-sm font-medium transition-all duration-150
                ${mode === m.id ? c.tabActive : c.tabInactive}`}
            >
              <span className="mr-1.5">{m.icon}</span>
              <span>{t(m.labelKey)}</span>
            </button>
          ))}
        </div>

        {/* Mode description */}
        <p className={`text-xs ${c.textMuted} italic`}>
          {t(MODES.find(m => m.id === mode)?.descKey)}
        </p>

        {/* Pre-result cross-ref */}
        <p className={`text-xs ${c.textMuted}`}>
          {t('trt_xref_heckler_pre')} <a href="/HecklerPrep" className={linkStyle}>🎤 {t('trt_heckler')}</a> {t('trt_xref_heckler_pre_tail')}
        </p>

        {/* Input card */}
        <div className={`${c.card} ${c.border} border rounded-2xl p-5 shadow-sm space-y-4`}>

          {/* Content textarea (all modes) */}
          <div className="space-y-2">
            <label className={`text-sm font-semibold ${c.text}`}>
              <span className="mr-1.5">📝</span> {t('trt_content_label')} <span className={c.required}>*</span>
            </label>
            <textarea
              value={content}
              onChange={(e) => setContent(e.target.value)}
              placeholder={t('trt_content_ph')}
              rows={6}
              className={`w-full px-4 py-3 rounded-xl text-base ${c.input} ${c.border} border ${c.text} resize-y outline-none transition-colors`}
            />
            <p className={`text-xs ${c.textMuted} text-right`}>{content.length > 0 ? t('trt_min_at_pace', { count: Math.max(1, Math.round(content.split(/\s+/).filter(Boolean).length / 130)) }) : ''}</p>
          </div>

          {/* Mode-specific inputs */}
          {mode === 'cut' && (
            <div className="space-y-4">
              <div className="space-y-2">
                <label className={`text-sm font-semibold ${c.text}`}>
                  <span className="mr-1.5">⏱️</span> {t('trt_time_label')}
                </label>
                <div className="flex items-center gap-3">
                  <input
                    type="range"
                    min={1}
                    max={30}
                    value={timeMinutes}
                    onChange={(e) => setTimeMinutes(Number(e.target.value))}
                    className="flex-1"
                  />
                  <span className={`text-sm font-bold ${c.text} w-12 text-center`}>{timeMinutes}{t('trt_min_suffix')}</span>
                </div>
              </div>
              <div className="space-y-2">
                <label className={`text-sm font-semibold ${c.text}`}>
                  <span className="mr-1.5">💡</span> {t('trt_context_label')} <span className={`font-normal ${c.textMuted}`}>({t('optional')})</span>
                </label>
                <input
                  type="text"
                  value={context}
                  onChange={(e) => setContext(e.target.value)}
                  placeholder={t('trt_context_ph')}
                  className={`w-full px-4 py-2.5 rounded-xl text-base ${c.input} ${c.border} border ${c.text} outline-none transition-colors`}
                />
              </div>
            </div>
          )}

          {mode === 'anticipate' && (
            <div className="space-y-4">
              <div className="space-y-2">
                <label className={`text-sm font-semibold ${c.text}`}>
                  <span className="mr-1.5">👥</span> {t('trt_audience_label')}
                </label>
                <div className="grid grid-cols-2 sm:grid-cols-3 gap-2">
                  {AUDIENCE_OPTIONS.map(a => (
                    <button
                      key={a.id}
                      onClick={() => setAudience(a.id)}
                      className={`${audience === a.id ? c.chipActive : c.chipInactive} border rounded-xl px-2 py-2.5 text-center transition-all duration-150`}
                    >
                      <span className="text-lg block mb-0.5">{a.icon}</span>
                      <p className="text-xs font-medium leading-tight">{t(a.labelKey)}</p>
                    </button>
                  ))}
                </div>
              </div>
              <div className="space-y-2">
                <label className={`text-sm font-semibold ${c.text}`}>
                  <span className="mr-1.5">🔥</span> {t('trt_stakes_label')} <span className={`font-normal ${c.textMuted}`}>({t('optional')})</span>
                </label>
                <input
                  type="text"
                  value={stakes}
                  onChange={(e) => setStakes(e.target.value)}
                  placeholder={t('trt_stakes_ph', { sym })}
                  className={`w-full px-4 py-2.5 rounded-xl text-base ${c.input} ${c.border} border ${c.text} outline-none transition-colors`}
                />
              </div>
            </div>
          )}

          {mode === 'hook' && (
            <div className="space-y-4">
              <div className="space-y-2">
                <label className={`text-sm font-semibold ${c.text}`}>
                  <span className="mr-1.5">🎭</span> {t('trt_tone_label')}
                </label>
                <div className="grid grid-cols-2 gap-2">
                  {TONE_OPTIONS.map(to => (
                    <button
                      key={to.id}
                      onClick={() => setTone(to.id)}
                      className={`${tone === to.id ? c.chipActive : c.chipInactive} border rounded-xl px-3 py-2.5 text-left transition-all duration-150`}
                    >
                      <div className="flex items-center gap-2">
                        <span className="text-lg">{to.icon}</span>
                        <div>
                          <p className="text-sm font-medium leading-tight">{t(to.labelKey)}</p>
                          <p className={`text-xs ${c.textMuted} leading-tight mt-0.5`}>{t(to.descKey)}</p>
                        </div>
                      </div>
                    </button>
                  ))}
                </div>
              </div>
              <div className="space-y-2">
                <label className={`text-sm font-semibold ${c.text}`}>
                  <span className="mr-1.5">🎯</span> {t('trt_goal_label')} <span className={`font-normal ${c.textMuted}`}>({t('optional')})</span>
                </label>
                <input
                  type="text"
                  value={goal}
                  onChange={(e) => setGoal(e.target.value)}
                  placeholder={t('trt_goal_ph')}
                  className={`w-full px-4 py-2.5 rounded-xl text-base ${c.input} ${c.border} border ${c.text} outline-none transition-colors`}
                />
              </div>
            </div>
          )}

          {/* Submit */}
          <button
            onClick={handleSubmit}
            disabled={loading || !content.trim()}
            className={`w-full ${c.btnPrimary} py-3 rounded-xl font-semibold text-sm shadow-md
              disabled:opacity-40 disabled:cursor-not-allowed transition-all duration-200 flex items-center justify-center gap-2`}
          >
            {loading ? (
              <>
                <span className="inline-block animate-spin">{tool?.icon ?? '🎙️'}</span>
                {loadingMessages[mode]}
              </>
            ) : (
              submitLabels[mode]
            )}
          </button>
          <p className={`text-xs text-center ${c.textMuted}`}>{t('trt_ai_disclaimer')}</p>
        </div>

        {/* Error */}
        {error && (
          <div className={`${c.danger} border rounded-xl p-4 text-sm flex items-center gap-2`}>
            <span>⚠️</span> {error}
          </div>
        )}

        {/* Results */}
        {results && (
          <div ref={resultsRef} className="space-y-5">
            {results.mode === 'cut' && <CutResults data={results.data} />}
            {results.mode === 'anticipate' && <AnticipateResults data={results.data} />}
            {results.mode === 'hook' && <HookResults data={results.data} />}

            {/* Cross-references */}
            <div className={`${c.cardAlt} ${c.border} border rounded-xl p-4 space-y-2`}>
              <p className={`text-xs font-semibold ${c.textMuted} uppercase tracking-wider`}>
                {t('trt_related_tools')}
              </p>
              <div className="space-y-1.5 text-xs">
                <p className={c.textSecondary}>{t('trt_xref_heckler_post')} <a href="/HecklerPrep" className={linkStyle}>🎤 {t('trt_heckler')}</a> {t('trt_xref_heckler_post_tail')}</p>
                <p className={c.textSecondary}>{t('trt_xref_toast_pre')} <a href="/ToastWriter" className={linkStyle}>🥂 {t('trt_toastwriter')}</a> {t('trt_xref_toast_tail')}</p>
              </div>
            </div>
          </div>
        )}
      </div>

      {/* Session sessionHistory */}
      {/* eslint-disable-next-line no-restricted-globals */}
      {sessionHistory.length > 0 && (
        <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
          <p className={`text-xs font-bold ${c.textMuted} mb-2`}>📋 {t('trt_recent_sessions')}</p>
          <div className="space-y-1">
            {/* eslint-disable-next-line no-restricted-globals */}

            {sessionHistory.map(s => (
              <div key={s.id} className="flex items-center justify-between">
                <span className={`text-xs ${c.textSecondary} truncate`}>{s.preview}</span>
                <span className={`text-xs ${c.textMuted} ml-2 shrink-0`}>{s.mode}</span>
              </div>
            ))}
          </div>
        </div>
      )}
    </div>
  );
};

TheRunthrough.displayName = 'TheRunthrough';
export default TheRunthrough;
