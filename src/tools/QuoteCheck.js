import React, { useState, useEffect, useRef, useCallback } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { usePersistentState } from '../hooks/usePersistentState';
import { useRegisterActions } from '../components/ActionBarContext';
import { useTranslation } from '../i18n/useTranslation';
import { currencySymbol } from '../utils/formatLocale';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';
const ALLOWED_FILE_TYPES = ['image/jpeg', 'image/png', 'application/pdf'];

const EXAMPLE = {
  repairType: 'appliance',
  itemDescription: 'qc_example_item',
  whatWentWrong: 'qc_example_wrong',
  whatTheyToldYou: 'qc_example_told',
  quotedPrice: '450',
  quotedBreakdown: 'qc_example_breakdown',
  itemAge: '6 years',
};

function QuoteCheck({ tool }) {
  const { callToolEndpoint, loading, userLocale, userCurrency, userRegion } = useClaudeAPI();
  const { isDark } = useTheme();
  const { t } = useTranslation();
  const sym = currencySymbol(userLocale, userCurrency);

  const REPAIR_TYPES = [
    { id: 'appliance', label: t('qc_type_appliance'), icon: '🔌' },
    { id: 'car',       label: t('qc_type_car'),        icon: '🚗' },
    { id: 'other',     label: t('qc_type_other'),       icon: '🔧' },
  ];

  const c = {
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:         isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20'
                          : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-400' : 'text-gray-500',
    labelText:     isDark ? 'text-zinc-200' : 'text-gray-700',
    accentTxt:     isDark ? 'text-cyan-400' : 'text-cyan-600',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white'
                          : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200'
                          : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200'
                          : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200'
                          : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200'
                          : 'bg-red-50 border-red-200 text-red-800',
    infoBox:       isDark ? 'bg-sky-900/20 border-sky-700 text-sky-200'
                          : 'bg-sky-50 border-sky-200 text-sky-800',
    pillActive:    isDark ? 'border-cyan-500 bg-cyan-900/30 text-cyan-200'
                          : 'border-cyan-600 bg-cyan-100 text-cyan-900',
    pillInactive:  isDark ? 'border-zinc-600 text-zinc-400 hover:border-zinc-500'
                          : 'border-gray-300 text-gray-500 hover:border-gray-400',
    required:      isDark ? 'text-amber-400' : 'text-amber-700',
    warningTxt:    isDark ? 'text-amber-300' : 'text-amber-800',
  };
  c.textMuteded = c.textMuted;
  c.label = c.labelText;

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-700 hover:text-cyan-800 underline underline-offset-2';

  const [repairType, setRepairType]           = useState('appliance');
  const [itemDescription, setItemDescription] = useState('');
  const [whatWentWrong, setWhatWentWrong]     = useState('');
  const [whatTheyToldYou, setWhatTheyToldYou] = useState('');
  const [quotedPrice, setQuotedPrice]         = useState('');
  const [quotedBreakdown, setQuotedBreakdown] = useState('');
  const [secondQuotePrice, setSecondQuotePrice] = useState('');
  const [itemAge, setItemAge]                 = useState('');
  const [uploadedFile, setUploadedFile]       = useState(null);
  const [fileBase64, setFileBase64]           = useState(null);
  const [results, setResults]                 = usePersistentState('quotecheck-results', null);
  const [sessionHistory, setSessionHistory]   = usePersistentState('quotecheck-history', []);
  const [error, setError]                     = useState('');

  const resultsRef      = useRef(null);
  const fileInputRef    = useRef(null);
  const handleCheckRef  = useRef(null);
  const canSubmitRef    = useRef(false);

  const canSubmit = itemDescription.trim().length > 0 && whatWentWrong.trim().length > 0 && quotedPrice !== '' && !isNaN(Number(quotedPrice));

  const handleReset = useCallback(() => {
    setResults(null);
    setError('');
    setRepairType('appliance');
    setItemDescription('');
    setWhatWentWrong('');
    setWhatTheyToldYou('');
    setQuotedPrice('');
    setQuotedBreakdown('');
    setSecondQuotePrice('');
    setItemAge('');
    setUploadedFile(null);
    setFileBase64(null);
    if (fileInputRef.current) fileInputRef.current.value = '';
  }, [setResults]);

  // ── File upload — optional supplementary evidence (photo or PDF of the
  //    actual quote/invoice), never replaces the required text fields. ──
  const handleFileUpload = useCallback((e) => {
    const file = e.target.files[0];
    if (!file) return;
    if (!ALLOWED_FILE_TYPES.includes(file.type)) { setError(t('qc_err_file_type')); return; }
    if (file.size > 10 * 1024 * 1024) { setError(t('qc_err_file_size')); return; }
    setError('');
    const reader = new FileReader();
    reader.onerror = () => setError(t('qc_err_file_read'));
    reader.onload = (event) => { setUploadedFile(file); setFileBase64(event.target.result); };
    reader.readAsDataURL(file);
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [t]);

  const removeFile = useCallback(() => {
    setUploadedFile(null);
    setFileBase64(null);
    setError('');
    if (fileInputRef.current) fileInputRef.current.value = '';
  }, []);

  const loadExample = useCallback(() => {
    setRepairType(EXAMPLE.repairType);
    setItemDescription(t(EXAMPLE.itemDescription));
    setWhatWentWrong(t(EXAMPLE.whatWentWrong));
    setWhatTheyToldYou(t(EXAMPLE.whatTheyToldYou));
    setQuotedPrice(EXAMPLE.quotedPrice);
    setQuotedBreakdown(t(EXAMPLE.quotedBreakdown));
    setItemAge(EXAMPLE.itemAge);
    setResults(null);
    setError('');
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [setResults, t]);

  const handleCheck = useCallback(async () => {
    if (!canSubmit || loading) return;
    setError('');
    setResults(null);

    try {
      const parsed = await callToolEndpoint('quote-check', {
        repairType,
        itemDescription,
        whatWentWrong,
        whatTheyToldYou,
        quotedPrice: Number(quotedPrice),
        quotedBreakdown,
        secondQuotePrice: secondQuotePrice ? Number(secondQuotePrice) : undefined,
        itemAge,
        quoteFileBase64: fileBase64 || undefined,
        userLocale,
        userCurrency,
        userRegion,
      });
      setResults({ ...parsed, repairType, _input: { itemDescription: itemDescription.trim(), whatWentWrong: whatWentWrong.trim(), quotedPrice } });
      // PF-25 exception: 80 is an item-description preview length, not a history cap.
      setSessionHistory(prev => [{
        preview: itemDescription.slice(0, 80) + (itemDescription.length > 80 ? '…' : ''),
        verdict: parsed.verdict,
        ts:      Date.now(),
      }, ...prev].slice(0, 8));
    } catch (err) {
      setError(err.message || t('qc_error'));
    }
  }, [canSubmit, loading, repairType, itemDescription, whatWentWrong, whatTheyToldYou, quotedPrice, quotedBreakdown, secondQuotePrice, itemAge, fileBase64, callToolEndpoint, setResults, setSessionHistory, userLocale, userCurrency, userRegion, t]);

  handleCheckRef.current = handleCheck;
  canSubmitRef.current   = canSubmit;

  const buildFullText = useCallback(() => {
    if (!results) return '';
    const lines = [t('qc_copy_header'), ''];
    const verdictLabel = {
      likely_fair:   t('qc_verdict_likely_fair'),
      somewhat_high: t('qc_verdict_somewhat_high'),
      overpriced:    t('qc_verdict_overpriced'),
      cant_tell:     t('qc_verdict_cant_tell'),
    }[results?.verdict] || results?.verdict;
    lines.push(`${t('qc_copy_verdict')} ${verdictLabel}`);
    if (results?.verdict_explanation) lines.push(`\n${results.verdict_explanation}`);
    if (results?.price_reality_check?.typical_range) {
      lines.push(`\n${t('qc_copy_typical')} ${results.price_reality_check.typical_range}`);
      if (results?.price_reality_check?.where_this_quote_falls) lines.push(results.price_reality_check.where_this_quote_falls);
    }
    if (results?.red_flags?.length) {
      lines.push(`\n${t('qc_copy_flags')}`);
      results.red_flags.forEach(f => lines.push(`• ${f?.flag}${f?.why_it_matters ? ` — ${f.why_it_matters}` : ''}`));
    }
    if (results?.negotiation_script) {
      lines.push(`\n${t('qc_copy_script')}`);
      lines.push(results.negotiation_script);
    }
    if (results?.questions_to_ask?.length) {
      lines.push(`\n${t('qc_copy_questions')}`);
      results.questions_to_ask.forEach((q, i) => lines.push(`${i + 1}. ${q}`));
    }
    lines.push(BRAND);
    return lines.join('\n');
  }, [results, t]);

  useRegisterActions(buildFullText(), tool?.title || 'QuoteCheck');

  useEffect(() => {
    if (!results || !resultsRef.current) return;
    const timer = setTimeout(() => resultsRef.current?.scrollIntoView({ behavior: 'smooth', block: 'start' }), 200);
    return () => clearTimeout(timer);
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [results]);

  useEffect(() => {
    const handler = (e) => {
      const tag = document.activeElement?.tagName;
      if (tag === 'SELECT') return;
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && !loading && canSubmitRef.current)
        handleCheckRef.current?.();
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [loading]);

  const verdictConfig = results ? {
    likely_fair:   { bg: isDark ? 'bg-emerald-900/40 border-emerald-600' : 'bg-emerald-50 border-emerald-400', txt: isDark ? 'text-emerald-200' : 'text-emerald-800', icon: '✅', label: t('qc_verdict_likely_fair') },
    somewhat_high: { bg: isDark ? 'bg-amber-900/40 border-amber-600'     : 'bg-amber-50 border-amber-400',     txt: isDark ? 'text-amber-200'   : 'text-amber-800',   icon: '⚠️', label: t('qc_verdict_somewhat_high') },
    overpriced:    { bg: isDark ? 'bg-red-900/40 border-red-600'         : 'bg-red-50 border-red-400',         txt: isDark ? 'text-red-200'     : 'text-red-800',     icon: '🚨', label: t('qc_verdict_overpriced') },
    cant_tell:     { bg: isDark ? 'bg-zinc-700/60 border-zinc-500'       : 'bg-slate-100 border-slate-300',    txt: isDark ? 'text-zinc-200'    : 'text-slate-800',   icon: '🤔', label: t('qc_verdict_cant_tell') },
  }[results?.verdict] ?? { bg: c.warning, txt: c.warningTxt, icon: '📋', label: results?.verdict } : null;

  const renderInput = () => (
    <div className={`${c.card} border ${c.border} rounded-xl shadow-sm p-5 space-y-4`}>
      <div className="pb-3 border-b border-zinc-500">
        <h2 className={`text-xl font-bold ${c.text}`}>
          <span className="mr-2">{tool?.icon ?? '🧾'}</span>{tool?.title ?? t('qc_title')}
        </h2>
        <p className={`text-sm ${c.textSecondary}`}>{tool?.tagline ?? t('qc_tagline')}</p>
        <button onClick={loadExample} disabled={loading} style={{ backgroundColor: (tool?.headerColor ?? '#888888') + '80' }} className={`mt-1.5 px-2.5 py-0.5 rounded-full text-xs font-medium border disabled:opacity-40 ${isDark ? 'text-white border-white/40' : 'text-gray-800 border-transparent'}`}>{t('try_example')}</button>
      </div>

      {/* Repair type */}
      <div>
        <label className={`block text-sm font-medium ${c.labelText} mb-2`}>{t('qc_type_label')}</label>
        <div className="flex flex-wrap gap-2">
          {REPAIR_TYPES.map(rt => (
            <button key={rt.id} onClick={() => setRepairType(rt.id)}
              className={`px-3 py-1.5 rounded-lg border text-xs font-medium transition-colors ${repairType === rt.id ? c.pillActive : c.pillInactive}`}>
              <span className="mr-1">{rt.icon}</span>{rt.label}
            </button>
          ))}
        </div>
      </div>

      {/* Item */}
      <div>
        <label className={`block text-sm font-medium ${c.labelText} mb-1`}>
          {t('qc_item_label')} <span className={c.required}>*</span>
        </label>
        <input type="text" value={itemDescription} onChange={e => setItemDescription(e.target.value)}
          placeholder={t('qc_item_ph')}
          className={`w-full px-3 py-2 border rounded-xl text-sm focus:outline-none focus:ring-2 ${c.input}`}
        />
      </div>

      {/* What's wrong */}
      <div>
        <label className={`block text-sm font-medium ${c.labelText} mb-1`}>
          {t('qc_wrong_label')} <span className={c.required}>*</span>
        </label>
        <textarea value={whatWentWrong} onChange={e => setWhatWentWrong(e.target.value)}
          placeholder={t('qc_wrong_ph')} rows={2}
          className={`w-full px-3 py-2.5 border rounded-xl text-sm resize-y focus:outline-none focus:ring-2 ${c.input}`}
        />
      </div>

      {/* What they told you */}
      <div>
        <label className={`block text-sm font-medium ${c.labelText} mb-1`}>
          {t('qc_told_label')} <span className={`text-xs font-normal ${c.textMuted}`}>{t('qc_told_hint')}</span>
        </label>
        <textarea value={whatTheyToldYou} onChange={e => setWhatTheyToldYou(e.target.value)}
          placeholder={t('qc_told_ph')} rows={2}
          className={`w-full px-3 py-2.5 border rounded-xl text-sm resize-y focus:outline-none focus:ring-2 ${c.input}`}
        />
      </div>

      {/* Quoted price */}
      <div>
        <label className={`block text-sm font-medium ${c.labelText} mb-1`}>
          {t('qc_price_label')} <span className={c.required}>*</span>
        </label>
        <div className="flex items-center gap-1">
          <span className={`text-sm font-bold ${c.textMuteded}`}>{sym}</span>
          <input type="number" value={quotedPrice} onChange={e => setQuotedPrice(e.target.value)}
            placeholder="0.00"
            className={`flex-1 px-3 py-2.5 border rounded-lg text-sm ${c.input} outline-none focus:ring-2`}
          />
        </div>
      </div>

      {/* Breakdown */}
      <div>
        <label className={`block text-sm font-medium ${c.labelText} mb-1`}>
          {t('qc_breakdown_label')} <span className={`text-xs font-normal ${c.textMuted}`}>{t('qc_breakdown_hint')}</span>
        </label>
        <textarea value={quotedBreakdown} onChange={e => setQuotedBreakdown(e.target.value)}
          placeholder={t('qc_breakdown_ph', { sym })} rows={2}
          className={`w-full px-3 py-2.5 border rounded-xl text-sm resize-y focus:outline-none focus:ring-2 ${c.input}`}
        />
      </div>

      {/* Upload the actual quote — optional supplementary evidence */}
      <div>
        <label className={`block text-sm font-medium ${c.labelText} mb-1`}>
          {t('qc_upload_label')} <span className={`text-xs font-normal ${c.textMuted}`}>{t('qc_upload_hint')}</span>
        </label>
        {!uploadedFile ? (
          <div onClick={() => fileInputRef.current?.click()}
            className={`border-2 border-dashed rounded-xl p-4 text-center cursor-pointer transition-all ${isDark ? 'border-zinc-600 hover:border-cyan-500 hover:bg-zinc-700/50' : 'border-gray-300 hover:border-cyan-400 hover:bg-cyan-50'}`}>
            <span className="text-2xl block mb-1">📎</span>
            <p className={`text-sm font-medium ${c.text}`}>{t('qc_upload_cta')}</p>
          </div>
        ) : (
          <div className={`flex items-center justify-between p-3 rounded-xl border ${c.success}`}>
            <div className="flex items-center gap-2">
              <span className="text-xl">{uploadedFile.type === 'application/pdf' ? '📄' : '🖼️'}</span>
              <div>
                <p className={`text-sm font-semibold ${c.text}`}>{uploadedFile.name}</p>
                <p className={`text-xs ${c.textMuted}`}>{(uploadedFile.size / 1024).toFixed(1)} KB</p>
              </div>
            </div>
            <button onClick={removeFile} className={`text-sm ${c.textMuted}`}>✕</button>
          </div>
        )}
        <input ref={fileInputRef} type="file" accept="image/jpeg,image/png,application/pdf" onChange={handleFileUpload} className="hidden" />
      </div>

      <div className="grid grid-cols-2 gap-3">
        {/* Second quote */}
        <div>
          <label className={`block text-sm font-medium ${c.labelText} mb-1`}>
            {t('qc_second_label')} <span className={`text-xs font-normal ${c.textMuted}`}>{t('qc_second_hint')}</span>
          </label>
          <div className="flex items-center gap-1">
            <span className={`text-sm font-bold ${c.textMuteded}`}>{sym}</span>
            <input type="number" value={secondQuotePrice} onChange={e => setSecondQuotePrice(e.target.value)}
              placeholder="0.00"
              className={`flex-1 px-3 py-2 border rounded-lg text-sm ${c.input} outline-none focus:ring-2`}
            />
          </div>
        </div>

        {/* Item age — only meaningful for appliances */}
        {repairType === 'appliance' && (
          <div>
            <label className={`block text-sm font-medium ${c.labelText} mb-1`}>
              {t('qc_age_label')} <span className={`text-xs font-normal ${c.textMuted}`}>{t('qc_age_hint')}</span>
            </label>
            <input type="text" value={itemAge} onChange={e => setItemAge(e.target.value)}
              placeholder={t('qc_age_ph')}
              className={`w-full px-3 py-2 border rounded-xl text-sm focus:outline-none focus:ring-2 ${c.input}`}
            />
          </div>
        )}
      </div>

      {error && <p className={`text-sm ${c.danger} border rounded-lg px-3 py-2`}>{error}</p>}

      <p className={`text-xs ${c.textMuted}`}>
        🔗 <a href="/LeverageLogic" className={linkStyle}>⚖️ {t('qc_leverage')}</a> <a href="/ContractDecoder" className={linkStyle}>📋 {t('qc_contract')}</a>
      </p>

      <button onClick={handleCheck} disabled={!canSubmit || loading}
        className={`w-full ${c.btnPrimary} disabled:opacity-40 font-bold py-3 rounded-lg flex items-center justify-center gap-2 min-h-[48px]`}>
        {loading
          ? <><span className="inline-block animate-spin">{tool?.icon ?? '🧾'}</span> {t('qc_analyzing')}</>
          : <><span className="mr-1">{tool?.icon ?? '🧾'}</span> {t('qc_check_it')}</>}
      </button>

      {sessionHistory.length > 0 && (
        <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
          <div className="flex items-center justify-between mb-3">
            <p className={`text-xs font-bold ${c.textMuted} uppercase tracking-wide`}>🕓 {t('qc_recent')}</p>
            <button onClick={() => setSessionHistory([])} className={`text-xs ${c.textMuted}`}>{t('qc_clear')}</button>
          </div>
          <ul className="space-y-2">
            {sessionHistory.map((h, i) => {
              const vIcon = h.verdict === 'overpriced' ? '🚨' : h.verdict === 'somewhat_high' ? '⚠️' : h.verdict === 'likely_fair' ? '✅' : '🤔';
              const vColor = h.verdict === 'overpriced'
                ? (isDark ? 'text-red-400' : 'text-red-600')
                : h.verdict === 'somewhat_high'
                  ? (isDark ? 'text-amber-400' : 'text-amber-600')
                  : h.verdict === 'likely_fair'
                    ? (isDark ? 'text-emerald-400' : 'text-emerald-600')
                    : c.textMuted;
              return (
                <li key={i} className={`text-xs ${c.textSecondary} flex items-start gap-2`}>
                  <span className="flex-shrink-0">{vIcon}</span>
                  <span>
                    <span className={`font-bold ${vColor}`}>{h.verdict?.replace(/_/g, ' ').toUpperCase()}</span>
                    <span className={`block ${c.textMuted} mt-0.5`}>{h.preview}</span>
                  </span>
                </li>
              );
            })}
          </ul>
        </div>
      )}
    </div>
  );

  const renderResults = () => {
    if (!results || !verdictConfig) return null;
    return (
      <div className="space-y-4" ref={resultsRef}>

        {/* Recap of what the user told us — anchors persisted results on revisits */}
        {results?._input && (
          <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
            <p className={`text-xs font-semibold uppercase tracking-wide mb-1.5 ${c.textMuted}`}>📝 {t('qc_your_situation')}</p>
            <p className={`text-sm ${c.textSecondary}`}>{results._input.itemDescription}{results._input.quotedPrice !== '' && results._input.quotedPrice != null ? ` — ${sym}${results._input.quotedPrice}` : ''}</p>
            {results._input.whatWentWrong && <p className={`text-xs mt-1.5 ${c.textMuted}`}>{results._input.whatWentWrong}</p>}
          </div>
        )}

        {/* Overall verdict */}
        <div className={`border-2 rounded-xl p-5 ${verdictConfig?.bg}`}>
          <div className="flex items-start gap-3">
            <span className="text-3xl">{verdictConfig?.icon}</span>
            <div>
              <p className={`text-xl font-black ${verdictConfig?.txt}`}>{verdictConfig?.label}</p>
              {results?.verdict_explanation && (
                <p className={`text-sm mt-2 ${verdictConfig?.txt}`}>{results.verdict_explanation}</p>
              )}
            </div>
          </div>
        </div>

        {/* Price reality check */}
        {results?.price_reality_check && (
          <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
            <p className={`text-xs font-bold ${c.textMuted} uppercase tracking-wide mb-3`}>💰 {t('qc_price_check_heading')}</p>
            <div className="space-y-2">
              {results?.price_reality_check?.typical_range && (
                <p className={`text-sm ${c.textSecondary}`}><span className="font-semibold">{t('qc_typical_range')}:</span> {results.price_reality_check.typical_range}</p>
              )}
              {results?.price_reality_check?.where_this_quote_falls && (
                <p className={`text-sm ${c.textSecondary}`}>{results.price_reality_check.where_this_quote_falls}</p>
              )}
              {results?.price_reality_check?.confidence && (
                <p className={`text-xs ${c.textMuted}`}>{t('qc_confidence')}: {t(`qc_confidence_${results.price_reality_check.confidence}`) || results.price_reality_check.confidence}</p>
              )}
            </div>
          </div>
        )}

        {/* Red flags */}
        <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
          <p className={`text-xs font-bold ${c.textMuted} uppercase tracking-wide mb-3`}>🚩 {t('qc_red_flags_heading')}</p>
          {results?.red_flags?.length > 0 ? (
            <ul className="space-y-2">
              {results.red_flags.map((f, i) => (
                <li key={i} className={`text-sm ${c.danger} border rounded-lg px-3 py-2`}>
                  <span className="font-semibold">{f?.flag}</span>
                  {f?.why_it_matters && <span className={`block text-xs mt-0.5 opacity-80`}>{f.why_it_matters}</span>}
                </li>
              ))}
            </ul>
          ) : (
            <p className={`text-sm ${c.success} border rounded-lg px-3 py-2`}>{t('qc_red_flags_none')}</p>
          )}
        </div>

        {/* Itemization check */}
        {results?.itemization_check && (
          <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
            <p className={`text-xs font-bold ${c.textMuted} uppercase tracking-wide mb-3`}>📝 {t('qc_itemization_heading')}</p>
            {results?.itemization_check?.is_itemized_enough ? (
              <p className={`text-sm ${c.success} border rounded-lg px-3 py-2`}>{t('qc_itemized_good')}</p>
            ) : (
              <p className={`text-sm ${c.warning} border rounded-lg px-3 py-2`}>
                <span className="font-semibold">{t('qc_itemized_missing')}</span> {results?.itemization_check?.whats_missing}
              </p>
            )}
          </div>
        )}

        {/* Replace vs repair */}
        {results?.replace_vs_repair?.guidance && (
          <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
            <p className={`text-xs font-bold ${c.textMuted} uppercase tracking-wide mb-3`}>♻️ {t('qc_replace_heading')}</p>
            <p className={`text-sm ${c.textSecondary}`}>{results.replace_vs_repair.guidance}</p>
          </div>
        )}

        {/* Negotiation script */}
        {results?.negotiation_script && (
          <div className={`${c.infoBox} border rounded-xl p-4`}>
            <p className={`text-xs font-bold uppercase tracking-wide mb-2`}>💬 {t('qc_script_heading')}</p>
            <p className="text-sm">{results.negotiation_script}</p>
          </div>
        )}

        {/* Questions to ask */}
        {results?.questions_to_ask?.length > 0 && (
          <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
            <p className={`text-xs font-bold ${c.textMuted} uppercase tracking-wide mb-3`}>❓ {t('qc_questions_heading')}</p>
            <ul className="space-y-2">
              {results.questions_to_ask.map((q, i) => (
                <li key={i} className={`text-sm ${c.textSecondary}`}>
                  <span className={`font-bold ${c.accentTxt}`}>{i + 1}.</span> {q}
                </li>
              ))}
            </ul>
          </div>
        )}

        {/* Second opinion */}
        {results?.second_opinion && (
          <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
            <p className={`text-xs font-bold ${c.textMuted} uppercase tracking-wide mb-3`}>🙋 {t('qc_second_opinion_heading')}</p>
            <p className={`text-sm ${c.textSecondary}`}>
              {results?.second_opinion?.recommended ? t('qc_second_opinion_yes') : t('qc_second_opinion_no')}
              {results?.second_opinion?.reason && ` — ${results.second_opinion.reason}`}
            </p>
          </div>
        )}

        <p className={`text-xs ${c.textMuted} px-1`}>{t('qc_disclaimer')}</p>

        {/* Post-result cross-refs */}
        <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
          <p className={`text-[10px] font-bold ${c.textMuted} uppercase mb-2`}>🔗 {t('qc_related')}</p>
          <div className="flex flex-wrap gap-3">
            <a href="/LeverageLogic" className={`text-xs ${linkStyle}`}>⚖️ {t('qc_leverage')}</a>
            <a href="/ContractDecoder" className={`text-xs ${linkStyle}`}>📋 {t('qc_contract')}</a>
            <a href="/ScamRadar" className={`text-xs ${linkStyle}`}>🎣 {t('qc_scam')}</a>
          </div>
        </div>
      </div>
    );
  };

  return (
    <div className={`space-y-4 ${c.text}`}>
      {!results && <p className={`text-xs ${c.textMuted} px-1`}>🔗 <a href="/LeverageLogic" className={linkStyle}>⚖️ {t('qc_leverage')}</a></p>}
      {!results && renderInput()}
      {results ? (
        <div className={`${c.card} border ${c.border} rounded-xl shadow-sm p-5`}>
          <div className="pb-3 border-b border-zinc-500">
            <div className="flex items-start justify-between">
              <div>
                <h2 className={`text-xl font-bold ${c.text}`}>
                  <span className="mr-2">{tool?.icon ?? '🧾'}</span>{tool?.title ?? t('qc_title')}
                </h2>
                <p className={`text-sm ${c.textSecondary}`}>{tool?.tagline ?? t('qc_tagline')}</p>
              </div>
              <button onClick={handleReset} className={`${c.btnSecondary} px-3 py-1.5 rounded-lg text-xs font-bold`}>
                ↺ {t('qc_new_quote')}
              </button>
            </div>
          </div>
        </div>
      ) : null}
      {results && renderResults()}
    </div>
  );
}

QuoteCheck.displayName = 'QuoteCheck';
export default QuoteCheck;
