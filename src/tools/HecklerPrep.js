import React, { useState, useCallback, useEffect, useRef } from 'react';
import { useRegisterActions } from '../components/ActionBarContext';
import { usePersistentState } from '../hooks/usePersistentState';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { useTranslation } from '../i18n/useTranslation';
import { currencySymbol } from '../utils/formatLocale';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const HecklerPrep = ({ tool }) => {
  const { callToolEndpoint, loading, userLocale, userCurrency, userRegion } = useClaudeAPI();
  const { isDark } = useTheme();
  const { t } = useTranslation();
  const sym = currencySymbol(userLocale, userCurrency);

  const EXAMPLE = {
    topic: t('hp_ex_topic'),
    audience: t('hp_ex_audience'),
    proposal: t('hp_ex_proposal', { sym }),
    knownObjections: t('hp_ex_objections'),
    stakes: 'high',
  };
  const STAKES_LEVELS = [
    { value: 'low', label: t('hp_stakes_low_label'), emoji: '😌', desc: t('hp_stakes_low_desc') },
    { value: 'moderate', label: t('hp_stakes_moderate_label'), emoji: '😐', desc: t('hp_stakes_moderate_desc') },
    { value: 'high', label: t('hp_stakes_high_label'), emoji: '😰', desc: t('hp_stakes_high_desc') },
  ];



  const c = {
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:         isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20' : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-400' : 'text-gray-500',
    labelText:     isDark ? 'text-zinc-200' : 'text-gray-700',
    accentTxt:     isDark ? 'text-cyan-400' : 'text-cyan-600',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white' : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200' : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200' : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200' : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200' : 'bg-red-50 border-red-200 text-red-800',
    pillActive:    isDark ? 'border-cyan-500 bg-cyan-900/30 text-cyan-200' : 'border-cyan-600 bg-cyan-100 text-cyan-900',
    pillInactive:  isDark ? 'border-zinc-600 text-zinc-400 hover:border-zinc-500' : 'border-gray-300 text-gray-500 hover:border-gray-400',
    required:      isDark ? 'text-amber-400' : 'text-amber-700',
    badgeNeutral:  isDark ? 'bg-zinc-700 text-zinc-300' : 'bg-gray-100 text-gray-600',
  };
  c.textMuteded = c.textMuted;
  c.label = c.labelText;

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-700 hover:text-cyan-800 underline underline-offset-2';

  const [audience, setAudience] = useState('');
  const [proposal, setProposal] = useState('');
  const [knownObjections, setKnownObjections] = useState('');
  const [stakes, setStakes] = useState('moderate');
  const [error, setError] = useState('');
  const [topic, setTopic] = usePersistentState('hp-topic', '');
  const [sessionHistory, setSessionHistory] = usePersistentState('hecklerprep-history', []);
  const [results, setResults] = usePersistentState('hp-results', null);
  const resultsRef = React.useRef(null);
  const [expandedQ, setExpandedQ] = useState(null);

  const generate = useCallback(async () => {
    if (!topic.trim()) return;
    setError(''); setResults(null); setExpandedQ(null);
    try {
      const data = await callToolEndpoint('heckler-prep', {
        topic: topic.trim(),
        audience: audience.trim() || null,
        proposal: proposal.trim() || null,
        knownObjections: knownObjections.trim() || null,
        stakes,
        userLocale, userCurrency, userRegion,
      });
      setResults(data);
      setSessionHistory(prev => [{ id: Date.now(), date: new Date().toISOString(), preview: '' }, ...prev].slice(0, 6));
    } catch (err) { setError(err.message || t('hp_error')); }
  }, [topic, audience, proposal, knownObjections, stakes, callToolEndpoint, setResults, setSessionHistory, userLocale, userCurrency, userRegion, t]);

  const loadExample = useCallback(() => {
    setTopic(EXAMPLE.topic);
    setAudience(EXAMPLE.audience);
    setProposal(EXAMPLE.proposal);
    setKnownObjections(EXAMPLE.knownObjections);
    setStakes(EXAMPLE.stakes);
    setResults(null);
  }, [setTopic, setAudience, setProposal, setKnownObjections, setStakes, setResults, EXAMPLE.topic, EXAMPLE.audience, EXAMPLE.proposal, EXAMPLE.knownObjections, EXAMPLE.stakes]);

  const handleReset = useCallback(() => {
    setTopic(''); setAudience(''); setProposal('');
    setKnownObjections(''); setStakes('moderate');
    setResults(null); setError(''); setExpandedQ(null);
  }, []);

  const buildFullText = useCallback(() => {
    if (!results) return '';
    const r = results;
    let text = `🎤 ${t('hp_title')}: ${topic}\n`;
    if (r.situation_read) text += `\n${r.situation_read}\n`;
    if (r.opening_move) text += `\n${t('hp_copy_opening')} ${r.opening_move}\n`;
    if (r.questions?.length) {
      r.questions.forEach(q => {
        text += `\n#${q.number} [${q.difficulty}] ${q.question}\n${t('hp_copy_concern')} ${q.real_concern}\n${t('hp_copy_answer')} ${q.model_answer}\n`;
      });
    }
    if (r.the_curveball) text += `\n${t('hp_copy_curveball')} ${r.the_curveball.question}\n${t('hp_copy_handle')} ${r.the_curveball.how_to_handle}`;
    return text + BRAND;
  }, [results, topic, t]);

  useRegisterActions(buildFullText(), tool?.title || 'HecklerPrep');

  const generateRef = useRef(null);
  const canSubmitRef = useRef(false);
  generateRef.current = generate;
  canSubmitRef.current = !!topic.trim();

  useEffect(() => {
    if (!results || !resultsRef.current) return;
    const t = setTimeout(() => resultsRef.current?.scrollIntoView({ behavior: 'smooth', block: 'start' }), 200);
    return () => clearTimeout(t);
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [results]);

  useEffect(() => {
    const handler = (e) => {
      const tag = document.activeElement?.tagName;
      if (tag === 'SELECT') return;
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && !loading && canSubmitRef.current)
        generateRef.current?.();
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [loading]);

  const difficultyBadge = (d) => d === 'brutal' ? c.danger : d === 'hard' ? c.warning : c.success;
  const difficultyEmoji = (d) => d === 'brutal' ? '🔴' : d === 'hard' ? '🟡' : '🟢';

  const r = results;

  return (
    <div className={`space-y-4 ${c.text}`}>
      <div className={`${c.card} border ${c.border} rounded-xl p-6`}>
        <div className="mb-5 pb-4 border-b border-zinc-500 flex items-start justify-between gap-3">
          <div>
            <h2 className={`text-xl font-bold ${c.text}`}>
              <span className="mr-2">{tool?.icon ?? '🎤'}</span>{tool?.title ?? t('hp_title')}
            </h2>
            <p className={`text-sm ${c.textSecondary}`}>{tool?.tagline ?? t('hp_tagline')}</p>
            <button onClick={loadExample} disabled={loading} style={{ backgroundColor: (tool?.headerColor ?? '#888888') + '80' }} className={`mt-1.5 px-2.5 py-0.5 rounded-full text-xs font-medium border disabled:opacity-40 ${isDark ? 'text-white border-white/40' : 'text-gray-800 border-transparent'}`}>{t('try_example')}</button>
          </div>
          {(results || topic.trim()) && (
            <button onClick={handleReset} className={`shrink-0 px-3 py-1.5 rounded-lg text-xs font-medium border transition-colors ${c.btnSecondary}`}>↩ {t('start_over')}</button>
          )}
        </div>

        <div className="mb-4">
          <p className={`text-xs ${c.textMuted} mb-2`}>
            {t('hp_coldopen_q')} <a href="/ColdOpenCraft" className={linkStyle}>📬 {t('hp_coldopen_name')}</a> {t('hp_coldopen_rest')}
          </p>
          <label className={`text-sm font-bold ${c.text} block mb-1.5`}>{t('hp_topic_label')} <span className={c.required}>*</span></label>
          <textarea value={topic} onChange={e => setTopic(e.target.value)} onKeyDown={e => { if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && topic.trim()) generate(); }} rows={2}
            placeholder={t('hp_topic_ph')}
            className={`w-full px-4 py-3 border rounded-xl text-sm ${c.input} ${c.text} outline-none focus:ring-2 resize-none`} />
        </div>

        <div className="mb-4">
          <label className={`text-sm font-bold ${c.text} block mb-1.5`}>{t('hp_audience_label')}</label>
          <input type="text" value={audience} onChange={e => setAudience(e.target.value)} onKeyDown={e => { if (e.key === 'Enter' && topic.trim()) generate(); }}
            placeholder={t('hp_audience_ph')}
            className={`w-full px-4 py-3 border rounded-xl text-sm ${c.input} ${c.text} outline-none focus:ring-2`} />
        </div>

        <div className="mb-4">
          <label className={`text-sm font-bold ${c.text} block mb-1.5`}>{t('hp_proposal_label')} <span className={`font-normal ${c.textMuteded}`}>({t('optional')})</span></label>
          <input type="text" value={proposal} onChange={e => setProposal(e.target.value)} onKeyDown={e => { if (e.key === 'Enter' && topic.trim()) generate(); }}
            placeholder={t('hp_proposal_ph', { sym })}
            className={`w-full px-4 py-3 border rounded-xl text-sm ${c.input} ${c.text} outline-none focus:ring-2`} />
        </div>

        <div className="mb-4">
          <label className={`text-sm font-bold ${c.text} block mb-1.5`}>{t('hp_objections_label')} <span className={`font-normal ${c.textMuteded}`}>({t('optional')})</span></label>
          <input type="text" value={knownObjections} onChange={e => setKnownObjections(e.target.value)} onKeyDown={e => { if (e.key === 'Enter' && topic.trim()) generate(); }}
            placeholder={t('hp_objections_ph')}
            className={`w-full px-4 py-3 border rounded-xl text-sm ${c.input} ${c.text} outline-none focus:ring-2`} />
        </div>

        <div className="mb-5">
          <label className={`text-sm font-bold ${c.text} block mb-2`}>{t('hp_stakes_label')}</label>
          <div className="flex gap-2">
          {STAKES_LEVELS.map(s => (
            <button key={s.value} onClick={() => setStakes(s.value)}
              className={`flex-1 py-2.5 rounded-xl text-[11px] font-bold border transition-colors min-h-[44px] flex flex-col items-center gap-0.5 ${
                stakes === s.value ? c.pillActive : c.pillInactive
              }`}>
              <span className="text-base">{s.emoji}</span>
              <span>{s.label}</span>
              <span className={`font-normal text-[9px] ${stakes === s.value ? 'opacity-80' : c.textMuteded}`}>{s.desc}</span>
            </button>
          ))}
          </div>
          {stakes === 'high' && (
            <p className={`text-[11px] mt-2 ${c.textMuteded}`}>{t('hp_high_note')}</p>
          )}
        </div>

        <button onClick={generate} disabled={loading || !topic.trim()}
          className={`w-full ${c.btnPrimary} disabled:opacity-40 disabled:cursor-not-allowed font-bold py-3 px-6 rounded-xl flex items-center justify-center gap-2 min-h-[48px] shadow-lg`}>
          {loading ? <><span className="animate-spin inline-block">{tool?.icon ?? '🎤'}</span> {t('hp_generating')}</> : <><span className="mr-1">{tool?.icon ?? '🎤'}</span> {t('hp_prep_me')}</>}
        </button>

        {!results && (
          <div className={`mt-5 pt-4 border-t ${c.border}`}>
            <p className={`text-xs ${c.textMuteded}`}>
              {t('hp_velvet_q')} <a href="/VelvetHammer" className={linkStyle}>{t('hp_velvet_name')}</a> {t('hp_velvet_rest')}
            </p>
          </div>
        )}
      </div>

      {error && (
        <div className={`${c.danger} border rounded-xl p-4 flex items-start gap-3`}>
          <span className="flex-shrink-0 mt-0.5">⚠️</span><p className="text-sm">{error}</p>
        </div>
      )}

      {results && (
        <div ref={resultsRef} className="space-y-4">
{r.situation_read && (
            <div className={`${c.card} border ${c.border} rounded-xl p-5`}>
              <p className={`text-sm ${c.textSecondary} leading-relaxed`}>{r.situation_read}</p>
            </div>
          )}

          {/* Opening move */}
          {r.opening_move && (
            <div className={`${c.success} border rounded-xl p-4 flex items-start gap-3`}>
              <span className="flex-shrink-0 mt-0.5">🎯</span>
              <div>
                <p className="text-xs font-bold mb-1">{t('hp_opening_label')}</p>
                <p className="text-sm font-medium leading-relaxed">{r.opening_move}</p>
              </div>
            </div>
          )}

          {/* Questions */}
          {r.questions?.length > 0 && (
            <div className="space-y-3">
              <h3 className={`text-sm font-bold ${c.text}`}>{t('hp_questions_title')}</h3>
              {r.questions.map((q, idx) => {
                const isExpanded = expandedQ === idx;
                return (
                  <div key={idx} className={`${c.card} border ${c.border} rounded-xl overflow-hidden`}>
                    <button onClick={() => setExpandedQ(isExpanded ? null : idx)}
                      className="w-full p-4 text-left">
                      <div className="flex items-start gap-3">
                        <span className={`text-xs font-bold w-6 h-6 rounded-full flex items-center justify-center flex-shrink-0 ${
                          q.difficulty === 'brutal' ? (isDark ? 'bg-red-900/30 text-red-300' : 'bg-red-50 text-red-700') :
                          q.difficulty === 'hard' ? (isDark ? 'bg-amber-900/20 text-amber-300' : 'bg-amber-50 text-amber-700') :
                          (isDark ? 'bg-emerald-900/20 text-emerald-300' : 'bg-emerald-50 text-emerald-700')
                        }`}>{q.number}</span>
                        <div className="flex-1 min-w-0">
                          <p className={`text-sm font-bold ${c.text} leading-snug`}>"{q.question}"</p>
                          <div className="flex gap-1.5 mt-1">
                            <span className={`text-[9px] font-bold px-2 py-0.5 rounded-full border ${difficultyBadge(q.difficulty)}`}>
                              {difficultyEmoji(q.difficulty)} {q.difficulty}
                            </span>
                            <span className={`text-[9px] font-bold px-2 py-0.5 rounded-full ${c.badgeNeutral}`}>{q.type}</span>
                          </div>
                        </div>
                        <span className={`${c.textMuteded} flex-shrink-0`}>{isExpanded ? '▲' : '▼'}</span>
                      </div>
                    </button>

                    {isExpanded && (
                      <div className={`px-4 pb-4 border-t ${c.border} pt-3 space-y-3`}>
                        <div>
                          <p className={`text-[10px] font-bold ${c.textMuteded} mb-1`}>{t('hp_real_concern')}</p>
                          <p className={`text-xs ${c.textSecondary} italic`}>{q.real_concern}</p>
                        </div>
                        <div className={`${c.cardAlt} rounded-lg p-4`}>
                          <p className={`text-[10px] font-bold ${c.textMuteded} mb-1`}>{t('hp_model_answer')}</p>
                          <p className={`text-xs ${c.text} leading-relaxed`}>{q.model_answer}</p>
                        </div>
                        {q.dont_say && (
                          <div className={`${c.danger} border rounded-lg p-3`}>
                            <p className="text-[10px] font-bold mb-0.5">{t('hp_dont_say')}</p>
                            <p className="text-xs">{q.dont_say}</p>
                          </div>
                        )}
                      </div>
                    )}
                  </div>
                );
              })}
            </div>
          )}

          {/* Curveball */}
          {r.the_curveball && (
            <div className={`${c.warning} border rounded-xl p-4 flex items-start gap-3`}>
              <span className="flex-shrink-0 mt-0.5">🎱</span>
              <div>
                <p className="text-xs font-bold mb-1">{t('hp_curveball_label')}</p>
                <p className="text-sm font-bold leading-relaxed mb-2">"{r.the_curveball.question}"</p>
                <p className="text-xs leading-relaxed">{r.the_curveball.how_to_handle}</p>
              </div>
            </div>
          )}

          {/* Confidence note */}
          {r.confidence_note && (
            <div className={`${c.success} border rounded-xl p-4 flex items-start gap-3`}>
              <span className="flex-shrink-0 mt-0.5">💪</span>
              <p className="text-sm font-medium leading-relaxed">{r.confidence_note}</p>
            </div>
          )}

          <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
            <p className={`text-[10px] font-bold ${c.textMuted} uppercase mb-2`}>🔗 {t('hp_related')}</p>
            <div className="flex flex-wrap gap-3">
              <a href="/VelvetHammer" className={`text-xs ${linkStyle}`}>🔨 {t('hp_velvet_name')}</a>
              <a href="/BragSheetBuilder" className={`text-xs ${linkStyle}`}>🏆 {t('hp_brag_name')}</a>
              <a href="/TheRunthrough" className={`text-xs ${linkStyle}`}>🏃 {t('hp_runthrough_name')}</a>
            </div>
          </div>
        </div>
      )}
      {sessionHistory.length > 0 && (
        <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
          <p className={`text-xs font-bold ${c.textMuted} mb-2`}>📋 {t('hp_recent')}</p>
          <div className="space-y-1">
            {sessionHistory.map(s => (
              <div key={s.id} className="flex items-center justify-between">
                <span className={`text-xs ${c.textSecondary} truncate`}>{s.preview || t('hp_session')}</span>
                <span className={`text-xs ${c.textMuted} ml-2`}>{new Date(s.date).toLocaleDateString()}</span>
              </div>
            ))}
          </div>
        </div>
      )}
    </div>
  );
};

HecklerPrep.displayName = 'HecklerPrep';
export default HecklerPrep;
