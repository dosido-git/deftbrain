import React, { useState, useRef, useCallback, useEffect } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { usePersistentState } from '../hooks/usePersistentState';
import { useRegisterActions } from '../components/ActionBarContext';
import { useTranslation } from '../i18n/useTranslation';
import { formatCurrency, currencySymbol } from '../utils/formatLocale';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';


const EXAMPLE = {
  inputMode: 'manual',
  subscriptions: [
    { name: 'Netflix', monthlyCost: '15.49', usage: 'Watch 2-3 times a month, mostly background', category: 'streaming' },
    { name: 'Spotify Premium', monthlyCost: '11.99', usage: 'Daily, on commute and at gym', category: 'music' },
    { name: 'Adobe Creative Cloud', monthlyCost: '54.99', usage: "Photoshop maybe twice a year. Bought it during a project two years ago and never canceled.", category: 'software' },
    { name: 'Peloton App', monthlyCost: '12.99', usage: "Used heavily for 4 months in 2023. Haven't opened it in a year.", category: 'fitness' },
    { name: 'NYT Digital', monthlyCost: '17.00', usage: 'Read maybe twice a week. Mostly the games actually.', category: 'news' },
    { name: 'iCloud 200GB', monthlyCost: '2.99', usage: 'Phone backups, photos. Use it constantly.', category: 'storage' },
  ],
};
const SubscriptionGuiltTrip = ({ tool }) => {
  const { callToolEndpoint, loading, userLocale, userCurrency, userRegion } = useClaudeAPI();
  const { t } = useTranslation();
  const sym = currencySymbol(userLocale, userCurrency);
  const { isDark } = useTheme();
  const fileInputRef = useRef(null);
  const resultsRef = useRef(null);

  // Persistent audit sessionHistory

  // Input mode
  const c = {
    card: isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt: isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    cardHover: isDark ? 'hover:border-zinc-600' : 'hover:border-cyan-300',
    input: isDark
      ? 'bg-zinc-900 border-zinc-700 text-zinc-50 placeholder:text-zinc-500 focus:border-cyan-500 focus:ring-cyan-500/20'
      : 'bg-white border-slate-300 text-slate-900 placeholder:text-slate-400 focus:border-cyan-500 focus:ring-cyan-200',
    text: isDark ? 'text-zinc-50' : 'text-slate-900',
    textSecondary: isDark ? 'text-zinc-400' : 'text-slate-600',
    textMuted: isDark ? 'text-zinc-500' : 'text-slate-500',
    required:      isDark ? 'text-amber-400' : 'text-amber-500',
    labelText: isDark ? 'text-zinc-300' : 'text-slate-700',
    accentTxt: isDark ? 'text-cyan-400' : 'text-cyan-600',
    btnPrimary: isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white' : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary: isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-50' : 'bg-slate-100 hover:bg-slate-200 text-slate-700',
    border: isDark ? 'border-zinc-700' : 'border-gray-200',
    success: isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200' : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning: isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200' : 'bg-amber-50 border-amber-300 text-amber-800',
    danger: isDark ? 'bg-red-900/20 border-red-700 text-red-200' : 'bg-red-50 border-red-200 text-red-800',
    dropzone: isDark
      ? 'border-zinc-600 bg-zinc-800/50 hover:border-cyan-500 hover:bg-zinc-800'
      : 'border-slate-300 bg-slate-50 hover:border-cyan-400 hover:bg-cyan-50',
    dropzoneActive: isDark ? 'border-cyan-400 bg-cyan-900/20' : 'border-cyan-400 bg-cyan-50',
    tabActive: isDark ? 'bg-cyan-600 text-white' : 'bg-cyan-600 text-white',
    tabInactive: isDark
      ? 'bg-zinc-800 text-zinc-400 hover:text-zinc-200'
      : 'bg-white text-slate-500 hover:text-slate-700',
    savingsBox: isDark
      ? 'bg-gradient-to-r from-cyan-900/40 to-emerald-900/40 border-cyan-700'
      : 'bg-gradient-to-r from-cyan-50 to-emerald-50 border-cyan-300',
    cancelCard: isDark ? 'bg-red-900/20 border-red-800' : 'bg-red-50 border-red-200',
    keepCard: isDark ? 'bg-emerald-900/20 border-emerald-800' : 'bg-emerald-50 border-emerald-200',
    permissionBox: isDark ? 'bg-sky-900/20 border-sky-700' : 'bg-sky-50 border-sky-200',
    wasteHigh: isDark ? 'bg-red-900/30 text-red-300 border-red-700' : 'bg-red-100 text-red-800 border-red-300',
    wasteMed: isDark ? 'bg-amber-900/30 text-amber-300 border-amber-700' : 'bg-amber-100 text-amber-800 border-amber-300',
    wasteLow: isDark ? 'bg-emerald-900/30 text-emerald-300 border-emerald-700' : 'bg-emerald-100 text-emerald-800 border-emerald-300',
    scriptBox: isDark ? 'bg-zinc-900 border-red-700' : 'bg-white border-red-300',
    emailBox: isDark ? 'bg-zinc-900 border-cyan-700' : 'bg-white border-cyan-300',
    guiltFree: isDark ? 'bg-emerald-900/30 border-emerald-700' : 'bg-emerald-50 border-emerald-300',
    // Tool-specific keys (extracted from inline isDark ternaries)
    privacyBadge:    isDark ? 'bg-emerald-900/30 text-emerald-300 border border-emerald-700' : 'bg-emerald-50 text-emerald-700 border border-emerald-200',
    subRow:          isDark ? 'bg-zinc-900 border-zinc-700' : 'bg-slate-50 border-slate-200',
    removeBtn:       isDark ? 'bg-zinc-800 border-zinc-600 text-zinc-400 hover:text-red-400 hover:border-red-700' : 'bg-white border-slate-300 text-slate-400 hover:text-red-600 hover:border-red-300',
    btnLoading:      isDark ? 'bg-zinc-700 text-zinc-400' : 'bg-slate-200 text-slate-400',
    verdictCancel:   isDark ? 'bg-red-800 text-red-200' : 'bg-red-200 text-red-800',
    verdictKeep:     isDark ? 'bg-emerald-800 text-emerald-200' : 'bg-emerald-200 text-emerald-800',
    verdictMaybe:    isDark ? 'bg-amber-800 text-amber-200' : 'bg-amber-200 text-amber-800',
    wasteBar:        isDark ? 'bg-zinc-700' : 'bg-slate-200',
    selectedSavings: isDark ? 'bg-cyan-900/30 border border-cyan-700' : 'bg-cyan-50 border border-cyan-200',
    retentionTag:    isDark ? 'bg-zinc-700 text-zinc-300' : 'bg-slate-100 text-slate-600',
    scriptLabel:     isDark ? 'text-red-400' : 'text-red-700',
    guiltFreeText:   isDark ? 'text-emerald-300' : 'text-emerald-800',
    keepBorder:      isDark ? 'border-emerald-500 bg-emerald-900/20' : 'border-emerald-500 bg-emerald-50',
    permissionHeading: isDark ? 'text-sky-300' : 'text-sky-800',
    permissionText:  isDark ? 'text-sky-200' : 'text-sky-700',
    diffHard:        isDark ? 'bg-red-900/40 text-red-300' : 'bg-red-100 text-red-700',
    diffMedium:      isDark ? 'bg-amber-900/40 text-amber-300' : 'bg-amber-100 text-amber-700',
    diffEasy:        isDark ? 'bg-emerald-900/40 text-emerald-300' : 'bg-emerald-100 text-emerald-700',
  };

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';

  const [inputMode, setInputMode] = useState('manual'); // 'manual', 'paste', 'upload'

  // Manual subscriptions
  const [subscriptions, setSubscriptions] = useState([
    { name: '', monthlyCost: '', usage: '', category: '' }
  ]);
  const [auditLog, setAuditLog] = usePersistentState('subscription-audit-log', []);

  const [results, setResults] = usePersistentState('subscription-audit-results', null);

  // Paste mode
  const [pastedText, setPastedText] = useState('');

  // Upload mode
  const [uploadedFileName, setUploadedFileName] = useState('');
  const [parsedTransactions, setParsedTransactions] = useState(null);

  // Results
  const [error, setError] = useState('');
  const [expandedCards, setExpandedCards] = useState({});
  const [selectedForCancel, setSelectedForCancel] = useState({});
  const [showEmails, setShowEmails] = useState({});
  const [isDragging, setIsDragging] = useState(false);

  // ─── Scroll to results ───
  useEffect(() => {
    if (!results) return;
    const t = setTimeout(() => resultsRef.current?.scrollIntoView({ behavior: 'smooth', block: 'start' }), 200);
    return () => clearTimeout(t);
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [results]);


  // ── Manual entry handlers ──
  const addSubscription = () => {
    setSubscriptions([...subscriptions, { name: '', monthlyCost: '', usage: '', category: '' }]);
  };

  const removeSubscription = (index) => {
    setSubscriptions(subscriptions.filter((_, i) => i !== index));
  };

  const updateSubscription = (index, field, value) => {
    const newSubs = [...subscriptions];
    newSubs[index][field] = value;
    setSubscriptions(newSubs);
  };

  // ── File upload handler ──
  const handleFileUpload = useCallback((e) => {
    const file = e.target.files?.[0];
    if (!file) return;

    setError('');
    setUploadedFileName(file.name);

    const isCSV = file.name.toLowerCase().endsWith('.csv') || file.type === 'text/csv';
    const isPDF = file.name.toLowerCase().endsWith('.pdf') || file.type === 'application/pdf';

    if (!isCSV && !isPDF && !file.type.startsWith('text/')) {
      setError(t('sgt_err_filetype'));
      setUploadedFileName('');
      return;
    }

    if (file.size > 10 * 1024 * 1024) {
      setError(t('sgt_err_filesize'));
      setUploadedFileName('');
      return;
    }

    const reader = new FileReader();
    reader.onload = (event) => {
      const content = event.target.result;
      if (isCSV) {
        parseCSV(content);
      } else {
        // For text/PDF-as-text, store raw content
        setPastedText(content);
        setInputMode('paste');
      }
    };
    reader.readAsText(file);
  }, []);

  const parseCSV = (content) => {
    const lines = content.split('\n').filter(l => l.trim());
    if (lines.length < 2) {
      setError(t('sgt_err_csv_empty'));
      return;
    }

    // Try to identify columns
    const header = lines[0].toLowerCase();
    const hasHeader = header.includes('date') || header.includes('description') || header.includes('amount') || header.includes('merchant');
    const dataLines = hasHeader ? lines.slice(1) : lines;

    const transactions = dataLines.map(line => {
      const parts = line.split(',').map(p => p.trim().replace(/^"|"$/g, ''));
      return parts.join(' | ');
    }).filter(t => t.trim());

    if (transactions.length > 0) {
      setParsedTransactions(transactions);
      setPastedText(transactions.join('\n'));
      setInputMode('paste');
    } else {
      setError(t('sgt_err_csv_parse'));
    }
  };

  // ── Drag and drop ──
  const handleDragOver = (e) => { e.preventDefault(); setIsDragging(true); };
  const handleDragLeave = (e) => { e.preventDefault(); setIsDragging(false); };
  const handleDrop = (e) => {
    e.preventDefault();
    setIsDragging(false);
    const file = e.dataTransfer.files[0];
    if (file) {
      const fakeEvent = { target: { files: [file] } };
      handleFileUpload(fakeEvent);
    }
  };

  // ── Analyze ──
  const handleAnalyze = async () => {
    let payload;

    if (inputMode === 'manual') {
      const filledSubs = subscriptions.filter(s => s.name.trim());
      if (filledSubs.length === 0) {
        setError(t('sgt_err_no_subs'));
        return;
      }
      payload = { subscriptions: filledSubs, inputType: 'manual' };
    } else if (inputMode === 'paste') {
      if (!pastedText.trim()) {
        setError(t('sgt_err_no_paste'));
        return;
      }
      payload = { transactionText: pastedText.trim(), inputType: 'text' };
    } else if (inputMode === 'upload') {
      if (!parsedTransactions && !pastedText.trim()) {
        setError(t('sgt_err_no_file'));
        return;
      }
      payload = { transactionText: pastedText.trim() || parsedTransactions?.join('\n'), inputType: 'text' };
    }

    setError('');
    setResults(null);
    setSelectedForCancel({});
    setExpandedCards({});
    setShowEmails({});

    try {
      const data = await callToolEndpoint('subscription-guilt-trip', { ...payload, userLocale, userCurrency, userRegion });
      setResults(data);

      // Log to audit sessionHistory
      setAuditLog(prev => [{
        id: `audit_${Date.now()}`,
        date: new Date().toISOString(),
        subsCount: data.subscriptions_analyzed?.length || 0,
        totalMonthly: data.total_monthly_cost || '?',
        savingsMonthly: data.total_savings_if_cancel_recommended?.monthly || null,
        preview: (subscriptions[0]?.name || t('sgt_audit_fallback')).slice(0, 40),
      }, ...prev].slice(0, 6));

      // Auto-select high-waste subscriptions for cancellation
      if (data.subscriptions_analyzed) {
        const autoSelect = {};
        data.subscriptions_analyzed.forEach((sub) => {
          if (sub.waste_likelihood >= 70 || sub.verdict === 'cancel') {
            autoSelect[sub.name] = true;
          }
        });
        setSelectedForCancel(autoSelect);
      }
    } catch (err) {
      setError(err.message || t('sgt_err_analyze'));
    }
  };

  // ── Build all-scripts copy content ──
  const loadExample = useCallback(() => {
    setInputMode(EXAMPLE.inputMode);
    setSubscriptions(EXAMPLE.subscriptions);
    setResults(null);
  }, [setInputMode, setSubscriptions, setResults]);

  const buildAllScriptsContent = () => {
    if (!results) return '';
    const lines = [];
    const section = (title) => { lines.push(''); lines.push(title); };

    const sv = results.total_savings_if_cancel_recommended;
    if (sv) {
      lines.push(t('sgt_potential_savings'));
      if (sv.monthly) lines.push(`${t('sgt_monthly')}: ${sv.monthly}`);
      if (sv.annual) lines.push(`${t('sgt_annual')}: ${sv.annual}`);
      if (sv.what_you_could_buy_instead) lines.push(`${t('sgt_could_buy')}: ${sv.what_you_could_buy_instead}`);
    }
    if (results.total_monthly_cost || results.annual_cost) {
      lines.push('');
      if (results.total_monthly_cost) lines.push(`${t('sgt_total_monthly')}: ${results.total_monthly_cost}`);
      if (results.annual_cost) lines.push(`${t('sgt_total_annual')}: ${results.annual_cost}`);
    }
    if (results.subscriptions_analyzed?.length) {
      section(t('sgt_breakdown'));
      results.subscriptions_analyzed.forEach(s => {
        lines.push(`- ${s.name} — ${s.monthly_cost}${t('sgt_per_mo')} — ${(s.verdict || '').toUpperCase()}${s.waste_likelihood != null ? ` (${s.waste_likelihood}% ${t('sgt_waste_likelihood')})` : ''}`);
        if (s.cost_per_use) lines.push(`  ${s.cost_per_use}`);
        if (s.reasoning) lines.push(`  ${s.reasoning}`);
      });
    }
    if (results.recommended_cancellations?.length) {
      section(t('sgt_recommended_cancellations'));
      results.recommended_cancellations.forEach(c => {
        lines.push(`- ${c.name}: ${t('sgt_save')} ${c.monthly_savings || ''}${t('sgt_per_mo')}`);
        if (c.how_to_cancel) lines.push(`  ${t('sgt_how_to_cancel')}: ${c.how_to_cancel}`);
        if (c.cancellation_script) lines.push(`  "${c.cancellation_script}"`);
      });
    }
    if (results.keep_these?.length) {
      section(t('sgt_worth_keeping'));
      results.keep_these.forEach(k => lines.push(`- ${k.name}: ${k.why || ''}`));
    }
    if (results.permission_statements?.length) {
      section(t('sgt_permission_to_cancel'));
      results.permission_statements.forEach(p => lines.push(`- ${p}`));
    }
    return lines.join('\n').trim() + BRAND;
  };

  // ─── Register export content ───
  useRegisterActions(results ? buildAllScriptsContent() : '', tool?.title);

  // ─── Keep handleAnalyze ref fresh so keyboard handler never stales ───
  const handleAnalyzeRef = useRef(null);
  handleAnalyzeRef.current = handleAnalyze;

  // ─── Global Cmd/Ctrl+Enter ───
  useEffect(() => {
    const handler = (e) => {
      const tag = document.activeElement?.tagName;
      if (tag === 'SELECT') return;
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && !loading)
        handleAnalyzeRef.current?.();
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
  }, [loading]);

  // ── Toggle helpers ──
  const toggleCard = (name) => {
    setExpandedCards(prev => ({ ...prev, [name]: !prev[name] }));
  };

  const toggleCancelSelect = (name) => {
    setSelectedForCancel(prev => ({ ...prev, [name]: !prev[name] }));
  };

  const toggleEmail = (name) => {
    setShowEmails(prev => ({ ...prev, [name]: !prev[name] }));
  };

  // ── Export as CSV ──
  const exportCSV = () => {
    if (!results?.subscriptions_analyzed) return;
    const headers = t('sgt_csv_headers');
    const rows = results.subscriptions_analyzed.map(s =>
      `"${s.name}","${s.monthly_cost}","${s.annual_cost || ''}","${s.category || ''}","${s.actual_usage || ''}",${s.waste_likelihood || ''}%,"${s.verdict}","${(s.reasoning || '').replace(/"/g, '""')}"`
    );
    const csv = [headers, ...rows].join('\n');
    const blob = new Blob([csv], { type: 'text/csv' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = 'subscription-audit.csv';
    a.click();
    URL.revokeObjectURL(url);
  };

  // ── Waste badge ──
  const getWasteBadge = (likelihood) => {
    const val = parseInt(likelihood) || 0;
    if (val >= 70) return { label: t('sgt_waste_likely_unused'), color: c.wasteHigh, icon: '⚠️' };
    if (val >= 30) return { label: t('sgt_waste_check_usage'), color: c.wasteMed, icon: '👁️' };
    return { label: t('sgt_waste_actively_used'), color: c.wasteLow, icon: '✓' };
  };

  // ── Difficulty badge ──
  const getDifficultyColor = (difficulty) => {
    const d = (difficulty || '').toLowerCase();
    if (d === 'hard') return c.diffHard;
    if (d === 'medium') return c.diffMedium;
    return c.diffEasy;
  };

  // Category icons map
  const categoryIcons = {
    streaming: '📺',
    fitness: '💪',
    software: '💻',
    news: '📰',
    gaming: '🎮',
    food: '🍕',
    music: '🎵',
    cloud: '☁️',
    productivity: '📋',
    other: '📦',
  };

  const getCategoryIcon = (category) => {
    return categoryIcons[(category || '').toLowerCase()] || '📦';
  };

  // ── Compute selected savings ──
  const getSelectedSavings = () => {
    if (!results?.recommended_cancellations) return { monthly: 0, annual: 0 };
    let monthly = 0;
    results.recommended_cancellations.forEach(c => {
      if (selectedForCancel[c.name]) {
        const m = parseFloat((c.monthly_savings || '').replace(/[^0-9.]/g, '')) || 0;
        monthly += m;
      }
    });
    return { monthly: monthly.toFixed(2), annual: (monthly * 12).toFixed(2) };
  };

  return (
    <div className={`space-y-4 ${c.text}`}>

        {/* ── Header ── */}
        <div className="pb-2 mb-2">
          <h2 className={`text-2xl font-bold ${c.text}`}>
            <span className="mr-2">{tool?.icon ?? '💳'}</span>{tool?.title ?? t('sgt_title')}
          </h2>
          <p className={`text-sm ${c.textSecondary} mt-1`}>
            {tool?.tagline ?? t('sgt_tagline')}
          </p>
          <button onClick={loadExample} disabled={loading} style={{ backgroundColor: (tool?.headerColor ?? '#888888') + '80' }} className={`mt-2 px-2.5 py-0.5 rounded-full text-xs font-medium border disabled:opacity-40 ${isDark ? 'text-white border-white/40' : 'text-gray-800 border-transparent'}`}>{t('sgt_try_example')}</button>
        </div>

        {/* ── Privacy Badge ── */}
        <div className="flex justify-center mb-6">
          <div className={`inline-flex items-center gap-2 px-4 py-2 rounded-full text-xs font-medium ${c.privacyBadge}`}>
            <span>🔒</span>
            {t('sgt_privacy_badge')}
          </div>
        </div>

        {/* ── Input Mode Tabs ── */}
        <div className={`${c.card} border ${c.border} rounded-2xl shadow-sm p-6 mb-6`}>
          <div className="flex gap-2 mb-6">
            {[
              { id: 'manual', label: t('sgt_tab_manual'), icon: '➕' },
              { id: 'paste', label: t('sgt_tab_paste'), icon: '📄' },
              { id: 'upload', label: t('sgt_tab_upload'), icon: '📤' },
            ].map(tab => (
              <button
                key={tab.id}
                onClick={() => setInputMode(tab.id)}
                className={`flex-1 flex items-center justify-center gap-2 py-2.5 px-4 rounded-xl text-sm font-semibold transition-all ${inputMode === tab.id ? c.tabActive : c.tabInactive}`}
              >
                <span>{tab.icon}</span>
                <span className="hidden sm:inline">{tab.label}</span>
              </button>
            ))}
          </div>

          {/* ── Manual Entry ── */}
          {inputMode === 'manual' && (
            <>
              <label className={`block text-sm font-semibold ${c.labelText} mb-3`}>
                {t('sgt_list_subs')}
              </label>
              <div className="space-y-3 mb-4">
                {subscriptions.map((sub, index) => {
                  const isLastRow = index === subscriptions.length - 1;
                  const advanceRow = (e) => {
                    if (e.key !== 'Enter') return;
                    e.preventDefault();
                    const row = e.target.closest('[data-sub-row]');
                    const fields = [...row.querySelectorAll('input, select')];
                    const idx = fields.indexOf(e.target);
                    if (idx < fields.length - 1) {
                      fields[idx + 1].focus();
                    } else if (isLastRow) {
                      addSubscription();
                      // Focus first field of new row after state update
                      setTimeout(() => {
                        const rows = document.querySelectorAll('[data-sub-row]');
                        rows[rows.length - 1]?.querySelector('input')?.focus();
                      }, 0);
                    }
                  };
                  return (
                  <div key={index} data-sub-row className={`relative rounded-lg p-3 border ${c.subRow}`}>
                    {subscriptions.length > 1 && (
                      <button
                        type="button"
                        onClick={() => removeSubscription(index)}
                        className={`absolute -top-2 -right-2 w-6 h-6 flex items-center justify-center rounded-full border text-sm leading-none shadow-sm transition-colors ${c.removeBtn}`}
                      >
                        ×
                      </button>
                    )}
                    <div className="grid grid-cols-1 sm:grid-cols-2 lg:grid-cols-4 gap-2">
                      <input
                        type="text"
                        value={sub.name}
                        onChange={(e) => updateSubscription(index, 'name', e.target.value)}
                        onKeyDown={advanceRow}
                        placeholder={t('sgt_ph_service')}
                        className={`px-3 py-2 border rounded-lg outline-none text-sm ${c.input}`}
                      />
                      <input
                        type="text"
                        value={sub.monthlyCost}
                        onChange={(e) => updateSubscription(index, 'monthlyCost', e.target.value)}
                        onKeyDown={advanceRow}
                        placeholder={t('sgt_ph_cost', { sym })}
                        className={`px-3 py-2 border rounded-lg outline-none text-sm ${c.input}`}
                      />
                      <input
                        type="text"
                        value={sub.usage}
                        onChange={(e) => updateSubscription(index, 'usage', e.target.value)}
                        onKeyDown={advanceRow}
                        placeholder={t('sgt_ph_usage')}
                        className={`px-3 py-2 border rounded-lg outline-none text-sm ${c.input}`}
                      />
                      <div className="relative">
                        <select
                          value={sub.category}
                          onChange={(e) => updateSubscription(index, 'category', e.target.value)}
                          onKeyDown={advanceRow}
                          className={`w-full appearance-none px-3 py-2 pr-7 border rounded-lg outline-none text-sm cursor-pointer ${c.input}`}
                        >
                          <option value="">{t('sgt_cat_label')}</option>
                          <option value="streaming">{t('sgt_cat_streaming')}</option>
                          <option value="fitness">{t('sgt_cat_fitness')}</option>
                          <option value="software">{t('sgt_cat_software')}</option>
                          <option value="news">{t('sgt_cat_news')}</option>
                          <option value="gaming">{t('sgt_cat_gaming')}</option>
                          <option value="food">{t('sgt_cat_food')}</option>
                          <option value="music">{t('sgt_cat_music')}</option>
                          <option value="cloud">{t('sgt_cat_cloud')}</option>
                          <option value="productivity">{t('sgt_cat_productivity')}</option>
                          <option value="other">{t('sgt_cat_other')}</option>
                        </select>
                        <span className={`pointer-events-none absolute right-2.5 top-1/2 -translate-y-1/2 text-[8px] ${c.textMuted}`}>▼</span>
                      </div>
                    </div>
                  </div>
                  );
                })}
              </div>
              <button
                onClick={addSubscription}
                className={`w-full mb-4 py-2.5 px-4 rounded-xl transition-colors flex items-center justify-center gap-2 text-sm font-medium ${c.btnSecondary}`}
              >
                <span>➕</span>
                {t('sgt_add_another')}
              </button>
            </>
          )}

          {/* ── Paste Transactions ── */}
          {inputMode === 'paste' && (
            <>
              <label className={`block text-sm font-semibold ${c.labelText} mb-2`}>
                {t('sgt_paste_label')}
              </label>
              <p className={`text-xs ${c.textMuted} mb-3`}>
                {t('sgt_paste_help')}
              </p>
              <textarea
                value={pastedText}
                onChange={(e) => setPastedText(e.target.value)}
                placeholder={t('sgt_paste_ph', { sym })}
                rows={10}
                className={`w-full p-4 border rounded-xl outline-none text-sm font-mono resize-y mb-4 ${c.input}`}
              />
              {parsedTransactions && (
                <div className={`text-xs ${c.textMuted} mb-4`}>
                  ✓ {t('sgt_parsed_rows', { count: parsedTransactions.length, file: uploadedFileName })}
                </div>
              )}
            </>
          )}

          {/* ── Upload CSV ── */}
          {inputMode === 'upload' && (
            <>
              <div
                onDragOver={handleDragOver}
                onDragLeave={handleDragLeave}
                onDrop={handleDrop}
                onClick={() => fileInputRef.current?.click()}
                className={`border-2 border-dashed rounded-2xl p-10 text-center cursor-pointer transition-all mb-4 ${isDragging ? c.dropzoneActive : c.dropzone}`}
              >
                <input
                  ref={fileInputRef}
                  type="file"
                  accept=".csv,.txt,.pdf"
                  onChange={handleFileUpload}
                  className="hidden"
                />
                <span className={`text-4xl block mx-auto mb-3`}>📤</span>
                <p className={`font-semibold ${c.text} mb-1`}>
                  {uploadedFileName || t('sgt_drop_here')}
                </p>
                <p className={`text-xs ${c.textMuted}`}>
                  {t('sgt_upload_hint')}
                </p>
              </div>
              <div className={`flex items-start gap-2 text-xs ${c.textMuted} mb-4`}>
                <span className="mt-0.5 flex-shrink-0">🔒</span>
                <span>{t('sgt_upload_privacy')}</span>
              </div>
            </>
          )}

          {/* ── Analyze Button ── */}
          <button
          onClick={handleAnalyze}
          disabled={loading}
          className={`w-full font-semibold py-3.5 px-6 rounded-xl transition-all flex items-center justify-center gap-2 shadow-sm ${loading ? c.btnLoading : c.btnPrimary} disabled:opacity-40`}
          >
          {loading ? (
            <><span className="inline-block animate-spin">{tool?.icon ?? '💳'}</span> {t('sgt_analyzing')}</>
          ) : (
            <><span className="mr-1">{tool?.icon ?? '💳'}</span> {t('sgt_audit_btn')}</>
          )}
          </button>
          <p className={`text-xs text-center mt-2 ${c.textMuted}`}>{t('sgt_ai_disclaimer')}</p>
          <p className={`text-xs text-center mt-1 ${c.textMuted}`}>
            {t('sgt_subsweep_q')} <a href="/SubSweep" className={linkStyle}>🧹 {t('sgt_subsweep_name')}</a> {t('sgt_subsweep_tail')}
          </p>

          {error && (
            <div className={`mt-4 p-4 rounded-xl flex items-start gap-3 ${c.danger} border`}>
              <span className={`flex-shrink-0 mt-0.5 ${c.danger}`}>⚠️</span>
              <p className={`text-sm ${c.danger}`}>{error}</p>
            </div>
          )}
        </div>

        {/* ══════════════════ RESULTS ══════════════════ */}
        {results && (
          <div ref={resultsRef} className="space-y-6">
            {/* ── Total Savings Banner ── */}
            {results.total_savings_if_cancel_recommended && (
              <div className={`${c.savingsBox} border-2 rounded-2xl shadow-sm p-6`}>
                <div className="flex items-center gap-2 mb-4">
                  <span>📉</span>
                  <h2 className={`text-lg font-bold ${c.text}`}>{t('sgt_potential_savings')}</h2>
                </div>
                <div className="space-y-3">
                  {[
                    { label: t('sgt_monthly'), value: results.total_savings_if_cancel_recommended.monthly },
                    { label: t('sgt_annual'), value: results.total_savings_if_cancel_recommended.annual },
                    { label: t('sgt_could_buy'), value: results.total_savings_if_cancel_recommended.what_you_could_buy_instead },
                  ].filter(row => row.value).map((row, i) => (
                    <div key={i} className="flex flex-col sm:flex-row sm:gap-4">
                      <div className={`text-[11px] uppercase tracking-wider font-bold ${c.textMuted} sm:w-28 sm:shrink-0 sm:pt-0.5 mb-0.5 sm:mb-0`}>{row.label}</div>
                      <div className={`text-sm ${c.text} flex-1`}>{row.value}</div>
                    </div>
                  ))}
                </div>
              </div>
            )}

            {/* ── Summary: short numbers as cards, sentence-insights as rows ── */}
            {(results.total_monthly_cost || results.annual_cost) && (
              <div className="grid grid-cols-2 gap-3">
                {[
                  { label: t('sgt_total_monthly'), value: results.total_monthly_cost, icon: '💵' },
                  { label: t('sgt_total_annual'), value: results.annual_cost, icon: '📊' },
                ].filter(s => s.value).map((stat, idx) => (
                  <div key={idx} className={`${c.card} border ${c.border} rounded-xl p-4 text-center`}>
                    <div className={`flex items-center justify-center gap-1 text-xs uppercase tracking-wider font-bold ${c.textMuted} mb-1`}>
                      <span>{stat.icon}</span> {stat.label}
                    </div>
                    <div className={`text-2xl font-bold ${c.text}`}>{stat.value}</div>
                  </div>
                ))}
              </div>
            )}

            {/* ── Full Subscription Analysis Cards ── */}
            {results.subscriptions_analyzed && results.subscriptions_analyzed.length > 0 && (
              <div className={`${c.card} border ${c.border} rounded-2xl shadow-sm p-6`}>
                <div className="flex items-center justify-between mb-4">
                  <h3 className={`text-lg font-bold ${c.text}`}>📊 {t('sgt_breakdown')}</h3>
                  <button onClick={exportCSV} className={`flex items-center gap-1.5 px-3 py-1.5 rounded-lg text-xs font-medium transition-colors ${c.btnSecondary}`}>
                    ⬇️ {t('sgt_export_csv')}
                  </button>
                </div>
                <div className="grid grid-cols-1 md:grid-cols-2 gap-4">
                  {results.subscriptions_analyzed.map((sub, idx) => {
                    const waste = getWasteBadge(sub.waste_likelihood);
                    const isExpanded = expandedCards[sub.name];
                    return (
                      <div
                        key={idx}
                        className={`border-2 rounded-xl p-4 transition-all cursor-pointer ${c.cardHover} ${sub.verdict === 'cancel' ? c.cancelCard : sub.verdict === 'keep' ? c.keepCard : `${c.card} border ${c.border}`}`}
                        onClick={() => toggleCard(sub.name)}
                      >
                        <div className="flex items-start justify-between mb-2">
                          <div className="flex items-center gap-2">
                            <span className="text-xl">{getCategoryIcon(sub.category)}</span>
                            <div>
                              <h4 className={`font-bold ${c.text}`}>{sub.name}</h4>
                              <div className={`text-xs ${c.textMuted}`}>{sub.category || t('sgt_cat_other')}</div>
                            </div>
                          </div>
                          <div className="flex items-center gap-2">
                            <span className={`inline-flex items-center gap-1 px-2 py-0.5 rounded-full text-xs font-semibold border whitespace-nowrap shrink-0 ${waste.color}`}>
                              {waste.icon} {waste.label}
                            </span>
                            <span className={`text-sm ${c.textMuted}`}>{isExpanded ? '▲' : '▼'}</span>
                          </div>
                        </div>

                        <div className="flex items-center justify-between gap-2 mb-2">
                          <div>
                            <span className={`text-lg font-bold ${c.text}`}>{sub.monthly_cost}</span>
                            <span className={`text-xs ${c.textMuted}`}>{t('sgt_per_mo')}</span>
                          </div>
                          <div className={`px-2 py-0.5 rounded text-xs font-bold uppercase whitespace-nowrap ${sub.verdict === 'cancel' ? c.verdictCancel : sub.verdict === 'keep' ? c.verdictKeep : c.verdictMaybe}`}>
                            {sub.verdict}
                          </div>
                        </div>
                        {sub.cost_per_use && (
                          <p className={`text-xs ${c.textMuted} mb-2`}>{sub.cost_per_use}</p>
                        )}

                        {sub.waste_likelihood !== undefined && (
                          <div className="mb-2">
                            <div className={`h-2 rounded-full overflow-hidden ${c.wasteBar}`}>
                              <div
                                className={`h-full rounded-full transition-all ${sub.waste_likelihood >= 70 ? 'bg-red-500' : sub.waste_likelihood >= 30 ? 'bg-amber-500' : 'bg-emerald-500'}`}
                                style={{ width: `${Math.min(100, sub.waste_likelihood)}%` }}
                              />
                            </div>
                            <div className={`text-xs mt-1 ${c.textMuted}`}>{sub.waste_likelihood}% {t('sgt_waste_likelihood')}</div>
                          </div>
                        )}

                        {isExpanded && (
                          <div className="mt-3 space-y-2" onClick={(e) => e.stopPropagation()}>
                            {sub.actual_usage && (
                              <p className={`text-sm ${c.textSecondary}`}><strong>{t('sgt_usage_label')}</strong> {sub.actual_usage}</p>
                            )}
                            {sub.reasoning && (
                              <p className={`text-sm ${c.textSecondary}`}><strong>{t('sgt_analysis_label')}</strong> {sub.reasoning}</p>
                            )}
                            {sub.alternatives && (
                              <p className={`text-sm ${c.textSecondary}`}><strong>{t('sgt_alternatives_label')}</strong> {sub.alternatives}</p>
                            )}
                            {sub.annual_cost && (
                              <p className={`text-sm ${c.textSecondary}`}><strong>{t('sgt_annual_cost_label')}</strong> {sub.annual_cost}</p>
                            )}
                          </div>
                        )}
                      </div>
                    );
                  })}
                </div>
              </div>
            )}

            {/* ── Recommended Cancellations ── */}
            {results.recommended_cancellations && results.recommended_cancellations.length > 0 && (
              <div className={`${c.card} border ${c.border} rounded-2xl shadow-sm p-6`}>
                <div className="flex items-center justify-between mb-4 flex-wrap gap-3">
                  <h3 className={`text-lg font-bold ${c.text}`}>🚫 {t('sgt_recommended_cancellations')}</h3>
                </div>

                {/* Selected savings tally */}
                {Object.values(selectedForCancel).some(v => v) && (
                  <div className={`mb-4 px-4 py-3 rounded-xl ${c.selectedSavings}`}>
                    <div className={`text-sm font-medium ${c.accentTxt}`}>
                      {t('sgt_selected_savings')} <strong>{formatCurrency(getSelectedSavings().monthly, userLocale, userCurrency)}{t('sgt_per_mo')}</strong> · <strong>{formatCurrency(getSelectedSavings().annual, userLocale, userCurrency)}{t('sgt_per_yr')}</strong>
                    </div>
                  </div>
                )}

                <div className="space-y-4">
                  {results.recommended_cancellations.map((cancel, idx) => (
                    <div key={idx} className={`border-2 rounded-xl p-5 ${c.cancelCard}`}>
                      <div className="flex items-start justify-between mb-3">
                        <div className="flex items-start gap-3">
                          <input
                            type="checkbox"
                            checked={!!selectedForCancel[cancel.name]}
                            onChange={() => toggleCancelSelect(cancel.name)}
                            className="mt-1.5 w-4 h-4 rounded border-slate-300 text-cyan-600 focus:ring-cyan-500"
                          />
                          <div>
                            <h4 className={`text-lg font-bold ${c.text}`}>{cancel.name}</h4>
                            <div className={`text-sm ${c.textSecondary}`}>
                              {t('sgt_save')} {cancel.monthly_savings}{t('sgt_per_mo')} · {cancel.annual_savings}{t('sgt_per_yr')}
                            </div>
                          </div>
                        </div>
                        <span className={`px-3 py-1 rounded-full text-xs font-semibold ${getDifficultyColor(cancel.cancellation_difficulty)}`}>
                          {cancel.cancellation_difficulty} {t('sgt_to_cancel')}
                        </span>
                      </div>

                      {/* How to cancel */}
                      <div className="mb-3">
                        <div className={`text-xs font-semibold uppercase tracking-wider ${c.textMuted} mb-1`}>{t('sgt_how_to_cancel')}</div>
                        <p className={`text-sm ${c.textSecondary}`}>{cancel.how_to_cancel}</p>
                      </div>

                      {/* Retention tactics */}
                      {cancel.retention_tactics_to_expect && cancel.retention_tactics_to_expect.length > 0 && (
                        <div className="mb-3">
                          <div className={`text-xs font-semibold uppercase tracking-wider ${c.textMuted} mb-1`}>{t('sgt_expect_retention')}</div>
                          <div className="flex flex-wrap gap-2">
                            {cancel.retention_tactics_to_expect.map((tactic, tIdx) => (
                              <span key={tIdx} className={`inline-block px-2 py-1 rounded-lg text-xs ${c.retentionTag}`}>
                                ⚠️ {tactic}
                              </span>
                            ))}
                          </div>
                        </div>
                      )}

                      {/* Cancellation script */}
                      {cancel.cancellation_script && (
                        <div className={`border-2 rounded-xl p-4 mb-3 ${c.scriptBox}`}>
                          <div className="flex items-center justify-between mb-2">
                            <div className={`text-xs font-semibold uppercase tracking-wider ${c.scriptLabel}`}>
                              {t('sgt_cancellation_script')}
                            </div>
                          </div>
                          <p className={`text-sm italic ${c.textSecondary}`}>"{cancel.cancellation_script}"</p>
                        </div>
                      )}

                      {/* Cancellation email */}
                      {cancel.cancellation_email && (
                        <div className="mb-3">
                          <button
                            onClick={() => toggleEmail(cancel.name)}
                            className={`flex items-center gap-1.5 text-xs font-medium ${c.accentTxt} mb-2`}
                          >
                            <span>✉️</span>
                            {showEmails[cancel.name] ? t('sgt_hide_email') : t('sgt_show_email')}
                          </button>
                          {showEmails[cancel.name] && (
                            <div className={`border-2 rounded-xl p-4 ${c.emailBox}`}>
                              <div className="flex items-center justify-between mb-2">
                                <div className={`text-xs font-semibold uppercase tracking-wider ${c.accentTxt}`}>{t('sgt_email_template')}</div>
                              </div>
                              <pre className={`text-sm whitespace-pre-wrap font-sans ${c.textSecondary}`}>{cancel.cancellation_email}</pre>
                            </div>
                          )}
                        </div>
                      )}

                      {/* Guilt-free framing */}
                      {cancel.guilt_free_framing && (
                        <div className={`rounded-xl p-3 border ${c.guiltFree}`}>
                          <p className={`text-sm ${c.guiltFreeText}`}>
                            💚 {cancel.guilt_free_framing}
                          </p>
                        </div>
                      )}
                    </div>
                  ))}
                </div>
              </div>
            )}

            {/* ── Keep These ── */}
            {results.keep_these && results.keep_these.length > 0 && (
              <div className={`${c.card} border ${c.border} rounded-2xl shadow-sm p-6`}>
                <h3 className={`text-lg font-bold ${c.guiltFreeText} mb-4`}>✅ {t('sgt_worth_keeping')}</h3>
                <div className="space-y-3">
                  {results.keep_these.map((keep, idx) => (
                    <div key={idx} className={`border-l-4 rounded-r-xl p-4 ${c.keepBorder}`}>
                      <div className={`font-bold ${c.text}`}>{keep.name}</div>
                      <p className={`text-sm mt-1 ${c.textSecondary}`}>{keep.why}</p>
                    </div>
                  ))}
                </div>
              </div>
            )}

            {/* ── Permission Statements ── */}
            {results.permission_statements && results.permission_statements.length > 0 && (
              <div className={`border-2 rounded-2xl p-6 ${c.permissionBox}`}>
                <h3 className={`text-lg font-bold mb-3 ${c.permissionHeading}`}>
                  💙 {t('sgt_permission_to_cancel')}
                </h3>
                <div className="space-y-2">
                  {results.permission_statements.map((statement, idx) => (
                    <p key={idx} className={`text-sm ${c.permissionText}`}>
                      • {statement}
                    </p>
                  ))}
                </div>
              </div>
            )}

            {/* ── Action Bar — Start Over only; CSV export lives atop Subscription Breakdown ── */}
            <div className={`${c.card} border ${c.border} rounded-2xl shadow-sm p-4 flex flex-wrap items-center gap-3 justify-center`}>
              <button
                onClick={() => {
                  setResults(null);
                  setSubscriptions([{ name: '', monthlyCost: '', usage: '', category: '' }]);
                  setPastedText('');
                  setUploadedFileName('');
                  setParsedTransactions(null);
                  setError('');
                }}
                className={`flex items-center gap-1.5 px-4 py-2 rounded-xl text-sm font-medium transition-colors ${c.btnSecondary}`}
              >
                🗑️ {t('sgt_start_over')}
              </button>
            </div>

            {/* ── Audit sessionHistory ── */}
            {auditLog.length > 0 && (
              <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
                <p className={`text-xs font-bold ${c.textMuted} mb-2`}>📋 {t('sgt_recent_audits')}</p>
                <div className="space-y-1">
                  {auditLog.map(entry => (
                    <div key={entry.id} className="flex items-center justify-between">
                      <span className={`text-xs ${c.textSecondary}`}>{entry.preview}</span>
                      <span className={`text-xs ${c.textMuted}`}>{entry.subsCount} {t('sgt_subs')} · {entry.totalMonthly}{t('sgt_per_mo')}</span>
                    </div>
                  ))}
                </div>
              </div>
            )}

            {/* ── Cross-tool links ── */}
            <div className={`${c.card} border ${c.border} rounded-xl p-4 space-y-2`}>
              <p className={`text-xs font-bold ${c.textMuted}`}>{t('sgt_also_helpful')}</p>
              <div className="flex flex-wrap gap-3">
                <a href="/SubSweep" className={`text-xs ${linkStyle}`}>✂️ {t('sgt_subsweep_cross')}</a>
                <a href="/UpsellShield" className={`text-xs ${linkStyle}`}>🛡️ {t('sgt_upsell_cross')}</a>
              </div>
            </div>
          </div>
        )}
    </div>
  );
};

SubscriptionGuiltTrip.displayName = 'SubscriptionGuiltTrip';
export default SubscriptionGuiltTrip;
