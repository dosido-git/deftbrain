import React, { useState, useCallback } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { CopyBtn, ActionBar } from '../components/ActionButtons';

// ════════════════════════════════════════════════════════════
// CONSTANTS
// ════════════════════════════════════════════════════════════
const SOURCE_TYPES = [
  { value: 'texts', label: 'Text Messages', icon: '💬' },
  { value: 'emails', label: 'Emails', icon: '📧' },
  { value: 'social', label: 'Social Posts', icon: '📱' },
  { value: 'dating', label: 'Dating Profile', icon: '💘' },
  { value: 'work-slack', label: 'Work Chat / Slack', icon: '💼' },
  { value: 'other', label: 'Other Writing', icon: '✍️' },
];

// ════════════════════════════════════════════════════════════
// COMPONENT
// ════════════════════════════════════════════════════════════
const WhatsMyVibe = () => {
  const { callToolEndpoint, loading } = useClaudeAPI();
  const { theme } = useTheme();
  const isDark = theme === 'dark';

  const c = {
    card: isDark ? 'bg-[#1a1a2e] border-[#2a2a4a]' : 'bg-white border-[#d4d4d4]',
    input: isDark
      ? 'bg-[#0f0f1e] border-[#2a2a4a] text-[#e8e8e8] placeholder:text-[#555] focus:border-[#c8a951] focus:ring-[#c8a951]/20'
      : 'bg-white border-[#ccc] text-[#1e3a5f] placeholder:text-[#999] focus:border-[#1e3a5f] focus:ring-[#1e3a5f]/20',
    text: isDark ? 'text-[#e8e8e8]' : 'text-[#1e3a5f]',
    textSec: isDark ? 'text-[#a0a0b8]' : 'text-[#4a6a8a]',
    textMuted: isDark ? 'text-[#666680]' : 'text-[#8a8a8a]',
    label: isDark ? 'text-[#c0c0d0]' : 'text-[#2a4a6a]',
    accent: isDark ? 'text-[#c8a951]' : 'text-[#1e3a5f]',
    accentBg: isDark ? 'bg-[#c8a951]/10 border-[#c8a951]/30' : 'bg-[#1e3a5f]/5 border-[#1e3a5f]/20',
    btnPrimary: isDark ? 'bg-[#c8a951] hover:bg-[#d4b85c] text-[#1a1a2e]' : 'bg-[#1e3a5f] hover:bg-[#2a4a6f] text-white',
    btnSec: isDark ? 'bg-[#2a2a4a] hover:bg-[#3a3a5a] text-[#e8e8e8]' : 'bg-[#f0f0f0] hover:bg-[#e0e0e0] text-[#1e3a5f]',
    pillActive: isDark ? 'bg-[#c8a951] border-[#c8a951] text-[#1a1a2e]' : 'bg-[#1e3a5f] border-[#1e3a5f] text-white',
    pillInactive: isDark ? 'bg-[#2a2a4a] border-[#3a3a5a] text-[#c0c0d0] hover:border-[#4a4a6a]' : 'bg-white border-[#d4d4d4] text-[#4a6a8a] hover:border-[#aaa]',
    divider: isDark ? 'border-[#2a2a4a]' : 'border-[#e0e0e0]',
    quoteBg: isDark ? 'bg-[#0f0f1e]/60' : 'bg-[#f8f8f8]',
    vibe: isDark ? 'bg-[#2e2a1a] border-[#c8a951]/30' : 'bg-[#fffbf0] border-[#c8a951]/30',
    insight: isDark ? 'bg-[#1a2e1a] border-[#2a4a2a] text-[#a0e0a0]' : 'bg-[#f0faf0] border-[#c0dcc0] text-[#2a5a2a]',
    danger: isDark ? 'bg-[#3a1a1a] border-[#5a2a2a] text-[#f0a0a0]' : 'bg-[#fef2f2] border-[#e8c0c0] text-[#7a2a2a]',
  };

  // ── State ──
  const [samples, setSamples] = useState('');
  const [sourceType, setSourceType] = useState('texts');
  const [results, setResults] = useState(null);
  const [error, setError] = useState('');

  // ── API ──
  const runVibe = useCallback(async () => {
    if (!samples.trim()) return;
    setError('');
    setResults(null);
    try {
      const data = await callToolEndpoint('whats-my-vibe', {
        samples: samples.trim(),
        sourceType,
      });
      setResults(data);
    } catch (err) {
      setError(err.message || 'Vibe check failed');
    }
  }, [samples, sourceType, callToolEndpoint]);

  // ── Build text ──
  const buildFullText = useCallback(() => {
    if (!results) return '';
    const lines = [`✨ Vibe Check Results`, ''];
    if (results.vibe_title) lines.push(`🏷️ ${results.vibe_title}`);
    if (results.vibe_description) lines.push(results.vibe_description, '');
    if (results.energy) lines.push(`⚡ Energy: ${results.energy}`);
    if (results.sounds_like) lines.push(`🔊 Sounds like: ${results.sounds_like}`);
    if (results.text_back_energy) lines.push(`📱 Text-back energy: ${results.text_back_energy}`, '');
    if (results.quirks?.length) {
      lines.push('QUIRKS:');
      results.quirks.forEach(q => lines.push(`  → ${q}`));
      lines.push('');
    }
    if (results.secret_tell) lines.push(`🔮 Secret tell: ${results.secret_tell}`);
    lines.push('\n— Generated by DeftBrain · deftbrain.com');
    return lines.join('\n');
  }, [results]);

  // ════════════════════════════════════════════════════════════
  // RENDER
  // ════════════════════════════════════════════════════════════
  return (
    <div className={`space-y-4 ${c.text}`}>
      {/* ── Input ── */}
      <div className={`${c.card} border rounded-xl p-5`}>
        <div className={`mb-4 pb-3 border-b ${c.divider}`}>
          <h2 className={`text-xl font-bold ${c.text}`}>
            <span className="mr-2">✨</span>WhatsMyVibe
          </h2>
          <p className={`text-sm ${c.textSec}`}>Paste your writing — find out what vibe you're actually giving off</p>
        </div>

        {/* Source type */}
        <div className="mb-4">
          <p className={`text-[10px] font-bold ${c.label} uppercase mb-2`}>Where's this from?</p>
          <div className="flex flex-wrap gap-1.5">
            {SOURCE_TYPES.map(s => (
              <button
                key={s.value}
                onClick={() => setSourceType(s.value)}
                className={`px-3 py-1.5 rounded-lg text-xs font-bold border transition-colors min-h-[36px] flex items-center gap-1.5 ${
                  sourceType === s.value ? c.pillActive : c.pillInactive
                }`}
              >
                <span>{s.icon}</span> {s.label}
              </button>
            ))}
          </div>
        </div>

        {/* Samples */}
        <div className="mb-5">
          <label className={`text-sm font-bold ${c.label} block mb-1.5`}>Your writing samples</label>
          <textarea
            value={samples}
            onChange={e => setSamples(e.target.value)}
            placeholder={"Paste several texts, emails, tweets, or messages. The more you give me, the better the read.\n\nTip: Copy a bunch of your recent texts or DMs — those show your real voice."}
            rows={8}
            className={`w-full px-3 py-2.5 border rounded-lg text-sm ${c.input} outline-none focus:ring-2 resize-none`}
          />
          <p className={`text-[10px] ${c.textMuted} mt-1`}>
            {samples.length > 0 ? `${samples.split(/\s+/).filter(Boolean).length} words — ${samples.split(/\s+/).filter(Boolean).length < 30 ? 'more is better!' : samples.split(/\s+/).filter(Boolean).length < 80 ? 'good amount' : 'great sample size'}` : 'More text = more accurate vibe read'}
          </p>
        </div>

        <button
          onClick={runVibe}
          disabled={!samples.trim() || loading}
          className={`w-full ${c.btnPrimary} disabled:opacity-40 font-bold py-3 rounded-lg flex items-center justify-center gap-2 min-h-[48px]`}
        >
          {loading
            ? <><span className="animate-spin inline-block">⏳</span> Reading your vibe...</>
            : <><span>✨</span> Check My Vibe</>}
        </button>
      </div>

      {/* ── Error ── */}
      {error && (
        <div className={`${c.danger} border rounded-lg p-4 flex items-start gap-3`}>
          <span className="flex-shrink-0">⚠️</span>
          <p className="text-sm">{error}</p>
        </div>
      )}

      {/* ── Results ── */}
      {results && (
        <div className="space-y-4">
          <div className="flex justify-end">
            <ActionBar content={buildFullText()} title="WhatsMyVibe Results" />
          </div>

          {/* Vibe title */}
          {results.vibe_title && (
            <div className={`${c.vibe} border-2 rounded-xl p-6 text-center`}>
              <span className="text-4xl block mb-3">✨</span>
              <p className={`text-2xl font-black ${c.text} mb-2`}>{results.vibe_title}</p>
              {results.vibe_description && <p className={`text-sm ${c.textSec} max-w-md mx-auto`}>{results.vibe_description}</p>}
            </div>
          )}

          {/* Energy + sounds like */}
          <div className="grid grid-cols-1 sm:grid-cols-2 gap-3">
            {results.energy && (
              <div className={`${c.quoteBg} rounded-xl p-4`}>
                <p className={`text-[10px] font-bold ${c.label} uppercase mb-1`}>⚡ Energy</p>
                <p className={`text-sm font-bold ${c.text}`}>{results.energy}</p>
              </div>
            )}
            {results.sounds_like && (
              <div className={`${c.quoteBg} rounded-xl p-4`}>
                <p className={`text-[10px] font-bold ${c.label} uppercase mb-1`}>🔊 Sounds like</p>
                <p className={`text-sm font-bold ${c.text}`}>{results.sounds_like}</p>
              </div>
            )}
          </div>

          {/* Punctuation + vocabulary */}
          {(results.punctuation_personality || results.vocabulary_read) && (
            <div className={`${c.card} border rounded-xl p-4 space-y-3`}>
              {results.punctuation_personality && (
                <div>
                  <p className={`text-[10px] font-bold ${c.label} uppercase mb-1`}>✏️ Punctuation personality</p>
                  <p className={`text-sm ${c.textSec}`}>{results.punctuation_personality}</p>
                </div>
              )}
              {results.vocabulary_read && (
                <div>
                  <p className={`text-[10px] font-bold ${c.label} uppercase mb-1`}>📚 Vocabulary read</p>
                  <p className={`text-sm ${c.textSec}`}>{results.vocabulary_read}</p>
                </div>
              )}
            </div>
          )}

          {/* Emotional temperature */}
          {results.emotional_temperature && (
            <div className={`${c.card} border rounded-xl p-4`}>
              <h3 className={`text-sm font-bold ${c.text} mb-3`}>🌡️ Emotional Temperature</h3>
              <div className="space-y-2">
                {results.emotional_temperature.surface && (
                  <div className={`${c.quoteBg} rounded-lg p-3`}>
                    <p className={`text-[10px] font-bold ${c.label} mb-0.5`}>On the surface</p>
                    <p className={`text-xs ${c.textSec}`}>{results.emotional_temperature.surface}</p>
                  </div>
                )}
                {results.emotional_temperature.underneath && (
                  <div className={`${c.quoteBg} rounded-lg p-3`}>
                    <p className={`text-[10px] font-bold ${c.label} mb-0.5`}>Underneath</p>
                    <p className={`text-xs ${c.textSec}`}>{results.emotional_temperature.underneath}</p>
                  </div>
                )}
                {results.emotional_temperature.gap_read && (
                  <div className={`${c.accentBg} border rounded-lg p-3`}>
                    <p className={`text-[10px] font-bold ${c.accent} mb-0.5`}>The gap</p>
                    <p className={`text-xs ${c.text}`}>{results.emotional_temperature.gap_read}</p>
                  </div>
                )}
              </div>
            </div>
          )}

          {/* Quirks */}
          {results.quirks?.length > 0 && (
            <div className={`${c.card} border rounded-xl p-4`}>
              <h3 className={`text-sm font-bold ${c.text} mb-3`}>🔍 Your Quirks</h3>
              <div className="space-y-2">
                {results.quirks.map((q, i) => (
                  <p key={i} className={`text-xs ${c.textSec} p-2.5 rounded-lg ${c.quoteBg}`}>→ {q}</p>
                ))}
              </div>
            </div>
          )}

          {/* Text-back energy */}
          {results.text_back_energy && (
            <div className={`${c.quoteBg} rounded-xl p-4`}>
              <p className={`text-[10px] font-bold ${c.label} uppercase mb-1`}>📱 What getting a text from you feels like</p>
              <p className={`text-sm ${c.text}`}>{results.text_back_energy}</p>
            </div>
          )}

          {/* Secret tell */}
          {results.secret_tell && (
            <div className={`${c.insight} border-2 rounded-xl p-5 text-center`}>
              <span className="text-2xl block mb-2">🔮</span>
              <p className={`text-[10px] font-bold uppercase mb-1`}>The thing you don't realize you're broadcasting</p>
              <p className={`text-sm font-bold`}>{results.secret_tell}</p>
            </div>
          )}

          {/* Share line */}
          {results.share_line && (
            <div className={`${c.card} border rounded-xl p-4 flex items-center justify-between gap-3`}>
              <div>
                <p className={`text-[10px] font-bold ${c.label} uppercase mb-1`}>📸 Your vibe in one line</p>
                <p className={`text-sm font-bold ${c.text}`}>{results.share_line}</p>
              </div>
              <CopyBtn content={`My vibe: ${results.share_line}\n\n— Generated by DeftBrain · deftbrain.com`} label="Copy" />
            </div>
          )}

          {/* Go again */}
          <button
            onClick={() => { setResults(null); setSamples(''); }}
            className={`${c.btnSec} px-4 py-2 rounded-lg text-xs font-bold min-h-[36px]`}
          >
            🔄 Check another vibe
          </button>
        </div>
      )}
    </div>
  );
};

WhatsMyVibe.displayName = 'WhatsMyVibe';
export default WhatsMyVibe;
