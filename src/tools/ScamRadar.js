import React, { useState, useEffect, useRef, useCallback } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { usePersistentState } from '../hooks/usePersistentState';
import { useRegisterActions } from '../components/ActionBarContext';
import { useTranslation } from '../i18n/useTranslation';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const MESSAGE_TYPES = [
  { id: 'email',    labelKey: 'scam_type_email',   icon: '📧' },
  { id: 'sms',      labelKey: 'scam_type_sms',     icon: '💬' },
  { id: 'dm',       labelKey: 'scam_type_dm',      icon: '📱' },
  { id: 'phone',    labelKey: 'scam_type_phone',   icon: '📞' },
  { id: 'invoice',  labelKey: 'scam_type_invoice', icon: '📄' },
  { id: 'social',   labelKey: 'scam_type_social',  icon: '🌐' },
  { id: 'other',    labelKey: 'scam_type_other',   icon: '❓' },
];

const EXAMPLE = {
  messageType: 'email',
  messageText: `Subject: URGENT: Your account has been suspended — verify now

Dear Valued Customer,

We have detected unusual activity on your account. Your access has been temporarily suspended for your protection.

To restore full access, you must verify your identity within 24 HOURS or your account will be permanently deleted.

Click here to verify: http://secure-paypa1-verify.com/restore

You will need to provide:
• Full name and date of birth
• Credit card number and CVV
• Social Security Number

This is an automated security alert. Failure to respond will result in permanent account closure and potential legal action.

PayPal Security Team
© PayPal Inc. All Rights Reserved`,
  senderContext: 'From: security@paypa1-alerts.net',
};

const SCAM_TYPE_ICONS = {
  'Phishing':                   '🎣',
  'Smishing':                   '📱',
  'Vishing':                    '📞',
  'Advance Fee Fraud':          '💸',
  'Romance Scam':               '💔',
  'Fake Invoice':               '📄',
  'Prize / Lottery Scam':       '🎰',
  'Authority Impersonation':    '🏛️',
  'Tech Support Scam':          '💻',
  'Job Offer Scam':             '💼',
  'Investment Scam':            '📈',
  'Subscription Trap':          '🔄',
  'Package Delivery Scam':      '📦',
  'Charity Scam':               '🤲',
  'Unknown':                    '⚠️',
};

function ScamRadar({ tool }) {
  const { callToolEndpoint, loading, userLocale, userCurrency, userRegion } = useClaudeAPI();
  const { isDark } = useTheme();
  const { t } = useTranslation();

  const c = {
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:         isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20'
                          : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-500' : 'text-gray-400',
    labelText:     isDark ? 'text-zinc-200' : 'text-gray-700',
    accentTxt:     isDark ? 'text-cyan-400' : 'text-cyan-600',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white'
                          : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200'
                          : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200'
                          : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200'
                          : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200'
                          : 'bg-red-50 border-red-200 text-red-800',
    successTxt:    isDark ? 'text-emerald-300' : 'text-emerald-800',
    warningTxt:    isDark ? 'text-amber-300' : 'text-amber-800',
    pillActive:    isDark ? 'border-cyan-500 bg-cyan-900/30 text-cyan-200'
                          : 'border-cyan-600 bg-cyan-100 text-cyan-900',
    pillInactive:  isDark ? 'border-zinc-600 text-zinc-400 hover:border-zinc-500'
                          : 'border-gray-300 text-gray-500 hover:border-gray-400',
    required:      isDark ? 'text-amber-400' : 'text-amber-500',
  };
  c.textMuteded = c.textMuted;
  c.label = c.labelText;

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';

  const [messageType, setMessageType]   = useState('email');
  const [messageText, setMessageText]   = useState('');
  const [senderContext, setSenderContext] = useState('');
  const [results, setResults]           = usePersistentState('scamradar-results', null);
  const [sessionHistory, setSessionHistory]           = usePersistentState('scamradar-history', []);
  const [error, setError]               = useState('');

  const resultsRef    = useRef(null);
  const handleScanRef = useRef(null);
  const canSubmitRef  = useRef(false);

  const canSubmit = !!messageText.trim();

  const handleReset = useCallback(() => {
    setResults(null);
    setError('');
    setMessageText('');
    setSenderContext('');
    setMessageType('email');
  }, [setResults]);

  const loadExample = useCallback(() => {
    setMessageType(EXAMPLE.messageType);
    setMessageText(EXAMPLE.messageText);
    setSenderContext(EXAMPLE.senderContext);
    setResults(null);
    setError('');
  }, [setResults]);

  const handleScan = useCallback(async () => {
    if (!canSubmit || loading) return;
    setError('');
    setResults(null);

    try {
      const parsed = await callToolEndpoint('scam-radar/stream', {
        messageText,
        messageType,
        senderContext,
        userLocale,
        userCurrency,
        userRegion,
      });
      const entry = { ...parsed, messageType, messageText, senderContext };
      setResults(entry);
      // PF-25 exception: 80 is a message-preview length; history itself is capped at 10 lookups.
      setSessionHistory(prev => [{ verdict: parsed.verdict, scam_type: parsed.scam_type, one_liner: parsed.one_liner, preview: (messageText.slice(0, 80) + (messageText.length > 80 ? '…' : '')), ts: Date.now() }, ...prev].slice(0, 10));
    } catch (err) {
      setError(err.message || t('scam_error'));
    }
  }, [canSubmit, loading, messageText, messageType, senderContext, callToolEndpoint, setResults, setSessionHistory, userLocale, userCurrency, userRegion, t]);

  handleScanRef.current = handleScan;
  canSubmitRef.current  = canSubmit;

  const buildFullText = useCallback(() => {
    if (!results) return '';
    const typeMeta = MESSAGE_TYPES.find(mt => mt.id === results.messageType);
    const lines = [t('scam_copy_header'), ''];
    lines.push(`${t('scam_copy_verdict')} ${results.verdict} (${results.confidence}% ${t('scam_copy_confidence')})`);
    if (results.scam_type) lines.push(`${t('scam_copy_scam_type')} ${results.scam_type}`);
    if (results.one_liner) lines.push(`\n${results.one_liner}`);
    if (results.red_flags?.length) {
      lines.push(`\n${t('scam_copy_red_flags')}`);
      results.red_flags.forEach(f => lines.push(`• ${f}`));
    }
    if (results.techniques_used?.length) {
      lines.push(`\n${t('scam_copy_techniques')}`);
      results.techniques_used.forEach(tech => lines.push(`• ${tech}`));
    }
    if (results.what_to_do?.length) {
      lines.push(`\n${t('scam_copy_what_to_do')}`);
      results.what_to_do.forEach(a => lines.push(`• ${a}`));
    }
    if (results.do_not?.length) {
      lines.push(`\n${t('scam_copy_do_not')}`);
      results.do_not.forEach(a => lines.push(`• ${a}`));
    }
    lines.push(`\n${t('scam_copy_msg_type')} ${typeMeta ? t(typeMeta.labelKey) : results.messageType}`);
    if (results.senderContext) lines.push(`${t('scam_copy_sender')} ${results.senderContext}`);
    lines.push(BRAND);
    return lines.join('\n');
  }, [results, t]);

  useRegisterActions(buildFullText(), tool?.title || 'ScamRadar');

  useEffect(() => {
    if (!results || !resultsRef.current) return;
    const timer = setTimeout(() => resultsRef.current?.scrollIntoView({ behavior: 'smooth', block: 'start' }), 200);
    return () => clearTimeout(timer);
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [results]);

  useEffect(() => {
    const handler = (e) => {
      const tag = document.activeElement?.tagName;
      if (tag === 'SELECT') return;
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && !loading && canSubmitRef.current)
        handleScanRef.current?.();
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [loading]);

  // ── Verdict styling ──
  const verdictConfig = results ? {
    SCAM:         { bg: isDark ? 'bg-red-900/40 border-red-600'     : 'bg-red-50 border-red-400',     txt: isDark ? 'text-red-200'     : 'text-red-800',     badge: 'bg-red-600 text-white',     icon: '🚨' },
    SUSPICIOUS:   { bg: isDark ? 'bg-amber-900/40 border-amber-600' : 'bg-amber-50 border-amber-400', txt: isDark ? 'text-amber-200'   : 'text-amber-800',   badge: 'bg-amber-500 text-white',   icon: '⚠️' },
    'LIKELY SAFE':{ bg: isDark ? 'bg-emerald-900/40 border-emerald-600' : 'bg-emerald-50 border-emerald-400', txt: isDark ? 'text-emerald-200' : 'text-emerald-800', badge: 'bg-emerald-600 text-white', icon: '✅' },
  }[results.verdict] ?? { bg: c.warning, txt: c.warningTxt, badge: 'bg-amber-500 text-white', icon: '⚠️' } : null;

  // ── Render helpers ──
  const renderInput = () => (
    <div className={`${c.card} border ${c.border} rounded-xl shadow-sm p-5 space-y-4`}>
      <div className="pb-3 border-b border-zinc-500">
        <h2 className={`text-xl font-bold ${c.text}`}>
          <span className="mr-2">{tool?.icon ?? '🎣'}</span>{tool?.title ?? t('scam_title')}
        </h2>
        <p className={`text-sm ${c.textSecondary}`}>{tool?.tagline ?? t('scam_tagline')}</p>
        <button onClick={loadExample} disabled={loading} style={{ backgroundColor: (tool?.headerColor ?? '#888888') + '80' }} className={`mt-1.5 px-2.5 py-0.5 rounded-full text-xs font-medium border disabled:opacity-40 ${isDark ? 'text-white border-white/40' : 'text-gray-800 border-transparent'}`}>{t('scam_try_example')}</button>
      </div>

      {/* Message type */}
      <div>
        <label className={`block text-sm font-medium ${c.labelText} mb-2`}>{t('scam_msg_type')}</label>
        <div className="flex flex-wrap gap-2">
          {MESSAGE_TYPES.map(mt => (
            <button
              key={mt.id}
              onClick={() => setMessageType(mt.id)}
              className={`px-3 py-1.5 rounded-lg border text-xs font-medium transition-colors ${messageType === mt.id ? c.pillActive : c.pillInactive}`}
            >
              <span className="mr-1">{mt.icon}</span>{t(mt.labelKey)}
            </button>
          ))}
        </div>
      </div>

      {/* Message text */}
      <div>
        <label className={`block text-sm font-medium ${c.labelText} mb-1`}>
          {t('scam_msg_content')} <span className={c.required}>*</span>
        </label>
        <textarea
          value={messageText}
          onChange={e => setMessageText(e.target.value)}
          placeholder={t('scam_msg_ph')}
          rows={8}
          className={`w-full px-3 py-2.5 border rounded-xl text-sm resize-y focus:outline-none focus:ring-2 ${c.input}`}
        />
      </div>

      {/* Sender context */}
      <div>
        <label className={`block text-sm font-medium ${c.labelText} mb-1`}>{t('scam_sender_label')} <span className={`text-xs font-normal ${c.textMuted}`}>{t('scam_sender_optional')}</span></label>
        <input
          type="text"
          value={senderContext}
          onChange={e => setSenderContext(e.target.value)}
          placeholder={t('scam_sender_ph')}
          className={`w-full px-3 py-2 border rounded-xl text-sm focus:outline-none focus:ring-2 ${c.input}`}
        />
      </div>

      {error && <p className={`text-sm ${c.danger} border rounded-lg px-3 py-2`}>{error}</p>}

      {/* Pre-result cross-ref */}
      <p className={`text-xs ${c.textMuted}`}>
        {t('scam_xref_pre')} <a href="/FakeReviewDetective" className={linkStyle}>🕵️ {t('scam_fakereview')}</a>.
      </p>

      <button
        onClick={handleScan}
        disabled={!canSubmit || loading}
        className={`w-full ${c.btnPrimary} disabled:opacity-40 font-bold py-3 rounded-lg flex items-center justify-center gap-2 min-h-[48px]`}
      >
        {loading
          ? <><span className="inline-block animate-spin">{tool?.icon ?? '🎣'}</span> {t('scam_scanning')}</>
          : <><span className="mr-1">{tool?.icon ?? '🎣'}</span> {t('scam_scan_btn')}</>}
      </button>

      {/* History panel */}
      {sessionHistory.length > 0 && (
        <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
          <div className="flex items-center justify-between mb-3">
            <p className={`text-xs font-bold ${c.textMuted} uppercase tracking-wide`}>🕓 {t('scam_recent')}</p>
            <button onClick={() => setSessionHistory([])} className={`text-xs ${c.textMuted} hover:${c.accentTxt}`}>{t('scam_clear')}</button>
          </div>
          <ul className="space-y-2">
            {sessionHistory.map((h, i) => {
              const vIcon = h.verdict === 'SCAM' ? '🚨' : h.verdict === 'SUSPICIOUS' ? '⚠️' : '✅';
              const vColor = h.verdict === 'SCAM'
                ? (isDark ? 'text-red-400' : 'text-red-600')
                : h.verdict === 'SUSPICIOUS'
                  ? (isDark ? 'text-amber-400' : 'text-amber-600')
                  : (isDark ? 'text-emerald-400' : 'text-emerald-600');
              return (
                <li key={i} className={`text-xs ${c.textSecondary} flex items-start gap-2`}>
                  <span className="flex-shrink-0">{vIcon}</span>
                  <span>
                    <span className={`font-bold ${vColor}`}>{h.verdict}</span>
                    {h.scam_type ? <span className={c.textMuted}> · {h.scam_type}</span> : null}
                    <span className={`block ${c.textMuted} mt-0.5`}>{h.preview}</span>
                  </span>
                </li>
              );
            })}
          </ul>
        </div>
      )}
    </div>
  );

  const renderResults = () => {
    if (!results || !verdictConfig) return null;
    const scamIcon = SCAM_TYPE_ICONS[results.scam_type] ?? '⚠️';

    return (
      <div className="space-y-4" ref={resultsRef}>

        {/* Verdict banner */}
        <div className={`border-2 rounded-xl p-5 ${verdictConfig.bg}`}>
          <div className="flex items-start justify-between gap-4">
            <div className="flex-1">
              <div className="flex items-center gap-3 mb-2">
                <span className="text-3xl">{verdictConfig.icon}</span>
                <div>
                  <span className={`text-2xl font-black tracking-tight ${verdictConfig.txt}`}>{results.verdict}</span>
                  <span className={`ml-3 text-sm font-bold ${verdictConfig.txt} opacity-70`}>{t('scam_confidence', { count: results.confidence })}</span>
                </div>
              </div>
              {results.one_liner && <p className={`text-sm font-medium ${verdictConfig.txt}`}>{results.one_liner}</p>}
            </div>
            {results.scam_type && (
              <div className="text-center flex-shrink-0">
                <div className="text-2xl mb-1">{scamIcon}</div>
                <span className={`text-[10px] font-bold uppercase tracking-wide ${verdictConfig.txt} opacity-80`}>{results.scam_type}</span>
              </div>
            )}
          </div>
        </div>

        {/* Safety disclaimer — automated guidance, not financial/legal advice */}
        <p className={`text-[11px] ${c.textMuted} px-1`}>ⓘ {t('scam_disclaimer')}</p>

        {/* Scam type explanation */}
        {results.scam_type_description && (
          <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
            <p className={`text-xs font-bold ${c.textMuted} uppercase tracking-wide mb-1`}>{scamIcon} {t('scam_what_is', { type: results.scam_type })}</p>
            <p className={`text-sm ${c.textSecondary}`}>{results.scam_type_description}</p>
          </div>
        )}

        {/* Red flags + Techniques side by side */}
        <div className="grid grid-cols-1 sm:grid-cols-2 gap-4">
          {results?.red_flags?.length > 0 && (
            <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
              <p className={`text-xs font-bold ${c.textMuted} uppercase tracking-wide mb-3`}>🚩 {t('scam_red_flags')}</p>
              <ul className="space-y-2">
                {results.red_flags.map((f, i) => (
                  <li key={i} className={`text-sm ${c.danger} border rounded-lg px-3 py-2`}>• {f}</li>
                ))}
              </ul>
            </div>
          )}

          {results?.techniques_used?.length > 0 && (
            <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
              <p className={`text-xs font-bold ${c.textMuted} uppercase tracking-wide mb-3`}>🧠 {t('scam_techniques')}</p>
              <ul className="space-y-2">
                {results.techniques_used.map((tech, i) => (
                  <li key={i} className={`text-sm ${c.warning} border rounded-lg px-3 py-2`}>• {tech}</li>
                ))}
              </ul>
            </div>
          )}
        </div>

        {/* Green flags if any */}
        {results?.green_flags?.length > 0 && (
          <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
            <p className={`text-xs font-bold ${c.textMuted} uppercase tracking-wide mb-3`}>✅ {t('scam_green_flags')}</p>
            <ul className="space-y-2">
              {results.green_flags.map((f, i) => (
                <li key={i} className={`text-sm ${c.success} border rounded-lg px-3 py-2`}>• {f}</li>
              ))}
            </ul>
          </div>
        )}

        {/* What to do + Do not */}
        <div className="grid grid-cols-1 sm:grid-cols-2 gap-4">
          {results?.what_to_do?.length > 0 && (
            <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
              <p className={`text-xs font-bold ${c.textMuted} uppercase tracking-wide mb-3`}>✅ {t('scam_what_to_do')}</p>
              <ul className="space-y-2">
                {results.what_to_do.map((a, i) => (
                  <li key={i} className={`text-sm ${c.textSecondary}`}>
                    <span className={`font-bold ${c.successTxt}`}>{i + 1}.</span> {a}
                  </li>
                ))}
              </ul>
            </div>
          )}

          {results?.do_not?.length > 0 && (
            <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
              <p className={`text-xs font-bold ${c.textMuted} uppercase tracking-wide mb-3`}>🚫 {t('scam_do_not')}</p>
              <ul className="space-y-2">
                {results.do_not.map((a, i) => (
                  <li key={i} className={`text-sm ${c.textSecondary}`}>
                    <span className={`font-bold ${isDark ? 'text-red-400' : 'text-red-600'}`}>✗</span> {a}
                  </li>
                ))}
              </ul>
            </div>
          )}
        </div>

        {/* Post-result cross-refs */}
        <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
          <p className={`text-[10px] font-bold ${c.textMuted} uppercase mb-2`}>🔗 {t('scam_related')}</p>
          <div className="flex flex-wrap gap-3">
            <a href="/ComplaintEscalationWriter" className={`text-xs ${linkStyle}`}>📨 {t('scam_complaint')}</a>
            <a href="/MagicMouth" className={`text-xs ${linkStyle}`}>🪄 {t('scam_magicmouth')}</a>
            <a href="/FakeReviewDetective" className={`text-xs ${linkStyle}`}>🕵️ {t('scam_fakereview')}</a>
          </div>
        </div>
      </div>
    );
  };

  return (
    <div className={`space-y-4 ${c.text}`}>
      {!results && <p className={`text-xs ${c.textMuted} px-1`}>{t('scam_top_hint')} <a href="/FakeReviewDetective" className={linkStyle}>🕵️ {t('scam_fakereview')}</a>.</p>}
      {!results && renderInput()}
      {results ? (
        <div className={`${c.card} border ${c.border} rounded-xl shadow-sm p-5`}>
          <div className="pb-3 border-b border-zinc-500">
            <div className="flex items-start justify-between">
              <div>
                <h2 className={`text-xl font-bold ${c.text}`}>
                  <span className="mr-2">{tool?.icon ?? '🎣'}</span>{tool?.title ?? t('scam_title')}
                </h2>
                <p className={`text-sm ${c.textSecondary}`}>{tool?.tagline ?? t('scam_tagline')}</p>
              </div>
              <button onClick={handleReset} className={`${c.btnSecondary} px-3 py-1.5 rounded-lg text-xs font-bold`}>
                ↺ {t('scam_scan_another')}
              </button>
            </div>
          </div>
        </div>
      ) : null}
      {results && renderResults()}
    </div>
  );
}

ScamRadar.displayName = 'ScamRadar';
export default ScamRadar;
