import React, { useState, useEffect, useRef, useCallback } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { usePersistentState } from '../hooks/usePersistentState';
import { useRegisterActions } from '../components/ActionBarContext';
import { useTranslation } from '../i18n/useTranslation';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const SITUATION_AREAS = [
  { id: 'anxiety',    labelKey: 'mhn_area_anxiety',      icon: '😰' },
  { id: 'mood',       labelKey: 'mhn_area_mood',         icon: '😔' },
  { id: 'stress',     labelKey: 'mhn_area_stress',       icon: '😤' },
  { id: 'trauma',     labelKey: 'mhn_area_trauma',       icon: '🫂' },
  { id: 'grief',      labelKey: 'mhn_area_grief',        icon: '💙' },
  { id: 'addiction',  labelKey: 'mhn_area_addiction',    icon: '🔄' },
  { id: 'eating',     labelKey: 'mhn_area_eating',       icon: '🍽️' },
  { id: 'sleep',      labelKey: 'mhn_area_sleep',        icon: '😴' },
  { id: 'work',       labelKey: 'mhn_area_work',         icon: '💼' },
  { id: 'relationship', labelKey: 'mhn_area_relationship', icon: '❤️' },
  { id: 'identity',   labelKey: 'mhn_area_identity',     icon: '🧭' },
  { id: 'parenting',  labelKey: 'mhn_area_parenting',    icon: '👨‍👩‍👧' },
  { id: 'medication', labelKey: 'mhn_area_medication',   icon: '💊' },
  { id: 'general',    labelKey: 'mhn_area_general',      icon: '❓' },
];

const TRIED_BEFORE = [
  { id: 'nothing',   labelKey: 'mhn_tried_nothing' },
  { id: 'therapy',   labelKey: 'mhn_tried_therapy' },
  { id: 'gp',        labelKey: 'mhn_tried_gp' },
  { id: 'medication',labelKey: 'mhn_tried_medication' },
  { id: 'self_help', labelKey: 'mhn_tried_self_help' },
  { id: 'crisis',    labelKey: 'mhn_tried_crisis' },
];

const BARRIERS = [
  { id: 'cost',       labelKey: 'mhn_barrier_cost' },
  { id: 'waitlists',  labelKey: 'mhn_barrier_waitlists' },
  { id: 'stigma',     labelKey: 'mhn_barrier_stigma' },
  { id: 'access',     labelKey: 'mhn_barrier_access' },
  { id: 'language',   labelKey: 'mhn_barrier_language' },
  { id: 'unsure',     labelKey: 'mhn_barrier_unsure' },
  { id: 'none',       labelKey: 'mhn_barrier_none' },
];

const EXAMPLE = {
  situationAreas: ['anxiety', 'work', 'sleep'],
  freeform: "I've been having really bad anxiety for the past 6 months, mostly around work. I can't sleep, I overthink everything, and I've started avoiding situations that make me nervous. I'm not sure if I need a therapist or a doctor. I tried talking to my GP once but they just told me to exercise more.",
  triedBefore: ['gp', 'self_help'],
  barriers: ['cost', 'unsure'],
  country: 'United States',
};

function MentalHealthNavigator({ tool }) {
  const { callToolEndpoint, loading, userLocale, userCurrency, userRegion } = useClaudeAPI();
  const { isDark } = useTheme();
  const { t } = useTranslation();

  const c = {
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:         isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20'
                          : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-400' : 'text-gray-500',
    labelText:     isDark ? 'text-zinc-200' : 'text-gray-700',
    accentTxt:     isDark ? 'text-cyan-400' : 'text-cyan-600',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white'
                          : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200'
                          : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    pillActive:    isDark ? 'border-cyan-500 bg-cyan-900/30 text-cyan-200'
                          : 'border-cyan-600 bg-cyan-100 text-cyan-900',
    pillInactive:  isDark ? 'border-zinc-600 text-zinc-400 hover:border-zinc-500'
                          : 'border-gray-300 text-gray-500 hover:border-gray-400',
    required:      isDark ? 'text-amber-400' : 'text-amber-700',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200'   : 'bg-red-50 border-red-200 text-red-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200' : 'bg-amber-50 border-amber-300 text-amber-800',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200' : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    infoBox:       isDark ? 'bg-sky-900/20 border-sky-700 text-sky-200'   : 'bg-sky-50 border-sky-200 text-sky-800',
    warmBox:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200'  : 'bg-amber-50 border-amber-200 text-amber-800',
  };
  c.textMuteded = c.textMuted;
  c.label = c.labelText;

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-700 hover:text-cyan-800 underline underline-offset-2';

  const [situationAreas, setSituationAreas] = useState([]);
  const [freeform, setFreeform]             = useState('');
  const [triedBefore, setTriedBefore]       = useState([]);
  const [barriers, setBarriers]             = useState([]);
  const [country, setCountry]               = useState('');
  const [results, setResults]               = usePersistentState('mentalhealthnav-results', null);
  const [sessionHistory, setSessionHistory]               = usePersistentState('mentalhealthnav-history', []);
  const [error, setError]                   = useState('');

  const resultsRef     = useRef(null);
  const handleFindRef  = useRef(null);
  const canSubmitRef   = useRef(false);

  const canSubmit = situationAreas.length > 0 || freeform.trim().length >= 20;

  const handleReset = useCallback(() => {
    setResults(null);
    setError('');
    setSituationAreas([]);
    setFreeform('');
    setTriedBefore([]);
    setBarriers([]);
    setCountry('');
  }, [setResults]);

  const loadExample = useCallback(() => {
    setSituationAreas(EXAMPLE.situationAreas);
    setFreeform(EXAMPLE.freeform);
    setTriedBefore(EXAMPLE.triedBefore);
    setBarriers(EXAMPLE.barriers);
    setCountry(EXAMPLE.country);
    setResults(null);
    setError('');
  }, [setResults]);

  const toggle = useCallback((setter) => (id) =>
    setter(prev => prev.includes(id) ? prev.filter(x => x !== id) : [...prev, id]), []);

  const handleFind = useCallback(async () => {
    if (!canSubmit || loading) return;
    setError('');
    setResults(null);

    try {
      const parsed = await callToolEndpoint('mental-health-navigator/stream', {
        situationAreas,
        freeform: freeform.trim() || null,
        triedBefore,
        barriers,
        country: country.trim() || null,
        userLocale,
        userCurrency,
        userRegion,
      });
      setResults({ ...parsed, _input: { freeform: freeform.trim(), situationAreas } });
      const previewText = situationAreas.map(id => { const a = SITUATION_AREAS.find(s => s.id === id); return a ? t(a.labelKey) : null; }).filter(Boolean).join(', ') || t('mhn_nav_completed');
      // PF-25 exception: 50 is a preview-text length, not a history cap (history caps at 5 below).
      setSessionHistory(prev => [{
        preview: previewText.slice(0, 50),
        ts: Date.now(),
      }, ...prev].slice(0, 5));
    } catch (err) {
      setError(err.message || t('mhn_error'));
    }
  }, [canSubmit, loading, situationAreas, freeform, triedBefore, barriers, country, callToolEndpoint, setResults, setSessionHistory, userLocale, userCurrency, userRegion, t]);

  handleFindRef.current = handleFind;
  canSubmitRef.current  = canSubmit;

  const buildFullText = useCallback(() => {
    if (!results) return '';
    const lines = [t('mhn_copy_header'), ''];
    if (results?.what_you_described) lines.push(results?.what_you_described, '');
    if (results?.recommended_support?.length) {
      lines.push(t('mhn_copy_recommended'));
      results?.recommended_support.forEach(s => {
        lines.push(`\n${s.type_name}`);
        lines.push(s.why);
        if (s.what_to_expect) lines.push(`${t('mhn_copy_expect')} ${s.what_to_expect}`);
        if (s.how_to_find) lines.push(`${t('mhn_copy_find')} ${s.how_to_find}`);
      });
    }
    if (results?.what_to_say?.length) {
      lines.push(`\n${t('mhn_copy_whatsay')}`);
      results?.what_to_say.forEach(s => lines.push(`• ${s}`));
    }
    if (results?.barriers_addressed?.length) {
      lines.push(`\n${t('mhn_copy_barriers')}`);
      results?.barriers_addressed.forEach(b => lines.push(`• ${b}`));
    }
    if (results?.immediate_steps?.length) {
      lines.push(`\n${t('mhn_copy_steps')}`);
      results?.immediate_steps.forEach((s, i) => lines.push(`${i + 1}. ${s}`));
    }
    lines.push(BRAND);
    return lines.join('\n');
  }, [results, t]);

  useRegisterActions(buildFullText(), tool?.title || 'MentalHealthNavigator');

  useEffect(() => {
    if (!results || !resultsRef.current) return;
    const t = setTimeout(() => resultsRef.current?.scrollIntoView({ behavior: 'smooth', block: 'start' }), 200);
    return () => clearTimeout(t);
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [results]);

  useEffect(() => {
    const handler = (e) => {
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && !loading && canSubmitRef.current)
        handleFindRef.current?.();
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [loading]);

  const renderInput = () => (
    <div className={`${c.card} border ${c.border} rounded-xl shadow-sm p-5 space-y-5`}>

      {/* Warm intro note */}
      <div className={`${c.infoBox} border rounded-xl px-4 py-3`}>
        <p className="text-sm">{t('mhn_intro')}</p>
      </div>

      {/* Situation areas */}
      <div>
        <label className={`block text-sm font-medium ${c.labelText} mb-2`}>
          {t('mhn_q_situation')} <span className={`text-xs font-normal ${c.textMuted}`}>{t('mhn_select_all')}</span>
        </label>
        <div className="flex flex-wrap gap-2">
          {SITUATION_AREAS.map(s => (
            <button key={s.id} onClick={() => toggle(setSituationAreas)(s.id)}
              className={`px-3 py-1.5 rounded-lg border text-xs font-medium transition-colors ${situationAreas.includes(s.id) ? c.pillActive : c.pillInactive}`}>
              <span className="mr-1">{s.icon}</span>{t(s.labelKey)}
            </button>
          ))}
        </div>
      </div>

      {/* Freeform */}
      <div>
        <label className={`block text-sm font-medium ${c.labelText} mb-1`}>
          {t('mhn_tell_more')} <span className={`text-xs font-normal ${c.textMuted}`}>{t('mhn_tell_more_hint')}</span>
        </label>
        <textarea value={freeform} onChange={e => setFreeform(e.target.value)}
          placeholder={t('mhn_freeform_ph')}
          rows={5}
          className={`w-full px-3 py-2.5 border rounded-xl text-sm resize-y focus:outline-none focus:ring-2 ${c.input}`} />
      </div>

      {/* What you've tried */}
      <div>
        <label className={`block text-sm font-medium ${c.labelText} mb-2`}>{t('mhn_q_tried')}</label>
        <div className="flex flex-wrap gap-2">
          {TRIED_BEFORE.map(opt => (
            <button key={opt.id} onClick={() => toggle(setTriedBefore)(opt.id)}
              className={`px-3 py-1.5 rounded-lg border text-xs font-medium transition-colors ${triedBefore.includes(opt.id) ? c.pillActive : c.pillInactive}`}>
              {t(opt.labelKey)}
            </button>
          ))}
        </div>
      </div>

      {/* Barriers */}
      <div>
        <label className={`block text-sm font-medium ${c.labelText} mb-2`}>{t('mhn_q_barriers')}</label>
        <div className="flex flex-wrap gap-2">
          {BARRIERS.map(b => (
            <button key={b.id} onClick={() => toggle(setBarriers)(b.id)}
              className={`px-3 py-1.5 rounded-lg border text-xs font-medium transition-colors ${barriers.includes(b.id) ? c.pillActive : c.pillInactive}`}>
              {t(b.labelKey)}
            </button>
          ))}
        </div>
      </div>

      {/* Country */}
      <div>
        <label className={`block text-sm font-medium ${c.labelText} mb-1`}>
          {t('mhn_country')} <span className={`text-xs font-normal ${c.textMuted}`}>{t('mhn_country_hint')}</span>
        </label>
        <input type="text" value={country} onChange={e => setCountry(e.target.value)}
          placeholder={t('mhn_country_ph')}
          className={`w-full px-3 py-2 border rounded-xl text-sm focus:outline-none focus:ring-2 ${c.input}`} />
      </div>

      {error && <p className={`text-sm ${c.danger} border rounded-lg px-3 py-2`}>{error}</p>}

      <p className={`text-xs ${c.textMuted}`}>
        {t('mhn_spiral_note')} <a href="/SpiralStopper" className={linkStyle}>🌀 {t('mhn_spiralstopper')}</a> {t('mhn_spiral_note_tail')}
      </p>

      <button onClick={handleFind} disabled={!canSubmit || loading}
        className={`w-full ${c.btnPrimary} disabled:opacity-40 font-bold py-3 rounded-lg flex items-center justify-center gap-2 min-h-[48px]`}>
        {loading
          ? <><span className="inline-block animate-spin">{tool?.icon ?? '🧭'}</span> {t('mhn_finding')}</>
          : <><span className="mr-1">{tool?.icon ?? '🧭'}</span> {t('mhn_find')}</>}
      </button>

      {sessionHistory.length > 0 && (
        <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
          <div className="flex items-center justify-between mb-2">
            <p className={`text-xs font-bold ${c.textMuted} uppercase tracking-wide`}>🕓 {t('mhn_prev_sessions')}</p>
            <button onClick={() => setSessionHistory([])} className={`text-xs ${c.textMuted}`}>{t('mhn_clear')}</button>
          </div>
          <ul className="space-y-1">
            {sessionHistory.map((h, i) => (
              <li key={i} className={`text-xs ${c.textMuted}`}>{h.preview}</li>
            ))}
          </ul>
        </div>
      )}
    </div>
  );

  const renderResults = () => {
    if (!results) return null;
    return (
      <div className="space-y-4" ref={resultsRef}>

        {/* Persistent safety note (carries crisis resources; visible on results too) */}
        <div className={`${c.infoBox} border rounded-xl px-4 py-2.5`}>
          <p className="text-xs">{t('mhn_intro')}</p>
        </div>

        {/* Recap of what the user told us — anchors persisted results on revisits */}
        {results?._input && (results._input.freeform || results._input.situationAreas?.length > 0) && (
          <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
            <p className={`text-xs font-semibold uppercase tracking-wide mb-1.5 ${c.textMuted}`}>📝 {t('mhn_your_situation')}</p>
            {results._input.freeform && <p className={`text-sm ${c.textSecondary}`}>{results._input.freeform}</p>}
            {results._input.situationAreas?.length > 0 && (
              <p className={`text-xs mt-1.5 ${c.textMuted}`}>{results._input.situationAreas.map(id => { const a = SITUATION_AREAS.find(s => s.id === id); return a ? `${a.icon} ${t(a.labelKey)}` : id; }).join(' · ')}</p>
            )}
          </div>
        )}

        {/* What you described */}
        {results?.what_you_described && (
          <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
            <p className={`text-xs font-bold ${c.textMuted} uppercase tracking-wide mb-2`}>🧭 {t('mhn_what_heard')}</p>
            <p className={`text-sm ${c.textSecondary}`}>{results?.what_you_described}</p>
          </div>
        )}

        {/* Recommended support types */}
        {results?.recommended_support?.length > 0 && (
          <div className="space-y-3">
            <p className={`text-xs font-bold ${c.textMuted} uppercase tracking-wide px-1`}>💙 {t('mhn_recommended')}</p>
            {results?.recommended_support.map((s, i) => (
              <div key={i} className={`${c.card} border-l-4 ${i === 0 ? 'border-l-cyan-500' : `border-l-zinc-400`} border ${c.border} rounded-xl p-4 space-y-2`}>
                <div className="flex items-center gap-2 flex-wrap">
                  <span className={`font-bold text-sm ${c.text}`}>{s.type_name}</span>
                  {i === 0 && <span className={`text-[10px] font-bold px-2 py-0.5 rounded-full ${c.infoBox} border`}>{t('mhn_best_fit')}</span>}
                </div>
                <p className={`text-sm ${c.textSecondary}`}>{s.why}</p>
                {s.what_to_expect && (
                  <p className={`text-xs ${c.textMuted}`}><span className="font-semibold">{t('mhn_what_to_expect')}</span>{s.what_to_expect}</p>
                )}
                {s.how_to_find && (
                  <p className={`text-xs ${c.infoBox} border rounded-lg px-3 py-2`}><span className="font-semibold">{t('mhn_how_to_find')}</span>{s.how_to_find}</p>
                )}
                {s.cost_note && (
                  <p className={`text-xs ${c.textMuted}`}><span className="font-semibold">{t('mhn_cost')}</span>{s.cost_note}</p>
                )}
              </div>
            ))}
          </div>
        )}

        {/* What to say */}
        {results?.what_to_say?.length > 0 && (
          <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
            <p className={`text-xs font-bold ${c.textMuted} uppercase tracking-wide mb-3`}>💬 {t('mhn_what_to_say')}</p>
            <ul className="space-y-2">
              {results?.what_to_say.map((s, i) => (
                <li key={i} className={`text-sm ${c.textSecondary} flex gap-2`}>
                  <span className={`flex-shrink-0 ${c.accentTxt}`}>→</span><span>{s}</span>
                </li>
              ))}
            </ul>
          </div>
        )}

        {/* Barriers addressed */}
        {results?.barriers_addressed?.length > 0 && (
          <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
            <p className={`text-xs font-bold ${c.textMuted} uppercase tracking-wide mb-3`}>🔓 {t('mhn_barriers_addressed')}</p>
            <ul className="space-y-2">
              {results?.barriers_addressed.map((b, i) => (
                <li key={i} className={`text-sm ${c.success} border rounded-lg px-3 py-2`}>{b}</li>
              ))}
            </ul>
          </div>
        )}

        {/* Immediate steps */}
        {results?.immediate_steps?.length > 0 && (
          <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
            <p className={`text-xs font-bold ${c.textMuted} uppercase tracking-wide mb-3`}>✅ {t('mhn_immediate_steps')}</p>
            <ol className="space-y-2">
              {results?.immediate_steps.map((s, i) => (
                <li key={i} className={`text-sm ${c.textSecondary} flex gap-2`}>
                  <span className={`flex-shrink-0 font-bold ${c.accentTxt}`}>{i + 1}.</span><span>{s}</span>
                </li>
              ))}
            </ol>
          </div>
        )}

        {/* Warm closing note */}
        <div className={`${c.warmBox} border rounded-xl px-4 py-3`}>
          <p className="text-sm">{t('mhn_closing')}</p>
        </div>

        {/* Post-result cross-refs */}
        <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
          <p className={`text-[10px] font-bold ${c.textMuted} uppercase mb-2`}>🔗 {t('mhn_related')}</p>
          <div className="flex flex-wrap gap-3">
            <a href="/SpiralStopper" className={`text-xs ${linkStyle}`}>🌀 {t('mhn_spiralstopper')}</a>
            <a href="/SocialBatteryAdvisor" className={`text-xs ${linkStyle}`}>⚡ {t('mhn_rechargeradar')}</a>
          </div>
        </div>
      </div>
    );
  };

  return (
    <div className={`space-y-4 ${c.text}`}>
      <div className={`${c.card} border ${c.border} rounded-xl shadow-sm p-5`}>
        <div className="flex items-start justify-between">
          <div>
            <h2 className={`text-xl font-bold ${c.text}`}>
              <span className="mr-2">{tool?.icon ?? '🧭'}</span>{tool?.title ?? t('mhn_title')}
            </h2>
            <p className={`text-sm ${c.textSecondary}`}>{tool?.tagline ?? t('mhn_tagline')}</p>
            <button onClick={loadExample} disabled={loading} style={{ backgroundColor: (tool?.headerColor ?? '#888888') + '80' }} className={`mt-1.5 px-2.5 py-0.5 rounded-full text-xs font-medium border disabled:opacity-40 ${isDark ? 'text-white border-white/40' : 'text-gray-800 border-transparent'}`}>{t('try_example')}</button>
          </div>
          <button
            onClick={handleReset}
            style={{ visibility: (results || situationAreas.length || freeform.trim()) ? 'visible' : 'hidden' }}
            className={`${c.btnSecondary} px-3 py-1.5 rounded-lg text-xs font-bold`}
          >
            ↺ {t('start_over')}
          </button>
        </div>
      </div>
      {!results && <p className={`text-xs ${c.textMuted} px-1`}>{t('mhn_sleep_note')} <a href="/SpiralStopper" className={linkStyle}>🌀 {t('mhn_spiralstopper')}</a> {t('mhn_sleep_note_tail')}</p>}
      {!results && renderInput()}
      {results && renderResults()}
    </div>
  );
}

MentalHealthNavigator.displayName = 'MentalHealthNavigator';
export default MentalHealthNavigator;
