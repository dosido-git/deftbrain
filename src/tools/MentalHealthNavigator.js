import React, { useState, useEffect, useRef, useCallback } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { usePersistentState } from '../hooks/usePersistentState';
import { useRegisterActions } from '../components/ActionBarContext';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const SITUATION_AREAS = [
  { id: 'anxiety',    label: 'Anxiety / worry',       icon: '😰' },
  { id: 'mood',       label: 'Low mood / depression', icon: '😔' },
  { id: 'stress',     label: 'Stress / burnout',      icon: '😤' },
  { id: 'trauma',     label: 'Trauma / past events',  icon: '🫂' },
  { id: 'grief',      label: 'Grief / loss',          icon: '💙' },
  { id: 'addiction',  label: 'Addiction / habits',    icon: '🔄' },
  { id: 'eating',     label: 'Eating / body image',   icon: '🍽️' },
  { id: 'sleep',      label: 'Sleep problems',        icon: '😴' },
  { id: 'work',       label: 'Work / career',         icon: '💼' },
  { id: 'relationship', label: 'Relationships',       icon: '❤️' },
  { id: 'identity',   label: 'Identity / life direction', icon: '🧭' },
  { id: 'parenting',  label: 'Parenting / family',    icon: '👨‍👩‍👧' },
  { id: 'medication', label: 'Medication / diagnosis', icon: '💊' },
  { id: 'general',    label: 'General / not sure',    icon: '❓' },
];

const TRIED_BEFORE = [
  { id: 'nothing',   label: 'Nothing yet' },
  { id: 'therapy',   label: 'Therapy / counseling' },
  { id: 'gp',        label: 'GP / primary care doctor' },
  { id: 'medication',label: 'Medication' },
  { id: 'self_help', label: 'Self-help / books / apps' },
  { id: 'crisis',    label: 'Crisis services' },
];

const BARRIERS = [
  { id: 'cost',       label: 'Cost / affordability' },
  { id: 'waitlists',  label: 'Long waitlists' },
  { id: 'stigma',     label: 'Stigma / privacy concerns' },
  { id: 'access',     label: 'Access / location' },
  { id: 'language',   label: 'Language / cultural fit' },
  { id: 'unsure',     label: 'Not sure where to start' },
  { id: 'none',       label: 'No barriers' },
];

const EXAMPLE = {
  situationAreas: ['anxiety', 'work', 'sleep'],
  freeform: "I've been having really bad anxiety for the past 6 months, mostly around work. I can't sleep, I overthink everything, and I've started avoiding situations that make me nervous. I'm not sure if I need a therapist or a doctor. I tried talking to my GP once but they just told me to exercise more.",
  triedBefore: ['gp', 'self_help'],
  barriers: ['cost', 'unsure'],
  country: 'United States',
};

function MentalHealthNavigator({ tool }) {
  const { callToolEndpoint, loading } = useClaudeAPI();
  const { isDark } = useTheme();

  const c = {
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:         isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20'
                          : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-500' : 'text-gray-400',
    labelText:     isDark ? 'text-zinc-200' : 'text-gray-700',
    accentTxt:     isDark ? 'text-cyan-400' : 'text-cyan-600',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white'
                          : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200'
                          : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    pillActive:    isDark ? 'border-cyan-500 bg-cyan-900/30 text-cyan-200'
                          : 'border-cyan-600 bg-cyan-100 text-cyan-900',
    pillInactive:  isDark ? 'border-zinc-600 text-zinc-400 hover:border-zinc-500'
                          : 'border-gray-300 text-gray-500 hover:border-gray-400',
    required:      isDark ? 'text-amber-400' : 'text-amber-500',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200'   : 'bg-red-50 border-red-200 text-red-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200' : 'bg-amber-50 border-amber-300 text-amber-800',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200' : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    infoBox:       isDark ? 'bg-sky-900/20 border-sky-700 text-sky-200'   : 'bg-sky-50 border-sky-200 text-sky-800',
    warmBox:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200'  : 'bg-amber-50 border-amber-200 text-amber-800',
  };
  c.textMuteded = c.textMuted;
  c.label = c.labelText;

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';

  const [situationAreas, setSituationAreas] = useState([]);
  const [freeform, setFreeform]             = useState('');
  const [triedBefore, setTriedBefore]       = useState([]);
  const [barriers, setBarriers]             = useState([]);
  const [country, setCountry]               = useState('');
  const [results, setResults]               = usePersistentState('mentalhealthnav-results', null);
  const [history, setHistory]               = usePersistentState('mentalhealthnav-history', []);
  const [error, setError]                   = useState('');

  const resultsRef     = useRef(null);
  const handleFindRef  = useRef(null);
  const canSubmitRef   = useRef(false);

  const canSubmit = situationAreas.length > 0 || freeform.trim().length >= 20;

  const handleReset = useCallback(() => {
    setResults(null);
    setError('');
    setSituationAreas([]);
    setFreeform('');
    setTriedBefore([]);
    setBarriers([]);
    setCountry('');
  }, [setResults]);

  const loadExample = useCallback(() => {
    setSituationAreas(EXAMPLE.situationAreas);
    setFreeform(EXAMPLE.freeform);
    setTriedBefore(EXAMPLE.triedBefore);
    setBarriers(EXAMPLE.barriers);
    setCountry(EXAMPLE.country);
    setResults(null);
    setError('');
  }, [setResults]);

  const toggle = useCallback((setter) => (id) =>
    setter(prev => prev.includes(id) ? prev.filter(x => x !== id) : [...prev, id]), []);

  const handleFind = useCallback(async () => {
    if (!canSubmit || loading) return;
    setError('');
    setResults(null);

    try {
      const parsed = await callToolEndpoint('mental-health-navigator/stream', {
        situationAreas,
        freeform: freeform.trim() || null,
        triedBefore,
        barriers,
        country: country.trim() || null,
      });
      setResults(parsed);
      const previewText = situationAreas.map(id => SITUATION_AREAS.find(s => s.id === id)?.label).filter(Boolean).join(', ') || 'Navigation completed';
      setHistory(prev => [{
        preview: previewText.slice(0, 50),
        ts: Date.now(),
      }, ...prev].slice(0, 5));
    } catch (err) {
      setError(err.message || 'Something went wrong. Please try again.');
    }
  }, [canSubmit, loading, situationAreas, freeform, triedBefore, barriers, country, callToolEndpoint, setResults, setHistory]);

  handleFindRef.current = handleFind;
  canSubmitRef.current  = canSubmit;

  const buildFullText = useCallback(() => {
    if (!results) return '';
    const lines = ['MENTAL HEALTH NAVIGATOR', ''];
    if (results?.what_you_described) lines.push(results?.what_you_described, '');
    if (results?.recommended_support?.length) {
      lines.push('RECOMMENDED SUPPORT');
      results?.recommended_support.forEach(s => {
        lines.push(`\n${s.type_name}`);
        lines.push(s.why);
        if (s.what_to_expect) lines.push(`What to expect: ${s.what_to_expect}`);
        if (s.how_to_find) lines.push(`How to find one: ${s.how_to_find}`);
      });
    }
    if (results?.what_to_say?.length) {
      lines.push('\nWHAT TO SAY WHEN ASKING FOR HELP');
      results?.what_to_say.forEach(s => lines.push(`• ${s}`));
    }
    if (results?.barriers_addressed?.length) {
      lines.push('\nADDRESSING YOUR BARRIERS');
      results?.barriers_addressed.forEach(b => lines.push(`• ${b}`));
    }
    if (results?.immediate_steps?.length) {
      lines.push('\nIMMEDIATE STEPS');
      results?.immediate_steps.forEach((s, i) => lines.push(`${i + 1}. ${s}`));
    }
    lines.push(BRAND);
    return lines.join('\n');
  }, [results]);

  useRegisterActions(buildFullText(), tool?.title || 'MentalHealthNavigator');

  useEffect(() => {
    if (!results || !resultsRef.current) return;
    const t = setTimeout(() => resultsRef.current?.scrollIntoView({ behavior: 'smooth', block: 'start' }), 200);
    return () => clearTimeout(t);
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [results]);

  useEffect(() => {
    const handler = (e) => {
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && !loading && canSubmitRef.current)
        handleFindRef.current?.();
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [loading]);

  const renderInput = () => (
    <div className={`${c.card} border ${c.border} rounded-xl shadow-sm p-5 space-y-5`}>

      {/* Warm intro note */}
      <div className={`${c.infoBox} border rounded-xl px-4 py-3`}>
        <p className="text-sm">This tool helps you understand what kind of support might fit your situation — and how to access it. It doesn't diagnose or treat anything. If you're in crisis, please reach out to a crisis line in your area right away.</p>
      </div>

      {/* Situation areas */}
      <div>
        <label className={`block text-sm font-medium ${c.labelText} mb-2`}>
          What's been on your mind? <span className={`text-xs font-normal ${c.textMuted}`}>(select all that apply)</span>
        </label>
        <div className="flex flex-wrap gap-2">
          {SITUATION_AREAS.map(s => (
            <button key={s.id} onClick={() => toggle(setSituationAreas)(s.id)}
              className={`px-3 py-1.5 rounded-lg border text-xs font-medium transition-colors ${situationAreas.includes(s.id) ? c.pillActive : c.pillInactive}`}>
              <span className="mr-1">{s.icon}</span>{s.label}
            </button>
          ))}
        </div>
      </div>

      {/* Freeform */}
      <div>
        <label className={`block text-sm font-medium ${c.labelText} mb-1`}>
          Tell us more <span className={`text-xs font-normal ${c.textMuted}`}>(optional — but the more you share, the more useful the guidance)</span>
        </label>
        <textarea value={freeform} onChange={e => setFreeform(e.target.value)}
          placeholder="What's been happening? How long has it been going on? How is it affecting your daily life? What have you tried? You don't have to share anything you're not comfortable with."
          rows={5}
          className={`w-full px-3 py-2.5 border rounded-xl text-sm resize-y focus:outline-none focus:ring-2 ${c.input}`} />
      </div>

      {/* What you've tried */}
      <div>
        <label className={`block text-sm font-medium ${c.labelText} mb-2`}>What have you tried before?</label>
        <div className="flex flex-wrap gap-2">
          {TRIED_BEFORE.map(t => (
            <button key={t.id} onClick={() => toggle(setTriedBefore)(t.id)}
              className={`px-3 py-1.5 rounded-lg border text-xs font-medium transition-colors ${triedBefore.includes(t.id) ? c.pillActive : c.pillInactive}`}>
              {t.label}
            </button>
          ))}
        </div>
      </div>

      {/* Barriers */}
      <div>
        <label className={`block text-sm font-medium ${c.labelText} mb-2`}>Any barriers to getting support?</label>
        <div className="flex flex-wrap gap-2">
          {BARRIERS.map(b => (
            <button key={b.id} onClick={() => toggle(setBarriers)(b.id)}
              className={`px-3 py-1.5 rounded-lg border text-xs font-medium transition-colors ${barriers.includes(b.id) ? c.pillActive : c.pillInactive}`}>
              {b.label}
            </button>
          ))}
        </div>
      </div>

      {/* Country */}
      <div>
        <label className={`block text-sm font-medium ${c.labelText} mb-1`}>
          Country <span className={`text-xs font-normal ${c.textMuted}`}>(optional — helps tailor resources and cost info)</span>
        </label>
        <input type="text" value={country} onChange={e => setCountry(e.target.value)}
          placeholder="e.g. United States, UK, Australia, India…"
          className={`w-full px-3 py-2 border rounded-xl text-sm focus:outline-none focus:ring-2 ${c.input}`} />
      </div>

      {error && <p className={`text-sm ${c.danger} border rounded-lg px-3 py-2`}>{error}</p>}

      <p className={`text-xs ${c.textMuted}`}>
        Feeling overwhelmed right now? <a href="/SpiralStopper" className={linkStyle}>🌀 Spiral Stopper</a> can help interrupt an anxiety spiral.
      </p>

      <button onClick={handleFind} disabled={!canSubmit || loading}
        className={`w-full ${c.btnPrimary} disabled:opacity-40 font-bold py-3 rounded-lg flex items-center justify-center gap-2 min-h-[48px]`}>
        {loading
          ? <><span className="inline-block animate-spin">{tool?.icon ?? '🧭'}</span> Finding your path…</>
          : <><span className="mr-1">{tool?.icon ?? '🧭'}</span> Find My Support Path</>}
      </button>

      {history.length > 0 && (
        <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
          <div className="flex items-center justify-between mb-2">
            <p className={`text-xs font-bold ${c.textMuted} uppercase tracking-wide`}>🕓 Previous sessions</p>
            <button onClick={() => setHistory([])} className={`text-xs ${c.textMuted}`}>Clear</button>
          </div>
          <ul className="space-y-1">
            {history.map((h, i) => (
              <li key={i} className={`text-xs ${c.textMuted}`}>{h.preview}</li>
            ))}
          </ul>
        </div>
      )}
    </div>
  );

  const renderResults = () => {
    if (!results) return null;
    return (
      <div className="space-y-4" ref={resultsRef}>

        {/* What you described */}
        {results?.what_you_described && (
          <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
            <p className={`text-xs font-bold ${c.textMuted} uppercase tracking-wide mb-2`}>🧭 What we heard</p>
            <p className={`text-sm ${c.textSecondary}`}>{results?.what_you_described}</p>
          </div>
        )}

        {/* Recommended support types */}
        {results?.recommended_support?.length > 0 && (
          <div className="space-y-3">
            <p className={`text-xs font-bold ${c.textMuted} uppercase tracking-wide px-1`}>💙 Recommended support</p>
            {results?.recommended_support.map((s, i) => (
              <div key={i} className={`${c.card} border-l-4 ${i === 0 ? 'border-l-cyan-500' : `border-l-zinc-400`} border ${c.border} rounded-xl p-4 space-y-2`}>
                <div className="flex items-center gap-2 flex-wrap">
                  <span className={`font-bold text-sm ${c.text}`}>{s.type_name}</span>
                  {i === 0 && <span className={`text-[10px] font-bold px-2 py-0.5 rounded-full ${c.infoBox} border`}>Best fit</span>}
                </div>
                <p className={`text-sm ${c.textSecondary}`}>{s.why}</p>
                {s.what_to_expect && (
                  <p className={`text-xs ${c.textMuted}`}><span className="font-semibold">What to expect: </span>{s.what_to_expect}</p>
                )}
                {s.how_to_find && (
                  <p className={`text-xs ${c.infoBox} border rounded-lg px-3 py-2`}><span className="font-semibold">How to find one: </span>{s.how_to_find}</p>
                )}
                {s.cost_note && (
                  <p className={`text-xs ${c.textMuted}`}><span className="font-semibold">Cost: </span>{s.cost_note}</p>
                )}
              </div>
            ))}
          </div>
        )}

        {/* What to say */}
        {results?.what_to_say?.length > 0 && (
          <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
            <p className={`text-xs font-bold ${c.textMuted} uppercase tracking-wide mb-3`}>💬 What to say when reaching out</p>
            <ul className="space-y-2">
              {results?.what_to_say.map((s, i) => (
                <li key={i} className={`text-sm ${c.textSecondary} flex gap-2`}>
                  <span className={`flex-shrink-0 ${c.accentTxt}`}>→</span><span>{s}</span>
                </li>
              ))}
            </ul>
          </div>
        )}

        {/* Barriers addressed */}
        {results?.barriers_addressed?.length > 0 && (
          <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
            <p className={`text-xs font-bold ${c.textMuted} uppercase tracking-wide mb-3`}>🔓 Addressing your barriers</p>
            <ul className="space-y-2">
              {results?.barriers_addressed.map((b, i) => (
                <li key={i} className={`text-sm ${c.success} border rounded-lg px-3 py-2`}>{b}</li>
              ))}
            </ul>
          </div>
        )}

        {/* Immediate steps */}
        {results?.immediate_steps?.length > 0 && (
          <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
            <p className={`text-xs font-bold ${c.textMuted} uppercase tracking-wide mb-3`}>✅ Immediate steps</p>
            <ol className="space-y-2">
              {results?.immediate_steps.map((s, i) => (
                <li key={i} className={`text-sm ${c.textSecondary} flex gap-2`}>
                  <span className={`flex-shrink-0 font-bold ${c.accentTxt}`}>{i + 1}.</span><span>{s}</span>
                </li>
              ))}
            </ol>
          </div>
        )}

        {/* Warm closing note */}
        <div className={`${c.warmBox} border rounded-xl px-4 py-3`}>
          <p className="text-sm">Reaching out for support takes courage. You don't have to have it all figured out before making the first call — professionals are used to helping people who aren't sure where to start.</p>
        </div>

        {/* Post-result cross-refs */}
        <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
          <p className={`text-[10px] font-bold ${c.textMuted} uppercase mb-2`}>🔗 Related tools</p>
          <div className="flex flex-wrap gap-3">
            <a href="/SpiralStopper" className={`text-xs ${linkStyle}`}>🌀 Spiral Stopper</a>
            <a href="/SpiralStopper" className={`text-xs ${linkStyle}`}>🌀 Spiral Stopper</a>
            <a href="/RechargeRadar" className={`text-xs ${linkStyle}`}>🔋 Recharge Radar</a>
          </div>
        </div>
      </div>
    );
  };

  return (
    <div className={`space-y-4 ${c.text}`}>
      <div className={`${c.card} border ${c.border} rounded-xl shadow-sm p-5`}>
        <div className="flex items-start justify-between">
          <div>
            <h2 className={`text-xl font-bold ${c.text}`}>
              <span className="mr-2">{tool?.icon ?? '🧭'}</span>{tool?.title ?? 'MentalHealthNavigator'}
            </h2>
            <p className={`text-sm ${c.textSecondary}`}>{tool?.tagline ?? 'Find the right support for what you\'re going through.'}</p>
            <button onClick={loadExample} disabled={loading} style={{ backgroundColor: (tool?.headerColor ?? '#888888') + '80' }} className={`mt-1.5 px-2.5 py-0.5 rounded-full text-xs font-medium border disabled:opacity-40 ${isDark ? 'text-white border-white/40' : 'text-gray-800 border-transparent'}`}>Try example</button>
          </div>
          <button
            onClick={handleReset}
            style={{ visibility: (results || situationAreas.length || freeform.trim()) ? 'visible' : 'hidden' }}
            className={`${c.btnSecondary} px-3 py-1.5 rounded-lg text-xs font-bold`}
          >
            ↺ Start over
          </button>
        </div>
      </div>
      {!results && <p className={`text-xs ${c.textMuted} px-1`}>Struggling to wind down at night? <a href="/SpiralStopper" className={linkStyle}>🌀 Spiral Stopper</a> builds a personalized sleep protocol.</p>}
      {!results && renderInput()}
      {results && renderResults()}
    </div>
  );
}

MentalHealthNavigator.displayName = 'MentalHealthNavigator';
export default MentalHealthNavigator;
