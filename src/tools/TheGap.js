import React, { useState, useRef, useCallback, useEffect } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { usePersistentState } from '../hooks/usePersistentState';
import { useRegisterActions } from '../components/ActionBarContext';
import {} from '../components/ActionButtons'; // PrintBtn/ShareBtn available via ActionBar in wrapper
import { useTranslation } from '../i18n/useTranslation';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

// ════════════════════════════════════════════════════════════
// MAIN
// ════════════════════════════════════════════════════════════
const TheGap = ({ tool }) => {
  const { callToolEndpoint, loading, userLocale, userCurrency, userRegion } = useClaudeAPI();
  const { isDark } = useTheme();
  const { t } = useTranslation();

  const LEVELS = [
    { value: 'high_school', label: t('tg_lvl_hs') },
    { value: 'undergrad', label: t('tg_lvl_undergrad') },
    { value: 'grad', label: t('tg_lvl_grad') },
    { value: 'self_taught', label: t('tg_lvl_self') },
  ];

  const EXAMPLES = [
    { concept: t('tg_ex1'), subject: 'Calculus', level: 'undergrad' },
    { concept: t('tg_ex2'), subject: 'Computer Science', level: 'undergrad' },
    { concept: t('tg_ex3'), subject: 'Economics', level: 'high_school' },
    { concept: t('tg_ex4'), subject: 'Biology', level: 'undergrad' },
    { concept: t('tg_ex5'), subject: 'Statistics', level: 'undergrad' },
  ];

  const GAP_TYPE_LABELS = {
    conceptual: t('tg_gt_conceptual'),
    procedural: t('tg_gt_procedural'),
    definitional: t('tg_gt_definitional'),
    notational: t('tg_gt_notational'),
  };

  const c = {
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:         isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20'
                          : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-400' : 'text-gray-500',
    labelText:     isDark ? 'text-zinc-200' : 'text-gray-700',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white'
                          : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200'
                          : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    btnDis:        isDark ? 'bg-zinc-700 text-zinc-500 cursor-not-allowed'
                          : 'bg-gray-200 text-gray-400 cursor-not-allowed',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200'
                          : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200'
                          : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200'
                          : 'bg-red-50 border-red-200 text-red-800',
    infoBox:       isDark ? 'bg-sky-900/20 border-sky-700' : 'bg-sky-50 border-sky-200',
    successBox:    isDark ? 'bg-emerald-900/20 border-emerald-700' : 'bg-emerald-50 border-emerald-300',
    successTxt:    isDark ? 'text-emerald-300' : 'text-emerald-800',
    warningBox:    isDark ? 'bg-amber-900/20 border-amber-700' : 'bg-amber-50 border-amber-300',
    warningTxt:    isDark ? 'text-amber-300' : 'text-amber-800',
    pillActive:    isDark ? 'border-cyan-500 bg-cyan-900/30 text-cyan-200'
                          : 'border-cyan-600 bg-cyan-100 text-cyan-900',
    pillInactive:  isDark ? 'border-zinc-600 text-zinc-400 hover:border-zinc-500'
                          : 'border-gray-300 text-gray-500 hover:border-gray-400',
    required:      isDark ? 'text-amber-400' : 'text-amber-700',
    // Tool-specific extras
    badge:         isDark ? 'bg-cyan-900/30 text-cyan-300' : 'bg-cyan-100 text-cyan-800',
    tipBg:         isDark ? 'bg-amber-900/20 border-amber-700' : 'bg-amber-50 border-amber-300',
    tipText:       isDark ? 'text-amber-300' : 'text-amber-800',
    histBg:        isDark ? 'bg-sky-900/20 border-sky-700/30' : 'bg-sky-50 border-sky-200',
    gapHigh:       isDark ? 'bg-red-900/20 border-red-700/50' : 'bg-red-50 border-red-300',
    gapHighText:   isDark ? 'text-red-300' : 'text-red-700',
    gapMed:        isDark ? 'bg-amber-900/20 border-amber-700/40' : 'bg-amber-50 border-amber-300',
    gapMedText:    isDark ? 'text-amber-300' : 'text-amber-700',
    gapLow:        isDark ? 'bg-emerald-900/20 border-emerald-700/30' : 'bg-emerald-50 border-emerald-300',
    gapLowText:    isDark ? 'text-emerald-300' : 'text-emerald-700',
  };
  c.textMuteded = c.textMuted;
  c.label = c.labelText;

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-700 hover:text-cyan-800 underline underline-offset-2';

  // useState — all together first
  const [showHistory, setShowHistory] = useState(false);
  const [subject, setSubject] = useState('');
  const [whatIKnow, setWhatIKnow] = useState('');
  const [whereItBroke, setWhereItBroke] = useState('');
  const [level, setLevel] = useState('undergrad');
  const [digResults, setDigResults] = useState(null);
  const [digTarget, setDigTarget] = useState(null);
  const [error, setError] = useState('');
  const [expandedNodes, setExpandedNodes] = useState({});
  const [showAlternatives, setShowAlternatives] = useState(false);
  const [showPlan, setShowPlan] = useState(true);

  // useRef
  const resultsRef = useRef(null);

  // usePersistentState — all together after useRef
  const [sessionHistory, setSessionHistory] = usePersistentState('the-gap-history', []);
  const [concept, setConcept] = usePersistentState('the-gap-concept', '');
  const [results, setResults] = usePersistentState('the-gap-results', null);

  useEffect(() => {
    if (!results) return;
    const timer = setTimeout(() => {
      resultsRef.current?.scrollIntoView({ behavior: 'smooth', block: 'start' });
    }, 200);
    return () => clearTimeout(timer);
  }, [results]);

  useEffect(() => {
    const handler = (e) => {
      if (e.key !== 'Enter' || !(e.metaKey || e.ctrlKey)) return;
      const tag = document.activeElement?.tagName;
      if (tag === 'SELECT') return;
      if (!loading && concept.trim()) trace();
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [loading, concept]);

  const trace = useCallback(async () => {
    if (!concept.trim()) { setError(t('tg_err_no_concept')); return; }
    setError(''); setResults(null); setDigResults(null); setDigTarget(null); setExpandedNodes({});
    try {
      const data = await callToolEndpoint('the-gap', {
        concept: concept.trim(), subject: subject.trim() || null,
        whatIKnow: whatIKnow.trim() || null, whereItBroke: whereItBroke.trim() || null, level,
        userLocale, userCurrency, userRegion,
      });
      setResults({ ...data, _concept: concept.trim() });
      const entry = { id: 'tg_' + Date.now(), date: new Date().toISOString(), concept: concept.trim(), subject: subject.trim(), likelyGap: data.likely_gap?.concept, preview: concept.trim().slice(0, 40) };
      setSessionHistory(prev => [entry, ...prev].slice(0, 6));
    } catch (err) { setError(err.message || t('tg_err_trace')); }
  }, [concept, subject, whatIKnow, whereItBroke, level, callToolEndpoint, setSessionHistory, setResults, userLocale, userCurrency, userRegion, t]);

  const dig = useCallback(async (prerequisite, testResult) => {
    setDigTarget(prerequisite); setDigResults(null);
    try {
      const data = await callToolEndpoint('the-gap/dig', {
        originalConcept: concept.trim(), prerequisite, testResult: testResult || 'failed',
        userLocale, userCurrency, userRegion,
      });
      setDigResults(data);
    } catch (err) { setError(err.message || t('tg_err_dig')); }
  }, [concept, callToolEndpoint, userLocale, userCurrency, userRegion, t]);

  const handleReset = useCallback(() => {
    setConcept(''); setSubject(''); setWhatIKnow(''); setWhereItBroke('');
    setResults(null); setDigResults(null); setDigTarget(null); setError(''); setExpandedNodes({});
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [setResults]);

  const loadExample = useCallback(() => {
    const ex = EXAMPLES[0];
    setConcept(ex.concept); setSubject(ex.subject); setLevel(ex.level);
    setResults(null); setDigResults(null); setError('');
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [setResults]);

  const toggleNode = useCallback((idx) => setExpandedNodes(prev => ({ ...prev, [idx]: !prev[idx] })), []);

  const gapStyle = (likelihood) => {
    if (likelihood === 'high') return { bg: c.gapHigh, text: c.gapHighText, emoji: '🔴' };
    if (likelihood === 'medium') return { bg: c.gapMed, text: c.gapMedText, emoji: '🟡' };
    return { bg: c.gapLow, text: c.gapLowText, emoji: '🟢' };
  };

  const buildCopy = useCallback(() => {
    if (!results) return '';
    const GAP_LABELS = {
      conceptual: t('tg_gt_conceptual'),
      procedural: t('tg_gt_procedural'),
      definitional: t('tg_gt_definitional'),
      notational: t('tg_gt_notational'),
    };
    const lines = [t('tg_copy_title'), '', `${t('tg_copy_struggling')} ${concept}`, ''];
    if (results?.chain_visualization) lines.push(results?.chain_visualization, '');
    if (results?.likely_gap) {
      lines.push(`${t('tg_copy_likely_gap')} ${results?.likely_gap?.concept}`);
      lines.push(`${t('tg_copy_type')} ${GAP_LABELS[results?.likely_gap?.gap_type] || results?.likely_gap?.gap_type}`);
      lines.push('', results?.likely_gap?.refresher?.core_idea || '');
    }
    if (results?.study_plan) {
      lines.push('', t('tg_copy_study_plan'));
      lines.push('1. ' + results?.study_plan?.step_1);
      lines.push('2. ' + results?.study_plan?.step_2);
      lines.push('3. ' + results?.study_plan?.step_3);
    }
    lines.push('', BRAND);
    return lines.join('\n');
  }, [results, concept, t]);

  // Register export content with the wrapper's persistent ActionBar
  useRegisterActions(buildCopy(), tool?.title || 'The Gap');

  const Pill = ({ active, onClick, children }) => (
    <button onClick={onClick} className={'px-3 py-1.5 rounded-lg border text-xs font-semibold transition-all ' + (active ? c.pillActive : c.pillInactive)}>
      {active && <span className="me-1">✓</span>}{children}
    </button>
  );

  // ══════════════════════════════════════════
  // INPUT
  // ══════════════════════════════════════════
  const renderInput = () => (
    <div className={`${c.card} border ${c.border} rounded-xl shadow-sm p-5 space-y-4`}>
      <div className="pb-3 border-b border-zinc-500">
        <h2 className={'text-xl font-bold ' + c.text}>
          <span className="me-2">{tool?.icon ?? '🔍'}</span>{tool?.title ?? t('tg_title')}
        </h2>
        <p className={'text-sm ' + c.textSecondary}>{tool?.tagline ?? t('tg_tagline')}</p>
        <button onClick={loadExample} disabled={loading} style={{ backgroundColor: (tool?.headerColor ?? '#888888') + '80' }} className={`mt-1.5 px-2.5 py-0.5 rounded-full text-xs font-medium border disabled:opacity-40 ${isDark ? 'text-white border-white/40' : 'text-gray-800 border-transparent'}`}>{t('try_example')}</button>
      </div>

      <div>
        <label className={'text-base font-bold ' + c.text + ' mb-1 block'}>{t('tg_concept_label')} <span className={c.required}>*</span></label>
        <p className={'text-xs ' + c.textMuted + ' mb-3'}>{t('tg_concept_help')}</p>
        <input type="text" value={concept} onChange={e => setConcept(e.target.value)}
          placeholder={t('tg_concept_ph')}
          className={'w-full px-4 py-3 border-2 rounded-xl ' + c.input + ' outline-none focus:ring-2 text-sm'}
          onKeyDown={e => { if (e.key === 'Enter' && concept.trim()) trace(); }} />
      </div>

      <div className="grid grid-cols-2 gap-3">
        <div>
          <label className={'text-xs font-bold ' + c.textSecondary + ' uppercase tracking-wide mb-1 block'}>{t('tg_subject_label')}</label>
          <input type="text" value={subject} onChange={e => setSubject(e.target.value)}
            placeholder={t('tg_subject_ph')}
            className={'w-full px-3 py-2 rounded-xl border text-sm ' + c.input + ' outline-none'} />
        </div>
        <div>
          <label className={'text-xs font-bold ' + c.textSecondary + ' uppercase tracking-wide mb-2 block'}>{t('tg_level_label')}</label>
          <div className="flex flex-wrap gap-1">
            {LEVELS.map(l => <Pill key={l.value} active={level === l.value} onClick={() => setLevel(l.value)}>{l.label}</Pill>)}
          </div>
        </div>
      </div>
      <div>
        <label className={'text-xs font-bold ' + c.textSecondary + ' uppercase tracking-wide mb-1 block'}>{t('tg_know_label')}</label>
        <input type="text" value={whatIKnow} onChange={e => setWhatIKnow(e.target.value)}
          placeholder={t('tg_know_ph')}
          className={'w-full px-3 py-2 rounded-xl border text-sm ' + c.input + ' outline-none'} />
      </div>
      <div>
        <label className={'text-xs font-bold ' + c.textSecondary + ' uppercase tracking-wide mb-1 block'}>{t('tg_broke_label')}</label>
        <input type="text" value={whereItBroke} onChange={e => setWhereItBroke(e.target.value)}
          placeholder={t('tg_broke_ph')}
          className={'w-full px-3 py-2 rounded-xl border text-sm ' + c.input + ' outline-none'} />
      </div>


      <button onClick={trace} disabled={loading || !concept.trim()}
        className={'w-full py-4 rounded-2xl text-sm font-bold flex items-center justify-center gap-2 transition-all disabled:opacity-40 ' + (loading || !concept.trim() ? c.btnDis : c.btnPrimary)}>
        {loading ? <><span className="inline-block animate-spin">{tool?.icon ?? '🔍'}</span> {t('tg_tracing')}</> : <><span className="me-1">{tool?.icon ?? '🔍'}</span> {t('tg_find_gap')}</>}
      </button>
      <p className={'text-xs text-center ' + c.textMuted}>{t('tg_ai_disclaimer')}</p>
      <p className={'text-xs ' + c.textMuted}>
        {t('tg_braindump_q')} <a href="/BrainDumpBuddy" className={linkStyle}>🧠 {t('tg_braindump_link')}</a> {t('tg_braindump_rest')}
      </p>
    </div>
  );

  // ══════════════════════════════════════════
  // RESULTS
  // ══════════════════════════════════════════
  const renderResults = () => {
    if (!results) return null;
    const chain = results?.prerequisite_chain || [];
    const gap = results?.likely_gap;

    return (
      <div ref={resultsRef} className="space-y-4 mt-4">
        {/* Analysis */}
        {results?.concept_analysis && (
          <div className={'p-5 rounded-2xl border-2 ' + c.tipBg}>
            <p className={'text-xs font-bold ' + c.tipText + ' uppercase mb-1'}>{t('tg_why_confusing_pre')}{results?._concept || concept || results?.prerequisite_chain?.slice(-1)[0]?.concept || ''}{t('tg_why_confusing_post')}</p>
            <p className={'text-sm ' + c.text}>{results?.concept_analysis}</p>
          </div>
        )}

        {/* Chain visualization */}
        {results?.chain_visualization && (
          <div className={'p-4 rounded-xl ' + c.cardAlt + ' text-center'}>
            <p className={'text-xs font-bold ' + c.textMuted + ' mb-2'}>{t('tg_chain_title')}</p>
            <p className={'text-sm font-mono ' + c.text + ' break-words'}>{results?.chain_visualization}</p>
          </div>
        )}

        {/* Chain nodes */}
        <div className="space-y-2">
          <p className={'text-xs font-bold ' + c.textMuted + ' uppercase'}>{t('tg_prereqs_title')}</p>
          {chain.map((node, idx) => {
            const gs = gapStyle(node.gap_likelihood);
            const expanded = expandedNodes[idx];
            return (
              <div key={idx} className={'rounded-xl border-2 overflow-hidden transition-all ' + gs.bg}>
                <button onClick={() => toggleNode(idx)} className="w-full text-start p-4">
                  <div className="flex items-center gap-3">
                    <div className="flex flex-col items-center">
                      <span className="text-base">{gs.emoji}</span>
                      <span className={'text-[9px] font-bold ' + gs.text}>{t('tg_level_short')}{node.level_in_chain}</span>
                    </div>
                    <div className="flex-1 min-w-0">
                      <p className={'text-sm font-bold ' + c.text}>{node.concept}</p>
                      <p className={'text-xs ' + c.textSecondary + ' mt-0.5'}>{node.why_needed}</p>
                    </div>
                    <span className={'text-[10px] font-bold px-2 py-0.5 rounded-full ' + c.badge}>
                      {node.gap_likelihood} {t('tg_risk_suffix')}
                    </span>
                  </div>
                </button>
                {expanded && (
                  <div className={'px-4 pb-4 border-t ' + c.border}>
                    <div className={'mt-3 p-3 rounded-lg ' + c.cardAlt}>
                      <p className={'text-xs font-bold ' + c.textMuted + ' mb-1'}>{t('tg_quick_test')}</p>
                      <p className={'text-sm ' + c.text + ' italic'}>{node.quick_test}</p>
                    </div>
                    <div className="flex gap-1.5 mt-3">
                      <button onClick={() => dig(node.concept, 'failed')}
                        className={'flex-1 py-2 rounded-lg text-xs font-bold border ' + c.pillInactive + ' hover:border-red-400'}>
                        {t('tg_cant_answer')}
                      </button>
                      <button onClick={() => dig(node.concept, 'unsure')}
                        className={'flex-1 py-2 rounded-lg text-xs font-bold border ' + c.pillInactive + ' hover:border-amber-500'}>
                        {t('tg_unsure')}
                      </button>
                      <button onClick={() => dig(node.concept, 'passed')}
                        className={'flex-1 py-2 rounded-lg text-xs font-bold border ' + c.pillInactive + ' hover:border-emerald-500'}>
                        {t('tg_got_it')}
                      </button>
                    </div>
                  </div>
                )}
              </div>
            );
          })}
        </div>

        {/* Likely gap */}
        {gap && (
          <div className={'p-5 rounded-2xl border-2 ' + c.gapHigh}>
            <div className="flex items-center gap-2 mb-3">
              <span className="text-xl">🎯</span>
              <div>
                <p className={'text-base font-bold ' + c.text}>{t('tg_likely_gap')} {gap.concept}</p>
                <p className={'text-xs ' + c.textSecondary}>{GAP_TYPE_LABELS[gap.gap_type] || gap.gap_type_explanation}</p>
              </div>
            </div>
            <p className={'text-xs ' + c.textSecondary + ' mb-3'}>{gap.why_this_one}</p>

            {/* Refresher */}
            {gap.refresher && (
              <div className={'rounded-xl border ' + c.card + ' p-4 space-y-3'}>
                <div>
                  <p className={'text-xs font-bold ' + c.textMuted + ' uppercase mb-1'}>{t('tg_quick_refresher')}</p>
                  <p className={'text-sm ' + c.text}>{gap.refresher.core_idea}</p>
                </div>
                {gap.refresher.common_confusion && (
                  <div className={'p-3 rounded-lg ' + c.warningBox}>
                    <p className={'text-xs font-bold ' + c.warningTxt}>{t('tg_common_confusion')} {gap.refresher.common_confusion}</p>
                  </div>
                )}
                {gap.refresher.practice && (
                  <div className={'p-3 rounded-lg ' + c.cardAlt}>
                    <p className={'text-xs font-bold ' + c.textMuted + ' mb-1'}>{t('tg_practice')}</p>
                    <p className={'text-xs ' + c.text}>{gap.refresher.practice}</p>
                  </div>
                )}
                {gap.refresher.time_estimate && (
                  <p className={'text-xs ' + c.tipText}>{t('tg_time_to_fill')} {gap.refresher.time_estimate}</p>
                )}
              </div>
            )}

            <button onClick={() => dig(gap.concept, 'failed')}
              className={'w-full mt-3 py-3 rounded-xl text-xs font-bold flex items-center justify-center gap-1.5 ' + c.btnPrimary}>
              <span>🔍</span> {t('tg_deep_dive_btn')}
            </button>
          </div>
        )}

        {/* Forward connection */}
        {results?.forward_connection && (
          <div className={'p-4 rounded-xl border ' + c.successBox}>
            <p className={'text-xs font-bold ' + c.successTxt + ' mb-1'}>{t('tg_connects_back_pre')} {concept}</p>
            <p className={'text-sm ' + c.successTxt}>{results?.forward_connection}</p>
          </div>
        )}

        {/* Study plan */}
        {results?.study_plan && (
          <div className={c.card + ' border rounded-xl overflow-hidden'}>
            <button onClick={() => setShowPlan(!showPlan)} className="w-full flex items-center justify-between p-4 hover:opacity-80">
              <div className="flex items-center gap-2">
                <span>📋</span>
                <span className={'text-sm font-semibold ' + c.text}>{t('tg_study_plan')}</span>
                {results?.study_plan?.total_time && <span className={'text-xs px-2 py-0.5 rounded-full ' + c.badge}>⏱️ {results?.study_plan?.total_time}</span>}
              </div>
              <span className={'text-xs ' + c.textMuted}>{showPlan ? '▲' : '▼'}</span>
            </button>
            {showPlan && (
              <div className={'px-4 pb-4 border-t ' + c.border + ' space-y-2 mt-3'}>
                {['step_1', 'step_2', 'step_3'].map((key, i) => results?.study_plan?.[key] && (
                  <div key={key} className={'p-3 rounded-lg ' + c.cardAlt + ' flex items-start gap-3'}>
                    <span className={'text-xs font-black ' + c.tipText}>{i + 1}</span>
                    <p className={'text-xs ' + c.text}>{results?.study_plan?.[key]}</p>
                  </div>
                ))}
              </div>
            )}
          </div>
        )}

        {/* Alternatives */}
        {results?.if_thats_not_it?.length > 0 && (
          <div>
            <button onClick={() => setShowAlternatives(!showAlternatives)}
              className={'w-full flex items-center gap-2 p-3 rounded-xl ' + c.cardAlt + ' border hover:opacity-80'}>
              <span>🤔</span>
              <span className={'text-xs font-bold ' + c.text + ' flex-1'}>{t('tg_alt_not_gap')}</span>
              <span className={'text-xs ' + c.textMuted}>{showAlternatives ? '▲' : '▼'}</span>
            </button>
            {showAlternatives && (
              <div className="mt-2 space-y-2">
                {results?.if_thats_not_it?.map((alt, i) => (
                  <div key={i} className={'p-3 rounded-lg border ' + c.cardAlt}>
                    <p className={'text-xs font-bold ' + c.text}>{alt.alternative_gap}</p>
                    <p className={'text-[10px] ' + c.textSecondary + ' mt-0.5'}>{t('tg_alt_symptom')} {alt.symptom}</p>
                    <button onClick={() => dig(alt.alternative_gap, 'failed')}
                      className={'mt-2 text-[10px] font-bold ' + linkStyle}>{t('tg_alt_dig')}</button>
                  </div>
                ))}
              </div>
            )}
          </div>
        )}

        {/* Encouragement */}
        {results?.encouragement && (
          <div className={'p-4 rounded-xl border ' + c.successBox}>
            <p className={'text-sm ' + c.successTxt}>💚 {results?.encouragement}</p>
          </div>
        )}

        {/* Dig results */}
        {digResults && renderDig()}
      </div>
    );
  };

  // ══════════════════════════════════════════
  // DIG RESULTS
  // ══════════════════════════════════════════
  const renderDig = () => {
    if (!digResults?.refresher) return null;
    const r = digResults.refresher;
    return (
      <div className={'p-5 rounded-2xl border-2 ' + c.infoBox}>
        <div className="flex items-center gap-2 mb-3">
          <span className="text-lg">🔍</span>
          <p className={'text-base font-bold ' + c.text}>{t('tg_dig_title')} {digResults.concept || digTarget}</p>
        </div>

        {r.in_plain_english && (
          <div className={'p-4 rounded-xl ' + c.card + ' border mb-3'}>
            <p className={'text-xs font-bold ' + c.textMuted + ' uppercase mb-1'}>{t('tg_plain_english')}</p>
            <p className={'text-sm ' + c.text}>{r.in_plain_english}</p>
          </div>
        )}

        {r.key_insight && (
          <div className={'p-3 rounded-lg ' + c.tipBg + ' mb-3'}>
            <p className={'text-xs font-bold ' + c.tipText + ' mb-1'}>{t('tg_key_insight')}</p>
            <p className={'text-sm ' + c.tipText}>{r.key_insight}</p>
          </div>
        )}

        {r.visual_or_analogy && (
          <div className={'p-3 rounded-lg ' + c.cardAlt + ' mb-3'}>
            <p className={'text-xs font-bold ' + c.textMuted + ' mb-1'}>{t('tg_analogy')}</p>
            <p className={'text-xs ' + c.text}>{r.visual_or_analogy}</p>
          </div>
        )}

        {r.formal_definition && (
          <div className={'p-3 rounded-lg ' + c.cardAlt + ' mb-3'}>
            <p className={'text-xs font-bold ' + c.textMuted + ' mb-1'}>{t('tg_formal_def')}</p>
            <p className={'text-xs ' + c.text + ' font-mono'}>{r.formal_definition}</p>
          </div>
        )}

        {r.worked_example && (
          <div className={c.card + ' border rounded-xl p-4 mb-3'}>
            <p className={'text-xs font-bold ' + c.textMuted + ' uppercase mb-1'}>{t('tg_worked_example')}</p>
            <p className={'text-sm ' + c.text + ' whitespace-pre-wrap'}>{r.worked_example}</p>
          </div>
        )}

        {r.practice_problems?.length > 0 && (
          <div className="space-y-2 mb-3">
            <p className={'text-xs font-bold ' + c.textMuted + ' uppercase'}>{t('tg_dig_practice')}</p>
            {r.practice_problems.map((pp, i) => (
              <div key={i} className={c.card + ' border rounded-xl p-3'}>
                <p className={'text-xs font-semibold ' + c.text + ' mb-1'}>{t('tg_problem')} {i + 1}: {pp.problem}</p>
                <details className="mt-1">
                  <summary className={'text-[10px] font-bold cursor-pointer ' + linkStyle}>{t('tg_show_hint')}</summary>
                  <div className="mt-2 space-y-1">
                    {pp.hint && <p className={'text-[10px] ' + c.tipText}>{t('tg_hint')} {pp.hint}</p>}
                    <p className={'text-[10px] ' + c.successTxt}>✅ {pp.answer}</p>
                  </div>
                </details>
              </div>
            ))}
          </div>
        )}

        {r.common_mistakes?.length > 0 && (
          <div className="space-y-1 mb-3">
            {r.common_mistakes.map((m, i) => (
              <div key={i} className={'p-2 rounded-lg ' + c.warningBox}>
                <p className={'text-[10px] ' + c.warningTxt}>⚠️ {m}</p>
              </div>
            ))}
          </div>
        )}

        {digResults.connects_forward && (
          <div className={'p-3 rounded-lg ' + c.successBox}>
            <p className={'text-xs font-bold ' + c.successTxt + ' mb-1'}>{t('tg_now_connects')}</p>
            <p className={'text-xs ' + c.successTxt}>{digResults.connects_forward}</p>
          </div>
        )}
      </div>
    );
  };

  // History
  const renderHistory = () => {
    if (sessionHistory.length === 0) return null;
    const formatDate = (iso) => { try { const d = new Date(iso); const diff = Math.floor((new Date() - d) / 86400000); return diff === 0 ? t('tg_today') : diff === 1 ? t('tg_yesterday') : diff < 7 ? t('tg_days_ago', { count: diff }) : d.toLocaleDateString(userLocale || 'en-US', { month: 'short', day: 'numeric' }); } catch { return ''; } };
    return (
      <div className={'mt-6 p-4 rounded-2xl border ' + c.histBg}>
        <button onClick={() => setShowHistory(!showHistory)} className="w-full flex items-center gap-2 text-start">
          <span>📝</span>
          <span className={'text-sm font-bold ' + c.text + ' flex-1'}>{t('tg_past_gaps')}</span>
          <span className={'text-xs ' + c.textMuted}>{sessionHistory.length}</span>
          <span className={'text-xs ' + c.textMuted}>{showHistory ? '▲' : '▼'}</span>
        </button>
        {showHistory && (
          <div className="mt-3 space-y-2">
            {sessionHistory.map(entry => (
              <div key={entry.id} className={'rounded-xl border ' + c.card + ' p-3'}>
                <div className={'text-sm font-semibold ' + c.text}>{entry.concept}</div>
                <div className={'text-xs ' + c.textMuted + ' mt-0.5'}>
                  {formatDate(entry.date)}{entry.subject ? ` · ${entry.subject}` : ''}{entry.likelyGap ? ` · ${t('tg_hist_gap')} ${entry.likelyGap}` : ''}
                </div>
              </div>
            ))}
          </div>
        )}
      </div>
    );
  };

  return (
    <div className={`space-y-4 ${c.text}`}>
      {!results && renderInput()}
      {results ? (
        <div className={`${c.card} border ${c.border} rounded-xl shadow-sm p-5`}>
          <div className="pb-3 border-b border-zinc-500">
            <div className="flex items-start justify-between">
              <div>
                <h2 className={'text-xl font-bold ' + c.text}>
                  <span className="me-2">{tool?.icon ?? '🔍'}</span>{tool?.title ?? t('tg_title')}
                </h2>
                <p className={'text-sm ' + c.textSecondary}>{tool?.tagline ?? t('tg_tagline')}</p>
              </div>
              <button onClick={handleReset} className={`${c.btnSecondary} px-3 py-1.5 rounded-lg text-xs font-bold flex-shrink-0`}>
                {t('tg_start_over')}
              </button>
            </div>
          </div>
        </div>
      ) : null}
      {results && renderResults()}
      {results && (
        <div className={'p-4 rounded-2xl border ' + c.card}>
          <p className={'text-xs font-bold ' + c.textMuted + ' uppercase tracking-wide mb-2'}>{t('tg_related_tools')}</p>
          <p className={'text-xs ' + c.textSecondary}>{t('tg_related_text')} <a href="/TheCrux" className={linkStyle}>🧠 {t('tg_recall_link')}</a> {t('tg_related_rest')}</p>
        </div>
      )}
      {error && (
        <div className={'mt-4 p-4 ' + c.danger + ' border rounded-xl flex items-start gap-3'}>
          <span className="text-base">⚠️</span>
          <p className="text-sm">{error}</p>
        </div>
      )}
      {renderHistory()}
    </div>
  );
};

TheGap.displayName = 'TheGap';
export default TheGap;
