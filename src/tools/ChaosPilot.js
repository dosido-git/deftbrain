import React, { useState } from 'react';
import { useTheme } from '../hooks/useTheme';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { CopyBtn, ActionBar } from '../components/ActionButtons';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const EXAMPLE_ROUTINES = [
  {
    routine: 'Wake up, make coffee, check phone for 20 mins, work from home 9-5, eat lunch at desk, gym 3x/week after work, cook or order dinner, watch TV or scroll until 11pm, repeat.',
    context: 'Remote software engineer, been in this city 2 years, same friend group, same bars',
    stuck: 'Everything feels fine but nothing feels exciting. The weeks blur together.',
  },
  {
    routine: 'Up at 6:30, get kids ready, commute 45 mins, back-to-back meetings all day, commute home, dinner with family, help with homework, collapse on couch, bed by 10.',
    context: 'Manager at a mid-size company, two kids under 10, been in the same role 4 years',
    stuck: 'No time for anything that feels like mine. Running on fumes.',
  },
  {
    routine: 'Sleep until 9, freelance work from home (highly variable hours), social media between tasks, lunch whenever, occasional gym, evenings very unstructured.',
    context: 'Freelance designer, work is good but inconsistent, mostly works alone',
    stuck: 'Productive some days, completely derailed others. No real rhythm.',
  },
];

const ChaosPilot = () => {
  const { theme } = useTheme();
  const isDark = theme === 'dark';
  const { callToolEndpoint, loading } = useClaudeAPI();

  const c = {
    pageBg:       isDark ? 'bg-[#1a1816]'    : 'bg-[#faf8f5]',
    card:         isDark ? 'bg-[#2a2623]'     : 'bg-white',
    cardAlt:      isDark ? 'bg-[#332e2a]'     : 'bg-[#faf8f5]',
    cardBorder:   isDark ? 'border-[#3d3630]' : 'border-[#e8e1d5]',
    input:        isDark ? 'bg-[#1a1816] border-[#3d3630] text-[#f0eeea] placeholder:text-[#8a8275]'
                         : 'bg-[#faf8f5] border-[#d5cab8] text-[#3d3935] placeholder:text-[#8a8275]',
    inputFocus:   'focus:outline-none focus:ring-2 focus:ring-[#2c4a6e]/20 focus:border-[#4a6a8a]',
    text:          isDark ? 'text-[#f0eeea]'  : 'text-[#3d3935]',
    textSecondary: isDark ? 'text-[#c8c3b9]'  : 'text-[#5a544a]',
    textMuted:     isDark ? 'text-[#8a8275]'  : 'text-[#8a8275]',
    heading:       isDark ? 'text-[#f3efe8]'  : 'text-[#1e2a3a]',
    btnPrimary:    isDark ? 'bg-[#2c4a6e] hover:bg-[#4a6a8a] text-white' : 'bg-[#2c4a6e] hover:bg-[#1e3a58] text-white',
    btnSecondary:  isDark ? 'bg-[#332e2a] hover:bg-[#3d3630] text-[#c8c3b9] border border-[#3d3630]' : 'bg-[#f3efe8] hover:bg-[#e8e1d5] text-[#5e5042] border border-[#d5cab8]',
    divider:       isDark ? 'border-[#3d3630]' : 'border-[#e8e1d5]',
    error:         isDark ? 'bg-[#b54a3f]/15 border-[#b54a3f]/50 text-[#e8a9a3]' : 'bg-[#fceae8] border-[#d4908a] text-[#8a3028]',
    goldBg:        isDark ? 'bg-[#c8872e]/15 border-[#c8872e]/40' : 'bg-[#fdf3e4] border-[#e0c49a]',
    goldText:      isDark ? 'text-[#d9a04e]' : 'text-[#93541f]',
  };

  const [routine, setRoutine] = useState('');
  const [context, setContext] = useState('');
  const [goals, setGoals] = useState('');
  const [whatsFeelingStuck, setWhatsFeelingStuck] = useState('');
  const [results, setResults] = useState(null);
  const [error, setError] = useState('');

  const handleSubmit = async () => {
    if (!routine.trim()) return;
    setError(''); setResults(null);
    try {
      const data = await callToolEndpoint('chaos-pilot', {
        routine: routine.trim(),
        context: context.trim() || undefined,
        goals: goals.trim() || undefined,
        whatsFeelingStuck: whatsFeelingStuck.trim() || undefined,
      });
      setResults(data);
    } catch (e) { setError(e.message || 'Failed to design your disruption.'); }
  };

  const handleExample = () => {
    const ex = EXAMPLE_ROUTINES[Math.floor(Math.random() * EXAMPLE_ROUTINES.length)];
    setRoutine(ex.routine);
    setContext(ex.context || '');
    setWhatsFeelingStuck(ex.stuck || '');
    setResults(null);
  };

  const buildText = () => {
    if (!results) return '';
    const d = results.the_disruption;
    const p = results.pattern_diagnosis;
    let t = `🎰 CHAOS PILOT\n\n`;
    t += `THE INVISIBLE RUT:\n${p?.the_invisible_rut}\n${p?.what_its_costing}\n\n`;
    t += `THE DISRUPTION:\n${d?.the_full_instruction}\n\n`;
    t += `When: ${d?.when}\n`;
    if (d?.the_slight_discomfort) t += `The friction you'll feel: ${d.the_slight_discomfort}\n\n`;
    if (results.the_downstream_effect?.compound_effect) t += `Compound effect: ${results.the_downstream_effect.compound_effect}\n\n`;
    if (results.if_they_resist) t += `If you want to skip it: ${results.if_they_resist}\n`;
    return t + BRAND;
  };

  return (
    <div className={`min-h-screen py-8 px-4 ${c.pageBg}`}>
      <div className="max-w-2xl mx-auto">

        {/* Header */}
        <div className="text-center mb-8">
          <div className="text-5xl mb-3">🎰</div>
          <h1 className={`text-3xl font-black tracking-tight mb-2 ${c.heading}`}>Chaos Pilot</h1>
          <p className={`text-sm ${c.textMuted}`}>One calculated disruption. Not random — strategically chaotic.</p>
        </div>

        {/* Input */}
        {!results && (
          <div className={`rounded-2xl border p-6 shadow-sm space-y-4 ${c.card} ${c.cardBorder}`}>
            <div>
              <label className={`block text-sm font-semibold mb-1.5 ${c.text}`}>
                Your typical week, hour by hour <span className="text-red-400">*</span>
              </label>
              <textarea value={routine} onChange={e => setRoutine(e.target.value)}
                placeholder="Walk me through a normal week — wake up time, work routine, evenings, weekends. The more specific, the better the disruption."
                rows={5} maxLength={800}
                className={`w-full px-4 py-3 rounded-xl border text-sm resize-none ${c.input} ${c.inputFocus}`} />
            </div>
            <div className="grid grid-cols-1 sm:grid-cols-2 gap-3">
              <div>
                <label className={`block text-sm font-semibold mb-1.5 ${c.text}`}>Context about you <span className={`font-normal ${c.textMuted}`}>(optional)</span></label>
                <textarea value={context} onChange={e => setContext(e.target.value)}
                  placeholder="Job, city, living situation, life stage…"
                  rows={2} maxLength={300}
                  className={`w-full px-4 py-3 rounded-xl border text-sm resize-none ${c.input} ${c.inputFocus}`} />
              </div>
              <div>
                <label className={`block text-sm font-semibold mb-1.5 ${c.text}`}>What's feeling stale? <span className={`font-normal ${c.textMuted}`}>(optional)</span></label>
                <textarea value={whatsFeelingStuck} onChange={e => setWhatsFeelingStuck(e.target.value)}
                  placeholder="What's the vague dissatisfaction you can't quite name…"
                  rows={2} maxLength={300}
                  className={`w-full px-4 py-3 rounded-xl border text-sm resize-none ${c.input} ${c.inputFocus}`} />
              </div>
            </div>
            {error && <div className={`p-3 rounded-xl border text-sm ${c.error}`}><span className="mr-1">⚠️</span>{error}</div>}
            <div className="flex gap-2">
              <button onClick={handleSubmit} disabled={loading || !routine.trim()}
                className={`flex-1 py-3 rounded-xl font-bold disabled:opacity-40 ${c.btnPrimary}`}>
                {loading ? <><span className="animate-spin inline-block mr-2">⏳</span>Designing your disruption…</> : '🎰 Give Me My Disruption'}
              </button>
              <button onClick={handleExample} disabled={loading}
                className={`px-4 py-3 rounded-xl text-sm font-medium ${c.btnSecondary}`}>
                Example
              </button>
            </div>
          </div>
        )}

        {/* Results */}
        {results && (
          <div className="space-y-4">

            {/* Pattern diagnosis */}
            <div className={`rounded-2xl border p-5 ${c.card} ${c.cardBorder}`}>
              <p className={`text-xs font-black uppercase tracking-widest mb-3 ${c.textMuted}`}>🔍 The Invisible Rut</p>
              <p className={`text-base font-bold mb-2 ${c.text}`}>{results.pattern_diagnosis?.the_invisible_rut}</p>
              {results.pattern_diagnosis?.why_its_invisible && (
                <p className={`text-sm mb-2 ${c.textSecondary}`}>{results.pattern_diagnosis.why_its_invisible}</p>
              )}
              {results.pattern_diagnosis?.what_its_costing && (
                <div className={`mt-3 p-3 rounded-xl border ${isDark ? 'bg-[#b54a3f]/10 border-[#b54a3f]/30' : 'bg-[#fceae8] border-[#d4908a]'}`}>
                  <p className={`text-xs font-bold uppercase tracking-wide mb-1 ${isDark ? 'text-[#e8a9a3]' : 'text-[#8a3028]'}`}>What it's costing you</p>
                  <p className={`text-sm ${c.textSecondary}`}>{results.pattern_diagnosis.what_its_costing}</p>
                </div>
              )}
            </div>

            {/* THE DISRUPTION — hero card */}
            {results.the_disruption && (
              <div className={`rounded-2xl border-2 overflow-hidden ${isDark ? 'border-[#c8872e]/60 bg-[#2a2623]' : 'border-[#c8872e] bg-white'}`}>
                <div className={`px-6 py-4 ${isDark ? 'bg-[#c8872e]/10' : 'bg-[#fdf3e4]'}`}>
                  <p className={`text-xs font-black uppercase tracking-widest mb-1 ${c.goldText}`}>🎰 Your Disruption</p>
                  <p className={`text-lg font-bold ${c.text}`}>{results.the_disruption.what}</p>
                  {results.the_disruption.when && (
                    <p className={`text-sm mt-1 font-medium ${c.goldText}`}>📅 {results.the_disruption.when}</p>
                  )}
                </div>
                <div className="px-6 py-5 space-y-4">
                  {results.the_disruption.the_full_instruction && (
                    <p className={`text-sm leading-relaxed ${c.textSecondary}`} style={{ fontStyle: 'italic' }}>
                      "{results.the_disruption.the_full_instruction}"
                    </p>
                  )}
                  {results.the_disruption.the_slight_discomfort && (
                    <div className={`p-3 rounded-xl border ${isDark ? 'bg-[#3d3630]/60 border-[#3d3630]' : 'bg-[#f3efe8] border-[#d5cab8]'}`}>
                      <p className={`text-xs font-bold uppercase tracking-wide mb-1 ${c.textMuted}`}>😬 The friction you'll feel</p>
                      <p className={`text-sm ${c.textSecondary}`}>{results.the_disruption.the_slight_discomfort}</p>
                    </div>
                  )}
                  {results.the_disruption.why_this_one && (
                    <div>
                      <p className={`text-xs font-bold uppercase tracking-wide mb-1 ${c.textMuted}`}>Why this one specifically</p>
                      <p className={`text-sm ${c.textSecondary}`}>{results.the_disruption.why_this_one}</p>
                    </div>
                  )}
                </div>
              </div>
            )}

            {/* Downstream effects */}
            {results.the_downstream_effect && (
              <div className={`rounded-2xl border p-5 space-y-3 ${c.card} ${c.cardBorder}`}>
                <p className={`text-xs font-black uppercase tracking-widest ${c.textMuted}`}>⚡ The Ripple Effect</p>
                {[
                  { key: 'immediate', label: 'First 30 minutes', icon: '⚡' },
                  { key: 'within_a_week', label: 'Within a week', icon: '🌊' },
                  { key: 'compound_effect', label: 'The door that opens', icon: '🚪' },
                ].map(row => results.the_downstream_effect[row.key] && (
                  <div key={row.key} className="flex gap-3">
                    <span className="flex-shrink-0 text-lg">{row.icon}</span>
                    <div>
                      <p className={`text-xs font-bold uppercase tracking-wide mb-0.5 ${c.textMuted}`}>{row.label}</p>
                      <p className={`text-sm ${c.textSecondary}`}>{results.the_downstream_effect[row.key]}</p>
                    </div>
                  </div>
                ))}
              </div>
            )}

            {/* The resistance */}
            {results.if_they_resist && (
              <div className={`rounded-2xl border p-4 ${c.goldBg}`}>
                <p className={`text-xs font-bold uppercase tracking-wider mb-2 ${c.goldText}`}>🧠 When You Want to Skip It</p>
                <p className={`text-sm ${c.textSecondary}`}>{results.if_they_resist}</p>
              </div>
            )}

            {/* Actions */}
            <div className="flex items-center gap-3 flex-wrap">
              <ActionBar content={buildText()} title="Chaos Pilot" />
              <button onClick={() => { setResults(null); setRoutine(''); setContext(''); setGoals(''); setWhatsFeelingStuck(''); }}
                className={`px-4 py-2 rounded-lg text-sm font-medium ${c.btnSecondary}`}>
                🔄 New Disruption
              </button>
            </div>

            {/* Cross-references */}
            <div className={`rounded-xl border p-4 ${c.cardAlt} ${c.cardBorder}`}>
              <p className={`text-xs font-semibold uppercase tracking-wide mb-3 ${c.textMuted}`}>Related tools</p>
              <div className="flex flex-wrap gap-2">
                {[
                  { id: 'OnePercenter', icon: '⚡', label: 'Find your highest-leverage habit change' },
                  { id: 'LuckSurface', icon: '🧲', label: 'Expand your luck surface area' },
                  { id: 'GravityWell', icon: '🌀', label: 'Pull the right people into your orbit' },
                ].map(r => (
                  <a key={r.id} href={`/tool/${r.id}`} target="_blank" rel="noopener noreferrer"
                    className={`px-3 py-1.5 rounded-lg text-xs font-medium transition-colors ${c.btnSecondary}`}>
                    {r.icon} {r.label}
                  </a>
                ))}
              </div>
            </div>
          </div>
        )}
      </div>
    </div>
  );
};

ChaosPilot.displayName = 'ChaosPilot';
export default ChaosPilot;
