import React, { useState, useCallback, useRef, useEffect } from 'react';
import { useTheme } from '../hooks/useTheme';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { usePersistentState } from '../hooks/usePersistentState';
import { ActionBar } from '../components/ActionButtons';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const EXAMPLE_ROUTINES = [
  {
    routine: 'Wake up, make coffee, check phone for 20 mins, work from home 9-5, eat lunch at desk, gym 3x/week after work, cook or order dinner, watch TV or scroll until 11pm, repeat.',
    context: 'Remote software engineer, been in this city 2 years, same friend group, same bars',
    stuck: 'Everything feels fine but nothing feels exciting. The weeks blur together.',
  },
  {
    routine: 'Up at 6:30, get kids ready, commute 45 mins, back-to-back meetings all day, commute home, dinner with family, help with homework, collapse on couch, bed by 10.',
    context: 'Manager at a mid-size company, two kids under 10, been in the same role 4 years',
    stuck: 'No time for anything that feels like mine. Running on fumes.',
  },
  {
    routine: 'Sleep until 9, freelance work from home (highly variable hours), social media between tasks, lunch whenever, occasional gym, evenings very unstructured.',
    context: 'Freelance designer, work is good but inconsistent, mostly works alone',
    stuck: 'Productive some days, completely derailed others. No real rhythm.',
  },
];

// ════════════════════════════════════════════════════════════
// MAIN COMPONENT
// ════════════════════════════════════════════════════════════
const ChaosPilot = ({ tool }) => {
  const { isDark } = useTheme();

  useEffect(() => {
    const handler = (e) => {
      const tag = document.activeElement?.tagName;
      if (tag === 'INPUT' || tag === 'TEXTAREA' || tag === 'SELECT') return;
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && !loading) handleSubmit();
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
  }, [loading]);
  const { callToolEndpoint, loading } = useClaudeAPI();


  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';

  const c = {
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:         isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20' : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-500' : 'text-gray-400',
    labelText:     isDark ? 'text-zinc-200' : 'text-gray-700',
    accentTxt:     isDark ? 'text-cyan-400' : 'text-cyan-600',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white' : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200' : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200' : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200' : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200' : 'bg-red-50 border-red-200 text-red-800',
    infoBox:       isDark ? 'bg-sky-900/20 border-sky-700 text-sky-200' : 'bg-sky-50 border-sky-200 text-sky-800',
    successBox:    isDark ? 'bg-emerald-900/20 border-emerald-700' : 'bg-emerald-50 border-emerald-300',
    successTxt:    isDark ? 'text-emerald-300' : 'text-emerald-800',
    warningBox:    isDark ? 'bg-amber-900/20 border-amber-700' : 'bg-amber-50 border-amber-300',
    warningTxt:    isDark ? 'text-amber-300' : 'text-amber-800',
    pillActive:    isDark ? 'border-cyan-500 bg-cyan-900/30 text-cyan-200' : 'border-cyan-600 bg-cyan-100 text-cyan-900',
    pillInactive:  isDark ? 'border-zinc-600 text-zinc-400 hover:border-zinc-500' : 'border-gray-300 text-gray-500 hover:border-gray-400',
  };

  // ── Persisted state ──
  const [routine, setRoutine] = usePersistentState('chaos-pilot-routine', '');
  const [context, setContext] = usePersistentState('chaos-pilot-context', '');
  const [goals, setGoals] = usePersistentState('chaos-pilot-goals', '');
  const [whatsFeelingStuck, setWhatsFeelingStuck] = usePersistentState('chaos-pilot-stuck', '');
  const [results, setResults] = usePersistentState('chaos-pilot-results', null);
  const [history, setHistory] = usePersistentState('chaos-pilot-history', []);

  const [error, setError] = useState('');
  const resultsRef = useRef(null);

  // ── Ctrl+Enter submit handler ──
  const handleKeyDown = useCallback((e) => {
    if ((e.ctrlKey || e.metaKey) && e.key === 'Enter') {
      e.preventDefault();
      if (!loading && routine.trim()) handleSubmit();
    }
  }, [loading, routine]); // eslint-disable-line react-hooks/exhaustive-deps

  const handleSubmit = async () => {
    if (!routine.trim()) return;
    setError('');
    setResults(null);
    try {
      const data = await callToolEndpoint('chaos-pilot', {
        routine: routine.trim(),
        context: context.trim() || undefined,
        goals: goals.trim() || undefined,
        whatsFeelingStuck: whatsFeelingStuck.trim() || undefined,
      });
      setResults(data);
      // Save to history
      const entry = {
        id: Date.now(),
        timestamp: new Date().toISOString(),
        preview: routine.trim().slice(0, 40),
        result: data,
      };
      setHistory(prev => [entry, ...prev].slice(0, 6));
    } catch (e) {
      setError(e.message || 'Failed to design your disruption.');
    }
  };

  const handleReset = () => {
    setResults(null);
    setRoutine('');
    setContext('');
    setGoals('');
    setWhatsFeelingStuck('');
    setError('');
  };

  const handleExample = () => {
    const ex = EXAMPLE_ROUTINES[Math.floor(Math.random() * EXAMPLE_ROUTINES.length)];
    setRoutine(ex.routine);
    setContext(ex.context || '');
    setWhatsFeelingStuck(ex.stuck || '');
    setResults(null);
    setError('');
  };

  const buildText = () => {
    if (!results) return '';
    const d = results.the_disruption;
    const p = results.pattern_diagnosis;
    let t = `🎰 CHAOS PILOT\n\n`;
    t += `THE INVISIBLE RUT:\n${p?.the_invisible_rut}\n`;
    if (p?.why_its_invisible) t += `${p.why_its_invisible}\n`;
    if (p?.what_its_costing) t += `What it's costing you: ${p.what_its_costing}\n`;
    t += `\nTHE DISRUPTION:\n${d?.what}\n`;
    if (d?.when) t += `When: ${d.when}\n`;
    if (d?.the_full_instruction) t += `\n${d.the_full_instruction}\n`;
    if (d?.the_slight_discomfort) t += `\nThe friction you'll feel: ${d.the_slight_discomfort}\n`;
    if (d?.why_this_one) t += `Why this one: ${d.why_this_one}\n`;
    if (results.the_downstream_effect) {
      t += `\nTHE RIPPLE EFFECT:\n`;
      if (results.the_downstream_effect.immediate) t += `First 30 min: ${results.the_downstream_effect.immediate}\n`;
      if (results.the_downstream_effect.within_a_week) t += `Within a week: ${results.the_downstream_effect.within_a_week}\n`;
      if (results.the_downstream_effect.compound_effect) t += `The door that opens: ${results.the_downstream_effect.compound_effect}\n`;
    }
    if (results.if_they_resist) t += `\nWhen you want to skip it: ${results.if_they_resist}\n`;
    return t + BRAND;
  };

  return (
    <div className={`min-h-screen py-8 px-4 ${c.cardAlt}`}>
      <div className="max-w-2xl mx-auto space-y-4">

        {/* ── Input Card ── */}
        <div className={`${c.card} rounded-xl shadow-lg p-5 border ${c.border}`}>
          {/* Standard header */}
          <div className={`mb-4 pb-3 border-b ${c.border}`}>
            <h2 className={`text-xl font-bold ${c.text} flex items-center gap-2`}>
              <span>{tool?.icon ?? '🎰'}</span>{tool?.title ?? 'Chaos Pilot'}
            </h2>
            <p className={`text-sm ${c.textSecondaryondary}`}>{tool?.tagline ?? 'One calculated disruption. Not random — strategically chaotic.'}</p>
          </div>

          {/* Pre-result cross-ref */}
          <p className={`text-xs text-center ${c.textMuteded} mb-4`}>
            Want to find the single highest-leverage habit change?{' '}
            <a href="/OnePercenter" className={linkStyle}>One Percenter</a> does the math.
          </p>

          <div className="space-y-4">
            {/* Required field */}
            <div>
              <label className={`block text-sm font-semibold mb-1.5 ${c.text}`}>
                Your typical week, hour by hour <span className={c.textMuteded}>*</span>
              </label>
              <textarea
                value={routine}
                onChange={e => setRoutine(e.target.value)}
                onKeyDown={handleKeyDown}
                placeholder="Walk me through a normal week — wake up time, work routine, evenings, weekends. The more specific, the better the disruption."
                rows={5}
                maxLength={800}
                className={`w-full px-4 py-3 rounded-xl border text-sm resize-none focus:outline-none transition-colors ${c.input}`}
              />
              <p className={`text-xs ${c.textMuteded} mt-1`}>Ctrl+Enter to submit</p>
            </div>

            <div className="grid grid-cols-1 sm:grid-cols-2 gap-3">
              <div>
                <label className={`block text-sm font-semibold mb-1.5 ${c.text}`}>
                  Context about you <span className={`font-normal ${c.textMuteded}`}>(optional)</span>
                </label>
                <textarea
                  value={context}
                  onChange={e => setContext(e.target.value)}
                  onKeyDown={handleKeyDown}
                  placeholder="Job, city, living situation, life stage…"
                  rows={2}
                  maxLength={300}
                  className={`w-full px-4 py-3 rounded-xl border text-sm resize-none focus:outline-none transition-colors ${c.input}`}
                />
              </div>
              <div>
                <label className={`block text-sm font-semibold mb-1.5 ${c.text}`}>
                  What's feeling stale? <span className={`font-normal ${c.textMuteded}`}>(optional)</span>
                </label>
                <textarea
                  value={whatsFeelingStuck}
                  onChange={e => setWhatsFeelingStuck(e.target.value)}
                  onKeyDown={handleKeyDown}
                  placeholder="What's the vague dissatisfaction you can't quite name…"
                  rows={2}
                  maxLength={300}
                  className={`w-full px-4 py-3 rounded-xl border text-sm resize-none focus:outline-none transition-colors ${c.input}`}
                />
              </div>
            </div>

            {error && (
              <div className={`p-3 rounded-xl border text-sm ${c.danger}`}>
                <span className="mr-1">⚠️</span>{error}
              </div>
            )}

            <div className="flex gap-2">
              <button
                onClick={handleSubmit}
                disabled={loading || !routine.trim()}
                className={`flex-1 w-full py-3 min-h-[48px] rounded-xl font-bold disabled:opacity-40 transition-colors ${c.btnPrimary}`}
              >
                {loading
                  ? <><span className="animate-spin inline-block mr-2">{tool?.icon ?? '🎰'}</span>Designing your disruption…</>
                  : <><span>{tool?.icon ?? '🎰'}</span> Give Me My Disruption</>}
              </button>
              <button
                onClick={handleExample}
                disabled={loading}
                className={`px-4 py-3 rounded-xl text-sm font-medium transition-colors ${c.btnSecondaryondary}`}
              >
                Example
              </button>
            </div>
          </div>
        </div>

        {/* ── History panel ── */}
        {history.length > 0 && !results && (
          <div className={`${c.card} rounded-xl border ${c.border} p-4`}>
            <h3 className={`text-sm font-bold ${c.text} mb-3`}>🕐 Recent Disruptions</h3>
            <div className="space-y-1.5">
              {history.map(entry => (
                <button
                  key={entry.id}
                  onClick={() => setResults(entry.result)}
                  className={`w-full text-left px-3 py-2 rounded-lg ${c.btnSecondaryondary} text-xs`}
                >
                  <span className={c.textMuteded}>
                    {new Date(entry.timestamp).toLocaleDateString(undefined, { month: 'short', day: 'numeric', hour: 'numeric', minute: '2-digit' })}
                  </span>
                  <span className={`ml-2 ${c.text}`}>{entry.preview}…</span>
                </button>
              ))}
            </div>
          </div>
        )}

        {/* ── Results ── */}
        {results && (
          <div ref={resultsRef} className="space-y-4">

            {/* ActionBar — top of results */}
            <div className="flex items-center justify-between gap-3 flex-wrap">
              <ActionBar content={buildText()} title="Chaos Pilot" />
              <div className="flex gap-2">
                <button
                  onClick={handleSubmit}
                  disabled={loading || !routine.trim()}
                  className={`px-4 py-2 rounded-lg text-sm font-bold disabled:opacity-40 transition-colors ${c.btnPrimary}`}
                >
                  {loading ? <span className="animate-spin inline-block">{tool?.icon ?? '🎰'}</span> : <><span>🎲</span> Different Disruption</>}
                </button>
                <button
                  onClick={handleReset}
                  className={`px-4 py-2 rounded-lg text-sm font-medium transition-colors ${c.btnSecondaryondary}`}
                >
                  ↺ Start Over
                </button>
              </div>
            </div>

            {/* Pattern diagnosis */}
            <div className={`${c.card} rounded-xl border ${c.border} p-5`}>
              <p className={`text-xs font-black uppercase tracking-widest mb-3 ${c.textMuteded}`}>🔍 The Invisible Rut</p>
              <p className={`text-base font-bold mb-2 ${c.text}`}>{results.pattern_diagnosis?.the_invisible_rut}</p>
              {results.pattern_diagnosis?.why_its_invisible && (
                <p className={`text-sm mb-2 ${c.textSecondaryondary}`}>{results.pattern_diagnosis.why_its_invisible}</p>
              )}
              {results.pattern_diagnosis?.what_its_costing && (
                <div className={`mt-3 p-3 rounded-xl border ${c.danger}`}>
                  <p className={`text-xs font-bold uppercase tracking-wide mb-1 ${c.textDanger}`}>What it's costing you</p>
                  <p className={`text-sm ${c.textDanger}`}>{results.pattern_diagnosis.what_its_costing}</p>
                </div>
              )}
            </div>

            {/* THE DISRUPTION — hero card */}
            {results.the_disruption && (
              <div className={`rounded-xl border-2 overflow-hidden ${c.disruptionHero}`}>
                <div className={`px-6 py-4 ${c.disruptionHead}`}>
                  <p className={`text-xs font-black uppercase tracking-widest mb-1 ${c.disruptionAccent}`}>🎰 Your Disruption</p>
                  <p className={`text-lg font-bold ${c.text}`}>{results.the_disruption.what}</p>
                  {results.the_disruption.when && (
                    <p className={`text-sm mt-1 font-medium ${c.disruptionAccent}`}>📅 {results.the_disruption.when}</p>
                  )}
                </div>
                <div className="px-6 py-5 space-y-4">
                  {results.the_disruption.the_full_instruction && (
                    <p className={`text-sm leading-relaxed italic ${c.textSecondaryondary}`}>
                      "{results.the_disruption.the_full_instruction}"
                    </p>
                  )}
                  {results.the_disruption.the_slight_discomfort && (
                    <div className={`p-3 rounded-xl border ${c.disruptionSub}`}>
                      <p className={`text-xs font-bold uppercase tracking-wide mb-1 ${c.textMuteded}`}>😬 The friction you'll feel</p>
                      <p className={`text-sm ${c.textSecondaryondary}`}>{results.the_disruption.the_slight_discomfort}</p>
                    </div>
                  )}
                  {results.the_disruption.why_this_one && (
                    <div>
                      <p className={`text-xs font-bold uppercase tracking-wide mb-1 ${c.textMuteded}`}>Why this one specifically</p>
                      <p className={`text-sm ${c.textSecondaryondary}`}>{results.the_disruption.why_this_one}</p>
                    </div>
                  )}
                </div>
              </div>
            )}

            {/* Downstream effects */}
            {results.the_downstream_effect && (
              <div className={`${c.card} rounded-xl border ${c.border} p-5 space-y-3`}>
                <p className={`text-xs font-black uppercase tracking-widest ${c.textMuteded}`}>⚡ The Ripple Effect</p>
                {[
                  { key: 'immediate',     label: 'First 30 minutes',  icon: '⚡' },
                  { key: 'within_a_week', label: 'Within a week',     icon: '🌊' },
                  { key: 'compound_effect', label: 'The door that opens', icon: '🚪' },
                ].map(row => results.the_downstream_effect[row.key] && (
                  <div key={row.key} className="flex gap-3">
                    <span className="flex-shrink-0 text-lg">{row.icon}</span>
                    <div>
                      <p className={`text-xs font-bold uppercase tracking-wide mb-0.5 ${c.textMuteded}`}>{row.label}</p>
                      <p className={`text-sm ${c.textSecondaryondary}`}>{results.the_downstream_effect[row.key]}</p>
                    </div>
                  </div>
                ))}
              </div>
            )}

            {/* Resistance */}
            {results.if_they_resist && (
              <div className={`rounded-xl border p-4 ${c.warning}`}>
                <p className={`text-xs font-bold uppercase tracking-wider mb-2`}>🧠 When You Want to Skip It</p>
                <p className={`text-sm`}>{results.if_they_resist}</p>
              </div>
            )}

            {/* Cross-refs — post-result */}
            <div className={`${c.cardAlt} rounded-xl border ${c.border} p-4 space-y-2`}>
              <p className={`text-xs ${c.textMuteded} text-center`}>
                Want to engineer a lasting change?{' '}
                <a href="/AlternatePath" className={linkStyle}>Alternate Path</a>{' '}
                maps the concrete steps between where you are and where you want to be.
              </p>
              {results.the_disruption?.why_this_one && (
                <p className={`text-xs ${c.textMuteded} text-center`}>
                  Want more people to serendipitously cross your path?{' '}
                  <a href="/LuckSurface" className={linkStyle}>Luck Surface</a>{' '}
                  expands where opportunity can find you.
                </p>
              )}
            </div>

            {/* AI disclaimer */}
            <p className={`text-xs text-center ${c.textMuteded}`}>
              AI-generated — treat as a creative prompt, not a prescription.
            </p>

          </div>
        )}

      </div>
    </div>
  );
};

ChaosPilot.displayName = 'ChaosPilot';
export default ChaosPilot;
