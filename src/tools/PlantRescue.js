import React, { useState, useRef, useCallback, useEffect } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { usePersistentState } from '../hooks/usePersistentState';
import { CopyBtn } from '../components/ActionButtons';
import { useRegisterActions } from '../components/ActionBarContext';
import { compressImage, CompressionPresets } from '../utils/imageCompression';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const EXAMPLE = {
  mode: 'rescue',
  plantDescription: "Pothos in a 6-inch pot. Leaves yellowing from the bottom up over the past two weeks. A few have brown tips. New growth still green but smaller than before.",
  lightLevel: 'partial-shade',
  wateringFreq: 'weekly',
  location: 'indoor',
  ageOfOwnership: '8 months',
  hasPets: true,
  hasChildren: false,
};

const PlantRescue = ({ tool }) => {
  const { callToolEndpoint, loading } = useClaudeAPI();
  const { isDark } = useTheme();

  // ── Theme — verbatim from CONVENTIONS.md PF-2 ──
  const c = {
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:         isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20'
                          : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-500' : 'text-gray-400',
    required:      isDark ? 'text-amber-400' : 'text-amber-500',
    labelText:     isDark ? 'text-zinc-200' : 'text-gray-700',
    accentTxt:     isDark ? 'text-cyan-400' : 'text-cyan-600',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white'
                          : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200'
                          : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200'
                          : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200'
                          : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200'
                          : 'bg-red-50 border-red-200 text-red-800',
    pillActive:    isDark ? 'border-cyan-500 bg-cyan-900/30 text-cyan-200'
                          : 'border-cyan-600 bg-cyan-100 text-cyan-900',
    pillInactive:  isDark ? 'border-zinc-600 text-zinc-400 hover:border-zinc-500'
                          : 'border-gray-300 text-gray-500 hover:border-gray-400',
    // PlantRescue-specific extensions
    critical:      isDark ? 'bg-red-900/20 border-red-700 text-red-200'       : 'bg-red-50 border-red-300 text-red-800',
    concerning:    isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200' : 'bg-amber-50 border-amber-300 text-amber-800',
    minor:         isDark ? 'bg-sky-900/20 border-sky-700 text-sky-200'       : 'bg-sky-50 border-sky-200 text-sky-800',
    propagation:   isDark ? 'bg-emerald-900/20 border-emerald-700'            : 'bg-emerald-50 border-emerald-200',
    btnDanger:     isDark ? 'bg-red-700 hover:bg-red-600 text-white'          : 'bg-red-500 hover:bg-red-600 text-white',
  };
  c.textMuteded  = c.textMuted;
  c.label        = c.labelText;

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';

  // ── Non-hook helpers (stable, no deps) ──
  const load = (k) => { try { const s = localStorage.getItem(k); return s ? JSON.parse(s) : null; } catch { return null; } };
  const save = (k, v) => { try { localStorage.setItem(k, JSON.stringify(v)); } catch {} };

  // ── State ──
  const [imagePreview, setImagePreview]         = useState(null);
  const [imageBase64, setImageBase64]           = useState(null);
  const [plantDescription, setPlantDescription] = useState('');
  const [lightLevel, setLightLevel]             = useState('');
  const [wateringFreq, setWateringFreq]         = useState('');
  const [location, setLocation]                 = useState('');
  const [ageOfOwnership, setAgeOfOwnership]     = useState('');
  const [uploading, setUploading]               = useState(false);
  const [hasPets, setHasPets]                   = useState(false);
  const [hasChildren, setHasChildren]           = useState(false);
  const [climateZone, setClimateZone]           = useState('');
  const [userLocation, setUserLocation]         = useState('');
  const [error, setError]                       = useState('');
  const [followUpQuestion, setFollowUpQuestion] = useState('');
  const [followUpAnswer, setFollowUpAnswer]     = useState('');
  const [followUpLoading, setFollowUpLoading]   = useState(false);
  const [mode, setMode]                         = useState('rescue');
  const [plantCollection, setPlantCollection]   = useState(() => load('plantCollection') || []);
  const [showCollection, setShowCollection]     = useState(false);
  const [plantName, setPlantName]               = useState('');
  const [activePlantId, setActivePlantId]       = useState(null);
  const [progressUploading, setProgressUploading] = useState(false);
  const [selectedSymptoms, setSelectedSymptoms] = useState([]);
  const [companionLoading, setCompanionLoading] = useState(false);
  const [companionResults, setCompanionResults] = useState(null);
  const [extraPhotos, setExtraPhotos]           = useState([null, null]);
  const [extraPreviews, setExtraPreviews]       = useState([null, null]);

  // ── Refs ──
  const fileInputRef     = useRef(null);
  const progressInputRef = useRef(null);
  const extraPhotoRef0   = useRef(null);
  const extraPhotoRef1   = useRef(null);
  const resultsRef       = useRef(null);
  const submitRef        = useRef(null);
  const canSubmitRef     = useRef(false);

  // ── Persistent state ──
  const [results, setResults] = usePersistentState('plantrescue-result', null);
  const [history, setHistory] = usePersistentState('plantrescue-history', []);

  // Derived array from stable refs (not calling hooks in array)
  const extraPhotoRefs = [extraPhotoRef0, extraPhotoRef1];

  // Live ref assignments
  canSubmitRef.current = !loading && !uploading && (mode === 'identify' ? !!imageBase64 : !!(imageBase64 || plantDescription.trim() || selectedSymptoms.length > 0));

  // ── Image handling ──
  const processImage = async (file) => {
    if (!file || !file.type.startsWith('image/')) { setError('Upload an image'); return null; }
    setError('');
    try { return await compressImage(file, CompressionPresets.API_UPLOAD); }
    catch (err) { setError(err.message || 'Image failed.'); return null; }
  };
  const handleFileUpload = async (e) => {
    const f = e.target.files?.[0];
    if (f) { setUploading(true); const r = await processImage(f); if (r) { setImagePreview(r); setImageBase64(r); } setUploading(false); }
  };
  const handlePaste = async (e) => {
    const items = e.clipboardData?.items; if (!items) return;
    for (let i = 0; i < items.length; i++) {
      if (items[i].type.indexOf('image') !== -1) {
        setUploading(true); const r = await processImage(items[i].getAsFile());
        if (r) { setImagePreview(r); setImageBase64(r); } setUploading(false); break;
      }
    }
  };
  const handleRemoveImage = () => { setImagePreview(null); setImageBase64(null); if (fileInputRef.current) fileInputRef.current.value = ''; };
  const handleDragOver = (e) => e.preventDefault();
  const handleDrop = async (e) => {
    e.preventDefault(); const f = e.dataTransfer.files?.[0];
    if (f) { setUploading(true); const r = await processImage(f); if (r) { setImagePreview(r); setImageBase64(r); } setUploading(false); }
  };
  const handleExtraPhoto = async (idx, e) => {
    const f = e.target.files?.[0]; if (!f) return;
    const r = await processImage(f);
    if (r) { setExtraPhotos(p => { const n = [...p]; n[idx] = r; return n; }); setExtraPreviews(p => { const n = [...p]; n[idx] = r; return n; }); }
    if (extraPhotoRefs[idx]?.current) extraPhotoRefs[idx].current.value = '';
  };
  const handleRemoveExtra = (idx) => {
    setExtraPhotos(p => { const n = [...p]; n[idx] = null; return n; });
    setExtraPreviews(p => { const n = [...p]; n[idx] = null; return n; });
  };

  // ── Severity helpers ──
  const getSeverityStyles = (s) => { switch (s?.toLowerCase()) { case 'critical': return c.critical; case 'concerning': return c.concerning; case 'minor': return c.minor; default: return c.cardAlt; } };
  const getSeverityEmoji  = (s) => { switch (s?.toLowerCase()) { case 'critical': return '🔴'; case 'concerning': return '🟡'; case 'minor': return '🟢'; default: return '🪴'; } };
  const getSeverityLabel  = (s) => { switch (s?.toLowerCase()) { case 'critical': return 'CRITICAL'; case 'concerning': return 'CONCERNING'; case 'minor': return 'MINOR'; default: return 'HEALTHY'; } };

  // ── Symptom definitions ──
  const symptomOptions = [
    { id: 'yellow_leaves', emoji: '🟡', label: 'Yellowing leaves' },
    { id: 'brown_tips',    emoji: '🟤', label: 'Brown tips' },
    { id: 'drooping',      emoji: '😞', label: 'Drooping/wilting' },
    { id: 'spots',         emoji: '🔵', label: 'Spots on leaves' },
    { id: 'mushy_stem',    emoji: '🫠', label: 'Mushy/soft stem' },
    { id: 'white_fuzz',    emoji: '🤍', label: 'White fuzz/mold' },
    { id: 'tiny_bugs',     emoji: '🐛', label: 'Tiny bugs/pests' },
    { id: 'leggy',         emoji: '🌱', label: 'Leggy/stretched' },
    { id: 'no_growth',     emoji: '⏸️', label: 'No new growth' },
    { id: 'leaf_drop',     emoji: '🍂', label: 'Leaf dropping' },
    { id: 'crispy',        emoji: '🥀', label: 'Crispy/dry leaves' },
    { id: 'root_rot',      emoji: '💀', label: 'Smells bad/root rot' },
  ];
  const toggleSymptom = (id) => setSelectedSymptoms(p => p.includes(id) ? p.filter(s => s !== id) : [...p, id]);

  // ── Watering tracker ──
  const handleMarkWatered = (plantId) => {
    setPlantCollection(p => p.map(pl => pl.id === plantId ? { ...pl, lastWatered: new Date().toISOString() } : pl));
  };
  const getWaterStatus = (plant) => {
    if (!plant.lastWatered || !plant.lastResults?.care_schedule?.watering) return null;
    const last = new Date(plant.lastWatered);
    const now = new Date();
    const daysSince = Math.floor((now - last) / (1000 * 60 * 60 * 24));
    const match = plant.lastResults?.care_schedule?.watering?.match(/every\s+(\d+)(?:\s*-\s*(\d+))?\s*days/i);
    if (!match) return { daysSince, status: 'unknown', nextIn: null };
    const interval = match[2] ? Math.floor((parseInt(match[1]) + parseInt(match[2])) / 2) : parseInt(match[1]);
    const nextIn = interval - daysSince;
    return { daysSince, interval, nextIn, status: nextIn <= 0 ? 'overdue' : nextIn <= 1 ? 'today' : 'ok' };
  };

  // ── Core handlers ──
  const handleAnalyze = useCallback(async () => {
    if (!imageBase64 && !plantDescription.trim() && selectedSymptoms.length === 0) { setError('Provide a photo, description, or select symptoms'); return; }
    setError(''); setResults(null); setFollowUpAnswer(''); setFollowUpQuestion(''); setCompanionResults(null);
    const symptomText = selectedSymptoms.length > 0
      ? selectedSymptoms.map(id => symptomOptions.find(s => s.id === id)?.label).filter(Boolean).join(', ')
      : '';
    const fullDescription = [plantDescription.trim(), symptomText ? `Symptoms: ${symptomText}` : ''].filter(Boolean).join('\n');
    try {
      const data = await callToolEndpoint('plant-rescue', {
        imageBase64, extraPhotos: extraPhotos.filter(Boolean),
        plantDescription: fullDescription, symptoms: selectedSymptoms,
        lightLevel, wateringFreq, location, ageOfOwnership: ageOfOwnership.trim(),
        hasPets, hasChildren, climateZone, userLocation: userLocation.trim(),
        mode, plantName: plantName.trim() || null
      });
      setResults(data);
      setHistory(prev => [{ id: Date.now(), date: new Date().toISOString(), preview: (plantDescription || '').slice(0, 40) }, ...prev].slice(0, 6));
    } catch (err) { setError(err.message || 'Analysis failed.'); }
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [imageBase64, extraPhotos, plantDescription, selectedSymptoms, lightLevel, wateringFreq, location,
      ageOfOwnership, hasPets, hasChildren, climateZone, userLocation, mode, plantName, callToolEndpoint]);

  const loadExample = useCallback(() => {
    setMode(EXAMPLE.mode);
    setPlantDescription(EXAMPLE.plantDescription);
    setLightLevel(EXAMPLE.lightLevel);
    setWateringFreq(EXAMPLE.wateringFreq);
    setLocation(EXAMPLE.location);
    setAgeOfOwnership(EXAMPLE.ageOfOwnership);
    setHasPets(EXAMPLE.hasPets);
    setHasChildren(EXAMPLE.hasChildren);
  }, []);

  const handleFollowUp = async () => {
    if (!followUpQuestion.trim() || !results) return;
    setFollowUpLoading(true); setFollowUpAnswer('');
    try {
      const data = await callToolEndpoint('plant-rescue/followup', {
        question: followUpQuestion.trim(), originalDiagnosis: results,
        plantDescription: plantDescription.trim(), imageProvided: !!imageBase64
      });
      setFollowUpAnswer(data.answer || 'No answer.');
    } catch { setFollowUpAnswer('Follow-up failed.'); }
    setFollowUpLoading(false);
  };

  const handleGetCompanions = async () => {
    if (plantCollection.length < 2) return;
    setCompanionLoading(true);
    try {
      const plants = plantCollection.map(p => ({
        name: p.name, species: p.species || p.commonName || 'Unknown',
        lightNeeds: p.lastResults?.environmental_adjustments?.light || '',
        waterNeeds: p.lastResults?.care_schedule?.watering || ''
      }));
      const data = await callToolEndpoint('plant-rescue/companions', { plants, climateZone, location });
      setCompanionResults(data);
    } catch { setError('Companion analysis failed.'); }
    setCompanionLoading(false);
  };

  const handleReset = () => {
    setImagePreview(null); setImageBase64(null); setPlantDescription(''); setLightLevel('');
    setWateringFreq(''); setLocation(''); setAgeOfOwnership(''); setHasPets(false);
    setHasChildren(false); setClimateZone(''); setUserLocation(''); setResults(null);
    setError(''); setFollowUpQuestion(''); setFollowUpAnswer(''); setSelectedSymptoms([]);
    setExtraPhotos([null, null]); setExtraPreviews([null, null]); setCompanionResults(null);
    if (fileInputRef.current) fileInputRef.current.value = '';
  };

  const handleSavePlant = () => {
    if (!results) return;
    const name = plantName.trim() || results.plant_identification?.common_name || results.plant_identification?.species || 'My Plant';
    const entry = {
      id: Date.now().toString(), name, date: new Date().toISOString(),
      species: results.plant_identification?.species, commonName: results.plant_identification?.common_name,
      severity: results.diagnosis?.severity || (mode === 'care' || mode === 'identify' ? 'healthy' : 'unknown'),
      diagnosis: results.diagnosis?.primary_problem || 'General care',
      isSaveable: results.is_saveable, mode, lastWatered: null,
      progressPhotos: imagePreview ? [{ date: new Date().toISOString(), note: mode === 'rescue' ? 'Initial diagnosis' : 'First check-in' }] : [],
      lastResults: results
    };
    setPlantCollection(p => [entry, ...p].slice(0, 6));
    setActivePlantId(entry.id);
  };
  const handleDeletePlant = (id) => { setPlantCollection(p => p.filter(pl => pl.id !== id)); if (activePlantId === id) setActivePlantId(null); };
  const handleLoadPlant = (plant) => { setPlantName(plant.name); setActivePlantId(plant.id); if (plant.lastResults) setResults(plant.lastResults); setShowCollection(false); };

  const handleAddProgressPhoto = async (e) => {
    const file = e.target.files?.[0]; if (!file || !activePlantId) return;
    const compressed = await processImage(file);
    if (compressed) { setPlantCollection(p => p.map(pl => pl.id !== activePlantId ? pl : { ...pl, progressPhotos: [...(pl.progressPhotos || []), { date: new Date().toISOString(), image: compressed, note: '' }] })); }
    if (progressInputRef.current) progressInputRef.current.value = '';
  };
  const getActivePlant = () => plantCollection.find(p => p.id === activePlantId);

  const months = ['Jan','Feb','Mar','Apr','May','Jun','Jul','Aug','Sep','Oct','Nov','Dec'];

  // ── buildFullText ──
  const buildFullText = useCallback(() => {
    if (!results) return '';
    const l = [`🪴 ${mode === 'identify' ? 'PLANT ID' : mode === 'care' ? 'CARE GUIDE' : 'RESCUE REPORT'}`, '═'.repeat(40)];
    if (results.plant_identification) { const pi = results.plant_identification; l.push('', `📷 ${pi.species || 'Unknown'}`); if (pi.common_name) l.push(`  ${pi.common_name}`); }
    if (results.toxicity_warning)  l.push('', `⚠️ TOXIC — ${results.toxicity_warning.level}`);
    if (results.diagnosis)         l.push('', `🔍 ${results.diagnosis.severity?.toUpperCase()}: ${results.diagnosis.primary_problem}`);
    if (results.care_schedule) {
      const cs = results.care_schedule; l.push('', '💧 CARE');
      if (cs.watering)    l.push(`  Water: ${cs.watering}`);
      if (cs.fertilizing) l.push(`  Fertilize: ${cs.fertilizing}`);
      if (cs.misting)     l.push(`  Mist: ${cs.misting}`);
    }
    if (results.seasonal_calendar?.length) { l.push('', '📅 SEASONAL CALENDAR'); results.seasonal_calendar.forEach(m => l.push(`  ${m.month}: ${m.tasks.join(', ')}`)); }
    if (results.action_plan?.length)       { l.push('', '🚑 ACTION PLAN'); results.action_plan.forEach(a => l.push(`  [P${a.priority}] ${a.action}`)); }
    if (results.repotting_guide)           l.push('', `🏺 SOIL: ${results.repotting_guide.soil_mix || 'See details'}`);
    if (results.propagation_guide)         l.push('', `✂️ PROPAGATION: ${results.propagation_guide.method}`);
    if (results.prevention_tips?.length)   { l.push('', '✨ PREVENTION'); results.prevention_tips.forEach(t => l.push(`  • ${t}`)); }
    if (followUpAnswer)                    l.push('', `Q: ${followUpQuestion}`, `A: ${followUpAnswer}`);
    l.push(BRAND);
    return l.join('\n');
  }, [results, mode, followUpQuestion, followUpAnswer]);

  // Assign submit ref after handleAnalyze is defined
  submitRef.current = handleAnalyze;

  useRegisterActions(buildFullText(), tool?.title || 'Plant Rescue');

  // ── Scroll to results ──
  useEffect(() => {
    if (!results || !resultsRef.current) return;
    const t = setTimeout(() => resultsRef.current?.scrollIntoView({ behavior: 'smooth', block: 'start' }), 200);
    return () => clearTimeout(t);
  }, [results]);

  // ── Keyboard shortcut ──
  useEffect(() => {
    const handler = (e) => {
      const tag = document.activeElement?.tagName;
      if (tag === 'SELECT') return;
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && canSubmitRef.current)
        submitRef.current?.();
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, []);

  // ── Persist plant collection ──
  useEffect(() => { save('plantCollection', plantCollection); }, [plantCollection]);

  // ── Render ──
  return (
    <div className={`space-y-4 ${c.text}`}>

      {/* ── Main input card (header + inputs combined) ── */}
      <div className={`${c.card} border ${c.border} rounded-xl shadow-sm overflow-hidden`}>
        <div className="px-5 pt-5">
          <div className="pb-3 border-b border-zinc-500">
            <div className="flex items-start justify-between gap-3">
              <div>
                <h2 className={`text-xl font-bold ${c.text}`}>
                  <span className="mr-2">{tool?.icon ?? '🪴'}</span>{tool?.title ?? 'Plant Rescue'}
                </h2>
                <p className={`text-sm ${c.textSecondary}`}>{tool?.tagline ?? 'Diagnose, identify, care for, and track your plants'}</p>
              </div>
              <div className="flex items-center gap-2 flex-shrink-0">
                <button onClick={() => setShowCollection(!showCollection)}
                  className={`${c.btnSecondary} px-3 py-1.5 rounded text-xs`}>
                  🪴 My Plants ({plantCollection.length})
                </button>
                {(results || plantDescription.trim() || imagePreview) && (
                  <button onClick={handleReset} className={`${c.btnSecondary} px-3 py-1.5 rounded-lg text-xs font-bold`}>
                    ↺ Start Over
                  </button>
                )}
              </div>
            </div>
          </div>
        </div>

        <div className="p-5 space-y-5">
          {/* Mode toggle */}
          <div className="flex gap-2">
            {[
              { key: 'rescue',   label: '🚑 Rescue' },
              { key: 'care',     label: '🌱 Care Guide' },
              { key: 'identify', label: '🔍 Identify' },
            ].map(m => (
              <button key={m.key} onClick={() => { setMode(m.key); setError(''); }}
                className={`flex-1 py-2.5 rounded-lg font-medium text-sm border transition-colors ${mode === m.key ? c.pillActive : c.pillInactive}`}>
                {m.label}
              </button>
            ))}
          </div>

          {/* Mode description */}
          <div className={`${c.cardAlt} border ${c.border} rounded-lg p-3`}>
            <p className={`text-sm ${c.textSecondary}`}>
              {mode === 'rescue'   ? 'Upload a photo of your struggling plant. Get diagnosis, rescue plan, and recovery timeline.' :
               mode === 'care'     ? 'Get a personalized care schedule, watering guide, and seasonal calendar.' :
                                     'Upload a photo to identify your plant and get its full care profile.'}
            </p>
          </div>

          {mode !== 'identify' && (
            <div>
              <label className={`block text-sm font-medium ${c.label} mb-1`}>🏷️ Plant Name</label>
              <input type="text" value={plantName} onChange={e => setPlantName(e.target.value)}
                placeholder="e.g., Kitchen Fern"
                className={`w-full p-3 border rounded-lg ${c.input} outline-none focus:ring-2`} />
            </div>
          )}

          {/* Main photo */}
          <div>
            <label className={`block text-sm font-medium ${c.label} mb-2`}>
              📸 {mode === 'rescue' ? 'Plant Photo — whole plant view' : 'Plant Photo'}
              {mode === 'identify'
                ? <> <span className={c.required}>*</span></>
                : <> <span className={`font-normal ${c.textMuted}`}>(or describe / select symptoms below)</span></>}
            </label>
            {!imagePreview ? (
              <div onDragOver={handleDragOver} onDrop={handleDrop} onPaste={handlePaste}
                className={`border-2 border-dashed rounded-lg p-6 text-center cursor-pointer ${isDark ? 'border-zinc-600 hover:border-zinc-400 bg-zinc-900/50' : 'border-gray-300 hover:border-gray-400 bg-white'}`}
                onClick={() => fileInputRef.current?.click()}>
                <input ref={fileInputRef} type="file" accept="image/*" onChange={handleFileUpload} className="hidden" />
                {uploading
                  ? <><span className="inline-block animate-spin">{tool?.icon ?? '🪴'}</span><p className={`text-sm ${c.text}`}>Compressing...</p></>
                  : <><span className="text-3xl block mb-2">📤</span><p className={`text-sm ${c.text}`}>Click, drag, or paste</p><p className={`text-xs ${c.textMuted}`}>JPG, PNG, WEBP</p></>}
              </div>
            ) : (
              <div className="relative">
                <img src={imagePreview} alt="Plant" className="w-full max-h-48 object-contain rounded-lg border" />
                <button onClick={handleRemoveImage}
                  className={`absolute top-2 right-2 ${c.btnDanger} w-7 h-7 rounded-full flex items-center justify-center text-xs`}>✕</button>
              </div>
            )}
          </div>

          {/* Extra photos — rescue mode only */}
          {mode === 'rescue' && imagePreview && (
            <div className="grid grid-cols-2 gap-3">
              {[['🔍 Close-up of problem area', 0], ['🌱 Soil / roots', 1]].map(([label, idx]) => (
                <div key={idx}>
                  <label className={`block text-xs font-medium ${c.label} mb-1`}>{label}</label>
                  {!extraPreviews[idx] ? (
                    <div onClick={() => extraPhotoRefs[idx]?.current?.click()}
                      className={`border-2 border-dashed rounded-lg p-4 text-center cursor-pointer ${isDark ? 'border-zinc-600 hover:border-zinc-400' : 'border-gray-300 hover:border-gray-400'}`}>
                      <input ref={extraPhotoRefs[idx]} type="file" accept="image/*"
                        onChange={e => handleExtraPhoto(idx, e)} className="hidden" />
                      <span className="text-xl">📸</span><p className={`text-xs ${c.textMuted}`}>Optional</p>
                    </div>
                  ) : (
                    <div className="relative">
                      <img src={extraPreviews[idx]} alt={`Extra ${idx + 1}`} className="w-full h-24 object-cover rounded-lg border" />
                      <button onClick={() => handleRemoveExtra(idx)}
                        className={`absolute top-1 right-1 ${c.btnDanger} w-5 h-5 rounded-full flex items-center justify-center text-xs`}>✕</button>
                    </div>
                  )}
                </div>
              ))}
            </div>
          )}

          {/* Symptom wizard — rescue only */}
          {mode === 'rescue' && (
            <div>
              <label className={`block text-sm font-medium ${c.label} mb-2`}>🩺 What do you see? <span className={c.required}>*</span> <span className={`font-normal ${c.textMuted}`}>(click all that apply)</span></label>
              <div className="grid grid-cols-2 md:grid-cols-3 gap-2">
                {symptomOptions.map(s => (
                  <label key={s.id}
                    className={`p-2.5 rounded-lg border-2 cursor-pointer flex items-center gap-2 text-sm ${selectedSymptoms.includes(s.id) ? isDark ? 'border-cyan-500 bg-zinc-700' : 'border-cyan-600 bg-slate-50' : isDark ? 'border-zinc-700' : 'border-gray-200'}`}>
                    <input type="checkbox" checked={selectedSymptoms.includes(s.id)} onChange={() => toggleSymptom(s.id)} className="sr-only" />
                    <span>{s.emoji}</span>
                    <span className={selectedSymptoms.includes(s.id) ? 'font-bold' : ''}>{s.label}</span>
                  </label>
                ))}
              </div>
            </div>
          )}

          {mode !== 'identify' && (
            <div>
              <label className={`block text-sm font-medium ${c.label} mb-1`}>
                📝 {mode === 'rescue' ? 'Additional details' : 'Describe your plant'} <span className={c.required}>*</span> <span className={`font-normal ${c.textMuted}`}>(if no photo)</span>
              </label>
              <textarea value={plantDescription} onChange={e => setPlantDescription(e.target.value)}
                placeholder={mode === 'rescue' ? 'Anything else about the symptoms...' : 'Type, size, where you keep it...'}
                className={`w-full p-3 border rounded-lg ${c.input} outline-none focus:ring-2 resize-y`} rows={2} />
            </div>
          )}

          {mode !== 'identify' && (
            <>
              <div className="grid grid-cols-1 md:grid-cols-3 gap-4">
                <div>
                  <label className={`block text-xs font-medium ${c.label} mb-1`}>☀️ Light</label>
                  <select value={lightLevel} onChange={e => setLightLevel(e.target.value)} className={`w-full p-2.5 border rounded-lg text-sm ${c.input}`}>
                    <option value="">Select...</option>
                    <option value="full-sun">Full Sun (6+h)</option>
                    <option value="partial-shade">Partial (3-6h)</option>
                    <option value="low-light">Low Light</option>
                  </select>
                </div>
                <div>
                  <label className={`block text-xs font-medium ${c.label} mb-1`}>💧 Watering</label>
                  <select value={wateringFreq} onChange={e => setWateringFreq(e.target.value)} className={`w-full p-2.5 border rounded-lg text-sm ${c.input}`}>
                    <option value="">Select...</option>
                    <option value="daily">Daily</option>
                    <option value="few-days">Few days</option>
                    <option value="weekly">Weekly</option>
                    <option value="rarely">Rarely</option>
                  </select>
                </div>
                <div>
                  <label className={`block text-xs font-medium ${c.label} mb-1`}>📍 Location</label>
                  <select value={location} onChange={e => setLocation(e.target.value)} className={`w-full p-2.5 border rounded-lg text-sm ${c.input}`}>
                    <option value="">Select...</option>
                    <option value="indoor">Indoor</option>
                    <option value="outdoor">Outdoor</option>
                    <option value="greenhouse">Greenhouse</option>
                  </select>
                </div>
              </div>

              <div className="grid grid-cols-1 md:grid-cols-2 gap-4">
                <div>
                  <label className={`block text-xs font-medium ${c.label} mb-1`}>⏱️ How long?</label>
                  <input type="text" value={ageOfOwnership} onChange={e => setAgeOfOwnership(e.target.value)}
                    placeholder="e.g., 2 months"
                    className={`w-full p-2.5 border rounded-lg text-sm ${c.input} outline-none focus:ring-2`} />
                </div>
                <div>
                  <label className={`block text-xs font-medium ${c.label} mb-1`}>🌡️ Climate</label>
                  <select value={climateZone} onChange={e => setClimateZone(e.target.value)} className={`w-full p-2.5 border rounded-lg text-sm ${c.input}`}>
                    <option value="">Select...</option>
                    <option value="tropical">Tropical</option>
                    <option value="subtropical">Subtropical</option>
                    <option value="temperate">Temperate</option>
                    <option value="cold">Cold</option>
                    <option value="arid">Arid</option>
                  </select>
                </div>
              </div>

              <div className="flex gap-4">
                <label className="flex items-center gap-2 cursor-pointer text-sm">
                  <input type="checkbox" checked={hasPets} onChange={e => setHasPets(e.target.checked)} className="w-4 h-4" /> 🐾 Pets
                </label>
                <label className="flex items-center gap-2 cursor-pointer text-sm">
                  <input type="checkbox" checked={hasChildren} onChange={e => setHasChildren(e.target.checked)} className="w-4 h-4" /> 👶 Children
                </label>
              </div>
            </>
          )}

          <div className="flex gap-2">
          <button onClick={handleAnalyze}
            disabled={loading || uploading || (mode === 'identify' ? !imageBase64 : (!imageBase64 && !plantDescription.trim() && selectedSymptoms.length === 0))}
            className={`flex-1 ${c.btnPrimary} disabled:opacity-40 font-bold py-3 rounded-lg flex items-center justify-center gap-2 min-h-[48px]`}>
            {loading
              ? <><span className="inline-block animate-spin">{tool?.icon ?? '🪴'}</span> Analyzing…</>
              : <><span>{tool?.icon ?? '🪴'}</span> {mode === 'rescue' ? 'Diagnose Plant' : mode === 'identify' ? 'Identify Plant' : 'Get Care Guide'}</>}
          </button>
          <button onClick={loadExample} className={`px-4 py-3 rounded-lg text-xs font-bold ${c.btnSecondary} min-h-[48px]`}>
            Try example
          </button>
          </div>

          <p className={`text-xs text-center ${c.textMuted}`}>
            Fixing something else? <a href="/BikeMedic" className={`text-xs ${linkStyle}`}>🚲 Bike Medic</a> works the same way.
          </p>

          {error && (
            <div className={`${c.critical} border rounded-lg p-4 flex items-start gap-3`}>
              <span>⚠️</span><p className="text-sm">{error}</p>
            </div>
          )}
        </div>
      </div>

      {/* ── Collection panel ── */}
      {showCollection && (
        <div className={`${c.card} border ${c.border} rounded-xl p-5`}>
          <div className="flex items-center justify-between mb-3">
            <h3 className={`font-bold ${c.text}`}>🪴 My Plants</h3>
            {plantCollection.length >= 2 && (
              <button onClick={handleGetCompanions} disabled={companionLoading}
                className={`${c.btnSecondary} px-3 py-1.5 rounded text-xs disabled:opacity-40`}>
                {companionLoading ? <><span className="inline-block animate-spin">{tool?.icon ?? '🪴'}</span></> : '🤝 Companion Check'}
              </button>
            )}
          </div>

          {plantCollection.length > 0 ? (
            <div className="space-y-2">
              {plantCollection.map(pl => {
                const ws = getWaterStatus(pl);
                return (
                  <div key={pl.id}
                    className={`p-3 rounded-lg border ${activePlantId === pl.id ? 'border-emerald-500' : isDark ? 'border-zinc-600' : 'border-gray-200'} ${isDark ? 'bg-zinc-700' : 'bg-white'}`}>
                    <div className="flex items-center justify-between">
                      <div className="min-w-0 flex-1 cursor-pointer" onClick={() => handleLoadPlant(pl)}>
                        <div className="flex items-center gap-2">
                          <h4 className={`font-bold ${c.text} truncate`}>{pl.name}</h4>
                          <span className={`text-xs px-1.5 py-0.5 rounded font-semibold ${pl.severity === 'critical' ? 'bg-red-100 text-red-700' : pl.severity === 'concerning' ? 'bg-amber-100 text-amber-700' : 'bg-green-100 text-green-700'}`}>
                            {pl.severity || 'ok'}
                          </span>
                        </div>
                        <p className={`text-xs ${c.textMuted}`}>{pl.commonName || pl.species || 'Unknown'}</p>
                      </div>
                      <div className="flex items-center gap-2">
                        {ws ? (
                          <div className="text-center">
                            <button onClick={(e) => { e.stopPropagation(); handleMarkWatered(pl.id); }}
                              className={`px-2 py-1 rounded text-xs font-bold ${ws.status === 'overdue' ? 'bg-red-100 text-red-700 animate-pulse' : ws.status === 'today' ? 'bg-amber-100 text-amber-700' : 'bg-green-100 text-green-700'}`}>
                              💧 {ws.status === 'overdue' ? 'OVERDUE' : ws.status === 'today' ? 'TODAY' : `${ws.nextIn}d`}
                            </button>
                            <p className={`text-xs ${c.textMuted}`}>{ws.daysSince}d ago</p>
                          </div>
                        ) : pl.lastResults?.care_schedule ? (
                          <button onClick={(e) => { e.stopPropagation(); handleMarkWatered(pl.id); }}
                            className={`${c.btnSecondary} px-2 py-1 rounded text-xs`}>💧 Watered</button>
                        ) : null}
                        <button onClick={() => handleDeletePlant(pl.id)}
                          className={`text-xs ${c.textMuted} hover:text-zinc-400`}>✕</button>
                      </div>
                    </div>
                  </div>
                );
              })}
            </div>
          ) : <p className={`text-sm ${c.textMuted}`}>No saved plants yet.</p>}

          {/* Companion results */}
          {companionResults && (
            <div className={`mt-4 p-4 rounded-lg border ${isDark ? 'bg-zinc-800 border-zinc-600' : 'bg-white border-gray-200'}`}>
              <h4 className={`font-bold mb-2 ${c.text}`}>🤝 Companion Analysis</h4>
              {companionResults.groupings?.length > 0 && (
                <div className="space-y-2 mb-3">
                  {companionResults.groupings.map((g, i) => (
                    <div key={i} className={`p-2 rounded ${isDark ? 'bg-zinc-800' : 'bg-white'}`}>
                      <p className={`text-xs font-bold ${c.accentTxt}`}>{g.group_name}</p>
                      <p className={`text-sm ${c.text}`}>{g.plants.join(', ')}</p>
                      <p className={`text-xs ${c.textMuted}`}>{g.reason}</p>
                    </div>
                  ))}
                </div>
              )}
              {companionResults.conflicts?.length > 0 && (
                <div className="space-y-1 mb-3">
                  {companionResults.conflicts.map((cf, i) => <p key={i} className={`text-sm ${c.textSecondary}`}>⚠️ {cf}</p>)}
                </div>
              )}
              {companionResults.suggestions?.length > 0 && (
                <div>
                  <p className={`text-xs font-bold ${c.label} mb-1`}>💡 Suggestions:</p>
                  {companionResults.suggestions.map((s, i) => <p key={i} className={`text-sm ${c.textSecondary}`}>• {s}</p>)}
                </div>
              )}
            </div>
          )}
        </div>
      )}

      {/* ── Results ── */}
      {results && (
        <div className="space-y-4" ref={resultsRef}>

          {/* Save button */}
          <div className="flex justify-center">
            <button onClick={handleSavePlant} className={`${c.btnSecondary} px-4 py-2 rounded-lg text-sm`}>🪴 Save to My Plants</button>
          </div>

          {/* Hero badge */}
          {mode === 'rescue' && results.diagnosis ? (
            <div className={`${c.card} border ${c.border} rounded-xl shadow-sm p-6 text-center`}>
              <div className="text-6xl mb-3">{getSeverityEmoji(results.diagnosis.severity)}</div>
              <div className={`text-2xl font-black ${c.text}`}>{getSeverityLabel(results.diagnosis.severity)}</div>
              <p className={`text-sm ${c.textSecondary}`}>{results.diagnosis.primary_problem}</p>
              {results.is_saveable !== undefined && (
                <p className={`text-sm mt-2 font-bold ${results.is_saveable ? 'text-emerald-500' : 'text-red-500'}`}>
                  {results.is_saveable ? '✅ Saveable!' : '⚰️ May be beyond saving'}
                </p>
              )}
              {results.recovery_timeline && <p className={`text-xs ${c.textMuted} mt-1`}>📅 {results.recovery_timeline}</p>}
            </div>
          ) : (
            <div className={`${c.card} border ${c.border} rounded-xl shadow-sm p-6 text-center`}>
              <div className="text-6xl mb-3">{mode === 'identify' ? '🔍' : '🌿'}</div>
              <div className={`text-2xl font-black ${c.text}`}>{mode === 'identify' ? 'IDENTIFIED' : 'CARE GUIDE'}</div>
              <p className={`text-sm ${c.textSecondary}`}>{results.plant_identification?.common_name || results.plant_identification?.species || ''}</p>
            </div>
          )}

          {/* Toxicity */}
          {results.toxicity_warning && (
            <div className={`${c.critical} border-4 rounded-xl p-6`}>
              <h3 className={`text-xl font-bold mb-2 ${c.text}`}>☠️ TOXICITY</h3>
              <p className="text-sm font-bold">{results.toxicity_warning.level?.toUpperCase()} — {results.toxicity_warning.dangerous_for?.join(' & ')}</p>
              <p className="text-sm"><strong>Symptoms:</strong> {results.toxicity_warning.symptoms}</p>
              <p className="text-sm"><strong>Safety:</strong> {results.toxicity_warning.safety_measures}</p>
              {results.toxicity_warning.alternative_plants && (
                <p className="text-sm mt-2"><strong>Safe alternatives:</strong> {results.toxicity_warning.alternative_plants}</p>
              )}
            </div>
          )}

          {/* Plant ID */}
          {results.plant_identification && (
            <div className={`${c.card} border ${c.border} rounded-xl p-5`}>
              <h3 className={`font-bold mb-2 ${c.text}`}>📷 Identification</h3>
              <p className={`text-lg font-bold ${c.text}`}>{results.plant_identification.species}</p>
              {results.plant_identification.common_name && <p className={`text-sm ${c.textSecondary}`}>{results.plant_identification.common_name}</p>}
              <span className={`text-xs px-2 py-1 rounded mt-2 inline-block ${results.plant_identification.confidence === 'high' ? 'bg-green-100 text-green-700' : results.plant_identification.confidence === 'medium' ? 'bg-amber-100 text-amber-700' : 'bg-red-100 text-red-700'}`}>
                {results.plant_identification.confidence_score ? `${results.plant_identification.confidence_score}%` : results.plant_identification.confidence?.toUpperCase()}
              </span>
              {results.plant_identification.alternative_species?.length > 0 && (
                <div className="mt-2">
                  {results.plant_identification.alternative_species.map((a, i) => (
                    <p key={i} className={`text-xs ${c.textMuted}`}>• {a.common_name || a.species} ({a.likelihood}%)</p>
                  ))}
                </div>
              )}
            </div>
          )}

          {/* Care Schedule */}
          {results.care_schedule && (
            <div className={`${c.card} ${c.success} border border-l-4 rounded-xl p-6`}>
              <h3 className={`text-lg font-bold mb-3 ${c.text}`}>💧 Care Schedule</h3>
              <div className="grid grid-cols-1 md:grid-cols-2 gap-3">
                {results.care_schedule.watering    && <div className={`p-3 rounded-lg ${isDark ? 'bg-zinc-800' : 'bg-white'}`}><p className="text-xs font-bold mb-1">💧 Watering</p><p className={`text-sm ${c.text}`}>{results.care_schedule.watering}</p></div>}
                {results.care_schedule.fertilizing && <div className={`p-3 rounded-lg ${isDark ? 'bg-zinc-800' : 'bg-white'}`}><p className="text-xs font-bold mb-1">🧪 Fertilizing</p><p className={`text-sm ${c.text}`}>{results.care_schedule.fertilizing}</p></div>}
                {results.care_schedule.misting     && <div className={`p-3 rounded-lg ${isDark ? 'bg-zinc-800' : 'bg-white'}`}><p className="text-xs font-bold mb-1">💨 Misting</p><p className={`text-sm ${c.text}`}>{results.care_schedule.misting}</p></div>}
                {results.care_schedule.rotation    && <div className={`p-3 rounded-lg ${isDark ? 'bg-zinc-800' : 'bg-white'}`}><p className="text-xs font-bold mb-1">🔄 Rotation</p><p className={`text-sm ${c.text}`}>{results.care_schedule.rotation}</p></div>}
                {results.care_schedule.pruning     && <div className={`p-3 rounded-lg ${isDark ? 'bg-zinc-800' : 'bg-white'}`}><p className="text-xs font-bold mb-1">✂️ Pruning</p><p className={`text-sm ${c.text}`}>{results.care_schedule.pruning}</p></div>}
                {results.care_schedule.repot_timing && <div className={`p-3 rounded-lg ${isDark ? 'bg-zinc-800' : 'bg-white'}`}><p className="text-xs font-bold mb-1">🏺 Repotting</p><p className={`text-sm ${c.text}`}>{results.care_schedule.repot_timing}</p></div>}
              </div>
              {results.care_schedule.seasonal_adjustments && <p className={`text-sm mt-3 ${c.textMuted}`}>🌦️ {results.care_schedule.seasonal_adjustments}</p>}
            </div>
          )}

          {/* Seasonal Calendar */}
          {results.seasonal_calendar?.length > 0 && (
            <div className={`${c.card} border ${c.border} rounded-xl p-6`}>
              <h3 className={`text-lg font-bold mb-4 ${c.text}`}>📅 Seasonal Care Calendar</h3>
              <div className="grid grid-cols-2 md:grid-cols-3 gap-2">
                {results.seasonal_calendar.map((m, i) => {
                  const isNow = months[new Date().getMonth()] === m.month?.slice(0, 3);
                  return (
                    <div key={i}
                      className={`p-3 rounded-lg border ${isNow ? 'border-emerald-500 ring-2 ring-emerald-500/30' : isDark ? 'border-zinc-600' : 'border-gray-200'} ${isDark ? 'bg-zinc-700' : 'bg-white'}`}>
                      <p className={`text-xs font-bold mb-1 ${isNow ? c.accentTxt : c.textMuted}`}>{isNow ? '📍 ' : ''}{m.month}</p>
                      <ul className="space-y-0.5">{m.tasks?.map((t, j) => <li key={j} className={`text-xs ${c.textSecondary}`}>• {t}</li>)}</ul>
                    </div>
                  );
                })}
              </div>
            </div>
          )}

          {/* Diagnosis */}
          {mode === 'rescue' && results.diagnosis && (
            <div className={`${getSeverityStyles(results.diagnosis.severity)} border-l-4 rounded-r-lg p-5`}>
              <h3 className={`font-bold mb-2 ${c.text}`}>{getSeverityEmoji(results.diagnosis.severity)} Diagnosis</h3>
              <p className={`text-sm ${c.text}`}><strong>Primary:</strong> {results.diagnosis.primary_problem}</p>
              {results.diagnosis.secondary_issues?.length > 0 && (
                <div className="mt-1">
                  {results.diagnosis.secondary_issues.map((is, i) => <p key={i} className={`text-sm ${c.textSecondary}`}>• {is}</p>)}
                </div>
              )}
              {results.diagnosis.uncertainty_note && <p className={`text-xs mt-2 ${c.textMuted}`}>ℹ️ {results.diagnosis.uncertainty_note}</p>}
            </div>
          )}

          {/* Action Plan */}
          {results.action_plan?.length > 0 && (
            <div className={`${c.card} border ${c.border} rounded-xl p-6`}>
              <h3 className={`text-lg font-bold ${c.text} mb-3`}>🚑 Action Plan</h3>
              <div className="space-y-3">
                {results.action_plan.map((a, idx) => (
                  <div key={idx}
                    className={`border-l-4 ${a.priority === 1 ? 'border-red-500' : a.priority === 2 ? 'border-amber-500' : 'border-sky-500'} ${c.cardAlt} border rounded-r-lg p-4`}>
                    <div className="flex items-center gap-2 mb-1">
                      <span className={`px-2 py-0.5 rounded text-xs font-bold ${a.priority === 1 ? c.critical : a.priority === 2 ? c.concerning : c.minor}`}>P{a.priority}</span>
                      <span className={`text-xs ${c.textMuted}`}>🕐 {a.timing}</span>
                    </div>
                    <h4 className={`font-bold ${c.text} mb-1`}>{a.action}</h4>
                    <p className="text-sm"><strong>Why:</strong> <span className={c.textSecondary}>{a.why}</span></p>
                    <p className="text-sm"><strong>How:</strong> <span className={c.textSecondary}>{a.how}</span></p>
                  </div>
                ))}
              </div>
            </div>
          )}

          {/* Repotting */}
          {results.repotting_guide && (
            <div className={`${c.card} border ${c.border} rounded-xl p-5`}>
              <h3 className={`font-bold mb-3 ${c.text}`}>🏺 Repotting</h3>
              <p className={`text-sm font-bold mb-2 ${c.text}`}>{results.repotting_guide.needs_repotting ? '⚠️ Recommended' : '✅ Not needed now'}</p>
              <div className="grid grid-cols-1 md:grid-cols-3 gap-3">
                {results.repotting_guide.soil_mix     && <div className={`p-3 rounded ${c.cardAlt}`}><p className="text-xs font-bold mb-1">🌱 Soil</p><p className={`text-sm ${c.text}`}>{results.repotting_guide.soil_mix}</p></div>}
                {results.repotting_guide.pot_size     && <div className={`p-3 rounded ${c.cardAlt}`}><p className="text-xs font-bold mb-1">📐 Size</p><p className={`text-sm ${c.text}`}>{results.repotting_guide.pot_size}</p></div>}
                {results.repotting_guide.pot_material && <div className={`p-3 rounded ${c.cardAlt}`}><p className="text-xs font-bold mb-1">🏺 Material</p><p className={`text-sm ${c.text}`}>{results.repotting_guide.pot_material}</p></div>}
              </div>
              {results.repotting_guide.drainage && <p className={`text-sm mt-2 ${c.textSecondary}`}>🕳️ {results.repotting_guide.drainage}</p>}
              {results.repotting_guide.steps?.length > 0 && (
                <div className="mt-2">
                  {results.repotting_guide.steps.map((s, i) => <p key={i} className={`text-sm ${c.textSecondary}`}>{i + 1}. {s}</p>)}
                </div>
              )}
            </div>
          )}

          {/* Propagation */}
          {results.propagation_guide && (
            <div className={`${c.propagation} border-2 rounded-xl p-5`}>
              <h3 className={`font-bold mb-2 ${c.text}`}>✂️ {results.is_saveable === false ? 'Save Its Legacy' : 'Propagation'}</h3>
              {results.is_saveable === false && <p className={`text-sm mb-2 ${c.textSecondary}`}>The mother plant may not survive, but you can propagate cuttings.</p>}
              <p className={`text-sm font-bold mb-1 ${c.text}`}>Method: {results.propagation_guide.method}</p>
              {results.propagation_guide.steps?.length > 0 && (
                <div className="space-y-1">
                  {results.propagation_guide.steps.map((s, i) => (
                    <div key={i} className={`p-2 rounded ${isDark ? 'bg-zinc-700/50' : 'bg-slate-50'}`}>
                      <p className={`text-sm ${c.text}`}><strong>{i + 1}.</strong> {s}</p>
                    </div>
                  ))}
                </div>
              )}
              {results.propagation_guide.success_rate && <p className={`text-sm mt-2 font-semibold ${c.accentTxt}`}>📊 {results.propagation_guide.success_rate}</p>}
              {results.propagation_guide.timeline && <p className={`text-xs ${c.textMuted}`}>⏱️ {results.propagation_guide.timeline}</p>}
            </div>
          )}

          {/* Environment */}
          {results.environmental_adjustments && (
            <div className={`${c.card} border ${c.border} rounded-xl p-5`}>
              <h3 className={`font-bold mb-2 ${c.text}`}>☀️ Environment</h3>
              <div className="grid grid-cols-1 md:grid-cols-3 gap-3">
                {results.environmental_adjustments.light    && <div className={`${c.cardAlt} border ${c.border} rounded-lg p-3`}><p className="text-xs font-bold mb-1">☀️ Light</p><p className={`text-sm ${c.text}`}>{results.environmental_adjustments.light}</p></div>}
                {results.environmental_adjustments.water    && <div className={`${c.cardAlt} border ${c.border} rounded-lg p-3`}><p className="text-xs font-bold mb-1">💧 Water</p><p className={`text-sm ${c.text}`}>{results.environmental_adjustments.water}</p></div>}
                {results.environmental_adjustments.location && <div className={`${c.cardAlt} border ${c.border} rounded-lg p-3`}><p className="text-xs font-bold mb-1">📍 Place</p><p className={`text-sm ${c.text}`}>{results.environmental_adjustments.location}</p></div>}
              </div>
            </div>
          )}

          {results.prevention_tips?.length > 0 && (
            <div className={`${c.card} ${c.success} border-l-4 rounded-r-lg p-5`}>
              <h3 className="font-bold mb-2">✨ Prevention</h3>
              <ul className="text-sm space-y-1">{results.prevention_tips.map((t, i) => <li key={i}>• {t}</li>)}</ul>
            </div>
          )}

          {results.climate_recommendations && (
            <div className={`${isDark ? 'bg-zinc-800 border-sky-700' : 'bg-white border-sky-300'} border-l-4 rounded-r-lg p-5`}>
              <h3 className={`font-bold mb-2 ${isDark ? 'text-sky-200' : 'text-sky-900'}`}>🌡️ Climate</h3>
              {results.climate_recommendations.seasonal_note && <p className="text-sm">{results.climate_recommendations.seasonal_note}</p>}
              {results.climate_recommendations.regional_tips?.length > 0 && (
                <ul className="text-sm space-y-1 mt-1">
                  {results.climate_recommendations.regional_tips.map((t, i) => <li key={i}>• {t}</li>)}
                </ul>
              )}
            </div>
          )}

          {/* Progress photos */}
          {activePlantId && (() => {
            const ap = getActivePlant()?.progressPhotos || [];
            return (
              <div className={`${c.card} border ${c.border} rounded-xl p-5`}>
                <div className="flex items-center justify-between mb-3">
                  <h3 className={`font-bold ${c.text}`}>📸 Progress</h3>
                  <div>
                    <input ref={progressInputRef} type="file" accept="image/*" onChange={handleAddProgressPhoto} className="hidden" />
                    <button onClick={() => progressInputRef.current?.click()}
                      className={`${c.btnSecondary} px-3 py-1.5 rounded text-xs`}>📸 Add</button>
                  </div>
                </div>
                {ap.length > 0 ? (
                  <div className="flex gap-3 overflow-x-auto pb-2">
                    {ap.map((p, i) => (
                      <div key={i} className="flex-shrink-0 w-24">
                        {p.image
                          ? <img src={p.image} alt={`P${i + 1}`} className="w-24 h-24 object-cover rounded-lg border" />
                          : <div className={`w-24 h-24 rounded-lg flex items-center justify-center ${isDark ? 'bg-zinc-700' : 'bg-emerald-100'}`}><span className="text-xl">🪴</span></div>}
                        <p className={`text-xs ${c.textMuted} text-center mt-1`}>{new Date(p.date).toLocaleDateString()}</p>
                      </div>
                    ))}
                  </div>
                ) : <p className={`text-sm ${c.textMuted}`}>Add photos to track recovery.</p>}
              </div>
            );
          })()}

          {/* Follow-up */}
          <div className={`${c.card} border ${c.border} rounded-xl p-5`}>
            <h3 className={`font-bold mb-2 ${c.text}`}>💬 Follow-Up</h3>
            <div className="flex gap-2">
              <label htmlFor="pr-followup-q" className="sr-only">Ask a follow-up</label>
              <input id="pr-followup-q" type="text" value={followUpQuestion} onChange={e => setFollowUpQuestion(e.target.value)}
                onKeyDown={e => e.key === 'Enter' && handleFollowUp()}
                placeholder="Ask anything..."
                className={`flex-1 p-3 border rounded-lg ${c.input} outline-none focus:ring-2`} />
              <button onClick={handleFollowUp} disabled={followUpLoading || !followUpQuestion.trim()}
                className={`${c.btnPrimary} px-4 py-2 rounded disabled:opacity-40`}>
                {followUpLoading ? <span className="inline-block animate-spin">{tool?.icon ?? '🪴'}</span> : '❓'}
              </button>
            </div>
            {followUpAnswer && (
              <div className={`mt-3 p-4 rounded-lg ${c.cardAlt} border ${c.border}`}>
                <p className={`text-sm ${c.textSecondary} whitespace-pre-wrap`}>{followUpAnswer}</p>
                <div className="mt-2">
                  <CopyBtn content={`Q: ${followUpQuestion}\nA: ${followUpAnswer}` + BRAND} label="Copy" />
                </div>
              </div>
            )}
          </div>

          {/* Post-result cross-refs */}
          <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
            <p className={`text-xs font-bold ${c.textMuted} uppercase tracking-wide mb-3`}>🔗 Related tools</p>
            <div className="flex flex-wrap gap-3">
              <a href="/BikeMedic" className={`text-xs ${linkStyle}`}>🚲 Bike Medic</a>
              <a href="/DoctorVisitTranslator" className={`text-xs ${linkStyle}`}>🏥 Doctor Visit Translator</a>
            </div>
          </div>
        </div>
      )}

      {/* ── Past Diagnoses ── */}
      {history.length > 0 && (
        <div className={`${c.card} border ${c.border} rounded-xl p-5`}>
          <h3 className={`text-sm font-bold mb-3 ${c.text}`}>📜 Past Diagnoses ({history.length})</h3>
          <div className="space-y-2">
            {history.map(h => (
              <div key={h.id} className={`flex items-center justify-between text-xs ${c.textSecondary} ${c.cardAlt} px-3 py-2 rounded-lg`}>
                <span className="truncate">{h.preview || '(no description)'}</span>
                <span className={`${c.textMuted} flex-shrink-0 ml-2`}>{new Date(h.date).toLocaleDateString()}</span>
              </div>
            ))}
          </div>
          <button onClick={() => setHistory([])} className={`text-xs ${c.textMuted} mt-3 hover:opacity-70`}>Clear history</button>
        </div>
      )}
    </div>
  );
};

PlantRescue.displayName = 'PlantRescue';
export default PlantRescue;
