import React, { useState, useEffect, useCallback, useRef } from 'react';
import { useRegisterActions } from '../components/ActionBarContext';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { usePersistentState } from '../hooks/usePersistentState';
import { useTheme } from '../hooks/useTheme';
import { useTranslation } from '../i18n/useTranslation';
import { currencySymbol } from '../utils/formatLocale';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const UpsellShield = ({ tool }) => {
  const { callToolEndpoint, loading, userLocale, userCurrency, userRegion } = useClaudeAPI();
  const { isDark } = useTheme();
  const { t } = useTranslation();
  const sym = currencySymbol(userLocale, userCurrency);

  const SITUATIONS = [
    { key: 'us_sit_car', emoji: '🚗' },
    { key: 'us_sit_phone', emoji: '📱' },
    { key: 'us_sit_furniture', emoji: '🛋️' },
    { key: 'us_sit_contractor', emoji: '🔨' },
    { key: 'us_sit_gym', emoji: '🏋️' },
    { key: 'us_sit_timeshare', emoji: '🏖️' },
    { key: 'us_sit_insurance', emoji: '📋' },
    { key: 'us_sit_realestate', emoji: '🏠' },
  ];

  const c = {
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:         isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20' : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-400' : 'text-gray-500',
    required:      isDark ? 'text-amber-400' : 'text-amber-700',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white' : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200' : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200' : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200' : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200' : 'bg-red-50 border-red-200 text-red-800',
    pillInactive:  isDark ? 'border-zinc-600 text-zinc-400 hover:border-zinc-500' : 'border-gray-300 text-gray-500 hover:border-gray-400',
    badge:         isDark ? 'bg-cyan-900/30 text-cyan-300' : 'bg-cyan-100 text-cyan-800',
    labelText:     isDark ? 'text-zinc-200' : 'text-gray-700',
    // Tool-specific
    hookBg:        isDark ? 'bg-cyan-900/30 border-cyan-800' : 'bg-cyan-50 border-cyan-200',
    hookText:      isDark ? 'text-cyan-200' : 'text-cyan-800',
  };
  c.textMuteded = c.textMuted;
  c.label = c.labelText;

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-700 hover:text-cyan-800 underline underline-offset-2';

  // ─── State (useState before usePersistentState — PF-11/PF-14) ───
  const [whatYouWant, setWhatYouWant] = useState('');
  const [budget, setBudget] = useState('');
  const [concerns, setConcerns] = useState('');
  const [error, setError] = useState('');
  const [expandedSections, setExpandedSections] = useState({ playbook: true });

  // ─── Persistent state ───
  const [situation, setSituation] = usePersistentState('upsellshield-situation', '');
  const [results, setResults] = usePersistentState('upsellshield-result', null);
  const [sessionHistory, setSessionHistory] = usePersistentState('upsellshield-history', []);

  const loadExample = () => {
    setSituation(t('us_ex_situation'));
    setWhatYouWant(t('us_ex_want'));
    setBudget(t('us_ex_budget', { sym }));
    setConcerns(t('us_ex_concerns', { sym }));
  };

  const resultsRef = useRef(null);

  const toggleSection = useCallback((key) => {
    setExpandedSections(p => ({ ...p, [key]: !p[key] }));
  }, []);

  // Cmd/Ctrl+Enter submits — SELECT-only guard, ref pattern to avoid stale closures
  const generateRef = useRef(null);
  const canSubmitRef = useRef(false);
  canSubmitRef.current = !!situation.trim() && !loading;

  useEffect(() => {
    const handler = (e) => {
      const tag = document.activeElement?.tagName;
      if (tag === 'SELECT') return;
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && !loading && canSubmitRef.current) {
        e.preventDefault();
        generateRef.current?.();
      }
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
  }, [loading]);

  const generate = useCallback(async (quickSit) => {
    const sit = quickSit || situation;
    if (!sit.trim()) return;
    setError(''); setResults(null);
    if (quickSit) { setSituation(quickSit); setWhatYouWant(''); setBudget(''); setConcerns(''); }
    try {
      const data = await callToolEndpoint('upsell-shield', {
        situation: sit.trim(),
        whatYouWant: whatYouWant.trim() || null,
        budget: budget.trim() || null,
        concerns: concerns.trim() || null,
        userLocale, userCurrency, userRegion,
      });
      setResults(data);
      setSessionHistory(prev => [{
        id: Date.now(), date: new Date().toISOString(),
        // PF-25 exception: 40-char preview-text truncation; session history is capped at 6.
        preview: sit.trim().slice(0, 40),
        result: data,
      }, ...prev].slice(0, 6));
    } catch (err) { setError(err.message || t('us_error')); }
  }, [situation, whatYouWant, budget, concerns, callToolEndpoint, setError, setResults, setSituation, setWhatYouWant, setBudget, setConcerns, setSessionHistory, userLocale, userCurrency, userRegion, t]);

  // Keep generateRef fresh every render so the keyboard handler always invokes the latest closure
  generateRef.current = generate;

  const handleReset = useCallback(() => {
    setSituation(''); setWhatYouWant(''); setBudget('');
    setConcerns(''); setResults(null); setError('');
  }, [setSituation, setWhatYouWant, setBudget, setConcerns, setResults, setError]);

  const buildFullText = useCallback(() => {
    if (!results) return '';
    const r = results;
    let text = `🧲 ${t('us_copy_header')}: ${situation}\n`;
    if (r.situation_read) text += `\n${r.situation_read}\n`;
    if (r.their_playbook?.length) {
      text += `\n${t('us_copy_playbook')}\n`;
      r.their_playbook.forEach((tac, i) => {
        text += `\n${i + 1}. ${tac.tactic_name}\n${t('us_copy_theydo')} ${tac.what_they_do}\n${t('us_copy_yousay')} "${tac.your_counter}"\n`;
      });
    }
    if (r.walk_away_line) text += `\n${t('us_copy_walkaway')} "${r.walk_away_line}"`;
    if (r.power_questions?.length) {
      text += `\n\n${t('us_copy_power')}\n`;
      r.power_questions.forEach(q => { text += `\n• "${q.question}"`; });
    }
    if (r.the_real_deal) {
      text += `\n\n${t('us_copy_realdeal')}\n${t('us_copy_margins')} ${r.the_real_deal.actual_margins}\n${t('us_copy_negotiable')} ${r.the_real_deal.whats_negotiable}\n${t('us_copy_insider')} ${r.the_real_deal.insider_price}`;
    }
    return text + BRAND;
  }, [results, situation, t]);

  // ─── Register export content (component-level hook) ───
  useRegisterActions(results ? buildFullText() : '', tool?.title);

  // ─── Scroll to results ───
  useEffect(() => {
    if (!results) return;
    const timer = setTimeout(() => resultsRef.current?.scrollIntoView({ behavior: 'smooth', block: 'start' }), 100);
    return () => clearTimeout(timer);
  }, [results]);

  const r = results;

  return (
    <div className={`space-y-4 ${c.text}`}>
      <div className={`${c.card} border ${c.border} rounded-xl p-6`}>
        <div className={`mb-5 pb-4 border-b ${c.border} flex items-start justify-between gap-3`}>
          <div>
            <h2 className={`text-2xl font-bold ${c.text}`}>
              <span className="mr-2">{tool?.icon ?? '🛡️'}</span>{tool?.title ?? t('us_title')}
            </h2>
            <p className={`text-sm ${c.textSecondary} mt-1`}>{tool?.tagline ?? t('us_tagline')}</p>
            <button onClick={loadExample} disabled={loading} style={{ backgroundColor: (tool?.headerColor ?? '#888888') + '80' }} className={`mt-1.5 px-2.5 py-0.5 rounded-full text-xs font-medium border disabled:opacity-40 ${isDark ? 'text-white border-white/40' : 'text-gray-800 border-transparent'}`}>{t('try_example')}</button>
          </div>
          {(results || situation.trim()) && (
            <button onClick={handleReset} className={`flex-shrink-0 ${c.btnSecondary} px-3 py-1.5 rounded-lg text-xs font-medium`}>
              ↺ {t('start_over')}
            </button>
          )}
        </div>

        {/* Opening hook */}
        <div className={`${c.hookBg} border rounded-xl p-4 mb-4`}>
          <p className={`text-sm font-medium ${c.hookText}`}>
            {t('us_hook')}
          </p>
        </div>

        <div className="mb-4">
          <label className={`text-sm font-bold ${c.text} block mb-1.5`}>{t('us_q_situation')} <span className={c.required}>*</span></label>
          <input type="text" value={situation} onChange={e => setSituation(e.target.value)}
            onKeyDown={e => e.key === 'Enter' && situation.trim() && !loading && generate()}
            placeholder={t('us_ph_situation')}
            className={`w-full px-4 py-3 border rounded-xl text-sm ${c.input} ${c.border} ${c.text} outline-none focus:ring-2`} />
        </div>

        {!results && (
          <div className="mb-4">
            <p className={`text-xs font-bold ${c.textMuted} mb-2`}>{t('us_common')}</p>
            <div className="flex flex-wrap gap-1.5">
              {SITUATIONS.map(s => (
                <button key={s.key} onClick={() => generate(t(s.key))} disabled={loading}
                  className={`px-3 py-1.5 rounded-lg text-xs font-medium border transition-colors min-h-[32px] ${c.pillInactive} disabled:opacity-40`}>
                  {s.emoji} {t(s.key)}
                </button>
              ))}
            </div>
          </div>
        )}

        <div className="mb-4">
          <label className={`text-sm font-bold ${c.text} block mb-1.5`}>{t('us_q_want')} <span className={`font-normal ${c.textMuted}`}>({t('optional')})</span></label>
          <input type="text" value={whatYouWant} onChange={e => setWhatYouWant(e.target.value)}
            placeholder={t('us_ph_want', { sym })}
            className={`w-full px-4 py-3 border rounded-xl text-sm ${c.input} ${c.border} ${c.text} outline-none focus:ring-2`} />
        </div>

        <div className="mb-4">
          <label className={`text-sm font-bold ${c.text} block mb-1.5`}>{t('us_q_budget')} <span className={`font-normal ${c.textMuted}`}>({t('optional')})</span></label>
          <input type="text" value={budget} onChange={e => setBudget(e.target.value)}
            placeholder={t('us_ph_budget', { sym })}
            className={`w-full px-4 py-3 border rounded-xl text-sm ${c.input} ${c.border} ${c.text} outline-none focus:ring-2`} />
        </div>

        <div className="mb-5">
          <label className={`text-sm font-bold ${c.text} block mb-1.5`}>{t('us_q_concerns')} <span className={`font-normal ${c.textMuted}`}>({t('optional')})</span></label>
          <input type="text" value={concerns} onChange={e => setConcerns(e.target.value)}
            placeholder={t('us_ph_concerns')}
            className={`w-full px-4 py-3 border rounded-xl text-sm ${c.input} ${c.border} ${c.text} outline-none focus:ring-2`} />
        </div>

        <p className={`text-xs text-center ${c.textMuted} mb-2`}>
          {t('us_xref_pre')} <a href="/FakeReviewDetective" className={linkStyle}>🔍 {t('us_xref_fakereview')}</a> {t('us_xref_post')}
        </p>

        <div className="flex gap-3">
          <button onClick={() => generate()} disabled={loading || !situation.trim()}
            className={`flex-1 ${c.btnPrimary} disabled:opacity-40 disabled:cursor-not-allowed font-bold py-3 px-6 rounded-xl flex items-center justify-center gap-2 min-h-[48px] shadow-lg`}>
            {loading ? <><span className="inline-block animate-spin">{tool?.icon ?? '🛡️'}</span> {t('us_arming')}</> : <><span>{tool?.icon ?? '🛡️'}</span> {t('us_prepare')}</>}
          </button>
        </div>

        {/* Try Example */}
        {!situation.trim() && !loading && (
          <div className="flex justify-center mt-3">
            <button
              onClick={loadExample}
              className={`text-xs font-medium ${c.textSecondary} underline underline-offset-2 min-h-[32px]`}
            >
              ✨ {t('try_example')}
            </button>
          </div>
        )}

        <p className={`text-xs text-center ${c.textMuted} mt-1`}>{t('us_disclaimer')}</p>
      </div>

      {error && (
        <div className={`${c.danger} border rounded-xl p-4 flex items-start gap-3`}>
          <span className="flex-shrink-0 mt-0.5">⚠️</span><p className="text-sm">{error}</p>
        </div>
      )}

      {results && (
        <div className="space-y-4">
          <div ref={resultsRef} data-results-anchor />

          {r.situation_read && (
            <div className={`${c.card} border ${c.border} rounded-xl p-5`}>
              <p className={`text-sm ${c.textSecondary} leading-relaxed`}>{r.situation_read}</p>
            </div>
          )}

          {/* Walk-away line — prominent */}
          {r.walk_away_line && (
            <div className={`${c.warning} border rounded-xl p-4`}>
              <p className="text-[10px] font-bold mb-1">{t('us_walkaway_label')}</p>
              <div className="flex items-start justify-between gap-2">
                <p className="text-sm font-bold leading-relaxed">"{r.walk_away_line}"</p>
              </div>
            </div>
          )}

          {/* Their playbook */}
          {r.their_playbook?.length > 0 && (
            <div className={`${c.card} border ${c.border} rounded-xl overflow-hidden`}>
              <button onClick={() => toggleSection('playbook')}
                className="w-full p-4 flex items-center justify-between text-left min-h-[44px]">
                <h3 className={`text-sm font-bold ${c.text} flex items-center gap-2`}>
                  <span>🎯</span> {t('us_playbook_title')}
                  <span className={`text-[9px] px-2 py-0.5 rounded-full ${c.badge}`}>{t('us_tactics_badge', { count: r.their_playbook.length })}</span>
                </h3>
                <span className={c.textMuted}>{expandedSections.playbook ? '▲' : '▼'}</span>
              </button>
              {expandedSections.playbook && (
                <div className={`px-4 pb-4 border-t ${c.border} pt-3 space-y-4`}>
                  {r.their_playbook.map((tactic, i) => (
                    <div key={i} className={`${c.cardAlt} rounded-xl p-4`}>
                      <div className="flex items-start justify-between mb-2">
                        <h4 className={`text-xs font-bold ${c.text}`}>{i + 1}. {tactic.tactic_name}</h4>
                        {tactic.when_to_expect && (
                          <span className={`text-[9px] px-2 py-0.5 rounded-full ${c.textMuted}`}>{tactic.when_to_expect}</span>
                        )}
                      </div>
                      <p className={`text-xs ${c.textSecondary} mb-2`}><span className="font-bold">{t('us_theyll_do')}</span> {tactic.what_they_do}</p>
                      <p className={`text-xs ${c.textMuted} mb-2 italic`}><span className="font-bold">{t('us_psychology')}</span> {tactic.the_psychology}</p>
                      <div className={`${c.success} border rounded-lg p-3 flex items-start justify-between gap-2`}>
                        <div>
                          <p className="text-[10px] font-bold mb-0.5">{t('us_your_counter')}</p>
                          <p className="text-xs font-medium">"{tactic.your_counter}"</p>
                        </div>
                      </div>
                    </div>
                  ))}
                </div>
              )}
            </div>
          )}

          {/* Power questions */}
          {r.power_questions?.length > 0 && (
            <div className={`${c.card} border ${c.border} rounded-xl overflow-hidden`}>
              <button onClick={() => toggleSection('power')}
                className="w-full p-4 flex items-center justify-between text-left min-h-[44px]">
                <h3 className={`text-sm font-bold ${c.text} flex items-center gap-2`}><span>💪</span> {t('us_power_title')}</h3>
                <span className={c.textMuted}>{expandedSections.power ? '▲' : '▼'}</span>
              </button>
              {expandedSections.power && (
                <div className={`px-4 pb-4 border-t ${c.border} pt-3 space-y-3`}>
                  {r.power_questions.map((q, i) => (
                    <div key={i} className={`${c.cardAlt} rounded-lg p-3`}>
                      <p className={`text-xs font-bold ${c.text} mb-1`}>"{q.question}"</p>
                      <p className={`text-xs ${c.textMuted} italic`}>{q.what_it_signals}</p>
                    </div>
                  ))}
                </div>
              )}
            </div>
          )}

          {/* The real deal */}
          {r.the_real_deal && (
            <div className={`${c.card} border ${c.border} rounded-xl p-5`}>
              <h3 className={`text-sm font-bold ${c.text} mb-3 flex items-center gap-2`}><span>🔍</span> {t('us_realdeal_title')}</h3>
              <div className="space-y-2">
                {[
                  [t('us_rd_margins'), r.the_real_deal.actual_margins],
                  [t('us_rd_negotiable'), r.the_real_deal.whats_negotiable],
                  [t('us_rd_insider'), r.the_real_deal.insider_price],
                  [t('us_rd_timing'), r.the_real_deal.timing_advantage],
                ].filter(([, v]) => v).map(([label, value], i) => (
                  <div key={i}>
                    <p className={`text-[10px] font-bold ${c.textMuted}`}>{label}</p>
                    <p className={`text-xs ${c.textSecondary}`}>{value}</p>
                  </div>
                ))}
              </div>
            </div>
          )}

          {/* Pre-visit checklist */}
          {r.pre_visit_checklist?.length > 0 && (
            <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
              <p className={`text-xs font-bold ${c.text} mb-2`}>{t('us_checklist_title')}</p>
              <div className="space-y-1.5">
                {r.pre_visit_checklist.map((item, i) => (
                  <p key={i} className={`text-xs ${c.textSecondary}`}>☐ {item}</p>
                ))}
              </div>
            </div>
          )}

          {/* Body language */}
          {r.body_language?.length > 0 && (
            <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
              <p className={`text-xs font-bold ${c.text} mb-2`}>{t('us_body_title')}</p>
              <div className="space-y-1.5">
                {r.body_language.map((tip, i) => (
                  <p key={i} className={`text-xs ${c.textSecondary}`}>• {tip}</p>
                ))}
              </div>
            </div>
          )}

          {/* Nuclear option */}
          {r.the_nuclear_option && (
            <div className={`${c.danger} border rounded-xl p-4 flex items-start gap-3`}>
              <span className="flex-shrink-0 mt-0.5">☢️</span>
              <div>
                <p className="text-xs font-bold mb-1">{t('us_nuclear_title')}</p>
                <p className="text-sm leading-relaxed">{r.the_nuclear_option}</p>
              </div>
            </div>
          )}

          {/* Conditional cross-ref: if they have a real deal section, surface finance tools */}
          {r.the_real_deal && (
            <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
              <p className={`text-xs font-bold ${c.textMuted} mb-2`}>{t('us_also_useful')}</p>
              <div className="flex flex-wrap gap-3">
                <a href="/MarkupDetective" className={`text-xs ${linkStyle}`}>🔍 {t('us_xref_markup')}</a>
              </div>
            </div>
          )}


        </div>
      )}


      {/* Session sessionHistory */}
      {/* eslint-disable-next-line no-restricted-globals */}
      {sessionHistory.length > 0 && (
        <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4 mt-4`}>
          <p className={`text-xs font-bold ${c.textMuted} mb-2`}>{t('us_recent')}</p>
          <div className="space-y-1">
            {/* eslint-disable-next-line no-restricted-globals */}

            {sessionHistory.map(s => (
              <div key={s.id} className="flex items-center justify-between">
                <span className={`text-xs ${c.textSecondary} truncate`}>{s.preview || t('us_session')}</span>
                <span className={`text-xs ${c.textMuted} ml-2 shrink-0`}>{new Date(s.date).toLocaleDateString()}</span>
              </div>
            ))}
          </div>
        </div>
      )}
    </div>
  );
};

UpsellShield.displayName = 'UpsellShield';
export default UpsellShield;
