import React, { useState, useCallback, useRef, useEffect } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { usePersistentState } from '../hooks/usePersistentState';
import { useRegisterActions } from '../components/ActionBarContext';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const MECHANISM_CONFIG = {
  broadcast:  { label: '📢 Broadcast a Signal',         color: (d) => d ? 'bg-[rgb(44,74,110)]/20 text-[rgb(168,185,206)] border-[rgb(44,74,110)]/40'  : 'bg-[rgb(232,238,245)] text-[rgb(30,58,88)] border-[rgb(176,196,216)]' },
  infiltrate: { label: '🚪 Infiltrate a New Room',       color: (d) => d ? 'bg-[rgb(200,135,46)]/15 text-[rgb(217,160,78)] border-[rgb(200,135,46)]/40'  : 'bg-[rgb(253,243,228)] text-[rgb(147,84,31)] border-[rgb(224,196,154)]' },
  create:     { label: '✍️ Create a Serendipity Artifact', color: (d) => d ? 'bg-[rgb(90,138,92)]/15 text-[rgb(138,186,140)] border-[rgb(90,138,92)]/40' : 'bg-[rgb(232,240,232)] text-[rgb(61,110,63)] border-[rgb(168,196,168)]' },
  curate:     { label: '🧲 Curate a High-Value Connection', color: (d) => d ? 'bg-[rgb(181,74,63)]/15 text-[rgb(232,169,163)] border-[rgb(181,74,63)]/40' : 'bg-[rgb(252,234,232)] text-[rgb(138,48,40)] border-[rgb(212,144,138)]' },
  compound:   { label: '⚡ Compound an Existing Asset',  color: (d) => d ? 'bg-[rgb(61,54,48)]/60 text-[rgb(200,195,185)] border-[rgb(61,54,48)]'     : 'bg-[rgb(243,239,232)] text-[rgb(94,80,66)] border-[rgb(213,202,184)]' },
};

const EXAMPLES = [
  {
    desc: 'Software engineer, WFH, same team for 3 years. Live in Denver. Go to the gym, cook, watch TV. Small tight friend group. Work is fine but I feel invisible professionally.',
    goals: 'Better job opportunities, interesting collaborators, maybe a co-founder someday',
  },
  {
    desc: 'Freelance designer, mostly work through referrals from 2 clients. Active on Instagram but not professionally. Go to the same coffee shop every day. Haven\'t been to an industry event in 2 years.',
    goals: 'More and better clients, creative collaborators, maybe an agent or gallery connection',
  },
];

const LuckSurface = ({ tool }) => {
  const { isDark } = useTheme();
  const { callToolEndpoint, loading } = useClaudeAPI();

  const c = {
    card:          isDark ? 'bg-[rgb(42,38,35)]' : 'bg-white',
    cardAlt:       isDark ? 'bg-[rgb(51,46,42)]' : 'bg-[rgb(250,248,245)]',
    input:         isDark ? 'bg-[rgb(26,24,22)] border-[rgb(61,54,48)] text-[rgb(240,238,234)] placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20'
                          : 'bg-[rgb(250,248,245)] border-[rgb(213,202,184)] text-[rgb(61,57,53)] placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-[rgb(240,238,234)]' : 'text-[rgb(61,57,53)]',
    textSecondary: isDark ? 'text-[rgb(200,195,185)]' : 'text-[rgb(90,84,74)]',
    textMuted:     isDark ? 'text-[rgb(138,130,117)]' : 'text-[rgb(138,130,117)]',
    labelText:     isDark ? 'text-zinc-200' : 'text-gray-700',
    accentTxt:     isDark ? 'text-cyan-400' : 'text-cyan-600',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white'
                          : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-[rgb(51,46,42)] hover:bg-[rgb(61,54,48)] text-[rgb(200,195,185)] border border-[rgb(61,54,48)]'
                          : 'bg-[rgb(243,239,232)] hover:bg-[rgb(232,225,213)] text-[rgb(94,80,66)] border border-[rgb(213,202,184)]',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200'
                          : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200'
                          : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200'
                          : 'bg-red-50 border-red-200 text-red-800',
    required:      isDark ? 'text-amber-400' : 'text-amber-500',
    // Tool-specific keys
    goldText:      isDark ? 'text-[rgb(217,160,78)]' : 'text-[rgb(147,84,31)]',
  };
  c.textMuteded = c.textMuted;
  c.label = c.labelText;

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';

  const [description, setDescription] = useState('');
  const [goals, setGoals] = useState('');
  const [currentExposures, setCurrentExposures] = useState('');
  const [results, setResults] = useState(null);
  const [error, setError] = useState('');
  const [expanded, setExpanded] = useState({});

  // ── Persistent state ──
  const [history, setHistory] = usePersistentState('lucksurface-history', []);
  const toggle = (k) => setExpanded(p => ({ ...p, [k]: !p[k] }));

  const handleSubmit = async () => {
    if (!description.trim()) return;
    setError(''); setResults(null);
    try {
      const data = await callToolEndpoint('luck-surface', {
        description: description.trim(),
        goals: goals.trim() || undefined,
        currentExposures: currentExposures.trim() || undefined,
      });
      setHistory(prev => [{ id: Date.now().toString(), date: new Date().toISOString(), preview: (description || '').slice(0, 40) }, ...prev].slice(0, 6));
      setResults(data);
    } catch (e) { setError(e.message || 'Failed to calculate luck surface.'); }
  };

  const handleExample = () => {
    const ex = EXAMPLES[Math.floor(Math.random() * EXAMPLES.length)];
    setDescription(ex.desc); setGoals(ex.goals); setResults(null);
  };

  const buildFullText = useCallback(() => {
    if (!results) return '';
    const a = results.audit;
    let t = `🧲 LUCK SURFACE ANALYSIS\n\nCurrent surface area: ${a?.current_surface_area}\n${a?.the_diagnosis}\n\n`;
    t += `THE INVISIBLE WALL: ${a?.the_invisible_wall}\n\n`;
    t += `5 MOVES:\n`;
    results.the_five_moves?.forEach((m, i) => {
      t += `${i + 1}. ${m.title}\n${m.the_move}\nWhy it works: ${m.why_asymmetric}\n\n`;
    });
    if (results.the_one_to_start) t += `START HERE: ${results.the_one_to_start}\n`;
    return t + BRAND;
  }, [results, description, goals]);

  useRegisterActions(buildFullText(), tool?.title || 'Luck Surface');

  // ── Keyboard + scroll refs ──
  const handleSubmitRef = useRef(null);
  const canSubmitRef = useRef(false);
  const resultsRef = useRef(null);
  handleSubmitRef.current = handleSubmit;
  canSubmitRef.current = !!description.trim();

  useEffect(() => {
    if (!results || !resultsRef.current) return;
    const t = setTimeout(() => resultsRef.current?.scrollIntoView({ behavior: 'smooth', block: 'start' }), 200);
    return () => clearTimeout(t);
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [results]);

  useEffect(() => {
    const handler = (e) => {
      const tag = document.activeElement?.tagName;
      if (tag === 'SELECT') return;
      if ((e.metaKey || e.ctrlKey) && e.key === 'Enter' && !loading && canSubmitRef.current) {
        e.preventDefault();
        handleSubmitRef.current?.();
      }
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [loading]);

  return (
    <div className={`space-y-4 ${c.text}`}>

      {/* ── Persistent Header ── */}
      <div className={`${c.card} border ${c.border} rounded-xl shadow-sm`}>
        <div className="px-5 pt-5">
          <div className="pb-3 border-b border-zinc-500">
            <h2 className={`text-xl font-bold ${c.text}`}>
              <span className="mr-2">{tool?.icon ?? '🧲'}</span>{tool?.title ?? 'Luck Surface'}
            </h2>
            <p className={`text-sm ${c.textSecondary}`}>{tool?.tagline ?? 'Calculate your luck surface area. Expand it with 5 specific moves.'}</p>
          </div>
        </div>
      </div>

      {!results && (
          <div className={`rounded-2xl border p-6 shadow-sm space-y-4 ${c.card} ${c.border}`}>
            <div>
              <label className={`block text-sm font-semibold mb-1.5 ${c.text}`}>
                Describe your life — what you do, where you go, who you know <span className={c.required}>*</span>
              </label>
              <textarea value={description} onChange={e => setDescription(e.target.value)}
                placeholder="Your work, hobbies, social patterns, where you spend time, what you make or share publicly…"
                rows={5} maxLength={800}
                className={`w-full px-4 py-3 rounded-xl border text-sm resize-none ${c.input}`} />
            </div>
            <div className="grid grid-cols-1 sm:grid-cols-2 gap-3">
              <div>
                <label className={`block text-sm font-semibold mb-1.5 ${c.text}`}>What kind of luck do you want? <span className={`font-normal ${c.textMuteded}`}>(optional)</span></label>
                <input type="text" value={goals} onChange={e => setGoals(e.target.value)}
                  placeholder="Better opportunities, collaborators, investors, mentors…"
                  className={`w-full px-4 py-3 rounded-xl border text-sm ${c.input}`} />
              </div>
              <div>
                <label className={`block text-sm font-semibold mb-1.5 ${c.text}`}>What you already do to put yourself out there <span className={`font-normal ${c.textMuteded}`}>(optional)</span></label>
                <input type="text" value={currentExposures} onChange={e => setCurrentExposures(e.target.value)}
                  placeholder="Events, online presence, communities…"
                  className={`w-full px-4 py-3 rounded-xl border text-sm ${c.input}`} />
              </div>
            </div>
            {error && <div className={`p-3 rounded-xl border text-sm ${c.danger}`}><span className="mr-1">⚠️</span>{error}</div>}
            <div className="flex gap-2">
              <button onClick={handleSubmit} disabled={loading || !description.trim()}
                className={`flex-1 py-3 rounded-xl font-bold disabled:opacity-40 ${c.btnPrimary}`}>
                {loading
                ? <><span className="inline-block animate-spin">{tool?.icon ?? '⚙️'}</span> Calculating…</>
                : <><span className="mr-1">{tool?.icon ?? '🧲'}</span> Calculate My Luck Surface</>}
              </button>
              <button onClick={handleExample} disabled={loading}
                className={`px-4 py-3 rounded-xl text-sm font-medium ${c.btnSecondary}`}>
                Example
              </button>
            </div>
          </div>
        )}

        {results && (
          <div className="space-y-4" ref={resultsRef}>

            {/* Audit — the gauge */}
            {results.audit && (
              <div className={`rounded-2xl border p-6 ${c.card} ${c.border}`}>
                <div className="flex items-center justify-between mb-4">
                  <p className={`text-xs font-black uppercase tracking-widest ${c.textMuteded}`}>Current Luck Surface Area</p>
                  <span className={`text-4xl font-black ${isDark ? 'text-[rgb(217,160,78)]' : 'text-[rgb(147,84,31)]'}`}>{results.audit.current_surface_area}</span>
                </div>
                {/* Progress bar */}
                <div className={`h-3 rounded-full mb-4 ${isDark ? 'bg-[rgb(61,54,48)]' : 'bg-[rgb(232,225,213)]'}`}>
                  <div className="h-full rounded-full transition-all"
                    style={{
                      width: results.audit.current_surface_area,
                      background: 'linear-gradient(90deg, #c8872e, #d9a04e)',
                    }} />
                </div>
                <p className={`text-sm leading-relaxed mb-3 ${c.textSecondary}`}>{results.audit.the_diagnosis}</p>
                {results.audit.the_invisible_wall && (
                  <div className={`p-3 rounded-xl border ${isDark ? 'bg-[rgb(181,74,63)]/10 border-[rgb(181,74,63)]/30' : 'bg-[rgb(252,234,232)] border-[rgb(212,144,138)]'}`}>
                    <p className={`text-xs font-bold uppercase tracking-wide mb-1 ${isDark ? 'text-[rgb(232,169,163)]' : 'text-[rgb(138,48,40)]'}`}>🧱 The Invisible Wall</p>
                    <p className={`text-sm ${c.textSecondary}`}>{results.audit.the_invisible_wall}</p>
                  </div>
                )}
                {results.audit.what_theyre_good_at && (
                  <p className={`text-xs mt-3 ${c.textMuteded}`}><span className="font-semibold">What's already working:</span> {results.audit.what_theyre_good_at}</p>
                )}
              </div>
            )}

            {/* Five moves */}
            {results.the_five_moves?.length > 0 && (
              <div className="space-y-3">
                <p className={`text-xs font-black uppercase tracking-widest px-1 ${c.textMuteded}`}>⚡ The Five Moves</p>
                {results.the_five_moves.map((move, i) => {
                  const mcfg = MECHANISM_CONFIG[move.mechanism] || MECHANISM_CONFIG.broadcast;
                  const isOpen = expanded[i];
                  return (
                    <div key={i} className={`rounded-2xl border overflow-hidden ${c.card} ${c.border}`}>
                      <button onClick={() => toggle(i)} className="w-full text-left p-5">
                        <div className="flex items-start justify-between gap-3">
                          <div className="flex items-center gap-3 min-w-0">
                            <span className={`flex-shrink-0 w-7 h-7 rounded-full flex items-center justify-center text-sm font-black ${isDark ? 'bg-[rgb(44,74,110)] text-white' : 'bg-[rgb(44,74,110)] text-white'}`}>{i + 1}</span>
                            <div className="min-w-0">
                              <p className={`font-bold text-sm ${c.text}`}>{move.title}</p>
                              <span className={`text-xs font-semibold px-2 py-0.5 rounded-full border ${mcfg.color(isDark)}`}>{mcfg.label}</span>
                            </div>
                          </div>
                          <span className={`flex-shrink-0 text-sm mt-1 ${c.textMuteded}`}>{isOpen ? '▲' : '▼'}</span>
                        </div>
                        {!isOpen && <p className={`text-sm mt-2 line-clamp-2 ${c.textMuteded}`}>{move.the_move}</p>}
                      </button>
                      {isOpen && (
                        <div className={`px-5 pb-5 space-y-3 border-t ${c.border}`}>
                          <div className="pt-3">
                            <p className={`text-xs font-bold uppercase tracking-wide mb-1 ${c.textMuteded}`}>The Move</p>
                            <p className={`text-sm ${c.text}`}>{move.the_move}</p>
                          </div>
                          {move.why_asymmetric && (
                            <p className={`text-xs ${c.textMuteded}`}><span className="font-semibold">Why it's asymmetric:</span> {move.why_asymmetric}</p>
                          )}
                          <div className="flex flex-wrap gap-3 text-xs">
                            {move.luck_multiplier && (
                              <span className={`px-2 py-1 rounded-lg ${c.cardAlt} ${c.border} border ${c.textMuteded}`}>🍀 {move.luck_multiplier}</span>
                            )}
                            {move.time_to_first_result && (
                              <span className={`px-2 py-1 rounded-lg ${c.cardAlt} ${c.border} border ${c.textMuteded}`}>⏱️ {move.time_to_first_result}</span>
                            )}
                          </div>
                        </div>
                      )}
                    </div>
                  );
                })}
              </div>
            )}

            {/* Target */}
            {results.the_target && (
              <div className={`rounded-2xl border p-5 ${isDark ? 'bg-[rgb(200,135,46)]/10 border-[rgb(200,135,46)]/40' : 'bg-[rgb(253,243,228)] border-[rgb(224,196,154)]'}`}>
                <div className="flex items-center justify-between mb-3">
                  <p className={`text-xs font-black uppercase tracking-widest ${c.goldText}`}>After 5 Moves</p>
                  <span className={`text-3xl font-black ${c.goldText}`}>{results.the_target.new_surface_area}</span>
                </div>
                <div className={`h-3 rounded-full mb-3 ${isDark ? 'bg-[rgb(61,54,48)]' : 'bg-[rgb(232,225,213)]'}`}>
                  <div className="h-full rounded-full" style={{ width: results.the_target.new_surface_area, background: 'linear-gradient(90deg, rgb(44,74,110), #4a6a8a)' }} />
                </div>
                {results.the_target.what_becomes_possible && (
                  <p className={`text-sm mb-2 ${c.textSecondary}`}>{results.the_target.what_becomes_possible}</p>
                )}
                {results.the_target.the_compound_effect && (
                  <p className={`text-xs italic ${c.textMuteded}`}>{results.the_target.the_compound_effect}</p>
                )}
              </div>
            )}

            {/* Start here */}
            {results.the_one_to_start && (
              <div className={`rounded-2xl border-2 p-5 ${isDark ? 'border-[rgb(44,74,110)] bg-[rgb(44,74,110)]/10' : 'border-[rgb(44,74,110)] bg-[rgb(232,238,245)]'}`}>
                <p className={`text-xs font-black uppercase tracking-widest mb-2 ${isDark ? 'text-[rgb(168,185,206)]' : 'text-[rgb(44,74,110)]'}`}>⚡ Start Here — Today</p>
                <p className={`text-sm font-medium ${c.text}`}>{results.the_one_to_start}</p>
              </div>
            )}

            <div className="flex items-center gap-3 flex-wrap">
              <button onClick={() => { setResults(null); setDescription(''); setGoals(''); setCurrentExposures(''); setExpanded({}); }}
                className={`px-4 py-2 rounded-lg text-sm font-medium ${c.btnSecondary}`}>
                🔄 New Analysis
              </button>
            </div>

            <div className={`rounded-xl border p-4 ${c.cardAlt} ${c.border}`}>
              <p className={`text-xs font-semibold uppercase tracking-wide mb-3 ${c.textMuteded}`}>Related tools</p>
              <div className="flex flex-wrap gap-2">
                {[
                  { id: 'GravityWell', icon: '🌀', label: 'Pull a specific person into your orbit' },
                  { id: 'ChaosPilot', icon: '🎰', label: 'One calculated disruption' },
                  { id: 'RulebookBreaker', icon: '🏴‍☠️', label: 'Find the cheat codes in any system' },
                ].map(r => (
                  <a key={r.id} href={`/${r.id}`}
                    className={`text-xs ${linkStyle}`}>
                    {r.icon} {r.label}
                  </a>
                ))}
              </div>
            </div>
          </div>
      )}

      {/* Disclaimer */}
      <div className={`${c.cardAlt} border ${c.border} rounded-xl p-3 text-center`}>
        <p className={`text-xs ${c.textMuted}`}>
          AI-generated suggestions — use your own judgment about what's right for your situation.
        </p>
      </div>

    </div>
  );
};

LuckSurface.displayName = 'LuckSurface';
export default LuckSurface;
