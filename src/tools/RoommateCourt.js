import React, { useState, useEffect, useCallback, useRef } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { usePersistentState } from '../hooks/usePersistentState';
import { CopyBtn } from '../components/ActionButtons';
import { useRegisterActions } from '../components/ActionBarContext';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

// ════════════════════════════════════════════════════════════
// MODULE-LEVEL CONSTANTS
// ════════════════════════════════════════════════════════════
const DISPUTE_CATEGORIES = [
  { id: 'chores',     label: '🧹 Chores' },
  { id: 'noise',      label: '🔊 Noise' },
  { id: 'guests',     label: '👥 Guests' },
  { id: 'space',      label: '🛋️ Shared space' },
  { id: 'money',      label: '💰 Money/Bills' },
  { id: 'food',       label: '🍕 Food/Kitchen' },
  { id: 'pets',       label: '🐾 Pets' },
  { id: 'boundaries', label: '🚪 Boundaries' },
  { id: 'lease',      label: '📝 Lease/Moving' },
  { id: 'other',      label: '💬 Other' },
];

const DURATION_OPTIONS = [
  { id: 'just_now', label: 'Just happened' },
  { id: 'days',     label: 'Days' },
  { id: 'weeks',    label: 'Weeks' },
  { id: 'months',   label: 'Months' },
  { id: 'forever',  label: 'Recurring forever' },
];

const COMM_OPTIONS = [
  { id: 'not_yet',     label: 'Not yet' },
  { id: 'tried',       label: 'Tried, went badly' },
  { id: 'circles',     label: 'Going in circles' },
  { id: 'wont_engage', label: "They won't engage" },
];

const LIVING_OPTIONS = [
  { id: 'dorm',     label: '🎓 College dorm' },
  { id: 'apartment',label: '🏢 Shared apt' },
  { id: 'house',    label: '🏠 House' },
  { id: 'partner',  label: '💑 Partner' },
  { id: 'family',   label: '👨‍👩‍👧 Family' },
];

const COMMON_CHORES = [
  'Dishes', 'Vacuum', 'Bathroom', 'Trash', 'Kitchen wipe-down',
  'Mopping', 'Laundry (shared)', 'Grocery run',
];

const EFFORT_LABEL = { 1: 'Light', 2: 'Medium', 3: 'Heavy' };

// ════════════════════════════════════════════════════════════
// MAIN COMPONENT
// ════════════════════════════════════════════════════════════
const RoommateCourt = ({ tool }) => {
  const { callToolEndpoint, loading } = useClaudeAPI();
  const { isDark } = useTheme();

  // ── Theme — verbatim from CONVENTIONS.md PF-2 ──
  const c = {
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:         isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20'
                          : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-500' : 'text-gray-400',
    labelText:     isDark ? 'text-zinc-200' : 'text-gray-700',
    accentTxt:     isDark ? 'text-cyan-400' : 'text-cyan-600',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white'
                          : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200'
                          : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200'
                          : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200'
                          : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200'
                          : 'bg-red-50 border-red-200 text-red-800',
    infoBox:       isDark ? 'bg-sky-900/20 border-sky-700 text-sky-200'
                          : 'bg-sky-50 border-sky-200 text-sky-800',
    successBox:    isDark ? 'bg-emerald-900/20 border-emerald-700' : 'bg-emerald-50 border-emerald-300',
    successTxt:    isDark ? 'text-emerald-300' : 'text-emerald-800',
    warningBox:    isDark ? 'bg-amber-900/20 border-amber-700' : 'bg-amber-50 border-amber-300',
    warningTxt:    isDark ? 'text-amber-300' : 'text-amber-800',
    pillActive:    isDark ? 'border-cyan-500 bg-cyan-900/30 text-cyan-200'
                          : 'border-cyan-600 bg-cyan-100 text-cyan-900',
    pillInactive:  isDark ? 'border-zinc-600 text-zinc-400 hover:border-zinc-500'
                          : 'border-gray-300 text-gray-500 hover:border-gray-400',
  };
  c.textMuteded   = c.textMuted;
  c.label         = c.labelText;
  // RoommateCourt extensions — tool-specific keys
  c.cardAltCard   = isDark ? 'bg-zinc-700/40' : 'bg-slate-50';
  c.cardAltInset  = isDark ? 'bg-zinc-800/80' : 'bg-white';
  c.barColors     = ['bg-cyan-500', 'bg-amber-500', 'bg-emerald-500', 'bg-purple-500', 'bg-rose-500'];
  c.effortHeavy   = isDark ? 'bg-red-900/30 border-red-700 text-red-300'     : 'bg-red-50 border-red-200 text-red-700';
  c.effortMed     = isDark ? 'bg-amber-900/30 border-amber-700 text-amber-300' : 'bg-amber-50 border-amber-200 text-amber-700';
  c.effortLight   = isDark ? 'bg-emerald-900/30 border-emerald-700 text-emerald-300' : 'bg-emerald-50 border-emerald-200 text-emerald-700';
  c.scriptBg      = isDark ? 'bg-zinc-900/60 border-zinc-600' : 'bg-slate-100 border-slate-200';
  c.tabActive     = isDark ? 'bg-cyan-600 text-white border-cyan-500' : 'bg-cyan-600 text-white border-cyan-600';
  c.tabInactive   = isDark ? 'bg-zinc-800 text-zinc-400 hover:text-zinc-200 border-zinc-700' : 'bg-white text-gray-500 hover:text-gray-700 border-gray-200';
  c.verdictBg     = isDark ? 'bg-amber-900/20 border-amber-700' : 'bg-amber-50 border-amber-300';
  c.verdictText   = isDark ? 'text-amber-200' : 'text-amber-900';
  c.warnText      = isDark ? 'text-amber-300' : 'text-amber-700';
  c.deleteHover   = isDark ? 'hover:text-red-400 transition-colors' : 'hover:text-red-500 transition-colors';

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';

  // ── State ──
  const [activeTab, setActiveTab] = useState('dispute');

  // Dispute tab
  const [dispute, setDispute] = useState('');
  const [category, setCategory] = useState('');
  const [yourSide, setYourSide] = useState('');
  const [theirSide, setTheirSide] = useState('');
  const [duration, setDuration] = useState('');
  const [priorComm, setPriorComm] = useState('');
  const [livingSituation, setLivingSituation] = useState('');
  const [disputeResult, setDisputeResult] = useState(null);
  const [expandedSections, setExpandedSections] = useState({
    verdict: true, underlying: false, resolution: true, stuck: false, prevention: false, reality: false,
  });

  // Chore tab
  const [newRoommate, setNewRoommate] = useState('');
  const [newChore, setNewChore] = useState('');
  const [assignResult, setAssignResult] = useState(null);
  const [isShuffling, setIsShuffling] = useState(false);
  const [showRebalance, setShowRebalance] = useState(false);
  const [complaint, setComplaint] = useState('');
  const [rebalanceResult, setRebalanceResult] = useState(null);
  const [showHistory, setShowHistory] = useState(false);

  // Shared
  const [error, setError] = useState('');

  // ── Refs ──
  const resultsRef   = useRef(null);
  const submitRef    = useRef(null);
  const canSubmitRef = useRef(false);
  const tabRef       = useRef('dispute');

  // ── Persistent state ──
  const [roommates, setRoommates]         = usePersistentState('court-members', []);
  const [choreList, setChoreList]         = usePersistentState('court-chores', []);
  const [assignHistory, setAssignHistory] = usePersistentState('court-history', []);
  const [currentRound, setCurrentRound]   = usePersistentState('court-current-round', null);

  // ── Helpers ──
  const toggleSection = useCallback((key) => {
    setExpandedSections(prev => ({ ...prev, [key]: !prev[key] }));
  }, []);

  const effortStyle = useCallback((effort) => {
    if (effort >= 3) return c.effortHeavy;
    if (effort >= 2) return c.effortMed;
    return c.effortLight;
  }, [c.effortHeavy, c.effortMed, c.effortLight]);

  // ── Dispute handlers ──
  const submitDispute = useCallback(async () => {
    if (!dispute.trim()) return;
    setError(''); setDisputeResult(null);
    try {
      const res = await callToolEndpoint('roommate-court', {
        action: 'mediate',
        dispute: dispute.trim(),
        category: DISPUTE_CATEGORIES.find(d => d.id === category)?.label.replace(/^..\s/, '') || category,
        yourSide: yourSide.trim(),
        theirSide: theirSide.trim(),
        duration: DURATION_OPTIONS.find(d => d.id === duration)?.label || duration,
        priorCommunication: COMM_OPTIONS.find(d => d.id === priorComm)?.label || priorComm,
        livingSituation: LIVING_OPTIONS.find(d => d.id === livingSituation)?.label.replace(/^..\s/, '') || livingSituation,
      });
      setDisputeResult(res);
      setExpandedSections({ verdict: true, underlying: false, resolution: true, stuck: false, prevention: false, reality: false });
    } catch (err) { setError(err.message || 'Failed to mediate dispute'); }
  }, [dispute, category, yourSide, theirSide, duration, priorComm, livingSituation, callToolEndpoint]);

  const resetDispute = useCallback(() => {
    setDispute(''); setCategory(''); setYourSide(''); setTheirSide('');
    setDuration(''); setPriorComm(''); setLivingSituation('');
    setDisputeResult(null); setError('');
  }, []);

  // ── Chore handlers ──
  const addRoommate = useCallback(() => {
    const name = newRoommate.trim();
    if (!name || roommates.includes(name)) return;
    setRoommates(prev => [...prev, name]);
    setNewRoommate('');
  }, [newRoommate, roommates, setRoommates]);

  const removeRoommate = useCallback((name) => {
    setRoommates(prev => prev.filter(n => n !== name));
  }, [setRoommates]);

  const addChore = useCallback((choreName) => {
    const name = (choreName || newChore).trim();
    if (!name || choreList.includes(name)) return;
    setChoreList(prev => [...prev, name]);
    setNewChore('');
  }, [newChore, choreList, setChoreList]);

  const removeChore = useCallback((name) => {
    setChoreList(prev => prev.filter(n => n !== name));
  }, [setChoreList]);

  const assignChores = useCallback(async () => {
    if (roommates.length < 2 || choreList.length < 1) return;
    setError(''); setAssignResult(null); setRebalanceResult(null);
    setShowRebalance(false); setComplaint('');
    setIsShuffling(true);

    // Brief shuffle animation delay
    await new Promise(r => setTimeout(r, 1200));

    try {
      const res = await callToolEndpoint('roommate-court', {
        action: 'assign',
        roommates,
        chores: choreList,
        history: assignHistory.slice(0, 6),
      });
      setAssignResult(res);
      setIsShuffling(false);
      const round = {
        id: Date.now(),
        date: new Date().toLocaleDateString(),
        assignments: res.assignments,
        fairness_score: res.fairness_score,
        completed: {},
        preview: `Round ${new Date().toLocaleDateString()}`.slice(0, 40),
      };
      setCurrentRound(round);
    } catch (err) {
      setError(err.message || 'Failed to assign chores');
      setIsShuffling(false);
    }
  }, [roommates, choreList, assignHistory, callToolEndpoint, setCurrentRound]);

  const saveRoundToHistory = useCallback(() => {
    if (!currentRound) return;
    setAssignHistory(prev => [currentRound, ...prev].slice(0, 6));
    setCurrentRound(null);
    setAssignResult(null);
  }, [currentRound, setAssignHistory, setCurrentRound]);

  const toggleChoreComplete = useCallback((roommate, choreName) => {
    if (!currentRound) return;
    const key = `${roommate}::${choreName}`;
    setCurrentRound(prev => ({
      ...prev,
      completed: { ...prev.completed, [key]: !prev.completed[key] },
    }));
  }, [currentRound, setCurrentRound]);

  const submitRebalance = useCallback(async () => {
    if (!complaint.trim() || !assignResult) return;
    setError(''); setRebalanceResult(null);
    try {
      const res = await callToolEndpoint('roommate-court', {
        action: 'rebalance',
        currentAssignments: assignResult.assignments,
        complaint: complaint.trim(),
        history: assignHistory.slice(0, 6),
        roommates,
      });
      setRebalanceResult(res);
      if (res.complaint_valid && res.revised_assignments) {
        setAssignResult(prev => ({
          ...prev,
          assignments: res.revised_assignments,
          effort_totals: res.revised_effort_totals || prev.effort_totals,
          fairness_score: res.revised_fairness_score || prev.fairness_score,
        }));
        setCurrentRound(prev => prev ? {
          ...prev,
          assignments: res.revised_assignments,
          fairness_score: res.revised_fairness_score || prev.fairness_score,
        } : prev);
      }
    } catch (err) { setError(err.message || 'Failed to rebalance'); }
  }, [complaint, assignResult, assignHistory, roommates, callToolEndpoint, setCurrentRound]);

  // ── buildFullText ──
  const buildFullText = useCallback(() => {
    const lines = [];
    if (disputeResult) {
      const r = disputeResult;
      lines.push('⚖️ ROOMMATE COURT', '');
      if (r.verdict) {
        const ruling = r.verdict.whos_right === 'you' ? "You're right"
          : r.verdict.whos_right === 'them' ? "They're right"
          : r.verdict.whos_right === 'both' ? "You're both right" : "Neither is right";
        lines.push(`Ruling: ${ruling}`);
        if (r.verdict.reasoning) lines.push(r.verdict.reasoning);
        lines.push(`Fault split: You ${r.verdict.your_fault_pct}% / Them ${r.verdict.their_fault_pct}%`);
      }
      if (r.resolution?.immediate_actions?.length) {
        lines.push('', 'IMMEDIATE ACTIONS:');
        r.resolution.immediate_actions.forEach((a, i) => lines.push(`${i + 1}. ${a}`));
      }
      if (r.resolution?.conversation_script) {
        lines.push('', 'CONVERSATION SCRIPT:', r.resolution.conversation_script);
      }
      if (r.resolution?.compromise) lines.push('', `Compromise: ${r.resolution.compromise}`);
    }
    if (assignResult) {
      lines.push('🎰 CHORE ASSIGNMENTS', '');
      assignResult.assignments?.forEach(a => {
        lines.push(`${a.roommate}: ${a.chores.map(ch => ch.name).join(', ')}`);
      });
      if (assignResult.fairness_score) lines.push(`\nFairness: ${assignResult.fairness_score}%`);
      if (assignResult.reasoning) lines.push(`\n${assignResult.reasoning}`);
    }
    const t = lines.join('\n').trim();
    return t ? t + BRAND : '';
  }, [disputeResult, assignResult]);

  // Live ref assignments
  submitRef.current = activeTab === 'dispute' ? submitDispute : assignChores;
  canSubmitRef.current = activeTab === 'dispute'
    ? !!dispute.trim()
    : roommates.length >= 2 && choreList.length >= 1;
  tabRef.current = activeTab;

  useRegisterActions(buildFullText(), tool?.title || 'RoommateCourt');

  // Scroll to results
  const anyResult = disputeResult || assignResult;
  useEffect(() => {
    if (!anyResult) return;
    const t = setTimeout(() => resultsRef.current?.scrollIntoView({ behavior: 'smooth', block: 'start' }), 100);
    return () => clearTimeout(t);
  }, [anyResult]);

  // Keyboard shortcut
  useEffect(() => {
    const handler = (e) => {
      const tag = document.activeElement?.tagName;
      if (tag === 'SELECT') return;
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && !loading && canSubmitRef.current)
        submitRef.current?.();
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [loading]);

  // ════════════════════════════════════════════════════════════
  // RENDER HELPERS
  // ════════════════════════════════════════════════════════════

  const renderPillRow = (options, value, setter) => (
    <div className="flex flex-wrap gap-1.5 mb-3">
      {options.map(opt => (
        <button key={opt.id} onClick={() => setter(value === opt.id ? '' : opt.id)}
          className={`px-2.5 py-1.5 rounded-lg text-xs font-semibold border transition-all
            ${value === opt.id ? c.pillActive : c.pillInactive}`}>
          {opt.label}
        </button>
      ))}
    </div>
  );

  const renderCollapsible = (key, icon, title, children) => {
    const isOpen = expandedSections[key];
    return (
      <div className={`rounded-xl border ${c.border} ${c.cardAltCard} overflow-hidden mb-3`}>
        <button onClick={() => toggleSection(key)} className="w-full flex items-center gap-2 p-4 text-left">
          <span>{icon}</span>
          <span className={`text-sm font-bold ${c.text} flex-1`}>{title}</span>
          <span>{isOpen ? '▲' : '▼'}</span>
        </button>
        {isOpen && <div className="px-4 pb-4">{children}</div>}
      </div>
    );
  };

  const renderFairnessBar = (effortTotals) => {
    if (!effortTotals) return null;
    const names = Object.keys(effortTotals);
    const totalEffort = Object.values(effortTotals).reduce((s, v) => s + v, 0) || 1;
    return (
      <div className="space-y-2">
        <div className="flex items-center justify-between">
          <span className={`text-xs font-bold ${c.textSecondary} uppercase`}>Effort balance</span>
          {assignResult?.fairness_score && (
            <span className={`text-xs font-bold ${assignResult.fairness_score >= 80 ? c.successTxt : assignResult.fairness_score >= 60 ? c.warnText : 'text-red-400'}`}>
              {assignResult.fairness_score}% fair
            </span>
          )}
        </div>
        <div className={`h-5 rounded-full overflow-hidden flex ${isDark ? 'bg-zinc-700' : 'bg-zinc-200'}`}>
          {names.map((name, i) => (
            <div key={name}
              className={`${c.barColors[i % c.barColors.length]} h-full transition-all`}
              style={{ width: `${(effortTotals[name] / totalEffort) * 100}%` }}
              title={`${name}: ${effortTotals[name]} pts`} />
          ))}
        </div>
        <div className="flex flex-wrap gap-2">
          {names.map((name, i) => (
            <span key={name} className={`flex items-center gap-1.5 text-xs ${c.textSecondary}`}>
              <span className={`w-2.5 h-2.5 rounded-full ${c.barColors[i % c.barColors.length]}`} />
              {name}: {effortTotals[name]} pts
            </span>
          ))}
        </div>
      </div>
    );
  };

  // ════════════════════════════════════════════════════════════
  // DISPUTE TAB
  // ════════════════════════════════════════════════════════════
  const renderDisputeResults = () => {
    const r = disputeResult;
    const yourPct = r.verdict?.your_fault_pct || 50;
    const theirPct = r.verdict?.their_fault_pct || 50;

    return (
      <div className="space-y-3 mt-4" ref={resultsRef}>

        {renderCollapsible('verdict', '⚖️', 'The Verdict', (
          <div className="space-y-3">
            <div className={`p-4 rounded-xl border-2 ${c.verdictBg}`}>
              <div className="flex items-center gap-2 mb-2">
                <span>🔨</span>
                <span className={`text-lg font-black ${c.verdictText} uppercase`}>
                  {r.verdict?.whos_right === 'you'  ? "You're right" :
                   r.verdict?.whos_right === 'them' ? "They're right" :
                   r.verdict?.whos_right === 'both' ? "You're both right" : "Neither is right"}
                </span>
              </div>
              <p className={`text-sm ${c.text}`}>{r.verdict?.reasoning}</p>
            </div>
            <div>
              <div className="flex justify-between mb-1">
                <span className={`text-xs font-semibold ${c.textSecondary}`}>You: {yourPct}%</span>
                <span className={`text-xs font-semibold ${c.textSecondary}`}>Them: {theirPct}%</span>
              </div>
              <div className={`h-3 rounded-full overflow-hidden flex ${isDark ? 'bg-zinc-700' : 'bg-zinc-200'}`}>
                <div className="bg-amber-500 h-full rounded-l-full transition-all" style={{ width: `${yourPct}%` }} />
                <div className="bg-sky-500 h-full rounded-r-full transition-all" style={{ width: `${theirPct}%` }} />
              </div>
            </div>
          </div>
        ))}

        {r.underlying_issues && renderCollapsible('underlying', '🔍', "What's Really Going On", (
          <div className="space-y-3">
            <div>
              <span className={`text-xs font-bold ${c.textMuted} uppercase`}>Surface conflict</span>
              <p className={`text-sm ${c.text} mt-0.5`}>{r.underlying_issues.surface_conflict}</p>
            </div>
            <div>
              <span className={`text-xs font-bold ${c.textSecondary} uppercase`}>Real conflict</span>
              <p className={`text-sm ${c.text} mt-0.5`}>{r.underlying_issues.real_conflict}</p>
            </div>
            <div>
              <span className={`text-xs font-bold ${c.textMuted} uppercase`}>Communication breakdown</span>
              <p className={`text-sm ${c.text} mt-0.5`}>{r.underlying_issues.communication_breakdown}</p>
            </div>
          </div>
        ))}

        {r.resolution && renderCollapsible('resolution', '🤝', 'Resolution Path', (
          <div className="space-y-4">
            {r.resolution.immediate_actions?.length > 0 && (
              <div>
                <span className={`text-xs font-bold ${c.textSecondary} uppercase block mb-2`}>Do these now</span>
                <div className="space-y-1.5">
                  {r.resolution.immediate_actions.map((act, i) => (
                    <div key={i} className={`flex items-start gap-2 text-sm ${c.text}`}>
                      <span className={`w-5 h-5 rounded-full flex-shrink-0 flex items-center justify-center text-[10px] font-black text-white ${isDark ? 'bg-amber-600' : 'bg-amber-500'}`}>{i + 1}</span>
                      <span>{act}</span>
                    </div>
                  ))}
                </div>
              </div>
            )}
            {r.resolution.conversation_script && (
              <div>
                <div className="flex items-center justify-between mb-2">
                  <span className={`text-xs font-bold ${c.textSecondary} uppercase`}>Conversation script</span>
                  <CopyBtn content={r.resolution.conversation_script + BRAND} label="Copy" />
                </div>
                <div className={`p-4 rounded-xl border ${c.scriptBg} font-mono text-xs leading-relaxed whitespace-pre-wrap ${c.text}`}>
                  {r.resolution.conversation_script}
                </div>
              </div>
            )}
            {r.resolution.compromise && (
              <div className={`p-3 rounded-xl border ${c.success}`}>
                <span className={`text-xs font-bold ${c.successTxt} uppercase block mb-1`}>Compromise</span>
                <p className={`text-sm ${c.successTxt}`}>{r.resolution.compromise}</p>
              </div>
            )}
            {r.resolution.boundaries?.length > 0 && (
              <div>
                <span className={`text-xs font-bold ${c.textSecondary} uppercase block mb-2`}>Set these boundaries</span>
                {r.resolution.boundaries.map((b, i) => (
                  <div key={i} className={`flex items-start gap-2 text-sm ${c.text} mb-1.5`}>
                    <span>🛡️</span><span>{b}</span>
                  </div>
                ))}
              </div>
            )}
          </div>
        ))}

        {r.if_stuck && renderCollapsible('stuck', '🚨', "If They Won't Cooperate", (
          <div className="space-y-3">
            {r.if_stuck.escalation_options?.length > 0 && (
              <div>
                <span className={`text-xs font-bold ${c.textSecondary} uppercase block mb-2`}>Escalation ladder</span>
                {r.if_stuck.escalation_options.map((opt, i) => (
                  <div key={i} className={`flex items-start gap-2 text-sm ${c.text} mb-1.5`}>
                    <span className={`text-xs font-bold ${c.textMuted}`}>{i + 1}.</span>
                    <span>{opt}</span>
                  </div>
                ))}
              </div>
            )}
            {r.if_stuck.self_protection && (
              <div className={`p-3 rounded-xl border ${c.warning}`}>
                <span className={`text-xs font-bold ${c.warnText} uppercase block mb-1`}>Self-protection</span>
                <p className={`text-sm ${c.warnText}`}>{r.if_stuck.self_protection}</p>
              </div>
            )}
            {r.if_stuck.exit_strategy && (
              <div className={`p-3 rounded-xl border ${c.danger}`}>
                <span className={`text-xs font-bold uppercase block mb-1`}>Exit strategy</span>
                <p className="text-sm">{r.if_stuck.exit_strategy}</p>
              </div>
            )}
          </div>
        ))}

        {r.prevention && renderCollapsible('prevention', '🛡️', 'Prevention', (
          <p className={`text-sm ${c.text}`}>{r.prevention}</p>
        ))}

        {r.reality_check && renderCollapsible('reality', '🪞', 'Reality Check', (
          <div className={`p-4 rounded-xl border-2 ${c.verdictBg}`}>
            <p className={`text-sm ${c.verdictText} italic`}>{r.reality_check}</p>
          </div>
        ))}

        <div className="flex gap-2">
          <CopyBtn content={buildFullText()} label="Copy Verdict" />
          <button onClick={resetDispute}
            className={`flex-1 py-3 rounded-xl text-sm font-bold flex items-center justify-center gap-2 ${c.btnSecondary}`}>
            <span>🔄</span> File Another Case
          </button>
        </div>
      </div>
    );
  };

  const renderDisputeForm = () => (
    <div className="mt-4">
      <div className={`p-5 rounded-2xl border ${c.border} ${c.cardAltCard} mb-4`}>
        <span className={`text-xs font-bold ${c.textSecondary} uppercase tracking-wide mb-3 block`}>File a case</span>

        <span className={`text-xs font-semibold ${c.textSecondary} mb-1.5 block`}>What's the dispute about? <span className="text-red-400">*</span></span>
        <textarea value={dispute} onChange={e => setDispute(e.target.value)}
          placeholder="Describe the situation as fairly as you can..."
          rows={3}
          className={`w-full px-4 py-2.5 rounded-xl border text-sm ${c.input} outline-none mb-3 resize-none`} />

        <span className={`text-xs font-semibold ${c.textSecondary} mb-1.5 block`}>Category</span>
        {renderPillRow(DISPUTE_CATEGORIES, category, setCategory)}

        <span className={`text-xs font-semibold ${c.textSecondary} mb-1.5 block`}>Your side</span>
        <textarea value={yourSide} onChange={e => setYourSide(e.target.value)}
          placeholder="What's your perspective? What do you want?"
          rows={2}
          className={`w-full px-4 py-2.5 rounded-xl border text-sm ${c.input} outline-none mb-3 resize-none`} />

        <span className={`text-xs font-semibold ${c.textSecondary} mb-1.5 block`}>Their side (be honest)</span>
        <textarea value={theirSide} onChange={e => setTheirSide(e.target.value)}
          placeholder="What would THEY say if they were here?"
          rows={2}
          className={`w-full px-4 py-2.5 rounded-xl border text-sm ${c.input} outline-none mb-3 resize-none`} />

        <span className={`text-xs font-semibold ${c.textSecondary} mb-1.5 block`}>How long?</span>
        {renderPillRow(DURATION_OPTIONS, duration, setDuration)}

        <span className={`text-xs font-semibold ${c.textSecondary} mb-1.5 block`}>Have you talked?</span>
        {renderPillRow(COMM_OPTIONS, priorComm, setPriorComm)}

        <span className={`text-xs font-semibold ${c.textSecondary} mb-1.5 block`}>Living situation</span>
        {renderPillRow(LIVING_OPTIONS, livingSituation, setLivingSituation)}
      </div>

      <button onClick={submitDispute} disabled={loading || !dispute.trim()}
        className={`w-full py-3.5 rounded-xl text-sm font-bold flex items-center justify-center gap-2 disabled:opacity-40 min-h-[48px] ${c.btnPrimary}`}>
        {loading
          ? <><span className="inline-block animate-spin">{tool?.icon ?? '⚖️'}</span> The court is deliberating…</>
          : <><span>🔨</span> Hear My Case</>}
      </button>
      <p className={`text-xs text-center mt-3 ${c.textMuted}`}>
        Need to write the message first?{' '}
        <a href="/VelvetHammer" className={`text-xs ${linkStyle}`}>🔨 Velvet Hammer</a> drafts it.
      </p>
    </div>
  );

  // ════════════════════════════════════════════════════════════
  // CHORE TAB
  // ════════════════════════════════════════════════════════════
  const renderAssignments = () => {
    const r = assignResult;
    return (
      <div className="space-y-4 mt-4" ref={resultsRef}>
        <div className={`p-4 rounded-xl border ${c.border} ${c.cardAltCard}`}>
          {renderFairnessBar(r.effort_totals)}
        </div>

        {r.assignments.map((a, idx) => (
          <div key={a.roommate} className={`p-4 rounded-xl border ${c.border} ${c.cardAltCard}`}>
            <div className="flex items-center gap-2 mb-3">
              <span className={`w-7 h-7 rounded-full flex items-center justify-center text-xs font-black text-white ${c.barColors[idx % c.barColors.length]}`}>
                {a.roommate.charAt(0).toUpperCase()}
              </span>
              <span className={`text-sm font-bold ${c.text}`}>{a.roommate}</span>
              <span className={`text-xs ${c.textMuted} ml-auto`}>{a.chores.reduce((s, ch) => s + ch.effort, 0)} pts</span>
            </div>
            <div className="space-y-1.5">
              {a.chores.map(ch => {
                const key = `${a.roommate}::${ch.name}`;
                const done = currentRound?.completed?.[key];
                return (
                  <button key={ch.name} onClick={() => toggleChoreComplete(a.roommate, ch.name)}
                    className={`w-full flex items-center gap-3 p-2.5 rounded-lg border text-left transition-all
                      ${done
                        ? (isDark ? 'bg-emerald-900/20 border-emerald-700' : 'bg-emerald-50 border-emerald-200')
                        : `${c.cardAltInset} ${c.border}`}`}>
                    <div className={`flex-shrink-0 w-5 h-5 rounded-full border-2 flex items-center justify-center
                      ${done ? 'bg-emerald-500 border-emerald-500 text-white' : c.border}`}>
                      {done && <span className="text-xs">✓</span>}
                    </div>
                    <span className={`flex-1 text-sm ${done ? `line-through ${c.textMuted}` : c.text}`}>{ch.name}</span>
                    <span className={`text-[10px] font-bold px-1.5 py-0.5 rounded border ${effortStyle(ch.effort)}`}>
                      {EFFORT_LABEL[ch.effort] || 'Med'}
                    </span>
                  </button>
                );
              })}
            </div>
          </div>
        ))}

        {r.reasoning && (
          <div className={`p-4 rounded-xl border ${c.border} ${c.cardAltCard}`}>
            <div className="flex items-center gap-2 mb-2">
              <span>✨</span>
              <span className={`text-xs font-bold ${c.textSecondary} uppercase`}>Why these assignments</span>
            </div>
            <p className={`text-sm ${c.textSecondary}`}>{r.reasoning}</p>
          </div>
        )}

        {/* Rebalance */}
        <div className={`p-4 rounded-xl border ${c.border} ${c.cardAltCard}`}>
          {!showRebalance ? (
            <button onClick={() => setShowRebalance(true)}
              className={`w-full py-2.5 rounded-xl text-sm font-bold flex items-center justify-center gap-2 ${c.btnSecondary}`}>
              <span>⚠️</span> That's Not Fair!
            </button>
          ) : (
            <div className="space-y-3">
              <span className={`text-xs font-bold ${c.textSecondary} uppercase`}>What's unfair?</span>
              <textarea value={complaint} onChange={e => setComplaint(e.target.value)}
                placeholder='e.g. "I always get bathroom" or "Jordan never does heavy chores"'
                rows={2}
                className={`w-full px-3 py-2.5 rounded-xl border text-sm ${c.input} outline-none resize-none`} />
              <div className="flex gap-2">
                <button onClick={submitRebalance} disabled={loading || !complaint.trim()}
                  className={`flex-1 py-2.5 rounded-xl text-sm font-bold flex items-center justify-center gap-2 disabled:opacity-40 ${c.btnPrimary}`}>
                  {loading
                    ? <span className="inline-block animate-spin">{tool?.icon ?? '⚙️'}</span>
                    : <span>⚖️</span>}
                  {loading ? 'Reviewing…' : 'Review Complaint'}
                </button>
                <button onClick={() => { setShowRebalance(false); setComplaint(''); setRebalanceResult(null); }}
                  className={`px-3 py-2.5 rounded-xl text-xs font-semibold ${c.btnSecondary}`}>Cancel</button>
              </div>
              {rebalanceResult && (
                <div className={`p-4 rounded-xl border ${rebalanceResult.complaint_valid ? c.success : c.warning}`}>
                  <div className="flex items-center gap-2 mb-2">
                    <span>{rebalanceResult.complaint_valid ? '✅' : '⚖️'}</span>
                    <span className={`text-sm font-bold ${rebalanceResult.complaint_valid ? c.successTxt : c.warnText}`}>
                      {rebalanceResult.complaint_valid ? 'Complaint upheld — assignments revised' : 'Complaint reviewed — assignments are fair'}
                    </span>
                  </div>
                  <p className={`text-sm ${rebalanceResult.complaint_valid ? c.successTxt : c.warnText}`}>
                    {rebalanceResult.explanation}
                  </p>
                </div>
              )}
            </div>
          )}
        </div>

        {/* Save / Reshuffle */}
        <div className="flex gap-2">
          <button onClick={saveRoundToHistory}
            className={`flex-1 py-3 rounded-xl text-sm font-bold flex items-center justify-center gap-2 ${c.btnPrimary}`}>
            <span>✓</span> Finalize Round
          </button>
          <button onClick={assignChores} disabled={loading || isShuffling}
            className={`py-3 px-4 rounded-xl text-sm font-bold flex items-center justify-center gap-2 ${c.btnSecondary}`}>
            <span>🔄</span> Spin Again
          </button>
        </div>
      </div>
    );
  };

  const renderShuffling = () => (
    <div className={`p-8 rounded-2xl border ${c.border} ${c.cardAltCard} mt-4 text-center`}>
      <span className="text-3xl block mb-2 animate-spin inline-block">🎰</span>
      <p className={`text-sm font-bold ${c.text} mb-1`}>Spinning the wheel…</p>
      <p className={`text-xs ${c.textMuted}`}>Balancing effort based on history</p>
    </div>
  );

  const renderHistorySection = () => {
    if (assignHistory.length === 0) return null;
    return (
      <div className={`mt-5 rounded-xl border ${c.border} ${c.cardAltCard} overflow-hidden`}>
        <button onClick={() => setShowHistory(!showHistory)} className="w-full flex items-center gap-2 p-4 text-left">
          <span className={`text-sm font-bold ${c.text} flex-1`}>📋 History</span>
          <span className={`text-xs ${c.textMuted}`}>{assignHistory.length} rounds</span>
          <span>{showHistory ? '▲' : '▼'}</span>
        </button>
        {showHistory && (
          <div className="px-4 pb-4 space-y-3">
            <div className={`p-3 rounded-lg ${c.cardAltInset} border ${c.border}`}>
              <span className={`text-xs font-bold ${c.textSecondary} uppercase block mb-2`}>All-time effort totals</span>
              <div className="flex flex-wrap gap-3">
                {(() => {
                  const totals = {};
                  assignHistory.forEach(round => {
                    round.assignments.forEach(a => {
                      if (!totals[a.roommate]) totals[a.roommate] = 0;
                      a.chores.forEach(ch => { totals[a.roommate] += ch.effort; });
                    });
                  });
                  return Object.entries(totals).sort((a, b) => b[1] - a[1]).map(([name, total]) => (
                    <span key={name} className={`text-xs font-semibold ${c.text}`}>
                      {name}: <span className={c.textSecondary}>{total} pts</span>
                    </span>
                  ));
                })()}
              </div>
            </div>
            {assignHistory.slice(0, 6).map((round, ri) => (
              <div key={round.id || ri} className={`p-3 rounded-lg border ${c.border}`}>
                <div className="flex items-center justify-between mb-2">
                  <span className={`text-xs font-bold ${c.textSecondary}`}>{round.date}</span>
                  {round.fairness_score && (
                    <span className={`text-xs font-semibold ${round.fairness_score >= 80 ? c.successTxt : c.warnText}`}>
                      {round.fairness_score}% fair
                    </span>
                  )}
                </div>
                <div className="space-y-1">
                  {round.assignments.map(a => (
                    <div key={a.roommate} className={`text-xs ${c.textSecondary}`}>
                      <span className={`font-semibold ${c.text}`}>{a.roommate}:</span>{' '}
                      {a.chores.map(ch => ch.name).join(', ')}
                    </div>
                  ))}
                </div>
              </div>
            ))}
            <button onClick={() => { setAssignHistory([]); setShowHistory(false); }}
              className={`w-full text-center text-xs font-semibold ${c.btnSecondary} py-1.5 rounded-lg`}>
              Clear all history
            </button>
          </div>
        )}
      </div>
    );
  };

  const renderChoreTab = () => {
    return (
      <div className="mt-4">
        {/* Household roster */}
        <div className={`p-4 rounded-2xl border ${c.border} ${c.cardAltCard} mb-4`}>
          <span className={`text-xs font-bold ${c.textSecondary} uppercase tracking-wide mb-3 block`}>
            👥 Household ({roommates.length})
          </span>
          <div className="flex gap-2 mb-3">
            <input type="text" value={newRoommate} onChange={e => setNewRoommate(e.target.value)}
              onKeyDown={e => { if (e.key === 'Enter') addRoommate(); }}
              placeholder="Add roommate…"
              className={`flex-1 px-3 py-2.5 rounded-xl border text-sm ${c.input} outline-none`} />
            <button onClick={addRoommate} disabled={!newRoommate.trim()}
              className={`px-3 py-2.5 rounded-xl text-sm font-bold disabled:opacity-40 ${c.btnPrimary}`}>
              <span>➕</span>
            </button>
          </div>
          {roommates.length > 0 ? (
            <div className="flex flex-wrap gap-1.5">
              {roommates.map((name, i) => (
                <span key={name} className={`inline-flex items-center gap-1.5 px-3 py-1.5 rounded-lg border text-xs font-semibold ${c.pillActive}`}>
                  <span className={`w-4 h-4 rounded-full flex items-center justify-center text-[9px] font-black text-white ${c.barColors[i % c.barColors.length]}`}>
                    {name.charAt(0).toUpperCase()}
                  </span>
                  {name}
                  <button onClick={() => removeRoommate(name)} className={`ml-0.5 ${c.deleteHover}`}><span>✕</span></button>
                </span>
              ))}
            </div>
          ) : (
            <p className={`text-xs ${c.textMuted} text-center py-2`}>Add at least 2 household members to get started</p>
          )}
        </div>

        {/* Chore list */}
        <div className={`p-4 rounded-2xl border ${c.border} ${c.cardAltCard} mb-4`}>
          <span className={`text-xs font-bold ${c.textSecondary} uppercase tracking-wide mb-3 block`}>Chores</span>
          <div className="flex flex-wrap gap-1.5 mb-3">
            {COMMON_CHORES.filter(ch => !choreList.includes(ch)).map(ch => (
              <button key={ch} onClick={() => addChore(ch)}
                className={`px-2.5 py-1.5 rounded-lg text-xs font-semibold border transition-all ${c.pillInactive}`}>
                + {ch}
              </button>
            ))}
          </div>
          <div className="flex gap-2 mb-3">
            <input type="text" value={newChore} onChange={e => setNewChore(e.target.value)}
              onKeyDown={e => { if (e.key === 'Enter') addChore(); }}
              placeholder="Custom chore…"
              className={`flex-1 px-3 py-2.5 rounded-xl border text-sm ${c.input} outline-none`} />
            <button onClick={() => addChore()} disabled={!newChore.trim()}
              className={`px-3 py-2.5 rounded-xl text-sm font-bold disabled:opacity-40 ${c.btnPrimary}`}>
              <span>➕</span>
            </button>
          </div>
          {choreList.length > 0 ? (
            <div className="flex flex-wrap gap-1.5">
              {choreList.map(ch => (
                <span key={ch} className={`inline-flex items-center gap-1.5 px-3 py-1.5 rounded-lg border text-xs font-semibold ${c.pillActive}`}>
                  {ch}
                  <button onClick={() => removeChore(ch)} className={`ml-0.5 ${c.deleteHover}`}><span>✕</span></button>
                </span>
              ))}
            </div>
          ) : (
            <p className={`text-xs ${c.textMuted} text-center py-2`}>What needs doing? Add chores above</p>
          )}
        </div>

        {/* Assign button */}
        {roommates.length >= 2 && choreList.length >= 1 && !assignResult && !isShuffling && (
          <button onClick={assignChores} disabled={loading}
            className={`w-full py-3.5 rounded-xl text-sm font-bold flex items-center justify-center gap-2 disabled:opacity-40 min-h-[48px] ${c.btnPrimary}`}>
            {loading
              ? <><span className="inline-block animate-spin">{tool?.icon ?? '⚙️'}</span> Spinning…</>
              : <><span>🎰</span> Spin the Wheel</>}
          </button>
        )}

        {roommates.length < 2 && choreList.length === 0 && (
          <div className={`p-6 rounded-2xl border border-dashed ${c.border} text-center`}>
            <span className="text-3xl block mb-2">⚖️</span>
            <p className={`text-sm ${c.textMuted}`}>Add roommates and chores above to get started.</p>
            <p className={`text-xs ${c.textMuted} mt-1`}>The AI balances workload so nobody can claim it's unfair.</p>
          </div>
        )}

        {isShuffling && !assignResult && renderShuffling()}
        {assignResult && renderAssignments()}
        {renderHistorySection()}
      </div>
    );
  };

  // ════════════════════════════════════════════════════════════
  // MAIN RENDER
  // ════════════════════════════════════════════════════════════
  const activeChoreCount = currentRound
    ? currentRound.assignments.reduce((sum, a) => sum + a.chores.filter(ch => !currentRound.completed[`${a.roommate}::${ch.name}`]).length, 0)
    : 0;

  return (
    <div className={`space-y-4 ${c.text}`}>

      {/* ── Persistent header ── */}
      <div className={`${c.card} border ${c.border} rounded-xl shadow-sm overflow-hidden`}>
        <div className="px-5 pt-5">
          <div className="pb-3 border-b border-zinc-500">
            <h2 className={`text-xl font-bold ${c.text}`}>
              <span className="mr-2">{tool?.icon ?? '⚖️'}</span>{tool?.title ?? 'RoommateCourt'}
            </h2>
            <p className={`text-sm ${c.textSecondary}`}>{tool?.tagline ?? 'Fair disputes, fair chores — no arguments'}</p>
          </div>
        </div>

        {/* Tab strip */}
        <div className="px-5 py-3 flex gap-1.5">
          {[
            { id: 'dispute', label: '⚖️ Dispute Court' },
            { id: 'chores',  label: '🎰 Chore Roulette', badge: activeChoreCount },
          ].map(tab => (
            <button key={tab.id} onClick={() => { setActiveTab(tab.id); setError(''); }}
              className={`flex-1 py-2.5 px-3 rounded-xl text-xs font-bold transition-all flex items-center justify-center gap-1.5 border
                ${activeTab === tab.id ? c.tabActive : `${c.tabInactive}`}`}>
              {tab.label}
              {tab.badge > 0 && (
                <span className={`ml-1 w-5 h-5 rounded-full flex items-center justify-center text-[10px] font-black
                  ${activeTab === tab.id ? 'bg-white/20 text-white' : (isDark ? 'bg-amber-500/30 text-amber-300' : 'bg-amber-100 text-amber-700')}`}>
                  {tab.badge}
                </span>
              )}
            </button>
          ))}
        </div>
      </div>

      {/* Tab content */}
      {activeTab === 'dispute' && (
        disputeResult ? renderDisputeResults() : renderDisputeForm()
      )}
      {activeTab === 'chores' && renderChoreTab()}

      {/* Error */}
      {error && (
        <div className={`p-4 ${c.danger} border rounded-xl flex items-start gap-3`}>
          <span>⚠️</span>
          <p className="text-sm">{error}</p>
        </div>
      )}

      {/* Cross-refs */}
      {(disputeResult || assignResult) && (
        <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
          <p className={`text-xs font-semibold uppercase tracking-wide mb-3 ${c.textMuted}`}>🔗 Related tools</p>
          <div className="flex flex-wrap gap-3">
            <a href="/VelvetHammer"       className={`text-xs ${linkStyle}`}>🔨 Velvet Hammer</a>
            <a href="/DifficultTalkCoach" className={`text-xs ${linkStyle}`}>🗣️ Difficult Talk Coach</a>
            <a href="/RulebookBreaker"    className={`text-xs ${linkStyle}`}>🏴‍☠️ Rulebook Breaker</a>
          </div>
        </div>
      )}

      <p className={`text-xs ${c.textMuted} text-center`}>AI-generated suggestions — use your own judgment.</p>
    </div>
  );
};

RoommateCourt.displayName = 'RoommateCourt';
export default RoommateCourt;
