import React, { useState} from 'react';
import { useTheme} from '../hooks/useTheme';
import { useClaudeAPI} from '../hooks/useClaudeAPI';
import { usePersistentState} from '../hooks/usePersistentState';
import { CopyBtn, ActionBar} from '../components/ActionButtons';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const EXAMPLE_BELIEFS = [
  'Hard work always pays off',
  'People don\'t fundamentally change',
  'Everything happens for a reason',
  'You can do anything if you put your mind to it',
  'Good things come to those who wait',
  'The free market produces the best outcomes',
  'Being kind is always the right choice',
  'Failure is the best teacher',
];

const VERDICT_CONFIG = {
  demolished:  { icon: '💥', light: 'bg-red-50 border-red-300 text-red-800',             dark: 'bg-red-900/20 border-red-700/50 text-red-300' },
  refined:     { icon: '🔧', light: 'bg-amber-50 border-amber-300 text-amber-800',       dark: 'bg-amber-900/20 border-amber-700/40 text-amber-300' },
  vindicated:  { icon: '✅', light: 'bg-emerald-50 border-emerald-300 text-emerald-800', dark: 'bg-emerald-900/20 border-emerald-700/40 text-emerald-300' },
  complicated: { icon: '🔵', light: 'bg-cyan-50 border-cyan-300 text-cyan-800',          dark: 'bg-cyan-900/20 border-cyan-700/40 text-cyan-300' },
};

const EgoKiller = ({ tool}) => {
  const { isDark} = useTheme();
  const { callToolEndpoint, loading} = useClaudeAPI();

  const c = {
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-500' : 'text-gray-400',
    input:         isDark ? 'bg-zinc-900 border-zinc-700 text-zinc-50 placeholder:text-zinc-500 focus:outline-none focus:ring-2 focus:ring-cyan-500/20 focus:border-cyan-500' : 'bg-slate-50 border-gray-300 text-gray-900 placeholder:text-gray-400 focus:outline-none focus:ring-2 focus:ring-cyan-500/20 focus:border-cyan-500',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white' : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-100 border border-zinc-600' : 'bg-gray-100 hover:bg-gray-200 text-gray-700 border border-gray-200',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-300' : 'bg-red-50 border-red-300 text-red-800',};

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';


  const [context, setContext] = useState('');
  const [howStrongly, setHowStrongly] = useState(7);
  const [results, setResults] = usePersistentState('egokiller-results', null);
  const [belief, setBelief] = usePersistentState('egokiller-belief', '');
  const [error, setError] = useState('');
  const resultsRef = React.useRef(null);

  const handleSubmit = async () => {
    if (!belief.trim()) return;
    setError(''); setResults(null);
    try {
      const data = await callToolEndpoint('ego-killer', {
        belief: belief.trim(),
        context: context.trim() || undefined,
        howStrongly,});
      setResults(data);} catch (e) { setError(e.message || 'Failed to demolish the belief.');}};

  const buildText = () => {
    if (!results) return '';
    let t = `🪦 EGO KILLER\n\n"${belief}"\n\n`;
    t += `STEELMANNED:\n${results.belief_steelmanned}\n\n`;
    t += `THE DEMOLITION:\n${results.the_demolition?.the_core_attack}\n\n`;
    t += `WHAT SURVIVES:\n${results.what_survives?.the_kernel}\n\n`;
    t += `THE REBUILD:\n${results.the_rebuild?.the_stronger_version}\n\n`;
    t += `VERDICT: ${results.the_verdict?.outcome_label} — ${results.the_verdict?.one_line}\n`;
    return t + BRAND;};

  const vcfg = results ? (VERDICT_CONFIG[results.the_verdict?.outcome] || VERDICT_CONFIG.complicated) : null;

  return (
    <div className={`min-h-screen py-8 px-4 ${c.card}`}>
      <div className="max-w-2xl mx-auto">

        <div className="text-center mb-8">
          <div className="text-5xl mb-3">🪦</div>
          <h1 className={`text-3xl font-black tracking-tight mb-2 ${c.text}`}>Ego Killer</h1>
          <p className={`text-sm ${c.textMuted}`}>Intellectual demolition and reconstruction. You come out changed or unshakeable.</p>
        </div>

        {!results && (
          <div className={`rounded-2xl border p-6 shadow-sm space-y-4 ${c.card} ${c.border}`}>
            <div>
              <label className={`block text-sm font-semibold mb-1.5 ${c.text}`}>
                Something you believe strongly <span className="text-red-400">*</span>
              </label>
              <textarea value={belief} onChange={e => setBelief(e.target.value)} onKeyDown={e => { if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && belief.trim()) handleSubmit(); }}
                placeholder="A principle, conviction, or belief that guides how you think or live…"
                rows={2} maxLength={300}
                className={`w-full px-4 py-3 rounded-xl border text-sm resize-none ${c.input}`} />
              <div className="flex flex-wrap gap-2 mt-2">
                {EXAMPLE_BELIEFS.map(b => (
                  <button key={b} onClick={() => { setBelief(b); setResults(null);}}
                    className={`px-2.5 py-1 rounded-lg text-xs ${c.btnSecondary}`}>
                    {b}
                  </button>
                ))}
              </div>
            </div>

            <div>
              <label className={`block text-sm font-semibold mb-1.5 ${c.text}`}>Why you hold it / your context <span className={`font-normal ${c.textMuted}`}>(optional)</span></label>
              <textarea value={context} onChange={e => setContext(e.target.value)} onKeyDown={e => { if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && belief.trim()) handleSubmit(); }}
                placeholder="Where this belief comes from, experiences that reinforced it…"
                rows={2} maxLength={300}
                className={`w-full px-4 py-3 rounded-xl border text-sm resize-none ${c.input}`} />
            </div>

            <div>
              <label className={`block text-sm font-semibold mb-2 ${c.text}`}>
                How strongly do you hold this? <span className={`font-normal ${c.textMuted}`}>{howStrongly}/10</span>
              </label>
              <input type="range" min={1} max={10} value={howStrongly} onChange={e => setHowStrongly(Number(e.target.value))}
                className="w-full accent-cyan-500" />
              <div className={`flex justify-between text-xs mt-1 ${c.textMuted}`}>
                <span>Curious about it</span><span>Would die on this hill</span>
              </div>
            </div>

            {error && <div className={`p-3 rounded-xl border text-sm ${c.danger}`}><span className="mr-1">⚠️</span>{error}</div>}

            <button onClick={handleSubmit} disabled={loading || !belief.trim()}
              className={`w-full py-3 rounded-xl font-bold disabled:opacity-40 ${c.btnPrimary}`}>
              {loading ? <><span className="animate-spin inline-block mr-2">{tool?.icon ?? '🪦'}</span>Demolishing…</> : '🪦 Destroy My Belief'}
            </button>
          </div>
        )}

        {results && (
          <div ref={resultsRef} className="space-y-4">

            {/* Verdict badge */}
            {results.the_verdict && (
              <div className={`rounded-2xl border p-5 text-center ${isDark ? vcfg.dark : vcfg.light}`}>
                <p className="text-3xl mb-2">{vcfg.icon}</p>
                <p className="text-xl font-black tracking-tight">{results.the_verdict.outcome_label}</p>
                <p className="text-sm mt-1 opacity-80">{results.the_verdict.one_line}</p>
              </div>
            )}

            {/* Steelmanned */}
            <div className={`rounded-2xl border p-5 ${c.card} ${c.border}`}>
              <p className={`text-xs font-bold uppercase tracking-wider mb-2 ${c.textMuted}`}>🛡️ Steelmanned</p>
              <p className={`text-sm leading-relaxed italic ${c.textSecondary}`}>"{results.belief_steelmanned}"</p>
            </div>

            {/* Demolition */}
            {results.the_demolition && (
              <div className={`rounded-2xl border-2 overflow-hidden ${isDark ? 'border-red-700/50 bg-zinc-800' : 'border-red-300 bg-white'}`}>
                <div className={`px-5 py-4 ${isDark ? 'bg-red-900/10' : 'bg-red-50'}`}>
                  <p className={`text-xs font-black uppercase tracking-widest ${isDark ? 'text-red-300' : 'text-red-800'}`}>💥 The Demolition</p>
                </div>
                <div className="px-5 py-4 space-y-4">
                  <div>
                    <p className={`text-xs font-bold uppercase tracking-wide mb-1 ${c.textMuted}`}>The Core Attack</p>
                    <p className={`text-sm leading-relaxed font-semibold ${c.text}`}>{results.the_demolition.the_core_attack}</p>
                  </div>
                  {results.the_demolition.why_its_devastating && (
                    <p className={`text-sm ${c.textSecondary}`}>{results.the_demolition.why_its_devastating}</p>
                  )}
                  {results.the_demolition.the_evidence && (
                    <div className={`p-3 rounded-xl border ${c.cardAlt} ${c.border}`}>
                      <p className={`text-xs font-bold uppercase tracking-wide mb-1 ${c.textMuted}`}>The Evidence</p>
                      <p className={`text-sm ${c.textSecondary}`}>{results.the_demolition.the_evidence}</p>
                    </div>
                  )}
                  {results.the_demolition.historical_counterexamples?.length > 0 && (
                    <div className="space-y-2">
                      <p className={`text-xs font-bold uppercase tracking-wide ${c.textMuted}`}>Counterexamples</p>
                      {results.the_demolition.historical_counterexamples.map((ex, i) => (
                        <div key={i} className={`p-3 rounded-xl border ${c.cardAlt} ${c.border}`}>
                          <p className={`text-sm font-semibold ${c.text}`}>{ex.example}</p>
                          <p className={`text-xs mt-0.5 ${c.textMuted}`}>{ex.what_it_shows}</p>
                        </div>
                      ))}
                    </div>
                  )}
                  {results.the_demolition.the_hidden_assumption && (
                    <div className={`p-3 rounded-xl border ${isDark ? 'bg-red-900/10 border-red-800/30' : 'bg-red-50 border-red-300'}`}>
                      <p className={`text-xs font-bold uppercase tracking-wide mb-1 ${isDark ? 'text-red-300' : 'text-red-800'}`}>💣 Hidden Assumption</p>
                      <p className={`text-sm ${c.textSecondary}`}>{results.the_demolition.the_hidden_assumption}</p>
                    </div>
                  )}
                </div>
              </div>
            )}

            {/* What survives */}
            {results.what_survives && (
              <div className={`rounded-2xl border p-5 ${isDark ? 'bg-emerald-900/10 border-emerald-700/40' : 'bg-emerald-50 border-emerald-300'}`}>
                <p className={`text-xs font-black uppercase tracking-widest mb-3 ${isDark ? 'text-emerald-300' : 'text-emerald-800'}`}>✅ What Survives</p>
                <p className={`text-sm font-semibold mb-2 ${c.text}`}>{results.what_survives.the_kernel}</p>
                {results.what_survives.under_what_conditions && (
                  <p className={`text-xs ${c.textMuted}`}><span className="font-semibold">When it's true:</span> {results.what_survives.under_what_conditions}</p>
                )}
              </div>
            )}

            {/* The Rebuild */}
            {results.the_rebuild && (
              <div className={`rounded-2xl border-2 p-5 ${isDark ? 'border-cyan-700 bg-cyan-900/10' : 'border-cyan-400 bg-cyan-50'}`}>
                <p className={`text-xs font-black uppercase tracking-widest mb-3 ${isDark ? 'text-cyan-300' : 'text-cyan-700'}`}>🔧 The Rebuild</p>
                <p className={`text-sm font-bold mb-2 ${c.text}`}>{results.the_rebuild.the_stronger_version}</p>
                {results.the_rebuild.the_key_qualification && (
                  <p className={`text-xs mb-2 ${c.textMuted}`}><span className="font-semibold">Key qualifier:</span> {results.the_rebuild.the_key_qualification}</p>
                )}
                {results.the_rebuild.now_unshakeable_because && (
                  <p className={`text-xs italic ${c.textMuted}`}>{results.the_rebuild.now_unshakeable_because}</p>
                )}
                <div className="mt-3">
                  <CopyBtn content={`🔧 Rebuilt belief:\n\n${results.the_rebuild.the_stronger_version}${BRAND}`} label="Copy Rebuild" />
                </div>
              </div>
            )}

            <div className="flex items-center gap-3 flex-wrap">
              <ActionBar content={buildText()} title="Ego Killer" />
              <button onClick={() => { setResults(null); setBelief(''); setContext('');}}
                className={`px-4 py-2 rounded-lg text-sm font-medium ${c.btnSecondary}`}>
                🔄 New Belief
              </button>
            </div>

            <div className={`rounded-xl border p-4 ${c.cardAlt} ${c.border}`}>
              <p className={`text-xs font-semibold uppercase tracking-wide mb-3 ${c.textMuted}`}>Related tools</p>
              <div className="flex flex-wrap gap-2">
                {[
                  { id: 'BeliefStressTest', icon: '🧪', label: 'Stress-test more beliefs'},
                  { id: 'TheFinalWord', icon: '⚖️', label: 'Deep Dissect a claim'},
                  { id: 'CrowdWisdom', icon: '👥', label: 'Get five perspectives'},
                ].map(r => (
                  <a key={r.id} href={`/${r.id}`} target="_blank" rel="noopener noreferrer"
                    className={`px-3 py-1.5 rounded-lg text-xs font-medium transition-colors ${c.btnSecondary}`}>
                    {r.icon} {r.label}
                  </a>
                ))}
              </div>
            </div>
          </div>
        )}
      </div>
    </div>
  );};

EgoKiller.displayName = 'EgoKiller';
export default EgoKiller;
