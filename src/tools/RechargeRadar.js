const BATTERY_PRESETS = [
  { value: 90, label: 'Full',  icon: '🔋', desc: 'Well-rested' },
  { value: 70, label: 'Good',  icon: '🟢', desc: 'Decent energy' },
  { value: 50, label: 'Half',  icon: '🟡', desc: 'Already drained' },
  { value: 30, label: 'Low',   icon: '🟠', desc: 'Running on fumes' },
  { value: 15, label: 'Empty', icon: '🔴', desc: 'Nearly shut down' },
];
const STYLE_OPTIONS = [
  { id: 'introvert', label: 'Introvert', icon: '🔇', desc: 'Social events drain me' },
  { id: 'ambivert',  label: 'Ambivert',  icon: '⚖️', desc: 'Depends on the situation' },
  { id: 'extrovert', label: 'Extrovert', icon: '🔊', desc: 'Social events energize me' },
];
const EXAMPLE_PROMPTS = [
  "Daily standup at 9am, team lunch Wednesday, friend's birthday party Friday night (probably 30+ people), family brunch Sunday",
  "3 back-to-back meetings Monday, networking event Tuesday evening, first date Thursday, hosting game night Saturday",
  "Quiet week — just one 1-on-1 coffee Tuesday and a phone call with mom Thursday",
];
