const ACTUAL_OPTIONS = [
  { id: 'less_draining', label: 'Less draining',       icon: '😌' },
  { id: 'as_expected',   label: 'As expected',          icon: '😐' },
  { id: 'more_draining', label: 'More draining',        icon: '😫' },
  { id: 'restorative',   label: 'Actually recharged me', icon: '✨' },
  { id: 'skipped',       label: 'Skipped it',           icon: '🚫' },
];

// ════════════════════════════════════════════════════════════
// MAIN COMPONENT
// ════════════════════════════════════════════════════════════
const RechargeRadar = ({ tool }) => {
  const { callToolEndpoint, loading } = useClaudeAPI();
  const { isDark } = useTheme();

  // ── Theme — verbatim from CONVENTIONS.md PF-2 ──
  const c = {
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:         isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20'
                          : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-500' : 'text-gray-400',
    labelText:     isDark ? 'text-zinc-200' : 'text-gray-700',
    accentTxt:     isDark ? 'text-cyan-400' : 'text-cyan-600',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white'
                          : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200'
                          : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200'
                          : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200'
                          : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200'
                          : 'bg-red-50 border-red-200 text-red-800',
    infoBox:       isDark ? 'bg-sky-900/20 border-sky-700 text-sky-200'
                          : 'bg-sky-50 border-sky-200 text-sky-800',
    successBox:    isDark ? 'bg-emerald-900/20 border-emerald-700' : 'bg-emerald-50 border-emerald-300',
    successTxt:    isDark ? 'text-emerald-300' : 'text-emerald-800',
    warningBox:    isDark ? 'bg-amber-900/20 border-amber-700' : 'bg-amber-50 border-amber-300',
    warningTxt:    isDark ? 'text-amber-300' : 'text-amber-800',
    pillActive:    isDark ? 'border-cyan-500 bg-cyan-900/30 text-cyan-200'
                          : 'border-cyan-600 bg-cyan-100 text-cyan-900',
    pillInactive:  isDark ? 'border-zinc-600 text-zinc-400 hover:border-zinc-500'
                          : 'border-gray-300 text-gray-500 hover:border-gray-400',
  };
  c.textMuteded  = c.textMuted;
  c.label        = c.labelText;
  // RechargeRadar extensions
  c.btnSoft      = isDark ? 'bg-zinc-700/50 hover:bg-zinc-600/50 text-zinc-300' : 'bg-gray-50 hover:bg-gray-100 text-gray-500';
  c.deleteHover  = isDark ? 'hover:text-red-400 transition-colors' : 'hover:text-red-500 transition-colors';

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';

  // ── State ──
  const [description, setDescription]     = useState('');
  const [currentBattery, setCurrentBattery] = useState(70);
  const [forecast, setForecast]            = useState(null);
  const [editedEvents, setEditedEvents]    = useState(null);
  const [error, setError]                  = useState('');
  const [triage, setTriage]                = useState(null);
  const [triageLoading, setTriageLoading]  = useState(false);
  const [declineFor, setDeclineFor]        = useState(null);
  const [declineMessages, setDeclineMessages] = useState(null);
  const [declineLoading, setDeclineLoading] = useState(false);
  const [addEventText, setAddEventText]    = useState('');
  const [addEventResult, setAddEventResult] = useState(null);
  const [addEventLoading, setAddEventLoading] = useState(false);
  const [showReflect, setShowReflect]      = useState(false);
  const [reflections, setReflections]      = useState({});
  const [reflectResult, setReflectResult]  = useState(null);
  const [reflectLoading, setReflectLoading] = useState(false);
  const [showTemplates, setShowTemplates]  = useState(false);
  const [templateName, setTemplateName]    = useState('');
  const [showSaveTemplate, setShowSaveTemplate] = useState(false);
  const [showHistory, setShowHistory]      = useState(false);
  const [showTrend, setShowTrend]          = useState(false);
  const [showCompare, setShowCompare]      = useState(false);

  // ── Refs ──
  const resultsRef   = useRef(null);
  const submitRef    = useRef(null);
  const canSubmitRef = useRef(false);

  // ── Persistent state ──
  const [socialStyle, setSocialStyle]         = usePersistentState('rr-style', 'introvert');
  const [templates, setTemplates]             = usePersistentState('rr-templates', []);
  const [energyProfiles, setEnergyProfiles]   = usePersistentState('rr-profiles', []);
  const [pastReflections, setPastReflections] = usePersistentState('rr-past-reflections', []);
  const [history, setHistory]                 = usePersistentState('rr-history', []);

  // ── Battery helpers ──
  const getBatteryColor = useCallback((level) => {
    if (level >= 60) return isDark ? 'text-emerald-400' : 'text-emerald-600';
    if (level >= 30) return isDark ? 'text-amber-400'   : 'text-amber-600';
    return isDark ? 'text-red-400' : 'text-red-600';
  }, [isDark]);

  const getBatteryFill = useCallback((level) =>
    level >= 60 ? 'bg-emerald-500' : level >= 30 ? 'bg-amber-500' : 'bg-red-500', []);

  const getBatteryBg = useCallback((level) => {
    if (level >= 60) return isDark ? 'bg-emerald-900/30' : 'bg-emerald-50';
    if (level >= 30) return isDark ? 'bg-amber-900/30'   : 'bg-amber-50';
    return isDark ? 'bg-red-900/30' : 'bg-red-50';
  }, [isDark]);

  // ── Template helpers ──
  const saveTemplate = useCallback(() => {
    if (!templateName.trim() || !description.trim()) return;
    setTemplates(prev => [...prev, { id: Date.now(), name: templateName.trim(), text: description.trim() }].slice(0, 6));
    setTemplateName(''); setShowSaveTemplate(false);
  }, [templateName, description, setTemplates]);

  const loadTemplate = useCallback((tmpl) => {
    setDescription(prev => prev ? `${prev}\n${tmpl.text}` : tmpl.text);
    setShowTemplates(false);
  }, []);

  // ── Inline editing + recalc ──
  const recalcForecast = useCallback((events) => {
    if (!forecast?.energy_forecast?.next_7_days) return;
    const costMap = {};
    events.forEach(e => { costMap[e.name] = e.energy_cost; });
    const days = JSON.parse(JSON.stringify(forecast.energy_forecast.next_7_days));
    let battery = currentBattery;
    days.forEach(day => {
      day.start_battery = Math.min(100, battery);
      if (day.events) {
        day.events.forEach(ev => {
          const cost = costMap[ev.event] ?? ev.energy_cost;
          ev.energy_cost = cost;
          battery = Math.max(0, battery - cost);
          ev.battery_after = battery;
        });
      }
      day.end_of_day_battery = battery;
      battery = Math.min(100, battery + (forecast.energy_forecast.overnight_recovery || 15));
    });
    setForecast(prev => ({ ...prev, energy_forecast: { ...prev.energy_forecast, next_7_days: days } }));
  }, [forecast, currentBattery]);

  const updateEventCost = useCallback((idx, newCost) => {
    const updated = [...(editedEvents || forecast?.parsed_events || [])];
    updated[idx] = { ...updated[idx], energy_cost: Math.max(0, Math.min(100, newCost)) };
    setEditedEvents(updated);
    recalcForecast(updated);
  }, [editedEvents, forecast, recalcForecast]);

  const removeEvent = useCallback((idx) => {
    const updated = [...(editedEvents || forecast?.parsed_events || [])].filter((_, i) => i !== idx);
    setEditedEvents(updated);
    recalcForecast(updated);
  }, [editedEvents, forecast, recalcForecast]);

  // ── API ──
  const generateForecast = useCallback(async () => {
    if (!description.trim()) return;
    setError(''); setForecast(null); setTriage(null); setEditedEvents(null);
    setAddEventResult(null); setDeclineMessages(null); setDeclineFor(null);
    try {
      const data = await callToolEndpoint('recharge-radar', {
        description: description.trim(), currentBattery, socialStyle,
        pastProfiles: energyProfiles.slice(0, 6),
      });
      setForecast(data);
      const lowestBattery = data.energy_forecast?.next_7_days
        ? Math.min(...data.energy_forecast.next_7_days.map(d => d.end_of_day_battery))
        : currentBattery;
      setHistory(prev => [{
        id: Date.now(), date: new Date().toISOString(),
        description: description.trim().slice(0, 40),
        lowestBattery, warningCount: data.warnings?.length || 0,
        eventCount: data.parsed_events?.length || 0,
        committed: data.energy_budgeting?.already_committed || 0,
      }, ...prev].slice(0, 6));
    } catch (err) { setError(err.message || 'Failed to generate forecast'); }
  }, [description, currentBattery, socialStyle, energyProfiles, callToolEndpoint, setHistory]);

  const runTriage = useCallback(async () => {
    if (!forecast) return;
    setTriageLoading(true);
    try {
      const data = await callToolEndpoint('recharge-radar/triage', {
        events: forecast.parsed_events || [], warnings: forecast.warnings || [],
        currentBattery, socialStyle,
      });
      setTriage(data);
    } catch (err) { setError(err.message || 'Failed to run triage'); }
    finally { setTriageLoading(false); }
  }, [forecast, currentBattery, socialStyle, callToolEndpoint]);

  const generateDecline = useCallback(async (eventName, eventType) => {
    setDeclineFor(eventName); setDeclineLoading(true); setDeclineMessages(null);
    try {
      const data = await callToolEndpoint('recharge-radar/decline', { eventName, eventType, socialStyle });
      setDeclineMessages(data.messages || []);
    } catch (err) { setError(err.message || 'Failed to generate decline message'); }
    finally { setDeclineLoading(false); }
  }, [socialStyle, callToolEndpoint]);

  const addNewEvent = useCallback(async () => {
    if (!addEventText.trim() || !forecast) return;
    setAddEventLoading(true); setAddEventResult(null);
    try {
      const data = await callToolEndpoint('recharge-radar/add-event', {
        eventDescription: addEventText.trim(),
        currentForecast: forecast, currentBattery, socialStyle,
      });
      setAddEventResult(data);
      if (data.new_event) {
        const updated = [...(editedEvents || forecast.parsed_events || []), data.new_event];
        setEditedEvents(updated);
      }
    } catch (err) { setError(err.message || 'Failed to assess event'); }
    finally { setAddEventLoading(false); }
  }, [addEventText, forecast, currentBattery, socialStyle, editedEvents, callToolEndpoint]);

  const submitReflection = useCallback(async () => {
    if (!forecast || Object.keys(reflections).length === 0) return;
    setReflectLoading(true);
    try {
      const activeEvents = editedEvents || forecast.parsed_events || [];
      const reflectionData = Object.entries(reflections).map(([idx, r]) => ({
        event: activeEvents[Number(idx)]?.name || '',
        predicted_cost: activeEvents[Number(idx)]?.energy_cost || 0,
        actual: r.actual, notes: r.notes || '',
      }));
      const data = await callToolEndpoint('recharge-radar/reflect', {
        reflections: reflectionData, socialStyle, pastReflections: pastReflections.slice(0, 6),
      });
      setReflectResult(data);
      if (data.updated_profiles?.length) {
        setEnergyProfiles(data.updated_profiles);
        setPastReflections(prev => [...reflectionData, ...prev].slice(0, 20));
      }
    } catch (err) { setError(err.message || 'Failed to process reflection'); }
    finally { setReflectLoading(false); }
  }, [forecast, reflections, editedEvents, socialStyle, pastReflections,
      callToolEndpoint, setEnergyProfiles, setPastReflections]);

  const buildCalendarEvent = useCallback(() => {
    if (!forecast?.recovery_plan) return '';
    const rp = forecast.recovery_plan;
    return `🛡️ Recovery Time — Do Not Schedule\n\nWhen: ${rp.recharge_needed_by}\nDuration: ${rp.minimum_hours_alone} hours minimum\n\nThis time is blocked for recharging. No meetings, no calls, no obligations.\n\n${rp.recommended_activities?.length ? `Recharge ideas: ${rp.recommended_activities.join(', ')}` : ''}${BRAND}`;
  }, [forecast]);

  // ── buildFullText ──
  const buildFullText = useCallback(() => {
    if (!forecast) return '';
    const lines = ['📡 Recharge Radar — Social Battery Forecast\n'];
    lines.push(`Starting battery: ${currentBattery}%\n`);
    if (forecast.energy_forecast?.next_7_days) {
      forecast.energy_forecast.next_7_days.forEach(day => {
        lines.push(`📅 ${day.date} — ${day.end_of_day_battery}%`);
        if (day.events) day.events.forEach(e => lines.push(`   ${e.event}: -${e.energy_cost}% → ${e.battery_after}%`));
        if (day.warning) lines.push(`   ⚠️ ${day.warning}`);
      });
    }
    if (forecast.warnings?.length) {
      lines.push('\n⚠️ WARNINGS:');
      forecast.warnings.forEach(w => lines.push(`• ${w.type}: ${w.why} → ${w.recommendation}`));
    }
    if (forecast.quick_tip) lines.push(`\n💡 ${forecast.quick_tip}`);
    return lines.join('\n').trim() + BRAND;
  }, [forecast, currentBattery]);

  const reset = useCallback(() => {
    setForecast(null); setDescription(''); setCurrentBattery(70); setError('');
    setEditedEvents(null); setTriage(null); setAddEventResult(null); setAddEventText('');
    setReflectResult(null); setShowReflect(false); setReflections({});
    setDeclineMessages(null); setDeclineFor(null); setShowCompare(false);
  }, []);

  // Live ref assignments
  submitRef.current    = generateForecast;
  canSubmitRef.current = !!description.trim();

  useRegisterActions(buildFullText(), tool?.title || 'Recharge Radar');

  // Scroll to results
  useEffect(() => {
    if (!forecast) return;
    const t = setTimeout(() => resultsRef.current?.scrollIntoView({ behavior: 'smooth', block: 'start' }), 100);
    return () => clearTimeout(t);
  }, [forecast]);

  // Keyboard shortcut
  useEffect(() => {
    const handler = (e) => {
      const tag = document.activeElement?.tagName;
      if (tag === 'SELECT') return;
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && !loading && canSubmitRef.current)
        submitRef.current?.();
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [loading]);

  const stats = useMemo(() => {
    if (history.length === 0) return null;
    const avgLowest      = Math.round(history.reduce((s, h) => s + (h.lowestBattery || 50), 0) / history.length);
    const totalWarnings  = history.reduce((s, h) => s + (h.warningCount || 0), 0);
    return { forecasts: history.length, avgLowest, totalWarnings };
  }, [history]);

  const trendData = useMemo(() =>
    history.slice(0, 6).reverse().map(h => ({
      lowest: h.lowestBattery || 50, committed: h.committed || 50,
      date: new Date(h.date).toLocaleDateString('en-US', { month: 'short', day: 'numeric' }),
    })), [history]);

  const comparisonData = useMemo(() => {
    if (history.length < 2) return null;
    return { current: history[0], previous: history[1] };
  }, [history]);

  const activeEvents = editedEvents || forecast?.parsed_events || [];

  // ════════════════════════════════════════════════════════════
  // RENDER
  // ════════════════════════════════════════════════════════════
  return (
    <div className={`space-y-4 ${c.text}`}>

      {/* ── Persistent header ── */}
      <div className={`${c.card} border ${c.border} rounded-xl shadow-sm overflow-hidden`}>
        <div className="px-5 pt-5">
          <div className="pb-3 border-b border-zinc-500">
            <h2 className={`text-xl font-bold ${c.text}`}>
              <span className="mr-2">{tool?.icon ?? '📡'}</span>{tool?.title ?? 'Recharge Radar'}
            </h2>
            <p className={`text-sm ${c.textSecondary}`}>{tool?.tagline ?? "Predict when you'll need alone time this week"}</p>
          </div>
        </div>

        {/* ════════ INPUT MODE ════════ */}
        {!forecast && (
          <div className="p-5 space-y-4">

            {/* Battery level */}
            <div>
              <p className={`text-xs font-bold uppercase tracking-wider mb-3 ${c.textMuted}`}>How's your battery right now?</p>
              <div className="grid grid-cols-5 gap-2">
                {BATTERY_PRESETS.map(bp => (
                  <button key={bp.value} onClick={() => setCurrentBattery(bp.value)}
                    className={`p-2.5 rounded-xl border text-center transition-all ${
                      currentBattery === bp.value
                        ? isDark ? 'border-cyan-500 bg-cyan-900/30 text-cyan-300' : 'border-cyan-500 bg-cyan-50 text-cyan-700'
                        : isDark ? 'border-zinc-600 hover:border-zinc-500 text-zinc-300' : 'border-zinc-200 hover:border-zinc-300 text-gray-700'
                    }`}>
                    <span className="text-xl block">{bp.icon}</span>
                    <span className="text-xs font-bold block mt-1">{bp.value}%</span>
                  </button>
                ))}
              </div>
            </div>

            {/* Social style */}
            <div>
              <p className={`text-xs font-bold uppercase tracking-wider mb-3 ${c.textMuted}`}>Your social style</p>
              <div className="grid grid-cols-3 gap-2">
                {STYLE_OPTIONS.map(opt => (
                  <button key={opt.id} onClick={() => setSocialStyle(opt.id)}
                    className={`p-3 rounded-xl border text-center transition-all ${
                      socialStyle === opt.id
                        ? isDark ? 'border-cyan-500 bg-cyan-900/30 text-cyan-300' : 'border-cyan-500 bg-cyan-50 text-cyan-700'
                        : isDark ? 'border-zinc-600 hover:border-zinc-500 text-zinc-300' : 'border-zinc-200 hover:border-zinc-300 text-gray-700'
                    }`}>
                    <span className="text-xl block">{opt.icon}</span>
                    <span className="text-sm font-bold block mt-1">{opt.label}</span>
                  </button>
                ))}
              </div>
            </div>

            {/* Energy profiles nudge */}
            {energyProfiles.length > 0 && (
              <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
                <p className={`text-xs font-bold ${c.textSecondary} mb-2`}>🧠 Your calibrated energy costs</p>
                <div className="flex flex-wrap gap-2">
                  {energyProfiles.map((p, i) => (
                    <span key={i} className={`text-xs px-2.5 py-1 rounded-full font-semibold ${c.cardAlt} border ${c.border}`}>
                      {p.category}: {p.suggested_estimate}
                    </span>
                  ))}
                </div>
                <p className={`text-[10px] mt-2 ${c.textMuted}`}>Built from your past reflections — forecasts get smarter over time</p>
              </div>
            )}

            {/* Main input */}
            <div>
              <div className="flex items-center justify-between mb-2">
                <p className={`text-xs font-bold uppercase tracking-wider ${c.textMuted}`}>
                  What's on your plate this week? <span className="text-red-400">*</span>
                </p>
                {templates.length > 0 && (
                  <button onClick={() => setShowTemplates(!showTemplates)}
                    className={`text-xs font-bold ${c.accentTxt} hover:underline`}>📋 Templates ({templates.length})</button>
                )}
              </div>
              {showTemplates && (
                <div className={`p-3 rounded-xl border ${c.cardAlt} space-y-2 mb-3`}>
                  {templates.map(t => (
                    <div key={t.id} className="flex items-center gap-2">
                      <button onClick={() => loadTemplate(t)}
                        className={`flex-1 text-left text-xs p-2.5 rounded-lg transition-all ${c.btnSecondary}`}>
                        <span className="font-bold">{t.name}:</span> {t.text.slice(0, 60)}…
                      </button>
                      <button onClick={() => setTemplates(p => p.filter(x => x.id !== t.id))}
                        className={`text-xs ${c.textMuted} ${c.deleteHover}`}>✕</button>
                    </div>
                  ))}
                </div>
              )}
              <textarea value={description} onChange={(e) => setDescription(e.target.value)}
                placeholder="Just list your social commitments in plain English..."
                rows={5} className={`w-full px-4 py-3 rounded-xl border-2 text-sm transition-all focus:outline-none focus:ring-2 resize-none ${c.input}`} />
              {description.trim().length > 20 && !showSaveTemplate && (
                <button onClick={() => setShowSaveTemplate(true)} className={`text-xs ${c.textMuted} hover:underline mt-1 block`}>
                  💾 Save as template
                </button>
              )}
              {showSaveTemplate && (
                <div className="flex gap-2 mt-2">
                  <input type="text" value={templateName} onChange={e => setTemplateName(e.target.value)}
                    placeholder="Template name" className={`flex-1 px-3 py-2 rounded-lg border-2 text-xs ${c.input}`} />
                  <button onClick={saveTemplate} disabled={!templateName.trim()}
                    className={`px-3 py-2 rounded-lg text-xs font-bold disabled:opacity-40 ${c.btnPrimary}`}>Save</button>
                  <button onClick={() => setShowSaveTemplate(false)} className={`px-3 py-2 rounded-lg text-xs ${c.btnSecondary}`}>✕</button>
                </div>
              )}
              {!description && (
                <div className="mt-3">
                  <p className={`text-xs ${c.textMuted} mb-2`}>Try an example:</p>
                  {EXAMPLE_PROMPTS.map((ex, i) => (
                    <button key={i} onClick={() => setDescription(ex)}
                      className={`block w-full text-left text-xs p-2.5 mb-1.5 rounded-lg transition-all ${c.btnSecondary}`}>
                      "{ex.slice(0, 60)}…"
                    </button>
                  ))}
                </div>
              )}
              <button onClick={generateForecast} disabled={loading || !description.trim()}
                className={`w-full py-4 mt-3 sm:py-5 rounded-xl font-black text-lg shadow-lg disabled:opacity-40 transition-all min-h-[48px] ${c.btnPrimary}`}>
                {loading
                  ? <><span className="animate-spin inline-block mr-2">{tool?.icon ?? '⚙️'}</span> Forecasting…</>
                  : <><span className="mr-2">{tool?.icon ?? '📡'}</span> Forecast My Energy</>}
              </button>
              {error && <div className={`p-3 rounded-xl border mt-2 ${c.danger}`}><span className="mr-1">⚠️</span> {error}</div>}
            </div>

            {/* Stats + Trend */}
            {stats && (
              <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
                <div className="flex items-center justify-between mb-3">
                  <p className={`text-xs font-bold uppercase tracking-wider ${c.textMuted}`}>📊 Past forecasts</p>
                  <div className="flex gap-2">
                    {trendData.length >= 3 && (
                      <button onClick={() => setShowTrend(!showTrend)}
                        className={`text-xs ${c.accentTxt} hover:underline font-bold`}>{showTrend ? 'Hide' : '📈 Trend'}</button>
                    )}
                    <button onClick={() => setShowHistory(!showHistory)}
                      className={`text-xs ${c.textMuted} hover:underline`}>{showHistory ? 'Hide' : `History (${history.length})`}</button>
                  </div>
                </div>
                <div className="flex gap-6">
                  <div><p className={`text-2xl font-black ${c.text}`}>{stats.forecasts}</p><p className={`text-xs ${c.textMuted}`}>Forecasts</p></div>
                  <div><p className={`text-2xl font-black ${getBatteryColor(stats.avgLowest)}`}>{stats.avgLowest}%</p><p className={`text-xs ${c.textMuted}`}>Avg. lowest</p></div>
                  <div><p className={`text-2xl font-black ${isDark ? 'text-amber-400' : 'text-amber-600'}`}>{stats.totalWarnings}</p><p className={`text-xs ${c.textMuted}`}>Warnings</p></div>
                </div>
                {showTrend && trendData.length >= 3 && (
                  <div className="mt-4">
                    <p className={`text-xs font-bold ${c.textMuted} mb-2`}>Lowest battery per forecast</p>
                    <div className="flex items-end gap-1.5 h-24">
                      {trendData.map((d, i) => (
                        <div key={i} className="flex-1 flex flex-col items-center justify-end h-full">
                          <div className={`w-full rounded-t-md ${getBatteryFill(d.lowest)}`}
                            style={{ height: `${Math.max(d.lowest, 5)}%` }}
                            title={`${d.date}: ${d.lowest}%`} />
                          <p className={`text-[8px] mt-1 ${c.textMuted}`}>{d.date}</p>
                        </div>
                      ))}
                    </div>
                  </div>
                )}
                {showHistory && (
                  <div className="mt-3 space-y-2 max-h-48 overflow-y-auto">
                    {history.slice(0, 6).map(h => (
                      <div key={h.id} className={`p-3 rounded-xl ${c.cardAlt} border ${c.border}`}>
                        <div className="flex items-center justify-between">
                          <p className={`text-sm font-semibold truncate flex-1 ${c.text}`}>{h.description}…</p>
                          <span className={`text-xs font-bold ml-2 ${getBatteryColor(h.lowestBattery)}`}>↓{h.lowestBattery}%</span>
                        </div>
                        <p className={`text-xs ${c.textMuted}`}>{new Date(h.date).toLocaleDateString()} · {h.eventCount} events</p>
                      </div>
                    ))}
                    <button onClick={() => { setHistory([]); setShowHistory(false); }}
                      className={`text-xs ${c.textMuted} hover:underline`}>🗑️ Clear</button>
                  </div>
                )}
              </div>
            )}

            {/* Pre-result cross-ref */}
            <p className={`text-xs text-center ${c.textMuted}`}>
              Already burned out?{' '}
              <a href="/BurnoutBreadcrumbTracker" className={`text-xs ${linkStyle}`}>🔥 Burnout Breadcrumb Tracker</a>
              {' '}helps you trace the pattern.
            </p>
          </div>
        )}
      </div>

      {/* ════════ FORECAST RESULTS ════════ */}
      {forecast && !showReflect && (
        <div className="space-y-4" ref={resultsRef}>

          {/* Top bar */}
          <div className="flex items-center justify-between flex-wrap gap-2">
            <button onClick={reset} className={`text-sm font-semibold px-4 py-2 rounded-xl ${c.btnSecondary}`}>← New</button>
            <div className="flex gap-2">
              <button onClick={() => setShowReflect(true)}
                className={`text-xs font-bold px-3 py-2 rounded-xl ${c.btnSecondary}`}>📝 Reflect</button>
              {comparisonData && (
                <button onClick={() => setShowCompare(!showCompare)}
                  className={`text-xs font-bold px-3 py-2 rounded-xl ${c.btnSecondary}`}>
                  {showCompare ? '✕ Close' : '⚖️ Compare'}
                </button>
              )}
            </div>
          </div>

          {/* Week comparison */}
          {showCompare && comparisonData && (
            <div className={`${c.card} border-2 rounded-2xl p-5 ${isDark ? 'border-cyan-700/50' : 'border-cyan-300'}`}>
              <p className={`text-xs font-bold uppercase tracking-wider mb-3 ${c.textSecondary}`}>⚖️ This Week vs Last Week</p>
              <div className="grid grid-cols-2 gap-4">
                {[{ label: 'This week', data: comparisonData.current }, { label: 'Last week', data: comparisonData.previous }].map(({ label, data }) => (
                  <div key={label} className={`p-3 rounded-xl border ${c.cardAlt}`}>
                    <p className={`text-xs font-bold ${c.textMuted} mb-2`}>{label}</p>
                    <p className={`text-xs ${c.textSecondary}`}>{data.eventCount} events</p>
                    <p className={`text-lg font-black ${getBatteryColor(data.lowestBattery)}`}>↓{data.lowestBattery}%</p>
                    <p className={`text-xs ${c.textMuted}`}>lowest point</p>
                    <div className={`mt-2 w-full h-3 rounded-full overflow-hidden ${isDark ? 'bg-zinc-700' : 'bg-gray-200'}`}>
                      <div className={`h-full rounded-full ${getBatteryFill(100 - (data.committed || 0))}`}
                        style={{ width: `${Math.min(100, data.committed || 0)}%` }} />
                    </div>
                    <p className={`text-[10px] ${c.textMuted} mt-1`}>{data.committed || 0}% committed</p>
                    <p className={`text-xs mt-1 ${data.warningCount > 0 ? (isDark ? 'text-amber-400' : 'text-amber-600') : (isDark ? 'text-emerald-400' : 'text-emerald-600')}`}>
                      {data.warningCount} warning{data.warningCount !== 1 ? 's' : ''}
                    </p>
                  </div>
                ))}
              </div>
            </div>
          )}

          {/* Quick tip */}
          {forecast.quick_tip && (
            <div className={`p-4 rounded-2xl border-2 ${isDark ? 'border-cyan-700/50 bg-cyan-900/10' : 'border-cyan-200 bg-cyan-50'}`}>
              <p className={`text-sm font-bold ${c.accentTxt}`}>💡 This week's tip</p>
              <p className={`text-sm mt-1 ${c.text}`}>{forecast.quick_tip}</p>
            </div>
          )}

          {/* Parsed events — editable */}
          {activeEvents.length > 0 && (
            <div className={`${c.card} border ${c.border} rounded-2xl p-5`}>
              <p className={`text-xs font-bold uppercase tracking-wider mb-3 ${c.textMuted}`}>
                📋 Events ({activeEvents.length}) — tap cost to edit
              </p>
              <div className="space-y-2">
                {activeEvents.map((ev, i) => (
                  <div key={i} className="flex items-center justify-between gap-2">
                    <p className={`text-sm flex-1 ${c.text}`}>{ev.name}</p>
                    <div className="flex items-center">
                      <button onClick={() => updateEventCost(i, ev.energy_cost - 5)}
                        className={`w-6 h-6 rounded-l-lg text-xs font-bold ${c.btnSecondary}`}>−</button>
                      <span className={`px-2 py-1 text-xs font-black ${isDark ? 'bg-zinc-600 text-red-400' : 'bg-gray-100 text-red-600'}`}>
                        -{ev.energy_cost}%
                      </span>
                      <button onClick={() => updateEventCost(i, ev.energy_cost + 5)}
                        className={`w-6 h-6 rounded-r-lg text-xs font-bold ${c.btnSecondary}`}>+</button>
                    </div>
                    <button onClick={() => removeEvent(i)} className={`text-xs ${c.textMuted} ${c.deleteHover}`}>✕</button>
                  </div>
                ))}
              </div>
            </div>
          )}

          {/* 7-Day Forecast */}
          {forecast.energy_forecast?.next_7_days && (
            <div className={`${c.card} border ${c.border} rounded-2xl p-5`}>
              <p className={`text-xs font-bold uppercase tracking-wider mb-4 ${c.textMuted}`}>📊 7-Day Energy Forecast</p>
              <div className="space-y-4">
                {forecast.energy_forecast.next_7_days.map((day, idx) => (
                  <div key={idx} className={`pb-4 ${idx < forecast.energy_forecast.next_7_days.length - 1 ? `border-b ${c.border}` : ''}`}>
                    <div className="flex items-center justify-between mb-2">
                      <h3 className={`text-sm font-black ${c.text}`}>{day.date}</h3>
                      <div className="flex items-center gap-2">
                        <div className={`w-20 h-3 rounded-full overflow-hidden ${isDark ? 'bg-zinc-700' : 'bg-gray-200'}`}>
                          <div className={`h-full rounded-full ${getBatteryFill(day.end_of_day_battery)}`}
                            style={{ width: `${Math.max(day.end_of_day_battery, 3)}%` }} />
                        </div>
                        <span className={`text-sm font-black ${getBatteryColor(day.end_of_day_battery)}`}>{day.end_of_day_battery}%</span>
                      </div>
                    </div>
                    {day.events?.map((event, eidx) => (
                      <div key={eidx} className={`flex items-center justify-between p-2.5 rounded-lg mb-1.5 ml-2 ${getBatteryBg(event.battery_after)}`}>
                        <div className="flex-1 min-w-0">
                          <p className={`text-xs font-bold truncate ${c.text}`}>{event.event}</p>
                          <p className={`text-[10px] ${c.textMuted}`}>{event.time}</p>
                        </div>
                        <div className="text-right ml-2">
                          <p className={`text-xs font-bold ${isDark ? 'text-red-400' : 'text-red-600'}`}>-{event.energy_cost}%</p>
                          <p className={`text-[10px] ${c.textMuted}`}>→ {event.battery_after}%</p>
                        </div>
                      </div>
                    ))}
                    {day.warning && (
                      <div className={`mt-2 p-2.5 rounded-lg border ${c.danger} flex items-start gap-2`}>
                        <span>⚠️</span><p className="text-xs font-semibold">{day.warning}</p>
                      </div>
                    )}
                  </div>
                ))}
              </div>
            </div>
          )}

          {/* Warnings + Triage */}
          {forecast.warnings?.length > 0 && (
            <div className={`${c.card} border-2 rounded-2xl p-5 ${isDark ? 'border-red-800/50' : 'border-red-300'}`}>
              <div className="flex items-center justify-between mb-3">
                <p className={`text-xs font-bold uppercase tracking-wider ${isDark ? 'text-red-400' : 'text-red-600'}`}>
                  ⚠️ Warnings ({forecast.warnings.length})
                </p>
                {!triage && (
                  <button onClick={runTriage} disabled={triageLoading}
                    className={`text-xs font-bold px-3 py-1.5 rounded-lg ${c.btnPrimary}`}>
                    {triageLoading ? <span className="animate-spin inline-block">{tool?.icon ?? '⚙️'}</span> : '🎯 What should I skip?'}
                  </button>
                )}
              </div>
              {forecast.warnings.map((w, i) => (
                <div key={i} className={`p-4 rounded-xl border mb-3 ${c.danger}`}>
                  <div className="flex items-center justify-between mb-1">
                    <p className={`text-sm font-black ${c.text}`}>{w.type}</p>
                    <span className={`text-xs font-bold px-2 py-0.5 rounded-full ${isDark ? 'bg-red-800/50 text-red-300' : 'bg-red-100 text-red-700'}`}>{w.battery_prediction}%</span>
                  </div>
                  <p className={`text-sm ${c.textSecondary} mb-2`}>{w.why}</p>
                  <div className={`p-2.5 rounded-lg ${isDark ? 'bg-zinc-800' : 'bg-white'}`}>
                    <p className={`text-xs font-bold ${c.text}`}>→ {w.recommendation}</p>
                  </div>
                </div>
              ))}
            </div>
          )}

          {/* Triage results */}
          {triage?.triage && (
            <div className={`${c.card} border-2 rounded-2xl p-5 ${isDark ? 'border-cyan-700/50' : 'border-cyan-300'}`}>
              <p className={`text-xs font-bold uppercase tracking-wider mb-1 ${c.accentTxt}`}>🎯 Triage</p>
              <p className={`text-xs mb-4 ${c.textMuted}`}>Easiest to skip → hardest</p>
              {triage.triage.map((t, i) => {
                const costStyle = SOCIAL_COST_COLORS[t.social_cost] || SOCIAL_COST_COLORS.medium;
                return (
                  <div key={i} className={`p-4 rounded-xl border mb-3 ${c.cardAlt}`}>
                    <div className="flex items-center justify-between mb-2">
                      <div className="flex items-center gap-2">
                        <span>{REC_ICONS[t.recommendation] || '❓'}</span>
                        <p className={`text-sm font-black ${c.text}`}>{t.event_name}</p>
                      </div>
                      <span className={`text-xs font-bold px-2 py-0.5 rounded-full ${costStyle.bg} ${costStyle.text}`}>{t.social_cost}</span>
                    </div>
                    <p className={`text-xs ${c.textSecondary} mb-1`}>{t.social_cost_reason}</p>
                    <div className="flex items-center gap-3 mt-2 flex-wrap">
                      <span className={`text-xs font-bold px-2 py-1 rounded-lg ${isDark ? 'bg-emerald-900/30 text-emerald-300' : 'bg-emerald-100 text-emerald-700'}`}>
                        Saves {t.energy_saved}%
                      </span>
                      {t.modification && <span className={`text-xs ${c.textMuted}`}>→ {t.modification}</span>}
                    </div>
                    <p className={`text-xs mt-2 italic ${c.textSecondary}`}>{t.net_benefit}</p>
                    {(t.recommendation === 'skip' || t.recommendation === 'modify') && (
                      <button onClick={() => generateDecline(t.event_name, activeEvents.find(e => e.name === t.event_name)?.event_type)}
                        disabled={declineLoading && declineFor === t.event_name}
                        className={`mt-2 text-xs font-bold px-3 py-1.5 rounded-lg ${c.btnSecondary}`}>
                        {declineLoading && declineFor === t.event_name
                          ? <span className="animate-spin inline-block">{tool?.icon ?? '⚙️'}</span>
                          : '💬 Draft decline message'}
                      </button>
                    )}
                    {declineFor === t.event_name && declineMessages && (
                      <div className="mt-3 space-y-2">
                        {declineMessages.map((m, mi) => (
                          <div key={mi} className={`p-3 rounded-lg border ${isDark ? 'bg-zinc-800 border-zinc-600' : 'bg-white border-zinc-200'}`}>
                            <p className={`text-xs font-bold mb-1 ${c.textMuted}`}>{m.best_for}</p>
                            <p className={`text-sm ${c.text}`}>{m.text}</p>
                            <div className="mt-2"><CopyBtn content={m.text} label="Copy" /></div>
                          </div>
                        ))}
                      </div>
                    )}
                  </div>
                );
              })}
              {triage.optimal_plan && (
                <div className={`p-4 rounded-xl border ${c.success}`}>
                  <p className={`text-xs font-bold mb-1 ${c.successTxt}`}>
                    ✅ Optimal plan (+{triage.energy_recovered}% → lowest {triage.new_lowest_battery}%)
                  </p>
                  <p className={`text-sm ${c.text}`}>{triage.optimal_plan}</p>
                </div>
              )}
            </div>
          )}

          {/* Energy Budget */}
          {forecast.energy_budgeting && (
            <div className={`${c.card} border ${c.border} rounded-2xl p-5`}>
              <p className={`text-xs font-bold uppercase tracking-wider mb-3 ${c.textMuted}`}>⚡ Energy Budget</p>
              <div className="flex items-center justify-between mb-1.5">
                <span className={`text-xs ${c.textMuted}`}>Committed</span>
                <span className={`text-sm font-black ${c.text}`}>{forecast.energy_budgeting.already_committed}%</span>
              </div>
              <div className={`w-full h-4 rounded-full overflow-hidden ${isDark ? 'bg-zinc-700' : 'bg-gray-200'}`}>
                <div className={`h-full rounded-full ${
                  forecast.energy_budgeting.already_committed >= 85 ? 'bg-red-500'
                  : forecast.energy_budgeting.already_committed >= 60 ? 'bg-amber-500' : 'bg-emerald-500'
                }`} style={{ width: `${Math.min(100, forecast.energy_budgeting.already_committed)}%` }} />
              </div>
              <p className={`text-sm font-semibold mt-3 ${c.text} p-3 rounded-lg ${
                forecast.energy_budgeting.already_committed >= 85 ? (isDark ? 'bg-red-900/20' : 'bg-red-50')
                : forecast.energy_budgeting.already_committed >= 60 ? (isDark ? 'bg-amber-900/20' : 'bg-amber-50')
                : isDark ? 'bg-emerald-900/20' : 'bg-emerald-50'
              }`}>{forecast.energy_budgeting.reality_check}</p>
            </div>
          )}

          {/* Add one more thing */}
          <div className={`${c.card} border ${c.border} rounded-2xl p-5 space-y-3`}>
            <p className={`text-xs font-bold uppercase tracking-wider ${c.textMuted}`}>➕ "What if I add one more thing?"</p>
            <div className="flex gap-2">
              <input type="text" value={addEventText} onChange={e => setAddEventText(e.target.value)}
                placeholder="e.g., drinks Thursday" onKeyDown={e => e.key === 'Enter' && addNewEvent()}
                className={`flex-1 px-3 py-2.5 rounded-xl border-2 text-sm ${c.input}`} />
              <button onClick={addNewEvent} disabled={addEventLoading || !addEventText.trim()}
                className={`px-4 py-2.5 rounded-xl text-sm font-bold disabled:opacity-40 ${c.btnPrimary}`}>
                {addEventLoading ? <span className="animate-spin inline-block">{tool?.icon ?? '⚙️'}</span> : 'Check'}
              </button>
            </div>
            {addEventResult?.impact && (
              <div className={`p-4 rounded-xl border ${
                addEventResult.impact.danger_level === 'safe' ? c.success
                : addEventResult.impact.danger_level === 'caution' ? c.warning : c.danger
              }`}>
                {addEventResult.new_event && (
                  <p className={`text-xs font-bold mb-1 ${c.text}`}>{addEventResult.new_event.name} — -{addEventResult.new_event.energy_cost}%</p>
                )}
                <p className={`text-sm font-semibold ${c.text}`}>{addEventResult.impact.verdict}</p>
                <div className="flex gap-3 mt-2">
                  <span className={`text-xs font-bold ${getBatteryColor(addEventResult.impact.previous_lowest_battery)}`}>Before: {addEventResult.impact.previous_lowest_battery}%</span>
                  <span>→</span>
                  <span className={`text-xs font-bold ${getBatteryColor(addEventResult.impact.new_lowest_battery)}`}>After: {addEventResult.impact.new_lowest_battery}%</span>
                </div>
                {addEventResult.impact.suggestion && <p className={`text-xs mt-2 italic ${c.textSecondary}`}>💡 {addEventResult.impact.suggestion}</p>}
              </div>
            )}
          </div>

          {/* Recovery Plan */}
          {forecast.recovery_plan && (
            <div className={`${c.card} border ${c.border} rounded-2xl p-5 border-l-4 ${isDark ? 'border-l-emerald-500' : 'border-l-emerald-500'}`}>
              <p className={`text-xs font-bold uppercase tracking-wider mb-3 ${isDark ? 'text-emerald-400' : 'text-emerald-600'}`}>🩹 Recovery Plan</p>
              <div className="flex gap-4 mb-3">
                <div>
                  <p className={`text-xs ${c.textMuted}`}>Recharge by</p>
                  <p className={`text-sm font-bold ${c.text}`}>{forecast.recovery_plan.recharge_needed_by}</p>
                </div>
                <div>
                  <p className={`text-xs ${c.textMuted}`}>Min. alone time</p>
                  <p className={`text-sm font-bold ${c.text}`}>{forecast.recovery_plan.minimum_hours_alone}h</p>
                </div>
              </div>
              {forecast.recovery_plan.recommended_activities?.length > 0 && (
                <div className="flex flex-wrap gap-1.5 mb-3">
                  {forecast.recovery_plan.recommended_activities.map((a, i) => (
                    <span key={i} className={`text-xs px-2.5 py-1 rounded-full font-semibold ${c.cardAlt} border ${c.border}`}>{a}</span>
                  ))}
                </div>
              )}
              {forecast.recovery_plan.what_to_decline && (
                <div className={`p-3 rounded-lg border mb-3 ${c.warning}`}>
                  <p className="text-xs font-bold">📌 {forecast.recovery_plan.what_to_decline}</p>
                </div>
              )}
              <CopyBtn content={buildCalendarEvent()} label="📅 Copy calendar block" />
            </div>
          )}

          {/* Permission Statements */}
          {forecast.permission_statements && (
            <div className={`${c.card} border ${c.border} rounded-2xl p-5 border-l-4 border-l-sky-500`}>
              <p className={`text-xs font-bold uppercase tracking-wider mb-3 ${isDark ? 'text-sky-400' : 'text-sky-600'}`}>💙 Permission to Rest</p>
              {forecast.permission_statements.its_okay_to?.map((perm, i) => (
                <div key={i} className="flex items-start gap-2 mb-1.5">
                  <span className={`text-xs mt-0.5 ${isDark ? 'text-sky-400' : 'text-sky-600'}`}>✓</span>
                  <p className={`text-sm ${c.textSecondary}`}>{perm}</p>
                </div>
              ))}
              {forecast.permission_statements.reframe && (
                <div className={`mt-3 p-3 rounded-lg ${isDark ? 'bg-sky-900/20' : 'bg-sky-50'}`}>
                  <p className={`text-sm italic font-semibold ${c.text}`}>"{forecast.permission_statements.reframe}"</p>
                </div>
              )}
            </div>
          )}

          {/* Post-result cross-refs */}
          <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
            <p className={`text-xs font-semibold uppercase tracking-wide mb-3 ${c.textMuted}`}>🔗 Related tools</p>
            <div className="flex flex-wrap gap-3">
              <a href="/SpiralStopper" className={`text-xs ${linkStyle}`}>🌀 Spiral Stopper</a>
            </div>
          </div>

          <button onClick={reset} className={`w-full py-4 rounded-xl font-bold ${c.btnSecondary}`}>
            <span className="mr-2">🔄</span> New Forecast
          </button>
        </div>
      )}

      {/* ════════ REFLECTION MODE ════════ */}
      {forecast && showReflect && (
        <div className="space-y-4">
          <div className="flex items-center justify-between">
            <button onClick={() => { setShowReflect(false); setReflectResult(null); }}
              className={`text-sm font-semibold px-4 py-2 rounded-xl ${c.btnSecondary}`}>← Back to forecast</button>
          </div>

          <div className={`${c.card} border ${c.border} rounded-2xl p-5`}>
            <p className={`text-xs font-bold uppercase tracking-wider mb-1 ${c.textSecondary}`}>📝 Post-Week Reflection</p>
            <p className={`text-xs mb-4 ${c.textMuted}`}>How did each event actually go? This trains your future forecasts.</p>

            {!reflectResult && (
              <div className="space-y-4">
                {activeEvents.map((ev, i) => (
                  <div key={i} className={`p-4 rounded-xl border ${c.cardAlt}`}>
                    <div className="flex items-center justify-between mb-2">
                      <p className={`text-sm font-bold ${c.text}`}>{ev.name}</p>
                      <span className={`text-xs ${c.textMuted}`}>Predicted: -{ev.energy_cost}%</span>
                    </div>
                    <div className="flex flex-wrap gap-1.5 mb-2">
                      {ACTUAL_OPTIONS.map(opt => (
                        <button key={opt.id}
                          onClick={() => setReflections(prev => ({ ...prev, [i]: { ...prev[i], actual: opt.id } }))}
                          className={`text-xs px-2.5 py-1.5 rounded-lg font-semibold transition-all ${
                            reflections[i]?.actual === opt.id
                              ? isDark ? 'bg-cyan-600 text-white' : 'bg-cyan-600 text-white'
                              : c.btnSecondary
                          }`}>
                          {opt.icon} {opt.label}
                        </button>
                      ))}
                    </div>
                    <input type="text" placeholder="Notes (optional)"
                      value={reflections[i]?.notes || ''}
                      onChange={e => setReflections(prev => ({ ...prev, [i]: { ...prev[i], notes: e.target.value } }))}
                      className={`w-full px-3 py-2 rounded-lg border-2 text-xs ${c.input}`} />
                  </div>
                ))}
                <button onClick={submitReflection} disabled={reflectLoading || Object.keys(reflections).length === 0}
                  className={`w-full py-4 rounded-xl font-black text-base shadow-lg disabled:opacity-40 min-h-[48px] ${c.btnPrimary}`}>
                  {reflectLoading
                    ? <><span className="animate-spin inline-block mr-2">{tool?.icon ?? '⚙️'}</span> Analyzing…</>
                    : <><span className="mr-2">{tool?.icon ?? '🔍'}</span> Analyze My Week</>}
                </button>
              </div>
            )}

            {reflectResult && (
              <div className="space-y-4">
                {reflectResult.event_calibrations?.map((cal, i) => (
                  <div key={i} className={`p-3 rounded-xl border ${c.cardAlt}`}>
                    <div className="flex items-center justify-between">
                      <p className={`text-sm font-bold ${c.text}`}>{cal.event_name}</p>
                      <span className={`text-xs font-bold px-2 py-0.5 rounded-full ${
                        cal.actual_assessment === 'more_draining' ? (isDark ? 'bg-red-900/30 text-red-300' : 'bg-red-100 text-red-700')
                        : cal.actual_assessment === 'less_draining' || cal.actual_assessment === 'restorative'
                        ? (isDark ? 'bg-emerald-900/30 text-emerald-300' : 'bg-emerald-100 text-emerald-700')
                        : (isDark ? 'bg-zinc-600 text-zinc-300' : 'bg-gray-100 text-gray-700')
                      }`}>{cal.suggested_adjustment}</span>
                    </div>
                    <p className={`text-xs mt-1 ${c.textMuted}`}>{cal.insight}</p>
                  </div>
                ))}
                {reflectResult.patterns_detected?.length > 0 && (
                  <div className={`p-4 rounded-xl border-2 ${isDark ? 'border-cyan-700/50 bg-cyan-900/10' : 'border-cyan-200 bg-cyan-50'}`}>
                    <p className={`text-xs font-bold mb-2 ${c.accentTxt}`}>🔍 Patterns detected</p>
                    {reflectResult.patterns_detected.map((p, i) => (
                      <p key={i} className={`text-sm mb-1 ${c.text}`}>• {p}</p>
                    ))}
                  </div>
                )}
                {reflectResult.week_summary && (
                  <div className={`p-4 rounded-xl ${isDark ? 'bg-zinc-700/50' : 'bg-gray-50'}`}>
                    <p className={`text-sm ${c.text}`}>{reflectResult.week_summary}</p>
                  </div>
                )}
                {reflectResult.next_week_advice && (
                  <div className={`p-4 rounded-xl border ${c.success}`}>
                    <p className={`text-xs font-bold mb-1 ${c.successTxt}`}>💡 For next week</p>
                    <p className={`text-sm ${c.text}`}>{reflectResult.next_week_advice}</p>
                  </div>
                )}
                <button onClick={() => { setShowReflect(false); setReflectResult(null); }}
                  className={`w-full py-3 rounded-xl font-bold ${c.btnSecondary}`}>← Back to forecast</button>
              </div>
            )}

            {/* Cross-ref — shown in both reflection input and results states */}
            <p className={`text-xs text-center ${c.textMuted}`}>
              Factor this into your energy plan with{' '}
              <a href="/SpoonBudgeter" className={linkStyle}>🥄 Spoon Budgeter</a>.
            </p>
          </div>
        </div>
      )}
    </div>
  );
};

RechargeRadar.displayName = 'RechargeRadar';
export default RechargeRadar;
