// GPS REVERSE GEOCODING — enriches backend with real location context
// Uses Nominatim (OpenStreetMap) — free, no key required
// ════════════════════════════════════════════════════════════
const reverseGeocode = async (lat, lng) => {
  try {
    const url = `https://nominatim.openstreetmap.org/reverse?format=json&lat=${lat}&lon=${lng}&zoom=12&addressdetails=1`;
    const r = await fetch(url, {
      headers: { 'Accept-Language': 'en', 'User-Agent': 'DeftBrain-SafeWalk/1.0' },
    });
    if (!r.ok) return '';
    const d = await r.json();
    const a = d.address || {};
    const parts = [
      a.suburb || a.neighbourhood || a.quarter || a.borough,
      a.city || a.town || a.village || a.municipality,
      a.state,
      a.country_code?.toUpperCase(),
    ].filter(Boolean);
    return parts.slice(0, 3).join(', ');
  } catch {
    return '';
  }
};

const getGpsContext = () =>
  new Promise((resolve) => {
    if (!navigator.geolocation) { resolve(''); return; }
    const timer = setTimeout(() => resolve(''), 4000);
    navigator.geolocation.getCurrentPosition(
      async (pos) => {
        clearTimeout(timer);
        const label = await reverseGeocode(pos.coords.latitude, pos.coords.longitude);
        resolve(label);
      },
      () => { clearTimeout(timer); resolve(''); },
      { timeout: 3500, maximumAge: 120000, enableHighAccuracy: false }
    );
  });

