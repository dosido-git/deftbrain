// Defense-in-depth: strip any Anthropic web_search citation markup that the
// backend's stripCites missed. Recursive — covers strings, arrays, objects.
// Catches both <cite index="..."> and  forms, with or without close tag.
const CITE_TAG_RE = /<\/?(antml:)?cite\b[^>]*>/g;
const stripCitesDeep = (v) => {
  if (typeof v === 'string') return v.replace(CITE_TAG_RE, '');
  if (Array.isArray(v)) return v.map(stripCitesDeep);
  if (v && typeof v === 'object') {
    const out = {};
    for (const k of Object.keys(v)) out[k] = stripCitesDeep(v[k]);
    return out;
  }
  return v;
};

