import React, { useState, useCallback, useMemo, useRef, useEffect } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { usePersistentState } from '../hooks/usePersistentState';
import { useRegisterActions } from '../components/ActionBarContext';
import { useTranslation } from '../i18n/useTranslation';

// ════════════════════════════════════════════════════════════
// CONSTANTS
const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';
// ════════════════════════════════════════════════════════════

const APPT_TYPES = [
  { id: 'new-problem',       labelKey: 'dvp_appt_new_problem',    hintKey: 'dvp_appt_new_problem_hint' },
  { id: 'follow-up',         labelKey: 'dvp_appt_follow_up',      hintKey: 'dvp_appt_follow_up_hint' },
  { id: 'annual-physical',   labelKey: 'dvp_appt_annual',         hintKey: 'dvp_appt_annual_hint' },
  { id: 'specialist',        labelKey: 'dvp_appt_specialist',     hintKey: 'dvp_appt_specialist_hint' },
  { id: 'second-opinion',    labelKey: 'dvp_appt_second_opinion', hintKey: 'dvp_appt_second_opinion_hint' },
  { id: 'procedure-consult', labelKey: 'dvp_appt_procedure',      hintKey: 'dvp_appt_procedure_hint' },
  { id: 'mental-health',     labelKey: 'dvp_appt_mental',         hintKey: 'dvp_appt_mental_hint' },
  { id: 'urgent-care',       labelKey: 'dvp_appt_urgent',         hintKey: 'dvp_appt_urgent_hint' },
];


const PRIORITY_COLOR = (p, d) => {
  if (p === 'high')   return d ? 'text-red-400 bg-red-900/20'   : 'text-red-700 bg-red-100';
  if (p === 'medium') return d ? 'text-amber-400 bg-amber-900/20' : 'text-amber-700 bg-amber-100';
  return d ? 'text-cyan-400 bg-cyan-900/20' : 'text-cyan-700 bg-cyan-100';
};

const CATEGORY_ICON = {
  diagnosis:  '🔍',
  treatment:  '💊',
  medication: '💊',
  'follow-up': '📅',
  logistics:  '📋',
  lifestyle:  '🌱',
  prognosis:  '🔮',
};

// Bilingual text helper — splits "English ||| Translation"
const BiText = ({ text, c }) => {
  if (!text || !text.includes('|||')) return <span>{text}</span>;
  const [en, tr] = text.split('|||').map(s => s.trim());
  return <span>{en}<br /><span className={`${c.textSecondary} italic`}>{tr}</span></span>;
};

// Collapsible section, matching the DVT pattern
function Sec({ icon, title, badge, open, onToggle, children, c }) {
  return (
    <div className={`${c.card} border ${c.border} rounded-xl p-5`}>
      <button onClick={onToggle} className="flex items-center gap-2 w-full text-left">
        <span className="text-lg">{icon}</span>
        <h3 className={`text-sm font-bold ${c.text} flex-1`}>{title}</h3>
        {badge != null && <span className={`text-[10px] font-bold px-1.5 py-0.5 rounded ${c.pillGray} border`}>{badge}</span>}
        <span className={c.textMuted}>{open ? '▲' : '▼'}</span>
      </button>
      {open && <div className="mt-4">{children}</div>}
    </div>
  );
}


const DoctorVisitPrep = ({ tool }) => {
  const { callToolEndpoint, loading, userLocale, userCurrency, userRegion } = useClaudeAPI();
  const { isDark } = useTheme();
  const { t } = useTranslation();

  const c = {
    // Standard keys
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-400' : 'text-gray-500',
    input:         isDark ? 'bg-zinc-900 border-zinc-700 text-zinc-50 placeholder:text-zinc-500 focus:border-cyan-500 focus:ring-cyan-500/20' : 'bg-white border-gray-300 text-gray-900 placeholder:text-gray-400 focus:border-cyan-500',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white' : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-100' : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    success:       isDark ? 'bg-green-900/20 border-green-700 text-green-200' : 'bg-green-50 border-green-300 text-green-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200' : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200' : 'bg-red-50 border-red-200 text-red-800',
    // Bespoke
    highlight:     isDark ? 'bg-cyan-900/20 border-cyan-700 text-cyan-200' : 'bg-cyan-50 border-cyan-200 text-cyan-800',
    pillGray:      isDark ? 'bg-zinc-700 text-zinc-400 border-zinc-600' : 'bg-zinc-100 text-zinc-500 border-zinc-200',
    deleteHover:   isDark ? 'hover:text-red-400' : 'hover:text-red-600',
    accentTxt:     isDark ? 'text-cyan-400' : 'text-cyan-600',
    labelText:     isDark ? 'text-zinc-200' : 'text-gray-700',
    required:      isDark ? 'text-amber-400' : 'text-amber-700',
  };
  c.textMuteded = c.textMuted;
  c.label = c.labelText;

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-700 hover:text-cyan-800 underline underline-offset-2';

  // ── useState (all first per PF-14) ──
  // FORM
  const [chiefConcern, setChiefConcern] = useState('');
  const [symptomDetails, setSymptomDetails] = useState('');
  const [durationText, setDurationText] = useState('');
  const [severity, setSeverity] = useState(5);
  const [whatMakesItBetterWorse, setWhatMakesItBetterWorse] = useState('');
  const [currentMedications, setCurrentMedications] = useState('');
  const [allergies, setAllergies] = useState('');
  const [relevantHistory, setRelevantHistory] = useState('');
  const [appointmentType, setAppointmentType] = useState('new-problem');
  const [specificWorry, setSpecificWorry] = useState('');
  // RESULTS
  const [results, setResults] = usePersistentState('dvp-results', null);
  const [error, setError] = useState('');
  // UI TOGGLES
  const [secs, setSecs] = useState({
    opener: true, redFlags: true, questions: true, mention: true,
    medQs: false, checklist: true, bring: false, tips: false,
  });

  // ── useRef (after all useState per PF-14) ──
  const resultsRef = useRef(null);
  const handleSubmitRef = useRef(null);
  const canSubmitRef = useRef(false);

  // ── usePersistentState (after all useRef per PF-14) ──
  const [prepHistory, setPrepHistory] = usePersistentState('dvp-prep-history', []);
  const [knownMeds] = usePersistentState('doctor-meds-list', []); // shared with DVT — read only here

  const tog = (k) => setSecs(p => ({ ...p, [k]: !p[k] }));

  // ────────────────────────────────────────────────────────────
  // HANDLERS
  // ────────────────────────────────────────────────────────────
  const activeMeds = useMemo(() => knownMeds.filter(m => m.active), [knownMeds]);

  const handleGenerate = async () => {
    if (!chiefConcern.trim()) { setError(t('dvp_err_chief')); return; }
    setError(''); setResults(null);
    const allMeds = currentMedications.trim()
      ? currentMedications.trim() + (activeMeds.length ? `\n\nAlso taking (from tracked list): ${activeMeds.map(m => m.name).join(', ')}` : '')
      : activeMeds.length ? activeMeds.map(m => m.name).join(', ') : null;
    try {
      const data = await callToolEndpoint('doctor-visit-prep', {
        chiefConcern: chiefConcern.trim(),
        symptomDetails: symptomDetails.trim() || null,
        durationText: durationText.trim() || null,
        severity: severity || null,
        whatMakesItBetterWorse: whatMakesItBetterWorse.trim() || null,
        currentMedications: allMeds,
        allergies: allergies.trim() || null,
        relevantHistory: relevantHistory.trim() || null,
        appointmentType,
        specificWorry: specificWorry.trim() || null,
        knownMedications: activeMeds.length
          ? activeMeds.map(m => ({ name: m.name, purpose: m.purpose, prescribedDate: m.prescribedDate }))
          : null,
        userLocale, userCurrency, userRegion,
      });
      setResults(data);
      // Open the most-actionable sections by default; collapse the rest
      setSecs(p => ({
        ...p,
        opener: true,
        redFlags: !!(data.red_flag_symptoms_to_report?.length),
        questions: true,
        mention: true,
        medQs: !!(data.questions_to_ask_if_medication_is_prescribed?.length),
        checklist: true,
        bring: false,
        tips: false,
      }));
    } catch (err) {
      setError(err.message || t('dvp_err_failed'));
    }
  };

  const handleReset = () => {
    setChiefConcern(''); setSymptomDetails(''); setDurationText('');
    setSeverity(5); setWhatMakesItBetterWorse(''); setCurrentMedications('');
    setAllergies(''); setRelevantHistory(''); setAppointmentType('new-problem');
    setSpecificWorry(''); setResults(null); setError('');
  };

  const loadExample = () => {
    setResults(null); setError('');
    setChiefConcern(t('dvp_ex_chief'));
    setSymptomDetails(t('dvp_ex_symptom'));
    setDurationText(t('dvp_ex_duration'));
    setSeverity(6);
    setWhatMakesItBetterWorse(t('dvp_ex_better_worse'));
    setCurrentMedications(t('dvp_ex_meds'));
    setAllergies(t('dvp_ex_allergies'));
    setRelevantHistory(t('dvp_ex_history'));
    setAppointmentType('follow-up');
    setSpecificWorry(t('dvp_ex_worry'));
  };

  const loadSavedPrep = (p) => {
    setChiefConcern(p.chiefConcern || '');
    setSymptomDetails(p.symptomDetails || '');
    setDurationText(p.durationText || '');
    setSeverity(p.severity || 5);
    setWhatMakesItBetterWorse(p.whatMakesItBetterWorse || '');
    setCurrentMedications(p.currentMedications || '');
    setAllergies(p.allergies || '');
    setRelevantHistory(p.relevantHistory || '');
    setAppointmentType(p.appointmentType || 'new-problem');
    setSpecificWorry(p.specificWorry || '');
    setResults(p.results || null);
    setError('');
  };

  const saveCurrentPrep = useCallback(() => {
    if (!results) return;
    const entry = {
      id: Date.now(),
      date: new Date().toISOString().split('T')[0],
      // PF-25 exception: 60/40 are label/preview text lengths, not a history cap.
      label: chiefConcern.trim().slice(0, 60) || t('dvp_history_label_default'),
      preview: chiefConcern.trim().slice(0, 40) || t('dvp_history_label_default'),
      chiefConcern: chiefConcern.trim(),
      symptomDetails: symptomDetails.trim(),
      durationText: durationText.trim(),
      severity,
      whatMakesItBetterWorse: whatMakesItBetterWorse.trim(),
      currentMedications: currentMedications.trim(),
      allergies: allergies.trim(),
      relevantHistory: relevantHistory.trim(),
      appointmentType,
      specificWorry: specificWorry.trim(),
      results,
    };
    setPrepHistory(prev => [entry, ...prev.filter(e => e.id !== entry.id)].slice(0, 6));
    return entry;
  }, [
    results, chiefConcern, symptomDetails, durationText, severity,
    whatMakesItBetterWorse, currentMedications, allergies, relevantHistory,
    appointmentType, specificWorry, setPrepHistory, t,
  ]);

  // ────────────────────────────────────────────────────────────
  // EXPORT — buildFullText covers the full result, used by ActionBar
  // ────────────────────────────────────────────────────────────
  const buildFullText = useCallback(() => {
    if (!results) return '';
    const r = results;
    const apptLabel = APPT_TYPES.find(a => a.id === appointmentType);
    const lines = [
      t('dvp_copy_header'),
      '═'.repeat(50),
      `${t('dvp_copy_date')} ${new Date().toISOString().split('T')[0]}`,
      `${t('dvp_copy_appt_type')} ${apptLabel ? t(apptLabel.labelKey).replace(/^\S+\s/, '') : appointmentType}`,
      chiefConcern ? `${t('dvp_copy_chief')} ${chiefConcern}` : '',
      '',
    ].filter(Boolean);

    if (r.opener) {
      lines.push(t('dvp_copy_opener'), '─'.repeat(40), r.opener, '');
    }

    if (r.symptom_description_clinical) {
      lines.push(t('dvp_copy_symptom'), '─'.repeat(40), r.symptom_description_clinical, '');
    }

    if (r.red_flag_symptoms_to_report?.length) {
      lines.push(t('dvp_copy_red_flags'), '─'.repeat(40));
      r.red_flag_symptoms_to_report.forEach(rf => lines.push(`• ${rf}`));
      lines.push('');
    }

    if (r.prioritized_questions?.length) {
      lines.push(t('dvp_copy_questions'), '─'.repeat(40));
      r.prioritized_questions.forEach((q, i) => {
        lines.push(`${i + 1}. [${(q.priority || '').toUpperCase()}] ${q.question}`);
        if (q.why_this_matters) lines.push(`   ${t('dvp_copy_why')} ${q.why_this_matters}`);
        lines.push('');
      });
    }

    if (r.things_to_mention_even_if_not_asked?.length) {
      lines.push(t('dvp_copy_mention'), '─'.repeat(40));
      r.things_to_mention_even_if_not_asked.forEach(item => lines.push(`• ${item}`));
      lines.push('');
    }

    if (r.questions_to_ask_if_medication_is_prescribed?.length) {
      lines.push(t('dvp_copy_med_qs'), '─'.repeat(40));
      r.questions_to_ask_if_medication_is_prescribed.forEach(q => lines.push(`? ${q}`));
      lines.push('');
    }

    if (r.pre_visit_checklist?.length) {
      lines.push(t('dvp_copy_checklist'), '─'.repeat(40));
      r.pre_visit_checklist.forEach(item => lines.push(`☐ ${item}`));
      lines.push('');
    }

    if (r.what_to_bring?.length) {
      lines.push(t('dvp_copy_bring'), '─'.repeat(40));
      r.what_to_bring.forEach(item => lines.push(`• ${item}`));
      lines.push('');
    }

    if (r.conversation_tips?.length) {
      lines.push(t('dvp_copy_tips'), '─'.repeat(40));
      r.conversation_tips.forEach(item => lines.push(`• ${item}`));
      lines.push('');
    }

    if (r.goal_for_the_visit) {
      lines.push(t('dvp_copy_goal'), '─'.repeat(40), r.goal_for_the_visit, '');
    }

    return lines.join('\n') + BRAND;
  }, [results, appointmentType, chiefConcern, t]);

  // ── PF-8: Register export content with the wrapper's persistent ActionBar ──
  useRegisterActions(buildFullText(), tool?.title || 'Doctor Visit Prep');

  // ── PF-6: Keyboard handler (ref pattern avoids stale closures) ──
  handleSubmitRef.current = handleGenerate;
  canSubmitRef.current = !loading && !!chiefConcern.trim();

  // ── PF-7: Scroll to results (with clearTimeout cleanup) ──
  useEffect(() => {
    if (!results) return;
    const t = setTimeout(() => resultsRef.current?.scrollIntoView({ behavior: 'smooth' }), 100);
    return () => clearTimeout(t);
  }, [results]);

  // ── PF-6: Keyboard useEffect ──
  useEffect(() => {
    const handler = (e) => {
      const tag = document.activeElement?.tagName;
      if (tag === 'SELECT') return;
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && canSubmitRef.current) {
        handleSubmitRef.current?.();
      }
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, []);

  // Visibility helper for the header reset button.
  // Combined into a single boolean so the S5.5 audit's "results &&" split anchors
  // to the actual results conditional further down, not this one.
  const hasResetableState = !!(
    chiefConcern.trim() || symptomDetails.trim() || durationText.trim() ||
    whatMakesItBetterWorse.trim() || currentMedications.trim() ||
    allergies.trim() || relevantHistory.trim() || specificWorry.trim() ||
    severity !== 5 || appointmentType !== 'new-problem' || results
  );

  // ════════════════════════════════════════════════════════════
  // RENDER
  // ════════════════════════════════════════════════════════════
  return (
    <div className={`space-y-4 ${c.text}`}>
      {/* HEADER CARD — standalone, not wrapped in c.card div per PF-4 */}
      <div className={`${c.card} border ${c.border} rounded-xl shadow-sm p-5`}>
        <div className="pb-3 border-b border-zinc-500 flex items-start justify-between gap-2">
          <div className="flex-1">
            <h2 className={`text-xl font-bold ${c.text} flex items-center`}>
              <span className="mr-2">{tool?.icon ?? '👨🏻'}</span>{tool?.title ?? t('dvp_title')}
            </h2>
            <p className={`text-sm ${c.textSecondary}`}>{tool?.tagline ?? t('dvp_tagline')}</p>
          </div>
          {hasResetableState && (
            <button
              onClick={handleReset}
              className={`${c.btnSecondary} text-xs px-3 py-1.5 rounded-lg font-semibold whitespace-nowrap`}
              title={t('dvp_start_over_title')}
            >
              ↺ {t('dvp_start_over')}
            </button>
          )}
        </div>
      </div>

      {/* INPUT CARD */}
      <div className={`${c.card} rounded-xl shadow-sm border ${c.border} p-6 space-y-5`}>
        {/* Disclaimer */}
        <div className={`${c.warning} border-l-4 rounded-r-lg p-4 flex items-start gap-2`}>
          <span>⚠️</span>
          <div>
            <h4 className="font-bold text-sm mb-0.5">{t('dvp_disclaimer_title')}</h4>
            <p className="text-xs">{t('dvp_disclaimer_body')}</p>
          </div>
        </div>

        {/* Appointment type + Try Example */}
        <div>
          <div className="flex items-center justify-between mb-1.5">
            <label className={`text-sm font-semibold ${c.textSecondary}`}>{t('dvp_appt_type')}</label>
            <button onClick={loadExample} disabled={loading} style={{ backgroundColor: (tool?.headerColor ?? '#888888') + '80' }} className={`px-2.5 py-0.5 rounded-full text-xs font-medium border disabled:opacity-40 ${isDark ? 'text-white border-white/40' : 'text-gray-800 border-transparent'}`} title={t('dvp_try_example_title')}>{t('try_example')}</button>
          </div>
          <div className="grid grid-cols-2 sm:grid-cols-4 gap-1.5">
            {APPT_TYPES.map(at => (
              <button
                key={at.id}
                onClick={() => setAppointmentType(at.id)}
                title={t(at.hintKey)}
                className={`p-2 border-2 rounded-lg text-center transition-colors ${
                  appointmentType === at.id
                    ? (isDark ? 'border-cyan-500 bg-cyan-900/20' : 'border-cyan-500 bg-cyan-50')
                    : (isDark ? 'border-zinc-700' : 'border-gray-200')
                }`}
              >
                <p className={`text-[11px] font-semibold ${c.text}`}>{t(at.labelKey)}</p>
              </button>
            ))}
          </div>
        </div>

        {/* Chief concern (required) */}
        <div>
          <label className={`text-sm font-semibold ${c.textSecondary} block mb-1.5`}>
            {t('dvp_chief_concern')} <span className={c.required}>*</span>
            <span className={`ml-1 text-[10px] font-normal ${c.textMuted}`}>{t('dvp_chief_concern_hint')}</span>
          </label>
          <textarea
            value={chiefConcern}
            onChange={e => setChiefConcern(e.target.value)}
            placeholder={t('dvp_chief_concern_ph')}
            className={`w-full h-20 p-3 border-2 rounded-lg ${c.input} outline-none focus:ring-2 resize-none text-sm`}
          />
        </div>

        {/* Symptom details + duration + severity */}
        <div>
          <label className={`text-sm font-semibold ${c.textSecondary} block mb-1.5`}>
            {t('dvp_symptom_details')} <span className={`text-[10px] font-normal ${c.textMuted}`}>{t('dvp_symptom_details_hint')}</span>
          </label>
          <textarea
            value={symptomDetails}
            onChange={e => setSymptomDetails(e.target.value)}
            placeholder={t('dvp_symptom_details_ph')}
            className={`w-full h-16 p-3 border-2 rounded-lg ${c.input} outline-none resize-none text-sm`}
          />
        </div>

        <div className="grid grid-cols-1 sm:grid-cols-2 gap-3">
          <div>
            <label className={`text-sm font-semibold ${c.textSecondary} block mb-1.5`}>{t('dvp_duration')}</label>
            <input
              value={durationText}
              onChange={e => setDurationText(e.target.value)}
              placeholder={t('dvp_duration_ph')}
              className={`w-full p-2.5 border rounded-lg ${c.input} outline-none text-sm`}
            />
          </div>
          <div>
            <label className={`text-sm font-semibold ${c.textSecondary} block mb-1.5`}>
              {t('dvp_severity')} <span className={`${c.text} font-bold`}>{severity}</span>
            </label>
            <input
              type="range" min="1" max="10"
              value={severity}
              onChange={e => setSeverity(Number(e.target.value))}
              className="w-full accent-cyan-600"
            />
            <div className="flex justify-between text-[9px]">
              <span className={c.textMuted}>{t('dvp_severity_mild')}</span>
              <span className={c.textMuted}>{t('dvp_severity_moderate')}</span>
              <span className={c.textMuted}>{t('dvp_severity_severe')}</span>
            </div>
          </div>
        </div>

        <div>
          <label className={`text-sm font-semibold ${c.textSecondary} block mb-1.5`}>{t('dvp_better_worse')}</label>
          <input
            value={whatMakesItBetterWorse}
            onChange={e => setWhatMakesItBetterWorse(e.target.value)}
            placeholder={t('dvp_better_worse_ph')}
            className={`w-full p-2.5 border rounded-lg ${c.input} outline-none text-sm`}
          />
        </div>

        {/* Meds + allergies + sessionHistory */}
        <div>
          <label className={`text-sm font-semibold ${c.textSecondary} block mb-1.5`}>
            {t('dvp_current_meds')} <span className={`text-[10px] font-normal ${c.textMuted}`}>{t('dvp_current_meds_hint')}</span>
          </label>
          {activeMeds.length > 0 && (
            <div className={`${c.success} border rounded-lg p-3 mb-2`}>
              <p className="text-[10px] font-bold mb-1">{t('dvp_meds_auto', { count: activeMeds.length })}</p>
              <p className="text-xs">{activeMeds.map(m => m.name).join(', ')}</p>
            </div>
          )}
          <textarea
            value={currentMedications}
            onChange={e => setCurrentMedications(e.target.value)}
            placeholder={t('dvp_current_meds_ph')}
            className={`w-full h-14 p-3 border-2 rounded-lg ${c.input} outline-none resize-none text-sm`}
          />
        </div>

        <div className="grid grid-cols-1 sm:grid-cols-2 gap-3">
          <div>
            <label className={`text-sm font-semibold ${c.textSecondary} block mb-1.5`}>
              {t('dvp_allergies')} <span className={`text-[10px] font-normal ${c.textMuted}`}>{t('dvp_allergies_hint')}</span>
            </label>
            <input
              value={allergies}
              onChange={e => setAllergies(e.target.value)}
              placeholder={t('dvp_allergies_ph')}
              className={`w-full p-2.5 border rounded-lg ${c.input} outline-none text-sm`}
            />
          </div>
          <div>
            <label className={`text-sm font-semibold ${c.textSecondary} block mb-1.5`}>
              {t('dvp_history')} <span className={`text-[10px] font-normal ${c.textMuted}`}>{t('dvp_history_hint')}</span>
            </label>
            <input
              value={relevantHistory}
              onChange={e => setRelevantHistory(e.target.value)}
              placeholder={t('dvp_history_ph')}
              className={`w-full p-2.5 border rounded-lg ${c.input} outline-none text-sm`}
            />
          </div>
        </div>

        {/* Specific worry */}
        <div>
          <label className={`text-sm font-semibold ${c.textSecondary} block mb-1.5`}>
            {t('dvp_worry')} <span className={`text-[10px] font-normal ${c.textMuted}`}>{t('dvp_worry_hint')}</span>
          </label>
          <textarea
            value={specificWorry}
            onChange={e => setSpecificWorry(e.target.value)}
            placeholder={t('dvp_worry_ph')}
            className={`w-full h-16 p-3 border-2 rounded-lg ${c.input} outline-none resize-none text-sm`}
          />
        </div>

        {/* Language */}
        {/* Submit */}
        <div className="flex gap-3">
          <button
            onClick={handleGenerate}
            disabled={loading || !chiefConcern.trim()}
            className={`w-full ${c.btnPrimary} disabled:opacity-40 font-bold py-3 min-h-[48px] rounded-lg flex items-center justify-center gap-2`}
          >
            {loading
              ? <><span className="animate-spin inline-block">{tool?.icon ?? '👨🏻'}</span> {t('dvp_preparing')}</>
              : <><span>{tool?.icon ?? '👨🏻'}</span> {t('dvp_prepare')}</>}
          </button>
        </div>

        {error && (
          <div className={`${c.danger} border rounded-lg p-4 flex items-start gap-2`}>
            <span>⚠️</span><p className="text-sm">{error}</p>
          </div>
        )}

        {/* Pre-result cross-ref (S5.5) */}
        <p className={`text-xs ${c.textMuted} text-center pt-2`}>
          {t('dvp_xref_pre')}{' '}
          <a href="/ProcedureProbe" className={linkStyle}>🔬 {t('dvp_procedure_probe')}</a>{' '}
          {t('dvp_xref_pre_suffix')}
        </p>
      </div>

      {/* ══════════ RESULTS ══════════ */}
      {results && (
        <div ref={resultsRef} className="space-y-4">
          {/* Save / saved feedback */}
          <div className="flex items-center justify-between flex-wrap gap-2">
            <div className="flex flex-wrap gap-2">
              <button
                onClick={saveCurrentPrep}
                className={`${c.btnSecondary} py-2 px-4 rounded-lg text-sm font-semibold border transition-colors`}
              >
                {t('dvp_save_history')}
              </button>
            </div>
          </div>

          {/* Red flags — top of results, conditional */}
          {results?.red_flag_symptoms_to_report?.length > 0 && (
            <Sec
              icon="🚨"
              title={t('dvp_sec_red_flags')}
              badge={`${results?.red_flag_symptoms_to_report?.length}`}
              open={secs.redFlags}
              onToggle={() => tog('redFlags')}
              c={c}
            >
              <div className={`${c.danger} border-2 rounded-lg p-4`}>
                <p className="text-xs mb-2 font-semibold">{t('dvp_red_flags_intro')}</p>
                {results?.red_flag_symptoms_to_report?.map((rf, i) => (
                  <p key={i} className="text-sm mb-1">• <BiText text={rf} c={c} /></p>
                ))}
              </div>
            </Sec>
          )}

          {/* Opening line */}
          {results?.opener && (
            <Sec icon="🎯" title={t('dvp_sec_opener')} open={secs.opener} onToggle={() => tog('opener')} c={c}>
              <div className={`${c.highlight} border rounded-lg p-4`}>
                <p className="text-[10px] font-bold mb-1">{t('dvp_say_first_minute')}</p>
                <p className={`text-base ${c.text} font-medium italic`}>"<BiText text={results?.opener} c={c} />"</p>
              </div>
              {results?.symptom_description_clinical && (
                <div className={`${c.cardAlt} border rounded-lg p-4 mt-3`}>
                  <p className="text-[10px] font-bold mb-1">{t('dvp_clinical_ready')}</p>
                  <p className={`text-sm ${c.textSecondary}`}><BiText text={results?.symptom_description_clinical} c={c} /></p>
                </div>
              )}
              {results?.goal_for_the_visit && (
                <div className={`${c.success} border rounded-lg p-4 mt-3`}>
                  <p className="text-[10px] font-bold mb-1">{t('dvp_your_goal')}</p>
                  <p className="text-sm">{results?.goal_for_the_visit}</p>
                </div>
              )}
            </Sec>
          )}

          {/* Prioritized questions */}
          {results?.prioritized_questions?.length > 0 && (
            <Sec
              icon="❓"
              title={t('dvp_sec_questions')}
              badge={`${results?.prioritized_questions?.length}`}
              open={secs.questions}
              onToggle={() => tog('questions')}
              c={c}
            >
              <div className="space-y-3">
                {results?.prioritized_questions?.map((q, i) => (
                  <div key={i} className={`${c.cardAlt} border rounded-lg p-4`}>
                    <div className="flex items-start gap-2 mb-2">
                      <span className="text-lg">{CATEGORY_ICON[q.category] || '❓'}</span>
                      <h4 className={`font-bold text-sm ${c.text} flex-1`}>
                        <BiText text={q.question} c={c} />
                      </h4>
                      {q.priority && (
                        <span className={`text-[10px] px-2 py-0.5 rounded font-bold ${PRIORITY_COLOR(q.priority, isDark)}`}>
                          {q.priority}
                        </span>
                      )}
                    </div>
                    {q.why_this_matters && (
                      <p className={`text-xs ${c.textMuted} ml-7`}>{t('dvp_why')} {q.why_this_matters}</p>
                    )}
                    {q.category && (
                      <p className={`text-[10px] ${c.textMuted} ml-7 mt-1 uppercase tracking-wide`}>{q.category}</p>
                    )}
                  </div>
                ))}
              </div>
            </Sec>
          )}

          {/* Things to mention even if not asked */}
          {results?.things_to_mention_even_if_not_asked?.length > 0 && (
            <Sec
              icon="💡"
              title={t('dvp_sec_mention')}
              badge={`${results?.things_to_mention_even_if_not_asked?.length}`}
              open={secs.mention}
              onToggle={() => tog('mention')}
              c={c}
            >
              <div className={`${c.warning} border rounded-lg p-4`}>
                {results?.things_to_mention_even_if_not_asked?.map((item, i) => (
                  <p key={i} className="text-sm mb-1">• <BiText text={item} c={c} /></p>
                ))}
              </div>
            </Sec>
          )}

          {/* If medication is prescribed */}
          {results?.questions_to_ask_if_medication_is_prescribed?.length > 0 && (
            <Sec
              icon="💊"
              title={t('dvp_sec_med_qs')}
              badge={`${results?.questions_to_ask_if_medication_is_prescribed?.length}`}
              open={secs.medQs}
              onToggle={() => tog('medQs')}
              c={c}
            >
              <div className={`${c.cardAlt} border rounded-lg p-4`}>
                {results?.questions_to_ask_if_medication_is_prescribed?.map((q, i) => (
                  <p key={i} className={`text-sm ${c.textSecondary} mb-1`}>❓ {q}</p>
                ))}
              </div>
            </Sec>
          )}

          {/* Pre-visit checklist */}
          {results?.pre_visit_checklist?.length > 0 && (
            <Sec
              icon="☑️"
              title={t('dvp_sec_checklist')}
              badge={`${results?.pre_visit_checklist?.length}`}
              open={secs.checklist}
              onToggle={() => tog('checklist')}
              c={c}
            >
              <div className={`${c.cardAlt} border rounded-lg p-4`}>
                {results?.pre_visit_checklist?.map((item, i) => (
                  <p key={i} className={`text-sm ${c.textSecondary} mb-1`}>☐ {item}</p>
                ))}
              </div>
            </Sec>
          )}

          {/* What to bring */}
          {results?.what_to_bring?.length > 0 && (
            <Sec
              icon="🎒"
              title={t('dvp_sec_bring')}
              badge={`${results?.what_to_bring?.length}`}
              open={secs.bring}
              onToggle={() => tog('bring')}
              c={c}
            >
              <div className={`${c.cardAlt} border rounded-lg p-4`}>
                {results?.what_to_bring?.map((item, i) => (
                  <p key={i} className={`text-sm ${c.textSecondary} mb-1`}>• {item}</p>
                ))}
              </div>
            </Sec>
          )}

          {/* Conversation tips */}
          {results?.conversation_tips?.length > 0 && (
            <Sec
              icon="🗣️"
              title={t('dvp_sec_tips')}
              open={secs.tips}
              onToggle={() => tog('tips')}
              c={c}
            >
              <div className={`${c.success} border rounded-lg p-4`}>
                {results?.conversation_tips?.map((tip, i) => (
                  <p key={i} className="text-sm mb-1">💬 {tip}</p>
                ))}
              </div>
            </Sec>
          )}

          <div className={`${c.warning} border-l-4 rounded-r-lg p-4 flex items-start gap-2`}>
            <span>⚠️</span>
            <p className="text-xs">
              <strong>{t('dvp_remember_label')}</strong> {t('dvp_remember_body')}
            </p>
          </div>

          {/* Post-result cross-ref (closes the loop with DVT) */}
          <p className={`text-xs ${c.textMuted} text-center`}>
            {t('dvp_xref_post')}{' '}
            <a href="/DoctorVisitTranslator" className={linkStyle}>🩺 {t('dvp_doctor_translator')}</a>.
          </p>
        </div>
      )}

      {/* Prep sessionHistory drawer (only when there are any) */}
      {prepHistory.length > 0 && (
        <div className={`${c.card} border ${c.border} rounded-xl p-5`}>
          <div className="flex items-center justify-between mb-3">
            <h3 className={`text-sm font-bold ${c.text}`}>{t('dvp_prep_history', { count: prepHistory.length })}</h3>
            <button
              onClick={() => { if (window.confirm(t('dvp_clear_confirm'))) setPrepHistory([]); }}
              className={`text-xs ${c.textMuted} ${c.deleteHover}`}
            >
              {t('dvp_clear')}
            </button>
          </div>
          <div className="space-y-2 max-h-72 overflow-y-auto">
            {prepHistory.map(p => (
              <div key={p.id} className={`${c.cardAlt} border rounded-lg p-3`}>
                <div className="flex items-center gap-2 mb-1">
                  <span className={`text-xs font-semibold ${c.text} flex-1 truncate`}>{p.label}</span>
                  <span className={`text-[10px] ${c.textMuted}`}>{p.date}</span>
                </div>
                <p className={`text-[10px] ${c.textMuted} mb-2`}>
                  {(() => { const at = APPT_TYPES.find(a => a.id === p.appointmentType); return at ? t(at.labelKey) : p.appointmentType; })()}
                </p>
                <div className="flex gap-2">
                  <button
                    onClick={() => loadSavedPrep(p)}
                    className={`${c.btnSecondary} text-xs px-3 py-1 rounded-lg`}
                  >
                    {t('dvp_load')}
                  </button>
                  <button
                    onClick={() => setPrepHistory(prev => prev.filter(e => e.id !== p.id))}
                    className={`text-xs ${c.textMuted} ${c.deleteHover} px-1`}
                  >
                    🗑️
                  </button>
                </div>
              </div>
            ))}
          </div>
        </div>
      )}


    </div>
  );
};

DoctorVisitPrep.displayName = 'DoctorVisitPrep';
export default DoctorVisitPrep;
