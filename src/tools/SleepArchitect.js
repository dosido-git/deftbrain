import React, { useState, useEffect, useRef, useCallback } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { usePersistentState } from '../hooks/usePersistentState';
import { useRegisterActions } from '../components/ActionBarContext';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const SLEEP_GOALS = [
  { id: 'fall_asleep',  label: 'Fall asleep faster',      icon: '😴' },
  { id: 'stay_asleep',  label: 'Stay asleep all night',   icon: '🌙' },
  { id: 'wake_rested',  label: 'Wake up feeling rested',  icon: '☀️' },
  { id: 'timing',       label: 'Fix my sleep schedule',   icon: '🕐' },
  { id: 'stress',       label: 'Sleep despite stress',    icon: '🧘' },
  { id: 'energy',       label: 'More daytime energy',     icon: '⚡' },
];

const SLEEP_DISRUPTORS = [
  { id: 'screens',      label: 'Screens before bed' },
  { id: 'caffeine',     label: 'Caffeine' },
  { id: 'alcohol',      label: 'Alcohol' },
  { id: 'noise',        label: 'Noise' },
  { id: 'light',        label: 'Light in room' },
  { id: 'temperature',  label: 'Temperature' },
  { id: 'stress',       label: 'Racing thoughts / stress' },
  { id: 'irregular',    label: 'Irregular schedule' },
  { id: 'partner',      label: 'Partner / pet disruptions' },
  { id: 'bathroom',     label: 'Waking to use bathroom' },
  { id: 'pain',         label: 'Pain or discomfort' },
  { id: 'unknown',      label: 'Not sure' },
];

const EXAMPLE = {
  goals: ['fall_asleep', 'wake_rested'],
  bedtime: '12:00 AM',
  wakeTime: '7:00 AM',
  hoursActual: '5',
  disruptors: ['screens', 'stress', 'caffeine'],
  freeform: "I lie in bed for at least an hour before falling asleep. My mind just races. I have coffee at 3pm most days. I scroll my phone in bed every night. Weekends I sleep until 10am which I think messes up my weekday schedule.",
};

function SleepArchitect({ tool }) {
  const { callToolEndpoint, loading } = useClaudeAPI();
  const { isDark } = useTheme();

  const c = {
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:         isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20'
                          : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-500' : 'text-gray-400',
    labelText:     isDark ? 'text-zinc-200' : 'text-gray-700',
    accentTxt:     isDark ? 'text-cyan-400' : 'text-cyan-600',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white'
                          : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200'
                          : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    pillActive:    isDark ? 'border-cyan-500 bg-cyan-900/30 text-cyan-200'
                          : 'border-cyan-600 bg-cyan-100 text-cyan-900',
    pillInactive:  isDark ? 'border-zinc-600 text-zinc-400 hover:border-zinc-500'
                          : 'border-gray-300 text-gray-500 hover:border-gray-400',
    required:      isDark ? 'text-amber-400' : 'text-amber-500',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200'   : 'bg-red-50 border-red-200 text-red-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200' : 'bg-amber-50 border-amber-300 text-amber-800',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200' : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    infoBox:       isDark ? 'bg-sky-900/20 border-sky-700 text-sky-200'   : 'bg-sky-50 border-sky-200 text-sky-800',
    indigo:        isDark ? 'bg-zinc-700/60 border-zinc-600 text-zinc-200' : 'bg-zinc-100 border-zinc-300 text-zinc-700',
  };
  c.textMuteded = c.textMuted;
  c.label = c.labelText;

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';

  const [goals, setGoals]             = useState([]);
  const [bedtime, setBedtime]         = useState('');
  const [wakeTime, setWakeTime]       = useState('');
  const [hoursActual, setHoursActual] = useState('');
  const [disruptors, setDisruptors]   = useState([]);
  const [freeform, setFreeform]       = useState('');
  const [results, setResults]         = usePersistentState('sleeparchitect-results', null);
  const [sessionHistory, setSessionHistory]         = usePersistentState('sleeparchitect-history', []);
  const [error, setError]             = useState('');

  const resultsRef    = useRef(null);
  const handleBuildRef = useRef(null);
  const canSubmitRef  = useRef(false);

  const canSubmit = goals.length > 0 || freeform.trim().length >= 20;

  const handleReset = useCallback(() => {
    setResults(null);
    setError('');
    setGoals([]);
    setBedtime('');
    setWakeTime('');
    setHoursActual('');
    setDisruptors([]);
    setFreeform('');
  }, [setResults]);

  const loadExample = useCallback(() => {
    setGoals(EXAMPLE.goals);
    setBedtime(EXAMPLE.bedtime);
    setWakeTime(EXAMPLE.wakeTime);
    setHoursActual(EXAMPLE.hoursActual);
    setDisruptors(EXAMPLE.disruptors);
    setFreeform(EXAMPLE.freeform);
    setResults(null);
    setError('');
  }, [setResults]);

  const toggleGoal = useCallback(id =>
    setGoals(prev => prev.includes(id) ? prev.filter(g => g !== id) : [...prev, id]), []);

  const toggleDisruptor = useCallback(id =>
    setDisruptors(prev => prev.includes(id) ? prev.filter(d => d !== id) : [...prev, id]), []);

  const handleBuild = useCallback(async () => {
    if (!canSubmit || loading) return;
    setError('');
    setResults(null);

    try {
      const parsed = await callToolEndpoint('sleep-architect/stream', {
        goals,
        bedtime: bedtime || null,
        wakeTime: wakeTime || null,
        hoursActual: hoursActual || null,
        disruptors,
        freeform: freeform.trim() || null,
      });
      setResults(parsed);
      setSessionHistory(prev => [{
        preview: goals.map(g => SLEEP_GOALS.find(sg => sg.id === g)?.label).filter(Boolean).join(', ') || freeform.slice(0, 60),
        score: parsed.sleep_score ?? null,
        ts: Date.now(),
      }, ...prev].slice(0, 6));
    } catch (err) {
      setError(err.message || 'Something went wrong. Please try again.');
    }
  }, [canSubmit, loading, goals, bedtime, wakeTime, hoursActual, disruptors, freeform, callToolEndpoint, setResults, setSessionHistory]);

  handleBuildRef.current = handleBuild;
  canSubmitRef.current   = canSubmit;

  const buildFullText = useCallback(() => {
    if (!results) return '';
    const lines = ['SLEEP ARCHITECT PROTOCOL', ''];
    if (results.sleep_score != null) lines.push(`Sleep health score: ${results.sleep_score}/10`);
    if (results.diagnosis) lines.push(`\n${results.diagnosis}`);
    if (results.key_issues?.length) {
      lines.push('\nKEY ISSUES');
      results.key_issues.forEach(i => lines.push(`• ${i}`));
    }
    if (results.protocol?.length) {
      lines.push('\nYOUR PROTOCOL');
      results.protocol.forEach(p => {
        lines.push(`\n[${p.phase?.toUpperCase()}] ${p.title}`);
        lines.push(p.description);
        p.actions?.forEach(a => lines.push(`  → ${a}`));
      });
    }
    if (results.schedule) {
      lines.push('\nTARGET SCHEDULE');
      if (results.schedule.bedtime) lines.push(`Bedtime: ${results.schedule.bedtime}`);
      if (results.schedule.wake_time) lines.push(`Wake time: ${results.schedule.wake_time}`);
      if (results.schedule.wind_down_start) lines.push(`Wind-down start: ${results.schedule.wind_down_start}`);
    }
    if (results.quick_wins?.length) {
      lines.push('\nQUICK WINS (start tonight)');
      results.quick_wins.forEach(w => lines.push(`• ${w}`));
    }
    lines.push(BRAND);
    return lines.join('\n');
  }, [results]);

  useRegisterActions(buildFullText(), tool?.title || 'SleepArchitect');

  useEffect(() => {
    if (!results || !resultsRef.current) return;
    const t = setTimeout(() => resultsRef.current?.scrollIntoView({ behavior: 'smooth', block: 'start' }), 200);
    return () => clearTimeout(t);
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [results]);

  useEffect(() => {
    const handler = (e) => {
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && !loading && canSubmitRef.current)
        handleBuildRef.current?.();
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [loading]);

  const scoreColor = (score) => {
    if (score >= 8) return isDark ? 'text-emerald-400' : 'text-emerald-600';
    if (score >= 6) return isDark ? 'text-amber-400' : 'text-amber-600';
    return isDark ? 'text-red-400' : 'text-red-600';
  };

  const phaseConfig = {
    immediate: { label: 'Start tonight',     bg: c.success,  icon: '⚡' },
    week1:     { label: 'This week',          bg: c.infoBox,  icon: '📅' },
    ongoing:   { label: 'Ongoing habits',     bg: c.indigo,   icon: '🔄' },
    environment: { label: 'Your environment', bg: c.warning,  icon: '🏠' },
  };

  const renderInput = () => (
    <div className={`${c.card} border ${c.border} rounded-xl shadow-sm p-5 space-y-5`}>

      {/* Sleep goals */}
      <div>
        <label className={`block text-sm font-medium ${c.labelText} mb-2`}>
          What's your biggest sleep struggle? <span className={c.required}>*</span>
        </label>
        <div className="flex flex-wrap gap-2">
          {SLEEP_GOALS.map(g => (
            <button key={g.id} onClick={() => toggleGoal(g.id)}
              className={`px-3 py-1.5 rounded-lg border text-xs font-medium transition-colors ${goals.includes(g.id) ? c.pillActive : c.pillInactive}`}>
              <span className="mr-1">{g.icon}</span>{g.label}
            </button>
          ))}
        </div>
      </div>

      {/* Times */}
      <div className="grid grid-cols-3 gap-3">
        <div>
          <label className={`block text-xs font-medium ${c.labelText} mb-1`}>Usual bedtime</label>
          <input type="text" value={bedtime} onChange={e => setBedtime(e.target.value)}
            placeholder="e.g. 11:30 PM"
            className={`w-full px-3 py-2 border rounded-xl text-sm focus:outline-none focus:ring-2 ${c.input}`} />
        </div>
        <div>
          <label className={`block text-xs font-medium ${c.labelText} mb-1`}>Wake time</label>
          <input type="text" value={wakeTime} onChange={e => setWakeTime(e.target.value)}
            placeholder="e.g. 7:00 AM"
            className={`w-full px-3 py-2 border rounded-xl text-sm focus:outline-none focus:ring-2 ${c.input}`} />
        </div>
        <div>
          <label className={`block text-xs font-medium ${c.labelText} mb-1`}>Actual hours slept</label>
          <input type="text" value={hoursActual} onChange={e => setHoursActual(e.target.value)}
            placeholder="e.g. 5.5"
            className={`w-full px-3 py-2 border rounded-xl text-sm focus:outline-none focus:ring-2 ${c.input}`} />
        </div>
      </div>

      {/* Disruptors */}
      <div>
        <label className={`block text-sm font-medium ${c.labelText} mb-2`}>Known disruptors <span className={`text-xs font-normal ${c.textMuted}`}>(select all that apply)</span></label>
        <div className="flex flex-wrap gap-2">
          {SLEEP_DISRUPTORS.map(d => (
            <button key={d.id} onClick={() => toggleDisruptor(d.id)}
              className={`px-3 py-1.5 rounded-lg border text-xs font-medium transition-colors ${disruptors.includes(d.id) ? c.pillActive : c.pillInactive}`}>
              {d.label}
            </button>
          ))}
        </div>
      </div>

      {/* Freeform */}
      <div>
        <label className={`block text-sm font-medium ${c.labelText} mb-1`}>
          Tell us more <span className={`text-xs font-normal ${c.textMuted}`}>(optional but helpful)</span>
        </label>
        <textarea value={freeform} onChange={e => setFreeform(e.target.value)}
          placeholder="Describe your sleep situation — what you've tried, what a typical night looks like, what time you feel naturally sleepy, anything else relevant…"
          rows={4}
          className={`w-full px-3 py-2.5 border rounded-xl text-sm resize-y focus:outline-none focus:ring-2 ${c.input}`} />
      </div>

      {error && <p className={`text-sm ${c.danger} border rounded-lg px-3 py-2`}>{error}</p>}

      <p className={`text-xs ${c.textMuted}`}>
        Stressed and can't switch off? <a href="/SpiralStopper" className={linkStyle}>🌀 Spiral Stopper</a> can help calm a racing mind before bed.
      </p>

      <button onClick={handleBuild} disabled={!canSubmit || loading}
        className={`w-full ${c.btnPrimary} disabled:opacity-40 font-bold py-3 rounded-lg flex items-center justify-center gap-2 min-h-[48px]`}>
        {loading
          ? <><span className="inline-block animate-spin">{tool?.icon ?? '😴'}</span> Building your protocol…</>
          : <><span className="mr-1">{tool?.icon ?? '😴'}</span> Build My Sleep Protocol</>}
      </button>

      {sessionHistory.length > 0 && (
        <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
          <div className="flex items-center justify-between mb-3">
            <p className={`text-xs font-bold ${c.textMuted} uppercase tracking-wide`}>🕓 Previous protocols</p>
            <button onClick={() => setSessionHistory([])} className={`text-xs ${c.textMuted}`}>Clear</button>
          </div>
          <ul className="space-y-1.5">
            {sessionHistory.map((h, i) => (
              <li key={i} className={`text-xs ${c.textSecondary} flex items-center gap-2`}>
                {h.score != null && <span className={`font-bold ${scoreColor(h.score)}`}>{h.score}/10</span>}
                <span className={c.textMuted}>{h.preview}</span>
              </li>
            ))}
          </ul>
        </div>
      )}
    </div>
  );

  const renderResults = () => {
    if (!results) return null;
    return (
      <div className="space-y-4" ref={resultsRef}>

        {/* Score + diagnosis */}
        <div className={`${c.card} border ${c.border} rounded-xl p-5`}>
          <div className="flex items-start gap-4 mb-3">
            {results.sleep_score != null && (
              <div className="text-center flex-shrink-0">
                <p className={`text-4xl font-black ${scoreColor(results.sleep_score)}`}>{results.sleep_score}</p>
                <p className={`text-[10px] font-bold ${c.textMuted} uppercase`}>/ 10</p>
              </div>
            )}
            <div className="flex-1">
              {results?.diagnosis && <p className={`text-sm font-medium ${c.text}`}>{results.diagnosis}</p>}
            </div>
          </div>
          {results?.key_issues?.length > 0 && (
            <ul className="space-y-1.5 mt-3">
              {results?.key_issues?.map((issue, i) => (
                <li key={i} className={`text-sm ${c.warning} border rounded-lg px-3 py-2 flex gap-2`}>
                  <span className="flex-shrink-0">⚠️</span><span>{issue}</span>
                </li>
              ))}
            </ul>
          )}
        </div>

        {/* Quick wins */}
        {results?.quick_wins?.length > 0 && (
          <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
            <p className={`text-xs font-bold ${c.textMuted} uppercase tracking-wide mb-3`}>⚡ Quick wins — start tonight</p>
            <ul className="space-y-2">
              {results.quick_wins.map((w, i) => (
                <li key={i} className={`text-sm ${c.success} border rounded-lg px-3 py-2 flex gap-2`}>
                  <span className="flex-shrink-0 font-bold">{i + 1}.</span><span>{w}</span>
                </li>
              ))}
            </ul>
          </div>
        )}

        {/* Protocol steps */}
        {results.protocol?.length > 0 && (
          <div className="space-y-3">
            <p className={`text-xs font-bold ${c.textMuted} uppercase tracking-wide px-1`}>📋 Your full protocol</p>
            {results.protocol.map((step, i) => {
              const cfg = phaseConfig[step.phase] ?? { label: step.phase, bg: c.infoBox, icon: '•' };
              return (
                <div key={i} className={`${c.card} border ${c.border} rounded-xl p-4`}>
                  <div className="flex items-start gap-3">
                    <span className="text-xl flex-shrink-0">{cfg.icon}</span>
                    <div className="flex-1">
                      <div className="flex items-center gap-2 flex-wrap mb-1">
                        <span className={`font-bold text-sm ${c.text}`}>{step.title}</span>
                        <span className={`text-[10px] font-bold border px-2 py-0.5 rounded-full ${cfg.bg}`}>{cfg.label}</span>
                      </div>
                      {step.description && <p className={`text-xs ${c.textSecondary} mb-2`}>{step.description}</p>}
                      {step.actions?.length > 0 && (
                        <ul className="space-y-1">
                          {step.actions.map((a, j) => (
                            <li key={j} className={`text-xs ${c.textSecondary} flex gap-1.5`}>
                              <span className={`flex-shrink-0 ${c.accentTxt}`}>→</span><span>{a}</span>
                            </li>
                          ))}
                        </ul>
                      )}
                    </div>
                  </div>
                </div>
              );
            })}
          </div>
        )}

        {/* Target schedule */}
        {results.schedule && (
          <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
            <p className={`text-xs font-bold ${c.textMuted} uppercase tracking-wide mb-3`}>🕐 Target schedule</p>
            <div className="grid grid-cols-3 gap-3">
              {results.schedule.wind_down_start && (
                <div className={`${c.cardAlt} border ${c.border} rounded-lg p-3 text-center`}>
                  <p className={`text-[10px] font-bold ${c.textMuted} uppercase mb-1`}>Wind-down</p>
                  <p className={`text-sm font-bold ${c.text}`}>{results.schedule.wind_down_start}</p>
                </div>
              )}
              {results.schedule.bedtime && (
                <div className={`${c.cardAlt} border ${c.border} rounded-lg p-3 text-center`}>
                  <p className={`text-[10px] font-bold ${c.textMuted} uppercase mb-1`}>Lights out</p>
                  <p className={`text-sm font-bold ${c.text}`}>{results.schedule.bedtime}</p>
                </div>
              )}
              {results.schedule.wake_time && (
                <div className={`${c.cardAlt} border ${c.border} rounded-lg p-3 text-center`}>
                  <p className={`text-[10px] font-bold ${c.textMuted} uppercase mb-1`}>Wake up</p>
                  <p className={`text-sm font-bold ${c.text}`}>{results.schedule.wake_time}</p>
                </div>
              )}
            </div>
            {results.schedule.note && <p className={`text-xs ${c.textMuted} mt-2`}>{results.schedule.note}</p>}
          </div>
        )}

        {/* Post-result cross-refs */}
        <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
          <p className={`text-[10px] font-bold ${c.textMuted} uppercase mb-2`}>🔗 Related tools</p>
          <div className="flex flex-wrap gap-3">
            <a href="/SpiralStopper" className={`text-xs ${linkStyle}`}>🌀 Spiral Stopper</a>
            <a href="/RechargeRadar" className={`text-xs ${linkStyle}`}>🔋 Recharge Radar</a>
            <a href="/FocusPocus" className={`text-xs ${linkStyle}`}>🎯 Focus Pocus</a>
          </div>
        </div>
      </div>
    );
  };

  return (
    <div className={`space-y-4 ${c.text}`}>
      <div className={`${c.card} border ${c.border} rounded-xl shadow-sm p-5`}>
        <div className="flex items-start justify-between">
          <div>
            <h2 className={`text-xl font-bold ${c.text}`}>
              <span className="mr-2">{tool?.icon ?? '😴'}</span>{tool?.title ?? 'SleepArchitect'}
            </h2>
            <p className={`text-sm ${c.textSecondary}`}>{tool?.tagline ?? 'A personalized sleep improvement protocol built around your habits.'}</p>
            <button onClick={loadExample} disabled={loading} style={{ backgroundColor: (tool?.headerColor ?? '#888888') + '80' }} className={`mt-1.5 px-2.5 py-0.5 rounded-full text-xs font-medium border disabled:opacity-40 ${isDark ? 'text-white border-white/40' : 'text-gray-800 border-transparent'}`}>Try example</button>
          </div>
          <button
            onClick={handleReset}
            style={{ visibility: (results || goals.length || freeform.trim()) ? 'visible' : 'hidden' }}
            className={`${c.btnSecondary} px-3 py-1.5 rounded-lg text-xs font-bold`}
          >
            ↺ Start over
          </button>
        </div>
      </div>
      {!results && <p className={`text-xs ${c.textMuted} px-1`}>Stressed and can't switch off? <a href="/SpiralStopper" className={linkStyle}>🌀 Spiral Stopper</a> can help calm a racing mind before bed.</p>}
      {!results && renderInput()}
      {results && renderResults()}
    </div>
  );
}

SleepArchitect.displayName = 'SleepArchitect';
export default SleepArchitect;
