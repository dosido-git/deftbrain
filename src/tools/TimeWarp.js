import React, { useState, useCallback, useEffect } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { usePersistentState } from '../hooks/usePersistentState';
import { useRegisterActions } from '../components/ActionBarContext';
import { useTranslation } from '../i18n/useTranslation';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

// ════════════════════════════════════════════════════════════
// CONSTANTS
// ════════════════════════════════════════════════════════════
// label/desc resolved at render via t(); icon + value are static.
const FORMATS = [
  { value: 'explain', icon: '🗣️', labelKey: 'tw_fmt_explain', descKey: 'tw_fmt_explain_desc' },
  { value: 'review', icon: '⭐', labelKey: 'tw_fmt_review', descKey: 'tw_fmt_review_desc' },
  { value: 'news', icon: '📰', labelKey: 'tw_fmt_news', descKey: 'tw_fmt_news_desc' },
  { value: 'letter', icon: '✉️', labelKey: 'tw_fmt_letter', descKey: 'tw_fmt_letter_desc' },
  { value: 'debate', icon: '⚖️', labelKey: 'tw_fmt_debate', descKey: 'tw_fmt_debate_desc' },
  { value: 'ad', icon: '📢', labelKey: 'tw_fmt_ad', descKey: 'tw_fmt_ad_desc' },
];

// modern: invariant brand name OR a key (modernKey) for generic terms.
// period is always localized via periodKey. The combo `modern`/`period`
// VALUES sent to the backend use the localized display strings.
const QUICK_COMBOS = [
  { modern: 'TikTok', periodKey: 'tw_period_victorian', icon: '🎭' },
  { modern: 'Uber', periodKey: 'tw_period_1920s_ny', icon: '🚕' },
  { modern: 'Tinder', periodKey: 'tw_period_medieval_eu', icon: '⚔️' },
  { modernKey: 'tw_combo_roomba', periodKey: 'tw_period_ancient_rome', icon: '🏛️' },
  { modernKey: 'tw_combo_wifi', periodKey: 'tw_period_wild_west', icon: '🤠' },
  { modern: 'Instagram', periodKey: 'tw_period_renaissance', icon: '🎨' },
  { modern: 'Amazon Prime', periodKey: 'tw_period_silk_road', icon: '🐪' },
  { modernKey: 'tw_combo_podcasts', periodKey: 'tw_period_ancient_greece', icon: '🏺' },
];

// ════════════════════════════════════════════════════════════
// COMPONENT
// ════════════════════════════════════════════════════════════
const EXAMPLES = [
  {
    modernThing: 'Influencer marketing and brand deals',
    historicalPeriod: 'Ancient Rome',
    format: 'news',
  },
  {
    modernThing: 'Zoom meetings',
    historicalPeriod: 'Victorian England',
    format: 'letter',
  },
  {
    modernThing: 'Online dating apps',
    historicalPeriod: 'Medieval Europe',
    format: 'ad',
  },
];
const TimeWarp = ({ tool }) => {
  const { callToolEndpoint, loading, userLocale, userCurrency, userRegion } = useClaudeAPI();
  const { isDark } = useTheme();
  const { t } = useTranslation();

  const c = {
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:         isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder:text-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20' : 'bg-white border-gray-300 text-gray-900 placeholder:text-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-500' : 'text-gray-400',
    required:      isDark ? 'text-amber-400' : 'text-amber-500',
    labelText:     isDark ? 'text-zinc-200' : 'text-gray-700',
    accentTxt:     isDark ? 'text-amber-400' : 'text-amber-700',
    accentBox:     isDark ? 'bg-amber-900/20 border-amber-700' : 'bg-amber-50 border-amber-300',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white' : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-100' : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200' : 'bg-red-50 border-red-200 text-red-800',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200' : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200' : 'bg-amber-50 border-amber-300 text-amber-800',
    pillActive:    isDark ? 'bg-cyan-600 border-cyan-600 text-white' : 'bg-cyan-600 border-cyan-600 text-white',
    pillInactive:  isDark ? 'bg-zinc-800 border-zinc-600 text-zinc-300 hover:border-zinc-500' : 'bg-white border-gray-200 text-gray-500 hover:border-gray-300',
    quoteBg:       isDark ? 'bg-zinc-900/60' : 'bg-slate-50',
    infoBox:       isDark ? 'bg-sky-900/20 border-sky-700 text-sky-200' : 'bg-sky-50 border-sky-200 text-sky-800',
  };

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';

  // ── State ──
  const [modernThing, setModernThing] = useState('');
  const [historicalPeriod, setHistoricalPeriod] = useState('');
  const [format, setFormat] = useState('explain');
  const [results, setResults] = usePersistentState('time-warp-result', null);
  const [sessionHistory, setSessionHistory] = usePersistentState('time-warp-history', []);

  const [error, setError] = useState('');

  // ── API ──
  const loadExample = () => {
    const ex = EXAMPLES[Math.floor(Math.random() * EXAMPLES.length)];
    setModernThing(ex.modernThing);
    setHistoricalPeriod(ex.historicalPeriod);
    setFormat(ex.format);
  };

  const runWarp = useCallback(async () => {
    if (!modernThing.trim() && !historicalPeriod.trim()) return;
    setError('');
    setResults(null);
    try {
      const data = await callToolEndpoint('time-warp', {
        modernThing: modernThing.trim(),
        historicalPeriod: historicalPeriod.trim(),
        format,
        seed: Date.now(),
        userLocale,
        userCurrency,
        userRegion,
      });
      setResults(data);
      setSessionHistory(prev => [{
        id: 'tw_' + Date.now(),
        date: new Date().toISOString(),
        // PF-25 exception: 40-char preview-text truncation; session history is capped at 6.
        preview: (modernThing + ' in ' + historicalPeriod).slice(0, 40),
        format,
      }, ...prev].slice(0, 6));
    } catch (err) {
      setError(err.message || t('tw_copy_failed'));
    }
  }, [modernThing, historicalPeriod, format, callToolEndpoint, userLocale, userCurrency, userRegion, t, setResults, setSessionHistory]);

  const applyCombo = (combo) => {
    setModernThing(combo.modernKey ? t(combo.modernKey) : combo.modern);
    setHistoricalPeriod(t(combo.periodKey));
    setResults(null);
  };

  // ── Copy text builder ──
  const buildFullText = useCallback(() => {
    if (!results) return '';
    const lines = [`⏰ ${t('tw_copy_header')}: ${results?.title || ''}`, ''];
    if (results?.era_context) lines.push(`📜 ${results?.era_context}`, '');
    if (results?.main_content) lines.push(results?.main_content, '');
    if (results?.historical_footnotes?.length) {
      lines.push(`📚 ${t('tw_copy_didyouknow')}`);
      results?.historical_footnotes?.forEach(f => lines.push(`  • ${f}`));
      lines.push('');
    }
    if (results?.anachronism_alert) lines.push(`💥 ${results?.anachronism_alert}`);
    return lines.join('\n') + BRAND;
  }, [results, t]);

  useRegisterActions(buildFullText(), tool?.title);

  useEffect(() => {
    const handler = (e) => {
      if ((e.metaKey || e.ctrlKey) && e.key === 'Enter') {
        if (modernThing.trim() || historicalPeriod.trim()) runWarp();
      }
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
  }, [runWarp, modernThing, historicalPeriod]);

  // RENDER
  // ════════════════════════════════════════════════════════════
  return (
    <div className={`space-y-4 ${c.text}`}>
      {/* Input */}
      <div className={`${c.card} border rounded-xl p-5`}>
        <div className={`mb-4 pb-3 border-b border-zinc-500`}>
          <h2 className={`text-xl font-bold ${c.text}`}>
            <span className="mr-2">{tool?.icon ?? '⏰'}</span>{tool?.title ?? t('tw_title')}
          </h2>
          <p className={`text-sm ${c.textSecondary}`}>{tool?.tagline ?? t('tw_tagline')}</p>
          <button onClick={loadExample} disabled={loading} style={{ backgroundColor: (tool?.headerColor ?? '#888888') + '80' }} className={`mt-1.5 px-2.5 py-0.5 rounded-full text-xs font-medium border disabled:opacity-40 ${isDark ? 'text-white border-white/40' : 'text-gray-800 border-transparent'}`}>{t('try_example')}</button>
        </div>

        {/* Quick combos */}
        <div className="mb-4">
          <div className="flex items-center justify-between mb-2">
            <label className={`text-[10px] font-bold ${c.labelText} uppercase`}>{t('tw_quick_combos')}</label>
            <button onClick={() => applyCombo(QUICK_COMBOS[Math.floor(Math.random() * QUICK_COMBOS.length)])}
              className={`${c.btnSecondary} px-2.5 py-1 rounded-lg text-[10px] font-bold`}>
              {t('tw_shuffle')}
            </button>
          </div>
          <div className="flex flex-wrap gap-1.5">
            {QUICK_COMBOS.map((combo, i) => (
              <button key={i} onClick={() => applyCombo(combo)}
                className={`${c.btnSecondary} px-2.5 py-1.5 rounded-lg text-[11px] font-medium min-h-[32px] flex items-center gap-1`}>
                <span>{combo.icon}</span> {combo.modernKey ? t(combo.modernKey) : combo.modern} × {t(combo.periodKey)}
              </button>
            ))}
          </div>
        </div>

        {/* Inputs */}
        <p className={`text-[10px] ${c.textMuted} mb-1.5`}>{t('tw_fill_hint')}</p>
        <div className="grid grid-cols-1 sm:grid-cols-2 gap-3 mb-4">
          <div>
            <label className={`text-xs font-bold ${c.labelText} block mb-1.5`}>{t('tw_modern_label')} <span className={c.required}>*</span></label>
            <input type="text" value={modernThing} onChange={e => setModernThing(e.target.value)}
              placeholder={t('tw_modern_ph')}
              className={`w-full px-3 py-2.5 border rounded-lg text-sm ${c.input} outline-none focus:ring-2`} />
          </div>
          <div>
            <label className={`text-xs font-bold ${c.labelText} block mb-1.5`}>{t('tw_period_label')} <span className={c.required}>*</span></label>
            <input type="text" value={historicalPeriod} onChange={e => setHistoricalPeriod(e.target.value)}
              placeholder={t('tw_period_ph')}
              className={`w-full px-3 py-2.5 border rounded-lg text-sm ${c.input} outline-none focus:ring-2`} />
          </div>
        </div>

        {/* Format */}
        <div className="mb-5">
          <label className={`text-xs font-bold ${c.labelText} uppercase block mb-2`}>{t('tw_format_label')}</label>
          <div className="grid grid-cols-2 sm:grid-cols-3 gap-2">
            {FORMATS.map(f => (
              <button key={f.value} onClick={() => setFormat(f.value)}
                className={`py-2.5 px-3 rounded-xl border text-left transition-colors min-h-[52px] ${
                  format === f.value ? c.pillActive : c.pillInactive
                }`}>
                <span className="text-sm block">{f.icon} {t(f.labelKey)}</span>
                <span className={`text-[9px] ${format === f.value ? 'opacity-80' : c.textMuted} block`}>{t(f.descKey)}</span>
              </button>
            ))}
          </div>
        </div>

        <p className={`text-[11px] ${c.textMuted} mb-2 text-center`}>
          {t('tw_xref')} <a href="/WrongAnswersOnly" className={linkStyle}>🎭 {t('tw_xref_wao')}</a> {t('tw_xref_desc')}
        </p>

        <button onClick={runWarp} disabled={(!modernThing.trim() && !historicalPeriod.trim()) || loading}
          className={`w-full ${c.btnPrimary} disabled:opacity-40 font-bold py-3 rounded-lg flex items-center justify-center gap-2 min-h-[48px]`}>
          {loading
            ? <><span className="inline-block animate-spin">{tool?.icon ?? '⏰'}</span> {t('tw_warping')}</>
            : <><span>{tool?.icon ?? '⏰'}</span> {t('tw_warp_it')}</>}
        </button>
      </div>

      {/* Error */}
      {error && (
        <div className={`${c.danger} border rounded-lg p-4 flex items-start gap-3`}>
          <span>⚠️</span><p className="text-sm">{error}</p>
        </div>
      )}

      {/* Results */}
      {results && (
        <div className="space-y-4">
          {/* Title + era context */}
          {results?.title && (
            <div className={`${c.accentBox} border-2 rounded-xl p-5 text-center`}>
              <span className="text-3xl block mb-2">⏰</span>
              <p className={`text-lg font-black ${c.text}`}>{results?.title}</p>
              {results?.era_context && (
                <p className={`text-xs ${c.textMuted} mt-2 italic`}>📜 {results?.era_context}</p>
              )}
            </div>
          )}

          {/* Main content */}
          {results?.main_content && (
            <div className={`${c.card} border rounded-xl p-5`}>
              <p className={`text-sm ${c.textSecondary} whitespace-pre-line leading-relaxed`}>{results?.main_content}</p>
            </div>
          )}

          {/* Historical footnotes */}
          {results?.historical_footnotes?.length > 0 && (
            <div className={`${c.infoBox} border rounded-xl p-4`}>
              <p className={`text-[10px] font-bold uppercase mb-2`}>📚 {t('tw_footnotes_title')}</p>
              <div className="space-y-1.5">
                {results?.historical_footnotes?.map((f, i) => (
                  <p key={i} className="text-xs">• {f}</p>
                ))}
              </div>
            </div>
          )}

          {/* Anachronism alert + flip it */}
          <div className="grid grid-cols-1 sm:grid-cols-2 gap-3">
            {results?.anachronism_alert && (
              <div className={`${c.quoteBg} border ${c.border} rounded-xl p-4`}>
                <p className={`text-[10px] font-bold ${c.accentTxt} uppercase mb-1`}>💥 {t('tw_best_moment')}</p>
                <p className={`text-xs ${c.textSecondary}`}>{results?.anachronism_alert}</p>
              </div>
            )}
            {results?.flip_it && (
              <div className={`${c.quoteBg} border ${c.border} rounded-xl p-4`}>
                <p className={`text-[10px] font-bold ${c.accentTxt} uppercase mb-1`}>🔀 {t('tw_flip_it')}</p>
                <p className={`text-xs ${c.textSecondary}`}>{results?.flip_it}</p>
              </div>
            )}
          </div>

          {/* Warp again */}
          <div className="flex gap-2">
            <button onClick={runWarp} disabled={loading}
              className={`flex-1 ${c.btnSecondary} disabled:opacity-40 font-bold py-3 rounded-lg min-h-[44px]`}>
              {t('tw_same_combo')}
            </button>
            <button onClick={() => { setModernThing(''); setHistoricalPeriod(''); setResults(null); }}
              className={`${c.btnSecondary} font-bold py-3 px-4 rounded-lg min-h-[44px]`}>
              {t('tw_new_warp')}
            </button>
          </div>
        </div>
      )}

      {/* Session sessionHistory */}
      {/* eslint-disable-next-line no-restricted-globals */}
      {sessionHistory.length > 0 && (
        <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4 mt-4`}>
          <p className={`text-xs font-bold ${c.textMuted} mb-2`}>📋 {t('tw_recent')}</p>
          <div className="space-y-1">
            {/* eslint-disable-next-line no-restricted-globals */}

            {sessionHistory.map(s => (
              <div key={s.id} className="flex items-center justify-between">
                <span className={`text-xs ${c.textSecondary} truncate`}>{s.preview}</span>
                <span className={`text-xs ${c.textMuted} ml-2 shrink-0`}>{s.format}</span>
              </div>
            ))}
          </div>
        </div>
      )}

      {/* Post-result cross-tool links */}
      {results && (
        <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4 mt-4`}>
          <p className={`text-xs font-bold ${c.textMuted} mb-2`}>{t('tw_next_step')}</p>
          <div className="flex flex-wrap gap-3">
            <a href="/WrongAnswersOnly" className={`text-xs ${linkStyle}`}>🎭 {t('tw_link_wao')}</a>
            <a href="/WhatIf" className={`text-xs ${linkStyle}`}>🤔 {t('tw_link_whatif')}</a>
            <a href="/PlotTwist" className={`text-xs ${linkStyle}`}>🌀 {t('tw_link_plottwist')}</a>
          </div>
        </div>
      )}
    </div>
  );
};

TimeWarp.displayName = 'TimeWarp';
export default TimeWarp;
