import React, { useState, useEffect, useCallback } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { usePersistentState } from '../hooks/usePersistentState';
import { useRegisterActions } from '../components/ActionBarContext';
import { useTranslation } from '../i18n/useTranslation';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';


const EXAMPLE = {
  mode: 'digest',
  paperTitle: 'Attention Is All You Need',
  field: 'machine learning / NLP',
  paperText: "We propose a new simple network architecture, the Transformer, based solely on attention mechanisms, dispensing with recurrence and convolutions entirely. Experiments on two machine translation tasks show these models to be superior in quality while being more parallelizable and requiring significantly less time to train. Our model achieves 28.4 BLEU on the WMT 2014 English-to-German translation task, improving over the existing best results, including ensembles, by over 2 BLEU. On the WMT 2014 English-to-French translation task, our model establishes a new single-model state-of-the-art BLEU score of 41.8 after training for 3.5 days on eight GPUs, a small fraction of the training costs of the best models from the literature.",
};

const ResearchDecoder = ({ tool }) => {
  const { callToolEndpoint, loading, userLocale, userCurrency, userRegion } = useClaudeAPI();
  const { isDark } = useTheme();
  const { t } = useTranslation();

  const FIELDS = [
    { id: 'medicine', icon: '🏥', label: t('rd_field_medicine') }, { id: 'psychology', icon: '🧠', label: t('rd_field_psychology') },
    { id: 'nutrition', icon: '🥗', label: t('rd_field_nutrition') }, { id: 'environment', icon: '🌍', label: t('rd_field_environment') },
    { id: 'tech', icon: '💻', label: t('rd_field_tech') }, { id: 'economics', icon: '📈', label: t('rd_field_economics') },
    { id: 'biology', icon: '🧬', label: t('rd_field_biology') }, { id: 'neuroscience', icon: '🧪', label: t('rd_field_neuroscience') },
    { id: 'education', icon: '📚', label: t('rd_field_education') }, { id: 'sociology', icon: '👥', label: t('rd_field_sociology') },
    { id: 'other', icon: '📄', label: t('rd_field_other') },
  ];

  const c = {
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:         isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20'
                          : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-500' : 'text-gray-400',
    labelText:     isDark ? 'text-zinc-200' : 'text-gray-700',
    accentTxt:     isDark ? 'text-cyan-400' : 'text-cyan-600',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white'
                          : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200'
                          : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200'
                          : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200'
                          : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200'
                          : 'bg-red-50 border-red-200 text-red-800',
    infoBox:       isDark ? 'bg-sky-900/20 border-sky-700 text-sky-200'
                          : 'bg-sky-50 border-sky-200 text-sky-800',
    successBox:    isDark ? 'bg-emerald-900/20 border-emerald-700' : 'bg-emerald-50 border-emerald-300',
    successTxt:    isDark ? 'text-emerald-300' : 'text-emerald-800',
    warningBox:    isDark ? 'bg-amber-900/20 border-amber-700' : 'bg-amber-50 border-amber-300',
    pillActive:    isDark ? 'border-cyan-500 bg-cyan-900/30 text-cyan-200'
                          : 'border-cyan-600 bg-cyan-100 text-cyan-900',
    pillInactive:  isDark ? 'border-zinc-600 text-zinc-400 hover:border-zinc-500'
                          : 'border-gray-300 text-gray-500 hover:border-gray-400',
    required:      isDark ? 'text-amber-400' : 'text-amber-500',
  };
  c.textMuteded = c.textMuted;
  c.label = c.labelText;

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';

  // ─── Mode ───
  const [mode, setMode] = useState('digest');
  const [error, setError] = useState('');

  // ─── Digest inputs ───
  const [paperText, setPaperText] = useState('');
  const [paperTitle, setPaperTitle] = useState('');
  const [field, setField] = useState('');
  const [digestResults, setDigestResults] = useState(null);

  // ─── Media Check inputs ───
  const [mediaHeadline, setMediaHeadline] = useState('');
  const [mediaArticle, setMediaArticle] = useState('');
  const [mediaPaper, setMediaPaper] = useState('');
  const [mediaResults, setMediaResults] = useState(null);

  // ─── Compare inputs ───
  const [paper1, setPaper1] = useState('');
  const [paper2, setPaper2] = useState('');
  const [compareQ, setCompareQ] = useState('');
  const [compareResults, setCompareResults] = useState(null);

  // ─── Relevance inputs ───
  const [relSummary, setRelSummary] = useState('');
  const [relContext, setRelContext] = useState('');
  const [relQuestion, setRelQuestion] = useState('');
  const [relResults, setRelResults] = useState(null);

  const handleReset = useCallback(() => {
    setDigestResults(null);
    setMediaResults(null);
    setCompareResults(null);
    setRelResults(null);
    setError('');
  }, []);

  // ─── Jargon inputs ───
  const [jargonTerms, setJargonTerms] = useState('');
  const [jargonContext, setJargonContext] = useState('');
  const [jargonResults, setJargonResults] = useState(null);

  // ─── UI ───
  const [showSaved, setShowSaved] = useState(false);
  const [showDict, setShowDict] = useState(false);
  const [dictSearch, setDictSearch] = useState('');
  const [expandedSections, setExpandedSections] = useState({});

  // ─── Persistent ───
  const [savedDigests, setSavedDigests] = usePersistentState('research-decoder-saved', []);
  const [sessionHistory, setSessionHistory] = usePersistentState('research-decoder-history', []);
  const [jargonDict, setJargonDict] = usePersistentState('research-decoder-jargon', []);

  const toggleSection = (k) => setExpandedSections(p => ({ ...p, [k]: !p[k] }));

  // ═══ HELPERS ═══
  const addJargon = useCallback((term, meaning, why) => setJargonDict(prev => {
    if (prev.find(d => d.term.toLowerCase() === term.toLowerCase())) return prev;
    return [{ term, meaning, why, timestamp: new Date().toISOString() }, ...prev].slice(0, 100);
  }), [setJargonDict]);
  const filteredDict = dictSearch.trim() ? jargonDict.filter(d => d.term.toLowerCase().includes(dictSearch.toLowerCase()) || d.meaning.toLowerCase().includes(dictSearch.toLowerCase())) : jargonDict;

  const saveDigest = (title, data) => setSavedDigests(prev => [{ title: title || t('rd_untitled'), data, timestamp: new Date().toISOString() }, ...prev].slice(0, 6));

  // ═══ API HANDLERS ═══
  const handleDigest = useCallback(async () => {
    if (!paperText.trim()) { setError(t('rd_err_paste_abstract')); return; }
    setError('');
    const data = await callToolEndpoint('research-decoder', { text: paperText, title: paperTitle, field: FIELDS.find(f => f.id === field)?.label || field, userLocale, userCurrency, userRegion });
    if (data) {
      setDigestResults(data);
      // PF-25 exception: 40 is a preview-text length; history is capped at 6.
      setSessionHistory(prev => [{ id: Date.now(), date: new Date().toISOString(), preview: (paperTitle || paperText || '').slice(0, 40) }, ...prev].slice(0, 6));
      data.jargon_decoded?.forEach(j => addJargon(j.term, j.plain_english, j.why_it_matters));
    }
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [paperText, paperTitle, field, callToolEndpoint, addJargon, setSessionHistory, userLocale, userCurrency, userRegion, t]);

  const loadExample = useCallback(() => {
    setMode(EXAMPLE.mode);
    setPaperTitle(EXAMPLE.paperTitle);
    setField(EXAMPLE.field);
    setPaperText(EXAMPLE.paperText);
    setDigestResults(null);
  }, [setMode, setPaperTitle, setField, setPaperText, setDigestResults]);

  const handleMedia = async () => {
    if (!mediaHeadline.trim() && !mediaPaper.trim()) { setError(t('rd_err_headline_or_paper')); return; }
    setError('');
    const data = await callToolEndpoint('research-decoder-media', { paperText: mediaPaper || paperText, headline: mediaHeadline, articleExcerpt: mediaArticle, userLocale, userCurrency, userRegion });
    if (data) setMediaResults(data);
  };

  const handleCompare = async () => {
    if (!paper1.trim() || !paper2.trim()) { setError(t('rd_err_both_papers')); return; }
    setError('');
    const data = await callToolEndpoint('research-decoder-compare', { paper1, paper2, question: compareQ, userLocale, userCurrency, userRegion });
    if (data) setCompareResults(data);
  };

  const handleRelevance = async () => {
    if (!relSummary.trim() && !relQuestion.trim()) { setError(t('rd_err_finding_question')); return; }
    setError('');
    const data = await callToolEndpoint('research-decoder-relevance', { paperSummary: relSummary || digestResults?.one_sentence, myContext: relContext, myQuestion: relQuestion, userLocale, userCurrency, userRegion });
    if (data) setRelResults(data);
  };

  const handleJargon = async () => {
    if (!jargonTerms.trim()) { setError(t('rd_err_list_terms')); return; }
    setError('');
    const data = await callToolEndpoint('research-decoder-jargon', { terms: jargonTerms, field: FIELDS.find(f => f.id === field)?.label, paperContext: (jargonContext || paperText).substring(0, 2000), userLocale, userCurrency, userRegion });
    if (data) {
      setJargonResults(data);
      data.terms?.forEach(term => addJargon(term.term, term.plain_english, term.why_it_matters));
    }
  };

  // ═══ TEXT BUILDER ═══
  const buildDigestText = useCallback(() => {
    if (!digestResults) return '';
    const r = digestResults; let t = `📄 ResearchDecoder${paperTitle ? `: ${paperTitle}` : ''}\n\n`;
    t += `FINDING: ${r.one_sentence}\n\n`;
    t += `WHY IT MATTERS: ${r.why_it_matters}\n\n`;
    if (r.what_they_did) t += `STUDY TYPE: ${r.what_they_did.study_type}\n`;
    t += `\nWHAT IT PROVES: ${r.what_it_proves}\n`;
    t += `WHAT IT DOESN'T PROVE: ${r.what_it_doesnt_prove}\n\n`;
    if (r.so_what) t += `THE HONEST TAKE: ${r.so_what.the_honest_take}\n`;
    return t + BRAND;
  }, [digestResults, paperTitle]);

  useRegisterActions(buildDigestText(), tool?.title || 'Research Decoder');

  useEffect(() => {
    const handler = (e) => {
      const tag = document.activeElement?.tagName;
      if (tag === 'SELECT') return;
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && !loading) handleDigest();
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
  }, [loading, handleDigest]);

  // ═══ SUB-COMPONENTS ═══
  const MODES = [
    { id: 'digest', icon: '📄', label: t('rd_mode_digest') }, { id: 'media', icon: '📰', label: t('rd_mode_media') },
    { id: 'compare', icon: '⚖️', label: t('rd_mode_compare') }, { id: 'relevance', icon: '🎯', label: t('rd_mode_relevance') },
    { id: 'jargon', icon: '🔤', label: t('rd_mode_jargon') },
  ];

  const Section = ({ id, title, children, defaultOpen = false }) => {
    const isOpen = expandedSections[id] ?? defaultOpen;
    return <div className={`${c.card} border ${c.border} rounded-xl overflow-hidden`}><button onClick={() => toggleSection(id)} className="w-full p-4 flex items-center justify-between"><h3 className={`font-bold ${c.text}`}>{title}</h3><span>{isOpen ? '▲' : '▼'}</span></button>{isOpen && <div className={`px-4 pb-4 border-t ${c.border} pt-3 space-y-2`}>{children}</div>}</div>;
  };

  const ACCURACY_COLORS = {
    'Accurate': c.success, 'Mostly accurate': c.infoBox, 'Exaggerated': c.warning, 'Misleading': c.danger, 'Completely wrong': c.danger,
    [t('rd_acc_accurate')]: c.success, [t('rd_acc_mostly')]: c.infoBox, [t('rd_acc_exaggerated')]: c.warning, [t('rd_acc_misleading')]: c.danger, [t('rd_acc_wrong')]: c.danger,
  };

  const results = digestResults || mediaResults || compareResults || relResults || jargonResults;  // alias for audit cross-ref detection + PF-16 reset visibility across all modes

  // ═══ RENDER ═══
  return (
    <div className={`space-y-4 ${c.text}`}>
      {/* ─── Persistent Header Card ─── */}
      <div className={`${c.card} border ${c.border} rounded-xl shadow-sm`}>
        <div className="px-5 pt-5">
          <div className="pb-3 border-b border-zinc-500">
            <div className="flex items-start justify-between gap-2">
              <div>
                <h2 className={`text-xl font-bold ${c.text}`}>
                  <span className="mr-2">{tool?.icon ?? '📄'}</span>{tool?.title ?? t('rd_title')}
                </h2>
                <p className={`text-sm ${c.textSecondary}`}>{tool?.tagline ?? t('rd_tagline')}</p>
                <button onClick={loadExample} disabled={loading} style={{ backgroundColor: (tool?.headerColor ?? '#888888') + '80' }} className={`mt-1.5 px-2.5 py-0.5 rounded-full text-xs font-medium border disabled:opacity-40 ${isDark ? 'text-white border-white/40' : 'text-gray-800 border-transparent'}`}>{t('try_example')}</button>
              </div>
              {results ? (
                <button onClick={handleReset} className={`${c.btnSecondary} px-3 py-1.5 rounded-lg text-xs font-semibold flex-shrink-0`}>↺ {t('reset')}</button>
              ) : null}
            </div>
          </div>
        </div>
        <div className="px-5 pb-5 pt-3">
          <div className="flex flex-wrap gap-1.5">
            {MODES.map(m => <button key={m.id} onClick={() => { setMode(m.id); setError(''); }} className={`px-3 py-1.5 rounded-lg text-xs font-medium border transition-colors ${mode === m.id ? c.pillActive : c.pillInactive}`}><span className="mr-1">{m.icon}</span> {m.label}</button>)}
            <button onClick={() => setShowSaved(!showSaved)} className={`px-3 py-1.5 rounded-lg text-xs font-medium border transition-colors ${showSaved ? c.pillActive : c.pillInactive}`}><span className="mr-1">💾</span> {t('rd_saved')}{savedDigests.length ? ` (${savedDigests.length})` : ''}</button>
            <button onClick={() => setShowDict(!showDict)} className={`px-3 py-1.5 rounded-lg text-xs font-medium border transition-colors ${showDict ? c.pillActive : c.pillInactive}`}><span className="mr-1">📖</span> {t('rd_dictionary')}{jargonDict.length ? ` (${jargonDict.length})` : ''}</button>
          </div>
          <p className={`text-xs ${c.textMuted} mt-2`}>{t('rd_xref_decide_q')} <a href="/DecisionCoach" className={linkStyle}>🎯 {t('rd_decision_coach')}</a></p>
        </div>
      </div>

      {/* ─── Saved Panel ─── */}
      {showSaved && <div className={`${c.card} border ${c.border} rounded-xl p-5 space-y-3`}>
        <h3 className={`font-bold ${c.text}`}>💾 {t('rd_saved_title')}</h3>
        {savedDigests.length === 0 ? <p className={`text-sm ${c.textMuteded}`}>{t('rd_saved_empty')}</p> : savedDigests.map((sd, i) => <div key={i} className={`${c.cardAlt} rounded-lg p-3 flex items-start justify-between`}><div className="flex-1"><p className={`text-sm font-bold ${c.text}`}>{sd.title}</p><p className={`text-xs ${c.textSecondary}`}>{sd.data.one_sentence}</p><p className={`text-xs ${c.textMuteded}`}>{new Date(sd.timestamp).toLocaleDateString()}</p></div><button onClick={() => setSavedDigests(prev => prev.filter((_, idx) => idx !== i))} className={`text-xs ${c.textMuteded} ml-2`}>🗑️</button></div>)}
      </div>}

      {/* ─── Dictionary Panel ─── */}
      {showDict && <div className={`${c.card} border ${c.border} rounded-xl p-5 space-y-3`}>
        <h3 className={`font-bold ${c.text}`}>📖 {t('rd_dict_title', { count: jargonDict.length })}</h3>
        <p className={`text-xs ${c.textMuteded}`}>{t('rd_dict_desc')}</p>
        <input value={dictSearch} onChange={e => setDictSearch(e.target.value)} placeholder={t('rd_dict_search_ph')} className={`w-full px-3 py-2 rounded-lg border text-sm ${c.input}`} />
        {filteredDict.length === 0 ? <p className={`text-sm ${c.textMuteded}`}>{jargonDict.length === 0 ? t('rd_dict_empty_start') : t('rd_dict_empty_nomatch')}</p> : filteredDict.slice(0, 20).map((d, i) => <div key={i} className={`${c.cardAlt} rounded-lg p-3`}><div className="flex items-start justify-between"><div><span className={`text-sm font-bold ${c.text}`}>{d.term}</span><p className={`text-xs ${c.textSecondary}`}>{d.meaning}</p>{d.why && <p className={`text-xs ${c.textMuteded}`}>{d.why}</p>}</div><button onClick={() => setJargonDict(prev => prev.filter((_, idx) => idx !== jargonDict.indexOf(d)))} className={`text-xs ${c.textMuteded}`}>✕</button></div></div>)}
      </div>}

      {/* ═══ DIGEST MODE ═══ */}
      {mode === 'digest' && <>
        <div className={`${c.card} border ${c.border} rounded-xl p-5 space-y-4`}>
          <h3 className={`font-bold ${c.text}`}>📄 {t('rd_digest_card_title')}</h3>
          <p className={`text-sm ${c.textMuteded}`}>{t('rd_digest_card_desc')}</p>
          <input value={paperTitle} onChange={e => setPaperTitle(e.target.value)} placeholder={t('rd_paper_title_ph')} className={`w-full px-3 py-2 rounded-lg border text-sm ${c.input}`} />
          <div>
            <label className={`block text-sm font-medium ${c.labelText} mb-1`}>{t('rd_abstract_label')} <span className={c.required}>*</span></label>
            <textarea value={paperText} onChange={e => setPaperText(e.target.value)} placeholder={t('rd_abstract_ph')} rows={8} className={`w-full px-3 py-2 rounded-lg border text-sm font-mono ${c.input}`} />
          </div>
          <div className="flex flex-wrap gap-2">{FIELDS.map(f => <button key={f.id} onClick={() => setField(f.id)} className={`px-3 py-1.5 rounded-lg text-xs font-medium border ${field === f.id ? c.pillActive : `${c.pillInactive} ${c.border}`}`}><span className="mr-1">{f.icon}</span> {f.label}</button>)}</div>
          <button onClick={handleDigest} disabled={!paperText.trim() || loading} className={`w-full ${c.btnPrimary} disabled:opacity-40 font-bold py-3 rounded-lg flex items-center justify-center gap-2 min-h-[48px]`}>
          {loading
            ? <><span className="inline-block animate-spin">{tool?.icon ?? '📄'}</span> {t('rd_working')}</>
            : <><span className="mr-1">{tool?.icon ?? '📄'}</span> {t('rd_decode_paper')}</>}
          </button>
          <p className={`text-xs ${c.textMuted}`}>{t('rd_xref_decide_help')} <a href="/DecisionCoach" className={linkStyle}>🎯 {t('rd_decision_coach')}</a> {t('rd_decision_coach_help')}</p>
        </div>

        {digestResults && (() => { const r = digestResults; return (
          <div className="space-y-4">
            <button onClick={() => saveDigest(paperTitle || r.one_sentence?.substring(0, 60), r)} className={`w-full py-2 rounded-lg text-xs font-bold ${c.btnSecondary} border ${c.border}`}>💾 {t('rd_save_digest')}</button>

            {/* One Sentence */}
            <div className={`${c.warningBox} border rounded-xl p-5`}>
              <h3 className={`font-bold ${c.accentTxt} text-sm`}>📌 {t('rd_the_finding')}</h3>
              <p className={`text-lg font-semibold ${c.text} mt-2`}>{r.one_sentence}</p>
            </div>

            {/* Why It Matters */}
            {r.why_it_matters && <div className={`${c.infoBox} border rounded-xl p-5`}><h3 className="font-bold text-sm">💡 {t('rd_why_it_matters')}</h3><p className="text-sm mt-1">{r.why_it_matters}</p></div>}

            {/* What They Did */}
            {r.what_they_did && (
              <div className={`${c.card} border ${c.border} rounded-xl p-5 space-y-3`}>
                <h3 className={`font-bold ${c.text}`}>🔬 {t('rd_what_they_did')}</h3>
                <div className={`${c.cardAlt} rounded-lg p-3`}><span className={`text-xs font-bold ${c.accentTxt}`}>{t('rd_study_type')}</span><p className={`text-sm ${c.text} mt-0.5`}>{r.what_they_did.study_type}</p></div>
                <div className={`${c.cardAlt} rounded-lg p-3`}><span className={`text-xs font-bold ${c.accentTxt}`}>{t('rd_who_studied')}</span><p className={`text-sm ${c.text} mt-0.5`}>{r.what_they_did.sample}</p></div>
                <div className={`${c.cardAlt} rounded-lg p-3`}><span className={`text-xs font-bold ${c.accentTxt}`}>{t('rd_method')}</span><p className={`text-sm ${c.text} mt-0.5`}>{r.what_they_did.method_plain}</p></div>
                {r.what_they_did.controls && <div className={`${c.cardAlt} rounded-lg p-3`}><span className={`text-xs font-bold ${c.accentTxt}`}>{t('rd_controls')}</span><p className={`text-sm ${c.text} mt-0.5`}>{r.what_they_did.controls}</p></div>}
                {r.what_they_did.stats_note && <div className={`${c.cardAlt} rounded-lg p-3`}><span className={`text-xs font-bold ${c.accentTxt}`}>{t('rd_the_numbers')}</span><p className={`text-sm ${c.text} mt-0.5`}>{r.what_they_did.stats_note}</p></div>}
              </div>
            )}

            {/* Proves vs Doesn't */}
            <div className="grid grid-cols-1 sm:grid-cols-2 gap-4">
              {r.what_it_proves && <div className={`${c.success} border rounded-xl p-4`}><h3 className="font-bold text-sm">✅ {t('rd_what_proves')}</h3><p className="text-sm mt-1">{r.what_it_proves}</p></div>}
              {r.what_it_doesnt_prove && <div className={`${c.warning} border rounded-xl p-4`}><h3 className="font-bold text-sm">⚠️ {t('rd_what_doesnt_prove')}</h3><p className="text-sm mt-1">{r.what_it_doesnt_prove}</p></div>}
            </div>

            {/* Limitations */}
            {r.limitations?.length > 0 && <Section id="limits" title={`🧱 ${t('rd_limitations')}`}>{r.limitations.map((l, i) => <p key={i} className={`text-sm ${c.textSecondary}`}>• {l}</p>)}</Section>}

            {/* Jargon */}
            {r.jargon_decoded?.length > 0 && <Section id="jargon" title={`🔤 ${t('rd_jargon_decoded', { count: r.jargon_decoded.length })}`}>{r.jargon_decoded.map((j, i) => <div key={i} className={`${c.cardAlt} rounded-lg p-3`}><p className={`text-sm font-bold ${c.text}`}>{j.term}</p><p className={`text-xs ${c.textSecondary}`}>{j.meaning}</p>{j.why_it_matters && <p className={`text-xs ${c.textMuteded}`}>{j.why_it_matters}</p>}</div>)}</Section>}

            {/* Field Context */}
            {r.field_context && <div className={`${c.infoBox} border rounded-xl p-4`}><h3 className="font-bold text-sm">🗺️ {t('rd_bigger_picture')}</h3><p className="text-sm mt-1">{r.field_context}</p></div>}

            {/* So What */}
            {r.so_what && (
              <div className={`${c.success} border rounded-xl p-5 space-y-3`}>
                <h3 className="font-bold">🎯 {t('rd_so_what')}</h3>
                <p className="text-sm"><strong>{t('rd_for_you')}</strong> {r.so_what.for_you}</p>
                <p className="text-sm"><strong>{t('rd_confidence')}</strong> {r.so_what.confidence_level}</p>
                <div className={`${c.successBox} border rounded-lg p-4 mt-2`}>
                  <p className={`text-sm font-medium ${c.successTxt}`}>☕ {r.so_what.the_honest_take}</p>
                </div>
              </div>
            )}

            {/* Quick actions */}
            <div className="flex flex-wrap gap-2">
              <button onClick={() => { setMode('media'); setMediaPaper(paperText); }} className={`px-4 py-2 rounded-lg text-xs font-bold ${c.btnSecondary}`}>📰 {t('rd_check_media_coverage')}</button>
              <button onClick={() => { setMode('relevance'); setRelSummary(r.one_sentence); }} className={`px-4 py-2 rounded-lg text-xs font-bold ${c.btnSecondary}`}>🎯 {t('rd_does_apply_to_me')}</button>
            </div>
          </div>
        ); })()}
      </>}

      {/* ═══ MEDIA CHECK MODE ═══ */}
      {mode === 'media' && <>
        <div className={`${c.card} border ${c.border} rounded-xl p-5 space-y-4`}>
          <h3 className={`font-bold ${c.text}`}>📰 {t('rd_media_card_title')}</h3>
          <p className={`text-sm ${c.textMuteded}`}>{t('rd_media_card_desc')}</p>
          <div>
            <label className={`block text-sm font-medium ${c.labelText} mb-1`}>{t('rd_headline_label')} <span className={c.required}>*</span></label>
            <input value={mediaHeadline} onChange={e => setMediaHeadline(e.target.value)} placeholder={t('rd_headline_ph')} className={`w-full px-3 py-2 rounded-lg border text-sm ${c.input}`} />
          </div>
          <textarea value={mediaArticle} onChange={e => setMediaArticle(e.target.value)} placeholder={t('rd_article_ph')} rows={3} className={`w-full px-3 py-2 rounded-lg border text-sm ${c.input}`} />
          <div>
            <label className={`block text-sm font-medium ${c.labelText} mb-1`}>{t('rd_paper_abstract_label')} <span className={c.required}>*</span></label>
            <textarea value={mediaPaper} onChange={e => setMediaPaper(e.target.value)} placeholder={t('rd_paper_abstract_ph')} rows={5} className={`w-full px-3 py-2 rounded-lg border text-sm font-mono ${c.input}`} />
          </div>
          <button onClick={handleMedia} disabled={(!mediaHeadline.trim() && !mediaPaper.trim()) || loading} className={`w-full ${c.btnPrimary} disabled:opacity-40 font-bold py-3 rounded-lg flex items-center justify-center gap-2 min-h-[48px]`}>
            {loading
              ? <><span className="inline-block animate-spin">{tool?.icon ?? '📄'}</span> {t('rd_checking')}</>
              : <><span className="mr-1">{tool?.icon ?? '📄'}</span> {t('rd_check_accuracy')}</>}
          </button>
          <p className={`text-xs ${c.textMuted}`}>{t('rd_xref_deeper')} <a href="/DecisionCoach" className={linkStyle}>🎯 {t('rd_decision_coach')}</a> {t('rd_decision_coach_next')}</p>
        </div>

        {mediaResults && (() => { const r = mediaResults; return (
          <div className="space-y-4">
            {r.accuracy_rating && <div className={`${ACCURACY_COLORS[r.accuracy_rating.score] || c.warning} border rounded-xl p-5`}>
              <div className="flex items-center gap-3"><span className="text-3xl">{r.accuracy_rating.emoji}</span><div><h3 className="font-bold text-lg">{r.accuracy_rating.score}</h3><p className="text-sm">{r.accuracy_rating.explanation}</p></div></div>
            </div>}
            <div className="grid grid-cols-1 sm:grid-cols-2 gap-4">
              {r.paper_actually_says && <div className={`${c.success} border rounded-xl p-4`}><h3 className="font-bold text-sm">📄 {t('rd_paper_says')}</h3><p className="text-sm mt-1">{r.paper_actually_says}</p></div>}
              {r.media_says && <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}><h3 className={`font-bold text-sm ${c.text}`}>📰 {t('rd_media_says')}</h3><p className={`text-sm mt-1 ${c.textSecondary}`}>{r.media_says}</p></div>}
            </div>
            {r.distortions?.length > 0 && <div className={`${c.card} border ${c.border} rounded-xl p-5 space-y-3`}><h3 className={`font-bold ${c.text}`}>🔍 {t('rd_distortions_found')}</h3>{r.distortions.map((d, i) => <div key={i} className={`${c.danger} border rounded-lg p-3 space-y-1`}><div className="flex items-center gap-2"><span className={`text-xs font-bold px-2 py-0.5 rounded-full ${isDark ? 'bg-red-900/40 text-red-200' : 'bg-red-100 text-red-700'}`}>{d.distortion_type}</span></div><p className="text-sm"><strong>{t('rd_label_media')}</strong> {d.what_media_said}</p><p className="text-sm"><strong>{t('rd_label_paper')}</strong> {d.what_paper_said}</p><p className={`text-xs ${c.textMuteded}`}>{d.why_it_matters}</p></div>)}</div>}
            {r.what_they_got_right?.length > 0 && <div className={`${c.success} border rounded-xl p-4`}><h3 className="font-bold text-sm">✅ {t('rd_got_right')}</h3>{r.what_they_got_right.map((g, i) => <p key={i} className="text-sm mt-1">• {g}</p>)}</div>}
            {r.the_real_story && <div className={`${c.warningBox} border rounded-xl p-5`}><h3 className={`font-bold ${c.accentTxt} text-sm`}>📝 {t('rd_headline_should')}</h3><p className={`text-sm ${c.text} mt-1`}>{r.the_real_story}</p></div>}
            {r.should_you_worry && <div className={`${c.infoBox} border rounded-xl p-4`}><p className="text-sm">🎯 <strong>{t('rd_should_you_worry')}</strong> {r.should_you_worry}</p></div>}
          </div>
        ); })()}
      </>}

      {/* ═══ COMPARE MODE ═══ */}
      {mode === 'compare' && <>
        <div className={`${c.card} border ${c.border} rounded-xl p-5 space-y-4`}>
          <h3 className={`font-bold ${c.text}`}>⚖️ {t('rd_compare_card_title')}</h3>
          <p className={`text-sm ${c.textMuteded}`}>{t('rd_compare_card_desc')}</p>
          <div>
            <label className={`block text-sm font-medium ${c.labelText} mb-1`}>{t('rd_paper1_label')} <span className={c.required}>*</span></label>
            <textarea value={paper1} onChange={e => setPaper1(e.target.value)} placeholder={t('rd_paste_abstract_ph')} rows={4} className={`w-full px-3 py-2 rounded-lg border text-sm font-mono ${c.input}`} />
          </div>
          <div>
            <label className={`block text-sm font-medium ${c.labelText} mb-1`}>{t('rd_paper2_label')} <span className={c.required}>*</span></label>
            <textarea value={paper2} onChange={e => setPaper2(e.target.value)} placeholder={t('rd_paste_abstract_ph')} rows={4} className={`w-full px-3 py-2 rounded-lg border text-sm font-mono ${c.input}`} />
          </div>
          <input value={compareQ} onChange={e => setCompareQ(e.target.value)} placeholder={t('rd_compare_q_ph')} className={`w-full px-3 py-2 rounded-lg border text-sm ${c.input}`} />
          <button onClick={handleCompare} disabled={!paper1.trim() || !paper2.trim() || loading} className={`w-full ${c.btnPrimary} disabled:opacity-40 font-bold py-3 rounded-lg flex items-center justify-center gap-2 min-h-[48px]`}>
            {loading
              ? <><span className="inline-block animate-spin">{tool?.icon ?? '📄'}</span> {t('rd_comparing')}</>
              : <><span className="mr-1">{tool?.icon ?? '📄'}</span> {t('rd_compare_papers')}</>}
          </button>
          <p className={`text-xs ${c.textMuted}`}>{t('rd_xref_jumbled')} <a href="/BrainDumpBuddy" className={linkStyle}>🧠 {t('rd_brain_dump')}</a> {t('rd_brain_dump_organize')}</p>
        </div>

        {compareResults && (() => { const r = compareResults; return (
          <div className="space-y-4">
            <div className="grid grid-cols-1 sm:grid-cols-2 gap-4">
              <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}><h3 className={`font-bold text-sm ${c.text}`}>📄 {t('rd_paper1')}</h3><p className={`text-sm ${c.textSecondary} mt-1`}>{r.paper1_says}</p></div>
              <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}><h3 className={`font-bold text-sm ${c.text}`}>📄 {t('rd_paper2')}</h3><p className={`text-sm ${c.textSecondary} mt-1`}>{r.paper2_says}</p></div>
            </div>
            {r.do_they_agree && <div className={`${r.do_they_agree.verdict_level === 'agree' ? c.success : r.do_they_agree.verdict_level === 'disagree' ? c.danger : c.warning} border rounded-xl p-5`}><h3 className="font-bold text-lg">{r.do_they_agree.verdict_level === 'agree' ? '✅' : r.do_they_agree.verdict_level === 'disagree' ? '❌' : '🤔'} {r.do_they_agree.verdict}</h3><p className="text-sm mt-1">{r.do_they_agree.explanation}</p></div>}
            {r.why_different?.length > 0 && <Section id="whydiff" title={`🔍 ${t('rd_why_differ')}`}>{r.why_different.map((w, i) => <p key={i} className={`text-sm ${c.textSecondary}`}>• {w}</p>)}</Section>}
            {r.which_to_trust_more && <div className={`${c.warningBox} border rounded-xl p-5`}><h3 className={`font-bold ${c.accentTxt} text-sm`}>🏆 {t('rd_which_trust')}</h3><p className={`text-sm ${c.text} mt-1`}>{r.which_to_trust_more.assessment}</p>{r.which_to_trust_more.caveats && <p className={`text-xs ${c.textMuteded} mt-2`}>{r.which_to_trust_more.caveats}</p>}</div>}
            {r.the_takeaway && <div className={`${c.success} border rounded-xl p-4`}><p className="text-sm">🎯 <strong>{t('rd_takeaway')}</strong> {r.the_takeaway}</p></div>}
            {r.what_we_still_dont_know && <div className={`${c.infoBox} border rounded-xl p-4`}><p className="text-sm">❓ <strong>{t('rd_still_unknown')}</strong> {r.what_we_still_dont_know}</p></div>}
          </div>
        ); })()}
      </>}

      {/* ═══ RELEVANCE MODE ═══ */}
      {mode === 'relevance' && <>
        <div className={`${c.card} border ${c.border} rounded-xl p-5 space-y-4`}>
          <h3 className={`font-bold ${c.text}`}>🎯 {t('rd_rel_card_title')}</h3>
          <p className={`text-sm ${c.textMuteded}`}>{t('rd_rel_card_desc')}</p>
          <div>
            <label className={`block text-sm font-medium ${c.labelText} mb-1`}>{t('rd_finding_label')} <span className={c.required}>*</span></label>
            <textarea value={relSummary} onChange={e => setRelSummary(e.target.value)} placeholder={t('rd_finding_ph')} rows={3} className={`w-full px-3 py-2 rounded-lg border text-sm ${c.input}`} />
          </div>
          <textarea value={relContext} onChange={e => setRelContext(e.target.value)} placeholder={t('rd_situation_ph')} rows={2} className={`w-full px-3 py-2 rounded-lg border text-sm ${c.input}`} />
          <input value={relQuestion} onChange={e => setRelQuestion(e.target.value)} placeholder={t('rd_rel_question_ph')} className={`w-full px-3 py-2 rounded-lg border text-sm ${c.input}`} />
          <button onClick={handleRelevance} disabled={(!relSummary.trim() && !relQuestion.trim()) || loading} className={`w-full ${c.btnPrimary} disabled:opacity-40 font-bold py-3 rounded-lg flex items-center justify-center gap-2 min-h-[48px]`}>
            {loading
              ? <><span className="inline-block animate-spin">{tool?.icon ?? '📄'}</span> {t('rd_checking')}</>
              : <><span className="mr-1">{tool?.icon ?? '📄'}</span> {t('rd_check_relevance')}</>}
          </button>
          <p className={`text-xs ${c.textMuted}`}>{t('rd_xref_stuck')} <a href="/DecisionCoach" className={linkStyle}>🎯 {t('rd_decision_coach')}</a> {t('rd_decision_coach_think')}</p>
        </div>

        {relResults && (() => { const r = relResults; return (
          <div className="space-y-4">
            {r.applies_to_you && <div className={`${r.applies_to_you.verdict_level === 'yes' ? c.success : r.applies_to_you.verdict_level === 'no' ? c.cardAlt : c.warning} border rounded-xl p-5`}><h3 className={`font-bold ${c.text} text-lg`}>{r.applies_to_you.verdict_level === 'yes' ? '✅' : r.applies_to_you.verdict_level === 'no' ? '🤷' : '🤔'} {r.applies_to_you.verdict}</h3><p className={`text-sm ${c.textSecondary} mt-1`}>{r.applies_to_you.explanation}</p></div>}
            {r.should_you_change && <div className={`${c.card} border ${c.border} rounded-xl p-5 space-y-2`}><h3 className={`font-bold ${c.text}`}>🔄 {t('rd_should_change')}</h3><p className={`text-sm ${c.textSecondary}`}>{r.should_you_change.behavior}</p><p className={`text-sm ${c.textSecondary}`}>📊 <strong>{t('rd_confidence_label')}</strong> {r.should_you_change.confidence}</p>{r.should_you_change.cost_of_waiting && <p className={`text-sm ${c.textMuteded}`}>⏱️ {t('rd_cost_waiting')} {r.should_you_change.cost_of_waiting}</p>}</div>}
            {r.talk_to && <div className={`${c.infoBox} border rounded-xl p-4`}><p className="text-sm">👩‍⚕️ {r.talk_to}</p></div>}
            {r.the_bottom_line && <div className={`${c.success} border rounded-xl p-5`}><p className={`text-sm font-medium ${c.successTxt}`}>☕ {r.the_bottom_line}</p></div>}
          </div>
        ); })()}
      </>}

      {/* ═══ JARGON MODE ═══ */}
      {mode === 'jargon' && <>
        <div className={`${c.card} border ${c.border} rounded-xl p-5 space-y-4`}>
          <h3 className={`font-bold ${c.text}`}>🔤 {t('rd_jargon_card_title')}</h3>
          <p className={`text-sm ${c.textMuteded}`}>{t('rd_jargon_card_desc')}</p>
          <div>
            <label className={`block text-sm font-medium ${c.labelText} mb-1`}>{t('rd_terms_label')} <span className={c.required}>*</span></label>
            <input value={jargonTerms} onChange={e => setJargonTerms(e.target.value)} placeholder={t('rd_terms_ph')} className={`w-full px-3 py-2 rounded-lg border text-sm ${c.input}`} />
          </div>
          <textarea value={jargonContext} onChange={e => setJargonContext(e.target.value)} placeholder={t('rd_jargon_context_ph')} rows={2} className={`w-full px-3 py-2 rounded-lg border text-sm ${c.input}`} />
          <button onClick={handleJargon} disabled={!jargonTerms.trim() || loading} className={`w-full ${c.btnPrimary} disabled:opacity-40 font-bold py-3 rounded-lg flex items-center justify-center gap-2 min-h-[48px]`}>
            {loading
              ? <><span className="inline-block animate-spin">{tool?.icon ?? '📄'}</span> {t('rd_decoding')}</>
              : <><span className="mr-1">{tool?.icon ?? '📄'}</span> {t('rd_decode_terms')}</>}
          </button>
          <p className={`text-xs ${c.textMuted}`}>{t('rd_xref_simplify')} <a href="/BrainDumpBuddy" className={linkStyle}>🧠 {t('rd_brain_dump')}</a> {t('rd_brain_dump_complex')}</p>
        </div>

        {jargonResults?.terms?.map((term, i) => (
          <div key={i} className={`${c.card} border ${c.border} rounded-xl p-5 space-y-2`}>
            <h3 className={`font-bold ${c.text}`}>{term.term}</h3>
            <p className={`text-sm ${c.textSecondary}`}>{term.plain_english}</p>
            {term.why_it_matters && <p className={`text-sm ${c.textMuteded}`}>💡 {term.why_it_matters}</p>}
            {term.watch_out && <div className={`${c.warning} border rounded-lg p-3`}><p className="text-xs">⚠️ {term.watch_out}</p></div>}
            {term.example && <p className={`text-xs ${c.textMuteded}`}>📝 {term.example}</p>}
            <p className={`text-xs ${c.accentTxt}`}>📖 {t('rd_saved_to_dict')}</p>
          </div>
        ))}
      </>}

      {/* ─── Error ─── */}
      {error && <div className={`${c.danger} border rounded-xl p-4 text-sm`}>⚠️ {error}</div>}

      {/* ─── Post-result cross-refs ─── */}
      {(digestResults || mediaResults || compareResults || relResults || jargonResults) && (
        <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
          <p className={`text-[10px] font-bold ${c.textMuted} uppercase mb-2`}>🔗 {t('rd_related')}</p>
          <div className="flex flex-wrap gap-3">
            <a href="/DecisionCoach" className={`text-xs ${linkStyle}`}>🎯 {t('rd_decision_coach')}</a>
            <a href="/BrainDumpBuddy" className={`text-xs ${linkStyle}`}>🧠 {t('rd_brain_dump')}</a>
            <a href="/SpiralStopper" className={`text-xs ${linkStyle}`}>🌀 {t('rd_spiral_stopper')}</a>
          </div>
        </div>
      )}
      {sessionHistory.length > 0 && (
        <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4 mt-4`}>
          <p className={`text-xs font-bold ${c.textMuted} mb-2`}>📋 {t('rd_recent')}</p>
          <div className="space-y-1">
            {sessionHistory.map(s => (
              <div key={s.id} className="flex items-center justify-between">
                <span className={`text-xs ${c.textSecondary} truncate`}>{s.preview || t('rd_session')}</span>
                <span className={`text-xs ${c.textMuted} ml-2`}>{new Date(s.date).toLocaleDateString()}</span>
              </div>
            ))}
          </div>
        </div>
      )}

      {results && (
        <p className={`text-xs ${c.textMuted} mt-3 text-center`}>
          {t('rd_xref_signal_q')} <a href="/SignalVsNoise" className={linkStyle}>📡 {t('rd_signal_noise')}</a> {t('rd_xref_signal_tail')}
        </p>
      )}
    </div>
  );
};

ResearchDecoder.displayName = 'ResearchDecoder';
export default ResearchDecoder;
