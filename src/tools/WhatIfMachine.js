import React, { useState, useCallback } from 'react';
import { CopyBtn } from '../components/ActionButtons';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const TIMEFRAMES = [
  { value: 'one_month', label: '1 month', emoji: '📅' },
  { value: 'six_months', label: '6 months', emoji: '🗓️' },
  { value: 'one_year', label: '1 year', emoji: '🔮' },
  { value: 'five_years', label: '5 years', emoji: '🌍' },
];

const WhatIfMachine = () => {
  const { callToolEndpoint, loading } = useClaudeAPI();
  const { theme } = useTheme();
  const isDark = theme === 'dark';

  const c = {
    pageBg:       isDark ? 'bg-[#1a1816]'    : 'bg-[#faf8f5]',
    card:         isDark ? 'bg-[#2a2623]'     : 'bg-white',
    cardAlt:      isDark ? 'bg-[#332e2a]'     : 'bg-[#faf8f5]',
    cardBorder:   isDark ? 'border-[#3d3630]' : 'border-[#e8e1d5]',
    inputBg:      isDark ? 'bg-[#1a1816]'     : 'bg-[#faf8f5]',
    inputBorder:  isDark ? 'border-[#3d3630]' : 'border-[#d5cab8]',
    inputFocus:   isDark ? 'focus:border-[#4a6a8a] focus:ring-[#2c4a6e]/20'
                         : 'focus:border-[#4a6a8a] focus:ring-[#2c4a6e]/12',
    text:          isDark ? 'text-[#f0eeea]'  : 'text-[#3d3935]',
    textSecondary: isDark ? 'text-[#c8c3b9]'  : 'text-[#5a544a]',
    textMuted:     isDark ? 'text-[#8a8275]'  : 'text-[#8a8275]',
    heading:       isDark ? 'text-[#f3efe8]'  : 'text-[#1e2a3a]',
    btnPrimary:    isDark ? 'bg-[#2c4a6e] hover:bg-[#4a6a8a] text-white' : 'bg-[#2c4a6e] hover:bg-[#1e3a58] text-white',
    btnSecondary:  isDark ? 'bg-[#332e2a] hover:bg-[#3d3630] text-[#c8c3b9] border border-[#3d3630]' : 'bg-[#f3efe8] hover:bg-[#e8e1d5] text-[#5e5042] border border-[#d5cab8]',
    btnGold:       isDark ? 'bg-[#b06d22] hover:bg-[#c8872e] text-white' : 'bg-[#c8872e] hover:bg-[#b06d22] text-white',
    badgeNeutral:  isDark ? 'bg-[#3d3630] text-[#c8c3b9]' : 'bg-[#f3efe8] text-[#73614e]',
    badgeGold:     isDark ? 'bg-[#b06d22]/20 text-[#d9a04e]' : 'bg-[#f9edd8] text-[#93541f]',
    divider:       isDark ? 'border-[#3d3630]' : 'border-[#e8e1d5]',
    link:          isDark ? 'text-[#6e8aaa] hover:text-[#a8b9ce]' : 'text-[#2c4a6e] hover:text-[#1e3a58]',
    success:       isDark ? 'bg-[#5a8a5c]/15 border-[#5a8a5c]/30 text-[#8abf8c]' : 'bg-[#e8f0e8] border-[#5a8a5c]/20 text-[#3d6b3f]',
    warning:       isDark ? 'bg-[#c8872e]/15 border-[#c8872e]/30 text-[#d9a04e]' : 'bg-[#fdf3e4] border-[#c8872e]/20 text-[#93541f]',
    error:         isDark ? 'bg-[#b54a3f]/15 border-[#b54a3f]/30 text-[#e08a82]' : 'bg-[#fceae8] border-[#b54a3f]/20 text-[#8a3530]',
    info:          isDark ? 'bg-[#4a7c94]/15 border-[#4a7c94]/30 text-[#7ab8d4]' : 'bg-[#e6f0f5] border-[#4a7c94]/20 text-[#3a6a7e]',
    pillActive:    isDark ? 'bg-[#2c4a6e] border-[#4a6a8a] text-white' : 'bg-[#2c4a6e] border-[#2c4a6e] text-white',
    pillInactive:  isDark ? 'bg-[#2a2623] border-[#3d3630] text-[#c8c3b9] hover:border-[#5a544a]' : 'bg-white border-[#e8e1d5] text-[#5a544a] hover:border-[#d5cab8]',
    sceneBg:       isDark ? 'bg-[#1a1816] border-[#3d3630]' : 'bg-[#faf8f5] border-[#e8e1d5]',
  };

  // ─── State ───
  const [decision, setDecision] = useState('');
  const [optionNotChosen, setOptionNotChosen] = useState('');
  const [context, setContext] = useState('');
  const [timeframe, setTimeframe] = useState('one_year');
  const [results, setResults] = useState(null);
  const [error, setError] = useState('');

  // ─── Actions ───
  const generate = useCallback(async () => {
    if (!decision.trim()) return;
    setError('');
    setResults(null);

    try {
      const data = await callToolEndpoint('what-if-machine', {
        decision: decision.trim(),
        optionNotChosen: optionNotChosen.trim() || null,
        context: context.trim() || null,
        timeframe,
      });
      setResults(data);
    } catch (err) {
      setError(err.message || 'Failed to generate simulation.');
    }
  }, [decision, optionNotChosen, context, timeframe, callToolEndpoint]);

  const handleReset = useCallback(() => {
    setDecision(''); setOptionNotChosen(''); setContext('');
    setTimeframe('one_year'); setResults(null); setError('');
  }, []);

  // ─── Build text ───
  const buildFullText = useCallback(() => {
    if (!results) return '';
    const r = results;
    let text = `🎲 WhatIfMachine: The Road Not Taken\n`;
    if (r.decision_read) text += `\n${r.decision_read}\n`;
    if (r.the_path_not_taken) text += `\nPath simulated: ${r.the_path_not_taken}\n`;
    if (r.scenarios?.length) {
      r.scenarios.forEach(s => {
        text += `\n--- ${s.timepoint} ---\n${s.scene}`;
        if (s.the_good) text += `\n✅ ${s.the_good}`;
        if (s.the_cost) text += `\n💔 ${s.the_cost}`;
      });
    }
    if (r.the_surprise) text += `\n\n⚡ The surprise: ${r.the_surprise}`;
    if (r.what_you_keep) text += `\n🔒 What stays: ${r.what_you_keep}`;
    if (r.what_you_lose) text += `\n💔 What you'd miss: ${r.what_you_lose}`;
    if (r.clarity_question) text += `\n\n❓ ${r.clarity_question}`;
    if (r.honest_take) text += `\n\n${r.honest_take}`;
    return text + BRAND;
  }, [results]);

  const r = results;

  // ════════════════════════════════════════════════════════════
  // RENDER
  // ════════════════════════════════════════════════════════════
  return (
    <div className={`space-y-6 ${c.text}`}>

      {/* ── HEADER ── */}
      <div className={`${c.card} border ${c.cardBorder} rounded-xl p-6`}>
        <div className={`mb-5 pb-4 border-b ${c.divider}`}>
          <h2 className={`text-2xl font-bold ${c.heading}`}>WhatIfMachine 🎲</h2>
          <p className={`text-sm ${c.textSecondary} mt-1`}>See the road not taken before you decide</p>
        </div>

        {/* Decision */}
        <div className="mb-4">
          <label className={`text-sm font-bold ${c.text} block mb-1.5`}>What decision are you facing?</label>
          <textarea
            value={decision}
            onChange={e => setDecision(e.target.value)}
            placeholder="e.g., I'm thinking about quitting my job to freelance, I'm deciding whether to move across the country for a relationship"
            rows={2}
            className={`w-full px-4 py-3 border rounded-xl text-sm ${c.inputBg} ${c.inputBorder} ${c.text} ${c.inputFocus} outline-none focus:ring-2 resize-none`}
          />
        </div>

        {/* Option not chosen */}
        <div className="mb-4">
          <label className={`text-sm font-bold ${c.text} block mb-1.5`}>
            Which option should I simulate? <span className={`font-normal ${c.textMuted}`}>(the path you're NOT leaning toward)</span>
          </label>
          <input
            type="text"
            value={optionNotChosen}
            onChange={e => setOptionNotChosen(e.target.value)}
            placeholder="e.g., staying at my job, not moving, saying no"
            className={`w-full px-4 py-3 border rounded-xl text-sm ${c.inputBg} ${c.inputBorder} ${c.text} ${c.inputFocus} outline-none focus:ring-2`}
          />
        </div>

        {/* Context */}
        <div className="mb-4">
          <label className={`text-sm font-bold ${c.text} block mb-1.5`}>
            Context about your life <span className={`font-normal ${c.textMuted}`}>(optional — makes scenes more realistic)</span>
          </label>
          <input
            type="text"
            value={context}
            onChange={e => setContext(e.target.value)}
            placeholder="e.g., 32, live in Denver, partner works remotely, have a dog, savings for 6 months"
            className={`w-full px-4 py-3 border rounded-xl text-sm ${c.inputBg} ${c.inputBorder} ${c.text} ${c.inputFocus} outline-none focus:ring-2`}
          />
        </div>

        {/* Timeframe */}
        <div className="mb-5">
          <label className={`text-sm font-bold ${c.text} block mb-2`}>How far into the future?</label>
          <div className="flex gap-2">
            {TIMEFRAMES.map(t => (
              <button
                key={t.value}
                onClick={() => setTimeframe(t.value)}
                className={`flex-1 py-2.5 rounded-xl text-[11px] font-bold border transition-colors min-h-[40px] flex flex-col items-center gap-0.5 ${
                  timeframe === t.value ? c.pillActive : c.pillInactive
                }`}
              >
                <span className="text-base">{t.emoji}</span>
                {t.label}
              </button>
            ))}
          </div>
        </div>

        {/* Actions */}
        <div className="flex gap-3">
          <button
            onClick={generate}
            disabled={loading || !decision.trim()}
            className={`flex-1 ${c.btnGold} disabled:opacity-40 disabled:cursor-not-allowed font-bold py-3 px-6 rounded-xl flex items-center justify-center gap-2 min-h-[48px] shadow-lg`}
          >
            {loading ? (
              <><span className="animate-spin inline-block">⏳</span> Simulating...</>
            ) : (
              <><span>🎲</span> Show Me That Future</>
            )}
          </button>
          {results && (
            <button onClick={handleReset} className={`px-5 py-3 ${c.btnSecondary} rounded-xl font-medium min-h-[48px]`}>
              New Decision
            </button>
          )}
        </div>

        {/* Pre-result cross-ref */}
        {!results && (
          <div className={`mt-5 pt-4 border-t ${c.divider}`}>
            <p className={`text-xs ${c.textMuted}`}>
              🤔 Need help deciding first? <a href="/DecisionCoach" target="_blank" rel="noopener noreferrer" className={`underline ${c.link}`}>Decision Coach</a> helps you think it through.
            </p>
          </div>
        )}
      </div>

      {/* Error */}
      {error && (
        <div className={`${c.error} border rounded-xl p-4 flex items-start gap-3`}>
          <span className="flex-shrink-0 mt-0.5">⚠️</span>
          <p className="text-sm">{error}</p>
        </div>
      )}

      {/* ══════════════════════════════════════════════════════════ */}
      {/* RESULTS                                                  */}
      {/* ══════════════════════════════════════════════════════════ */}
      {r && (
        <div className="space-y-4">

          <div className="flex justify-end">
            <CopyBtn content={buildFullText()} label="Copy All" />
          </div>

          {/* ── DECISION READ ── */}
          {r.decision_read && (
            <div className={`${c.card} border ${c.cardBorder} rounded-xl p-5`}>
              <p className={`text-sm ${c.textSecondary} leading-relaxed`}>{r.decision_read}</p>
            </div>
          )}

          {/* ── PATH LABEL ── */}
          {r.the_path_not_taken && (
            <div className={`${c.info} border rounded-xl p-4 text-center`}>
              <p className="text-xs font-bold mb-0.5">Simulating the path where you chose:</p>
              <p className="text-sm font-bold">{r.the_path_not_taken}</p>
            </div>
          )}

          {/* ── SCENARIOS / TIMELINE ── */}
          {r.scenarios?.length > 0 && (
            <div className="space-y-0">
              {r.scenarios.map((scene, idx) => (
                <div key={idx} className="relative">
                  {/* Timeline connector */}
                  {idx < r.scenarios.length - 1 && (
                    <div className={`absolute left-5 top-full w-0.5 h-4 ${isDark ? 'bg-[#3d3630]' : 'bg-[#e8e1d5]'}`} />
                  )}

                  <div className={`${c.card} border ${c.cardBorder} rounded-xl overflow-hidden ${idx > 0 ? 'mt-4' : ''}`}>
                    {/* Timepoint header */}
                    <div className={`px-5 py-3 border-b ${c.divider} flex items-center gap-2`}>
                      <span className={`text-xs font-bold px-2.5 py-1 rounded-full ${c.badgeGold}`}>
                        {scene.timepoint}
                      </span>
                    </div>

                    {/* The scene */}
                    <div className={`p-5`}>
                      <div className={`${c.sceneBg} border rounded-xl p-5 mb-4`}>
                        <p className={`text-sm ${c.text} leading-[1.9] italic`}>{scene.scene}</p>
                      </div>

                      <div className="grid grid-cols-1 sm:grid-cols-2 gap-3">
                        {scene.the_good && (
                          <div className={`${c.success} border rounded-lg p-3`}>
                            <p className="text-[10px] font-bold mb-1">✅ What's better</p>
                            <p className="text-xs leading-relaxed">{scene.the_good}</p>
                          </div>
                        )}
                        {scene.the_cost && (
                          <div className={`${c.warning} border rounded-lg p-3`}>
                            <p className="text-[10px] font-bold mb-1">💔 What it costs</p>
                            <p className="text-xs leading-relaxed">{scene.the_cost}</p>
                          </div>
                        )}
                      </div>
                    </div>
                  </div>
                </div>
              ))}
            </div>
          )}

          {/* ── THE SURPRISE ── */}
          {r.the_surprise && (
            <div className={`${c.warning} border rounded-xl p-4 flex items-start gap-3`}>
              <span className="flex-shrink-0 mt-0.5">⚡</span>
              <div>
                <p className="text-xs font-bold mb-1">The surprise you wouldn't expect</p>
                <p className="text-sm leading-relaxed">{r.the_surprise}</p>
              </div>
            </div>
          )}

          {/* ── KEEP / LOSE ── */}
          <div className="grid grid-cols-1 sm:grid-cols-2 gap-3">
            {r.what_you_keep && (
              <div className={`${c.card} border ${c.cardBorder} rounded-xl p-4`}>
                <p className={`text-[10px] font-bold ${c.textMuted} mb-1`}>🔒 What stays the same</p>
                <p className={`text-xs ${c.textSecondary} leading-relaxed`}>{r.what_you_keep}</p>
              </div>
            )}
            {r.what_you_lose && (
              <div className={`${c.card} border ${c.cardBorder} rounded-xl p-4`}>
                <p className={`text-[10px] font-bold ${c.textMuted} mb-1`}>💔 What you'd miss most</p>
                <p className={`text-xs ${c.textSecondary} leading-relaxed`}>{r.what_you_lose}</p>
              </div>
            )}
          </div>

          {/* ── CLARITY QUESTION ── */}
          {r.clarity_question && (
            <div className={`${c.card} border ${c.cardBorder} rounded-xl p-5 text-center`}>
              <p className={`text-xs font-bold ${c.textMuted} mb-2`}>❓ The question to ask yourself</p>
              <p className={`text-base font-bold ${c.heading} leading-relaxed`}>{r.clarity_question}</p>
            </div>
          )}

          {/* ── HONEST TAKE ── */}
          {r.honest_take && (
            <div className={`${c.success} border rounded-xl p-4 flex items-start gap-3`}>
              <span className="flex-shrink-0 mt-0.5">🎯</span>
              <div>
                <p className="text-xs font-bold mb-1">Honest take</p>
                <p className="text-sm leading-relaxed">{r.honest_take}</p>
              </div>
            </div>
          )}

          {/* ── CROSS-REFERENCES ── */}
          <div className={`${c.card} border ${c.cardBorder} rounded-xl p-4`}>
            <p className={`text-xs font-bold ${c.text} mb-2`}>Related tools</p>
            <div className="flex flex-wrap gap-2">
              <a href="/DecisionCoach" target="_blank" rel="noopener noreferrer" className={`text-xs px-3 py-1.5 rounded-lg border ${c.pillInactive} no-underline`}>
                🤔 Decision Coach
              </a>
              <a href="/AlternatePath" target="_blank" rel="noopener noreferrer" className={`text-xs px-3 py-1.5 rounded-lg border ${c.pillInactive} no-underline`}>
                🔀 Alternate Path
              </a>
              <a href="/SpiralStopper" target="_blank" rel="noopener noreferrer" className={`text-xs px-3 py-1.5 rounded-lg border ${c.pillInactive} no-underline`}>
                🌀 Spiral Stopper
              </a>
            </div>
          </div>

          <p className={`text-[10px] ${c.textMuted} text-center px-4`}>
            This is a thought experiment, not a prediction. Real life is more complex and more surprising than any simulation.
          </p>
        </div>
      )}
    </div>
  );
};

WhatIfMachine.displayName = 'WhatIfMachine';
export default WhatIfMachine;
