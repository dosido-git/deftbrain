import React, { useState, useEffect} from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { usePersistentState } from '../hooks/usePersistentState';
import { CopyBtn, ActionBar } from '../components/ActionButtons';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const HistoryToday = ({ tool }) => {
  const { callToolEndpoint, loading } = useClaudeAPI();
  const { isDark } = useTheme();

  const c = {
    card: isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt: isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    text: isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted: isDark ? 'text-zinc-500' : 'text-gray-400',
    input: isDark ? 'bg-zinc-700 border-zinc-600 text-zinc-100 placeholder-zinc-400' : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400',
    btnPrimary: isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white' : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary: isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-100' : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border: isDark ? 'border-zinc-700' : 'border-gray-200',
    success: isDark ? 'bg-green-900/20 border-green-700 text-green-200' : 'bg-green-50 border-green-300 text-green-800',
    warning: isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200' : 'bg-amber-50 border-amber-300 text-amber-800',
    danger: isDark ? 'bg-red-900/20 border-red-700 text-red-200' : 'bg-red-50 border-red-200 text-red-800',
    highlight: isDark ? 'bg-cyan-900/20 border-cyan-700 text-cyan-200' : 'bg-cyan-50 border-cyan-200 text-cyan-800',
    parallelCard: isDark ? 'bg-cyan-900/20 border-cyan-700' : 'bg-cyan-50 border-cyan-200',
    counter: isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200' : 'bg-amber-50 border-amber-300 text-amber-800',
  };
  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';


  // ─── State ───
  const [event, setEvent] = useState('');
  const [context, setContext] = useState('');
  const [results, setResults] = usePersistentState('history-today-results', null);
  const [error, setError] = useState('');
  const resultsRef = React.useRef(null);
  const [expandedParallel, setExpandedParallel] = useState(null);
  const [deeperData, setDeeperData] = useState({});
  const [deeperLoading, setDeeperLoading] = useState(null);
  const [counterData, setCounterData] = useState(null);
  const [counterLoading, setCounterLoading] = useState(false);
  const [expandedSections, setExpandedSections] = useState({});
  const [savedSearches, setSavedSearches] = usePersistentState('history-today-saved', []);

  const toggleSection = (key) => setExpandedSections(prev => ({ ...prev, [key]: !prev[key] }));

  // ─── Handlers ───
  const handleSearch = async () => {
    if (!event.trim()) { setError('Describe an event, trend, or controversy.'); return; }
    setError(''); setResults(null); setDeeperData({}); setCounterData(null); setExpandedParallel(null);
    try {
      const data = await callToolEndpoint('history-today', {
        event: event.trim(), context: context.trim() || null,
        userLanguage: navigator.language || 'en',
      });
      setResults(data);
      setTimeout(() => resultsRef.current?.scrollIntoView({ behavior: 'smooth' }), 100);
      setSavedSearches(prev => [{ event: event.trim(), date: new Date().toISOString(), parallels: data.parallels?.length || 0, preview: event.trim().slice(0, 40) }, ...prev].slice(0, 6));
    } catch (err) { setError(err.message || 'Failed to find parallels.'); }
  };

  const handleDeeper = async (idx) => {
    const p = results?.parallels?.[idx];
    if (!p) return;
    setDeeperLoading(idx); setError('');
    try {
      const data = await callToolEndpoint('history-today-deeper', {
        event: event.trim(), parallel: p, userLanguage: navigator.language || 'en',
      });
      setDeeperData(prev => ({ ...prev, [idx]: data }));
    } catch (err) { setError(err.message || 'Failed to go deeper.'); }
    setDeeperLoading(null);
  };

  const handleCounter = async () => {
    setCounterLoading(true); setError('');
    try {
      const data = await callToolEndpoint('history-today-counter', {
        event: event.trim(), parallels: results?.parallels || [],
        userLanguage: navigator.language || 'en',
      });
      setCounterData(data);
    } catch (err) { setError(err.message || 'Failed to find counter-example.'); }
    setCounterLoading(false);
  };

  const reset = () => {
    setEvent(''); setContext(''); setResults(null); setError('');
    setDeeperData({}); setCounterData(null); setExpandedParallel(null); setExpandedSections({});
  };

  const buildFullText = () => {
    if (!results) return '';
    const lines = [`HISTORY TODAY: ${results.event_summary || event}`, ''];
    results.parallels?.forEach((p, i) => {
      lines.push(`═══ PARALLEL ${i + 1}: ${p.title} (${p.period}) ═══`);
      lines.push(`Match: ${p.structural_match_score}%`, '', p.what_happened, '');
      p.structural_similarities?.forEach(s => lines.push(`• ${s.mechanism}`, `  Then: ${s.then}`, `  Now: ${s.now}`, ''));
      lines.push(`Contemporary view: ${p.contemporary_understanding}`, `What happened next: ${p.what_happened_next}`, '');
      lines.push('Where it breaks down:');
      p.where_it_breaks_down?.forEach(b => lines.push(`  ✕ ${b}`));
      if (p.surprise_insight) lines.push('', `Surprise: ${p.surprise_insight}`);
      lines.push('');
    });
    if (results.synthesis) {
      lines.push('═══ SYNTHESIS ═══', results.synthesis.collective_pattern, '',
        `Prediction: ${results.synthesis.consensus_prediction}`,
        `Wildcard: ${results.synthesis.wildcard}`,
        results.synthesis.confidence_note, '');
    }
    if (counterData) {
      lines.push(`═══ COUNTER-EXAMPLE: ${counterData.title} ═══`, counterData.what_happened_differently, '',
        `Key difference: ${counterData.why_it_diverged?.key_difference}`,
        `Implication: ${counterData.implication_for_today}`, '');
    }
    lines.push('───', 'Generated by DeftBrain · deftbrain.com');
    return lines.join('\n');
  };

  // Score bar
  const ScoreBar = ({ score }) => {
    const color = score >= 70 ? 'bg-cyan-500' : score >= 50 ? 'bg-amber-500' : 'bg-zinc-500';
    return (
      <div className={`w-full ${isDark ? 'bg-zinc-700' : 'bg-gray-200'} rounded-full h-1.5 overflow-hidden`}>
        <div className={`${color} h-1.5 rounded-full transition-all`} style={{ width: `${Math.min(100, Math.max(0, score))}%` }} />
      </div>
    );
  };

  useEffect(() => {
    const handler = (e) => {
      const tag = document.activeElement?.tagName;
      if (tag === 'INPUT' || tag === 'TEXTAREA' || tag === 'SELECT') return;
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && !loading) handleSearch();
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [loading]);

  return (
    <div>
      {/* Header */}
      <div className="mb-5">
        <h2 className={`text-2xl font-bold ${c.text}`}><span className="mr-2">{tool?.icon ?? '📅'}</span>{tool?.title || 'History Today'}</h2>
        <p className={`text-sm ${c.textMuteded}`}>{tool?.tagline || 'Find the structural historical parallel — not the obvious one'}</p>
      </div>

      {/* ═══ INPUT ═══ */}
      {!results && (
        <div className="space-y-4">
          <div className={`${c.card} ${c.border} rounded-xl shadow-lg p-6 space-y-4`}>
            <div>
              <label className={`block font-semibold ${c.text} mb-2`}>What's happening?</label>
              <textarea value={event} onChange={e => setEvent(e.target.value)} rows={3}
                placeholder="Describe a current event, trend, or controversy. Be as specific or broad as you want. e.g., 'Tech companies doing mass layoffs while reporting record profits' or 'The debate over AI regulation in Congress'"
                className={`w-full p-3 border rounded-xl outline-none text-sm resize-y focus:ring-2 focus:ring-cyan-500/30 ${c.input}`} />
            </div>
            <div>
              <label className={`block text-sm font-semibold ${c.textSecondary} mb-1.5`}>
                Specific angle <span className={`font-normal ${c.textMuteded}`}>(optional)</span>
              </label>
              <input type="text" value={context} onChange={e => setContext(e.target.value)}
                placeholder="e.g., 'I'm interested in the labor dynamics' or 'What about the regulatory side?'"
                className={`w-full p-3 border rounded-xl outline-none text-sm focus:ring-2 focus:ring-cyan-500/30 ${c.input}`} />
            </div>
          </div>

          {/* Quick examples */}
          <div className={`${c.card} ${c.border} rounded-xl shadow-lg p-5`}>
            <p className={`text-xs font-bold ${c.textMuteded} mb-2`}>TRY THESE</p>
            <div className="flex flex-wrap gap-2">
              {[
                'Social media companies testifying before Congress',
                'Countries banning TikTok over national security',
                'AI replacing white-collar jobs for the first time',
                'Housing prices making cities unaffordable for young workers',
                'A billionaire buying a major media platform',
              ].map((ex, i) => (
                <button key={i} onClick={() => setEvent(ex)}
                  className={`text-xs px-3 py-1.5 rounded-lg border ${isDark ? 'border-zinc-600 text-zinc-400 hover:border-cyan-500 hover:text-cyan-300' : 'border-gray-200 text-gray-500 hover:border-cyan-400 hover:text-cyan-600'} transition-all`}>
                  {ex}
                </button>
              ))}
            </div>
          </div>

          <button onClick={handleSearch} disabled={loading || !event.trim()}
            className={`w-full py-4 px-6 rounded-xl font-semibold text-lg transition-all flex items-center justify-center gap-2 shadow-lg ${loading ? (isDark ? 'bg-zinc-700 text-zinc-400' : 'bg-gray-200 text-gray-400') : c.btnPrimaryPrimary}`}>
            {loading ? (<><span className="inline-block animate-spin text-lg">{tool?.icon ?? '⚙️'}</span> Searching history...</>) : (<><span className="text-lg">📰</span> Find Parallels</>)}
          </button>

          {/* Saved searches */}
          {savedSearches.length > 0 && (
            <div className={`${c.card} ${c.border} rounded-xl shadow-lg p-4`}>
              <button onClick={() => toggleSection('saved')} className={`w-full flex items-center justify-between ${c.text}`}>
                <span className={`text-xs font-bold ${c.textMuteded}`}>PREVIOUS SEARCHES ({savedSearches.length})</span>
                <span className="text-xs">{expandedSections.saved ? '▲' : '▼'}</span>
              </button>
              {expandedSections.saved && (
                <div className="space-y-1.5 mt-2">
                  {savedSearches.map((s, i) => (
                    <button key={i} onClick={() => setEvent(s.event)}
                      className={`w-full text-left p-2 rounded-lg text-xs ${isDark ? 'hover:bg-zinc-700' : 'hover:bg-gray-100'} transition-colors flex items-center justify-between`}>
                      <span className={c.text}>{s.event}</span>
                      <span className={c.textMuteded}>{new Date(s.date).toLocaleDateString()}</span>
                    </button>
                  ))}
                </div>
              )}
            </div>
          )}

          {error && <div className={`p-4 rounded-xl flex items-start gap-3 ${c.danger} border`}><span>⚠️</span><p className="text-sm">{error}</p></div>}
        </div>
      )}

      {/* ═══ RESULTS ═══ */}
      {results && (
        <div className="space-y-5">
          {/* Controls */}
          <div className={`${c.card} ${c.border} rounded-xl shadow-lg p-4 flex items-center justify-between flex-wrap gap-3`}>
            <div className="flex-1 min-w-0">
              <p className={`text-sm font-semibold ${c.text} truncate`}>📰 {results.event_summary || event}</p>
              <p className={`text-xs ${c.textMuteded}`}>{results.parallels?.length || 0} parallels found</p>
            </div>
            <div className="flex items-center gap-2 flex-wrap">
              <ActionBar content={buildFullText()} title="HistoryToday" copyLabel="Copy All" />
              {!counterData && (
                <button onClick={handleCounter} disabled={counterLoading}
                  className={`flex items-center gap-1.5 px-3 py-1.5 rounded-lg text-sm font-medium ${isDark ? 'bg-amber-900/30 text-amber-300 hover:bg-amber-800/40' : 'bg-amber-50 text-amber-700 hover:bg-amber-100'}`}>
                  {counterLoading ? <span className="inline-block animate-spin">{tool?.icon ?? '⚙️'}</span> : <span>🔄</span>} Counter-Example
                </button>
              )}
              <button onClick={reset} className={`flex items-center gap-1.5 px-3 py-1.5 rounded-lg text-sm font-medium ${c.btnPrimarySecondaryondary}`}>
                <span>🔄</span> New Search
              </button>
            </div>
          </div>

          {error && <div className={`p-4 rounded-xl flex items-start gap-3 ${c.danger} border`}><span>⚠️</span><p className="text-sm">{error}</p></div>}

          {/* Parallels */}
          {results.parallels?.map((p, idx) => (
            <div key={idx} className={`${c.card} ${c.border} rounded-xl shadow-lg overflow-hidden`}>
              {/* Header */}
              <div className={`p-5 ${c.parallelCard} border-b ${c.border}`}>
                <div className="flex items-start justify-between gap-3">
                  <div className="flex-1">
                    <div className="flex items-center gap-2 mb-1">
                      <span className={`text-xs font-black ${c.textSecondary}`}>PARALLEL {idx + 1}</span>
                      <span className={`text-[10px] px-2 py-0.5 rounded-full font-bold ${p.structural_match_score >= 70 ? c.success : p.structural_match_score >= 50 ? c.warning : c.highlight} border`}>
                        {p.structural_match_score}% match
                      </span>
                    </div>
                    <h3 className={`text-lg font-bold ${c.text}`}>{p.title}</h3>
                    <p className={`text-xs ${c.textMuteded} mt-0.5`}>{p.period} · {p.region}</p>
                  </div>
                  <CopyBtn content={`${p.title} (${p.period})\n\n${p.what_happened}\n\nWhere it breaks down:\n${p.where_it_breaks_down?.map(b => `• ${b}`).join('\n')}${BRAND}`} label="Copy" />
                </div>
                <ScoreBar score={p.structural_match_score} />
              </div>

              <div className="p-5 space-y-4">
                {/* What happened */}
                <div>
                  <p className={`text-[10px] font-bold ${c.textMuteded} uppercase mb-1`}>What happened</p>
                  <p className={`text-sm ${c.text} leading-relaxed`}>{p.what_happened}</p>
                </div>

                {/* Structural similarities */}
                {p.structural_similarities?.length > 0 && (
                  <div>
                    <p className={`text-[10px] font-bold ${c.textMuteded} uppercase mb-2`}>Structural match</p>
                    <div className="space-y-2">
                      {p.structural_similarities.map((s, si) => (
                        <div key={si} className={`p-3 rounded-lg border ${c.border}`}>
                          <p className={`text-xs font-bold ${c.textSecondary} mb-1.5`}>{s.mechanism}</p>
                          <div className="grid grid-cols-2 gap-2">
                            <div className={`p-2 rounded ${isDark ? 'bg-zinc-700/50' : 'bg-gray-50'}`}>
                              <p className={`text-[9px] font-bold ${c.textMuteded}`}>THEN</p>
                              <p className={`text-xs ${c.textSecondary}`}>{s.then}</p>
                            </div>
                            <div className={`p-2 rounded ${isDark ? 'bg-cyan-900/20' : 'bg-cyan-50/80'}`}>
                              <p className={`text-[9px] font-bold ${c.textSecondary}`}>NOW</p>
                              <p className={`text-xs ${c.text}`}>{s.now}</p>
                            </div>
                          </div>
                        </div>
                      ))}
                    </div>
                  </div>
                )}

                {/* Contemporary understanding */}
                {p.contemporary_understanding && (
                  <div className={`${isDark ? 'bg-amber-900/15 border-amber-800' : 'bg-amber-50/80 border-amber-200'} border rounded-lg p-3`}>
                    <p className={`text-[10px] font-bold ${isDark ? 'text-amber-400' : 'text-amber-700'} mb-1`}>💭 HOW THEY SAW IT AT THE TIME</p>
                    <p className={`text-xs ${c.text}`}>{p.contemporary_understanding}</p>
                  </div>
                )}

                {/* What happened next */}
                {p.what_happened_next && (
                  <div className={`${c.highlight} border rounded-lg p-3`}>
                    <p className={`text-[10px] font-bold mb-1`}>⏩ WHAT HAPPENED NEXT</p>
                    <p className="text-xs">{p.what_happened_next}</p>
                  </div>
                )}

                {/* WHERE IT BREAKS DOWN — the star of the show */}
                {p.where_it_breaks_down?.length > 0 && (
                  <div className={`${c.danger} border-2 rounded-lg p-4`}>
                    <p className={`text-[10px] font-black uppercase mb-2`}>⚠️ WHERE THIS ANALOGY BREAKS DOWN</p>
                    <div className="space-y-1.5">
                      {p.where_it_breaks_down.map((b, bi) => (
                        <p key={bi} className="text-xs">✕ {b}</p>
                      ))}
                    </div>
                  </div>
                )}

                {/* Key figures */}
                {p.key_figures?.length > 0 && (
                  <div>
                    <button onClick={() => toggleSection(`fig-${idx}`)} className={`flex items-center gap-1.5 text-xs font-bold ${c.textMuteded}`}>
                      <span>👤</span> Key Figures <span>{expandedSections[`fig-${idx}`] ? '▲' : '▼'}</span>
                    </button>
                    {expandedSections[`fig-${idx}`] && (
                      <div className="flex flex-wrap gap-2 mt-2">
                        {p.key_figures.map((kf, fi) => (
                          <div key={fi} className={`text-[10px] px-2.5 py-1 rounded-lg ${c.cardAlt} border ${c.border}`}>
                            <span className={c.textSecondary}>{kf.historical}</span>
                            <span className={c.textMuteded}> ↔ </span>
                            <span className={c.text}>{kf.modern_parallel}</span>
                          </div>
                        ))}
                      </div>
                    )}
                  </div>
                )}

                {/* Surprise */}
                {p.surprise_insight && (
                  <div className={`flex items-start gap-2 ${isDark ? 'bg-cyan-900/15 border-purple-700' : 'bg-cyan-50 border-purple-200'} border rounded-lg p-3`}>
                    <span className="flex-shrink-0">💡</span>
                    <p className={`text-xs ${c.text}`}><span className="font-bold">Surprise: </span>{p.surprise_insight}</p>
                  </div>
                )}

                {/* Dig Deeper button */}
                <div className={`flex items-center gap-2 pt-2 border-t ${c.border}`}>
                  {!deeperData[idx] ? (
                    <button onClick={() => handleDeeper(idx)} disabled={deeperLoading === idx}
                      className={`flex items-center gap-1.5 text-xs font-semibold px-3 py-1.5 rounded-lg ${c.btnPrimaryPrimary} disabled:opacity-40`}>
                      {deeperLoading === idx ? (<><span className="inline-block animate-spin">{tool?.icon ?? '⚙️'}</span> Loading...</>) : (<><span>🔍</span> Dig Deeper</>)}
                    </button>
                  ) : (
                    <span className={`text-[10px] font-bold ${isDark ? 'text-green-400' : 'text-green-600'}`}>✅ Deep dive loaded below</span>
                  )}
                </div>
              </div>

              {/* ─── DEEPER EXPANSION ─── */}
              {deeperData[idx] && (
                <div className={`border-t-2 ${isDark ? 'border-cyan-700' : 'border-cyan-200'} p-5 space-y-4 ${isDark ? 'bg-zinc-800/80' : 'bg-cyan-50/30'}`}>
                  <h4 className={`font-bold ${c.textSecondary} flex items-center gap-2`}><span>🔍</span> Deep Dive: {deeperData[idx].title}</h4>

                  {/* Detailed narrative */}
                  {deeperData[idx].detailed_narrative && (
                    <div>
                      <p className={`text-[10px] font-bold ${c.textMuteded} uppercase mb-1`}>Full narrative</p>
                      <p className={`text-sm ${c.text} leading-relaxed`}>{deeperData[idx].detailed_narrative}</p>
                    </div>
                  )}

                  {/* Timeline */}
                  {deeperData[idx].timeline?.length > 0 && (
                    <div>
                      <button onClick={() => toggleSection(`tl-${idx}`)} className={`flex items-center gap-1.5 text-xs font-bold ${c.textMuteded} mb-2`}>
                        <span>📅</span> Timeline ({deeperData[idx].timeline.length} events) <span>{expandedSections[`tl-${idx}`] !== false ? '▲' : '▼'}</span>
                      </button>
                      {expandedSections[`tl-${idx}`] !== false && (
                        <div className={`space-y-0 ml-3 border-l-2 ${isDark ? 'border-cyan-700' : 'border-cyan-200'}`}>
                          {deeperData[idx].timeline.map((t, ti) => (
                            <div key={ti} className="pl-4 pb-3 relative">
                              <div className={`absolute -left-[5px] top-1.5 w-2 h-2 rounded-full ${t.modern_echo ? (isDark ? 'bg-cyan-400' : 'bg-cyan-500') : (isDark ? 'bg-zinc-500' : 'bg-gray-400')}`} />
                              <p className={`text-[10px] font-bold ${c.textSecondary}`}>{t.date}</p>
                              <p className={`text-xs font-semibold ${c.text}`}>{t.event}</p>
                              <p className={`text-[10px] ${c.textMuteded}`}>{t.significance}</p>
                              {t.modern_echo && <p className={`text-[10px] ${isDark ? 'text-cyan-400' : 'text-cyan-600'} mt-0.5 italic`}>↪ Today: {t.modern_echo}</p>}
                            </div>
                          ))}
                        </div>
                      )}
                    </div>
                  )}

                  {/* Turning points */}
                  {deeperData[idx].turning_points?.length > 0 && (
                    <div>
                      <p className={`text-[10px] font-bold ${c.textMuteded} uppercase mb-2`}>🔀 Turning points</p>
                      <div className="space-y-2">
                        {deeperData[idx].turning_points.map((tp, ti) => (
                          <div key={ti} className={`p-3 rounded-lg border ${c.border}`}>
                            <p className={`text-xs font-bold ${c.text} mb-1`}>{tp.moment}</p>
                            <p className={`text-xs ${c.textSecondary}`}><span className="font-semibold">Happened:</span> {tp.what_actually_happened}</p>
                            <p className={`text-xs ${c.textMuteded}`}><span className="font-semibold">Could have:</span> {tp.alternative}</p>
                            <p className={`text-[10px] ${c.textSecondary} mt-1`}>Why: {tp.why_it_went_this_way}</p>
                          </div>
                        ))}
                      </div>
                    </div>
                  )}

                  {/* Information environment */}
                  {deeperData[idx].information_environment && (
                    <div>
                      <button onClick={() => toggleSection(`info-${idx}`)} className={`flex items-center gap-1.5 text-xs font-bold ${c.textMuteded}`}>
                        <span>📡</span> Information Environment <span>{expandedSections[`info-${idx}`] ? '▲' : '▼'}</span>
                      </button>
                      {expandedSections[`info-${idx}`] && (
                        <div className="grid grid-cols-2 gap-2 mt-2">
                          {[
                            { label: 'What they knew', val: deeperData[idx].information_environment.what_people_knew },
                            { label: 'What they were told', val: deeperData[idx].information_environment.what_they_were_told },
                            { label: 'What they believed', val: deeperData[idx].information_environment.what_they_believed },
                            { label: 'What was actually true', val: deeperData[idx].information_environment.what_was_actually_true },
                          ].map((item, ii) => item.val ? (
                            <div key={ii} className={`p-2.5 rounded-lg ${c.cardAlt}`}>
                              <p className={`text-[9px] font-bold ${c.textMuteded} uppercase`}>{item.label}</p>
                              <p className={`text-xs ${c.text} mt-0.5`}>{item.val}</p>
                            </div>
                          ) : null)}
                        </div>
                      )}
                    </div>
                  )}

                  {/* Echoing quotes */}
                  {deeperData[idx].echoing_quotes?.length > 0 && (
                    <div>
                      <p className={`text-[10px] font-bold ${c.textMuteded} uppercase mb-2`}>🗣️ Quotes that echo today</p>
                      <div className="space-y-2">
                        {deeperData[idx].echoing_quotes.map((q, qi) => (
                          <div key={qi} className={`${c.cardAlt} border rounded-lg p-3`}>
                            <p className={`text-sm italic ${c.text}`}>"{q.quote}"</p>
                            <p className={`text-[10px] ${c.textMuteded} mt-1`}>— {q.speaker}, {q.date}</p>
                            <p className={`text-[10px] ${c.textSecondary} mt-1`}>↪ {q.modern_resonance}</p>
                          </div>
                        ))}
                      </div>
                    </div>
                  )}

                  {/* Lessons */}
                  {deeperData[idx].lessons_drawn && (
                    <div className="grid grid-cols-1 sm:grid-cols-3 gap-2">
                      {[
                        { label: 'Lesson drawn at the time', val: deeperData[idx].lessons_drawn.at_the_time, icon: '📢' },
                        { label: 'Lesson in hindsight', val: deeperData[idx].lessons_drawn.in_hindsight, icon: '🔭' },
                        { label: 'What applies now', val: deeperData[idx].lessons_drawn.applicable_now, icon: '🎯' },
                      ].map((item, li) => item.val ? (
                        <div key={li} className={`p-3 rounded-lg border ${c.border}`}>
                          <p className={`text-[10px] font-bold ${c.textMuteded} mb-1`}>{item.icon} {item.label}</p>
                          <p className={`text-xs ${c.text}`}>{item.val}</p>
                        </div>
                      ) : null)}
                    </div>
                  )}
                </div>
              )}
            </div>
          ))}

          {/* ─── COUNTER-EXAMPLE ─── */}
          {counterData && (
            <div className={`${c.card} ${c.border} rounded-xl shadow-lg overflow-hidden`}>
              <div className={`p-5 ${c.counter} border-b ${c.border}`}>
                <div className="flex items-center gap-2 mb-1">
                  <span className={`text-xs font-black`}>🔄 COUNTER-EXAMPLE</span>
                  <span className={`text-[10px] px-2 py-0.5 rounded-full font-bold border ${counterData.hope_or_warning === 'hope' ? c.success : c.danger}`}>
                    {counterData.hope_or_warning === 'hope' ? '🌿 Hopeful' : '⚠️ Warning'}
                  </span>
                </div>
                <h3 className={`text-lg font-bold ${c.text}`}>{counterData.title}</h3>
                <p className={`text-xs ${c.textMuteded}`}>{counterData.period} · {counterData.region}</p>
              </div>
              <div className="p-5 space-y-4">
                {counterData.similar_starting_conditions?.length > 0 && (
                  <div>
                    <p className={`text-[10px] font-bold ${c.textMuteded} uppercase mb-1`}>Same starting conditions</p>
                    <div className="flex flex-wrap gap-1.5">
                      {counterData.similar_starting_conditions.map((cond, i) => (
                        <span key={i} className={`text-[10px] px-2 py-0.5 rounded-full border ${c.highlight}`}>{cond}</span>
                      ))}
                    </div>
                  </div>
                )}
                <div>
                  <p className={`text-[10px] font-bold ${c.textMuteded} uppercase mb-1`}>What happened differently</p>
                  <p className={`text-sm ${c.text} leading-relaxed`}>{counterData.what_happened_differently}</p>
                </div>
                {counterData.why_it_diverged && (
                  <div className={`p-4 rounded-lg border-2 ${isDark ? 'border-amber-700 bg-amber-900/10' : 'border-amber-200 bg-amber-50'}`}>
                    <p className={`text-xs font-bold ${c.text} mb-1`}>🔑 {counterData.why_it_diverged.key_difference}</p>
                    <p className={`text-xs ${c.textSecondary}`}>{counterData.why_it_diverged.structural_reason}</p>
                    <p className={`text-[10px] ${c.textMuteded} mt-1 italic`}>{counterData.why_it_diverged.was_it_luck_or_choice}</p>
                  </div>
                )}
                {counterData.implication_for_today && (
                  <div className={`${c.cardAlt} border rounded-lg p-3`}>
                    <p className={`text-[10px] font-bold ${c.textSecondary} mb-1`}>FOR TODAY</p>
                    <p className={`text-xs ${c.text}`}>{counterData.implication_for_today}</p>
                  </div>
                )}
                {counterData.key_takeaway && (
                  <p className={`text-sm font-semibold ${c.text} text-center py-2`}>💎 {counterData.key_takeaway}</p>
                )}
              </div>
            </div>
          )}

          {/* ─── SYNTHESIS ─── */}
          {results.synthesis && (
            <div className={`${c.card} ${c.border} rounded-xl shadow-lg p-6 space-y-4`}>
              <h3 className={`font-bold ${c.text} flex items-center gap-2`}><span className="text-lg">🔮</span> Synthesis</h3>
              {results.synthesis.collective_pattern && (
                <p className={`text-sm ${c.text} leading-relaxed`}>{results.synthesis.collective_pattern}</p>
              )}
              {results.synthesis.consensus_prediction && (
                <div className={`${c.cardAlt} border rounded-lg p-4`}>
                  <p className={`text-[10px] font-bold ${c.textSecondary} mb-1`}>IF HISTORY RHYMES</p>
                  <p className={`text-sm font-semibold ${c.text}`}>{results.synthesis.consensus_prediction}</p>
                </div>
              )}
              {results.synthesis.wildcard && (
                <div className={`${c.warning} border rounded-lg p-3 flex items-start gap-2`}>
                  <span className="flex-shrink-0">🃏</span>
                  <div>
                    <p className={`text-[10px] font-bold mb-0.5`}>WILDCARD</p>
                    <p className="text-xs">{results.synthesis.wildcard}</p>
                  </div>
                </div>
              )}
              {results.synthesis.confidence_note && (
                <p className={`text-xs ${c.textMuteded} italic`}>⚖️ {results.synthesis.confidence_note}</p>
              )}
            </div>
          )}

          {/* ─── FURTHER READING ─── */}
          {results.further_reading?.length > 0 && (
            <div className={`${c.card} ${c.border} rounded-xl shadow-lg p-5`}>
              <h4 className={`font-bold ${c.text} mb-3 flex items-center gap-2`}><span>📚</span> Go Deeper</h4>
              <div className="space-y-2">
                {results.further_reading.map((fr, i) => (
                  <div key={i} className={`flex items-start gap-3 p-2.5 rounded-lg ${c.cardAlt}`}>
                    <span className="text-lg flex-shrink-0">📖</span>
                    <div>
                      <p className={`text-sm font-semibold ${c.text}`}>{fr.title}</p>
                      <p className={`text-xs ${c.textMuteded}`}>by {fr.author}</p>
                      <p className={`text-xs ${c.textSecondary} mt-0.5`}>{fr.why}</p>
                    </div>
                  </div>
                ))}
              </div>
            </div>
          )}

          {/* Cross-references */}
          <div className={`p-4 rounded-xl text-center ${isDark ? 'bg-zinc-800/50' : 'bg-gray-50'}`}>
            <p className={`text-xs ${c.textMuteded}`}>
              Want to decode the rhetoric around this event? <a href="/JargonAssassin" target="_blank" rel="noopener noreferrer" className={`font-semibold ${isDark ? 'text-cyan-400' : 'text-cyan-600'}`}>Jargon Assassin</a> cuts through the language.
              Analyzing a decision? <a href="/DecisionCoach" target="_blank" rel="noopener noreferrer" className={`font-semibold ${isDark ? 'text-cyan-400' : 'text-cyan-600'} ml-1`}>Decision Coach</a> can help.
            </p>
          </div>
        </div>
      )}
        <div className={`mt-6 pt-4 border-t text-sm ${c.border} ${c.textMuted}`}>
          <p className="mb-2 font-medium">You might also like:</p>
          <div className="flex flex-wrap gap-2">
            {[{slug:'brain-roulette',label:'🎲 Brain Roulette'},{slug:'fact-check',label:'🔍 Fact Check'},{slug:'time-warp',label:'⏰ Time Warp'}].map(({slug,label})=>(
              <a key={slug} href={`${slug}`} className={linkStyle}>{label}</a>
            ))}
          </div>
        </div>
    </div>
  );
};

