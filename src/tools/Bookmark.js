import React, { useState, useEffect, useRef, useCallback } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { usePersistentState } from '../hooks/usePersistentState';
import { useRegisterActions } from '../components/ActionBarContext';
import { useTranslation } from '../i18n/useTranslation';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

// ════════════════════════════════════════════════════════════
// CONSTANTS
// ════════════════════════════════════════════════════════════
// label/desc/placeholder/stoppedPlaceholder are now key-driven (t(item.*Key)).
const MEDIA_TYPES = [
  { id: 'show', labelKey: 'bk_mt_show_label', descKey: 'bk_mt_show_desc', phKey: 'bk_mt_show_ph', stoppedPhKey: 'bk_mt_show_stopped_ph' },
  { id: 'book', labelKey: 'bk_mt_book_label', descKey: 'bk_mt_book_desc', phKey: 'bk_mt_book_ph', stoppedPhKey: 'bk_mt_book_stopped_ph' },
  { id: 'game', labelKey: 'bk_mt_game_label', descKey: 'bk_mt_game_desc', phKey: 'bk_mt_game_ph', stoppedPhKey: 'bk_mt_game_stopped_ph' },
  { id: 'sports', labelKey: 'bk_mt_sports_label', descKey: 'bk_mt_sports_desc', phKey: 'bk_mt_sports_ph', stoppedPhKey: 'bk_mt_sports_stopped_ph' },
];

// value is the backend enum (English) — labelKey/descKey are localized.
const SPOILER_LEVELS = [
  { value: 'strict', labelKey: 'bk_sl_strict_label', descKey: 'bk_sl_strict_desc' },
  { value: 'moderate', labelKey: 'bk_sl_moderate_label', descKey: 'bk_sl_moderate_desc' },
  { value: 'don\'t-care', labelKey: 'bk_sl_open_label', descKey: 'bk_sl_open_desc' },
];

// Example seeds — key-driven, resolved through t() in loadExample.
const EXAMPLES = {
  show: { titleKey: 'bk_ex_show_title', stoppedKey: 'bk_ex_show_stopped', rememberKey: 'bk_ex_show_remember' },
  book: { titleKey: 'bk_ex_book_title', stoppedKey: 'bk_ex_book_stopped', rememberKey: 'bk_ex_book_remember' },
  game: { titleKey: 'bk_ex_game_title', stoppedKey: 'bk_ex_game_stopped', rememberKey: 'bk_ex_game_remember' },
  sports: { titleKey: 'bk_ex_sports_title', stoppedKey: 'bk_ex_sports_stopped', rememberKey: 'bk_ex_sports_remember' },
};

// ════════════════════════════════════════════════════════════
// MAIN COMPONENT
// ════════════════════════════════════════════════════════════
const Bookmark = ({ tool }) => {
  const { callToolEndpoint, loading } = useClaudeAPI();
  const { isDark } = useTheme();
  const { t } = useTranslation();

  const c = {
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:         isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20'
                          : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-400' : 'text-gray-500',
    labelText:     isDark ? 'text-zinc-200' : 'text-gray-700',
    accentTxt:     isDark ? 'text-cyan-400' : 'text-cyan-600',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white'
                          : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200'
                          : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200'
                          : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200'
                          : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200'
                          : 'bg-red-50 border-red-200 text-red-800',
    pillActive:    isDark ? 'border-cyan-500 bg-cyan-900/30 text-cyan-200'
                          : 'border-cyan-600 bg-cyan-100 text-cyan-900',
    required:      isDark ? 'text-amber-400' : 'text-amber-700',
    // Tool-specific
    modeActive:    isDark ? 'bg-cyan-700 border-cyan-500 text-white' : 'bg-cyan-600 border-cyan-600 text-white',
    modeInactive:  isDark ? 'bg-zinc-800 border-zinc-700 text-zinc-400 hover:border-zinc-500' : 'bg-white border-gray-200 text-gray-500 hover:border-gray-400',
    badge:         isDark ? 'bg-zinc-700 text-zinc-300' : 'bg-gray-100 text-gray-600',
    tip:           isDark ? 'bg-cyan-900/20 border-cyan-700' : 'bg-cyan-50 border-cyan-300',
    tipFg:         isDark ? 'text-cyan-200' : 'text-cyan-900',
    inset:         isDark ? 'bg-zinc-700/40' : 'bg-slate-100',
    charCard:      isDark ? 'bg-zinc-700/30' : 'bg-slate-50',
    threadCard:    isDark ? 'bg-zinc-700/30' : 'bg-slate-50',
    histBg:        isDark ? 'bg-zinc-800/60 border-zinc-700' : 'bg-slate-50 border-gray-200',
    histAccent:    isDark ? 'text-cyan-400' : 'text-cyan-600',
    btnDisabled:   'bg-zinc-400 text-zinc-200 cursor-not-allowed opacity-60',
    btnGhost:      isDark ? 'bg-transparent text-zinc-500 hover:bg-zinc-700' : 'bg-transparent text-gray-400 hover:bg-gray-100',
    dangerFg:      isDark ? 'text-red-300' : 'text-red-700',
    successFg:     isDark ? 'text-emerald-300' : 'text-emerald-800',
    mustWatchBg:   isDark ? 'bg-amber-900/20 border-amber-700' : 'bg-amber-50 border-amber-300',
  };
  c.textMuteded = c.textMuted;
  c.label = c.labelText;

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-700 hover:text-cyan-800 underline underline-offset-2';

  // ── useState ──────────────────────────────────────────────
  const [showHistory, setShowHistory] = useState(false);
  const [mediaType, setMediaType] = useState('show');
  const [whatYouRemember, setWhatYouRemember] = useState('');
  const [spoilerLevel, setSpoilerLevel] = useState('strict');
  const [specificQuestions, setSpecificQuestions] = useState('');
  const [results, setResults] = useState(null);
  const [error, setError] = useState('');
  const [showChars, setShowChars] = useState(true);
  const [showThreads, setShowThreads] = useState(true);
  const [showAnswers, setShowAnswers] = useState(true);
  const [showMustWatch, setShowMustWatch] = useState(true);
  const [showRoster, setShowRoster] = useState(false);
  const [showStorylines, setShowStorylines] = useState(true);

  // ── useRef ────────────────────────────────────────────────
  const resultsRef = useRef(null);
  const handleSubmitRef = useRef(null);
  const canSubmitRef = useRef(false);

  // ── usePersistentState ────────────────────────────────────
  const [sessionHistory, setSessionHistory] = usePersistentState('bookmark-history', []);
  const [title, setTitle] = usePersistentState('bookmark-title', '');
  const [stoppedAt, setStoppedAt] = usePersistentState('bookmark-stopped-at', '');

  // ── Handlers ──────────────────────────────────────────────
  const recall = useCallback(async () => {
    if (!title.trim() || !stoppedAt.trim()) { setError(t('bk_err_missing')); return; }
    setError(''); setResults(null);
    try {
      const data = await callToolEndpoint('bookmark', {
        mediaType, title: title.trim(), stoppedAt: stoppedAt.trim(),
        whatYouRemember: whatYouRemember.trim() || null,
        spoilerLevel,
        specificQuestions: specificQuestions.trim() || null,
      });
      setResults(data);
      saveToHistory(data);
    } catch (err) { setError(err.message || t('bk_err_failed')); }
  }, [mediaType, title, stoppedAt, whatYouRemember, spoilerLevel, specificQuestions, callToolEndpoint, t]);

  const handleReset = useCallback(() => {
    setTitle(''); setStoppedAt(''); setWhatYouRemember(''); setSpecificQuestions('');
    setResults(null); setError('');
  }, []);

  const loadExample = useCallback(() => {
    const ex = EXAMPLES[mediaType];
    if (ex) { setTitle(t(ex.titleKey)); setStoppedAt(t(ex.stoppedKey)); setWhatYouRemember(ex.rememberKey ? t(ex.rememberKey) : ''); }
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [mediaType, t]);

  const saveToHistory = useCallback((data) => {
    const entry = { id: 'bm_' + Date.now(), date: new Date().toISOString(), type: mediaType, title: data.title || title, stoppedAt: data.stopped_at || stoppedAt, preview: data.the_story_so_far?.slice(0, 80) || '', results: data };
    setSessionHistory(prev => [entry, ...prev].slice(0, 6));
  }, [mediaType, title, stoppedAt, setSessionHistory]);

  // ── buildFullCopy ─────────────────────────────────────────
  const buildFullCopy = useCallback(() => {
    if (!results) return '';
    const isSports = results?.media_type === 'sports';
    const isGame = results?.media_type === 'game';
    const emoji = { show: '📺', book: '📖', game: '🎮', sports: '🏟️' }[results?.media_type] || '🔖';
    const lines = [emoji + ' ' + t('bk_copy_header', { title: results?.title }), t('bk_copy_stopped', { at: results?.stopped_at }), ''];
    if (results?.the_story_so_far) lines.push(t('bk_copy_recap'), results?.the_story_so_far, '');
    if (results?.where_you_left_off) lines.push(t('bk_copy_last_scene', { scene: results?.where_you_left_off }), '');
    if (results?.vibe_check) lines.push(t('bk_vibe_check'), results?.vibe_check, '');
    if (results?.characters?.length) {
      lines.push(t('bk_copy_characters'));
      results?.characters?.forEach(ch => lines.push('  • ' + ch.name + ': ' + ch.refresher));
      lines.push('');
    }
    if (results?.active_threads?.length) {
      lines.push(t('bk_copy_threads'));
      results?.active_threads?.forEach(th => lines.push('  • ' + th.thread + ': ' + th.status));
      lines.push('');
    }
    // Game-specific
    if (isGame && results?.gameplay_refresh) {
      lines.push(t('bk_gameplay_refresh'));
      if (results?.gameplay_refresh?.mechanics_unlocked) lines.push('  ' + t('bk_unlocked') + ' ' + results?.gameplay_refresh?.mechanics_unlocked);
      if (results?.gameplay_refresh?.current_objective) lines.push('  ' + t('bk_objective') + ' ' + results?.gameplay_refresh?.current_objective);
      if (results?.gameplay_refresh?.difficulty_note) lines.push('  ' + results?.gameplay_refresh?.difficulty_note);
      lines.push('');
    }
    // Book-specific
    if (results?.world_building_refresh) lines.push(t('bk_world_refresh'), results?.world_building_refresh, '');
    // Sports-specific
    if (isSports && results?.standings_context) lines.push(t('bk_standings'), results?.standings_context, '');
    if (isSports && results?.key_storylines?.length) {
      lines.push(t('bk_key_storylines'));
      results?.key_storylines?.forEach(s => lines.push('  • ' + s.storyline + ': ' + s.what_happened));
      lines.push('');
    }
    if (isSports && results?.roster_changes?.length) {
      lines.push(t('bk_roster_changes'));
      results?.roster_changes?.forEach(r => lines.push('  • ' + r.change + (r.impact ? ' — ' + r.impact : '')));
      lines.push('');
    }
    if (isSports && results?.must_watch_games?.length) {
      lines.push(t('bk_must_watch'));
      results?.must_watch_games?.forEach(g => lines.push('  • ' + g.game + (g.spoiler_level === 'outcome_unknown' ? ' (' + t('bk_watch_blind') + ')' : '') + ': ' + g.why));
      lines.push('');
    }
    if (isSports && results?.conversation_ready) lines.push(t('bk_copy_talking', { points: results?.conversation_ready }), '');
    // Answers
    if (results?.answers?.length) {
      lines.push(t('bk_your_questions'));
      results?.answers?.forEach(a => { lines.push('  ' + t('bk_question_prefix', { question: a.question })); lines.push('  ' + a.answer); });
      lines.push('');
    }
    if (results?.worth_continuing) lines.push(t('bk_worth_continuing'), results?.worth_continuing, '');
    const reentry = results?.reading_tip || results['re-entry_tip'];
    if (reentry) lines.push(t('bk_getting_back_in'), reentry, '');
    lines.push(BRAND);
    return lines.join('\n');
  }, [results, t]);

  // Ref assignments — always fresh
  handleSubmitRef.current = recall;
  canSubmitRef.current = !!(title.trim() && stoppedAt.trim());

  useRegisterActions(buildFullCopy(), tool?.title || 'Bookmark');

  // ── Scroll ────────────────────────────────────────────────
  useEffect(() => {
    if (!results || !resultsRef.current) return;
    const t = setTimeout(() => resultsRef.current?.scrollIntoView({ behavior: 'smooth', block: 'start' }), 200);
    return () => clearTimeout(t);
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [results]);

  // ── Keyboard handler ──────────────────────────────────────
  useEffect(() => {
    const handler = (e) => {
      const tag = document.activeElement?.tagName;
      if (tag === 'SELECT') return;
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && !loading && canSubmitRef.current)
        handleSubmitRef.current?.();
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [loading]);

  // ══════════════════════════════════════════
  // RENDER HELPERS
  // ══════════════════════════════════════════
  const Section = ({ title: sectionTitle, emoji, open, onToggle, badge, children }) => (
    <div className={c.card + ' ' + c.border + ' border rounded-xl overflow-hidden'}>
      <button onClick={onToggle} className="w-full flex items-center justify-between p-5 text-start hover:opacity-80">
        <div className="flex items-center gap-3">
          <span className="text-lg">{emoji}</span>
          <span className={'text-base font-semibold ' + c.text}>{sectionTitle}</span>
          {badge && <span className={'text-xs px-2 py-0.5 rounded-full ' + c.badge}>{badge}</span>}
        </div>
        <span className={'text-xs ' + c.textMuted}>{open ? '▲' : '▼'}</span>
      </button>
      {open && <div className={'px-5 pb-5 border-t ' + c.border}>{children}</div>}
    </div>
  );

  const mt = MEDIA_TYPES.find(m => m.id === mediaType) || MEDIA_TYPES[0];

  // ══════════════════════════════════════════
  // INPUT
  // ══════════════════════════════════════════
  const renderInput = () => (
    <div className={`${c.card} border ${c.border} rounded-xl shadow-sm p-5 space-y-4`}>
      {/* Header */}
      <div className="pb-3 border-b border-zinc-500">
        <h2 className={`text-xl font-bold ${c.text}`}>
          <span className="me-2">{tool?.icon ?? '🔖'}</span>{tool?.title ?? 'Bookmark'}
        </h2>
        <p className={`text-sm ${c.textSecondary}`}>{tool?.tagline ?? t('bk_tagline')}</p>
        <button onClick={loadExample} disabled={loading} style={{ backgroundColor: (tool?.headerColor ?? '#888888') + '80' }} className={`mt-1.5 px-2.5 py-0.5 rounded-full text-xs font-medium border disabled:opacity-40 ${isDark ? 'text-white border-white/40' : 'text-gray-800 border-transparent'}`}>{t('try_example')}</button>
      </div>

      {/* Media type selector */}
      <div className="grid grid-cols-2 sm:grid-cols-4 gap-2">
        {MEDIA_TYPES.map(m => (
          <button key={m.id} onClick={() => { setMediaType(m.id); setError(''); }}
            className={'py-3 px-2 rounded-xl border-2 text-center transition-all ' + (mediaType === m.id ? c.modeActive : c.modeInactive)}>
            <span className="text-sm font-bold block">{t(m.labelKey)}</span>
            <span className={'text-[10px] block mt-0.5 ' + (mediaType === m.id ? 'text-white/70' : c.textMuted)}>{t(m.descKey)}</span>
          </button>
        ))}
      </div>

      {/* Main inputs */}
      <div className="space-y-3">
        <div>
          <label className={'text-xs font-bold ' + c.textSecondary + ' uppercase tracking-wide mb-1 block'}>
            {mediaType === 'sports' ? t('bk_label_team') : t('bk_label_title', { type: t(mt.labelKey).split(' ')[0] })}{' '}
            <span className={c.required}>*</span>
          </label>
          <input type="text" value={title} onChange={e => setTitle(e.target.value)}
            onKeyDown={e => { if (e.key === 'Enter' && title.trim() && stoppedAt.trim() && !loading) recall(); }}
            placeholder={t(mt.phKey)} className={'w-full px-4 py-3 border-2 rounded-xl ' + c.input + ' outline-none focus:ring-2 text-base'} />
        </div>
        <div>
          <label className={'text-xs font-bold ' + c.textSecondary + ' uppercase tracking-wide mb-1 block'}>
            {mediaType === 'sports' ? t('bk_label_when_stopped') : t('bk_label_where_stopped')}{' '}
            <span className={c.required}>*</span>
          </label>
          <input type="text" value={stoppedAt} onChange={e => setStoppedAt(e.target.value)}
            onKeyDown={e => { if (e.key === 'Enter' && title.trim() && stoppedAt.trim() && !loading) recall(); }}
            placeholder={t(mt.stoppedPhKey)} className={'w-full px-4 py-2.5 rounded-xl border text-base ' + c.input + ' outline-none'} />
        </div>
        <div>
          <label className={'text-xs font-bold ' + c.textSecondary + ' uppercase tracking-wide mb-1 block'}>{t('bk_label_remember')}</label>
          <input type="text" value={whatYouRemember} onChange={e => setWhatYouRemember(e.target.value)}
            placeholder={t('bk_ph_remember')} className={'w-full px-4 py-2.5 rounded-xl border text-base ' + c.input + ' outline-none'} />
        </div>
        <div>
          <label className={'text-xs font-bold ' + c.textSecondary + ' uppercase tracking-wide mb-1 block'}>{t('bk_label_questions')}</label>
          <input type="text" value={specificQuestions} onChange={e => setSpecificQuestions(e.target.value)}
            onKeyDown={e => { if (e.key === 'Enter' && title.trim() && stoppedAt.trim() && !loading) recall(); }}
            placeholder={mediaType === 'sports' ? t('bk_ph_questions_sports') : t('bk_ph_questions_default')}
            className={'w-full px-4 py-2.5 rounded-xl border text-base ' + c.input + ' outline-none'} />
        </div>
      </div>

      {/* Spoiler level */}
      <div>
        <label className={'text-xs font-bold ' + c.textSecondary + ' uppercase tracking-wide mb-2 block'}>{t('bk_label_spoiler')}</label>
        <div className="grid grid-cols-1 sm:grid-cols-3 gap-2">
          {SPOILER_LEVELS.map(s => (
            <button key={s.value} onClick={() => setSpoilerLevel(s.value)}
              className={'p-3 rounded-xl border-2 text-start transition-all ' + (spoilerLevel === s.value ? c.pillActive : c.card + ' ' + c.border + ' hover:border-gray-400')}>
              <span className={'text-sm font-medium block ' + (spoilerLevel === s.value ? '' : c.textSecondary)}>{t(s.labelKey)}</span>
              <span className={'text-[10px] ' + c.textMuted}>{t(s.descKey)}</span>
            </button>
          ))}
        </div>
      </div>

      <button onClick={recall} disabled={loading || !title.trim() || !stoppedAt.trim()}
        className={'flex-1 py-4 rounded-2xl text-sm font-bold flex items-center justify-center gap-2 transition-all disabled:opacity-40 ' + (loading || !title.trim() || !stoppedAt.trim() ? c.btnDisabled : c.btnPrimary)}>
        {loading ? <><span className="animate-spin inline-block">{tool?.icon ?? '🔖'}</span> {t('bk_recalling')}</> : <><span>{tool?.icon ?? '🔖'}</span> {t('bk_where_was_i')}</>}
      </button>
    </div>
  );

  // ══════════════════════════════════════════
  // RESULTS — shared
  // ══════════════════════════════════════════
  const renderResults = () => {
    if (!results) return null;
    const isSports = results?.media_type === 'sports';
    const isGame = results?.media_type === 'game';
    const emoji = { show: '📺', book: '📖', game: '🎮', sports: '🏟️' }[results?.media_type] || '🔖';

    return (
      <div ref={resultsRef} className="space-y-4 mt-4">
        {/* Header */}
        <div className={'p-5 rounded-2xl border-2 ' + c.tip}>
          <div className="flex items-center gap-2 mb-2">
            <span className="text-lg">{emoji}</span>
            <span className={'text-sm font-bold ' + c.tipFg}>{results?.title}</span>
            {results?.confidence && results?.confidence !== 'high' && (
              <span className={'text-xs px-2 py-0.5 rounded-full ' + c.badge}>{t('bk_confidence_badge', { level: results?.confidence })}</span>
            )}
          </div>
          <p className={'text-xs ' + c.tipFg + ' mb-2'}>{t('bk_stopped_at', { at: results?.stopped_at })}</p>
          {results?.confidence_note && <p className={'text-xs ' + c.textMuted + ' italic'}>{results?.confidence_note}</p>}
        </div>

        {/* The story so far */}
        {results?.the_story_so_far && (
          <div className={c.card + ' ' + c.border + ' border rounded-xl p-5'}>
            <p className={'text-xs font-bold ' + c.textMuted + ' uppercase mb-3'}>📖 {isSports ? t('bk_season_so_far') : t('bk_story_so_far')}</p>
            <p className={'text-sm leading-relaxed whitespace-pre-wrap ' + c.text}>{results?.the_story_so_far}</p>
          </div>
        )}

        {/* Where you left off */}
        {results?.where_you_left_off && (
          <div className={'p-4 rounded-xl border ' + c.success}>
            <p className={'text-xs font-bold ' + c.successFg + ' mb-1'}>{t('bk_last_scene')}</p>
            <p className={'text-sm ' + c.successFg + ' italic'}>{results?.where_you_left_off}</p>
          </div>
        )}

        {/* Vibe check */}
        {results?.vibe_check && (
          <div className={'p-4 rounded-xl ' + c.inset}>
            <p className={'text-xs font-bold ' + c.textMuted + ' mb-1'}>{t('bk_vibe_check')}</p>
            <p className={'text-sm ' + c.text}>{results?.vibe_check}</p>
          </div>
        )}

        {/* Characters */}
        {results?.characters?.length > 0 && (
          <Section title={t('bk_characters')} emoji="👥" open={showChars} onToggle={() => setShowChars(!showChars)} badge={results?.characters?.length + ''}>
            <div className="space-y-3 mt-4">
              {results?.characters?.map((ch, idx) => (
                <div key={idx} className={'p-4 rounded-xl border ' + c.border + ' ' + c.charCard}>
                  <p className={'text-sm font-bold ' + c.text + ' mb-1'}>{ch.name}</p>
                  <p className={'text-xs ' + c.textSecondary + ' mb-1'}>{ch.refresher}</p>
                  {ch.relationships && <p className={'text-xs ' + c.textMuted}>🔗 {ch.relationships}</p>}
                  {ch.last_seen && <p className={'text-xs ' + c.textMuted + ' mt-1'}>{t('bk_last_seen', { where: ch.last_seen })}</p>}
                </div>
              ))}
            </div>
          </Section>
        )}

        {/* Active threads */}
        {results?.active_threads?.length > 0 && (
          <Section title={t('bk_active_threads')} emoji="🧵" open={showThreads} onToggle={() => setShowThreads(!showThreads)} badge={results?.active_threads?.length + ''}>
            <div className="space-y-3 mt-4">
              {results?.active_threads?.map((th, idx) => (
                <div key={idx} className={'p-4 rounded-xl border ' + c.border + ' ' + c.threadCard}>
                  <p className={'text-sm font-bold ' + c.text + ' mb-1'}>{th.thread}</p>
                  <p className={'text-xs ' + c.textSecondary}>{th.status}</p>
                  {th.tension && <p className={'text-xs ' + c.tipFg + ' mt-1 italic'}>❓ {th.tension}</p>}
                </div>
              ))}
            </div>
          </Section>
        )}

        {/* Game-specific: Gameplay refresh */}
        {isGame && results?.gameplay_refresh && (
          <div className={c.card + ' ' + c.border + ' border rounded-xl p-5'}>
            <p className={'text-xs font-bold ' + c.textMuted + ' uppercase mb-3'}>{t('bk_gameplay_refresh')}</p>
            {results?.gameplay_refresh?.mechanics_unlocked && <p className={'text-sm ' + c.text + ' mb-2'}>🔓 <strong>{t('bk_unlocked')}</strong> {results?.gameplay_refresh?.mechanics_unlocked}</p>}
            {results?.gameplay_refresh?.current_objective && <p className={'text-sm ' + c.text + ' mb-2'}>🎯 <strong>{t('bk_objective')}</strong> {results?.gameplay_refresh?.current_objective}</p>}
            {results?.gameplay_refresh?.difficulty_note && <p className={'text-xs ' + c.textMuted + ' italic'}>⚠️ {results?.gameplay_refresh?.difficulty_note}</p>}
          </div>
        )}

        {/* Book-specific: World building */}
        {results?.world_building_refresh && (
          <div className={c.card + ' ' + c.border + ' border rounded-xl p-5'}>
            <p className={'text-xs font-bold ' + c.textMuted + ' uppercase mb-3'}>{t('bk_world_refresh')}</p>
            <p className={'text-sm ' + c.text}>{results?.world_building_refresh}</p>
          </div>
        )}

        {/* Sports-specific: Standings */}
        {isSports && results?.standings_context && (
          <div className={c.card + ' ' + c.border + ' border rounded-xl p-5'}>
            <p className={'text-xs font-bold ' + c.textMuted + ' uppercase mb-3'}>{t('bk_standings')}</p>
            <p className={'text-sm ' + c.text}>{results?.standings_context}</p>
          </div>
        )}

        {/* Sports: Key storylines */}
        {isSports && results?.key_storylines?.length > 0 && (
          <Section title={t('bk_key_storylines')} emoji="📰" open={showStorylines} onToggle={() => setShowStorylines(!showStorylines)} badge={results?.key_storylines?.length + ''}>
            <div className="space-y-3 mt-4">
              {results?.key_storylines?.map((s, idx) => (
                <div key={idx} className={'p-4 rounded-xl border ' + c.border + ' ' + c.threadCard}>
                  <p className={'text-sm font-bold ' + c.text + ' mb-1'}>{s.storyline}</p>
                  <p className={'text-xs ' + c.textSecondary}>{s.what_happened}</p>
                  {s.why_it_matters && <p className={'text-xs ' + c.tipFg + ' mt-1'}>💡 {s.why_it_matters}</p>}
                </div>
              ))}
            </div>
          </Section>
        )}

        {/* Sports: Roster changes */}
        {isSports && results?.roster_changes?.length > 0 && (
          <Section title={t('bk_roster_changes')} emoji="🔄" open={showRoster} onToggle={() => setShowRoster(!showRoster)} badge={results?.roster_changes?.length + ''}>
            <div className="space-y-2 mt-4">
              {results?.roster_changes?.map((r, idx) => (
                <div key={idx} className={'p-3 rounded-lg ' + c.inset}>
                  <p className={'text-sm font-semibold ' + c.text}>{r.change}</p>
                  <p className={'text-xs ' + c.textMuted}>{r.impact}</p>
                </div>
              ))}
            </div>
          </Section>
        )}

        {/* Sports: Must-watch games */}
        {isSports && results?.must_watch_games?.length > 0 && (
          <Section title={t('bk_must_watch')} emoji="🔥" open={showMustWatch} onToggle={() => setShowMustWatch(!showMustWatch)} badge={results?.must_watch_games?.length + ''}>
            <div className="space-y-3 mt-4">
              {results?.must_watch_games?.map((g, idx) => (
                <div key={idx} className={'p-4 rounded-xl border ' + c.mustWatchBg}>
                  <div className="flex items-center gap-2 mb-1">
                    <span className={'text-sm font-bold ' + c.text}>{g.game}</span>
                    {g.spoiler_level === 'outcome_unknown' && <span className={'text-[10px] px-2 py-0.5 rounded-full ' + c.badge}>{t('bk_watch_blind')}</span>}
                  </div>
                  <p className={'text-xs ' + c.tipFg}>{g.why}</p>
                </div>
              ))}
            </div>
          </Section>
        )}

        {/* Sports: Conversation ready */}
        {isSports && results?.conversation_ready && (
          <div className={'p-4 rounded-xl border ' + c.success}>
            <p className={'text-xs font-bold ' + c.successFg + ' mb-1'}>{t('bk_conversation_ready')}</p>
            <p className={'text-sm ' + c.successFg}>{results?.conversation_ready}</p>
          </div>
        )}

        {/* Answers to specific questions */}
        {results?.answers?.length > 0 && (
          <Section title={t('bk_your_questions')} emoji="❓" open={showAnswers} onToggle={() => setShowAnswers(!showAnswers)}>
            <div className="space-y-3 mt-4">
              {results?.answers?.map((a, idx) => (
                <div key={idx} className={'p-4 rounded-xl border ' + c.cardAlt}>
                  <p className={'text-xs font-bold ' + c.textMuted + ' mb-1'}>{t('bk_question_prefix', { question: a.question })}</p>
                  <p className={'text-sm ' + c.text}>{a.answer}</p>
                </div>
              ))}
            </div>
          </Section>
        )}

        {/* Worth continuing */}
        {results?.worth_continuing && (
          <div className={'p-4 rounded-xl border ' + c.tip}>
            <p className={'text-xs font-bold ' + c.tipFg + ' mb-1'}>{t('bk_worth_continuing')}</p>
            <p className={'text-sm ' + c.tipFg}>{results?.worth_continuing}</p>
          </div>
        )}

        {/* Re-entry tips */}
        {(results?.reading_tip || results['re-entry_tip']) && (
          <div className={'p-4 rounded-xl ' + c.inset}>
            <p className={'text-xs font-bold ' + c.textMuted + ' mb-1'}>{t('bk_getting_back_in')}</p>
            <p className={'text-sm ' + c.text}>{results?.reading_tip || results['re-entry_tip']}</p>
          </div>
        )}

        <p className={'text-[10px] text-center ' + c.textMuted}>
          {t('bk_ai_disclaimer')}
        </p>

        {/* Cross-references */}
        <div className={'p-4 rounded-2xl border ' + c.border + ' ' + c.card}>
          <p className={'text-xs font-bold ' + c.textMuted + ' uppercase tracking-wide mb-2'}>{t('bk_related_tools')}</p>
          <div className={'space-y-1.5 text-xs ' + c.textSecondary}>
            {(results?.media_type === 'show' || results?.media_type === 'game') && (
              <p>{t('bk_xref_plottwist_pre')} <a href="/PlotTwist" className={linkStyle}>{t('bk_xref_plottwist_link')}</a> {t('bk_xref_plottwist_post')}</p>
            )}
            <p>{t('bk_xref_decoder_pre')} <a href="/DecoderRing" className={linkStyle}>{t('bk_xref_decoder_link')}</a> {t('bk_xref_decoder_post')}</p>
          </div>
        </div>
      </div>
    );
  };

  // ══════════════════════════════════════════
  // HISTORY
  // ══════════════════════════════════════════
  const renderHistory = () => {
    if (sessionHistory.length === 0) return null;
    const formatDate = (iso) => { try { const d = new Date(iso); const diff = Math.floor((new Date() - d) / 86400000); return diff === 0 ? t('bk_hist_today') : diff === 1 ? t('bk_hist_yesterday') : diff < 7 ? t('bk_hist_days_ago', { n: diff }) : d.toLocaleDateString('en-US', { month: 'short', day: 'numeric' }); } catch { return ''; } };
    const typeEmoji = { show: '📺', book: '📖', game: '🎮', sports: '🏟️' };
    return (
      <div className={'mt-6 p-4 rounded-2xl border ' + c.histBg}>
        <button onClick={() => setShowHistory(!showHistory)} className="w-full flex items-center gap-2 text-start">
          <span className={'text-base ' + c.histAccent}>🔖</span>
          <span className={'text-sm font-bold ' + c.text + ' flex-1'}>{t('bk_past_bookmarks')}</span>
          <span className={'text-xs ' + c.textMuted}>{sessionHistory.length}</span>
          <span className={'text-xs ' + c.textMuted}>{showHistory ? '▲' : '▼'}</span>
        </button>
        {showHistory && (
          <div className="mt-3 space-y-2">
            {sessionHistory.map(entry => (
              <div key={entry.id} className={'rounded-xl border ' + c.border + ' ' + c.card + ' p-3 flex items-center gap-3'}>
                <span className="text-base">{typeEmoji[entry.type] || '🔖'}</span>
                <div className="flex-1 min-w-0">
                  <div className={'text-sm font-semibold ' + c.text + ' truncate'}>{entry.title}</div>
                  <div className={'text-xs ' + c.textMuted + ' mt-0.5'}>{formatDate(entry.date)} · {entry.stoppedAt}</div>
                </div>
                <button onClick={() => { setResults(entry.results); setShowHistory(false); }}
                  className={'px-3 py-1.5 rounded-lg text-xs font-bold ' + c.btnSecondary}>{t('bk_view')}</button>
                <button onClick={() => setSessionHistory(prev => prev.filter(h => h.id !== entry.id))}
                  className={'px-2 py-1.5 rounded-lg text-xs ' + c.btnGhost}>🗑️</button>
              </div>
            ))}
          </div>
        )}
      </div>
    );
  };

  return (
    <div className={`space-y-4 ${c.text}`}>
      {/* Input state: header unified inside input card (MarkupDetective pattern) */}
      {!results && renderInput()}
      {!results && (
        <p className={'text-xs text-center ' + c.textMuted}>
          {t('bk_hint_pre')}{' '}
          <a href="/PlotTwist" className={linkStyle}>{t('bk_hint_link')}</a>{' '}
          {t('bk_hint_post')}
        </p>
      )}
      {/* Results state: persistent standalone header with reset, results below */}
      {results ? (
        <div className={`${c.card} border ${c.border} rounded-xl shadow-sm p-5`}>
          <div className="pb-3 border-b border-zinc-500">
            <div className="flex items-start justify-between">
              <div>
                <h2 className={`text-xl font-bold ${c.text}`}>
                  <span className="me-2">{tool?.icon ?? '🔖'}</span>{tool?.title ?? 'Bookmark'}
                </h2>
                <p className={`text-sm ${c.textSecondary}`}>{tool?.tagline ?? t('bk_tagline')}</p>
              </div>
              <button onClick={handleReset} className={`${c.btnSecondary} px-3 py-1.5 rounded-lg text-xs font-bold`}>
                ↺ {t('start_over')}
              </button>
            </div>
          </div>
        </div>
      ) : null}
      {results && renderResults()}
      {error && (
        <div className={'mt-4 p-4 ' + c.danger + ' border rounded-xl flex items-start gap-3'}>
          <span className={'text-base ' + c.dangerFg}>⚠️</span>
          <p className={'text-sm ' + c.dangerFg}>{error}</p>
        </div>
      )}
      {renderHistory()}
    </div>
  );
};

Bookmark.displayName = 'Bookmark';
export default Bookmark;
