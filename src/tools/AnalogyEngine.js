import React, { useState, useCallback } from 'react';
import { CopyBtn, ActionBar } from '../components/ActionButtons';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { usePersistentState } from '../hooks/usePersistentState';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const DEPTH_LEVELS = [
  { value: 'quick_grasp', label: 'Quick grasp', emoji: '⚡' },
  { value: 'solid_understanding', label: 'Solid understanding', emoji: '📖' },
  { value: 'deep_understanding', label: 'Deep dive', emoji: '🔬' },
];

const EXAMPLE_PAIRS = [
  { concept: 'Blockchain', audience: 'My grandmother who loves gardening', emoji: '🌱' },
  { concept: 'Inflation', audience: 'A 12-year-old', emoji: '🎈' },
  { concept: 'Machine learning', audience: 'A chef', emoji: '👨‍🍳' },
  { concept: 'DNA', audience: 'Someone who builds furniture', emoji: '🪚' },
  { concept: 'Compound interest', audience: 'A teenager saving for a car', emoji: '🚗' },
  { concept: 'Quantum computing', audience: 'A musician', emoji: '🎵' },
];

const AnalogyEngine = ({ tool }) => {
  const { callToolEndpoint, loading } = useClaudeAPI();
  const { isDark } = useTheme();

  const c = {
    card:          isDark ? 'bg-zinc-800'       : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50'    : 'bg-slate-50',
    text:          isDark ? 'text-zinc-50'      : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300'     : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-500'     : 'text-gray-400',
    input:         isDark ? 'bg-zinc-700 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20'
                          : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-500/20',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white'
                          : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-100'
                          : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700'   : 'border-gray-200',
    success:       isDark ? 'bg-green-900/20 border-green-700 text-green-200'
                          : 'bg-green-50 border-green-300 text-green-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200'
                          : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200'
                          : 'bg-red-50 border-red-200 text-red-800',
    pillActive:    isDark ? 'bg-cyan-600 border-cyan-600 text-white'
                          : 'bg-cyan-600 border-cyan-600 text-white',
    pillInactive:  isDark ? 'bg-zinc-700 border-zinc-600 text-zinc-300 hover:border-zinc-500'
                          : 'bg-white border-gray-200 text-gray-600 hover:border-gray-300',
  };

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';

  // ─── State ───
  const [concept, setConcept] = usePersistentState('analogyengine-concept', '');
  const [audience, setAudience] = useState('');
  const [audienceInterests, setAudienceInterests] = useState('');
  const [depth, setDepth] = useState('solid_understanding');
  const [results, setResults] = usePersistentState('analogyengine-last', null);
  const [history, setHistory] = usePersistentState('analogyengine-history', []);
  const [error, setError] = useState('');

  // ─── Actions ───
  const generate = useCallback(async (example) => {
    const c = example?.concept || concept;
    const a = example?.audience || audience;
    if (!c.trim()) return;
    setError('');
    setResults(null);
    if (example) { setConcept(example.concept); setAudience(example.audience); setAudienceInterests(''); }

    try {
      const data = await callToolEndpoint('analogy-engine', {
        concept: c.trim(),
        audience: a.trim() || 'general adult',
        audienceInterests: audienceInterests.trim() || null,
        depth,
      });
      setResults(data);
      const newEntry = {
        id: Date.now(),
        timestamp: new Date().toISOString(),
        preview: c.trim().slice(0, 6),
        result: data,
      };
      setHistory(prev => [newEntry, ...(prev || [])].slice(0, 6));
    } catch (err) {
      setError(err.message || 'Failed to generate analogies.');
    }
  }, [concept, audience, audienceInterests, depth, callToolEndpoint, setResults, setHistory]);

  const handleReset = useCallback(() => {
    setConcept(''); setAudience(''); setAudienceInterests('');
    setDepth('solid_understanding'); setResults(null); setError('');
  }, [setConcept, setResults]);

  // ─── Build text ───
  const buildFullText = useCallback(() => {
    if (!results) return '';
    const r = results;
    let text = `💡 Analogy Engine: ${r.concept_name}\n`;
    if (r.one_liner) text += `\nIn one sentence: ${r.one_liner}\n`;
    if (r.analogies?.length) {
      r.analogies.forEach((a, i) => {
        text += `\n${i + 1}. ${a.title} [${a.type}]\n${a.analogy}`;
        if (a.where_it_breaks) text += `\n⚠️ Limit: ${a.where_it_breaks}`;
      });
    }
    if (r.the_key_insight) text += `\n\n💡 Key insight: ${r.the_key_insight}`;
    if (r.common_misconceptions?.length) {
      text += '\n\nCommon misconceptions:';
      r.common_misconceptions.forEach(m => { text += `\n• ${m}`; });
    }
    return text + BRAND;
  }, [results]);

  const buildAnalogyText = useCallback((a) => {
    return `${a.title}\n\n${a.analogy}\n\n⚠️ Where it breaks down: ${a.where_it_breaks || 'N/A'}` + BRAND;
  }, []);

  const r = results;

  // ════════════════════════════════════════════════════════════
  // RENDER
  // ════════════════════════════════════════════════════════════
  return (
    <div className={`space-y-6 ${c.text}`}>

      {/* ── HEADER ── */}
      <div className={`${c.card} border ${c.border} rounded-xl shadow-lg p-5`}>
        <div className={`mb-4 pb-3 border-b ${c.border}`}>
          <h2 className={`text-xl font-bold ${c.text} flex items-center gap-2`}><span>{tool?.icon}</span> Analogy Engine</h2>
          <p className={`text-sm ${c.textSecondaryondary} mt-1`}>Explain anything to anyone — using their world</p>
        </div>

        {/* Concept */}
        <div className="mb-4">
          <label className={`text-xs font-bold ${c.textSecondaryondary} block mb-1.5`}>What do you need to explain? <span className="text-zinc-400">*</span></label>
          <input
            type="text"
            value={concept}
            onChange={e => setConcept(e.target.value)}
            onKeyDown={e => e.key === 'Enter' && concept.trim() && generate()}
            placeholder="e.g., blockchain, compound interest, how vaccines work"
            className={`w-full px-3 py-2.5 border rounded-lg text-sm ${c.input} outline-none focus:ring-2`}
          />
        </div>

        {/* Audience */}
        <div className="mb-4">
          <label className={`text-xs font-bold ${c.textSecondaryondary} block mb-1.5`}>Who are you explaining it to?</label>
          <input
            type="text"
            value={audience}
            onChange={e => setAudience(e.target.value)}
            onKeyDown={e => e.key === 'Enter' && concept.trim() && generate()}
            placeholder="e.g., my 8-year-old, my boss who hates tech, my grandmother"
            className={`w-full px-3 py-2.5 border rounded-lg text-sm ${c.input} outline-none focus:ring-2`}
          />
        </div>

        <div className="mb-4">
          <label className={`text-xs font-bold ${c.textSecondaryondary} block mb-1.5`}>
            Their interests or world <span className={`font-normal ${c.textMuteded}`}>(optional — makes analogies way better)</span>
          </label>
          <input
            type="text"
            value={audienceInterests}
            onChange={e => setAudienceInterests(e.target.value)}
            onKeyDown={e => e.key === 'Enter' && concept.trim() && generate()}
            placeholder="e.g., loves cooking, plays basketball, builds furniture, into fashion"
            className={`w-full px-3 py-2.5 border rounded-lg text-sm ${c.input} outline-none focus:ring-2`}
          />
        </div>

        <div className="mb-4">
          <label className={`text-xs font-bold ${c.textSecondaryondary} uppercase block mb-2`}>How deep?</label>
          <div className="flex gap-2">
            {DEPTH_LEVELS.map(d => (
              <button
                key={d.value}
                onClick={() => setDepth(d.value)}
                className={`flex-1 py-2.5 rounded-xl text-[11px] font-bold border transition-colors min-h-[40px] flex flex-col items-center gap-0.5 ${
                  depth === d.value ? c.pillActive : c.pillInactive
                }`}
              >
                <span className="text-base">{d.emoji}</span>
                {d.label}
              </button>
            ))}
          </div>
        </div>

        {/* Examples */}
        {!results && (
          <div className="mb-5">
            <p className={`text-xs font-bold ${c.textMuteded} mb-2`}>Try one:</p>
            <div className="flex flex-wrap gap-1.5">
              {EXAMPLE_PAIRS.map(ex => (
                <button
                  key={ex.concept}
                  onClick={() => generate(ex)}
                  disabled={loading}
                  className={`px-3 py-1.5 rounded-lg text-xs font-medium border transition-colors min-h-[32px] ${c.pillInactive} disabled:opacity-40`}
                >
                  {ex.emoji} {ex.concept} → {ex.audience.split(' ').slice(0, 3).join(' ')}...
                </button>
              ))}
            </div>
          </div>
        )}

        <p className={`text-xs text-center ${c.textMuteded} mb-3`}>
          Need to simplify language too?{' '}
          <a href="/PlainTalk" className={linkStyle}>Plain Talk</a>{' '}
          strips jargon from any text.
        </p>

        <div className="flex gap-3">
          <button
            onClick={() => generate()}
            disabled={loading || !concept.trim()}
            className={`flex-1 ${c.btnPrimaryPrimary} disabled:opacity-40 disabled:cursor-not-allowed font-bold py-3 px-6 rounded-lg flex items-center justify-center gap-2 min-h-[48px]`}
          >
            {loading
              ? <><span className="animate-spin inline-block">{tool?.icon ?? '⚙️'}</span> Crafting analogies...</>
              : <><span>💡</span> Generate Analogies</>
            }
          </button>
          {results && (
            <button onClick={handleReset} className={`px-5 py-3 ${c.btnPrimarySecondaryondary} rounded-lg font-medium min-h-[48px]`}>
              ↩ Start Over
            </button>
          )}
        </div>
      </div>

      {/* Error */}
      {error && (
        <div className={`${c.danger} border rounded-lg p-4 flex items-start gap-3`}>
          <span className="flex-shrink-0 mt-0.5">⚠️</span>
          <p className="text-sm">{error}</p>
        </div>
      )}

      {/* ══════════════════════════════════════════════════════════ */}
      {/* RESULTS                                                  */}
      {/* ══════════════════════════════════════════════════════════ */}
      {r && (
        <div className="space-y-4">

          <div className="flex justify-end">
            <ActionBar content={buildFullText()} title="Analogy Engine" />
          </div>

          {/* ── ONE LINER ── */}
          {r.one_liner && (
            <div className={`${c.card} border ${c.border} rounded-xl p-5`}>
              <p className={`text-xs font-bold ${c.textMuteded} mb-1`}>In one sentence:</p>
              <p className={`text-base font-bold ${c.text} leading-relaxed`}>{r.one_liner}</p>
            </div>
          )}

          {r.analogies?.length > 0 && (
            <div className="space-y-3">
              <h3 className={`text-sm font-bold ${c.text}`}>
                {r.analogies.length} Analog{r.analogies.length === 1 ? 'y' : 'ies'} for {r.concept_name || concept}
              </h3>
              {r.analogies.map((analogy, idx) => (
                <div key={idx} className={`${c.card} border ${c.border} rounded-xl overflow-hidden`}>
                  <div className="p-5">
                    <div className="flex items-start justify-between mb-3">
                      <div>
                        <h4 className={`text-sm font-bold ${c.text}`}>{analogy.title}</h4>
                        <div className="flex gap-1.5 mt-1">
                          <span className={`text-[9px] font-bold px-2 py-0.5 rounded-full ${c.success}`}>{analogy.type}</span>
                          {analogy.accuracy === 'high' && (
                            <span className={`text-[9px] font-bold px-2 py-0.5 rounded-full border ${c.success}`}>✅ High accuracy</span>
                          )}
                          {analogy.memorability === 'high' && (
                            <span className={`text-[9px] font-bold px-2 py-0.5 rounded-full ${c.warning}`}>⭐ Memorable</span>
                          )}
                        </div>
                      </div>
                      <CopyBtn content={buildAnalogyText(analogy)} label="Copy" />
                    </div>
                    <div className={`${c.cardAlt} rounded-lg p-4 mb-3`}>
                      <p className={`text-sm ${c.text} leading-relaxed`}>{analogy.analogy}</p>
                    </div>
                    {analogy.why_it_works && (
                      <p className={`text-xs ${c.textSecondaryondary} mb-2`}>
                        <span className="font-bold">Why it works:</span> {analogy.why_it_works}
                      </p>
                    )}
                    {analogy.where_it_breaks && (
                      <p className={`text-xs ${c.textMuteded} italic`}>⚠️ Limit: {analogy.where_it_breaks}</p>
                    )}
                  </div>
                </div>
              ))}
            </div>
          )}

          {r.the_key_insight && (
            <div className={`${c.success} border rounded-xl p-4 flex items-start gap-3`}>
              <span className="flex-shrink-0 mt-0.5">💡</span>
              <div>
                <p className={`text-xs font-bold mb-1`}>The key insight</p>
                <p className="text-sm font-medium leading-relaxed">{r.the_key_insight}</p>
              </div>
            </div>
          )}

          {r.common_misconceptions?.length > 0 && (
            <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
              <p className={`text-xs font-bold ${c.text} mb-2`}>🚫 Common misconceptions</p>
              <div className="space-y-2">
                {r.common_misconceptions.map((m, i) => (
                  <p key={i} className={`text-xs ${c.textSecondaryondary} leading-relaxed`}>• {m}</p>
                ))}
              </div>
            </div>
          )}

          <div className="grid grid-cols-1 sm:grid-cols-2 gap-3">
            {r.go_deeper && (
              <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
                <p className={`text-xs font-bold ${c.textMuteded} mb-1`}>📚 Go deeper</p>
                <p className={`text-xs ${c.textSecondaryondary} leading-relaxed`}>{r.go_deeper}</p>
              </div>
            )}
            {r.teaching_tip && (
              <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
                <p className={`text-xs font-bold ${c.textMuteded} mb-1`}>🎓 Teaching tip</p>
                <p className={`text-xs ${c.textSecondaryondary} leading-relaxed`}>{r.teaching_tip}</p>
              </div>
            )}
          </div>

          <p className={`text-xs text-center ${c.textMuteded}`}>
            Want to strip the jargon from your explanation?{' '}
            <a href="/JargonAssassin" className={linkStyle}>Jargon Assassin</a>{' '}
            clears out the noise.
          </p>
          {r.analogies?.length > 0 && r.analogies[0]?.accuracy === 'medium' && (
            <p className={`text-xs text-center ${c.textMuteded}`}>
              Accuracy concerns?{' '}
              <a href="/BeliefStressTest" className={linkStyle}>Belief Stress Test</a>{' '}
              finds the weakest assumptions in any explanation.
            </p>
          )}
        </div>
      )}

      {history?.length > 0 && (
        <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
          <h3 className={`text-sm font-bold ${c.text} mb-3`}>🕐 Recent Explanations</h3>
          <div className="space-y-1.5">
            {history.map(entry => (
              <button key={entry.id}
                onClick={() => setResults(entry.result)}
                className={`w-full text-left px-3 py-2 rounded-lg ${c.btnPrimarySecondaryondary} text-xs flex items-center gap-2`}>
                <span className={c.textMuteded}>
                  {new Date(entry.timestamp).toLocaleDateString(undefined, { month: 'short', day: 'numeric', hour: 'numeric', minute: '2-digit' })}
                </span>
                <span className={c.text}>{entry.preview}{entry.preview.length >= 40 ? '…' : ''}</span>
              </button>
            ))}
          </div>
        </div>
      )}
        <div className={`mt-6 pt-4 border-t text-sm ${c.border} ${c.textMuted}`}>
          <p className="mb-2 font-medium">You might also like:</p>
          <div className="flex flex-wrap gap-2">
            {[{slug:'brain-roulette',label:'🎲 Brain Roulette'},{slug:'plain-talk',label:'💬 Plain Talk'},{slug:'jargon-assassin',label:'🗡️ Jargon Assassin'}].map(({slug,label})=>(
              <a key={slug} href={`${slug}`} className={linkStyle}>{label}</a>
            ))}
          </div>
        </div>
    </div>
  );
};

AnalogyEngine.displayName = 'AnalogyEngine';
export default AnalogyEngine;
