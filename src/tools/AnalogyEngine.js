import React, { useState, useCallback } from 'react';
import { CopyBtn } from '../components/ActionButtons';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const DEPTH_LEVELS = [
  { value: 'quick_grasp', label: 'Quick grasp', emoji: '⚡' },
  { value: 'solid_understanding', label: 'Solid understanding', emoji: '📖' },
  { value: 'deep_understanding', label: 'Deep dive', emoji: '🔬' },
];

const EXAMPLE_PAIRS = [
  { concept: 'Blockchain', audience: 'My grandmother who loves gardening', emoji: '🌱' },
  { concept: 'Inflation', audience: 'A 12-year-old', emoji: '🎈' },
  { concept: 'Machine learning', audience: 'A chef', emoji: '👨‍🍳' },
  { concept: 'DNA', audience: 'Someone who builds furniture', emoji: '🪚' },
  { concept: 'Compound interest', audience: 'A teenager saving for a car', emoji: '🚗' },
  { concept: 'Quantum computing', audience: 'A musician', emoji: '🎵' },
];

const AnalogyEngine = () => {
  const { callToolEndpoint, loading } = useClaudeAPI();
  const { theme } = useTheme();
  const isDark = theme === 'dark';

  const c = {
    pageBg:       isDark ? 'bg-[#1a1816]'    : 'bg-[#faf8f5]',
    card:         isDark ? 'bg-[#2a2623]'     : 'bg-white',
    cardAlt:      isDark ? 'bg-[#332e2a]'     : 'bg-[#faf8f5]',
    cardBorder:   isDark ? 'border-[#3d3630]' : 'border-[#e8e1d5]',
    inputBg:      isDark ? 'bg-[#1a1816]'     : 'bg-[#faf8f5]',
    inputBorder:  isDark ? 'border-[#3d3630]' : 'border-[#d5cab8]',
    inputFocus:   isDark ? 'focus:border-[#4a6a8a] focus:ring-[#2c4a6e]/20'
                         : 'focus:border-[#4a6a8a] focus:ring-[#2c4a6e]/12',
    text:          isDark ? 'text-[#f0eeea]'  : 'text-[#3d3935]',
    textSecondary: isDark ? 'text-[#c8c3b9]'  : 'text-[#5a544a]',
    textMuted:     isDark ? 'text-[#8a8275]'  : 'text-[#8a8275]',
    heading:       isDark ? 'text-[#f3efe8]'  : 'text-[#1e2a3a]',
    btnPrimary:    isDark ? 'bg-[#2c4a6e] hover:bg-[#4a6a8a] text-white' : 'bg-[#2c4a6e] hover:bg-[#1e3a58] text-white',
    btnSecondary:  isDark ? 'bg-[#332e2a] hover:bg-[#3d3630] text-[#c8c3b9] border border-[#3d3630]' : 'bg-[#f3efe8] hover:bg-[#e8e1d5] text-[#5e5042] border border-[#d5cab8]',
    btnGold:       isDark ? 'bg-[#b06d22] hover:bg-[#c8872e] text-white' : 'bg-[#c8872e] hover:bg-[#b06d22] text-white',
    badgeNeutral:  isDark ? 'bg-[#3d3630] text-[#c8c3b9]' : 'bg-[#f3efe8] text-[#73614e]',
    badgePrimary:  isDark ? 'bg-[#2c4a6e]/30 text-[#a8b9ce]' : 'bg-[#d4dde8] text-[#1e3a58]',
    badgeGold:     isDark ? 'bg-[#b06d22]/20 text-[#d9a04e]' : 'bg-[#f9edd8] text-[#93541f]',
    divider:       isDark ? 'border-[#3d3630]' : 'border-[#e8e1d5]',
    link:          isDark ? 'text-[#6e8aaa] hover:text-[#a8b9ce]' : 'text-[#2c4a6e] hover:text-[#1e3a58]',
    success:       isDark ? 'bg-[#5a8a5c]/15 border-[#5a8a5c]/30 text-[#8abf8c]' : 'bg-[#e8f0e8] border-[#5a8a5c]/20 text-[#3d6b3f]',
    warning:       isDark ? 'bg-[#c8872e]/15 border-[#c8872e]/30 text-[#d9a04e]' : 'bg-[#fdf3e4] border-[#c8872e]/20 text-[#93541f]',
    error:         isDark ? 'bg-[#b54a3f]/15 border-[#b54a3f]/30 text-[#e08a82]' : 'bg-[#fceae8] border-[#b54a3f]/20 text-[#8a3530]',
    pillActive:    isDark ? 'bg-[#2c4a6e] border-[#4a6a8a] text-white' : 'bg-[#2c4a6e] border-[#2c4a6e] text-white',
    pillInactive:  isDark ? 'bg-[#2a2623] border-[#3d3630] text-[#c8c3b9] hover:border-[#5a544a]' : 'bg-white border-[#e8e1d5] text-[#5a544a] hover:border-[#d5cab8]',
  };

  // ─── State ───
  const [concept, setConcept] = useState('');
  const [audience, setAudience] = useState('');
  const [audienceInterests, setAudienceInterests] = useState('');
  const [depth, setDepth] = useState('solid_understanding');
  const [results, setResults] = useState(null);
  const [error, setError] = useState('');

  // ─── Actions ───
  const generate = useCallback(async (example) => {
    const c = example?.concept || concept;
    const a = example?.audience || audience;
    if (!c.trim()) return;
    setError('');
    setResults(null);
    if (example) { setConcept(example.concept); setAudience(example.audience); setAudienceInterests(''); }

    try {
      const data = await callToolEndpoint('analogy-engine', {
        concept: c.trim(),
        audience: a.trim() || 'general adult',
        audienceInterests: audienceInterests.trim() || null,
        depth,
      });
      setResults(data);
    } catch (err) {
      setError(err.message || 'Failed to generate analogies.');
    }
  }, [concept, audience, audienceInterests, depth, callToolEndpoint]);

  const handleReset = useCallback(() => {
    setConcept(''); setAudience(''); setAudienceInterests('');
    setDepth('solid_understanding'); setResults(null); setError('');
  }, []);

  // ─── Build text ───
  const buildFullText = useCallback(() => {
    if (!results) return '';
    const r = results;
    let text = `🔬 AnalogyEngine: ${r.concept_name}\n`;
    if (r.one_liner) text += `\nIn one sentence: ${r.one_liner}\n`;
    if (r.analogies?.length) {
      r.analogies.forEach((a, i) => {
        text += `\n${i + 1}. ${a.title} [${a.type}]\n${a.analogy}`;
        if (a.where_it_breaks) text += `\n⚠️ Limit: ${a.where_it_breaks}`;
      });
    }
    if (r.the_key_insight) text += `\n\n💡 Key insight: ${r.the_key_insight}`;
    if (r.common_misconceptions?.length) {
      text += '\n\nCommon misconceptions:';
      r.common_misconceptions.forEach(m => { text += `\n• ${m}`; });
    }
    return text + BRAND;
  }, [results]);

  const buildAnalogyText = useCallback((a) => {
    return `${a.title}\n\n${a.analogy}\n\n⚠️ Where it breaks down: ${a.where_it_breaks || 'N/A'}` + BRAND;
  }, []);

  const r = results;

  // ════════════════════════════════════════════════════════════
  // RENDER
  // ════════════════════════════════════════════════════════════
  return (
    <div className={`space-y-6 ${c.text}`}>

      {/* ── HEADER ── */}
      <div className={`${c.card} border ${c.cardBorder} rounded-xl p-6`}>
        <div className={`mb-5 pb-4 border-b ${c.divider}`}>
          <h2 className={`text-2xl font-bold ${c.heading}`}>AnalogyEngine 🔬</h2>
          <p className={`text-sm ${c.textSecondary} mt-1`}>Explain anything to anyone — using their world</p>
        </div>

        {/* Concept */}
        <div className="mb-4">
          <label className={`text-sm font-bold ${c.text} block mb-1.5`}>What do you need to explain?</label>
          <input
            type="text"
            value={concept}
            onChange={e => setConcept(e.target.value)}
            placeholder="e.g., blockchain, compound interest, how vaccines work, quantum physics"
            className={`w-full px-4 py-3 border rounded-xl text-sm ${c.inputBg} ${c.inputBorder} ${c.text} ${c.inputFocus} outline-none focus:ring-2`}
          />
        </div>

        {/* Audience */}
        <div className="mb-4">
          <label className={`text-sm font-bold ${c.text} block mb-1.5`}>Who are you explaining it to?</label>
          <input
            type="text"
            value={audience}
            onChange={e => setAudience(e.target.value)}
            placeholder="e.g., my 8-year-old, my boss who hates tech, my grandmother"
            className={`w-full px-4 py-3 border rounded-xl text-sm ${c.inputBg} ${c.inputBorder} ${c.text} ${c.inputFocus} outline-none focus:ring-2`}
          />
        </div>

        {/* Audience interests */}
        <div className="mb-4">
          <label className={`text-sm font-bold ${c.text} block mb-1.5`}>
            Their interests or world <span className={`font-normal ${c.textMuted}`}>(optional — makes analogies way better)</span>
          </label>
          <input
            type="text"
            value={audienceInterests}
            onChange={e => setAudienceInterests(e.target.value)}
            placeholder="e.g., loves cooking, plays basketball, builds furniture, into fashion"
            className={`w-full px-4 py-3 border rounded-xl text-sm ${c.inputBg} ${c.inputBorder} ${c.text} ${c.inputFocus} outline-none focus:ring-2`}
          />
        </div>

        {/* Depth */}
        <div className="mb-4">
          <label className={`text-sm font-bold ${c.text} block mb-2`}>How deep?</label>
          <div className="flex gap-2">
            {DEPTH_LEVELS.map(d => (
              <button
                key={d.value}
                onClick={() => setDepth(d.value)}
                className={`flex-1 py-2.5 rounded-xl text-[11px] font-bold border transition-colors min-h-[40px] flex flex-col items-center gap-0.5 ${
                  depth === d.value ? c.pillActive : c.pillInactive
                }`}
              >
                <span className="text-base">{d.emoji}</span>
                {d.label}
              </button>
            ))}
          </div>
        </div>

        {/* Examples */}
        {!results && (
          <div className="mb-5">
            <p className={`text-xs font-bold ${c.textMuted} mb-2`}>Try one:</p>
            <div className="flex flex-wrap gap-1.5">
              {EXAMPLE_PAIRS.map(ex => (
                <button
                  key={ex.concept}
                  onClick={() => generate(ex)}
                  disabled={loading}
                  className={`px-3 py-1.5 rounded-lg text-xs font-medium border transition-colors min-h-[32px] ${c.pillInactive} disabled:opacity-40`}
                >
                  {ex.emoji} {ex.concept} → {ex.audience.split(' ').slice(0, 3).join(' ')}...
                </button>
              ))}
            </div>
          </div>
        )}

        {/* Actions */}
        <div className="flex gap-3">
          <button
            onClick={() => generate()}
            disabled={loading || !concept.trim()}
            className={`flex-1 ${c.btnGold} disabled:opacity-40 disabled:cursor-not-allowed font-bold py-3 px-6 rounded-xl flex items-center justify-center gap-2 min-h-[48px] shadow-lg`}
          >
            {loading ? (
              <><span className="animate-spin inline-block">⏳</span> Crafting analogies...</>
            ) : (
              <><span>🔬</span> Generate Analogies</>
            )}
          </button>
          {results && (
            <button onClick={handleReset} className={`px-5 py-3 ${c.btnSecondary} rounded-xl font-medium min-h-[48px]`}>
              New
            </button>
          )}
        </div>
      </div>

      {/* Error */}
      {error && (
        <div className={`${c.error} border rounded-xl p-4 flex items-start gap-3`}>
          <span className="flex-shrink-0 mt-0.5">⚠️</span>
          <p className="text-sm">{error}</p>
        </div>
      )}

      {/* ══════════════════════════════════════════════════════════ */}
      {/* RESULTS                                                  */}
      {/* ══════════════════════════════════════════════════════════ */}
      {r && (
        <div className="space-y-4">

          <div className="flex justify-end">
            <CopyBtn content={buildFullText()} label="Copy All" />
          </div>

          {/* ── ONE LINER ── */}
          {r.one_liner && (
            <div className={`${c.card} border ${c.cardBorder} rounded-xl p-5`}>
              <p className={`text-xs font-bold ${c.textMuted} mb-1`}>In one sentence:</p>
              <p className={`text-base font-bold ${c.heading} leading-relaxed`}>{r.one_liner}</p>
            </div>
          )}

          {/* ── ANALOGIES ── */}
          {r.analogies?.length > 0 && (
            <div className="space-y-3">
              <h3 className={`text-sm font-bold ${c.heading}`}>
                {r.analogies.length} Analog{r.analogies.length === 1 ? 'y' : 'ies'} for {r.concept_name || concept}
              </h3>
              {r.analogies.map((analogy, idx) => (
                <div key={idx} className={`${c.card} border ${c.cardBorder} rounded-xl overflow-hidden`}>
                  <div className="p-5">
                    {/* Header */}
                    <div className="flex items-start justify-between mb-3">
                      <div>
                        <h4 className={`text-sm font-bold ${c.heading}`}>{analogy.title}</h4>
                        <div className="flex gap-1.5 mt-1">
                          <span className={`text-[9px] font-bold px-2 py-0.5 rounded-full ${c.badgePrimary}`}>{analogy.type}</span>
                          {analogy.accuracy === 'high' && (
                            <span className={`text-[9px] font-bold px-2 py-0.5 rounded-full ${c.success} border`}>✅ High accuracy</span>
                          )}
                          {analogy.memorability === 'high' && (
                            <span className={`text-[9px] font-bold px-2 py-0.5 rounded-full ${c.badgeGold}`}>⭐ Memorable</span>
                          )}
                        </div>
                      </div>
                      <CopyBtn content={buildAnalogyText(analogy)} label="Copy" />
                    </div>

                    {/* The analogy */}
                    <div className={`${c.cardAlt} rounded-lg p-4 mb-3`}>
                      <p className={`text-sm ${c.text} leading-relaxed`}>{analogy.analogy}</p>
                    </div>

                    {/* Why it works */}
                    {analogy.why_it_works && (
                      <p className={`text-xs ${c.textSecondary} mb-2`}>
                        <span className="font-bold">Why it works:</span> {analogy.why_it_works}
                      </p>
                    )}

                    {/* Where it breaks */}
                    {analogy.where_it_breaks && (
                      <p className={`text-xs ${c.textMuted} italic`}>
                        ⚠️ Limit: {analogy.where_it_breaks}
                      </p>
                    )}
                  </div>
                </div>
              ))}
            </div>
          )}

          {/* ── KEY INSIGHT ── */}
          {r.the_key_insight && (
            <div className={`${c.success} border rounded-xl p-4 flex items-start gap-3`}>
              <span className="flex-shrink-0 mt-0.5">💡</span>
              <div>
                <p className="text-xs font-bold mb-1">The key insight</p>
                <p className="text-sm font-medium leading-relaxed">{r.the_key_insight}</p>
              </div>
            </div>
          )}

          {/* ── MISCONCEPTIONS ── */}
          {r.common_misconceptions?.length > 0 && (
            <div className={`${c.card} border ${c.cardBorder} rounded-xl p-4`}>
              <p className={`text-xs font-bold ${c.heading} mb-2`}>🚫 Common misconceptions</p>
              <div className="space-y-2">
                {r.common_misconceptions.map((m, i) => (
                  <p key={i} className={`text-xs ${c.textSecondary} leading-relaxed`}>• {m}</p>
                ))}
              </div>
            </div>
          )}

          {/* ── GO DEEPER + TEACHING TIP ── */}
          <div className="grid grid-cols-1 sm:grid-cols-2 gap-3">
            {r.go_deeper && (
              <div className={`${c.cardAlt} border ${c.cardBorder} rounded-xl p-4`}>
                <p className={`text-xs font-bold ${c.textMuted} mb-1`}>📚 Go deeper</p>
                <p className={`text-xs ${c.textSecondary} leading-relaxed`}>{r.go_deeper}</p>
              </div>
            )}
            {r.teaching_tip && (
              <div className={`${c.cardAlt} border ${c.cardBorder} rounded-xl p-4`}>
                <p className={`text-xs font-bold ${c.textMuted} mb-1`}>🎓 Teaching tip</p>
                <p className={`text-xs ${c.textSecondary} leading-relaxed`}>{r.teaching_tip}</p>
              </div>
            )}
          </div>

          {/* ── CROSS-REFERENCES ── */}
          <div className={`${c.card} border ${c.cardBorder} rounded-xl p-4`}>
            <p className={`text-xs font-bold ${c.text} mb-2`}>Related tools</p>
            <div className="flex flex-wrap gap-2">
              <a href="/PlainTalk" target="_blank" rel="noopener noreferrer" className={`text-xs px-3 py-1.5 rounded-lg border ${c.pillInactive} no-underline`}>
                💬 PlainTalk
              </a>
              <a href="/JargonAssassin" target="_blank" rel="noopener noreferrer" className={`text-xs px-3 py-1.5 rounded-lg border ${c.pillInactive} no-underline`}>
                🗡️ Jargon Assassin
              </a>
              <a href="/DebateMe" target="_blank" rel="noopener noreferrer" className={`text-xs px-3 py-1.5 rounded-lg border ${c.pillInactive} no-underline`}>
                🥊 DebateMe
              </a>
            </div>
          </div>
        </div>
      )}
    </div>
  );
};

AnalogyEngine.displayName = 'AnalogyEngine';
export default AnalogyEngine;
