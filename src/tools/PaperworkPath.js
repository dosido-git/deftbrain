import React, { useState, useEffect, useRef, useCallback } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { usePersistentState } from '../hooks/usePersistentState';
import { useRegisterActions } from '../components/ActionBarContext';
import { useTranslation } from '../i18n/useTranslation';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';
const PREVIEW_CHARS = 40;

// Life events — value is sent to the backend verbatim; labelKey is the i18n label.
const EVENTS = [
  { value: 'Moving to a new home',        emoji: '📦', labelKey: 'pp_ev_move' },
  { value: 'New baby',                    emoji: '👶', labelKey: 'pp_ev_baby' },
  { value: 'Starting a new job',          emoji: '💼', labelKey: 'pp_ev_job' },
  { value: 'Getting married',             emoji: '💍', labelKey: 'pp_ev_marriage' },
  { value: 'Going through a divorce',     emoji: '📄', labelKey: 'pp_ev_divorce' },
  { value: 'Death of a loved one',        emoji: '🕊️', labelKey: 'pp_ev_death' },
  { value: 'Buying a home',               emoji: '🏡', labelKey: 'pp_ev_buyhome' },
  { value: 'Retiring',                    emoji: '🌅', labelKey: 'pp_ev_retire' },
];

const PaperworkPath = ({ tool }) => {
  const { callToolEndpoint, loading, userLocale, userCurrency, userRegion } = useClaudeAPI();
  const { isDark } = useTheme();
  const { t } = useTranslation();

  const c = {
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:         isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20'
                          : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-400' : 'text-gray-500',
    labelText:     isDark ? 'text-zinc-200' : 'text-gray-700',
    accentTxt:     isDark ? 'text-cyan-400' : 'text-cyan-600',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white'
                          : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200'
                          : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200'
                          : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200'
                          : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200'
                          : 'bg-red-50 border-red-200 text-red-800',
    successTxt:    isDark ? 'text-emerald-300' : 'text-emerald-800',
    warningTxt:    isDark ? 'text-amber-300' : 'text-amber-800',
    pillActive:    isDark ? 'border-cyan-500 bg-cyan-900/30 text-cyan-200'
                          : 'border-cyan-600 bg-cyan-100 text-cyan-900',
    pillInactive:  isDark ? 'border-zinc-600 text-zinc-400 hover:border-zinc-500'
                          : 'border-gray-300 text-gray-500 hover:border-gray-400',
    required:      isDark ? 'text-amber-400' : 'text-amber-700',
  };
  c.textMuteded = c.textMuted;
  c.label = c.labelText;

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-700 hover:text-cyan-800 underline underline-offset-2';

  // ── State ──
  const [lifeEvent, setLifeEvent] = useState('');
  const [situation, setSituation] = useState('');
  const [location, setLocation] = useState('');
  const [error, setError] = useState('');
  const [showHistory, setShowHistory] = useState(false);

  // ── Persistent state ──
  const [results, setResults] = usePersistentState('paperwork-path-results', null);
  const [sessionHistory, setSessionHistory] = usePersistentState('paperwork-path-history', []);

  // ── Refs ──
  const resultsRef = useRef(null);
  const handleGenerateRef = useRef(null);
  const canSubmitRef = useRef(false);

  const loadExample = useCallback(() => {
    setLifeEvent(EVENTS[0].value);
    setSituation(t('pp_example_situation'));
    setLocation(t('pp_example_location'));
    setResults(null); setError('');
  }, [t, setResults]);

  // ── Handlers ──
  const handleGenerate = useCallback(async () => {
    if (!lifeEvent.trim()) return;
    setError('');
    setResults(null);
    try {
      const data = await callToolEndpoint('paperwork-path', {
        lifeEvent, situation: situation.trim(), location: location.trim(),
        userLocale, userCurrency, userRegion,
      });
      setResults(data);
      setSessionHistory(prev => [{
        id: Date.now(),
        date: new Date().toISOString(),
        preview: lifeEvent.slice(0, PREVIEW_CHARS),
      }, ...prev].slice(0, 6));
    } catch (err) {
      setError(err.message || t('pp_error'));
    }
  }, [lifeEvent, situation, location, callToolEndpoint, setResults, setSessionHistory, userLocale, userCurrency, userRegion, t]);

  const handleReset = () => { setResults(null); setLifeEvent(''); setSituation(''); setLocation(''); setError(''); };

  handleGenerateRef.current = handleGenerate;
  canSubmitRef.current = !!lifeEvent.trim();

  // ── Build copy text ──
  const buildFullText = useCallback(() => {
    if (!results) return '';
    const lines = [`🗂️ ${t('pp_copy_header')} — ${lifeEvent}`, ''];
    if (results?.situation_summary) { lines.push(results.situation_summary); lines.push(''); }
    lines.push(t('pp_copy_checklist'));
    (results?.document_checklist || []).forEach(d => {
      lines.push(`  [${(d.priority || '').toUpperCase()}] ${d.document} — ${d.why}`);
      if (d.where_to_get) lines.push(`      ${t('pp_copy_where')}: ${d.where_to_get}`);
    });
    lines.push('');
    lines.push(t('pp_copy_order'));
    (results?.ordered_steps || []).forEach(s => {
      lines.push(`  ${s.order}. ${s.action} (${s.timing})`);
    });
    if (results?.watch_outs?.length) {
      lines.push('');
      lines.push(t('pp_copy_watch'));
      results.watch_outs.forEach(w => lines.push(`  • ${w}`));
    }
    if (results?.verify_note) { lines.push(''); lines.push(`⚠️ ${results.verify_note}`); }
    lines.push(BRAND);
    return lines.join('\n');
  }, [results, lifeEvent, t]);

  useRegisterActions(buildFullText(), tool?.title || 'PaperworkPath');

  // ── Scroll to results ──
  useEffect(() => {
    if (!results || !resultsRef.current) return;
    const timer = setTimeout(() => resultsRef.current?.scrollIntoView({ behavior: 'smooth', block: 'start' }), 200);
    return () => clearTimeout(timer);
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [results]);

  // ── Keyboard handler ──
  useEffect(() => {
    const handler = (e) => {
      if (document.activeElement?.tagName === 'SELECT') return;
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && !loading && canSubmitRef.current)
        handleGenerateRef.current?.();
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [loading]);

  // ── Render helpers ──
  const priorityStyle = (p) => {
    const v = (p || '').toLowerCase();
    if (v === 'critical') return c.danger;
    if (v === 'important') return c.warning;
    return c.cardAlt;
  };

  return (
    <div className={`space-y-4 ${c.text}`}>

      {/* ═══ INPUT CARD ═══ */}
      <div className={`${c.card} border ${c.border} rounded-xl shadow-sm`}>
        <div className="px-5 pt-5">
          <div className="pb-3 border-b border-zinc-500 flex items-center justify-between">
            <div>
              <h2 className={`text-xl font-bold ${c.text}`}>
                <span className="me-2">{tool?.icon ?? '🗂️'}</span>{tool?.title ?? t('pp_title')}
              </h2>
              <p className={`text-sm ${c.textSecondary}`}>{tool?.tagline ?? t('pp_tagline')}</p>
              <button onClick={loadExample} disabled={loading} style={{ backgroundColor: (tool?.headerColor ?? '#888888') + '80' }} className={`mt-1.5 px-2.5 py-0.5 rounded-full text-xs font-medium border disabled:opacity-40 ${isDark ? 'text-white border-white/40' : 'text-gray-800 border-transparent'}`}>{t('try_example')}</button>
            </div>
            <div className="flex items-center gap-2 flex-shrink-0">
              {sessionHistory.length > 0 && (
                <button onClick={() => setShowHistory(!showHistory)}
                  className={`text-xs font-bold px-3 py-1.5 rounded-lg ${c.btnSecondary}`}>
                  📋 {sessionHistory.length}
                </button>
              )}
              {(results || lifeEvent || situation.trim() || location.trim()) ? (
                <button onClick={handleReset} className={`${c.btnSecondary} px-3 py-1.5 rounded-lg text-xs font-bold`}>
                  ↺ {t('start_over')}
                </button>
              ) : null}
            </div>
          </div>
        </div>

        <div className="px-5 pb-5 pt-4 space-y-4">

          {/* History panel */}
          {showHistory && sessionHistory.length > 0 && (
            <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4 space-y-2`}>
              <p className={`text-xs font-bold ${c.textMuted} mb-1`}>{t('pp_recent')}</p>
              {sessionHistory.map(h => (
                <button key={h.id} onClick={() => { setLifeEvent(h.preview); setShowHistory(false); }}
                  className={`w-full text-start px-3 py-2 rounded-lg ${c.card} border ${c.border} hover:opacity-80 transition-opacity`}>
                  <span className={`text-sm ${c.textSecondary} truncate`}>{h.preview}</span>
                </button>
              ))}
            </div>
          )}

          {/* Life event — required */}
          <div>
            <label className={`text-sm font-medium ${c.label} block mb-2`}>
              {t('pp_event_label')} <span className={c.required}>*</span>
            </label>
            <div className="grid grid-cols-2 sm:grid-cols-4 gap-1.5">
              {EVENTS.map(ev => (
                <button key={ev.value} onClick={() => setLifeEvent(ev.value)}
                  className={`px-3 py-2 rounded-lg border text-xs font-medium transition-all ${lifeEvent === ev.value ? c.pillActive : c.pillInactive}`}>
                  <span className="me-1">{ev.emoji}</span>{t(ev.labelKey)}
                </button>
              ))}
            </div>
          </div>

          {/* Location — optional */}
          <div>
            <label className={`text-sm font-medium ${c.label} block mb-2`}>{t('pp_location_label')}</label>
            <input
              type="text"
              value={location}
              onChange={e => setLocation(e.target.value)}
              placeholder={t('pp_location_ph')}
              className={`w-full p-3 border rounded-lg text-sm ${c.input}`}
            />
          </div>

          {/* Situation — optional */}
          <div>
            <label className={`text-sm font-medium ${c.label} block mb-2`}>{t('pp_situation_label')}</label>
            <textarea
              value={situation}
              onChange={e => setSituation(e.target.value)}
              placeholder={t('pp_situation_ph')}
              rows={3}
              className={`w-full p-3 border rounded-lg text-sm resize-none ${c.input}`}
            />
          </div>

          {/* Pre-result cross-ref (S5.5 — a tool link must be visible before submit) */}
          <p className={`text-xs text-center ${c.textMuted}`}>
            {t('pp_xref_pre')} <a href="/RentersDepositSaver" className={`text-xs ${linkStyle}`}>🏠 {t('pp_xref_deposit')}</a>.
          </p>

          {/* Submit */}
          <button
            onClick={handleGenerate}
            disabled={loading || !canSubmitRef.current}
            className={`w-full ${c.btnPrimary} disabled:opacity-40 font-bold py-3 rounded-lg flex items-center justify-center gap-2 min-h-[48px]`}>
            {loading
              ? <><span className="inline-block animate-spin">{tool?.icon ?? '🗂️'}</span> {t('pp_building')}</>
              : <><span className="me-1">{tool?.icon ?? '🗂️'}</span> {t('pp_build')}</>}
          </button>

          <p className={`text-xs text-center ${c.textMuted}`}>{t('pp_disclaimer')}</p>

          {error && (
            <div className={`p-3 rounded-lg border text-sm ${c.danger}`}>⚠️ {error}</div>
          )}
        </div>
      </div>

      {/* ═══ RESULTS ═══ */}
      {results && (
        <div ref={resultsRef} className="space-y-4">

          {/* Summary */}
          {results?.situation_summary && (
            <div className={`${c.card} border ${c.border} rounded-xl p-5`}>
              <p className={`text-sm ${c.textSecondary}`}>{results.situation_summary}</p>
            </div>
          )}

          {/* Document checklist */}
          {results?.document_checklist?.length > 0 && (
            <div className={`${c.card} border ${c.border} rounded-xl p-5`}>
              <h3 className={`font-bold text-sm ${c.text} mb-3`}>🗂️ {t('pp_checklist_title')}</h3>
              <div className="space-y-2">
                {results.document_checklist.map((d, i) => (
                  <div key={i} className={`border rounded-lg p-3 ${priorityStyle(d.priority)}`}>
                    <div className="flex items-start justify-between gap-2">
                      <p className="text-sm font-semibold">{d.document}</p>
                      <span className={`text-[10px] font-bold uppercase shrink-0 ${c.textMuted}`}>{d.priority}</span>
                    </div>
                    <p className={`text-xs mt-1 ${c.textSecondary}`}>{d.why}</p>
                    {d.where_to_get && <p className={`text-xs mt-1 ${c.textMuted}`}>📍 {d.where_to_get}</p>}
                  </div>
                ))}
              </div>
            </div>
          )}

          {/* Ordered steps */}
          {results?.ordered_steps?.length > 0 && (
            <div className={`${c.card} border ${c.border} rounded-xl p-5`}>
              <h3 className={`font-bold text-sm ${c.text} mb-3`}>🔢 {t('pp_order_title')}</h3>
              <div className="space-y-3">
                {results.ordered_steps.map((s, i) => (
                  <div key={i} className="flex items-start gap-3">
                    <span className={`shrink-0 w-6 h-6 flex items-center justify-center rounded-full text-xs font-bold ${c.pillActive}`}>{s.order}</span>
                    <div>
                      <p className={`text-sm font-medium ${c.text}`}>{s.action}</p>
                      <p className={`text-xs ${c.accentTxt}`}>{s.timing}</p>
                      {s.why_first && <p className={`text-xs mt-0.5 ${c.textMuted}`}>{s.why_first}</p>}
                    </div>
                  </div>
                ))}
              </div>
            </div>
          )}

          {/* Watch-outs */}
          {results?.watch_outs?.length > 0 && (
            <div className={`${c.warning} border rounded-xl p-5`}>
              <h3 className={`font-bold text-sm mb-3 ${c.warningTxt}`}>⚠️ {t('pp_watch_title')}</h3>
              <ul className="space-y-1.5">
                {results.watch_outs.map((w, i) => (
                  <li key={i} className="text-sm flex items-start gap-2"><span className="mt-0.5 shrink-0">•</span>{w}</li>
                ))}
              </ul>
            </div>
          )}

          {/* Verify note */}
          {results?.verify_note && (
            <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
              <p className={`text-xs ${c.textMuted}`}>🧭 {results.verify_note}</p>
            </div>
          )}

          {/* Cross-refs */}
          <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
            <p className={`text-[10px] font-bold ${c.textMuted} uppercase mb-2`}>🔗 {t('pp_related')}</p>
            <div className="flex flex-wrap gap-3">
              <a href="/RentersDepositSaver" className={`text-xs ${linkStyle}`}>🏠 {t('pp_xref_deposit')}</a>
              <a href="/BillRescue" className={`text-xs ${linkStyle}`}>💸 {t('pp_xref_bill')}</a>
              <a href="/FinalWish" className={`text-xs ${linkStyle}`}>🕊️ {t('pp_xref_final')}</a>
            </div>
          </div>

        </div>
      )}
    </div>
  );
};

PaperworkPath.displayName = 'PaperworkPath';
export default PaperworkPath;
