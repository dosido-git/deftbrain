import React, { useState } from 'react';
import { useTheme } from '../hooks/useTheme';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { CopyBtn, ActionBar } from '../components/ActionButtons';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const EXAMPLE_BELIEFS = [
  'Hard work always pays off',
  'Money doesn\'t buy happiness',
  'You should follow your passion',
  'People don\'t change',
  'Everything in moderation',
  'Trust your gut',
  'Forgiveness is always the right choice',
  'Success requires sacrifice',
];

const SEVERITY_CONFIG = {
  fatal:       { label: 'FATAL',       color: (d) => d ? 'bg-[#b54a3f]/20 text-[#e8a9a3] border-[#b54a3f]/40' : 'bg-[#fceae8] text-[#8a3028] border-[#d4908a]' },
  significant: { label: 'SIGNIFICANT', color: (d) => d ? 'bg-[#c8872e]/20 text-[#d9a04e] border-[#c8872e]/40' : 'bg-[#fdf3e4] text-[#93541f] border-[#e0c49a]' },
  minor:       { label: 'MINOR',       color: (d) => d ? 'bg-[#3d3630]/60 text-[#c8c3b9] border-[#3d3630]'    : 'bg-[#f3efe8] text-[#5e5042] border-[#d5cab8]' },
};

const VERDICT_CONFIG = {
  mostly_true:          { icon: '✅', label: 'MOSTLY TRUE',          color: (d) => d ? 'bg-[#5a8a5c]/15 border-[#5a8a5c]/40 text-[#8aba8c]'    : 'bg-[#e8f0e8] border-[#a8c4a8] text-[#3d6e3f]' },
  context_dependent:    { icon: '◇',  label: 'CONTEXT-DEPENDENT',    color: (d) => d ? 'bg-[#2c4a6e]/15 border-[#2c4a6e]/40 text-[#a8b9ce]'    : 'bg-[#e8eef5] border-[#b0c4d8] text-[#1e3a58]' },
  useful_simplification:{ icon: '🔧', label: 'USEFUL SIMPLIFICATION', color: (d) => d ? 'bg-[#c8872e]/15 border-[#c8872e]/40 text-[#d9a04e]'   : 'bg-[#fdf3e4] border-[#e0c49a] text-[#93541f]' },
  mostly_false:         { icon: '❌', label: 'MOSTLY FALSE',          color: (d) => d ? 'bg-[#b54a3f]/15 border-[#b54a3f]/40 text-[#e8a9a3]'   : 'bg-[#fceae8] border-[#d4908a] text-[#8a3028]' },
  it_depends:           { icon: '🔵', label: 'IT DEPENDS',            color: (d) => d ? 'bg-[#3d3630]/60 border-[#3d3630] text-[#c8c3b9]'       : 'bg-[#f3efe8] border-[#d5cab8] text-[#5e5042]' },
};

const BeliefStressTest = () => {
  const { theme } = useTheme();
  const isDark = theme === 'dark';
  const { callToolEndpoint, loading } = useClaudeAPI();

  const c = {
    pageBg:       isDark ? 'bg-[#1a1816]'    : 'bg-[#faf8f5]',
    card:         isDark ? 'bg-[#2a2623]'     : 'bg-white',
    cardAlt:      isDark ? 'bg-[#332e2a]'     : 'bg-[#faf8f5]',
    cardBorder:   isDark ? 'border-[#3d3630]' : 'border-[#e8e1d5]',
    input:        isDark ? 'bg-[#1a1816] border-[#3d3630] text-[#f0eeea] placeholder:text-[#8a8275]'
                         : 'bg-[#faf8f5] border-[#d5cab8] text-[#3d3935] placeholder:text-[#8a8275]',
    inputFocus:   'focus:outline-none focus:ring-2 focus:ring-[#2c4a6e]/20 focus:border-[#4a6a8a]',
    text:          isDark ? 'text-[#f0eeea]'  : 'text-[#3d3935]',
    textSecondary: isDark ? 'text-[#c8c3b9]'  : 'text-[#5a544a]',
    textMuted:     isDark ? 'text-[#8a8275]'  : 'text-[#8a8275]',
    heading:       isDark ? 'text-[#f3efe8]'  : 'text-[#1e2a3a]',
    btnPrimary:    isDark ? 'bg-[#2c4a6e] hover:bg-[#4a6a8a] text-white' : 'bg-[#2c4a6e] hover:bg-[#1e3a58] text-white',
    btnSecondary:  isDark ? 'bg-[#332e2a] hover:bg-[#3d3630] text-[#c8c3b9] border border-[#3d3630]' : 'bg-[#f3efe8] hover:bg-[#e8e1d5] text-[#5e5042] border border-[#d5cab8]',
    divider:       isDark ? 'border-[#3d3630]' : 'border-[#e8e1d5]',
    error:         isDark ? 'bg-[#b54a3f]/15 border-[#b54a3f]/50 text-[#e8a9a3]' : 'bg-[#fceae8] border-[#d4908a] text-[#8a3028]',
  };

  const [belief, setBelief] = useState('');
  const [context, setContext] = useState('');
  const [results, setResults] = useState(null);
  const [error, setError] = useState('');
  const [expanded, setExpanded] = useState({ tests: true, hidden: false });

  const toggle = (k) => setExpanded(p => ({ ...p, [k]: !p[k] }));

  const handleSubmit = async () => {
    if (!belief.trim()) return;
    setError(''); setResults(null);
    try {
      const data = await callToolEndpoint('belief-stress-test', {
        belief: belief.trim(),
        context: context.trim() || undefined,
      });
      setResults(data);
    } catch (e) { setError(e.message || 'Failed to stress-test belief.'); }
  };

  const buildText = () => {
    if (!results) return '';
    let t = `🧪 BELIEF STRESS TEST\n\n"${belief}"\n\n`;
    t += `WHERE IT HOLDS:\n${results.where_it_holds?.the_conditions}\n\n`;
    t += `STRESS TESTS:\n`;
    results.stress_tests?.forEach(st => { t += `• [${st.test_label}] ${st.the_test}\n  ${st.what_it_reveals}\n`; });
    t += `\nTHE UPGRADE:\n${results.the_nuanced_version?.the_upgrade}\n`;
    t += `Conditions: ${results.the_nuanced_version?.the_key_conditions}\n\n`;
    t += `VERDICT: ${results.verdict?.rating_label} — ${results.verdict?.one_line}\n`;
    return t + BRAND;
  };

  const vcfg = results ? (VERDICT_CONFIG[results.verdict?.rating] || VERDICT_CONFIG.it_depends) : null;

  return (
    <div className={`min-h-screen py-8 px-4 ${c.pageBg}`}>
      <div className="max-w-2xl mx-auto">

        <div className="text-center mb-8">
          <div className="text-5xl mb-3">🧪</div>
          <h1 className={`text-3xl font-black tracking-tight mb-2 ${c.heading}`}>Belief Stress Test</h1>
          <p className={`text-sm ${c.textMuted}`}>Your guiding beliefs, pressure-tested. Where they hold. Where they break.</p>
        </div>

        {!results && (
          <div className={`rounded-2xl border p-6 shadow-sm space-y-4 ${c.card} ${c.cardBorder}`}>
            <div>
              <label className={`block text-sm font-semibold mb-1.5 ${c.text}`}>
                A belief that guides your life <span className="text-red-400">*</span>
              </label>
              <textarea value={belief} onChange={e => setBelief(e.target.value)}
                placeholder="A principle, rule, or conviction you operate by…"
                rows={2} maxLength={300}
                className={`w-full px-4 py-3 rounded-xl border text-sm resize-none ${c.input} ${c.inputFocus}`} />
              <div className="flex flex-wrap gap-2 mt-2">
                {EXAMPLE_BELIEFS.map(b => (
                  <button key={b} onClick={() => { setBelief(b); setResults(null); }}
                    className={`px-2.5 py-1 rounded-lg text-xs ${c.btnSecondary}`}>
                    {b}
                  </button>
                ))}
              </div>
            </div>

            <div>
              <label className={`block text-sm font-semibold mb-1.5 ${c.text}`}>Your context <span className={`font-normal ${c.textMuted}`}>(optional)</span></label>
              <input type="text" value={context} onChange={e => setContext(e.target.value)}
                placeholder="How this belief shows up in your life, where you apply it most…"
                className={`w-full px-4 py-3 rounded-xl border text-sm ${c.input} ${c.inputFocus}`} />
            </div>

            {error && <div className={`p-3 rounded-xl border text-sm ${c.error}`}><span className="mr-1">⚠️</span>{error}</div>}

            <button onClick={handleSubmit} disabled={loading || !belief.trim()}
              className={`w-full py-3 rounded-xl font-bold disabled:opacity-40 ${c.btnPrimary}`}>
              {loading ? <><span className="animate-spin inline-block mr-2">⏳</span>Running stress tests…</> : '🧪 Run the Stress Test'}
            </button>
          </div>
        )}

        {results && (
          <div className="space-y-4">

            {/* Verdict */}
            {results.verdict && (
              <div className={`rounded-2xl border p-5 text-center ${vcfg.color(isDark)}`}>
                <p className="text-3xl mb-2">{vcfg.icon}</p>
                <p className="text-xl font-black tracking-tight">{results.verdict.rating_label}</p>
                <p className="text-sm mt-1 opacity-80">{results.verdict.one_line}</p>
              </div>
            )}

            {/* Where it holds */}
            {results.where_it_holds && (
              <div className={`rounded-2xl border p-5 ${isDark ? 'bg-[#5a8a5c]/10 border-[#5a8a5c]/40' : 'bg-[#e8f0e8] border-[#a8c4a8]'}`}>
                <p className={`text-xs font-black uppercase tracking-widest mb-3 ${isDark ? 'text-[#8aba8c]' : 'text-[#3d6e3f]'}`}>✅ Where It Holds</p>
                <p className={`text-sm font-semibold mb-2 ${c.text}`}>{results.where_it_holds.the_conditions}</p>
                {results.where_it_holds.the_evidence_for && (
                  <p className={`text-xs mb-1 ${c.textMuted}`}><span className="font-semibold">Evidence for it:</span> {results.where_it_holds.the_evidence_for}</p>
                )}
                {results.where_it_holds.why_people_hold_it && (
                  <p className={`text-xs ${c.textMuted}`}><span className="font-semibold">Why it spread:</span> {results.where_it_holds.why_people_hold_it}</p>
                )}
              </div>
            )}

            {/* Stress tests */}
            {results.stress_tests?.length > 0 && (
              <div className={`rounded-2xl border overflow-hidden ${c.card} ${c.cardBorder}`}>
                <button onClick={() => toggle('tests')} className="w-full text-left px-5 py-4 flex items-center justify-between">
                  <p className={`text-xs font-black uppercase tracking-widest ${isDark ? 'text-[#e8a9a3]' : 'text-[#8a3028]'}`}>
                    🔬 Stress Tests ({results.stress_tests.length})
                  </p>
                  <span className={`text-sm ${c.textMuted}`}>{expanded.tests ? '▲' : '▼'}</span>
                </button>
                {expanded.tests && (
                  <div className={`border-t ${c.divider}`}>
                    {results.stress_tests.map((st, i) => {
                      const scfg = SEVERITY_CONFIG[st.severity] || SEVERITY_CONFIG.minor;
                      return (
                        <div key={i} className={`px-5 py-4 ${i > 0 ? `border-t ${c.divider}` : ''}`}>
                          <div className="flex items-center gap-2 mb-2 flex-wrap">
                            <span className={`text-xs font-bold px-2 py-0.5 rounded-full border ${scfg.color(isDark)}`}>{scfg.label}</span>
                            <span className={`text-xs font-semibold ${c.textMuted}`}>{st.test_label}</span>
                          </div>
                          <p className={`text-sm font-semibold mb-1 ${c.text}`}>{st.the_test}</p>
                          <p className={`text-xs ${c.textMuted}`}>{st.what_it_reveals}</p>
                        </div>
                      );
                    })}
                  </div>
                )}
              </div>
            )}

            {/* Hidden structure */}
            {results.the_hidden_structure && (
              <div className={`rounded-2xl border overflow-hidden ${c.card} ${c.cardBorder}`}>
                <button onClick={() => toggle('hidden')} className="w-full text-left px-5 py-4 flex items-center justify-between">
                  <p className={`text-xs font-bold uppercase tracking-wider ${c.textMuted}`}>🔦 The Hidden Structure</p>
                  <span className={`text-sm ${c.textMuted}`}>{expanded.hidden ? '▲' : '▼'}</span>
                </button>
                {expanded.hidden && (
                  <div className={`px-5 pb-4 space-y-3 border-t ${c.divider} pt-4`}>
                    {results.the_hidden_structure.what_its_really_saying && (
                      <p className={`text-sm ${c.textSecondary}`}><span className={`font-semibold ${c.text}`}>What it's really saying:</span> {results.the_hidden_structure.what_its_really_saying}</p>
                    )}
                    {results.the_hidden_structure.the_psychological_function && (
                      <p className={`text-sm ${c.textSecondary}`}><span className={`font-semibold ${c.text}`}>Psychological function:</span> {results.the_hidden_structure.the_psychological_function}</p>
                    )}
                    {results.the_hidden_structure.when_it_becomes_harmful && (
                      <div className={`p-3 rounded-xl border ${isDark ? 'bg-[#b54a3f]/10 border-[#b54a3f]/30' : 'bg-[#fceae8] border-[#d4908a]'}`}>
                        <p className={`text-xs font-bold uppercase tracking-wide mb-1 ${isDark ? 'text-[#e8a9a3]' : 'text-[#8a3028]'}`}>When it becomes harmful</p>
                        <p className={`text-sm ${c.textSecondary}`}>{results.the_hidden_structure.when_it_becomes_harmful}</p>
                      </div>
                    )}
                  </div>
                )}
              </div>
            )}

            {/* The nuanced version */}
            {results.the_nuanced_version && (
              <div className={`rounded-2xl border-2 p-5 ${isDark ? 'border-[#2c4a6e] bg-[#2c4a6e]/10' : 'border-[#2c4a6e] bg-[#e8eef5]'}`}>
                <p className={`text-xs font-black uppercase tracking-widest mb-3 ${isDark ? 'text-[#a8b9ce]' : 'text-[#2c4a6e]'}`}>⬆️ The Upgrade</p>
                <p className={`text-sm font-bold mb-2 ${c.text}`}>{results.the_nuanced_version.the_upgrade}</p>
                {results.the_nuanced_version.the_key_conditions && (
                  <p className={`text-xs mb-2 ${c.textMuted}`}><span className="font-semibold">When it's actually true:</span> {results.the_nuanced_version.the_key_conditions}</p>
                )}
                {results.the_nuanced_version.example_of_upgrade_in_action && (
                  <p className={`text-xs mb-3 italic ${c.textMuted}`}>{results.the_nuanced_version.example_of_upgrade_in_action}</p>
                )}
                <CopyBtn content={`🧪 Upgraded belief:\n\n${results.the_nuanced_version.the_upgrade}${BRAND}`} label="Copy Upgrade" />
              </div>
            )}

            <div className="flex items-center gap-3 flex-wrap">
              <ActionBar content={buildText()} title="Belief Stress Test" />
              <button onClick={() => { setResults(null); setBelief(''); setContext(''); }}
                className={`px-4 py-2 rounded-lg text-sm font-medium ${c.btnSecondary}`}>
                🔄 New Belief
              </button>
            </div>

            <div className={`rounded-xl border p-4 ${c.cardAlt} ${c.cardBorder}`}>
              <p className={`text-xs font-semibold uppercase tracking-wide mb-3 ${c.textMuted}`}>Related tools</p>
              <div className="flex flex-wrap gap-2">
                {[
                  { id: 'EgoKiller', icon: '🪦', label: 'Full demolition & rebuild' },
                  { id: 'SignalVsNoise', icon: '📡', label: 'Cut through contradictions' },
                  { id: 'TheFinalWord', icon: '⚖️', label: 'Fact-check a specific claim' },
                ].map(r => (
                  <a key={r.id} href={`/tool/${r.id}`} target="_blank" rel="noopener noreferrer"
                    className={`px-3 py-1.5 rounded-lg text-xs font-medium transition-colors ${c.btnSecondary}`}>
                    {r.icon} {r.label}
                  </a>
                ))}
              </div>
            </div>
          </div>
        )}
      </div>
    </div>
  );
};

BeliefStressTest.displayName = 'BeliefStressTest';
export default BeliefStressTest;
