import React, { useState, useEffect, useRef, useCallback } from 'react';
import { useTheme } from '../hooks/useTheme';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useRegisterActions } from '../components/ActionBarContext';
import { usePersistentState } from '../hooks/usePersistentState';
import { useTranslation } from '../i18n/useTranslation';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const SEVERITY_CONFIG = {
  fatal:       { labelKey: 'bst_sev_fatal',       colorKey: 'severityFatal' },
  significant: { labelKey: 'bst_sev_significant', colorKey: 'severitySignificant' },
  minor:       { labelKey: 'bst_sev_minor',       colorKey: 'severityMinor' },
};

const VERDICT_CONFIG = {
  mostly_true:           { icon: '✅', labelKey: 'bst_v_true',    colorKey: 'verdictTrue' },
  context_dependent:     { icon: '◇',  labelKey: 'bst_v_context', colorKey: 'verdictContext' },
  useful_simplification: { icon: '🔧', labelKey: 'bst_v_useful',  colorKey: 'verdictUseful' },
  mostly_false:          { icon: '❌', labelKey: 'bst_v_false',   colorKey: 'verdictFalse' },
  it_depends:            { icon: '🔵', labelKey: 'bst_v_depends', colorKey: 'verdictDepends' },
};

const BeliefStressTest = ({ tool }) => {
  const { callToolEndpoint, loading, userLocale, userCurrency, userRegion } = useClaudeAPI();
  const { isDark } = useTheme();
  const { t } = useTranslation();

  const c = {
    card:          isDark ? 'bg-zinc-800'      : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50'   : 'bg-slate-50',
    cardAltHover:  isDark ? 'hover:bg-zinc-700' : 'hover:bg-gray-100',
    text:          isDark ? 'text-zinc-50'     : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300'    : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-500'    : 'text-gray-400',
    textMuteded:   isDark ? 'text-zinc-500'    : 'text-gray-400',
    input:         isDark
      ? 'bg-zinc-700 border-zinc-600 text-zinc-100 placeholder:text-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20'
      : 'bg-white border-gray-300 text-gray-900 placeholder:text-gray-400 focus:border-cyan-500 focus:ring-cyan-500/20',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white' : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-100' : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700'  : 'border-gray-200',
    borderHover:   isDark ? 'hover:border-zinc-500' : 'hover:border-gray-400',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200'  : 'bg-red-50 border-red-200 text-red-800',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200' : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200' : 'bg-amber-50 border-amber-300 text-amber-800',
    required:      isDark ? 'text-amber-400'   : 'text-amber-500',
    amberText:     isDark ? 'text-amber-400'   : 'text-amber-600',
    labelText:     isDark ? 'text-zinc-200'    : 'text-gray-700',
    deleteHover:   'hover:text-red-400',
    // Severity classes (compound: bg + text + border)
    severityFatal:       isDark ? 'bg-red-900/20 text-red-200 border-red-700/50'     : 'bg-red-50 text-red-800 border-red-300',
    severitySignificant: isDark ? 'bg-amber-900/20 text-amber-200 border-amber-700/50' : 'bg-amber-50 text-amber-800 border-amber-300',
    severityMinor:       isDark ? 'bg-zinc-700/60 text-zinc-300 border-zinc-600'       : 'bg-gray-100 text-gray-600 border-gray-300',
    // Verdict classes (compound: bg + border + text)
    verdictTrue:    isDark ? 'bg-green-900/20 border-green-700/50 text-green-200'   : 'bg-green-50 border-green-300 text-green-800',
    verdictContext: isDark ? 'bg-sky-900/20 border-sky-700/50 text-sky-200'         : 'bg-sky-50 border-sky-300 text-sky-800',
    verdictUseful:  isDark ? 'bg-amber-900/20 border-amber-700/50 text-amber-200'   : 'bg-amber-50 border-amber-300 text-amber-800',
    verdictFalse:   isDark ? 'bg-red-900/20 border-red-700/50 text-red-200'         : 'bg-red-50 border-red-300 text-red-800',
    verdictDepends: isDark ? 'bg-zinc-700/60 border-zinc-600 text-zinc-300'         : 'bg-gray-100 border-gray-300 text-gray-600',
  };
  // Always include these two alias lines immediately after the closing brace (per CONVENTIONS.md):
  c.textMuteded = c.textMuted;
  c.label = c.labelText;

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';

  const [context, setContext] = useState('');
  const [error, setError] = useState('');
  const [expanded, setExpanded] = useState({ tests: true, hidden: false });
  const [belief, setBelief] = usePersistentState('beliefstresstest-belief', '');
  const [sessionHistory, setSessionHistory] = usePersistentState('beliefstresstest-history', []);
  const [results, setResults] = usePersistentState('beliefstresstest-last', null);

  const EXAMPLE_BELIEFS = [
    t('bst_ex1'),
    t('bst_ex2'),
    t('bst_ex3'),
    t('bst_ex4'),
    t('bst_ex5'),
    t('bst_ex6'),
    t('bst_ex7'),
    t('bst_ex8'),
  ];

  const toggle = (k) => setExpanded(p => ({ ...p, [k]: !p[k] }));

  const handleSubmit = async () => {
    if (!belief.trim()) return;
    setError(''); setResults(null);
    try {
      const data = await callToolEndpoint('belief-stress-test', {
        belief: belief.trim(),
        context: context.trim() || undefined,
        userLocale, userCurrency, userRegion,
      });
      setResults(data);
      const newEntry = {
        id: Date.now(),
        timestamp: new Date().toISOString(),
        preview: belief.trim().slice(0, 40),
        result: data,
      };
      setSessionHistory(prev => [newEntry, ...prev].slice(0, 6));
    } catch (e) { setError(e.message || t('bst_error')); }
  };

  const handleReset = () => {
    setResults(null);
    setBelief('');
    setContext('');
    setError('');
  };

  const loadExample = useCallback(() => {
    setBelief(t('bst_ex1'));
    setContext('');
    setResults(null);
  }, [setBelief, setResults, t]);

  const buildFullText = useCallback(() => {
    if (!results) return '';
    let out = `🧪 ${t('bst_copy_header')}\n\n"${belief}"\n\n`;
    out += `${t('bst_copy_where')}\n${results.where_it_holds?.the_conditions}\n\n`;
    out += `${t('bst_copy_tests')}\n`;
    results.stress_tests?.forEach(st => { out += `• [${st.test_label}] ${st.the_test}\n  ${st.what_it_reveals}\n`; });
    out += `\n${t('bst_copy_upgrade')}\n${results.the_nuanced_version?.the_upgrade}\n`;
    out += `${t('bst_copy_conditions')} ${results.the_nuanced_version?.the_key_conditions}\n\n`;
    out += `${t('bst_copy_verdict')} ${results.verdict?.rating_label} — ${results.verdict?.one_line}\n`;
    return out + BRAND;
  }, [results, belief, t]);

  const vcfg = results ? (VERDICT_CONFIG[results.verdict?.rating] || VERDICT_CONFIG.it_depends) : null;

  // Stale-closure-safe refs for global keyboard handler
  const handleSubmitRef = useRef(null);
  const canSubmitRef = useRef(false);
  handleSubmitRef.current = handleSubmit;
  canSubmitRef.current = !!(belief?.trim()) && !loading;

  useRegisterActions(buildFullText(), tool?.title || 'Belief Stress Test');

  useEffect(() => {
    const handler = (e) => {
      const tag = document.activeElement?.tagName;
      if (tag === 'SELECT') return;
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && canSubmitRef.current) {
        e.preventDefault();
        handleSubmitRef.current?.();
      }
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
  }, []);

  return (
    <div className={`space-y-4 ${c.text}`}>

      {/* Persistent header — shown on every screen (PF-16: reset button top-right) */}
      <div className="pb-3 border-b border-zinc-500">
        <div className="flex items-center justify-between">
          <div>
            <h2 className={`text-xl font-bold ${c.text}`}>
              <span className="mr-2">{tool?.icon ?? '🔬'}</span>{tool?.title ?? t('bst_title')}
            </h2>
            <p className={`text-sm ${c.textSecondary}`}>{tool?.tagline ?? t('bst_tagline')}</p>
            <button onClick={loadExample} disabled={loading} style={{ backgroundColor: (tool?.headerColor ?? '#888888') + '80' }} className={`mt-1.5 px-2.5 py-0.5 rounded-full text-xs font-medium border disabled:opacity-40 ${isDark ? 'text-white border-white/40' : 'text-gray-800 border-transparent'}`}>{t('try_example')}</button>
          </div>
          {(results || belief.trim()) ? (
            <button onClick={handleReset} className={`${c.btnSecondary} px-3 py-1.5 rounded-lg text-xs font-bold flex-shrink-0`}>
              ↺ {t('start_over')}
            </button>
          ) : null}
        </div>
      </div>

      {sessionHistory.length > 0 && !results && (
        <div className={`${c.card} rounded-xl border ${c.border} p-4`}>
          <h3 className={`text-sm font-bold ${c.text} mb-3`}>🕐 {t('bst_recent')}</h3>
          <div className="space-y-1.5">
            {sessionHistory.map(entry => (
              <button key={entry.id}
                onClick={() => setResults(entry.result)}
                className={`w-full text-left px-3 py-2 rounded-lg ${c.btnSecondary} border ${c.border} text-xs`}>
                <span className={c.textMuteded}>
                  {new Date(entry.timestamp).toLocaleDateString(undefined, { month: 'short', day: 'numeric', hour: 'numeric', minute: '2-digit' })}
                </span>
                <span className={`ml-2 ${c.text}`}>{entry.preview}…</span>
              </button>
            ))}
          </div>
        </div>
      )}

      {!results && (
          <div className={`${c.card} rounded-xl shadow-sm p-5 space-y-4`}>
            <div>
              <label className={`block text-sm font-semibold mb-1.5 ${c.text}`}>
                {t('bst_belief_label')} <span className={c.required}>*</span>
              </label>
              <textarea value={belief} onChange={e => setBelief(e.target.value)}
                placeholder={t('bst_belief_ph')}
                rows={2} maxLength={300}
                onKeyDown={e => { if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && belief.trim()) { e.preventDefault(); handleSubmit(); } }}
                className={`w-full px-4 py-3 rounded-xl border text-sm resize-none ${c.input} focus:outline-none focus:ring-2`} />
              <div className="flex flex-wrap gap-2 mt-2">
                {EXAMPLE_BELIEFS.map(b => (
                  <button key={b} onClick={() => { setBelief(b); setResults(null); }}
                    className={`px-2.5 py-1 rounded-lg text-xs ${c.btnSecondary}`}>
                    {b}
                  </button>
                ))}
              </div>
            </div>

            <div>
              <label className={`block text-sm font-semibold mb-1.5 ${c.text}`}>{t('bst_context_label')} <span className={`font-normal ${c.textMuteded}`}>{t('bst_optional_paren')}</span></label>
              <input type="text" value={context} onChange={e => setContext(e.target.value)}
                placeholder={t('bst_context_ph')}
                onKeyDown={e => { if (e.key === 'Enter' && belief.trim()) { e.preventDefault(); handleSubmit(); } }}
                className={`w-full px-4 py-3 rounded-xl border text-sm ${c.input} focus:outline-none focus:ring-2`} />
            </div>

            {error && <div className={`p-3 rounded-xl border text-sm ${c.danger}`}><span className="mr-1">⚠️</span>{error}</div>}

            <p className={`text-xs text-center ${c.textMuteded}`}>
              {t('bst_pre_xref')}{' '}
              <a href="/TheFinalWord" className={linkStyle}>{t('bst_finalword')}</a> {t('bst_pre_xref_after')}
            </p>

            <button onClick={handleSubmit} disabled={loading || !belief.trim()}
              className={`w-full py-3 rounded-xl font-bold disabled:opacity-40 ${c.btnPrimary}`}>
              {loading ? <><span className="animate-spin inline-block mr-2">{tool?.icon ?? '🔬'}</span>{t('bst_running')}</> : <><span className="mr-2">{tool?.icon ?? '🔬'}</span>{t('bst_run')}</>}
            </button>
          </div>
        )}

        {results && (
          <div className="space-y-4">


            {results.belief_as_understood && (
              <p className={`text-xs font-medium text-center ${c.textMuted} -mb-2`}>{t('bst_analyzing')} "{results.belief_as_understood}"</p>
            )}

            {/* Verdict */}
            {results.verdict && (
              <div className={`rounded-2xl border p-5 text-center ${c[vcfg.colorKey] || c.text}`}>
                <p className="text-3xl mb-2">{vcfg.icon}</p>
                <p className="text-xl font-black tracking-tight">{results.verdict.rating_label}</p>
                <p className="text-sm mt-1 opacity-80">{results.verdict.one_line}</p>
                {results.belief_type && (
                  <p className={`text-[10px] font-bold uppercase tracking-wider mt-2 opacity-60`}>{results.belief_type} {t('bst_belief_suffix')}</p>
                )}
              </div>
            )}

            {/* Where it holds */}
            {results.where_it_holds && (
              <div className={`rounded-2xl border p-5 ${isDark ? 'bg-green-900/20 border-green-700/50' : 'bg-green-50 border-green-200'}`}>
                <p className={`text-xs font-black uppercase tracking-widest mb-3 ${isDark ? 'text-green-300' : 'text-green-700'}`}>✅ {t('bst_where_holds')}</p>
                <p className={`text-sm font-semibold mb-2 ${c.text}`}>{results.where_it_holds.the_conditions}</p>
                {results.where_it_holds.the_evidence_for && (
                  <p className={`text-xs mb-1 ${c.textMuteded}`}><span className="font-semibold">{t('bst_evidence_for')}</span> {results.where_it_holds.the_evidence_for}</p>
                )}
                {results.where_it_holds.why_people_hold_it && (
                  <p className={`text-xs ${c.textMuteded}`}><span className="font-semibold">{t('bst_why_spread')}</span> {results.where_it_holds.why_people_hold_it}</p>
                )}
              </div>
            )}

            {/* Stress tests */}
            {results.stress_tests?.length > 0 && (
              <div className={`rounded-2xl border overflow-hidden ${c.card} border ${c.border}`}>
                <button onClick={() => toggle('tests')} className="w-full text-left px-5 py-4 flex items-center justify-between">
                  <p className={`text-xs font-black uppercase tracking-widest ${isDark ? 'text-red-300' : 'text-red-700'}`}>
                    🔬 {t('bst_stress_tests')} ({results.stress_tests.length})
                  </p>
                  <span className={`text-sm ${c.textMuteded}`}>{expanded.tests ? '▲' : '▼'}</span>
                </button>
                {expanded.tests && (
                  <div className={`border-t ${c.border}`}>
                    {results.stress_tests.map((st, i) => {
                      const scfg = SEVERITY_CONFIG[st.severity] || SEVERITY_CONFIG.minor;
                      return (
                        <div key={i} className={`px-5 py-4 ${i > 0 ? `border-t ${c.border}` : ''}`}>
                          <div className="flex items-center gap-2 mb-2 flex-wrap">
                            <span className={`text-xs font-bold px-2 py-0.5 rounded-full border ${c[scfg.colorKey] || c.textMuted}`}>{t(scfg.labelKey)}</span>
                            <span className={`text-xs font-semibold ${c.textMuteded}`}>{st.test_label}</span>
                          </div>
                          <p className={`text-sm font-semibold mb-1 ${c.text}`}>{st.the_test}</p>
                          <p className={`text-xs ${c.textMuteded}`}>{st.what_it_reveals}</p>
                        </div>
                      );
                    })}
                  </div>
                )}
              </div>
            )}

            {/* Hidden structure */}
            {results.the_hidden_structure && (
              <div className={`rounded-2xl border overflow-hidden ${c.card} border ${c.border}`}>
                <button onClick={() => toggle('hidden')} className="w-full text-left px-5 py-4 flex items-center justify-between">
                  <p className={`text-xs font-bold uppercase tracking-wider ${c.textMuteded}`}>🔦 {t('bst_hidden_structure')}</p>
                  <span className={`text-sm ${c.textMuteded}`}>{expanded.hidden ? '▲' : '▼'}</span>
                </button>
                {expanded.hidden && (
                  <div className={`px-5 pb-4 space-y-3 border-t ${c.border} pt-4`}>
                    {results.the_hidden_structure.what_its_really_saying && (
                      <p className={`text-sm ${c.textSecondary}`}><span className={`font-semibold ${c.text}`}>{t('bst_really_saying')}</span> {results.the_hidden_structure.what_its_really_saying}</p>
                    )}
                    {results.the_hidden_structure.the_psychological_function && (
                      <p className={`text-sm ${c.textSecondary}`}><span className={`font-semibold ${c.text}`}>{t('bst_psych_function')}</span> {results.the_hidden_structure.the_psychological_function}</p>
                    )}
                    {results.the_hidden_structure.when_it_becomes_harmful && (
                      <div className={`p-3 rounded-xl border ${isDark ? 'bg-red-900/15 border-red-700/40' : 'bg-red-50 border-red-200'}`}>
                        <p className={`text-xs font-bold uppercase tracking-wide mb-1 ${isDark ? 'text-red-300' : 'text-red-700'}`}>{t('bst_when_harmful')}</p>
                        <p className={`text-sm ${c.textSecondary}`}>{results.the_hidden_structure.when_it_becomes_harmful}</p>
                      </div>
                    )}
                  </div>
                )}
              </div>
            )}

            {/* The nuanced version */}
            {results.the_nuanced_version && (
              <div className={`rounded-2xl border-2 p-5 ${isDark ? 'border-sky-700 bg-sky-900/20' : 'border-sky-300 bg-sky-50'}`}>
                <p className={`text-xs font-black uppercase tracking-widest mb-3 ${isDark ? 'text-sky-300' : 'text-sky-700'}`}>⬆️ {t('bst_upgrade')}</p>
                <p className={`text-sm font-bold mb-2 ${c.text}`}>{results.the_nuanced_version.the_upgrade}</p>
                {results.the_nuanced_version.the_key_conditions && (
                  <p className={`text-xs mb-2 ${c.textMuteded}`}><span className="font-semibold">{t('bst_actually_true')}</span> {results.the_nuanced_version.the_key_conditions}</p>
                )}
                {results.the_nuanced_version.still_useful_because && (
                  <p className={`text-xs mb-2 ${c.textMuteded}`}><span className="font-semibold">{t('bst_worth_keeping')}</span> {results.the_nuanced_version.still_useful_because}</p>
                )}
                {results.the_nuanced_version.example_of_upgrade_in_action && (
                  <p className={`text-xs mb-3 italic ${c.textMuteded}`}>{results.the_nuanced_version.example_of_upgrade_in_action}</p>
                )}
              </div>
            )}



            <div className="space-y-2">
              <p className={`text-xs text-center ${c.textMuteded}`}>
                {t('bst_go_deeper')}{' '}
                <a href="/EgoKiller" className={linkStyle}>{t('bst_egokiller')}</a>{' '}
                {t('bst_go_deeper_after')}
              </p>
              {results?.verdict?.rating === 'mostly_false' && (
                <p className={`text-xs text-center ${c.textMuteded}`}>
                  {t('bst_didnt_hold')}{' '}
                  <a href="/EgoKiller" className={linkStyle}>{t('bst_egokiller')}</a>{' '}
                  {t('bst_egokiller_after')}
                </p>
              )}
              {(results?.stress_tests?.some(st => st.severity === 'fatal')) && results?.verdict?.rating !== 'mostly_false' && (
                <p className={`text-xs text-center ${c.textMuteded}`}>
                  {t('bst_fatal_found')}{' '}
                  <a href="/TheFinalWord" className={linkStyle}>{t('bst_finalword')}</a>{' '}
                  {t('bst_finalword_after')}
                </p>
              )}
            </div>
          </div>
        )}

      </div>
  );
};

BeliefStressTest.displayName = 'BeliefStressTest';
export default BeliefStressTest;
