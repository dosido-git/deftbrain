import React, { useState, useRef, useCallback, useEffect } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { usePersistentState } from '../hooks/usePersistentState';
import { useRegisterActions } from '../components/ActionBarContext';
import { useTranslation } from '../i18n/useTranslation';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

// ════════════════════════════════════════════════════════════
// CONSTANTS
// ════════════════════════════════════════════════════════════
// `value` and `labelKey`/`placeholderKey` are stable identifiers; the visible
// text is resolved through t() at render time.
const CATEGORIES = [
  { value: 'name',      emoji: '👤', labelKey: 'pir_cat_name',      placeholderKey: 'pir_ph_name' },
  { value: 'food',      emoji: '🍝', labelKey: 'pir_cat_food',      placeholderKey: 'pir_ph_food' },
  { value: 'place',     emoji: '📍', labelKey: 'pir_cat_place',     placeholderKey: 'pir_ph_place' },
  { value: 'brand',     emoji: '👗', labelKey: 'pir_cat_brand',     placeholderKey: 'pir_ph_brand' },
  { value: 'music_art', emoji: '🎵', labelKey: 'pir_cat_music_art', placeholderKey: 'pir_ph_music_art' },
  { value: 'science',   emoji: '🔬', labelKey: 'pir_cat_science',   placeholderKey: 'pir_ph_science' },
  { value: 'phrase',    emoji: '💬', labelKey: 'pir_cat_phrase',    placeholderKey: 'pir_ph_phrase' },
  { value: 'other',     emoji: '❓', labelKey: 'pir_cat_other',     placeholderKey: 'pir_ph_other' },
];

// `value` is the backend identifier (kept English); `labelKey` is the localized label.
const LANGUAGES = [
  { value: 'English (American)', labelKey: 'pir_lang_en_us' },
  { value: 'English (British)',  labelKey: 'pir_lang_en_gb' },
  { value: 'Spanish',            labelKey: 'pir_lang_es' },
  { value: 'French',             labelKey: 'pir_lang_fr' },
  { value: 'German',             labelKey: 'pir_lang_de' },
  { value: 'Italian',            labelKey: 'pir_lang_it' },
  { value: 'Portuguese',         labelKey: 'pir_lang_pt' },
  { value: 'Mandarin Chinese',   labelKey: 'pir_lang_zh' },
  { value: 'Japanese',           labelKey: 'pir_lang_ja' },
  { value: 'Korean',             labelKey: 'pir_lang_ko' },
  { value: 'Arabic',             labelKey: 'pir_lang_ar' },
  { value: 'Hindi',              labelKey: 'pir_lang_hi' },
  { value: 'Russian',            labelKey: 'pir_lang_ru' },
  { value: 'Dutch',              labelKey: 'pir_lang_nl' },
  { value: 'Swedish',            labelKey: 'pir_lang_sv' },
  { value: 'Turkish',            labelKey: 'pir_lang_tr' },
  { value: 'Polish',             labelKey: 'pir_lang_pl' },
  { value: 'Other',              labelKey: 'pir_lang_other' },
];

const EXAMPLES = [
  { word: 'Gnocchi', category: 'food' },
  { word: 'Hermès', category: 'brand' },
  { word: 'Siobhan', category: 'name' },
  { word: 'Worcestershire', category: 'place' },
  { word: 'Chopin', category: 'music_art' },
  { word: 'Schadenfreude', category: 'phrase' },
  { word: 'Quinoa', category: 'food' },
  { word: 'Dvořák', category: 'music_art' },
];

// ════════════════════════════════════════════════════════════
// MAIN
// ════════════════════════════════════════════════════════════
const PronounceItRight = ({ tool }) => {
  const { callToolEndpoint, loading, userLocale, userCurrency, userRegion } = useClaudeAPI();
  const { isDark } = useTheme();
  const { t } = useTranslation();

  const c = {
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:         isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20'
                          : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-400' : 'text-gray-500',
    required:      isDark ? 'text-amber-400' : 'text-amber-700',
    labelText:     isDark ? 'text-zinc-200' : 'text-gray-700',
    accentTxt:     isDark ? 'text-cyan-400' : 'text-cyan-600',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white'
                          : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200'
                          : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200'
                          : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200'
                          : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200'
                          : 'bg-red-50 border-red-200 text-red-800',
    infoBox:       isDark ? 'bg-sky-900/20 border-sky-700 text-sky-200'
                          : 'bg-sky-50 border-sky-200 text-sky-800',
    successBox:    isDark ? 'bg-emerald-900/20 border-emerald-700' : 'bg-emerald-50 border-emerald-300',
    successTxt:    isDark ? 'text-emerald-300' : 'text-emerald-800',
    warningTxt:    isDark ? 'text-amber-300' : 'text-amber-800',
    pillActive:    isDark ? 'border-cyan-500 bg-cyan-900/30 text-cyan-200'
                          : 'border-cyan-600 bg-cyan-100 text-cyan-900',
    pillInactive:  isDark ? 'border-zinc-600 text-zinc-400 hover:border-zinc-500'
                          : 'border-gray-300 text-gray-500 hover:border-gray-400',
    // Bespoke keys for this tool
    tipBg:         isDark ? 'bg-amber-900/20 border-amber-700/40' : 'bg-amber-50 border-amber-200',
    tipText:       isDark ? 'text-amber-300' : 'text-amber-700',
    phonBg:        isDark ? 'bg-sky-900/20 border-sky-700/40' : 'bg-sky-50 border-sky-200',
    inset:         isDark ? 'bg-zinc-900/60' : 'bg-slate-50',
    ipaBg:         isDark ? 'bg-zinc-700' : 'bg-slate-100',
  };
  c.textMuteded = c.textMuted;
  c.label = c.labelText;

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-700 hover:text-cyan-800 underline underline-offset-2';

  const resultsRef = useRef(null);

  const [showHistory, setShowHistory] = useState(false);

  // Input
  const [word, setWord] = useState('');
  const [category, setCategory] = useState('other');
  const [context, setContext] = useState('');
  const [nativeLang, setNativeLang] = useState('English (American)');
  const [showIPA, setShowIPA] = useState(false);

  // Batch mode
  const [batchMode, setBatchMode] = useState(false);
  const [batchWords, setBatchWords] = useState('');

  // Results (transient session state — not persisted)
  const [batchResults, setBatchResults] = useState(null);
  const [error, setError] = useState('');
  const [expandedSections, setExpandedSections] = useState({});

  // Audio
  const [audioUrl, setAudioUrl] = useState(null);
  const [audioLoading, setAudioLoading] = useState(false);

  // Persistent
  const [sessionHistory, setSessionHistory] = usePersistentState('pronounce-it-right-history', []);
  const [results, setResults] = usePersistentState('pronounce-it-right-results', null);

  useEffect(() => {
    if (!(results || batchResults) || !resultsRef.current) return;
    const timer = setTimeout(() => resultsRef.current?.scrollIntoView({ behavior: 'smooth', block: 'start' }), 200);
    return () => clearTimeout(timer);
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [results, batchResults]);

  const toggleSection = useCallback((key) => setExpandedSections(prev => ({ ...prev, [key]: !prev[key] })), []);

  const activeCat = CATEGORIES.find(cat => cat.value === category) || CATEGORIES[7];

  const submit = useCallback(async () => {
    setError(''); setResults(null); setBatchResults(null);
    try {
      if (batchMode) {
        const words = batchWords.split(/[,\n]/).map(w => w.trim()).filter(Boolean);
        if (words.length < 2) { setError(t('pir_err_min_words')); return; }
        const data = await callToolEndpoint('pronounce-it-right/batch', { words, category, nativeLang, userLocale, userCurrency, userRegion });
        setBatchResults(data);
        words.forEach(w => {
          const entry = { id: 'sir_' + Date.now() + Math.random(), date: new Date().toISOString(), word: w, category, emoji: activeCat.emoji, preview: `${activeCat.emoji} ${w}` };
          setSessionHistory(prev => [entry, ...prev].slice(0, 6));
        });
      } else {
        if (!word.trim()) { setError(t('pir_err_enter_word')); return; }
        const data = await callToolEndpoint('pronounce-it-right', { word: word.trim(), category, context: context.trim() || null, nativeLang, userLocale, userCurrency, userRegion });
        setResults(data);
        const entry = { id: 'sir_' + Date.now(), date: new Date().toISOString(), word: word.trim(), category, emoji: activeCat.emoji, phonetic: data?.pronunciation?.phonetic, preview: `${activeCat.emoji} ${word.trim()}` };
        setSessionHistory(prev => [entry, ...prev].slice(0, 6));
      }
    } catch (err) { setError(err.message || t('pir_err_failed')); }
  }, [word, category, context, nativeLang, batchMode, batchWords, callToolEndpoint, setSessionHistory, activeCat.emoji, userLocale, userCurrency, userRegion, t]);

  const handleReset = useCallback(() => {
    setWord(''); setContext(''); setResults(null); setBatchResults(null); setError('');
    setBatchWords(''); setExpandedSections({}); setAudioUrl(null);
  }, []);

  const loadExample = useCallback((ex) => {
    setWord(ex.word); setCategory(ex.category);
    setResults(null); setBatchResults(null); setError(''); setBatchMode(false); setAudioUrl(null);
  }, []);

  const fetchAudio = useCallback(async () => {
    if (!results?.word) return;
    setAudioLoading(true); setAudioUrl(null); setError('');
    try {
      const base = process.env.REACT_APP_API_URL || 'http://localhost:3001';
      const res = await fetch(`${base}/api/pronounce-it-right-audio`, {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ word: results.word, languageOfOrigin: results.language_of_origin || null }),
      });
      if (!res.ok) throw new Error('Audio unavailable');
      const blob = await res.blob();
      setAudioUrl(URL.createObjectURL(blob));
    } catch {
      setError(t('pir_err_audio'));
    } finally { setAudioLoading(false); }
  }, [results, t]);

  const buildCopy = useCallback(() => {
    if (!results) return '';
    const p = results.pronunciation;
    const lines = [
      `🗣️ ${t('pir_title')} — ${results.word}`,
      '',
      `${t('pir_copy_pronunciation')} ${p?.phonetic || ''}`,
      p?.ipa ? `${t('pir_copy_ipa')} ${p.ipa}` : '',
      p?.syllables?.length ? `${t('pir_copy_syllables')} ${p.syllables.join(' · ')}` : '',
      p?.stress ? `${t('pir_copy_stress')} ${p.stress}` : '',
      p?.sounds_like ? `${t('pir_copy_sounds_like')} ${p.sounds_like}` : '',
      '',
      results.context_info?.what_it_is ? `${t('pir_copy_what_it_is')} ${results.context_info.what_it_is}` : '',
      results.fun_fact ? `${t('pir_copy_fun_fact')} ${results.fun_fact}` : '',
      '',
      BRAND,
    ].filter(Boolean);
    return lines.join('\n');
  }, [results, t]);

  useRegisterActions(buildCopy(), tool?.title || 'Pronounce It Right');

  const submitRef = useRef(null);
  const canSubmitRef = useRef(false);
  submitRef.current = submit;
  canSubmitRef.current = batchMode ? !!batchWords.trim() : !!word.trim();

  useEffect(() => {
    const handler = (e) => {
      const tag = document.activeElement?.tagName;
      if (tag === 'SELECT') return;
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && !loading && canSubmitRef.current)
        submitRef.current?.();
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [loading]);

  const Pill = ({ active, onClick, children }) => (
    <button onClick={onClick} className={'px-3 py-1.5 rounded-lg border text-xs font-semibold transition-all ' + (active ? c.pillActive : c.pillInactive)}>
      {active && <span className="me-1">✓</span>}{children}
    </button>
  );

  const Section = ({ title, emoji, sKey, defaultOpen = false, children }) => {
    const open = expandedSections[sKey] ?? defaultOpen;
    return (
      <div className={c.card + ' border rounded-xl overflow-hidden'}>
        <button onClick={() => toggleSection(sKey)} className="w-full flex items-center justify-between p-4 text-start hover:opacity-80">
          <div className="flex items-center gap-2">
            <span className="text-base">{emoji}</span>
            <span className={'text-sm font-semibold ' + c.text}>{title}</span>
          </div>
          <span className={'text-xs ' + c.textMuted}>{open ? '▲' : '▼'}</span>
        </button>
        {open && <div className={'px-4 pb-4 border-t ' + c.border}>{children}</div>}
      </div>
    );
  };

  // ════════════════════════════════════════════════════════════
  // INPUT
  // ════════════════════════════════════════════════════════════
  const renderInput = () => (
    <div className="space-y-4">
      {/* Category picker */}
      <div className="flex gap-1.5 overflow-x-auto pb-1">
        {CATEGORIES.map(cat => (
          <button key={cat.value} onClick={() => setCategory(cat.value)}
            className={'flex items-center gap-1.5 px-3 py-2.5 rounded-xl border-2 transition-all flex-shrink-0 ' +
              (category === cat.value ? c.pillActive + ' border-2' : c.pillInactive)}>
            <span className="text-base">{cat.emoji}</span>
            <span className={'text-xs font-bold ' + (category === cat.value ? '' : c.textMuted)}>{t(cat.labelKey)}</span>
          </button>
        ))}
      </div>

      {/* Word input */}
      <div className={c.card + ' border rounded-xl p-5'}>
        <div className="flex items-center justify-between mb-2">
          <label className={'text-base font-bold ' + c.text}>
            {activeCat.emoji} {batchMode ? t('pir_enter_multiple') : t('pir_what_say_right')}
          </label>
          <button onClick={() => { setBatchMode(!batchMode); setResults(null); setBatchResults(null); }}
            className={'text-[10px] font-bold px-2.5 py-1 rounded-lg border ' + (batchMode ? c.pillActive : c.pillInactive)}>
            {batchMode ? '✓ ' + t('pir_batch_mode') : t('pir_batch_mode')}
          </button>
        </div>

        {batchMode ? (
          <>
            <label className={`text-xs font-bold ${c.labelText} block mb-1.5`}>{t('pir_batch_words_label')} <span className={c.required}>*</span></label>
            <p className={'text-xs ' + c.textMuted + ' mb-3'}>{t('pir_batch_hint')}</p>
            <textarea value={batchWords} onChange={e => setBatchWords(e.target.value)}
              placeholder={t('pir_batch_ph')}
              className={'w-full h-28 p-4 border-2 rounded-xl ' + c.input + ' outline-none focus:ring-2 resize-none text-sm'} />
          </>
        ) : (
          <>
            <label className={`text-xs font-bold ${c.labelText} block mb-1.5`}>{t('pir_word_label')} <span className={c.required}>*</span></label>
            <p className={'text-xs ' + c.textMuted + ' mb-3'}>{t('pir_word_hint')}</p>
            <input type="text" value={word} onChange={e => setWord(e.target.value)}
              placeholder={t(activeCat.placeholderKey)}
              className={'w-full px-4 py-3 border-2 rounded-xl ' + c.input + ' outline-none focus:ring-2 text-sm'}
              onKeyDown={e => { if (e.key === 'Enter' && word.trim()) submit(); }} />
          </>
        )}
      </div>

      {/* Options */}
      <div className={c.card + ' border rounded-xl p-5 space-y-3'}>
        <div>
          <label className={'text-xs font-bold ' + c.textSecondary + ' uppercase tracking-wide mb-2 block'}>🗣️ {t('pir_native_lang')}</label>
          <div className="flex flex-wrap gap-1.5">
            {LANGUAGES.slice(0, 8).map(lang => (
              <Pill key={lang.value} active={nativeLang === lang.value} onClick={() => setNativeLang(lang.value)}>{t(lang.labelKey)}</Pill>
            ))}
            <select value={LANGUAGES.slice(0, 8).some(l => l.value === nativeLang) ? '' : nativeLang}
              onChange={e => { if (e.target.value) setNativeLang(e.target.value); }}
              className={'px-2 py-1.5 rounded-lg border text-xs ' + c.input + ' outline-none'}>
              <option value="">{t('pir_more')}</option>
              {LANGUAGES.slice(8).map(lang => <option key={lang.value} value={lang.value}>{t(lang.labelKey)}</option>)}
            </select>
          </div>
        </div>
        {!batchMode && (
          <div>
            <label className={'text-xs font-bold ' + c.textSecondary + ' uppercase tracking-wide mb-1 block'}>📝 {t('pir_extra_context')}</label>
            <input type="text" value={context} onChange={e => setContext(e.target.value)}
              placeholder={t('pir_context_ph')}
              className={'w-full px-3 py-2 rounded-xl border text-sm ' + c.input + ' outline-none'} />
          </div>
        )}
      </div>

      {/* Examples */}
      {!batchMode && (
        <div className={'p-4 rounded-xl ' + c.inset}>
          <p className={'text-xs font-bold ' + c.textMuted + ' mb-2'}>💡 {t('pir_try_one')}</p>
          <div className="flex flex-wrap gap-1.5">
            {EXAMPLES.map((ex, i) => (
              <button key={i} onClick={() => loadExample(ex)}
                className={'px-3 py-1.5 rounded-lg border text-xs font-semibold ' + c.pillInactive}>
                {CATEGORIES.find(c2 => c2.value === ex.category)?.emoji} {ex.word}
              </button>
            ))}
          </div>
        </div>
      )}

      <button onClick={submit} disabled={loading || (!batchMode && !word.trim()) || (batchMode && !batchWords.trim())}
        className={'w-full py-4 rounded-2xl text-sm font-bold flex items-center justify-center gap-2 transition-all disabled:opacity-40 ' +
          c.btnPrimary}>
        {loading ? <><span className="inline-block animate-spin">{tool?.icon ?? '🗣️'}</span> {t('pir_looking_up')}</>
          : <><span className="me-1">{tool?.icon ?? '🗣️'}</span> {t('pir_cta')}</>}
      </button>
    </div>
  );

  // ════════════════════════════════════════════════════════════
  // SINGLE RESULT
  // ════════════════════════════════════════════════════════════
  const renderResult = () => {
    if (!results) return null;
    const p = results.pronunciation;
    const ctx = results.context_info;

    return (
      <div ref={resultsRef} className="space-y-4 mt-4">
        {/* Hero pronunciation */}
        <div className={'p-6 rounded-2xl border-2 ' + c.phonBg + ' text-center'}>
          <p className={'text-xs font-bold ' + c.textMuted + ' uppercase mb-1'}>{results.language_of_origin || t('pir_pronunciation')}</p>
          <p className={'text-3xl font-black tracking-wide ' + c.text + ' mb-2'}>{results.word}</p>
          <p className={'text-xl font-bold ' + c.tipText + ' mb-3'}>{p?.phonetic}</p>

          {/* Hear it */}
          <div className="flex flex-col items-center gap-2">
            {!audioUrl ? (
              <button onClick={fetchAudio} disabled={audioLoading}
                className={'text-sm font-bold px-4 py-2 rounded-xl border disabled:opacity-40 ' + c.btnSecondary}>
                {audioLoading
                  ? <><span className="inline-block animate-spin me-1">{tool?.icon ?? '🗣️'}</span> {t('pir_loading')}</>
                  : <>🔊 {t('pir_hear_it')}</>}
              </button>
            ) : (
              <div className="flex flex-col items-center gap-1.5">
                <audio controls autoPlay src={audioUrl} className="h-8 w-48"
                  onEnded={() => { URL.revokeObjectURL(audioUrl); setAudioUrl(null); }} />
                <button onClick={() => { URL.revokeObjectURL(audioUrl); setAudioUrl(null); }}
                  className={'text-[10px] ' + c.textMuted}>✕ {t('pir_close')}</button>
              </div>
            )}
            <p className={'text-[10px] ' + c.textMuted}>{t('pir_audio_note')}</p>
          </div>

          {p?.ipa && (
            <button onClick={() => setShowIPA(!showIPA)} className={'text-xs mt-3 block mx-auto ' + linkStyle}>
              {showIPA ? `${t('pir_copy_ipa')} ${p.ipa}` : t('pir_show_ipa')}
            </button>
          )}
        </div>

        {/* Syllables & stress */}
        {p?.syllables?.length > 0 && (
          <div className={'p-4 rounded-xl ' + c.inset + ' flex items-center justify-center gap-1'}>
            {p.syllables.map((syl, i) => (
              <React.Fragment key={i}>
                {i > 0 && <span className={'text-lg font-light ' + c.textMuted}>·</span>}
                <span className={'text-lg font-bold px-2 py-1 rounded-lg ' +
                  (p.stress?.toLowerCase().includes(syl.toLowerCase()) || p.phonetic?.includes(syl.toUpperCase())
                    ? c.tipBg + ' ' + c.tipText : c.text)}>
                  {syl}
                </span>
              </React.Fragment>
            ))}
          </div>
        )}

        {/* Sounds like + Mouth guide */}
        {(p?.sounds_like || p?.mouth_guide) && (
          <div className={c.card + ' border rounded-xl p-4 space-y-3'}>
            {p.sounds_like && (
              <div>
                <p className={'text-xs font-bold ' + c.textMuted + ' uppercase mb-1'}>👂 {t('pir_sounds_like')}</p>
                <p className={'text-sm ' + c.text}>{p.sounds_like}</p>
              </div>
            )}
            {p.mouth_guide && (
              <div>
                <p className={'text-xs font-bold ' + c.textMuted + ' uppercase mb-1'}>👄 {t('pir_mouth_guide')}</p>
                <p className={'text-sm ' + c.text}>{p.mouth_guide}</p>
              </div>
            )}
            {p.stress && (
              <div>
                <p className={'text-xs font-bold ' + c.textMuted + ' uppercase mb-1'}>💪 {t('pir_stress')}</p>
                <p className={'text-xs ' + c.textSecondary}>{p.stress}</p>
              </div>
            )}
          </div>
        )}

        {/* Common mistakes */}
        {results.common_mistakes?.length > 0 && (
          <Section title={t('pir_common_mistakes')} emoji="❌" sKey="mistakes" defaultOpen>
            <div className="space-y-2 mt-3">
              {results.common_mistakes.map((m, i) => (
                <div key={i} className={'p-3 rounded-lg border ' + c.warning}>
                  <div className="flex items-center gap-2 mb-1">
                    <span className={'text-xs font-bold line-through ' + c.warningTxt}>{m.wrong}</span>
                    <span className={'text-xs ' + c.textMuted}>→</span>
                    <span className={'text-xs font-bold ' + c.successTxt}>{m.fix}</span>
                  </div>
                  <p className={'text-[10px] ' + c.textSecondary}>{m.why}</p>
                </div>
              ))}
            </div>
          </Section>
        )}

        {/* Context info */}
        {ctx && (
          <div className={c.card + ' border rounded-xl p-4 space-y-3'}>
            {ctx.what_it_is && (
              <div>
                <p className={'text-xs font-bold ' + c.textMuted + ' uppercase mb-1'}>{activeCat.emoji} {t('pir_what_it_is')}</p>
                <p className={'text-sm ' + c.text}>{ctx.what_it_is}</p>
              </div>
            )}
            {ctx.origin_story && (
              <div>
                <p className={'text-xs font-bold ' + c.textMuted + ' uppercase mb-1'}>📖 {t('pir_origin')}</p>
                <p className={'text-xs ' + c.textSecondary}>{ctx.origin_story}</p>
              </div>
            )}
            {ctx.use_in_sentence && (
              <div className={'p-3 rounded-lg ' + c.inset}>
                <p className={'text-xs font-bold ' + c.textMuted + ' mb-1'}>💬 {t('pir_use_in_sentence')}</p>
                <p className={'text-sm italic ' + c.text}>"{ctx.use_in_sentence}"</p>
              </div>
            )}
            {ctx.pro_tip && (
              <div className={'p-3 rounded-lg ' + c.tipBg}>
                <p className={'text-xs font-bold ' + c.tipText}>💡 {t('pir_pro_tip')} {ctx.pro_tip}</p>
              </div>
            )}
          </div>
        )}

        {/* Don't confuse with */}
        {results.dont_confuse_with?.length > 0 && (
          <Section title={t('pir_dont_confuse')} emoji="⚠️" sKey="confuse">
            <div className="space-y-2 mt-3">
              {results.dont_confuse_with.map((item, i) => (
                <div key={i} className={'p-3 rounded-lg ' + c.inset}>
                  <p className={'text-xs font-bold ' + c.text}>{item.word}</p>
                  <p className={'text-[10px] ' + c.textSecondary}>{item.difference}</p>
                </div>
              ))}
            </div>
          </Section>
        )}

        {/* Regional variants */}
        {results.regional_variants?.length > 0 && (
          <Section title={t('pir_regional_variants')} emoji="🌍" sKey="regional">
            <div className="space-y-2 mt-3">
              {results.regional_variants.map((v, i) => (
                <div key={i} className={'p-3 rounded-lg ' + c.inset}>
                  <div className="flex items-center gap-2">
                    <span className={'text-xs font-bold ' + c.text}>{v.region}</span>
                    <span className={'text-xs ' + c.tipText}>{v.pronunciation}</span>
                  </div>
                  {v.note && <p className={'text-[10px] ' + c.textMuted + ' mt-0.5'}>{v.note}</p>}
                </div>
              ))}
            </div>
          </Section>
        )}

        {/* Confidence script */}
        {results.confidence_script && (
          <div className={'p-4 rounded-xl border ' + c.successBox}>
            <p className={'text-xs font-bold ' + c.successTxt + ' mb-1'}>💬 {t('pir_unsure_moment')}</p>
            <p className={'text-sm italic ' + c.successTxt}>"{results.confidence_script}"</p>
          </div>
        )}

        {/* Fun fact */}
        {results.fun_fact && (
          <div className={'p-3 rounded-lg ' + c.infoBox}>
            <p className={'text-xs ' + c.accentTxt}>🧠 {results.fun_fact}</p>
          </div>
        )}

        {/* Post-result cross-ref */}
        <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
          <p className={`text-[10px] font-bold ${c.textMuted} uppercase mb-2`}>{t('pir_next_step')}</p>
          <p className={`text-xs ${c.textSecondary}`}>
            {t('pir_xref_nervecheck_q')} <a href="/NerveCheck" className={linkStyle}>💪 {t('pir_nervecheck')}</a> {t('pir_xref_nervecheck_tail')}
          </p>
        </div>

      </div>
    );
  };

  // ════════════════════════════════════════════════════════════
  // BATCH RESULTS
  // ════════════════════════════════════════════════════════════
  const renderBatch = () => {
    if (!batchResults?.guides?.length) return null;
    return (
      <div ref={resultsRef} className="space-y-3 mt-4">
        <p className={'text-xs font-bold ' + c.textMuted + ' uppercase'}>🗣️ {t('pir_batch_count', { count: batchResults.guides.length })}</p>
        {batchResults.guides.map((g, i) => (
          <div key={i} className={c.card + ' border rounded-xl p-4'}>
            <div className="flex items-start justify-between mb-2">
              <div>
                <p className={'text-lg font-bold ' + c.text}>{g.word}</p>
                <p className={'text-base font-bold ' + c.tipText}>{g.phonetic}</p>
              </div>
              {g.ipa && <span className={'text-[10px] font-mono px-2 py-0.5 rounded ' + c.ipaBg + ' ' + c.textMuted}>{g.ipa}</span>}
            </div>
            {g.syllables?.length > 0 && (
              <p className={'text-xs ' + c.textSecondary + ' mb-1'}>{g.syllables.join(' · ')}</p>
            )}
            {g.sounds_like && <p className={'text-xs ' + c.text + ' mb-1'}>👂 {g.sounds_like}</p>}
            {g.top_mistake && (
              <div className={'p-2 rounded-lg ' + c.warning + ' mt-2'}>
                <p className={'text-[10px] ' + c.warningTxt}>❌ {g.top_mistake}</p>
              </div>
            )}
            {g.what_it_is && <p className={'text-[10px] ' + c.textMuted + ' mt-1'}>{activeCat.emoji} {g.what_it_is}</p>}
            {g.fun_fact && <p className={'text-[10px] ' + c.textMuted + ' mt-0.5 italic'}>🧠 {g.fun_fact}</p>}
          </div>
        ))}
      </div>
    );
  };

  // ════════════════════════════════════════════════════════════
  // HISTORY
  // ════════════════════════════════════════════════════════════
  const renderHistory = () => {
    if (sessionHistory.length === 0) return null;
    return (
      <div className={'mt-6 p-4 rounded-2xl border ' + c.cardAlt}>
        <button onClick={() => setShowHistory(!showHistory)} className="w-full flex items-center gap-2 text-start">
          <span>🗣️</span>
          <span className={'text-sm font-bold ' + c.text + ' flex-1'}>{t('pir_words_looked_up')}</span>
          <span className={'text-xs ' + c.textMuted}>{sessionHistory.length}</span>
          <span className={'text-xs ' + c.textMuted}>{showHistory ? '▲' : '▼'}</span>
        </button>
        {showHistory && (
          <div className="mt-3 flex flex-wrap gap-1.5">
            {sessionHistory.map(entry => (
              <button key={entry.id}
                onClick={() => { setWord(entry.word); setCategory(entry.category); setResults(null); setBatchResults(null); setBatchMode(false); }}
                className={'px-3 py-1.5 rounded-lg border text-xs font-semibold ' + c.pillInactive}>
                {entry.emoji} {entry.word}
                {entry.phonetic && <span className={'ms-1.5 ' + c.textMuted}>({entry.phonetic})</span>}
              </button>
            ))}
          </div>
        )}
      </div>
    );
  };

  // ════════════════════════════════════════════════════════════
  // MAIN RENDER
  // ════════════════════════════════════════════════════════════
  return (
    <div className={`space-y-4 ${c.text}`}>
      <div className={`${c.card} border ${c.border} rounded-xl shadow-sm p-5`}>
        <div className="pb-3 border-b border-zinc-500 flex items-start justify-between gap-3">
          <div>
            <h2 className={`text-xl font-bold ${c.text}`}>
              <span className="me-2">{tool?.icon ?? '🗣️'}</span>{tool?.title ?? t('pir_title')}
            </h2>
            <p className={`text-sm ${c.textSecondary}`}>{tool?.tagline ?? t('pir_tagline')}</p>
          </div>
          {(results || batchResults || word.trim()) && (
            <button onClick={handleReset} className={`flex-shrink-0 ${c.btnSecondary} px-3 py-1.5 rounded-lg text-xs font-medium`}>
              ↺ {t('start_over')}
            </button>
          )}
        </div>
      </div>
      {!results && !batchResults && renderInput()}
      {!results && !batchResults && (
        <p className={`text-[11px] ${c.textMuted} text-center`}>
          {t('pir_xref_plaintalk_q')} <a href="/PlainTalk" className={linkStyle}>📄 {t('pir_plaintalk')}</a> {t('pir_xref_plaintalk_tail')}
        </p>
      )}
      {renderResult()}
      {renderBatch()}
      {(results || batchResults) && (
        <div className="mt-4 space-y-3">
          <div className="flex gap-2">
            <button onClick={() => { setResults(null); setBatchResults(null); setError(''); setAudioUrl(null); }}
              className={'flex-1 py-3 rounded-xl text-xs font-bold flex items-center justify-center gap-1.5 ' + c.btnSecondary}>
              <span>✏️</span> {t('pir_edit_input')}
            </button>
          </div>
          <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
            <p className={`text-[10px] font-bold ${c.textMuted} uppercase mb-2`}>🔗 {t('pir_related_tools')}</p>
            <div className="flex flex-wrap gap-3">
              <a href="/DecoderRing" className={`text-xs ${linkStyle}`}>💬 {t('pir_decoder_ring')}</a>
              <a href="/TipOfTongue" className={`text-xs ${linkStyle}`}>💡 {t('pir_tip_of_tongue')}</a>
            </div>
          </div>
        </div>
      )}
      {error && (
        <div className={`mt-4 p-4 border rounded-xl flex items-start gap-3 ${c.danger}`}>
          <span className="text-base">⚠️</span>
          <p className="text-sm">{error}</p>
        </div>
      )}
      {renderHistory()}
    </div>
  );
};

PronounceItRight.displayName = 'PronounceItRight';
export default PronounceItRight;
