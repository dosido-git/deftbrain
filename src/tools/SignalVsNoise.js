import React, { useState, useEffect } from 'react';
import { useTheme } from '../hooks/useTheme';
import { usePersistentState } from '../hooks/usePersistentState';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { CopyBtn, ActionBar } from '../components/ActionButtons';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const EXAMPLE_TOPICS = [
  { topic: 'Sleep optimization', conflict: 'Sleep 8 hours vs. polyphasic sleep, phone before bed ruins sleep vs. blue light glasses fix it, sleep debt is real vs. you can\'t catch up' },
  { topic: 'Nutrition and weight loss', conflict: 'Calories in vs. out is all that matters vs. hormone balance matters more, breakfast is essential vs. intermittent fasting, processed food is fine in moderation vs. it\'s all poison' },
  { topic: 'Career advice for young professionals', conflict: 'Follow your passion vs. passion follows mastery, job hop for salary vs. loyalty pays off, get an MBA vs. skip grad school' },
  { topic: 'Investing strategies', conflict: 'Index funds always win vs. active management can beat the market, time in market beats timing the market vs. valuations matter, real estate vs. stocks' },
  { topic: 'Parenting approaches', conflict: 'Screen time is destroying kids vs. it depends on content, tiger parenting vs. free-range, homework helps vs. homework is harmful' },
];

const NOISE_TYPE_CONFIG = {
  marketing:            { label: '💰 Marketing',           color: (d) => d ? 'bg-amber-600/15 border-amber-600/40 text-zinc-500' : 'bg-zinc-500 border-zinc-500 text-zinc-500' },
  methodology_problem:  { label: '🔬 Bad Methodology',     color: (d) => d ? 'bg-zinc-500 border-zinc-500 text-zinc-500' : 'bg-zinc-500 border-zinc-500 text-zinc-500' },
  cherry_picked:        { label: '🍒 Cherry-Picked',       color: (d) => d ? 'bg-zinc-500 border-zinc-500 text-zinc-500' : 'bg-zinc-500 border-zinc-500 text-zinc-500' },
  outdated:             { label: '📅 Outdated',             color: (d) => d ? 'bg-zinc-500 border-zinc-500 text-zinc-500'    : 'bg-zinc-500 border-zinc-500 text-zinc-500' },
  oversimplified:       { label: '✂️ Oversimplified',      color: (d) => d ? 'bg-amber-600/15 border-amber-600/40 text-zinc-500' : 'bg-zinc-500 border-zinc-500 text-zinc-500' },
  ideology:             { label: '🏴 Ideology',            color: (d) => d ? 'bg-zinc-500 border-zinc-500 text-zinc-500'    : 'bg-zinc-500 border-zinc-500 text-zinc-500' },
  individual_variation: { label: '🧬 Individual Variation', color: (d) => d ? 'bg-cyan-800/15 border-cyan-800/40 text-zinc-500' : 'bg-zinc-500 border-zinc-500 text-zinc-500' },
  media_distortion:     { label: '📺 Media Distortion',    color: (d) => d ? 'bg-zinc-500 border-zinc-500 text-zinc-500' : 'bg-zinc-500 border-zinc-500 text-zinc-500' },
};

const SignalVsNoise = ({ tool }) => {
  const { isDark } = useTheme();

  useEffect(() => {
    const handler = (e) => {
      const tag = document.activeElement?.tagName;
      if (tag === 'INPUT' || tag === 'TEXTAREA' || tag === 'SELECT') return;
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && !loading) handleSubmit();
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
  }, [loading]);
  const { callToolEndpoint, loading } = useClaudeAPI();


  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';

  const c = {
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:         isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20' : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-500' : 'text-gray-400',
    labelText:     isDark ? 'text-zinc-200' : 'text-gray-700',
    accentTxt:     isDark ? 'text-cyan-400' : 'text-cyan-600',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white' : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200' : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200' : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200' : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200' : 'bg-red-50 border-red-200 text-red-800',
    infoBox:       isDark ? 'bg-sky-900/20 border-sky-700 text-sky-200' : 'bg-sky-50 border-sky-200 text-sky-800',
    successBox:    isDark ? 'bg-emerald-900/20 border-emerald-700' : 'bg-emerald-50 border-emerald-300',
    successTxt:    isDark ? 'text-emerald-300' : 'text-emerald-800',
    warningBox:    isDark ? 'bg-amber-900/20 border-amber-700' : 'bg-amber-50 border-amber-300',
    warningTxt:    isDark ? 'text-amber-300' : 'text-amber-800',
    pillActive:    isDark ? 'border-cyan-500 bg-cyan-900/30 text-cyan-200' : 'border-cyan-600 bg-cyan-100 text-cyan-900',
    pillInactive:  isDark ? 'border-zinc-600 text-zinc-400 hover:border-zinc-500' : 'border-gray-300 text-gray-500 hover:border-gray-400',
  };;

  const [topic, setTopic] = useState('');
  const [conflictingAdvice, setConflictingAdvice] = useState('');
  const [userContext, setUserContext] = useState('');
  const [results, setResults] = usePersistentState('signalvsnoise-result', null);
  const [history, setHistory] = usePersistentState('signalvsnoise-history', []);
  const [error, setError] = useState('');
  const [expanded, setExpanded] = useState({ noise: true, debated: false, sources: false });

  const toggle = (k) => setExpanded(p => ({ ...p, [k]: !p[k] }));

  const handleSubmit = async () => {
    if (!topic.trim()) return;
    setError(''); setResults(null);
    try {
      const data = await callToolEndpoint('signal-vs-noise', {
        topic: topic.trim(),
        conflictingAdvice: conflictingAdvice.trim() || undefined,
        userContext: userContext.trim() || undefined,
      });
      setResults(data);
      setHistory(prev => [{ id: Date.now(), date: new Date().toISOString(), preview: (topic || '').slice(0, 40) }, ...prev].slice(0, 6));
    } catch (e) { setError(e.message || 'Failed to analyze.'); }
  };

  const handleExample = () => {
    const ex = EXAMPLE_TOPICS[Math.floor(Math.random() * EXAMPLE_TOPICS.length)];
    setTopic(ex.topic);
    setConflictingAdvice(ex.conflict);
    setResults(null);
  };

  const buildText = () => {
    if (!results) return '';
    let t = `📡 SIGNAL VS. NOISE: ${results.topic_as_understood}\n\n`;
    t += `WHY THIS FIELD IS NOISY:\n${results.why_this_field_is_noisy}\n\n`;
    t += `THE SIGNAL (What's actually established):\n`;
    results.the_signal?.items?.forEach(s => { t += `• ${s.claim}\n  Why we know: ${s.why_we_know_this}\n`; });
    t += `\nTHE NOISE:\n`;
    results.the_noise?.forEach(n => { t += `• ${n.claim} [${n.noise_label}]\n  ${n.the_problem}\n`; });
    if (results.genuinely_debated?.length) {
      t += `\nGENUINELY DEBATED:\n`;
      results.genuinely_debated.forEach(d => { t += `• ${d.question}\n`; });
    }
    if (results.the_bottom_line) {
      t += `\nBOTTOM LINE:\nDo: ${results.the_bottom_line.what_to_do}\nIgnore: ${results.the_bottom_line.what_to_ignore}\n`;
    }
    return t + BRAND;
  };

  return (
    <div className={`min-h-screen py-8 px-4 ${c.cardAlt}`}>
      <div className="max-w-2xl mx-auto">

        {/* Header */}
        <div className="text-center mb-8">
          <div className="text-5xl mb-3">📡</div>
          <h1 className={`text-3xl font-black tracking-tight mb-2 ${c.text}`}>Signal vs. Noise</h1>
          <p className={`text-sm ${c.textMuteded}`}>Cut through the contradiction. Find what experts actually agree on.</p>
        </div>

        {/* Input */}
        {!results && (
          <div className={`rounded-2xl border p-6 shadow-sm space-y-4 ${c.card} ${c.border}`}>
            <div>
              <label className={`block text-sm font-semibold mb-1.5 ${c.text}`}>Topic <span className="text-red-400">*</span></label>
              <input type="text" value={topic} onChange={e => setTopic(e.target.value)}
                placeholder="e.g. sleep optimization, low-carb diets, passive investing, screen time for kids…"
                className={`w-full px-4 py-3 rounded-xl border text-sm ${c.input} `} />
            </div>
            <div>
              <label className={`block text-sm font-semibold mb-1.5 ${c.text}`}>Conflicting advice you've seen <span className={`font-normal ${c.textMuteded}`}>(optional — helps target the analysis)</span></label>
              <textarea value={conflictingAdvice} onChange={e => setConflictingAdvice(e.target.value)}
                placeholder="Paste the contradictions that are confusing you…"
                rows={3} maxLength={600}
                className={`w-full px-4 py-3 rounded-xl border text-sm resize-none ${c.input} `} />
            </div>
            <div>
              <label className={`block text-sm font-semibold mb-1.5 ${c.text}`}>Your context <span className={`font-normal ${c.textMuteded}`}>(optional)</span></label>
              <input type="text" value={userContext} onChange={e => setUserContext(e.target.value)}
                placeholder="e.g. 38 years old, trying to lose 20lbs, have tried intermittent fasting…"
                className={`w-full px-4 py-3 rounded-xl border text-sm ${c.input} `} />
            </div>
            {error && <div className={`p-3 rounded-xl border text-sm ${c.danger}`}><span className="mr-1">⚠️</span>{error}</div>}
            <div className="flex gap-2">
              <button onClick={handleSubmit} disabled={loading || !topic.trim()}
                className={`flex-1 py-3 rounded-xl font-bold disabled:opacity-40 ${c.btnPrimary}`}>
                {loading ? <><span className="inline-block animate-spin">{tool?.icon ?? '⚙️'}</span>Separating signal…</> : '📡 Find the Signal'}
              </button>
              <button onClick={handleExample} disabled={loading}
                className={`px-4 py-3 rounded-xl text-sm font-medium ${c.btnSecondaryondary}`}>
                Example
              </button>
            </div>
          </div>
        )}

        {/* Results */}
        {results && (
          <div className="space-y-4">

            {/* Topic + why noisy */}
            <div className={`rounded-2xl border p-5 ${c.card} ${c.border}`}>
              <p className={`text-xs font-bold uppercase tracking-wider mb-1 ${c.textMuteded}`}>Analyzing</p>
              <p className={`font-bold text-base mb-3 ${c.text}`}>{results.topic_as_understood}</p>
              <p className={`text-xs font-bold uppercase tracking-wider mb-1 ${c.textMuteded}`}>Why this field generates so much noise</p>
              <p className={`text-sm leading-relaxed ${c.textSecondaryondary}`}>{results.why_this_field_is_noisy}</p>
            </div>

            {/* THE SIGNAL */}
            {results.the_signal?.items?.length > 0 && (
              <div className={`rounded-2xl border p-5 ${c.signalBg}`}>
                <p className={`text-xs font-black uppercase tracking-widest mb-3 ${c.signalText}`}>✅ The Signal — What's Actually Established</p>
                <div className="space-y-4">
                  {results.the_signal.items.map((item, i) => (
                    <div key={i} className={`p-4 rounded-xl ${isDark ? 'bg-zinc-900/60' : 'bg-white/80'}`}>
                      <p className={`text-sm font-semibold mb-1 ${c.text}`}>{item.claim}</p>
                      <p className={`text-xs mb-1 ${c.textMuteded}`}>
                        <span className="font-semibold">Why we know this:</span> {item.why_we_know_this}
                      </p>
                      {item.the_nuance && (
                        <p className={`text-xs italic mt-1 ${c.textMuteded}`}>⚠️ Important nuance: {item.the_nuance}</p>
                      )}
                    </div>
                  ))}
                </div>
              </div>
            )}

            {/* THE NOISE */}
            {results.the_noise?.length > 0 && (
              <div className={`rounded-2xl border overflow-hidden ${c.card} ${c.border}`}>
                <button onClick={() => toggle('noise')} className="w-full text-left px-5 py-4 flex items-center justify-between">
                  <p className={`text-xs font-black uppercase tracking-widest ${isDark ? 'text-zinc-500' : 'text-zinc-500'}`}>
                    🚫 The Noise — {results.the_noise.length} Popular Claims That Don't Hold Up
                  </p>
                  <span className={`text-sm ${c.textMuteded}`}>{expanded.noise ? '▲' : '▼'}</span>
                </button>
                {expanded.noise && (
                  <div className={`border-t ${c.border}`}>
                    {results.the_noise.map((item, i) => {
                      const cfg = NOISE_TYPE_CONFIG[item.noise_type] || NOISE_TYPE_CONFIG.oversimplified;
                      return (
                        <div key={i} className={`px-5 py-4 ${i > 0 ? `border-t ${c.border}` : ''}`}>
                          <div className="flex items-start gap-2 mb-2 flex-wrap">
                            <span className={`text-xs font-bold px-2 py-0.5 rounded-full border ${cfg.color(isDark)}`}>{cfg.label}</span>
                          </div>
                          <p className={`text-sm font-semibold mb-1 ${c.text}`}>"{item.claim}"</p>
                          <p className={`text-sm ${c.textSecondaryondary}`}>{item.the_problem}</p>
                          {item.kernel_of_truth && (
                            <p className={`text-xs mt-2 ${c.textMuteded}`}><span className="font-semibold">Kernel of truth:</span> {item.kernel_of_truth}</p>
                          )}
                        </div>
                      );
                    })}
                  </div>
                )}
              </div>
            )}

            {/* GENUINELY DEBATED */}
            {results.genuinely_debated?.length > 0 && (
              <div className={`rounded-2xl border overflow-hidden ${c.card} ${c.border}`}>
                <button onClick={() => toggle('debated')} className="w-full text-left px-5 py-4 flex items-center justify-between">
                  <p className={`text-xs font-black uppercase tracking-widest ${c.debatedText}`}>
                    🔵 Genuinely Debated — {results.genuinely_debated.length} Questions Still Open
                  </p>
                  <span className={`text-sm ${c.textMuteded}`}>{expanded.debated ? '▲' : '▼'}</span>
                </button>
                {expanded.debated && (
                  <div className={`border-t ${c.border}`}>
                    {results.genuinely_debated.map((item, i) => (
                      <div key={i} className={`px-5 py-4 ${i > 0 ? `border-t ${c.border}` : ''}`}>
                        <p className={`text-sm font-semibold mb-2 ${c.text}`}>{item.question}</p>
                        <div className="grid grid-cols-1 sm:grid-cols-2 gap-2">
                          <div className={`p-2.5 rounded-lg text-xs ${c.signalBg}`}>
                            <p className={`font-bold mb-1 ${c.signalText}`}>One view</p>
                            <p className={c.textSecondaryondary}>{item.side_a}</p>
                          </div>
                          <div className={`p-2.5 rounded-lg text-xs ${isDark ? 'bg-amber-600/10 border border-amber-600/30' : 'bg-zinc-500 border border-zinc-500'}`}>
                            <p className={`font-bold mb-1 ${isDark ? 'text-zinc-500' : 'text-zinc-500'}`}>Another view</p>
                            <p className={c.textSecondaryondary}>{item.side_b}</p>
                          </div>
                        </div>
                        {item.why_unsettled && (
                          <p className={`text-xs mt-2 italic ${c.textMuteded}`}>Why unsettled: {item.why_unsettled}</p>
                        )}
                      </div>
                    ))}
                  </div>
                )}
              </div>
            )}

            {/* THE BOTTOM LINE */}
            {results.the_bottom_line && (
              <div className={`rounded-2xl border-2 p-5 space-y-3 ${isDark ? 'border-cyan-800 bg-cyan-800/10' : 'border-cyan-800 bg-zinc-500'}`}>
                <p className={`text-xs font-black uppercase tracking-widest ${isDark ? 'text-zinc-500' : 'text-cyan-800'}`}>⚡ The Bottom Line</p>
                {results.the_bottom_line.what_to_do && (
                  <div>
                    <p className={`text-xs font-bold uppercase tracking-wide mb-1 ${c.signalText}`}>✅ What the evidence supports</p>
                    <p className={`text-sm ${c.textSecondaryondary}`}>{results.the_bottom_line.what_to_do}</p>
                  </div>
                )}
                {results.the_bottom_line.what_to_ignore && (
                  <div>
                    <p className={`text-xs font-bold uppercase tracking-wide mb-1 ${isDark ? 'text-zinc-500' : 'text-zinc-500'}`}>🚫 What to ignore</p>
                    <p className={`text-sm ${c.textSecondaryondary}`}>{results.the_bottom_line.what_to_ignore}</p>
                  </div>
                )}
                {results.the_bottom_line.the_honest_uncertainty && (
                  <div>
                    <p className={`text-xs font-bold uppercase tracking-wide mb-1 ${c.textMuteded}`}>🔵 Honest uncertainty</p>
                    <p className={`text-sm ${c.textSecondaryondary}`}>{results.the_bottom_line.the_honest_uncertainty}</p>
                  </div>
                )}
              </div>
            )}

            {/* NOISE SOURCES */}
            {results.sources_of_noise?.length > 0 && (
              <div className={`rounded-2xl border overflow-hidden ${c.card} ${c.border}`}>
                <button onClick={() => toggle('sources')} className="w-full text-left px-5 py-4 flex items-center justify-between">
                  <p className={`text-xs font-bold uppercase tracking-wider ${c.textMuteded}`}>🕵️ Who's Generating the Noise</p>
                  <span className={`text-sm ${c.textMuteded}`}>{expanded.sources ? '▲' : '▼'}</span>
                </button>
                {expanded.sources && (
                  <div className={`border-t ${c.border}`}>
                    {results.sources_of_noise.map((src, i) => (
                      <div key={i} className={`px-5 py-4 ${i > 0 ? `border-t ${c.border}` : ''}`}>
                        <p className={`text-sm font-semibold mb-0.5 ${c.text}`}>{src.actor}</p>
                        <p className={`text-xs mb-1 ${c.textMuteded}`}><span className="font-semibold">Incentive:</span> {src.incentive}</p>
                        <p className={`text-xs ${c.textMuteded}`}><span className="font-semibold">How to spot it:</span> {src.how_to_spot_it}</p>
                      </div>
                    ))}
                  </div>
                )}
              </div>
            )}

            {/* Actions */}
            <div className="flex items-center gap-3 flex-wrap">
              <ActionBar content={buildText()} title="Signal vs. Noise" />
              <button onClick={() => { setResults(null); setTopic(''); setConflictingAdvice(''); setUserContext(''); }}
                className={`px-4 py-2 rounded-lg text-sm font-medium ${c.btnSecondaryondary}`}>
                🔄 New Topic
              </button>
            </div>

            {/* Cross-references */}
            <div className={`rounded-xl border p-4 ${c.cardAlt} ${c.border}`}>
              <p className={`text-xs font-semibold uppercase tracking-wide mb-3 ${c.textMuteded}`}>Related tools</p>
              <div className="flex flex-wrap gap-2">
                {[
                  { id: 'BeliefStressTest', icon: '🧪', label: 'Stress-test a belief' },
                  { id: 'TheFinalWord', icon: '⚖️', label: 'Fact-check a specific claim' },
                  { id: 'FutureProof', icon: '🔮', label: 'Assess a 5-year trajectory' },
                ].map(r => (
                  <a key={r.id} href={`/tool/${r.id}`} target="_blank" rel="noopener noreferrer"
                    className={`px-3 py-1.5 rounded-lg text-xs font-medium transition-colors ${c.btnSecondaryondary}`}>
                    {r.icon} {r.label}
                  </a>
                ))}
              </div>
            </div>
          </div>
        )}
      </div>

      {/* eslint-disable-next-line no-restricted-globals */}
      {history.length > 0 && (<div className={`${c.cardAlt} border ${c.border} rounded-xl p-4 mt-4`}><p className={`text-xs font-bold ${c.textMuted} mb-2`}>📋 Recent</p><div className="space-y-1">{history.map(s => (<div key={s.id} className="flex items-center justify-between"><span className={`text-xs ${c.textSecondary} truncate`}>{s.preview||'Session'}</span><span className={`text-xs ${c.textMuted} ml-2`}>{new Date(s.date).toLocaleDateString()}</span></div>))}</div></div>)}
    </div>
  );
};

SignalVsNoise.displayName = 'SignalVsNoise';
export default SignalVsNoise;
