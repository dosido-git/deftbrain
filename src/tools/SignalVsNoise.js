import React, { useState } from 'react';
import { useTheme } from '../hooks/useTheme';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { CopyBtn, ActionBar } from '../components/ActionButtons';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const EXAMPLE_TOPICS = [
  { topic: 'Sleep optimization', conflict: 'Sleep 8 hours vs. polyphasic sleep, phone before bed ruins sleep vs. blue light glasses fix it, sleep debt is real vs. you can\'t catch up' },
  { topic: 'Nutrition and weight loss', conflict: 'Calories in vs. out is all that matters vs. hormone balance matters more, breakfast is essential vs. intermittent fasting, processed food is fine in moderation vs. it\'s all poison' },
  { topic: 'Career advice for young professionals', conflict: 'Follow your passion vs. passion follows mastery, job hop for salary vs. loyalty pays off, get an MBA vs. skip grad school' },
  { topic: 'Investing strategies', conflict: 'Index funds always win vs. active management can beat the market, time in market beats timing the market vs. valuations matter, real estate vs. stocks' },
  { topic: 'Parenting approaches', conflict: 'Screen time is destroying kids vs. it depends on content, tiger parenting vs. free-range, homework helps vs. homework is harmful' },
];

const NOISE_TYPE_CONFIG = {
  marketing:            { label: '💰 Marketing',           color: (d) => d ? 'bg-[#c8872e]/15 border-[#c8872e]/40 text-[#d9a04e]' : 'bg-[#fdf3e4] border-[#e0c49a] text-[#93541f]' },
  methodology_problem:  { label: '🔬 Bad Methodology',     color: (d) => d ? 'bg-[#b54a3f]/15 border-[#b54a3f]/40 text-[#e8a9a3]' : 'bg-[#fceae8] border-[#d4908a] text-[#8a3028]' },
  cherry_picked:        { label: '🍒 Cherry-Picked',       color: (d) => d ? 'bg-[#b54a3f]/15 border-[#b54a3f]/40 text-[#e8a9a3]' : 'bg-[#fceae8] border-[#d4908a] text-[#8a3028]' },
  outdated:             { label: '📅 Outdated',             color: (d) => d ? 'bg-[#3d3630]/60 border-[#3d3630] text-[#c8c3b9]'    : 'bg-[#f3efe8] border-[#d5cab8] text-[#5e5042]' },
  oversimplified:       { label: '✂️ Oversimplified',      color: (d) => d ? 'bg-[#c8872e]/15 border-[#c8872e]/40 text-[#d9a04e]' : 'bg-[#fdf3e4] border-[#e0c49a] text-[#93541f]' },
  ideology:             { label: '🏴 Ideology',            color: (d) => d ? 'bg-[#3d3630]/60 border-[#3d3630] text-[#c8c3b9]'    : 'bg-[#f3efe8] border-[#d5cab8] text-[#5e5042]' },
  individual_variation: { label: '🧬 Individual Variation', color: (d) => d ? 'bg-[#2c4a6e]/15 border-[#2c4a6e]/40 text-[#a8b9ce]' : 'bg-[#e8eef5] border-[#b0c4d8] text-[#1e3a58]' },
  media_distortion:     { label: '📺 Media Distortion',    color: (d) => d ? 'bg-[#b54a3f]/15 border-[#b54a3f]/40 text-[#e8a9a3]' : 'bg-[#fceae8] border-[#d4908a] text-[#8a3028]' },
};

const SignalVsNoise = () => {
  const { theme } = useTheme();
  const isDark = theme === 'dark';
  const { callToolEndpoint, loading } = useClaudeAPI();

  const c = {
    pageBg:       isDark ? 'bg-[#1a1816]'    : 'bg-[#faf8f5]',
    card:         isDark ? 'bg-[#2a2623]'     : 'bg-white',
    cardAlt:      isDark ? 'bg-[#332e2a]'     : 'bg-[#faf8f5]',
    cardBorder:   isDark ? 'border-[#3d3630]' : 'border-[#e8e1d5]',
    input:        isDark ? 'bg-[#1a1816] border-[#3d3630] text-[#f0eeea] placeholder:text-[#8a8275]'
                         : 'bg-[#faf8f5] border-[#d5cab8] text-[#3d3935] placeholder:text-[#8a8275]',
    inputFocus:   'focus:outline-none focus:ring-2 focus:ring-[#2c4a6e]/20 focus:border-[#4a6a8a]',
    text:          isDark ? 'text-[#f0eeea]'  : 'text-[#3d3935]',
    textSecondary: isDark ? 'text-[#c8c3b9]'  : 'text-[#5a544a]',
    textMuted:     isDark ? 'text-[#8a8275]'  : 'text-[#8a8275]',
    heading:       isDark ? 'text-[#f3efe8]'  : 'text-[#1e2a3a]',
    btnPrimary:    isDark ? 'bg-[#2c4a6e] hover:bg-[#4a6a8a] text-white' : 'bg-[#2c4a6e] hover:bg-[#1e3a58] text-white',
    btnSecondary:  isDark ? 'bg-[#332e2a] hover:bg-[#3d3630] text-[#c8c3b9] border border-[#3d3630]' : 'bg-[#f3efe8] hover:bg-[#e8e1d5] text-[#5e5042] border border-[#d5cab8]',
    divider:       isDark ? 'border-[#3d3630]' : 'border-[#e8e1d5]',
    error:         isDark ? 'bg-[#b54a3f]/15 border-[#b54a3f]/50 text-[#e8a9a3]' : 'bg-[#fceae8] border-[#d4908a] text-[#8a3028]',
    signalBg:      isDark ? 'bg-[#5a8a5c]/15 border-[#5a8a5c]/40' : 'bg-[#e8f0e8] border-[#a8c4a8]',
    signalText:    isDark ? 'text-[#8aba8c]' : 'text-[#3d6e3f]',
    debatedBg:     isDark ? 'bg-[#2c4a6e]/15 border-[#2c4a6e]/40' : 'bg-[#e8eef5] border-[#b0c4d8]',
    debatedText:   isDark ? 'text-[#a8b9ce]' : 'text-[#1e3a58]',
  };

  const [topic, setTopic] = useState('');
  const [conflictingAdvice, setConflictingAdvice] = useState('');
  const [userContext, setUserContext] = useState('');
  const [results, setResults] = useState(null);
  const [error, setError] = useState('');
  const [expanded, setExpanded] = useState({ noise: true, debated: false, sources: false });

  const toggle = (k) => setExpanded(p => ({ ...p, [k]: !p[k] }));

  const handleSubmit = async () => {
    if (!topic.trim()) return;
    setError(''); setResults(null);
    try {
      const data = await callToolEndpoint('signal-vs-noise', {
        topic: topic.trim(),
        conflictingAdvice: conflictingAdvice.trim() || undefined,
        userContext: userContext.trim() || undefined,
      });
      setResults(data);
    } catch (e) { setError(e.message || 'Failed to analyze.'); }
  };

  const handleExample = () => {
    const ex = EXAMPLE_TOPICS[Math.floor(Math.random() * EXAMPLE_TOPICS.length)];
    setTopic(ex.topic);
    setConflictingAdvice(ex.conflict);
    setResults(null);
  };

  const buildText = () => {
    if (!results) return '';
    let t = `📡 SIGNAL VS. NOISE: ${results.topic_as_understood}\n\n`;
    t += `WHY THIS FIELD IS NOISY:\n${results.why_this_field_is_noisy}\n\n`;
    t += `THE SIGNAL (What's actually established):\n`;
    results.the_signal?.items?.forEach(s => { t += `• ${s.claim}\n  Why we know: ${s.why_we_know_this}\n`; });
    t += `\nTHE NOISE:\n`;
    results.the_noise?.forEach(n => { t += `• ${n.claim} [${n.noise_label}]\n  ${n.the_problem}\n`; });
    if (results.genuinely_debated?.length) {
      t += `\nGENUINELY DEBATED:\n`;
      results.genuinely_debated.forEach(d => { t += `• ${d.question}\n`; });
    }
    if (results.the_bottom_line) {
      t += `\nBOTTOM LINE:\nDo: ${results.the_bottom_line.what_to_do}\nIgnore: ${results.the_bottom_line.what_to_ignore}\n`;
    }
    return t + BRAND;
  };

  return (
    <div className={`min-h-screen py-8 px-4 ${c.pageBg}`}>
      <div className="max-w-2xl mx-auto">

        {/* Header */}
        <div className="text-center mb-8">
          <div className="text-5xl mb-3">📡</div>
          <h1 className={`text-3xl font-black tracking-tight mb-2 ${c.heading}`}>Signal vs. Noise</h1>
          <p className={`text-sm ${c.textMuted}`}>Cut through the contradiction. Find what experts actually agree on.</p>
        </div>

        {/* Input */}
        {!results && (
          <div className={`rounded-2xl border p-6 shadow-sm space-y-4 ${c.card} ${c.cardBorder}`}>
            <div>
              <label className={`block text-sm font-semibold mb-1.5 ${c.text}`}>Topic <span className="text-red-400">*</span></label>
              <input type="text" value={topic} onChange={e => setTopic(e.target.value)}
                placeholder="e.g. sleep optimization, low-carb diets, passive investing, screen time for kids…"
                className={`w-full px-4 py-3 rounded-xl border text-sm ${c.input} ${c.inputFocus}`} />
            </div>
            <div>
              <label className={`block text-sm font-semibold mb-1.5 ${c.text}`}>Conflicting advice you've seen <span className={`font-normal ${c.textMuted}`}>(optional — helps target the analysis)</span></label>
              <textarea value={conflictingAdvice} onChange={e => setConflictingAdvice(e.target.value)}
                placeholder="Paste the contradictions that are confusing you…"
                rows={3} maxLength={600}
                className={`w-full px-4 py-3 rounded-xl border text-sm resize-none ${c.input} ${c.inputFocus}`} />
            </div>
            <div>
              <label className={`block text-sm font-semibold mb-1.5 ${c.text}`}>Your context <span className={`font-normal ${c.textMuted}`}>(optional)</span></label>
              <input type="text" value={userContext} onChange={e => setUserContext(e.target.value)}
                placeholder="e.g. 38 years old, trying to lose 20lbs, have tried intermittent fasting…"
                className={`w-full px-4 py-3 rounded-xl border text-sm ${c.input} ${c.inputFocus}`} />
            </div>
            {error && <div className={`p-3 rounded-xl border text-sm ${c.error}`}><span className="mr-1">⚠️</span>{error}</div>}
            <div className="flex gap-2">
              <button onClick={handleSubmit} disabled={loading || !topic.trim()}
                className={`flex-1 py-3 rounded-xl font-bold disabled:opacity-40 ${c.btnPrimary}`}>
                {loading ? <><span className="animate-spin inline-block mr-2">⏳</span>Separating signal…</> : '📡 Find the Signal'}
              </button>
              <button onClick={handleExample} disabled={loading}
                className={`px-4 py-3 rounded-xl text-sm font-medium ${c.btnSecondary}`}>
                Example
              </button>
            </div>
          </div>
        )}

        {/* Results */}
        {results && (
          <div className="space-y-4">

            {/* Topic + why noisy */}
            <div className={`rounded-2xl border p-5 ${c.card} ${c.cardBorder}`}>
              <p className={`text-xs font-bold uppercase tracking-wider mb-1 ${c.textMuted}`}>Analyzing</p>
              <p className={`font-bold text-base mb-3 ${c.text}`}>{results.topic_as_understood}</p>
              <p className={`text-xs font-bold uppercase tracking-wider mb-1 ${c.textMuted}`}>Why this field generates so much noise</p>
              <p className={`text-sm leading-relaxed ${c.textSecondary}`}>{results.why_this_field_is_noisy}</p>
            </div>

            {/* THE SIGNAL */}
            {results.the_signal?.items?.length > 0 && (
              <div className={`rounded-2xl border p-5 ${c.signalBg}`}>
                <p className={`text-xs font-black uppercase tracking-widest mb-3 ${c.signalText}`}>✅ The Signal — What's Actually Established</p>
                <div className="space-y-4">
                  {results.the_signal.items.map((item, i) => (
                    <div key={i} className={`p-4 rounded-xl ${isDark ? 'bg-[#1a1816]/60' : 'bg-white/80'}`}>
                      <p className={`text-sm font-semibold mb-1 ${c.text}`}>{item.claim}</p>
                      <p className={`text-xs mb-1 ${c.textMuted}`}>
                        <span className="font-semibold">Why we know this:</span> {item.why_we_know_this}
                      </p>
                      {item.the_nuance && (
                        <p className={`text-xs italic mt-1 ${c.textMuted}`}>⚠️ Important nuance: {item.the_nuance}</p>
                      )}
                    </div>
                  ))}
                </div>
              </div>
            )}

            {/* THE NOISE */}
            {results.the_noise?.length > 0 && (
              <div className={`rounded-2xl border overflow-hidden ${c.card} ${c.cardBorder}`}>
                <button onClick={() => toggle('noise')} className="w-full text-left px-5 py-4 flex items-center justify-between">
                  <p className={`text-xs font-black uppercase tracking-widest ${isDark ? 'text-[#e8a9a3]' : 'text-[#8a3028]'}`}>
                    🚫 The Noise — {results.the_noise.length} Popular Claims That Don't Hold Up
                  </p>
                  <span className={`text-sm ${c.textMuted}`}>{expanded.noise ? '▲' : '▼'}</span>
                </button>
                {expanded.noise && (
                  <div className={`border-t ${c.divider}`}>
                    {results.the_noise.map((item, i) => {
                      const cfg = NOISE_TYPE_CONFIG[item.noise_type] || NOISE_TYPE_CONFIG.oversimplified;
                      return (
                        <div key={i} className={`px-5 py-4 ${i > 0 ? `border-t ${c.divider}` : ''}`}>
                          <div className="flex items-start gap-2 mb-2 flex-wrap">
                            <span className={`text-xs font-bold px-2 py-0.5 rounded-full border ${cfg.color(isDark)}`}>{cfg.label}</span>
                          </div>
                          <p className={`text-sm font-semibold mb-1 ${c.text}`}>"{item.claim}"</p>
                          <p className={`text-sm ${c.textSecondary}`}>{item.the_problem}</p>
                          {item.kernel_of_truth && (
                            <p className={`text-xs mt-2 ${c.textMuted}`}><span className="font-semibold">Kernel of truth:</span> {item.kernel_of_truth}</p>
                          )}
                        </div>
                      );
                    })}
                  </div>
                )}
              </div>
            )}

            {/* GENUINELY DEBATED */}
            {results.genuinely_debated?.length > 0 && (
              <div className={`rounded-2xl border overflow-hidden ${c.card} ${c.cardBorder}`}>
                <button onClick={() => toggle('debated')} className="w-full text-left px-5 py-4 flex items-center justify-between">
                  <p className={`text-xs font-black uppercase tracking-widest ${c.debatedText}`}>
                    🔵 Genuinely Debated — {results.genuinely_debated.length} Questions Still Open
                  </p>
                  <span className={`text-sm ${c.textMuted}`}>{expanded.debated ? '▲' : '▼'}</span>
                </button>
                {expanded.debated && (
                  <div className={`border-t ${c.divider}`}>
                    {results.genuinely_debated.map((item, i) => (
                      <div key={i} className={`px-5 py-4 ${i > 0 ? `border-t ${c.divider}` : ''}`}>
                        <p className={`text-sm font-semibold mb-2 ${c.text}`}>{item.question}</p>
                        <div className="grid grid-cols-1 sm:grid-cols-2 gap-2">
                          <div className={`p-2.5 rounded-lg text-xs ${c.signalBg}`}>
                            <p className={`font-bold mb-1 ${c.signalText}`}>One view</p>
                            <p className={c.textSecondary}>{item.side_a}</p>
                          </div>
                          <div className={`p-2.5 rounded-lg text-xs ${isDark ? 'bg-[#c8872e]/10 border border-[#c8872e]/30' : 'bg-[#fdf3e4] border border-[#e0c49a]'}`}>
                            <p className={`font-bold mb-1 ${isDark ? 'text-[#d9a04e]' : 'text-[#93541f]'}`}>Another view</p>
                            <p className={c.textSecondary}>{item.side_b}</p>
                          </div>
                        </div>
                        {item.why_unsettled && (
                          <p className={`text-xs mt-2 italic ${c.textMuted}`}>Why unsettled: {item.why_unsettled}</p>
                        )}
                      </div>
                    ))}
                  </div>
                )}
              </div>
            )}

            {/* THE BOTTOM LINE */}
            {results.the_bottom_line && (
              <div className={`rounded-2xl border-2 p-5 space-y-3 ${isDark ? 'border-[#2c4a6e] bg-[#2c4a6e]/10' : 'border-[#2c4a6e] bg-[#e8eef5]'}`}>
                <p className={`text-xs font-black uppercase tracking-widest ${isDark ? 'text-[#a8b9ce]' : 'text-[#2c4a6e]'}`}>⚡ The Bottom Line</p>
                {results.the_bottom_line.what_to_do && (
                  <div>
                    <p className={`text-xs font-bold uppercase tracking-wide mb-1 ${c.signalText}`}>✅ What the evidence supports</p>
                    <p className={`text-sm ${c.textSecondary}`}>{results.the_bottom_line.what_to_do}</p>
                  </div>
                )}
                {results.the_bottom_line.what_to_ignore && (
                  <div>
                    <p className={`text-xs font-bold uppercase tracking-wide mb-1 ${isDark ? 'text-[#e8a9a3]' : 'text-[#8a3028]'}`}>🚫 What to ignore</p>
                    <p className={`text-sm ${c.textSecondary}`}>{results.the_bottom_line.what_to_ignore}</p>
                  </div>
                )}
                {results.the_bottom_line.the_honest_uncertainty && (
                  <div>
                    <p className={`text-xs font-bold uppercase tracking-wide mb-1 ${c.textMuted}`}>🔵 Honest uncertainty</p>
                    <p className={`text-sm ${c.textSecondary}`}>{results.the_bottom_line.the_honest_uncertainty}</p>
                  </div>
                )}
              </div>
            )}

            {/* NOISE SOURCES */}
            {results.sources_of_noise?.length > 0 && (
              <div className={`rounded-2xl border overflow-hidden ${c.card} ${c.cardBorder}`}>
                <button onClick={() => toggle('sources')} className="w-full text-left px-5 py-4 flex items-center justify-between">
                  <p className={`text-xs font-bold uppercase tracking-wider ${c.textMuted}`}>🕵️ Who's Generating the Noise</p>
                  <span className={`text-sm ${c.textMuted}`}>{expanded.sources ? '▲' : '▼'}</span>
                </button>
                {expanded.sources && (
                  <div className={`border-t ${c.divider}`}>
                    {results.sources_of_noise.map((src, i) => (
                      <div key={i} className={`px-5 py-4 ${i > 0 ? `border-t ${c.divider}` : ''}`}>
                        <p className={`text-sm font-semibold mb-0.5 ${c.text}`}>{src.actor}</p>
                        <p className={`text-xs mb-1 ${c.textMuted}`}><span className="font-semibold">Incentive:</span> {src.incentive}</p>
                        <p className={`text-xs ${c.textMuted}`}><span className="font-semibold">How to spot it:</span> {src.how_to_spot_it}</p>
                      </div>
                    ))}
                  </div>
                )}
              </div>
            )}

            {/* Actions */}
            <div className="flex items-center gap-3 flex-wrap">
              <ActionBar content={buildText()} title="Signal vs. Noise" />
              <button onClick={() => { setResults(null); setTopic(''); setConflictingAdvice(''); setUserContext(''); }}
                className={`px-4 py-2 rounded-lg text-sm font-medium ${c.btnSecondary}`}>
                🔄 New Topic
              </button>
            </div>

            {/* Cross-references */}
            <div className={`rounded-xl border p-4 ${c.cardAlt} ${c.cardBorder}`}>
              <p className={`text-xs font-semibold uppercase tracking-wide mb-3 ${c.textMuted}`}>Related tools</p>
              <div className="flex flex-wrap gap-2">
                {[
                  { id: 'BeliefStressTest', icon: '🧪', label: 'Stress-test a belief' },
                  { id: 'TheFinalWord', icon: '⚖️', label: 'Fact-check a specific claim' },
                  { id: 'FutureProof', icon: '🔮', label: 'Assess a 5-year trajectory' },
                ].map(r => (
                  <a key={r.id} href={`/tool/${r.id}`} target="_blank" rel="noopener noreferrer"
                    className={`px-3 py-1.5 rounded-lg text-xs font-medium transition-colors ${c.btnSecondary}`}>
                    {r.icon} {r.label}
                  </a>
                ))}
              </div>
            </div>
          </div>
        )}
      </div>
    </div>
  );
};

SignalVsNoise.displayName = 'SignalVsNoise';
export default SignalVsNoise;
