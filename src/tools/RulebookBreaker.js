import React, { useState, useEffect, useRef, useCallback } from 'react';
import { useTheme } from '../hooks/useTheme';
import { usePersistentState } from '../hooks/usePersistentState';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useRegisterActions } from '../components/ActionBarContext';
import { useTranslation } from '../i18n/useTranslation';
import { currencySymbol } from '../utils/formatLocale';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const WIN_LIKELIHOOD_CONFIG = {
  high:      { icon: '🟢' },
  medium:    { icon: '🟡' },
  low:       { icon: '🟠' },
  very_low:  { icon: '🔴' },
};

const RulebookBreaker = ({ tool }) => {
  const { callToolEndpoint, loading, userLocale, userCurrency, userRegion } = useClaudeAPI();
  const { isDark } = useTheme();
  const { t } = useTranslation();
  const sym = currencySymbol(userLocale, userCurrency);

  // ── Theme ──
  const c = {
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:         isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20'
                          : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-500' : 'text-gray-400',
    required:      isDark ? 'text-amber-400' : 'text-amber-500',
    labelText:     isDark ? 'text-zinc-200' : 'text-gray-700',
    accentTxt:     isDark ? 'text-cyan-400' : 'text-cyan-600',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white'
                          : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200'
                          : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200'
                          : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200'
                          : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200'
                          : 'bg-red-50 border-red-200 text-red-800',
  };
  c.textMuteded = c.textMuted;

  // Win-likelihood text colors — resolved here (inside component) so isDark is in scope
  const wlColors = {
    high:     isDark ? 'text-emerald-400' : 'text-emerald-700',
    medium:   isDark ? 'text-amber-400'   : 'text-amber-600',
    low:      isDark ? 'text-orange-400'  : 'text-orange-600',
    very_low: isDark ? 'text-red-400'     : 'text-red-600',
  };
  c.label = c.labelText;

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';

  // Win-likelihood labels — localized
  const wlLabels = {
    high:     t('rb_wl_high'),
    medium:   t('rb_wl_medium'),
    low:      t('rb_wl_low'),
    very_low: t('rb_wl_very_low'),
  };

  // Example seeds — localized + currency-aware
  const SYSTEM_EXAMPLES = [
    { system: t('rb_ex_system'),  problem: t('rb_ex_problem', { sym }) },
    { system: t('rb_ex2_system'), problem: t('rb_ex2_problem') },
    { system: t('rb_ex3_system'), problem: t('rb_ex3_problem') },
    { system: t('rb_ex4_system'), problem: t('rb_ex4_problem', { sym }) },
    { system: t('rb_ex5_system'), problem: t('rb_ex5_problem') },
  ];

  // ── State ──
  const [system, setSystem] = useState('');
  const [problem, setProblem] = useState('');
  const [whatTried, setWhatTried] = useState('');
  const [goal, setGoal] = useState('');
  const [error, setError] = useState('');
  const [expanded, setExpanded] = useState({ loopholes: true, phrases: true, regulatory: false, nuclear: false });

  // ── Refs ──
  const resultsRef = useRef(null);
  const submitRef = useRef(null);
  const canSubmitRef = useRef(false);

  // ── Persistent state ──
  const [results, setResults] = usePersistentState('rulebookbreaker-result', null);
  const [sessionHistory, setSessionHistory] = usePersistentState('rulebookbreaker-history', []);

  // ── Handlers ──
  const toggle = (k) => setExpanded(p => ({ ...p, [k]: !p[k] }));

  const handleSubmit = useCallback(async () => {
    if (!system.trim() || !problem.trim()) return;
    setError(''); setResults(null);
    try {
      const data = await callToolEndpoint('rulebook-breaker', {
        system: system.trim(),
        problem: problem.trim(),
        whatTried: whatTried.trim() || undefined,
        goal: goal.trim() || undefined,
        userLocale, userCurrency, userRegion,
      });
      setResults(data);
      setSessionHistory(prev => [
        // PF-25 exception: 40-char preview-text truncation; session history is capped at 6.
        { id: Date.now(), date: new Date().toISOString(), preview: (system || problem || '').slice(0, 40) },
        ...prev,
      ].slice(0, 6));
    } catch (e) { setError(e.message || t('rb_error')); }
  }, [system, problem, whatTried, goal, callToolEndpoint, setResults, setSessionHistory, userLocale, userCurrency, userRegion, t]);

  const loadExample = useCallback(() => {
    const ex = SYSTEM_EXAMPLES[Math.floor(Math.random() * SYSTEM_EXAMPLES.length)];
    setSystem(ex.system); setProblem(ex.problem); setResults(null);
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [setResults, setSystem, setProblem]);

  const handleReset = useCallback(() => {
    setResults(null); setError('');
    setSystem(''); setProblem(''); setWhatTried(''); setGoal('');
  }, [setResults, setError, setSystem, setProblem, setWhatTried, setGoal]);

  // ── buildFullText ──
  const buildFullText = useCallback(() => {
    if (!results) return '';
    let out = `🏴‍☠️ ${t('rb_copy_header')}\n\n${t('rb_copy_system')} ${system}\n${t('rb_copy_problem')} ${problem}\n\n`;
    out += `${t('rb_copy_ladder')}\n`;
    results?.the_ladder?.forEach(r => {
      out += `${t('rb_copy_step')} ${r.rung}: ${r.title}\n${r.action}\n`;
      if (r.magic_words?.length) out += `${t('rb_copy_magic_words')} ${r.magic_words.join(' | ')}\n`;
      out += '\n';
    });
    if (results?.magic_phrases?.length) {
      out += `${t('rb_copy_phrases')}\n`;
      results?.magic_phrases?.forEach(p => { out += `• "${p.phrase}"\n  ${p.why_it_works}\n`; });
      out += '\n';
    }
    if (results?.the_first_move) out += `${t('rb_copy_first_move')}\n${results?.the_first_move}\n`;
    return out + BRAND;
  }, [results, system, problem, t]);

  // Live ref assignments
  submitRef.current = handleSubmit;
  canSubmitRef.current = !!(system.trim() && problem.trim());

  useRegisterActions(buildFullText(), tool?.title || 'Rulebook Breaker');

  // Scroll to results
  useEffect(() => {
    if (!results) return;
    const timer = setTimeout(() => resultsRef.current?.scrollIntoView({ behavior: 'smooth', block: 'start' }), 100);
    return () => clearTimeout(timer);
  }, [results]);

  // Keyboard shortcut
  useEffect(() => {
    const handler = (e) => {
      const tag = document.activeElement?.tagName;
      if (tag === 'SELECT') return;
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && !loading && canSubmitRef.current)
        submitRef.current?.();
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [loading]);

  const wlcfg = results
    ? (WIN_LIKELIHOOD_CONFIG[results?.honest_assessment?.win_likelihood] || WIN_LIKELIHOOD_CONFIG.medium)
    : null;

  // ── Render helpers ──
  const rungHeaderBg = isDark ? 'bg-cyan-800/10' : 'bg-zinc-100';
  const rungNumBg    = 'bg-cyan-800 text-white';
  const magicWordBg  = isDark
    ? 'bg-zinc-700 border-zinc-600 text-emerald-400'
    : 'bg-zinc-100 border-zinc-300 text-zinc-700';
  const firstMoveBg  = isDark
    ? 'border-cyan-800 bg-cyan-800/10'
    : 'border-cyan-700 bg-cyan-50';
  const firstMoveLabel = isDark ? 'text-cyan-400' : 'text-cyan-800';

  return (
    <div className={`space-y-4 ${c.text}`}>

      {/* ── Input card ── */}
      {!results && !loading && (
        <div className={`${c.card} border ${c.border} rounded-xl shadow-sm p-5 space-y-4`}>

          {/* Header */}
          <div className="pb-3 border-b border-zinc-500">
            <h2 className={`text-xl font-bold ${c.text}`}>
              <span className="mr-2">{tool?.icon ?? '🏴‍☠️'}</span>{tool?.title ?? t('rb_title')}
            </h2>
            <p className={`text-sm ${c.textSecondary}`}>{tool?.tagline ?? t('rb_tagline')}</p>
            <button onClick={loadExample} disabled={loading} style={{ backgroundColor: (tool?.headerColor ?? '#888888') + '80' }} className={`mt-1.5 px-2.5 py-0.5 rounded-full text-xs font-medium border disabled:opacity-40 ${isDark ? 'text-white border-white/40' : 'text-gray-800 border-transparent'}`}>{t('try_example')}</button>
          </div>

          <div>
            <label className={`block text-sm font-semibold mb-1.5 ${c.labelText}`}>
              {t('rb_system_label')} <span className={c.required}>*</span>
            </label>
            <input type="text" value={system} onChange={e => setSystem(e.target.value)}
              placeholder={t('rb_system_ph')}
              className={`w-full px-4 py-3 rounded-xl border text-sm ${c.input}`} />
          </div>

          <div>
            <label className={`block text-sm font-semibold mb-1.5 ${c.labelText}`}>
              {t('rb_problem_label')} <span className={c.required}>*</span>
            </label>
            <textarea value={problem} onChange={e => setProblem(e.target.value)}
              placeholder={t('rb_problem_ph')}
              rows={3} maxLength={600}
              className={`w-full px-4 py-3 rounded-xl border text-sm resize-none ${c.input}`} />
          </div>

          <div className="grid grid-cols-1 sm:grid-cols-2 gap-3">
            <div>
              <label className={`block text-sm font-semibold mb-1.5 ${c.labelText}`}>
                {t('rb_tried_label')} <span className={`font-normal ${c.textMuted}`}>{t('rb_optional')}</span>
              </label>
              <textarea value={whatTried} onChange={e => setWhatTried(e.target.value)}
                placeholder={t('rb_tried_ph')}
                rows={2} maxLength={300}
                className={`w-full px-4 py-3 rounded-xl border text-sm resize-none ${c.input}`} />
            </div>
            <div>
              <label className={`block text-sm font-semibold mb-1.5 ${c.labelText}`}>
                {t('rb_goal_label')} <span className={`font-normal ${c.textMuted}`}>{t('rb_optional')}</span>
              </label>
              <textarea value={goal} onChange={e => setGoal(e.target.value)}
                placeholder={t('rb_goal_ph')}
                rows={2} maxLength={300}
                className={`w-full px-4 py-3 rounded-xl border text-sm resize-none ${c.input}`} />
            </div>
          </div>

          {error && (
            <div className={`p-3 rounded-xl border text-sm ${c.danger}`}>
              <span className="mr-1">⚠️</span>{error}
            </div>
          )}

          <button onClick={handleSubmit} disabled={loading || !system.trim() || !problem.trim()}
            className={`w-full py-3 rounded-xl font-bold disabled:opacity-40 flex items-center justify-center gap-2 min-h-[48px] ${c.btnPrimary}`}>
            {loading
              ? <><span className="inline-block animate-spin">{tool?.icon ?? '🏴‍☠️'}</span> {t('rb_finding')}</>
              : <><span>{tool?.icon ?? '🏴‍☠️'}</span> {t('rb_find_leverage')}</>}
          </button>

          <p className={`text-xs text-center ${c.textMuted}`}>{t('rb_disclaimer')}</p>

          {/* Pre-result cross-ref */}
          <p className={`text-xs text-center ${c.textMuted}`}>
            {t('rb_xref_pre')}{' '}
            <a href="/VelvetHammer" className={`text-xs ${linkStyle}`}>🔨 {t('rb_velvethammer')}</a> {t('rb_xref_pre_suffix')}
          </p>
        </div>
      )}

      {/* ── Loading state ── */}
      {loading && (
        <div className={`${c.card} border ${c.border} rounded-xl shadow-sm p-8 flex flex-col items-center gap-3`}>
          <span className="text-4xl animate-spin inline-block">{tool?.icon ?? '🏴‍☠️'}</span>
          <p className={`text-sm font-semibold ${c.textSecondary}`}>{t('rb_finding')}</p>
          <p className={`text-xs ${c.textMuted}`}>{t('rb_loading_sub')}</p>
        </div>
      )}

      {/* ── Results-phase header card with reset (ternary, not && — PF-3 replace-mode) ── */}
      {results ? (
        <div className={`${c.card} border ${c.border} rounded-xl shadow-sm p-5`}>
          <div className="pb-3 border-b border-zinc-500">
            <div className="flex items-start justify-between">
              <div>
                <h2 className={`text-xl font-bold ${c.text}`}>
                  <span className="mr-2">{tool?.icon ?? '🏴‍☠️'}</span>{tool?.title ?? t('rb_title')}
                </h2>
                <p className={`text-sm ${c.textSecondary}`}>{tool?.tagline ?? t('rb_tagline')}</p>
              </div>
              <button onClick={handleReset} className={`${c.btnSecondary} px-3 py-1.5 rounded-lg text-xs font-bold flex-shrink-0`}>
                ↺ {t('start_over')}
              </button>
            </div>
          </div>
        </div>
      ) : null}

      {/* ── Results ── */}
      {results && (
        <div className="space-y-4" ref={resultsRef}>

          {/* System analysis */}
          {results?.system_analysis && (
            <div className={`rounded-2xl border p-5 ${c.card} ${c.border}`}>
              <p className={`text-xs font-black uppercase tracking-widest mb-3 ${c.textMuted}`}>🔍 {t('rb_sys_works')}</p>
              <p className={`text-sm mb-2 ${c.textSecondary}`}>{results?.system_analysis?.how_it_actually_works}</p>
              {results?.system_analysis?.where_the_power_is && (
                <p className={`text-xs mb-1 ${c.textMuted}`}><span className="font-semibold">{t('rb_where_power')}</span> {results?.system_analysis?.where_the_power_is}</p>
              )}
              {results?.system_analysis?.their_pressure_points && (
                <p className={`text-xs ${c.textMuted}`}><span className="font-semibold">{t('rb_pressure_points')}</span> {results?.system_analysis?.their_pressure_points}</p>
              )}
            </div>
          )}

          {/* Win likelihood */}
          {results?.honest_assessment && (
            <div className={`rounded-2xl border p-4 flex items-center gap-4 ${c.card} ${c.border}`}>
              <div className="text-center flex-shrink-0">
                <p className="text-2xl">{wlcfg?.icon}</p>
                <p className={`text-xs font-black mt-1 ${wlColors[results?.honest_assessment?.win_likelihood] || wlColors.medium}`}>{wlLabels[results?.honest_assessment?.win_likelihood] || wlLabels.medium}</p>
                <p className={`text-xs ${c.textMuted}`}>{t('rb_win_odds')}</p>
              </div>
              <div>
                <p className={`text-sm font-semibold mb-1 ${c.text}`}>{results?.honest_assessment?.the_realistic_outcome}</p>
                {results?.honest_assessment?.when_to_cut_losses && (
                  <p className={`text-xs ${c.textMuted}`}><span className="font-semibold">{t('rb_cut_losses')}</span> {results?.honest_assessment?.when_to_cut_losses}</p>
                )}
              </div>
            </div>
          )}

          {/* Escalation ladder */}
          {results?.the_ladder?.length > 0 && (
            <div>
              <p className={`text-xs font-black uppercase tracking-widest px-1 mb-3 ${c.textMuted}`}>📶 {t('rb_ladder')}</p>
              <div className="space-y-3">
                {results?.the_ladder?.map((rung, i) => (
                  <div key={i} className={`rounded-2xl border overflow-hidden ${c.card} ${c.border}`}>
                    <div className={`px-5 py-3 flex items-center gap-3 ${rungHeaderBg}`}>
                      <span className={`w-7 h-7 rounded-full flex items-center justify-center text-sm font-black flex-shrink-0 ${rungNumBg}`}>{rung.rung}</span>
                      <p className={`font-bold text-sm ${c.text}`}>{rung.title}</p>
                      {rung.time_to_try && <span className={`text-xs ml-auto flex-shrink-0 ${c.textMuted}`}>{rung.time_to_try}</span>}
                    </div>
                    <div className="px-5 py-4 space-y-2">
                      <p className={`text-sm ${c.textSecondary}`}>{rung.action}</p>
                      {rung.the_mechanism && (
                        <p className={`text-xs italic ${c.textMuted}`}>{rung.the_mechanism}</p>
                      )}
                      {rung.magic_words?.length > 0 && (
                        <div className="flex flex-wrap gap-2 pt-1">
                          {rung.magic_words.map((w, j) => (
                            <span key={j} className={`text-xs px-2.5 py-1 rounded-lg border font-mono ${magicWordBg}`}>
                              "{w}"
                            </span>
                          ))}
                        </div>
                      )}
                      {rung.expected_outcome && (
                        <p className={`text-xs ${c.textMuted}`}><span className="font-semibold">{t('rb_expected')}</span> {rung.expected_outcome}</p>
                      )}
                    </div>
                  </div>
                ))}
              </div>
            </div>
          )}

          {/* Loopholes */}
          {results?.the_loopholes?.length > 0 && (
            <div className={`rounded-2xl border overflow-hidden ${c.card} ${c.border}`}>
              <button onClick={() => toggle('loopholes')} className="w-full text-left px-5 py-4 flex items-center justify-between">
                <p className={`text-xs font-bold uppercase tracking-wider ${c.textMuted}`}>🕳️ {t('rb_loopholes')}</p>
                <span className={`text-sm ${c.textMuted}`}>{expanded.loopholes ? '▲' : '▼'}</span>
              </button>
              {expanded.loopholes && (
                <div className={`border-t ${c.border}`}>
                  {results?.the_loopholes?.map((l, i) => (
                    <div key={i} className={`px-5 py-4 ${i > 0 ? `border-t ${c.border}` : ''}`}>
                      <p className={`text-sm font-semibold mb-1 ${c.text}`}>{l.loophole}</p>
                      <p className={`text-xs mb-1 ${c.textMuted}`}><span className="font-semibold">{t('rb_how_invoke')}</span> {l.how_to_invoke_it}</p>
                      {l.why_it_exists && <p className={`text-xs italic ${c.textMuted}`}>{l.why_it_exists}</p>}
                    </div>
                  ))}
                </div>
              )}
            </div>
          )}

          {/* Magic phrases */}
          {results?.magic_phrases?.length > 0 && (
            <div className={`rounded-2xl border overflow-hidden ${c.card} ${c.border}`}>
              <button onClick={() => toggle('phrases')} className="w-full text-left px-5 py-4 flex items-center justify-between">
                <p className={`text-xs font-bold uppercase tracking-wider ${isDark ? 'text-emerald-400' : 'text-emerald-700'}`}>✨ {t('rb_magic_phrases')}</p>
                <span className={`text-sm ${c.textMuted}`}>{expanded.phrases ? '▲' : '▼'}</span>
              </button>
              {expanded.phrases && (
                <div className={`border-t ${c.border}`}>
                  {results?.magic_phrases?.map((p, i) => (
                    <div key={i} className={`px-5 py-4 ${i > 0 ? `border-t ${c.border}` : ''}`}>
                      <p className={`text-sm font-mono font-semibold mb-1 ${isDark ? 'text-emerald-400' : 'text-emerald-700'}`}>"{p.phrase}"</p>
                      <p className={`text-xs mb-0.5 ${c.textMuted}`}><span className="font-semibold">{t('rb_when')}</span> {p.when_to_use}</p>
                      <p className={`text-xs ${c.textMuted}`}><span className="font-semibold">{t('rb_why_works')}</span> {p.why_it_works}</p>
                    </div>
                  ))}
                </div>
              )}
            </div>
          )}

          {/* Regulatory angle */}
          {results?.the_regulatory_angle && (
            <div className={`rounded-2xl border overflow-hidden ${c.card} ${c.border}`}>
              <button onClick={() => toggle('regulatory')} className="w-full text-left px-5 py-4 flex items-center justify-between">
                <p className={`text-xs font-bold uppercase tracking-wider ${c.textMuted}`}>🏛️ {t('rb_regulatory')}</p>
                <span className={`text-sm ${c.textMuted}`}>{expanded.regulatory ? '▲' : '▼'}</span>
              </button>
              {expanded.regulatory && (
                <div className={`px-5 pb-5 border-t ${c.border} pt-4 space-y-2`}>
                  {results?.the_regulatory_angle?.relevant_bodies && (
                    <p className={`text-sm ${c.textSecondary}`}><span className={`font-semibold ${c.text}`}>{t('rb_relevant_bodies')}</span> {results?.the_regulatory_angle?.relevant_bodies}</p>
                  )}
                  {results?.the_regulatory_angle?.filing_a_complaint && (
                    <p className={`text-sm ${c.textSecondary}`}><span className={`font-semibold ${c.text}`}>{t('rb_filing_complaint')}</span> {results?.the_regulatory_angle?.filing_a_complaint}</p>
                  )}
                  {results?.the_regulatory_angle?.what_they_fear && (
                    <p className={`text-sm ${c.textSecondary}`}><span className={`font-semibold ${c.text}`}>{t('rb_what_fear')}</span> {results?.the_regulatory_angle?.what_they_fear}</p>
                  )}
                </div>
              )}
            </div>
          )}

          {/* Nuclear options */}
          {results?.the_nuclear_options?.length > 0 && (
            <div className={`rounded-2xl border overflow-hidden ${c.card} ${c.border}`}>
              <button onClick={() => toggle('nuclear')} className="w-full text-left px-5 py-4 flex items-center justify-between">
                <p className={`text-xs font-bold uppercase tracking-wider ${c.textMuted}`}>💣 {t('rb_nuclear')}</p>
                <span className={`text-sm ${c.textMuted}`}>{expanded.nuclear ? '▲' : '▼'}</span>
              </button>
              {expanded.nuclear && (
                <div className={`border-t ${c.border}`}>
                  {results?.the_nuclear_options?.map((n, i) => (
                    <div key={i} className={`px-5 py-4 ${i > 0 ? `border-t ${c.border}` : ''}`}>
                      <p className={`text-sm font-semibold mb-1 ${c.text}`}>{n.option}</p>
                      {n.how_to_execute && <p className={`text-xs mb-0.5 ${c.textMuted}`}><span className="font-semibold">{t('rb_how')}</span> {n.how_to_execute}</p>}
                      {n.when_to_use_it && <p className={`text-xs mb-0.5 ${c.textMuted}`}><span className="font-semibold">{t('rb_when')}</span> {n.when_to_use_it}</p>}
                      {n.real_likelihood && <p className={`text-xs italic ${c.textMuted}`}>{n.real_likelihood}</p>}
                    </div>
                  ))}
                </div>
              )}
            </div>
          )}

          {/* First move */}
          {results?.the_first_move && (
            <div className={`rounded-2xl border-2 p-5 ${firstMoveBg}`}>
              <p className={`text-xs font-black uppercase tracking-widest mb-2 ${firstMoveLabel}`}>⚡ {t('rb_first_move')}</p>
              <p className={`text-sm font-medium ${c.text}`}>{results?.the_first_move}</p>
            </div>
          )}

          {/* Post-result cross-refs */}
          <div className={`rounded-xl border p-4 ${c.cardAlt} ${c.border}`}>
            <p className={`text-xs font-semibold uppercase tracking-wide mb-3 ${c.textMuted}`}>🔗 {t('rb_related')}</p>
            <div className="flex flex-wrap gap-2">
              <a href="/VelvetHammer" className={`text-xs ${linkStyle}`}>🔨 {t('rb_velvethammer')}</a>
              <a href="/MagicMouth" className={`text-xs ${linkStyle}`}>🎤 {t('rb_magicmouth')}</a>
              <a href="/LeaseTrapDetector" className={`text-xs ${linkStyle}`}>🔍 {t('rb_leasetrap')}</a>
            </div>
          </div>

        </div>
      )}

      {/* History */}
      {sessionHistory.length > 0 && !results && (
        <div className={`rounded-xl border p-4 ${c.card} ${c.border}`}>
          <p className={`text-xs font-semibold uppercase tracking-wide mb-3 ${c.textMuted}`}>{t('rb_recent')}</p>
          <div className="space-y-1">
            {sessionHistory.map(h => (
              <p key={h.id} className={`text-xs ${c.textMuted} truncate`}>{h.preview}</p>
            ))}
          </div>
        </div>
      )}

    </div>
  );
};

RulebookBreaker.displayName = 'RulebookBreaker';
export default RulebookBreaker;
