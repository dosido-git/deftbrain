import React, { useState, useCallback, useEffect } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { useRegisterActions } from '../components/ActionBarContext';
import { usePersistentState } from '../hooks/usePersistentState';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

// ════════════════════════════════════════════════════════════
// CONSTANTS
// ════════════════════════════════════════════════════════════
const MEDIA_TYPES = [
  { value: 'movie', label: 'Movie', icon: '🎬' },
  { value: 'show', label: 'TV Show', icon: '📺' },
  { value: 'book', label: 'Book', icon: '📖' },
  { value: 'game', label: 'Game', icon: '🎮' },
];

const DIRECTIONS = [
  { value: 'wild', label: 'Surprise Me', icon: '🎲', desc: 'Most creative angle' },
  { value: 'villain', label: 'Secret Villain', icon: '😈', desc: 'Hero is actually evil' },
  { value: 'connected', label: 'Shared Universe', icon: '🔗', desc: 'Connected to another franchise' },
  { value: 'timeline', label: 'Timeline Twist', icon: '⏰', desc: 'Events are out of order' },
  { value: 'alive', label: 'Dead or Alive', icon: '👻', desc: 'Dead character is alive, or vice versa' },
  { value: 'simulation', label: 'It\'s a Simulation', icon: '🖥️', desc: 'None of it is real' },
];

const EVIDENCE_STYLES = {
  'COMPELLING':    { icon: '🔥', color: 'accentTxt' },
  'SUSPICIOUS':    { icon: '🧐', color: 'warningTxt' },
  'A STRETCH':     { icon: '🤸', color: 'textMuted' },
  'PURE DELUSION': { icon: '🤡', color: 'textSecondary' },
};

// ════════════════════════════════════════════════════════════
// COMPONENT
// ════════════════════════════════════════════════════════════
const FanTheory = ({ tool }) => {
  const { callToolEndpoint, loading } = useClaudeAPI();
  const { isDark } = useTheme();



  const c = {
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:         isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20'
                          : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-500' : 'text-gray-400',
    labelText:     isDark ? 'text-zinc-200' : 'text-gray-700',
    accentTxt:     isDark ? 'text-cyan-400' : 'text-cyan-600',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white'
                          : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200'
                          : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200'
                          : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200'
                          : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200'
                          : 'bg-red-50 border-red-200 text-red-800',
    infoBox:       isDark ? 'bg-sky-900/20 border-sky-700 text-sky-200'
                          : 'bg-sky-50 border-sky-200 text-sky-800',
    successBox:    isDark ? 'bg-emerald-900/20 border-emerald-700' : 'bg-emerald-50 border-emerald-300',
    successTxt:    isDark ? 'text-emerald-300' : 'text-emerald-800',
    warningBox:    isDark ? 'bg-amber-900/20 border-amber-700' : 'bg-amber-50 border-amber-300',
    warningTxt:    isDark ? 'text-amber-300' : 'text-amber-800',
    pillActive:    isDark ? 'border-cyan-500 bg-cyan-900/30 text-cyan-200'
                          : 'border-cyan-600 bg-cyan-100 text-cyan-900',
    pillInactive:  isDark ? 'border-zinc-600 text-zinc-400 hover:border-zinc-500'
                          : 'border-gray-300 text-gray-500 hover:border-gray-400',
    required:      isDark ? 'text-amber-400' : 'text-amber-500',
    quoteBg:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
  };
  c.textMuteded = c.textMuted;
  c.label = c.labelText;

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';

  // ── State ──
  const [title, setTitle] = usePersistentState('fantheory-title', '');
  const [savedTheories, setSavedTheories] = usePersistentState('fantheory-saved', []);
  const [expandedSaved, setExpandedSaved] = useState(null); // id of expanded saved card
  const [mediaType, setMediaType] = useState('movie');
  const [direction, setDirection] = useState('wild');
  const [results, setResults] = usePersistentState('fantheory-results', null);
  const [myTheory, setMyTheory] = useState('');
  const [gradeResults, setGradeResults] = useState(null);
  const [error, setError] = useState('');

  // ── Refs ──
  const resultsRef = React.useRef(null);
  const scrollTimerRef = React.useRef(null);

  // ── API: Generate theory ──
  const runGenerate = useCallback(async (saveFirst = false) => {
    if (!title.trim()) return;
    // "Different Theory" — archive current before overwriting
    if (saveFirst && results) {
      setSavedTheories(prev => [{ id: Date.now(), title: title.trim(), data: results }, ...prev].slice(0, 5));
    }
    setError('');
    // Don't blank results yet — keep showing previous while loading
    try {
      const data = await callToolEndpoint('fan-theory', { title: title.trim(), mediaType, direction });
      setResults(data);
      setGradeResults(null);
      if (scrollTimerRef.current) clearTimeout(scrollTimerRef.current);
      scrollTimerRef.current = setTimeout(() => resultsRef.current?.scrollIntoView({ behavior: 'smooth' }), 200);
    } catch (err) { setError(err.message || 'Theory failed'); }
  }, [title, mediaType, direction, callToolEndpoint, results, setSavedTheories]);

  // ── API: Grade theory ──
  const runGrade = useCallback(async () => {
    if (!myTheory.trim()) return;
    setError(''); setGradeResults(null);
    try {
      const data = await callToolEndpoint('fan-theory/grade', { title: title.trim(), theory: myTheory.trim() });
      setGradeResults(data);
    } catch (err) { setError(err.message || 'Grading failed'); }
  }, [title, myTheory, callToolEndpoint]);

  // ── Copy builder ──
  const buildFullText = useCallback(() => {
    if (gradeResults) {
      const lines = [
        `📝 FAN THEORY GRADE: ${gradeResults.grade}`,
        gradeResults.grade_title ? `"${gradeResults.grade_title}"` : '',
        '',
        `Plausibility: ${gradeResults.plausibility}/10 | Creativity: ${gradeResults.creativity}/10`,
        gradeResults.evidence_quality ? `Evidence quality: ${gradeResults.evidence_quality}` : '',
        '',
      ];
      if (gradeResults.strengths?.length) {
        lines.push('STRENGTHS:');
        gradeResults.strengths.forEach(s => lines.push(`• ${s}`));
        lines.push('');
      }
      if (gradeResults.weaknesses?.length) {
        lines.push('WEAKNESSES:');
        gradeResults.weaknesses.forEach(w => lines.push(`• ${w}`));
        lines.push('');
      }
      if (gradeResults.professor_notes) lines.push(`PROFESSOR'S NOTES:\n${gradeResults.professor_notes}`, '');
      if (gradeResults.improvement_suggestion) lines.push(`IMPROVEMENT:\n${gradeResults.improvement_suggestion}`, '');
      if (gradeResults.would_reddit_upvote) lines.push(`REDDIT PREDICTION:\n${gradeResults.would_reddit_upvote}`, '');
      lines.push(BRAND);
      return lines.filter(l => l !== undefined).join('\n');
    }
    if (!results) return '';
    const lines = [`🧵 FAN THEORY: ${results.theory_name || ''}`, ''];
    if (results.one_line) lines.push(`💡 ${results.one_line}`, '');
    if (results.the_theory) lines.push(results.the_theory, '');
    if (results.evidence?.length) {
      lines.push('EVIDENCE:');
      results.evidence.forEach(e => lines.push(`  ${e.strength === 'COMPELLING' ? '🔥' : '🧐'} ${e.detail} → ${e.spin}`));
      lines.push('');
    }
    if (results.the_smoking_gun) lines.push(`🔫 Smoking Gun: ${results.the_smoking_gun}`);
    if (results.plausibility) lines.push(`\nPlausibility: ${results.plausibility}/10`);
    lines.push(BRAND);
    return lines.join('\n');
  }, [results, gradeResults]);

  useRegisterActions(buildFullText(), tool?.title || 'Fan Theory Generator');

  // ── PF-6: Keyboard handler ──
  const runGenerateRef = React.useRef(null);
  const runGradeRef = React.useRef(null);
  const gradeTextareaRef = React.useRef(null);
  const canGenerateRef = React.useRef(false);
  const canGradeRef = React.useRef(false);
  runGenerateRef.current = runGenerate;
  runGradeRef.current = runGrade;
  canGenerateRef.current = !!title.trim() && !loading;
  canGradeRef.current = !!myTheory.trim() && !loading;

  useEffect(() => {
    const handler = (e) => {
      const tag = document.activeElement?.tagName;
      if (tag === 'SELECT') return;
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey)) {
        // If focus is inside the grade textarea → grade
        if (document.activeElement === gradeTextareaRef.current && canGradeRef.current) {
          runGradeRef.current?.();
        } else if (canGenerateRef.current) {
          // Anywhere else (title input, direction buttons, etc.) → generate
          runGenerateRef.current?.();
        }
      }
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [loading]);

  const evStyle = (str) => EVIDENCE_STYLES[str] || EVIDENCE_STYLES['SUSPICIOUS'];

  // ════════════════════════════════════════════════════════════
  // RENDER
  // ════════════════════════════════════════════════════════════
  return (
    <div className={`space-y-4 ${c.text}`}>

      {/* Persistent header card — h2 FIRST, inputs after border-b per PF-3 */}
      <div className={`${c.card} border ${c.border} rounded-xl shadow-sm p-5`}>
        <div className="pb-3 border-b border-zinc-500">
          <h2 className={`text-xl font-bold ${c.text} flex items-center gap-2`}>
            <span>{tool?.icon ?? '🧵'}</span>{tool?.title ?? 'Fan Theory Generator'}
          </h2>
          <p className={`text-sm ${c.textSecondary}`}>{tool?.tagline ?? "Name anything — I'll generate a wild but defensible fan theory"}</p>
        </div>
        <div className="mt-4 space-y-4">
          <div className="grid grid-cols-1 sm:grid-cols-2 gap-3">
            <div>
              <label className={`text-xs font-bold ${c.textSecondary} block mb-1.5`}>Title <span className={c.required}>*</span></label>
              <input type="text" value={title} onChange={e => setTitle(e.target.value)}
                placeholder="Movie, show, book, or game..."
                className={`w-full px-3 py-2.5 border rounded-lg text-sm ${c.input} outline-none focus:ring-2`} />
            </div>
            <div>
              <label className={`text-xs font-bold ${c.textSecondary} uppercase block mb-1.5`}>Type</label>
              <div className="flex gap-1.5 flex-wrap">
                {MEDIA_TYPES.map(m => (
                  <button key={m.value} onClick={() => setMediaType(m.value)}
                    className={`px-2.5 py-1.5 rounded-lg text-[11px] font-bold border min-h-[30px] ${
                      mediaType === m.value ? c.pillActive : c.pillInactive}`}>
                    {m.icon} {m.label}
                  </button>
                ))}
              </div>
            </div>
          </div>
          <div>
            <label className={`text-xs font-bold ${c.textSecondary} uppercase block mb-2`}>Theory direction</label>
            <div className="grid grid-cols-2 sm:grid-cols-3 gap-2">
              {DIRECTIONS.map(d => (
                <button key={d.value} onClick={() => setDirection(d.value)}
                  className={`py-2 px-3 rounded-xl border text-left min-h-[48px] transition-colors ${
                    direction === d.value ? c.pillActive : c.pillInactive}`}>
                  <span className="text-sm">{d.icon} {d.label}</span>
                  <span className={`text-[9px] block ${direction === d.value ? 'opacity-80' : c.textMuted}`}>{d.desc}</span>
                </button>
              ))}
            </div>
          </div>
          <button onClick={runGenerate} disabled={!title.trim() || loading}
            className={`w-full ${c.btnPrimary} disabled:opacity-40 font-bold py-3 rounded-lg flex items-center justify-center gap-2 min-h-[48px]`}>
            {loading
              ? <><span className="animate-spin inline-block">{tool?.icon ?? '🧵'}</span> Theorizing...</>
              : <><span className="mr-1">{tool?.icon ?? '🧵'}</span>Generate Theory</>}
          </button>
        </div>
      </div>

      {/* Saved theories stack — pinned when "Different Theory" is clicked */}
      {savedTheories.length > 0 && (
        <div className="space-y-2">
          <p className={`text-[10px] font-bold ${c.textMuted} uppercase px-1`}>📌 Saved Theories</p>
          {savedTheories.map(saved => (
            <div key={saved.id} className={`${c.card} border ${c.border} rounded-xl overflow-hidden`}>
              <button onClick={() => setExpandedSaved(expandedSaved === saved.id ? null : saved.id)}
                className={`w-full flex items-center justify-between gap-3 p-3 text-left hover:${c.cardAlt} transition-colors`}>
                <div className="min-w-0">
                  <p className={`text-xs font-bold ${c.text} truncate`}>🧵 {saved.data.theory_name}</p>
                  <p className={`text-[11px] ${c.textMuted} truncate`}>{saved.title} · {saved.data.one_line}</p>
                </div>
                <span className={`text-xs ${c.textMuted} flex-shrink-0`}>{expandedSaved === saved.id ? '▲' : '▼'}</span>
              </button>
              {expandedSaved === saved.id && (
                <div className={`border-t ${c.border} p-4 space-y-3`}>
                  {saved.data.one_line && <p className={`text-sm ${c.textSecondary} italic`}>"{saved.data.one_line}"</p>}
                  {saved.data.the_theory && <p className={`text-sm ${c.textSecondary} leading-relaxed`}>{saved.data.the_theory}</p>}
                  {saved.data.the_smoking_gun && (
                    <div className={`${c.warningBox} border rounded-lg p-3`}>
                      <p className={`text-[10px] font-bold ${c.warningTxt} uppercase mb-1`}>🔫 Smoking Gun</p>
                      <p className={`text-xs ${c.text}`}>{saved.data.the_smoking_gun}</p>
                    </div>
                  )}
                  <button onClick={() => setSavedTheories(prev => prev.filter(s => s.id !== saved.id))}
                    className={`text-xs ${c.textMuted} hover:text-red-500 transition-colors`}>
                    ✕ Remove
                  </button>
                </div>
              )}
            </div>
          ))}
        </div>
      )}

      {/* Current generated theory */}
      {results && (
        <div ref={resultsRef} className="space-y-4">
          <div className={`${c.warningBox} border-2 rounded-xl p-5 text-center`}>
            <span className="text-3xl block mb-2">🧵</span>
            <p className={`text-lg font-black ${c.text} mb-2`}>{results.theory_name}</p>
            {results.one_line && <p className={`text-sm ${c.textSecondary} italic`}>{results.one_line}</p>}
            <div className="flex justify-center gap-4 mt-3">
              {results.plausibility && (
                <div><span className={`text-xl font-black ${c.accentTxt}`}>{results.plausibility}</span><span className={`text-xs ${c.textMuted}`}>/10 plausible</span></div>
              )}
              {results.mind_blown_factor && (
                <div><span className={`text-xl font-black ${c.accentTxt}`}>{results.mind_blown_factor}</span><span className={`text-xs ${c.textMuted}`}>/10 mind-blown</span></div>
              )}
            </div>
          </div>

          {results.the_theory && (
            <div className={`${c.card} border ${c.border} rounded-xl p-5`}>
              <h3 className={`text-sm font-bold ${c.text} mb-2`}>🕵️ The Theory</h3>
              <p className={`text-sm ${c.textSecondary} leading-relaxed whitespace-pre-line`}>{results.the_theory}</p>
            </div>
          )}

          {results.evidence?.length > 0 && (
            <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
              <h3 className={`text-sm font-bold ${c.text} mb-3`}>📎 Evidence</h3>
              <div className="space-y-2">
                {results.evidence.map((e, i) => {
                  const es = evStyle(e.strength);
                  return (
                    <div key={i} className={`${c.quoteBg} rounded-lg p-3`}>
                      <div className="flex items-start justify-between gap-2 mb-1">
                        <p className={`text-xs font-medium ${c.text}`}>{es.icon} {e.detail}</p>
                        <span className={`text-[8px] font-black ${c[es.color]} whitespace-nowrap`}>{e.strength}</span>
                      </div>
                      <p className={`text-[11px] ${c.textMuted}`}>→ {e.spin}</p>
                    </div>
                  );
                })}
              </div>
            </div>
          )}

          {results.the_smoking_gun && (
            <div className={`${c.warning} border-2 rounded-xl p-4`}>
              <p className="text-[10px] font-bold uppercase mb-1">🔫 The Smoking Gun</p>
              <p className="text-sm font-bold">{results.the_smoking_gun}</p>
            </div>
          )}

          <div className="grid grid-cols-1 sm:grid-cols-2 gap-3">
            {results.counterargument && (
              <div className={`${c.quoteBg} border ${c.border} rounded-xl p-4`}>
                <p className={`text-[10px] font-bold ${c.warningTxt} uppercase mb-1`}>🤔 Counterargument</p>
                <p className={`text-xs ${c.textSecondary}`}>{results.counterargument}</p>
              </div>
            )}
            {results.rabbit_hole && (
              <div className={`${c.quoteBg} border ${c.border} rounded-xl p-4`}>
                <p className={`text-[10px] font-bold ${c.accentTxt} uppercase mb-1`}>🐇 Rabbit Hole</p>
                <p className={`text-xs ${c.textSecondary}`}>{results.rabbit_hole}</p>
              </div>
            )}
          </div>

          {/* Actions */}
          <div className="flex gap-2">
            <button onClick={() => runGenerate(true)} disabled={loading}
              className={`flex-1 ${c.btnSecondary} font-bold py-3 rounded-lg min-h-[44px]`}>
              <span className="mr-1">{tool?.icon ?? '🧵'}</span>Different Theory
            </button>
            <button onClick={() => { setResults(null); setGradeResults(null); setMyTheory(''); setError(''); setTitle(''); setMediaType('movie'); setDirection('wild'); setSavedTheories([]); }}
              className={`px-5 py-3 rounded-lg font-bold text-sm min-h-[44px] ${c.btnSecondary}`}>
              🔄 Reset
            </button>
          </div>

          <p className={`text-xs ${c.textMuted} text-center`}>
            Want to stress-test the idea?{' '}
            <a href="/BeliefStressTest" className={`text-xs ${linkStyle}`}>🧪 Belief Stress Test</a>{' '}
            puts any claim through the wringer.
          </p>
        </div>
      )}

      {/* Grade YOUR theory — separate card, clearly distinct from AI output */}
      {results && (
        <div className={`${c.card} border ${c.border} rounded-xl shadow-sm p-5`}>
          <div className="pb-3 border-b border-zinc-500 mb-4">
            <h3 className={`text-base font-bold ${c.text}`}>📝 Now you try</h3>
            <p className={`text-xs ${c.textSecondary}`}>Have your own theory about {title || 'this'}? Write it below and the professor will grade it for plausibility, creativity, and evidence quality.</p>
          </div>
          <div className="space-y-3">
            <textarea ref={gradeTextareaRef} value={myTheory} onChange={e => setMyTheory(e.target.value)}
              placeholder="My theory is that..."
              rows={4} className={`w-full px-3 py-2.5 border rounded-lg text-sm ${c.input} outline-none focus:ring-2 resize-y`} />
            <button onClick={runGrade} disabled={!myTheory.trim() || loading}
              className={`w-full ${c.btnPrimary} disabled:opacity-40 font-bold py-2.5 rounded-lg flex items-center justify-center gap-2 min-h-[44px]`}>
              {loading
                ? <><span className="animate-spin inline-block">{tool?.icon ?? '🧵'}</span> Grading…</>
                : <><span className="mr-1">{tool?.icon ?? '🧵'}</span>Grade My Theory</>}
            </button>
          </div>

          {/* Grade results — inline in the same card, directly below the submit */}
          {gradeResults && (
            <div className="mt-5 space-y-3 pt-4 border-t border-zinc-500">
              <div className="text-center">
                <p className={`text-4xl font-black ${c.text} mb-1`}>{gradeResults.grade}</p>
                {gradeResults.grade_title && <p className={`text-sm ${c.accentTxt} font-bold`}>{gradeResults.grade_title}</p>}
                <div className="flex justify-center gap-4 mt-2">
                  {gradeResults.plausibility && (
                    <div><span className={`text-lg font-black ${c.accentTxt}`}>{gradeResults.plausibility}</span><span className={`text-xs ${c.textMuted}`}>/10</span><span className={`text-[9px] ${c.textMuted} block`}>plausible</span></div>
                  )}
                  {gradeResults.creativity && (
                    <div><span className={`text-lg font-black ${c.accentTxt}`}>{gradeResults.creativity}</span><span className={`text-xs ${c.textMuted}`}>/10</span><span className={`text-[9px] ${c.textMuted} block`}>creative</span></div>
                  )}
                </div>
                {gradeResults.evidence_quality && <p className={`text-xs ${c.textMuted} mt-1`}>Evidence: {gradeResults.evidence_quality}</p>}
              </div>

              <div className="grid grid-cols-1 sm:grid-cols-2 gap-2">
                {gradeResults.strengths?.length > 0 && (
                  <div className={`${c.success} border rounded-xl p-3`}>
                    <p className="text-[10px] font-bold uppercase mb-1.5">✅ Strengths</p>
                    {gradeResults.strengths.map((s, i) => <p key={i} className="text-xs mb-1">• {s}</p>)}
                  </div>
                )}
                {gradeResults.weaknesses?.length > 0 && (
                  <div className={`${c.danger} border rounded-xl p-3`}>
                    <p className="text-[10px] font-bold uppercase mb-1.5">❌ Weaknesses</p>
                    {gradeResults.weaknesses.map((w, i) => <p key={i} className="text-xs mb-1">• {w}</p>)}
                  </div>
                )}
              </div>

              {gradeResults.professor_notes && (
                <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
                  <p className={`text-[10px] font-bold ${c.accentTxt} uppercase mb-1`}>🎓 Professor's Notes</p>
                  <p className={`text-sm ${c.textSecondary} italic`}>{gradeResults.professor_notes}</p>
                </div>
              )}

              {gradeResults.improvement_suggestion && (
                <div className={`${c.infoBox} border rounded-xl p-3`}>
                  <p className="text-[10px] font-bold uppercase mb-1">💡 How to improve it</p>
                  <p className="text-xs">{gradeResults.improvement_suggestion}</p>
                </div>
              )}

              {gradeResults.would_reddit_upvote && (
                <p className={`text-xs ${c.textMuted} italic text-center`}>📊 {gradeResults.would_reddit_upvote}</p>
              )}

              <button onClick={() => { setGradeResults(null); setMyTheory(''); }}
                className={`w-full text-xs ${c.textMuted} py-1`}>
                ✕ Clear grade
              </button>
            </div>
          )}
        </div>
      )}

      {error && (
        <div className={`${c.danger} border rounded-lg p-4 flex items-start gap-3`}>
          <span>⚠️</span><p className="text-sm">{error}</p>
        </div>
      )}
    </div>
  );
};


FanTheory.displayName = 'FanTheory';
export default FanTheory;
