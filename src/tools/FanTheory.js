import React, { useState, useCallback, useEffect } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { CopyBtn, ActionBar } from '../components/ActionButtons';
import { usePersistentState } from '../hooks/usePersistentState';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

// ════════════════════════════════════════════════════════════
// CONSTANTS
// ════════════════════════════════════════════════════════════
const MEDIA_TYPES = [
  { value: 'movie', label: 'Movie', icon: '🎬' },
  { value: 'show', label: 'TV Show', icon: '📺' },
  { value: 'book', label: 'Book', icon: '📖' },
  { value: 'game', label: 'Game', icon: '🎮' },
];

const DIRECTIONS = [
  { value: 'wild', label: 'Surprise Me', icon: '🎲', desc: 'Most creative angle' },
  { value: 'villain', label: 'Secret Villain', icon: '😈', desc: 'Hero is actually evil' },
  { value: 'connected', label: 'Shared Universe', icon: '🔗', desc: 'Connected to another franchise' },
  { value: 'timeline', label: 'Timeline Twist', icon: '⏰', desc: 'Events are out of order' },
  { value: 'alive', label: 'Dead or Alive', icon: '👻', desc: 'Dead character is alive, or vice versa' },
  { value: 'simulation', label: 'It\'s a Simulation', icon: '🖥️', desc: 'None of it is real' },
];

const EVIDENCE_STYLES = {
  'COMPELLING': { icon: '🔥', color: 'accentText' },
  'SUSPICIOUS': { icon: '🧐', color: 'warningText' },
  'A STRETCH': { icon: '🤸', color: 'textMuted' },
  'PURE DELUSION': { icon: '🤡', color: 'dangerText' },
};

// ════════════════════════════════════════════════════════════
// COMPONENT
// ════════════════════════════════════════════════════════════
const FanTheory = ({ tool }) => {
  const { callToolEndpoint, loading } = useClaudeAPI();
  const { isDark } = useTheme();



  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';

  const c = {
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:         isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20' : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-500' : 'text-gray-400',
    labelText:     isDark ? 'text-zinc-200' : 'text-gray-700',
    accentTxt:     isDark ? 'text-cyan-400' : 'text-cyan-600',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white' : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200' : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200' : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200' : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200' : 'bg-red-50 border-red-200 text-red-800',
    infoBox:       isDark ? 'bg-sky-900/20 border-sky-700 text-sky-200' : 'bg-sky-50 border-sky-200 text-sky-800',
    successBox:    isDark ? 'bg-emerald-900/20 border-emerald-700' : 'bg-emerald-50 border-emerald-300',
    successTxt:    isDark ? 'text-emerald-300' : 'text-emerald-800',
    warningBox:    isDark ? 'bg-amber-900/20 border-amber-700' : 'bg-amber-50 border-amber-300',
    warningTxt:    isDark ? 'text-amber-300' : 'text-amber-800',
    pillActive:    isDark ? 'border-cyan-500 bg-cyan-900/30 text-cyan-200' : 'border-cyan-600 bg-cyan-100 text-cyan-900',
    pillInactive:  isDark ? 'border-zinc-600 text-zinc-400 hover:border-zinc-500' : 'border-gray-300 text-gray-500 hover:border-gray-400',
  };

  // ── State ──
  const [view, setView] = useState('generate'); // generate | grade
  const [title, setTitle] = usePersistentState('fantheory-title', '');
  const [history, setHistory] = usePersistentState('fantheory-history', []);
  const [mediaType, setMediaType] = useState('movie');
  const [direction, setDirection] = useState('wild');
  const [results, setResults] = usePersistentState('fantheory-results', null);
  const [myTheory, setMyTheory] = useState('');
  const [gradeResults, setGradeResults] = useState(null);
  const resultsRef = React.useRef(null);
  const [error, setError] = useState('');

  // ── API: Generate theory ──
  const runGenerate = useCallback(async () => {
    if (!title.trim()) return;
    setError(''); setResults(null);
    try {
      const data = await callToolEndpoint('fan-theory', { title: title.trim(), mediaType, direction });
      setResults(data);
      setHistory(prev => [{ id: Date.now(), date: new Date().toISOString(), preview: '' }, ...prev].slice(0, 6));
      setTimeout(() => resultsRef.current?.scrollIntoView({ behavior: 'smooth' }), 100);
    } catch (err) { setError(err.message || 'Theory failed'); }
  }, [title, mediaType, direction, callToolEndpoint]);

  // ── API: Grade theory ──
  const runGrade = useCallback(async () => {
    if (!myTheory.trim()) return;
    setError(''); setGradeResults(null);
    try {
      const data = await callToolEndpoint('fan-theory/grade', { title: title.trim(), theory: myTheory.trim() });
      setGradeResults(data);
    } catch (err) { setError(err.message || 'Grading failed'); }
  }, [title, myTheory, callToolEndpoint]);

  // ── Copy builder ──
  const buildFullText = useCallback(() => {
    if (!results) return '';
    const lines = [`🧵 FAN THEORY: ${results.theory_name || ''}`, ''];
    if (results.one_line) lines.push(`💡 ${results.one_line}`, '');
    if (results.the_theory) lines.push(results.the_theory, '');
    if (results.evidence?.length) {
      lines.push('EVIDENCE:');
      results.evidence.forEach(e => lines.push(`  ${e.strength === 'COMPELLING' ? '🔥' : '🧐'} ${e.detail} → ${e.spin}`));
      lines.push('');
    }
    if (results.the_smoking_gun) lines.push(`🔫 Smoking Gun: ${results.the_smoking_gun}`);
    if (results.plausibility) lines.push(`\nPlausibility: ${results.plausibility}/10`);
    lines.push('\n— Generated by DeftBrain · deftbrain.com');
    return lines.join('\n');
  }, [results]);

  const evStyle = (str) => EVIDENCE_STYLES[str] || EVIDENCE_STYLES['SUSPICIOUS'];

  // ════════════════════════════════════════════════════════════
  // RENDER
  // ════════════════════════════════════════════════════════════
  return (
    <div className={`space-y-4 ${c.text}`}>
      {/* Nav */}
      <div className="flex gap-2">
        {[
          { key: 'generate', label: '🧵 Generate Theory' },
          { key: 'grade', label: '📝 Grade My Theory' },
        ].map(t => (
          <button key={t.key} onClick={() => setView(t.key)}
            className={`px-4 py-2 rounded-lg text-xs font-bold border transition-colors min-h-[36px] ${
              view === t.key ? c.pillActive : c.pillInactive}`}>
            {t.label}
          </button>
        ))}
      </div>

      {/* Generate view */}
      {view === 'generate' && (
        <>
          <div className={`${c.card} border rounded-xl p-5`}>
            <div className={`mb-4 pb-3 border-b ${isDark ? 'border-zinc-700' : 'border-gray-200'}`}>
              <h2 className={`text-xl font-bold ${c.text}`}><span className="mr-2">{tool?.icon ?? '🧵'}</span>{tool?.title || 'Fan Theory Generator'}</h2>
              <p className={`text-sm ${c.textSecondary}`}>Name anything — I'll generate a wild but defensible fan theory</p>
            </div>

            <div className="grid grid-cols-1 sm:grid-cols-2 gap-3 mb-3">
              <div>
                <label className={`text-xs font-bold ${c.textSecondary} block mb-1.5`}>Title</label>
                <input type="text" value={title} onChange={e => setTitle(e.target.value)} onKeyDown={e => { if (e.key === 'Enter' && title.trim()) runGenerate(); }}
                  placeholder="Movie, show, book, or game..."
                  className={`w-full px-3 py-2.5 border rounded-lg text-sm ${c.input} outline-none focus:ring-2`} />
              </div>
              <div>
                <label className={`text-xs font-bold ${c.textSecondary} uppercase block mb-1.5`}>Type</label>
                <div className="flex gap-1.5 flex-wrap">
                  {MEDIA_TYPES.map(m => (
                    <button key={m.value} onClick={() => setMediaType(m.value)}
                      className={`px-2.5 py-1.5 rounded-lg text-[11px] font-bold border min-h-[30px] ${
                        mediaType === m.value ? c.pillActive : c.pillInactive}`}>
                      {m.icon} {m.label}
                    </button>
                  ))}
                </div>
              </div>
            </div>

            <div className="mb-5">
              <label className={`text-xs font-bold ${c.textSecondary} uppercase block mb-2`}>Theory direction</label>
              <div className="grid grid-cols-2 sm:grid-cols-3 gap-2">
                {DIRECTIONS.map(d => (
                  <button key={d.value} onClick={() => setDirection(d.value)}
                    className={`py-2 px-3 rounded-xl border text-left min-h-[48px] transition-colors ${
                      direction === d.value ? c.pillActive : c.pillInactive}`}>
                    <span className="text-sm">{d.icon} {d.label}</span>
                    <span className={`text-[9px] block ${direction === d.value ? 'opacity-80' : c.textMuteded}`}>{d.desc}</span>
                  </button>
                ))}
              </div>
            </div>

            <button onClick={runGenerate} disabled={!title.trim() || loading}
              className={`w-full ${c.btnPrimary} disabled:opacity-40 font-bold py-3 rounded-lg flex items-center justify-center gap-2 min-h-[48px]`}>
              {loading ? <><span className="animate-spin inline-block">{tool?.icon ?? '🧵'}</span> Theorizing...</> : <><span>🧵</span> Generate Theory</>}
            </button>
          </div>

          {/* Results */}
          {results && (<div ref={resultsRef}>
            <div className="space-y-4">
              <div className="flex justify-end"><ActionBar content={buildFullText()} title="Fan Theory" /></div>

              {/* Theory header */}
              <div className={`${c.warningBox} border-2 rounded-xl p-5 text-center`}>
                <span className="text-3xl block mb-2">🧵</span>
                <p className={`text-lg font-black ${c.text} mb-2`}>{results.theory_name}</p>
                {results.one_line && <p className={`text-sm ${c.textSecondary} italic`}>"{results.one_line}"</p>}
                <div className="flex justify-center gap-4 mt-3">
                  {results.plausibility && (
                    <div><span className={`text-xl font-black ${c.accentTxt}`}>{results.plausibility}</span><span className={`text-xs ${c.textMuteded}`}>/10 plausible</span></div>
                  )}
                  {results.mind_blown_factor && (
                    <div><span className={`text-xl font-black ${c.accentTxt}`}>{results.mind_blown_factor}</span><span className={`text-xs ${c.textMuteded}`}>/10 mind-blown</span></div>
                  )}
                </div>
              </div>

              {/* The theory */}
              {results.the_theory && (
                <div className={`${c.card} border rounded-xl p-5`}>
                  <h3 className={`text-sm font-bold ${c.text} mb-2`}>🕵️ The Theory</h3>
                  <p className={`text-sm ${c.textSecondary} leading-relaxed whitespace-pre-line`}>{results.the_theory}</p>
                </div>
              )}

              {/* Evidence */}
              {results.evidence?.length > 0 && (
                <div className={`${c.card} border rounded-xl p-4`}>
                  <h3 className={`text-sm font-bold ${c.text} mb-3`}>📎 Evidence</h3>
                  <div className="space-y-2">
                    {results.evidence.map((e, i) => {
                      const es = evStyle(e.strength);
                      return (
                        <div key={i} className={`${c.quoteBg} rounded-lg p-3`}>
                          <div className="flex items-start justify-between gap-2 mb-1">
                            <p className={`text-xs font-medium ${c.text}`}>{es.icon} {e.detail}</p>
                            <span className={`text-[8px] font-black ${c[es.color]} whitespace-nowrap`}>{e.strength}</span>
                          </div>
                          <p className={`text-[11px] ${c.textMuteded}`}>→ {e.spin}</p>
                        </div>
                      );
                    })}
                  </div>
                </div>
              )}

              {/* Smoking gun */}
              {results.the_smoking_gun && (
                <div className={`${c.warning} border-2 rounded-xl p-4`}>
                  <p className={`text-[10px] font-bold uppercase mb-1`}>🔫 The Smoking Gun</p>
                  <p className="text-sm font-bold">{results.the_smoking_gun}</p>
                </div>
              )}

              {/* Counterargument + Rabbit hole */}
              <div className="grid grid-cols-1 sm:grid-cols-2 gap-3">
                {results.counterargument && (
                  <div className={`${c.quoteBg} border ${isDark ? 'border-zinc-700' : 'border-gray-200'} rounded-xl p-4`}>
                    <p className={`text-[10px] font-bold ${c.warningTxt} uppercase mb-1`}>🤔 Counterargument</p>
                    <p className={`text-xs ${c.textSecondary}`}>{results.counterargument}</p>
                  </div>
                )}
                {results.rabbit_hole && (
                  <div className={`${c.quoteBg} border ${isDark ? 'border-zinc-700' : 'border-gray-200'} rounded-xl p-4`}>
                    <p className={`text-[10px] font-bold ${c.accentTxt} uppercase mb-1`}>🐇 Rabbit Hole</p>
                    <p className={`text-xs ${c.textSecondary}`}>{results.rabbit_hole}</p>
                  </div>
                )}
              </div>

              <div className="flex gap-2">
                <button onClick={runGenerate} disabled={loading} className={`flex-1 ${c.btnSecondaryondary} font-bold py-3 rounded-lg min-h-[44px]`}>
                  🧵 Different Theory
                </button>
              </div>
            </div>

            {/* Cross-reference */}
            <p className={`text-xs ${c.textMuteded} text-center mt-4`}>
              AI-generated — the best theories come from people who know the source material deeply.{' '}
              Want to stress-test the idea?{' '}
              <a href="/BeliefStressTest" target="_blank" rel="noopener noreferrer" className={linkStyle}>Belief Stress Test</a>{' '}
              puts any claim through the wringer.
            </p>
          </div>)}
        </>
      )}

      {/* Grade view */}
      {view === 'grade' && (
        <>
          <div className={`${c.card} border rounded-xl p-5`}>
            <h2 className={`text-lg font-bold mb-1`}><span className="mr-2">📝</span>Grade My Theory</h2>
            <p className={`text-sm ${c.textSecondary} mb-4`}>Share your fan theory — the professor will grade it</p>

            <div className="mb-3">
              <label className={`text-xs font-bold ${c.textSecondary} block mb-1.5`}>About what?</label>
              <input type="text" value={title} onChange={e => setTitle(e.target.value)} onKeyDown={e => { if (e.key === 'Enter' && title.trim()) runGenerate(); }}
                placeholder="Movie, show, book, or game..."
                className={`w-full px-3 py-2.5 border rounded-lg text-sm ${c.input} outline-none focus:ring-2`} />
            </div>
            <div className="mb-4">
              <label className={`text-xs font-bold ${c.textSecondary} block mb-1.5`}>Your theory</label>
              <textarea value={myTheory} onChange={e => setMyTheory(e.target.value)} onKeyDown={e => { if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && title.trim()) runGrade(); }}
                placeholder="Share your theory... the wilder the better"
                rows={5} className={`w-full px-3 py-2.5 border rounded-lg text-sm ${c.input} outline-none focus:ring-2 resize-y`} />
            </div>

            <button onClick={runGrade} disabled={!myTheory.trim() || loading}
              className={`w-full ${c.btnPrimary} disabled:opacity-40 font-bold py-3 rounded-lg flex items-center justify-center gap-2 min-h-[48px]`}>
              {loading ? <><span className="animate-spin inline-block">{tool?.icon ?? '🧵'}</span> Grading...</> : <><span>📝</span> Grade It</>}
            </button>
          </div>

          {gradeResults && (
            <div className="space-y-4">
              {/* Grade */}
              <div className={`${c.warningBox} border-2 rounded-xl p-5 text-center`}>
                <p className={`text-4xl font-black ${c.text} mb-1`}>{gradeResults.grade}</p>
                {gradeResults.grade_title && <p className={`text-sm ${c.accentTxt} font-bold`}>{gradeResults.grade_title}</p>}
                <div className="flex justify-center gap-4 mt-3">
                  {gradeResults.plausibility && (
                    <div><span className={`text-lg font-black ${c.accentTxt}`}>{gradeResults.plausibility}</span><span className={`text-xs ${c.textMuteded}`}>/10</span><span className={`text-[9px] ${c.textMuteded} block`}>plausible</span></div>
                  )}
                  {gradeResults.creativity && (
                    <div><span className={`text-lg font-black ${c.accentTxt}`}>{gradeResults.creativity}</span><span className={`text-xs ${c.textMuteded}`}>/10</span><span className={`text-[9px] ${c.textMuteded} block`}>creative</span></div>
                  )}
                </div>
                {gradeResults.evidence_quality && (
                  <p className={`text-xs ${c.textMuteded} mt-2`}>Evidence quality: {gradeResults.evidence_quality}</p>
                )}
              </div>

              {/* Strengths + Weaknesses */}
              <div className="grid grid-cols-1 sm:grid-cols-2 gap-3">
                {gradeResults.strengths?.length > 0 && (
                  <div className={`${c.success} border rounded-xl p-4`}>
                    <p className={`text-[10px] font-bold uppercase mb-2`}>✅ Strengths</p>
                    {gradeResults.strengths.map((s, i) => <p key={i} className="text-xs mb-1">• {s}</p>)}
                  </div>
                )}
                {gradeResults.weaknesses?.length > 0 && (
                  <div className={`${c.danger} border rounded-xl p-4`}>
                    <p className={`text-[10px] font-bold uppercase mb-2`}>❌ Weaknesses</p>
                    {gradeResults.weaknesses.map((w, i) => <p key={i} className="text-xs mb-1">• {w}</p>)}
                  </div>
                )}
              </div>

              {gradeResults.professor_notes && (
                <div className={`${c.card} border rounded-xl p-4`}>
                  <p className={`text-[10px] font-bold ${c.accentTxt} uppercase mb-1`}>🎓 Professor's Notes</p>
                  <p className={`text-sm ${c.textSecondary} italic`}>{gradeResults.professor_notes}</p>
                </div>
              )}

              {gradeResults.would_reddit_upvote && (
                <div className={`${c.quoteBg} border ${isDark ? 'border-zinc-700' : 'border-gray-200'} rounded-xl p-4`}>
                  <p className={`text-[10px] font-bold ${c.textMuteded} uppercase mb-1`}>📊 Reddit Prediction</p>
                  <p className={`text-xs ${c.textSecondary}`}>{gradeResults.would_reddit_upvote}</p>
                </div>
              )}

              <CopyBtn content={`📝 Fan Theory Grade: ${gradeResults.grade} "${gradeResults.grade_title}"\n\n${gradeResults.professor_notes || ''}\n\nPlausibility: ${gradeResults.plausibility}/10 | Creativity: ${gradeResults.creativity}/10\n\n— Generated by DeftBrain · deftbrain.com`} label="Copy Report Card" />
            </div>
          )}
        </>
      )}

      {error && (
        <div className={`${c.danger} border rounded-lg p-4 flex items-start gap-3`}>
          <span>⚠️</span><p className="text-sm">{error}</p>
        </div>
      )}
    </div>
  );
};

FanTheory.displayName = 'FanTheory';
export default FanTheory;
