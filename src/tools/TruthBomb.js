import React, { useState, useEffect, useCallback, useRef } from 'react';
import { usePersistentState } from '../hooks/usePersistentState';
import { useTheme } from '../hooks/useTheme';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useRegisterActions } from '../components/ActionBarContext';
import { useTranslation } from '../i18n/useTranslation';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';


const WHO_OPTIONS = [
  { value: 'myself', key: 'tb_who_myself' },
  { value: 'a partner', key: 'tb_who_partner' },
  { value: 'a friend', key: 'tb_who_friend' },
  { value: 'a family member', key: 'tb_who_family' },
  { value: 'a colleague', key: 'tb_who_colleague' },
  { value: 'a boss', key: 'tb_who_boss' },
  { value: 'a client', key: 'tb_who_client' },
  { value: 'no one yet — just myself', key: 'tb_who_noone' },
];

// Fixed: replaced banned 'sky' with 'cyan'; dot uses theme-aware className, not raw hex
const DIRECTNESS_COLORS = [
  { bg: (d) => d ? 'bg-amber-900/20 border-amber-700' : 'bg-amber-50 border-amber-300', text: (d) => d ? 'text-amber-300' : 'text-amber-700', dot: (d) => d ? 'bg-amber-400' : 'bg-amber-500' },
  { bg: (d) => d ? 'bg-cyan-900/20 border-cyan-700'  : 'bg-cyan-50 border-cyan-200',   text: (d) => d ? 'text-cyan-300'  : 'text-cyan-700',  dot: (d) => d ? 'bg-cyan-400'  : 'bg-cyan-500'  },
  { bg: (d) => d ? 'bg-red-900/20 border-red-700'    : 'bg-red-50 border-red-200',     text: (d) => d ? 'text-red-300'   : 'text-red-700',   dot: (d) => d ? 'bg-red-400'   : 'bg-red-500'   },
];

const TruthBomb = ({ tool }) => {
  const { isDark } = useTheme();
  const { callToolEndpoint, loading, userLocale, userCurrency, userRegion } = useClaudeAPI();
  const { t } = useTranslation();

  const c = {
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:         isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20' : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-400' : 'text-gray-500',
    required:      isDark ? 'text-amber-400' : 'text-amber-700',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white' : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200' : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200' : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200' : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200' : 'bg-red-50 border-red-200 text-red-800',
    hook:          isDark ? 'bg-cyan-900/20 border-cyan-700 text-cyan-200' : 'bg-cyan-50 border-cyan-200 text-cyan-800',
    labelText:     isDark ? 'text-zinc-300' : 'text-gray-700',
  };
  c.textMuteded = c.textMuted;
  c.label = c.labelText;

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-700 hover:text-cyan-800 underline underline-offset-2';

  // All useState / useRef before any useEffect
  const [whoItsAbout, setWhoItsAbout] = useState('');
  const [whyNotSaying, setWhyNotSaying] = useState('');
  const [relationshipContext, setRelationshipContext] = useState('');
  const [error, setError] = useState('');
  const [expanded, setExpanded] = useState({ timing: false, permission: false });
  const [theUnsaidThing, setTheUnsaidThing] = usePersistentState('truthbomb-unsaid', '');
  const [results, setResults] = usePersistentState('truthbomb-result', null);
  const [sessionHistory, setSessionHistory] = usePersistentState('truthbomb-history', []);
  const resultsRef = useRef(null);

  const toggle = useCallback((k) => setExpanded(p => ({ ...p, [k]: !p[k] })), []);

  const handleSubmit = useCallback(async () => {
    if (!theUnsaidThing.trim()) return;
    setError(''); setResults(null);
    try {
      const data = await callToolEndpoint('truth-bomb', {
        theUnsaidThing: theUnsaidThing.trim(),
        whoItsAbout: whoItsAbout.trim() || undefined,
        whyNotSaying: whyNotSaying.trim() || undefined,
        relationshipContext: relationshipContext.trim() || undefined,
        userLocale, userCurrency, userRegion,
      });
      setResults(data);
      setSessionHistory(prev => [{
        id: Date.now(), date: new Date().toISOString(),
        // PF-25 exception: 40-char preview-text truncation; session history is capped at 6.
        preview: theUnsaidThing.trim().slice(0, 40),
        result: data,
      }, ...prev].slice(0, 6));
    } catch (e) { setError(e.message || t('tb_error')); }
  }, [theUnsaidThing, whoItsAbout, whyNotSaying, relationshipContext, callToolEndpoint, setError, setResults, setSessionHistory, userLocale, userCurrency, userRegion, t]);

  // Cmd/Ctrl+Enter submits from anywhere
  useEffect(() => {
    const handler = (e) => {
      const tag = document.activeElement?.tagName;
      if (tag === 'SELECT') return;
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && !loading) handleSubmit();
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [loading, handleSubmit]);

  const loadExample = useCallback(() => {
    setWhoItsAbout(t('tb_ex_who'));
    setTheUnsaidThing(t('tb_ex_thing'));
    setWhyNotSaying(t('tb_ex_why'));
    setRelationshipContext(t('tb_ex_context'));
    setResults(null);
  }, [setTheUnsaidThing, setResults, t]);

  const buildText = useCallback(() => {
    if (!results) return '';
    let txt = `💣 ${t('tb_copy_header')}\n\n${t('tb_copy_thing')} "${theUnsaidThing}"\n\n`;
    if (results?.the_thing_examined) {
      txt += `${t('tb_copy_really_about')}\n${results?.the_thing_examined?.what_its_really_about}\n\n`;
      txt += `${t('tb_copy_hiding_costs')}\n${results?.the_thing_examined?.what_hiding_it_costs}\n\n`;
    }
    txt += `${t('tb_copy_three_ways')}\n\n`;
    results?.three_ways_to_say_it?.forEach(v => {
      txt += `${v.version} (${t('tb_copy_directness')} ${v.directness}/3)\n"${v.the_words}"\n${v.what_it_accomplishes}\n\n`;
    });
    return txt + BRAND;
  }, [results, theUnsaidThing, t]);

  // ─── Register export content ───
  useRegisterActions(results ? buildText() : '', tool?.title);

  // ─── Scroll to results ───
  useEffect(() => {
    if (!results) return;
    const timer = setTimeout(() => resultsRef.current?.scrollIntoView({ behavior: 'smooth', block: 'start' }), 100);
    return () => clearTimeout(timer);
  }, [results]);

  const handleReset = useCallback(() => {
    setResults(null); setTheUnsaidThing(''); setWhoItsAbout('');
    setWhyNotSaying(''); setRelationshipContext(''); setError('');
  }, [setResults, setTheUnsaidThing, setError]);

  return (
    <div className={`space-y-4 ${c.text}`}>

      {/* Input card */}
      <div className={`${c.card} border ${c.border} rounded-xl p-6`}>
        <div className={`mb-5 pb-4 border-b ${c.border}`}>
          <div className="flex items-start justify-between gap-3">
            <div>
              <h2 className={`text-2xl font-bold ${c.text}`}>
                <span className="mr-2">{tool?.icon ?? '💣'}</span>{tool?.title ?? t('tb_title')}
              </h2>
              <p className={`text-sm ${c.textSecondary} mt-1`}>{tool?.tagline ?? t('tb_tagline')}</p>
              <button onClick={loadExample} disabled={loading} style={{ backgroundColor: (tool?.headerColor ?? '#888888') + '80' }} className={`mt-1.5 px-2.5 py-0.5 rounded-full text-xs font-medium border disabled:opacity-40 ${isDark ? 'text-white border-white/40' : 'text-gray-800 border-transparent'}`}>{t('try_example')}</button>
            </div>
            {(results || theUnsaidThing.trim()) && (
              <button onClick={handleReset} className={`${c.btnSecondary} px-3 py-1.5 rounded-lg text-xs font-bold flex-shrink-0`}>
                ↺ {t('start_over')}
              </button>
            )}
          </div>
        </div>

        {/* Opening hook */}
        <div className={`p-3 rounded-xl border text-sm mb-4 ${c.hook}`}>
          {t('tb_hook')}
        </div>

        <div className="space-y-4">
          <div>
            <label className={`block text-sm font-semibold mb-1.5 ${c.text}`}>
              {t('tb_unsaid_label')} <span className={c.required}>*</span>
            </label>
            <textarea value={theUnsaidThing} onChange={e => setTheUnsaidThing(e.target.value)}
              onKeyDown={e => { if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && !loading && theUnsaidThing.trim()) { e.preventDefault(); handleSubmit(); } }}
              placeholder={t('tb_unsaid_ph')}
              rows={4} maxLength={600}
              className={`w-full px-4 py-3 rounded-xl border text-sm resize-none ${c.input}`} />
          </div>

          <div className="grid grid-cols-1 sm:grid-cols-2 gap-3">
            <div>
              <label className={`block text-sm font-semibold mb-1.5 ${c.text}`}>{t('tb_who_label')} <span className={`font-normal ${c.textMuted}`}>({t('optional')})</span></label>
              <div className="flex flex-wrap gap-1.5 mb-2">
                {WHO_OPTIONS.map(w => (
                  <button key={w.value} onClick={() => setWhoItsAbout(whoItsAbout === w.value ? '' : w.value)}
                    className={`px-2.5 py-1 rounded-lg text-xs border transition-all ${
                      whoItsAbout === w.value ? c.btnPrimary : c.btnSecondary
                    }`}>
                    {t(w.key)}
                  </button>
                ))}
              </div>
            </div>
            <div>
              <label className={`block text-sm font-semibold mb-1.5 ${c.text}`}>{t('tb_why_label')} <span className={`font-normal ${c.textMuted}`}>({t('optional')})</span></label>
              <textarea value={whyNotSaying} onChange={e => setWhyNotSaying(e.target.value)}
                placeholder={t('tb_why_ph')}
                rows={3} maxLength={300}
                className={`w-full px-4 py-3 rounded-xl border text-sm resize-none ${c.input}`} />
            </div>
          </div>

          <div>
            <label className={`block text-sm font-semibold mb-1.5 ${c.text}`}>{t('tb_context_label')} <span className={`font-normal ${c.textMuted}`}>({t('optional')})</span></label>
            <input type="text" value={relationshipContext} onChange={e => setRelationshipContext(e.target.value)}
              placeholder={t('tb_context_ph')}
              className={`w-full px-4 py-3 rounded-xl border text-sm ${c.input}`} />
          </div>

          {error && <div className={`p-3 rounded-xl border text-sm ${c.danger}`}><span className="mr-1">⚠️</span>{error}</div>}

          <button onClick={handleSubmit} disabled={loading || !theUnsaidThing.trim()}
          className={`w-full py-3 rounded-xl font-bold disabled:opacity-40 min-h-[48px] flex items-center justify-center gap-2 ${c.btnPrimary}`}>
          {loading
            ? <><span className="inline-block animate-spin">{tool?.icon ?? '💣'}</span> {t('tb_processing')}</>
            : <><span>{tool?.icon ?? '💣'}</span> {t('tb_submit')}</>}
          </button>
          <p className={`text-xs text-center ${c.textMuted}`}>{t('tb_disclaimer')}</p>
          <p className={`text-xs text-center ${c.textMuted} mt-1`}>
            {t('tb_say_it_q')} <a href="/DifficultTalkCoach" className={linkStyle}>{t('tb_difficulttalk')}</a> {t('tb_say_it_after')}
          </p>
        </div>
      </div>

      {/* Results */}
      {results && (
        <div className="space-y-4">
          <div ref={resultsRef} data-results-anchor />
          {/* What it's really about */}
          {results?.the_thing_examined && (
            <div className={`rounded-xl border p-5 space-y-3 ${c.card} ${c.border}`}>
              <p className={`text-xs font-black uppercase tracking-widest ${c.textMuted}`}>🔍 {t('tb_really_about')}</p>
              {results?.the_thing_examined?.what_its_really_about && (
                <p className={`text-sm leading-relaxed ${c.textSecondary}`}>{results?.the_thing_examined?.what_its_really_about}</p>
              )}
              {results?.the_thing_examined?.why_its_hard_to_say && (
                <p className={`text-xs ${c.textMuted}`}><span className="font-semibold">{t('tb_why_hard')}</span> {results?.the_thing_examined?.why_its_hard_to_say}</p>
              )}
              {results?.the_thing_examined?.what_hiding_it_costs && (
                <div className={`p-3 rounded-xl border ${c.danger}`}>
                  <p className="text-xs font-bold uppercase tracking-wide mb-1">{t('tb_hiding_costs')}</p>
                  <p className="text-sm">{results?.the_thing_examined?.what_hiding_it_costs}</p>
                </div>
              )}
            </div>
          )}

          {/* What would actually happen */}
          {results?.what_would_actually_happen && (
            <div className={`rounded-xl border p-5 space-y-3 ${c.success}`}>
              <p className="text-xs font-black uppercase tracking-widest">🌿 {t('tb_would_happen')}</p>
              {results?.what_would_actually_happen?.most_likely_scenario && (
                <p className="text-sm">{results?.what_would_actually_happen?.most_likely_scenario}</p>
              )}
              {results?.what_would_actually_happen?.the_fear_vs_reality_gap && (
                <p className="text-xs"><span className="font-semibold">{t('tb_the_gap')}</span> {results?.what_would_actually_happen?.the_fear_vs_reality_gap}</p>
              )}
              {results?.what_would_actually_happen?.what_it_would_change && (
                <p className="text-xs"><span className="font-semibold">{t('tb_what_changes')}</span> {results?.what_would_actually_happen?.what_it_would_change}</p>
              )}
              {results?.what_would_actually_happen?.what_wouldnt_change && (
                <p className="text-xs"><span className="font-semibold">{t('tb_wont_change')}</span> {results?.what_would_actually_happen?.what_wouldnt_change}</p>
              )}
            </div>
          )}

          {/* Three ways to say it */}
          {results?.three_ways_to_say_it?.length > 0 && (
            <div className="space-y-3">
              <p className={`text-xs font-black uppercase tracking-widest px-1 ${c.textMuted}`}>🗣️ {t('tb_three_ways')}</p>
              {results?.three_ways_to_say_it?.map((v, i) => {
                const dcfg = DIRECTNESS_COLORS[i] || DIRECTNESS_COLORS[0];
                return (
                  <div key={i} className={`rounded-xl border p-5 space-y-3 ${dcfg.bg(isDark)}`}>
                    <div className="flex items-center justify-between">
                      <p className={`text-xs font-black uppercase tracking-widest ${dcfg.text(isDark)}`}>{v.version}</p>
                      <div className="flex gap-1">
                        {[1, 2, 3].map(n => (
                          <div key={n} className={`w-2 h-2 rounded-full ${dcfg.dot(isDark)} ${n <= v.directness ? 'opacity-100' : 'opacity-20'}`} />
                        ))}
                      </div>
                    </div>
                    {v.when_to_use && (
                      <p className={`text-xs ${c.textMuted}`}><span className="font-semibold">{t('tb_when')}</span> {v.when_to_use}</p>
                    )}
                    {v.the_words && (
                      <div className={`p-4 rounded-xl ${c.card} border ${c.border}`}>
                        <p className={`text-sm font-medium leading-relaxed italic ${c.text}`}>
                          "{v.the_words}"
                        </p>
                      </div>
                    )}
                    {v.what_it_accomplishes && (
                      <p className={`text-xs ${c.textMuted}`}>{v.what_it_accomplishes}</p>
                    )}
                  </div>
                );
              })}
            </div>
          )}

          {/* Timing */}
          {results?.the_timing && (
            <div className={`rounded-xl border overflow-hidden ${c.card} ${c.border}`}>
              <button onClick={() => toggle('timing')} className="w-full text-left px-5 py-4 flex items-center justify-between min-h-[44px]">
                <p className={`text-xs font-bold uppercase tracking-wider ${c.textMuted}`}>🕐 {t('tb_timing_title')}</p>
                <span className={`text-sm ${c.textMuted}`}>{expanded.timing ? '▲' : '▼'}</span>
              </button>
              {expanded.timing && (
                <div className={`px-5 pb-5 space-y-2 border-t ${c.border} pt-4`}>
                  {[
                    { key: 'when_to_say_it', label: t('tb_best_moment') },
                    { key: 'what_to_avoid', label: t('tb_avoid') },
                    { key: 'if_they_dont_respond_well', label: t('tb_no_respond') },
                  ].map(row => results?.the_timing?.[row.key] && (
                    <p key={row.key} className={`text-sm ${c.textSecondary}`}>
                      <span className={`font-semibold ${c.text}`}>{row.label}:</span> {results?.the_timing?.[row.key]}
                    </p>
                  ))}
                </div>
              )}
            </div>
          )}

          {/* Permission to not say it */}
          {results?.permission_to_not_say_it && (
            <div className={`rounded-xl border overflow-hidden ${c.card} ${c.border}`}>
              <button onClick={() => toggle('permission')} className="w-full text-left px-5 py-4 flex items-center justify-between min-h-[44px]">
                <p className={`text-xs font-bold uppercase tracking-wider ${c.textMuted}`}>🤫 {t('tb_permission_title')}</p>
                <span className={`text-sm ${c.textMuted}`}>{expanded.permission ? '▲' : '▼'}</span>
              </button>
              {expanded.permission && (
                <div className={`px-5 pb-5 space-y-2 border-t ${c.border} pt-4`}>
                  {results?.permission_to_not_say_it?.when_silence_is_okay && (
                    <p className={`text-sm ${c.textSecondary}`}>
                      <span className={`font-semibold ${c.text}`}>{t('tb_silence_okay')}</span> {results?.permission_to_not_say_it?.when_silence_is_okay}
                    </p>
                  )}
                  {results?.permission_to_not_say_it?.is_silence_legitimate !== undefined && (
                    <p className={`text-sm font-semibold ${results?.permission_to_not_say_it?.is_silence_legitimate ? (isDark ? 'text-emerald-400' : 'text-emerald-600') : (isDark ? 'text-amber-400' : 'text-amber-600')}`}>
                      {results?.permission_to_not_say_it?.is_silence_legitimate ? t('tb_silence_legit') : t('tb_silence_costs')}
                    </p>
                  )}
                  {results?.permission_to_not_say_it?.the_honest_cost && (
                    <p className={`text-sm ${c.textSecondary}`}>
                      <span className={`font-semibold ${c.text}`}>{t('tb_cost_accepting')}</span> {results?.permission_to_not_say_it?.the_honest_cost}
                    </p>
                  )}
                </div>
              )}
            </div>
          )}

          {/* Conditional cross-ref — shown only when scripts are ready */}
          {results?.three_ways_to_say_it?.length > 0 && (
            <div className={`rounded-xl border p-4 ${c.cardAlt} ${c.border}`}>
              <p className={`text-xs font-semibold uppercase tracking-wide mb-2 ${c.textMuted}`}>{t('tb_ready_to_say')}</p>
              <a href="/MagicMouth" className={`text-xs ${linkStyle}`}>🎤 {t('tb_magicmouth')}</a>
            </div>
          )}

          {/* Always-on related tools */}
          <div className={`rounded-xl border p-4 ${c.cardAlt} ${c.border}`}>
            <p className={`text-xs font-semibold uppercase tracking-wide mb-3 ${c.textMuted}`}>{t('tb_related')}</p>
            <div className="flex flex-wrap gap-3">
              <a href="/ApologyCalibrator" className={`text-xs ${linkStyle}`}>🤝 {t('tb_apologycalibrator')}</a>
            </div>
          </div>
        </div>
      )}

      {/* Session sessionHistory */}
      {sessionHistory.length > 0 && (
        <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
          <p className={`text-xs font-bold ${c.textMuted} mb-2`}>📋 {t('tb_recent')}</p>
          <div className="space-y-1">
            {sessionHistory.map(s => (
              <div key={s.id} className="flex items-center justify-between">
                <span className={`text-xs ${c.textSecondary} truncate`}>{s.preview || t('tb_session')}</span>
                <span className={`text-xs ${c.textMuted} ml-2 shrink-0`}>{new Date(s.date).toLocaleDateString()}</span>
              </div>
            ))}
          </div>
        </div>
      )}
    </div>
  );
};

TruthBomb.displayName = 'TruthBomb';
export default TruthBomb;
