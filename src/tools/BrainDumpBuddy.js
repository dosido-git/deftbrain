          {renderCategory('decisions', r.decisions)}
          {renderCategory('tell_someone', r.tell_someone)}
          {renderCategory('worries', r.worries)}
          {renderCategory('ideas', r.ideas)}
          {renderCategory('not_your_problem', r.not_your_problem)}
          {renderCategory('feelings', r.feelings)}
          {renderCategory('can_drop', r.can_drop)}

