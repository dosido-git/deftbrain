  const startAdaptiveMode = useCallback(async () => {
    try {
      const stream = await navigator.mediaDevices.getUserMedia({ audio: true });
      micStreamRef.current = stream;
      let ctx = ctxRef.current;
      if (!ctx || ctx.state === 'closed') {
        ctx = new (window.AudioContext || window.webkitAudioContext)();
        ctxRef.current = ctx;
      }
      const source = ctx.createMediaStreamSource(stream);
      const analyser = ctx.createAnalyser();
      analyser.fftSize = 256;
      source.connect(analyser);
      analyserRef.current = analyser;

      const dataArray = new Uint8Array(analyser.frequencyBinCount);
      let baselineVolume = masterVolume;

      const tick = () => {
        if (!analyserRef.current) return;
        analyser.getByteFrequencyData(dataArray);
        // Average amplitude in speech range (roughly bins 4-40 for 256 FFT at 44.1k)
        const speechBins = dataArray.slice(4, 40);
        const avg = speechBins.reduce((s, v) => s + v, 0) / speechBins.length;

        // Scale: ambient >30 = voices present, boost masking
        const boost = Math.min(25, Math.max(0, (avg - 30) * 0.5));
        const targetVol = Math.min(100, baselineVolume + boost);
        if (masterGainRef.current && ctxRef.current?.state === 'running') {
          masterGainRef.current.gain.setTargetAtTime(targetVol / 100, ctxRef.current.currentTime, 0.3);
        }
        adaptiveRafRef.current = requestAnimationFrame(tick);
      };
      tick();
      setAdaptiveMode(true);
    } catch (e) {
      setError(t('fsa_err_mic_denied'));
    }
  }, [masterVolume]);

