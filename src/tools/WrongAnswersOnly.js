import React, { useState, useCallback, useEffect, useRef } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { useRegisterActions } from '../components/ActionBarContext';
import { usePersistentState } from '../hooks/usePersistentState';
import { useTranslation } from '../i18n/useTranslation';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

// ════════════════════════════════════════════════════════════
// COMPONENT
// ════════════════════════════════════════════════════════════
const WrongAnswersOnly = ({ tool }) => {
  const { callToolEndpoint, loading, userLocale, userCurrency, userRegion } = useClaudeAPI();
  const { isDark } = useTheme();
  const { t } = useTranslation();

  // ── Localized constants ──
  const CATEGORIES = [
    { value: 'science', label: t('wao_cat_science'), icon: '🔬' },
    { value: 'history', label: t('wao_cat_history'), icon: '📜' },
    { value: 'cooking', label: t('wao_cat_cooking'), icon: '👨‍🍳' },
    { value: 'life', label: t('wao_cat_life'), icon: '🧘' },
    { value: 'tech', label: t('wao_cat_tech'), icon: '💻' },
    { value: 'nature', label: t('wao_cat_nature'), icon: '🌿' },
  ];

  const SERIOUSNESS = [
    { value: 'deadpan', label: t('wao_ser_deadpan'), icon: '😐', desc: t('wao_ser_deadpan_desc') },
    { value: 'playful', label: t('wao_ser_playful'), icon: '😏', desc: t('wao_ser_playful_desc') },
    { value: 'unhinged', label: t('wao_ser_unhinged'), icon: '🤪', desc: t('wao_ser_unhinged_desc') },
  ];

  const QUICK_QUESTIONS = [
    t('wao_qq_sky'),
    t('wao_qq_planes'),
    t('wao_qq_cats'),
    t('wao_qq_thunder'),
    t('wao_qq_wifi'),
    t('wao_qq_dream'),
    t('wao_qq_magnets'),
    t('wao_qq_pizza'),
    t('wao_qq_wwi'),
    t('wao_qq_vaccines'),
  ];

  const EXAMPLES = [
    { question: t('wao_ex_yawn'), category: 'science' },
    { question: t('wao_ex_gray'), category: 'science' },
    { question: t('wao_ex_dream'), category: 'science' },
  ];

  const c = {
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:         isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20' : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-500' : 'text-gray-400',
    required:      isDark ? 'text-amber-400' : 'text-amber-500',
    labelText:     isDark ? 'text-zinc-200' : 'text-gray-700',
    accentTxt:     isDark ? 'text-cyan-400' : 'text-cyan-600',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white' : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200' : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    success:       isDark ? 'bg-[#2a5248]/30 border-[#3a6e60] text-[#e4f2f0]' : 'bg-[#fafefe] border-[#b8dcd8] text-[#1e3030]',
    warning:       isDark ? 'bg-[#2a5248]/60 border-[#5aaa9e] text-[#e4f2f0]' : 'bg-[#f0f8f6] border-[#8cc8c0] text-[#1e3030]',
    danger:        isDark ? 'bg-[#2a5248]/30 border-[#3a6e60] text-[#e4f2f0]' : 'bg-[#fafefe] border-[#b8dcd8] text-[#1e3030]',
    pillActive:    isDark ? 'border-cyan-500 bg-cyan-900/30 text-cyan-200' : 'border-cyan-600 bg-cyan-100 text-cyan-900',
    pillInactive:  isDark ? 'border-zinc-600 text-zinc-400 hover:border-zinc-500' : 'border-gray-300 text-gray-500 hover:border-gray-400',
    infoBox:       isDark ? 'bg-[#1e3030]/60 border-[#3a6e60] text-[#e4f2f0]' : 'bg-[#f0f8f6] border-[#8cc8c0] text-[#1e3030]',
    quoteBg:       isDark ? 'bg-zinc-700/40' : 'bg-[#f3efe8]',
  };
  c.textMuteded = c.textMuted;
  c.label       = c.labelText;

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';

  // ── State ──
  const [question, setQuestion] = useState('');
  const [category, setCategory] = useState('science');
  const [seriousness, setSeriousness] = useState('playful');
  const [results, setResults] = usePersistentState('wronganswersonly-result', null);
  const [error, setError] = useState('');
  const loadExample = () => {
    const ex = EXAMPLES[Math.floor(Math.random() * EXAMPLES.length)];
    setQuestion(ex.question);
    setCategory(ex.category);
  };

  const resultsRef = useRef(null);
  const [sessionHistory, setSessionHistory] = usePersistentState('wronganswersonly-history', []);

  const [showReal, setShowReal] = useState(false);

  useEffect(() => {
    if (!results) return;
    const timer = setTimeout(() => {
      resultsRef.current?.scrollIntoView({ behavior: 'smooth', block: 'start' });
    }, 200);
    return () => clearTimeout(timer);
  }, [results]);

  // ── API ──
  const runWrong = useCallback(async () => {
    if (!question.trim()) return;
    setError('');
    setResults(null);
    setShowReal(false);
    try {
      const data = await callToolEndpoint('wrong-answers-only', {
        question: question.trim(),
        category,
        seriousness,
        seed: Date.now(),
        userLocale,
        userCurrency,
        userRegion,
      });
      setResults(data);
      setSessionHistory(prev => [{
        id: Date.now(), date: new Date().toISOString(),
        // PF-25 exception: 40-char preview-text truncation; session history is capped at 6.
        preview: question.trim().slice(0, 40),
      }, ...prev].slice(0, 6));
    } catch (err) {
      setError(err.message || t('wao_error'));
    } }, [question, category, seriousness, callToolEndpoint, setResults, setSessionHistory, userLocale, userCurrency, userRegion, t]);

  // Run with an explicit question value — avoids stale closure from pill clicks

  // ── Copy text builder ──
  const buildFullText = useCallback(() => {
    if (!results) return '';
    const lines = [`🙃 ${t('wao_copy_header')}`, ''];
    if (results.question_rephrased) lines.push(`${t('wao_copy_q')} ${results.question_rephrased}`, '');
    if (results.confident_answer) lines.push(`${t('wao_copy_a')} ${results.confident_answer}`, '');
    if (results.supporting_evidence?.length) {
      lines.push(t('wao_copy_sources'));
      results.supporting_evidence.forEach(e => lines.push(`  📎 ${e.fake_fact} — ${e.fake_source}`));
      lines.push('');
    } if (results.expert_tip) lines.push(`💡 ${t('wao_copy_tip')} ${results.expert_tip}`);
    lines.push(`\n${t('wao_copy_footer')}`);
    lines.push(BRAND);
    return lines.join('\n');
  }, [results, t]);

  // Register export content with wrapper ActionBar
  useRegisterActions(buildFullText(), tool?.title || 'Wrong Answers Only');

  // ── Keyboard shortcuts ──
  useEffect(() => {
    const handler = (e) => {
      if (e.key !== 'Enter') return;
      if (!question.trim() || loading) return;
      const tag = document.activeElement?.tagName;
      // Cmd/Ctrl+Enter from anywhere; plain Enter only when not in a text field
      if (e.metaKey || e.ctrlKey) {
        e.preventDefault();
        runWrong();
      } else if (tag !== 'INPUT' && tag !== 'TEXTAREA') {
        e.preventDefault();
        runWrong();
      } };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
  }, [question, loading, runWrong]);

  // ── Wrongness bar ──
  const wrongnessLevel = results?.wrongness_level || 0;
  const wrongnessWidth = `${Math.min(wrongnessLevel * 10, 100)}%`;

  // ════════════════════════════════════════════════════════════
  // RENDER
  // ════════════════════════════════════════════════════════════
  return (<div className={`space-y-4 ${c.text}`}>
      {/* Input */} <div className={`${c.card} border rounded-xl p-5`}>
        <div className={`mb-4 pb-3 border-b ${isDark ? 'border-zinc-500' : 'border-zinc-500'}`}>
          <h2 className={`text-xl font-bold ${c.text}`}><span className="mr-2">{tool?.icon}</span>{tool?.title}</h2>
          <p className={`text-sm ${c.textSecondary}`}>{tool?.tagline}</p>
          <button onClick={loadExample} disabled={loading} style={{ backgroundColor: (tool?.headerColor ?? '#888888') + '80' }} className={`mt-1.5 px-2.5 py-0.5 rounded-full text-xs font-medium border disabled:opacity-40 ${isDark ? 'text-white border-white/40' : 'text-gray-800 border-transparent'}`}>{t('try_example')}</button>
        </div>

        {/* Quick questions */} <div className="mb-4">
          <label className={`text-[10px] font-bold ${c.labelText} uppercase block mb-2`}>{t('wao_try_one')}</label>
          <div className="flex flex-wrap gap-1.5">
            {QUICK_QUESTIONS.map((q, i) => (<button key={i} onClick={() => {
                  setQuestion(q);
                  setResults(null);
                  setShowReal(false);
                  setError('');
                }} className={`px-2.5 py-1.5 rounded-lg text-[11px] font-medium min-h-[28px] border transition-colors ${
                  question === q ? c.pillActive : c.btnSecondary
                }`}>
                {q} </button>
            ))} </div>
        </div>

        {/* Question */} <div className="mb-4">
          <label className={`text-xs font-bold ${c.labelText} block mb-1.5`}>{t('wao_q_label')} <span className={c.required}>*</span></label>
          <input type="text" value={question} onChange={e => setQuestion(e.target.value)} placeholder={t('wao_ph')}
            className={`w-full px-3 py-2.5 border rounded-lg text-sm ${c.input} outline-none focus:ring-2`} onKeyDown={e => e.key === 'Enter' && question.trim() && runWrong()} />
        </div>

        {/* Category + Seriousness */} <div className="grid grid-cols-1 sm:grid-cols-2 gap-4 mb-5">
          <div>
            <label className={`text-xs font-bold ${c.labelText} uppercase block mb-2`}>{t('wao_category')}</label>
            <div className="flex flex-wrap gap-1.5">
              {CATEGORIES.map(cat => (<button key={cat.value} onClick={() => setCategory(cat.value)} className={`px-2.5 py-1.5 rounded-lg text-[11px] font-bold border transition-colors min-h-[30px] ${
                    category === cat.value ? c.pillActive : c.pillInactive
                  }`}>
                  <span className="mr-1">{cat.icon}</span>{cat.label} </button>
              ))} </div>
          </div>
          <div>
            <label className={`text-xs font-bold ${c.labelText} uppercase block mb-2`}>{t('wao_how_serious')}</label>
            <div className="flex gap-2">
              {SERIOUSNESS.map(s => (<button key={s.value} onClick={() => setSeriousness(s.value)} className={`flex-1 py-2 rounded-lg border text-center transition-colors min-h-[44px] ${
                    seriousness === s.value ? c.pillActive : c.pillInactive
                  }`}>
                  <span className="text-sm block">{s.icon}</span>
                  <span className="text-[10px] font-bold block">{s.label}</span>
                </button>
              ))} </div>
          </div>
        </div>

        <p className={`text-xs ${c.textMuted} mb-2`}>{t('wao_chaos_q')} <a href="/PlotTwist" className={linkStyle}>🔀 {t('wao_plottwist')}</a> {t('wao_chaos_tail')}</p>

        <button onClick={runWrong} disabled={!question.trim() || loading} className={`w-full ${c.btnPrimary} disabled:opacity-40 font-bold py-3 rounded-lg flex items-center justify-center gap-2 min-h-[48px]`}>
          {loading
            ? <><span className="inline-block animate-spin">{tool?.icon ?? '🙃'}</span> {t('wao_making')}</>
            : <><span>{tool?.icon ?? '🙃'}</span> {t('wao_run')}</>} </button>
        <p className={`text-xs text-center ${c.textMuted}`}>{t('wao_disclaimer')}</p>
      </div>

      {/* Error */} {error && (<div className={`${c.danger} border rounded-lg p-4 flex items-start gap-3`}>
          <span>⚠️</span><p className="text-sm">{error}</p>
        </div>
      )} {/* Results */} {results && (<div className="space-y-4">
          <div className="flex justify-end">
            <div ref={resultsRef} data-results-anchor />
          </div>

          {/* Question + Wrongness meter */} {results.question_rephrased && (<div className={`${c.infoBox} border-2 rounded-xl p-5`}>
              <p className={`text-xs ${c.textMuted} mb-1`}>{t('wao_you_asked')}</p>
              <p className={`text-sm font-bold ${c.text} mb-3`}>{results.question_rephrased}</p>
              {wrongnessLevel > 0 && (<div>
                  <div className="flex justify-between mb-1">
                    <span className={`text-[9px] font-bold ${c.labelText}`}>{t('wao_wrongness')}</span>
                    <span className={`text-[9px] font-bold ${c.accentTxt}`}>{wrongnessLevel}/10</span>
                  </div>
                  <div className={`h-2 rounded-full ${isDark ? 'bg-zinc-500' : 'bg-zinc-500'}`}>
                    <div className="h-full rounded-full bg-gradient-to-r from-[#c8a951] to-[#a03030] transition-all"
                      style={{ width: wrongnessWidth }} />
                  </div>
                </div>
              )} </div>
          )} {/* The wrong answer */} {results.confident_answer && (<div className={`${c.card} border rounded-xl p-5`}>
              <h3 className={`text-sm font-bold ${c.text} mb-2`}>🎓 {t('wao_expert_answer')}</h3>
              <p className={`text-sm ${c.textSecondary} leading-relaxed whitespace-pre-line`}>{results.confident_answer}</p>
            </div>
          )} {/* Fake evidence */} {results.supporting_evidence?.length > 0 && (<div className={`${c.card} border rounded-xl p-4`}>
              <h3 className={`text-sm font-bold ${c.text} mb-3`}>📎 {t('wao_evidence')}</h3>
              <div className="space-y-2">
                {results.supporting_evidence.map((e, i) => (<div key={i} className={`${c.quoteBg} rounded-lg p-3`}>
                    <p className={`text-xs ${c.text} font-medium mb-1`}>{e.fake_fact}</p>
                    <p className={`text-[10px] ${c.textMuted} italic`}>— {e.fake_source}</p>
                    {showReal && e.how_wrong && (<p className={`text-[10px] ${isDark ? 'text-zinc-500' : 'text-zinc-500'} mt-1`}>❌ {t('wao_reality')} {e.how_wrong}</p>
                    )} </div>
                ))} </div>
            </div>
          )} {/* Common "misconception" (actually the real answer) */} {results.common_misconception && (<div className={`${c.warning} border rounded-xl p-4`}>
              <p className={`text-[10px] font-bold uppercase mb-1`}>⚠️ {t('wao_misconception')}</p>
              <p className={`text-xs`}>{results.common_misconception}</p>
              <p className={`text-[9px] ${c.textMuted} mt-1 italic`}>{t('wao_misconception_note')}</p>
            </div>
          )} {/* Expert tip */} {results.expert_tip && (<div className={`${c.quoteBg} border ${isDark ? 'border-zinc-500' : 'border-zinc-500'} rounded-xl p-4`}>
              <p className={`text-[10px] font-bold ${c.accentTxt} uppercase mb-1`}>💡 {t('wao_expert_tip')}</p>
              <p className={`text-sm font-medium ${c.text}`}>{results.expert_tip}</p>
            </div>
          )} {/* Real answer hint (toggled) */} {results.real_answer_hint && (<div className="flex gap-2">
              <button onClick={() => setShowReal(p => !p)} className={`${c.btnSecondary} px-4 py-2 rounded-lg text-xs font-bold min-h-[36px]`}>
                {showReal ? `🙈 ${t('wao_hide_real')}` : `👀 ${t('wao_show_real')}`} </button>
            </div>
          )} {showReal && results.real_answer_hint && (<div className={`${c.success} border rounded-xl p-4`}>
              <p className={`text-[10px] font-bold uppercase mb-1`}>✅ {t('wao_actually')}</p>
              <p className="text-xs">{results.real_answer_hint}</p>
            </div>
          )} {/* Again */} <button onClick={runWrong} disabled={loading || !question.trim()} className={`w-full ${c.btnSecondary} font-bold py-3 rounded-lg min-h-[44px] disabled:opacity-40`}>
            {tool?.icon ?? '🙃'} {t('wao_different')}
          </button>

          {/* Post-result cross-refs */}
          <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
            <p className={`text-xs font-bold ${c.textMuted} mb-2`}>🔗 {t('wao_more_like')}</p>
            <div className="flex flex-wrap gap-3">
              <a href="/TimeWarp" className={`text-xs ${linkStyle}`}>⏰ {t('wao_timewarp')}</a>
              <a href="/WhichLife" className={`text-xs ${linkStyle}`}>🤔 {t('wao_whatif')}</a>
            </div>
          </div>
        </div>
      )} {/* eslint-disable-next-line no-restricted-globals */} {sessionHistory.length > 0 && (<div className={`${c.cardAlt} border ${c.border} rounded-xl p-4 mt-4`}>
          <p className={`text-xs font-bold ${c.textMuted} mb-2`}>📋 {t('wao_recent')}</p>
          <div className="space-y-1">
            {/* eslint-disable-next-line no-restricted-globals */} {sessionHistory.map(s => (<div key={s.id} className="flex items-center justify-between">
                <span className={`text-xs ${c.textSecondary} truncate`}>{s.preview || t('wao_session')}</span>
                <span className={`text-xs ${c.textMuted} ml-2`}>{new Date(s.date).toLocaleDateString()}</span>
              </div>
            ))} </div>
        </div>
      )}
    </div>
  );
};

WrongAnswersOnly.displayName = 'WrongAnswersOnly';
export default WrongAnswersOnly;
