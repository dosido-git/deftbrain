import React, { useState, useCallback } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { CopyBtn, ActionBar } from '../components/ActionButtons';

// ════════════════════════════════════════════════════════════
// CONSTANTS
// ════════════════════════════════════════════════════════════
const CATEGORIES = [
  { value: 'science', label: 'Science', icon: '🔬' },
  { value: 'history', label: 'History', icon: '📜' },
  { value: 'cooking', label: 'Cooking', icon: '👨‍🍳' },
  { value: 'life', label: 'Life Advice', icon: '🧘' },
  { value: 'tech', label: 'Tech', icon: '💻' },
  { value: 'nature', label: 'Nature', icon: '🌿' },
];

const SERIOUSNESS = [
  { value: 'deadpan', label: 'Deadpan', icon: '😐', desc: '100% serious delivery' },
  { value: 'playful', label: 'Playful', icon: '😏', desc: 'Starts real, ends absurd' },
  { value: 'unhinged', label: 'Unhinged', icon: '🤪', desc: 'Beautiful nonsense' },
];

const QUICK_QUESTIONS = [
  'Why is the sky blue?',
  'How do airplanes fly?',
  'Why do cats purr?',
  'What causes thunder?',
  'How does WiFi work?',
  'Why do we dream?',
  'How do magnets work?',
  'Why is pizza round?',
  'What started World War I?',
  'How do vaccines work?',
];

// ════════════════════════════════════════════════════════════
// COMPONENT
// ════════════════════════════════════════════════════════════
const WrongAnswersOnly = () => {
  const { callToolEndpoint, loading } = useClaudeAPI();
  const { theme } = useTheme();
  const isDark = theme === 'dark';

  const c = {
    card: isDark ? 'bg-[#1a1a2e] border-[#2a2a4a]' : 'bg-white border-[#d4d4d4]',
    input: isDark
      ? 'bg-[#0f0f1e] border-[#2a2a4a] text-[#e8e8e8] placeholder:text-[#555] focus:border-[#c8a951] focus:ring-[#c8a951]/20'
      : 'bg-white border-[#ccc] text-[#1e3a5f] placeholder:text-[#999] focus:border-[#1e3a5f] focus:ring-[#1e3a5f]/20',
    text: isDark ? 'text-[#e8e8e8]' : 'text-[#1e3a5f]',
    textSec: isDark ? 'text-[#a0a0b8]' : 'text-[#4a6a8a]',
    textMuted: isDark ? 'text-[#666680]' : 'text-[#8a8a8a]',
    label: isDark ? 'text-[#c0c0d0]' : 'text-[#2a4a6a]',
    accent: isDark ? 'text-[#c8a951]' : 'text-[#1e3a5f]',
    accentBg: isDark ? 'bg-[#c8a951]/10 border-[#c8a951]/30' : 'bg-[#1e3a5f]/5 border-[#1e3a5f]/20',
    btnPrimary: isDark ? 'bg-[#c8a951] hover:bg-[#d4b85c] text-[#1a1a2e]' : 'bg-[#1e3a5f] hover:bg-[#2a4a6f] text-white',
    btnSec: isDark ? 'bg-[#2a2a4a] hover:bg-[#3a3a5a] text-[#e8e8e8]' : 'bg-[#f0f0f0] hover:bg-[#e0e0e0] text-[#1e3a5f]',
    danger: isDark ? 'bg-[#3a1a1a] border-[#5a2a2a] text-[#f0a0a0]' : 'bg-[#fef2f2] border-[#e8c0c0] text-[#7a2a2a]',
    pillActive: isDark ? 'bg-[#c8a951] border-[#c8a951] text-[#1a1a2e]' : 'bg-[#1e3a5f] border-[#1e3a5f] text-white',
    pillInactive: isDark ? 'bg-[#2a2a4a] border-[#3a3a5a] text-[#c0c0d0] hover:border-[#4a4a6a]' : 'bg-white border-[#d4d4d4] text-[#4a6a8a] hover:border-[#aaa]',
    quoteBg: isDark ? 'bg-[#0f0f1e]/60' : 'bg-[#f8f8f8]',
    warning: isDark ? 'bg-[#2e2a1a] border-[#4a3a2a] text-[#e0c080]' : 'bg-[#fffbf0] border-[#dcc8a0] text-[#6a4a1a]',
    success: isDark ? 'bg-[#1a2e1a] border-[#2a4a2a] text-[#a0e0a0]' : 'bg-[#f0faf0] border-[#c0dcc0] text-[#2a5a2a]',
  };

  // ── State ──
  const [question, setQuestion] = useState('');
  const [category, setCategory] = useState('science');
  const [seriousness, setSeriousness] = useState('playful');
  const [results, setResults] = useState(null);
  const [error, setError] = useState('');
  const [showReal, setShowReal] = useState(false);

  // ── API ──
  const runWrong = useCallback(async () => {
    if (!question.trim()) return;
    setError('');
    setResults(null);
    setShowReal(false);
    try {
      const data = await callToolEndpoint('wrong-answers-only', {
        question: question.trim(),
        category,
        seriousness,
      });
      setResults(data);
    } catch (err) {
      setError(err.message || 'Even the wrong answers failed');
    }
  }, [question, category, seriousness, callToolEndpoint]);

  // ── Copy text builder ──
  const buildFullText = useCallback(() => {
    if (!results) return '';
    const lines = ['🙃 WRONG ANSWERS ONLY', ''];
    if (results.question_rephrased) lines.push(`Q: ${results.question_rephrased}`, '');
    if (results.confident_answer) lines.push(`A: ${results.confident_answer}`, '');
    if (results.supporting_evidence?.length) {
      lines.push('SOURCES:');
      results.supporting_evidence.forEach(e => lines.push(`  📎 ${e.fake_fact} — ${e.fake_source}`));
      lines.push('');
    }
    if (results.expert_tip) lines.push(`💡 Expert tip: ${results.expert_tip}`);
    lines.push('\n(All answers are intentionally, beautifully wrong)');
    lines.push('\n— Generated by DeftBrain · deftbrain.com');
    return lines.join('\n');
  }, [results]);

  // ── Wrongness bar ──
  const wrongnessLevel = results?.wrongness_level || 0;
  const wrongnessWidth = `${Math.min(wrongnessLevel * 10, 100)}%`;

  // ════════════════════════════════════════════════════════════
  // RENDER
  // ════════════════════════════════════════════════════════════
  return (
    <div className={`space-y-4 ${c.text}`}>
      {/* Input */}
      <div className={`${c.card} border rounded-xl p-5`}>
        <div className={`mb-4 pb-3 border-b ${isDark ? 'border-[#2a2a4a]' : 'border-[#e0e0e0]'}`}>
          <h2 className={`text-xl font-bold ${c.text}`}>
            <span className="mr-2">🙃</span>Wrong Answers Only
          </h2>
          <p className={`text-sm ${c.textSec}`}>Ask any question — get a confidently, beautifully wrong answer</p>
        </div>

        {/* Quick questions */}
        <div className="mb-4">
          <label className={`text-[10px] font-bold ${c.label} uppercase block mb-2`}>Try one</label>
          <div className="flex flex-wrap gap-1.5">
            {QUICK_QUESTIONS.map((q, i) => (
              <button key={i} onClick={() => { setQuestion(q); setResults(null); }}
                className={`${c.btnSec} px-2.5 py-1.5 rounded-lg text-[11px] font-medium min-h-[28px]`}>
                {q}
              </button>
            ))}
          </div>
        </div>

        {/* Question */}
        <div className="mb-4">
          <label className={`text-xs font-bold ${c.label} block mb-1.5`}>Your question</label>
          <input type="text" value={question} onChange={e => setQuestion(e.target.value)}
            placeholder="Ask me anything — I'll get it spectacularly wrong..."
            className={`w-full px-3 py-2.5 border rounded-lg text-sm ${c.input} outline-none focus:ring-2`}
            onKeyDown={e => e.key === 'Enter' && question.trim() && runWrong()}
          />
        </div>

        {/* Category + Seriousness */}
        <div className="grid grid-cols-1 sm:grid-cols-2 gap-4 mb-5">
          <div>
            <label className={`text-xs font-bold ${c.label} uppercase block mb-2`}>Category</label>
            <div className="flex flex-wrap gap-1.5">
              {CATEGORIES.map(cat => (
                <button key={cat.value} onClick={() => setCategory(cat.value)}
                  className={`px-2.5 py-1.5 rounded-lg text-[11px] font-bold border transition-colors min-h-[30px] ${
                    category === cat.value ? c.pillActive : c.pillInactive
                  }`}>
                  <span className="mr-1">{cat.icon}</span>{cat.label}
                </button>
              ))}
            </div>
          </div>
          <div>
            <label className={`text-xs font-bold ${c.label} uppercase block mb-2`}>How serious?</label>
            <div className="flex gap-2">
              {SERIOUSNESS.map(s => (
                <button key={s.value} onClick={() => setSeriousness(s.value)}
                  className={`flex-1 py-2 rounded-lg border text-center transition-colors min-h-[44px] ${
                    seriousness === s.value ? c.pillActive : c.pillInactive
                  }`}>
                  <span className="text-sm block">{s.icon}</span>
                  <span className="text-[10px] font-bold block">{s.label}</span>
                </button>
              ))}
            </div>
          </div>
        </div>

        <button onClick={runWrong} disabled={!question.trim() || loading}
          className={`w-full ${c.btnPrimary} disabled:opacity-40 font-bold py-3 rounded-lg flex items-center justify-center gap-2 min-h-[48px]`}>
          {loading
            ? <><span className="animate-spin inline-block">⏳</span> Making things up...</>
            : <><span>🙃</span> Wrong Answers Only</>}
        </button>
      </div>

      {/* Error */}
      {error && (
        <div className={`${c.danger} border rounded-lg p-4 flex items-start gap-3`}>
          <span>⚠️</span><p className="text-sm">{error}</p>
        </div>
      )}

      {/* Results */}
      {results && (
        <div className="space-y-4">
          <div className="flex justify-end">
            <ActionBar content={buildFullText()} title="Wrong Answers Only" />
          </div>

          {/* Question + Wrongness meter */}
          {results.question_rephrased && (
            <div className={`${c.accentBg} border-2 rounded-xl p-5`}>
              <p className={`text-xs ${c.textMuted} mb-1`}>You asked:</p>
              <p className={`text-sm font-bold ${c.text} mb-3`}>{results.question_rephrased}</p>
              {wrongnessLevel > 0 && (
                <div>
                  <div className="flex justify-between mb-1">
                    <span className={`text-[9px] font-bold ${c.label}`}>WRONGNESS LEVEL</span>
                    <span className={`text-[9px] font-bold ${c.accent}`}>{wrongnessLevel}/10</span>
                  </div>
                  <div className={`h-2 rounded-full ${isDark ? 'bg-[#2a2a4a]' : 'bg-[#e0e0e0]'}`}>
                    <div className="h-full rounded-full bg-gradient-to-r from-[#c8a951] to-[#a03030] transition-all"
                      style={{ width: wrongnessWidth }} />
                  </div>
                </div>
              )}
            </div>
          )}

          {/* The wrong answer */}
          {results.confident_answer && (
            <div className={`${c.card} border rounded-xl p-5`}>
              <h3 className={`text-sm font-bold ${c.text} mb-2`}>🎓 The Expert Answer</h3>
              <p className={`text-sm ${c.textSec} leading-relaxed whitespace-pre-line`}>{results.confident_answer}</p>
            </div>
          )}

          {/* Fake evidence */}
          {results.supporting_evidence?.length > 0 && (
            <div className={`${c.card} border rounded-xl p-4`}>
              <h3 className={`text-sm font-bold ${c.text} mb-3`}>📎 Supporting "Evidence"</h3>
              <div className="space-y-2">
                {results.supporting_evidence.map((e, i) => (
                  <div key={i} className={`${c.quoteBg} rounded-lg p-3`}>
                    <p className={`text-xs ${c.text} font-medium mb-1`}>{e.fake_fact}</p>
                    <p className={`text-[10px] ${c.textMuted} italic`}>— {e.fake_source}</p>
                    {showReal && e.how_wrong && (
                      <p className={`text-[10px] ${isDark ? 'text-[#f0a0a0]' : 'text-[#a03030]'} mt-1`}>❌ Reality: {e.how_wrong}</p>
                    )}
                  </div>
                ))}
              </div>
            </div>
          )}

          {/* Common "misconception" (actually the real answer) */}
          {results.common_misconception && (
            <div className={`${c.warning} border rounded-xl p-4`}>
              <p className={`text-[10px] font-bold uppercase mb-1`}>⚠️ Common Misconception</p>
              <p className={`text-xs`}>{results.common_misconception}</p>
              <p className={`text-[9px] ${c.textMuted} mt-1 italic`}>(This is probably the actual answer)</p>
            </div>
          )}

          {/* Expert tip */}
          {results.expert_tip && (
            <div className={`${c.quoteBg} border ${isDark ? 'border-[#2a2a4a]' : 'border-[#d4d4d4]'} rounded-xl p-4`}>
              <p className={`text-[10px] font-bold ${c.accent} uppercase mb-1`}>💡 Expert Tip</p>
              <p className={`text-sm font-medium ${c.text}`}>{results.expert_tip}</p>
            </div>
          )}

          {/* Real answer hint (toggled) */}
          {results.real_answer_hint && (
            <div className="flex gap-2">
              <button onClick={() => setShowReal(p => !p)}
                className={`${c.btnSec} px-4 py-2 rounded-lg text-xs font-bold min-h-[36px]`}>
                {showReal ? '🙈 Hide Real Answer' : '👀 Show Real Answer'}
              </button>
            </div>
          )}
          {showReal && results.real_answer_hint && (
            <div className={`${c.success} border rounded-xl p-4`}>
              <p className={`text-[10px] font-bold uppercase mb-1`}>✅ Actually...</p>
              <p className="text-xs">{results.real_answer_hint}</p>
            </div>
          )}

          {/* Again */}
          <button onClick={runWrong} disabled={loading}
            className={`w-full ${c.btnSec} font-bold py-3 rounded-lg min-h-[44px]`}>
            🙃 Different Wrong Answer
          </button>
        </div>
      )}
    </div>
  );
};

WrongAnswersOnly.displayName = 'WrongAnswersOnly';
export default WrongAnswersOnly;
