import React, { useState, useEffect, useRef, useCallback } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { usePersistentState } from '../hooks/usePersistentState';
import { CopyBtn } from '../components/ActionButtons';
import { useRegisterActions } from '../components/ActionBarContext';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const DEPTHS = [
  { value: 'quick',  label: 'Quick',     icon: '⚡', desc: '5 consequences, 50 years' },
  { value: 'deep',   label: 'Deep Dive', icon: '🔬', desc: '10 consequences, 100+ years' },
  { value: 'absurd', label: 'Absurd',    icon: '🤪', desc: 'Plausible → hilariously extreme' },
];

const QUICK_PROMPTS = [
  'The internet was invented in 1920',
  'Dinosaurs never went extinct',
  'Rome never fell',
  'Electricity was never discovered',
  'The printing press was invented 500 years earlier',
  'Humans could fly',
];

const AlternatePath = ({ tool }) => {
  const { callToolEndpoint, loading } = useClaudeAPI();
  const { isDark } = useTheme();

  // ─── Colors ───
  const c = {
    card:          isDark ? 'bg-zinc-800'      : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50'   : 'bg-slate-50',
    input:         isDark
      ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20'
      : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-zinc-50'     : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300'    : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-500'    : 'text-gray-400',
    labelText:     isDark ? 'text-zinc-200'    : 'text-gray-700',
    accentTxt:     isDark ? 'text-cyan-400'    : 'text-cyan-600',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white' : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200' : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700'  : 'border-gray-200',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200' : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200' : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200' : 'bg-red-50 border-red-200 text-red-800',
    infoBox:       isDark ? 'bg-sky-900/20 border-sky-700 text-sky-200' : 'bg-sky-50 border-sky-200 text-sky-800',
    successBox:    isDark ? 'bg-emerald-900/20 border-emerald-700' : 'bg-emerald-50 border-emerald-300',
    successTxt:    isDark ? 'text-emerald-300' : 'text-emerald-800',
    warningBox:    isDark ? 'bg-amber-900/20 border-amber-700' : 'bg-amber-50 border-amber-300',
    warningTxt:    isDark ? 'text-amber-300'   : 'text-amber-800',
    pillActive:    isDark ? 'bg-cyan-600 border-cyan-600 text-white' : 'bg-cyan-600 border-cyan-600 text-white',
    pillInactive:  isDark ? 'bg-zinc-700 border-zinc-600 text-zinc-300 hover:border-zinc-500' : 'bg-white border-gray-200 text-gray-600 hover:border-gray-300',
    required:      isDark ? 'text-amber-400'   : 'text-amber-500',
    // divergence banner — cyan-themed accent box for the "what if" statement
    divergenceBg:  isDark ? 'bg-cyan-900/20 border-cyan-700' : 'bg-cyan-50 border-cyan-300',
    divergenceTxt: isDark ? 'text-cyan-200'    : 'text-cyan-900',
  };
  c.textMuteded = c.textMuted;
  c.label = c.labelText;

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';

  // ─── State ───
  const [whatIf, setWhatIf] = usePersistentState('alternatepath-whatif', '');
  const [yearOrContext, setYearOrContext] = useState('');
  const [depth, setDepth] = useState('quick');
  const [error, setError] = useState('');

  const [results, setResults] = usePersistentState('alternatepath-last', null);
  const [history, setHistory] = usePersistentState('alternatepath-history', []);

  // ─── Refs ───
  const resultsRef = useRef(null);
  const handleSubmitRef = useRef(null);
  const canSubmitRef = useRef(false);

  // ─── Handlers ───
  const handleSubmit = async () => {
    if (!whatIf.trim()) return;
    setError('');
    setResults(null);

    try {
      const data = await callToolEndpoint('alternate-path', {
        whatIf: whatIf.trim(),
        yearOrContext: yearOrContext.trim(),
        depth,
      });
      setResults(data);
      setHistory(prev => [{
        id: Date.now(),
        timestamp: new Date().toISOString(),
        preview: whatIf.trim().slice(0, 40),
        result: data,
      }, ...(prev || [])].slice(0, 6));
    } catch (err) {
      setError(err.message || 'Timeline failed');
    }
  };

  const handleReset = () => {
    setWhatIf('');
    setYearOrContext('');
    setDepth('quick');
    setResults(null);
    setError('');
  };

  const handleQuickPrompt = (q) => {
    setWhatIf(q);
    setResults(null);
  };

  // ─── Build copyable text ───
  const buildFullText = useCallback(() => {
    if (!results) return '';
    const lines = [`🌀 ALTERNATE PATH: ${results.divergence_point || whatIf}`, ''];
    if (results.real_history) lines.push(`📜 Real history: ${results.real_history}`, '');
    if (results.timeline?.length) {
      lines.push('━━ TIMELINE ━━');
      results.timeline.forEach((t, i) => {
        lines.push(`${i + 1}. [${t.year_range}] ${t.event}`);
        if (t.because) lines.push(`   ↳ ${t.because}`);
        if (t.real_world_contrast) lines.push(`   Reality: ${t.real_world_contrast}`);
      });
      lines.push('');
    }
    if (results.today_looks_like) lines.push(`🌍 Today: ${results.today_looks_like}`, '');
    if (results.biggest_surprise) lines.push(`💡 Biggest surprise: ${results.biggest_surprise}`);
    if (results.butterfly_moment) lines.push(`🦋 Butterfly moment: ${results.butterfly_moment}`);
    if (results.plausibility) lines.push(`Plausibility: ${results.plausibility}/10`);
    return lines.join('\n') + BRAND;
  }, [results, whatIf]);

  // ─── Register global actions ───
  useRegisterActions(buildFullText(), tool?.title || 'Alternate Path');

  // ─── Ref assignments every render ───
  handleSubmitRef.current = handleSubmit;
  canSubmitRef.current = Boolean(whatIf.trim()) && !loading;

  // ─── Scroll to results ───
  useEffect(() => {
    if (!results || !resultsRef.current) return;
    const t = setTimeout(() => resultsRef.current?.scrollIntoView({ behavior: 'smooth', block: 'start' }), 250);
    return () => clearTimeout(t);
  }, [results]);

  // ─── Global ⌘/Ctrl+Enter keyboard handler ───
  useEffect(() => {
    const handler = (e) => {
      const tag = document.activeElement?.tagName;
      if (tag === 'SELECT') return;
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && canSubmitRef.current) {
        e.preventDefault();
        handleSubmitRef.current?.();
      }
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
  }, []);

  // ─── Render ───
  const hasAnyInput = whatIf.trim() || yearOrContext.trim();

  return (
    <div className={`space-y-4 ${c.text}`}>

      {/* ── Input Card ── */}
      <div className={`${c.card} border ${c.border} rounded-2xl p-5 shadow-sm space-y-4`}>

        {/* Header with title + reset */}
        <div className="flex items-start justify-between gap-3 pb-4 border-b border-zinc-500">
          <div className="flex-1 min-w-0">
            <h2 className={`text-xl font-bold ${c.text} flex items-center gap-2`}>
              <span className="mr-2">{tool?.icon ?? '🌀'}</span>{tool?.title ?? 'Alternate Path'}
            </h2>
            <p className={`text-sm ${c.textSecondary} mt-1`}>{tool?.tagline ?? 'What if history went differently?'}</p>
          </div>
          {(results || hasAnyInput) ? (
            <button
              onClick={handleReset}
              className={`shrink-0 px-3 py-2 rounded-lg text-sm font-bold min-h-[40px] ${c.btnSecondary}`}
            >↺ Start Over</button>
          ) : null}
        </div>

        {/* Quick prompts — pre-result only */}
        {!results && (
          <div>
            <label className={`block text-xs font-bold ${c.textMuteded} uppercase tracking-wider mb-2`}>Try one</label>
            <div className="flex flex-wrap gap-1.5">
              {QUICK_PROMPTS.map((q, i) => (
                <button
                  key={i}
                  onClick={() => handleQuickPrompt(q)}
                  disabled={loading}
                  className={`px-3 py-1.5 rounded-lg text-xs font-medium border min-h-[32px] transition-colors ${c.pillInactive} disabled:opacity-40`}
                >
                  {q}
                </button>
              ))}
            </div>
          </div>
        )}

        {/* What if — REQUIRED */}
        <div>
          <label className={`block text-sm font-semibold ${c.labelText} mb-1.5`}>
            What if... <span className={c.required}>*</span>
          </label>
          <input
            type="text"
            value={whatIf}
            onChange={e => setWhatIf(e.target.value)}
            placeholder="e.g., The internet was invented in 1920..."
            className={`w-full px-3 py-2.5 border rounded-lg text-sm ${c.input} outline-none focus:ring-2`}
          />
        </div>

        {/* Year or context */}
        <div>
          <label className={`block text-sm font-semibold ${c.labelText} mb-1.5`}>
            Year or context <span className={`font-normal ${c.textMuteded}`}>(optional)</span>
          </label>
          <input
            type="text"
            value={yearOrContext}
            onChange={e => setYearOrContext(e.target.value)}
            placeholder="e.g., 1920, during WWI, instead of..."
            className={`w-full px-3 py-2.5 border rounded-lg text-sm ${c.input} outline-none focus:ring-2`}
          />
        </div>

        {/* Depth */}
        <div>
          <label className={`block text-sm font-semibold ${c.labelText} mb-2`}>Depth</label>
          <div className="flex gap-2">
            {DEPTHS.map(d => (
              <button
                key={d.value}
                onClick={() => setDepth(d.value)}
                className={`flex-1 py-2.5 rounded-xl border text-center min-h-[56px] transition-colors ${
                  depth === d.value ? c.pillActive : c.pillInactive
                }`}
              >
                <span className="text-sm font-bold block">{d.icon} {d.label}</span>
                <span className={`text-[10px] block mt-0.5 ${depth === d.value ? 'opacity-80' : c.textMuteded}`}>{d.desc}</span>
              </button>
            ))}
          </div>
        </div>

        {/* Pre-result cross-ref */}
        <p className={`text-xs text-center ${c.textMuteded}`}>
          More of a fiction person?{' '}
          <a href="/FanTheory" className={linkStyle}>🎬 Fan Theory</a>{' '}
          builds theories about stories and films.
        </p>

        {/* Submit */}
        <button
          onClick={handleSubmit}
          disabled={loading || !whatIf.trim()}
          className={`w-full ${c.btnPrimary} disabled:opacity-40 font-bold py-3 rounded-lg flex items-center justify-center gap-2 min-h-[48px]`}
        >
          {loading ? (
            <>
              <span className="animate-spin inline-block">{tool?.icon ?? '🌀'}</span>
              Rewriting history...
            </>
          ) : (
            <>
              <span>{tool?.icon ?? '🌀'}</span> Explore Timeline
            </>
          )}
        </button>
      </div>

      {/* ── Error ── */}
      {error && (
        <div className={`${c.danger} border rounded-xl p-4 text-sm flex items-start gap-2`}>
          <span className="flex-shrink-0 mt-0.5">⚠️</span>
          <p>{error}</p>
        </div>
      )}

      {/* ── Results ── */}
      {results && (
        <div ref={resultsRef} className="space-y-4">

          {/* Divergence banner */}
          <div className={`${c.divergenceBg} border-2 rounded-2xl p-5 text-center`}>
            <span className="text-3xl block mb-2">🌀</span>
            <p className={`text-lg font-black ${c.divergenceTxt} mb-2 leading-snug`}>{results.divergence_point}</p>
            {results.real_history && (
              <p className={`text-xs ${c.textMuteded} italic`}>📜 Reality: {results.real_history}</p>
            )}
            {typeof results.plausibility !== 'undefined' && (
              <p className={`text-xs ${c.textSecondary} mt-2`}>
                Plausibility: <span className="font-bold">{results.plausibility}/10</span>
              </p>
            )}
          </div>

          {/* Timeline */}
          {results.timeline?.length > 0 && (
            <div className={`${c.card} border ${c.border} rounded-2xl p-5`}>
              <h3 className={`text-sm font-bold ${c.text} mb-3 uppercase tracking-wider`}>📅 Timeline</h3>
              <div className="space-y-2">
                {results.timeline.map((t, i) => (
                  <div key={i} className={`${c.cardAlt} rounded-lg p-3 border-l-4 border-cyan-500`}>
                    <div className="flex items-start gap-2">
                      <span className={`text-xs font-black ${c.accentTxt} whitespace-nowrap`}>{t.year_range}</span>
                      <div className="flex-1 min-w-0">
                        <p className={`text-sm font-medium ${c.text}`}>{t.event}</p>
                        {t.because && <p className={`text-xs ${c.textMuteded} mt-1`}>↳ {t.because}</p>}
                        {t.real_world_contrast && <p className={`text-xs ${c.textMuteded} italic mt-1`}>Reality: {t.real_world_contrast}</p>}
                      </div>
                    </div>
                  </div>
                ))}
              </div>
            </div>
          )}

          {/* Today in this timeline */}
          {results.today_looks_like && (
            <div className={`${c.success} border rounded-2xl p-4`}>
              <p className={`text-xs font-bold uppercase tracking-wider mb-1`}>🌍 Today in this timeline</p>
              <p className="text-sm leading-relaxed">{results.today_looks_like}</p>
            </div>
          )}

          {/* Biggest surprise + Butterfly moment */}
          <div className="grid grid-cols-1 sm:grid-cols-2 gap-3">
            {results.biggest_surprise && (
              <div className={`${c.warning} border rounded-2xl p-4`}>
                <p className={`text-xs font-bold uppercase tracking-wider mb-1`}>💡 Biggest surprise</p>
                <p className="text-sm leading-relaxed">{results.biggest_surprise}</p>
              </div>
            )}
            {results.butterfly_moment && (
              <div className={`${c.cardAlt} border ${c.border} rounded-2xl p-4`}>
                <p className={`text-xs font-bold ${c.textMuteded} uppercase tracking-wider mb-1`}>🦋 Butterfly moment</p>
                <p className={`text-sm ${c.textSecondary} leading-relaxed`}>{results.butterfly_moment}</p>
              </div>
            )}
          </div>

          {/* Secondary action: roll again with same "what if" */}
          <button
            onClick={handleSubmit}
            disabled={loading}
            className={`w-full ${c.btnSecondary} font-bold py-3 rounded-lg min-h-[44px] disabled:opacity-40`}
          >
            🌀 Spin Another Timeline
          </button>

          {/* Conditional cross-ref: low plausibility */}
          {typeof results.plausibility === 'number' && results.plausibility < 4 && (
            <p className={`text-xs text-center ${c.textMuteded}`}>
              Low plausibility score?{' '}
              <a href="/BrainRoulette" className={linkStyle}>🎲 Brain Roulette</a>{' '}
              might spark a wilder but more grounded scenario.
            </p>
          )}

          {/* Post-result cross-refs */}
          <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4 space-y-2`}>
            <p className={`text-xs font-semibold ${c.textMuteded} uppercase tracking-wider`}>
              Keep exploring
            </p>
            <div className="flex flex-wrap gap-x-4 gap-y-1">
              <a href="/BeliefStressTest" className={`text-xs ${linkStyle}`}>🧪 Belief Stress Test</a>
              <a href="/FutureProof" className={`text-xs ${linkStyle}`}>🔭 Future Proof</a>
            </div>
          </div>
        </div>
      )}

      {/* ── History ── */}
      {history?.length > 0 && (
        <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
          <h3 className={`text-sm font-bold ${c.text} mb-3`}>🕐 Recent Timelines</h3>
          <div className="space-y-1.5">
            {history.map(entry => (
              <button key={entry.id}
                onClick={() => setResults(entry.result)}
                className={`w-full text-left px-3 py-2 rounded-lg ${c.btnSecondary} text-xs flex items-center gap-2`}>
                <span className={c.textMuteded}>
                  {new Date(entry.timestamp).toLocaleDateString(undefined, { month: 'short', day: 'numeric', hour: 'numeric', minute: '2-digit' })}
                </span>
                <span className={`${c.text} truncate`}>{entry.preview}{entry.preview?.length >= 40 ? '…' : ''}</span>
              </button>
            ))}
          </div>
        </div>
      )}
    </div>
  );
};

AlternatePath.displayName = 'AlternatePath';
export default AlternatePath;
