import React, { useState, useCallback, useRef, useEffect } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { useRegisterActions } from '../components/ActionBarContext';
import { usePersistentState } from '../hooks/usePersistentState';
import { useTranslation } from '../i18n/useTranslation';
const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';


const EXAMPLE = {
  situation: 'in-law',
  whatTheySaid: "When are you going to have kids? You're not getting any younger.",
  relationship: "My partner's mother — sees us a few times a year, always finds a way to bring it up at family gatherings, ostensibly 'just curious'",
  mood: 'witty',
};

const TECHNIQUE_EMOJIS = {
  callback:              '🔁',
  reframe:               '🔄',
  deadpan:               '😐',
  'rhetorical question': '❓',
  understatement:        '🤏',
  escalation:            '📈',
  'agreement-twist':     '🔀',
  'compliment-bomb':     '💐',
  'exit line':           '🚪',
  reversal:              '↩️',
  silence:               '🤐',
};

// ════════════════════════════════════════════════════════════
// MAIN COMPONENT
// ════════════════════════════════════════════════════════════
const ComebackCooker = ({ tool }) => {
  const { callToolEndpoint, loading, userLocale, userCurrency, userRegion } = useClaudeAPI();
  const { isDark } = useTheme();
  const { t } = useTranslation();

  const MOOD_OPTIONS = [
    { id: 'surgical',  label: t('cc_mood_surgical'),  icon: '🧊', desc: t('cc_mood_surgical_desc') },
    { id: 'witty',     label: t('cc_mood_witty'),     icon: '⚡', desc: t('cc_mood_witty_desc') },
    { id: 'petty',     label: t('cc_mood_petty'),     icon: '💅', desc: t('cc_mood_petty_desc') },
    { id: 'dignified', label: t('cc_mood_dignified'), icon: '👑', desc: t('cc_mood_dignified_desc') },
  ];

  const c = {
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:         isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20' : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-400' : 'text-gray-500',
    labelText:     isDark ? 'text-zinc-200' : 'text-gray-700',
    required:      isDark ? 'text-amber-400' : 'text-amber-700',
    accentTxt:     isDark ? 'text-cyan-400' : 'text-cyan-600',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white' : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200' : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200' : 'bg-red-50 border-red-200 text-red-800',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200' : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200' : 'bg-amber-50 border-amber-300 text-amber-800',
    pillActive:    isDark ? 'border-cyan-500 bg-cyan-900/30 text-cyan-200' : 'border-cyan-600 bg-cyan-100 text-cyan-900',
    pillInactive:  isDark ? 'border-zinc-600 text-zinc-400 hover:border-zinc-500' : 'border-gray-300 text-gray-500 hover:border-gray-400',
    deliveryBg:    isDark ? 'bg-zinc-700/40' : 'bg-slate-100',
    deliveryText:  isDark ? 'text-zinc-400' : 'text-gray-500',
    highRoadBg:    isDark ? 'bg-emerald-900/20 border-emerald-700' : 'bg-emerald-50 border-emerald-300',
    highRoadText:  isDark ? 'text-emerald-400' : 'text-emerald-700',
    nuclearBg:     isDark ? 'bg-red-900/20 border-red-800' : 'bg-red-50 border-red-200',
    nuclearText:   isDark ? 'text-red-400' : 'text-red-600',
  };
  c.textMuteded = c.textMuted;
  c.label = c.labelText;

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-700 hover:text-cyan-800 underline underline-offset-2';

  // ── State ──
  const [error,           setError]           = useState('');
  const [revealedNuclear, setRevealedNuclear] = useState(false);
  const [expandedCards,   setExpandedCards]   = useState({});
  const [results,         setResults]         = useState(null);

  // ── Refs ──
  const resultsRef      = useRef(null);
  const handleSubmitRef = useRef(null);
  const canSubmitRef    = useRef(false);

  // ── Persistent state ──
  const [situation,    setSituation]    = usePersistentState('comeback-cooker-situation', '');
  const [whatTheySaid, setWhatTheySaid] = usePersistentState('comeback-cooker-theysaid', '');
  const [sessionHistory,      setSessionHistory]      = usePersistentState('comebackcooker-history', []);
  const [relationship, setRelationship] = usePersistentState('comeback-cooker-relationship', '');
  const [mood,         setMood]         = usePersistentState('comeback-cooker-mood', 'witty');

  // ── Handlers ──
  const handleSubmit = async () => {
    if (!situation.trim()) return;
    setError('');
    setResults(null);
    setRevealedNuclear(false);
    setExpandedCards({});

    try {
      const data = await callToolEndpoint('comeback-cooker', {
        situation: situation.trim(),
        whatTheySaid: whatTheySaid.trim() || undefined,
        relationship: relationship.trim() || undefined,
        mood,
        userLocale,
        userCurrency,
        userRegion,
      });
      setResults(data);
      const entry = {
        id: Date.now(),
        timestamp: new Date().toISOString(),
        preview: situation.trim().slice(0, 40),
        result: data,
      };
      setSessionHistory(prev => [entry, ...prev].slice(0, 6));
    } catch (err) {
      setError(err.message || t('cc_error'));
    }
  };

  const loadExample = useCallback(() => {
    setSituation(EXAMPLE.situation);
    setWhatTheySaid(EXAMPLE.whatTheySaid);
    setRelationship(EXAMPLE.relationship);
    setMood(EXAMPLE.mood);
    setResults(null);
  }, [setSituation, setWhatTheySaid, setRelationship, setMood, setResults]);

  const handleReset = () => {
    setSituation('');
    setWhatTheySaid('');
    setRelationship('');
    setResults(null);
    setError('');
    setRevealedNuclear(false);
    setExpandedCards({});
  };

  const toggleCard = (idx) => {
    setExpandedCards(prev => ({ ...prev, [idx]: !prev[idx] }));
  };

  // ── Assign refs every render ──
  handleSubmitRef.current = handleSubmit;
  canSubmitRef.current = !loading && !!situation.trim();

  // ── Build copy text ──
  const buildFullText = useCallback(() => {
    if (!results) return '';
    const moodLabel = MOOD_OPTIONS.find(m => m.id === mood)?.label || mood;
    let out = `🍳 ${t('cc_copy_header')}\n\n`;
    out += `${t('cc_copy_situation')} ${situation}\n`;
    if (whatTheySaid) out += `${t('cc_copy_theysaid')} "${whatTheySaid}"\n`;
    out += `${t('cc_copy_mood')} ${moodLabel}\n\n`;
    if (results?.situation_read) out += `${t('cc_copy_read')} ${results?.situation_read}\n\n`;
    out += `━━ ${t('cc_copy_comebacks')} ━━\n\n`;
    (results?.comebacks || []).forEach((cb, i) => {
      out += `${i + 1}. "${cb.line}"\n`;
      out += `   [${cb.technique}] ${cb.why_it_works}\n`;
      out += `   🎬 ${cb.delivery_note}\n\n`;
    });
    if (results?.the_nuclear_option) {
      out += `━━ ${t('cc_copy_nuclear')} ━━\n"${results?.the_nuclear_option?.line}"\n⚠️ ${results?.the_nuclear_option?.warning}\n\n`;
    }
    if (results?.the_high_road) {
      out += `━━ ${t('cc_copy_highroad')} ━━\n"${results?.the_high_road?.line}"\n${results?.the_high_road?.why_its_devastating}\n\n`;
    }
    return out + BRAND;
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [results, situation, whatTheySaid, mood, t]);

  // ── Register global actions ──
  useRegisterActions(buildFullText(), tool?.title || 'Comeback Cooker');

  // ── Scroll to results ──
  useEffect(() => {
    if (!results) return;
    const timer = setTimeout(() => {
      resultsRef.current?.scrollIntoView({ behavior: 'smooth', block: 'start' });
    }, 100);
    return () => clearTimeout(timer);
  }, [results]);

  // ── Keyboard shortcut (Ctrl/Cmd+Enter) ──
  useEffect(() => {
    const handler = (e) => {
      if (e.target.tagName === 'SELECT') return;
      if ((e.ctrlKey || e.metaKey) && e.key === 'Enter') {
        e.preventDefault();
        if (canSubmitRef.current) handleSubmitRef.current();
      }
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
  }, []);

  // ════════════════════════════════════════════════════════════
  // RENDER
  // ════════════════════════════════════════════════════════════
  return (
    <div className={`space-y-4 ${c.text}`}>

      {/* ── INPUT CARD ── */}
      <div className={`${c.card} border ${c.border} rounded-xl shadow-sm p-5`}>
        {/* Standard header — with reset in top-right */}
        <div className="mb-4 pb-3 border-b border-zinc-500">
          <div className="flex items-center justify-between">
            <div>
              <h2 className={`text-xl font-bold ${c.text} flex items-center gap-2`}>
                <span className="mr-2">{tool?.icon ?? '🍳'}</span>{tool?.title ?? t('cc_title')}
              </h2>
              <p className={`text-sm ${c.textSecondary}`}>{tool?.tagline ?? t('cc_tagline')}</p>
              <button onClick={loadExample} disabled={loading} style={{ backgroundColor: (tool?.headerColor ?? '#888888') + '80' }} className={`mt-1.5 px-2.5 py-0.5 rounded-full text-xs font-medium border disabled:opacity-40 ${isDark ? 'text-white border-white/40' : 'text-gray-800 border-transparent'}`}>{t('try_example')}</button>
            </div>
            {(!!results || !!situation.trim()) && (
              <button
                onClick={handleReset}
                className={`${c.btnSecondary} px-3 py-1.5 rounded-lg text-xs`}
              >
                ↺ {t('start_over')}
              </button>
            )}
          </div>
        </div>

        {/* Pre-result cross-ref */}
        <p className={`text-xs text-center ${c.textMuted} mb-4`}>
          {t('cc_xref_pre_q')}{' '}
          <a href="/VelvetHammer" className={linkStyle}>✉️ {t('cc_velvethammer')}</a>{' '}
          {t('cc_xref_pre_post')}
        </p>

        <div className="space-y-4">
          {/* Situation */}
          <div>
            <label className={`text-sm font-semibold ${c.text} block mb-1.5`}>
              <span className="mr-1.5">📍</span>{t('cc_q_label')} <span className={c.required}>*</span>
            </label>
            <textarea
              value={situation}
              onChange={e => setSituation(e.target.value)}
              placeholder={t('cc_q_ph')}
              rows={3}
              maxLength={500}
              className={`w-full px-4 py-3 rounded-xl text-sm border focus:outline-none transition-colors resize-none ${c.input}`}
            />
            <p className={`text-xs ${c.textMuted} mt-1`}>{t('cc_ctrl_enter')}</p>
          </div>

          {/* What they said */}
          <div>
            <label className={`text-sm font-semibold ${c.text} block mb-1.5`}>
              <span className="mr-1.5">💬</span>{t('cc_said_label')}{' '}
              <span className={`font-normal ${c.textMuted}`}>{t('cc_said_hint')}</span>
            </label>
            <input
              type="text"
              value={whatTheySaid}
              onChange={e => setWhatTheySaid(e.target.value)}
              placeholder={t('cc_said_ph')}
              maxLength={300}
              className={`w-full px-4 py-2.5 rounded-xl text-sm border focus:outline-none transition-colors ${c.input}`}
            />
          </div>

          {/* Relationship */}
          <div>
            <label className={`text-sm font-semibold ${c.text} block mb-1.5`}>
              <span className="mr-1.5">👤</span>{t('cc_rel_label')}{' '}
              <span className={`font-normal ${c.textMuted}`}>{t('cc_rel_hint')}</span>
            </label>
            <input
              type="text"
              value={relationship}
              onChange={e => setRelationship(e.target.value)}
              placeholder={t('cc_rel_ph')}
              maxLength={100}
              className={`w-full px-4 py-2.5 rounded-xl text-sm border focus:outline-none transition-colors ${c.input}`}
            />
          </div>

          {/* Mood */}
          <div>
            <label className={`text-sm font-semibold ${c.text} block mb-2`}>
              <span className="mr-1.5">🎭</span>{t('cc_mood_label')}
            </label>
            <div className="grid grid-cols-2 sm:grid-cols-4 gap-2">
              {MOOD_OPTIONS.map(m => (
                <button
                  key={m.id}
                  onClick={() => setMood(m.id)}
                  className={`border rounded-xl px-2 py-2.5 text-center transition-colors ${mood === m.id ? c.pillActive : c.pillInactive}`}
                >
                  <span className="text-lg block mb-0.5">{m.icon}</span>
                  <p className="text-xs font-medium leading-tight">{m.label}</p>
                  <p className={`text-xs leading-tight mt-0.5 hidden sm:block ${mood === m.id ? 'opacity-80' : c.textMuted}`}>{m.desc}</p>
                </button>
              ))}
            </div>
          </div>

          {error && (
            <div className={`${c.danger} border rounded-xl p-4 text-sm flex items-center gap-2`}>
              <span>⚠️</span>{error}
            </div>
          )}

          <button
          onClick={handleSubmit}
          disabled={loading || !situation.trim()}
          className={`w-full ${c.btnPrimary} py-3 rounded-xl font-semibold text-sm min-h-[48px] disabled:opacity-40 disabled:cursor-not-allowed transition-colors flex items-center justify-center gap-2`}
          >
          {loading
            ? <><span className="animate-spin inline-block">{tool?.icon ?? '🍳'}</span> {t('cc_cooking')}</>
            : <><span>{tool?.icon ?? '🍳'}</span> {t('cc_cook')}</>}
          </button>
        </div>
      </div>

      {/* ── HISTORY ── */}
      {sessionHistory.length > 0 && !results && (
        <div className={`${c.card} rounded-xl border ${c.border} p-4`}>
          <h3 className={`text-sm font-bold ${c.text} mb-3`}>🕐 {t('cc_recent')}</h3>
          <div className="space-y-1.5">
            {sessionHistory.map(entry => (
              <button
                key={entry.id}
                onClick={() => setResults(entry.result)}
                className={`w-full text-left px-3 py-2 rounded-lg ${c.btnSecondary} text-xs`}
              >
                <span className={c.textMuted}>
                  {new Date(entry.timestamp).toLocaleDateString(undefined, { month: 'short', day: 'numeric', hour: 'numeric', minute: '2-digit' })}
                </span>
                <span className={`ml-2 ${c.text}`}>{entry.preview}…</span>
              </button>
            ))}
          </div>
        </div>
      )}

      {/* ════════════════════════════════════════════════════════
          RESULTS
          ════════════════════════════════════════════════════════ */}
      {results && (
        <div ref={resultsRef} className="space-y-4">

          {/* Regenerate — top of results */}
          <div className="flex items-center justify-start gap-3 flex-wrap">
            <button
              onClick={handleSubmit}
              disabled={loading || !situation.trim()}
              className={`px-4 py-2 rounded-lg text-sm font-bold disabled:opacity-40 transition-colors ${c.btnPrimary}`}
            >
              {loading ? <span className="animate-spin inline-block">{tool?.icon ?? '🍳'}</span> : <><span>🔁</span> {t('cc_different_mood')}</>}
            </button>
          </div>

          {/* Situation read */}
          {results?.situation_read && (
            <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4 text-center`}>
              <p className={`text-xs font-semibold uppercase tracking-wider ${c.textMuted} mb-1`}>{t('cc_the_read')}</p>
              <p className={`text-sm italic ${c.text}`}>{results?.situation_read}</p>
            </div>
          )}

          {/* Comebacks */}
          {results?.comebacks?.length > 0 && (
            <div className="space-y-3">
              <h3 className={`text-xs font-semibold uppercase tracking-wider ${c.textMuted} px-1`}>
                {t('cc_should_have_said')}
              </h3>
              {results?.comebacks?.map((cb, i) => {
                const isOpen = expandedCards[i];
                const emoji = TECHNIQUE_EMOJIS[cb.technique?.toLowerCase()] || '💬';
                return (
                  <div key={i} className={`${c.card} border ${c.border} rounded-xl overflow-hidden`}>
                    <button onClick={() => toggleCard(i)} className="w-full p-5 text-left">
                      <div className="flex items-start gap-3">
                        <span className={`text-lg font-bold ${c.textMuted} flex-shrink-0`}>{i + 1}.</span>
                        <div className="flex-1 min-w-0">
                          <p className={`text-base font-semibold ${c.text} leading-snug`}
                             style={{ fontFamily: 'Georgia, "Times New Roman", serif' }}>
                            "{cb.line}"
                          </p>
                          <div className="flex items-center gap-2 mt-2">
                            <span className="text-sm">{emoji}</span>
                            <span className={`text-xs ${c.textMuted}`}>{cb.technique}</span>
                          </div>
                        </div>
                        <span className={`text-sm ${c.textMuted} flex-shrink-0 mt-1`}>
                          {isOpen ? '▲' : '▼'}
                        </span>
                      </div>
                    </button>

                    {isOpen && (
                      <div className={`px-5 pb-5 space-y-3 border-t ${c.border} pt-3 ml-8`}>
                        <div>
                          <p className={`text-xs font-semibold ${c.textMuted} mb-1`}>{t('cc_why_it_works')}</p>
                          <p className={`text-sm ${c.textSecondary} leading-relaxed`}>{cb.why_it_works}</p>
                        </div>
                        <div className={`${c.deliveryBg} rounded-xl p-3`}>
                          <p className={`text-xs font-semibold ${c.deliveryText} mb-1`}>🎬 {t('cc_delivery')}</p>
                          <p className={`text-sm ${c.textSecondary} leading-relaxed italic`}>{cb.delivery_note}</p>
                        </div>
                      </div>
                    )}
                  </div>
                );
              })}
            </div>
          )}

          {/* High Road */}
          {results?.the_high_road && (
            <div className={`${c.highRoadBg} border rounded-xl p-5`}>
              <p className={`text-xs font-semibold uppercase tracking-wider ${c.highRoadText} mb-3 flex items-center gap-1.5`}>
                <span>👑</span> {t('cc_high_road')}
              </p>
              <p className={`text-base font-semibold ${c.text} leading-snug mb-3`}
                 style={{ fontFamily: 'Georgia, "Times New Roman", serif' }}>
                "{results?.the_high_road?.line}"
              </p>
              <p className={`text-sm ${c.textSecondary} leading-relaxed italic`}>
                {results?.the_high_road?.why_its_devastating}
              </p>
            </div>
          )}

          {/* Nuclear Option — hidden by default */}
          {results?.the_nuclear_option && (
            <div>
              {!revealedNuclear ? (
                <button
                  onClick={() => setRevealedNuclear(true)}
                  className={`w-full ${c.btnSecondary} py-3 rounded-xl text-sm font-medium transition-colors flex items-center justify-center gap-2`}
                >
                  <span>☢️</span> {t('cc_reveal_nuclear')}
                  <span className={`text-xs ${c.textMuted}`}>{t('cc_fantasy_drawer')}</span>
                </button>
              ) : (
                <div className={`${c.nuclearBg} border rounded-xl p-5`}>
                  <p className={`text-xs font-semibold uppercase tracking-wider ${c.nuclearText} mb-3 flex items-center gap-1.5`}>
                    <span>☢️</span> {t('cc_nuclear_option')}
                  </p>
                  <p className={`text-base font-semibold ${c.text} leading-snug mb-3`}
                     style={{ fontFamily: 'Georgia, "Times New Roman", serif' }}>
                    "{results?.the_nuclear_option?.line}"
                  </p>
                  <p className={`text-xs ${c.textMuted} italic flex items-start gap-1.5`}>
                    <span>⚠️</span>{results?.the_nuclear_option?.warning}
                  </p>
                </div>
              )}
            </div>
          )}

          {/* Disclaimer */}
          <p className={`text-xs ${c.textMuted} text-center italic`}>
            {t('cc_disclaimer')}
          </p>

          {/* Cross-refs — post-result with conditional */}
          <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4 space-y-2`}>
            <p className={`text-xs ${c.textMuted} text-center`}>
              {t('cc_xref_ongoing_q')}{' '}
              <a href="/ConflictCoach" className={linkStyle}>🥊 {t('cc_conflictcoach')}</a>{' '}
              {t('cc_xref_ongoing_post')}
            </p>
            <p className={`text-xs ${c.textMuted} text-center`}>
              {t('cc_xref_practice_q')}{' '}
              <a href="/ArgueBetter" className={linkStyle}>🥊 {t('cc_argumentsimulator')}</a>{' '}
              {t('cc_xref_practice_post')}
            </p>
          </div>

        </div>
      )}
    </div>
  );
};

ComebackCooker.displayName = 'ComebackCooker';
export default ComebackCooker;
