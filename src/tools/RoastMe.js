import React, { useState, useCallback, useEffect } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { usePersistentState } from '../hooks/usePersistentState';
import { CopyBtn, ActionBar } from '../components/ActionButtons';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

// ════════════════════════════════════════════════════════════
// CONSTANTS
// ════════════════════════════════════════════════════════════
const CONTENT_TYPES = [
  { value: 'resume', label: 'Resume / CV', icon: '📄' },
  { value: 'dating', label: 'Dating Profile', icon: '💘' },
  { value: 'linkedin', label: 'LinkedIn', icon: '💼' },
  { value: 'email', label: 'Email / Message', icon: '📨' },
  { value: 'social', label: 'Social Media', icon: '📱' },
  { value: 'other', label: 'Other', icon: '🤷' },
];

const HEAT_LEVELS = [
  { value: 'gentle', label: 'Gentle', icon: '😊', desc: 'Playful teasing' },
  { value: 'medium', label: 'Medium', icon: '🔥', desc: 'Comedy Central' },
  { value: 'scorched', label: 'Scorched', icon: '💀', desc: 'No mercy' },
];

// ════════════════════════════════════════════════════════════
// COMPONENT
// ════════════════════════════════════════════════════════════
const RoastMe = ({ tool }) => {
  const { callToolEndpoint, loading } = useClaudeAPI();
  const { isDark } = useTheme();

  useEffect(() => {
    const handler = (e) => {
      const tag = document.activeElement?.tagName;
      if (tag === 'INPUT' || tag === 'TEXTAREA' || tag === 'SELECT') return;
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && !loading) runRoast();
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
  }, [loading]);


  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';

  const c = {
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:         isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20' : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-500' : 'text-gray-400',
    labelText:     isDark ? 'text-zinc-200' : 'text-gray-700',
    accentTxt:     isDark ? 'text-cyan-400' : 'text-cyan-600',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white' : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200' : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200' : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200' : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200' : 'bg-red-50 border-red-200 text-red-800',
    infoBox:       isDark ? 'bg-sky-900/20 border-sky-700 text-sky-200' : 'bg-sky-50 border-sky-200 text-sky-800',
    successBox:    isDark ? 'bg-emerald-900/20 border-emerald-700' : 'bg-emerald-50 border-emerald-300',
    successTxt:    isDark ? 'text-emerald-300' : 'text-emerald-800',
    warningBox:    isDark ? 'bg-amber-900/20 border-amber-700' : 'bg-amber-50 border-amber-300',
    warningTxt:    isDark ? 'text-amber-300' : 'text-amber-800',
    pillActive:    isDark ? 'border-cyan-500 bg-cyan-900/30 text-cyan-200' : 'border-cyan-600 bg-cyan-100 text-cyan-900',
    pillInactive:  isDark ? 'border-zinc-600 text-zinc-400 hover:border-zinc-500' : 'border-gray-300 text-gray-500 hover:border-gray-400',
  };;

  // ── State ──
  const [content, setContent] = useState('');
  const [contentType, setContentType] = useState('other');
  const [heatLevel, setHeatLevel] = useState('medium');
  const [results, setResults] = usePersistentState('roastme-result', null);
  const [history, setHistory] = usePersistentState('roastme-history', []);
  const [error, setError] = useState('');

  // ── API ──
  const runRoast = useCallback(async () => {
    if (!content.trim()) return;
    setError('');
    setResults(null);
    try {
      const data = await callToolEndpoint('roast-me', {
        content: content.trim(),
        contentType,
        heatLevel,
      });
      setResults(data);
      setHistory(prev => [{ id: Date.now(), date: new Date().toISOString(), preview: (content || '').slice(0, 40) }, ...prev].slice(0, 6));
    } catch (err) {
      setError(err.message || 'Roast failed');
    }
  }, [content, contentType, heatLevel, callToolEndpoint]);

  // ── Copy text builder ──
  const buildFullText = useCallback(() => {
    if (!results) return '';
    const lines = [`🔥 ROAST: ${results.content_type_detected || 'Unknown'}`, ''];
    if (results.first_impression) lines.push(`💀 ${results.first_impression}`, '');
    if (results.roasts?.length) {
      results.roasts.forEach((r, i) => lines.push(`${i + 1}. ${r.line}`));
      lines.push('');
    }
    if (results.summary_roast) lines.push(`🎤 ${results.summary_roast}`, '');
    if (results.one_nice_thing) lines.push(`😇 Nice thing: ${results.one_nice_thing}`);
    lines.push('\n— Generated by DeftBrain · deftbrain.com');
    return lines.join('\n');
  }, [results]);

  const heatColor = heatLevel === 'gentle' ? c.heatGentle : heatLevel === 'scorched' ? c.heatScorched : c.heatMedium;

  // ════════════════════════════════════════════════════════════
  // RENDER
  // ════════════════════════════════════════════════════════════

  const handleReset = () => { setResults(null); setError(''); };
  return (
    <div className={`space-y-4 ${c.text}`}>
      {/* Input */}
      <div className={`${c.card} border rounded-xl p-5`}>
        <div className={`mb-4 pb-3 border-b ${isDark ? 'border-zinc-500' : 'border-zinc-500'}`}>
          <h2 className={`text-xl font-bold ${c.text}`}>
            <span className="mr-2">🔥</span>Roast Me
          </h2>
          <p className={`text-sm ${c.textSecondary}`}>Paste anything — get a personalized comedy roast</p>
        </div>

        {/* Content type */}
        <div className="mb-4">
          <label className={`text-xs font-bold ${c.label} uppercase block mb-2`}>What is this?</label>
          <div className="flex flex-wrap gap-1.5">
            {CONTENT_TYPES.map(t => (
              <button key={t.value} onClick={() => setContentType(t.value)}
                className={`px-3 py-1.5 rounded-lg text-xs font-bold border transition-colors min-h-[32px] ${
                  contentType === t.value ? c.pillActive : c.pillInactive
                }`}>
                <span className="mr-1">{t.icon}</span>{t.label}
              </button>
            ))}
          </div>
        </div>

        {/* Content */}
        <div className="mb-4">
          <label className={`text-xs font-bold ${c.label} uppercase block mb-2`}>Paste your content</label>
          <textarea
            value={content}
            onChange={e => setContent(e.target.value)}
            placeholder="Paste your resume, dating profile, LinkedIn bio, tweet, email, whatever you dare..."
            rows={8}
            className={`w-full px-3 py-2.5 border rounded-lg text-sm ${c.input} outline-none focus:ring-2 resize-y`}
          />
          <p className={`text-[10px] ${c.textMuteded} mt-1 text-right`}>{content.length}/3000</p>
        </div>

        {/* Heat level */}
        <div className="mb-5">
          <label className={`text-xs font-bold ${c.label} uppercase block mb-2`}>Heat level</label>
          <div className="flex gap-2">
            {HEAT_LEVELS.map(h => (
              <button key={h.value} onClick={() => setHeatLevel(h.value)}
                className={`flex-1 py-3 rounded-xl border-2 text-center transition-colors min-h-[60px] ${
                  heatLevel === h.value
                    ? (h.value === 'gentle' ? c.heatGentle : h.value === 'scorched' ? c.heatScorched : c.heatMedium) + ' border-current'
                    : c.pillInactive
                }`}>
                <span className="text-xl block">{h.icon}</span>
                <span className="text-xs font-bold block">{h.label}</span>
                <span className={`text-[9px] ${c.textMuteded} block`}>{h.desc}</span>
              </button>
            ))}
          </div>
        </div>

        <button onClick={runRoast} disabled={!content.trim() || loading}
          className={`w-full ${c.btnPrimary} disabled:opacity-40 font-bold py-3 rounded-lg flex items-center justify-center gap-2 min-h-[48px]`}>
          {loading
            ? <><span className="inline-block animate-spin">{tool?.icon ?? '⚙️'}</span> Writing your roast...</>
            : <><span>🔥</span> Roast Me</>}
        </button>
      </div>

      {/* Error */}
      {error && (
        <div className={`${c.danger} border rounded-lg p-4 flex items-start gap-3`}>
          <span>⚠️</span>
          <p className="text-sm">{error}</p>
        </div>
      )}

      {/* Results */}
      {results && (
        <div className="space-y-4">
          <div className="flex justify-end">
            <ActionBar content={buildFullText()} title="Roast Me" />
          </div>

          {/* First impression */}
          {results.first_impression && (
            <div className={`${heatColor} border-2 rounded-xl p-5 text-center`}>
              <span className="text-3xl block mb-2">💀</span>
              <p className={`text-sm font-bold`}>{results.first_impression}</p>
              {results.content_type_detected && (
                <p className={`text-[10px] ${c.textMuteded} mt-2`}>Detected: {results.content_type_detected}</p>
              )}
            </div>
          )}

          {/* Roast lines */}
          {results.roasts?.length > 0 && (
            <div className={`${c.card} border rounded-xl p-4`}>
              <h3 className={`text-sm font-bold ${c.text} mb-3`}>🎤 The Roast</h3>
              <div className="space-y-3">
                {results.roasts.map((r, i) => (
                  <div key={i} className={`${c.quoteBg} rounded-lg p-3`}>
                    <p className={`text-sm ${c.text} font-medium mb-1`}>"{r.line}"</p>
                    <div className="flex items-start gap-2">
                      {r.target && <span className={`text-[9px] ${c.textMuteded}`}>🎯 {r.target}</span>}
                      {r.why_it_hurts && <span className={`text-[9px] ${c.textMuteded}`}>· 💉 {r.why_it_hurts}</span>}
                    </div>
                  </div>
                ))}
              </div>
            </div>
          )}

          {/* Summary roast */}
          {results.summary_roast && (
            <div className={`${c.warningBox} border-2 rounded-xl p-5 text-center`}>
              <p className={`text-[10px] font-bold ${c.label} uppercase mb-2`}>Mic Drop</p>
              <p className={`text-sm font-bold ${c.text}`}>🎤 {results.summary_roast}</p>
            </div>
          )}

          {/* Nice thing + share line */}
          <div className="grid grid-cols-1 sm:grid-cols-2 gap-3">
            {results.one_nice_thing && (
              <div className={`${isDark ? 'bg-zinc-500 border-zinc-500' : 'bg-zinc-500 border-zinc-500'} border rounded-xl p-4`}>
                <p className={`text-[10px] font-bold ${isDark ? 'text-zinc-500' : 'text-zinc-500'} uppercase mb-1`}>😇 One Nice Thing</p>
                <p className={`text-xs ${c.textSecondary}`}>{results.one_nice_thing}</p>
              </div>
            )}
            {results.share_line && (
              <div className={`${c.card} border rounded-xl p-4`}>
                <p className={`text-[10px] font-bold ${c.accentTxt} uppercase mb-1`}>📸 Screenshot This</p>
                <p className={`text-sm font-bold ${c.text}`}>"{results.share_line}"</p>
                <CopyBtn content={`"${results.share_line}"\n\n— Roasted by DeftBrain · deftbrain.com`} label="Copy" />
              </div>
            )}
          </div>

          {/* Roast again */}
          <button onClick={runRoast} disabled={loading}
            className={`w-full ${c.btnSecondary} font-bold py-3 rounded-lg min-h-[44px]`}>
            🔥 Roast Again (Different Lines)
          </button>
        </div>
      )}
    </div>
  );
};

RoastMe.displayName = 'RoastMe';
export default RoastMe;
