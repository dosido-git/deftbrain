    if (petNames.length < 2) return null;
    // Check for overlapping categories or similar urgency
    const categories = {};
    recent.forEach(e => {
      const cat = e.category?.toLowerCase() || '';
      if (!categories[cat]) categories[cat] = new Set();
      categories[cat].add(e.petName || e.petType);
    });
