import React, { useState, useEffect, useRef, useCallback } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { usePersistentState } from '../hooks/usePersistentState';
import { useRegisterActions } from '../components/ActionBarContext';
import { useTranslation } from '../i18n/useTranslation';
import { currencySymbol } from '../utils/formatLocale';

const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

const ProcedureProbe = ({ tool }) => {
  const { callToolEndpoint, loading, userLocale, userCurrency, userRegion } = useClaudeAPI();
  const { isDark } = useTheme();
  const { t } = useTranslation();
  const sym = currencySymbol(userLocale, userCurrency);

  // ── Theme — verbatim from CONVENTIONS.md PF-2 ──
  const c = {
    card:          isDark ? 'bg-zinc-800' : 'bg-white',
    cardAlt:       isDark ? 'bg-zinc-700/50' : 'bg-slate-50',
    input:         isDark ? 'bg-zinc-900 border-zinc-600 text-zinc-100 placeholder-zinc-400 focus:border-cyan-500 focus:ring-cyan-500/20'
                          : 'bg-white border-gray-300 text-gray-900 placeholder-gray-400 focus:border-cyan-500 focus:ring-cyan-100',
    text:          isDark ? 'text-zinc-50' : 'text-gray-900',
    textSecondary: isDark ? 'text-zinc-300' : 'text-gray-600',
    textMuted:     isDark ? 'text-zinc-500' : 'text-gray-400',
    required:      isDark ? 'text-amber-400' : 'text-amber-500',
    labelText:     isDark ? 'text-zinc-200' : 'text-gray-700',
    accentTxt:     isDark ? 'text-cyan-400' : 'text-cyan-600',
    btnPrimary:    isDark ? 'bg-cyan-600 hover:bg-cyan-500 text-white'
                          : 'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-zinc-700 hover:bg-zinc-600 text-zinc-200'
                          : 'bg-gray-100 hover:bg-gray-200 text-gray-700',
    border:        isDark ? 'border-zinc-700' : 'border-gray-200',
    success:       isDark ? 'bg-emerald-900/20 border-emerald-700 text-emerald-200'
                          : 'bg-emerald-50 border-emerald-300 text-emerald-800',
    warning:       isDark ? 'bg-amber-900/20 border-amber-700 text-amber-200'
                          : 'bg-amber-50 border-amber-300 text-amber-800',
    danger:        isDark ? 'bg-red-900/20 border-red-700 text-red-200'
                          : 'bg-red-50 border-red-200 text-red-800',
    pillActive:    isDark ? 'border-cyan-500 bg-cyan-900/30 text-cyan-200'
                          : 'border-cyan-600 bg-cyan-100 text-cyan-900',
    pillInactive:  isDark ? 'border-zinc-600 text-zinc-400 hover:border-zinc-500'
                          : 'border-gray-300 text-gray-500 hover:border-gray-400',
  };
  c.textMuteded = c.textMuted;
  c.label       = c.labelText;

  const linkStyle = isDark
    ? 'text-cyan-400 hover:text-cyan-300 underline underline-offset-2'
    : 'text-cyan-600 hover:text-cyan-700 underline underline-offset-2';

  // ── Localized option lists & example seed ──
  const PROVIDER_TYPES = [
    { value: 'dentist',            label: t('pp_pt_dentist'),            emoji: '🦷' },
    { value: 'doctor',             label: t('pp_pt_doctor'),             emoji: '🩺' },
    { value: 'surgeon',            label: t('pp_pt_surgeon'),            emoji: '🏥' },
    { value: 'dermatologist',      label: t('pp_pt_dermatologist'),      emoji: '🧴' },
    { value: 'orthodontist',       label: t('pp_pt_orthodontist'),       emoji: '😁' },
    { value: 'chiropractor',       label: t('pp_pt_chiropractor'),       emoji: '🦴' },
    { value: 'physical_therapist', label: t('pp_pt_physical_therapist'), emoji: '💪' },
    { value: 'eye_doctor',         label: t('pp_pt_eye_doctor'),         emoji: '👁️' },
    { value: 'other',              label: t('pp_pt_other'),              emoji: '⚕️' },
  ];

  const URGENCY_LEVELS = [
    { value: 'emergency', label: t('pp_ur_emergency'), emoji: '🚨' },
    { value: 'soon',      label: t('pp_ur_soon'),      emoji: '🕐' },
    { value: 'elective',  label: t('pp_ur_elective'),  emoji: '📅' },
    { value: 'unsure',    label: t('pp_ur_unsure'),    emoji: '🤷' },
  ];

  // ── State ──
  const [quote, setQuote]           = useState('');
  const [provider, setProvider]     = useState('');
  const [insurance, setInsurance]   = useState('');
  const [concerns, setConcerns]     = useState('');
  const [urgency, setUrgency]       = useState('');
  const [error, setError]           = useState('');
  const [expandedSections, setExpandedSections] = useState({ questions: true });

  // ── Refs ──
  const resultsRef   = useRef(null);
  const submitRef    = useRef(null);
  const canSubmitRef = useRef(false);

  // ── Persistent state ──
  const [procedure, setProcedure] = usePersistentState('procedureprobe-procedure', '');
  const [results, setResults]     = usePersistentState('procedureprobe-last', null);
  const [sessionHistory, setSessionHistory]     = usePersistentState('procedureprobe-history', []);

  // ── Helpers ──
  const toggleSection = useCallback((key) => {
    setExpandedSections(p => ({ ...p, [key]: !p[key] }));
  }, []);

  // ── Handlers ──
  const generate = useCallback(async () => {
    if (!procedure.trim()) return;
    setError('');
    setResults(null);
    try {
      const data = await callToolEndpoint('procedure-probe', {
        procedure: procedure.trim(),
        quote:     quote.trim() || null,
        provider:  provider || null,
        insurance: insurance.trim() || null,
        concerns:  concerns.trim() || null,
        urgency:   urgency || null,
        userLocale, userCurrency, userRegion,
      });
      setResults(data);
      // PF-25 exception: 40 below is a preview-text length, not a history cap (history caps at 6).
      setSessionHistory(prev => [{
        id: Date.now(),
        timestamp: new Date().toISOString(),
        preview: procedure.trim().slice(0, 40),
        result: data,
      }, ...(prev || [])].slice(0, 6));
    } catch (err) {
      setError(err.message || t('pp_error'));
    }
  }, [procedure, quote, provider, insurance, concerns, urgency, callToolEndpoint, setResults, setSessionHistory, userLocale, userCurrency, userRegion, t]);

  const loadExample = useCallback(() => {
    setProcedure(t('pp_ex_procedure'));
    setQuote(t('pp_ex_quote', { sym }));
    setProvider(t('pp_ex_provider'));
    setInsurance(t('pp_ex_insurance', { sym }));
    setConcerns(t('pp_ex_concerns'));
    setUrgency('soon');
    setResults(null);
  }, [setProcedure, setQuote, setProvider, setInsurance, setConcerns, setUrgency, setResults, t, sym]);

  const handleReset = useCallback(() => {
    setProcedure(''); setQuote(''); setProvider('');
    setInsurance(''); setConcerns(''); setUrgency('');
    setResults(null); setError('');
  }, [setProcedure, setResults]);

  // ── buildFullText ──
  const buildFullText = useCallback(() => {
    if (!results) return '';
    const r = results;
    let text = `🔬 ${t('pp_copy_header')} ${r.procedure_name}\n`;
    if (r.plain_english) text += `\n${r.plain_english}\n`;
    if (r.is_this_standard) {
      text += `\n${t('pp_copy_verdict')} ${r.is_this_standard.verdict}\n${r.is_this_standard.explanation}`;
      if (r.is_this_standard.alternatives?.length)
        text += `\n${t('pp_copy_alternatives')} ` + r.is_this_standard.alternatives.join('; ');
    }
    if (r.questions_to_ask?.length) {
      text += `\n\n— ${t('pp_copy_questions')} —\n`;
      r.questions_to_ask.forEach(q => { text += `\n• "${q.question}" — ${q.why_it_matters}`; });
    }
    if (r.cost_picture) {
      const cp = r.cost_picture;
      text += `\n\n— ${t('pp_copy_cost')} —\n${t('pp_copy_typical')} ${cp.typical_range}\n${t('pp_copy_insurance')} ${cp.insurance_typically}\n${t('pp_copy_oop')} ${cp.out_of_pocket_estimate}`;
      if (cp.money_saving_tip) text += `\n💡 ${cp.money_saving_tip}`;
    }
    if (r.what_to_expect) {
      const w = r.what_to_expect;
      text += `\n\n— ${t('pp_copy_expect')} —\n${t('pp_copy_duration')} ${w.procedure_duration}\n${t('pp_copy_recovery')} ${w.recovery_time}\n${t('pp_copy_pain')} ${w.pain_level}\n${t('pp_copy_lifestyle')} ${w.lifestyle_impact}`;
    }
    return text + `\n\n⚕️ ${t('pp_copy_disclaimer')}` + BRAND;
  }, [results, t]);

  // Live ref assignments
  submitRef.current    = generate;
  canSubmitRef.current = !!procedure.trim();

  useRegisterActions(buildFullText(), tool?.title || 'Procedure Probe');

  // Scroll to results
  useEffect(() => {
    if (!results) return;
    const timer = setTimeout(() => resultsRef.current?.scrollIntoView({ behavior: 'smooth', block: 'start' }), 100);
    return () => clearTimeout(timer);
  }, [results]);

  // Keyboard shortcut
  useEffect(() => {
    const handler = (e) => {
      const tag = document.activeElement?.tagName;
      if (tag === 'SELECT') return;
      if (e.key === 'Enter' && (e.metaKey || e.ctrlKey) && !loading && canSubmitRef.current)
        submitRef.current?.();
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [loading]);

  const r = results;

  return (
    <div className={`space-y-4 ${c.text}`}>

      {/* ── Input card ── */}
      <div className={`${c.card} border ${c.border} rounded-xl shadow-sm p-5 space-y-4`}>

        {/* Header — inset border-b */}
        <div className="pb-3 border-b border-zinc-500">
          <div className="flex items-center justify-between">
            <div>
              <h2 className={`text-xl font-bold ${c.text}`}>
                <span className="mr-2">{tool?.icon ?? '🔬'}</span>{tool?.title ?? t('pp_title')}
              </h2>
              <p className={`text-sm ${c.textSecondary}`}>{tool?.tagline ?? t('pp_tagline')}</p>
              <button onClick={loadExample} disabled={loading} style={{ backgroundColor: (tool?.headerColor ?? '#888888') + '80' }} className={`mt-1.5 px-2.5 py-0.5 rounded-full text-xs font-medium border disabled:opacity-40 ${isDark ? 'text-white border-white/40' : 'text-gray-800 border-transparent'}`}>{t('try_example')}</button>
            </div>
            {(results || procedure.trim()) && (
              <button onClick={handleReset} className={`${c.btnSecondary} px-3 py-1.5 rounded-lg text-xs`}>
                ↺ {t('start_over')}
              </button>
            )}
          </div>
        </div>

        {/* Procedure */}
        <div>
          <label className={`text-sm font-bold ${c.text} block mb-1.5`}>
            {t('pp_q_procedure')} <span className={c.required}>*</span>
          </label>
          <input
            type="text"
            value={procedure}
            onChange={e => setProcedure(e.target.value)}
            onKeyDown={e => { if (e.key === 'Enter') generate(); }}
            placeholder={t('pp_ph_procedure')}
            className={`w-full px-4 py-3 border rounded-xl text-sm ${c.input} outline-none focus:ring-2`}
          />
        </div>

        {/* Provider type */}
        <div>
          <label className={`text-sm font-bold ${c.text} block mb-2`}>{t('pp_q_provider')}</label>
          <div className="flex flex-wrap gap-1.5">
            {PROVIDER_TYPES.map(p => (
              <button key={p.value}
                onClick={() => setProvider(provider === p.value ? '' : p.value)}
                className={`px-3 py-1.5 rounded-lg text-xs font-bold border transition-colors min-h-[32px]
                  ${provider === p.value ? c.pillActive : c.pillInactive}`}>
                {p.emoji} {p.label}
              </button>
            ))}
          </div>
        </div>

        {/* Quote */}
        <div>
          <label className={`text-sm font-bold ${c.text} block mb-1.5`}>
            {t('pp_q_quote')} <span className={`font-normal ${c.textMuted}`}>({t('optional')})</span>
          </label>
          <input type="text" value={quote} onChange={e => setQuote(e.target.value)}
            onKeyDown={e => { if (e.key === 'Enter') generate(); }}
            placeholder={t('pp_ph_quote', { sym })}
            className={`w-full px-4 py-3 border rounded-xl text-sm ${c.input} outline-none focus:ring-2`} />
        </div>

        {/* Insurance */}
        <div>
          <label className={`text-sm font-bold ${c.text} block mb-1.5`}>
            {t('pp_q_insurance')} <span className={`font-normal ${c.textMuted}`}>({t('optional')})</span>
          </label>
          <input type="text" value={insurance} onChange={e => setInsurance(e.target.value)}
            onKeyDown={e => { if (e.key === 'Enter') generate(); }}
            placeholder={t('pp_ph_insurance')}
            className={`w-full px-4 py-3 border rounded-xl text-sm ${c.input} outline-none focus:ring-2`} />
        </div>

        {/* Urgency */}
        <div>
          <label className={`text-sm font-bold ${c.text} block mb-2`}>{t('pp_q_urgency')}</label>
          <div className="flex gap-2">
            {URGENCY_LEVELS.map(u => (
              <button key={u.value}
                onClick={() => setUrgency(urgency === u.value ? '' : u.value)}
                className={`flex-1 py-2 rounded-xl text-[11px] font-bold border transition-colors min-h-[36px]
                  ${urgency === u.value ? c.pillActive : c.pillInactive}`}>
                {u.emoji} {u.label}
              </button>
            ))}
          </div>
        </div>

        {/* Concerns */}
        <div>
          <label className={`text-sm font-bold ${c.text} block mb-1.5`}>
            {t('pp_q_concerns')} <span className={`font-normal ${c.textMuted}`}>({t('optional')})</span>
          </label>
          <input type="text" value={concerns} onChange={e => setConcerns(e.target.value)}
            onKeyDown={e => { if (e.key === 'Enter') generate(); }}
            placeholder={t('pp_ph_concerns')}
            className={`w-full px-4 py-3 border rounded-xl text-sm ${c.input} outline-none focus:ring-2`} />
        </div>

        {/* Submit */}
        <button onClick={generate} disabled={loading || !procedure.trim()}
        className={`w-full ${c.btnPrimary} disabled:opacity-40 font-bold py-3 rounded-xl flex items-center justify-center gap-2 min-h-[48px]`}>
        {loading
          ? <><span className="animate-spin inline-block">{tool?.icon ?? '🔬'}</span> {t('pp_researching')}</>
          : <><span>{tool?.icon ?? '🔬'}</span> {t('pp_prepare')}</>}
        </button>

        {/* Pre-result cross-ref */}
        <p className={`text-xs text-center ${c.textMuted}`}>
          {t('pp_xref_bill_q')}{' '}
          <a href="/BillRescue" className={`text-xs ${linkStyle}`}>🧾 {t('pp_billrescue')}</a>
          {' '}{t('pp_xref_bill_helps')}
        </p>
      </div>

      {/* ── Error ── */}
      {error && (
        <div className={`${c.danger} border rounded-xl p-4 flex items-start gap-3`}>
          <span className="flex-shrink-0 mt-0.5">⚠️</span>
          <p className="text-sm">{error}</p>
        </div>
      )}

      {/* ── Results ── */}
      {results && (
        <div className="space-y-4" ref={resultsRef}>

          {/* Plain English */}
          {r.plain_english && (
            <div className={`${c.card} border ${c.border} rounded-xl p-5`}>
              <p className={`text-xs font-bold ${c.textMuted} mb-1`}>{t('pp_plain_label')}</p>
              <p className={`text-sm ${c.text} leading-relaxed`}>{r.plain_english}</p>
            </div>
          )}

          {/* Urgency check */}
          {r.urgency_check && (
            <div className={`${r.urgency_check.time_sensitive ? c.warning : c.cardAlt} border rounded-xl p-4 flex items-start gap-3`}>
              <span className="flex-shrink-0 mt-0.5">{r.urgency_check.time_sensitive ? '⏰' : 'ℹ️'}</span>
              <div>
                <p className="text-xs font-bold mb-1">{r.urgency_check.time_sensitive ? t('pp_time_sensitive') : t('pp_not_urgent')}</p>
                <p className="text-sm leading-relaxed">{r.urgency_check.explanation}</p>
              </div>
            </div>
          )}

          {/* Is this standard */}
          {r.is_this_standard && (
            <div className={`${c.card} border ${c.border} rounded-xl p-5`}>
              <div className="flex items-center gap-2 mb-2">
                <span className={`text-[9px] font-bold px-2.5 py-1 rounded-full border
                  ${r.is_this_standard.verdict === 'Standard' ? c.success :
                    r.is_this_standard.verdict.includes('second opinion') ? c.warning : c.success}`}>
                  {r.is_this_standard.verdict}
                </span>
              </div>
              <p className={`text-sm ${c.textSecondary} leading-relaxed mb-3`}>{r.is_this_standard.explanation}</p>
              {r.is_this_standard.alternatives?.length > 0 && (
                <div>
                  <p className={`text-[10px] font-bold ${c.textMuted} mb-1`}>{t('pp_alternatives_label')}</p>
                  <div className="space-y-1">
                    {r.is_this_standard.alternatives.map((alt, i) => (
                      <p key={i} className={`text-xs ${c.textSecondary}`}>• {alt}</p>
                    ))}
                  </div>
                </div>
              )}
            </div>
          )}

          {/* Questions to ask */}
          {r.questions_to_ask?.length > 0 && (
            <div className={`${c.card} border ${c.border} rounded-xl overflow-hidden`}>
              <button onClick={() => toggleSection('questions')}
                className="w-full p-4 flex items-center justify-between text-left min-h-[44px]">
                <h3 className={`text-sm font-bold ${c.text} flex items-center gap-2`}>
                  <span>❓</span> {t('pp_questions_title')}
                  <span className={`text-[9px] px-2 py-0.5 rounded-full ${c.warning}`}>{r.questions_to_ask.length}</span>
                </h3>
                <span className={c.textMuted}>{expandedSections.questions ? '▲' : '▼'}</span>
              </button>
              {expandedSections.questions && (
                <div className={`px-4 pb-4 border-t ${c.border} pt-3 space-y-3`}>
                  {r.questions_to_ask.map((q, i) => (
                    <div key={i} className={`${c.cardAlt} rounded-lg p-3`}>
                      <p className={`text-xs font-bold ${c.text} mb-1`}>"{q.question}"</p>
                      <p className={`text-xs ${c.textMuted} italic`}>{q.why_it_matters}</p>
                    </div>
                  ))}
                </div>
              )}
            </div>
          )}

          {/* Cost picture */}
          {r.cost_picture && (
            <div className={`${c.card} border ${c.border} rounded-xl p-5`}>
              <h3 className={`text-sm font-bold ${c.text} mb-3 flex items-center gap-2`}><span>💰</span> {t('pp_cost_title')}</h3>
              <div className="grid grid-cols-3 gap-3 mb-3">
                <div className={`${c.cardAlt} rounded-lg p-3 text-center`}>
                  <p className={`text-[10px] font-bold ${c.textMuted} mb-1`}>{t('pp_cost_typical')}</p>
                  <p className={`text-sm font-bold ${c.text}`}>{r.cost_picture.typical_range}</p>
                </div>
                <div className={`${c.cardAlt} rounded-lg p-3 text-center`}>
                  <p className={`text-[10px] font-bold ${c.textMuted} mb-1`}>{t('pp_cost_insurance')}</p>
                  <p className={`text-xs font-medium ${c.textSecondary}`}>{r.cost_picture.insurance_typically}</p>
                </div>
                <div className={`${c.cardAlt} rounded-lg p-3 text-center`}>
                  <p className={`text-[10px] font-bold ${c.textMuted} mb-1`}>{t('pp_cost_oop')}</p>
                  <p className={`text-sm font-bold ${isDark ? 'text-amber-300' : 'text-amber-700'}`}>{r.cost_picture.out_of_pocket_estimate}</p>
                </div>
              </div>
              {r.cost_picture.money_saving_tip && (
                <p className={`text-xs ${isDark ? 'text-emerald-300' : 'text-emerald-700'}`}>💡 {r.cost_picture.money_saving_tip}</p>
              )}
            </div>
          )}

          {/* What to expect */}
          {r.what_to_expect && (
            <div className={`${c.card} border ${c.border} rounded-xl overflow-hidden`}>
              <button onClick={() => toggleSection('expect')}
                className="w-full p-4 flex items-center justify-between text-left min-h-[44px]">
                <h3 className={`text-sm font-bold ${c.text} flex items-center gap-2`}><span>📋</span> {t('pp_expect_title')}</h3>
                <span className={c.textMuted}>{expandedSections.expect ? '▲' : '▼'}</span>
              </button>
              {expandedSections.expect && (
                <div className={`px-4 pb-4 border-t ${c.border} pt-3 space-y-2`}>
                  {[
                    [t('pp_expect_duration'),  r.what_to_expect.procedure_duration],
                    [t('pp_expect_recovery'),  r.what_to_expect.recovery_time],
                    [t('pp_expect_pain'),      r.what_to_expect.pain_level],
                    [t('pp_expect_lifestyle'), r.what_to_expect.lifestyle_impact],
                    [t('pp_expect_followup'),  r.what_to_expect.follow_up],
                  ].filter(([, v]) => v).map(([label, value], i) => (
                    <div key={i} className="flex gap-2">
                      <p className={`text-xs font-bold ${c.text} w-32 flex-shrink-0`}>{label}</p>
                      <p className={`text-xs ${c.textSecondary}`}>{value}</p>
                    </div>
                  ))}
                </div>
              )}
            </div>
          )}

          {/* Red flags */}
          {r.red_flags?.length > 0 && (
            <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
              <p className={`text-xs font-bold ${c.text} mb-2`}>{t('pp_redflags_title')}</p>
              <div className="space-y-1.5">
                {r.red_flags.map((flag, i) => (
                  <p key={i} className={`text-xs ${c.textSecondary} leading-relaxed`}>• {flag}</p>
                ))}
              </div>
            </div>
          )}

          {/* Second opinion */}
          {r.second_opinion && (
            <div className={`${r.second_opinion.recommended ? c.warning : c.success} border rounded-xl p-4 flex items-start gap-3`}>
              <span className="flex-shrink-0 mt-0.5">{r.second_opinion.recommended ? '🔍' : '✅'}</span>
              <div>
                <p className="text-xs font-bold mb-1">{r.second_opinion.recommended ? t('pp_second_rec') : t('pp_second_opt')}</p>
                <p className="text-sm leading-relaxed">{r.second_opinion.reason}</p>
              </div>
            </div>
          )}

          {/* Empowerment */}
          {r.empowerment_note && (
            <div className={`${c.success} border rounded-xl p-4 flex items-start gap-3`}>
              <span className="flex-shrink-0 mt-0.5">💪</span>
              <p className="text-sm font-medium leading-relaxed">{r.empowerment_note}</p>
            </div>
          )}

          {/* Disclaimer */}
          <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4 flex items-start gap-3`}>
            <span className="flex-shrink-0 mt-0.5">⚕️</span>
            <p className="text-xs leading-relaxed">{t('pp_disclaimer')}</p>
          </div>

          <div className={`${c.cardAlt} border ${c.border} rounded-xl p-4`}>
            <p className={`text-xs font-semibold ${c.textMuted} uppercase tracking-wider mb-3`}>🔗 {t('pp_related')}</p>
            <div className="flex flex-wrap gap-3">
              <a href="/DoctorVisitTranslator"       className={`text-xs ${linkStyle}`}>🩺 {t('pp_doctorvisit')}</a>
              <a href="/ComplaintEscalationWriter"   className={`text-xs ${linkStyle}`}>📝 {t('pp_complaint')}</a>
            </div>
          </div>
        </div>
      )}

      {/* ── History ── */}
      {sessionHistory?.length > 0 && (
        <div className={`${c.card} border ${c.border} rounded-xl p-4`}>
          <h3 className={`text-sm font-bold ${c.text} mb-3`}>🕐 {t('pp_recent_title')}</h3>
          <div className="space-y-1.5">
            {sessionHistory.map(entry => (
              <button key={entry.id} onClick={() => setResults(entry.result)}
                className={`w-full text-left px-3 py-2 rounded-lg ${c.btnSecondary} text-xs flex items-center gap-2`}>
                <span className={c.textMuted}>
                  {new Date(entry.timestamp).toLocaleDateString(undefined, { month: 'short', day: 'numeric', hour: 'numeric', minute: '2-digit' })}
                </span>
                <span className={`${c.text} truncate`}>{entry.preview}{entry.preview?.length >= 40 ? '…' : ''}</span>
              </button>
            ))}
          </div>
        </div>
      )}
    </div>
  );
};

