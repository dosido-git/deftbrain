import React, { useState, useRef, useCallback, useEffect } from 'react';
import { useClaudeAPI } from '../hooks/useClaudeAPI';
import { useTheme } from '../hooks/useTheme';
import { usePersistentState } from '../hooks/usePersistentState';
import { useRegisterActions } from '../components/ActionBarContext';
import { useTranslation } from '../i18n/useTranslation';

// ════════════════════════════════════════════════════════════
// BRAND
// ════════════════════════════════════════════════════════════
const BRAND = '\n\n— Generated by DeftBrain · deftbrain.com';

// ════════════════════════════════════════════════════════════
// CONSTANTS — labels/placeholders/hints localized at render time
// ════════════════════════════════════════════════════════════
const CATEGORIES = [
  { value: 'food',    emoji: '🍝', labelKey: 'tot_cat_food',    phKey: 'tot_ph_food',    hintKey: 'tot_hint_food' },
  { value: 'music',   emoji: '🎵', labelKey: 'tot_cat_music',   phKey: 'tot_ph_music',   hintKey: 'tot_hint_music' },
  { value: 'film',    emoji: '🎬', labelKey: 'tot_cat_film',    phKey: 'tot_ph_film',    hintKey: 'tot_hint_film' },
  { value: 'product', emoji: '🧴', labelKey: 'tot_cat_product', phKey: 'tot_ph_product', hintKey: 'tot_hint_product' },
  { value: 'scent',   emoji: '🕯️', labelKey: 'tot_cat_scent',   phKey: 'tot_ph_scent',   hintKey: 'tot_hint_scent' },
  { value: 'color',   emoji: '🎨', labelKey: 'tot_cat_color',   phKey: 'tot_ph_color',   hintKey: 'tot_hint_color' },
  { value: 'place',   emoji: '📍', labelKey: 'tot_cat_place',   phKey: 'tot_ph_place',   hintKey: 'tot_hint_place' },
  { value: 'fabric',  emoji: '👕', labelKey: 'tot_cat_fabric',  phKey: 'tot_ph_fabric',  hintKey: 'tot_hint_fabric' },
  { value: 'other',   emoji: '❓', labelKey: 'tot_cat_other',   phKey: 'tot_ph_other',   hintKey: 'tot_hint_other' },
];

// Example seed data — display label + field text localized at render time.
const EXAMPLES = [
  { labelKey: 'tot_ex_pasta', category: 'food',    descKey: 'tot_ex_pasta_desc', whenKey: 'tot_ex_pasta_when' },
  { labelKey: 'tot_ex_song',  category: 'music',   descKey: 'tot_ex_song_desc',  whenKey: 'tot_ex_song_when' },
  { labelKey: 'tot_ex_movie', category: 'film',    descKey: 'tot_ex_movie_desc', notKey: 'tot_ex_movie_not' },
  { labelKey: 'tot_ex_cream', category: 'product', descKey: 'tot_ex_cream_desc', whenKey: 'tot_ex_cream_when' },
  { labelKey: 'tot_ex_green', category: 'color',   descKey: 'tot_ex_green_desc', notKey: 'tot_ex_green_not' },
];

// ════════════════════════════════════════════════════════════
// MAIN COMPONENT
// ════════════════════════════════════════════════════════════
const TipOfTongue = ({ tool }) => {
  const { callToolEndpoint, loading, userLocale, userCurrency, userRegion } = useClaudeAPI();
  const { isDark } = useTheme();
  const { t } = useTranslation();
  const resultsRef = useRef(null);

  // ── Theme ────────────────────────────────────────────────
  const c = {
    card:          isDark ? 'bg-[#2a2623]'  : 'bg-white',
    cardAlt:       isDark ? 'bg-[#332e2a]'  : 'bg-[#faf8f5]',
    inset:         isDark ? 'bg-[#1a1816]'  : 'bg-[#faf8f5]',
    input:         isDark ? 'bg-[#1a1816] border-[#3d3630] text-[#f0eeea] placeholder-[#8a8275] focus:border-[#4a6a8a] focus:ring-[#4a6a8a]/20'
                          : 'bg-[#faf8f5] border-[#d5cab8] text-[#3d3935] placeholder-[#8a8275] focus:border-[#4a6a8a] focus:ring-[#4a6a8a]/20',
    text:          isDark ? 'text-[#f0eeea]'  : 'text-[#3d3935]',
    headingTxt:    isDark ? 'text-[#f3efe8]'  : 'text-[#1e2a3a]',
    textSecondary: isDark ? 'text-[#c8c3b9]'  : 'text-[#5a544a]',
    textMuted:     isDark ? 'text-zinc-400'  : 'text-[#6e675c]',
    labelText:     isDark ? 'text-[#c8c3b9]'  : 'text-[#5a544a]',
    required:      isDark ? 'text-amber-400' : 'text-amber-700',
    border:        isDark ? 'border-[#3d3630]' : 'border-[#e8e1d5]',
    btnPrimary:    'bg-cyan-600 hover:bg-cyan-700 text-white',
    btnSecondary:  isDark ? 'bg-[#332e2a] hover:bg-[#3d3630] text-[#c8c3b9] border border-[#3d3630]' : 'bg-[#f3efe8] hover:bg-[#e8e1d5] text-[#5e5042] border border-[#d5cab8]',
    btnDis:        isDark ? 'bg-[#332e2a] text-[#5a544a] cursor-not-allowed' : 'bg-[#e8e1d5] text-[#8a8275] cursor-not-allowed',
    success:       isDark ? 'bg-[#5a8a5c]/10 border-[#5a8a5c]/40 text-[#7aba7c]' : 'bg-[#e8f0e8] border-[#5a8a5c]/30 text-[#3a6a3c]',
    warning:       isDark ? 'bg-[#c8872e]/10 border-[#c8872e]/30 text-[#d9a04e]' : 'bg-[#f9edd8] border-[#c8872e]/30 text-[#93541f]',
    danger:        isDark ? 'bg-[#b54a3f]/15 border-[#b54a3f]/40 text-[#e88880]' : 'bg-[#fceae8] border-[#e8a8a0] text-[#b54a3f]',
    // Tool-specific UI states
    pillActive:    isDark ? 'border-[#4a6a8a] bg-[#2c4a6e]/30 text-[#a8b9ce]' : 'border-[#2c4a6e] bg-[#d4dde8] text-[#1e3a58]',
    pillInactive:  isDark ? 'border-[#3d3630] text-[#8a8275] hover:border-[#5a544a]' : 'border-[#d5cab8] text-[#5a544a] hover:border-[#8a8275]',
    tipBg:         isDark ? 'bg-[#c8872e]/10 border-[#c8872e]/30' : 'bg-[#f9edd8] border-[#c8872e]/30',
    tipText:       isDark ? 'text-[#d9a04e]' : 'text-[#93541f]',
    // Confidence levels — tool-specific
    highBg:        isDark ? 'bg-[#5a8a5c]/10 border-[#5a8a5c]/40' : 'bg-[#e8f0e8] border-[#5a8a5c]/30',
    highText:      isDark ? 'text-[#7aba7c]' : 'text-[#3a6a3c]',
    medBg:         isDark ? 'bg-[#c8872e]/10 border-[#c8872e]/30' : 'bg-[#f9edd8] border-[#c8872e]/30',
    medText:       isDark ? 'text-[#d9a04e]' : 'text-[#93541f]',
    lowBg:         isDark ? 'bg-[#2c4a6e]/15 border-[#4a6a8a]/30' : 'bg-[#d4dde8]/30 border-[#2c4a6e]/15',
    lowText:       isDark ? 'text-[#a8b9ce]' : 'text-[#1e3a58]',
    histBg:        isDark ? 'bg-[#2c4a6e]/10 border-[#4a6a8a]/30' : 'bg-[#d4dde8]/30 border-[#2c4a6e]/15',
    accentTxt:     isDark ? 'text-[#a8b9ce]' : 'text-[#2c4a6e]',
  };
  c.textMuteded = c.textMuted;
  c.label = c.labelText;

  const linkStyle = isDark ? 'text-[#6e8aaa] hover:text-[#a8b9ce] underline' : 'text-[#2c4a6e] hover:text-[#1e3a58] underline';

  // ── State (useState before usePersistentState — PF-11/PF-14) ─────
  const [showHistory, setShowHistory] = useState(false);

  // Input
  const [category, setCategory] = useState('food');
  const [description, setDescription] = useState('');
  const [notThis, setNotThis] = useState('');
  const [whenWhere, setWhenWhere] = useState('');
  const [extraClues, setExtraClues] = useState('');

  // Results / UI
  const [error, setError] = useState('');
  const [expandedMatch, setExpandedMatch] = useState(0);

  // Refinement
  const [refineMode, setRefineMode] = useState(false);
  const [matchFeedback, setMatchFeedback] = useState({}); // { idx: 'yes' | 'close' | 'no' }
  const [refinement, setRefinement] = useState('');

  // ── Persistent state ────
  const [sessionHistory, setSessionHistory] = usePersistentState('tip-of-tongue-history', []);
  const [results, setResults] = usePersistentState('tip-of-tongue-results', null);

  const currentCat = CATEGORIES.find(cat => cat.value === category) || CATEGORIES[0];

  // Plain function — called by useRegisterActions and in JSX; no dep array needed
  const buildCopy = () => {
    if (!results?.matches?.length) return '';
    const lines = [`🔍 ${currentCat.emoji} ${t(currentCat.labelKey)}`, '', `"${description}"`, ''];
    results.matches.forEach((m, i) => {
      lines.push(`${i + 1}. ${m.name} (${m.confidence_pct || '?'}% confidence)`);
      lines.push(`   ${m.why_it_fits}`);
      if (m.how_to_find) lines.push(`   → ${m.how_to_find}`);
      lines.push('');
    });
    return lines.join('\n') + BRAND;
  };

  useRegisterActions(buildCopy(), tool?.title);

  const identify = useCallback(async () => {
    if (!description.trim()) { setError(t('tot_err_describe')); return; }
    setError(''); setResults(null); setRefineMode(false); setMatchFeedback({});
    try {
      const data = await callToolEndpoint('tip-of-tongue', {
        category, description: description.trim(),
        notThis: notThis.trim() || null, whenWhere: whenWhere.trim() || null,
        extraClues: extraClues.trim() || null,
        userLocale, userCurrency, userRegion,
      });
      setResults(data);
      setExpandedMatch(0);
      const catEmoji = CATEGORIES.find(cat => cat.value === category)?.emoji || '❓';
      const entry = {
        id: 'tot_' + Date.now(),
        date: new Date().toISOString(),
        emoji: catEmoji,
        preview: description.trim().substring(0, 40),
        topMatch: data.matches?.[0]?.name,
        // Full inputs — used to restore this search
        inputs: { category, description: description.trim(), notThis: notThis.trim() || null, whenWhere: whenWhere.trim() || null, extraClues: extraClues.trim() || null },
        // Full results — stored so replay is instant, no API call
        results: data,
      };
      // PF-25 exception: cap 10 (not 6) is deliberate — entries store full results objects, so a longer history is the intended tradeoff.
      setSessionHistory(prev => [entry, ...prev].slice(0, 10));
    } catch (err) { setError(err.message || t('tot_err_identify')); }
  }, [category, description, notThis, whenWhere, extraClues, callToolEndpoint, setSessionHistory, setResults, userLocale, userCurrency, userRegion, t]);

  const refine = useCallback(async () => {
    if (!results?.matches?.length) return;
    const feedback = results.matches.map((m, i) => ({
      name: m.name, was_it: matchFeedback[i] || 'no',
    }));
    if (!feedback.some(f => f.was_it !== 'no') && !refinement.trim()) {
      setError(t('tot_err_mark')); return;
    }
    setError('');
    try {
      const data = await callToolEndpoint('tip-of-tongue/refine', {
        category, originalDescription: description.trim(),
        previousMatches: feedback, refinement: refinement.trim() || null,
        userLocale, userCurrency, userRegion,
      });
      setResults(data);
      setRefineMode(false); setMatchFeedback({}); setRefinement('');
      setExpandedMatch(0);
    } catch (err) { setError(err.message || t('tot_err_refine')); }
  }, [results, matchFeedback, refinement, category, description, callToolEndpoint, setResults, userLocale, userCurrency, userRegion, t]);

  const handleReset = useCallback(() => {
    setDescription(''); setNotThis(''); setWhenWhere(''); setExtraClues('');
    setResults(null); setError(''); setRefineMode(false); setMatchFeedback({}); setRefinement('');
  }, []);

  const loadExample = useCallback((ex) => {
    setCategory(ex.category); setDescription(t(ex.descKey));
    setNotThis(ex.notKey ? t(ex.notKey) : ''); setWhenWhere(ex.whenKey ? t(ex.whenKey) : '');
    setResults(null); setError('');
  }, [setResults, t]);

  const confBg   = (conf) => conf === 'high' ? c.highBg : conf === 'medium' ? c.medBg : c.lowBg;
  const confText = (conf) => conf === 'high' ? c.highText : conf === 'medium' ? c.medText : c.lowText;
  const confBar  = (pct) => Math.max(5, Math.min(100, pct || 50));

  useEffect(() => {
    if (results && resultsRef.current) {
      setTimeout(() => resultsRef.current.scrollIntoView({ behavior: 'smooth', block: 'start' }), 200);
    }
  }, [results]);

  useEffect(() => {
    const handler = (e) => {
      if ((e.metaKey || e.ctrlKey) && e.key === 'Enter') {
        if (refineMode && results) {
          refine();
        } else if (!results && description.trim()) {
          identify();
        }
      }
    };
    document.addEventListener('keydown', handler);
    return () => document.removeEventListener('keydown', handler);
  }, [identify, refine, results, refineMode, description]);

  // ══════════════════════════════════════════
  // INPUT
  // ══════════════════════════════════════════
  const renderInput = () => (
    <div className="space-y-4">
      {/* Category picker */}
      <div className={'border rounded-xl p-5 ' + c.card + ' ' + c.border}>
        <label className={'text-xs font-bold ' + c.textSecondary + ' uppercase tracking-wide mb-3 block'}>{t('tot_cat_question')}</label>
        <div className="grid grid-cols-3 sm:grid-cols-5 gap-1.5">
          {CATEGORIES.map(cat => (
            <button key={cat.value} onClick={() => setCategory(cat.value)}
              className={'flex flex-col items-center gap-1 p-2.5 rounded-xl border-2 transition-all text-center ' +
                (category === cat.value ? c.pillActive + ' border-2' : c.pillInactive)}>
              <span className="text-xl">{cat.emoji}</span>
              <span className="text-[10px] font-semibold leading-tight">{t(cat.labelKey)}</span>
            </button>
          ))}
        </div>
      </div>

      {/* Description */}
      <div className={'border rounded-xl p-5 ' + c.card + ' ' + c.border}>
        <label className={'text-base font-bold ' + c.text + ' mb-1 block'}>
          {currentCat.emoji} {t('tot_describe_label')} <span className={c.required}>*</span>
        </label>
        <p className={'text-xs ' + c.textMuted + ' mb-3'}>{t(currentCat.hintKey)}</p>
        <textarea value={description} onChange={e => setDescription(e.target.value)}
          placeholder={t(currentCat.phKey)}
          className={'w-full h-28 p-4 border-2 rounded-xl ' + c.input + ' outline-none focus:ring-2 resize-none text-sm'} />
      </div>

      {/* Context fields */}
      <div className={'border rounded-xl p-5 space-y-3 ' + c.card + ' ' + c.border}>
        <div>
          <label className={'text-xs font-bold ' + c.textSecondary + ' uppercase tracking-wide mb-1 block'}>🚫 {t('tot_not_label')}</label>
          <input type="text" value={notThis} onChange={e => setNotThis(e.target.value)}
            placeholder={t('tot_not_ph')}
            className={'w-full px-4 py-2.5 rounded-xl border text-sm ' + c.input + ' outline-none'} />
        </div>
        <div>
          <label className={'text-xs font-bold ' + c.textSecondary + ' uppercase tracking-wide mb-1 block'}>📍 {t('tot_when_label')}</label>
          <input type="text" value={whenWhere} onChange={e => setWhenWhere(e.target.value)}
            placeholder={t('tot_when_ph')}
            className={'w-full px-4 py-2.5 rounded-xl border text-sm ' + c.input + ' outline-none'} />
        </div>
        <div>
          <label className={'text-xs font-bold ' + c.textSecondary + ' uppercase tracking-wide mb-1 block'}>💡 {t('tot_clues_label')}</label>
          <input type="text" value={extraClues} onChange={e => setExtraClues(e.target.value)}
            placeholder={t('tot_clues_ph')}
            className={'w-full px-4 py-2.5 rounded-xl border text-sm ' + c.input + ' outline-none'} />
        </div>
      </div>

      {/* Examples */}
      <div className={'p-4 rounded-xl ' + c.inset}>
        <p className={'text-xs font-bold ' + c.textMuted + ' mb-2'}>💡 {t('tot_try_example')}</p>
        <div className="flex flex-wrap gap-1.5">
          {EXAMPLES.map((ex, i) => (
            <button key={i} onClick={() => loadExample(ex)}
              className={'px-3 py-1.5 rounded-lg border text-xs font-semibold ' + c.pillInactive}>
              {CATEGORIES.find(cat => cat.value === ex.category)?.emoji} {t(ex.labelKey)}
            </button>
          ))}
        </div>
      </div>

      <button onClick={identify} disabled={loading || !description.trim()}
        className={'w-full py-4 rounded-2xl text-sm font-bold flex items-center justify-center gap-2 transition-all disabled:opacity-40 ' + (loading || !description.trim() ? c.btnDis : c.btnPrimary)}>
        {loading
          ? <><span className="animate-spin inline-block">{tool?.icon ?? "💭"}</span> {t('tot_searching')}</>
          : <><span>{tool?.icon ?? '💭'}</span> {t('tot_identify')}</>}
      </button>
    </div>
  );

  // ══════════════════════════════════════════
  // RESULTS
  // ══════════════════════════════════════════
  const renderResults = () => {
    if (!results) return null;
    const matches = results.matches || [];

    return (
      <div ref={resultsRef} className="space-y-4 mt-4">
        {results.category_detected && (
          <p className={'text-xs font-medium ' + c.textMuted}>{t('tot_looking_for')} {results.category_detected}</p>
        )}
        {/* Thinking */}
        {(results.thinking || results.refined_thinking) && (
          <div className={'p-4 rounded-xl ' + c.inset}>
            <p className={'text-xs font-bold ' + c.textMuted + ' mb-1'}>🧠 {t('tot_reasoning')}</p>
            <p className={'text-sm ' + c.text}>{results.refined_thinking || results.thinking}</p>
          </div>
        )}

        {/* Matches */}
        {matches.map((m, idx) => (
          <div key={idx} className={'rounded-xl border-2 overflow-hidden transition-all ' + confBg(m.confidence)}>
            <button onClick={() => setExpandedMatch(expandedMatch === idx ? -1 : idx)}
              className="w-full text-start p-5">
              <div className="flex items-center gap-3 mb-2">
                <span className={'text-xl font-bold ' + confText(m.confidence)}>#{idx + 1}</span>
                <div className="flex-1 min-w-0">
                  <p className={'text-base font-bold ' + c.text + ' truncate'}>{m.name}</p>
                  <p className={'text-xs ' + c.textMuted + ' mt-0.5'}>{t('tot_confidence', { pct: m.confidence_pct })}</p>
                </div>
                <span className={'text-xs font-bold px-2.5 py-1 rounded-full ' + confBg(m.confidence) + ' ' + confText(m.confidence)}>
                  {m.confidence === 'high' ? '🎯 ' + t('tot_conf_likely') : m.confidence === 'medium' ? '🤔 ' + t('tot_conf_maybe') : '💭 ' + t('tot_conf_possible')}
                </span>
              </div>
              {/* Confidence bar */}
              <div className={'w-full h-1.5 rounded-full ' + c.inset + ' overflow-hidden'}>
                <div className={'h-full rounded-full transition-all ' + (m.confidence === 'high' ? 'bg-[#5a8a5c]' : m.confidence === 'medium' ? 'bg-[#c8872e]' : 'bg-[#4a6a8a]')}
                  style={{ width: confBar(m.confidence_pct) + '%' }} />
              </div>
            </button>

            {expandedMatch === idx && (
              <div className={'px-5 pb-5 border-t ' + c.border + ' space-y-3'}>
                {/* Why it fits */}
                <div className="mt-3">
                  <p className={'text-xs font-bold ' + c.textMuted + ' uppercase mb-1'}>{t('tot_why_matches')}</p>
                  <p className={'text-sm ' + c.text}>{m.why_it_fits}</p>
                </div>

                {/* Memory trigger */}
                {m.memory_trigger && (
                  <div className={'p-3 rounded-lg border ' + c.tipBg}>
                    <p className={'text-xs font-bold ' + c.tipText + ' mb-1'}>⚡ {t('tot_memory_trigger')}</p>
                    <p className={'text-sm ' + c.tipText}>{m.memory_trigger}</p>
                  </div>
                )}

                {/* How to verify */}
                {m.how_to_verify && (
                  <div>
                    <p className={'text-xs font-bold ' + c.textMuted + ' uppercase mb-1'}>🔍 {t('tot_how_verify')}</p>
                    <p className={'text-xs ' + c.textSecondary}>{m.how_to_verify}</p>
                  </div>
                )}

                {/* How to find */}
                {m.how_to_find && (
                  <div className={'border rounded-lg p-3 ' + c.cardAlt + ' ' + c.border}>
                    <p className={'text-xs font-bold ' + c.textMuted + ' uppercase mb-1'}>📍 {t('tot_how_find')}</p>
                    <p className={'text-sm ' + c.text}>{m.how_to_find}</p>
                  </div>
                )}

                {/* Fun fact */}
                {m.fun_fact && (
                  <p className={'text-xs ' + c.textMuted + ' italic'}>💎 {m.fun_fact}</p>
                )}


                {/* Refine feedback buttons */}
                {!refineMode && (
                  <div className="flex gap-1.5">
                    <button onClick={() => { setRefineMode(true); setMatchFeedback(prev => ({ ...prev, [idx]: 'yes' })); }}
                      className={'flex-1 py-2 rounded-lg text-xs font-bold border ' + c.pillInactive + ' hover:border-[#5a8a5c]'}>
                      ✅ {t('tot_thats_it')}
                    </button>
                    <button onClick={() => { setRefineMode(true); setMatchFeedback(prev => ({ ...prev, [idx]: 'close' })); }}
                      className={'flex-1 py-2 rounded-lg text-xs font-bold border ' + c.pillInactive + ' hover:border-[#c8872e]'}>
                      🤏 {t('tot_close_no')}
                    </button>
                  </div>
                )}
              </div>
            )}
          </div>
        ))}

        {/* Also try */}
        {results.also_try?.length > 0 && (
          <div className={'border rounded-xl p-5 ' + c.card + ' ' + c.border}>
            <p className={'text-xs font-bold ' + c.textMuted + ' uppercase mb-3'}>✨ {t('tot_also_like')}</p>
            <div className="space-y-2">
              {results.also_try.map((item, idx) => (
                <div key={idx} className={'p-3 rounded-lg ' + c.inset}>
                  <p className={'text-sm font-semibold ' + c.text}>{item.name}</p>
                  <p className={'text-xs ' + c.textSecondary + ' mt-0.5'}>{item.why}</p>
                </div>
              ))}
            </div>
          </div>
        )}

        {/* Refine panel */}
        {refineMode && (
          <div className={'p-5 rounded-xl border-2 ' + c.tipBg}>
            <p className={'text-sm font-bold ' + c.tipText + ' mb-3'}>🎯 {t('tot_narrow')}</p>
            <p className={'text-xs ' + c.textSecondary + ' mb-3'}>{t('tot_mark_each')}</p>
            <div className="space-y-2 mb-3">
              {matches.map((m, idx) => (
                <div key={idx} className="flex items-center gap-2">
                  <span className={'text-xs font-bold ' + c.textMuted + ' w-6'}>{idx + 1}.</span>
                  <span className={'text-xs ' + c.text + ' flex-1 truncate'}>{m.name}</span>
                  {['yes', 'close', 'no'].map(val => (
                    <button key={val} onClick={() => setMatchFeedback(prev => ({ ...prev, [idx]: val }))}
                      className={'px-2 py-1 rounded-lg text-[10px] font-bold border transition-all ' +
                        (matchFeedback[idx] === val
                          ? val === 'yes'   ? 'border-[#5a8a5c] bg-[#5a8a5c]/20 text-[#5a8a5c]'
                          : val === 'close' ? 'border-[#c8872e] bg-[#c8872e]/20 text-[#c8872e]'
                                            : 'border-[#b54a3f] bg-[#b54a3f]/20 text-[#b54a3f]'
                          : c.pillInactive)}>
                      {val === 'yes' ? '✅' : val === 'close' ? '🤏' : '❌'}
                    </button>
                  ))}
                </div>
              ))}
            </div>
            <textarea value={refinement} onChange={e => setRefinement(e.target.value)}
              placeholder={t('tot_refine_ph')}
              className={'w-full h-16 p-3 border-2 rounded-xl ' + c.input + ' outline-none focus:ring-2 resize-none text-sm mb-3'} />
            <div className="flex gap-2">
              <button onClick={refine} disabled={loading}
                className={'flex-1 py-3 rounded-xl text-xs font-bold flex items-center justify-center gap-1.5 disabled:opacity-40 ' + (loading ? c.btnDis : c.btnPrimary)}>
                {loading ? <><span className="animate-spin inline-block">{tool?.icon ?? "💭"}</span> {t('tot_refining')}</> : <><span>🔍</span> {t('tot_try_again')}</>}
              </button>
              <button onClick={() => { setRefineMode(false); setMatchFeedback({}); }}
                className={'px-4 py-3 rounded-xl text-xs font-bold ' + c.btnSecondary}>{t('tot_cancel')}</button>
            </div>
          </div>
        )}

        {/* If none match */}
        {results.if_none_match && !refineMode && (
          <div className={'p-4 rounded-xl ' + c.inset}>
            <p className={'text-xs font-bold ' + c.textMuted + ' mb-1'}>🤷 {t('tot_none')}</p>
            <p className={'text-sm ' + c.text + ' mb-2'}>{results.if_none_match}</p>
            <button onClick={() => setRefineMode(true)}
              className={'px-4 py-2 rounded-lg text-xs font-bold ' + c.btnSecondary}>🔍 {t('tot_refine_search')}</button>
          </div>
        )}

        {/* Cross-refs */}
        <div className={'p-4 rounded-2xl border ' + c.card + ' ' + c.border}>
          <p className={'text-xs font-bold ' + c.textMuted + ' uppercase tracking-wide mb-2'}>🔗 {t('tot_related')}</p>
          <div className={'space-y-1.5 text-xs ' + c.textSecondary}>
            <p>{t('tot_xref_recipe')} <a href="/MiseEnPlace" className={linkStyle}>🍳 {t('tot_xref_mise')}</a> {t('tot_xref_mise_tail')}</p>
            <p>{t('tot_xref_show')} <a href="/Bookmark" className={linkStyle}>🔖 {t('tot_xref_bookmark')}</a> {t('tot_xref_bookmark_tail')}</p>
          </div>
        </div>
      </div>
    );
  };

  // ══════════════════════════════════════════
  // HISTORY
  // ══════════════════════════════════════════
  const restoreFromHistory = useCallback((entry) => {
    // Guard against old-shape entries (pre-full-results storage) that lack inputs/results
    if (!entry.inputs || !entry.results) return;
    // Restore inputs
    setCategory(entry.inputs.category);
    setDescription(entry.inputs.description);
    setNotThis(entry.inputs.notThis || '');
    setWhenWhere(entry.inputs.whenWhere || '');
    setExtraClues(entry.inputs.extraClues || '');
    // Restore results instantly — no API call
    setResults(entry.results);
    setExpandedMatch(0);
    setRefineMode(false);
    setMatchFeedback({});
    setRefinement('');
    setError('');
    setShowHistory(false);
    setTimeout(() => resultsRef.current?.scrollIntoView({ behavior: 'smooth', block: 'start' }), 100);
  }, []);
  const renderHistory = () => {
    if (sessionHistory.length === 0) return null;
    const formatDate = (iso) => {
      try {
        const d = new Date(iso);
        const diff = Math.floor((new Date() - d) / 86400000);
        return diff === 0 ? t('tot_today') : diff === 1 ? t('tot_yesterday') : diff < 7 ? t('tot_days_ago', { n: diff }) : d.toLocaleDateString(userLocale || 'en-US', { month: 'short', day: 'numeric' });
      } catch { return ''; }
    };
    return (
      <div className={'mt-6 p-4 rounded-2xl border ' + c.histBg}>
        <button onClick={() => setShowHistory(!showHistory)} className="w-full flex items-center gap-2 text-start">
          <span>🕐</span>
          <span className={'text-sm font-bold ' + c.text + ' flex-1'}>{t('tot_past_searches')}</span>
          <span className={'text-xs ' + c.textMuted}>{sessionHistory.length}</span>
          <span className={'text-xs ' + c.textMuted}>{showHistory ? '▲' : '▼'}</span>
        </button>
        {showHistory && (
          <div className="mt-3 space-y-2">
            {sessionHistory.map(entry => {
              const restorable = !!(entry.inputs && entry.results);
              return restorable ? (
              <button key={entry.id} onClick={() => restoreFromHistory(entry)}
                className={'w-full rounded-xl border p-3 flex items-start gap-3 text-start transition-all hover:border-[#4a6a8a] ' + c.card + ' ' + c.border}>
                <span className="text-lg mt-0.5 flex-shrink-0">{entry.emoji}</span>
                <div className="flex-1 min-w-0">
                  <div className={'text-sm font-semibold ' + c.text + ' truncate'}>{entry.preview}</div>
                  <div className={'text-xs ' + c.textMuted + ' mt-0.5'}>
                    {formatDate(entry.date)}{entry.topMatch ? ` · ${entry.topMatch}` : ''}
                  </div>
                  {entry.results?.matches?.length > 1 && (
                    <div className={'text-xs ' + c.textMuted + ' mt-1'}>
                      {entry.results.matches.length > 2
                        ? t('tot_more_matches', { n: entry.results.matches.length - 1 })
                        : t('tot_more_match', { n: entry.results.matches.length - 1 })}
                    </div>
                  )}
                </div>
                <span className={'text-xs flex-shrink-0 mt-1 ' + c.textMuted}>{t('tot_restore')}</span>
              </button>
              ) : (
              <div key={entry.id} className={'rounded-xl border p-3 flex items-start gap-3 opacity-50 ' + c.card + ' ' + c.border}>
                <span className="text-lg mt-0.5 flex-shrink-0">{entry.emoji}</span>
                <div className="flex-1 min-w-0">
                  <div className={'text-sm font-semibold ' + c.text + ' truncate'}>{entry.description || entry.preview}</div>
                  <div className={'text-xs ' + c.textMuted + ' mt-0.5'}>
                    {formatDate(entry.date)}{entry.topMatch ? ` · ${entry.topMatch}` : ''}
                  </div>
                </div>
                <span className={'text-xs flex-shrink-0 mt-1 ' + c.textMuted}>{t('tot_legacy')}</span>
              </div>
              );
            })}
          </div>
        )}
      </div>
    );
  };

  // ══════════════════════════════════════════
  // MAIN RENDER
  // ══════════════════════════════════════════
  return (
    <div className={`space-y-4 ${c.text}`}>

      {/* Persistent header card */}
      <div className={`${c.card} border ${c.border} rounded-xl shadow-sm p-5`}>
        <div className="pb-3 border-b border-zinc-500 flex items-center justify-between gap-3">
          <div>
            <h2 className={`text-xl font-bold ${c.headingTxt}`}>
              <span className="me-2">{tool?.icon ?? '💭'}</span>{tool?.title ?? t('tot_title')}
            </h2>
            <p className={`text-sm ${c.textMuted}`}>{tool?.tagline ?? t('tot_tagline')}</p>
          </div>
          {(results || description.trim()) && (
            <button onClick={handleReset} className={`${c.btnSecondary} px-3 py-1.5 rounded-lg text-xs font-bold flex-shrink-0`}>↺ {t('tot_start_over')}</button>
          )}
        </div>
      </div>

      {!results && renderInput()}
      {!results && (
        <p className={`text-xs text-center ${c.textMuted}`}>
          {t('tot_pre_xref')} <a href="/Bookmark" className={linkStyle}>🔖 {t('tot_xref_bookmark')}</a> {t('tot_pre_xref_tail')}
        </p>
      )}
      {renderResults()}
      {error && (
        <div className={'mt-4 p-4 border rounded-xl flex items-start gap-3 ' + c.danger}>
          <span className="text-base">⚠️</span>
          <p className="text-sm">{error}</p>
        </div>
      )}
      {renderHistory()}
    </div>
  );
};

TipOfTongue.displayName = 'TipOfTongue';
export default TipOfTongue;
