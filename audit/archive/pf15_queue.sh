#!/usr/bin/env bash
# pf15_queue.sh — generate the PF-15 phase 2c queue from current repo state.
#
# Models pf16_sweep.sh from the rate-limit catalog sweep. Runs audit_v2-3-2.py
# against every src/tools/*.js, filters output to PF-15 violations, sorts by
# violation count descending. Output is markdown-ready for paste into
# audit-backlog.md under "## PF-15 phase 2c queue".
#
# Usage:  ./pf15_queue.sh > /tmp/pf15_queue.md
#   then  cat /tmp/pf15_queue.md  and paste under the parked section.
#
# Run from repo root. Requires audit/audit_v2-3-2.py and src/tools/*.js.

set -e
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
AUDIT="${AUDIT:-audit/audit_v2-3-2.py}"
TOOLS_DIR="${TOOLS_DIR:-src/tools}"

if [[ ! -f "$AUDIT" ]]; then
  echo "ERROR: audit script not found at $AUDIT" >&2
  echo "Set AUDIT env var or run from repo root." >&2
  exit 1
fi

if [[ ! -d "$TOOLS_DIR" ]]; then
  echo "ERROR: tools dir not found at $TOOLS_DIR" >&2
  exit 1
fi

TMP="$(mktemp)"
trap 'rm -f "$TMP"' EXIT

for f in "$TOOLS_DIR"/*.js; do
  name="$(basename "$f" .js)"
  count="$(python3 "$AUDIT" "$f" 2>&1 | grep -c 'PF-15:' || true)"
  if [[ "$count" -gt 0 ]]; then
    printf '%s\t%s\n' "$count" "$name" >> "$TMP"
  fi
done

# Sort: violation count desc, then alpha. Output as a checklist.
echo "<!-- generated by pf15_queue.sh on $(date +%Y-%m-%d) -->"
echo ""
total_files="$(wc -l < "$TMP" | tr -d ' ')"
total_viols="$(awk '{s+=$1} END{print s}' "$TMP")"
echo "**Files with PF-15 violations: $total_files · total violations: $total_viols**"
echo ""
sort -t$'\t' -k1,1nr -k2,2 "$TMP" | while IFS=$'\t' read -r cnt name; do
  printf -- "- [ ] **%s** — %s violation%s\n" "$name" "$cnt" "$([[ $cnt -eq 1 ]] || echo s)"
done
