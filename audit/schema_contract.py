#!/usr/bin/env python3
"""
schema_contract.py — DeftBrain Tool Contract Checker
v1.0 · 2026-05-17

For each tool pair (Frontend.js + backend.js), reports:
  ✅ CONTRACT  — fields generated AND rendered
  ⚠  ORPHAN    — fields generated by backend, never rendered in frontend (waste)
  ❌ MISSING   — fields referenced in frontend, not in backend schema (bug)
  ℹ  I18N      — localization compliance check (withLanguage, withLocaleContext,
                  all 4 locale fields destructured)

Usage:
  python3 schema_contract.py Frontend.js backend.js
  python3 schema_contract.py --dir src/tools/ backend/routes/    # batch mode

The goal is EFFICIENCY + QUALITY — every generated field must earn its place.
Orphan fields are pure token waste. Missing fields are silent bugs.
"""

import sys
import re
import os
import argparse
from pathlib import Path

# ─── 13 supported languages ────────────────────────────────────────────────
SUPPORTED_LANGUAGES = ['en', 'es', 'zh', 'hi', 'ar', 'pt', 'fr', 'de', 'ja', 'ko', 'ru', 'th', 'vi']

# ─── Locale fields that every backend route should destructure ─────────────
LOCALE_FIELDS = ['userLanguage', 'userLocale', 'userCurrency', 'userRegion']

# ─── Patterns ──────────────────────────────────────────────────────────────

def extract_schema_fields(backend_src):
    """
    Extract field names defined in the backend JSON schema (from prompt template strings).
    Looks for "fieldName": patterns inside template literals / strings.
    Returns set of field names.
    """
    fields = set()

    # Match "fieldName": inside the prompt — covers object keys in JSON schema examples
    # Both double and single quoted, and bare (in template literal JSON)
    for m in re.finditer(r'"([a-zA-Z_][a-zA-Z0-9_]*)"\s*:', backend_src):
        name = m.group(1)
        # Filter out obvious non-schema keys (HTTP headers, config keys, etc.)
        if name not in {'error', 'role', 'content', 'model', 'max_tokens',
                        'system', 'status', 'source', 'media_type', 'data',
                        'name', 'url', 'id', 'key', 'value', 'text', 'code', 'method'}:
            fields.add(name)

    return fields


def extract_rendered_fields(frontend_src):
    """
    Extract field names referenced in the frontend JSX.
    Handles: r.field, results.field, r.field.sub, r.field[i].sub,
    and iteration vars from results arrays.
    Returns flat set of all referenced field names.
    """
    fields = set()
    SKIP_METHODS = {'length', 'map', 'filter', 'find', 'forEach', 'some', 'every',
                    'slice', 'join', 'trim', 'split', 'replace', 'toString',
                    'current', 'style', 'className', 'onClick', 'onChange',
                    'indexOf', 'includes', 'reduce', 'flat', 'keys', 'entries',
                    'ref', 'key', 'id', 'index'}

    # r.fieldName, results.fieldName — direct, chained, and optional chaining (?.)
    for m in re.finditer(r'\b(?:r|results|data)\??\.([a-zA-Z_][a-zA-Z0-9_]*)(?:\??\.([a-zA-Z_][a-zA-Z0-9_]*))?', frontend_src):
        name = m.group(1)
        sub  = m.group(2)
        if name not in SKIP_METHODS:
            fields.add(name)
        if sub and sub not in SKIP_METHODS:
            fields.add(sub)

    # Iteration variables — ONLY from results-chain .map() calls
    # handles: r.field.map, results?.field.map, results?.field?.map
    iter_patterns = re.findall(
        r'\b(?:r|results|data)\??\.\w+(?:\??)?\s*\??\.map\s*\(\s*\(?\s*([a-zA-Z_][a-zA-Z0-9_]*)\s*(?:,\s*[a-zA-Z_]\w*)?\s*\)?\s*=>',
        frontend_src
    )

    SKIP_VARS = {'e', 'event', 'el', 'elem', 'node', 'ref', 'i', 'idx', 'index',
                 'm', 'k', 'f', 'b', 't', 'x', 'y', 'n', 'p', 'g', 'v', 'c'}
    for var in set(iter_patterns):
        if var in SKIP_VARS:
            continue
        for m in re.finditer(rf'\b{re.escape(var)}\.([a-zA-Z_][a-zA-Z0-9_]*)\b', frontend_src):
            name = m.group(1)
            if name not in SKIP_METHODS:
                fields.add(name)

    # Destructured ONLY from results/r/data (not from hooks or other sources)
    for m in re.finditer(r'const\s*\{([^}]+)\}\s*=\s*(?:results|r\b|data\b)', frontend_src):
        for part in m.group(1).split(','):
            name = part.strip().split(':')[0].strip()
            if re.match(r'^[a-zA-Z_][a-zA-Z0-9_]*$', name) and name not in SKIP_METHODS:
                fields.add(name)

    return fields


def check_localization(backend_src, frontend_src, tool_name):
    """
    Check localization compliance.
    Returns list of (level, message) tuples.
    """
    issues = []

    # Backend: withLanguage import
    if 'withLanguage' not in backend_src:
        issues.append(('❌', 'Backend missing withLanguage import/usage'))

    # Backend: withLanguage called with 2 args (not single-arg bug)
    single_arg_bug = re.search(r'withLanguage\s*\(\s*(?:userLanguage|lang)\s*\)', backend_src)
    if single_arg_bug:
        issues.append(('❌', 'Backend uses withLanguage(userLanguage) single-arg bug — returns language code, not instruction'))

    # Backend: withLocaleContext for currency-signal tools
    currency_signals = ['currency', 'price', 'cost', 'budget', 'salary', 'wage',
                        'income', 'payment', 'invoice', 'bill', 'financial', 'money',
                        'rent', 'loan', 'tax', 'subscription']
    is_currency_tool = any(sig in backend_src.lower() for sig in currency_signals)

    # Backend: all 4 locale fields destructured from req.body
    # Non-currency tools only need userLanguage; currency tools need all 4
    body_destructure = re.search(r'const\s*\{([^}]+)\}\s*=\s*req\.body', backend_src)
    if body_destructure:
        destructured = body_destructure.group(1)
        if 'userLanguage' not in destructured:
            issues.append(('❌', 'Backend req.body missing userLanguage — tool will not localize'))
        if is_currency_tool:
            for field in ['userLocale', 'userCurrency', 'userRegion']:
                if field not in destructured:
                    issues.append(('⚠ ', f'Currency tool missing {field} from req.body'))
    else:
        issues.append(('⚠ ', 'Backend: could not find req.body destructure — verify locale fields are extracted'))

    # Backend: withLocaleContext for currency-signal tools
    if is_currency_tool and 'withLocaleContext' not in backend_src:
        issues.append(('⚠ ', 'Currency-signal tool missing withLocaleContext — non-USD users get wrong currency context'))

    # Frontend: callToolEndpoint sends locale context automatically via useClaudeAPI
    # Just verify useClaudeAPI is imported (it handles locale injection)
    if 'useClaudeAPI' not in frontend_src:
        issues.append(('❌', 'Frontend missing useClaudeAPI import — locale context not sent'))

    if not issues:
        issues.append(('✅', 'Localization: withLanguage present, locale fields destructured'))

    return issues


def check_multi_call(backend_src, tool_name):
    """
    Detect genuinely sequential multi-call routes.
    Action-dispatched tools (switch/case or if/action patterns) are NOT multi-call —
    each request makes exactly 1 call. Only flag when calls exceed routes AND
    no action-dispatch pattern is detected.
    """
    issues = []

    # Count actual call sites (exclude import/require lines)
    def count_calls(pattern, src):
        return sum(1 for line in src.split('\n')
                   if re.search(pattern, line) and 'require(' not in line)

    retry_calls  = count_calls(r'\bawait\s+callClaudeWithRetry\s*\(', backend_src)
    direct_calls = count_calls(r'\bawait\s+anthropic\.messages\.create\s*\(', backend_src)
    stream_calls = count_calls(r'\bawait\s+anthropic\.messages\.stream\s*\(', backend_src)
    total = retry_calls + direct_calls + stream_calls

    # Count router handlers
    handlers = len(re.findall(r'router\.\w+\s*\(', backend_src))
    handlers = max(handlers, 1)

    # Detect action-dispatch patterns — these mean 1 call per request regardless of total
    has_action_dispatch = bool(
        re.search(r'switch\s*\(\s*\w*action\w*\s*\)', backend_src) or
        re.search(r'case\s+[\'"][a-z]', backend_src) or
        re.search(r'if\s*\(\s*(?:action|mode|type)\s*===?\s*[\'"]', backend_src) or
        re.search(r'if\s*\(\s*req\.body\.(?:action|mode|type)\s*===?\s*[\'"]', backend_src)
    )

    if total == 0:
        issues.append(('⚠ ', 'No API calls detected — verify route is functional'))
    elif has_action_dispatch:
        issues.append(('✅', f'Action-dispatched architecture ({total} handlers, 1 call per request)'))
    elif total > handlers:
        extra = total - handlers
        issues.append(('🔴', f'SEQUENTIAL MULTI-CALL: {total} API calls in {handlers} route(s) — {extra} extra per request'))
        issues.append(('   ', f'   Breakdown: {retry_calls} callClaudeWithRetry, {direct_calls} messages.create, {stream_calls} messages.stream'))
    else:
        issues.append(('✅', f'Single-call architecture ({total} call(s) across {handlers} route(s))'))

    return issues


def check_max_tokens(backend_src):
    """
    Report max_tokens values. Detects literal, bracket-notation, and variable forms.
    Flags values > 3000 as review candidates post prompt-engineering.
    """
    issues = []
    found = []

    # Literal: max_tokens: 2500
    for m in re.finditer(r'max_tokens\s*:\s*(\d+)', backend_src):
        found.append(int(m.group(1)))

    # Bracket/variable: max_tokens: maxTokensByStakes[stakes] — report the dict values
    for m in re.finditer(r'max_tokens\s*:\s*[a-zA-Z_]\w*\s*[\[.]', backend_src):
        # Extract the dict/object values it references
        dict_values = re.findall(r'(?:low|moderate|high|default)\s*:\s*(\d+)', backend_src)
        if dict_values:
            found.extend(int(v) for v in dict_values)
            break

    if not found:
        issues.append(('⚠ ', 'max_tokens not detected — verify it is set explicitly'))
    else:
        unique = sorted(set(found))
        for val in unique:
            if val > 3000:
                issues.append(('⚠ ', f'max_tokens: {val} — calibrate after prompt engineering (target: 125% of actual output)'))
            else:
                issues.append(('✅', f'max_tokens: {val}'))

    return issues


def check_system_prompt_length(backend_src):
    """
    Estimate system prompt word count. Follows variable references.
    Flags if > 150 words; target is < 100.
    """
    issues = []

    # Direct inline: system: `...` or system: withLanguage(`...`, ...)
    matches = re.findall(r'system\s*:\s*(?:withLanguage\s*\()?`([^`]{20,})`', backend_src)

    # Variable reference: system: withLanguage(systemPrompt, ...) — find the variable's value
    if not matches:
        var_ref = re.search(r'system\s*:\s*(?:withLanguage\s*\()?\s*([a-zA-Z_]\w*)\s*(?:[,)]|$)', backend_src, re.MULTILINE)
        if var_ref:
            var_name = var_ref.group(1)
            if var_name not in ('userLanguage', 'lang', 'true', 'false'):
                var_val = re.search(rf'const\s+{re.escape(var_name)}\s*=\s*`([^`]{{20,}})`', backend_src)
                if var_val:
                    matches = [var_val.group(1)]

    if not matches:
        # Plain string form
        matches = re.findall(r'system\s*:\s*[\'"]([^\'"]{20,})[\'"]', backend_src)

    if matches:
        for prompt in matches:
            clean = re.sub(r'\$\{[^}]+\}', 'X', prompt)
            words = len(clean.split())
            if words > 150:
                issues.append(('🔴', f'System prompt ~{words} words — target <100 words for efficiency'))
            elif words > 100:
                issues.append(('⚠ ', f'System prompt ~{words} words — slightly over 100-word target'))
            else:
                issues.append(('✅', f'System prompt ~{words} words'))
    else:
        issues.append(('ℹ ', 'System prompt in variable — count manually'))

    return issues


def run_contract_check(frontend_path, backend_path):
    """
    Run the full contract check for one tool pair.
    """
    tool_name = Path(frontend_path).stem

    try:
        with open(frontend_path, 'r') as f:
            frontend_src = f.read()
    except FileNotFoundError:
        print(f"❌ Frontend not found: {frontend_path}")
        return 1

    try:
        with open(backend_path, 'r') as f:
            backend_src = f.read()
    except FileNotFoundError:
        print(f"❌ Backend not found: {backend_path}")
        return 1

    schema_fields   = extract_schema_fields(backend_src)
    rendered_fields = extract_rendered_fields(frontend_src)

    contract  = schema_fields & rendered_fields
    orphans   = schema_fields - rendered_fields
    missing   = rendered_fields - schema_fields

    # ─── Header ──────────────────────────────────────────────────────────
    width = 62
    print()
    print('═' * width)
    print(f'  {tool_name}')
    print('═' * width)

    # ─── Field contract ──────────────────────────────────────────────────
    print('\n── Field Contract ──────────────────────────────────────')

    if contract:
        print(f'  ✅ MATCHED ({len(contract)}): {", ".join(sorted(contract))}')

    if orphans:
        print(f'  ⚠  ORPHAN  ({len(orphans)}): {", ".join(sorted(orphans))}')
        print(f'     → Generated but never rendered. Remove from schema = fewer tokens.')

    if missing:
        print(f'  ❌ MISSING ({len(missing)}): {", ".join(sorted(missing))}')
        print(f'     → Frontend references these but backend may not generate them.')

    if not orphans and not missing:
        print(f'  ✅ Contract clean — all generated fields are rendered')

    # ─── Multi-call check ────────────────────────────────────────────────
    print('\n── API Call Architecture ───────────────────────────────')
    for level, msg in check_multi_call(backend_src, tool_name):
        print(f'  {level} {msg}')

    # ─── max_tokens ──────────────────────────────────────────────────────
    print('\n── Token Budget ────────────────────────────────────────')
    for level, msg in check_max_tokens(backend_src):
        print(f'  {level} {msg}')

    # ─── System prompt length ────────────────────────────────────────────
    print('\n── System Prompt ───────────────────────────────────────')
    for level, msg in check_system_prompt_length(backend_src):
        print(f'  {level} {msg}')

    # ─── Localization ────────────────────────────────────────────────────
    print('\n── Localization (13 languages) ─────────────────────────')
    for level, msg in check_localization(backend_src, frontend_src, tool_name):
        print(f'  {level} {msg}')

    # ─── Summary score ───────────────────────────────────────────────────
    total_issues = len(orphans) + len(missing)
    print('\n── Summary ─────────────────────────────────────────────')
    if total_issues == 0:
        print(f'  ✅ No field contract issues')
    else:
        print(f'  {total_issues} field issue(s): {len(orphans)} orphan(s), {len(missing)} missing')

    print()
    return total_issues


def batch_mode(frontend_dir, backend_dir):
    """
    Run contract check on all matched tool pairs in two directories.
    Matches by converting FrontendName.js → frontend-name.js for backend lookup.
    """
    frontend_dir = Path(frontend_dir)
    backend_dir  = Path(backend_dir)

    results = []
    unmatched = []

    for fe_path in sorted(frontend_dir.glob('*.js')):
        name = fe_path.stem
        # Convert PascalCase to kebab-case for backend filename
        kebab = re.sub(r'(?<!^)(?=[A-Z])', '-', name).lower()
        be_path = backend_dir / f'{kebab}.js'

        if not be_path.exists():
            unmatched.append((name, str(be_path)))
            continue

        issues = run_contract_check(str(fe_path), str(be_path))
        results.append((name, issues))

    # Batch summary
    print('\n' + '═' * 62)
    print('  BATCH SUMMARY')
    print('═' * 62)

    clean   = [(n, i) for n, i in results if i == 0]
    flagged = [(n, i) for n, i in results if i > 0]

    print(f'\n  Checked:   {len(results)} tool pairs')
    print(f'  Clean:     {len(clean)}')
    print(f'  Flagged:   {len(flagged)}')

    if flagged:
        print(f'\n  Tools with field issues:')
        for name, count in sorted(flagged, key=lambda x: -x[1]):
            print(f'    {count:2d} issue(s) — {name}')

    if unmatched:
        print(f'\n  No backend match found ({len(unmatched)}):')
        for name, path in unmatched:
            print(f'    {name} → expected {path}')

    print()


# ─── Entry point ───────────────────────────────────────────────────────────

if __name__ == '__main__':
    parser = argparse.ArgumentParser(
        description='DeftBrain schema contract checker — field coverage, i18n, token efficiency'
    )
    parser.add_argument('frontend',  nargs='?', help='Frontend .js file (e.g. HecklerPrep.js)')
    parser.add_argument('backend',   nargs='?', help='Backend .js file (e.g. heckler-prep.js)')
    parser.add_argument('--dir',     nargs=2, metavar=('FRONTEND_DIR', 'BACKEND_DIR'),
                        help='Batch mode: scan two directories and match pairs')

    args = parser.parse_args()

    if args.dir:
        batch_mode(args.dir[0], args.dir[1])
    elif args.frontend and args.backend:
        issues = run_contract_check(args.frontend, args.backend)
        sys.exit(0 if issues == 0 else 1)
    else:
        parser.print_help()
        sys.exit(1)
